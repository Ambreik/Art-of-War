##  **Common Aquatic Mount Active Skills**

### **1. Aqua Surge / Jet Stream**

**Description:**
The mount channels its strength to surge forward rapidly, leaving a pressurized water trail that pushes aside smaller enemies.

**Mechanics:**

* Quick linear dash (6–8 meters) with player-controlled aim.
* Pushes enemies slightly aside or backward.
* Short invulnerability window (0.5s) during movement.
* Consumes stamina or oxygen-based energy.
* Cooldown: 10s.
  **Usage:** Core mobility and escape skill — aquatic version of “Dash,” adjusted for underwater physics.

---

### **2. Pressure Wave**

**Description:**
The mount releases a concentrated pulse of water pressure from its body or mouth, knocking back nearby enemies.

**Mechanics:**

* 360° close-range AOE (3m radius).
* Deals minor physical/water-elemental damage.
* Pushes or staggers enemies.
* Cooldown: 12–15s.
  **Usage:** Defensive space-creator, ideal when surrounded or ambushed by aquatic predators.

---

### **3. Abyssal Cloak**

**Description:**
The mount blends into the murky depths, becoming semi-transparent and harder to detect for a short time.

**Mechanics:**

* Reduces enemy detection range by 40%.
* Player and mount gain brief stealth-like camouflage for 5 seconds.
* Cancels on attack or damage.
* Cooldown: 25s.
  **Usage:** Tactical evasion or scouting skill, emphasizing stealth-based aquatic exploration.

---

### **4. Tidal Guard**

**Description:**
The mount conjures a thin layer of water pressure or bio-luminescent shield around itself, deflecting minor projectiles and reducing damage.

**Mechanics:**

* 8%–12% damage reduction for 5s.
* Reflects small projectiles (like underwater bolts).
* Slight stamina regeneration boost while active.
* Cooldown: 20s.
  **Usage:** Defensive sustain tool, enhances survivability in aquatic combat zones.

---

### **5. Whirlpool Spin**

**Description:**
The mount spins rapidly, generating a swirling vortex that draws in nearby enemies and briefly disorients them.

**Mechanics:**

* 4m AOE pull effect for 2 seconds.
* Minor water damage + confusion or reduced accuracy for 3s.
* Cooldown: 18s.
  **Usage:** Crowd-control ability — used to group enemies or interrupt attacks.

---

### **6. Deep Current**

**Description:**
The mount manipulates the surrounding water currents to temporarily increase its own and the rider’s movement speed.

**Mechanics:**

* +25% swim speed for 6 seconds.
* Slight boost to vertical mobility (easier ascents/descents).
* Cooldown: 15s.
  **Usage:** Travel or chase skill; highly useful in exploration, avoiding underwater hazards, or maneuvering during aquatic combat.

---

### **7. Bubble Pulse (Optional, for magic/elemental mounts)**

**Description:**
The mount emits a cluster of high-pressure bubbles that explode after a short delay, dealing small AOE magic damage.

**Mechanics:**

* Deploys bubbles that detonate after 2s.
* 3m radius explosion, minor water-elemental damage.
* Cooldown: 12s.
  **Usage:** Offensive tool for magical aquatic species; less physical, more thematic for seahorse or mystical leviathan types.

---

### **Design Notes**

* All these skills function **underwater or semi-submerged**, with automatic **disabling on land** (unless amphibious).
* They prioritize **fluid control, space, and defense**, mirroring how aquatic combat should *feel* — more about grace and adaptation than brute force.
* Mount stamina can be replaced by **oxygen cost** in some designs, adding immersion to long dives.

