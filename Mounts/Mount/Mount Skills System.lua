	Flying Mounts Common Skills

---

### **1. Aerial Dash / Wing Burst**

**Description:**
The mount flaps its wings with explosive force, surging in a chosen direction to evade attacks or close distance.

**Mechanics:**

* Short dash (5–7 meters) in any direction — forward, backward, upward, or sideways.
* 0.5s invulnerability during movement.
* No damage; focused on maneuvering.
* Consumes stamina.
* Cooldown: 8s.

**Usage:**
Primary evasive move; used to avoid projectiles or quickly reposition in 3D space.

---

### **2. Wind Slash / Gust Edge**

**Description:**
The mount channels its wing power to release a cutting arc of air, slicing through enemies ahead.

**Mechanics:**

* Mid-range linear attack (6–8 meters).
* Deals moderate wind-elemental or physical damage.
* Slight chance to stagger flying enemies.
* Cooldown: 10–12s.

**Usage:**
Reliable offensive skill for mid-air combat; visually dynamic and adaptable for all flying types.

---

### **3. Sky Dive / Aerial Plunge**

**Description:**
The mount folds its wings and dives at high speed, striking enemies below before pulling up again.

**Mechanics:**

* Dive attack from above (min. 4m height difference).
* Deals impact damage + small AOE on landing (2–3m).
* Consumes high stamina.
* Cooldown: 15s.

**Usage:**
High-risk, high-impact attack — perfect for initiating combat or breaking ground enemy formations.

---

### **4. Cyclone Wing / Air Burst**

**Description:**
By spinning mid-air or flapping intensely, the mount creates a turbulent air wave that pushes back nearby foes.

**Mechanics:**

* 360° AOE around the mount (3–4m radius).
* Deals light air damage + knockback.
* Short cast time, interrupts flying enemies.
* Cooldown: 14s.

**Usage:**
Defensive ability — ideal for clearing space when surrounded or stabilizing after a dive.

---

### **5. Gale Glide / Tailwind**

**Description:**
The mount aligns with the upper air currents, increasing flight speed and stamina regeneration temporarily.

**Mechanics:**

* +25% flight speed for 6 seconds.
* +10% stamina regen during duration.
* No damage.
* Cooldown: 20s.

**Usage:**
Mobility and endurance tool; encourages fast-paced traversal or escape from aerial combat.

---

### **6. Sky Guard / Wind Barrier**

**Description:**
The mount summons a protective air vortex around itself, reducing ranged damage and partially deflecting projectiles.

**Mechanics:**

* Reduces ranged damage by 15% for 5 seconds.
* Reflects weak projectiles (arrows, bolts).
* Consumes moderate stamina.
* Cooldown: 25s.

**Usage:**
Defensive shield for sky battles or when evading ranged NPCs from below.

---

### **7. Featherfall / Controlled Descent**

**Description:**
The mount cushions its rider’s fall with a controlled wing glide, preventing fall damage and allowing smooth landings.

**Mechanics:**

* Negates fall damage entirely.
* Reduces landing impact radius if dismounting mid-air.
* Passive or toggle skill (low cooldown if active).

**Usage:**
Utility skill; ensures safe transitions between flight and ground combat zones.

---

✅ **Summary of Role Balance:**

* **Mobility:** Aerial Dash, Gale Glide
* **Offense:** Wind Slash, Sky Dive
* **Defense/Utility:** Cyclone Wing, Sky Guard, Featherfall

