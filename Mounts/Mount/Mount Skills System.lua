###  **Ground-Exclusive Active Skills**

---

### **1. Earthbreaker Charge**

**Description:**
The mount builds momentum and slams into the ground with its front hooves, creating a powerful shockwave that damages and briefly knocks down enemies in front.

**Mechanics:**

* Frontal cone AOE (4–6 meters).
* Moderate physical damage + 1s knockdown.
* Slightly slows mount movement for 2 seconds after impact.
* Cooldown: 15s.
* Consumes high stamina.
  **Usage:** Ideal for mounted combat initiation or interrupting clustered enemies.

---

### **2. Dust Veil**

**Description:**
The mount stomps the ground, raising a dense dust cloud around itself, briefly reducing visibility and reducing ranged accuracy of enemies.

**Mechanics:**

* Creates a small AOE cloud lasting 5 seconds.
* Reduces ranged enemy accuracy by 20–30%.
* Does not deal damage.
* Cooldown: 20s.
  **Usage:** Tactical defense tool for escaping ranged enemies or breaking target lock in PvP.

---

### **3. Ground Split / Shock Rift**

**Description:**
The mount slams the earth, causing a linear fissure that travels forward, damaging and slightly knocking back enemies hit along the line.

**Mechanics:**

* Linear ground wave extending up to 8m.
* Moderate AOE damage (element type scales with mount’s elemental affinity).
* 10s cooldown.
  **Usage:** Offensive skill; perfect for Fire, Earth, or Shadow elemental mounts.

---

### **4. Terrashield**

**Description:**
The mount anchors its stance and uses its connection to the ground to fortify itself and its rider.

**Mechanics:**

* Temporary 10% damage reduction and stagger resistance for 5 seconds.
* Does not stack with other defensive auras.
* Cooldown: 25s.
  **Usage:** Defensive response in tough fights or during boss encounters.

---

### **5. Stone Roar / War Cry**

**Description:**
The mount releases a thunderous roar, causing nearby enemies to hesitate or briefly fear.

**Mechanics:**

* 360° AOE (3–4m radius).
* Causes fear or panic for 1–2 seconds (PvE only).
* No damage dealt.
* Cooldown: 18s.
  **Usage:** Crowd control skill, useful for repositioning or breaking engagement.

---

### **6. Stampede Call**

**Description:**
If the mount is an evolved or exotic type, it briefly summons ethereal echoes or illusions of itself to rush forward, trampling enemies in a straight path.

**Mechanics:**

* Summons spectral copies that charge 6–10m forward.
* Each echo deals minor AOE impact damage.
* Duration: 3s. Cooldown: 30s.
  **Usage:** Signature skill for advanced mounts — strong visual impact, hybrid offensive/CC role.

---

### **7. Rallying Step**

**Description:**
The mount’s forceful step inspires nearby allies, granting them slight movement speed and stamina recovery for a short time.

**Mechanics:**

* 10% speed and stamina regen boost for nearby allies (8m radius).
* Lasts 6 seconds.
* Cooldown: 25s.
  **Usage:** Support-oriented skill for cooperative or party-based mounted combat.

---

### **8. Mud Drag / Ground Anchor**

**Description:**
The mount lowers its stance, generating a local gravitational pull or friction field that slows enemies approaching from any side.

**Mechanics:**

* 360° slow effect within 3m.
* Enemies’ movement speed reduced by 40% for 4s.
* Cooldown: 15s.
  **Usage:** Tactical defense when surrounded — especially valuable for tank-style mounts.

---

These are all **terrain-dependent**, meaning they:

* Only trigger on solid ground (not water or aerial zones).
* Have **visual and audio feedback tied to ground impact or dust**.
* Fit thematically with Earth, Fire, and Shadow mounts (but any ground mount could learn some version).

