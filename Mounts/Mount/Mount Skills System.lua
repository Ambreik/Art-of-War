# **Common Mount Active Skills** *(Usable by all mounts)*

### **1. Dash / Charge**

**Description:** Mount moves forward rapidly, pushing aside any enemy in its path. Useful to close gaps, evade attacks, or reposition in combat.
**Mechanics:**

* Moves mount quickly in a straight line; small player-controlled directional adjustment allowed.
* Pushes enemies hit slightly back (distance scales with mount size).
* Minor stun applied to small enemies (1–2 seconds).
* Consumes stamina based on distance.

---

### **2. Stomp / Shockwave**

**Description:** Mount strikes the ground forcefully with its hooves, creating a shockwave affecting nearby enemies.
**Mechanics:**

* AOE radius around the mount (3–5 meters).
* Slight push effect + minor damage.
* May destabilize or stagger enemies.
* Cooldown scales with mount rank.

---

### **3. Rear Kick / Hip Strike**

**Description:** Mount strikes a single enemy behind or beside it with its hind legs.
**Mechanics:**

* Single-target knockback or push (distance scales with rank).
* Minor damage applied.
* At higher mount ranks, chance to stun briefly.

---

### **4. Horseback Slash / Forward Slash**

**Description:** Player guides mount forward through enemies, slashing all enemies in a short line.
**Mechanics:**

* Multi-target frontal attack.
* Moderate damage to enemies in path.
* Player can slightly control direction with camera.
* Short cooldown; scales with mount speed and rank.

---

### **5. Jump / Leap**

**Description:** Mount leaps over enemies or obstacles. Upon landing, small AOE around landing spot affects enemies.
**Mechanics:**

* Small push effect and minor damage in radius (2–4 meters).
* Useful for navigating terrain or avoiding attacks.
* Cooldown scales with mount rank.

---

### **6. Tremble / Ground Shake**

**Description:** Mount stomps the ground, creating a minor tremor that slows nearby enemies.
**Mechanics:**

* Small AOE radius (2–4 meters).
* Slows movement speed of enemies for 3–5 seconds.
* Minor chance to interrupt attacks.
* Cooldown scales with mount rank.

---

### **7. Tail Whip**

**Description:** Mount swings its tail to hit enemies behind it.
**Mechanics:**

* Single-target knockback or minor AOE behind the mount.
* Minor damage applied.
* Cooldown: 6–10 seconds, depending on mount rank.

---

### **8. Push / Charge Push**

**Description:** Mount strikes with front or side body to push enemies back.
**Mechanics:**

* Minor knockback without damage, ideal for repositioning in combat.
* Can hit multiple enemies in small radius (front of mount).
* Scales with mount size and rank.

---

### **9. General Mechanics Notes**

* Skills consume mount stamina; usage must be managed.
* Push, knockback, and AOE effects respect mount position and player camera control.
* Damage and effects scale with mount rank/level, but **do not scale with player stats**.
* Works for **all mount environments** (ground, flying, aquatic) as a baseline skill set.
* These skills are the foundation; unique mounts may later gain specialized skills or elemental abilities.

