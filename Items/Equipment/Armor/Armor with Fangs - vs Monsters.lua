			Armor with Fangs
			
	This piece of equipment is common for all characters and is different from all others that is the "Special" Armor type item.
	Looks similar for all characters with small diferences and come in two colors : 
*Blue : for knights with blue sparks.
*Red : for pindars with red sparks.
The higher improved is, the higher and bigger are the sparks.

	On its back face multiple fangs that come out and looks like a open mouth.
	This can be crafted only, by a "Legendary Blacksmith" profession character at Rank V using a special "scroll".
	This armor is intended to be used only in the new continent "Legrand".
	
		Stats : 
• Cannot Evolve.
• Upgrade to +10.
• Can be equipped from level 110.
• Can be created from level 105.
• Required level, depends of + of armor.
• 3 Slots for "Spirit Stones".
• Can receive different bonuses when crafted.
• Each bonus slot can contain a specific bonus only.
• If have less than 3 bonuses when is crafted, then receive a 3rd one once is upgraded.
• Once crafted this armor already have a standard bonus that cannot be changed and also by chance when is created to have up to other 3 stats on it.
• New bonuses cannot be added.
