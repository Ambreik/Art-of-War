			Weapon of the Spirit King
			
	This piece of equipment is crafted at "Sten - Spiritual Blacksmith" and is same for each race for both factions.
	For each race, require different pieces for crafting.
	
	Weapon of the Spirit King can receive various base bonuses during refinement.
	Each bonus slot can only contain one specific bonus.
	If the weapon has less than 3 standard bonuses when refined, it receives the missing bonuses when enhanced.
	
	The only bonus that never miss are : "Attack Value" , "Magic Attack" , "Attack Speed" and "Shadow Damage".
		
	Random Possible Bonus I : 
*Strong against Elemental : 1% - 15% ; low chance ;
*Strong against Beast : 1% - 15%
*Strong against Esoteric : 1% - 15%
*Strong against Undead : 1% - 15%
*Strong against Demon : 1% - 15%

	Random Possible Bonus II : 
*PM : 2/4% - 25/40% ; low chance ;
*PC : 1/3% - /25% ; low chance ; 
*Piercing Attack : 1% - 10%

	Random Possible Bonus III : 
*Experience bonus chance : 1/3% - 15/30%
*Spell Speed : 1/3%	 - 15/30%
*Strong against the Monsters : 1/3%	 - 15/25% ; low chance ;
	
	This set of weapon can be sold to NPC, dropped, distroyed, traded, sold on Market, add "Spirit Stones".
	This set of weapon cannot be disenchanted, feed the pet, Guardian/Baron, mount, offer as a gift.
	This set of weapon at +9 have "+10 Shadow Damage".
	
	Once crafted 3 slots of Spirit Stones are available.
