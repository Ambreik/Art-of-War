			Soul Bean (Rough)
			
	If you right-click on the "Soul Bean (Rough)", you will get a "Soul Core (Rough)" in a random color : 
*Diamond - WHITE, 
*Ruby - RED, 
*Garnet - ORANGE,
*Amethyst - VIOLET, 
*Sapphire - BLUE, 
*Emerald - GREEN, 
*Onyx - BLACK.

	Is required for the "Inner Core" System.
	"Soul Core" is automatically stored in the "Inner Core System" inventory.
	Handling blocked except stack.

	Origin
"Soul Bean (Rough)"
Item-Shop 
"Inner Core" Chest

	Before you open it, you must first have completed the Dragon Stone quest.
	Cannot be opened before level 60.
