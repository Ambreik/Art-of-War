			Horse Window
	
	This window is dedicated to your horse from description, abilities or revive when died.
	Consist in 3 pages such as : 
	
	First page is shown when this window open and contain a UI with a title "Horse". 
	Under is a subtitle with the actual horse name with standard "<Player> Horse" or changed by player, with actual name.
	In the top left side is the symbol of horse that is unique representing the horse only, where in "Mount Window" represent a specific mount having each individual
mounts own symbol logo.
	Under this, is the horse informations such as : 
*Level : this is current level of the horse.
*Age : current age of the horse.
	Under this is a horizontal long line that symbolize the health of the horse. 
	Full is green color. Once the health getting lower is becoming yellow. When starving, meaning less than 30% become orange and red  when is less than 15%. This 
line is getting shorter when the horse health is getting lower.
	Under, is another section that represent the current status of the horse such as : 
*STR : current value.
*INT : current value.
*DEX : current value, which are increasing with every level.

	Second Page is about horse skills that had learned. All are listed down in a list, adding one by one once is learned with a description in the right side.
	
	Third page is about horse health and other mechanics, each in individual tabs, such as : 
*Horse Revive : a botton that once pressed, open a small UI where you must put the seal of the horse in the left and the item to revive it in the right slot. At the 
bottom is another botton, "Revive" that pressed, create the process to revive your horse.
*Feed Horse : a botton that once pressed, open a small UI where you have to put corresponding food and press the botton from down bellow the slot, "Feed" to feed it.
*Name Horse : a botton that once pressed, open a small UI with a slot where you have to put the item that change the name of your horse. Bellow this slot, 
"Change Name" botton to create the process to change the name of your horse.

	In the background of the UI is a horse picture, a black stallion for pindars and a armored white horse for knights.