			Order Window
			
	Order Window is how most players will keep up-to-date on the what, where, who, and maybe why of their guilds. It is also the main control panel for guild 
leaders to control the permissions of guild related access and permissions.
	Order Window can be accessed by (default keybind: CTRL + G).
	This UI is a large window with several tabs placed at the bottom of the window.
	The title is placed right in the upper central edge of the window, with a yellow font.
	
	The first page of the window, once it has been opened, represents all the information of the Order, such as : 
	Right Side of the page : 
*Order Name : name of the Order.
*Leader of the Order : name of the Leader of the Order.
*Order Level : level of the Order.
*Experience : current experience collected.
*Experience Needed : experience needed for next level.
*Members Number : number of the current members.
*Average Level : average level of members.
*Gold : amount of Gold that the Order made in expeditions.
	Left Side of the page : 
*Symbol of Order
*Enemy Order : name of the enemy order if any.
*Order Bonuses : list of all bonuses that the Order own with a botton that open on the right side a list with all current bonuses.
	Under this section are 3 bottons, such as : 
*List of Orders : clicked, open the List of all Orders similar with "Order Finder Window".
*Daily Participation : clicked, player donate exp or gold if the Order reach level 10, used to pay the fee for all NPCs from Order Territory.
*Declare War : used to declare war against other Order or Order. Clicked, open a new window.

		News Tab 
	Here is a list of all news such as wars, players reaching levels, Order upgrades such as new building was upgraded or NPCs level up. Also Order news about raids 
or dungeon completion in Order Territory. All this news are kept for 30days since was published.
	Here are news such as new players joins or dismiss by Master or resing from Order.
	At the bottom of this page is a botton called "Message" which is used by high ranked grade members to send messages here in news tab.
	
		Members 
	This tab show all the members of the Order by list : 
*Name : name of the member.
*Rank : rank of the member, usually is 	called member by default but can be changed by Leader.
*Class : class of member such as warrior, warlock, werewolf or ninja.
*Level : level of member character.
*Participation Experience : here is the number of participation. Usually is made automatically by system once the player teleport to Guild Territory, but can be 
changed from here, clicking in the section to untick it.
*Combatant : with a tick, only by Leader can be made a member, combatant.

	At the bottom is a botton called "Permisions" accessible only by Leader. Clicked open next page of this called "Administration Rights" from a list : 
*Ranks start from Rank 1 and continue down to Rank 10 with Rank I used by Leader only with all rights ticked.
	Right : 
*Name : this is automatically called "Member" but can be chanced by Leader.
*Invite Members : permission to accept other players invitation to Order.
*Dismiss Members : permission to dismiss other players from Order.
*Messenger : permission to write messages in News Tab.
*Order Bonus : permission to use the Order bonuses.
*Combatant Permision : permission to choose combatant members.
*Bank : permission to use Order bank.
*Store : permission to use Order Store.

