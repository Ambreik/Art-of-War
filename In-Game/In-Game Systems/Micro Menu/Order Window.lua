	+1 : +1%
	+2 : +2%
	+3 : +2%
	+4 : +2%
	+5 : +3% of total +10% Max Energy.
*Dragon Shield : passive ability that increases max Defence. Can be mastered up to +5. 
	+1 : +1%
	+2 : +2%
	+3 : +2%
