			Character Sheet
	
	Character Sheet is the default display shown in the character info frame. It shows the player character looks like positioned on the left side of the window. On
the right side a list of character status in two category as : 
*Primary Attributes : 
	*HP : in the slot is shown the current number of HP Points with a "+" botton if the case and at the right side the actual value of total HP that the character 
		have it. end 
	*INT / EN : in this slot is different for each individual race or class. INT for classes that use "inteligence" for skills with current number of INT Points with 
			a "+" botton if the case and at the right side the actual value of total mana that the character have it. Same with "energy". end 
	*STR : show the number of "strenght" points with a "+" botton if the case. end 
	*DEX : show the number of "dexterity" points with a "+" botton if the case. end 
*Secondary Attributes : 
	*Movement Speed : total value of Movement Speed that the character have it.
	*Attack Speed : total value of Attack Speed that the character have it.
	*Casting Speed : total value of Casting Speed that the character have it.
	*Attack Value : total value of physical damage that the character have it with two values as "Min" and "Max" value.
	*Defence (ACC) : total physical defence that the character have it.
	*Magic Attack Value: total value of magic damage that the character have it with two values as "Min" and "Max" value.
	*Magic Defence : total magic defence that the character have it.
	*Evasion : represent the % of the evasion that the character have it. This include all hits from ranged attacks.
	*Rage : value of the current "Rage" that the character have it.
	*Stamina : value of the currencies "Stamina" that the character have it.
	
	At the top of the window is the title of the UI called "Character Sheet". Right under it in a long slot is the name of the character and in the right side in 
another long slot is the name of the faction. Next to this slot in the right is another slot where is shown the value of the "PvP Power".
	Under, is a slot in the left side with the title "Level" and the current level. In the middle is a nother slot called "EXP" with the value of the current exp 
gathered. In the right side is a slot with title called "Necessary EXP" with the value of it.

	On the right side of the window, is an arrow pointing to the side that is clickable. Once clicked, open an extra window called "Bonus List Window" that show all
the current other bonuses that the character have it on items at the moment.
*PvM Stats : list of all bonuses regarding "strenght against type of monster" ; resistance and defence against "Elements".
*Class Stats : list of all bonuses regarding "strenght against class of character" ; "defence against class of character" ; Value of attacks such as "PM" or "PC".
*Other Stats : list of other stats such as "EXP" ; "Total Drop Gold" ; "Total Drop Items" ; "Poison/Bleed/Fire" chance ; "immunity" against different "CCs" , etc.

	"Character Sheet" can also be open using (default keybind: C).