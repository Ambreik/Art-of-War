			Armor penetration

	Armor penetration is a value indicating the percentage of armor that is ignored by magical attacks.
	
	The following abilities reduce or ignore a target's armor:
*Ambush : Assassin class ability.
*Meteor Fire : Necromancer class ability.
*Fireblaze : Shaman Dragon class ability.

*Bane : Debuff of some Elite monsters in "Underworld" that affect the character.

		Countering armor penetration

	To effectively neutralize or counter the effectiveness of armor penetration one could simply equipping a Shield, dropping a "Earth Golem Totem" or simply wear a 
plate armor. In short, armor penetration is really only countered by more armor. For tanks, increasing the armor, have no effect of countering such skills but can 
reduce the amount of damage received.
	

