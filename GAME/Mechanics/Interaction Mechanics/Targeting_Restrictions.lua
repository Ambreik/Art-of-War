			Targeting_Restrictions

	Rules :  
1. Line of Sight :  
	- Targets must be visible to interact or attack. Obstacles block targeting.  

2. Range :  
	- Certain abilities or interactions require the target to be within a specific range, displayed visually or numerically on the UI.  

3. Stealth :  
	- Stealthed enemies or players cannot be targeted unless revealed through specific abilities or effects.  

4. Neutral Entities :  
	- Neutral NPCs can only be targeted for non-hostile interactions unless provoked.  
