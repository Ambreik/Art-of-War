			Intoxication (Damage over Time - DoT)

	Type : Intoxication is a form of "Damage over Time" (DoT) and is classified as a type of "Crowd Control" (CC).

	Source :
- Intoxication is applied only by physical damage from monsters in the "Underworld Realm".

	Intoxication Mechanics :
1. Duration : Intoxication lasts for 10 seconds on the affected target.
2. Damage :
   - Every second, a burst of 5% of the current HP is dealt as damage.
3. Effect on Target :
   - The affected character’s vision becomes distorted, causing erratic movement.
   - The character can still perform attacking, moving, and other actions despite the disorienting effect.
   - The body is surrounded by a purple mucus that decays shortly when the character moves.
4. Cure :
   - Intoxication can be cured by specific items or abilities, including the [Cure] skill or the [Burning Spirit] skill from Healers or Sorcerers.
5. Resistance to Toxic :
   - "Resistance to Toxic" is a bonus that reduces the duration of Intoxication on a target. It can be found in the following values:
     - 2%, 5%, 8%, 15% — each percentage reduces the duration by the specified amount.
6. Status Icon :
   - When affected by Intoxication, the target will have a "status icon" indicating the presence of the effect.

	Additional Notes :
- Intoxication is a "DoT effect" that disorients the character while dealing damage over time, making it dangerous in both PvE and PvP combat.
- The purple mucus effect adds a visual indicator, further increasing the disorienting experience.

