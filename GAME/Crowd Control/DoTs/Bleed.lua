	Additional Notes :
- Bleed is a "DoT effect" that damages over time, making it effective in prolonged combat.
- It can be cured or mitigated with the right items or abilities, offering strategic options for both players and NPCs affected by it.
- **PvP Adjustment : To balance gameplay, Bleed duration is reduced to 8 seconds when it affects other players. This ensures that DoT effects are not overly
powerful in player-versus-player combat.
- Special Weapon Bonus : "Chance to Bleed" on weapons is a significant factor in applying this DoT in both PvE and PvP situations.


