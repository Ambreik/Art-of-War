			Bleed (Damage over Time - DoT)

	Type : Bleed is a form of "Damage over Time" (DoT) and is classified as a type of "Crowd Control" (CC).

	Source :
- Bleed can only be applied by physical damage, typically from hits by different monsters.
- Characters can also inflict "Bleed" when their weapon carries a "Bleed bonus".

	Bleed Mechanics :

1. Duration :
   - For Monsters (PvE) : Bleed lasts for 10 seconds on the affected target.
   - For Players (PvP) : Bleed lasts for 8 seconds when affecting another player in combat to ensure balanced gameplay.  
     (This adjustment is made to prevent imbalance in PvP situations, where players can use long-duration DoTs unfairly).

2. Damage :
   - Every second, a burst of 4.3% of the current HP is dealt as damage.
   - This damage burst applies to both PvE and PvP, with the duration change noted above for PvP.

3. Cure :
   - Bleed can be cured by specific items or abilities, including the [Cure] skill or the [Burning Spirit] skill from Healers or Sorcerers.

4. Resistance to Bleed :
   - "Resistance to Bleed" is a bonus that reduces the duration of Bleed’s effect on a target. It can be found in the following values:
     - 2%, 5%, 8%, 15% — each percentage reduces the duration of Bleed by that amount.

5. Chance to Bleed :
   - "Chance to Bleed" is a bonus commonly found on weapons that gives the chance to apply the Bleed DoT to the target. This can be found in the following values:
     - 2%, 4%, 6%, 8% — this indicates the probability of applying Bleed when the weapon hits.

6. Status Icon :
   - When affected by Bleed, the target will have a status icon indicating that they are bleeding.

