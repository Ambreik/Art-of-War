			Fire (Damage over Time - DoT)

	Type : Fire is a form of "Damage over Time" (DoT) and is classified as a type of "Crowd Control" (CC).

	Source :
- Fire is applied through "magical damage", typically by "Fire monsters" or certain monster classes like "Shamans".
- "Shaman Dragon" and "Warlock Necromancer" can also apply Fire with some of their skills.

	Fire Mechanics :
1. Duration : Fire lasts for 10 seconds on the affected target.
2. Damage :
   - Every second, a burst of 4.3% of the current HP is dealt as damage.
3. Cure :
   - Fire can be cured by specific items or abilities, including the [Cure] skill or the [Burning Spirit] skill from Healers or Sorcerers.
4. Resistance to Fire :
   - "Resistance to Fire" is a bonus that reduces the duration of Fire’s effect on a target. It can be found in the following values:
     - 2%, 5%, 8%, 15% — each percentage reduces the duration of Fire by that amount.
5. Chance to Fire :
   - "Chance to Fire" is a bonus commonly found on weapons that gives the chance to apply the Fire DoT to the target. This can be found in the following values:
     - 2%, 4%, 6%, 8% — this indicates the probability of applying Fire when the weapon hits.
6. Status Icon :
   - When affected by Fire, the target will have a status icon indicating that they are burning.

	Additional Notes :
- Fire is a "DoT effect" that damages over time, making it effective in prolonged combat.
- It can be "cured" or mitigated with the right items or abilities, offering strategic options for both players and NPCs affected by it.

