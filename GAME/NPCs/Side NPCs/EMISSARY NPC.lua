			EMISSARY NPC

---

## **I. Introduction**

**Emissaries** are special side-class NPCs who serve as the main intermediaries between players and the various **minor factions** that inhabit the **Aarong Forest region**.
They act as **official representatives**, **quest givers**, and **vendors**, allowing players to engage with regional, non-playable factions that exist outside the authority of major kingdoms or playable factions.

Each Emissary is responsible for one distinct faction and is stationed in the **Ancient City of Sankis – Main City**, located in
**Aarong Forest – Arberin Forest (Area I) – Langoosa (Subarea I)**.
Every Emissary resides within a separate, faction-themed building guarded by their own followers, maintaining both political neutrality and individual identity.

While the Emissaries are not directly involved in the main storyline of the playable factions, their influence is felt across the region — shaping alliances, providing rare knowledge, and granting access to unique opportunities for adventurers who earn their trust.

---

## **II. Purpose and Function**

Emissary NPCs exist to connect players with **regional side factions** — independent groups that influence local trade, exploration, and lore.
Players may interact with Emissaries to:

* Learn about the history, nature, and purpose of their respective factions.
* Access **side-questlines** that build reputation with these regional groups.
* Unlock **unique systems**, **crafting paths**, **buffs**, **Runes**, **mounts**, **pets**, or **regional knowledge**.
* Purchase or craft **specialized items** once certain reputation ranks are achieved.

These Emissaries serve as a gateway to regional progression, creating incentives for exploration and immersion within the **Outland Territories**.

> ⚠️ Players **cannot join** these factions as members; they only **gain Reputation (REP)** through cooperation and service.

---

## **III. Reputation System**

All Emissary factions are integrated into the global **Reputation System** and use a **five-rank structure**, reflecting the player’s standing with that faction:

| Rank | Title      | Description                                                                                         |
| ---- | ---------- | --------------------------------------------------------------------------------------------------- |
| I    | Stranger   | The faction knows nothing about the player. Limited interaction.                                    |
| II   | Recognized | The player’s deeds are noticed; small quests and information become available.                      |
| III  | Trusted    | The faction begins to share minor trade secrets, crafting insights, and limited benefits.           |
| IV   | Honored    | The player gains access to advanced quests, knowledge, and special Emissary privileges.             |
| V    | Exalted    | The highest level of recognition; the Emissary grants access to unique mechanics, recipes, or lore. |

Each reputation level enhances the player’s relationship with the Emissary’s faction, unlocking new opportunities or insights into the region’s mysteries.

---

## **IV. Location and Environment**

All Emissaries are based within the **Ancient City of Sankis**, the primary hub of Aarong Forest.
The city itself is a convergence of multiple cultures and architectures — a neutral sanctuary where trade and diplomacy coexist beneath towering trees and runic banners.

Each Emissary building reflects the aesthetics and traditions of their respective faction, often guarded by loyal followers or enchanted wards to mark their domain.

