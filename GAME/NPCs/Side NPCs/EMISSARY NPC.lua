## **VI. NPC Characteristics and Interaction**

* **Type:** Skillable NPCs — interactable via dialogue and trade interface.
* **Role:** Quest Giver / Vendor / Faction Representative.
* **Map Icon:** Marked with the **“Faction Sigil”**, a neutral emblem shared across Emissary buildings.
* **Protection:** Each Emissary is guarded by their faction soldiers or defensive systems.
* **Behavior:**

  * Neutral toward all player factions.
  * Non-hostile under any circumstances.
  * Offer different dialogue options based on REP rank.
  * Each building functions as a **Faction Embassy**, featuring decor, music, and atmosphere matching their homeland.

