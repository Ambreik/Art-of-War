			Profession Teachers

	1. Introduction
	Overview : "Profession Teachers" are a class of NPCs that teach players new professions, enabling them to craft goods, upgrade gear, and enhance gameplay. These
NPCs are skillable vendors who sell resources and recipes necessary for advancing in their respective profession. Players can improve their professions through 
quests, favors, and crafting.

---

	2. Profession Teachers Roles

- Vendor NPCs :  
	Profession Teachers offer goods, recipes, and crafting materials related to the profession they teach.
  
- Teacher NPCs :  
	They provide training for new professions and unlock new crafting abilities, equipment, and style of play as the player advances in the profession.

- Quest Givers :  
	Each Profession Teacher can provide quests that, when completed, grant rewards such as resources, currency, or unique items. Quests also advance the player’s 
rank in their profession.

- Craft NPCs : 
	They provide different crafts or help players to craft different items.
	
---

	3. Types of Profession Teachers

	"Primary Professions"  
	These are essential starting professions where players learn the basics of crafting and resource gathering.

1. "Fisherman Teacher"  
	- Role : Teaches fishing skills and provides resources to improve fishing quality.
	- Goods Offered : Fishing poles, bait, recipes for rare catches, and enhancement items.
	- Location : Major cities or along water bodies.
   
2. "Miner Teacher"  
	- Role : Introduces mining, ore extraction, and how to refine ores into ingots.
	- Goods Offered : Mining tools, ore refining recipes, and materials for blacksmithing.
	- Location : Mines or mountainous regions.

3. "Farmer Teacher"  
	- Role : Teaches farming and harvesting of crops, which are essential for crafting runes or other consumables.
	- Goods Offered : Seeds, farming tools, farming recipes, and harvest enhancements.
	- Location : Farmland or rural settlements.

---

	"Secondary Professions"  
	These are specialized crafting professions that build on the foundation of primary professions.

4. "Alchemy Teacher"  
	- Role : Teaches the art of mixing ingredients to create potions, essences, and other magical items.
	- Goods Offered : Reagents, alchemy recipes, potions, and enhancements.
	- Location : Apothecaries, alchemist shops, or magical hubs.

5. "Archaeology Teacher"  
	- Role : Teaches players how to uncover hidden artifacts and ancient treasures.
	- Goods Offered : Archaeological tools, rare find maps, and artifact crafting.
	- Location : Ruins, ancient temples, or libraries.

6. "Blacksmith Teacher"  
	- Role : Teaches the art of forging weapons, armor, and unique items.
	- Goods Offered : Metalworking tools, rare ores, blacksmithing recipes, and crafted items.
	- Location : Smithies, forges, or crafting hubs.

7. "Cooking Teacher"  
	- Role : Teaches players how to craft consumable food items for buffs and healing.
	- Goods Offered : Cooking utensils, recipes for high-quality dishes, and ingredients.
	- Location : Taverns, inns, or specialized cooking areas.

8. "Herbalism Teacher"  
	- Role : Teaches the collection and crafting of potions, ointments, and other herb-based goods.
	- Goods Offered : Herb gathering tools, recipes for potions and remedies, and enhancing ingredients.
	- Location : Herb gardens, forests, or medicinal hubs.

9. "Tailoring Teacher"  
	- Role : Teaches crafting of cloth-based items such as armor, bags, and other fabric goods.
	- Goods Offered : Fabric, tailoring patterns, and tools for crafting.
	- Location : Tailoring shops, fabric stores, or market districts.

---

	"Third-Party Professions"  
	These professions are non-standard, reputation-based, or more niche, often associated with unique quests or tasks.

10. "Bounty Hunter Teacher"  
    - Role : Teaches players how to track down and eliminate targets for bounties.
    - Goods Offered : Bounty hunting contracts, tracking tools, and bounty hunter rewards.
    - Location : Bounty boards, guild halls, or law enforcement offices.

11. "Scribe Teacher"  
    - Role : Teaches the art of recording history, making scrolls, or generating mini-quests.
    - Goods Offered : Scrolls, maps, mini-quest generators, and scribing tools.
    - Location : Libraries, academies, or scholar hubs.

12. "Pickpocket Teacher"  
    - Role : Teaches the art of stealing from NPCs, a secondary skill that may be used for specific quests.
    - Goods Offered : Stealth tools, training in pickpocketing techniques, and unique stealing items.
	- Location : Back alleys, underground locations, or rogue guilds.

---

	4. NPC Characteristics and Features

- Location : Profession Teachers can be found in major cities, towns, neutral areas, or specialized regions tied to their profession.
- Vendor Window : Each Profession Teacher has a specific vendor window containing relevant goods, recipes, and tools. The background logo will match the profession type.
- No Reputation System : Profession Teachers do not have a Reputation system, but their goods and services become available as the player’s Profession Rank progresses.
- Rank and Unlocking : As players advance in their profession, they unlock new recipes, tools, and quests from Profession Teachers.

---

	5. NPC Interaction Mechanics

- Vendor Interaction : Players interact with Profession Teachers as vendors to purchase materials, recipes, and tools necessary for their chosen profession.
- Quest Interaction : Profession Teachers provide quests that help players advance in their profession and unlock new crafting options or resources.
- Learning New Abilities : Each Profession Teacher offers a learning function, where the player can learn new crafting abilities or trade skills based on their progress.
- Crafting Items from Recipes : Some Trainers can craft different items or materials used by the players for their progression in their adventure.

---

	6. Special NPC Behaviors

- Dynamic Availability : Some Profession Teachers might only be available at specific times of day or under certain environmental conditions 
(e.g., weather, time of year).
- Quests and Favors : As players improve in their profession, they may be given higher-level quests or special favors by Profession Teachers, unlocking rare 
crafting abilities or recipes.

---

	7. NPC Vendor Window Structure

	Each Profession Teacher has a unique "Vendor Window" with the following elements:
- Recipes : Profession-specific recipes that players can purchase to advance in their chosen profession.
- Materials : Raw materials and crafted items tied to the profession (e.g., ores, herbs, fabrics).
- Tools : Special tools required for the profession (e.g., fishing rods, mining picks, cooking tools).
- Currency : Vendors will accept standard gold or unique currency for their goods and services.

