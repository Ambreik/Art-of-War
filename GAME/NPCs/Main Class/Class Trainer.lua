		6. NPC Interaction Mechanics

* Training Sessions : Players can train with class trainers to learn new abilities or reset their skill points.
* Dual Talent Specialization : Class trainers offer the option to purchase dual talent specialization, allowing players to have two active talent trees.
* Quest Giving : Trainers offer quests related to their class and these may be required to unlock additional abilities or talents.

	Additional Info / Mechanics :
* High REP may unlock advanced quests or allow alternative talent reset options.
* Favor items may temporarily provide bonus skill points or special training opportunities.

---

		7. Trainer Behavior and Features

* Duel Challenge : Class trainers can be challenged by characters of the same class to test their skills. These duels do not affect Reputation Grades and serve as a
test of strength or ability.
* No PvP Flags : Trainers are not flagged for PvP and cannot be attacked by players. They are safe NPCs that players can interact with freely.
* Assisting Guardians : When necessary, these NPCs can assist nearby Guardians with enemy-faction players or outlaws.

	Additional Info / Mechanics :
* Tier I NPCs are essential for player progression and remain neutral and protected in all settlement zones.
* Certain advanced interactions or hidden tips may unlock for players with high REP or special Favor items.

