		3. Trainer Services

* Talent Reset : Class trainers allow players to reset their specialization and talents for a price. This feature enables players to experiment with different builds or adjust to new content.
* Dual Talent Specialization : Class trainers sell the ability to learn dual specialization, allowing players to switch between two different specializations without visiting the trainer each time.
* Skill Enhancement : Profession trainers teach new skills, provide recipes, and sell materials related to their class.
* Quest Givers : Some trainers provide quests related to their class, often granting valuable rewards or progression within that specialization.

	Additional Info / Mechanics :

* Certain advanced abilities or quests may require specific Favor items or REP rank with the trainer.
* Trainers may also provide hidden tips or advanced training techniques to players with high REP.

