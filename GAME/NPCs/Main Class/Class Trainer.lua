		4. NPC Characteristics and Appearance

* Class Trainers : Typically found in main capital cities and all major human settlements from Outland Territories continent. Reflect their specific class or role, 
wearing armor or robes suited to their specialization.
* Other Trainers : Non-class trainers (e.g., Pet Master, Mount Master) often have unique appearances based on the type of animal or skill they specialize in.

	Additional Info / Mechanics :
* Visual Cues : Class trainers may display emblems, banners, or tools representing their class specialization.
* Tier I NPC : Safe from PvP and central to player class progression.

