			Arena Manager

	Overview

	The "Arena Manager" is a special NPC responsible for coordinating PvP duels and managing the "Arena", where players engage in combat to improve their PvP 
ranking. The Arena Manager facilitates exhibition matches, challenges, and records the progress of all participants.

	Additional Info / Mechanics :

* Tier : I – High-importance NPC, immune to PvP, central for PvP progression.
* REP / Favor Items : While Arena Managers primarily manage PvP, high REP may unlock access to special tournaments, bonus rewards, or cosmetic items in the Arena. 
Favor items may allow priority registration for events or access to rare PvP events.
* PvP Behavior : Arena Managers act as Guardians in their radius; any PvP starting nearby triggers their intervention to maintain peace.

---

	Locations

* Found in towns and cities, typically near important buildings such as army barracks or military training grounds.
* The Arena Manager oversees the "Arena" area where players gather to participate in PvP battles.

	Additional Info / Mechanics :
* Tier I NPC: Cannot be attacked.
* Nearby area functions as a PvP-neutral zone, except for official Arena events.

---

	Appearance

* Arena Managers are distinctly dressed in attire that signifies their role in organizing and overseeing duels.
* They are positioned in locations that are often associated with combat or military affairs.

	Additional Info / Mechanics :
* Visual cues indicate Arena functionality: banners, emblems, or trophies signaling active PvP events.

---

	Functions

	1. PvP Duels and Arena Exhibitions :

* Players can initiate an arena duel with random players or challenge a specific player by entering their name. The challenged player has the option to accept or 
refuse the duel, with a cooldown of 30 seconds before a response is required.
* The "Arena Manager" manages both exhibition matches (friendly battles) and official duels (ranked PvP matches) within the Arena.

	2. PvP Ranking and Classments :

* The Arena Manager keeps track of all players who participate in the Arena, recording their PvP rankings and performance in each battle.
* PvP scores are updated after each duel, and the Arena Manager maintains a leaderboard for players who have engaged in the Arena.
* All PvP events and battlefields of any kind are connected with this NPC and are updated according with each player participation and results.
* For global PvP between players in the World Game Maps, each duel outside of official places, such as in open world skirmishes, results are also stored and ranking
updated constantly.

	3. Insignia System :

* Starting at level 70, players can receive a quest from the Arena Manager that introduces them to the Insignia System, which rewards players for PvP victories.
* Insignias are earned by defeating other players in various PvP events. The amount of Insignias a player earns is tied to their success in these battles.
* Insignias serve as a special currency used for unique rewards and ranking purposes in the Arena.

	4. Training and Progression :

* Players who reach level 70 and complete the training quest will gain access to a new PvP UI that helps manage their PvP experience and track Insignias.
* After completing the quest, the Arena Manager will spawn a training dummy for the player to fight, allowing them to earn their first Insignia (even if they lose 
the fight, they will receive one Insignia).

	5. Outlaws’ Mission :

* Starting at level 40, Outlaws can begin a special mission in Abalan, where they receive guidance and objectives from an NPC to improve their PvP skills. This 
quest is distinct and available only to Outlaws.

---

	Additional Rules / Mechanics

* Level 70 and above : Only players of level 70 or higher can participate in PvP duels that contribute to PvP ranking and Insignia earnings.
* No Reputation Loss for PvP Duels : In Arena duels, there is no loss of reputation for participants, and no Insignias are awarded for defeats in these matches. 
Only wins contribute to the PvP Insignia system.
* No PvP Recognition for Players Below Level 70 : Players under level 70 will not be counted in the official PvP ranking system, and they will not lose reputation 
or earn Insignias from PvP kills.
* Guardian Behavior : Arena Managers act as "Guardians" if a PvP is started nearby, maintaining peace around them. Their radius is extensive, similar to Guardian 
NPCs, and prevents illegal PvP or interference near Arena areas.

