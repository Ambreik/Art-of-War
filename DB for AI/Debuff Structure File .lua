			Debuff File Skeleton Structure

Here’s the standard structure you can paste at the top of future discussions or files to remind me how to format new debuffs:

---

	Name of the Debuff : `<Debuff Name>`

	Description :
A concise but vivid explanation of what the debuff represents in-world and its intended purpose or thematic flavor.

	Triggered by :
`<Monster / Boss / NPC / Item / Environment>` — specify who or what causes this debuff to occur.

	Effect :

* `<Primary mechanical effect>`
* `<Secondary mechanical effect>`
	*(List all effects, one per line)*

	Duration : `<x seconds / indefinite / until dispelled>`

	Countered by :
`<Item / Buff / Skill>` — optional, list what can remove or negate this effect.

	Stacking :
`Yes/No` — describe if it stacks, and under what limit.

	Synergy :
Explain if it interacts with other effects, status conditions, or mechanics (e.g., amplifies Fear, drains mana faster during Rage Mode, etc.).

	UI :
Describe the icon, color, and animation style that appears on the player’s debuff bar.

	Flavor :

> “Optional short quote, line of dialogue, or lore snippet reflecting the effect’s theme.”

---