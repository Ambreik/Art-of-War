			TRAP SYSTEM MECHANIC

	Tactical Ground-Based Consumable Combat Tools

---

	Core Definition : "Traps" are deployable consumable items placed on the ground by players, NPCs, or siege defenses, designed to trigger when enemies interact 
with them.

	They are:
✔ Active items (not passive effects)
✔ Terrain-dependent
✔ Time-limited
✔ Tactical and premeditated
✔ Non-instant damage (mostly control & denial)

	Traps emphasize :
* Preparation
* Positioning
* Area control
* Tactical combat

---

		How Traps Are Used

	Deployment

Players can :
* Place traps manually on valid terrain
* Deploy them during combat or pre-combat
* Carry a limited number based on skill
* Only place one trap of each type per player by default (scalable via skills)

	Deployment Rules:
* Requires a short placement time (0.5–2s)
* Interrupted by damage or CC
* Cannot be placed while stunned/rooted
* Must be on solid ground (no air, water unless specified)
* Visible or hidden depending on trap type

---

		Trap Categories

	Traps are divided by function :

---

	1. Control Traps

	Designed to stop or restrict enemies.

	Examples :
* "Root Trap" – Roots target for X seconds
* "Freeze Trap" – Freezes target (full CC)
* "Snare Trap" – Slows movement heavily
* "Silence Trap" – Prevents spellcasting briefly

	Use case:
➡ Control elite enemies, manage packs, protect casters

---

	2. Denial Traps

	Designed to block space or routes.

	Examples :
* "Fire Field Trap" – Burning ground
* "Poison Cloud Trap" – Area DoT & debuff
* "Fear Mist Trap" – Causes fear when entering

	Use case:
➡ Chokepoints, corridors, retreats, sieges

---

	3. Utility Traps

	Supportive or tactical.

	Examples:
* "Alarm Trap" – Alerts when triggered
* "Reveal Trap" – Reveals stealthed enemies
* "Marking Trap" – Applies tracking debuff

	Use case:
➡ Recon, defense, stealth counterplay

---

	4. Damage Traps

	Not primary DPS tools, but burst or pressure.

	Examples:
* "Explosive Trap" – Burst damage on trigger
* "Shard Trap" – Bleed & armor reduction

	Use case:
➡ Finishing, punishing careless movement

---

	Trap Triggering

	A trap activates when :
* Enemy steps on it
* Enemy attacks it (some traps can be destroyed)
* Trap timer expires (some self-detonate)
* Environmental interaction occurs (fire spreads, frost breaks)

	Triggers depend on trap type :
* Proximity-based
* Contact-based
* Timer-based

---

		Trap Visibility & Detection

	Not all traps are visible.

	Trap States :
* Visible traps – Can be avoided or destroyed
* Hidden traps – Require detection or trigger blindly

	Detection:
* Rogue/Scout classes have higher detection
* Some consumables reveal traps
* Environmental light affects visibility (ties into Light/Darkness system)
* Assassin Playable classes.
* Focus Vision - passive ability.

---

		Trap Durability & Counterplay

	Traps can be:
* Destroyed by AoE
* Burned by fire
* Dispelled by magic
* Triggered prematurely
* Can be detected by Focus Vision.

	Some traps are immune to certain countermeasures.
	This ensures traps are tactical, not permanent.

---

		Trap Limits

	To prevent abuse :
* Players can only have X traps active at once (default: 2–3)
* Same trap type cannot stack from one player
* Traps expire after a time if not triggered
* Cooldowns per trap type

	Scaling via:
* Trap Trainer skills
* Specializations
* Gear bonuses

---

		PvE vs PvP Trap Rules

	PvE : 
* Full effect
* Used against monsters, elites, bosses (some immune)
* Bosses often trigger but resist full CC

	PvP : 

* Reduced CC durations
* Diminishing returns apply
* Some traps disabled in arenas
* Certain trap types only work in open-world PvP

---

		Environmental Interactions

	Traps integrate deeply with your environmental systems :

	Fire :
* Can ignite fire-based traps
* Can destroy frost traps
* Can trigger chain reactions

	Frost : 
* Enhances freeze traps
* Can extend root durations
* Makes ground slippery for traps

	Wind : 
* Can push poison clouds
* Can disperse mist traps
* Can reduce placement accuracy

	Light & Darkness : 
* Light reveals hidden traps
* Darkness improves stealth traps
* Some traps only function in low light

---

		Trap Crafting System

	Traps are crafted consumables, not learned skills.

	Crafting involves:
* Schematics
* Components
* Assembly
* Trap quality tiers

---

	Trap Quality Tiers : 
* Common – Basic effects
* Refined – Longer duration, stronger control
* Masterwork – Additional effects
* Ascended – Special interactions, rare materials

---

		Trap Components

	Each trap consists of :
* Trigger Mechanism – Determines how it activates
* Payload – Effect type
* Casing – Durability & stealth
* Modifier Slot – Optional enhancement

Example : Freeze Trap = Pressure Trigger + Frost Crystal Payload + Reinforced Casing + Shatter Modifier

---

		Trap Trainer Integration

	The Trap Trainer NPC :

	1. Teaches:
* Trap placement rules
* Trap limits
* Detection & counterplay
* Environmental interactions
* Advanced crafting

	2. Sells:
* Schematics
* Rare components
* Trap tools
* Enhancers
* Trap removal kits

	3. Unlocks:
* Specialization paths:
	* Control Trapper
	* Demolition Trapper
	* Recon Trapper

---

		Trap Specializations

	Control Specialist : 
* Increased CC durations
* Reduced cooldowns
* More trap slots

	Demolition Specialist : 
* Higher damage
* Chain reactions
* Environmental synergy

	Recon Specialist : 
* Detection bonuses
* Alarm traps enhanced
* Stealth interaction

---

		Trap & Siege Integration

	In sieges and village defense:
* Traps can be placed in:
	* Stairways
	* Gates
	* Corridors
	* Rooftops
* NPCs may deploy traps
* Siege-specific traps unlockable
* Some traps restricted to defensive use

---

		Trap Limitations

	To preserve balance:
✔ Traps cannot one-shot bosses
✔ Traps cannot permanently lock targets
✔ Traps require planning
✔ Traps are consumables
✔ Traps are counterable

