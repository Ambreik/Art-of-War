			Advanced Trap Counterplay System

		1. Core Philosophy

	Traps are not meant to be "unavoidable" damage sources. The "Advanced Counterplay System" allows players to interact with, evade, or manipulate traps, providing
depth for both PvE and PvP.

* Encourages skillful detection and strategic gameplay
* Rewards Assassins or players with Focus Vision passive
* Promotes team-based coordination : tank detects, others manipulate or bypass

---

		2. Counterplay Categories

*Disarm
Player or NPC disables a trap before activation
- Requires skill or item (Ex: “Disarm Kit”) 
- Success depends on trap rank: Rank I–V, success chance lowers for higher rank 
- Cooldown: 10–15 seconds per attempt 
- PvP: Partial success possible (trap partially triggers, reduced effect)

*Trigger Manipulation
Players manipulate the trap to affect another target
- Example: Push/Pull the trap with a ranged spell or skill
- Only certain trap types (like Pushback, Fire, or Explosive traps) 
- PvE: trap damage redirected to mobs 
- PvP: trap effect may shift to nearest hostile player (if detected and manipulated)

Pets & Companions
Pets can detect, trigger, or absorb traps
- Pets with "Trap Awareness" can detect nearby traps 
- Pets may activate traps first to protect the owner 
- Pet size / durability affects their success (e.g., small pets may trigger lighter traps)

---

		3. Detection & Interaction Mechanics

	3.1 Trap Detection

* Passive Ability : Focus Vision
	* Detects traps in range (PvP & PvE)
	* Assassin class: +20–30% detection chance for hidden traps
* Active Interaction :
	* Player can scan for traps using skill or consumable (e.g., “Trap Scanner Device”)

	3.2 Trap Interaction Outcomes

| Outcome         | Effect                       | PvP                   | PvE                         |
| --------------- | ---------------------------- | --------------------- | --------------------------- |
| Success         | Trap disarmed or manipulated | Partial/Full negation | Trap disabled or redirected |
| Failure         | Trap triggers on player      | Normal effect         | Normal effect               |
| Partial Success | Trap partially triggers      | Reduced damage        | Reduced damage or effect    |

---

		4. Trigger Manipulation Details

* Applicable Trap Types : Fire, Freeze, Pushback, Root, Poison, Explosive
* Skill / Action Needed : “Manipulate Trap” or relevant spell/ability
* Cooldowns : 10–20s, shared per player to avoid spam
* PvE Notes : Can redirect trap to enemy mobs
* PvP Notes : Trap effect redirects toward closest detected hostile player

---

		5. Pets & Companions Mechanics

* Pets can interact with traps if equipped with "Trap Awareness" passive
* Abilities :
	* Trap Detection : Sends alert to player (visual icon or ping)
	* Trap Triggering : Pet can intentionally trigger trap to reduce risk for player
	* Trap Absorption : Some tank-type pets can absorb damage/effect of trap once per trap
* Cooldown & Limitations :
	* 1–2 interactions per trap per pet
	* Only works with pets near trap (proximity requirement)

---

		6. PvP / PvE Balance Considerations

* PvP :
	* Disarm and manipulation have partial chance only
	* High-rank traps (IV–V) cannot be fully disarmed without high skill or special items
	* Prevents abuse of trap mechanics to dominate PvP unfairly

* PvE :
	* Full disarm possible with skill and tools
	* Encourages dungeon teams to coordinate tank/pet/player interactions
	* Traps scale with dungeon difficulty / mob density

---

		7. Integration with Trap Trainer System

* Roderic Ironbind (Stormreach Watch) : Players learn trap basics, low-rank disarm/manipulate skills
* Sly Coppernail (Outlaw Trainer) : Advanced counterplay training (higher success rate, access to advanced skills)
* Rare schematics from Outlaws allow practice on elite traps (PvP/PvE)
* Focus Vision passive integrated: assists in both PvE & PvP for detection

