		3. World Source Rules

1. Region-Based Harvesting
	* Each material is tied to one or more regions.
	* Harvesting often requires environmental survival tools (e.g., warm clothes in Frozen Territory, fire resistance in Tenebress).

2. Material Rarity
	* Materials are tied to trap ranks (higher rank = lower spawn / success rate).
	* Common base materials (e.g., wood, metallic strings) are always retained on failure; rare materials are lost on failed crafting.

3. Elemental Hazard Interaction
	* Materials harvested in elemental zones may impose temporary environmental debuffs (e.g., Frosted Iron Shard causes slow without proper gear, Obsidian Shard area causes burn damage).
   
4. PvE / PvP Balance
	* Rare materials spawn in dangerous locations (elite monsters, environmental hazards) to avoid over-farming.
	* Certain materials are Outlaw-exclusive (can only be crafted into tradable traps by Outlaw faction).

5. Event & Spawn Notes
	* Some rare materials may be tied to world events (e.g., thin-ice tremor in "Frozen Territory" reveals Frozen Core nodes).
	* Others require exploration of hidden or secret locations (e.g., Mystic Bone Fragment in Middlean Islands ruins, Shadow Resin in Underworld).

---

		Integration Notes

* Trap Trainer Locations :
	* Stormreach Watch – beginner traps, uses common materials (non-tradable).
	* Roderic Ironbind / Outlaw Trainer – advanced traps (III–V), rare materials, tradable traps.

* Global Cooldown & Max Trap Rules :
	* Even with rare materials, max active traps per player, placement cooldowns, and dungeon scaling remain in effect.

---

	4. Crafting Implications : 

* Rank I–II traps : Only common materials, easily available
* Rank III–V traps : Require at least 1–2 rare components
* Outlaw traps crafted at Alabam : All materials tradable
* Stormreach Watch traps : Rare materials cannot be used (to keep system controlled)
* Balance : Low chance of crafting high-rank traps avoids flood in Auction House or PvP harassment

---

	5. Notes on Material Recovery / Salvage

* Stormreach Watch: Fail → lose only extra materials
* Outlaw traps: Fail → lose everything, except base common components (like Perfect Wood + Metallic String)
* Encourages planning and risk-reward for crafting rare traps

