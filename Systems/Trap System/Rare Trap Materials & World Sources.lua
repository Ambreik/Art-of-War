			Rare Trap Materials & World Sources

	1. Core Philosophy

	Rare trap materials are special crafting components used only in high-rank traps (III–V).
	They are world-limited, event-limited, or region-specific, which prevents players from over-farming and ensures that crafting high-tier traps requires 
exploration, strategy, and risk.

* Regular traps (I–II) : Common materials only
* Elite traps (III–V) : Requires rare materials + common materials

