			PvP vs PvE TRAP TUNING TABLES
	*(System Balancing File)*

---

		1. CORE BALANCE PHILOSOPHY (NON-NEGOTIABLE)

> PvE traps = control + punishment
> PvP traps = pressure + disruption

	Traps must :
* Feel powerful in PvE
* Feel fair and readable in PvP
* Never remove player agency in PvP
* Scale with rank without scaling frustration

---

		2. GLOBAL TRAP MODIFIERS

	These modifiers are applied after trap rank calculations.

| Context | Damage Multiplier | CC Duration | Detection Time  |
| ------- | ----------------- | ----------- | --------------- |
| PvE     | 100%              | 100%        | Normal          |
| PvP     | 55–70%            | 40–60%      | Slightly Faster |

	Exact % inside PvP depends on trap category, not rank.

---

		3. TRAP CATEGORY TUNING (PvP vs PvE)

	3.1 DAMAGE TRAPS
	
*(Fire, Spike, Explosive, Poison)*

| Aspect           | PvE          | PvP                                |
| ---------------- | ------------ | ---------------------------------- |
| Damage           | Full scaling | Reduced                            |
| DoT Duration     | Full         | Shortened                          |
| Critical Effects | Enabled      | Disabled                           |
| Kill Potential   | Yes          | No (cannot solo-kill equal player) |

	Traps soften targets in PvP — they do not finish them.

---

	3.2 CONTROL / CC TRAPS

*(Root, Snare, Frozen, Net, Shock)*

| Aspect              | PvE     | PvP     |
| ------------------- | ------- | ------- |
| CC Duration         | Full    | Reduced |
| Chain CC            | Allowed | Limited |
| Break on Damage     | No      | Yes     |
| Diminishing Returns | No      | Yes     |

	Frozen (example) :
* PvE: Full freeze
* PvP: Short immobilize + slow

---

	3.3 UTILITY TRAPS

*(Reveal, Silence Zone, Fear Pulse, Detection)*

| Aspect          | PvE  | PvP              |
| --------------- | ---- | ---------------- |
| Duration        | Full | Slightly reduced |
| Area            | Full | Full             |
| Cooldown Impact | None | Enforced         |

	Utility traps remain strategic even in PvP.

---

	4. TRAP RANK SCALING (IMPORTANT)

	Rank increases reliability, not raw dominance.

	Rank Effects Scale :
* Trigger reliability
* Detection difficulty
* Environmental resistance

	Rank Does NOT Scale :
* PvP damage beyond cap
* PvP CC duration beyond limit

---

		5. RANK-BASED PvP CAPS

| Trap Rank | PvP Damage Cap | PvP CC Cap |
| --------- | -------------- | ---------- |
| I         | Low            | Very short |
| II        | Low–Medium     | Short      |
| III       | Medium         | Moderate   |
| IV        | Medium+        | Controlled |
| V         | Fixed Max      | Fixed Max  |

	Rank V traps feel elite, not oppressive.

---

		6. MULTI-TRAP & STACKING RULES

	PvE : 
* Full stacking allowed
* Synergies encouraged
* Area denial viable

	PvP : 
* Same-type trap stacking reduced
* Different categories allowed
* Soft internal cooldown between triggers

	Example :
* Two Frost traps → diminishing effect
* Frost + Fire → valid combo

---

		7. DETECTION & COUNTERPLAY ADJUSTMENTS

| Mechanic            | PvE      | PvP                    |
| ------------------- | -------- | ---------------------- |
| Focus Vision        | Standard | Slightly stronger      |
| Assassin Detection  | Standard | Slightly higher chance |
| Environmental Tells | Normal   | Clearer                |

	PvP favors reaction and awareness, not surprise kills.

---

		8. OUTLAW (ALABAM) TRAPS – SPECIAL CASE

| Aspect               | PvE    | PvP             |
| -------------------- | ------ | --------------- |
| Detection Difficulty | High   | Medium          |
| Damage               | Full   | Same PvP caps   |
| Trigger Delay        | Short  | Slightly longer |
| Counterplay Window   | Narrow | Clear           |

	Outlaw traps are scarier, not unfair.

---

		9. DUNGEON & MEGADUNGEON ADJUSTMENTS

	In high-density PvE zones :
* Lower internal cooldowns
* Higher max active traps
* Faster arming times

	PvP rules never change, even in shared zones.

---

		10. ONE-SCREEN SUMMARY (DESIGN RULE)

> PvE traps dominate the battlefield.
> PvP traps influence decisions.

