			TRAP SYSTEM — CATEGORY & RANK CLASSIFICATION

	*(System-Level Design File — Foundation for Schematics, Trainers, PvP Tuning)*

---

		1. Trap System Core Philosophy

	Traps are active, placeable, consumable combat items :
* Tactical, not dominant
* Preparatory, not reactive
* Limited, not spammed
* Risk-based crafting
* Strongly tied to knowledge, positioning, and intent

	They exist to:
* Shape encounters
* Create terrain control
* Apply soft crowd control
* Enable escape or pressure
* Reward foresight

---

		2. Trap Categories (Independent Rank Progression)

> Each trap category has its own Rank I–V progression.
> Ranks do NOT transfer between categories.

	This prevents:
* Universal trap mastery
* Over-centralized power
* One-player-does-all abuse

---

	2.1 CONTROL TRAPS

	Purpose : Enemy restriction & tempo control

	Effects :
* Slow
* Root
* Snare
* Short freeze
* Directional knock

	Examples :
* Snare Net Trap
* Frost Clamp
* Grounding Coil

	PvP Philosophy:
* Short duration
* Subject to diminishing returns
* Breakable or avoidable

---

	2.2 DAMAGE TRAPS

	Purpose : Supplemental damage & pressure

	Effects :
* Physical burst
* Elemental pulse
* Bleed / burn over time

	Examples :
* Shrapnel Plate
* Fireburst Rune
* Razor Coil

	PvP Philosophy :
* Never lethal alone
* Damage tuned lower in PvP
* No burst stacking allowed

---

	2.3 DEBUFF TRAPS

	Purpose : Combat disruption

	Effects :
* Accuracy reduction
* Cast time increase
* Resource drain (minor)
* Vision impairment

	Examples :
* Dust Veil Trap
* Mana Fracture Snare
* Wind Hex Sigil

	PvP Philosophy :
* Tactical advantage only
* Counterplay available
* Duration limited

---

	2.4 DEFENSIVE / UTILITY TRAPS

	Purpose : Protection & battlefield shaping

	Effects :
* Temporary shields
* Area denial
* Stealth reveal
* Alarm triggers

	Examples :
* Ward Plate
* Lightflare Beacon
* Echo Alarm Trap

	PvP Philosophy :
* Information and positioning tools
* No direct damage
* Strong synergy, low raw power

---

	2.5 ENVIRONMENTAL TRAPS

	Purpose : Terrain exploitation

	Effects :
* Ice slicks
* Wind push zones
* Collapsing ground
* Line-of-sight blockers

	Examples :
* Frostslide Trap
* Gust Anchor
* Stonefall Trigger

	PvP Philosophy :
* Highly situational
* Map-dependent
* Skill expression heavy

---

		3. Trap Rank System (Per Category)

	Each category has five quality ranks, progressing independently.

| Rank | Quality Tier      | Description                    |
| ---- | ----------------- | ------------------------------ |
| I    | Crude             | Basic function, short duration |
| II   | Refined           | Stable, improved effect        |
| III  | Engineered        | Reliable, tactical             |
| IV   | Masterworked      | High efficiency                |
| V    | Illicit Prototype | Rare, dangerous                |

---

		4. Crafting Success Logic (Rank-Based)

	4.1 Universal Success Chances (Baseline)

| Rank | Base Craft Chance |
| ---- | ----------------- |
| I    | 95–100%           |
| II   | 70–85%            |
| III  | 40–60%            |
| IV   | 15–25%            |
| V    | 5–10%             |

	Modifiers may apply later (tools, events, buffs).

---

		5. Failure Consequences (Trainer-Dependent)

	5.1 Stormreach Watch — Roderic Ironbind (Netar Island)

	Failure Rule: Partial Loss

* Common Materials : Preserved
* Extra / Advanced Materials : Lost

	Example :
* Common: Perfect Wood Piece, Metallic String → Saved
* Extra: Copper Patches, Frost Resin → Lost

	Design Purpose :
* Encourages learning
* Protects new players
* Limits abuse without punishment

	Crafted traps :
* Soulbound
* Non-tradable
* PvE-focused

---

	5.2 Alabam Trap Master (Outlaw Territory)

	Failure Rule: Total Loss
* ALL materials lost
* No recovery
* No safety net

	Design Purpose :
* Risk vs reward
* Economic tension
* Black-market scarcity

	Crafted traps :
* Tradable
* Auctionable
* Smuggler-distributed

---

		6. Rank Access Restrictions

| Rank | Regular Players | Outlaws |
| ---- | --------------- | ------- |
| I–II | ✔ Yes           | ✔ Yes   |
| III  | ✔ Limited       | ✔ Yes   |
| IV   | ❌ No            | ✔ Yes   |
| V    | ❌ No            | ✔ Yes   |

	Stormreach Trainer :
* Caps at Rank III
* No access to illicit schematics

	Alabam Trainer :
* Full Rank I–V access

---

		7. PvP Safeguards (Global)

* Max active traps per player
* Internal cooldown on placement
* PvP effect scaling applied automatically
* CC diminishing returns apply
* Trap stacking rules enforced
* Placement restrictions in towns/safe zones

---

		8. Detection & Counterplay Hooks (System-Level)

	Hooks for later files :
* Focus Vision Passive (enhanced detection)
* Assassin Class Chance-Based Detection
* Environmental clues (faint glow, sound, distortion)

