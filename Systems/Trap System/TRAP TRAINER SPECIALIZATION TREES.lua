			TRAP TRAINER SPECIALIZATION TREES
	*(System File – Progression & Access Control)*

	This file defines who can craft what, how far they can go, and what risks apply.

	We will clearly separate :
* "Roderic Ironbind" → Initiation & Defensive Utility
* "Sly Coppernail" → Full System, High Risk, High Power

	No overlap confusion. No economy leaks.

---

		1. CORE DESIGN PHILOSOPHY

	Two Trainers, Two Philosophies

| Trainer                          | Purpose                                    |
| -------------------------------- | ------------------------------------------ |
| Roderic Ironbind (Stormreach)    | Teach traps as tactical tools              |
| Alabam Trapmaster (Outlaw Lands) | Teach traps as weapons and control systems |

	Key rule:

> Roderic creates discipline. Alabam creates dominance.

---

		2. RODERIC IRONBIND – STORMREACH WATCH

* Role : Initiation Trap Trainer
* Audience : Regular players (non-Outlaws)
* Risk Level : Low
* Economy Impact : Safe
* Tradability : ❌ Non-tradable traps only

---

	2.1 Roderic’s Specialization Tree

	Tier 1 – Field Preparation
*(Unlocked immediately)*

* Learn "Trap Placement"
* Unlocks:
	* Snare Net Trap (Rank I–II)
	* Dust Veil Trap (Rank I)
* Reduces trap arming time slightly

	Purpose : Teach when and where to place traps.

---

	Tier 2 – Defensive Control
*(Requires Tier 1 + village REP)*

* Unlocks:
	* Frost Clamp Trap (Rank II)
	* Lightflare Beacon Trap (Rank II)
* Increases trap visibility to allies
* Slight increase in trap durability (PvE)

	Purpose : Village defense, chokepoints, invasion prep.

---

	Tier 3 – Tactical Deployment
*(Max for Roderic)*

* Unlocks:
	* Ward Plate Trap (Rank II–III)
	* Gust Anchor Trap (Rank II)
* Reduces "Global Trap Cooldown" by 10% (PvE only)
* Trap failure:
	* Common materials refunded
	* Extra materials lost

	HARD LIMIT :
* ❌ No Damage Traps
* ❌ No Debilitating Mana Traps
* ❌ No Rank IV–V traps

---

	2.2 Roderic’s Hard Restrictions

* Cannot craft:
	* Fireburst Rune Trap
	* Mana Fracture Sigil
	* Environmental traps above Rank II
* Traps:
	* Bound on creation
	* Cannot be sold, traded, or mailed
* PvP:
	* Reduced effectiveness baseline

	Narrative Justification : Roderic refuses to teach "ambush warfare."

---

		3. Sly Coppernail - TRAPMASTER – OUTLAW FACTION

* Role : Master Trap Trainer
* Audience : Outlaws only
* Risk Level : High
* Economy Impact : Controlled chaos
* Tradability : ✅ Tradable traps only

---

	3.1 Entry Requirement

	To train under Sly Coppernail, player must:

* Be flagged "Outlaw"
* Complete betrayal chain

	This locks power behind consequence.

---

	3.2 Sly Coppernail’s Specialization Tree

	Tier 1 – Improvised Cruelty
*(Entry tier)*

* Unlocks :
	* Snare Net Trap (Rank I–III)
	* Shrapnel Plate (Rank I–II)
* Trap placement is faster
* Failure :
	* ALL materials lost

	Lesson: Risk starts immediately.

---

	Tier 2 – Control & Damage Synergy

* Unlocks :
	* Frost Clamp Trap (Rank III–IV)
	* Dust Veil Trap (Rank III–IV)
	* Fireburst Rune Trap (Rank III)
* Gain ability :
	* Place traps closer together
* GTC reduced by 10% globally

---

	Tier 3 – Battlefield Manipulation

* Unlocks :
	* Mana Fracture Sigil (Rank III–IV)
	* Gust Anchor Trap (Rank III–IV)
	* Frostslide Trap (Rank III)
* Traps gain :
	* Improved trigger logic
	* Smarter enemy prioritization

---

	Tier 4 – Master Saboteur
*(Maximum progression)*

* Unlocks :
	* ALL traps Rank V
* Additional mechanics :
	* +1 Max Active Trap
	* Reduced detection chance
* Failure chance is high :
	* Rank V traps have very low success rates
	* Complete material loss on failure

	Designed to :
* Prevent market flooding
* Reward dedication, not grinding

---

		4. FAILURE & RISK COMPARISON

| Trainer | Failure Result            |
| ------- | ------------------------- |
| Roderic | Common materials refunded |
| Alabam  | Total loss                |

	This reinforces :
* Safety vs Power
* Defense vs Aggression

---

		5. TRAP DETECTION INTERACTION (HOOK)

* Alabam traps :
	* Harder to detect
* Roderic traps :
	* Easier to detect, clearer visuals
* Detection affected by :
	* Trap rank
	* Focus Vision
	* Assassin passive (chance-based)



