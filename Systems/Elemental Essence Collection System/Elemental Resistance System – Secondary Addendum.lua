			Elemental Resistance System – Secondary Addendum

		1. Purpose & Design Philosophy

	This file serves as a secondary system addendum to the core "Elemental System". Its role is to define how "Elemental Resistance" functions mechanically, 
ensuring:

* Resistance mitigates damage without enabling immunity
* High-end grinding remains dangerous
* No build can trivialize elemental encounters
* Long-term balance and future-proofing

	This system explicitly avoids linear scaling and hard-immunity outcomes.

---

		2. Elemental Resistance Sources

	2.1 Permanent Gear Sources (Hard-Limited)

| Slot     | Max Resistance |
| -------- | -------------- |
| Armor    | 10%            |
| Boots    | 10%            |
| Necklace | 10%            |

	Maximum permanent resistance: 30%
	These values are considered baseline mitigation and are always active.

---

	2.2 Temporary & Conditional Sources

	Temporary resistance sources may increase total resistance but are capped globally.

	Examples:
* Consumables (Flasks, Potions, Food)
* Mount passive bonuses
* Pet auras or traits
* Temporary buffs or world effects

---

		3. Consumable Hierarchy Rules

	Elemental resistance consumables follow a strict priority system :

| Rank | Consumable Type | Rules                   |
| ---- | --------------- | ----------------------- |
| I    | Flask           | Overrides Potion & Food |
| II   | Potion          | Overrides Food          |
| III  | Food            | Lowest priority         |

	Rules:
* Consumables of the same effect do not stack
* Lower-rank consumables cannot override higher-rank effects
* Only one resistance consumable per element can be active

---

		4. Global Resistance Cap (Hard Cap)

	Maximum total elemental resistance: 50%

	Rules:
* Any resistance value above 50% is ignored
* Excess resistance is not converted or scaled
* Cap applies universally (gear + consumables + mounts + pets)

	This hard cap exists as a safety ceiling, not a balance target.

---

		5. Resistance Scaling Model (Non-Linear)

	"Elemental Resistance" does not reduce damage linearly.
	Instead, resistance uses diminishing effectiveness scaling, where each additional percent grants less real mitigation.

	5.1 Effective Mitigation Table

| Resistance Shown | Effective Damage Reduction (Approx.) |
| ---------------- | ------------------------------------ |
| 1%               | ~0.7%                                |
| 5%               | ~3.5%                                |
| 10%              | ~7%                                  |
| 20%              | ~15%                                 |
| 30%              | ~23%                                 |
| 40%              | ~33%                                 |
| 50% (Cap)        | ~45%                                 |

	At no point does resistance negate damage entirely.

---

		6. Combat vs Environmental Damage

	6.1 Environmental Damage

	Includes:
* Frost exposure
* Wind chill
* Heat zones
* Elemental terrain hazards

	Rules:
* Resistance applies at full effectiveness
* Intended to ease traversal and survival
* Does not trivialize combat

---

	6.2 Combat Damage

	Includes:
* Monster elemental attacks
* Spell-based elemental damage
* Environmental combat zones

	Rules:
* Resistance applies with diminishing scaling
* Large pulls remain lethal
* Healing, positioning, and control remain mandatory

---

		7. High-Density Combat Safeguards

	Even at maximum resistance :
* Players take more than half elemental damage
* Multiple attackers scale damage faster than mitigation
* Burst damage remains dangerous
* Resistance buys time, not immunity

	This ensures grind zones and elite encounters retain threat.

---

		8. Pets & Mount Interaction

* Pet and mount resistance bonuses count toward the 50% cap
* They do not bypass scaling rules
* Intended as situational advantages, not permanent power

---

		9. Anti-Exploit Rules

	The following rules are absolute :
* Resistance cannot exceed 50%
* Excess resistance is discarded
* No resistance converts into avoidance or immunity
* No stacking loopholes exist between systems

---

		10. System Intent Summary

	Elemental Resistance is designed to :
* Smooth incoming elemental damage
* Reward preparation and regional awareness
* Preserve danger in all combat scenarios
* Prevent AFK tanking or abuse

	Resistance enhances survival — it never replaces skill.

