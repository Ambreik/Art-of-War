---

		Equipment Enhancement – Elemental Stats

	Armor :
* Adds "Elemental Resistance"
* Max value: 10%
* Each application:
	* Chance-based
	* Failure consume the core

	Weapon Enhancement
* Stat: "Strong Against [Element]"
* Max value: 10%

	Each enhancement attempt is independent
	Can be added 10x successful "Elemental Core" and doesnt influence the upgrade of the weapon (+).
	
