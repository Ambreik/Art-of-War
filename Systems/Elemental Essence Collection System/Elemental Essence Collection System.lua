			Elemental Essence Collection System

	Core Concept : "Elemental Essences" are latent energies extracted from living creatures, unlocked only through "Alchemical Catalysts". Without a catalyst, 
monsters cannot drop essences.

	This ensures:
✔ Controlled progression
✔ Prevents passive farming abuse
✔ Gives Alchemist a permanent economy role

---

		Alchemist – Essence Catalyst Items

	Essence Extraction Catalysts

	Purchased exclusively from "Alchemist NPCs".

| Item Name               | Duration    | Drop Chance Modifier | Notes                   |
| ----------------------- | ----------- | -------------------- | ----------------------- |
| Minor Extraction Vial   | 30 minutes  | Low                  | Entry-level, affordable |
| Refined Essence Phial   | 60 minutes  | Medium               | Standard farming        |
| Stabilized Alchemy Core | 120 minutes | High                 | Rare, costly            |
| Master Alchemist Sigil  | 180 minutes | Highest              | Limited availability    |

	Activation Rules : 

* Must be manually activated from the inventory.
* Only one catalyst active at a time
* Timer pauses when logged out
* Cannot be stacked or refreshed

	UI shows active essence type eligibility + remaining time

---

		Essence Drop Rules

	General Rules : 
* Drops only when a catalyst is active
* Very low base chance (modified by catalyst + monster rank)
* Never drops from :
	* Mystic Stones
	* Bosses
	* Elite Creatures
	* World Bosses
	* Notorious Monsters (NM)

	This keeps essences:
✔ Grind-based
✔ Anti-burst
✔ Non-exploitable

---

		Elemental Essence Types & Sources

	Earth Essence 
	
	Sources :
* Beasts, golems, animals, humanoids tied to land
* Regions :
	* Dendera
	* Zonen
	* Aarong Forest

	Affinity Logic : Physical, soil, stone, rooted life

---

	Wind / Thunder Essence

	Sources :
* Agile creatures, flying beasts, jungle predators
* Regions :
	* Lagooma Jungle (primary)
	* Rare spawns in other regions

	Element Notes :
* Wind and Thunder are mechanically linked
* Used interchangeably in crafting logic

---

	Fire Essence

	Sources :
* Flame-affiliated creatures
* Regions :
	* Tenebress Territories
	* Fire-aligned mobs across all continents

	Environmental Tie :
* Fire creatures often immune to frost debuffs

---

	Water Essence

	Sources :
* Aquatic and amphibious creatures
* Regions:
	* Underwater Realm
	* Seashores, beaches
	* Rivers and lakes

	Special Rule :
* Higher drop chance underwater
* Slightly reduced on land-based water mobs

---

	Shadow Essence

	Sources :
* Shadow-aligned creatures only (not monsters)
* Regions:
	* Underworld Realm (primary)
	* Select caves and dungeons in normal realm

	Design Rule :
* Shadow is an independent element
* Not affected by light-based resist systems

---

	Light Essence (RAREST)

	Sources :
* Holy or light-aligned creatures
* Regions:
	* Tower of Infinite (special floors)
	* Very rare creatures in normal realm

	Drop Logic :
* Lowest base drop chance
* Only from specifically flagged creatures
	No generic farming for Light Essence

---

		Drop Chance Modifiers

	Monster Level & Rank Scaling

* Higher-level monsters = higher chance
* Rank increases chance slightly:
	* Rank I → Rank II → Rank III
* Caps exist to prevent high-level abuse

	Catalyst Influence : 

Final drop chance =
`Base Chance × Catalyst Modifier × Monster Rank Modifier`

