			Commander: Tanira the Tideblade

**Location:** Lagooma Jungle, Third Region → Ambra Woods (Area II) → *Coastal City*
**Element:** Water / Wind
**Threat Tier:** Moderate (Elite Commander – Precision Duelist & Battlefield Disruptor)
**Combat Arena:** The flooded docks and coral ruins of the Coastal City, surrounded by shallow waters where the tides shift dynamically as the fight progresses.

---

### 🧬 **Lore**

Tanira was once the sworn protector of the Coastal Guardians, warriors who defended the sacred shores from deep-sea horrors. When the corruption reached the tides, her soul fused with the ocean’s fury — turning her into the Tideblade, a living current of vengeance.
Her movements ripple through the air and water, and each strike carries the weight of a storm.

> “You hear the sea’s song… but it sings only for the drowned.”

---

### ⚔️ **Combat Overview**

Tanira’s encounter is a **movement-heavy duel** emphasizing agility, positioning, and reaction time.
She thrives in chaos — punishing stationary groups while rewarding mobility and awareness.

#### **Phase 1 — Dance of the Currents (100%–60%)**

* Alternates between melee and ranged water strikes.
* Uses the terrain’s shallow water to boost her evasion and speed.
* Dodges predictable attacks using *Tidal Step*.

#### **Phase 2 — Stormbound Requiem (60%–0%)**

* The tide surges, flooding most of the arena.
* Gains new AoE wind-water combos.
* Introduces the “Siren’s Echo” mechanic — a haunting mirror illusion that mimics her attacks with delay.

---

### 💀 **Abilities**

#### 🌊 **Wave Crash**

* Summons a high-pressure water wave that sweeps across a 120° cone.
* Deals moderate Water damage and knocks back targets hit near the center.
* Leaves behind slippery ground, reducing player movement speed by 30% for 6s.

#### 🌪️ **Cyclone Dash**

* Dashes in a straight line, slashing enemies caught in her path.
* Each dash creates *Wind Lacerations* that persist for 8s, dealing periodic damage.
* Chain-casts up to 3 times, changing direction unpredictably.

#### 🧜‍♀️ **Siren’s Echo**

* Creates an illusory clone that repeats her last used ability 2 seconds later.
* Clone deals 50% reduced damage but applies *Echo Confusion* — a debuff that reverses player movement controls for 3s.

#### 🌫️ **Mist Veil (Trait)**

* Tanira disperses into vapor, avoiding all damage for 3 seconds.
* Triggers automatically once every **60 seconds** when a lethal hit would occur.
* After using *Mist Veil*, she becomes vulnerable for 5 seconds, taking +25% damage.

#### 💨 **Tempest Calling**

* At 50% HP, Tanira channels the storm — summoning 4 rotating whirlwinds around the arena.
* Each whirlwind pulls players inward, dealing Water/Wind mixed damage.
* Destroying a whirlwind reduces Tanira’s dodge chance by 5% (stacks).

---

### 💫 **Phase Transition – “The Rising Tide”**

At 60% HP, the water level rises, covering most of the battlefield.
New hazards appear: broken ship masts, debris, and strong currents that drag players.
During this phase:

* Movement speed is reduced by 15%.
* Jumping or evading abilities cost +25% more stamina.
* Tanira gains a passive regen aura while standing in deep water (+1% HP/sec).

---

### 🌪️ **Unique Commander Mechanic — “Fluid Grace”**

* Each time Tanira avoids an attack via dodge, parry, or immunity, she gains a stack of **Fluid Grace**.
* At 10 stacks, her next ability becomes empowered, gaining a *Storm Surge* modifier:

  * *Wave Crash* → summons twin waves.
  * *Cyclone Dash* → leaves behind a static storm zone.
  * *Siren’s Echo* → clone persists until destroyed.

---

### 🏹 **Recommended Strategy**

* Use ranged crowd control to interrupt *Siren’s Echo* and *Cyclone Dash* combos.
* Tanks should bait *Wave Crash* away from the main group.
* Melee players must disengage when *Mist Veil* triggers to avoid wasted cooldowns.
* Focus burst damage during her *Vulnerability Window* (after Mist Veil ends).

---

### 💠 **Shared Commander Trait – “Rage of the Sentry”**

Tanira’s rage bar builds as the tide rises or when illusions are destroyed.
Upon reaching 100 Rage:

* Unleashes **Tidal Rupture**, a massive wave across the entire arena.
* Pushes all players to the edge and resets all whirlwinds on the field.
* Defeating Tanira before her second *Rage Surge* awards +5 **Emblems of Sentries**.

