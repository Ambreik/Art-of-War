			SENTRY COMMANDER — HARLEY JHOO, THE FAITHLESS CLERIC

---

## I. GENERAL INFORMATION

* **Boss Name:** Harley Jhoo
* **Title:** The Faithless Cleric
* **Region / Settlement:** Dendera – Dravenhold / Zonen – Dreadhollow Fortress
* **Race / Type:** Undead Human – Corrupted Priest of Light
* **Combat Role:** Hybrid Support / Controller
* **Energy Resource:** Rage (max 100)
* **Scaling:** Dynamic – adjusts to players’ average level and party size
* **Combat Style:** Defensive caster who alternates between healing his sentries and draining the life of intruders. He excels at attrition and punishes clustered players.

---

## II. VISUAL DESCRIPTION

Harley Jhoo appears as a tall, gaunt figure cloaked in tattered priest robes once radiant white, now soaked in shadow. His eyes burn faintly with golden embers — the last remnants of his forsaken light. He carries a corrupted censer and a rune-covered staff that leaks radiant mist, alternating between holy light and dark energy.

* **Aura:** A shifting halo that flickers between light and shadow.
* **Spawn Animation:** Slowly levitates from a kneeling position, whispering, *“Light abandoned me… now I return the favor.”*
* **Death Effect:** Implodes into a burst of radiant dust, leaving a *Broken Censer* relic on the ground.

---

## III. SHARED MECHANICS

| Mechanic                   | Description                                                                                                         |
| -------------------------- | ------------------------------------------------------------------------------------------------------------------- |
| **Rage System**            | Builds rage from healing allies and receiving damage. Rage enables his strongest curses and area control abilities. |
| **Sentry Instinct**        | Reacts to the number of players — if outnumbered, Harley increases his healing output and shield capacity.          |
| **Corruption Pulse**       | Every 90 seconds, emits a radiant-dark explosion, reducing healing received by players within 20m by 5% for 10s.    |
| **Phase Transition (50%)** | Channels “Penance of the Fallen” — converts all healing into pure shadow damage.                                    |
| **Final Stand (15%)**      | Enters “Sanctum of Shadows,” becoming immune to CC and performing continuous drain combos for 10s.                  |

---

## IV. UNIQUE MECHANICS

1. **Twilight Benediction** – Harley periodically blesses one of his sentry allies, converting 50% of all damage dealt by that ally into healing for himself.
   *(Encourages players to focus-target the buffed unit or interrupt the cast.)*

2. **Liturgy of Agony** – Summons radiant circles around himself; players standing within gain reduced damage but suffer HP degeneration over time.
   *(A deceptive mechanic — looks beneficial but punishes standing still.)*

3. **Hymn of False Salvation** – At 25% HP, Harley attempts to “cleanse” the battlefield. He kneels for 5 seconds, then releases a massive explosion of light that deals damage equal to 40% of each player’s max HP. Can be interrupted if his Rage is reduced below 30 before the cast completes.

