## VI. PHASES

### **Phase 1 – “Echo of Light” (100–51%)**

* Alternates between healing sentries and casting AoE smites.
* Uses *Excommunication* frequently to disrupt players.
* Gains Rage faster when allies die.

### **Phase 2 – “Penance of Shadows” (50–16%)**

* Converts healing into shadow damage; allies no longer receive heals.
* Casts *Liturgy of Agony* zones and *Cathedral Collapse* more often.
* Periodically drains HP from random players to sustain himself.

### **Phase 3 – “Sanctum of Shadows” (15–0%)**

* Immune to control.
* Uses *Penance of the Fallen* if Rage reaches 100.
* Gains +25% attack speed and deals increased shadow damage.

---

## VII. ADDITIONAL NOTES

* **Difficulty:** Moderate – Designed for 10–25 players.
* **Scaling:** Stat multiplier increases with player presence (1.05x per 5 players).
* **Death Effect:** Leaves behind a *Fragment of Corruption (Radiant Dust)* collectible.
* **Lore Connection:** Once the High Cleric of Dravenhold, Harley Jhoo turned to the Sentries after his prayers for deliverance went unanswered. His corrupted hymns now echo through the ruined cathedrals of Dendera.

