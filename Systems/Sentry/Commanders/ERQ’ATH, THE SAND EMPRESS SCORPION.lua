## VII. ADDITIONAL NOTES

* **Difficulty:** Moderate+ — Requires strong mobility and healing coordination.
* **Scaling:** Each active player adds +3% to her HP and +2% to her damage.
* **Death Effect:** The earth drinks her venom, turning the ground crystalline for 2 minutes before dissolving.
* **Lore Connection:** Once a guardian spirit of the desert, Serq’ath was corrupted by the Sentries’ curse. Her pride and venom became one — birthing the Empress of Sands.

