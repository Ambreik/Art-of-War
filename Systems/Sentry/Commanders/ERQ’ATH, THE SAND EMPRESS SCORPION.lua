---

## IV. UNIQUE MECHANICS

1. **Queen’s Dominion** – While standing on sand, Serq’ath regenerates 2% HP every 10 seconds. Players can disrupt this by shattering *Venom Spires* during her Burrow Phase, which nullifies her healing.
   *(Encourages mobility and environmental control.)*

2. **Brood Summoning (Burrow Phase)** – During burrow, spawns 6–12 *Sand Broodlings* that apply mini poison stacks on hit. Destroying them prevents her from gaining excess Rage upon resurfacing.
   *(Adds teamwork and AoE responsibility.)*

3. **Empress’s Gaze** – Marks a random player every 20 seconds; after 5s, the target is impaled by her tail from underground unless they move at least 20 meters away.
   *(A positional awareness mechanic.)*

4. **Venom Cycle (Rage Core)** – Every 25 Rage gained changes her venom type:

   * **25 Rage:** *Paralytic Venom* (slows attack & movement speed)
   * **50 Rage:** *Corrosive Venom* (reduces armor/resistance)
   * **75 Rage:** *Withering Venom* (damage over time amplifies)
   * **100 Rage:** *Eternal Venom* (unleashes *Venom Tempest*, her ultimate ability)

