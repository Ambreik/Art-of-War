## VI. PHASES

### **Phase 1 – “Awakening Sands” (100–76%)**

* Heavy melee focus, stacking *Toxic Bloom*.
* Uses *Venom Wave* and *Tail Strike* frequently.
* Marks first *Empress’s Gaze* target at 90%.

### **Phase 2 – “Burrow and Brood” (75–51%)**

* Burrows, becoming untargetable. Spawns Broodlings and Venom Spires.
* Resurfaces with 50 Rage and increased poison damage.
* Uses *Sandstorm Dominion* twice per rotation.

### **Phase 3 – “Tyrant of the Dunes” (50–16%)**

* Alternates venom types rapidly; uses *Burrow Ambush* mid-combat.
* Gains +20% movement and attack speed.
* Players must manage constant poison stacks.

### **Phase 4 – “Eternal Venom” (15–0%)**

* Automatically reaches 100 Rage; triggers *Venom Tempest*.
* Arena floods with shifting sand and poison.
* Surviving the 15s storm ends her reign.

