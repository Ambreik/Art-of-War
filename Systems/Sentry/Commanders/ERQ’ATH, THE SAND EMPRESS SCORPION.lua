			SENTRY COMMANDER — SERQ’ATH, THE SAND EMPRESS SCORPION

---

## I. GENERAL INFORMATION

* **Boss Name:** Serq’ath
* **Title:** The Sand Empress Scorpion
* **Region / Settlement:**

  * *Fanthe’kar — Anghal Desert (Black Sands, Goldcap Town)*
  * *WasteLands — Sandee Lanes (Tandara City)*
* **Race / Type:** Colossal Scorpid Queen (Corrupted Beast)
* **Combat Role:** Brutal melee bruiser with venom DoTs and area denial control
* **Energy Resource:** Rage (max 100)
* **Scaling:** Adapts to players’ average item level and party size
* **Combat Style:** Highly mobile and punishing. Specializes in **poison stacking**, **burrow ambushes**, and **ground corruption** to dominate space.

---

## II. VISUAL DESCRIPTION

A titanic scorpion with glistening obsidian armor, her carapace adorned with glowing golden runes that pulse like molten veins. Her tail is segmented into barbs that leak luminous venom, and her eyes shimmer emerald with malice. As she moves, the sand beneath her feet boils and swirls.

* **Aura:** A shimmering heat distortion with a golden shimmer and drifting sand particles.
* **Spawn Animation:** Emerges violently from the desert ground, roaring, *“The sands bow to none but the Empress!”*
* **Death Effect:** Bursts apart into sand and crystal fragments; her stinger remains lodged in the earth as a relic.

