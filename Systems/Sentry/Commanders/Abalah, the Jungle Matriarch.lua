			Commander: Abalah, the Jungle Matriarch

**Location:** Lagooma Jungle, Third Region → Riverlands (Area I) → *Silvermoon City*
**Element:** Nature / Poison
**Threat Tier:** Moderate (Elite Commander – Strategic Battlefield Control)
**Combat Arena:** Dense overgrown battlefield filled with animated flora, toxic puddles, and creeping vines that react dynamically during combat.

---

### 🧬 **Lore**

Once a revered guardian spirit of the Riverlands, *Abalah* was bound to the cycle of growth and decay. When the Sentry corruption spread, her essence was twisted — the voice of the jungle now whispers madness through her. Her serpentine body winds through the vines of Silvermoon City as she calls the flora to consume all trespassers.

> “You trample my roots... you breathe my poison... and still, you dare call this yours?”

---

### ⚔️ **Combat Overview**

Abalah’s fight is a **two-phase encounter** that blends wide-area control with summon management.
She punishes groups that cluster together and rewards tactical use of terrain.

#### **Phase 1 — Wrath of the Wilds (100%–50%)**

* Constantly creates toxic ground zones and summons lesser beasts.
* Prioritizes ranged targets and healers with venomous abilities.
* Periodically releases jungle spores that cause confusion.

#### **Phase 2 — Venomous Ascension (50%–0%)**

* The battlefield becomes flooded with spreading vines.
* Abalah coils around a massive jungle tree and uses aerial attacks.
* Gains a new ultimate ability: *Jungle Wrathstorm* — transforming the entire area into a deadly bloom of poison and life-drain pulses.

---

### 💀 **Abilities**

#### 🌿 **Spore Burst**

* Unleashes clouds of toxic spores in random locations.
* **Effect:** Applies *Toxic Confusion* (chance to attack allies or NPCs for 4s).
* Spores linger for 10s; destroying them prematurely spreads smaller spores.

#### 🐍 **Venomous Embrace**

* Constricts the nearest target, immobilizing and applying stacking *Venom Bleed*.
* Each tick increases Poison damage taken by +5%.
* If the target dies, Abalah absorbs their vitality, healing 10% of her HP.

#### 🌺 **Jungle Wrath**

* Causes the ground to erupt with vines that lash all players within 20m.
* Each lash deals Nature damage and has a 25% chance to root for 2s.
* Summons **Vine Horrors** (small adds) that fixate on ranged targets.

#### 🐾 **Beastcall of the Matriarch (Trait)**

* Every 45s, Abalah calls forth 2–4 jungle beasts:

  * **Jungle Panther:** Stealth ambusher; targets lowest-HP player.
  * **Thornback Boar:** Charges through groups, dealing heavy knockback.
  * **Screeching Bird:** Casts *Echo of Madness* (reduces healing by 20%).

Killing beasts grants players a temporary **Herbal Essence Buff** (+2% Nature Resistance, stacks up to 3x).

---

### 💫 **Phase Transition – “Venomous Ascension”**

At 50% HP, Abalah slithers up the central jungle pillar and emits *Pollen Storm*.
Visibility drops and the entire field becomes a pulsing toxic jungle.
Players must destroy **four Venomous Pods** around the area to ground her again.

**Failure:**
If pods are not destroyed in 90s, *Jungle Wrathstorm* triggers early — an AoE burst dealing 80% of all players’ max HP.

---

### 🌿 **Unique Commander Mechanic — “Cycle of Bloom and Decay”**

* Each beast or plant minion killed increases Abalah’s *Decay Power*.
* At 100 stacks, she enters **Decay Overgrowth**, gaining +30% damage for 15s, but also taking +25% damage.
* This encourages controlled killing of her summons rather than mindless AoE.

---

### 🏹 **Recommended Strategy**

* Assign small strike teams to manage beasts and spores.
* Destroy Venomous Pods during Phase 2 to prevent *Wrathstorm*.
* Avoid heavy clustering; poison and confusion stack exponentially.
* Tank should rotate her facing away from the main group — her breath cone applies stacking *Venom Rot*.

---

### 🧩 **Shared Commander Trait – “Rage of the Sentry”**

As part of the Sentry Invasion roster, Abalah accumulates **Rage** over time:

* Gains Rage faster when her beasts die.
* Upon reaching 100 Rage, unleashes **Verdant Collapse** — instant poison nova around her.
* Killing her before the 3rd Rage build grants a bonus *Emblem of Sentries +5*.

