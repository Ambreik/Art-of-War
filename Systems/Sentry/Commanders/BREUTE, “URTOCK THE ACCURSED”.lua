## IV. UNIQUE MECHANICS

1. **Cursed Weapon (Rotshift Mechanic)** –
   Every time Breute enters **Cursed Frenzy**, *Hollow Remorse* changes form, gaining one of three curses:

   * **Bloodrend:** Converts part of his damage into lifesteal.
   * **Soulbreak:** Increases his crit chance and drains stamina on hit.
   * **Dreadflame:** Adds AoE explosions to cleave attacks.
     *(Each Frenzy alternates the curse, forcing defenders to adapt.)*

2. **Rage Echoes** –
   When exiting Cursed Frenzy, Breute leaves behind three *Rage Echoes* — spectral copies that mimic his previous combos with reduced damage.
   *(Players must destroy them quickly or they persist for 30s.)*

3. **Executioner’s Command** –
   Targets the top two players in threat; if they stay within melee range for 5s, he executes a massive overhead cleave (*Decapitating Wrath*) dealing 80% max HP.
   *(Tanks must alternate threat and use mitigation carefully.)*

4. **Chains of Defiance** –
   Periodically summons cursed chains from the ground, immobilizing players for 4s unless broken by allies.
   *(Promotes team coordination.)*

