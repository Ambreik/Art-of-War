## VI. PHASES

### **Phase 1 – “The Defiler Rises” (100–71%)**

* Uses *Groundbreaker Slam* and *Chains of Defiance* frequently.
* Builds Rage steadily.
* Introduces *Defiled Ground* and *Cursed Steel* stacks.
* Minor tank-swap mechanics start.

### **Phase 2 – “Cursed Frenzy: Bloodrend” (70–46%)**

* Enters first Frenzy at 100 Rage.
* Sword gains blood-red glow — life leech mechanics begin.
* Players must kite him to avoid his empowered cleaves.

### **Phase 3 – “Cursed Frenzy: Soulbreak” (45–21%)**

* Uses *Unholy Howl* more often, draining stamina/energy.
* *Rage Echoes* spawn after Frenzy ends, forcing split focus.
* Random player chains become more frequent.

### **Phase 4 – “Cursed Frenzy: Dreadflame” (20–0%)**

* Final berserk. Sword wreathed in black flame.
* Every 10s releases *Cursed Cataclysm* AoE burst.
* *Executioner’s Command* becomes lethal — tank coordination crucial.
* Upon defeat, the area is purged of his corruption with a blinding flash.

