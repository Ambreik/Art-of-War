			SENTRY COMMANDER — BREUTE, “URTOCK THE ACCURSED”

---

## I. GENERAL INFORMATION

* **Boss Name:** Breute
* **Title:** *Urtock the Accursed*
* **Region / Settlement:**

  * *Southern Abyll — Strongfield Castle (Tuskreach Province)*
  * *Outland Territories — Ironvale Ruins (Forgotten Citadel)*
* **Race / Type:** Undead Warlord (Cursed Human Knight)
* **Combat Role:** Heavy melee juggernaut with berserking phases and blood-drain mechanics
* **Energy Resource:** Rage (max 100)
* **Scaling:** Adapts to the number of active participants and the average level of defenders
* **Combat Style:** Close-quarters monster — punishes proximity with cleaves and lifesteal bursts, but highly vulnerable during “curse overflows.”

---

## II. VISUAL DESCRIPTION

A towering undead knight clad in cracked obsidian armor etched with runes that pulse crimson. His helmet’s visor bleeds faint red mist, and his greatsword — *“Hollow Remorse”* — leaks a constant trail of black smoke.
Every step he takes echoes with a hollow metallic growl, and the earth beneath him withers.

* **Aura:** A swirling vortex of red-black decay and burning embers.
* **Spawn Animation:** Materializes through a rift of corrupted light, planting his greatsword into the ground with a thunderous quake.
  *“Your walls cannot keep me from my vengeance…”*
* **Death Effect:** His armor collapses to ash, leaving only a cursed sword stuck in the earth — radiating faint light before vanishing.

