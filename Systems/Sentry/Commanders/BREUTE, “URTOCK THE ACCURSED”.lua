## VII. ADDITIONAL NOTES

* **Difficulty:** Moderate+ to Hard — endurance and positioning heavy fight.
* **Scaling:** Each additional player increases his HP by +4% and damage by +2%.
* **Death Effect:** Leaves a crimson crater where his sword fell.
* **Lore Connection:** Once a loyal knight of the Abyllan legions, Breute was cursed after slaughtering innocents during a siege. His soul fused with his blade — now he fights eternally, seeking redemption through endless war.

