			Azhira the Frostbinder

*Commander of the Sentry Invasion – Arnhold, Bloodrock Peak (Frozen Territory)*
**Element:** Frost / Arcane
**Combat Role:** Ranged Control Caster – battlefield zoning, healing suppression, illusion-based pressure.

---

### 🧊 **Lore**

Azhira was once the high arcanist of the Northern Conclave, a scholar of elemental harmony. Her pursuit of perfect stillness led her deep into the Frozen Peaks, where she discovered a form of magic that could halt not only motion but time itself.
The Sentry corruption twisted her obsession — now, she sees the world as a flaw to be “frozen into perfection.”

Her presence brings an unnatural calm before every storm, and her voice echoes through the snow like crystal shards breaking:

> “All motion ends beneath the frost.”
> “Perfection is stillness.”
> “Your warmth betrays you.”

---

### ⚔️ **Combat Overview**

Azhira is a **zonal control caster** who punishes clustered players with area freezes and mirror duplications. She relies on timing and crowd control rather than brute strength.

* Primarily stationary, teleporting short distances to reposition.
* Frequently summons **frost clones** to mislead and surround attackers.
* Excels at locking down healers and forcing defensive play.

---

### ⚡ **Core Abilities**

#### ❄️ **Frost Nova (Primary AoE)**

Azhira emits a burst of frost energy, freezing all nearby enemies for 3s and applying *Chilled Veins* (–20% attack and movement speed for 10s).

* Each frozen player releases a small *Frost Pulse* upon thawing, dealing minor damage to nearby allies (chain reaction mechanic).

#### 🪞 **Mirror Storm (Signature Skill)**

Azhira splits into **three mirrored copies** that mimic her attacks for 15s. Each clone deals 50% of her normal damage but can cast *Frostbolt* and *Ice Prison*.

* Clones have 30% of her HP.
* Destroying all mirrors prematurely cancels her next ability cast.

#### 🧊 **Ice Prison (Targeted CC)**

Encases a random target in solid ice for 8s or until the ice takes enough damage to shatter.

* While imprisoned, targets **cannot receive healing** — this is Azhira’s signature anti-healing mechanic.
* Shattering the prison explodes frost shards in a 6m radius, dealing AoE damage.

#### 🌨️ **Glacial Winds (Secondary AoE)**

Summons a rotating storm that moves slowly across the battlefield, dealing periodic Frost + Arcane damage and reducing visibility.

* The storm absorbs *Mirror Fragments* (created by her clones when destroyed), growing stronger and larger.

---

### 💠 **Special Trait — Frozen Silence**

Whenever a player or NPC is healed within 15m of Azhira, there’s a **25% chance** that healing will be completely negated for 3s.

* This effect cannot occur more than once every 10s per target.
* Also applies to summoned units and defensive NPCs.

**Visual Cue:** brief frost sigil forms under the affected target, and their body emits a blue shimmer indicating healing suppression.

---

### 🧩 **Combat Flow & Phases**

#### **Phase 1 – Glacial Pressure (100–60%)**

Azhira maintains distance, using *Frost Nova* and *Ice Prison* to split the battlefield. She summons her first **Mirror Storm** when she drops below 80% HP.

#### **Phase 2 – Frostbinder’s Dance (60–30%)**

She gains mobility through **Arcane Blink** (short teleport every 10s).

* Uses *Glacial Winds* more aggressively.
* Ice Prisons now trigger chain-freeze effects if multiple targets are caught.

#### **Phase 3 – The Silent Blizzard (30–0%)**

Azhira fuses her clones into a massive blizzard around herself, becoming semi-transparent and gaining **Arcane Shield** (reduces all damage by 30% for 20s).

* Random mirror images reappear and pulse small Frost Novas.
* Every 15s, she casts **Absolute Stillness**, freezing all players for 2s and resetting her *Frozen Silence* cooldown.

---

### 💥 **Universal Mechanic — Rage of the Sentry**

Shared by all Commanders.

* Builds Rage over time and when taking damage.
* At 100 Rage, releases **Sentry’s Wrath** — a shockwave of frozen energy that knocks back players and slows them by 30% for 5s.

---

### 🕯️ **Defeat and Aftermath**

Upon defeat, Azhira’s mirrors shatter in unison, leaving behind an *Arcane Sigil of Stillness* — a floating crystal that dissolves into mist.

> “Even in death... the frost remembers.”

**Victory Effects:**

* Settlement gains **Frozen Harmony**, increasing Ice resistance by +5% and Spell Power by +10% for 30 minutes.
* Froststorm clouds clear from the area, restoring visibility.

