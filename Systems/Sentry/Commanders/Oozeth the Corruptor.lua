			Commander: Oozeth the Corruptor

**Location:** Tenebress Territory — Tellanar (Area I) → Osidian Forest (subArea I) → **Berlang Fortress**
**Element:** Shadow / Fire
**Threat Tier:** Moderate–High (Elite Commander — Area Control & Split-Phase)
**Combat Arena:** The fortress’ ruined keep and moat: jagged stone walkways above pools of steaming sludge, narrow bridges and alcoves that shape the flow of combat.

---

### 🧾 Lore

Once forged by corrosive magics unleashed in the northern wars, Oozeth is the unwilling consequence of armies turned to refuse — a sentient amalgam of burnt steel, ash and dark ichor. Where it crawls, stone blackens and metal palls to rust; where it claws, old wounds fester anew. The Corruptor does not hunger for conquest — it corrupts to become whole.

> “Where iron weeps and stone boils, a thing born of ruin learns to name itself.”

---

### ⚔️ Combat Overview

Oozeth is a **zone-control commander** who punishes grouping and forces players to reposition constantly. It combines standing area hazards (acid pools), burst damage (sludge explosions), and single-target grab control (Corrosive Grip). The encounter emphasizes movement, controlled burst, and handling the split-phase at 50% HP.

#### Two Phases (simple but tactical):

* **Phase 1 — Oozeth’s Maw (100% → 50%)**
  Establishes pools of acid and spammed Sludge Explosions, forcing players to avoid lingering in zones while handling adds or small minions that emerge from sludge.
* **Phase 2 — Shards of Ichor (50% → 0%)**
  Oozeth splits into **two smaller Corruptors** (each ~55% of the original HP). These halves have slightly different specializations (one more CC-heavy, one more DoT/area-heavy), and the fight becomes a two-target choreography. Rejoining (if mechanics allow) creates a short, powerful final slam.

