### 💀 Abilities

#### ⚗️ **Acid Pool**

* Oozeth floods a patch of the arena with corrosive ichor that lingers for 12s.
* Standing in the pool applies **Corrode**: stacking DoT (Shadow/Fire) and reduces armor by a small percent per stack.
* Pools spread slowly outward if multiple pools overlap (merging increases potency).

#### 💥 **Sludge Explosion**

* Channels a three-second cone charge then detonates, dealing high burst damage and applying **Searrot** (fire+shadow DoT).
* Explosions can rupture nearby Acid Pools, creating violent geysers that knockback targets and apply extra stacks of Corrode.

#### 🦴 **Corrosive Grip**

* Single-target slam that grabs and immobilizes the target for 2–3s while draining a percentage of their max HP to heal Oozeth.
* If the grabbed player remains above a HP threshold when released, Oozeth shreds their gear temporarily (a soft debuff: reduced healing received / reduced defensive stats for 12s).

#### 🔁 **Split: Shards of Ichor (Phase Trigger at 50%)**

* Oozeth fractures into two Autonomous Corruptors:

  * **Oozeth-Sear** — focuses on fire/eruption damage, larger Sludge Explosions, lower mobility.
  * **Oozeth-Murk** — focuses on shadow control: shorter-range Corrosive Grips, AoE pulses that silence or slow.
* Each half has independent rage bars and mechanics; they coordinate attacks (e.g., a synchronized Sludge Explosion if both reach 100 Rage).

#### 🔥 **Rage Mechanics (Shared Trait: Rage of the Sentry)**

* Oozeth gains **Rage** when:

  * it is hit (especially criticals),
  * acid pools are ruptured by players,
  * either Corruptor is damaged during Phase 2.
* At 100 Rage (per entity):

  * **Single-form Oozeth**: unleashes **Cataclysmic Eruption** — a ring blast that strongly increases corruption spread (larger Acid Pools) and briefly reduces player healing effectiveness.
  * **Split-form**: the two halves synchronize **Conflagration Bond** — link that shares damage and dramatically increases their regeneration until the link is broken by killing one half quickly.

