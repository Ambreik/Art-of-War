### 🧩 Unique Mechanics & Interactions

* **Sludge Minions**: small sludge crawlers periodically emerge from pools and must be dispatched to avoid stacking corruption. They also feed Oozeth’s regen if left alive.
* **Corrosion Zones**: repeated standing in corrosion zones causes a persistent **Corroded Stacking** debuff. If players reach max stacks, they are briefly petrified (stun) — incentivizes active movement.
* **Split Vulnerability Window**: after splitting, when one half dies, the surviving half becomes **exposed** for 6 seconds (takes +35% damage). Coordinated DPS on both halves is rewarded.
* **Gear Friction** (soft mechanic): Corrosive Grip’s temporary shredding of defenses simulates equipment stress — tactical for tanks to avoid being grabbed.

---

### 🛡️ Tactics & Role Guidance

* **Tanks**: kite Oozeth across clean ground and avoid pulling through multiple Acid Pools; resist being grabbed. In split phase, split-tanks on each half if possible.
* **Healers**: cleanse Corrode stacks when possible; prepare burst heals for Corrosive Grip victims and counter the drain.
* **DPS**: focus high-burst to exploit Split Vulnerability Window; ranged DPS should avoid stacking into pools or concentrated Sludge Explosion cones.
* **Movement & Purge**: players with mobility or cleanse mechanics can quickly deny geysers and prevent merged pool amplification.

