			Vyrnax the Frostwyrm

*Commander of the Sentry Invasion – Windstorm, Snowfall Plains (Frozen Territory)*
**Element:** Frost / Wind
**Combat Role:** Aerial Devastator – zone control, environmental manipulation, and attrition-based combat.

---

### 🐉 **Lore**

Vyrnax was the ancient guardian of the Northern Peaks, a wyrm once revered by the Frostborn tribes as the *Bringer of Renewal* — his icy storms preserved the purity of the land. When the Sentry corruption spread, Vyrnax’s duty twisted into a curse: he now sees warmth and life as infection, and winter as the only form of purity worth preserving.

From the sky, his roars shake the glaciers themselves, and his wings blot out the sun. His arrival is heralded by a haunting storm that lasts for days.

> “Your breath clouds the purity of this world.”
> “Let your hearts still in eternal frost.”
> “Even the wind bends to the cold.”

---

### ⚔️ **Combat Overview**

Vyrnax is a **hybrid ground–air boss**, alternating between aerial bombardment and grounded rampage phases.
Players must adapt between long-range and close-combat tactics as he shifts stance.

* **Ground Phase:** Heavy melee attacks and frost breath cones.
* **Flight Phase:** Massive AoE zones, blizzards, and movement impairment.
* **Environmental Threat:** Ice shards and snow cyclones spawn dynamically across the battlefield.

---

### ⚡ **Core Abilities**

#### 🌀 **Blizzard Breath (Primary AoE Cone)**

Exhales a sweeping cone of icy wind that deals Frost + Wind damage over time for 6s.

* Applies *Frostbite* (slows attack and cast speed by 20%).
* Freezes any summoned units instantly.
* Leaves behind a lingering frost zone that persists for 10s.

#### 🧊 **Frozen Dive (Gap Closer / Burst)**

Vyrnax launches into the air and crashes down upon a random group of players, causing an **ice shockwave** that deals heavy damage and knocks targets back.

* Ice spikes erupt outward in a ring, creating short-lived impassable barriers (5s).
* Grounded NPCs hit by the impact are frozen solid for 3s.

#### 🌬️ **Arctic Howl (Rage Attack)**

Unleashes a deafening roar that summons a spiraling **blizzard vortex** in the arena center.

* Pulls players inward gradually (like a slow suction).
* At the end of the effect, a **wind burst** knocks everyone outward.
* The vortex remains as a storm hazard for 20s, dealing periodic Frost damage.

#### ❄️ **Wingstorm (Phase Skill)**

Only during flight: Vyrnax beats his wings powerfully, creating slicing gusts across the arena.

* Each gust deals Wind damage in a line and has a 30% chance to inflict *Disoriented* (reverses movement for 3s).
* While airborne, all players suffer **–50% movement speed** (his unique Special Trait).

#### 🪶 **Shards of the Tempest (Environmental)**

When Vyrnax lands after a flight phase, **ice shards** rain down across the map.

* Each shard deals minor AoE damage and leaves behind a *Chilled Zone* (reduces Stamina regen by 25% for 15s).

---

### 💠 **Special Trait — Breath of Eternal Winter**

While flying, Vyrnax emanates a chilling aura that:

* Reduces **movement speed by 50%** for all players and NPCs.
* Causes healing-over-time effects to **tick 30% slower**.
* Increases frost resistance of allied Sentry units nearby by 25%.

This aura fades **10 seconds after landing**.

---

### 🧩 **Combat Flow & Phases**

#### **Phase 1 – The Ground Quakes (100–70%)**

Vyrnax remains grounded, using **Blizzard Breath** and **Frozen Dive** frequently.

* At 75%, begins summoning small *Frost Elementals* that explode on death, leaving ice patches.
* Environmental winds begin to swirl around the arena, pushing players off balance.

#### **Phase 2 – Winds of Despair (70–30%)**

Takes flight for the first time.

* Activates **Wingstorm** and **Arctic Howl**, drastically reducing visibility and mobility.
* Uses **Blizzard Breath** from above to carve zones of denial.
* At 50% HP, lands with a massive *Frozen Dive*, transitioning to ground combat again.

#### **Phase 3 – Heart of the Blizzard (30–0%)**

Vyrnax alternates between air and ground rapidly, gaining a stacking **Rage of the Sentry** buff each time he takes flight.

* The battlefield becomes encased in frost.
* Visibility drops, snowstorms intensify.
* Every 20s, he uses **Frozen Dive** followed by **Arctic Howl**, creating alternating push–pull cycles.

When reaching 10% HP, he channels **Absolute Winter** — a short invulnerability phase where he draws in frost energy. If not interrupted by destroying his *Frozen Heart Crystals* (three small crystals around the arena), he unleashes a devastating frost explosion.

---

### 💥 **Universal Mechanic — Rage of the Sentry**

As with all Commanders:

* Builds Rage as combat progresses.
* At 100 Rage, Vyrnax releases **Sentry’s Wrath**, freezing all non-flying units for 3s and summoning a snowstorm that deals AoE Frost damage over time.

---

### 🕯️ **Defeat and Aftermath**

Upon defeat, Vyrnax’s body slowly disintegrates into snowflakes as the blizzard clears and sunlight breaks through.

> “The wind... remembers... warmth...”

**Victory Effects:**

* The storm ceases across Snowfall Plains for 1 hour.
* Settlements nearby gain **Warmth’s Blessing**: +10% Frost Resistance and +15% Movement Speed for 30 minutes.

