			Semi-Boss Reinforcements of Vyrnax the Frostwyrm

During every flight phase of Vyrnax, icy tempests tear open rifts in the sky, summoning semi-boss entities that aid him.
Each wave spawns **1 to 3** of these lieutenants at random points around the arena.
Destroying them weakens Vyrnax’s Frost Aura and restores visibility.

---

### ❄️ **1. Frost Drakelings – “Children of the Storm”**

**Type:** Elite flying semi-bosses (Wind / Frost)
**HP Tier:** ~15% of Vyrnax’s total
**Mechanics:** High mobility, aerial harassers

**Abilities**

* **Glacial Spear:** Fires an icicle at a random ranged attacker, dealing Frost damage and applying *Slow* (–25% Movement Speed, 5 s).
* **Wind Gust:** Creates a line knockback that pushes players toward arena edges.
* **Shared Cry:** Every active Drakeling increases Vyrnax’s movement speed by 5% and reduces his Rage buildup delay by 10%.

**Defeating a Drakeling:**

* Reduces Vyrnax’s Frost Aura radius by 10 m.
* Slightly staggers his current cast if he’s channeling *Blizzard Breath*.

---

### 🪶 **2. Shard-Bound Elemental – “Frozen Core Servant”**

**Type:** Grounded mini-boss (Frost / Earth)
**HP Tier:** ~20% of Vyrnax’s total
**Mechanics:** Area control and tank-breaker

**Abilities**

* **Crystal Burst:** AoE explosion around the Elemental; leaves behind *Ice Crystals* that explode again if ignored for 10 s.
* **Frozen Grasp:** Roots the nearest target for 4 s.
* **Chill Pulse (Passive):** Constantly emits waves of frost that reduce Stamina regeneration of anyone within 15 m by 30%.

**Defeating an Elemental:**

* Shatters part of the ice storm, creating *Safe Zones* (clear air pockets) for 10 s.
* Interrupts Vyrnax’s flight for 3 s, forcing him lower to the ground.

---

### 🌪️ **3. Ice Siren – “Whisper of the Deep Chill”**

**Type:** Elite support caster (Frost / Shadow)
**HP Tier:** ~12% of Vyrnax’s total
**Mechanics:** Crowd control and deception

**Abilities**

* **Frost Charm:** Charms one random player or NPC for 5 s, forcing them to attack allies.
* **Mirror Veil:** Creates illusory clones (max 3) that explode when struck.
* **Lament of Winter:** Emits a haunting wail that increases Frost damage taken by 15% for 10 s.

**Defeating a Siren:**

* Dispels *Frostbite* from all players.
* Reduces Vyrnax’s damage by 10% for the remainder of that flight phase.

---

### 🧩 **Wave Scaling & Coordination**

* **Wave 1 (80–60% HP):** 1 Drakeling + 1 Elemental.
* **Wave 2 (60–40% HP):** 2 Drakelings + 1 Siren.
* **Wave 3 (40–20% HP):** 1 Drakeling + 1 Elemental + 1 Siren (enhanced versions).

Each time a full wave is cleared, Vyrnax loses **25 Rage** and his next *Arctic Howl* is delayed by 15 s.

---

### 💠 **Unified Mechanic – “Echo of Frostbond”**

All semi-bosses share a resonance with Vyrnax.
As long as at least one remains alive:

* Vyrnax gains +15% Frost Damage.
* The *Rage of the Sentry* bar fills 20% faster.

When all are defeated:

* The storm calms, players regain full movement speed for 10 s.
* Vyrnax descends, entering a brief **Vulnerable Phase** (takes +25% damage for 8 s).

