			Ser Rhaegor the Crossed Knight

*Commander of the Sentry Invasion – Frozenwall Citadel, Alaskia (Frozen Territory)*
**Element:** Ice / Light
**Combat Role:** Duelist Boss – sustained melee pressure, frostfire AoE control, phase-based shield mechanic.

---

### 🕯️ **Lore**

Once hailed as the *Paragon of Frostwall*, Ser Rhaegor led the Citadel’s holy guard against the forces of corruption. But in his final stand, he was cursed by the same light he wielded — frozen between purity and wrath. His heart became ice, his faith fractured. Now, as the **Crossed Knight**, he rides again under the Sentry banner, wielding twin blades of opposing forces — frost and flame — that sear and freeze in unison.

Rhaegor is not entirely mindless; players will occasionally hear his fractured voice echo lines like:

> “The light... it blinds me still...”
> “Mercy was my sin.”
> “In frost, all sins are preserved.”

---

### ⚔️ **Combat Overview**

Rhaegor fights as a **mobile melee juggernaut**, mixing holy bursts with frost AoEs. His attacks are methodical — alternating between light-empowered smites and frostfire strikes that punish proximity and coordination errors.

**Battlefield Terrain:**

* The arena floor gradually ices over during combat, reducing player traction.
* Holy runes appear under Rhaegor when casting *Judgment of Winter*, warning of his next smite zone.
* Frozen shards from his *Blade Cyclone* can impale the ground, creating temporary cover for ranged players.

---

### ⚡ **Core Abilities**

#### 🧊 **Frostbrand (Primary Attack)**

Each blade swing deals moderate ice damage with a stacking *Frostbite* debuff. At 5 stacks, the target is frozen for 3s and takes +15% damage from all sources for 10s.

* Passive aura: nearby enemies have 10% reduced movement speed.

#### 🌨️ **Judgment of Winter**

Rhaegor channels the frozen essence of the Light, marking several locations with glowing runes. After 3s, divine lances of frostfire strike the marks, dealing Ice + Light damage in small AoEs.

* Standing within a rune briefly grants *Frozen Purity*, reducing incoming AoE damage by 25% for 5s (hidden mechanic rewarding observant players).

#### 🌀 **Blade Cyclone**

Rhaegor spins both swords in a deadly storm, dealing high physical + ice damage to all nearby players for 6s.

* During the spin, he moves slowly but deflects projectiles.
* Leaves behind *Frozen Shards* (environmental hazards) that can later be shattered by *Lightburst* for AoE explosions.

#### ✨ **Lightburst**

At random intervals, Rhaegor channels his remaining holy power, causing all frozen shards in the arena to detonate, dealing massive AoE damage and applying a 20% slow for 8s.

* Combo synergy: *Blade Cyclone → Lightburst* forms his iconic two-step combo phase.

---

### 💠 **Special Trait — Frostfire Reflection (Phase 2 Trigger)**

At **50% HP**, Rhaegor slams both blades into the ground, summoning a **Mirror of Frostfire**, a radiant-ice shield that reflects 50% of all incoming damage.

* The shield lasts 25s or until it absorbs 500k total damage (scaled).
* During this time, Rhaegor gains *Frostfire Wrath*, increasing his attack speed by 15%.
* Hitting him with Light- or Fire-based attacks accelerates shield decay (strategic vulnerability).

**Visual Cue:** his body glows with twin-colored aura — left half icy blue, right half radiant gold — signifying his split soul.

---

### 🔥 **Phase 3 — The Crossed Judgment (20% HP)**

Rhaegor fuses his two blades into *Sanctis Frostfang*, unleashing a chaotic hybrid phase:

* Alternates between Light and Ice damage every 10s.
* Gains **Wrath of Duality** — each successful hit increases his damage by 2%, stacking up to 30.
* At 0 Rage, he channels *Divine Shatter* — a massive conical strike that deals pure damage and removes all buffs from hit players.

**Arena Hazard:** ice pillars collapse intermittently; using them for line-of-sight blocks can avoid *Divine Shatter*.

---

### 🩸 **Universal Commander Trait — Rage of the Sentry**

Shared across all Sentry Commanders.

* Gains +5 Rage per second and +10 Rage per hit taken.
* At 100 Rage, unleashes **Sentry’s Wrath** — a zone-wide shockwave dealing damage based on missing HP and applying a stacking debuff *Exhaustion*, reducing movement speed by 5% (up to 30%).
* Resets Rage to 0 afterward.

---

### 🧩 **Unique Mechanics**

* **Frozen Faith:** Every 60s, Rhaegor kneels momentarily, healing for 5% HP and summoning 3 *Specters of the Fallen*, ghostly paladins who fight for 15s before fading.
* **Blades of Betrayal:** When a player dies during the fight, a spectral copy of their weapon manifests for 20s, attacking nearby players.

---

### 🕯️ **Defeat and Aftermath**

Upon defeat, Rhaegor shatters into icy fragments that melt slowly into the ground, leaving behind a single radiant sigil.

> “The frost... preserves no truth... only regret...”

**Victory Effects:**

* Unlocks a temporary **Light Sanctum Vendor** at Frozenwall Citadel.
* Event-wide buff: **Blessing of Frozen Valor** (+5% Ice resistance, +10% Light spell power for 30 min).

