			SENTRY COMMANDER — VAELTHORN, “THE ROOTBORN PROTECTOR”

---

## I. GENERAL INFORMATION

* **Boss Name:** Vaelthorn
* **Titles:**

  * *Rootborn Protector* (Garland)
  * *Vaelthorn the Rootborn* (Askania, Mirror)
* **Region / Settlement:**

  * **Garland Continent:** Sharpthorne (Area I) → Bayswick
  * **Askania Continent:** Copthorne (Area I) → Fortress of Eldergate
* **Race / Type:** Ancient Guardian (Corrupted Treant)
* **Combat Role:** Area denial and sustain-based boss — uses battlefield control, nature-based regeneration, and stamina drain.
* **Energy Resource:** Rage (max 100)
* **Element Alignment:**

  * Garland — **Nature / Earth**
  * Askania — **Nature / Poison**
* **Difficulty:** Moderate — heavy positional mechanics and sustained fight.

---

## II. VISUAL DESCRIPTION

A towering amalgamation of bark, stone, and twisted vines. His body resembles an ancient oak bound with molten roots, his core pulsing with green light corrupted into a sickly hue. Eyes glow amber or violet depending on the continent. When enraged, his body sprouts pulsating thorns and spores that poison the air.

* **Garland Variant (Nature/Earth):** Bark glows deep emerald; roots shimmer with moss and soil.
* **Askania Variant (Nature/Poison):** Bark glows faint violet; sap oozes greenish toxins.
* **Aura:** Emits drifting leaves and spores.
* **Spawn Animation:** Erupts from the ground in a massive tremor, scattering vines outward.
  *“You reap what you’ve sown… mortals.”*
* **Death Effect:** Crumbles into roots that sink back into the ground, leaving behind a glowing seed that withers to dust.

