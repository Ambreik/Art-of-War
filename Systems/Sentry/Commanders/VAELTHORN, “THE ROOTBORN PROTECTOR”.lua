## IV. UNIQUE MECHANICS

1. **Heart of Decay (100 Rage Phase)**
   Vaelthorn roots himself into the ground for 20 seconds, becoming immobile but drastically empowered:

   * Spawns *Lifeseed Nodes* that heal him unless destroyed.
   * Periodically releases *Root Maelstrom* shockwaves that knock back players.
   * Upon ending, unleashes *Groundquake* (Garland) or *Poison Burst* (Askania).

2. **Nature’s Consumption**
   Periodically drains vitality from nearby flora and summons — players’ pets and summons lose 5% HP per second while within his aura. Healing absorbed this way restores Vaelthorn.

3. **Cycle of Rot**
   Every 25% HP lost, Vaelthorn shifts the battlefield:

   * *Garland:* Creates new fissures and erupting roots.
   * *Askania:* Spreads toxic ground pools that slowly expand.

