## VII. ADDITIONAL NOTES

* **Combat Difficulty:** Moderate — strong positional awareness required; healing sustain is key.
* **Scaling:** +3% HP per additional participant.
* **Death Effect:** Roots retract, revealing an ancient sigil marking the cleansing of the grove.
* **Lore Summary:**
  Once the guardian of all forests within Garland and Askania, Vaelthorn’s spirit fractured when the corruption of the Sentries spread through the land. One half remained bound to earth, the other to poison — together, they embody nature’s vengeance turned inward.

