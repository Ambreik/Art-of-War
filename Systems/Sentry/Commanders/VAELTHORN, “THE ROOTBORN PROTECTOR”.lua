## VI. PHASES

### **Phase 1 – “Awakening of Roots” (100–71%)**

* Uses *Entangling Thorns* and *Thorn Wall* to restrict mobility.
* Periodic *Verdant Recovery* pulses heal him slightly.
* Builds Rage quickly as players destroy flora objects nearby.

### **Phase 2 – “Heart of Decay I” (70–41%)**

* Activates his first *Heart of Decay* phase.
* Spawns *Lifeseed Nodes* — must be destroyed to stop healing.
* Ground hazards form (fissures or toxic pools).

### **Phase 3 – “Cycle of Rot” (40–11%)**

* Summons vines from random directions to pull players in.
* Healing from flora absorption increases by +50%.
* Uses *Groundquake* or *Toxic Sap* more often.

### **Phase 4 – “Final Uprising” (10–0%)**

* Constant *Root Maelstrom* pulses.
* Gains *Rage of the Grove* buff permanently (+20% damage, +15% speed).
* Occasionally roars, summoning two **Vinebound Husks** (adds).

