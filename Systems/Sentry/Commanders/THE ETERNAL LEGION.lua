## IV. PHASE I — **THE MARCH OF THE LEGION**

> *“From the roots of death they march, endless and unbroken.”*

### **Objective:**

Hold the city’s core plaza against **five reinforcement waves** of Sentry soldiers.

### **Enemy Composition**

| Wave | Description        | Units Spawned                           |
| ---- | ------------------ | --------------------------------------- |
| 1    | Scout Rush         | 20x Sentry Scouts, 2x Corrupted Beasts  |
| 2    | Frontline Push     | 25x Soldiers, 3x Venom Archers          |
| 3    | Siege Breach       | 2x Legion War Beasts, 10x Engineers     |
| 4    | Dual-Flank         | 30x mixed infantry from East/West gates |
| 5    | Commander’s Herald | 1x Herald of the Legion (mini-boss)     |

### **Mechanics**

* **Siege Repair:** Players must protect Sanctum Wards (3 crystals) that power city defenses.
* **If a Ward is destroyed**, a section of the city becomes corrupted (visual and damage-over-time effect).
* **Herald of the Legion (Mini-boss):**

  * Uses *“Mark of Endless War”* → applies stacking damage increase to all enemies alive.
  * Must be slain to begin Phase II.

### **Transition Trigger:**

After Herald’s death, the sky darkens; corruption surges and three colored sigils (Green–Purple–Black) appear above the plaza.

---

## V. PHASE II — **THE TRI-CHAMPIONS**

> *“Three hearts, one purpose — the Legion endures.”*

### **Objective:**

Defeat the **Tri-Champions** — lieutenants that rotate leadership and buff all Legion forces under their active element.

### **Enemies:**

* **Champion of Thorns (Nature)** – Uses entangling roots and regeneration.
* **Champion of Venom (Poison)** – Specializes in DoT stacking and spreading plagues.
* **Champion of Shadows (Shadow)** – Uses teleportation and fear auras.

### **Mechanic: Rotating Leadership**

* Every **2 minutes**, one Champion becomes *Active Leader* (receiving +100% damage, new abilities, and aura).
* The other two become *Dormant*, healing 1% HP every 10 seconds.
* If not killed within their leadership phase, they transfer buffs to the next Champion.
* If all three survive more than **8 minutes**, they merge temporarily into **The Legion Aspect** — a hybrid superform (Nature/Poison/Shadow) lasting 90 seconds.

### **Abilities Overview**

| Champion            | Signature Skills                                | Special Trait                                                   |
| ------------------- | ----------------------------------------------- | --------------------------------------------------------------- |
| **Thorns (Nature)** | *Entangling Bloom, Verdant Regrowth, Rootquake* | Summons vines from corpses to attack nearby players.            |
| **Venom (Poison)**  | *Toxic Cloud, Plague Spit, Venom Eruption*      | Applies stacking poison that persists through phase transition. |
| **Shadow (Dark)**   | *Fear Pulse, Shadow Blink, Void Rend*           | Creates clones that explode on death.                           |

### **Transition Trigger:**

Upon defeating all three Champions, their dying energies flow into three summoning circles — awakening the **Tri-Commanders** of the Eternal Legion.

---

## VI. PHASE III — **THE TRI-COMMANDERS**

> *“We are Legion — neither time nor death shall end us.”*

### **Objective:**

Defeat the **three Commanders** — each controlling one elemental domain — before the **25-minute corruption timer** expires.

| Commander                       | Element        | Role                                | Arena Area      |
| ------------------------------- | -------------- | ----------------------------------- | --------------- |
| **Vael’mor, the Rooted Tyrant** | Nature / Earth | Tank-breaker, battlefield control   | West Courtyard  |
| **Nerithra, the Plague Matron** | Poison / Water | Healer disruptor, AoE poison fields | East Garden     |
| **Thalen, the Shadow Captain**  | Shadow / Fire  | DPS punisher, mobility & illusions  | Central Plaza   |

### **Shared Health Mechanic**

* Commanders share a **linked vitality network**.
* When one reaches 0%, it **revives at 50%** after 90 seconds **unless** all three are defeated within 60 seconds of each other.
* Encourages synchronized takedown.

### **Environmental Effects**

* Each arena area becomes infused with its element:

  * *Nature:* Vines periodically root players.
  * *Poison:* Green mist expands and corrupts terrain.
  * *Shadow:* Visibility reduced; stealth enemies appear.

### **Global Corruption Timer**

* 25:00 countdown from the start of the event.
* Every 5 minutes elapsed increases all Commander damage by 20%.
* If timer expires → city becomes fully corrupted, blocking NPC access and trade for **4 hours**.

