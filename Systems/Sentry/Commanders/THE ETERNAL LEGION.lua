## 🜁 **Commander: Nerithra, the Plague Matron**

**Element:** Poison / Water
**Combat Role:** Healer Disruptor & AoE Corruption Spreader
**Arena:** East Garden (overrun by poisonous vines and bubbling pools)
**Resource Type:** Rage (builds as her minions die or her healing is interrupted)

---

### 🧬 **Lore & Appearance**

Once a revered herbalist of the Elven Sanctum, Nerithra’s body was warped when she fused with the toxic waters of Sankis. Her lower body is a serpentine mass dripping venom, while her torso retains a ghostly humanoid form.
A halo of mist and spores follows her — a sign of the corruption that sustains her.

---

### ⚔️ **Combat Overview**

Nerithra is a **control & attrition-based boss**, excelling at spreading long-duration debuffs and weakening groups over time.
Her main mechanic revolves around **Toxic Saturation**, which stacks as players stand near contaminated pools or corpses — increasing Poison damage taken.

---

### 💧 **Phase 1: The Garden Awakens (100%–70%)**

Nerithra fights defensively at first, spreading plague zones.

**Abilities:**

1. **Venom Burst** — Fires a cone of poison that leaves a puddle dealing 3% HP/s to anyone inside for 6s.
   *Stacks Toxic Saturation.*

2. **Mist of Decay** — Emits a 15m gas cloud. Healers inside restore 50% less mana and healing effectiveness is reduced by 30%.

3. **Corrupted Bloom (Passive)** — Summons “Plague Petals” — small toxic flowers that pulse minor AoE damage every 5s.

4. **Toxic Gift** *(Special)* — When a player dies, their corpse releases a toxin cloud. If another player revives them within 5s, that player inherits half of their Toxic Saturation stacks.

---

### ☠️ **Phase 2: Venomous Grace (70%–40%)**

Nerithra becomes more aggressive, her pools merging into larger ones.

**Abilities:**

1. **Serpent Lash** — A multi-hit melee combo that poisons on each strike; final hit applies **Corrosive Weakness**, reducing armor by 20% for 10s.

2. **Rain of Plagues** — Summons acid rain across the arena for 10s. Standing in water doubles damage taken.
   *Safe spots rotate every 5 seconds.*

3. **Reanimate Husk** — Revives any *dead Plague Petals* as “Toxic Husks” — small minions that explode upon death.

4. **Fetid Renewal (Rage Ability)** — At 100 Rage, she releases a healing pulse for all sentries within 30m, restoring 10% HP and removing CC effects.

---

### 🧫 **Phase 3: Mistress of Rot (40%–0%)**

Her form fully transforms — she gains four serpent heads that each breathe different toxins.

**Abilities:**

1. **Quadruple Breath** — Randomly chooses 2 toxin types per cast: Paralysis (stun), Sleep, Blind, or Corrosion.
   *Players affected twice by the same type instantly fall unconscious for 5s.*

2. **Poison Nova** — Massive AoE explosion every 30s. Can be mitigated if at least one corrupted pool is purified (via killing Toxic Husks).

3. **Essence of Plague (Passive)** — Converts all healing received by players above 80% HP into Poison damage instead.

4. **Tide of Pestilence (Ultimate)** — At 10% HP, she floods the arena with green water, forcing players to fight on raised platforms or drown in toxins.

---

### 💀 **Defeat Mechanics**

When defeated, her body dissolves into mist.
Players nearby gain **“Blessing of Purity”** — a 30-minute debuff immunity and +10% Poison Resistance.

