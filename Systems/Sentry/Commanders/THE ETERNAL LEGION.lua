			SENTRY COMMANDER EVENT — THE ETERNAL LEGION

### *The Corruption of the Ancient City of Sankis*

*(Region: Aarong Forest – Capital Ruins)*

---

## I. GENERAL INFORMATION

* **Event Name:** *The Eternal Legion*
* **Location:** Ancient City of Sankis (Aarong Forest Region)
* **Type:** Multi-Boss Sentry Invasion Event
* **Structure:** 3-Phase Raid Battle
* **Duration Limit:** 25 minutes (failure triggers 4-hour corruption)
* **Elements:** Nature / Poison / Shadow (rotating between phases)
* **Difficulty:** Extreme — multi-objective, high coordination required.
* **Participation:** Open-world raid event (up to 40 players).

---

## II. VISUAL THEME & ATMOSPHERE

The once-holy ruins of Sankis are now shrouded in green mist and black thorns. Corrupted roots twist through stone walls, and spectral banners of the Sentry Legion flutter above broken statues.
The air vibrates with unnatural hums — a sign of synchronized life-draining magic.
As the invasion progresses, visual corruption spreads across the city, with pools of poison and darkness expanding outward.

### **Sound Design**

* Phase 1: War drums and marching echoes.
* Phase 2: Chants of the Tri-Champions.
* Phase 3: Three overlapping boss themes, merging in crescendo.

### **Environmental Behavior**

* The city reacts dynamically to progress — light sources dim, nature warps, and the sky shifts from green to deep crimson.

