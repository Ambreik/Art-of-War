			Commander: Thalen, the Shadow Captain

**Element:** Shadow / Fire
**Combat Role:** Mobility DPS Punisher & Illusionist Duelist
**Arena:** Central Plaza (open battlefield surrounded by burning ruins)
**Resource Type:** Rage (charges with every critical hit taken or dealt)

---

### ⚔️ **Lore & Appearance**

Thalen was once a royal knight who embraced shadow magic to “protect” Sankis — but the darkness consumed him.
Now he wears an ashen black armor with molten veins, his sword crackling between fire and void energy. His shadow walks beside him, echoing his movements with a delay — his deadliest trick.

---

### ⚔️ **Combat Overview**

Thalen thrives in **fast-paced melee duels**. He punishes stationary targets and teleports between ranged attackers.
He uses **Mirrored Shadows**, creating clones that replicate his abilities but at reduced strength.

---

### 🌑 **Phase 1: The Duel Begins (100%–70%)**

Thalen tests players’ reflexes.

**Abilities:**

1. **Shadow Rush** — Dashes to a distant target, slashing through everything in his path for Shadow damage.
   Leaves behind a “Shadow Echo” that repeats the dash after 2s.

2. **Infernal Brand** — Marks a target; every hit they take for 10s triggers a fiery explosion around them.

3. **Soul Rend (Passive)** — Each hit has a 10% chance to drain stamina and energy from the target.

4. **Burning Mirage** — Creates 2 shadow clones (at 60% power) that use weaker versions of his basic skills.

---

### 🔥 **Phase 2: Dance of Embers (70%–35%)**

The plaza ignites with fire pillars, limiting safe movement.

**Abilities:**

1. **Shadow Reaver** — Wide spin attack, dealing heavy damage and reflecting 25% of incoming ranged attacks for 8s.

2. **Blazing Step** — Every teleport leaves behind a flame zone lasting 6s. Stepping into it grants Thalen bonus Rage.

3. **Flame Rift** — Opens a rift that spews projectiles of fire and shadow energy for 10s.

4. **Mirror Exchange (Special)** — Swaps position with one of his clones, instantly resetting threat.
   *(Clones disappear upon his death.)*

---

### 💢 **Phase 3: Twilight Reign (35%–0%)**

Thalen’s shadow fuses with him, becoming a dual-element hybrid.

**Abilities:**

1. **Obsidian Strike** — Charged overhead attack dealing split Fire and Shadow damage; creates a lingering burn zone.

2. **Eclipse Storm (Ultimate)** — Dark fire tornado forms around him for 12s, pulling in players while dealing ramping DoT.

3. **Last Stand (Passive)** — At 5% HP, Thalen splits into two shadow forms with shared health — both must die within 15s or he resurrects.

4. **Blaze of Defiance** *(Enrage Mechanic)* — After 6 minutes, he burns the plaza, creating permanent fire zones until defeated.

---

### 💀 **Defeat Mechanics**

When both Thalens fall, his armor remains glowing — a relic of pride and damnation.
Nearby players receive **“Mark of the Shadowflame”** — +10% Fire & Shadow Resistance for 30 minutes.

