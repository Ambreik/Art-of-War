			Commander: Vael’mor, the Rooted Tyrant**

**Element:** Nature / Earth
**Combat Role:** Tank-breaker & Battlefield Control
**Arena:** West Courtyard (overgrown with stone roots and living vines)
**Resource Type:** Rage (builds through taking or dealing damage)

---

### 🗿 **Lore & Appearance**

Once a forest guardian of the old world, Vael’mor was twisted by corruption seeping through the roots beneath Sankis. His stone-like skin is covered in bark plating, and his arms are entangled with pulsating vines that drip green luminescence.
Where he steps, the earth cracks and roots burst forth — the living embodiment of a cursed forest turned to rot.

---

### ⚔️ **Combat Overview**

Vael’mor is a **frontline siege boss** — he punishes tanks and melee-heavy groups. His strength lies in **area denial**, **root-based control**, and **terrain manipulation**.
He grows stronger the longer he stays in combat, gaining **Rage Growth stacks** that empower his damage and defenses.

---

### 🌳 **Phase 1: Root Awakening (100%–70%)**

Vael’mor begins with heavy slams and terrain disruption.

**Abilities:**

1. **Entangling Grasp** — Roots erupt in a 10m cone, rooting all targets for 3s and dealing minor Nature damage per second.
   *If a rooted target dies, they explode, dealing AoE damage.*

2. **Barkskin Armor** — Reduces all incoming damage by 25% for 10s. Each attack blocked restores 5 Rage.

3. **Groundquake Slam** — Heavy AoE smash that knocks players back and cracks the ground, creating temporary impassable fissures for 8s.

4. **Rage Mechanic:** Each time he’s hit by a ranged attack, Vael’mor gains +1 Rage (max 100). At 100 Rage, he enters **“Verdant Frenzy”** (next phase).

---

### 🌿 **Phase 2: Verdant Frenzy (70%–35%)**

Vael’mor channels the forest’s corruption, gaining massive Rage regeneration.

**Abilities:**

1. **Verdant Surge (Passive)** — Periodic waves of vines lash out from him every 10s, damaging all within 15m and pulling them inward.

2. **Corrupted Growths** — Summons *3 Root Pillars* around the courtyard. These spawn *Sporelings* (adds) that explode for poison damage when slain.

3. **Photosiphon** — Absorbs nearby *Sporelings* or *fallen players*, healing himself for 3% HP each.
   *(Unique mechanic: corpses glow faintly when absorbable.)*

4. **Boulder Crush** — Throws a massive chunk of corrupted stone at a random target, stunning and dealing heavy Earth damage.

---

### 🌌 **Phase 3: Wrath of the Grove (35%–0%)**

Vael’mor fuses with the courtyard, becoming semi-immobile but massively empowered.

**Abilities:**

1. **Rootstorm (Ultimate)** — Roots erupt from the entire arena for 10s, dealing lethal damage to all outside safe zones. (Safe zones are randomly chosen stone circles.)

2. **Thorn Barrage** — Launches spikes that target ranged players; each hit reduces target healing received by 20% for 10s.

3. **Essence of the Wilds (Passive)** — Drains health from all *plants, corpses, or summoned entities* every 5s for 1% HP per source.

4. **Verdant Collapse** *(Enrage Mechanic)* — After 6 minutes of combat, Vael’mor enrages, doubling attack speed and damage for the final 30s.

---

### 💀 **Defeat Mechanics**

When slain, Vael’mor’s body petrifies into stone. Roots retract, leaving the courtyard purified.
A temporary buff, **“Breath of the Grove”**, appears at the arena’s center, granting:

> +5% Nature Resistance and +2% Health Regeneration for 15 minutes.

