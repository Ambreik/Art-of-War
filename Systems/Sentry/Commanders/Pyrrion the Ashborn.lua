			Commander: Pyrrion the Ashborn

**Location:** Tenebress Territory — Ashen Hills (Area II) → Vanguard Outpost
**Element:** Fire / Light
**Threat Tier:** Moderate (Rebirth Commander — High Burst / Low Sustain)
**Combat Arena:** The burning ruins of the Vanguard Outpost — open, circular ground surrounded by scorched barricades, charred siege engines, and scattered fire crystals emitting light bursts.

---

### 🧾 Lore

From the ashes of a hundred fallen sentries rose **Pyrrion**, a divine ember given consciousness by the eternal cycle of flame. Once a celestial guardian of rebirth, Pyrrion’s heart was blackened by the endless wars of mortals. Now it soars above the Ashen Hills as both purifier and executioner, seeking to cleanse the world in cleansing light and fire.

> “All things end not in shadow, but in flame — that they may burn anew.”

---

### ⚔️ Combat Overview

Pyrrion is an **aerial-dominant commander** who alternates between **Ground Phase (Ash Aspect)** and **Flight Phase (Flame Aspect)**.
It favors quick strikes, large AoE zones, and a **death-and-rebirth cycle**, forcing players to adapt after its fiery resurrection.

* **Phase 1 – The Burning Sky (100% → 0%)**
  Pyrrion cycles between ground and flight, using mobility-heavy flame attacks and radiant bursts. At “death,” its ashes scatter and the arena ignites.

* **Phase 2 – The Ashborn Rebirth (Triggered on death)**
  After 10 seconds, Pyrrion resurrects with **30% health**, empowered by light energy and enhanced resistance. New patterns emerge: higher AoE pressure, radiant explosions, and chain burns.

---

### 💀 Abilities

#### 🔥 **Immolation**

* Pyrrion surrounds itself in a corona of living flame, pulsing every 2s for moderate Fire/Light damage.
* Players within melee range stack **Burning Soul**, reducing healing received by 5% per stack for 10s.
* During Flight Phase, Immolation becomes **Radiant Immolation**, adding small light bolts that randomly target ranged players.

#### 🕊️ **Flame Dive**

* Pyrrion takes to the air, marking up to three players with a fiery sigil before diving at high speed.
* On impact, creates **Scorch Zones** that persist for 8s and deal heavy Fire damage per second.
* The third dive leaves behind an **Ashstorm** vortex — a swirling flame funnel that drags players inward over 6s.

#### 🌅 **Phoenix Rebirth (Phase Transition)**

* When reduced to 0 HP, Pyrrion disintegrates into burning ash, leaving behind **Embers of Renewal** (floating fire orbs).
* After 10s, Pyrrion resurrects with **30% HP**, triggering a massive **Blaze Pulse** (Fire + Light AoE burst).
* While reborn, Pyrrion gains:

  * +20% attack speed
  * +15% movement speed
  * **New passive:** Light-infused attacks now cause **Solar Flare**, a blinding effect that reduces accuracy briefly.

#### ☀️ **Solar Judgment (Rebirth-only ability)**

* Fires a concentrated light beam at a random target every 20s.
* The beam splits into smaller rays when intercepted by another player, spreading damage but lowering total impact — encourages group positioning.

#### 🔥 **Rage of the Ashborn (Universal Rage Mechanic)**

* Pyrrion generates **Rage** when struck by critical hits or interrupted while channeling Flame Dive.
* At 100 Rage, Pyrrion uses **Ashen Cataclysm**:

  * Expels all remaining fire in a radial explosion.
  * Leaves behind **Ash Craters** (temporary no-heal zones) for 6s.
  * Reduces the cooldown of Phoenix Rebirth by 20s (if triggered mid-phase).

---

### 🧩 Unique Mechanics & Interactions

* **Ash Trails:** Pyrrion’s movement leaves short-lived burning trails. Crossing them inflicts **Smolder**, a slow-burning DoT that can stack up to 5 times. Trails disappear faster in water or rain-weather conditions (if dynamically simulated).
* **Light Mirrors (Arena Feature):** Crystals around the arena refract Pyrrion’s light attacks. Clever players can hide behind them to avoid **Solar Judgment** — but after absorbing three beams, a mirror shatters, causing a brief flash that blinds nearby players.
* **Ashfall (Environmental Phase Effect):** During Rebirth Phase, the sky darkens and ash falls, reducing visibility and movement speed by 10%.

---

### 🛡️ Tactics & Role Guidance

* **Tanks:** Maintain threat during ground phases and rotate positioning to direct Flame Dive zones away from the group. Be prepared for sudden flight transitions.
* **Healers:** Preemptively heal through Immolation ticks and stabilize raid HP after Blaze Pulse. Cleanse Burning Soul stacks when possible.
* **DPS:** Focus burst during Pyrrion’s grounded phase — avoid DoTs spreading from Flame Dive impacts. Save cooldowns for Rebirth Phase to prevent extended Ashfall duration.
* **Movement:** Avoid Ash Trails and rotate clockwise around Scorch Zones to maintain safe distance from Ashstorm vortex centers.

---

### ⚙️ Design Notes

* **Difficulty Curve:** Moderate — high visual spectacle and mobility challenge but straightforward damage patterns.
* **Signature Mechanic:** *Rebirth Cycle* — Pyrrion’s unique revival at 30% HP introduces a second wave of light/fire synergy and burst damage.
* **Shared Rage Trait:** All Commanders use a “Rage” resource to trigger ultimate attacks — Pyrrion’s version ties to its rebirth theme.

