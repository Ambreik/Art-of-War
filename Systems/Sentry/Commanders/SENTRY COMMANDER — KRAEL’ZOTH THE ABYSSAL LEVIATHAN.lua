## VIII. PHASES

### **Phase 1 – “Awakening from the Depths” (100–70%)**

* Tentacle strikes and Ink Spray primary attacks.
* Random harpoon and siege targets.
* One Nymph spawn every 2–3 minutes.

### **Phase 2 – “Waves of Madness” (70–40%)**

* Begins *Tidal Pull* and *Tentacle Crush* rotation.
* Ink Vortex activates — docks shrouded in darkness.
* Two Nymphs per wave; CC frequency increases.

### **Phase 3 – “Abyssal Surge” (40–10%)**

* Dive phase with massive tsunami attack.
* Three empowered Nymphs spawn simultaneously.
* Priests must maintain protective wards or risk total dock collapse.

### **Phase 4 – “The Leviathan’s Last Roar” (10–0%)**

* Constant ink and tentacle attacks.
* Rage fills continuously.
* Defeated when all tentacles destroyed, causing body collapse offshore.

