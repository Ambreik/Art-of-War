## IX. ADDITIONAL NOTES

* **Combat Difficulty:** High coordination encounter — strong anti-range defenses, positional control, and CC management.
* **Scaling:** +5% HP per 5 players nearby.
* **Common Mechanic with Other Commanders:** Uses **Rage Meter** that triggers a unique ultimate phase (*Abyssal Surge*).
* **Unique Trait:** Cannot enter land — forces ranged-heavy engagements and environmental participation.
* **Lore Summary:**
  From the black trenches beneath Middlean’s port, Krael’zoth rose when the Sentry corruption reached the sea. His whispers lure sailors to madness, and the Nymphs that once guided ships now serve as his haunting choir. To face him is to battle the ocean itself.

