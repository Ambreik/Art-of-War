## V. UNIQUE MECHANICS

### 1. **Abyssal Surge (100 Rage Phase)**

* Krael’zoth dives underwater and reemerges violently.
* Triggers a **tsunami wave** sweeping across the docks, knocking down players and destroying siege weapons.
* Spawns **3 empowered Nymphs of the Sea** (semi-bosses).
* For 30 seconds, all water-based attacks deal +25% damage.

### 2. **Ink of Oblivion**

* Sprays a jet of ink across the harbor.
* Creates a **black fog zone** that reduces visibility and deals minor damage every second.
* Inside the ink zone, *Confusion* debuff has a 30% chance to trigger on players (reversed movement).

### 3. **Tentacle Crush**

* Selects random dock structures or groups of players, smashing the ground and causing area knockback.
* If a harpoon or siege tower is targeted, it becomes unusable for 15s.

### 4. **Echo of the Depths**

* Every time a Nymph dies, Krael’zoth emits a roar, healing 5% HP and summoning a **Shadow Wave** (water + darkness pulse) damaging all NPCs on the docks.

