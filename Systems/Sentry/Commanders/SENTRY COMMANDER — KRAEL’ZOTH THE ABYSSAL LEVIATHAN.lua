			SENTRY COMMANDER — KRAEL’ZOTH THE ABYSSAL LEVIATHAN

---

## I. GENERAL INFORMATION

* **Boss Name:** Krael’zoth
* **Title:** *The Abyssal Leviathan*
* **Region / Settlement:**

  * **Middlean Islands (Capital City – Port District)**
  * Spawn point: Offshore waters, visible from the harbor.
* **Race / Type:** Abyssal Sea Colossus (Mythic Kraken)
* **Combat Role:** Stationary siege boss — relies on massive tentacle strikes, ink-based AoEs, and summoning waves of Nymphs.
* **Energy Resource:** Rage (max 100)
* **Element Alignment:** Water / Shadow
* **Difficulty:** Moderate–Hard — large-scale, cooperative, with active environmental defenses.

---

## II. VISUAL DESCRIPTION

A titanic kraken whose body remains half-submerged beneath the waves, visible only through its glistening black tentacles and a singular, glowing abyssal eye.
Its skin glows with faint runes of bioluminescent blue, reflecting the corrupted Sentry sigils. Each movement sends ripples through the sea, and when enraged, waves rise violently along the docks.

* **Aura:** Dark mist mixed with ink and sea spray.
* **Spawn Animation:** The ocean churns; a whirlpool forms and bursts open as tentacles rise, wrapping around dock pylons.
  *“From the depths… you called the tide. Now drown in it.”*
* **Death Effect:** A massive final splash as the tentacles sink, leaving blackened water and a floating, glowing heart-core that fades away.

