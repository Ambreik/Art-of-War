			The Buried Truth of the Ruined Temple of Arliton

## The Dragon’s Name

Her name is:

## **Veylthara**

> Later known in forgotten texts as *“The Rotfire Mother”* and *“The Poison That Remembers Flame”*

---

## I. Creation — The First Great War

During the **First Great War** between
**Flammer** and
**Palloc**,

Flammer forged a living weapon from divine fire and rage.

From volcanic breath and solar flame, he shaped **Veylthara**, a colossal female dragon, born as a **pure Fire Dragon**, created for one purpose:

> To burn through the legions of Palloc and melt the earth he commanded.

Veylthara was not a creature of instinct.
She was intelligent, tactical, and fiercely loyal to her creator.

She became Flammer’s greatest weapon in the war.

---

## II. Death and Corruption

In one of the final clashes of the war, Veylthara was struck down by Palloc’s forces.

As her burning body fell upon shattered land, **the blood of Palloc spilled over her corpse**.

Something impossible happened.

The fire did not die.

It changed.

The divine earth-blood seeped into her essence and **reanimated** her.

She rose again — no longer fire alone.

She became something new:

> A fusion of flame and rot.
> A dragon whose breath carried **poisonous spores**, decay, and corruption.

Her fire no longer burned clean — it **infected**, **rotted**, and **spread**.

She had become resistant to:

* Fire (her origin)
* Earthly force (Palloc’s essence within her)

This is extremely important later for her boss design.

---

## III. The Second Great War — The Failed Campaign

During the **Second Great War**, Flammer commanded Veylthara to strike at the armies of
Ogres and Cyclops — ancient followers of Palloc.

She descended upon them with her new corrupted power.

But this time, she was not fighting divine beings.

She was fighting prepared mortal armies.

* The **Cyclops**, with their incredible sight, spotted her from miles away.
* The **Ogres** prepared siege tactics, throwing massive stones and enchanted boulders.

They did something no god had managed before:

> They destroyed her wings with harpoons and lightning strikes.

Veylthara fell from the sky and crashed onto **Arliton Island**.

---

## IV. The Creation of Her “Wings”

Unable to fully restore her, Flammer did something else.

From:

* Veylthara’s venomous saliva
* His divine fire

He created:

## **Harbinger of Forgotten Roots**

A powerful entity bound to her life force.
A being capable of spreading spores, decay, and root-like corruption.

Then, combining:

* His fire
* Energy stolen from the Underworld Realm

He created:

## **The Echo of Silence**

A shadowy, echoing entity able to:

* Shield Veylthara
* Heal her
* Manipulate darkness and sound

These two entities became her **left and right wings**.

Not physically.

But functionally.

---

## V. The Second Failure

Veylthara tried again to assault the Ogre and Cyclops nations.

This time:

* Harbinger lost his staff in the chaos and lost much of his power.
* The Echo of Silence exhausted itself protecting her.
* Cyclops lightning magic continuously shattered her regrown wings.

After days of fighting, they retreated again to Arliton Island.

But this time — someone else was waiting.

---

## VI. The Humans of Dendera

A military detachment from the continent of Dendera was stationed on Arliton.

Led by a knight general, they witnessed the wounded dragon and her weakened guardians.

Using:

* Ballistae
* Chains
* Ritual suppression magic

They did the unthinkable.

> They captured Veylthara alive.

They imprisoned:

* The dragon
* Harbinger of Forgotten Roots
* The Echo of Silence

Deep beneath a constructed stone structure.

That structure, over centuries, became known as:

## The Ruined Temple

But it was never a temple.

It was a prison and a labyrinth, deliberately reshaped so that:

> If anyone ever tried to reach the dragon — they would be tested, slowed, and likely die.

---

## VII. Why the Dungeon Has Its Mechanics

Everything in the dungeon originates from Veylthara’s nature:

| Dungeon Element                 | True Origin                       |
| ------------------------------- | --------------------------------- |
| Darkness empowers enemies       | Echo of Silence influence         |
| Light weakens corruption        | Fire origin fighting rot          |
| Spores in air                   | Harbinger’s corruption            |
| Noise / chaos triggering danger | Echo’s sensitivity to sound       |
| Labyrinth design                | Human prison architecture         |
| Increasing difficulty           | Getting closer to the sealed core |

---

## VIII. Why the Archaeology Ritual Exists

Over centuries, small fragments of this truth leaked into the wilds of Arliton.

Broken statues. Seals. Forgotten markings.

Archaeologists unknowingly retrace the **four principles needed to survive Veylthara’s prison**:

* Roots (Harbinger)
* Light (her fire origin)
* Silence (Echo)
* Spores (her corruption)

The ritual doesn’t give power.

It gives **understanding** of how to survive what lies inside.

---

## IX. Present Day

Veylthara is still alive.

Still imprisoned.

Still guarded by the weakened remnants of her two “wings”.

And the Ruined Temple still stands.

Waiting.

