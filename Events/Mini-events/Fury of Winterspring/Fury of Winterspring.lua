			Fury of Winterspring
			
	Fury of Winterspring este un eveniment ce tine xclusiv de tavernele ce se gasesc from regiunea a2a a fiecarui regat pana in "Outland Territories".
	Can acest eveniment incepe, cerul se intuneca cu nori si un cooldown de 10 minute incepe, pana cand toate usile tavernelor sunt inchise.
	
	Jucatorii trebuie sa se aventureze catre o taverna si sa se adaposteasca inauntru.
	Cand timpul s-a scurs, usile se inchid iar afara incepe un vant puternic iar toti monstrii de erau in jurul acestor cradiri se transforma in fantome ce incearca sa 
ajunga inauntru. Toti cei dinauntru primesc un buff special de la ownerul cladirii ce ii ajuta sa reziste la acesti monstrii. Pe urma jucatorii sunt lasati sa iasa 
afara din cladiri si incepe lupta cu fantomele.
	V-or fii 3 serii de spawnuri de fantome pana cand semi-bosi apar. Daca toti monstrii au fost infranti, un lider fantoma este spawnat in fata cladirii si trebuie 
infrant.
	Evenimentul dureaza 2 ore iar buff dureaza doar o ora. Buff ar trebuii luat inainte ca boss sa se spawneze. Inainte ca un boss sa se spawneze cerul devine mai 
agitat iar efect de fulgere se intinte de raza intinsa. De aici in 5 minute v-a aparea si liderul timp in care jucatorii ar trebuii sa ceara acel buff de la owner.
	Monstrii ce apar au level apropiat de area sau regiune unde se spawneaza, iar liderul este type berserk si vampiric.
	
	Reward 
	De la owner se ofera un reward semnificativ, gold sau experienta, in functie de ce isi doreste jucatorul.
	
	Acest eveniment se desfasoara in fiecare seara din luna Octombrie pana in Decembrie 15, in fiecare zii de Joi. de la ora 17** pana la 7** ziua urmatoare, pe 
intreaga durata a noptii.

	Winterspring este un Boss care este predecesorul iernii. Acesta dropeaza "Spring Leaf" si "Winter Magic" ce sunt folosite in primavara la un Eveniment numit
"Spring Ceremony" si se poate cumpara de acolo diverse bunuri cu aceste monede.