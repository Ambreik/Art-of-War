## **3. Dragon Isle**

**Location:** Isolated, far from the main Middlean Islands cluster
**Environment:** Jungle island with mountainous terrain, pirate encounters
**Key Features & POIs:**

* Dragon God Dungeon – Hidden dungeon inside mountain
* Entrance Challenge – Lake with crocodiles and statue mechanism requiring archer skill to access dungeon
* No flying mounts available on the island

