## **2. Arliton Island**

**Location:** Southeast of Middlean Islands cluster
**Environment:** Crescent-shaped, tropical jungle, highlands, beaches, rivers
**Key Features & POIs:**

* Ruined Temple – Central landmark, overgrown, ancient carvings
* Rainbow Trout Goldmine – Eastern jungle, combines mining and fishing
* Southern Beaches – Resource-rich landing points and fishing spots
* Western Sea Caves – Mineral veins, glowing fungi
* Northern Ponds & Waterfall – Rare herbs, unique fish
* Jungle Heart Clearing – Open area, unusual flora and fauna

**Paths & Natural Routes:**

* Main Southern Path – Beach to Ruined Temple
* Eastern Jungle Trail – Leads to Rainbow Trout Goldmine
* Western Cliff Path – Cliffside caves and observation points
* Northern Stream Route – Connects ponds and hidden alcoves

