# **Middlean Islands Region**

**Location:** Central cluster of islands on the world map
**Purpose:** Neutral region; access hub via travel system; high-level quests, professions, and exploration
**Player Level Access:** Lv. 25+ for traveling; certain content restricted by level (e.g., some Capital City areas)
**Environment Type:** Varied — urban, tropical, jungle, coastal, and mountainous islands
**Monster & Pet Tier:** Lv. 25–60 depending on island and zone

---

## **1. Capital Island (Capital City)**

### **Districts & Key Features**

**West Side: Angler’s Haven**
Focus: Fishing, maritime culture, quests, and aquatic exploration

* Anglers’ Shelter – Learning fishing, tournaments, rare fish exhibits
* Fisherman’s Wharf – Trading, unloading catches, boat races
* Sailor’s Tavern – Quest hub for seafaring events and pirate sightings
* Dockworkers’ Guild – Shipping/delivery quests, dock defense
* Delight Stall – Regional snacks for temporary buffs
* Navigator’s Lodge – Maps, guides, exploration quests

**Central Side: Heart of Governance & Defense**
Focus: Administration, law, military training

* Mayor’s Throne – Administrative hub, diplomatic quests
* Great Library – Lore, abilities, ancient knowledge
* Military Barracks – Combat training, patrols
* Guardhouse – Crime reporting, bounty quests
* Temple of Virtue – Blessings, relic quests
* Judgment Hall – Justice system, rule enforcement

**East Side: Academy of Development**
Focus: Player skill advancement, combat/magic training

* Academies of Combat Arts – 14 specialized academies (Swordsmen, Nightfall Arrow, Enchanted Fan, etc.)
* Freshwater Plunge – Fisherman profession training and quests
* Arena of Valor (Heroes’ Ascent) – PvP, dueling, tournaments
* Mystic Emporium – Magic, enchantments, potions, mystic quests
* Training Grounds – Combat tutorials, NPC challenges

**Additional POIs Across Capital Island**

* Blacksmith’s Forge – Weapon crafting and upgrades
* Merchant’s Row – Marketplace and rare artifacts
* Alchemist’s Tower – Potion-making and consumables
* Celestial Clocktower – Day-night cycle events
* Pilgrim’s Sanctuary – Meditation, spiritual quests
* Adventurer’s Guild – Party quests, achievements, group challenges
* Beast Tamer’s Den – Pet and mount taming, races, battles

