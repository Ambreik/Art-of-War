			Middlean Islands
			
	Middlean Islands are 7 islands located central between continent of "Dendera" and "Zonen" spread through the "Great Sea" closed to each other also.
	In the old books is said that here was another continent, crashed by the wars between "Dragons" and the army of Gods and "Giants".
	
	This days are left only 7 islands as : 
*Capital Island - located in the middle of the region.
*Asku'lan - located in the south-west and is the second biggest island.
*Dragon Isle - located in the west and is the the most distant island. A NPC - Eliora — The Relic Breaker, lost sage.
*Netar Island - located in north and is the biggest island. Tomb of Great Crusaders located in "Netar Island" as a megadungeon.:-> see "Dungeon" file.
*Arliton - located in south-east side. Here can be found "Ruined Temple" (a dungeon).
*Isle of Mirath - to be developed. (check lore - the ones who stole the vials of living water and ran)
*Veyra’s Reach - to be developed.

	Each individual island have own monsters and some of them have dungeons.
	Each individual island have own mechanics.
	Each individual island have their own resources nodes.
	Some islands have some NPCs, their duty here are for questlines, provide some crafting, some are profession trainers found here, or some are Guards of the 
entrances in the dungeons or Runs found on the islands. Not all islands have dungeons or Instances or Runs.Those will just be decided when we develop them one by one.