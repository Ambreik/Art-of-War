			Elite Predator – “The Hidden Watcher”

---

### **Elite Monster – The Hidden Watcher**

**Type:** Rare Elite Monster
**Location:** Hidden Watcher’s Lair (Western cliff cave, behind waterfall)
**Level:** Scales with average player level in region
**Combat Group:** Solo
**Max Players Recommended:** 1–3 (can be engaged by more, but difficulty scales)
**Respawn:** 24–48 in-game hours

---

#### **Appearance & Description**

* A sleek, shadowy feline-like predator with reflective eyes that pierce darkness.
* Dark grey-black fur with subtle streaks of bioluminescent markings, blending perfectly with the cave walls.
* Long, clawed limbs and tail, allowing it to climb and leap across cliff surfaces.

---

#### **Combat Mechanics**

* **Ambush Predator:** Remains idle and partially camouflaged until players enter a radius of ~15–20 meters.
* **Movement:** Can climb walls and leap from the ceiling; high mobility in vertical spaces.

**Abilities:**

1. **Shadow Pounce (Single Target)**

   * Leaps onto a player, dealing high physical damage.
   * Applies minor bleed over 5 seconds.
2. **Sonic Screech (AoE)**

   * Emits a loud screech, reducing movement speed by 20% and attack speed by 15% for 8 seconds.
   * Cooldown: 15 seconds.
3. **Camouflage (Passive)**

   * When idle for >10 seconds, partially invisible (difficult to detect unless within 5 meters).
4. **Claw Swipe (Multi-target)**

   * Sweeps front arc (~3 meters), moderate damage to all targets in front.
5. **Leap Retreat (Utility)**

   * Jumps back to a higher ledge or cave wall to reposition; occasionally breaks line-of-sight.

**Weaknesses:**

* Fire and Light elemental damage slightly increased (+10%)
* Vulnerable to traps and AoE knockdowns (environmental synergy)

---

#### **Rewards / Loot Table**

**Guaranteed:**

* Rare crafting materials (e.g., Predator Fang, Shadowhide)
* Gold (scaled to player level)

**Random Drops:**

* Mistleaf or Shadowbloom herbs
* Glimmerstone or Cliff Iron ore
* Rare consumables (temporary buff items)
* Small chance of unique island item (e.g., “Watcher’s Eye” – used in crafting or quest)

---

#### **Quotes**

*When Engaged:* “You should not have come here…”
*When Use First Skill:* “Feel my shadow strike!”
*When Under 50% HP:* “I won’t fall so easily!”
*When About to Die:* “This… isn’t… the end…”
*When Player Killed:* “Pathetic… prey…”

---

#### **Special Mechanics / Environment Interaction**

* Players are vulnerable while swimming to reach the lair.
* The predator can use vertical terrain to leap and escape temporarily.
* Small treasure caches or herbs/ores can be harvested safely if players avoid agro.
* Optional tactical reward: killing The Hidden Watcher increases drop rates of herbs/ores for the next 24 hours in the same cave.

