			“Drenmor, the Tide-Reaper” - Mini-Boss 

**Type:** Elite Aquatic Beast
**Spawn Condition:** Appears when a player attempts to open the main chest aboard the Forsaken Vessel.
**Appearance:** A mutated hybrid between a sea serpent and a giant anglerfish, its body shimmering with faint blue bioluminescence and barnacle-like scales. Its jaw is lined with uneven fangs, dripping saltwater and faint luminescent residue.

**Abilities:**

* **Tidal Crush:** A heavy splash attack that deals area water damage around its target.
* **Anchor Drag:** Pulls the player underwater briefly (simulated as a root effect for 3 sec).
* **Pressure Wave:** Emits a shockwave that reduces movement speed and oxygen efficiency for 10 seconds (only while swimming).
* **Last Tide (Passive):** Upon death, releases a burst of corrosive water dealing minor damage to nearby players.

**Rewards (on kill):**

* **Aquatic Hide** (used in crafting consumables and special armor)
* **Living Coral Fragment** (used for potion and jewelry crafting)
* **Tide Reaper’s Fang** (rare — used for crafting a dungeon-specific equipment item)
* Chance for **“Deepwater Vial”** (unique consumable, described below)

