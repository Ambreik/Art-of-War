### **III. Vendor Inventory – Tavia Morn**

All items are purchased with standard gold. Prices scale slightly with player level or island tier.

**General Supplies:**

* Health Potion (Restores 25 % HP)
* Mana Potion (Restores 25 % MP)
* Energy Draught (Restores 50 % Stamina / Energy)
* Basic Antidote (Removes poison and weak debuffs)
* Torch Oil (Restores durability for torch-based light sources — used cosmetically, not mechanically)

**Island Specialties:**

* **Ashen Tonic** – A mixture brewed from local herbs; grants +5 % damage reduction for 10 minutes.
* **Miner’s Ration** – Provides +10 % mining speed for 15 minutes.
* **Scout’s Ration** – Increases movement speed by 5 % for 10 minutes.
* **Gorilla Steak** – Cooking ingredient; provides +2 % HP regeneration out of combat.
* **Living Shard Fragment** – A rare consumable drop sometimes sold by Tavia after completing her mini-quest chain; temporarily grants +10 % damage vs. Mirath’s followers for 5 minutes.

---

### **IV. Main Storyline Quest Chain – “The Forsaken Echo”**

**Quest 1: Landing at Ashen Cliffs**

* Objective: Speak with Commander Edran Holt.
* Purpose: Introduction to the island and current mission briefing.
* Reward: Basic supply package (Health Potion × 3, Mana Potion × 3).

**Quest 2: Silent Watch**

* Objective: Scout three locations around Fort Mirath Watchtowers and recover evidence of enemy movement (maps, broken armor, message fragments).
* Mechanic: Use “Search Area” interaction points hidden near watchtowers.
* Reward: Gold + Exp + “Scout’s Ration”.

**Quest 3: Echoes of Betrayal**

* Objective: Locate Mirath’s personal ledger (inside Fort Mirath’s lower warehouse) and collect 3 sealed bottles of Living Water.
* Mechanic: Players must avoid patrol groups; using the fortress cannons as distractions is optional.
* Reward: Gold + Exp + “Tattered Journal Page” (lore item).

**Quest 4: Shackled Faith**

* Objective: Rescue Private Alaric Venn from a cage in the northern barracks.
* Sidekick: Rhenna Vale joins temporarily, assisting with ranged cover.
* Reward: Exp + choice of “Living Shard Fragment” or “Ashen Tonic”.

**Quest 5: The Forsaken Stand**

* Objective: Defeat Mirath himself at the top of Fort Mirath.
* Mechanics: Standard boss encounter; Mirath uses his sword and rifle depending on distance.
* Completion Trigger: Retrieve the “Shattered Pendant of Mirath” and return to Commander Edran Holt.
* Reward: High exp + gold + unique title **“The Silent Witness.”**

**Quest 6: Return to Capital**

* Objective: Report back via the Dock NPC to the Capital City command.
* Reward: Exp + permanent access to Ashen Cliffs repeatable quests.

