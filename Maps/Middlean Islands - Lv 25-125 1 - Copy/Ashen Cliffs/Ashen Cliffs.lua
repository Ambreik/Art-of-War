### **V. Repeatable Quests and Daily Tasks**

After completing the main storyline, players unlock several daily / weekly repeatable quests from the NPCs at The Sunken Encampment.

**Commander Edran Holt:**

* *“Holding the Line”* – Defeat 20 of Mirath’s Followers in Fort Mirath.
  • Reward: Gold + Exp + chance for Living Shard Fragment.

**Rhenna Vale:**

* *“Sharp Eyes, Steady Hands”* – Eliminate 5 enemy archers in the watchtowers.
  • Reward: Exp + Scout’s Ration.

**Garrin Muld (Miner Trainer):**

* *“Under the Ash”* – Mine 5 ore nodes from the island’s caves.
  • Reward: Ore materials + small gold bonus.

**Sereth Lume (Herbalist Trainer):**

* *“Breath of the Wild Root”* – Gather 10 herbs of any type from the inner forest.
  • Reward: Exp + Ashen Tonic.

**Tavia Morn (Vendor):**

* *“Supplies for the Encampment”* – Deliver 10 crafted provisions (fish meals, bandages, etc.).
  • Reward: Gold + vendor discount for 24 hours.

**Daily Cycle Reward:**
Completing five repeatable quests within five consecutive days grants the player a bonus reward package:
→ “Crate of Ashen Provisions” (containing gold, Ashen Tonic, Miner’s Ration, and Scout’s Ration).

---

### **VI. Optional Mini-Events**

**1. The Cannon Challenge**
Occasionally, a nearby Gorilla Alpha or Elite Beast will spawn within range of one of the fortress cannons.
Players may use a loaded cannon to defeat it for extra loot and a temporary +5 % experience buff.

**2. The Echo of Mirath Event**
Randomly, after nightfall, the island echoes with Mirath’s voice — his followers enter a state of frenzy for five minutes, increasing their spawn rate and drop quality.
Players are encouraged to hunt during this window for bonus resources.

---

### **VII. Quest Completion Outcome**

Once the storyline is complete, Commander Edran Holt’s camp remains operational, serving as the official human foothold on Ashen Cliffs.
Players can return at any time for repeatables, profession training, or resource farming.
Mirath’s defeat does not end the island’s cycle — his followers respawn eternally, symbolizing the curse of the Living Water.

