### **Creature / AI Mechanics – Ashen Cliffs**

#### 1. **Human Enemy Behavior**

* **Patrolling Guards**

  * Soldiers, archers, and riflemen patrol Fort Mirath, watchtowers, and pathways connecting island pieces.
  * Patrol paths are semi-random but constrained to key areas around fortifications.
  * Human groups spawn in packs (like Metin2 group spawns), typically 3–5 NPCs per group.
  * Aggro radius: medium (10–15 meters), reacts to players entering proximity or performing hostile actions.

* **Night Boost**

  * During night cycles, human enemies gain:

    * +5% movement speed
    * +5% attack speed
    * +10% damage
    * Ranged units have +15% chance to inflict stun with rifles or bows
  * Encourages strategic engagement, especially for solo players or small groups.

* **Luring Mechanics**

  * Players can lure human enemies away from patrols into choke points, traps, or ambush areas.
  * Lured groups follow the player until reaching a distance limit or losing sight.

* **Combat Tactics**

  * Soldiers use melee skills; archers and riflemen stay at distance and focus ranged attacks.
  * Groups coordinate attacks: melee NPCs engage, ranged provide support.

---

#### 2. **Trap Zones**

* **Environmental Debuff Mechanics**

  * Specific locations on the island are **trap zones** (roots, spike patches, snare vines).
  * Entering a trap zone roots the player in place for up to **5 seconds**.
  * Traps are **passive zones** only—no human enemy sets them; they are pre-placed environmental hazards.
  * Heavily populated areas (high monster density, near Fort Mirath or cliffs) have higher chance of traps.
  * Purpose: increase difficulty for survival, encourage tactical movement, and group coordination.

---

#### 3. **Wildlife Behavior**

* **Group vs. Solo Spawns**

  * Gorillas and other herd-like animals spawn in groups of 3–5, mimicking coordinated behavior.
  * Jaguars, crocodiles, and other predators spawn individually.
  * Ambush mechanics: Jaguars and stealth predators use **stealth**, appearing suddenly from dense foliage to attack unsuspecting players.
  * Crocodiles patrol river mouths and water channels, ambushing players entering the water.

* **Aggro Mechanics**

  * Animals can pursue players beyond their spawn location, allowing for luring into traps or ambush points.
  * Elite monsters or rare spawns are persistent and may follow players for extended distances.

---

#### 4. **Boss Mechanics – Fort Mirath Lake Boss**

* Multi-skill encounter for **up to 5 players**.
* Boss stays near the lake area but can be lured slightly into the island edges without leaving water entirely.
* **Abilities**

  * Area-of-effect smash attacks
  * Targeted lunges at players with knockback
  * Periodic roar that causes temporary debuff (slight slow)
* **Rewards**

* No currency, event loot, or lore integration; purely combat-focused.

---

#### 5. **Nocturnal AI**

* **Animals**

  * Certain predators (jaguars, crocodiles) become more aggressive at night.
  * Jaguars use stealth to ambush players; crocodiles patrol more frequently at night.
  * Other animals may gain small buffs (+5% movement speed) for night hunting.
* **Humans**

  * Increased stats for night combat (see Human Enemy Behavior – Night Boost).

---

#### 6. **Lure / Follow Mechanics**

* Rare monsters, elite mobs, and bosses can be **lured** by players to strategic locations (traps, chokepoints).
* Encourages tactical gameplay for small groups or solo players to manipulate monster positioning.

---

#### 7. **Cannon Mechanics**

* Cannons are pre-placed on key locations of the island, such as Fort Mirath and cliff edges.
* Players may interact with cannons if loaded, firing at gorillas, elite monsters, or boss encounters.
* **Limitations**

  * Cannons have cooldowns and finite ammunition.
  * Positioning and timing are key to successfully using them in combat.

---

#### 8. **Environmental Sounds**

* AI and events are **hinted through environmental audio cues**:

  * Rustling foliage: stealth predators nearby
  * Footsteps: human patrols approaching
  * Distant gorilla roars: group monster location
  * Cannon fire echoes: indicates active player use or NPC cannon defense

