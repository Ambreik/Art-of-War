## **Section 2: Points of Interest (POIs) – Ashen Cliffs**

### **1. Fort Mirath (Central Fortress HQ)**

* **Type:** Main Story / Enemy Stronghold
* **Location:** Center of the island, atop the highest plateau of the semicircular mountain formation.
* **Description:** The original headquarters of Mirath and the Forsaken Band. Surrounded by walls, battlements, and watchtowers, the fortress bristles with cannons and armed sentries. Its interior houses key NPCs (traitor leaders) and quest objectives.
* **Mechanics & Features:**

  * Players must navigate patrolled courtyards, guard towers, and traps to progress in story quests.
  * Cannons can be disabled or used strategically by players during siege quests.
  * Multi-tiered fortress: rooftops, inner halls, and dungeons provide vertical exploration and tactical combat opportunities.
* **Relevance:** Core location for storyline quests involving Mirath’s betrayal, recovering stolen supplies, and confronting enemy leaders.

---

### **2. The Shattered Rampart**

* **Type:** Defensive Ruins / Exploration POI
* **Location:** Western edge of the fortress, overlooking the cliffside.
* **Description:** Collapsed walls and broken battlements, a remnant of Mirath’s defenses during the initial island occupation.
* **Mechanics & Features:**

  * Provides high ground for archers or ranged players during combat encounters.
  * Hidden caches and minor loot spawn in rubble areas.
  * Natural chokepoints allow ambushes and stealth opportunities.

---

### **3. The Watcher’s Perch**

* **Type:** Tower / Observation POI
* **Location:** Northern cliffs, overlooking the inner island waterways.
* **Description:** A tall, stone watchtower, still partially operational with signal fires. Used historically to spot intruders approaching the island.
* **Mechanics & Features:**

  * Offers strategic view of multiple waterways and smaller island pieces.
  * Players may trigger minor alert events, causing patrolling NPCs to converge.
  * Serves as a vantage point for story missions or reconnaissance quests.

---

### **4. The Sunken Encampment**

* **Type:** NPC Hub / Quest POI
* **Location:** Near a secluded cove along the southern shore, adjacent to the small landing dock.
* **Description:** Remnants of a shipwreck salvaged by survivors; tents and temporary structures were set up here. A safe haven for allies with several NPCs:
  
  * Commander (storyline quest giver)
  * Soldier (he got captured)
  * Miner NPC (profession trainer)
  * Herbalist NPC (profession trainer)
  * Archer NPC (quest sidekick)
  * Vendor NPC (basic supplies and minor quests)
* **Mechanics & Features:**

  * Acts as a “local hub” for story quests, profession training, and item purchases.
  * Provides temporary safe zone for players after landing.
  * NPCs provide narrative guidance for attacking Fort Mirath or recovering stolen supplies.
  * Side quests may involve using the archer NPC to clear obstacles or protect players during incursions.

---

### **5. The Broken Maw (Waterfall Split Basin)**

* **Type:** Environmental / Navigation POI
* **Location:** Central island basin, where cascading waterfalls split the land into smaller pieces.
* **Description:** Water from the peaks pours through rocky channels, dividing the island naturally into smaller, closely spaced land masses. Pools and streams connect the areas, creating natural bridges and swimming paths.
* **Mechanics & Features:**

  * Natural obstacle for navigation; requires careful swimming or timing to traverse safely.
  * Potential fishing points in calm pools.
  * Some small islands may contain secret resources or minor loot caches.

---

### **6. The Dock of Solitude**

* **Type:** Arrival / Departure POI
* **Location:** South-east shore of the island, in a sheltered cove.
* **Description:** Small pontoon-style dock used for arriving from and departing to Capital City.
* **Mechanics & Features:**

  * Players cannot teleport; must arrive via boat/ferry or other approved long-range transport.
  * NPC shipmaster offers boat rentals and minor travel guidance.
  * Safe zone radius; no hostile spawns near the dock.
  * Serves as **“Resurrect in Town”** anchor.

---

### **7. The Shipwrecked Refuge**

* **Type:** Environmental / Exploration POI
* **Location:** Secluded beach on the island’s western shore.
* **Description:** A hidden shipwreck partially embedded in sand and vegetation. Provides minor resources, hidden loot, and small environmental hazards (sharp debris, collapsed masts).
* **Mechanics & Features:**

  * Resource nodes (wood, minor ores) scattered across wreck.
  * Occasional ambush by island fauna or low-tier enemy patrols.
  * Can serve as staging ground for side quests or temporary hideouts.

---

### **8. The Cannon of Betrayal**

* **Type:** Environmental / Combat POI
* **Location:** Eastern cliffs near the main landing approaches.
* **Description:** A fully operational cannon placed by traitors to repel invaders. Rusted but deadly, overlooking key approach paths.
* **Mechanics & Features:**

  * Players may disable the cannon through stealth or ranged attacks.
  * Firing range affects certain landing or beach areas.
  * Provides tactical advantages for NPCs and adds environmental danger for players approaching the fortress.

