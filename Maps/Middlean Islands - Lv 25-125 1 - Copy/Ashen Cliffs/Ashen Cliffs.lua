			Ashen Cliffs (Isle of Mirath)

---

## **1. General Overview**

The **Ashen Cliffs**, once whispered as the **Isle of Mirath**, lie within the storm-shrouded heart of the **Middlean Islands**. No official chart marks its coordinates, and few sailors dare approach its jagged horizon, where mist rolls like smoke from a dying fire.

**Location on the world map:**
Ashen Cliffs sits in the **southwestern sector of the Middlean Islands cluster**, on the leeward side of the archipelago. It is positioned roughly southwest of the Capital Island and near the western islands (closer to Asku'lan/Dragon Isle corridors), in waters often avoided by merchant routes. Strong currents and frequent coastal fogs make direct approach difficult — one reason the island is rarely visited and often omitted from formal maps.

---

### **Geography and Terrain**

The island is fractured by **rivers and waterfalls** that descend from a **semi-circular mountain ridge** — a natural wall that shields the inner basin from sea winds. Over centuries these torrents carved the land apart, creating a chain of **fragmented land sections** connected by shallow crossings and remnants of once-solid paths.

The core of the island remains one large mass — the **Inner Basin** — surrounded by smaller splintered pieces of terrain that resemble a shattered crown. These peripheral landmasses can be reached by swimming or by stepping across exposed stone when the water runs low.

Around the base of the mountain, a **dense forest of towering trees** thrives, their canopies so thick they block most sunlight. Between the forest edge and the beaches grow **palm clusters** and ferns, forming a humid, coastal jungle. Broken **stone walls**, **watchtowers**, and **rusted cannons** line the cliffs — relics of the desperate fortifications built by Mirath and his Forsaken Band in their final days.

---

### **Atmosphere**

The air is heavy, tinted with the scent of wet stone and salt. Distantly, waterfalls echo like whispers of voices long gone. Occasionally faint silhouettes move between the trees — remnants of Mirath’s soldiers or the living memory of those who stayed.

At night the island is lit only by scattered campfires and watch torches along the fortifications; mist drifts through the ruins and the sound of marching boots or shouted orders sometimes carries on the wind. The island feels less like a place and more like a memory made solid.

---

### **Lore Connection**

**Mirath**, once a trusted officer and messenger during the **Second Great War**, betrayed both **King Aric** and **Lord Kellan**, spreading false rumors that fractured the human alliance. He fled under cover of darkness with stolen supplies and loyal defectors, establishing a refuge here — the place that now bears his name.

Cut off from the mainland and hunted by both sides, Mirath and his followers built their fortress in paranoia and hunger. They sealed themselves behind walls of stone and madness, turning their refuge into a prison. None escaped. None were forgiven.

To this day, **the Forsaken Band** still haunt the Ashen Cliffs — pale-skinned, hollow-eyed remnants of their former selves. Their leader’s fate remains unknown, but some say Mirath’s influence lingers in the fort’s tactics and the traps that still catch the unwary.

---

### **Modern Role in Gameplay**

Ashen Cliffs is a **mid-tier exploration and combat zone** that emphasizes human conflict, survival, and environmental hazards rather than high-fantasy monsters alone. Key design roles:

* Human-type enemies (Forsaken Soldiers, Marauders, Outriders, Trappers) with trap networks and patrol AI.
* Hazardous terrain: collapsing walls, water-split land, ambush points, and defensive fortifications.
* Resource opportunities tied to rivers and waterfall basins (freshwater fishing, rare herbs).
* Storyline progression: rescue missions, sabotage, and the assault on Fort Mirath.
* Exploration and profession ties: profession trainers and small hubs (survivor camp, dock spot) placed as non-intrusive spots on the fractured terrain.

