## **9. Event or Quest Links – Ashen Cliffs**

### **I. Overview**

Ashen Cliffs offers a mixture of **storyline** and **repeatable tasks**, all centered around the investigation of **Mirath** and his forsaken followers.
The quests originate from **The Sunken Encampment**, a small survivor outpost built around a wrecked ship, where the player first lands after arriving on the island.

All events and quests are designed to be undertaken by **a single player or small groups (up to 5 players)**, emphasizing survival, infiltration, and tactical exploration over large-scale battles.
No additional currencies are used; all rewards are provided in experience, gold, consumables, or materials relevant to professions.

