### **2. Flying Fox Tiroliana / Zip Line**

**POI Name:** *Skyreach Zip Line Station*
**Location:** Peak of *Skyreach Spire*, the tallest mountain on the island.
**Mechanics:**

* Player reaches the mechanical device (zip line launcher) and chooses a direction. Some directions lead to:

  * Major POIs (e.g., Hidden River Gorge, Twilight Grove, Emerald Jungle Clearing)
  * Secret resource nodes (rare fish pools, herbs, or minerals)
* **Obstacle System on the way down:**

  * Falling debris, large birds, or environmental hazards appear.
  * Player can **move left/right** to avoid collisions.
  * 3 hits = fall → death or heavy damage (linked to fatigue/fall mechanics).
* **Usefulness:**

  * Fast traversal across the island (shortcut for gathering, exploration, or escaping monsters).
  * Adds skill-based challenge to traversal, not just convenience.

