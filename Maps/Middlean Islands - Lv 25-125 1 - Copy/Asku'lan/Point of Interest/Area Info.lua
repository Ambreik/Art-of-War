### **4. Resources – Asku’lan Island**

**Fishing:**

* **Rockscale Snapper** – Found along the jagged western cliffs and Western Mangrove Labyrinth. Tough and fast, used in high-level cooking recipes.
* **Thunder Trout** – Freshwater species inhabiting Hidden River Gorge and Thunder Falls pools. Emits faint electric sparks at night, rare and prized for alchemy.
* **Spirefin Bass** – Saltwater fish found near the southern coves. Medium rarity, often caught near tidal currents.
* **Glowtail Eel** – Rare, lives in Twilight Grove pools at dusk. Bioluminescent; used in potions enhancing night vision or stealth buffs.

**Mining:**

* **Iron Veins** – Found scattered in Western Cliff Overhangs and Broken Plateau. Standard high-level material.
* **Silver Ore** – Occurs near Skyreach Spire base and Northern High Ridge. Used for advanced crafting and magical items.
* **Quartz Crystals** – Found along Thunder Falls and Hidden River Gorge caverns. Occasionally used in alchemy or crafting glowing items.
* **Runestone Fragments** – Rare mineral shards near Spire Bloom Tree clusters in Twilight Grove; used for rune crafting and high-tier stat bonuses.

**Herbalism:**

* **Stormleaf** – Found near Skyreach Spire; reacts to high winds, used in potions that boost agility or movement speed.
* **Glowfern** – Appears in Twilight Grove and northern riversides; harvested for magical crafting and buffs.
* **Rivermist Herb** – Grows near Hidden River Gorge pools; requires careful gathering to avoid aggressive wildlife.
* **Thunderbloom** – Rare flower found around Thunder Falls cliffs; used in high-level alchemy and potion recipes.

**Skin/Leather (Optional – from local monsters):**

* **Jaggedhide** – From Rank III–IV reptilian or jungle predators near Emerald Jungle Clearing and Western Mangrove Labyrinth. Used in armor crafting.
* **Stormfur** – From highland predators along Northern High Ridge and Broken Plateau; rare, valuable for high-level cloaks or crafting buffs.
* **Spirewing Scales** – Dropped by flying predators near Skyreach Spire; used in specialty runes and rare crafting recipes.

