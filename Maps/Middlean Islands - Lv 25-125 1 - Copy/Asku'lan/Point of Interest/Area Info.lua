### **Creature / AI Mechanics**

1. **Ambush Predators / Stealth Mobs**

   * *Thunderfang Serpents* and *Stormclaw Raptors* hide in dense jungle or cliff crevices.
   * Aggro triggers if player enters reduced detection radius; adds danger to exploration, fishing, or gathering.

2. **Linked Aggro / Environmental Alert**

   * Disturbing wildlife (birds, small critters) alerts nearby hostile monsters (*Skyreach Golems*, *Jungle Lynx packs*).
   * Chain reactions can lead to sudden combat encounters; encourages strategic movement.

3. **Day/Night Behavior System**

   * **Day:** wildlife and monsters behave normally; low detection range.
   * **Night:** monsters gain buffs (+aggro, +damage); certain rare/semi-boss monsters spawn only at night near POIs (Skyreach Spire, northern cliffs, secret coves).
   * Encourages planning for farming puzzle pieces or exploring secret POIs.

4. **Monster Interaction with Environmental Mechanics**

   * Flying predators may intercept players on **zip lines**, forcing skill-based movement (left/right dodging).
   * Monsters near Totem of the Thunder Guardians can interrupt puzzle attempts if the player is not careful.

