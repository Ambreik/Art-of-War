### **3. Skyreach Spire Totem – Puzzle Mini-Game**

**POI Name:** *Totem of the Thunder Guardians*
**Location:** Summit of Skyreach Spire.
**Game Concept:**

* Monsters across Asku’lan drop **puzzle pieces** necessary to interact with the totem:

  * **Piece of the Storm Orb** – dropped by *Stormclaw Raptors* (Flying Rank III)
  * **Shard of Skyrunestone** – dropped by *Skyreach Golems* (Ground Rank IV)
  * **Lightning Sigil Fragment** – dropped by *Thunderfang Serpents* (Ambush Predator Rank III)
* Players must collect **all three types of pieces** to interact with the Totem.

**How the Game is Played:**

1. Player approaches the Totem with all required pieces.
2. A **puzzle interface** appears (visual: floating slots on the Totem).
3. Player must **drag and place each piece in the correct slot**.

   * The correct combination triggers the event.
   * Incorrect placement resets the puzzle with a short cooldown (1–2 minutes).
4. Once completed:

**Event Outcomes:**

* **If other players are on the island:**

  * Lightning strikes **all other players**, instantly killing them.
  * A **3-minute resurrection cooldown** applies.
  * No mount usage for **15 minutes** after resurrection.
  * Player who completed the puzzle sees a **system message**, e.g., “You have summoned the Thunder Strike upon Asku’lan!”

* **If no other players are on the island:**

  * Lightning strikes 3 **specific locations** on the island.
  * Each strike **summons a rare/semi-boss monster**:

    1. *Stormclaw Drake* – Emerald Jungle Clearing
    2. *Skyreach Elemental* – Northern High Ridge
    3. *Thunderfang Hydra* – Hidden River Gorge
  * System message informs the player where monsters spawned.

**Game Interaction Notes:**

* Puzzle pieces are **account-bound or tradeable**, depending on desired difficulty.
* The Totem can be spammed as you wanted — **multiple players can attempt the puzzle simultaneously**, leading to strategic competition.
* Rare monsters / lightning effect gives both PvE and PvP-like consequences, making this island unique.

**Visual / Gameplay Feel:**

* Totem glows with **blue electricity runes**.
* Lightning strikes are **dynamic**, synchronized with visual and sound effects.
* Puzzle pieces are **brightly colored orbs/shards** that float above player’s head once equipped, giving a clear visual cue.
* Mini-game interface is **simple drag-and-drop**, but the timing and placement create tension.

