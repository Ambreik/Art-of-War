### **3. Vegetation**

* * Lush, dense tropical jungle dominates the interior, with towering canopy trees and thick undergrowth.
  * Vines, ferns, and moss are common, creating stealth opportunities for both players and monsters.
  * Unique to Asku’lan: *Spire Bloom Trees*, rare bioluminescent plants near Skyreach Spire that open only at twilight, glowing faintly and attracting rare wildlife and some herb nodes.
  * Narrow riverbanks and waterfalls have thick reeds and flowering plants that serve as both decorative and functional resource nodes.

