## **Resource-Based Mechanics**

1. **Rare Spawn Herbs / Minerals**

   * Only **one rare herb** and **one rare mineral vein** spawn at a time per island.
   * **Herbs respawn** ≈ 15 minutes; **minerals** ≈ 30 minutes+; veins have HP and must be mined/attacked.
   * Creates competition and encourages player-driven economy.
   * **Examples:**

     * *Skyblossom Herb* – Grows near northern high cliffs and Skyreach Zip Line landing zones. Used in high-level potions and crafting.
     * *Thunderstone Vein* – Found near Skyreach Spire slopes; used for crafting Totem puzzle pieces and rare equipment enhancements.

2. **Fishing Pools / Schools**

   * Appear along **southern coast, hidden coves, and inland freshwater ponds**.
   * Pools provide unique fish for Asku’lan:

     * *Stormfin Snapper* – Rare fish, spawns in northern ponds; used for high-level cooking or potions.
     * *Golden Marlin* – Coastal pool fish, respawn slow; used in luxury dishes.
   * **Fishing outside pools** yields common/low-value fish.
   * Pools deplete after a certain number of catches → forces exploration and movement.

3. **Environmental Gathering (Weather-Tied Resources)**

   * Certain herbs appear only during **storms, gusty winds**, or night fog (e.g., *Nightwind Fern* on cliff edges).
   * Zip line landing points may spawn **temporary resources** if weather aligns (e.g., rare insects or glow moss for alchemy).

