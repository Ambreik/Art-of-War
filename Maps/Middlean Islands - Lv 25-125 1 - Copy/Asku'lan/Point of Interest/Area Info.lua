	Asku’lan – Middlean Islands

### **1. Overview**

* **Location:** South-west of the Middlean Islands cluster.
* **Summary:** Asku’lan is the second-largest island of the Middlean archipelago. Its rugged coastline, dense forests, and jagged highlands make it a formidable destination for adventurers seeking high-level challenges. Despite being uninhabited by civilians, the island’s dangerous environment attracts both daring explorers and professional hunters.
* **Known For:**

  * **High-level content:** recommended for players level **58–70**, with monsters ranging from **Rank II to Rank IV**.
  * **Treacherous terrain:** sheer cliffs, rapid rivers, and dense jungle make navigation perilous.
  * **Unique Feature:** *The Skyreach Spire*, a massive natural stone pillar rising from the center of the island, acts as both a navigation landmark and a site for rare environmental phenomena. At certain times, strong winds and lightning strikes around the spire create natural hazards and special resource opportunities.
* **Importance / Notable Site:**

  * *Skyreach Spire* — center island landmark, focal point for exploration and potential high-value resource zones.
  * Serves as a **challenge island** for players progressing from the Capital City or Arliton Island, introducing more advanced environmental hazards and monster encounters.

