### **5. Environmental Mechanics**

#### *Global Mechanics

### **1. Global Noise System**

* Player actions generate noise that can alert monsters.
* Terrain (jungle, cliffs, mangroves) amplifies or dampens noise.
* Rank II–IV monsters have higher sensitivity on high-level islands like Asku’lan.

### **2. Monster Awareness System**

* Governs how creatures perceive, react, and adapt in real time.
* Ambush predators use height, shadows, or cover strategically.
* Monsters can call nearby packs (linked aggro) or retreat to safer areas.

### **3. Night-Day Behavior System**

* Dynamic monster behavior, spawn patterns, and environmental interactions based on time.
* Day: normal behavior, easier hunting/fishing.
* Night: more aggressive monsters, stealth zones more effective but more dangerous.
* Lightning strikes on peaks (Skyreach Spire) create temporary illumination and hazards.

### **4. Stealth & Camouflage System**

* Dense vegetation, tall grass, jungle, and mangroves grant temporary stealth/camouflage.
* Detection radius reduced, but high-rank monsters still pose ambush risks.
* Works across all islands with vegetation coverage.

### **5. Weather & Lighting System (Limited)**

* Handles lighting effects, especially night and storm visuals.
* Lightning strikes can illuminate terrain, reveal paths, or stun monsters.
* Provides tension and dynamic visual cues without adding full weather simulation.

### **6. Fatigue Waters**

* Swimming too far into deep seas applies a stacking debuff until death (drowning).
* Prevents endless ocean travel exploitation and adds risk to aquatic exploration.
* Applies to all islands surrounded by ocean, including Asku’lan.

### **7. Stealth Zones (Dense Vegetation Camouflage)**

* Dense jungle, mangroves, tall grass, or overgrowth grant camouflage buffs.
* Temporary invisibility to mobs/players (not total), breaks on attack or ability use.
* High-rank monsters can also use it, creating ambush opportunities.

