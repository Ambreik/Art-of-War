			Dangers on the decks of the Capital City

		1. Wandering Spirits  
	These are restless ghosts of former sailors, traders, and travelers who met untimely fates on the high seas. They wander the docks in search of their lost 
possessions or retribution against those who disrupted their rest. While they are less powerful than other spectral threats, they can be highly aggressive when 
provoked or approached.
- Danger : "Wandering Spirits" often appear suddenly, surprising players and draining their health or stamina with chilling touches. If players linger too long in their presence, they may become "marked", causing more spirits to be drawn to them.

---

		2. Spectral Watchers  
	"Guardians of the Ghost Ship", "Spectral Watchers patrol" key points on the dock and around the vessel. They are attuned to intruders and can detect players who
stray too close to the ship’s restricted areas.
- Danger : Once a "Spectral Watcher" spots a player, it will trigger a haunting alarm that calls more spirits and powerful specters to the area. Players must either
evade the "Watcher’s" line of sight or use talismans, like "Spirit Driftwood", to pass undetected.

---

		3. Ethereal Boundaries  
	These invisible “walls” are enchanted barriers created by ancient curses to prevent mortals from reaching certain areas or the "Ghost Ship" itself. Stepping too
close to these barriers can result in intense spiritual backlash, freezing players in place or dealing large amounts of damage.
- Danger : Players who inadvertently walk into an Ethereal Boundary may experience a severe "energy drain" or be forced to retreat. Those with high spirit 
resistance may notice a slight shimmering in the air, giving them a chance to avoid these hidden barriers.

---

		4. Ghastly Wails  
	Echoes of past shipwrecks, the "Ghastly Wails" are disembodied voices and cries for help that randomly occur throughout the dock. While these sounds are 
illusions, they have a deep psychological effect on adventurers.
- Danger : Prolonged exposure to the wails causes the "Dreaded" debuff, lowering the player’s morale, stamina recovery, and accuracy. Players affected by "Dreaded" 
for too long may suffer hallucinations, leading them off the dock paths or into "Ethereal Boundaries".

---

		5. Spectral Tides  
	An eerie mist rises from the water, filled with the energies of the cursed sea. When the "Spectral Tides" arrive, they flood certain dock areas, covering the 
ground with a shimmering fog that pulses with ghostly energy.
- Danger : Entering these fog-filled areas deals consistent damage over time and disrupts a player’s vision, making navigation difficult. The "Spectral Tides" ebb 
and flow, and players need to time their movements to avoid walking directly into the mist.

---

		6. The Shadowed Mariner  
	An elite spirit who serves as an enforcer on the docks, the "Shadowed Mariner" is rumored to be the shade of the "Ghost Ship’s" original captain. He moves 
swiftly and silently, appearing only to those who challenge the boundaries of the restricted dock areas.
- Danger : If the "Shadowed Mariner" spots a player, he pursues relentlessly. His strikes drain life energy directly, making him exceptionally dangerous to 
unprepared adventurers. "The Mariner" can only be avoided through special routes or talismans, as he is immune to traditional combat.

---

		7. Poltergeist Objects  
	Throughout the docks, barrels, crates, and other objects may suddenly animate due to the heavy concentration of spiritual energy. These objects lurch, tip, and 
roll toward players in an attempt to knock them down or block their way.
- Danger : Players caught off guard by moving crates or barrels risk being knocked off balance or into dangerous areas. Some players can use agility or magic to ward off these mischievous objects, but it requires quick reaction times to avoid being struck.

---

		8. The Shrieking Wind  
	At random intervals, an icy, ghostly wind sweeps over the docks, carrying a cacophony of whispers, screams, and eerie melodies. This wind disorients players, causes chilling damage, and attracts other spirits to the area.
- Danger : Players caught in the "Shrieking Wind" will find their movement slowed and vision blurred, making them vulnerable to ambushes from other spirits. The 
wind also extinguishes light sources, leaving players to navigate in near darkness until it passes.

---

		9. Cursed Water Elementals  
	Born from the remnants of the "Ghost Ship’s" doomed voyages, "Cursed Water Elementals" rise from the harbor waters to haunt the docks. These entities are drawn
to areas with high spectral activity, especially near the ship.
- Danger : They deal water-based magic damage and can trap players in their swirling currents, pushing them into other dangerous areas. Players must use magic or 
physical resistance to weaken them and avoid getting too close to the water’s edge, where the elementals emerge.

---

		10. Whispers of the Dead  
	Near the "Ghost Ship", players may encounter strange, incorporeal voices offering guidance, warnings, or strange tales of riches hidden nearby. These whispers 
are cryptic and often lure players off the beaten path, leading them into danger.
- Danger : Following the whispers may lead players into traps or cause them to step into haunted areas. Some whispers may, however, give clues to avoid specific dangers or offer insight into secret dock routes, rewarding the courageous and cautious alike.

--- 

	These spectral dangers create a challenging environment where players must stay alert, use "Varrik’s guidance", and rely on protective items to survive the 
eerie atmosphere of the docks after dark.