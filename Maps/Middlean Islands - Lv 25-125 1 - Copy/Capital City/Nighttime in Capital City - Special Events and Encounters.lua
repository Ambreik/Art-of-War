			Nighttime in Capital City - Special Events and Encounters

	As the moon rises and the city sleeps, the atmosphere of the "Capital City" transforms into a mysterious and haunting landscape. The streets and docks take on 
an eerie glow under the soft light, and strange, spectral events begin to unfold. Players can expect the following:

1. "Rare NPC Vendors" :
   At night, certain "spectral NPC vendors" make their way to the "Capital City’s" streets and docks. These vendors offer unique items not found during the day, 
such as rare ghostly artifacts, mystical potions, and enchanted gear that grant temporary advantages. Some vendors may also offer unusual, one-time items based on 
specific quests or interactions players have completed.
	
