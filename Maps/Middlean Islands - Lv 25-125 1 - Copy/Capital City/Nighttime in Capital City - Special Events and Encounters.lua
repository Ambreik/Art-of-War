2. "Ghostly Visitors at the Docks" : 
   Players will occasionally encounter ghostly visitors who appear at the docks after midnight. These spectral beings could offer quests, provide information on 
hidden locations, or give cryptic hints about the city’s dark history. Not all visitors are friendly; some might be hostile spirits or cursed entities who 
challenge the players to prove their worth or strength.
3. "Hidden Areas Under Moonlight" :
   Under the moon’s glow, certain areas of the city become visible, revealing secrets and pathways that are otherwise hidden during the day. These hidden areas 
might lead to forbidden locations, eerie landmarks, or spectral realms that are only accessible at night. Players who explore these spaces may find special 
treasures or encounter danger in the form of vengeful spirits and cursed objects.
4. "Spectral NPCs" :
   Throughout the night, various "Spectral NPCs" can be found in different parts of the city, particularly near the docks, taverns, and haunted areas. These NPCs 
offer a range of services, quests, or interactions tied to their ghostly nature. Some may provide guidance, while others may need help lifting curses or 
performing unfinished tasks from their past lives. These NPCs include characters such as:
   - "Helmsman Varrik - The Stormwatcher"
   - "First Mate Edrin - The Gloomsinger"
   - "Quartermaster Rhyla - The Lantern Keeper"
   - "Cook Jarvo - The Hungry Ghost"
   - "Bosun Nyra - The Seeker"
   - "Lookout Tarl - The Silent Watcher"
   - "The Scribe of Shadows"

	These "Spectral NPCs" are integral to the story, offering valuable quests, items, and lore that help players uncover the mysteries of the city and the 
surrounding areas. However, interactions with these spectral beings require players to meet certain criteria, such as increased reputation (REP) or the completion 
of specific quests.

5. "Mysterious Nighttime Quests" :
	The quest board at the tavern refreshes after midnight with unique and time-sensitive quests. These quests might involve gathering ghostly ingredients, solving 
riddles left by the spirits, or protecting the city from emerging threats that only manifest under the cover of darkness. Completing these quests rewards 
players with rare items, lore, and increased reputation with spectral NPCs.

	As the night deepens, the "Capital City" reveals its hidden layers, making it a thrilling and dangerous place for adventurers seeking to uncover its spectral 
secrets. Every night promises a new set of challenges, opportunities, and eerie encounters that bring players closer to the unknown.