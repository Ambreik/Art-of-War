	2. "Guardian Solara Kyne"  
	Role : Solara Kyne is the vigilant guardian of the "Monthly Mayhem", a monthly PvP tournament that takes place regularly starting from "Expansion II". Solara is
known for her efficiency, sharp instincts, and swift decision-making skills, making her the perfect figure to oversee such a fast-paced event. 

   Appearance : Solara is a striking figure, her armor a dazzling shade of blue that gleams like the sky on a clear day. Her helmet is sleek, covering most of her 
face and leaving only her glowing, piercing eyes visible, giving her an almost mystical appearance. Her posture is always alert, with a keen gaze that takes in 
every detail of the arena. The blue hues of her armor are further enhanced by the swirling energy around her, a reflection of her close connection to the forces of 
the tournament. She exudes an aura of confidence and decisiveness, making her a respected figure among the competitors.

   Special Function : Solara is tasked with maintaining order during the "Monthly Mayhem" event, overseeing the matchmaking process, ensuring that all participants 
adhere to the rules, and managing the rankings.She keeps a close eye on every match and is quick to step in should there be any signs of unfair play or disruptions.
Players looking to enter or resolve issues with the event will find Solara at the center of it all, her expertise ensuring the integrity of the competition.

	These NPCs maintain the structure and discipline of the tournaments, each playing an integral role in upholding the events’ fairness and smooth operation. They 
are both seen as embodiments of authority and order, ensuring that the tournaments are memorable and prestigious for all players involved.