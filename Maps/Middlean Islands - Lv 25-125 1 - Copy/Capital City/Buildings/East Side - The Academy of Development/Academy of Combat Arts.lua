			Academy of Combat Arts

	"Academy of Combat Arts" is a sprawling, multi-winged structure, with dedicated sections for each specialized academy. Set in a serene, yet fortified location, 
this long, stone building is nestled within towering walls adorned with tapestries and symbols representing each unique combat art. Ornate banners and statues 
reflect the diversity of arts taught within, inspiring respect for the mastery and tradition of each school. Here, skilled trainers for each combat discipline offer
intense, personalized training, allowing players to evolve and refine their fighting skills according to their race and class.

		Overall Structure and Mechanics
	In the "Academy of Combat Arts", players advance by participating in training exercises, sparring matches, and specialized drills that mimic battle scenarios. 
Each section of the academy allows players to enhance abilities relevant to their class and race, giving them unique bonuses and skill upgrades as they progress. A 
point-based training system, lets players allocate points to different stats or skills based on combat performance. Players also gain access to skill trainers that 
unlock new moves and combinations once certain requirements are met, offering a sense of growth and mastery.

	Each academy also holds weapon displays, sparring rings, and practice dummies, enabling players to get hands-on experience with their combat styles. Some 
academies even contain unique terrain features, like rocky platforms or elevated ledges, which allow players to train with situational awareness and terrain 
advantages.

---

	The "Academy of Combat Arts" serves as a grand training hall where warriors, rogues, archers, and other combatants hone their skills. Each academy is dedicated 
to a specific combat style, offering players the opportunity to develop their abilities through skill progression, from "Basic" to "Master", "Grand Master", and 
ultimately "Perfect Master". The classes within the academy are designed to be accessible, with a simple progression system inspired by points earned through combat
and quests. The system is straightforward but allows for specialization and growth within each class.


		Academy of Combat Arts

	1. "Academy of Swordsmen"
   - Trainer : "Seraphim Caelen", a seasoned warrior, teaches mastery of the sword.
   - Class Mechanic : The Swordsman class (both Knight and Arahan factions) focuses on swordsmanship. Skills like "Slash", "Shield Charge", and "Cleave" evolve with
points into stronger versions, and unlocking new combo attacks as they progress. Players can choose between defensive or offensive builds but use the same core 
skill progression system.

	2. "Academy of Blood Knife (Ninja Assassin / Ninja Rogue)"
   - Trainer : "Mistress Asaya", a shadowy figure, specializes in stealth-based combat.
   - Class Mechanic : "Combo Points" – Both the Ninja Assassin (Dendera) and Ninja Rogue (Zonen) use combo points to enhance their skills. Stealth-based attacks 
generate combo points, which can then be spent on powerful techniques like "Backstab", "Silent Strike", or "Poisoned Blade", with each subsequent rank offering 
faster generation and stronger combos.

	3. "Academy of Eternal Shield (Warrior Defender / Warrior Paladin)"
   - Trainer : "Barak Ironfist", a defender of the realm, teaches shield defense and tanking.
   - Class Mechanic : Both Warrior Defenders (Dendera) and Warrior Paladins (Zonen) share the same core defensive skills, with abilities like "Shield Block", 
"Defensive Stance", and "Guardian's Aegis" enhancing as players gain points. As they rank up, they gain improved damage mitigation, self-healing abilities, and AoE 
threat generation for better aggro control.

	4. "Academy of Wrath Sword (Warrior Knight / Warrior Arahan)"
   - Trainer : "Master Arvos", a hardened fighter skilled in offensive combat.
   - Class Mechanic : The Warrior Knight (Dendera) and Warrior Arahan (Zonen) specialize in offense, with a focus on devastating strikes like "Heavy Strike" and
"Charge Slash". These abilities evolve into faster, more powerful attacks as they progress, with additional buffs that enhance the player’s damage output.

	5. "Academy of the Essence of Life (Healers)"
   - Trainer : "Seer Kalista", a wise healer, teaches restorative magic and life-force enhancement.
   - Class Mechanic : Healing abilities are tied to the allocation of skill points. As players progress through the academy, they unlock stronger healing skills 
such as "Healing Touch", "Revitalizing Aura", and "Sacred Blessing". Players can choose to specialize in single-target or area-of-effect healing.

	6. "Academy of Night (Warlocks / Mages)"
   - Trainer : "Lilith of the Shadows", a master of dark magic and curses.
   - Class Mechanic : Warlocks (or Mages) specialize in elemental magic, focusing on abilities like *Dark Bolt*, *Fireball*, and *Shadow Curse*. The more points invested, the greater the potency, range, and AoE effects of the spells. They can also unlock unique abilities, such as draining the life force from enemies or summoning dark minions.

	7. "Academy of Purple Fangs (Rogues)"
   - Trainer : "Trainer Jorak", an expert in stealth and poison tactics.
   - Class Mechanic : Rogues focus on poison and agility. Abilities like "Poison Strike", "Quick Step", and "Trap Setting" evolve to provide stronger effects. 
Players can specialize in stealth, gaining invisibility, or in trap setting, which deals higher damage and causes debilitating debuffs to enemies.

	8. "Academy of Bow Master (Archers / Rangers)"
   - Trainer : "Ranger Ailith", a skilled archer of the forest.
   - Class Mechanic : Archers or Rangers develop their ranged abilities, such as "Aimed Shot" and "Piercing Arrow". As players advance, their attacks become faster,
more accurate, and hit multiple targets at once. Additional skills like "Nature’s Call" (summoning creatures) can be unlocked as they rank up.

	9. "Academy of Eternal Scepter (Mages / Mystics)"
   - Trainer : "Archmage Teralyn", an expert in arcane and elemental magic.
   - Class Mechanic : Mystics and Mages specialize in elemental abilities like "Fireball", "Lightning Bolt", and "Ice Storm". As players progress, their spells 
become more powerful, affect larger areas, or deal additional elemental damage (e.g., Fire Damage + Burn). "Arcane Power" becomes more available, allowing for 
stronger, mana-efficient spells.

	10. "Academy of Bright Bell (Bards)"
    - Trainer : "Melodian Callen", an expert in song magic and buffs.
    - Class Mechanic : Bards use their music to buff allies. Abilities like *Song of Valor* and "Harmonic Shield" improve as the player gains experience, affecting 
more allies, providing longer-lasting buffs, or adding more powerful debuffs to enemies.

	11. "Academy of Mastery Guardians (Paladins)"
    - Trainer : "Captain Rathar", a legendary paladin who focuses on both offense and defense.
    - Class Mechanic : Paladins can choose to specialize in either offense or defense, but they share the same core abilities like "Divine Shield", "Holy Strike",
and "Aegis of Light". These abilities grow stronger over time, with additional healing effects, stronger tanking skills, and the ability to buff allies.

	12. "Academy of Nightfall Arrow (Shadow Archers)"
    - Trainer : "Trainer Sable", an expert in stealth archery.
    - Class Mechanic : Shadow Archers rely on stealth and agility. Skills like "Shadow Arrow" and "Silent Kill" become stronger the more points are invested, with 
additional damage when attacking from stealth. As they advance, they can unlock new abilities that allow them to move in and out of stealth more rapidly.

	13. "Academy of Dancing Daggers (Agile Fighters)"
    - Trainer : "Mistress Elena", a graceful fighter skilled in dual-wielding daggers.
    - Class Mechanic : Agile Fighters focus on speed and dexterity. Skills like "Dagger Dance" and "Evasion Slash" increase in power as players rank up, with 
improved agility, quicker attacks, and the ability to dodge incoming damage more effectively.

	14. "Academy of Enchanted Fan (Mystics)"
    - Trainer : "Maestra Lyra", a practitioner of wind and light magic.
    - Class Mechanic : Mystics control wind and light-based abilities. As they progress, they can enhance spells like "Gale Fan" and "Light Shield", with increased 
radius, additional buffs, or debuffs, and stronger elemental effects.

		Class-Specific Mechanics :

	1. Assassins / Rogues : Use "Combo Points" to increase the damage of special abilities, with combos stacking from stealth-based attacks to unleash devastating 
finishers.
	2. Vampires : Use "Blood Orbs" to empower offensive or defensive skills. Each attack generates "Blood Orbs" that can be used for additional damage or healing 
over time.
	3. Werewolves : Utilize "Blood Orbs" for enhanced defense and reaction time, focusing on counterattacks and damage reduction during key moments in battle.

---

	This revision aligns the classes to ensure that the same core mechanics apply across races, while differentiating them through unique names and lore. Players of
the same class (like Assassins and Rogues, or Warrior Knight and Warrior Arahan) will still use the same skill progression mechanics, making it easy to specialize 
within a familiar structure.