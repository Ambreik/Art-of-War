	2. Archivist Jaron Lightwell  
- Appearance : Jaron is a middle-aged man with neatly combed salt-and-pepper hair and wire-rimmed glasses that give him a scholarly look. Dressed in simple yet 
elegant robes, he carries an aura of quiet reverence and dedication to his work. He’s often seen with a leather-bound notebook in hand, ready to jot down 
discoveries or observations at any moment.

- Personality : Jaron is meticulous, passionate, and deeply absorbed in the preservation of the city’s history. He has an insatiable curiosity about the past, which
has led him to amass a vast knowledge of Capital City’s heroes, legends, and ancient relics. Though he can be somewhat distracted and lost in thought, he becomes 
animated and enthusiastic when discussing historical tales and the artifacts in his care.

- Role : As the temple’s historian and archivist, Jaron is responsible for safeguarding the stories of Capital City’s past. He catalogues records, stories, and 
relics linked to the heroes who shaped the city’s history. His deep understanding of relics and their origins has made him an invaluable resource for adventurers 
seeking to understand the significance of the treasures they uncover. Jaron views these relics as more than simple artifacts; he considers them pieces of a larger 
narrative that defines the city’s spirit.

- Special Interaction : Jaron does not offer quests, but players can approach him to learn about the historical significance of certain relics they discover. When 
presented with a relic, he shares its backstory, recounting legendary tales tied to each item and connecting players with Capital City’s storied past. He often 
refers to specific heroes or events to provide context, enriching the player's journey with layers of lore and symbolism. Jaron also encourages players to look at 
each relic not merely as an object but as a link to the city’s heritage.

		Background Information on Relics:

	1. "The Shield of Valor"  
	- Description : An ornately crafted shield adorned with silver and brass engravings of a fierce lion, the emblem of courage.  
	- Lore : Jaron recounts that this shield once belonged to Sir Garrick, a fearless knight who defended Capital City against a marauding band of invaders single-
handedly. His heroic stand inspired the city’s people, and it’s said that those who wield the shield feel a surge of unbreakable resolve and bravery.  
	- Significance : According to Jaron, the shield represents the spirit of selfless defense and is a symbol of unity in the city’s darkest hour.

	2. "The Amulet of Tranquil Waters"  
	- Description : A delicate pendant with a teardrop-shaped sapphire set in gold, symbolizing calmness and clarity.  
	- Lore : Jaron explains that the amulet was once worn by Sister Aeliana, a healer and peacemaker known for her skill in calming tensions during times of civil 
unrest. The sapphire is said to reflect the wearer’s inner peace, channeling her wisdom to resolve conflicts.  
	- Significance : Jaron describes this amulet as a reminder of the city’s need for harmony and resilience and tells players it’s particularly sought after by 
those facing moral decisions.

	3. "The Sword of the Phoenix"  
	- Description : A legendary blade with a hilt shaped like a phoenix’s wings, forged from rare metals that give it a glint of crimson in sunlight.  
	- Lore : This sword, Jaron tells players, belonged to Commander Aiden, who was believed to rise from the ashes after being defeated, inspiring hope in those 
around him. His final battle saved Capital City from an ancient foe, and his courage is thought to be embedded in the blade itself.  
	- Significance : Jaron often recites that this relic is a symbol of rebirth and unwavering commitment, and is revered as an emblem of the city’s survival 
against overwhelming odds.

	4. "The Staff of the Starlit Sage"  
	- Description : An elegant staff adorned with crystalized stars along its length, said to shimmer when moonlight touches it.  
	- Lore : Jaron shares that this staff was wielded by Lyra the Seer, a wise mage who could see into the future and prevent many threats to Capital City. Her 
insights guided the city through turbulent times, and it’s believed the staff still holds traces of her foresight.  
	- Significance : Jaron reveres this relic as a guidepost for wisdom and vigilance, encouraging adventurers to consider the city’s future with each decision they
make.


