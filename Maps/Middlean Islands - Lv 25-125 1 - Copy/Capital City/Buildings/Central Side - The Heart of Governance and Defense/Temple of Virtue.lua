		Temple of Virtue Chain-Quest Overview

- Quest 1 : "The Missing Memento"  
	Description : Aric tasks the player with finding a relic that has recently gone missing from the temple’s collection. Rumor has it that the item was seen at the
	city’s edge, near an old hideout.  
	Reward : Gold, minor relic, and temple access privileges.

- Quest 2 : "Shattered Legacy"  
	Description : Aric asks the player to seek out pieces of a broken shield, once wielded by a revered paladin, scattered across Capital City. Each piece brings 
	new insight into the paladin’s valor and sacrifice.  
	Reward : Gold, relic piece, and a temporary Strength buff for the final quest.

- Quest 3 : "The Ancestral Blade"  
	Description : The ultimate quest involves recovering Aric’s ancestral sword, hidden in a treacherous location outside the city. Players face a powerful foe 
	guarding it and must prove their strength and resilience.  
	Reward : Unique title ("Defender of Virtue"), a replica of Aric’s ancestral weapon, and a permanent +1 to the player’s Luck attribute.

	The "Temple of Virtue" provides a unique, lore-rich experience, allowing players to dive deep into Capital City’s heritage while testing their abilities through
challenging quests. Each interaction and quest reinforces the player’s bond with the city and its legendary defenders, making their journey more meaningful.