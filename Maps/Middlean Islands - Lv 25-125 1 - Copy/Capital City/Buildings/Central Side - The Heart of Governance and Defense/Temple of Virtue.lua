	3. Paladin Aric Stormvale 
- Appearance : Aric is a formidable presence, with a tall, muscular build and a battle-hardened look. His armor, marked by years of wear, is emblazoned with his 
family’s crest—a golden hawk over a field of blue—symbolizing their dedication to protecting Capital City. His piercing blue eyes and a streak of silver in his dark
hair reflect both his lineage and the weight of his role as the last of his line.

- Personality : Aric is known for his unwavering loyalty and quiet strength. Though he often appears serious and solemn, there is a deep sense of honor in his every
word and action. He carries the pride and burden of his heritage with dignity and speaks sparingly, letting his actions convey his dedication to the temple and the 
city. Aric is respectful toward those who show courage and resolve, often taking time to share wisdom about the values of bravery and sacrifice.

- Role : As the last descendant of one of Capital City’s legendary heroes, Aric’s duty at the Temple of Virtue is both a personal vow and a tribute to his family’s 
legacy. He is stationed there to protect the city’s sacred relics and ensure that their significance is preserved. Known for his combat prowess and unyielding 
spirit, he embodies the ideals of valor and integrity that his ancestors defended. Aric is committed to passing on his family’s values and teachings, hoping that 
others will follow in their footsteps.

- Special Interaction : Aric offers a challenging journey to players, connecting them with the city’s heritage and guiding them through the history of the city’s 
most revered heroes. His chain of tasks is designed to build the player’s understanding of true courage and honor, uncovering details about Capital City’s defenders
that few know. Eventually, players who persevere will be entrusted with Aric’s ancestral weapon—a powerful relic that serves as both a symbol of his family’s valor 
and a testament to the player's own dedication.

