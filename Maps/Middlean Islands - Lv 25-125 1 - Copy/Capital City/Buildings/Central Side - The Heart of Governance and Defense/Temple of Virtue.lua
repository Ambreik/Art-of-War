			Temple of Virtue

	The "Temple of Virtue" stands as a sacred monument to "Capital City"'s defenders and those who sacrificed for its safety and prosperity. Its impressive 
architecture and solemn atmosphere draw citizens and adventurers alike, seeking strength, blessings, and connection to the city's rich history. Inside the temple, 
statues and relics honor fallen heroes, and the place hums with an aura of reverence. While it is a haven of peace, it also holds mysteries and treasures from ages 
past, accessible only to the worthy.

	Role and Features of the Temple of Virtue

- Blessings of Virtue : The temple offers blessings to players, granting them temporary boosts to attributes like "Luck", "Strength", or "Wisdom". These blessings
are beneficial for adventurers preparing to face difficult challenges, enhancing their odds in battles and quests.
- Chain-Quest Series : A unique chain-quest series is available from one of the temple’s wise figures. This series focuses on retrieving ancient relics and 
exploring the heroic lore associated with Capital City’s past. Completing each stage in the chain reveals more about the city’s protectors, culminating in 
significant rewards and a unique title of honor.
- Relic Recovery Quests : Players can undertake quests to recover lost relics scattered throughout the city and its outskirts. Each relic represents a piece of the 
city’s history and has its own story, making these quests an engaging dive into Capital City’s legacy.

		Notable NPCs in the Temple of Virtue

	1. High Priestess Elara Grace  
- Appearance : Elara is a graceful figure with silver hair cascading over her shoulders, and a serene expression that radiates warmth and wisdom. She wears a 
flowing, white-and-gold robe adorned with symbols representing virtues such as courage, humility, and honor. Her eyes are a piercing blue, reflecting a depth of 
experience and a calm insight into those she encounters.
- Personality : Elara is known for her unwavering kindness and wisdom. She embodies patience and empathy, always ready to listen to visitors’ concerns. Her calming 
aura and gentle demeanor make those in her presence feel at ease, and she is regarded as a moral compass for the city’s citizens and adventurers alike. Though she 
seldom intervenes directly in conflicts, her words and guidance are respected by all who seek her wisdom.
- Role : As the primary guardian of the Temple of Virtue, Elara is dedicated to honoring the memories of the city’s protectors and fallen heroes. She ensures that 
the temple remains a sanctuary for those who seek strength, guidance, or comfort. Elara provides blessings to adventurers preparing to face the city’s dangers, 
empowering them with temporary boosts in luck, strength, or resolve. Her blessings are subtle yet powerful, aiding those who are about to embark on challenging 
tasks or quests.
- Special Interaction : While Elara does not provide quests, she is known for sharing meaningful insights with those who come to her for counsel. Her words often 
carry hints of Capital City’s hidden histories or suggest solutions to moral dilemmas that adventurers face. Her wisdom is rumored to be vast, and some believe she 
possesses knowledge of secrets that go beyond the city’s walls. Elara’s presence provides a grounding force for the Temple of Virtue, making her one of the most 
respected figures in the city.

