			Dockworkers' Guild - A Building Near Sailor’s Tavern
			
	The "Dockworkers' Guild*" is a prominent, functional building located just outside "Sailor’s Tavern", in the bustling dock area of "Capital City". It serves as 
the central hub for the city’s dock-related activities, from loading ships to managing cargo, all while maintaining a deep connection to the island's underworld 
dealings. 	
	The Guild’s building is a large, utilitarian structure made from weathered timber, ropes, and iron reinforcements. It stands as a symbol of both the raw power of 
the working class and the undercurrents of smuggling and secrecy that flow through the city’s docks. Players who wish to gain favor with the Guild, take on work 
orders, or learn about hidden opportunities will find themselves visiting this building frequently.

		Overview of the Dockworkers’ Guild Building

	The "Dockworkers’ Guild Building" is a busy, industrious place, with the noise of loading and unloading cargo filling the air. It’s a space where workers gather
to relax after long days on the docks, and it’s where the leadership makes important decisions.
- "Exterior" : The building’s exterior is a patchwork of wooden planks and iron supports, with large doors that open up to the waterfront. Fishermen, dockhands, and 
traders are often seen coming in and out. The scent of saltwater and fish hangs in the air, and the sound of busy work is always present.
- "Interior" : Inside the guild building, the air is thick with the smell of old wood, wet ropes, and seawater. Rough-hewn tables line the walls where sailors and 
dockhands come together, often discussing contracts, negotiations, or trade routes. There are also dark corners where more secretive conversations occur. The main 
room features a large table with a map of the island and the surrounding seas, where guild members plan upcoming shipments and missions.

		Key Areas of the Dockworkers’ Guild

	A. "Guild Leader’s Office"
- Description : The "Guildmaster’s Office" is located at the back of the building, elevated on a small platform. "Burlin Tidespanner", the grizzled guildmaster, 
oversees operations from here. The room is filled with maps, old tomes of sea navigation, and various trade documents. It's the place where high-level decisions are 
made, and where important quests and tasks are assigned to players.
- Key Interactions :
	- "Guildmaster Burlin Tidespanner" : Players can approach "Burlin" for special quests, high-level tasks, and to gain access to privileged information about 
	dock-related activities and the underworld. "Burlin" is tough but fair, and his insight into the underworld makes him an invaluable ally for those looking to 
	dive 	into the city's hidden operations.

