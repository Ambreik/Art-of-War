	D. Guild Hall (Common Room)

- Description : The "Guild Hall" is a lively, bustling space within the "Dockworkers' Guild" where workers come to unwind after a hard day’s labor. It is the heart 
of the social and recreational activities within the guild. The room is often filled with the sound of hearty laughter, clinking mugs, the tunes of a local bard, and 
occasional raucous arguments about the day's work or the latest gossip. The space is dimly lit, with rough-hewn wooden tables and benches. It’s a place where patrons 
can bond, engage in lighthearted competition, and network with others within the guild or even outside it.
	
	Key Interactions :

	1. "Drinking Games"
- Description : The "Guild Hall" hosts various drinking contests where players can test their stamina, reflexes, and balance against the skilled dockworkers. These games are a fun and challenging way to engage with the guild members, and successful players earn buffs, gold, or even special items that improve their gameplay.
   
   - Games :
     - "Stamina Shot Contest" : Players compete in a series of rapid drinking contests, with each successful round boosting their stamina but decreasing their balance. After a certain number of drinks, players must pass a dexterity check to keep their footing.
       - Game Mechanics : After each drink, the screen wobbles, and the player must use the mouse or controller to keep the character from swaying too much. The longer the player manages to stay upright, the better the reward.
       - Reward : A temporary buff to stamina or health regeneration.
     - "Balance Test" : After drinking a few shots, players must navigate a wobbly plank placed over a pit of water or another hazardous area. The challenge increases in difficulty with each level of drinks consumed.
       - Game Mechanics : Players are presented with a moving screen effect that simulates dizziness. Players must keep their character balanced by pressing specific keys or using the analog stick to maintain stability.
       - Reward : A rare item or a permanent increase to movement speed.
     - "Shot-for-Shot" : A one-on-one drinking contest with a guild member, where each player takes turns drinking until one passes out. The player who stays conscious the longest wins the contest.
       - Game Mechanics : Players must engage in quick time events to successfully drink and react. Failure causes the character to stumble, reducing their chances of winning.
       - Reward : A unique guild item or an item from the **Smuggling Cellar**.

	2. "Dice Games"
- Description : The "Guild Hall" also hosts games of chance, primarily dice or card games, where players can test their luck and socialize with guild members. These games are easy to pick up but can be addictive, with players wagering gold or other valuable items on the outcome.
   
   - Games :
	- "Pirate’s Dice" : A game where players roll five dice and try to make the best combination. It’s a simple, fast-paced game that revolves around chance and 
	luck.
		- Game Mechanics : Players take turns rolling dice, trying to achieve specific combinations like a "full house" or "three of a kind." Higher combinations win 
		larger pots.
		- Reward : Gold, items, or information about secret locations or underworld dealings.
	- "Cards Against Luck" : A popular card game in the **Guild Hall**, similar to poker but with a few twists. Players must match cards to win, but the game 
	includes random "wild" cards that alter the entire round.
		- Game Mechanics : Players try to form combinations, but wild cards introduce unexpected changes to the flow of the game. Bluffing and strategy are 
		important, but luck plays a big role.
		   - Reward : Special quests, sometimes linked to the **Smuggling Cellar**, or large sums of gold.
	- "Dice of Fate" : A high-stakes dice game where players gamble gold or valuable items. Players roll dice with varying sides, each side representing a different 
	outcome (such as "double gold" or "lose everything").
		- Game Mechanics : Each roll of the dice has potential for a massive win or loss. A "lucky" player might roll a jackpot, while others may lose all their 
		winnings.
		- Reward : High-risk, high-reward loot or new NPC allies depending on performance.

