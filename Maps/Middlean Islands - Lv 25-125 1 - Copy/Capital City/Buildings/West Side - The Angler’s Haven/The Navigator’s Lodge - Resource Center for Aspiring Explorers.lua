	"Mapmaker Ellis" is a spirited and ambitious cartographer, driven by her dream of mapping the uncharted corners of the world. Though young, her expertise and 
dedication have made her a valuable member of the Navigator’s Lodge. Ellis believes that every uncharted territory holds a new story and an opportunity to expand 
the knowledge of her fellow explorers.

- Role at the Lodge : Ellis manages and updates maps, sharing her most recent discoveries and guiding players on quests that involve advanced cartography and exploration.
- Appearance : Ellis has a vibrant, enthusiastic demeanor with short, wind-tossed auburn hair, bright green eyes, and a practical, leather-bound outfit covered with small pockets holding pencils, compasses, and parchment. Her hands are always ink-stained, a testament to her endless cartography work.

		Notable Quests Offered by Mapmaker Ellis

	1. Quest: "Mapping the Unknown Shores"
	- Objective : Ellis provides players with an incomplete map of an area that she’s recently started charting. The task is to visit these locations, observe key 
landmarks, and return with detailed reports so she can fill in the missing sections.
	- Details : Players must travel to specified points, mark down landmarks, and avoid or handle hostile creatures along the way. Each point reveals lore tidbits 
that Ellis will later compile into a completed map.
	- Reward : A rare Cartographer’s Satchel that improves the accuracy of map markers and grants a slight bonus to exploration Reputation Points, along with 
Reputation with the Navigator’s Lodge.

	2. Quest: "Sketching the Tidal Maze"
	- Objective : Ellis has found an area of waters filled with ever-shifting sandbars and small islets that change with the tides. She tasks players with charting 
a safe path through this treacherous maze.
	- Details : Players need to visit specific locations in the tidal maze, map the changes, and solve puzzles involving tide manipulation to navigate safely. This 
requires a blend of navigation skill and environmental awareness.
	- Reward : “Tidewatcher’s Lens,” a special tool that enhances visibility in murky waters, making underwater exploration easier, plus Reputation Points and 
Ellis’ Seal, a token showing her approval and respect.

	3. Quest: "The Isles of the Lost"
	- Objective : Ellis has a fragment of an ancient map that shows small, isolated isles rumored to hold artifacts from ancient civilizations. She sends players on
a quest to locate these isles and gather information.
	- Details : Players must follow cryptic clues and sail across distant waters, marking each isle’s unique features and any unusual finds. These isles have hidden
secrets, and players might uncover hidden treasure or lore.
	- Reward : An “Ancient Cartographer’s Charm” that boosts the player's chances of finding rare items on remote islands, along with substantial Reputation Points 
and access to new cartography skills in future quests.

	4. Quest: "Daily Cartographer’s Reports"
	- Objective : Each day, Ellis requests players to assist in updating minor sections of her maps by surveying nearby areas, reporting any changes or unusual 
occurrences.
	- Reward : Small daily Reputation Points, map-enhancing materials like parchment and ink, and a chance to earn a rare “Explorer’s Compass” which slightly 
increases map reading accuracy for a limited duration.

	5. Special Event: "Ellis’s Expedition Day"
	- Objective : Once a month, Ellis leads a group expedition to a mysterious location that she’s recently discovered. Players join her to uncover rare resources 
and lore unique to this area.
	- Reward : High Reputation Points, unique event-only items like “Expeditioner’s Map Case” for storing maps, and temporary buffs that enhance exploration skills 
during the event.

	"Mapmaker Ellis" brings a passion for discovery and a focus on precision to her quests, making her a guide to both the known and unknown. Her challenges are 
immersive and encourage players to look beyond the surface, enhancing their experience in the world of exploration and cartography.

