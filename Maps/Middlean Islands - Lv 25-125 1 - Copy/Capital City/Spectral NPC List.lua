7. Lookout Tarl - The Silent Watcher
   - Role : Informant and warning giver
   - Description : A tall, silent specter who stands vigil over the docks, Tarl was once the ship’s lookout. His presence is calming but intimidating, as he stares out to sea with eyes that seem to pierce through the mists.
   - Interactions : Tarl does not speak but interacts with players by pointing out dangers, especially warning about areas with high spectral activity or nearby hostile NPCs. High-REP players can “sense” his warnings about ambushes or cursed zones.
   - Unique Item : "Lookout’s Charm" – A charm that grants players temporary night-vision while exploring the docks,helping them spot potential ambushes or dangers.
   
		"Lookout Tarl - The Silent Watcher" is an enigmatic figure among the spectral crew, shrouded in quiet vigilance. His silence adds an eerie quality to his 
interactions, relying on gestures and subtle, otherworldly cues to communicate with players. Despite his muteness, "Tarl's" actions convey vital guidance, 
particularly regarding threats around the docks.

		Interactions with "Lookout Tarl" :

	1. "Silent Gestures"  
	Tarl uses subtle hand signals or points towards areas of interest or danger. A wave of his hand, a nod, or an outstretched finger can direct players toward key 
locations or alert them to areas with heavy spectral presence. This non-verbal interaction requires players to observe and interpret his movements, fostering 
immersion.

	2. "Aura of Warning"  
	Players with high "REP" can “sense” "Tarl’s" warnings as an aura effect. When nearing cursed zones or areas known for frequent spectral ambushes, a faint, 
pulsing blue light may surround "Tarl", alerting players to proceed with caution. This effect subtly warns players, enhancing "Tarl's" mystique and reinforcing his 
protective presence.

	3. "Phantom Whisper Mechanic"  
	For players he trusts, "Tarl's" warnings occasionally manifest as faint whispers in the ambient soundscape, hinting at nearby threats. For instance, when 
nearing hostile "NPCs", players might hear distant phrases like, “Not safe... watch closely...” which vanish as quickly as they arrive, adding suspense to the 
encounters.

	4. "High-REP Perception Ability: Tarl’s Sight"  
	Players with high "REP "gain access to "Tarl’s Sight" - a passive ability allowing them to detect cursed zones and ambush points more easily. When active, 
spectral shimmers appear over specific areas at night, warning players of heightened danger. This ability helps players avoid unnecessary confrontations and 
navigate the docks more strategically.

	5. "Danger Marks"  
	Occasionally, "Tarl" leaves spectral markers along pathways he deems dangerous or volatile. Players who follow these marks may find safer routes through the 
docks or even shortcuts bypassing hostile spirits. The spectral marks fade with daylight, making them visible only during nighttime, emphasizing "Tarl’s" nocturnal 
role as a silent guardian.

	"Tarl’s" mysterious approach to communication creates an immersive experience, requiring players to be attentive and cautious. His gestures, whispers, and auras
become lifelines for navigating the spectral-infested docks, lending depth to his role as "The Silent Watcher".

