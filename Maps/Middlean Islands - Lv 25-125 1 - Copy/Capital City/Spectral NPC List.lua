			Spectral NPC List 

1. Captain Marek - The Restless
   - Role : Leader of the spectral crew, quest giver
   - Description : Captain Marek is the former commander of the Ghost Ship crew, with a sharp, weathered face and a commanding presence. Wearing a tattered, ghostly tricorn hat and spectral cutlass, he is bound to the docks, eternally seeking redemption for his crew’s misdeeds.
   - *Interactions : Offers lore about the cursed Ghost Ship and the crew’s tragic tale. He grants certain quests like *Echoes of the Fallen* and *Secrets of the Tide* that allow players to help him lift the curse on his crew.
   - Unique Item : "Captain’s Locket" (Lore Item) – A mysterious pendant with faintly glowing runes, which provides backstory on Marek's last voyage when examined under moonlight.

		"Story"
	The "cursed Ghost Ship" and its spectral crew are well-known in the lore of the capital, haunting the docks by moonlight, tied by an ancient curse. Long ago,
"Captain Marek" and his crew were seasoned sailors known for their unmatched skills on the open seas and their relentless pursuit of treasure. Their fate changed when they discovered a strange artifact—a shimmering, otherworldly compass—during one of their expeditions. It was said to be crafted by a forgotten sea deity and 
held the power to guide anyone to whatever they desired most.
	Obsessed with finding the legendary "Lost Isle", a mythical place said to contain untold riches, "Captain Marek" used the compass to guide them. But what 
"Marek" didn’t know was that the compass was cursed. It didn’t simply guide them to riches; it slowly warped their minds, filling them with paranoia, distrust, and 
greed. The once-loyal crew began to turn on each other, whispering of betrayal and hidden secrets.
	As they approached the "Lost Isle", a fierce storm swept through, tearing apart their sails and splintering the ship. In their final moments, the crew realized 
the compass had led them to ruin, its curse claiming their lives and binding them to the ship forever. Now, "Marek" and his crew are doomed to haunt the docks, 
seeking redemption for their betrayal of each other and longing to break free of the curse.

		Quests 
		
	Quest Title : "Echoes of the Fallen"
	- Quest Level : 75
	- Objective : Gather scattered relics of the crew and place them at the Altar of Tides.
	- Description : Captain Marek reveals that remnants of his crew’s belongings were lost to the waters during the ship’s final voyage. Only by reclaiming these relics and offering them at the Altar of Tides can the crew's spirits find a chance for peace. Players must venture into the mist-laden docks, past restless spirits, and recover relics scattered around the area.
	- Mechanics :
		- Spectral Relic Collection : Players must locate and gather five spectral relics that appear only at night near the Ghost Ship Docks. Each relic represents a different crew member and will randomly spawn at different points in the docks.
		- Spectral Confrontation : As players collect these items, spectral versions of the crew members appear, each presenting a unique challenge: one may create waves of cold, others might summon illusions or lock players in a fog.
		- Altar of Tides Interaction : After obtaining all relics, players return to the Ghost Ship and place the items at the Altar of Tides, initiating a ritual where the spirits of the fallen crew briefly appear, granting the player a blessing for their bravery.
	- Reward : "Spectral Compass Fragment" (a piece of the original cursed compass) and a boost in reputation with "Captain Marek".

	Quest Title : "Secrets of the Tide"
	- Quest Level : 80
	- Objective : Uncover the Lost Isle’s secret coordinates by deciphering fragments of a cursed map and facing down the lingering wrath of the sea deity.
	- Description : Captain Marek believes the Lost Isle holds the key to breaking the curse on his crew, but he lacks the map’s final fragments. To lift the curse, players must recover these fragments from spectral guardians across the docks and piece together the coordinates. However, a sea deity, angered by their trespass, has sent vengeful spirits to protect the island’s secrets.
	- Mechanics :
		- Map Fragment Recovery : Players must explore the dock’s hidden areas, finding map fragments guarded by spectral monsters tied to the sea deity. Each guardian requires a different strategy to defeat, such as resisting tidal waves or dispelling illusions.
		- Puzzle Solving : After collecting all fragments, players solve a riddle to reveal the coordinates to the Lost Isle.
		- Spectral Confrontation : Upon solving the puzzle, a massive spectral sea serpent appears as a final trial to test the player’s resolve. Players must evade its attacks and use special abilities to weaken it.
	- Reward : "Cursebreaker’s Amulet" (an item that grants brief immunity to curses during spectral encounters) and a significant reputation boost with 
	"Captain Marek".

	These quests allow players to help "Captain Marek" and his crew earn a chance at redemption by righting the wrongs of their past, delving into the eerie 
mysteries of the docks, and facing the wrath of supernatural forces bound to the cursed compass.

