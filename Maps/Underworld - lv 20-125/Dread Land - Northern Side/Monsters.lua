			Monsters and Creatures for Dread Land - Northern Side
1. **Undead Warriors**
   - **Description:** These skeletal soldiers are remnants of the fallen heroes of the Underworld. Clad in rusted armor and wielding tattered weapons, they seek vengeance on intruders.
   - **Mechanics:** 
     - **Abilities:** "Pierce Attack" that ignores armor, and can inflict a **Bleed** effect on players.
     - **Summon:** They can summon additional lesser undead when defeated.

2. **Wraiths**
   - **Description:** Ethereal spirits of the dead that float through the air, wreathed in dark shadows. They embody the sorrow of lost souls and can drain the life force from their victims.
   - **Mechanics:**
     - **Abilities:** Inflict **Slowdown** and a chance to **Stun** players upon contact. They may also leave a lingering "Fear" debuff.
     - **Special Attack:** A wailing cry that can weaken nearby players’ resolve, reducing damage dealt for a short duration.

3. **Manticore**
   - **Description:** A fearsome beast with the body of a lion, the tail of a scorpion, and the wings of a bat. It prowls the more desolate parts of the Dread Land, hunting for prey.
   - **Mechanics:**
     - **Abilities:** Can inflict **Poison** with its sting, causing damage over time. Its claws deal substantial damage and can knock back players.
     - **Flight:** Occasionally takes to the air to swoop down on unsuspecting players, dealing area damage.

4. **Siren**
   - **Description:** Lurking near the River of Sorrow, these enchanting creatures lure adventurers with their haunting songs, drawing them into the depths.
   - **Mechanics:**
     - **Abilities:** Inflict **Charm** on players who fail a willpower check, temporarily causing them to attack their allies.
     - **Water Manipulation:** Can create waves or whirlpools that affect player movement and cause **Drowning** effects.

5. **Hollows’ Emissaries**
   - **Description:** Twisted humanoid figures composed of shadows and decay, these beings serve as the guardians of the Forest of Hollows.
   - **Mechanics:**
     - **Abilities:** Inflict **Slowdown** and possess shadow teleportation skills, allowing them to dodge attacks and reposition quickly.
     - **Illusions:** Can create illusory duplicates to confuse players during combat.

6. **Banshee**
   - **Description:** A mournful spirit that roams the Field of the Dead, her cries echoing through the air as she seeks to avenge her own demise.
   - **Mechanics:**
     - **Abilities:** Her scream can cause **Fear** or **Stun** effects on players, and she can drain health with her spectral touch.
     - **Summoning:** Can call forth minor undead to aid her in battle.

7. **Revenants**
   - **Description:** These vengeful spirits are warriors who refuse to rest until their grievances are avenged. They manifest as fierce combatants.
   - **Mechanics:**
     - **Abilities:** Inflict significant physical damage with a chance to deal double damage when under half health.
     - **Revenge Mechanic:** Upon being defeated, they can explode, dealing area damage and leaving behind a lingering curse that affects nearby players.

8. **Fallen Treants**
   - **Description:** Corrupted trees that have turned malevolent, these entities are now protectors of the Forest of Hollows, attacking intruders with their powerful limbs.
   - **Mechanics:**
     - **Abilities:** Can root players in place with their branches, inflicting **Slowdown** and dealing damage over time.
     - **Regeneration:** Slowly regain health when not in combat, forcing players to defeat them quickly.

### **Boss Encounters**

- **Guardian of the Dread Land: The Lich King**
  - **Description:** A powerful undead sorcerer who commands the forces of the Underworld. He resides in a hidden fortress within the Field of the Dead.
  - **Mechanics:**
    - **Phases:** Transitions through different phases of combat, summoning undead minions and casting devastating spells.
    - **Environmental Hazards:** Can summon walls of darkness that restrict player movement and visibility.

- **Queen of the Sirens**
  - **Description:** The leader of the Sirens, she possesses unparalleled control over water and sound, making her a formidable foe by the River of Sorrow.
  - **Mechanics:**
    - **Song of the Abyss:** A powerful song that can incapacitate players, pulling them into the water if they fail to resist.
    - **Minion Summon:** Regularly calls upon lesser Sirens to distract and hinder players during the fight.

