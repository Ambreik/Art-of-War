			Monsters and Creatures of Sangaria - Western Side

1. **Wraith of the Hollow Vale**
   - **Description:** A translucent, ethereal figure that drifts silently through the shadows. Its hollow eyes glow with an eerie light, and it is surrounded by a chilling breeze.
   - **Abilities:** 
     - **Soul Siphon:** Drains health from nearby players, transferring it to itself.
     - **Shadow Veil:** Can become invisible for a short duration, making it difficult to track.

2. **Bloodthorn Briar**
   - **Description:** A massive, thorn-covered vine creature that lurks in the underbrush. Its bark-like skin blends seamlessly with the surrounding foliage.
   - **Abilities:**
     - **Entangling Roots:** Can ensnare players with its vines, immobilizing them temporarily.
     - **Bleed Burst:** When damaged, it releases a cloud of thorns that can cause bleeding effects to nearby players.

3. **Echoing Revenant**
   - **Description:** A ghostly figure that resembles a lost warrior, clad in tattered armor. Its voice echoes mournfully as it wanders the landscape, searching for something it lost.
   - **Abilities:**
     - **Mournful Howl:** A cry that can stun players within a certain radius.
     - **Spirit Strike:** A melee attack that deals both physical and spectral damage.

4. **Hollow Stalker**
   - **Description:** A feline-like creature with ghostly fur and glowing eyes. It moves with unmatched stealth, often seen as just a shadow out of the corner of one's eye.
   - **Abilities:**
     - **Predator’s Pounce:** Can leap from the shadows to deliver a devastating attack.
     - **Cloak of Shadows:** Gains a temporary boost to evasion, making it harder to hit.

5. **Spiritbinder**
   - **Description:** A humanoid figure adorned with tattered robes, wielding a staff that channels dark magic. They are often found in the Whispering Grove, invoking ancient rituals.
   - **Abilities:**
     - **Soulbind:** Links their health to a player for a short time, causing damage dealt to them to be transferred to the player.
     - **Shadow Grasp:** Summons shadowy hands that immobilize players, dealing damage over time.

6. **Ravenous Harpy**
   - **Description:** A monstrous bird-woman with sharp talons and a sinister screech. These creatures lure travelers with their songs before attacking.
   - **Abilities:**
     - **Luring Song:** Can charm players, causing them to move toward the harpy, leaving them vulnerable.
     - **Talons of Death:** A swift, devastating melee attack that can inflict bleeding.

7. **Phantom Manticore**
   - **Description:** A spectral variant of the traditional manticore, with a ghostly body and glowing eyes. Its tail ends in a stinger that glimmers ominously.
   - **Abilities:**
     - **Soul Stinger:** Attacks with its tail, dealing both physical and spectral damage that can cause a **Slow** effect.
     - **Phantom Charge:** Can charge at players with great speed, dealing damage and knocking them back.

8. **Wandering Shade**
   - **Description:** A shadowy figure that appears lost, wandering the landscape. While they seem harmless, they can become aggressive if approached.
   - **Abilities:**
     - **Fearful Presence:** Lowers the courage of players nearby, causing a chance to miss attacks.
     - **Dark Embrace:** Can envelop a player in shadow, dealing damage over time while draining their stamina.

### Summary
These creatures add depth and diversity to **Sangaria - Western Side**, enhancing the themes of darkness, sorrow, and spectral presence. They provide players with a mix of challenges and encounters, contributing to the rich lore and atmosphere of the area. Feel free to modify or expand upon any of these concepts!