			7 Bullets of Horror

1. **Mortar**  
   - **Description**: A massive creature resembling a black dragon with obsidian scales that shimmer ominously in the dark. Its eyes glow with an eerie red light, and it can unleash torrents of fiery breath.  
   - **Effects**: Causes **Fear** to all who gaze upon it, inducing a chilling dread that hampers the will to fight.

2. **Bosniar**  
   - **Description**: A hulking humanoid boss with the head of a wolf. His body is covered in coarse, dark fur, and he dons a helmet adorned with jagged spikes. Armed with two massive axes, he embodies the essence of a berserker.  
   - **Effects**: Inflicts **Slowdown** on his targets with each heavy swing of his axes, making them sluggish in battle.

3. **Dread Knight**  
   - **Description**: A warrior clad in dark red and black armor that appears to be forged from molten lava. The armor constantly drips with flames, leaving a trail of heat in its wake.  
   - **Effects**: Afflicts enemies with **Burn**, causing them to suffer damage over time.

4. **Shadewitch**  
   - **Description**: A powerful witch shrouded in dark robes, with a hood that obscures her face. She wields shadowy magic and can manipulate the darkness around her.  
   - **Effects**: Casts **Confusion** on enemies, causing disorientation and making it hard for them to focus.

5. **Skeleton Horseman**  
   - **Description**: A grim figure riding a skeletal horse, both adorned with tattered remnants of what was once regal armor. The horse gallops with an unnatural speed, leaving behind a trail of ghostly mist.  
   - **Effects**: Causes **Chill**, slowing enemies and instilling a sense of dread with every approach.

6. **Phoenix of Fire and Shadows**  
   - **Description**: A majestic creature with flaming feathers interwoven with shadowy tendrils. It can soar through the air, leaving behind a plume of smoke and embers.  
   - **Effects**: Inflicts **Blindness**, obscuring vision and hindering enemy attacks.

7. **Gloombringer**  
   - **Description**: A monstrous entity enveloped in shadows, with elongated limbs and a constantly shifting form. Its presence drains light from the surroundings.  
   - **Effects**: Spreads **Debilitation**, weakening foes and diminishing their ability to fight effectively.

This classification highlights their strengths and gives insight into their unique effects without delving into their specific skills or abilities yet.