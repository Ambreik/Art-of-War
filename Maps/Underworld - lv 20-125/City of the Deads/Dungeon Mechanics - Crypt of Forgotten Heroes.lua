			Dungeon Mechanics: Crypt of Forgotten Heroes

The **Crypt of Forgotten Heroes** is designed with intricate mechanics that challenge players on multiple levels, encouraging teamwork, strategy, and adaptability. Each floor presents its unique obstacles, traps, and puzzles.

#### Floor Mechanics Overview

1. **Floor One: Hall of Valor**  
   - **Mechanics**:  
     - **Statue Awakening**: As players explore, they can interact with the central statue of the hero. If they approach without due reverence (failing a check), the statue awakens as a powerful guardian, forcing players into combat.
     - **Echoing Whispers**: Players must listen carefully to the echoes in the chamber. Solving audio-based puzzles can reveal hidden passages or treasures, while failing may summon wraiths.
  
2. **Floor Two: Chamber of Echoes**  
   - **Mechanics**:  
     - **Rune Activation**: Players must activate runes in a specific order based on clues given by the whispers. Completing the sequence correctly opens a locked door to the next area, while failure summons spectral guardians.
     - **Memory Phantoms**: Random phantoms representing the past lives of heroes appear throughout the room. Players can choose to engage for loot or evade them to conserve resources.
  
3. **Floor Three: Tomb of the Fallen**  
   - **Mechanics**:  
     - **Sarcophagus Guardian**: Players can choose to open a sarcophagus to gain a buff or items, but doing so may awaken its guardian. The guardian will attack and can be defeated for unique rewards.
     - **Honor System**: Players must perform actions to honor the fallen (like offering items or engaging in combat against undead). Each action accumulates points that can grant buffs for future encounters or challenges.
  
4. **Floor Four: Sanctum of Shadows**  
   - **Mechanics**:  
     - **Dark Magic Pillars**: The chamber contains pillars that pulse with dark energy. Players must destroy or disable these pillars to weaken the shadowy boss that resides within. If a player is too close when a pillar collapses, they suffer significant damage.
     - **Shadow Manifestation**: Players must face illusions of their past mistakes, engaging in combat against manifestations of their fears that inflict debuffs based on their previous choices in the dungeon.
  
5. **Floor Five: Hall of Trials**  
   - **Mechanics**:  
     - **Challenge Rooms**: Each room presents a different trial—combat, puzzle-solving, or resource management. Players must complete these trials to earn keys that unlock the Boss Chamber.
     - **Guardian Spirits**: Players may encounter guardian spirits that can provide hints or engage in combat. Choosing to fight them results in better loot, while opting for diplomacy may yield other rewards.

### Boss Chamber: The Ancestor Wraith

**Accessing the Boss Chamber**:  
The **Boss Chamber** can only be accessed under specific circumstances. Players must gather an item called the **"Hero's Memento,"** which can be crafted from unique materials found within the crypt or dropped from mini-bosses on each floor. Once obtained, players can use the Hero's Memento to destroy a **Dark Magic Pillar** on the Sanctum of Shadows floor. Upon destruction, a hidden door reveals the Boss Chamber.

**Boss Room Description**:  
The Boss Chamber is an expansive, circular hall adorned with intricate carvings that depict the ancient battles of the heroes. At the center stands the **Ancestor Wraith**, a colossal spirit made of swirling shadows and spectral light. The room is filled with remnants of past battles, including broken weapons and shattered shields.

### Boss: The Ancestor Wraith

**Lore**: The Ancestor Wraith is the manifestation of all the pain, honor, and sacrifice of the heroes interred in the crypt. It seeks to test those who dare to challenge it, ensuring only the worthy can claim its power.

**Abilities and Mechanics**:

1. **Phantom Charge**:  
   - **Description**: The Ancestor Wraith charges at a player, dealing high damage and knocking them back. Players must dodge or block the charge to avoid significant damage.
  
2. **Spectral Waves**:  
   - **Description**: The Wraith unleashes waves of dark energy that ripple through the room, inflicting AoE damage and applying a **Fear** debuff to players caught in the blast.
  
3. **Summon Spirits**:  
   - **Description**: At 75% and 50% health, the Wraith summons spectral warriors to assist in the battle. These minions will attack players and can be defeated for additional loot.
  
4. **Memento of Power**:  
   - **Description**: The Wraith absorbs the energy from players’ failed attempts at honoring the fallen, increasing its damage and resistance with each failed attempt. Players must be strategic in their actions leading up to the boss fight to prevent this.
  
5. **Reverberate**:  
   - **Description**: A channeling ability that the Wraith performs periodically, drawing energy from the environment. Players must destroy the nearby Dark Magic Pillars within a time limit to interrupt this ability; failure results in a powerful damage blast hitting all players.

### Mechanics Summary:
- **Dynamic Boss Fight**: The battle against the Ancestor Wraith involves adapting to its changing mechanics based on player performance and choices throughout the dungeon.
- **Environmental Interaction**: Players can utilize the environment by destroying Dark Magic Pillars to weaken the boss.
- **Teamwork Required**: Players must communicate and strategize to avoid AoE damage, manage adds, and complete objectives effectively.

