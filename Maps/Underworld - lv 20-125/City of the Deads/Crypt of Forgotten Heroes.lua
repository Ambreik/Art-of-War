### Final Floor: The Ancestor’s Wrath – Boss Chamber

**Room Description**:  
The Boss Chamber is a colossal, circular arena encased in layers of ancient stone, darkly inscribed with luminescent runes that tell the story of the ancient fallen heroes and their battles. Flickering torches line the walls, casting shadows over rows of towering crystal formations that emit a faint hum, absorbing energy from every clash within the chamber. At the far end of the room sits an imposing, armored figure on a cracked obsidian throne, its head bowed and grasping a massive shield and spear. An icy aura surrounds the figure, indicating its undead nature. As players step into the center of the chamber, the ground trembles slightly, and ominous echoes rise from the walls, hinting at the powerful force awaiting them.

#### Accessing the Boss Chamber:
Players must have gathered the **Hero’s Memento** from previous floors, which is necessary to destroy one of the Dark Magic Pillars in the **Sanctum of Shadows**. When the pillar collapses, it reveals a secret passage to the Boss Chamber, sealed until the party is fully prepared. 

### Initial Encounter – Waves of Minions
As players gather at the center, the Ancestor Wraith remains motionless, its hollow eyes unlit as it appears in a deep slumber. Shadows flicker, and the runes on the walls pulse as the room responds to the players’ presence. 

1. **Wave 1**:  
   - **Description**: The first wave consists of skeletal foot soldiers, each wielding ancient, rusted weaponry. Their purpose is to probe the players’ defenses.
   - **Mechanics**: These minions are relatively weak but numerous, swarming players with melee attacks. Their quick strikes aim to pressure players, forcing them to use crowd control and AoE spells early in the encounter.
  
