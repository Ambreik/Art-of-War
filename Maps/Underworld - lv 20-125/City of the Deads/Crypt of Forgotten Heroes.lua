#### Stage 1: The Undying Sentinel

- **Appearance**: The Ancestor Wraith stands, a towering figure of undead might, clutching its massive shield in one hand and a spear in the other. Its armor is worn but thick, and a cold, blue light glows in its eyes, indicating a soul far from peaceful. It moves deliberately, with a chilling slowness that belies its strength.

