### **Stage 3: Echo of the Damned**

**Room Environment**:
- The room undergoes a drastic change, with green, necrotic energy emanating from the ground and sickly green flames igniting along the walls. Ominous mists swirl, obscuring sight and concealing danger.
- As the **Echo of the Damned** begins, wails fill the room as spectral mages are summoned to aid Sir Ebonhollow. These mages have abilities that bolster Ebonhollow, such as **Healing**, **Increased Attack Speed**, and **Movement Speed**.

**Sir Ebonhollow’s HP Bar**: Fully replenished once more with enhanced attributes and additional mechanics.

**Abilities and Mechanics**:
- **Death’s Embrace**: Ebonhollow summons three spectral mages to assist him. Each mage grants him a buff — either **Healing**, **Increased Attack Speed**, or **Movement Speed**. The mages must be defeated quickly to prevent Ebonhollow from becoming too powerful.
- **Absorb Life**: Sir Ebonhollow channels a life-draining ability, stealing health from all players within a set range and transferring it to his own health bar. The **Torch of Veiled Light** can diminish its effect, but the player must remain in close proximity for the duration.
- **Deathly Reclamation**: With a powerful blow, he marks the ground, and players standing in the marked area take massive damage over time.
- **Wrathful Spirit**: Ebonhollow can summon lingering spirits that chase down players, exploding for high damage if they make contact. These spirits are slow-moving, but require careful kiting.

**Combat Strategy**:
1. **Mage Elimination**: Players need to prioritize the mages first, reducing Ebonhollow’s boosts to make him more manageable. If all mages are left unchecked, his power will become overwhelming.
2. **Light Management**: Players holding the **Torch of Veiled Light** must be strategic, using its glow to weaken the effects of **Absorb Life** and assist others in avoiding **Wrathful Spirits**.
3. **Avoiding AOE Zones**: Constant movement and awareness of **Deathly Reclamation** areas will be crucial for survival.

**Stage Conclusion**: Once players bring Sir Ebonhollow to zero health in the **Echo of the Damned** stage, he collapses, his armor shattering as he’s finally defeated. The green flames extinguish, and silence fills the chamber, allowing players to move onward to the next floor.

