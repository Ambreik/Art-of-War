**Floor Three: Tomb of the Fallen**

**Room Overview:**  
The **Tomb of the Fallen** is a colossal stone chamber with a sunken center that appears darkened from years of neglect. Four massive warrior statues stand guard in each corner, their stone forms ominously cracked and ready to awaken. Along the chamber walls are rows of narrow slits, from which poison arrows can be triggered to shoot across the room. Suspended high above is an immense slab of stone, poised to fall at any moment, adding a constant sense of impending danger. Players can see faint carvings beneath their feet that pulse with eerie energy.

**Primary Mechanics:**

- **Warrior Statues**: Each of the four statues represents a deadly fallen warrior, and the players must prevent as many of these statues as possible from coming to life.
- **Waves of Monsters**: Monsters spawn in the room's center in three escalating waves, each more intense and numerous than the last.
- **Seal of the Fallen**: A rare item dropped by monsters in each wave, the "Seal of the Fallen" can be applied to a statue, reinforcing its petrification and preventing it from coming to life temporarily. However, each seal expires after five seconds, requiring players to act quickly. The number of seals per wave varies randomly, with a minimum of two and a maximum of five, forcing players to make strategic choices.

### Encounter Scenarios:

#### Scenario 1: Only One Statue is Sealed (Three Statues Awaken)

This is the most challenging outcome, as three of the four statues will come to life once all waves are complete.

- **Statue Mechanics**: Each statue has unique abilities, and with three active, players will be severely tested:
  - **Statue 1**: Performs a sweeping cleave, dealing heavy damage in a wide area around it. This statue moves slowly but will prioritize targeting the highest health player.
  - **Statue 2**: Channels an ability called **Crushing Earth**, summoning debris from the ceiling that falls randomly across the room. Players must dodge the falling rocks to avoid taking significant damage.
  - **Statue 3**: Activates the **Poison Barrage**, triggering the wall-mounted poison arrow traps. Poison arrows shoot across the room in crisscrossing patterns, requiring players to stay agile.

- **Strategy**: Players must work quickly to destroy each statue while contending with the room-wide hazards. Since only one statue is sealed, DPS should focus on one active statue at a time to eliminate it quickly. Healers need to stay close to avoid the arrows but far enough to avoid cleaves and falling rocks.

#### Scenario 2: Two Statues are Sealed (Two Statues Awaken)

With two statues still petrified, players can focus on the remaining two statues, which presents a manageable, though still difficult, battle.

- **Statue Mechanics**:
  - **Statue 1**: (If active) Still performs the **Sweeping Cleave** and targets high-health players.
  - **Statue 2**: (If active) Channels **Crushing Earth**, but with two statues sealed, the frequency of falling debris is reduced.
  - **Poison Arrow Mechanic**: Poison arrows now fire sporadically across the room as traps are triggered randomly, but players have slightly more time to maneuver.

- **Strategy**: Players focus their efforts on taking down the statues in sequence. While dodging arrows, they can use the room's edges to avoid poison bursts and **Crushing Earth** zones. Once one statue is down, the encounter becomes easier to manage.

#### Scenario 3: Three Statues are Sealed (One Statue Awakens)

With three statues petrified, only one will become an active threat.

- **Statue Mechanics**: The lone statue is empowered and gains access to additional abilities:
  - **Enraged Cleave**: A devastating area-of-effect slam that knocks back any nearby players.
  - **Summon Allies**: Occasionally summons a few spectral warriors to aid it, which need to be quickly dispatched.
  - **Poison Barrage Enhancement**: This statue can activate the poison arrows at double speed, forcing players to react quickly and navigate around the room’s hazards.

- **Strategy**: Players must balance taking down the empowered statue while dodging poison arrows. The spectral warriors it summons will attempt to distract players, so they should be defeated promptly. This scenario is challenging due to the statue’s enhanced abilities, though manageable with coordination.

#### Scenario 4: All Statues are Sealed

If players manage to seal all statues, they bypass the statue battle entirely. However, doing so will initiate the final test of the room:

- **Ceiling Collapse Mechanic**: The stone slab above begins to tremble, and players are given a **Crush Timer** as the entire ceiling threatens to collapse.
- **Objective**: Players must eliminate the remaining waves of monsters rapidly before the timer expires. Once the timer runs out, the slab will come crashing down, causing an instant wipe if any enemies remain in the room.

### Additional Mechanics and Strategy:

- **Seal of the Fallen Timers**: Seals must be applied within a strict five-second window to be effective. If the seal is not used in time, it disintegrates, leaving players with fewer chances to lock the statues. This can add an unpredictable element to the fight.
- **Poison Arrow Overload**: During wave spawns, the poison arrows may fire sporadically across the room, creating dangerous zones for players to avoid. It is critical for players to be mindful of these traps while managing the waves.
- **Debris Dodge**: Random sections of the ceiling begin to fall as soon as the statues awaken. Players must move swiftly to avoid heavy damage or debuffs applied by the rubble.

### Boss Room Unlock Mechanic:

Once all statues are sealed or defeated, a hidden **Pillar of Souls** in one of the chamber’s corners is revealed. To access the next floor, players must place a **Soul Fragment** on the pillar, which can either drop from the final wave or be crafted using **Seal of the Fallen** tokens collected during the fight. Placing the fragment dispels a magical barrier leading to a previously hidden corridor, which takes players onward to the next floor.

