### **Floor Four: Sanctum of Shadows**

**Room Description**:  
The Sanctum of Shadows is an immense, ominous chamber blanketed in thick darkness. Shadowy pillars surround the room, pulsating with ancient energy and casting flickering lights across the floor. The whispers of lost souls echo throughout, and an unsettling tension fills the air, creating an atmosphere of foreboding for all who enter. Players must rely on a special item, the **Torch of Veiled Light**, obtained from an NPC in Eldergate, which creates a sphere of light around them to see nearby threats and navigate the shadows. 

At the center of this darkness stands the armored skeletal knight, **Sir Ebonhollow, the Shadowbound Knight**, waiting to confront any intruders. Each stage of the fight progresses through **Echo of Warning, Echo of Betrayal,** and **Echo of the Damned** — each bringing its own unique challenges, environment changes, and mechanics. 

---

### **Stage 1: Echo of Warning**

**Room Environment**:
- Shadows flicker and twist around the players as they approach, revealing four cursed pillars that encircle the room. These pillars empower Sir Ebonhollow, enhancing his health regeneration and overall defense. Destroying them will be critical to weakening his strength.
- Low whispers fill the chamber, issuing a warning to players, and any failure to heed the **Echo of Warning** will set off shadowy traps, ensnaring players in dark vines that inflict ongoing damage.

**Sir Ebonhollow’s HP Bar**: Fully replenished at the start. Players must deplete this initial HP bar to progress to the next stage.

**Abilities and Mechanics**:
- **Shadowblade Swipe**: Sir Ebonhollow performs a broad, powerful swing, dealing significant damage in a cone. Players must dodge to avoid being hit.
- **Dark Infusion**: Ebonhollow channels energy from the cursed pillars to regenerate a portion of his health every 10 seconds. Destroying each pillar reduces his regeneration by 25% and lowers his defense, allowing players to inflict greater damage.
- **Rending Scream**: A piercing wail that slows all players for 5 seconds, making them more vulnerable to his other attacks.
- **Cursed Pillars**: Each of the four cursed pillars must be destroyed to eliminate Ebonhollow’s regeneration ability and defense bonus. The final pillar’s destruction leaves him open to increased player damage.

**Combat Strategy**:
1. **Pillar Priority**: Players must target and destroy the pillars quickly. As each pillar is destroyed, the room brightens slightly, and the darkness begins to weaken, reducing Ebonhollow’s regeneration.
2. **Damage Phase**: With each destroyed pillar, Sir Ebonhollow’s defense wanes. Players can now focus on heavy attacks to bring his HP bar down.
3. **Positioning**: Players should spread out to avoid **Shadowblade Swipe** and be ready to dodge **Rending Scream**.

**Stage Progression**: Once his HP bar is drained, Sir Ebonhollow falls to one knee, chuckling darkly. Shadows thicken, and the **Echo of Betrayal** commences.

---

### **Stage 2: Echo of Betrayal**

**Room Environment**:
- The room grows darker, with thick, spectral chains erupting from the ground, dividing it into segmented zones that limit movement. These chains hum with cursed energy and cause damage to any player who gets too close. The **Torch of Veiled Light** can weaken these chains briefly, allowing players to navigate between segments.
- Sinister laughter echoes, and a voice warns, “Trust no one; even the light betrays!” Players face a series of choices as they encounter illusions that either provide assistance or activate curses if mishandled.

**Sir Ebonhollow’s HP Bar**: Completely refilled, with increased health and new abilities.

**Abilities and Mechanics**:
- **Spectral Betrayal**: Two players are marked with the **Essence of Betrayal** debuff, which causes minor damage to nearby allies, requiring them to keep a safe distance from teammates.
- **Shattered Faith**: Ebonhollow releases a radial burst of energy that pushes all players toward the edges of the room, forcing them to navigate back to the center while avoiding chains.
- **Vampiric Strike**: Sir Ebonhollow targets the player with the highest threat, dealing significant damage and absorbing health to form a temporary shield. The **Torch of Veiled Light** can be used to weaken the shield.
- **Soul-Linked Chains**: Periodically, the spectral chains will pulse with energy, emitting **AoE damage**. If players fail to avoid these pulses, they are briefly stunned.

**Combat Strategy**:
1. **Chain Navigation**: Players use the **Torch of Veiled Light** to pass safely through segments and avoid becoming isolated in dangerous areas.
2. **Focus Fire**: After **Vampiric Strike**, players need to break through the shield quickly to prevent Ebonhollow from regaining too much health.
3. **Debuff Management**: Those marked by **Essence of Betrayal** need to keep a safe distance to prevent damage to allies.

**Stage Progression**: Upon reaching zero health, Ebonhollow lets out a final piercing scream, causing the chains to vanish and revealing the final form of the chamber, lit with a sickly green glow. This initiates the **Echo of the Damned** stage.

