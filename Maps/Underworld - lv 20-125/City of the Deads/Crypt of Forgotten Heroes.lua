   - **Spear Thrust**: The Wraith thrusts its long spear forward in a sweeping motion, hitting all players within range. While the attack is slow, it causes **Knock Away** if it lands, sending players back toward the chamber walls.
   - **Shield Bash**: Every few moments, the Wraith raises its shield and slams it down, sending out a shockwave. Players hit by the shield are **Dazed** and briefly unable to use abilities.
   - **Dark Aura**: A continuous AoE effect surrounds the Wraith, causing periodic “Root” effects to any player that strays too close for too long, discouraging players from remaining in melee range.

   - **Crystal Interaction**: With each strike taken by the Wraith, 2-10% of the damage is absorbed by the crystals around the room. These crystals pulse, storing energy for the boss’s second stage. This creates a progressive damage cap that players must work around, as their strongest abilities will be slightly less effective in this stage.

#### Stage 1 Strategy:
Players should focus on dealing consistent damage without overcommitting, as any extreme attack will be partially absorbed by the room’s crystals. Coordination is key; players must time their attacks to avoid triggering **Knock Away** or getting rooted by **Dark Aura**, working to chip away the Wraith’s health while anticipating its movements.  

