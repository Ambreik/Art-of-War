   - In Stage 2, the **Spectral Betrayal** debuff could have lingering effects, like a slow movement penalty, that lasts into the next phase if it isn’t cleansed by a healer. Alternatively, being hit by **Rending Scream** could apply a stacking debuff that weakens player damage, making prolonged exposure dangerous.

5. **Environmental Hazards in Stage 3**:
   - **Falling Debris**: In the final stage, as the mages are summoned, parts of the ceiling could start crumbling. Players would have to dodge falling rocks or pieces, giving the room a sense of increasing instability.
   - **Environmental Triggers**: Randomly activate poison clouds or weakening zones that last for short bursts, requiring players to keep track of the environment or lose health over time.

6. **Optional Boss Phases**:
   - Introduce an optional “enrage” phase if players don’t meet a soft time limit. This could increase Sir Ebonhollow’s speed, reduce his cooldowns, or unleash an especially powerful AOE attack that’s otherwise rare.

