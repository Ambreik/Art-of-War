   - **Room-wide Darkness Events**: At certain HP thresholds in each stage, the entire room could plunge into darkness, leaving only the torch's light and briefly disorienting players. This gives the sense that the room and boss are fighting back against the light itself.

2. **Player Positioning Mechanic**:
   - Add an effect where certain areas of the room are “corrupted zones” that deal damage over time, or zones that randomly spawn during boss phases. Players would need to react quickly to avoid these as they move between pillars or evade boss attacks.

3. **Skill Synergies for the Boss**:
   - **Combo Attacks**: In later stages, the boss could chain abilities like **Deathly Reclamation** into **Absorb Life**, or summon the **Wrathful Spirit** during other attacks. This forces players to juggle multiple threats and increases the difficulty in predictable ways.
