			### Eldergate City Overview

		Architecture and Construction  
	Eldergate was constructed by a coalition of skilled architects and craftsmen from various realms, blending gothic and medieval designs with ethereal influences. 
	The city is characterized by its towering stone walls, intricately carved with symbols of protection and unity. Built atop elevated ground, Eldergate serves as a 
stronghold against the chaotic forces of the Underworld, strategically designed to withstand sieges and attacks.

		Gates  
	The city boasts three majestic gates  
1. The Main Gate : The grand entrance adorned with luminous runes, symbolizing safety and strength.  
2. The Eastern Gate : Smaller but fortified, primarily used by merchants and travelers.  
3. The Western Gate : Serves as an emergency exit for soldiers, reinforced for quick escapes.

		City Layout and Buildings  
	Eldergate features a bustling market district, residential quarters, and sacred spaces  
- Market District : A vibrant area filled with shops and stalls, where merchants trade goods, including rare artifacts and essential supplies.  
- Residential Quarters : Cozy homes for the citizens, each uniquely designed but maintaining a uniform aesthetic.  
- Temples : Places of worship dedicated to various deities, decorated with stained glass and soft, flickering light.  

		Towers  
	Several imposing towers dominate the skyline  
- The Watchtower The tallest structure, manned by guards who survey the surrounding lands for threats.  
- The Mage’s Tower A place for magical studies and research, glowing with arcane symbols and hosting enchanting light displays.

		Lighting and Atmosphere  
	Eldergate is illuminated by magical orbs that float above the streets, casting a warm, inviting glow. During the night, these orbs create a mystical ambiance, 
enhancing the city’s enchanting appearance. Lanterns crafted by Liora, the Lightweaver, are placed throughout the city, radiating a soothing light that wards off 
darkness.

		Other Notable Features  
- The Forge : A central hub for crafting weapons and armor, echoing with the sounds of hammers striking anvils.  
- Training Grounds : An area for soldiers to practice their skills, equipped with dummies and obstacle courses.  
- The Great Hall : A communal space for meetings, celebrations, and gatherings, adorned with tapestries depicting Eldergate’s history.

		Surroundings  
	Eldergate is encircled by lush forests and steep cliffs, providing natural defense. Paths leading into the wilderness are well-marked, ensuring travelers can 
safely navigate the terrain while also being warned of potential dangers lurking nearby. 

	Eldergate stands not just as a city, but as a beacon of hope, strength, and community in the dark, unpredictable world of the Underworld.