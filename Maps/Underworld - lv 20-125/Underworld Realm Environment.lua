			Underworld Realm Environment

1. "Overall Aesthetic" : The Underworld Realm is blanketed in perpetual darkness with intermittent, ghostly glows. Its layout and ambience are shaped by eerie atmospherics, echoing sounds, spectral clouds, and ever-present shadows, contributing to a foreboding yet immersive setting.

2. "Unique Locations" :
   - "The Obsidian Wastes" : A desert of jagged, blackened rock with toxic gas fissures and floating obsidian shards casting heavy shadows.
   - "The Shifting Pits" : A labyrinth of floating platforms with unpredictable gravity shifts and spectral waves.
   - "The Black River" : A murky, whispering river featuring spectral barges and guardians protecting relics.
   - "The Unseen Sanctum" : An area of shifting corridors and spectral wards, culminating in high-stakes encounters.
   - "The Eternal Maw" : A cavernous, final area with increasing environmental hazards like Soul Drain and shadow vortexes, leading to a climactic boss battle.

	Each zone includes a mix of physical dangers, spectral challenges, and debuffs that affect players’ vision, stamina, health, and interaction, creating an intense 
environment requiring careful navigation and preparedness.