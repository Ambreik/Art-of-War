			Item Drop List for Necrokeep

#### Common Loot (50% Drop Rate)

1. Shadow Shard
   - Description A small, jagged piece of shadow essence.
   - Use Used as a crafting material to enhance weapons with shadow damage.

2. Phantom Essence
   - Description A swirling mist of ghostly energy.
   - Use Can be used to upgrade armor, granting bonuses to evasion and shadow resistance.

3. Wraith’s Tear
   - Description A glistening droplet of liquid shadow.
   - Use Used in potions to enhance shadow-related abilities, increasing potency for a short duration.

#### Uncommon Loot (30% Drop Rate)

4. Cloak of Whispers
   - Description A dark cloak that shimmers with shadowy energy.
   - Use Increases stealth abilities and grants a chance to evade attacks when equipped.

5. Grasping Chains
   - Description Shadowy chains that can ensnare foes.
   - Use Can be crafted into a weapon mod, adding a crowd control effect to melee weapons.

6. Necrotic Tome
   - Description An ancient book filled with dark incantations.
   - Use Grants a one-time use spell that deals shadow damage to all enemies in an area.

#### Rare Loot (15% Drop Rate)

7. Zarok’s Shadow Blade
   - Description A blade forged from pure shadow, pulsating with dark energy.
   - Use A powerful weapon with increased damage against undead and shadow creatures.

8. Crown of the Damned
   - Description A twisted crown worn by the most powerful undead.
   - Use Provides bonuses to intelligence and shadow spell damage.

9. Amulet of the Abyss
   - Description An amulet that absorbs shadows, empowering its wearer.
   - Use Increases shadow resistance and grants a chance to reflect shadow damage back to the attacker.

#### Epic Loot (5% Drop Rate)

10. Heart of the Shadowbinder
    - Description A pulsing, dark crystal that resonates with shadow magic.
    - Use Used to craft powerful equipment with unique abilities, such as shadow teleportation.

11. Staff of the Undying
    - Description A staff that channels the power of the undead.
    - Use Increases spell power and grants a chance to summon a phantom ally in battle.

12. Soulbound Armor
    - Description Armor that is fused with the essence of the dead.
    - Use Provides high damage resistance and can absorb a percentage of damage dealt to replenish the wearer’s health.

#### Legendary Loot (1% Drop Rate)

13. Zarok’s Grimoire
    - Description A legendary book that holds the secrets of the shadows.
    - Use Grants the user the ability to cast a powerful shadow spell that affects multiple targets.

14. Dark Ascendant Ring
    - Description A ring that grants its bearer control over shadows.
    - Use Increases all shadow-related skills and abilities and allows the wearer to temporarily become incorporeal.

### Summary
- Common Loot 50% drop rate items for general crafting and upgrades.
- Uncommon Loot 30% drop rate items providing useful enhancements.
