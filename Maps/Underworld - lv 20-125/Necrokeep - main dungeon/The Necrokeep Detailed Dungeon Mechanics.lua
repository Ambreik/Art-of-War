### **Stage 4: The Infernal Catacombs**

The **Infernal Catacombs** represent one of the final and most treacherous areas within Necrokeep. These chambers are lined with unholy relics, cursed graves, and chains embedded into the walls. The area pulses with dark energy, intensifying as players move deeper into the catacombs. Sounds of chains dragging across stone and whispers of ancient incantations echo throughout, as if the spirits themselves are watching every step.

#### **Environmental Mechanics**

1. **The Cursed Ground**  
   - Throughout the catacombs, areas of the floor are coated in a thick, cursed mist that damages players who stand on it. The mist originates from enchanted obelisks that must be destroyed to clear paths forward. However, only players wielding a **Soulfire Lantern** can interact with these obelisks.
   - **Effect**: Each second a player stands on the Cursed Ground, they receive stacking **Curse of Decay** debuffs, increasing shadow damage taken by 10% per stack. The obelisks intermittently recharge, reigniting Cursed Ground if players linger too long.

2. **Shadow Chains**  
   - As players approach certain passages, Shadow Chains erupt from the walls, briefly binding players in place. Trapped players must perform a series of skill checks or wait for teammates to attack the chains and free them.
   - **Effect**: Bound players suffer 15% reduced movement speed and are vulnerable to attacks from nearby enemies.

3. **Soulfire Lantern Checkpoints**  
   - Scattered through the catacombs are checkpoints where the **Soulfire Lantern** must be placed temporarily to recharge. During recharges, ambient darkness encroaches, leaving players vulnerable to ambushes by shadowy foes.
   - **Effect**: While the lantern is charging, players have limited visibility and must rely on defensive skills and formations to protect the lantern and one another.

#### **Monsters**

1. **Infernal Wraiths**  
   - **Mechanics**:
     - **Phase Shift**: At regular intervals, Infernal Wraiths shift between physical and ethereal forms, making them immune to damage while ethereal. They deal high melee damage when in physical form.
     - **Inferno Touch**: Each melee attack applies a **Burning Shadows** debuff, dealing fire and shadow damage over time.
   - **Appearance**: Large, translucent wraiths with crackling flames entwined within their ghostly forms, with burning eyes and elongated claws.

2. **Bone Golems**  
   - **Mechanics**:
     - **Bone Crush**: A powerful melee attack that stuns players briefly and inflicts heavy physical damage.
     - **Necrotic Aura**: Emits an aura that reduces healing effects by 30% within a certain radius, requiring players to spread out to avoid multiple auras.
     - **Bone Fusion**: When two Bone Golems are within close proximity, they fuse into a larger **Juggernaut Golem**, gaining increased health and new abilities.
   - **Appearance**: Massive, animated structures formed from twisted bones and fragments of armor, with dark green energy holding their frames together.

#### **Mini-Boss: Dreadbound Behemoth**

- **Description**: The Dreadbound Behemoth is a hulking, grotesque creature built from the fused bones of fallen warriors, held together by ancient magic. As it moves, the very ground trembles, and ghostly screams echo from the bones that form its body.
- **Abilities**:
  1. **Death Tremor**: Slams the ground, causing tremors throughout the room. This ability knocks back all players and inflicts the **Unsteady** debuff, which increases damage taken from subsequent tremors.
  2. **Soul Devour**: Channels energy from nearby defeated minions to heal itself. Players must interrupt or pull it away from fallen enemies to prevent healing.
  3. **Bone Shatter**: Shoots sharp bone shards across the room, inflicting a bleeding effect. Players must take cover behind scattered pillars to avoid heavy damage.
  4. **Enrage at 50% Health**: At half health, the Behemoth becomes frenzied, gaining increased attack speed and randomly summoning Bone Golems to assist it. It also releases a pulse every 10 seconds that reanimates fallen Bone Golems as lesser **Bone Wraiths** that swarm players.
- **Environmental Interactions**: **Sealing Crystals** scattered around the room can temporarily drain the Behemoth’s strength. By shattering a crystal, players reduce the Behemoth’s attack power for a brief period, allowing for concentrated bursts of damage.

---

### **Final Encounter: Ebon Throne Room with Valthorax, Lord of Shadows**

The final room is the heart of Necrokeep and reflects the essence of Valthorax’s power. Dark stone columns rise from the floor, engraved with forbidden runes and cloaked in shadows that shift and pulse with an eerie light. A massive throne sits at the room's far end, with Valthorax, the **Lord of Shadows**, seated in his full, terrifying glory. His form is partially obscured by tendrils of living darkness, and his glowing crimson eyes pierce through the dimly lit room.

#### **Environmental Mechanics**

1. **Shadows of the Past**  
   - The Ebon Throne Room hosts shadowy figures that rise periodically from the floor. These figures are the souls of past adventurers who failed to defeat Valthorax and now haunt the room as passive traps. Approaching too closely triggers a **Memory Trap** that freezes players in place momentarily, making them vulnerable to Valthorax’s attacks.

2. **Shattered Light Crystals**  
   - Scattered around the room are ancient **Light Crystals**—relics of a time when the throne room was a place of justice. Valthorax has shattered these crystals to suppress light. However, players can restore these crystals temporarily by interacting with them, creating brief **Beams of Light** that weaken Valthorax’s shadow-based defenses.
   - **Effect**: Each activated Light Crystal reduces Valthorax’s damage resistance by 10% for 20 seconds. The crystals must recharge before they can be used again, so players must time their activation for critical moments.

