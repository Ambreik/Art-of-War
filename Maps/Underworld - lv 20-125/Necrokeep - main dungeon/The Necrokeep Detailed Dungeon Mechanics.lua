			Necrokeep – Overview

The Necrokeep is a haunted fortress set on a secluded island surrounded by thick, swirling fog and relentless storm clouds. Inside, the shadows pulse with dark energy, whispering remnants of ancient, trapped souls. This multi-floor dungeon demands intense coordination, strategy, and mastery of light and shadow, as players venture through eerie corridors, cursed chambers, and an otherworldly throne room to confront Valthorax, Lord of Shadows. The **Soulfire Lantern** is essential, casting light that banishes darkness, reveals hidden dangers, and weakens certain enemies.

---

### **Stage 1: Entrance Hall**

#### **Environment**
- The **Entrance Hall** is a grand, vaulted chamber with decaying tapestries, faded murals depicting forgotten battles, and flickering shadowy shapes along the walls. As players step further inside, echoes of distant screams build, creating a mounting sense of dread.
- **Environmental Mechanics**:  
  - **Spectral Ambush**: Spectral warriors emerge from the walls when players enter, ambushing them in waves. If players linger too long, more spectral warriors rise, adding to the chaos.
  - **Resonance of Despair**: The hall exudes a suffocating aura that inflicts a stacking debuff, **Despair**, increasing the damage players take. Moving quickly and avoiding prolonged battles prevents excessive stacks of this debuff.

#### **Monsters**

1. **Spectral Guardians**  
   - **Mechanics**:
     - **Phasing Strike**: The Guardians can teleport short distances, confusing players by striking from behind.
     - **Soul Drain**: Attacks that deplete a portion of a player's mana with each hit, encouraging quick elimination.
   - **Appearance**: Cloaked in ethereal armor, the Guardians carry ghostly swords and have glowing, hollow eyes.  

2. **Terror Wraiths**  
   - **Mechanics**:
     - **Nightmare Gaze**: Terror Wraiths afflict players with **Fear**, causing brief disorientation and forcing players to run uncontrollably in random directions.
     - **Wail of Sorrow**: Deals area damage around the Wraith, scaling with the number of nearby players, punishing clustering.
   - **Appearance**: Emaciated ghostly figures with elongated, clawed hands and faces twisted in agony.  

#### **Mini-Boss: The Sorrowbound Sentinel**
- **Description**: A towering figure with cracked armor, infused with dark energy. Flickering shadows cling to his form, and his weapon exudes a black mist that corrodes all it touches.
- **Abilities**:
  - **Aura of Despair**: Players within range of the Sentinel gain **Despair** stacks more quickly, increasing the challenge of staying close to the boss.
  - **Shadow Barrage**: Hurls bolts of darkness that explode, forcing players to spread out or take damage.
  - **Final Grasp**: At low health, he summons spectral chains to immobilize a random player, requiring teamwork to break the chains.
- **Environment Interaction**: Scattered **Light Orbs** around the room can be activated to briefly stun the Sentinel, allowing players a brief window to unleash high damage.

---

### **Stage 2: The Shadow Pits**

#### **Environment**
- The **Shadow Pits** are dark, cavernous chambers connected by narrow, crumbling bridges. Each pit below teems with swirling shadow mists, from which dark figures rise. Distant cries and the clinking of chains create a menacing background.
- **Environmental Mechanics**:
  - **Shadow Shroud**: Certain areas of the room are steeped in dense shadows that reduce visibility and player speed. Only **Soulfire Lantern** illumination dispels the Shroud, allowing safe passage.
  - **Spectral Rifts**: Periodically, rifts open, releasing waves of shadow minions. These rifts must be sealed in time by interacting with their cores, or they’ll spawn more formidable creatures.

#### **Monsters**

1. **Gloom Stalkers**  
   - **Mechanics**:
     - **Ambush Leap**: The Stalkers emerge from shadowy corners, leaping onto players to deal heavy damage.
     - **Dark Mist**: Creates a misty area that blinds players within, forcing reliance on the **Soulfire Lantern** for vision.
   - **Appearance**: Hunched, wolf-like creatures with shadowy fur and eyes glowing a faint purple.

2. **Abyssal Minions**  
   - **Mechanics**:
     - **Dark Binding**: Links health between minions, making them share damage taken, which requires players to prioritize effective area attacks.
     - **Void Pulse**: Emits a pulse of void energy, damaging all players within a radius every few seconds.
   - **Appearance**: Slender, robed figures with elongated arms and no visible face, only an orb of dark energy where the head should be.

#### **Mini-Boss: Riftwarden Myrath**
- **Description**: A spectral spellcaster wrapped in shadowy robes, levitating just above the ground. His hands hold a staff that pulses with dark magic, and his form shifts constantly as if made of living smoke.
- **Abilities**:
  - **Rift Summon**: Summons smaller rifts that release Abyssal Minions into the battle.
  - **Void Lance**: Fires a lance of dark energy that impales players in a line, causing lingering shadow damage over time.
  - **Shadow Shell**: Creates a protective shield that reflects ranged damage until dispelled by striking specific crystal formations scattered around the room.
- **Environment Interaction**: To remove the Riftwarden’s **Shadow Shell**, players must destroy the **Crystal Shards** found in the room’s corners. Breaking these crystals weakens his defenses temporarily, allowing full damage.

---

### **Stage 3: The Hall of Whispers**

#### **Environment**
- The **Hall of Whispers** is a vast, twisting labyrinth filled with lingering spirits. The air is thick with whispers, creating an oppressive silence punctuated by sudden cries of anguish. Shattered mirrors and distorted reflections make navigation confusing, while hidden traps lie in wait.
- **Environmental Mechanics**:
  - **Echoes of the Damned**: Unseen apparitions emit confusing echoes, causing players to lose control temporarily. **Purification Runes** placed throughout the hall can counter this effect when activated.
  - **Haunting Mirrors**: Certain mirrors project phantasmal clones of the players, causing deceptive reflections that attack until shattered.

#### **Monsters**

1. **Spectral Apparitions**  
   - **Mechanics**:
     - **Haunting Touch**: Deals damage to a player, inflicting a debuff that reduces healing received temporarily.
     - **Phantom Doppelganger**: Upon death, splits into multiple clones that deal reduced damage but swarm players.
   - **Appearance**: Semi-transparent figures with blurred, indistinct features that flicker in and out of reality.

2. **Lamenting Shadows**  
   - **Mechanics**:
     - **Wail of Agony**: Emits a cry that reduces players’ maximum health temporarily, stacking if not removed.
     - **Cursed Breath**: Breathes a cursed mist that slows and drains mana from all players hit.
   - **Appearance**: Twisted, humanoid figures with skeletal faces, their mouths perpetually open in silent screams.

#### **Mini-Boss: Sorrowweaver Malora**
- **Description**: A ghostly woman draped in tattered robes, her face obscured by a veil. Chains of shadow hang from her wrists, and she floats with a mournful, wailing presence.
- **Abilities**:
  - **Chain of Sorrow**: Chains players together, causing shared damage until they are broken apart by distance.
  - **Spectral Embrace**: Pulls players into a cursed mist, dealing damage over time until they escape.
  - **Echoed Scream**: Unleashes a powerful scream that briefly stuns players and summons **Spectral Apparitions** to attack.
- **Environment Interaction**: To damage Malora effectively, players must shatter cursed mirrors around the room, which weakens her spectral form and stops her **Chain of Sorrow**.

