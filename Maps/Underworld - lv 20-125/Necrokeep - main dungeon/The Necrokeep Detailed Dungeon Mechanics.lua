#### **1. Entry and Atmosphere**

The entrance to the Hall is concealed beneath the **Ebon Throne Room**, and accessing it requires players to locate and activate several shadowed sigils scattered throughout the Necrokeep. Once unlocked, the trapdoor creaks open, and an icy draft pulls players into a spiral descent. The air is thick with an aura of melancholy, pressing on the party’s morale as a dark, unsettling melody echoes faintly, growing louder with each step.

Upon entering the Hall, the room seems endless, stretching out in darkness. Shadowy tendrils creep up the stone walls, which are carved with scenes of valor, loss, and despair. Along the walls are engraved names of fallen heroes, reminders of those who ventured here before and never returned. Dimly flickering torches illuminate the path, their feeble light casting distorted, flickering shadows across the floor.

---

#### **2. Environmental Mechanics**

The Hall of Eternal Sorrow is designed to impose an oppressive challenge on players, blending atmospheric effects with environmental hazards. As players move through the hall, they encounter various mechanics that test their resilience and teamwork:

- **Aura of Sorrow**: A constant debuff affecting all players, reducing healing effects by 25% and gradually sapping health. The only way to remove stacks of this aura is to activate **Relics of Remembrance**—small, ancient monuments scattered throughout the hall. These relics provide brief respites but quickly fall back into darkness after a short time, requiring players to push forward despite their dwindling health.

- **Statues of Fallen Champions**: Along the hall, players encounter statues of heroes who fought Valthorax in the past. Each statue can be interacted with to briefly grant players a **Champion’s Boon**, offering small but powerful temporary buffs like increased damage or resistances. However, disturbing these statues too often triggers **Spectral Champions**—phantom versions of the heroes themselves who attack with relentless, vengeful power. Defeating these Spectral Champions provides unique items, such as **Soulstones**, which temporarily resist shadow damage.

- **Grieving Shadows**: Occasionally, clouds of mist swirl up from the floor, forming **Grieving Shadows**. These spectral figures follow players, whispering ominous messages that instill fear and reduce movement speed. They can be dispelled with light-based attacks, but otherwise, they drain stamina over time, slowly exhausting players as they journey deeper.

---

#### **3. Visual Details and Sound Design**

The Hall's environment immerses players in an atmosphere of overwhelming sorrow:

- **Sound**: A mournful, distant dirge reverberates through the chamber, punctuated by the rattling of chains and occasional, anguished wails. Players may even hear whispers—remnants of fallen heroes calling for help, some accusing players of intruding on their resting place.

- **Visuals**: Faintly glowing orbs drift throughout the room, casting a dim, haunting light. Each orb corresponds to the souls of warriors defeated by Valthorax, trapped in eternal lament. As players approach, the orbs dim, as if recoiling, while the walls seem to pulsate with dark energy.

---

#### **4. The Throne of Sorrow – The Knight of Eternal Sorrow**

At the hall’s end, an immense set of doors engraved with shadowy figures stands before the players. Once opened, they reveal a vast, circular chamber with an enormous, cracked throne at its center. Seated upon it is the **Knight of Eternal Sorrow**, a towering figure clad in tarnished armor encrusted with fragments of shadowstone, a rare and potent mineral imbued with shadow magic. Chains drape from his shoulders, linking him to the throne, symbolizing his curse and eternal duty to guard the Hall.

The Knight’s armor bears signs of countless battles: dents, scorch marks, and clawed gashes mar its surface, while a continuous stream of shadowy mist seeps from the joints. His face remains obscured beneath a cracked helm, with only two haunting blue lights visible—what remains of his eyes. 

#### **Boss Mechanics – Knight of Eternal Sorrow**

---

**Phase 1: Shield of Regret (100% - 75% Health)**

- **Sorrowful Strike**: The Knight swings his enormous blade in wide arcs, dealing area damage and applying **Grief**—a debuff that lowers movement speed and increases vulnerability to shadow damage.
  
- **Aura of Regret**: A powerful, localized aura saps mana and stamina from players. As the aura intensifies, it causes all nearby players to receive reduced healing. To counter, players must activate **Memory Beacons** found at key points around the arena, which temporarily dispel the aura’s effects within a limited radius.

**Phase 2: Unleashed Anguish (75% - 50% Health)**

- **Revenant Summoning**: The Knight summons **Revenants of Fallen Heroes**—spectral warriors who were previous champions slain by Valthorax. These revenants relentlessly target players, dealing both physical and shadow damage. Destroying these revenants quickly is critical, as they become stronger if left alive for too long.

- **Chains of Despair**: The Knight occasionally binds a player to one of the statues in the room, immobilizing them until their allies break the chains by attacking the statue. If players fail to free them within a short window, the chained player suffers high damage over time.

**Phase 3: Final Stand (50% - 0% Health)**

- **Shroud of Agony**: The Knight envelops himself in a dark shroud, becoming invulnerable while he channels **Wrath of the Damned**. This devastating attack must be interrupted by activating all **Memory Beacons** in quick succession, creating a blinding light that weakens the Knight and stuns him briefly.

- **Resurrected Revenants**: As his health reaches 10%, the Knight summons more revenants who become progressively powerful. Each revenant explodes upon reaching critical health, dealing shadow damage across the room. Players must coordinate to defeat the Knight quickly, as too many revenants will lead to an overwhelming force.

---

#### **Rewards and Aftermath**

Defeating the Knight of Eternal Sorrow rewards players with powerful items and unique artifacts:

- **Soulsteel Armor Fragment**: A rare crafting component that players can use to create armor pieces resistant to shadow damage and curses.
  
- **Eternal Blade**: A rare weapon with a chance to trigger **Soul Strike**, dealing bonus damage to undead and shadow-based enemies, invaluable against Valthorax and his minions.
  
- **Amulet of Redemption**: This amulet grants players significant resistance to shadow-based attacks and curses, preparing them for the Necrokeep's remaining horrors.

Upon the Knight’s defeat, the Hall’s oppressive atmosphere lessens slightly, and the sorrowful whispers fade. However, a lingering chill remains, serving as a reminder of the Hall’s tragic past. The final exit becomes visible—a narrow stairway spiraling back up to the Ebon Throne Room, marking the players’ completion of the Hall’s harrowing trials.