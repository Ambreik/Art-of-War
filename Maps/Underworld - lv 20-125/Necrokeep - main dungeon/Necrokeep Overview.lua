			Necrokeep Overview


	The Necrokeep is a multi-layered dungeon located on a dark, mist-shrouded island. Each floor is themed around different aspects of death and shadow, filled with 
malevolent spirits, undead minions, and powerful bosses.

---

### Floor 1 The Shadows' Ascent
Environment Dark corridors and flickering shadows, with whispers echoing in the distance.

Monsters
1. Shadow Stalkers  
   - Mechanics Stealth ability allowing them to ambush players. They can phase out, becoming invulnerable for a short time.
  
2. Wraiths of Despair  
   - Mechanics Can cast Aura of Sorrow, dealing damage over time to players in close proximity. When defeated, they explode in a shadowy burst, damaging nearby players.

Boss Ebonshade Warden
- Skills
  - Shadow Grasp Roots a player in place, dealing damage over time until broken.
  - Phantom Strike Teleports behind a player and deals heavy damage.
  - Ultimate Eclipse Reduces light in the area, causing all players to suffer increased damage from shadow-based attacks for a short duration.

---

### Floor 2 The Hall of Echoes
Environment Long, echoing halls filled with the sounds of lost souls.

Monsters
1. Lost Souls  
   - Mechanics Can possess players, causing them to attack allies for a short period. Requires a player to break the possession.

2. Skeleton Sentinels  
   - Mechanics They can summon smaller skeletons when low on health and have a Shield of Bones that reduces damage taken.

Boss Haunting Echo
- Skills
  - Echo of Pain Reflects a percentage of damage taken back to the attacker.
  - Soul Rend Targets a player, causing them to bleed for a percentage of their maximum health.
  - Ultimate Resonating Screech A powerful scream that stuns all players and summons Lost Souls to fight alongside.

---

### Floor 3 The Catacombs of Gloom
Environment A maze-like area with burial sites and ancient runes glowing faintly.

Monsters
1. Gravebound Minions  
   - Mechanics Can raise fallen players as temporary allies for a short duration. Their attacks drain mana from players.

2. Gloombeasts  
   - Mechanics Fast-moving creatures that deal heavy damage with quick strikes and can leap to attack players from a distance.

Boss Lord of the Grave
- Skills
  - Grave Call Summons waves of Gravebound Minions.
  - Bone Shatter A powerful melee attack that stuns players in melee range.
  - Ultimate Cursed Ground Creates a zone that drains health from players standing in it and heals the boss for a percentage of the damage dealt.

---

### Floor 4 The Necromancer's Sanctum
Environment A dark throne room with pulsating shadow energy and sinister relics.

Monsters
1. Spectral Guards  
   - Mechanics Shield allies and can taunt players. They have a Shield Bash that can knock back players.

2. Infernal Wraiths  
   - Mechanics Fly above players and can cast Shadow Flame, dealing damage over time in a targeted area.

Final Boss Zarok, the Shadowbinder
- Skills
  - Shadow Bind Roots players in place and deals damage over time.
  - Summon Shadows Calls forth Shadow Stalkers and Wraiths to fight.
  - Ultimate Realm of Darkness Envelops the area in darkness, causing all players to lose sight of each other and suffer damage until they defeat the summoned shadows.

---

### Unique Mechanics for Boss Fights
- Environmental Hazards Each boss could create temporary hazards (e.g., areas of shadow that cause debuffs).
- Phases Bosses could change their attack patterns and summon additional minions at specific health thresholds.
- Debuff and Buff Mechanics Players may gain or lose buffs based on their performance, encouraging teamwork and strategy.
