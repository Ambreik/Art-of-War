			Naga Kingdom: Vashthara

	Location : Vashthara is an underwater kingdom located directly north of "Netar Island" and south of the "Outland Territories". This strategic positioning allows
the "Naga" to control important sea routes and pose a significant threat to both human settlements and adventurers traveling between the two lands.

	Overview : "Vashthara", also known as the "City of the Abyss", is a dark and foreboding realm constructed among the coral reefs and hidden caverns of the ocean 
floor. Illuminated by eerie bioluminescent flora and the flickering light of magical crystals, the architecture is a blend of monstrous and beautiful designs, 
featuring twisted coral spires and organic shapes that reflect the kingdom's sinister nature. As creations of the "Horras", these "Naga" are formidable foes, 
driven by a desire for revenge against humanity for past grievances.

---

		Areas of Vashthara

1. "Coral Throne Hall"  
	- "Location" : Central hub of "Vashthara", situated deep within the coral formations, accessible through the main entrance of the kingdom.
	- "Description" : A majestic hall where the "Queen" holds court, adorned with bioluminescent corals and guarded by elite "Naga warriors".

2. "The Abyssal Garden"  
	- "Location" : South of the "Coral Throne Hall", surrounded by thick kelp forests.
	- "Description" : A lush underwater garden where rare plants thrive. Known for its healing properties, it is tended by the high priests and priestesses.

3. "The Ruins of Aqualis"  
	- "Location" : East of the "Coral Throne Hall", hidden behind a curtain of algae.
	- "Description" : Ancient remnants of a once-great "Naga civilization", filled with forgotten treasures and secrets of the past.

4. "The Caves of Echoes"  
	- "Location" : Northwest corner of "Vashthara", accessible through a narrow passageway.
	- "Description" : These caves are known for their haunting echoes and magical acoustics, often used for ceremonial chants.

5. "The Shimmering Grotto"  
	- "Location" : Northeast of the "Abyssal Garden".
	- "Description" : A dazzling cave filled with sparkling minerals and luminous jellyfish, rumored to hold hidden treasures.

		Hidden Areas of Vashthara

1. "The Trench of Lost Souls"  
	- "Location" : To the south of "Vashthara", bordering the "Great Sea".
	- "Description" : A treacherous trench where lost sailors’ spirits are said to roam. It's avoided by most "Naga" due to its dark reputation.

2. "The Glimmering Falls"  
	- "Location" : Southwest of the "Coral Throne Hall".
	- "Description" : An underwater waterfall that cascades into a hidden lagoon, where rare fish can be found.

3. "The Glooming Caverns"  
	- "Location" : Near the entrance to "The Depths of Vashthara".
	- "Description" : Dark caverns that house many dangers and rewards, including rare crafting materials.

4. "The Sunken Shipwreck"  
	- "Location" : East of the "Ruins of Aqualis".
	- "Description" : A sunken vessel filled with lost treasures, guarded by aggressive sea creatures.

5. "The Pool of Visions"  
	- "Location" : In the depths of the "Abyssal Garden".
	- "Description" : A mystical pool where "Naga" can glimpse their futures or receive guidance from "Horras".

6. "The Guardian’s Chamber"  
	- "Location" : In the "Glooming Caverns".
	- "Description" : A hidden chamber containing a powerful guardian creature. A treasure chest lies here, filled with artifacts of old.

		Treasure Chests Locations

1. "Coral Throne Hall" : Behind the throne, hidden by a magical barrier.
2. "The Abyssal Garden" : Underneath a large, ancient stone marked with runes.
3. "The Ruins of Aqualis" : Within a crumbled archway.
4. "The Caves of Echoes" : At the bottom of the cave, accessible by solving an acoustic puzzle.
5. "The Shimmering Grotto" : Behind a waterfall made of shimmering water.
6. "The Trench of Lost Souls" : At the very bottom of the trench, guarded by spirits.
7. "The Glimmering Falls" : At the base of the falls, camouflaged with rocks.
8. "The Glooming Caverns" : In a side tunnel, guarded by traps.
9. "The Sunken Shipwreck" : Inside the captain’s cabin, requiring a key held by a mini-boss.
10. "The Pool of Visions" : On the shore of the pool, it can only be opened after a vision is received.
11. "The Guardian’s Chamber" : Within the chamber, protected by the guardian creature.

		The Depths of Vashthara (Dungeon)

- "Description" : A vast, intricate dungeon that winds deep beneath "Vashthara", filled with perilous traps, lurking monsters, and valuable treasures. It can be 
accessed from multiple points within Vashthara but is most commonly entered through the Glooming Caverns. 

- "Level Range" : Designed for players between levels 70 and 90, offering a challenging experience with both environmental hazards and fierce Naga guardians.

- "Key Features" : 
  - Multiple levels with different biomes (e.g., dark caverns, flooded ruins).
  - Mini-boss encounters that test players’ strategies and skills.
  - A final boss room that culminates in a battle against a powerful manifestation of Horras's will, granting unique rewards upon defeat.

---

		Lore and History

- "Creation Myth" : "Vashthara" was founded by the first "Naga" queen, a creature of vengeance brought to life by the "Horras". After being cast out by surface 
dwellers, she rallied her kin to create a kingdom that would rise from the depths and reclaim the ocean for their kind.
- "Current Events" : The "Naga" are actively plotting against humanity, seeking to expand their territory and retaliate for past betrayals. Players will encounter 
"Naga" raiders on the seas and face threats from within "Vashthara" as they navigate the waters.
- "Naga Society" : The "Naga" operate under a strict hierarchy, with a ruthless queen at the top, followed by her elite warriors and cunning schemers. Their society
is steeped in dark magic and a thirst for power, making them relentless foes to any who dare to challenge them.

---

		Gameplay Elements

- "Quests" : Players may engage in quests that involve thwarting "Naga" ambushes, retrieving stolen relics, or attempting to broker peace (or war) with the 
underwater kingdom.
- "Raids" : Epic encounters in the "Depths of Vashthara" will test players against powerful "Naga" warriors and legendary sea monsters, providing rich rewards for 
those who succeed.
