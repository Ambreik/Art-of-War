			Hidden Treasures in the Underwater Realm

	Mechanics : 
*Can be opened multiple times a day, by the same player, only chests that require the "Lockpicking" skill.
*Players can open a maximum of 3 treasure chests that doesnt require a "Lockpicking" skill.
*Some of the chests, spawn in a random place in their specific location, using a wider area where they spawn.
*Every Treasure Chest, can contain 1 to 3 items as reward with a random chance to pick anything from its own "LootTable".

1."Coral Crown"
	- Location : Beneath the "Coral Spire".
	- Level Requirement : 40.
	- Lockpicking Required : No.
	- Creature Inside : None.
	- *Item Found : 
		- Coral Enchantment Gem
		- Gold (1.5G)
		- Potion of Underwater Breathing
		- Deepsea Herb
		- Runes of the Coral King (quest starter)
