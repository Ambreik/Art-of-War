			Treasure Chest Locations

---

1. "Coral Throne Hall" :  
	Location : Hidden behind the "Naga Queen's" throne, secured by a magical barrier only dispelled by completing certain in-game quests or meeting faction 
requirements.
	Description : This chest contains regal items befitting the "Coral Throne", such as rare jewels, "Naga relics", and possibly hints toward "Naga lore" and 
"Horras'" influence.

2. "The Abyssal Garden" : 
 	Location : Buried beneath a massive ancient stone etched with runes, symbolizing protection and legacy.
	Description : Unlocking this chest involves solving a puzzle that references ancient "Naga symbols". Rewards include plant-based potions, rare crafting items, 
and possibly a lore scroll related to "Horras" and the "Naga's origin".

3. "The Ruins of Aqualis" : 
 	Location : Nestled within the archway of a crumbling, algae-covered structure, blending seamlessly with its surroundings.
	Description : Contains artifacts from "Aqualis’" prime, like enchanted jewelry or weaponry. There is a chance for players to discover relics that hint at the 
fall of "Aqualis" and its connection to the "Naga's" undersea rise.

4. "The Caves of Echoes" :  
	Location : Found at the cave's depths, accessible only by solving an intricate acoustic puzzle that involves mimicking the echoes of the cave.
	Description : This treasure contains unique sound-based relics or items that enhance auditory skills, as well as precious stones. The chest is cloaked by a 
siren-like melody, adding to the mysterious ambiance.

5. "The Shimmering Grotto" :  
	Location : Hidden behind a cascading waterfall of shimmering, otherworldly water, which distorts visibility.
	Description : This chest holds rare bioluminescent items, ideal for underwater navigation. It may also include potions that boost visibility in dark or dimly 
lit environments, enhancing the exploration of other hidden areas.

6. "The Trench of Lost Souls" :
	Location : Located at the trench’s deepest point, surrounded by the spectral guardians of lost sailors.
	Description : Accessing this chest requires a blessing to repel the spirits. It holds powerful spiritual artifacts that grant resistance to dark magic and can 
be invaluable for quests involving "Naga lore" or "Horras' powers".

7. "The Glimmering Falls" :  
	Location : Camouflaged among rocks at the base of the underwater waterfall.
	Description : This chest contains rare fish, aquatic potions, and items that grant brief spells of invisibility. It's a well-guarded treasure, blending into 
the falls, making it both a valuable and elusive find.

8. "The Glooming Caverns" : 
	Location : Tucked away in a narrow side tunnel lined with lethal traps.
	Description : Requires players to disable traps for access. This chest offers rare minerals for crafting and unique armor enhancements, adding significant 
defensive stats for combat against "Naga forces".

9. "The Sunken Shipwreck" : 
	Location : Located in the captain's cabin, accessible only with a key held by a mini-boss encountered elsewhere in "Vashthara".
	Description : This chest includes nautical treasures like maps, silver, and rare seashell amulets that boost underwater agility. The mini-boss drops clues 
about "Horras’" influence over the "Naga domain".

10. "The Pool of Visions" :  
	Location : Placed by the pool’s edge, which can only be opened once a prophetic vision is received from "Horras" himself.
	Description : This chest rewards players with vision-based abilities or guidance scrolls for upcoming quests. The vision itself may hint at "Vashthara’s" future
or "Horras’ plans", offering insights into the lore.

11. "The Guardian’s Chamber" : 
	Location : Within the "Guardian’s Chamber", fiercely protected by the creature that guards this revered space.
	Description : This chest contains ancient relics, powerful weapons, and unique artifacts once wielded by the original "Naga" protectors of "Vashthara".
Obtaining this treasure requires players to either defeat or negotiate with the guardian, making it one of the more challenging and rewarding chests in the region.

---

	These hidden chests throughout "Vashthara" not only serve as valuable finds but also deepen player immersion by weaving together challenges, unique
environmental themes, and lore pieces that emphasize "Horras’" creation of the "Naga" and the complex underwater kingdom they inhabit. 