			Item List for the Underwater Realm

1. "Leviathan Scale Armor"
	- Type : Heavy Armor
	- Level Requirement : 70
	- Description : Crafted from the scales of the "Darkscale Leviathan", this armor provides high physical defense and magic resistance. 
	- Price : 10x "Leviathan Scales", 5x "Abyssal Thread", 3x "Kraken Ink".
	- Origin : brought from "Elandra the Enchantress".

2. "Wraith Scale Armor"
	- Type : Medium Armor
	- Level Requirement : 75
	- Description : This armor is made from the scales of the "Shadow Wraith", granting enhanced shadow resistance and agility.
	- Drop : "Shadow Forge" in the "Depths".

3. "Siren's Call Vest"
	- Type : Light Armor
	- Level Requirement : 65
	- Description : This vest is adorned with charms and siren scales, enhancing charm resistance and providing light protection.
	- Crafting Ingredients : 5x "Siren's Scales", 3x "Mystic Stones", 2x "Ethereal Threads".
	- Crafting Location : "Enchanter’s Workshop".

4. "Inkveil Helm"
	- Type : Helmet
	- Level Requirement : 70
	- Description : A helmet crafted from the ink sac of the "Inkveil Leviathan", offering excellent visibility underwater and protection against blinding effects.
	- Crafting Ingredients : 3x "Ink Sacs", 5x "Abyssal Shells", 1x "Leviathan Scale".
	- Crafting Location : "Enchanter’s Workshop".

5. "Dragon Scale Plate"
	- Type : Heavy Armor
	- Level Requirement : 80
	- Description : Forged from the scales of the "Ancient Abyssal Dragon", this armor is highly resistant to elemental damage and physical attacks.
	- Crafting Ingredients : 12x "Dragon Scales", 6x "Abyssal Essence", 4x "Titanite".
	- Crafting Location : Legendary Forge in the Abyss.

6. "Shiverspine Bracers"
	- Type : Light Armor
	- Level Requirement : 68
	- Description : Made from the spines of the "Shiverspine Prowler", these bracers enhance agility and provide some poison resistance.
	- Price : 5x "Prowler Spines", 3x "Neurotoxin Glands".
	- Crafting Location : brought from "Elandra the Enchantress".

