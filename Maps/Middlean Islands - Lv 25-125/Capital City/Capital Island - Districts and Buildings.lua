			Capital Island Districts and Buildings
			
			
	West Side: The Angler’s Haven : 
The "West Side" celebrates fishing and maritime culture, creating a lively environment that caters to players with an interest in aquatic quests, trade, and 
exploration.

- "Anglers’ Shelter" : Owned by "Curty L." and his brothers, this large exhibit hall features a wide range of fishing displays, interactive stations, and historical
artifacts related to their affiliation as anglers. Players can learn fishing techniques, partake in seasonal fishing tournaments, and view rare fish specimens.
- "Fisherman’s Wharf" : The main hub for local fishermen, where players can assist in unloading catches, partake in trading fish, or join boat races and fishing challenges. "The Wharf" also features market stalls for marine equipment and seafood.
- "Sailor’s Tavern" A lively meeting place for sailors and adventurers, rich with maritime stories and legends. Players can gather here for seafaring quests or 
receive information on oceanic events and pirate sightings.
- "Dockworkers’ Guild" : A base for laborers and traders involved in "Capital Island’s" bustling maritime trade. Players can take up shipping and delivery quests, 
help maintain the dock facilities, or protect goods from sea-bound raids.
- "Delight Stall" : Run by "Arkan the Great", this vibrant stall offers a variety of regional snacks and specialty dishes, providing players with unique food items for temporary buffs or enhancements.
- "The Navigator’s Lodge" : A resource center for navigators, featuring maps, ship models, and guides for safe sea travel. It offers quests that involve exploring uncharted islands or discovering secret locations around "Capital Island".

---

	Central Side: The Heart of Governance and Defense : 
The "Central Side" is the administrative and military core, focusing on law, order, and the preservation of knowledge.

- "Mayor’s Throne" : The primary administrative building where the city’s officials convene. Players can engage in diplomatic quests, assist with governance tasks, 
or influence local laws affecting trade and security.
- "Great Library" : An impressive structure filled with ancient texts, tomes, and scrolls. Players can gain knowledge here to unlock abilities or access quests tied
to historical events, mythological lore, or ancient languages.
- "Military Barracks" : A fortified area where the city’s guards train and prepare for potential invasions.Players can participate in combat training, join patrols,
or complete quests related to city defense.
- "Guardhouse" : The main operation center for the city’s security force, where players can report crimes, take up bounty quests, or receive missions to protect 
vulnerable areas.
- "Temple of Virtue" : A grand temple that honors the city’s protectors and fallen heroes. Players can seek blessings here for luck and strength or undertake quests
that involve recovering lost relics.
- "Judgment Hall" : An imposing courtroom where criminals and rule-breakers are tried. This is a significant place for justice and serves as a reminder of the 
island’s strict adherence to order.

---

	East Side: The Academy of Development : 
The "East Side" is dedicated to education, skill enhancement, and character growth, providing opportunities for players to advance in various disciplines.

- "Academies of Combat Arts": These smaller huts are designed to train players in specific fighting styles. Each academy has unique quests for enhancing skills in the following arts:
   1. "Academy of Swordsmen"
   2. "Academy of Blood Knife"
   3. "Academy of Eternal Shield"
   4. "Academy of the Essence of Life"
   5. "Academy of Wrath Sword"
   6. "Academy of Night"
   7. "Academy of Purple Fangs"
   8. "Academy of Bow Master"
   9. "Academy of Eternal Scepter"
   10. "Academy of Bright Bell"
   11. "Academy of Mastery Guardians"
   12. "Academy of Nightfall Arrow"
   13. "Academy of Dancing Daggers"
   14. "Academy of Enchanted Fan"
- "Freshwater Plunge": Owned by "Carp Barbeau D.", this secluded area near the docks is where players can swim, practice water-based skills, or take quests that 
test their agility and endurance only for "Fisherman Profession Players".
- "Arena of Valor": A grand combat arena for player duels, sparring events, and official tournaments. Players earn rewards and titles by winning matches or climbing
the arena rankings. This is called also "Heroes' Ascent" and can be accessed also from "Hiel Mall" located in "Aarong Forest-Ancient City of Sankis".
- "Mystic Emporium": A store and training ground for magic users, offering unique spells, enchantments, and crafting materials for creating magical artifacts. The Emporium is also a place where players can receive mystic quests or access powerful potions.
- "Training Grounds": An open area for combat practice, where players can train with dummies, test weapons, or challenge NPCs to friendly duels. These grounds host regular combat tutorials and events to hone players’ skills.

