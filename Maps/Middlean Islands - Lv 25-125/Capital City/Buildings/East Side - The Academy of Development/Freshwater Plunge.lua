	3. "Caden Swiftcurrent"  
	- Role : Swimmer and Fisherman  
	- Description : A young, energetic fisherman, Caden spends his time practicing agility in the pool. He’s often found showing off by performing flips into the 
water or diving to retrieve items at the pool’s bottom. His lighthearted attitude and playful spirit add a sense of camaraderie to the space, making it feel more 
like a community of friends rather than just a training ground.

	4. "Elda Marshstrider"  
	- Role : Herb Collector  
	- Description : Elda is a quiet, focused herbalist who gathers aquatic plants from the freshwater pool for her potions. She’s usually seen sitting near the pool
with a collection of jars, carefully extracting herbs and algae. Although she doesn’t offer quests, she’s knowledgeable about the natural plants found around the 
area and sometimes shares tips with players on spotting rare herbs underwater.

	5. "Reefy the Otter"  
	- Role : Friendly Companion  
	- Description : A tame otter named Reefy often swims in the pool, delighting players with playful antics. He’s a favorite among the regulars, especially those 
looking to practice swimming alongside an agile creature. Reefy occasionally helps Fisherman Profession players with small tasks like finding underwater objects, 
making him a charming addition to the Freshwater Plunge atmosphere. 

	These NPCs add to the immersive, community-driven feel of "Freshwater Plunge", giving players both helpful trainers and friendly faces to make their training 
experience enjoyable.