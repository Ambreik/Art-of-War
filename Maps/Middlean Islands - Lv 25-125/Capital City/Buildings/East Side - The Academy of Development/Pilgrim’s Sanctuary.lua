			Pilgrim’s Sanctuary

	"Pilgrim’s Sanctuary" is a place of calm and mystery, designed for players seeking solace, wisdom, and guidance. Nestled amid ancient oaks and blooming flora, 
the sanctuary’s exterior is built from pale stone, intricately carved with symbols of unity,peace, and transcendence. The walls are adorned with gentle vines and 
flowers,giving the building an aged yet serene look. At the top, a domed roof crowned with a radiant crystal catches sunlight, illuminating the sanctuary with a 
soft, divine glow even during nighttime.

	Inside, the sanctuary is open and inviting, with spacious halls and circular rooms dedicated to meditation and reflection. Natural light filters through stained
glass windows, casting colorful patterns on the stone floor, while faint incense adds a calming fragrance. Soft, ambient sounds of chimes and nature fill the air, 
creating an atmosphere of peaceful introspection. At the heart of the sanctuary is a circular fountain with healing water, where players can sit, recharge, and 
meditate. 

		The sanctuary’s five NPCs each provide unique experiences:

	1. "Elder Maridia"
	Elder Maridia is a wise and introspective NPC who has a reputation for her calm demeanor and philosophical outlook. With flowing silver hair and gentle eyes, 
she wears simple robes adorned with symbols of ancient wisdom, giving her an aura of quiet strength. Maridia’s role as a guide leads players to reflect on the 
natural balance and interconnectedness of the World Game. Her speech is poetic and cryptic, leading players to think deeply about her words. 

	Questline: "The Path of Insight"
	1. Quest : "Gathering the Elements"  
	Maridia starts the player’s journey with a simple yet profound task. She instructs players to collect items representing the four classical elements: Earth, 
Water, Fire, and Air. She hints that these can be found in various faction-controlled lands and near elemental hotspots. For example, players might be directed to 
"Crystal Flower Lake" in the "Snowfall Plains" for Water, or to gather "Flame Crystals" from "Emberwaste" in the "Tenebress Territory".  
	Reward : Minor elemental resistance potion and a scroll of wisdom.

	2. Quest: "The Hidden Grove"  
	Maridia asks the player to journey to "Farellia Plateau" in "Aarong Forest". There, they must locate an ancient, concealed grove known for its rare flora. She 
hints at a specific time of day for finding the grove, encouraging players to observe the natural world. Upon arrival, players are tasked with retrieving a rare 
herb for Maridia’s personal ritual of renewal.  
	Reward : Recipe for a unique health-boosting elixir, plus increased reputation with Maridia.

	3. Quest: "The Soul of the Land"  
	As the final task, Maridia requests that players meditate at "Thunder Inn" in the "Frozen Forest" to experience the essence of the land. After meditating, 
players confront a spirit of the land in a vision, which reveals part of the World Game’s hidden lore. This experience grants players insight into the ancient war 
between gods and mortals.  
	Reward : Title "Seeker of Wisdom" and a rare potion recipe.

---

	2. "Guardian Harlos"
	Guardian Harlos is a figure of resilience and valor, clad in battle-worn armor with scars that speak of his past confrontations with dark forces. With a firm voice and a sense of duty, Harlos provides quests that challenge players to protect and reclaim sacred spaces from corruption.

	Questline: "Defender of the Sacred"
	1. Quest: "The Lost Shrine"  
	Harlos tasks the player with purging a shrine located in "Balcombe" of "Timberthorn", which has fallen to a dark spirit. He provides holy water and a protective
charm, cautioning players to stay vigilant. After cleansing the shrine, players must face a spectral guardian to prove their worth.  
	Reward : Holy relic (increases armor for PvM encounters) and minor faction reputation.

	2. Quest: "Guardians of the Hidden Way"  
	Harlos sends players to protect a hidden passage in "Highland". Here, players encounter a faction of NPCs who seek to control access to ancient lore. The quest 
demands players hold the area for a specified duration against waves of enemies.  
	Reward : Armor fragment, which when combined with other fragments, can be crafted into a unique piece.

	3. Quest: "Call of the Eternal Flame"  
	For the final quest, Harlos entrusts players with a mission to retrieve an Eternal Flame from the "Glimmering Spring" in "Bartallin Peak". The journey is 
perilous, requiring players to solve puzzles and defeat a powerful fire elemental that guards the flame.  
	Reward : Symbol of Valor (boosts PvP resilience) and special faction commendation.

