	3. "Historian Jaren"
	Historian Jaren is an elderly NPC who spends his days in the sanctuary’s study, surrounded by scrolls and relics from the past. Jaren is passionate about 
recounting the events of the "Second Great War", offering players vivid retellings of battles, alliances, and legendary figures.

		Storyline: "Echoes of the Second Great War"

		Chapter 1: "Rising Tensions"

	The first chapter of Historian Jaren’s story delves into the period before the "Second Great War", a time of strained relationships and growing fears among 
factions. As he recounts, the seeds of conflict were sown over decades, beginning as minor disputes over resources and territory but eventually morphing into open hostilities. 

	1. "The Sparks of Dispute"  
	Jaren describes how early animosities grew from a series of misunderstandings, border clashes, and competing resources, particularly in "Timberthorn" and
"Lagooma Jungle", which were highly contested for their natural wealth. The Knights of Valor and the Pindars, factions with longstanding grievances, were embroiled 
in skirmishes that occasionally flared into larger conflicts. Even in relatively neutral territories like "Aarong Forest", rumors of spies and saboteurs abounded, 
heightening the atmosphere of distrust.

	2. "The Role of Mysterious Powers"  
	Jaren hints at dark forces in the background, manipulating events and fanning the flames of discord. Some factions began to fear that an ancient evil, believed 
to have been vanquished long ago, was quietly rising again. Players learn that whispers of these shadowy figures haunted the realms of the underworld, perhaps an 
early warning sign of Flammar’s influence. Prophecies from seers and religious figures warned of a coming storm, further unsettling the peace.

	3. "Failed Diplomacies"  
	Historian Jaren’s voice grows somber as he recounts the tragic events that unfolded in the foreboding "Nakazamian Castle". He explains how, in a last-ditch 
attempt to prevent the impending war, representatives from the major human factions were forced to negotiate at "Nakazamian Castle", despite the dangers. This
ancient stronghold, nestled deep within hostile terrain and under the dominion of "Thundralis" and his thunderous minions, was known for its harsh climate and 
perilous entryways, accessible only through narrow mountain passes. However, its neutrality—and the fear it inspired—was what made it the only possible meeting 
ground for such rival factions.

	A Place of Dark Omens : 
	Jaren describes how, upon their arrival, the envoys felt a palpable sense of dread. The castle was cloaked in constant storms, as if "Thundralis" himself were 
summoning lightning to remind his guests of his power. As they entered the ominous halls of "Nakazamian", the sound of thunder echoed through the walls, each strike
unsettling even the bravest representatives. end 

	The Sabotage :
	The tension inside "Nakazamian Castle" grew when key representatives began to fall ill under mysterious circumstances. Jaren reveals that it was later 
discovered that agents of shadowy forces had infiltrated the ranks of the diplomats, poisoning their food and spreading distrust. Though the human representatives 
tried to maintain their composure, whispered accusations began to fill the hall, fragmenting the already-tense alliances between factions.

	The Assassination Attempt : 
	The final blow came during a secret midnight meeting, where one of the most influential envoys—General Orith, a noble figure from the Knights of Valor—was 
ambushed in his quarters by an assassin cloaked in shadows. Though Orith survived, the attack left him gravely wounded, and suspicions of betrayal rippled through 
the ranks of the diplomats. Some factions blamed the others, claiming that it was a plot to weaken their power; others accused Thundralis of using the meeting as a 
way to eliminate human leaders and cement his control over the outland territories.

	The Fall of Diplomacy : 
	The morning after the attempt on General Orith’s life, the representatives abandoned any hope of peace. Forced to retreat from "Nakazamian Castle" under 
Thundralis watchful gaze, each faction left with renewed resolve to bolster their armies rather than their alliances. Jaren recounts how Thundralis, watching from 
the highest tower, seemed to revel in the humans' failure, as if he had foreseen the inevitable war they would bring upon themselves.

---

		Chapter 2: "The Great Alliance"

	The second chapter shifts from division to unity, recounting how, despite their differences, the mortal factions eventually found common ground when facing a 
greater enemy. This section introduces many legendary figures and heroes whose courage and sacrifices turned the tide of the conflict.

	1. "Unexpected Alliances"  
	Jaren narrates the story of how disparate factions such as the Knights of Valor, the Pindars, and even rogue factions like the Outlaws joined forces under a 
common banner. Many of these alliances were formed out of necessity, and for some, loyalty to the land outweighed factional pride. Players are introduced to the 
story of "General Alithar", a revered knight who famously forged the initial alliance, and **Elder Fenris**, a shaman who acted as a mediator and guide, bridging 
the gap between warriors and mystics alike.

	2. "The Role of the Gods"  
	At this point, Jaren touches upon the intervention of gods such as Thundralis and Horras. Recognizing that mortal efforts alone might be insufficient to curb 
the looming threat, these gods provided boons, enchanted weapons, and, in some instances, divine insight. Horras, the God of Water, offered guidance to those 
defending "Fathomless Abyss", while "Thundralis" extended his protection over the fortress "Frozenwall Citadel". Their aid was invaluable but came at a price: any
who accepted divine assistance were bound to respect the gods’ ancient rules, even after the war ended.

	3. "The Turning Point"	  
	A critical event marked the shift from defense to offense. This was known as "The Siege of Emberwaste", where allied forces successfully reclaimed the region 
from enemy hands. Jaren describes the brutal battle scenes, with sky-darkening flames and desperate last stands. Heroes rose to fame here, particularly "Captain Lyra", who led the vanguard, and "Mage Eldros", who called upon forbidden magic to protect his comrades. The success of this siege lifted morale and inspired other 
regions to rally under the Great Alliance’s banner.

---

		Chapter 3: "The Final Battle"

	Jaren’s final tale centers on the climactic battle that brought the Second Great War to a close, a moment of both victory and sacrifice that solidified the 
alliances and shattered the ambitions of their foes.

	1. "Gathering of the Forces"  
   In preparation for the final battle, the Great Alliance amassed its forces at "Bloodrock Peak", the highest point in "Winterspring Peaks". The atmosphere was 
tense as soldiers, mages, and rogues from across the realms awaited the signal to march. Jaren notes that the tension was compounded by the arrival of "Harlet", a 
semi-god whose motives were uncertain. Though Harlet joined the fray, many feared his intentions, yet none dared reject his power.

	2. "Battle of the Abyssal Gorge"  
	Jaren recounts the battle in "Abyssal Gorge", a haunting, desolate land at the edge of Tenebress Territory, where Flammar’s legions, led by the 7 Bullets of 
Horror, made their last stand. Here, the forces of darkness clashed violently with the armies of the alliance. Jaren details the intense encounters between mortal 
champions and the 7 Bullets, including "Bosniar", the axe-wielding beast who wreaked havoc before being brought down by a coordinated attack. Players learn that 
legendary artifacts like the "Ebon Spear" were wielded here, artifacts that vanished after the battle, rumored to lie hidden somewhere within the abyss.

	3. "Sealing Flammar"  
	As the alliance pushed forward, it became evident that Flammar himself was leading the enemy. In a breathtaking moment, "Zarrah, the creator god", appeared, 
summoned by mortal prayers and sacrifices. Zarrah unleashed divine energies, helping to weaken Flammar. The players learn that "Necrokeep", a dark, mystical 
fortress, was specially constructed to imprison Flammar and prevent his return. In a ritual that nearly cost the lives of all involved, the alliance managed to seal
Flammar in Necrokeep, banishing him to the Underworld Realm.

	4. "Aftermath and Sacrifices"  
	Jaren’s tale ends on a somber note, reflecting on the many lives lost and the scars left on the world. Major figures who led the Great Alliance either perished 
in battle or withdrew into seclusion. Monuments to the fallen were erected in places like "Golden Spear Barracks" and "Northshire Fryy", reminders of the sacrifices
made. The alliances forged during the war became the foundations of current political boundaries and alliances in the game world.

