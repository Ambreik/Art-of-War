			Adventurer’s Guild

	The "Adventurer’s Guild" is a striking, well-fortified building exclusive to "Bounty Hunters", players who seek both fame and fortune through tackling bounties 
and completing high-risk quests. The Guild is purposefully designed to inspire both camaraderie and a sense of adventure, with its impressive facade and practical, 
welcoming interior.

		Exterior Description : 

	The "Adventurer’s Guild" stands as a symbol of resilience and daring. Built from thick, dark stone blocks, the guild building exudes strength, with reinforced 
iron accents and intricately carved wooden panels that depict scenes of legendary hunts and epic battles. A large, arched entryway invites players to step into the 
halls of the guild, marked by a banner emblazoned with the Bounty Hunters’ insignia—a symbol of crossed swords over a blazing sun, symbolizing both vigilance and 
honor.
	Above the main door hangs a large, iron-forged crest of the guild, and two flickering torch sconces stand guard, adding to its formidable yet enticing 
appearance. On each side of the building, tall stained-glass windows depict mythical beasts and heroic figures, their vivid colors casting an enchanting light at 
dusk and dawn. In front of the entryway, two statues of fierce warrior figures hold watchful poses, representing the bounty hunters' unyielding dedication to 
justice.

		Interior Description

	Upon entering, players are greeted by a grand hall bustling with life and decorated with trophies of past hunts—massive horns, beast skulls, and rare pelts 
drape the walls, giving the place a rugged yet majestic aura. Dark wood floors lead to a large, circular stone hearth in the center of the room, surrounded by long 
wooden tables where bounty hunters discuss their quests and strategies. Overhead, chandeliers made from antlers cast a warm, amber glow, filling the room with an 
inviting yet rustic ambiance.
	The hall branches off into several sections: a notice board area filled with requests and bounties, a resting area with comfortable leather armchairs and low 
tables, and a display case containing relics and mementos of past hunters. Shelves line the walls, containing rolled maps, rare monster manuals, and collections of 
bounty hunting lore.

