		NPCs

	1. "Administrator - Elara Ironhand"  
	"Elara Ironhand", a skilled and sharp-witted administrator, oversees all payments and bounty distributions within the guild. Known for her no-nonsense approach,
she has a reputation for being extremely efficient and fair, ensuring that each bounty hunter is rewarded based on their accomplishments. Elara is a tall, stern 
woman with a commanding presence, her dark hair tied back in a tight braid, and she often wears a tailored leather outfit adorned with small pouches and a utility 
belt, symbolizing her hands-on involvement in the guild’s operations. Behind her desk, she keeps records meticulously organized, and she’s always ready to provide 
advice or a quick debrief to anyone who approaches her.

	2. "Collector - Alden Greaves"  
	"Alden Greaves" is the collector, responsible for tracking all the information regarding outlaw bounties and high-ranking boss rewards. A calm and studious man 
with a keen eye for detail, Alden wears a long coat and round glasses, constantly studying documents and maps. He keeps a carefully curated list of wanted players 
and dangerous monsters, meticulously recording new sightings and confirmed captures.He speaks in a low, thoughtful tone, and his desk is piled with notes, sketches,
and a large world map pinned with various markers, representing the last known locations of elusive targets.

	3. "Captain - Ryoko Serian"  
	"Ryoko Serian", the guild’s captain, is a legendary figure in his own right. As the one who created the bounty hunting system, Ryoko is both revered and 	
respected among guild members. Clad in an armor set that resembles a samurai suit—minus the helmet—he is always prepared to defend the guild should trouble arise. 
His armor is crafted from dark metal with intricate engravings, and he wields two sleek, curved swords (similar to ninjatō) that rest sheathed at his left side, 
symbols of his unmatched skill in battle. Though quiet and enigmatic, Ryoko’s mere presence commands respect, and his dedication to keeping the peace and upholding 
justice is unwavering. With deep wisdom and a lifetime of stories, he occasionally shares tales of his past adventures with those who seek his guidance.

	Together, the "Adventurer’s Guild" and its remarkable NPCs provide players with not only practical support for their bounty-hunting missions but also an 
inspiring atmosphere that emphasizes the honor and camaraderie of the guild.