	3. "Marshal Cedric Arwell" is the vigilant overseer of "Capital City’s" security, renowned for his unwavering commitment to safeguarding its residents. With a 
reputation for strategic thinking and discipline, Cedric is responsible for upholding law and order, protecting city boundaries, and responding swiftly to emerging 
threats. His leadership is both respected and feared by those who would bring harm to the city.

		Role and Responsibilities
- City Boundary Security : Marshal Cedric organizes patrols along city borders and monitors for any unusual activities or breaches.
- Law Enforcement : He enforces city laws, cracks down on criminal behavior, and coordinates with guards to maintain public order.
- Threat Intelligence : Cedric is dedicated to identifying potential threats, both internal and external, and taking proactive measures to prevent attacks or chaos.

		Quests Offered by Marshal Cedric Arwell

	1. Quest: "Border Watch"
	- Objective : Marshal Cedric assigns players to patrol a stretch of the city’s outer boundary and report any suspicious activity.
	- Details : Players join a group of guards to inspect checkpoints and handle encounters with rogue bandits or suspicious individuals. The quest reinforces the 
city’s defenses by gathering valuable intelligence on potential threats.
	- Reward : "Guard’s Insignia" (provides a small boost to defense) and Reputation Points with the city guard faction.

	2. Quest: "The Stolen Ledger"
	- Objective : A critical ledger containing records of all recent patrols and guard activities has gone missing. Cedric tasks players with investigating and 
retrieving it.
	- Details : Players follow a trail of clues across the city, uncovering a hidden network of thieves aiming to compromise city security. Successfully retrieving 
the ledger reinforces Cedric’s trust and grants players an exclusive reward.
	- Reward : "Spyglass of Insight" (enhances detection of hidden or stealthy enemies) and additional Reputation Points with city security forces.

	3. Quest: "Crackdown on Crime"
	- Objective : Marshal Cedric launches a crackdown on criminal activity within Capital City and needs players’ assistance to target hotspots.
	- Details : This multi-part quest takes players into city alleys, taverns, and markets to gather intel, apprehend known criminals, and disrupt black-market 
dealings. Players will need diplomacy and combat skills to handle this mission.
	- Reward : "Lawkeeper’s Amulet" (small increase to reputation gain in the city) and a notable boost in Reputation Points with Cedric and his network.

	4. Questline: "Echoes of War"
	- Objective : Cedric senses mounting threats beyond city boundaries and organizes a series of preemptive operations. Players assist in scouting, identifying 
potential allies, and preparing defenses.
	- Details : Players participate in reconnaissance missions, negotiate with nearby factions, and set up defensive structures along the perimeter. This questline 
challenges players to build Cedric’s trust through skill and strategy.
	- Reward : "Marshal’s Mark" (a medal granting access to exclusive quests and privileges within the city) and significant Reputation Points, establishing players 
as trusted allies in Cedric’s ranks.

	5. Daily Task: "Neighborhood Patrol"
	- Objective : Cedric assigns players daily patrols in various neighborhoods, ensuring peace and order.
	- Details : Each day brings a new patrol task, from mediating disputes to investigating minor disturbances. Completing these tasks helps players steadily build 
reputation and gain familiarity with the city’s layout.
	- Reward : Modest Reputation Points and a "Daily Ration Kit" (small buff for health or energy regeneration).

	"Marshal Cedric Arwell" provides a key connection to the city’s security, allowing players to experience a blend of tactical combat, diplomacy, and investigative 
work. Through Cedric’s tasks, players contribute to the city’s peace and stability, gaining a respected position among "Capital City’s" defenders and access to 
specialized rewards that enhance their standing in city law enforcement.

