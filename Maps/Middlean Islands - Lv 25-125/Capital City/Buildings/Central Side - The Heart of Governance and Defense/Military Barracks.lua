	3. "Quartermaster Harlan Drake" is the meticulous and resourceful overseer of the "Military Barracks’" inventory and armory. Known for his keen eye and 
dedication to order, he manages supplies for the city’s guard and ensures they are always well-equipped. With years of experience, "Harlan" is trusted to maintain 
the barracks’ vast inventory, which includes a diverse range of weapons, armor, and provisions essential for the city’s defense. Although often gruff, he appreciates 
capable hands and rewards those who help him maintain his high standards.

		Role and Responsibilities
- Inventory Management : Harlan is responsible for keeping detailed records of the supplies, managing everything from daily essentials to high-value military 
equipment.
- Armory Organization : Harlan ensures that weapons and armor are properly maintained, sorted, and ready for deployment.
- Supply Resourcing : When supplies run low or specific items are needed, Harlan arranges for their procurement, sometimes requiring assistance from adventurers to 
collect or deliver them.

		Quests Offered by Quartermaster Harlan Drake

	1. Quest: "Reinforce the Rations"
	- Objective : Harlan needs help resupplying essential provisions for the guards, such as food, water, and basic medical supplies.
	- Details : Players are tasked with collecting supplies from nearby traders or even venturing out to gather certain ingredients themselves. Along the way, they 
may encounter bandits attempting to steal the provisions.
	- Reward : "Quartermaster’s Pack" (a consumable pack with minor health potions or rations) and "Reputation Points" with the city guard.

	2. Quest: "Sharp and Ready"
	- Objective : Harlan requests assistance in sharpening and maintaining the barracks’ weaponry to ensure they are battle-ready.
	- Details : Players work alongside Harlan, sharpening blades and performing minor repairs on damaged equipment. This quest offers a unique look into the 
importance of equipment upkeep, along with some hands-on practice.
	- Reward : "Maintenance Kit" (a one-time-use item that allows players to temporarily boost their weapon’s durability in the field) and Reputation Points.

	3. Quest: "Special Delivery"
	- Objective : The city guards have requested specialized equipment that Harlan has ordered from a distant supplier. Players are tasked with retrieving it safely.
	- Details : Players must travel to a nearby location, retrieve the ordered supplies, and transport them back to the barracks. They’ll likely face ambushes or 
natural obstacles, challenging them to defend the goods.
	- Reward : "Guard’s Band" (a ring that provides a slight boost to physical defense) and additional "Reputation Points".

	4. Questline: "The Armory’s Secrets"
	- Objective : In a multi-step questline, Harlan entrusts players with collecting rare materials for crafting unique weapons and armor upgrades for the guard.
	- Details : Players are sent to gather rare metals, magical essences, and other valuable materials scattered across the realm. This questline not only deepens 
players’ involvement with Harlan but also uncovers lore about the barracks and the unique equipment the guards use.
	- Reward : "Guard’s Choice Gear" (a special set of equipment tailored for players, providing minor bonuses to defense or attack) and a significant boost in 
"Reputation".

	5. Daily Task: "Inventory Check"
	- Objective : Harlan requires regular help with organizing and checking the barracks’ inventory, ensuring everything is accounted for and properly stored.
	- Details : Players assist Harlan by conducting a detailed inventory check and reorganizing supplies. Occasionally, they may encounter misplaced items or 
identify items in need of urgent repair.
	- Reward : Small Reputation Points increase and consumable "Harlan’s Whetstone" (boosts weapon sharpness for a short duration).

	"Quartermaster Harlan Drake" brings a structured, logistics-focused quest experience to players, emphasizing resource management, equipment upkeep, and the 
critical role of supply lines in defending the city. His quests allow players to engage directly with the city’s military infrastructure, gaining useful items and 
progressing their reputation within the guard.

