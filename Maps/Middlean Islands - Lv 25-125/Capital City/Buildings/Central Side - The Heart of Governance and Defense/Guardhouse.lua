	3. "Tracker Gareth Hollis" is an invaluable asset to the city’s security network, specializing in tracking and locating Outlaws who may pose a threat to the 
peace of "Capital City". Stationed within the "Guardhouse", Gareth is a grizzled, seasoned tracker whose expertise in tracing footsteps and piecing together clues 
has made him renowned among the city’s defense forces and adventurers alike.

		Personality and Background
	Gareth’s keen senses and strategic mind were honed from years spent navigating rugged terrains and dense forests, which made him the perfect fit for tracking 
Outlaws and other potential threats within the city. While he is generally reserved and focused, Gareth has a passion for sharing his knowledge with those who show 
a serious interest in tracking and bounty hunting. His no-nonsense demeanor and dedication to his craft have earned him respect among both guards and citizens, as 
he always strives to keep the city safe from lurking dangers.

	Though he’s not particularly chatty, he’s known to open up to those who prove themselves worthy of the challenge of bounty hunting. Gareth is the go-to figure 
for any information on recent Outlaw sightings, and he’s always a step ahead when it comes to identifying hidden threats.

		Role and Functions**

- Outlaw Sightings Information : Gareth is updated on all recent Outlaw sightings and keeps a record of these reports. Players interested in bounty hunting or 
tracking dangerous individuals can come to him for the latest intel on Outlaw movements around "Capital City".
- Bounty Quest Trail Guidance : For those eager to earn rewards by hunting Outlaws, Gareth offers guidance on where to start. He can provide detailed descriptions 
of potential hiding spots and advice on tracking techniques that will give players an edge.
- Tracking Lessons : On occasion, Gareth hosts small training sessions on basic tracking skills. He teaches players how to spot subtle signs, such as broken 
branches, footprints, and other clues that may lead to a hiding Outlaw or even to treasures hidden by them in the past.
- Outlaw Tracking Reports : Gareth regularly updates his log of sightings, providing players with insight into patterns and movement habits of certain Outlaws. This
information can be crucial for players looking to follow specific individuals or predict future hideout locations.
- Bounty Board Coordination : Though Lieutenant Rainey Briggs manages the bounty board, Gareth works closely with her to ensure accurate information is provided. He
also recommends specific Outlaws based on a player’s skill level, ensuring they aren’t overwhelmed by an opponent too strong for them.

