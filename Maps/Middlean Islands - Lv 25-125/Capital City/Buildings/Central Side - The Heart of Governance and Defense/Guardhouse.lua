			Guardhouse

	The "Guardhouse" is the heart of the "Capital City’s" security operations, functioning as both a coordination hub and an information center for players. Though 
not a source of quests, it serves an essential role in keeping order and providing support to players navigating city life.

		Functions of the Guardhouse

	1. "Crime Reporting"
	- Players can approach the Guardhouse to report any disturbances, suspicious behavior, or criminal activities they have witnessed or encountered. The Guardhouse
acts swiftly to dispatch guards or alert relevant authorities, helping to maintain the city's peace and safety.

	2. "Bounty Information"
	- The Guardhouse has an updated bounty board displaying profiles and recent locations of wanted outlaws. While bounty quests are offered through separate 
channels, the Guardhouse provides critical details on criminals at large, enabling players to stay informed on potential threats or valuable targets.

	3. "Guidance and Directions"
	- Guards stationed at the Guardhouse offer helpful information on the city’s layout, guiding players to specific buildings, shops, or NPCs. If players are 
searching for notable individuals like the Mayor or specific merchants, the Guardhouse is the ideal place to seek directions.

	4. "Outlaw Alerts"
	- The Guardhouse tracks recent sightings or reported activities of outlaws within or near the city. Players can inquire about recent alerts, making it easier to
avoid (or pursue) outlaws lurking in certain areas. Guards provide hints on potential hideouts or last-known locations, adding an extra layer of strategy for those 
involved in bounty hunting.

	5. "Daily Patrol Updates"
	- Information on current guard patrols and high-risk areas is available here, helping players identify safer routes or discover which neighborhoods are under 
watch. This function is particularly useful for Outlaws looking to avoid patrols or for bounty hunters aiming to coordinate with guards to catch criminals. end

		Guardhouse NPCs and Their Roles

	1. "Lieutenant Rainey Briggs" is a seasoned and highly respected member of the city’s security forces, known for her keen mind and unwavering dedication to 
maintaining order within the "Capital City". As the overseer of the "Guardhouse", she plays a vital role in ensuring the smooth operation of the city’s law 
enforcement activities, from updating the bounty board to coordinating patrols throughout the city’s districts.

		Personality and Background
	"Lieutenant Briggs" is sharp, calculated, and no-nonsense, yet she has a deep sense of duty to the city and its citizens. She’s often seen in the "Guardhouse", 
where her piercing eyes seem to constantly scan for the next threat or disturbance. Her reputation is built on her vast experience with tracking criminals, knowing 
the city’s underbelly better than anyone else, and making swift, decisive judgments during critical situations. 
	Before her rise to Lieutenant, Rainey spent years working undercover in the city’s criminal underground, gathering intelligence on the outlaws who wreak havoc 
in the city. Her work was instrumental in dismantling several notorious gangs, and her understanding of criminal psychology has made her invaluable to the 
Guardhouse.

		Role and Functions

- Updating the Bounty Board : Lieutenant Briggs is the one who keeps the bounty board in the Guardhouse up to date. She ensures that any new information about 
outlaws, notorious criminals, or high-profile targets is immediately displayed. Players can come to her for advice about which bounties are most lucrative or 
provide the best opportunities for gaining "Reputation Points".
- Coordinating Patrols : She oversees all patrol schedules and ensures that guards are stationed in the right areas of the city, particularly those with higher #
risks of criminal activity. Players can inquire about current patrols and learn which districts are under the heaviest surveillance.
- Tracking and Intelligence : With her extensive network of informants and her own experience as a field agent, Lieutenant Briggs maintains critical intelligence on
the whereabouts of wanted criminals and outlaws. Players looking to pursue a bounty or gain further insight into criminal activity can turn to her for leads.
- Mission Briefings : Lieutenant Briggs occasionally offers high-stakes missions related to city security, from tracking down escaped prisoners to uncovering 
corruption within the city's administration. These missions may have an impact on local law, order, and the player’s standing with various factions in the city.
- Criminal Psychology and Tactics : Having spent years studying criminal behavior, Lieutenant Briggs also provides players with tips and tactics on how to catch 
elusive criminals or how to avoid getting caught themselves. Her understanding of criminal operations and strategic thinking makes her an essential advisor for 
bounty hunters and law-abiding citizens alike.

