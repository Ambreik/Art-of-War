- "Merchants and Travelers" : Merchants often come here after unloading their goods at the docks, sharing stories of their travels across the seas. The tavern also 
serves as a meeting spot for adventurers, treasure hunters, and explorers who pass through "Capital City".
- "Locals" : Many of the city’s working-class citizens, especially those involved in the shipping or fishing industries, frequent the tavern for a drink after a 
hard day's work.

	Sailors and Pirates :
- "Sailors" :  
	The tavern is primarily filled with sailors who have just returned from long voyages or are preparing for the next journey across the seas. These patrons are 
	often rough, with weathered faces and sea-toughened hands. They wear worn clothes, stained by saltwater and wind, and they are constantly sharing tales of their
	adventures. Some are boisterous and loud, recounting daring battles with krakens or describing treasure-filled islands they’ve visited,while others are quieter,
	lost in thought about the sea's dangers.
	- "Personality" : They are typically hardworking and straightforward, but some can be a bit superstitious or aloof, especially if they’ve had a run-in with 
	dangerous sea creatures or treacherous weather. Sailors often form small groups or cliques, where they discuss their next trip or the latest gossip from the 
	docks.
	- "Interaction" : Players might strike up a conversation with a sailor to learn about distant lands or to receive a rumor about a hidden treasure. However, some
	sailors are more reserved and might require a few drinks before they open up.
  
- "Pirates" :  
	A less frequent but still present group, pirates tend to come to the tavern to lay low after a successful raid or to plan their next heist. These pirates are 
	often older and seasoned, with tattoos marking their past deeds, and they usually keep to the shadows, avoiding too much attention. Some wear long, billowy 
	coats, while others prefer more practical attire for the sea.
	- "Personality" : Pirates are charismatic, but their conversation can be dangerous. They love to boast about their exploits, but they also tend to be cautious, 
	knowing that their presence in the tavern could draw unwanted attention from the authorities. Some pirates might offer under-the-table work for adventurers 
	looking to make a quick profit.
	- "Interaction" : Players may be approached by pirates seeking a crew for a secret mission or given a "black market" item for a quest. But interacting with them
	could be risky, especially if the local authorities are nearby.

