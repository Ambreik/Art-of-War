			Anglers' Shelter
			
	Owner : Curty L. (Major NPC for Fisherman Profession Players)  
	Location : "West side of Capital City" (close to the coast or docks)

		Description :  
	"Anglers' Shelter" is a prominent institution in "Capital City", owned and managed by Curty L. and his brothers. The Shelter is a sprawling hall dedicated to 
the art of fishing, showcasing the history, techniques, and legendary catches of the "L. family". It is a sanctuary for those who take their fishing seriously, 
offering a wealth of knowledge and hands-on experience for anyone dedicated to the "Fisherman Profession".

		Key Features and Interactions:
	1. Fishing Displays :  
	- The Shelter houses countless displays of rare fish specimens from various seas, showcasing their origins and the methods used to catch them. It also includes 
historical fishing tools used by the "L. family", adding a rich, nostalgic feel for veteran anglers.
	2. Interactive Stations :  
	- Fishing stations where players can practice various techniques. These stations are essential for players looking to sharpen their fishing skills, whether it 
be casting, reeling, or using specialized bait for specific fish.
	3. Fishing Techniques :  
	- "Curty L." offers lessons on advanced fishing techniques unique to the local waters. From using specific baits to identifying fishing spots, these techniques 
are essential for mastering the "Fisherman Profession".  
	- For "Fisherman Profession Players", these lessons help unlock new fishing abilities and give insight into rare and legendary fish.
	4. Seasonal Fishing Tournaments :  
   - The Shelter hosts seasonal fishing competitions that provide "Fisherman Profession Players" with unique challenges and opportunities to show off their skills. 
These tournaments come with great rewards like rare fishing rods, hooks, and exclusive fishing spots only available to highly ranked players.
	5. Historical Artifacts :  
   - The Shelter displays ancient fishing relics and trophies from past generations of the "L. family". It includes unique artifacts like prized fishing rods, a 
collection of rare fish scales, and even journals detailing past fishing expeditions and secrets of the sea.

		Major NPC: "Curty L"
	- Role : Major NPC for "Fisherman Profession Players"  
	- Reputation System : "Curty L." will build a special "Reputation" (REP) with players who have chosen the "Fisherman Profession". As players assist him, 
complete fishing quests, and participate in events, they will increase their reputation with him, unlocking new rewards, fishing spots, and quests.
  
		Curty L.'s Reputation System:
	- Reputation Levels :  
		- Novice Fisherman : Accessible when players first begin the profession, offering introductory quests and basic fishing tips.
		- Skilled Angler : Once players complete certain tasks or catch specific rare fish, they reach this level, unlocking more challenging quests and higher 
rewards.
		- Grand Master Fisherman : The highest rank, earned after completing "Curty's" most difficult quests. At this level, players unlock access to the most 
exclusive fishing gear, secret fishing locations, and even the chance to fish alongside Curty L. during special events.

		Quests from Curty L.:

	1. "The Master’s Challenge"  
	- Description : Curty L. has been searching for the perfect fishing spot, but it’s located deep in a dangerous, uncharted area. He needs someone capable to 
retrieve a legendary fish known as the "Azure Leviathan."  
	- Objective : Catch the Azure Leviathan, a massive fish that only appears once every season in an unpredictable location.  
	- Requirements : Player must have achieved at least "Skilled Angler" reputation to unlock this quest.  
	- Reward : Rare fishing rod that boosts catch rate for legendary fish, increased reputation.

	2. "A Fisherman’s Craft"  
	- Description : Curty has a special request: he needs a unique bait made from several rare fish ingredients. These ingredients are scattered across various 
dangerous locations.  
	- Objective : Collect rare fish parts such as the "Golden Scale" from the high cliffs, "Bloodfin" from the Abyssal Sea, and "Crystal Tear" from the Frozen 
Caverns.  
	- Requirements : Must be at least "Skilled Angler" reputation to begin.  
	- Reward : Special bait that attracts rare, high-value fish, and a significant reputation boost with "Curty L".

	3. "The Great Tournament"  
	- Description : Curty L. invites players to compete in a prestigious fishing tournament held once every in-game season. The tournament will test all fishing 
skills, including precision and patience.  
	- Objective : Compete against other NPCs or players to catch the most valuable fish within a limited time frame. Players must submit their catches to Curty for 
judging.  
	- Requirements : Must be at least "Grand Master Fisherman" to qualify for the tournament.  
	- Reward : Unique, high-tier fishing gear (e.g., enchanted fishing rod or special hooks), significant reputation boost with Curty L.

	4. "Fisherman’s Favor"  
   - Description : Curty has a personal favor to ask. He once had a fishing partner, an elderly man who disappeared under mysterious circumstances while on a 
fishing expedition. Curty asks the player to find out what happened to him.  
   - Objective : Investigate the disappearance by exploring the area where the partner was last seen, encountering dangerous creatures and uncovering clues.  
   - Requirements : Must be at least **Skilled Angler** to unlock this quest.  
   - Reward : A rare fishing lure crafted by Curty, which grants access to secret fish species and increases the player’s chances of finding high-quality fish.

		Additional Features for Fisherman Profession Players:
- Special Fishing Spots :  
	Players with a high reputation with "Curty L." unlock access to "exclusive fishing spots" in the nearby oceans and lakes, including mysterious underwater caves 
where legendary fish live. These areas have increased difficulty due to natural hazards like high tides, underwater creatures, and weather changes.
  
- Fishing Gear :  
	Reputation rewards from "Curty L." can also include "exclusive fishing gear" like enchanted rods, magical hooks, and bait that attracts mythical fish. This gear
can help players in both quests and tournaments.

- Mentorship :  
	At the highest level of reputation, "Grand Master Fisherman", "Curty L." will offer a mentorship system where he teaches players advanced fishing strategies, 
including how to catch the rarest species, giving a permanent buff to fishing skills.

	"Anglers' Shelter" becomes a pivotal location for "Fisherman Profession Players", providing an environment that fosters growth, challenges, and a deep 
connection to the world of fishing, while "Curty L.'s" quests and mentorship shape players into true masters of the craft.