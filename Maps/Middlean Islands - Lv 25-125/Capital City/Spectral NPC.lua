			Spectral NPC
			
		Spectral NPC Quests at the Ghost Ship Docks

	These eerie quests from spectral "NPCs" appear only under the cover of moonlight at the docks in the capital city, accessible to players with a certain 
"REP level" or after completing a special initiation task with "Dockmaster Lorik". These quests and items are shrouded in mystery and offer insights into lost lore,
ancient powers, and rare artifacts. 

	Each quest requires players to solve unique challenges, face spectral foes, and navigate through otherworldly elements that add an eerie complexity to the 
experience.

---

		Quest Series: Moonlit Mysteries

	1.Quest Title : "Echoes of the Fallen"
   Quest Level : 78  
   Objective : Speak with the spectral captain and locate three lost sailor tokens in the area.
   
   Description :  
   "Arr, ye brave soul who can hear my call! My crew and I are trapped, cursed to wander these docks forevermore. Retrieve our sailor tokens from where they lay scattered across these shores, and perhaps, ye can help us finally rest."

   Mechanics :  
   - Hidden Zones : Each token appears at different points along the docks only at specific times of the night (e.g., the first token appears at midnight, the second an hour later). The player must locate the tokens in these hidden zones, guided by faint ghostly lights.
   - Spectral Enemies : As players pick up each token, spectral enemies will emerge, testing their combat skills. Each enemy possesses debuff skills like 
   “Chill of the Abyss” that temporarily slow or weaken players.
   
   Reward :  
   "Ethereal Compass" – A non-equippable item that grants the ability to sense hidden spectral NPCs and objects in certain areas of the docks at night.

---

2.Quest Title : "Secrets of the Tide"
   Quest Level : 80  
   Objective : Retrieve a series of sunken logbooks from the nearby underwater ruins.

   Description :  
   "We left behind our logbooks, and within them lies the knowledge of what brought this curse upon us. Brave the depths of the sea and reclaim them from the ruins!"

   Mechanics :  
   - Timed Swim : Players must navigate underwater ruins within a time limit, as they only appear in the moonlight. Failing to complete the dive within the time 
   means the logbooks vanish, resetting the quest.
   - Puzzle Challenge : The logbooks are locked with ghostly seals. Players must solve a series of rune puzzles at the docks to unlock each logbook, deciphering 
   spectral symbols while defending themselves from lurking spectral enemies that they doesnt affect players with “Chill of the Abyss”.
   
   Reward :  
   "Enchanted Shell Necklace" – Grants access to specific spectral "NPCs" who sell rare artifacts.

---

3.Quest Title : *Whispers from Beyond*
   Quest Level : 82  
   Objective : Find and commune with three spectral ghosts hidden across the docks, who only reveal themselves at specific times.

   Description :  
   "Three of my crew wander here in solitude, and their knowledge will be vital to ending our torment. Seek them out, but know that they’re easily spooked and vanish quickly."

   Mechanics :  
   - Stealth Mechanic : Players must avoid being detected by the ghost guards wandering the docks. Detection will cause the hidden spectral NPCs to disappear until the next night.
   - Timed Interaction : Each ghost NPC will appear only for a brief window, requiring players to interact quickly and answer riddles or perform tasks to unlock a “whispered” clue to their next location.
   
   Reward :  
   "Ghostly Beacon" – A mystical, non-equippable item that allows players to summon spectral "NPCs" with unique items once every three days.

---

		Unique Item Rewards and Mechanics for Obtaining Them

	1.Moonlit Lantern  
   - Description : A lantern that glows with ghostly light, illuminating hidden paths on specific nights and revealing unique NPCs who may offer quests.
   - Mechanic to Obtain : This lantern is obtained by successfully completing all three “Moonlit Mysteries” quests above. Upon completing each, players will unlock pieces of the lantern. When all pieces are collected, they combine to form the Moonlit Lantern.
   - Unique Ability : Allows players to access a hidden area on the docks for one night each week, where they can encounter rare spectral merchants with unique lore and otherworldly curios.

	2. Tidecaller’s Chalice  
   - Description : A silver chalice said to be crafted from the waters of the underworld. It allows players to “commune” with the spirits of the dock, receiving fragments of lore or hints to uncovering other hidden NPCs.
   - Mechanic to Obtain : Players must catch a spectral fish using bait found only in the area and present it as an offering at the shipwreck on the docks during the full moon.
   - Unique Ability : Drinking from the chalice once a week grants a temporary buff called “Ethereal Insight,” enhancing vision in foggy areas and revealing hidden treasures or lore-based hints.

	3.Specter’s Map
   - Description : An enchanted map showing locations of spectral treasures buried around the dock area.
   - Mechanic to Obtain : Players who have max "REP" with "Lorik" and have completed “Echoes of the Fallen” can purchase the map from a spectral merchant "NPC". The map’s markings appear only when the player holds a Moonlit Lantern nearby.
   - Unique Ability : Grants the ability to uncover hidden relics or lore items scattered in locations throughout the docks on certain nights, which can be collected and traded among players.