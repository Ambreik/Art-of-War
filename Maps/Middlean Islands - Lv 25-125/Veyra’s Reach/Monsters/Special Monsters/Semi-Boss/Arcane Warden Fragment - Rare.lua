			Arcane Warden Fragment

Remnant of an ancient sentinel from the megadungeon.

**Rank:** III (mini-boss)
**Abilities:**

* Arcane Beam
* Summon Rune Shards (adds)
* Detonation Core (enrage at 20%)

**Drops:**

* *Warden Core*
* *Pattern: Rune of Titanhide*
* 1–3 rare crafting components

### **Alyndor Behavior:**

* He becomes fully serious
* Uses shield spells on players
* If he drops <40% HP → teleports away and says:
  “Continue. I will return with reinforcements… perhaps.”

### **Trigger Conditions**

Rare spawns appear when:

* Nighttime (higher chance)
* During heavy fog or storms
* When 2+ players are near the walls
* When Alyndor is patrolling

