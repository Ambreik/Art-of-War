			Sunken Sentinel (Semi-Boss)

**Type:** Undead (Aquatic-bound)
**Location:** *Interior of the Sunken Warship* — deepest chamber, locked behind collapsed hull plating.

---

## **1. Overview**

The **Sunken Sentinel** is a semi-boss guardian bound by drowned arcane magic.
It protects the warship’s final chamber and acts as a gatekeeper to the Sunken Warship Key.

A player entering this area **is always aggro’d immediately**, as the Sentinel awakens from a kneeling position.

---

## **2. Spawn Conditions**

* Always present inside the warship interior.
* Respawn timer: **15 minutes** after death.
* Upon player entering the warship interior, the Sentinel delivers an automatic taunt quote.
* Cannot be killed by the same player in the next hour.

---

## **3. Appearance**

A tall, humanoid skeletal figure wrapped in seaweed, barnacles, and rusted naval armor. Magical blue water-arcane energy leaks from joints and eye sockets.

---

## **4. Combat Mechanics**

### **Primary Attacks**

* **Arcane Lash**
  A medium-range sweeping magical strike dealing arcane damage.

* **Tidal Burst (AoE)**
  3-second cast. Releases a circular surge of water, knocking players back and dealing medium AoE damage.

---

### **Add Summoning**

**Skeletal Reefsharks** (minion adds)

* Summoned every **20 seconds** if the player remains at range or does not interrupt the Sentinel.
* Health is low; they act mainly as pressure mobs.

Summon limit: **3 active minions at a time**.

---

### **Defensive Traits**

* Resistant to **Nature** and **Arcane** by 10%.
* Weak to **Fire** by 10% (drying brittle bone).

---

### **Arena Behavior**

* Cannot leave the warship interior.
* If the player runs out of the warship, the boss resets to full HP.

---

## **5. Quotes (Taunts)**

*(Triggers when entering or re-entering the chamber)*

* “Your bones… will join the hull…”
* “The tides claim all things…”
* “Return… to the deep…”

(He has **no death quote** — collapses silently.)

---

## **6. Loot & Rewards**

### **Guaranteed Drop**

**Sunken Warship Key** (100%)

* Unlocks a hidden chest that spawns in the captain’s quarters **only after the Sentinel dies**.
* **Rare Drop:** Core Beast Fragment -> item used for crafting "“Mini Mauler.”

---

### **Additional Loot**

* **Rank III Alchemy reagent fragments** (rare)
* **Water-Runed Bone Shards**
* **Abyssal Thread** for rune crafting
* **Salt-Encrusted Vertebrae** (crafting material)

---

## **7. Special Notes**

* Not tamable.
* Not a mount source.
* Classified as **Semi-Boss** — stronger than elites, weaker than dungeon bosses.
* Designed for **solo players**, but can be group-killed without restrictions.

