			Skeletal Reefshark (Add Unit)

**Type:** Undead Beast (Aquatic)
**Role:** Summoned Add (Sunken Sentinel)

---

## **1. Overview**

Skeletal Reefsharks are small, fast-moving undead shark constructs created from magically animated bone. They act as pressure adds during the Sunken Sentinel fight, forcing players to manage crowd control, interrupts, or burst damage.

They **never exist independently** in the world and can only appear when summoned by the Sunken Sentinel.

---

## **2. Spawn Conditions**

* Only summoned by **Sunken Sentinel** during combat.
* Trigger: Every **20 seconds**, if the player remains at range or fails to interrupt the Sentinel.
* Maximum active adds: **3 at once**.
* Despawn:

  * When the Sunken Sentinel is defeated
  * When the fight resets
  * When the player dies

---

## **3. Appearance**

Small skeletal sharks covered in barnacles and hanging seaweed strips. Eyes glow faint cyan with aquatic magic. They hover/swim through the flooded interior even without water, sustained by necromantic tides.

---

## **4. Combat Mechanics**

### **Attacks**

* **Gnawing Bite**
  Fast melee attack dealing minor physical damage.

* **Bleeding Rip** (10% chance on hit)
  Applies a weak bleed (6 sec). Pure pressure, not lethal alone.

---

### **Behavior**

* Always target the player currently fighting the Sunken Sentinel.
* Prioritize interrupting caster builds by applying quick melee pressure.
* No special resistances or weaknesses — designed to be killable quickly.

---

### **Stats**

* Low HP (intended to be burst down).
* High movement speed (they reach the player quickly).
* Deal low-to-medium chip damage but become dangerous if ignored.

---

## **5. AI Notes**

* They don’t flee, taunt, or switch targets.
* They never attack other mobs or NPCs.
* They cannot be crowd-controlled for longer than 5 seconds (soft resistance to stuns/roots).
* They do not reset individually; reset only with the semi-boss.

---

## **6. Loot**

**None.**
Adds do *not* drop gold, items, or experience.
They exist purely as boss mechanics.

---

## **7. Special Notes**

* Not tamable.
* Not mount or pet related.
* They never spawn in the open world.
* They disappear instantly when Sunken Sentinel dies.

