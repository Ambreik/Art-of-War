			Cavernstone Behemoth

**Type:** Semi-Boss • Grimstone Quarry
**Location:** **Deepstone Hollow**, a small chamber at the end of one of Grimstone Quarry’s main tunnels
**Role:** Tanky, environmental hazard semi-boss

---

# **1. Overview**

The *Cavernstone Behemoth* is a hulking, rock-encrusted elemental that dwells in the mineral-rich Deepstone Hollow. Its body is a mix of jagged stone and glowing mineral veins, making it **appear as part of the cavern until it moves**.

This semi-boss is the first real “mini-boss” for players exploring the quarry’s deeper tunnels and is designed for a **solo or small-group encounter**.

---

# **2. Visual Description**

* Massive, bipedal creature, roughly three times the height of a player
* Jagged greenish stone plates protrude from its body, glowing faintly in dark caves
* Cracks in its rocky skin leak a faint green mist
* Movements are slow but powerful, and its steps shake the ground

---

# **3. Combat Behavior & Mechanics**

### **Abilities**

#### **• Rock Slam**

* Smashes the ground in front of it
* Deals AoE physical damage + minor stun
* Can knock back players against cavern walls

#### **• Mineral Spikes (Passive)**

* Occasionally erupts spikes from the ground within 6 meters
* Medium damage and movement slow
* Environmental hazard encourages careful positioning

#### **• Crushing Grip**

* Single-target grab attack
* Moderate damage + immobilizes the player for 2 seconds
* Can interrupt mining/crafting animations if nearby

#### **• Echo Roar**

* Stuns nearby players for 1.5 seconds every 30 seconds
* Causes visual shake effect in cavern to disorient

---

# **4. Spawn Rules**

* Only spawns in **Deepstone Hollow**
* One instance active at a time
* Respawn: 40–60 minutes
* Conditional spawn:

  * Triggered if players mine **10+ Veyrite Ores or Dreadstone nodes** in Grimstone Quarry and enter Deepstone Hollow

---

# **5. Loot Table**

**Focused on utility and crafting:**

* **Guaranteed:**

  * Crystalline Shard ×4–7 (high-tier crafting material)
  * Stoneheart Fragment ×1–2 (enhances armor crafting)

* **Rare (5%)**

  * Behemoth’s Vein Powder — used in crafting strong rune enhancements or potions

