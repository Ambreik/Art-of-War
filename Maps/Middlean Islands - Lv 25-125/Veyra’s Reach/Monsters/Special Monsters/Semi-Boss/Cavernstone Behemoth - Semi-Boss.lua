* Mid-fight:

> “The cavern bends… but I do not break!”

* Low HP:

> “I… am… eternal… like the stone…”

These give **flavor and intimidation**, without tying to loot or mount mechanics.

---

# **7. Location Details**

### **Deepstone Hollow**

* Narrow cavern with jagged walls and scattered mineral veins
* Players must navigate small platforms and avoid natural hazards
* Semi-boss chamber is slightly elevated, giving it environmental advantage

**Return to surface:**

* Natural cavern exits leading back to Grimstone Quarry main tunnel
* Rare **Propulsion Stones** near ledges for quick vertical escape

