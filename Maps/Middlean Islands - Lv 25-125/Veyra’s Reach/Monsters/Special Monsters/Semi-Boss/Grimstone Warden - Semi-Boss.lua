# **7. Location Details**

### **Deep Cavern Core**

* Central chamber, large and semi-circular with jagged walls
* Surrounded by smaller tunnels that feed into the arena
* Pillars of natural stone provide partial cover from AoE attacks
* Shallow fissures of molten rock and glowing Dreadstone veins create ambient hazards
* Players must navigate carefully; knockbacks and environmental hazards punish poor positioning
* **Escape/Return Mechanics:**

  * Climbable stone ledges lead back to surface shaft
  * Players can use **“Mining Propulsion Crystals”** or jump pads found near cavern walls
  * Optional: teleport items or ropes can be brought in for fast ascent

---

# **8. Special Mechanics**

* **Environmental Awareness:** Players must avoid knockback into fissures and rockfall zones
* **Minion Control:** Small minions must be dealt with; ignoring them makes boss abilities more dangerous
* **Phase Shift:** Warden’s vanish mechanic forces players to reposition continuously

