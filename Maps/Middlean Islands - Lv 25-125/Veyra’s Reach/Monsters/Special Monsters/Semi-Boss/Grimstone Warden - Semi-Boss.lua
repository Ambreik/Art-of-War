			Grimstone Warden

**Type:** Semi-Boss • Grimstone Quarry
**Location:** **Deep Cavern Core**, central chamber of the quarry, guarded by narrow tunnels and natural stone pillars
**Role:** Area-control tank with knockback and minion summon mechanics

---

# **1. Overview**

The *Grimstone Warden* is a colossal, jagged stone entity animated by the essence of **Dreadstone** deep in the caverns. Unlike regular rares, it **actively manipulates the environment**, slamming walls to cause rockfalls, summoning smaller minions, and punishing poor positioning.

**Difficulty:** Designed for high-level solo players or small parties (mid-to-high 70s recommended).

---

# **2. Visual Description**

* Towering stone humanoid with **Dreadstone cores embedded in shoulders and chest**
* Dark veins pulse with an eerie purple glow
* Shards of black stone float around its head like a crown
* Emits deep rumbling noises that shake the cavern

---

# **3. Combat Behavior & Mechanics**

### **Abilities**

#### **• Stonequake Slam**

* Slams the ground with both fists, generating AoE damage + knockback
* Stuns players in the center for 1.5 seconds

#### **• Dreadstone Spikes**

* Summons spikes from cavern floor at random locations
* Can cause heavy damage if players are hit

#### **• Minion Summon – “Cave Stalwights”**

* Summons 2–3 small stone minions per 30 seconds
* Minions deal minor physical damage but act as crowd control

#### **• Crushing Grip**

* Grabs a single player, dealing massive damage after 3 seconds if not interrupted
* Throws player into a nearby wall, creating additional AoE

#### **• Roaming Rockfall**

* Randomized environmental hazard
* Causes boulders to fall from ceiling
* Punishes stationary or inattentive players

#### **• Retreat & Reappear**

* At 50% HP, Grimstone Warden **vanishes into the cavern walls**
* Reappears after 10–15 seconds at a random point in the arena
* Visual cue: faint glowing purple mist marks its return point

---

# **4. Spawn Rules**

* Only spawns in **Deep Cavern Core**
* Single active instance
* Respawn: 90–120 minutes
* Conditional spawn:

  * Requires at least **2 previous rare elites in Grimstone Quarry to be killed within last 3 hours**
  * Chance increases if **Veyrite Ore or Dreadstone mining activity** occurs nearby

