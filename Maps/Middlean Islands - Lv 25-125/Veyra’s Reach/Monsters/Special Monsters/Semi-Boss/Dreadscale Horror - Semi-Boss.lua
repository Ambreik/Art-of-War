			Dreadscale Horror

**Type:** Semi-Boss • Grimstone Quarry
**Location:** **Shadow Vein Cavern**, a dimly lit side tunnel below Grimstone Quarry’s main shaft
**Role:** High-damage, agile ambusher

---

# **1. Overview**

The *Dreadscale Horror* is a serpentine, draconic creature adapted to the cavern environment. Its sleek, black-scaled body glimmers faintly from bioluminescent veins in the cavern walls. Unlike the Behemoth, it **relies on speed, ambush tactics, and area denial**, punishing careless players exploring Shadow Vein Cavern.

---

# **2. Visual Description**

* Long, sinuous body (~2.5x player height)
* Black scales with faint greenish glow along spine and eyes
* Wing-like frills along shoulders for limited jumps over obstacles
* Thin, whip-like tail ending in a spiked club

---

# **3. Combat Behavior & Mechanics**

### **Abilities**

#### **• Venomous Strike**

* Quick bite attack
* Deals moderate physical + poison damage over 5 seconds
* Chance to reduce healing effects

#### **• Tail Sweep**

* Sweeps tail in a wide arc, knocking players back
* Environmental hazard: can push players into cavern edges

#### **• Ambush Dive**

* Leaps from walls or ledges to strike players
* Deals high burst damage; interrupts spell casting

#### **• Shadow Slither**

* Occasionally blends with cavern shadows
* Increases movement speed + temporary damage immunity for 5 seconds
* Can disengage from players to reposition

---

# **4. Spawn Rules**

* Only spawns in **Shadow Vein Cavern**
* One instance active at a time
* Respawn: 50–70 minutes
* Conditional spawn:

  * Triggered if players mine **5+ Dreadstone nodes** or enter cavern with high aggression level monsters nearby

---

# **5. Loot Table**

**Focused on crafting/alchemy materials and rare drops:**

* **Guaranteed:**

  * Dreadscale Fragment ×3–5 (used for high-tier armor or weapon enhancements)
  * Shadow Vein Powder ×1–2 (alchemy reagent)

* **Rare (3–5%)**

  * Horror’s Essence — used in special potions for temporary damage boost or debuff resistance

**No mounts, pets, or chest mechanics.**

