# **7. Location Details**

### **Shadow Vein Cavern**

* Narrow, winding tunnels with natural ledges and shadowed pockets
* Bioluminescent fungi for subtle lighting
* Semi-boss uses verticality and shadows to ambush
* Safe exits: players can retrace tunnel paths back to Grimstone Quarry main shaft or use **Propulsion Stones** scattered on ledges for fast vertical movement

---

This semi-boss introduces **mobility, ambush tactics, and poison mechanics**, giving a contrast to the previous **Cavernstone Behemoth**, which was tanky and environmental hazard-oriented.

