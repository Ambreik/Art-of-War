**Ridgehowl Terrorclaw**

**Classification:** Elite Monster (Rank III)
**Level:** 69
**Habitat:** Jagged cliffs and forested ridges near Veyra’s Edge
**Type:** Animal / Beast
**Element:** Wind
**Monster Type:** Berserk / Skillable / Leader of a group / Melee / Spawner

---

### **Description**

The Ridgehowl Terrorclaw is a massive wind-element beast with elongated claws capable of rending stone and steel. It dominates the cliff ridges with its terrifying roar and hunts in coordinated packs, often summoning smaller “Wind Talon Raptors” to overwhelm intruders. Its natural agility allows it to leap between ledges and cliffs, making it unpredictable in open terrain.

---

### **Behavior**

* Patrols cliff edges, using verticality to ambush players.
* Aggressive; prioritizes ranged or spellcasting targets first.
* Summons 2–4 Wind Talon Raptors when below 50% HP.
* Alternates between sweeping melee attacks, AoE claw strikes, and high-speed leaps.
* Enters Berserk mode at 30% HP, increasing damage and attack speed while reducing its own vulnerability to CC.

---

### **Skills and Abilities**

* **Talon Sweep:** Sweeps claws in a wide arc, hitting multiple targets and applying “Bleed” over 5s.
* **Leap Ambush:** Leaps to a distant target, dealing high burst damage and applying “Stun” for 2s.
* **Wind Howl:** AoE cone attack that pushes back and applies “Slowdown” for 6s.
* **Raptor Summon:** Calls 2–4 Wind Talon Raptors to join combat; they target squishier players.
* **Berserk Fury:** Activates at 30% HP; increases attack speed by 40% and grants a chance to pierce armor with every hit.
* **Cliff Rend:** Heavy single-target strike with high critical damage; applies “Bleed” and knocks back if near cliff edges.

---

### **Status Effects & CC**

* **Bleed:** Talon Sweep & Cliff Rend
* **Stun:** Leap Ambush
* **Slowdown:** Wind Howl
* **Knockback:** Cliff Rend near edges

---

### **Mechanics Interaction**

* Environmental hazards: can push players off ledges for fall damage.
* Minion synergy: Wind Talon Raptors focus ranged or healer players, forcing threat management.
* Cliff mobility: can leap between ledges to reposition or evade frontal attacks.

---

### **Quotes**

• “The wind carries death to all who trespass!”
