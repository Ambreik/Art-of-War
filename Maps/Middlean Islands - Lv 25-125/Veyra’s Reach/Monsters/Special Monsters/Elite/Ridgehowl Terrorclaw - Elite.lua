### **Respawn / Encounter Rules**

* **Spawn Timer:** 3–4 hours
* Appears alone but can summon minions.
* Soloable by high-level groups, but recommended party engagement due to mobility, adds, and AoE attacks.

---

### **Special Rules**

* Minions persist until Ridgehowl Terrorclaw is defeated.
* Berserk Fury triggers only once per combat phase.
* Can reposition mid-combat via Leap Ambush or vertical movement.

