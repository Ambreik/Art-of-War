## **♻ Respawn / Encounter Rules**

* Spawn timer: 15 seconds (Rank III)
* Solo elite monster; can appear with summoned sand wyrms during combat.
* Remains aggressive until killed or players leave a 20m radius.

---

## **⚠ Special Rules**

* Burrow mechanic makes it temporarily untargetable.
* Berserk phase triggers automatically below 25% HP.
* Occasionally triggers a spawner mechanic summoning 1–3 sand wyrms.

