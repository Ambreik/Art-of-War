			Deepcoil Sand Lurker

**Rank:** Elite (III)
**Level:** 65
**Type:** Beast / Animal
**Element:** Earth
**Habitat:** Veyra’s Edge – sandy dunes and coastal cliffs

---

## **📘 Description**

The Deepcoil Sand Lurker is a serpentine predator that burrows beneath loose sands to ambush prey. Its segmented armored body is coated in rough sand-colored scales, providing near-perfect camouflage. It rarely attacks on sight, preferring to strike from beneath, often leading to sudden, devastating ambushes.

---

## **🧬 Behavior**

* Solo elite monster; **Berserk** and **Skillable** type.
* Burrows under sand to remain hidden; emerges for attacks.
* Aggressive toward intruders, prioritizes lightly armored or isolated players.
* Can summon smaller sand wyrms in rare cases during combat (**Spawner type mechanic**).

---

## **⚔️ Combat Mechanics**

* **Burrow Strike**: Emerges from sand to bite target, applies **Bleed** for 8 seconds.
* **Tail Whip**: Sweeps in a 180° arc, knocks back all nearby players.
* **Sandstorm**: Creates AoE dust cloud around itself for 6 seconds, applying **Slowdown** and reducing visibility.
* **Constriction Coil**: Wraps around a single target, immobilizing them for 4 seconds.
* **Berserk Phase**: HP < 25%, attack speed +25%, damage +30%, ignores minor terrain penalties.

---

## **☠ Status Effects & CC**

* Affects with: **Bleed**, **Stun**, **Slowdown**, **Root**, **Knockback**

---

## **🌀 Mechanics Interaction**

* Burrowing allows it to avoid ranged attacks temporarily.
* Can manipulate terrain by displacing sand, affecting player movement.
* May trigger nearby sand wyrms to attack in small groups if engaged.

---

