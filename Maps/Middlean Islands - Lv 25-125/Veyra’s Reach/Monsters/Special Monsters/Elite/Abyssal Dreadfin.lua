			Abyssal Dreadfin

**Type:** Elite Aquatic Monster
**Location:** Surrounding the Sunken Warship, patrols in a circular route around the wreck at variable depth (25–40 meters).
**Element:** Water
**Rank:** II Elite
**Respawn Time:** 4 hours real-time

**Description:**
The Abyssal Dreadfin is a mutated shark-like creature infused with residual dark energy from the ship’s cursed cargo. Its translucent skin glows faintly blue, and its jaw is reinforced with coral-like bone plates. When approached, it emits low-frequency pulses that distort visibility and disrupt glider and mount controls underwater.

**Combat Behavior:**

1. **Aggression Range:** 20 meters radius; automatically engages any player approaching the wreck.
2. **Special Abilities:**

   * **Pressure Pulse:** Knocks back all targets, forcing them to lose buoyancy for 3 seconds.
   * **Depth Charge:** Creates a shockwave that reduces oxygen time by 15 seconds for players within range.
   * **Thrash Frenzy:** Increases its own speed and damage by 30% when HP drops below 20%.
3. **Movement Pattern:** Patrols in a slow horizontal circle; after being defeated, it respawns near a different side of the wreck.

