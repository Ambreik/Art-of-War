**Mechanics Interaction:**

* Only **one Dreadfin** spawns per Sunken Warship instance.
* Once defeated, the **Abyssal Captain’s Hoard** chest becomes interactable for 10 minutes before despawning if unopened.
* The Dreadfin’s defeat triggers a short **water current surge**, temporarily reducing visibility and displacing players by several meters.

**Special Rule:**
If multiple players engage the Dreadfin, loot rights follow the **highest total damage** rule. All eligible players may roll individually for crafting materials, but **only the player with loot rights** receives the **Pressure-Sealed Key**.

