			Coralback Crustacean — Elite Monster

**Type:** Aquatic Elite (Animal)
**Rank:** III
**Location:** Shallow coral reefs and tide pools along **Veyra’s Edge** coastline
**Spawn Rules:**

* Only spawns **after all semi-bosses from Grimstone Quarry** underground have been killed.
* Solo encounter recommended; **if multiple players engage, loot is limited to gold only**.
* Can be lured out of water to shallow rocks, triggering **weakness/resistance mechanic**.

---

### 🦀 **Appearance & Behavior**

* Enormous crustacean, ~3 meters wide, covered in **thick coral-like armor**.
* Massive claws capable of slamming players into rocks or shallow pools.
* Low, rumbling growl; slow but extremely strong in melee.
* Moves primarily in shallow water or near edges of tide pools.

---

### ⚙️ **Combat Mechanics**

* **Attacks:**

  * Claw Slam: heavy melee, chance to **knockback** players into rocks or water.
  * Grapple Claw: grabs a single player, immobilizing briefly while dealing damage.
  * Shell Crush: AoE on the ground, knocks back small creatures/players in shallow water.

* **Special Mechanics:**

  * **Weak Point Attack:** Multiple players attacking simultaneously can reduce armor by 15% temporarily.
  * **Lured Out Weakness:**

    * When pulled out of water onto rocks:

      * Physical damage **+15%** (vulnerability)
      * Magic resistance **+15%** (more resistant)
  * **Solo Kill Requirement:** Only solo kills grant rare quest-starting drop; group kills yield only gold.

---

### 🎁 **Loot / Quest Item**

* **Gold coins** — always dropped.
* **Crustacean Carapace Fragment** — **rare drop** (starts quest to return to Grimstone Quarry and kill semi-bosses in a specific order).
* **Notes:**
  * Loot cannot be sold; used exclusively for quest activation.

---

### 🌊 **Environmental / Location Integration**

* Prefers **shallow water zones with exposed coral**.
* Can be **lured out of water** by attacking or using bait items (cosmetic/mechanics).
* Acts as a **gateway encounter** to unlock the underground solo quest in Grimstone Quarry.

---

### 🛠️ **Gameplay Notes**

* Encourages **solo tactical play** with environmental awareness (knockback hazards, shallow pools).
* Provides **quest progression hook** rather than generic farming reward.
* Dynamic spawn mechanic ensures **players must have cleared quarry content** before encounter becomes available.

