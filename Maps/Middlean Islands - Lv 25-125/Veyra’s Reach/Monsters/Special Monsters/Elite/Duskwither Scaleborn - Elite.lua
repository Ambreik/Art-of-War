**Duskwither Scaleborn**

**Classification:** Elite Monster (Rank III)
**Level:** 62
**Habitat:** Jungle Ridge, dense canopy regions and cliffside ledges
**Type:** Animal / Reptile
**Element:** Wind
**Monster Type:** Tanky / Skillable / Berserk / Leader of a group

---

### **Description**

The Duskwither Scaleborn is a massive, wind-swept drake that dominates the upper canopy of Jungle Ridge. Its scales shimmer with a grey-blue tint, blending with the shadows of the treetops. Known for sudden bursts of berserk rage, it coordinates attacks with its smaller drake minions, making encounters unpredictable.

---

### **Behavior**

* Patrols cliff edges and treetop branches.
* Aggressive toward all intruders within 25 meters.
* Commands smaller drakes (2–4 per group), using them to harass and flank players.
* Alternates between ranged gust attacks and melee strikes.
* Enter berserk mode at 50% HP, increasing damage output and attack speed.

---

### **Skills and Abilities**

* **Whirling Talons:** Sweeping melee attack, hits multiple targets, moderate knockback.
* **Gale Roar:** AoE wind-based knockback and minor stun for 3s.
* **Tail Whip:** Heavy melee attack, knocks back and deals DoT bleed effect over 5s.
* **Summon Fledglings:** Calls 1–3 smaller drakes to assist in combat, cooldown 60s.
* **Berserk Surge:** Triggered at 50% HP, increases attack speed by 40% and damage by 30%.
* **Wing Buffet:** AoE knockdown for 2s, short-range burst damage, can interrupt spellcasting.

---

### **Status Effects & CC**

* **Bleed:** Tail Whip
* **Stun:** Gale Roar
* **Knockback / Knockdown:** Wing Buffet, Whirling Talons

---

### **Mechanics Interaction**

* Interacts with terrain: cliff edges amplify knockback effects; falling can damage players.
* Group dynamics: smaller drakes coordinate with Scaleborn for flanking attacks.
* Environmental synergy: strong gust attacks can dislodge loose debris, causing minor environmental damage.

---

### **Quotes**

• “You dare climb my domain?”
• “The winds themselves obey me!”

