### **Respawn / Encounter Rules**

* **Spawn Timer:** 2–3 hours, solo or with minion group.
* Aggro radius: 25 meters, tracks players across treetops.
* Encounters may split players if separated by cliffs or canopy layers.

---

### **Special Rules**

* Berserk phase activates automatically at 50% HP.
* Minion drakes despawn 30s after the Scaleborn dies.

