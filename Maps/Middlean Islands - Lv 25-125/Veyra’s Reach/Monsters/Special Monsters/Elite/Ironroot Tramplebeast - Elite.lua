## **♻ Respawn / Encounter Rules**

* Spawn timer: 15 seconds (Rank III)
* Solo group elite; does not appear with minions unless previously engaged with quarry mobs.
* Aggro persists until the beast is killed or all players leave a 20m radius.

---

## **⚠ Special Rules**

* Berserk phase triggers automatically below 30% HP.
* Immune to displacement mechanics from players’ knockback abilities.

