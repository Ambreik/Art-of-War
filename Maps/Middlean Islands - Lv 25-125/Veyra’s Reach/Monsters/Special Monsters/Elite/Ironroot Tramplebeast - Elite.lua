			Ironroot Tramplebeast

**Rank:** Elite (III)
**Level:** 68
**Type:** Beast / Animal
**Element:** Earth
**Habitat:** Grimstone Quarry, dense rock formations and quarry pits

---

## **📘 Description**

The Ironroot Tramplebeast is a colossal, quadrupedal creature covered in jagged stone-like plates, giving the appearance of moving terrain. Its massive hooves can crush stone and foes alike. Often found patrolling the deeper quarry areas alone, but occasionally leads smaller rockback boars in packs.

---

## **🧬 Behavior**

* Solo elite monster; acts as a **Tanky** and **Skillable** creature.
* Patrols quarry corridors and ambushes intruders.
* Highly aggressive; engages melee combat immediately on detection.
* Uses stomp and charge attacks to disrupt player formations.

---

## **⚔️ Combat Mechanics**

* **Earthshatter Stomp**: AoE physical damage around the creature, knocks back enemies.
* **Stone Charge**: Charges a straight line, dealing high damage and applying **Stun** for 2s.
* **Boulder Toss**: Throws rock debris up to 10m, causing **Bleed** DoT for 6s.
* **Trampling Rampage**: Spins in place, hits multiple targets in melee range.
* **Vampiric Vitality**: Restores 3% HP per successful melee hit.
* **Berserk Phase**: Activates below 30% HP, increases attack speed by 20% and movement speed by 15%.

---

## **☠ Status Effects & CC**

* Affects with: **Stun**, **Bleed**, **Knockback**

---

## **🌀 Mechanics Interaction**

* Terrain aware: uses narrow corridors to funnel players into AoE attacks.
* Aggro monsters with high HP; can pull groups of trash mobs nearby.
* Resistant to knockback; less affected by physical displacement from players.

---

