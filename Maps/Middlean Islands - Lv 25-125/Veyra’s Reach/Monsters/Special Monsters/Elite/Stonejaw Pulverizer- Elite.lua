## **♻ Respawn / Encounter Rules**

* Spawn timer: 15 seconds (Rank III)
* Solo elite, usually appears with minions forming a **leader + group** dynamic.
* Aggro persists within 20 meters; retreats to center of cave if players flee far enough.

---

## **⚠ Special Rules**

* Adds are spawned every 25 seconds during combat until the Pulverizer reaches 25% HP.
* Becomes **Skillable & Berserk** during low HP phase.

