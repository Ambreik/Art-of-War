			Stonejaw Pulverizer

**Rank:** Elite (III)
**Level:** 66
**Type:** Beast / Rock Elemental
**Element:** Earth
**Habitat:** Grimstone Quarry, large open caverns and collapsed tunnels

---

## **📘 Description**

The Stonejaw Pulverizer is a massive, quadrupedal rock creature with jagged crystalline protrusions along its spine and jaw. Its powerful limbs allow it to crush boulders effortlessly, making it a natural apex in quarry regions. Known to lead smaller rock elementals in hunting parties, it is highly territorial and exceptionally resilient.

---

## **🧬 Behavior**

* Acts as **leader of a group**, often accompanied by 2–3 smaller Stonejaw Miners.
* Patrols tunnels and open quarry floors, seeking intruders to ambush.
* Becomes enraged when allies are attacked, increasing aggression and attack speed.

---

## **⚔️ Combat Mechanics**

* Melee attacks: heavy jaw crush with **Crushing Blow**, capable of ignoring partial armor.
* Rock Slam: AoE attack that causes a temporary **stun** on players within 5 meters.
* Pulverizing Roar: applies **Fear** and **Slowdown** to all players within line of sight.
* Summon Adds (Spawner type): periodically calls 1–2 Stonejaw Miners to aid in combat.
* Phase Mechanic: at 50% HP, gains **Berserk** status; attack speed and damage increase by 40%.

---

## **☠ Status Effects & CC**

* Affects with: **Stun**, **Fear**, **Slowdown**, **Bleed** (from jagged rocks)

---

## **🌀 Mechanics Interaction**

* Crushes fragile terrain during combat, creating temporary obstacles or hazards.
* Players using ranged attacks trigger counter-rock boulders from surrounding walls.
* Aggro radius 18 meters; will switch targets if a nearby add is attacked.

---

## **📜 Quotes**

• “No stone remains unbroken.”
• “The quarry trembles with my fury!”

