**Brineveil Shellguard**

**Classification:** Elite Monster (Rank III)
**Level:** 65
**Habitat:** Coastal shallows and tidal pools near Veyra’s Edge
**Type:** Animal / Aquatic
**Element:** Water
**Monster Type:** Tanky / Skillable / Berserk / Leader of a group

---

### **Description**

The Brineveil Shellguard is a massive, shell-covered crustacean that dominates shallow coastal waters. Its thick, barnacle-encrusted carapace allows it to absorb tremendous damage while coordinating attacks with smaller crustaceans in the area. Its pincers can crush bone, and its sudden bursts of movement create waves that disrupt players' positioning.

---

### **Behavior**

* Patrols tidal pools and shallow waters, moving between rocks and coral formations.
* Aggressive toward all players entering its territory.
* Commands smaller “Shellcrab” minions (2–4 per group).
* Alternates between melee crushing attacks, AoE shockwave, and ranged water sprays.
* Enters berserk mode at 50% HP, increasing attack speed and AoE radius.

---

### **Skills and Abilities**

* **Crushing Claw:** Melee pincer attack dealing high physical damage and minor knockback.
* **Tidal Slam:** AoE shockwave dealing moderate damage to all nearby players; slows movement by 30% for 5s.
* **Water Jet:** Medium-range attack pushing players back, interrupts casting.
* **Summon Shellcrabs:** Summons 2–4 small minions to attack the same target, cooldown 60s.
* **Berserk Surge:** Activates at 50% HP; increases attack speed by 40% and damage by 30%.
* **Armor Shell:** Passive; reduces incoming damage by 35%, particularly from physical attacks.

---

### **Status Effects & CC**

* **Bleed:** Crushing Claw
* **Slowdown:** Tidal Slam
* **Knockback:** Water Jet
* **Stun:** Minor stun on Tidal Slam if near rocks or debris

---

### **Mechanics Interaction**

* Interacts with water terrain: AoE attacks can displace players into shallow or deep areas.
* Group dynamics: summoned minions prioritize weakened players to force split-targeting.
* Environmental synergy: rocks and coral can block some AoE attacks, altering the path of Tidal Slam.

