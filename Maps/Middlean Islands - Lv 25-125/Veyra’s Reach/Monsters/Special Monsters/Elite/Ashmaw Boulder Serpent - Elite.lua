**Ashmaw Boulder Serpent**

**Classification:** Elite Monster (Rank III)
**Level:** 68
**Habitat:** Rocky ravines and boulder-strewn slopes of Grimstone Quarry
**Type:** Animal / Beast
**Element:** Earth
**Monster Type:** Tanky / Skillable / Berserk / Leader of a group / Spawner

---

### **Description**

The Ashmaw Boulder Serpent is a colossal serpentine predator that slithers among the jagged rocks and caves of Grimstone Quarry. Its stone-like scales provide near-impenetrable defense, and it can summon smaller rock serpents to overwhelm intruders. Its massive body allows it to crush rocks, creating environmental hazards for players.

---

### **Behavior**

* Patrols rocky slopes and cave entrances, occasionally hiding under boulders to ambush players.
* Aggressive toward all players; prioritizes high-damage classes first.
* Summons “Stone Serpent Hatchlings” when below 60% HP.
* Alternates between direct attacks, AoE rock slams, and burrowing maneuvers to reposition or escape.
* Enters Berserk mode at 40% HP, increasing attack speed and adding environmental hazards.

---

### **Skills and Abilities**

* **Rock Slam:** Heavy AoE attack; knocks back players, applies “Slowdown” 50% for 5s.
* **Crushing Coil:** Melee strike wrapping around a single target, deals high physical damage and “Bleed” over 6s.
* **Burrow Ambush:** Burrows underground to reappear behind the player; deals moderate damage with “Stun” for 2s.
* **Stone Hatchling Summon:** Spawns 2–3 Stone Serpent Hatchlings to assist in combat; they act independently but follow target priorities.
* **Boulder Toss:** Throws large boulders in a line, dealing moderate damage to all players hit; knockback applies if near rocks.
* **Berserk Surge:** Activates at 40% HP; increases attack speed by 50% and chance to “Crush” by 20%.
* **Earthquake Roar:** AoE tremor every 90s, damages and dazes all players in a 10m radius.

---

### **Status Effects & CC**

* **Bleed:** Crushing Coil
* **Slowdown:** Rock Slam & Boulder Toss
* **Stun:** Burrow Ambush
* **Daze:** Earthquake Roar

---

### **Mechanics Interaction**

* Terrain-based interactions: boulders in the environment can be dislodged by Earthquake Roar, creating additional damage zones.
* Minion synergy: Stone Serpent Hatchlings focus weakened players, forcing split targeting.
* Burrowing: can bypass player defenses, reposition, or force AoE attacks to be avoided.

---

### **Quotes**

