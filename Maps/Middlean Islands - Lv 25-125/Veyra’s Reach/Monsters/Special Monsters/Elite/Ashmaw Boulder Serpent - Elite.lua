### **Respawn / Encounter Rules**

* **Spawn Timer:** 3–4 hours.
* Appears with initial patrol zone; burrows can extend encounter range.
* Minions remain until Ashmaw is dead or despawned after 2 minutes.
* Solo and group engagement possible; groups recommended due to multi-phase mechanics.

---

### **Special Rules**

* Minions spawned cannot despawn until Ashmaw is defeated.
* Burrow Ambush can bypass certain environmental obstacles.
* Berserk and Earthquake Roar occur only once per combat phase.

