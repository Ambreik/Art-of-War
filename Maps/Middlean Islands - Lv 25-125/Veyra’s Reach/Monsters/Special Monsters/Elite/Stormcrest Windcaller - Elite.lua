			Stormcrest Windcaller

**Rank:** Elite (III)
**Level:** 66
**Type:** Humanoid / Lizard Mage
**Element:** Wind
**Habitat:** Veyra’s Edge, cliffside ruins and high rocky plateaus

---

## **📘 Description**

The Stormcrest Windcaller is a lithe, cunning lizard mage adorned with ceremonial feathers and wind-rune tattoos. They manipulate air currents to attack from distance, summon gusts, and disorient intruders. Often found leading small groups of Windcrest Acolytes or Archer Lizards.

---

## **🧬 Behavior**

* Leader of a small group (1–3 Acolytes).
* Uses ranged magical attacks with precision, staying at mid-range to kite melee players.
* Prioritizes spellcasting targets (mages, healers, ranged DPS).
* Exhibits **Skillable** behavior: casts spells in combos and adjusts attack patterns based on player positioning.

---

## **⚔️ Combat Mechanics**

* **Wind Bolt**: Medium damage, applies **Slowdown**.
* **Gale Burst**: AoE knockback (4m radius), interrupts casting.
* **Tempest Bind**: Roots a single target for 6s.
* **Cyclone Shield**: Passive shield, reduces incoming melee damage by 30%.
* **Rally Winds**: Calls 1–2 nearby minions to join combat, enhancing group damage.
* **Berserk Phase**: At 35% HP, movement speed +25%, attack speed +20%, and casts spells 15% faster.

---

## **☠ Status Effects & CC**

* Affects with: **Slowdown**, **Root**, **Knockback**, **Interrupts Casting**

---

## **🌀 Mechanics Interaction**

* Avoids close melee range; will retreat 10–12 meters if engaged in melee.
* Minions summoned can block paths, creating choke points.
* Uses terrain elevation to maximize range advantage, especially cliffs and ledges.

---

## **📜 Quotes**

• “The winds obey my command.”
• “You cannot outrun the storm I bring.”

---

## **♻ Respawn / Encounter Rules**

* Spawn timer: 15 seconds (Rank III)
* Appears as **leader + group** encounter, may summon 1–2 Acolytes during combat.
* Aggro persists until all minions and the Windcaller are defeated or players retreat beyond 20 meters.

---

## **⚠ Special Rules**

* Can reposition using **Gale Burst** to knock back and reposition melee players.
* Berserk phase automatically triggers at 35% HP.

