			Fangridge Ravager

**Rank:** Elite (III)
**Level:** 68
**Type:** Beast / Animal Predator
**Element:** Wind
**Habitat:** Jungle Ridge, dense forest clearings and rocky outcrops

---

## **📘 Description**

The Fangridge Ravager is a large, predatory reptilian beast with jagged fangs and razor-like claws. Its sinewy frame allows for lightning-fast strikes, and its frilled neck spikes flare when enraged. Typically prowls in packs, using its keen senses to coordinate attacks with smaller predators.

---

## **🧬 Behavior**

* **Leader of a pack**, usually accompanied by 3–4 smaller Fangridge Stalkers.
* Ambush predator: prefers to hide in dense foliage and strike when players are within range.
* Highly territorial; will aggressively patrol hunting grounds.

---

## **⚔️ Combat Mechanics**

* Melee attacks: Bite (high damage, applies **Bleed**) and Claw Swipe (medium AoE).
* Pounce: jumps up to 6 meters, dealing **Crushing Blow** and knockback.
* Frenzy: enters **Berserk** state at 40% HP; attack speed increases by 50%, damage by 35%.
* Roar of Dominance: AoE **Fear** and interrupts player casting.
* Skillable: occasionally performs a tail sweep that applies **Slowdown** and minor **Bleed** to multiple targets.

---

## **☠ Status Effects & CC**

* Affects with: **Bleed**, **Fear**, **Slowdown**, **Knockback**

---

## **🌀 Mechanics Interaction**

* Ambushes triggered if players step into tall grass or dense foliage.
* Aggro radius: 15 meters; will prioritize weaker players or ranged attackers.
* Coordinated attacks with smaller pack members increase total damage output.

---

## **📜 Quotes**

• “Your blood will stain my fangs.”
• “Run, and I will hunt you further.”

---

## **♻ Respawn / Encounter Rules**

* Spawn timer: 15 seconds (Rank III)
* Appears with 3–4 pack members as a **leader + group** encounter.
* Aggro persists until all pack members are defeated or players retreat 20 meters from lair.

---

## **⚠ Special Rules**

* Pounce can knock players into environmental hazards (rocks, pits) if in line of sight.
* Berserk activates automatically at 40% HP, increasing threat generation and attack speed.

