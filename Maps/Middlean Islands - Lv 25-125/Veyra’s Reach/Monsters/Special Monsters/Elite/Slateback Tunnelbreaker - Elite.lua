			Slateback Tunnelbreaker

**Rank:** Elite (III)
**Level:** 62
**Type:** Beast / Animal
**Element:** Earth
**Habitat:** Grimstone Quarry – deep tunnels and rocky outcrops

---

## **📘 Description**

The Slateback Tunnelbreaker is a massive, armored creature adapted to crushing its way through tunnels. Its carapace is thick and jagged, resembling shattered slate, and its claws are capable of collapsing weak tunnel walls. The creature is slow but devastatingly powerful, often leading small packs of burrowing predators.

---

## **🧬 Behavior**

* **Leader of a group** of burrowing animals.
* Aggressive toward players entering its tunnel systems.
* Uses slow, crushing attacks; prefers to ambush players at choke points.
* **Skillable** and **Tanky** type, capable of withstanding heavy damage while leading pack attacks.

---

## **⚔️ Combat Mechanics**

* **Tunnel Slam**: Slams claws into the ground, dealing AoE damage and applying **Stun** for 3 seconds.
* **Burrow Charge**: Charges forward, knocking back players and causing minor **Bleed** damage.
* **Crag Splitter**: Smashes the ceiling/walls of the tunnel, creating temporary rock debris that slows movement (**Slowdown**).
* **Summon Minions**: Calls 2–3 smaller burrowers to attack intruders during combat (**Spawner type**).
* **Stone Fortification**: Passive, reduces all incoming physical damage by 30% while stationary.

---

## **☠ Status Effects & CC**

* Affects with: **Bleed**, **Stun**, **Slowdown**, **Knockback**

---

## **🌀 Mechanics Interaction**

* Terrain-aware; may collapse tunnel sections, forcing players to reposition.
* Smaller burrowers summoned obey AI and target player with coordinated attacks.

