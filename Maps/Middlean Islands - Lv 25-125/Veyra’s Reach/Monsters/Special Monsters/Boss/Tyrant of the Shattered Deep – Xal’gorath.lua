			Tyrant of the Shattered Deep – Xal’gorath

**Classification:** Boss (Rank V)
**Level:** 70
**Habitat:** Sunken Warship and surrounding deep underwater chasms
**Type:** Aquatic / Beast / Legendary Monster
**Element:** Water
**Monster Type:** God / Skillable / Berserk / Melee / Spawner

---

### **Description**

Xal’gorath, the Tyrant of the Shattered Deep, is a colossal leviathan whose massive tentacles crush ships and foes alike. Its bioluminescent patterns pulse in the dark, signaling its moods and phase transitions. The surrounding waters become turbulent, creating environmental hazards that punish players who misstep.

---

### **Behavior**

* Highly territorial; patrols in and around the Sunken Warship wreckage.
* Prioritizes melee attackers but uses tentacle slams to disrupt casters and ranged players.
* Phased combat: the fight evolves based on HP thresholds (75%, 50%, 25%).
* Summons aquatic minions (Abyssal Dreadfins) during Phase 2 and 3 to assist.
* Interacts with terrain: whirlpools form around it, slowing movement and forcing positioning.

---

### **Phases & Combat Mechanics**

**Phase 1 – Depths of Wrath (100–75% HP)**

* Tentacle Slam: AoE knockback with high damage, applies **Stun**.
* Crushing Bite: Single-target high damage with **Bleed** DoT.
* Whirlpool Pull: Pulls nearby players towards Xal’gorath’s center.
* Moderate movement and attack speed.

**Phase 2 – Abyssal Surge (75–50% HP)**

* Increases movement speed and tentacle attack frequency.
* Summons 2–4 Abyssal Dreadfins to target ranged/caster players.
* Water Spout: AoE attack that throws players back and applies **Slowdown**.
* Begins occasional **Berserk Tentacle Flail**, hitting multiple targets.

**Phase 3 – Tyrant Ascendant (50–25% HP)**

* Enters **God Mode**: gains attack speed boost and partial damage immunity for 15s bursts.
* Tentacle Slam now applies **Root** for 3s.
* Crushing Bite applies **Poison** in addition to Bleed.
* Summons whirlpool hazards more frequently, forcing players to reposition constantly.

**Phase 4 – Dying Fury (25–0% HP)**

* Fully enraged; tentacle attacks and Bite deal massive burst damage.
* Every 10s, summons Abyssal Spore Clouds on random areas, causing minor AoE damage over time.
* If players fail to maintain distance, they are at high risk of **combo burst death** from tentacle + Bite + environmental damage.

---

### **Skills and Abilities**

* **Tentacle Slam:** AoE knockback + Stun
* **Crushing Bite:** Single-target Bleed + Poison (Phase 4)
* **Whirlpool Pull:** Environmental hazard, pulls players to center
* **Water Spout:** AoE cone damage + Slowdown
* **Berserk Tentacle Flail:** Multi-target melee burst (Phase 2+)
* **Summon Abyssal Dreadfins:** 2–4 minions (Phase 2–4)
* **Abyssal Spore Cloud:** AoE DoT hazard (Phase 4)

---

### **Status Effects & CC**

* Stun: Tentacle Slam
* Bleed: Crushing Bite
* Poison: Crushing Bite (Phase 4)
* Slowdown: Water Spout
* Root: Tentacle Slam (Phase 3)

---

### **Mechanics Interaction**

* Terrain hazards: whirlpools pull players into melee range; environmental damage is unavoidable if positioning is poor.
* Minion synergy: Abyssal Dreadfins target casters and ranged to disrupt healer DPS.
* Phases force movement and adaptability; ignoring AoE hazards results in high risk of wipes.

