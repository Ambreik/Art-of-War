			Elder Stoneheart Colossus – Grundmor

**Classification:** Boss (Rank V)
**Level:** 70
**Habitat:** Grimstone Quarry – Central Open Cavern
**Type:** Beast / Elemental Stone Golem
**Element:** Earth
**Monster Type:** Tanky / God / Skillable / Melee

---

### **Description**

Grundmor is an ancient stone colossus, awakened by the tremors of the Grimstone Quarry. Massive and immovable, its body is encrusted with glowing crystal veins, pulsating with earthen energy. Grundmor dominates the cavern floor, its footsteps shaking the ground and creating fissures that harm any who dare approach too closely.

---

### **Behavior**

* Primarily a territorial guardian, reacts aggressively to intruders.
* Uses ground-based AoE attacks to control space and punishes groups that cluster.
* Phases trigger based on health thresholds, altering attack patterns and environmental hazards.
* Able to summon smaller stone golems during combat as adds.

---

### **Phases & Combat Mechanics**

**Phase 1 – Awakening (100–70% HP)**

* Basic melee attacks: powerful but slow swings.
* Ground Slam: small AoE tremor causing minor **Stun**.
* Passive: absorbs damage slowly due to hardened stone.

**Phase 2 – Colossal Wrath (70–40% HP)**

* God Mode: attack speed increased, AoE radius grows.
* Summons 2–3 Stone Minions (Patrol Golem) to assist in combat.
* Uses **Fissure Cracks**: creates temporary hazardous zones, dealing damage over time (**Bleed** effect from rock shards).

**Phase 3 – Tremor Cataclysm (40–10% HP)**

* Berserk: massive increase in attack speed and movement.
* **Earthquake Slam:** AoE knockback, applies **Root** CC.
* **Rolling Boulder:** Targeted charge attack; deals high damage and knocks down players.
* Constant passive AoE: rocks slowly fall from the cavern ceiling, dealing minor damage (**Direct Damage**).

**Phase 4 – Stoneheart Fury (10–0% HP)**

* Maximum Berserk + God Mode active.
* Summons all stone minions simultaneously.
* AoE **Seismic Shockwaves**: interrupts casting and applies **Stun**.
* Can reflect 10–15% of physical damage back to attackers.

---

### **Skills and Abilities**

* **Ground Slam:** Small AoE melee attack, Stun 2s
* **Earthquake Slam:** Large AoE knockback, Root 3s
* **Rolling Boulder:** Single-target high damage + knockdown
* **Fissure Cracks:** AoE damage-over-time, minor Bleed
* **Seismic Shockwaves:** Interrupts spellcasting, AoE Stun
* **Stone Minion Summon:** Summons 2–3 adds, patrol and assist
* **Berserk Mode:** Passive attack speed and damage increase
* **God Mode:** Increases resistance, reduces incoming damage

---

### **Status Effects & CC**

* Stun: Ground Slam, Seismic Shockwaves
* Root: Earthquake Slam
* Bleed: Fissure Cracks
* Knockback: Rolling Boulder
* Minor AoE environmental damage from falling debris

---

### **Mechanics Interaction**

* Terrain interaction: cavern ceiling may collapse during combat phases, requiring players to move.
* Stone Minions act as mechanics to split player attention and force positioning.
* Damage reflection requires careful targeting and coordination.

