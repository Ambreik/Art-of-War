			Lord of the Sunken Legion – Marrowflood Krakhun

**Classification:** Boss (Rank V)
**Level:** 70
**Habitat:** Sunken Warship – Submerged wreckage and surrounding shallow waters
**Type:** Aquatic / Undead Apex Predator
**Element:** Water
**Monster Type:** Skillable / Vampiric / Melee / Berserk

---

### **Description**

Marrowflood Krakhun is the corrupted captain of the Sunken Legion, an undead leviathan clad in rusted armor fused with barnacles and coral. Its skeletal frame is partially covered by drifting seaweed, and its gaping maw constantly drips tainted seawater. The warship itself seems to echo its cries, amplifying its presence.

---

### **Behavior**

* Highly territorial, patrols the wreckage before fully engaging intruders.
* Prefers ambush attacks from the water, pulling players into melee range.
* Summons spectral skeleton marines from the wreckage to overwhelm targets.
* Multiple combat phases triggered by health thresholds; becomes increasingly aggressive with enhanced AoE control.

---

### **Phases & Combat Mechanics**

**Phase 1 – Dread Captain (100–70% HP)**

* Standard melee swipes with weapon + water-swept AoE.
* Passive Vampiric ability: 5% HP drain per successful hit.
* Summons 1–2 Skeleton Mariners when players cluster.

**Phase 2 – Tidal Onslaught (70–40% HP)**

* Berserk activation: attack speed +25%, movement speed +20%.
* AoE tidal slam pushes back players and applies **Slowdown**.
* Summons 3–4 Skeleton Mariners intermittently.
* Bite attack now inflicts **Bleed** DoT.

**Phase 3 – Abyssal Domination (40–10% HP)**

* God Mode active: +40% all resistances, +20% damage reduction.
* AoE Water Jet attacks hitting multiple players in a line.
* Summons additional Skeleton Mariners + spectral tendrils that root players.
* Tidal Roar: Fear for 4s + interrupts casting.

**Phase 4 – Drowned Terror (10–0% HP)**

* Maximum Berserk + God Mode active.
* Summons all remaining Skeleton Mariners simultaneously.
* Water Maelstrom AoE: heavy knockback, minor DoT, and stacking **Slowdown**.
* Bite now applies stacking **Bleed** up to 3 layers.

---

### **Skills and Abilities**

* **Abyssal Slash:** Melee weapon swipe, moderate damage
* **Water Slam:** Knockback AoE, applies **Slowdown**
* **Spectral Summon:** Calls Skeleton Mariners
* **Bite of the Drowned:** Melee attack applying Bleed DoT
* **Tidal Roar:** AoE Fear + casting interrupt
* **Water Jet:** Linear AoE, moderate damage
* **Vampiric Drain:** Restores HP on hit
* **Berserk Mode:** Increases attack and movement speed
* **God Mode:** Reduces incoming damage, increases resistances

---

### **Status Effects & CC**

* Bleed: Bite of the Drowned
* Slowdown: Water Slam, Water Maelstrom
* Fear: Tidal Roar
* Root: Spectral tendrils during Abyssal Domination phase
* Knockback: Water Slam, Water Maelstrom

---

### **Mechanics Interaction**

* Skeleton Mariners force players to manage adds while avoiding AoE and crowding.
* Water Maelstrom interacts with terrain; players may be pushed into hazards (wreckage debris).
* Vampiric Drain punishes unhealed players and punishes long fights.
* Fear + Root mechanics require coordinated positioning.

