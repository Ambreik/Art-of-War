			Mistveil Jungle Matriarch – Serasynth the Bloomfeeder

**Classification:** Boss (Rank V)
**Level:** 70
**Habitat:** Jungle Ridge – Dense Mistveil Jungle
**Type:** Plant / Fungal Apex Predator
**Element:** Earth
**Monster Type:** Skillable / Spawner / Melee / Berserk

---

### **Description**

Serasynth is a colossal fungal entity, its body composed of thick, semi-living mushroom stalks and entangled vines. Spores constantly float around it, creating a toxic, humid haze that clings to intruders. Its presence warps the local flora, infecting the area with smaller aggressive spore creatures.

---

### **Behavior**

* Territorial and ambush-focused, rarely leaves its spore-laden grove.
* Uses spore clouds and vine attacks to control battlefield space.
* Can summon smaller spore minions during combat.
* Multiple combat phases triggered by health thresholds, each increasing area control and DoT effects.

---

### **Phases & Combat Mechanics**

**Phase 1 – Blooming Hunger (100–70% HP)**

* Standard melee vine strikes and stomps.
* Passive: Small spore clouds deal minor **Poison** DoT in proximity.
* Summons 1–2 Sporelings when players cluster.

**Phase 2 – Fungal Fury (70–40% HP)**

* Berserk activation: attack speed increases by 30%.
* Releases larger spore clouds with stacking **Poison** DoT.
* Summons 3–4 Sporelings at intervals.
* Vine Sweep AoE attacks knock back players slightly.

**Phase 3 – Bloomfeeder Ascendant (40–10% HP)**

* God Mode active: resistance +40%, damage reduction +20%.
* Constant spore emission creates temporary zones applying **Slowdown** and **Poison**.
* Uses **Entangling Vines** to Root 3–5 players at random.
* Occasionally performs **Sporeburst**: AoE AoE knockback + fear effect for 3 seconds.

**Phase 4 – Mycelial Overgrowth (10–0% HP)**

* Maximum Berserk + God Mode active.
* Summons all remaining Sporelings and spawns random Mushroom Caps that explode after 6 seconds, dealing **Poison DoT**.
* AoE Vine Slam: large knockback + Stun 2s.
* Aura of spores intensifies, continuous minor DoT to nearby players.

---

### **Skills and Abilities**

* **Vine Strike:** Melee swipe, medium damage
* **Vine Sweep:** AoE knockback, minor Stun
* **Entangling Vines:** Roots multiple players for 3 seconds
* **Sporeburst:** AoE knockback + Fear 3s
* **Poison Spore Clouds:** Continuous AoE Poison DoT
* **Sporeling Summon:** Spawns small minions that attack players
* **Berserk Mode:** Increases attack speed and damage
* **God Mode:** Enhances resistances and reduces damage taken

---

### **Status Effects & CC**

* Poison: Spore Clouds, Sporeburst
* Root: Entangling Vines
* Slowdown: Dense spore zones
* Knockback: Vine Sweep, Sporeburst
* Stun: Vine Sweep AoE

---

### **Mechanics Interaction**

* Spore zones damage over time and affect positioning.
* Sporelings force players to manage adds while avoiding AoE.
* Exploding mushroom caps introduce environmental hazard mechanics.
* Coordination required to avoid stacking DoTs and CCs.

