			Fungal Rot Sovereign – Mycelus the Spore Emperor

**Classification:** Boss (Rank V)
**Level:** 70
**Habitat:** Infested fungal groves, swampy regions, occasionally near underground caves
**Type:** Beast / Fungal Apex Predator
**Element:** Earth
**Monster Type:** Skillable / Melee / Spawner / God / Berserk

---

### **Description**

Mycelus is an enormous, semi-sentient fungal mass. Its cap is dark and mottled, pulsating with spores that form visible clouds around it. Long, sinewy tendrils covered in fungal filaments lash outward to attack intruders. The air smells of decay and damp soil, and spores emitted from its body can infect nearby creatures.

---

### **Behavior**

* Highly territorial, aggressive toward intruders.
* Teleports strategically around battlefield to prevent players from exploiting static positions.
* Summons up to 20 minor fungal minions at every 5% HP drop.
* Pushback/Root phase occurs frequently depending on the number of players engaged.
* Shields itself for 5–10 seconds when pushback/root phase is triggered, regenerating significant HP.
* Uses AoE DoT infection spores that persist in combat zones.

---

### **Phases & Combat Mechanics**

**Phase 1 – Spore Awakening (100–70% HP)**

* Standard melee attacks with tendrils and fungal filaments.
* Casts “Infection” DoT on nearby players.
* Summons small fungal minions occasionally.

**Phase 2 – Mycelial Onslaught (70–40% HP)**

* Teleports randomly to avoid concentrated player damage.
* Summons 10–15 fungal minions at once every 5% HP lost.
* AoE Root + Fear in a 10-meter radius; minor damage.
* Pushback attacks begin at random intervals; applies minor DoT.

**Phase 3 – Emperor’s Dominance (40–10% HP)**

* Berserk Mode: +30% attack speed and +20% movement speed.
* Summons up to 20 fungal minions at each 5% HP drop.
* Pushback/Root phase occurs more frequently depending on player count.
* Shields itself for 5–10 seconds, regenerating HP; duration scales with number of players.
* AoE Infection persists and spreads; deals minor DoT and slows movement by 50%.

**Phase 4 – Rotstorm Ascendant (10–0% HP)**

* God Mode: +40% resistances, +50% damage mitigation.
* Pushback/Root phase nearly constant; teleportation occurs rapidly.
* AoE DoT and Fear pulses every 8 seconds.
* Spawns fungal minions continuously; max simultaneous 20 minions.
* Final 5% HP triggers massive AoE DoT burst and full Fear + Root combo.

---

### **Skills and Abilities**

* **Tendril Slam:** Melee attack; moderate damage + small AoE knockback
* **Infection Spores:** AoE DoT; applies “Infection” debuff
* **Pushback Root:** Knocks back and roots players for 3–5 seconds
* **Fearful Mycelium:** AoE Fear effect, interrupts casting
* **Summon Minions:** Spawns up to 20 fungal monsters per 5% HP lost
* **Shielded Regeneration:** Becomes invulnerable for 5–10 seconds, regenerating HP
* **Teleport:** Randomly teleports to reposition in the battlefield

---

### **Status Effects & CC**

* DoT: Infection
* Root: Tendril immobilization
* Fear: AoE pulses
* Daze: Occasional during pushback/root phase
* Knockback: Tendril slam

---

### **Mechanics Interaction**

* Pushback/root phases disrupt positioning and channeling abilities.
* Shielded regeneration phase scales with player count, forcing strategic timing.
* Summoned minions add pressure and must be managed to avoid overwhelming players.
* Teleports prevent players from kiting or cornering the boss.

