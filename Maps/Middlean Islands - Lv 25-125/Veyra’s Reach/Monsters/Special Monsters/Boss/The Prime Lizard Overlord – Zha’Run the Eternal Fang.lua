			The Prime Lizard Overlord – Zha’Run the Eternal Fang

**Classification:** Boss (Rank V)
**Level:** 70
**Habitat:** Jungle Ridge High Cliffs & Hidden Lizard Tribe Sanctuaries
**Type:** Animal / Beast / Legendary Monster
**Element:** Wind
**Monster Type:** God / Skillable / Berserk / Melee / Tanky

---

### **Description**

Zha’Run, the Eternal Fang, is the apex of the Lizard Tribe, a colossal reptilian predator whose scales shimmer with wind-imbued patterns. Its roar resonates across the ridge, rallying lesser lizards to its side. Zha’Run’s combat style is highly adaptive, shifting through phases that leverage raw physical might, summon reinforcements, and elemental wind strikes.

---

### **Behavior**

* Highly territorial and extremely intelligent; uses ambush tactics and terrain to advantage.
* Prioritizes players dealing the most damage but can shift focus dynamically during Berserk phases.
* Commands nearby lizard mobs as part of its combat strategy, occasionally summoning elite or regular lizards mid-battle.
* Uses environmental terrain, gusts of wind, and cliff edges for positional hazards.

---

### **Phases & Combat Mechanics**

**Phase 1 – Dominance of the Ridge (100–70% HP)**

* Melee sweeps and claw slashes with moderate damage.
* Summons **Wind Gusts**: AoE pushback that can disrupt positioning.
* Commands 3–5 Soldier Lizard minions to join battle.
* Medium movement and attack speed.

**Phase 2 – Predator Ascendant (70–40% HP)**

* Enters **God Mode**: faster attacks and temporary resistance boost (+25% physical damage reduction).
* Adds ranged attacks via **Spine Shards**: small projectiles from tail spikes, medium damage, applies **Bleed**.
* Summons 2–4 Archer Lizards to provide ranged harassment.
* Uses **Tail Sweep** to knock back multiple players and cause **Stun**.

**Phase 3 – Eternal Wrath (40–10% HP)**

* Berserk: attack speed and movement increase by 40%; tail and claw attacks now apply **Poison** DoT.
* Summons 2–3 Shaman Lizards that cast minor AoE **Wind Slashes** and **Root** CCs.
* Environmental hazard: cliff edges may collapse during melee impacts, causing minor AoE damage and movement penalties.
* Occasional **Leap Slam**: targets clusters of players, heavy AoE damage + **Bleed**.

**Phase 4 – Final Fury (10–0% HP)**

* Maximum Berserk and God Mode stacks.
* Summons all nearby minions (Soldier, Archer, and Shaman types) for one last assault.
* Tail Sweep applies **Fear** for 3s; Spine Shard becomes AoE splash.
* Constant AoE Wind Gusts push players, requiring careful positioning.

---

### **Skills and Abilities**

* **Tail Sweep:** AoE knockback + Stun, medium damage
* **Claw Slash:** Single-target melee, applies Bleed
* **Spine Shard:** Medium-range projectile, applies Bleed
* **Leap Slam:** AoE melee from leap, high damage + Bleed
* **Wind Gust:** Pushback AoE, disrupts positioning
* **Command Minions:** Summons Soldier, Archer, and Shaman lizards during combat
* **Berserk Frenzy:** Passive at <40% HP, increases attack speed, movement, and DoT application

---

### **Status Effects & CC**

* Stun: Tail Sweep
* Bleed: Claw Slash & Spine Shard & Leap Slam
* Poison: Claw and Tail attacks (Phase 3+)
* Root: Shaman Lizard summons
* Fear: Tail Sweep (Phase 4)

---

### **Mechanics Interaction**

* Terrain: cliff edges collapse during heavy attacks, forcing players to move.
* Minions synergize with boss abilities, requiring crowd control focus.
* Phases require adaptation: ignoring AoE or minions can result in wipe due to stacking DoTs and CCs.
* Positioning crucial: Wind Gusts push players toward hazards.

