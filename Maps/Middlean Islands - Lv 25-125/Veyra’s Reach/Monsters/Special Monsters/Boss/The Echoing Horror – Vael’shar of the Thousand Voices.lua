**The Echoing Horror – Vael’shar of the Thousand Voices**

**Classification:** Boss (Rank V)
**Level:** 70
**Habitat:** Various locations on the island, spawns only at night or during weekends after all Mystic Stones are destroyed
**Type:** Esoteric / Mystic Apex Predator
**Element:** Wind
**Monster Type:** Skillable / Melee / Magical / Berserk / Spawner

---

### **Description**

Vael’shar is a nightmarish, semi-ethereal being composed of distorted, shifting shadows and phantom voices. Its body constantly ripples with spectral forms, making it hard to target, and its eyes emit an eerie violet glow. The air around Vael’shar is thick with an unnatural fog, and a chorus of whispers unnerves any intruder.

---

### **Behavior**

* Appears only under very specific conditions: night-time or weekend events after all Mystic Stones on the island are destroyed.
* Aggressive and relentless; uses ranged spectral attacks while closing in with melee strikes.
* Spawns Mystic Stones during combat, which must be destroyed immediately to prevent excessive HP regeneration.
* Boss periodically teleports around the battlefield to prevent players from staying stationary.

---

### **Phases & Combat Mechanics**

**Phase 1 – Whispered Menace (100–70% HP)**

* Melee swipes and spectral ranged attacks.
* Periodically spawns 1–3 Mystic Stones.
* HP regenerates 1% every 10 seconds while stones exist.
* CC: Fear and Knockback from AoE spectral pulses.

**Phase 2 – Chorus of Agony (70–40% HP)**

* Increases attack speed by 25%.
* Summons additional Mystic Stones up to 5 at once.
* While stones exist, all damage dealt to Vael’shar “Misses” unless stones are destroyed.
* Stones destroyed trigger double HP loss retroactively.
* Uses AoE Fear and Knockback more frequently.

**Phase 3 – Thousand Voices (40–10% HP)**

* Berserk mode: +50% attack speed, +20% movement speed.
* Teleports frequently to Mystic Stone locations.
* Summons spectral minions (illusionary phantoms) to attack players while stones are active.
* AoE Fear lasts 6 seconds; Knockback damage amplified.
* HP regen mechanics remain active until all Mystic Stones are destroyed.

**Phase 4 – Echoing Collapse (10–0% HP)**

* God Mode activated: +40% resistances, +50% damage mitigation.
* Mystic Stones spawn simultaneously at random locations; player coordination critical.
* AoE Fear, Knockback, and additional spectral chains root and stun players.
* Teleports more aggressively to avoid being cornered.

---

### **Skills and Abilities**

* **Phantom Strike:** Melee slash with spectral energy; moderate damage + minor DoT
* **Voice of Dread:** AoE Fear + Knockback around boss
* **Mystic Stone Summon:** Summons Mystic Stones; HP regen active while stones exist
* **Spectral Pulse:** Ranged AoE attack, interrupts casting
* **Shadow Teleport:** Randomly moves around battlefield, avoids clustering damage
* **Illusory Minions:** Summons phantoms that attack players
* **Berserk Mode:** Increases attack and movement speed during Phase 3
* **God Mode:** Reduces incoming damage and increases resistances

---

### **Status Effects & CC**

* Fear: AoE during all phases
* Knockback: AoE pulses, increases during Phase 3+
* Root: Spectral chains during Phase 4
* Miss: All damage “misses” while Mystic Stones exist
* Bleed/DoT: Phantom Strike adds minor DoT

---

### **Mechanics Interaction**

* Mystic Stones must be destroyed quickly, or boss HP regen escalates and prevents damage.
* Damage to the boss is fully ineffective while stones exist; coordination required.
* Teleports prevent players from exploiting static positions.
* AoE Fear + Knockback forces continuous movement and careful positioning.
* Illusory Minions distract players and can interrupt Mystic Stone destruction if ignored.

