			Gloomvine Creeper

**Rank:** Rare (III)
**Level:** 60
**Type:** Beast / Plant Hybrid
**Element:** Earth
**Habitat:** Dense undergrowth and jungle ridges of the island

---

## **📘 Description**

The Gloomvine Creeper is a sinister, vine-entwined predator that slithers along forest floors. Its body resembles a twisted amalgamation of thick vines and sinewy muscles, with jagged thorns protruding along its back. Camouflaged perfectly in underbrush, it waits patiently for prey to come near before striking.

---

## **🧬 Behavior**

* Ambush predator; remains motionless until a target approaches.
* Can retract into foliage, disappearing from sight.
* Moves silently and swiftly through dense vegetation.

---

## **⚔️ Combat Mechanics**

* Melee attacks: lashing vine strikes, inflicting physical damage and minor DoT from thorns.
* Grapple: attempts to entangle a single player, reducing movement speed by 50% for 5 seconds.
* Vine Swipe: AoE melee attack hitting all nearby players.
* High evasion; avoids projectiles occasionally.

---

## **☠ Status Effects & CC**

* Affects with: **Bleed**, **Root**, **Slowdown**

---

## **🌀 Mechanics Interaction**

* Gains attack bonus (+20% damage) when hidden in foliage or shadows.
* Interactions with fire: if area is on fire, takes minor increased damage but releases toxic spores, applying **Poison** DoT to nearby players.
* Stealthy players have reduced detection chance; noisy players trigger ambushes.

---

## **📜 Quotes**

• “The vines remember every step you take.”
• “Do not linger… or be claimed by the shadows.”

---

## **♻ Respawn / Encounter Rules**

* Spawn timer: 15 seconds (Rank III)
* Solo monster; may spawn in pairs (2–3) in particularly dense jungle clusters.
* Aggro is persistent within 15m radius; retreats if target leaves area.

---

## **⚠ Special Rules**

* Can camouflage in undergrowth; partially invisible when idle.
* Environmental trigger: if the jungle floor is disturbed by other creatures, may initiate early ambush.

