			Shalehide Burrowmaw

**Type:** Rare Elite
**Threat Level:** ★★★☆☆
**Location:** *Grimstone Quarry – Collapse Tunnel #2*, near a broken mining lift.
**Spawn Behavior:** **Conditional Rare** (explained below).

---

## 🩸 **1. Appearance & Identity**

A massive, armored subterranean beast resembling a fusion of a mole, pangolin, and blind basilisk.
Its hide is made of layered **stone-like shale plates**, giving it both its name and unmatched physical resilience.

* Eyes: milky white, nearly blind.
* Body: thick segmented armor, constantly shedding dust.
* Claws: obsidian-black, perfect for carving through stone.
* Sound: low rumbling growl that vibrates through the ground.

---

## ⚔️ **2. Combat Style & Abilities**

**Role:** Burrowing ambusher / anti-melee bruiser.

### **Abilities**

1. **Shale Charge**
   Burrowmaw erupts from underground, dealing heavy physical damage in a straight line.

2. **Dust Choke**
   A cone of rock dust that blinds players for 2 sec.

3. **Hardened Plates**
   Temporarily gains 25% damage reduction.

4. **Seismic Shudder**
   Burrows underground and emerges randomly, knocking players back.

---

## 🧭 **3. Spawn Rules**

This rare **only spawns under certain conditions**, making it feel unique and rewarding to find.

### ✔ **Spawn Condition**

When players mine **any Grimstone Quarry ore node**, there is a **0.8% chance** that:

* The ground begins shaking
* The ore node collapses
* **Shalehide Burrowmaw bursts from below**

This creates a fun “dangerous mining zone” experience.

### Additional Rules

* Can spawn only **once every 30–45 minutes** if not defeated.
* Shares detection with all players (whoever triggers it first).

