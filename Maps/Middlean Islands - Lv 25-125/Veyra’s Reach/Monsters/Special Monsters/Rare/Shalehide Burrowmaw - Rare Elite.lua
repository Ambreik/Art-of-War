## 🗝️ **5. Special Mechanics**

Here is where the fun begins:

### ⭐ **A. Burrowmaw Key → Special Chest**

If the player obtains a **Burrowmaw Key**, after the rare dies a **Shale-Locked Chest** spawns nearby for 60 seconds.

* Only players who dealt damage can open it.
* Contains 1 guaranteed special resource and 1 roll rare reward.

### ⭐ **B. Mount/Pet Mechanic**

* Rare chance (e.g., 1%) to drop **Shalehide Seal** → unlocks a **ground mount**:
  **Shalehide Crusher** (stony armored mole creature).
* OR a very small chance (3–5%) to drop a **Cracking Pet Egg**.

### ⭐ **C. Mining Danger Mechanic**

While fighting Burrowmaw, nearby ore nodes **shatter and deal AoE damage** if players stand too close — reinforcing the idea that he’s disturbing the quarry.

---

## 🗺️ **6. Location Details (No excessive caves)**

Grimstone Quarry has **3 surface tunnels and 1 deep cavern**.

Burrowmaw is associated only with:

* **Surface Tunnel #2**
* Found near a **collapsed mining lift**, exactly where miners “recently triggered seismic activity.”

This keeps the POI coherent.

---

## 📌 **7. Notes for Balance**

* Soloable by geared players, more comfortable as a small group.
* Nighttime makes him 15% stronger like all monsters in the zone.

