			Spectral Serpent of the Walls

**Rank:** III
**Appearance:** A long blue spectral snake phasing through stone
**Abilities:**

* Phase Bite (ignores armor)
* Stone Merge (hides in wall briefly)
* Spectral Shriek (fear)

**Drops:**

* *Spectral Fang*
* *Rune of Ethereal Wings* (rare)
* *Rank III Rune Core*

### **Alyndor Behavior:**

If the Serpent appears:

* He loudly says: “This one requires precision.”
* Uses high-damage arcane spells
* Occasionally calls: “Strike now while it materializes!”

### **Trigger Conditions**

Rare spawns appear when:

* Nighttime (higher chance)
* During heavy fog or storms
* When 2+ players are near the walls
* When Alyndor is patrolling

