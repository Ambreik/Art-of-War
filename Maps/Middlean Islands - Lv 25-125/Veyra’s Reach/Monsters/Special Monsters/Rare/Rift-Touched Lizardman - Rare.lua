			Rift-Touched Lizardman

**Rank:** II→III (scales based on party size)
**Appearance:** Lizardman with glowing cracks across body
**Abilities:**

* Arcane Slash
* Rift Leap (teleport short distance)
* Instability Surge (AoE pulse every 20 sec)

**Drops:**

* *Crackling Rune Core (Rank II)*
* *Arcane Residue*
* Chance: *Pattern: Rune of Static Veil*

### **Alyndor Behavior:**

If he detects it:

* Calls player via system message
* Begins casting *Arcane Anchor* to reduce monster teleporting
* Fights aggressively but stops if at <30% HP and teleports away

### **Trigger Conditions**

Rare spawns appear when:

* Nighttime (higher chance)
* During heavy fog or storms
* When 2+ players are near the walls
* When Alyndor is patrolling

