# **Hostile Remnant – Mini Encounter Design**

## **Overview**

A spectral fragment of a long-fallen warrior trapped within the artifact.
It is not a full mob—more of a short, intense **solo challenge** designed to surprise the player when interacting with the quest item *Barnacle-Sealed Token*.
It cannot leave the artifact space and does not drop loot.
Its purpose is purely narrative + challenge.

---

## **1. Encounter Trigger**

**Triggered by:**

* Attempting to pry open or magically analyze the *Barnacle-Sealed Token* (quest item).
* 20% base chance to trigger **Hostile Remnant Emergence Event**.
* If triggered, the token cracks and releases a spectral echo of a drowned fighter.
* The encounter locks the player in a **ghostly bubble arena** (radius 8m) for the duration.

---

## **2. Hostile Remnant – Entity Type**

**Name:** *Sunken Echo Remnant*
**Type:** Undead • Water-Phantom • Mini-Encounter
**Level Scaling:** Matches player level (±0)

---

## **3. Combat Mechanics**

### **Abilities:**

#### **1. Drowned Slash**

* **Effect:** Melee strike dealing moderate physical + minor water damage.
* **Extra:** Applies *“Waterlogging”* (stackable slow, +5% slow per hit, max 4 stacks).

---

#### **2. Echo Pulse**

* **Effect:** 6m AoE burst of spectral energy.
* **Damage:** Low–moderate.
* **Additional:** Briefly disrupts casting (0.5s pushback).

---

#### **3. Anchor Grasp**

* **Effect:** The remnant pulls the player toward itself.
* **If player reaches 0m distance:** Applies *“Drowning Grip”* (stun 1s + minor damage).
* **Cooldown:** 12s.

---

#### **4. Fading Reconstitution**

* **Trigger:** When reaching 25% HP.
* **Effect:** Becomes semi-transparent, taking 30% reduced damage for 5 seconds.
* **Visual:** Flickering broken silhouette.

---

## **4. Win / Fail Conditions**

### **Win:**

* Defeat the remnant →

  * The *Barnacle-Sealed Token* fully cracks open.
  * The quest item inside becomes accessible.
  * Player receives a small surge of XP (tiny amount, because mini-encounter).

### **Fail:**

* Player dies or leaves the bubble (leaving counts as a fail).
* After failure, the token resets and remains unopened.
* The remnant despawns after 5 seconds.

---

## **5. Behavior & AI Style**

* Aggressive.
* Focuses on staying within 4–6m of the player.
* Prioritizes **Anchor Grasp** if the player attempts to kite.
* Mild unpredictability: occasionally pauses to “shiver”, then teleports 4m behind the player (cooldown 15s).

---

## **6. Visual & Audio**

* **Visual:**

  * Transparent ghost coated in dripping spectral water.
  * Broken anchor chain floats around its torso.
  * Emits pale teal glow pulses.

* **Audio:**

  * Muffled underwater growls.
  * Chain rattling.
  * Heartbeat bass pulse when low HP.

---

## **7. Location Context**

It exists *inside the artifact*, not in the world.
The token creates a fantasy micro-space: swirling water illusion, dim bluish light, echo distortion.

---

## **8. Purpose in Progression**

* Gives the player a surprising challenge.
* Suggests ancient magic within the token.
* Introduces the theme of **sunken phantoms** and drowned warriors (ties to the warship zone).
* Prepares the player narratively for later content involving spectral water foes.

