		6. Respawn & Encounter Rules

* **Spawn Timer:** 4–6 real hours (server-wide).
* **Spawn Chance:** 100% base, but 10% chance to spawn **Rank III variant (Abyssal Sovereign Vyrraak)** — with doubled HP, improved drops, and enhanced visual effects (purple glow, mist trail).
* **Combat Lock:** Only one group may engage the creature at a time; once in combat, the cave entrance seals with falling debris.
* **Escape Condition:** If all players die, Vyrraak retreats into the depths, despawning after 3 minutes.

