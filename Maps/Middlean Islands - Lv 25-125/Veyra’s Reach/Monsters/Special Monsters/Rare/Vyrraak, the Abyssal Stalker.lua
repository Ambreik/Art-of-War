			Vyrraak, the Abyssal Stalker

**Classification:** Rare Elite Predator (Rank II → can occasionally spawn Rank III variant)
**Habitat:** *Secluded Cliff Cave*, western cliffs behind the waterfalls of "Veyra’s Reach"
**Type:** Aquatic / Amphibious Beast
**Behavior:** Ambush predator, territorial guardian

---

		1. Overview

	Hidden behind the roaring veil of the western waterfalls, "Vyrraak, the Abyssal Stalker", lurks within a half-flooded cavern network.
	Few who dive deep enough survive the encounter — the creature is said to move between the shadows and water currents, its form barely visible before it strikes.

	Legends claim Vyrraak was once the hunting companion of a forgotten Crusader captain, twisted by the tainted waters that seep from the mountain’s sealed tomb.

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

		2. Appearance

* **Body:** Long, eel-like frame (6–8 meters), plated with dark, reflective scales.
* **Eyes:** Luminous cyan glow; visibility hint for players in darkness.
* **Markings:** Jagged bioluminescent streaks running from the head to tail.
* **Special Detail:** Along its spine are broken chains — remnants of its lost master’s command harness.

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

		3. Behavior & AI

| State               | Description                                                                                              |
| ------------------- | -------------------------------------------------------------------------------------------------------- |
| **Idle State**      | Remains submerged, circling slowly in the inner pool. Emits occasional growls and bubble bursts.         |
| **Detection State** | Detects player movement or sound within 12m; faint water disturbance alerts nearby players.              |
| **Ambush Phase**    | Leaps from water or ceiling crevice with 2-second delay, dealing high burst damage.                      |
| **Combat State**    | Alternates between slashing melee and long-range acid spit attacks. Occasionally dives to reposition.    |
| **Retreat Phase**   | At 20% HP, dives and disappears temporarily (5–7 sec), re-emerges with a defensive buff “Abyssal Shell”. |
| **Death Phase**     | Emits a dying roar that shakes the cave, causing rockfall (minor AoE hazard).                            |

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

		4. Combat Abilities

	1. Crushing Leap
* Jumps from high ledges or water surface, slamming target for heavy physical damage. Small shockwave knocks nearby players down. 
	
	2. Acid Torrent
* Spits a line of corrosive fluid; deals damage over 5 seconds and reduces armor by 15%.
	
	3. Echo of Chains
* Whip-like tail attack; applies “Suppression” debuff (reduces movement speed by 40% for 8s).
	
	4. Abyssal Shell (Enrage Phase)
*When HP < 20%, gains +25% resistance to all elements and emits shadow mist reducing visibility for players.
	
	5. Lurking Hunger (Passive)
* Regenerates 2% HP every 5 seconds while underwater or submerged. Forces players to fight aggressively to prevent healing.

