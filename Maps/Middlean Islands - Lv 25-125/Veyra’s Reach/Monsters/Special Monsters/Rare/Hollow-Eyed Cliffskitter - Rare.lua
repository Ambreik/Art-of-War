## **🌀 Mechanics Interaction**

* Cliffs and uneven terrain enhance attack accuracy; players on ledges or slopes may take extra damage.
* Can trap players against cliff walls or in crevices temporarily.
* Detects movement up to 18m; stealthier players are harder to detect.

