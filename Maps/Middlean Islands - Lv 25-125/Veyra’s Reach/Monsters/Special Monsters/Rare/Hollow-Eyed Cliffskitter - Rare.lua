			Hollow-Eyed Cliffskitter

**Rank:** Rare (III)
**Level:** 62
**Type:** Animal / Beast
**Element:** Wind
**Habitat:** Rocky cliff faces and ledges near Veyra’s Edge

---

## **📘 Description**

The Hollow-Eyed Cliffskitter is a lithe, nocturnal predator, its sinewy body covered in mottled gray scales that blend seamlessly with cliff stone. Its unnerving, empty eye sockets glow faintly in darkness, creating a haunting presence for any intruder. Agile and cunning, it strikes from ledges and crevices, preferring hit-and-run tactics.

---

## **🧬 Behavior**

* Prefers vertical terrain; scales cliff faces with ease.
* Circles patrol areas quietly before attacking from ambush.
* Will retreat to higher ledges if heavily damaged.

---

## **⚔️ Combat Mechanics**

* Melee attacks: quick slashes with clawed forelimbs.
* Leap strike: jumps down from ledges to target a player, causing moderate AoE impact.
* Skittering rush: rapid multi-strike movement along cliff edges.
* High mobility and precision strikes; focuses on isolated targets.

---

## **☠ Status Effects & CC**

