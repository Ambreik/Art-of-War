			Grimhowl Burrowfang

**Rank:** Rare (III)
**Level:** 62
**Type:** Animal / Reptile
**Element:** Earth
**Habitat:** Grimstone Quarry – Collapse Tunnel #1

---

## **📘 Description**

The Grimhowl Burrowfang is a massive, subterranean lizard with armored scales adapted to the rocky tunnels of Grimstone Quarry. Its jagged claws and bone-like protrusions allow it to burrow rapidly, ambushing unsuspecting miners and explorers. Its growl reverberates through the tunnels, warning those nearby of its presence.

---

## **🧬 Behavior**

* Ambush predator that uses underground tunnels to appear suddenly near intruders.
* Patrols short distances within its tunnel network.
* Can retreat into burrows to escape combat if heavily damaged.

---

## **⚔️ Combat Mechanics**

* Melee attacks with sharp claws and tail sweeps.
* Burrow strike: lunges from underground to knock back players.
* Rapid bite combos: moderate burst damage with chance to apply bleeding DoT.
* Uses hit-and-run tactics if player group is large.

---

## **☠ Status Effects & CC**

* Affects with: **Bleed**, **Knockback**
* Can apply minor **Slowdown** when slashing multiple targets

---

## **🌀 Mechanics Interaction**

* Burrows can temporarily collapse parts of the tunnel, slightly damaging players in melee range.
* Aggressive in confined spaces — leash radius 15 meters.
* Players attacking from above (ledge) may be targeted with burrow strike more frequently.

---

## **📜 Quotes**

