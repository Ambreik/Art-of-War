# **7. Location Details**

### **Molten Vein Chamber**

* Semi-open chamber with shallow lava streams and natural stone bridges
* Narrow paths over lava serve as both hazard and tactical element
* Players must navigate carefully; knockbacks from attacks may push into molten pools
* Safe exits: climbable ledges, **Propulsion Stones**, or retracing path to Grimstone Quarry main shaft

---

This rare elite introduces **environmental hazard and positioning-based combat**, adding variety alongside the **tanky Behemoth** and **agile Dreadscale Horror**.

