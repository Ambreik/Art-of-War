			Molten Spire Golem

**Type:** Rare Elite • Grimstone Quarry
**Location:** **Molten Vein Chamber**, a semi-open cavern near volcanic fissures with flowing lava streams
**Role:** Heavy-hitting tank/area control

---

# **1. Overview**

The *Molten Spire Golem* is a hulking construct of magma and jagged stone, animated by residual elemental energy within the Veyrite veins. It patrols a narrow bridge-like area over shallow lava flows, making its arena **hazardous for players**. Its main threat is **fire damage and knockback**, forcing players to pay attention to positioning.

---

# **2. Visual Description**

* Massive stone body with glowing molten cracks
* Lava cores visible inside joints
* Arms ending in magma-crusted fists
* Emits a low, rumbling heat distortion around it

---

# **3. Combat Behavior & Mechanics**

### **Abilities**

#### **• Magma Slam**

* Slams fists onto ground, causing area-of-effect fire damage
* Leaves temporary burning patches on the ground

#### **• Lava Surge**

* Summons a small eruption from fissures
* Deals moderate AoE fire damage and briefly stuns players

#### **• Rock Throw**

* Hurls chunks of molten rock at range
* High knockback + damage

#### **• Volcanic Roar**

* Taunts players within 8m radius, reducing movement speed for 5 seconds
* Environmental hazard: lava fissures near edges may push players into fire

#### **• Hardening Shell**

* Temporarily reduces damage taken by 40%
* Visual cue: molten cracks dim as the shell hardens

---

# **4. Spawn Rules**

* Only spawns in **Molten Vein Chamber**
* Limited to one active instance
* Respawn: 60–90 minutes
* Conditional spawn:

  * Triggered if **5+ Veyrite Ore nodes** are mined in the chamber
  * Chance to spawn increases if previous elite/semi-bosses have been killed in the quarry recently

