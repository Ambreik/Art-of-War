			Abyssal Maw — Rare Monster

**Type:** Aquatic Rare Monster
**Rank:** III (Endgame-level for solo or small party)
**Location:** Coastal reefs and deep water near Coralvine Bloom patches; occasionally patrols near Sunken Warship.
**Spawn Rules:**

* Only spawns **during daytime** or low-tide phases.
* Requires player presence in the area; **dynamic respawn** every 1–2 hours after being killed.
* Maximum 1 spawn per Reef Cluster.

---

### 🦈 **Appearance & Behavior**

* Large shark-like creature, approx. 8–10 meters long.
* Fins and tail have jagged edges; **bioluminescent stripes** along its body that pulse when agitated.
* Mouth filled with serrated teeth, capable of **grappling swimmers**.
* Agile in water; can leap partially out to attack players on shore or boats.
* Behavior: patrols reef clusters slowly; becomes **aggressive** when players enter proximity (15–20m).

---

### ⚙️ **Combat Mechanics**

* **Attacks:**

  * Bite: standard melee, chance to apply *Bleeding* (damage over time).
  * Tail Swipe: AoE knockback in water, knocks swimmers back to shallow edges.
  * Leap Attack: jumps from water to hit players on shore (medium damage).

* **Special Mechanics:**

  * **Ambush Behavior:** hides near Coralvine Bloom patches, attacking unsuspecting players who harvest herbs nearby.
  * **Environmental Interaction:** can destroy small fishing boats or fishing platforms (cosmetic/temporary only).

* **Combat Notes:**

  * Players fighting Abyssal Maw should **stay on reef edges or use ranged attacks** to minimize risk of knockback/leap damage.
  * Occasionally retreats to **Sunken Warship debris** if HP drops below 30%, making pursuit challenging.

---

### 🎁 **Loot Concept**

* **Drops:**

  * Abyssal Scale (common) → crafting material for armor or potions.
  * Rare drop: *Shard of the Reef* (chance 5%) → used for high-tier aquatic potion crafting.
  * Coin: small silver/gold amount.

* **Notes:**

  * **No taming mechanic**.
  * **No mount/pet**.
  * Loot is **solo or group shared**, encouraging group engagement.

