### 🌊 **Environmental / Location Integration**

* **Preferred terrain:** coral reefs with multiple small islands, underwater caves, and near sunken ship.
* **Special interaction:** players can **lure it toward Sunken Warship** to unlock rare treasure mechanics (optional).
* **Spawn cues:** bioluminescent stripes glow brighter before it attacks.

---

### 🛠️ **Gameplay Notes**

* Can be used for **rare herb guarding mechanic** (players must harvest Coralvine Bloom while avoiding or defeating it).
* Encourages **group tactical play**, especially near shallow reefs.
* Provides **dynamic rare spawn content** to keep Veyra’s Edge engaging.

