			Tidehunter Lurker (Rare)

### **Overview**

* Amphibious predator, both in shallow waters and on rocky tidal cliffs.
* Rare encounter designed for **group play**.
* Cannot be damaged by solo players.
* Loot is individual, based on consistent damage contribution.

---

### **Spawn Location**

* **Primary:** Coastal cliffs and tide pools.
* **Spawn Condition:** Only during **low tide**, visible for a short period (~10–15 minutes per tide cycle).
* Can emerge from shallow waters or rocks depending on tide.

---

### **Combat Mechanics**

#### **Party Requirement**

* **Minimum Party Size:** 3 players.
* Solo players deal **0 damage**.
* Scaling:

  * More players → harder to kill (higher HP and stronger attacks).
  * More players → **wider loot distribution per participant**.

#### **Abilities**

1. **Ambush Strike**

   * Leaps from rocks or water, dealing moderate physical damage.
   * If underwater → applies **“Tidal Shock”**, briefly disorients player (screen sway + slowed swimming, 3s).

2. **Poisonous Bite**

   * Inflicts **-15% swimming speed** for 10 seconds.
   * If underwater, player bubbles mechanic (see below).

3. **Tidal Slam**

   * AoE slam in 3m radius (on rocks or water edge), knocks back nearby players.
   * Slight stagger if hit.

---

### **Unique Mechanics (Aquatic Version)**

* **Oxygen Bubble Mechanic:**

  * When underwater, bubbles escape from the Lurker’s mouth every 10–15s.
  * Players must **attack bubbles** to refill their **Oxygen Bar**.
  * If all players fail to pop bubbles in time → gradual HP loss from drowning.
  * On land, bubbles mechanic ignored.

---

### **Loot / Rewards**

* **Rare Drop:** Tidehunter Carapace → used for cosmetic gear or cooking recipes.
* **Rare Drop:** Core Beast Fragment -> item used for crafting "“Mini Mauler.”
* **Loot Distribution:** Individual based on consistent damage dealt.
* Drops **gold / minor consumables** randomly.
* Cannot be tamed.

---

### **Visual & Audio**

* **Visual:**

  * Amphibious, slick-scaled predator.
  * Dark green and gray camouflaged skin.
  * Mouth emits bubbles when submerged.
  * Red eyes glow faintly in low tide pools or shaded rocks.

* **Audio:**

  * Hissing and splashing.
  * Distorted underwater growl when attacking.

---

### **Behavior**

* Highly territorial, hides near water edges.
* Prefers to **ambush players** stepping too close.
* Can move between shallow waters and rocks during combat.
* Targets **the player dealing highest threat** first.

---

### **Notes / Optional Flavor**

* The underwater bubble mechanic encourages **coordination in group combat**, especially for shallow dives.
* Mechanic teaches players to **manage oxygen** while fighting aquatic rares, adding environmental challenge.
* Terrestrial version could have **rock traps or algae patches** to slow players, adding strategy for land combat.

