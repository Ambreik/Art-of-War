			Venomlash Mirestalker

**Classification:** Rare Monster (Rank III)
**Habitat:** Havenstead Plains – marshy lowlands, overgrown roots, wet ditches
**Type:** Beast / Reptile
**Behavior:** Silent ambusher, toxin-based assassin, hates loud noises

---

# **1. Overview**

The **Venomlash Mirestalker** is a highly evolved swamp predator, known for its extreme patience and lethal toxin glands. It hides beneath shallow water or mud, striking only when prey comes within range. Its elastic jaw structure allows a whip-like venom spit, giving the creature its name. Rarely seen walking, it moves through the wetlands with near-silent glide-like motions.

A single encounter can be fatal to unprepared players due to its **stacking poison**, sudden ambush strikes, and high evasion.

---

# **2. Appearance**

* **Size:** 2.5 meters long, low to the ground
* **Coloration:** Swamp-green scales with darker mottled patches for camouflage
* **Eyes:** Bright yellow, narrow pupils adapted to low light
* **Body Features:**

  * Translucent venom sacs along the jawline
  * Razor-lined maw capable of extending outward like a whip
  * Micro-spines on its back that emit faint green glow when agitated
* **Movement:** Slithers with a low, fluid motion, leaving thin venom trails

