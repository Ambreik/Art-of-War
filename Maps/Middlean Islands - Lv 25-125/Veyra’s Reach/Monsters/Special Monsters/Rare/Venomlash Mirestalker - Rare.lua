# **3. Behavior & AI**

| State                       | Description                                                                                  |
| --------------------------- | -------------------------------------------------------------------------------------------- |
| **Dormant Ambush State**    | Buried under mud with only eyes exposed; 80% reduced detection radius unless stepped near.   |
| **Detection State**         | Detects vibrations and movement within 14 meters.                                            |
| **Ambush Opening**          | Springs out with **Toxin Pounce**, dealing high initial damage and applying 2 poison stacks. |
| **Combat State**            | Keeps distance when possible, using venom spit; closes in when target’s HP < 50%.            |
| **Evasion Pattern**         | Performs sudden zig-zag dashes every 12 seconds, increasing dodge chance drastically for 3s. |
| **Frenzy State (HP < 30%)** | Gains +25% movement speed and +40% attack speed; venom stacks apply twice as fast.           |
| **Death Behavior**          | Releases a final toxin burst that poisons anyone within 6 meters.                            |

