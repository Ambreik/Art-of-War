# **7. Respawn & Encounter Rules**

* **Rank:** III – Rare Monster
* **Spawn Timer:** 15 seconds
* **Spawn Pattern:**

  * Fixed rare spawn points around swampy areas
  * Has a 30% chance to spawn instead of normal swamp creatures
* **Leash Radius:** Medium – returns to mud pool after 25 meters
