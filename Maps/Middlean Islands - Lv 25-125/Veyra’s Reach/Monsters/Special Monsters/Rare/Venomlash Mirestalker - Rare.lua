# **6. Mechanics Interaction**

* **Environmental Advantage:** Gains passive regen and +10% evasion when in water or mud.
* **Stacking Poison Mechanic:** Works with game’s standard DoT system; stacks refresh independently.
* **Armor-Penetration Mechanics:** Fully compatible with Crushing Blow / AP builds.
* **Night Behavior:** Slightly increased detection radius during night cycle.
* **Cannot be stunned while burrowed.**

