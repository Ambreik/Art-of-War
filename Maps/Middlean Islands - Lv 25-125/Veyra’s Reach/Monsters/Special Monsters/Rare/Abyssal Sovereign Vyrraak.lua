			Abyssal Sovereign Vyrraak

**Classification:** Rare Elite Predator (Rank III)
**Habitat:** Secluded Cliff Cave, western cliffs behind waterfalls (Veyra’s Reach)
**Type:** Aquatic / Amphibious Apex Predator
**Behavior:** Highly aggressive, territorial, ambush hunter

---

		1. Overview

	The "Abyssal Sovereign Vyrraak" is the evolved terror of the Secluded Cliff Cave. Larger, stronger, and far more cunning than its Rank II counterpart, it 
dominates the underwater cavern. Its bioluminescent markings glow purple, signaling extreme danger.

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

		2. Appearance

* **Size:** 10–12 meters, thicker armored body
* **Coloration:** Darker scales with purple luminescent streaks along the spine
* **Eyes:** Bright violet, more intense glow
* **Markings:** Broken chains fused into its scales, emitting faint sparks when enraged
* **Special Features:** Tail fin is elongated and serrated, used for sweeping attacks

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

		3. Behavior & AI

| State               | Description                                                                                                                               |
| ------------------- | ----------------------------------------------------------------------------------------------------------------------------------------- |
| **Idle State**      | Circles cave pools, remains mostly submerged. Patrols larger area than Rank II.                                                           |
| **Detection State** | Detects player movement or noise within 18m; aggressively tracks intruders.                                                               |
| **Ambush Phase**    | Leaps from water or ledges with 1.5-second delay. Attack damage increased by 50% over Rank II.                                            |
| **Combat State**    | Alternates between melee slashes, acid spit, and tail sweep. Occasionally performs “Roar of Terror” causing knockback and disorientation. |
| **Enrage Phase**    | HP < 25%, activates **Abyssal Sovereign Shell**, increasing resistance +40% and adding AoE poison aura.                                   |
| **Death Phase**     | Emits a deafening roar; cave debris falls, slightly damaging nearby players.                                                              |

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

		4. Combat Abilities

| Ability                     | Description                                                                                               |
| --------------------------- | --------------------------------------------------------------------------------------------------------- |
| **Crushing Leap**           | Jump from ledges or water surface; deals very heavy physical damage plus shockwave (larger than Rank II). |
| **Acid Torrent**            | Spits corrosive line; damage increased by 35%, reduces armor by 25% for 8 seconds.                        |
| **Tail Sweep**              | Wide arc tail attack, knocks back multiple players, applies “Suppression” (movement slowed 50% for 10s).  |
| **Roar of Terror**          | AoE fear effect for 4 seconds, interrupts casting, cooldown 60s.                                          |
| **Abyssal Sovereign Shell** | Passive when enraged: +40% all resistances, AoE poison aura dealing minor DoT every 2s to nearby players. |
| **Lurking Hunger**          | Regenerates 5% HP every 5 seconds while submerged.                                                        |

