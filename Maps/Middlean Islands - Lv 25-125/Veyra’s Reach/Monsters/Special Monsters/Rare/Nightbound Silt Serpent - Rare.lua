## **♻ Respawn / Encounter Rules**

* Spawn timer: 15 seconds (Rank III)
* Solo monster; may spawn in pairs near high-prey zones.
* Aggro is persistent within 12m; retreats if target leaves water or reaches shallow banks.

---

## **⚠ Special Rules**

* Fully aquatic: cannot be engaged effectively outside water.
* Environmental trigger: rises to surface during heavy rainfall or storm events.

