			Nightbound Silt Serpent

**Rank:** Rare (III)
**Level:** 63
**Type:** Beast / Aquatic Predator
**Element:** Water
**Habitat:** Murky riverbeds, marshes, and coastal mudflats

---

## **📘 Description**

The Nightbound Silt Serpent is a sleek, dark-scaled aquatic predator that glides silently beneath the water’s surface. Its eyes are pale and reflective, adapted to detect movement in near-complete darkness. It prefers ambush tactics, striking with precision before retreating back into the silt.

---

## **🧬 Behavior**

* Ambush predator, remaining submerged until prey comes close.
* Avoids prolonged combat on land; retreats if forced out of water.
* Patrols slow-moving rivers and marsh channels, occasionally circling back to previous ambush points.

---

## **⚔️ Combat Mechanics**

* Melee attacks: rapid bite sequences with piercing fangs.
* Tail lash: medium-range sweeping attack, knocks back players and interrupts casting.
* Water Surge: AoE knockdown in a small radius around the serpent.
* Burst strike: single-target high-damage attack after detecting multiple targets.

---

## **☠ Status Effects & CC**

* Affects with: **Poison**, **Slowdown**, **Knockback**

---

## **🌀 Mechanics Interaction**

* Gains +15% movement speed and damage while submerged.
* Aggro radius increases if water is disturbed by other monsters or environmental events.
* Players using fire-based attacks trigger a minor counterattack, releasing a splash of silt that blinds players for 3 seconds.

---

## **📜 Quotes**

• “The mud conceals… but never forgets.”
