			Shardback Mauler

**Type:** Rare Elite • Crystal Beast
**Location:** **Cracked Crystal Cliffs** (Deep Cavern)
**Role:** High mobility crystal predator • Optional pet/mount unlock chance

---

# **1. Overview**

The *Shardback Mauler* is a quadrupedal beast covered in jagged crystal plating. It moves surprisingly fast for its heavy, spiked body. Crystals embedded in its spine glow with shifting hues as it channels energy from the cavern’s mineral veins.

It roams a narrow ledge system high along the **Cracked Crystal Cliffs**, occasionally leaping between platforms, making the encounter inherently hazardous.

---

# **2. Visual Description**

* Body resembles a muscular cave-lion
* Back, shoulders, and tail covered with sharp quartz-like plates
* Glowing crystal eyes reflect nearby torches
* Emits a soft resonating hum when agitated
* Crystal tail ends in a hammer-like cluster

The creature is visually striking and fits the mineral theme of Grimstone Quarry.

---

# **3. Combat Behavior & Mechanics**

The fight occurs on narrow platforms, making positioning critical. The Mauler uses mobility and cliff-based pressure rather than raw damage.

### **Main Abilities**

#### **• Crystal Pounce**

Leaps to a distant platform or directly onto the player.
– Deals moderate damage
– Knocks the player backwards (dangerous near ledges!)
– Can chain with Crystal Surge

#### **• Resonant Slam**

Smashes its crystalline tail into the ground.
– Crystals erupt in a cone
– Players hit are slowed by 40% for 6 sec

#### **• Shimmer Hide (Passive)**

Every 15–20 sec, the Mauler becomes semi-transparent:
– 25% damage reduction
– Movement speed increased
– Attacks apply “Crystal Cuts” (minor DoT)

#### **• Crystal Surge (Enrage)**

At 40% HP, the creature’s plates glow brightly.
– Gains +25% haste
– Pounce becomes more frequent
– Arena crystals hum audibly, warning players

This phase makes the ledges especially deadly.

---

# **4. Spawn Rules**

* Spawns **deep in the Cracked Crystal Cliffs platform maze**
* Only **one Mauler** exists at a time
* **Respawn timer:** 55–70 minutes
* **Conditional spawn:**
  If no one has gathered **Crystal Veins** in the area for 20 minutes, the Shardback Mauler emerges from hibernation.

This ties its existence to resource activity.

---

# **5. Loot Table (light, flexible)**

Only thematic drops — nothing overwhelming.

### **Guaranteed:**

* Small amount of Mining XP
* **Crystal Dust Fragment** (used later for rune-crafting, cosmetics, or upgrades)

### **Rare Chance (5–8%):**

* **Mauler’s Resonant Core**
  *Can be combined with 3 other rare beast cores on the island to unlock a cosmetic pet.*

### **Tiny Chance (0.5%):**

* **Cracked Crystal Key**
  Opens a **shimmering chest** that appears for 60 seconds after the Mauler’s death.
  Encourages farming, excitement, and competition.

---

# **6. Can it become a Pet/Mount?**

Yes — through drops, not by taming directly.

### **Pet:**

Collect **3 Resonant Cores** from different rare beasts on Veyra’s Reach → unlock “Mini Mauler.”

