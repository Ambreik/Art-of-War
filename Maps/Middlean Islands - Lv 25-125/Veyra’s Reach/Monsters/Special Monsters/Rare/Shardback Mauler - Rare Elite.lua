# **7. Location Details**

### **Area:** Cracked Crystal Cliffs

A large vertical cavern side-section with:

* Narrow mineral platforms
* Glowing crystal pillars
* Occasional jump-puzzles
* Crystal Veins for miners

**Platform 3 to Platform 5** are the Mauler’s patrol zone.

Respawn message:

> “The cavern hums with crystalline energy… something stirs in the cliffs.”

