# **7. Location Details**

### **Molten Vein Cavern**

* Deep, narrow cavern with flowing lava streams and exposed ore veins
* Players must navigate precarious ledges to mine safely
* Occasional **semi-boss alcoves** and small tunnels connecting to other Grimstone Quarry areas

**Return to surface:**

* Elevators/ropes near cavern entrance
* Rare “Propulsion Stones” scattered near lava flows for emergency upward movement

