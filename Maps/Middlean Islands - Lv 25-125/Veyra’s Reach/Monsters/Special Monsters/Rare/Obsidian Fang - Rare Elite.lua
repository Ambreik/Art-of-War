			Obsidian Fang

**Type:** Rare Elite • Cave Predator
**Location:** **Molten Vein Cavern** (deep Grimstone Quarry cavern)
**Role:** Heavy-hitting, territorial predator • creates rare resource drops

---

# **1. Overview**

The *Obsidian Fang* is a massive, black-scaled lizard-like predator with glowing orange fissures along its body. It’s highly territorial and often ambushes miners and explorers near the molten ore veins.

Its aggression is enhanced at night, making it a dangerous encounter for unprepared players.

---

# **2. Visual Description**

* Large quadruped, about twice the size of a player character
* Jet-black scales with glowing magma-like fissures
* Eyes glow orange, reflecting nearby light sources
* Emits low rumbling growls that echo through the cavern
* Tail ends in a club-like mass of hardened obsidian

---

# **3. Combat Behavior & Mechanics**

### **Abilities**

#### **• Lava Swipe**

* Sweeps tail in a cone
* Deals moderate AoE fire damage and knocks players back slightly
* Can knock players off ledges if near cliffs

#### **• Molten Roar**

* AoE debuff: 8-meter radius
* Reduces player movement speed by 30% for 5 seconds
* Causes minor burning DoT

#### **• Ambush Strike**

* Leaps from above on players mining nearby ores
* Heavy single-target damage
* Applies “Staggered” debuff (reduces attack speed for 3 sec)

#### **• Hardened Obsidian (Passive)**

* Physical damage reduction +20%
* Resistant to stuns and knockbacks

---

# **4. Spawn Rules**

* Spawns **only in Molten Vein Cavern**, near the deepest ore veins
* Only one exists at a time
* **Respawn timer:** 60–80 minutes
* Conditional spawn:

  * Player must have mined **at least 5 Veyrite Ores or Dreadstone nodes** in the cavern for the Obsidian Fang to appear.

This ensures it doesn’t spawn randomly and ties it to active mining gameplay.

---

# **5. Loot Table**

Drops are **focused on utility, crafting, or event rewards** rather than taming:

### **Guaranteed:**

* Obsidian Fragment ×3–6 (crafting or upgrade material)

### **Rare (5%)**

* **Obsidian Fang Key** — opens a timed chest near the cavern entrance (appears for 60 seconds)

### **Very Rare (2%)**

* **Molten Vein Dust** — used in advanced rune crafting or special potion recipes

