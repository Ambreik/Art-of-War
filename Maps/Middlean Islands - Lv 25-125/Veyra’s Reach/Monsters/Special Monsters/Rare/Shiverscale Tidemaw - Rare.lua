			Shiverscale Tidemaw

**Rank:** Rare (III)
**Level:** 65
**Type:** Animal / Aquatic
**Element:** Water
**Habitat:** Coastal reefs and tide pools along Veyra’s Edge

---

## **📘 Description**

The Shiverscale Tidemaw is a fearsome aquatic predator, its scales glimmering with icy blue hues. Lurking just beneath shallow waters, it strikes suddenly, dragging prey into the depths. Its elongated jaws are lined with serrated teeth, capable of tearing through armor, while its whip-like tail can knock swimmers off balance.

---

## **🧬 Behavior**

* Prefers ambush from submerged positions near coral outcroppings.
* Patrols small reef areas, moving swiftly between hiding spots.
* Will retreat if overwhelmed, using tight reef corridors to evade.

---

## **⚔️ Combat Mechanics**

* Melee attacks: powerful bites and tail whips.
* Water surge: AoE knockback in a small radius when enraged.
* Leap strike: jumps partially out of water to target players on shallow sandbanks.
* Moderate burst damage with persistent melee pressure on targets.

---

## **☠ Status Effects & CC**

