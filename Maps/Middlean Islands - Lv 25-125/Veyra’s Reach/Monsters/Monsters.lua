	AQUATIC & SHORELINE MONSTERS (Near water, beach, broken ships)


* Sandclaw Crab : Animal type monster. Aquatic monster. Level 41. Rank I. Starter class. Solo group. Medium HP Bar. Tanky & Skillable monster type.
Low movement speed. Fast attack speed. Melee attacks. Water element. Affects with Stun, Knockback, Bleed (DoT). Low damage dealer. Pierce attacks 
(armor ignore). Physical damage. Reflects a small amount of damage. Dormant monster. Passive HP regen while standing in water. Spawn every 5 seconds.
* Coral Snapper : Animal type monster. Aquatic monster. Level 40. Rank I. Starter class. Group of 3. Small HP Bar. Melee type. Low movement speed. 
Low attack speed. Melee attacks. Water element. Affects with Bleed (DoT). White physical damage only. Spawn every 5 seconds.

---

* Tidewatch Eel : Animal type monster. Aquatic monster. Level 43. Rank I. Starter class. Solo group. Small HP Bar. Coward monster type. Medium movement speed 
(slithering). Fast attack speed. Melee attacks. Water element. Affects with Poison (Tick DoT). Low physical damage. Flees at 25% HP. Spawn every 5 seconds.
* Saltfin Forager : Animal type monster. Aquatic monster. Level 44. Rank I. Starter class. Group of 3 or 5. Small HP Bar. Melee monster type. Medium movement speed.
Medium attack speed. Water element. Affects with Bleed. Direct physical damage. Spawn every 5 seconds.


	LAND BEASTS & ANIMALS (Coastal grass, dunes)


* Shoreline Boar : Animal type monster. Earth element. Level 42. Rank I. Starter class. Group of 3. Small HP Bar. Melee type. Medium movement speed. Medium attack 
speed. Affects with Bleed. White physical damage. Spawn every 5 seconds.
* Savage Tide Boar : Animal type monster. Earth element. Level 46. Rank II. Starter class. Group of 3–5. Medium HP Bar. Berserk monster type (50% HP = more damage).
Medium movement speed. Fast attack speed. Melee attacks. Affects with Bleed, Knockback. Burst physical damage when Berserk. Spawn every 9 seconds.
* Seastone Tortoise : Animal type monster. Earth element. Level 45. Rank II. Starter class. Solo group. Medium HP Bar. Tanky monster type. Low movement speed. 
Low attack speed. Melee attacks. High defense. Affects with Slowdown applying heavy basic attacks. Low physical damage. Passive high armor. Spawn every 9 seconds.
* Giant Seastone Tortoise : Animal type monster. Earth element. Level 49. Rank III. Regular class. Solo group. Giant HP Bar. Tanky & Skillable type.
Low movement speed. Medium attack speed. Melee attacks. Affects with Stun, Knockback. Tick damage through shell slam tremor. High physical damage.
Long chase radius (elite behavior). Spawn every 15 seconds.



	LIZARD SCOUTS (First Lizard tribe presence)

*All Lizards = Wind Element.*


* Scout Lizard : Humanoid beast type. Wind element. Level 44. Rank I. Starter class. Group of 3 or 5. Small HP Bar. Melee type. Medium movement speed. Medium attack
speed. Affects with Bleed. Direct physical damage. Spawn every 5 seconds.
* Archer Skytalon Lizard : Humanoid beast type. Wind element. Level 45. Rank I. Starter class. Group of 3. Ranged attacks. Small HP Bar. Ranger monster type.
High movement speed. Medium attack speed. Affects with Poison on arrow hit (Tick DoT). Direct physical damage. Spawn every 5 seconds.
* Soldier Lizard : Humanoid beast type. Wind element. Level 46. Rank II. Starter class. Part of a group. Medium HP Bar. Melee monster type. Medium movement speed. 
Fast attack speed. Affects with Bleed. Burst physical damage. High aggression radius. Spawn every 9 seconds.
* Shaman Lizard Initiate : Humanoid beast type. Wind element. Level 47. Rank II. Starter class. Part of a group of 3–5 (usually behind Soldiers). Medium HP Bar. 
Magical attack type. Medium movement speed. Low attack speed. Affects with Poison, Daze, Slowdown. Casts physical-based magical hits (non-elemental). Low physical 
armor. Spawn every 9 seconds.
* Cruel Berserk Lizard : Humanoid beast type. Wind element. Level 50. Rank III. Regular class.Leader of a group (2–4 Soldiers).Medium HP Bar (but very high damage).
Berserk monster type. High movement speed. Fast attack speed. Affects with Bleed, Knockback. Crushing blow mechanic. Rage Mode at 30% HP → doubles attack speed.
Spawn every 15 seconds.
* Lizard Patrol Leader : Humanoid beast type. Wind element. Level 48. Rank III. Regular class. Solo group but moves with 2–3 rotating patrol Soldiers.Medium HP Bar.
Patrol monster type. Medium movement speed. Medium attack speed. Affects with Bleed, Slowdown. Uses combo-point slashes (up to 3-stack). Long chase radius.
Spawn every 15 seconds.

	COASTAL SERPENTS (rocks, shallow water mixed)



* Reefscale Serpent : Animal type monster. Water element. Level 43. Rank I. Solo group. Small HP Bar. Melee type. Medium movement speed. Medium attack speed.
Affects with Poison (Tick). Low physical damage. Spawn every 5 seconds.
* Venomtide Serpent : Animal type monster. Water element. Level 47. Rank II. Solo group. Medium HP Bar. Skillable type. Medium movement speed. Fast attack speed.
Affects with Poison, Daze. Tick poison damage is higher than Rank I variant. Moderate physical damage. Spawn every 9 seconds.
* Coastal Serpent Matriarch : Animal type monster. Water element. Level 50. Rank III.Leader of group (2–3 Reefscale Serpents).Medium HP Bar. Skillable monster type.
High movement speed. Fast attack speed. Affects with Poison, Knockback, Slowdown. Crushing bite (armor penetration). High burst physical damage.
Spawn every 15 seconds.

