## **4. Resources**

### **Fishing**

Veyra’s Reach is surrounded by nutrient-rich waters that host a wide variety of fish families. Both coastal and deep-sea fishing opportunities exist, with some species being exclusive to specific areas or time conditions.
Fishing pools on this island hold **a higher quantity of fish** than other regions.

* **Duskscale Barracuda** – Found near rocky southern shores; aggressive and fast to bite.
* **Redfin Pike** – Common in calm inland waters and near river mouths.
* **Crystalback Tuna** – Rare, deep-sea fish found offshore in the western waters.
* **Nightveil Mackerel** – Only appears at night; slightly bioluminescent scales make it easy to spot.
* **Ashgill Carp** – A freshwater species inhabiting small inner ponds; used in advanced cooking recipes.
* **Stormbelly Ray** – Found during storms or heavy rainfall; highly valuable for alchemical ingredients.
* **Ironjaw Snapper** – Rare predator fish caught near submerged wrecks; yields strong crafting materials.

