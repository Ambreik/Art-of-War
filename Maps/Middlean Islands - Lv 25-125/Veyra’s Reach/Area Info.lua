		Creature / AI Mechanics

1. **High Spawn Rates**

   * Monsters spawn extremely frequently across the island:

     * **Rank I:** every 5 seconds
     * **Rank II:** every 9 seconds
     * **Rank III:** every 15 seconds
   * The dense population creates constant engagement opportunities but reduces individual monster value, making strategic movement and crowd management crucial.

2. **Low Experience & Gold Yield**

   * Standard monsters provide minimal EXP (0.001 per monster) and low gold rewards:

     * Rank I/normal monsters: 1 copper each
     * Rare or higher rank monsters (elites, semi-bosses): 1 silver each
   * Encourages players to focus on objectives beyond grinding, such as rare drops, resources, or key acquisition for the megadungeon.

3. **Hunter Profession Bonuses**

   * Players who chose Hunting as their First Profession receive unique drops from monsters on Veyra’s Reach.
   * These drops are unavailable to other players, reinforcing the island’s profession-specific mechanics and incentivizing player specialization.

4. **Nighttime Aggression & Buffs**

   * At night, all monsters become aggressive toward players.
   * Night modifiers:

     * +15% to all primary stats (health, damage, speed, etc.)
     * +10% increased drop chance for rare items, consumables, or special crafting materials like *Powder of Stinky Pollen*
   * Players must account for increased danger while gathering or navigating the island after dark.

5. **Mount Restrictions**

   * Flying mounts are not allowed anywhere on the island, forcing players to traverse the terrain by foot or with ground/aquatic mounts.
   * This enhances the tactical challenge of exploration and combat in dense environments.

6. **Environmental Hazard Interaction**

   * Certain island features interact with player actions and monster AI:

     * **Pier Crane:** Players can use the crane to dive deep into the sea. Failure to use an aquatic mount results in rapid ejection from the water, causing fall damage proportional to carried weight; gliders can mitigate this.
     * **Grimstone Quarry Catapults:** Players can launch themselves using catapults. Launch trajectory is random, posing a risk of fall damage, collision, or death.
     * **Village Fire Pillars:** Using a consumable *fire torch* at night allows players to ignite pillars safely, without attracting nearby monsters.

7. **Rank-Based Behavior Patterns**

   * **Rank I:** Minor threat, patrols common areas, easily distracted.
   * **Rank II:** Aggressive, patrols in groups, responds to player noise or activity.
   * **Rank III / Elite & Semi-Bosses:** Highly aggressive, patrol or guard specific areas, immune to distraction mechanics, and drop rare crafting/megadungeon items.
   * Rare monsters and boss-level creatures spawn at specified timers:

     * 7 bosses, 5 semi-bosses, 12 elites, 17 rare monsters
     * Spawn timers: 4–8 hours for major bosses; Mystic Stones: 1–3 hours per spawn, with possible double drop.

