## **3. Vegetation**

Veyra’s Reach is covered in dense and varied vegetation, thriving under the island’s humid, sun-drenched climate. Much of the terrain alternates between lush jungles, rocky slopes, and fertile valleys that sustain a vibrant ecosystem. The vegetation density changes across the island’s four major regions, creating clear visual and environmental contrasts between each area.

The coasts and lowlands of the island are dominated by **tropical shrubs, thick grass patches, and low palm formations**, creating a rich green perimeter visible even from distant waters. As players move inland, the foliage becomes **denser and more entangled**, filled with vines, large-leaf trees, and ground mosses that cover most exposed rocks.

In the higher regions, particularly toward the central and northern zones, the vegetation thins, replaced by **sturdy trees adapted to wind and thin soil**. In contrast, the southern wetlands sustain **a mixture of aquatic and semi-aquatic vegetation**, including reed clusters, tangled mangroves, and floating leaf beds that obscure shallow waters.

Despite its natural beauty, much of the island’s vegetation hides potential danger — **predators use the thick growth to ambush travelers**, and several mineral veins and herb spots are concealed beneath heavy root systems. The constant humidity also gives the entire island a faint mist in early mornings and after rainfall, giving Veyra’s Reach its signature dim, mystical atmosphere.

