## **2. Geography and Area Division**

**Veyra’s Reach** is a medium-sized satellite island located southwest of *Netar Island*. Though smaller in scale, it holds significant strategic and resource value, serving as a vital supply and training ground for adventurers advancing toward the *Tomb of Great Crusaders*. The island’s terrain is rugged and layered, with cliffs, quarries, dense plains, and a wild northern frontier. Four primary regions define its geography and gameplay flow.

---

### **1. Seabreak Landing**

*Entry Point & Coastal Encampment*

* This area serves as the player’s point of arrival on the island.
* A small dock and abandoned encampment mark the shoreline, where early-tier monsters and wildlife roam.
* The environment mixes open beaches with low rocky cliffs and sparse vegetation.
* Several low-level mining and herb nodes are scattered throughout, ideal for gathering upon arrival.
* Serves as a transition zone connecting the coast with the *Havenstead Plains*.
* Some fishermen’s supplies, small fishing pools, and rare sea plants can be found near the waterline.

---

### **2. Havenstead Plains**

*Central Village & Profession Hub*

* The heart of Veyra’s Reach, home to the main **village** and most **NPCs**.
* Profession trainers for *Herbalism, Mining, Cooking, Blacksmithing, Legendary Blacksmithing, Hunting,* and *Fishing* are located here.
* Also houses several *vendors, guards, a commander, quest giver,* and the *rare Rune Alchemist*.
* The plains surrounding the village are open, fertile, and dotted with trees, making it a gathering hotspot for herbs and animal resources.
* Player safety is relatively high within the village limits, but aggressive wildlife patrol the nearby fields.
* This area acts as the main social and operational hub for crafting, trading, and quest progression.

---

### **3. Grimstone Quarry**

*Southern Mining Grounds & Cavern Network*

* A hazardous mining region rich in veins and ores, two of which are unique to Veyra’s Reach.
* The terrain is jagged and uneven, with exposed rock, dust, and scattered excavation camps.
* Mines and ores here have increased durability (“HP”) compared to other islands, requiring more time to extract but yielding greater quantities and higher-grade materials.
* Occasional mining accidents have opened deep caverns below, inhabited by rare elite monsters and small clusters of semi-bosses.
* During nighttime, the area becomes highly dangerous, as monsters gain +15 % enhanced stats and aggression range.
* Serves as one of the most lucrative yet perilous gathering zones on the island.

---

### **4. Veyra’s Edge**

*Northern Wilds & Coastal Depths*

* The wild frontier of the island, defined by steep cliffs, dense vegetation, and coastal drop-offs leading to the deep sea.
* This area contains **rare fish families**, unique **aquatic monsters**, and **hidden resources** that can only be harvested during certain hours.
* Nighttime here is extremely perilous; all monsters become aggressive, with enhanced drops and improved stats.
* Beneath the coastal waters lies the **sunken warship**—a secret key location where rare items, keys, and special loot tied to the *Tomb of Great Crusaders* can be found.
* This zone features several rare and elite monsters, serving as endgame-level combat territory for small parties of the island level.
* Exploration and survival are the primary challenges, with few safe spots or escape routes.

