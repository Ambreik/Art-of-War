## **Section 1 – Overview**

**Island Name:** Veyra’s Reach
**Associated Region:** Satellite island of *Netar Island* (located southwest of it)
**Recommended Level Range:** 40–70
**Travel Requirement:** Accessible only after completing the storyline questline from *Commander Edran Holt* on *Ashen Cliffs Island*
**Island Type:** Resource-Heavy / Combat-Intensive / Profession-Oriented

### **General Description**

Veyra’s Reach is a mid-sized island situated to the southwest of Netar Island, serving as its strategic and resource-rich satellite. Despite its moderate size, the island is densely packed with high-value resources, rare creatures, and powerful bosses. Its geography is divided into **four main areas**, each defined by unique mechanics and environmental conditions, offering distinct opportunities for combat, gathering, and exploration.

The island’s environment is known for its rugged terrain, dense foliage, and a mixture of elevated cliffs and open mining zones. The presence of powerful creatures and volatile weather patterns make Veyra’s Reach a dangerous yet rewarding location for experienced adventurers.

### **Notable Features**

* High concentration of rare **ores**, **herbs**, and **fish species**.
* Unique **profession mechanics** — herbs can be harvested twice, fishing pools hold extra fish, and mining nodes have significantly higher durability.
* High monster **spawn rate** with minimal experience and gold reward, making it primarily a **grinding and resource** zone.
* At night, all monsters become **aggressive**, gaining +15% overall stats and a +10% increase to item drop chance.
* Nighttime monsters can drop a rare material — **Powder of Stinky Pollen**, used as currency for the island’s vendors or for specific trades on *Netar Island*.
* No **Stealth & Camouflage System** is available here — players must rely on combat and terrain awareness.
* The island prohibits **flying mounts**, forcing exploration by foot or ground-based travel.

### **Settlement**

A small but fortified **village** serves as the island’s central hub, housing various profession trainers, vendors, and guards. This settlement provides essential services such as profession advancement, supply purchasing, and limited questing. It acts as the only safe zone on the island, offering players respite between dangerous expeditions into the wild areas.

