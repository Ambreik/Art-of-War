* **Fellmoss** – Grows on shaded rocks and ruins; serves as a stabilizer in strong potions.
* **Ashen Dewleaf** – Can be harvested twice per node; essential in the preparation of megadungeon elixirs.
* **Ironbark Spores** – Found near old stumps in the northern woods; mixed in defense potions.

