# I. 🌑 **Surface Area Layout (Top Level)**

The surface of Grimstone Quarry is structured into **three major excavation zones**, each connected to one or more tunnels. The terrain is jagged, dusty, with debris from mining accidents.

## **1. Surface Features**

* Broken track rails from old mining carts
* Cranes and wooden scaffolding
* Ore piles, shattered rock clusters
* Warning signs & lanterns
* Two abandoned excavation camps

## **2. Environmental Hazards**

* Occasional **rockfalls** (small AoE hazards)
* **Dust gusts** reducing visibility
* Random **ground tremors** hinting at underground monsters
* At night: +15% monster stats and +10m aggro range

