# IV. 🌋 **Deep Cavern Network (End-Zone)**

This is the finale area of Grimstone Quarry and the home of the **semi-bosses**.

The Deep Cavern Network is composed of **three distinct sections:**

## **A. Abyssal Drop**

A massive pit with echoing sounds and darkness.

Features:

* Falling pebbles
* Constant deep rumbling
* One gigantic central ore cluster
* Elite predators hunting each other

## **B. The Hollow Halls**

A wide-open stone hall formed naturally over centuries.

Features:

* Massive stone pillars
* Hanging crystalline formations
* Echo-laden boss arena vibes

## **C. The Furnace Depths**

Closest to volcanic activity.

Features:

* Lava runoff
* Superheated air
* Dreadstone concentration highest here

Monster Style:

* Demonic, molten, or corrupted semi-bosses

