			MINECART LIFT PLATFORM

### *Mechanical Shortcut Extraction System*

**Category:** Environmental Mechanic / Traversal Device
**Location:** Grimstone Quarry — *Tunnel 2: Collapsed Lift Passage*

---

# **1. OVERVIEW**

The **Minecart Lift Platform** is an old, vertical shaft ascent system used by miners before the collapses. It is partially operational and serves as a fast shortcut back to the surface.

Players can **repair** it, **ride** it, wait for an **auto-reset**, or observe its occasional quirks.

Provides a **fast-travel utility** for players who prefer speed-clears, ore loops, or repeated semi-boss farming.

---

# **2. VISUAL & WORLD DESIGN**

### **Structure**

* A narrow vertical shaft (15–20 meters)
* Rusted gear rails on both sides
* A dangling minecart platform suspended by thick chains
* Broken rope lines hanging from the ceiling
* Faint pulleys still turning slowly when wind circulates
* Sparks occasionally flicker at the control lever
* A broken caution sign reading:
  *“Lift unsafe — use at own risk.”*

### **Ambient Effects**

* Constant echo of chains
* Loose stones dropping from above
* Metallic creaking at random intervals
* Subtle draft rising upward

---

# **3. PLAYER INTERACTION MECHANICS**

## **A. Repairing the Lift**

Players may manually repair the lift using:

| Material      | Quantity |
| ------------- | -------- |
| Iron Scraps   | 3        |
| Wooden Planks | 2        |
| Grease / Oil  | 1        |

These items can be:

* Dropped by monsters
* Looted from barrels in tunnels
* Purchased from the village vendors

### **Repair Time**

**6-second channel**
Interrupted by damage.

### **Success**

UI Message:
**“The lift rumbles back to life!”**

It becomes usable for all players for **10 minutes**, unless auto-resets sooner.

---

## **B. Automatic Reset**

If players do not repair it, the minecart will **auto-repair and reset every 10 minutes** by the old Grimstone mechanical system.

When this happens:

* Gears spin violently
* Dust falls in a short burst
* The minecart platform rises from the darkness
* Small sparks shoot out (cosmetic only)

UI system message:
**“You hear machinery activating somewhere below…”**

---

## **C. Riding the Lift**

### **Capacity:**

**Up to 3 players** per ascension cycle.

### **Ride Duration:**

**5 seconds** from Tunnel 2 → Northern Scaffolding Exit.

### **Animation Sequence**

1. Player steps onto the minecart platform.
2. Pulls a lever to begin ascent.
3. Cart shakes briefly, gears grind.
4. Rapid upward movement with rattling metal sound.
5. Teleports or smoothly transitions to surface.

### **Destination:**

**Grimstone Quarry — Northern Scaffolding Platform**,
near the exit rope bridge that leads back toward the main quarry area.

---

# **4. FAILURE STATES & SPECIAL BEHAVIORS**

### **A. Malfunction Sparks (Cosmetic)**

5% chance when starting the lift:

* Blue-white sparks burst near the lever
* Low-voltage arc effect
* Cart shakes more violently
* Sound: *bzzt-krrrnnk!*

No damage, purely atmospheric.

### **B. “False Start” Failure (Rare scripted event)**

1% chance:

* Cart rises 1 meter
* Stops abruptly
* Drops back down a few inches
* Then rises normally

This is ONLY a visual + sound effect.

### **C. Overload Attempt**

If **more than 3 players** try to board:

* The platform refuses to activate
* Brakk’s voice echoes from somewhere:
  *“Cart ain’t rated for that many! One of ya step off!”*

---

# **5. GAMEPLAY VALUE**

### **For speedfarmers / ore loop runners**

* Allows extremely fast reset routes
* Encourages farming efficiency
* Reduces downtime between deep cave semi-boss runs

### **For explorers**

* Unlocks new shortcuts
* Allows players to quickly reach the safer upper quarry for repairs and vendor access

### **For multiplayer parties**

* Forces small coordination
* “Who goes first? Who repairs?” interactions
* Makes the lift a tiny social hotspot inside the Quarry

