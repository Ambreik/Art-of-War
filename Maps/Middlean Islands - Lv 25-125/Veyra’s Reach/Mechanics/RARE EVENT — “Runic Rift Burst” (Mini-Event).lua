			RARE EVENT — “Runic Rift Burst” (Mini-Event)

Once every 4–6 hours real time, a major anomaly forms.

**Spawns:**

* 3–5 “Rift Imps”
* 1 randomly selected rare monster
* Arcane residue piles on the ground (harvestable)

**System Broadcast:**
**“A Runic Rift has opened near the village walls! Seek Alyndor Redveil at once!”**

Alyndor immediately teleports to the event and fights.

