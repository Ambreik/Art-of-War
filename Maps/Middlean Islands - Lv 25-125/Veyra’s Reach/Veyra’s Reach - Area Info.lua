*this island is a "sattelite" island for "Netar Island".
*is located in the south west side of "Netar Island".
*to reach this island players must complete the storyline from "Commander Edran Holt" located on the island "Ashen Cliffs".
*this island is medium size island, but contain alot of resources, rares and bosses.
*island is split into 4 major areas, each of them with its own unique mechanics or porposes.
*as a drop, from one of the bosses found here (spawn every 4-8 hours) is a key which is needed for aceessing a side chamber that leads to a secret boss, in the 
"Tomb of Great Crusaders" megadungeon.
*as herbs from this island, are used in Herbalism for creating strong potions, used only for the megadungeon.
*a rare monster, drop another key for another secret corridor in the "Tomb of Great Crusaders".
*in the depts of the sea, as a secret key location, is a sunken warship. sometimes a chest is spawned inside this ship and a few items can be looted such : 
	*key for the "Tomb of Great Crusaders" -> used to access the megadungeon on easy mode.
	*"Cape of Dark Leader" -> a sash that provide to the user, a temporary ability to be untargetable inside the "Tomb of Great Crusaders" megadungeon.
	*"Whistle of a Coward" -> this item attract all the monsters from the minimap radar of the player which use it inside the "Tomb of Great Crusaders" megadungeon.
	*"Note for Advancing Troops" -> this item provide knowledge of how to conquer the first boss in the "Tomb of Great Crusaders" megadungeon. This item is for the Alchemist to create a potion that is only used for the first boss in this dungeon, to weaken him for 5 minutes. The alchemist will send the player to gather some ingredients to create the potion.
	
