### **2. Monster AI / Creature Mechanics (Linked to Puzzle & Resources)**

**Stealth / Ambush Mechanics:**

* Certain monsters like *Thunderfang Serpents* hide in dense jungle underbrush (use Stealth & Camouflage system).
* High-rank flying monsters (*Stormclaw Raptors*) patrol the upper canopy and respond to noise (Global Noise System).

**Linked Aggro / Herd Behavior:**

* Disturbing small mobs (birds, monkeys) can alert nearby predators (Linked Aggro).
* Encourages strategic movement when farming puzzle pieces.

**Day/Night Behavior:**

* Night: monsters gain detection range buffs and stealth becomes more dangerous.
* Day: monsters roam normally, some rare spawns only appear during daylight.

**Drops for Totem Mini-Game:**

* Each monster drops a **puzzle piece** with a low drop rate (~2–5%).
* Other normal drops: gold, resource nodes (Storm Shards), minor herbs/fish.

**Environmental Interaction:**

* Monsters can push players off zip lines or interrupt fishing attempts if nearby.
* Certain monsters are tied to POIs (e.g., *Skyreach Elemental* near northern high ridge).

