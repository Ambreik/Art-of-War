			Skyreach Spire Totem Mini-Game

### **1. Skyreach Spire Totem – Puzzle Mini-Game**

**POI Name:** *Totem of the Thunder Guardians*
**Location:** Summit of *Skyreach Spire*, the tallest mountain on the island.

**Purpose:**

* A high-risk, high-reward interactive mechanic for players who collect puzzle pieces dropped by island monsters.
* Can trigger lightning strikes on players or summon rare/semi-boss monsters.

**Puzzle Pieces & Sources:**

| Piece Name               | Monster Source       | Rank                       |
| ------------------------ | -------------------- | -------------------------- |
| Piece of the Storm Orb   | Stormclaw Raptors    | Rank III – Flying          |
| Shard of Skyrunestone    | Skyreach Golems      | Rank IV – Ground           |
| Lightning Sigil Fragment | Thunderfang Serpents | Rank III – Ambush Predator |

**Gameplay Mechanics:**

1. Player collects all three pieces and approaches Totem.
2. Mini-game interface appears: drag-and-drop puzzle pieces into correct slots.
3. Correct placement triggers **event outcomes**:

**Event Outcomes:**

* **Other players on island:**

  * Lightning strikes all except puzzle-completing player.
  * Death → 3-minute resurrection cooldown.
  * No mount usage for 15 minutes after resurrection.
  * Player sees system message: *“You have summoned the Thunder Strike upon Asku’lan!”*

* **No other players present:**

  * Lightning strikes 3 fixed locations on island, summoning rare/semi-boss monsters:

    1. *Stormclaw Drake* – Emerald Jungle Clearing
    2. *Skyreach Elemental* – Northern High Ridge
    3. *Thunderfang Hydra* – Hidden River Gorge
  * System message directs player to spawned monsters.

**Visual / Gameplay Feel:**

* Totem glows with **electric blue runes**.
* Puzzle pieces float above the player’s head once equipped.
* Lightning visual effects are dramatic, synchronized with sound.

