			Harmonic Grooves (Non-Obvious Interaction)

* Type : Environmental / Sound-Based Reaction
* Triggered by : Wind angle + player presence

	Description : 

The flute-like grooves respond to positioning, not interaction.

When players:
* Stand still in certain grooves
* Or move slowly along them

The sound changes pitch.

	Hidden Effects : 

* Certain pitch combinations:
	* Calm nearby neutral creatures
	* Agitate aggressive ones
* Some creatures avoid loud harmonic zones
* Others patrol toward them

	This creates:
* Accidental stealth paths
* Accidental danger zones

No explanation. No tooltip.

