			Rare Moment: Perfect Silence

* Type : Environmental / Rare Occurrence
* Frequency : Very low

	Description : 

	For a brief moment:

* The wind dies
* Sounds stop
* The cliffs go eerily quiet

	Effect : 
* Creatures stop moving
* Casts feel faster
* Camera subtly steadies

Nothing happens mechanically… except tension.
Players who notice it will never forget it.

