			Wind Pressure Zones (Primary Core Mechanic)

* Type : Environmental / Movement Hazard
* Visibility : Subtle (felt before fully understood)

	Description : Not all wind here is equal.

	Certain cliff-edge segments generate directional pressure zones :
* Some push sideways
* Some lift vertically
* Some destabilize footing

	Gameplay Effects : 
* Slight movement drift while standing
* Casting time increases slightly
* Sprinting increases loss of control
* Jumping incorrectly may:
	* Launch the player
	* Slam them into rock
	* Drop them into the sea

	Why This Matters : 
* No UI warning
* No map marker
* Players learn by experience

	This is the kind of mechanic people talk about:

> “I didn’t fall. The wind threw me.”

