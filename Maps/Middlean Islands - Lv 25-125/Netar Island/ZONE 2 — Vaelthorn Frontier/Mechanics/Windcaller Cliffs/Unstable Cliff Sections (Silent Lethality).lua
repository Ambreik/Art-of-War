			Unstable Cliff Sections (Silent Lethality)

* Type : Environmental / Delayed Hazard

	Description : Some cliff surfaces are structurally compromised.

They do not collapse immediately.

	Mechanics : 

* Standing still too long
* Channeling long casts
* Heavy armor users linger longer

→ causes:

* Rock cracking sounds
* Micro camera shake
* Then partial collapse

	Result : 
* Player may fall
* Or be forced into a dangerous slide
* Or drop gear durability significantly (non-lethal punishment)

	This discourages:
* AFK behavior
* Camping edges
* Safe sniping abuse

