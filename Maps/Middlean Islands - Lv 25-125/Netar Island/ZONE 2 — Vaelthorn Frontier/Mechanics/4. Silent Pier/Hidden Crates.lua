			Replayability / Randomization

```
Type: Gameplay / Environmental Randomization
Mechanics:
- Crate contents randomized per player per tide cycle
- Rare fish spawn points randomized
- Treasure maps vary weekly, pointing to different secret locations
- Encourages timed visits, exploration, and social interaction
```

