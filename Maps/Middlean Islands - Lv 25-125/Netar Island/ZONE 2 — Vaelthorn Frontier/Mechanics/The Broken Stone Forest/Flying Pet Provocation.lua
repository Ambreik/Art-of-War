			Flying Pet Provocation

	Type : World Reaction / Aggression Shift

	If a player summons a flying pet at night in the Broken Stone Forest:

	Effect
* Nearby monsters enter an aggressive state
* Some packs may:
	* howl
	* reposition
	* converge toward the summoner’s area

	Reasoning (world logic, not explained)
* The flying silhouette disrupts the fungi glow
* Sound + shadow interaction triggers territorial behavior

	This:
* punishes careless summoning
* reinforces night danger
* feels organic

