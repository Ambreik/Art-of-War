			Midnight Pillar Ignition

Type : Rare Event / Conditional Summon

	At midnight, a specific formation of tall stone pillars:
* briefly ignites with unnatural fire at their tips

	Conditions

* Requires:
	* either Shaman + Archer OR Warlock + Archer
	* both present in the center
	* a night-aligned pet summoned
* No UI indication
* Fire lasts only a short time

	Outcome
* A semi-boss entity manifests within the stone circle
* If conditions are not met, nothing happens

	This feels:
* mythic
* accidental
* community-discovered


