			Luminescent Ground Reaction

Type : Visual / Conditional World State

	At night:
* glowing fungi between pillars reacts to movement and magic

	Observed behaviors
* Stepping repeatedly on glowing patches causes them to brighten briefly
* Spellcasting may trigger flickering patterns
* Sudden extinguishing of glow can signal nearby creature movement

	This mechanic:
* does not reward directly
* increases tension
* helps attentive players read the area

