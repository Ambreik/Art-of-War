			Sound-Carrying Stone

Type : Audio / Monster Behavior

The stone pillars amplify and redirect sound in unnatural ways.

	What happens
* Combat noises, shouts, spell casts travel farther than expected
* Monsters respond from unexpected directions
* Some packs move together when sound propagates through aligned pillars

	Result
* Players may accidentally pull multiple groups
* Careful players can intentionally lure enemies

This makes sound itself a risk and tool.

