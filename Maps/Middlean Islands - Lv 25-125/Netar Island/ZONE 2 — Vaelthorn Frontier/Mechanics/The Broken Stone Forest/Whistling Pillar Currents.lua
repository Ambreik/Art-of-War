			Whistling Pillar Currents

Type : Environmental / Movement / Sound

	Wind moving through specific arrangements of stone spires creates:
* directional gusts
* unpredictable side pushes
* short bursts of increased wind force

	Effect

* Player movement subtly drifts when running between certain pillars
* Light characters may be pushed sideways
* Poor positioning near broken or tilted pillars may cause falls or knockdowns

This is not constant — it happens in pulses depending on wind direction.

