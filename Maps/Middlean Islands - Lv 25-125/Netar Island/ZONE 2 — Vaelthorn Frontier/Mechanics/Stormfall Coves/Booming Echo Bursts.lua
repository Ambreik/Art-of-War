			Booming Echo Bursts

	Type : Environmental / Audio / Monster Interaction

Inside the wider chambers, certain wave impacts produce a much louder echo than normal.

* These are not constant.
* When they happen:
  * the sound travels far beyond the cove
  * nearby creatures react unusually

	Effects
* Idle monsters may:
  * regroup into larger packs
  * move toward the sound source
  * become briefly aggressive
* Rare monsters may use these moments to appear (not guaranteed)


