			Predator Attraction Behavior

	Type : Monster Spawn Behavior

	Certain predators associated with Stormfall Coves:
* do not spawn immediately
* arrive after activity

	Triggers may include:
* prolonged combat
* repeated wave surges
* sound accumulation

	They often:
* arrive in small numbers first
* escalate into larger groups if players stay

This gives the location a pressure curve :
> stay too long → the cove pushes back

