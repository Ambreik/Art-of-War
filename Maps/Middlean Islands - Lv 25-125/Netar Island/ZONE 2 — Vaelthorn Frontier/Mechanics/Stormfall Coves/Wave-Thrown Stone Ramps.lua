			Wave-Thrown Stone Ramps

	Type : Environmental / Movement

Certain smooth, slanted rock formations near the inner walls act as natural launch ramps.

* When a strong wave crashes inward, water pressure surges up these slopes.
* Players standing or running onto them at the right moment can be:
  * pushed forward
  * thrown upward
  * or launched across parts of the cove

	Results
* Can reach otherwise inaccessible ledges
* Can be used intentionally or accidentally
* Poor timing may throw players into walls or water


