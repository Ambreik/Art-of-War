			Light-Pierced Stone

	Type : Visual / Timing

	At certain times of day:
* sunlight enters through cliff cracks
* stone surfaces briefly reflect light in unnatural ways

	Result
* markings, cracks, or surface irregularities become noticeable
* nothing activates by itself
* it simply makes players aware of something they might later interact with (here or elsewhere)

	This mechanic does not guarantee a reward — it creates memory.

