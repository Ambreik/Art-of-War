			Low-Tide Exposure Zones

	Type : Visibility / Opportunity

	At very low tide:
* parts of the cove floor that are normally submerged become visible
* objects embedded in stone or sand can be seen clearly

	What can appear
* remains of ship debris
* sealed containers
* mineral seams
* bones or markers hinting at danger

These are not always rewards — sometimes just information, sometimes bait, sometimes nothing.
When tide rises, these areas disappear again.

