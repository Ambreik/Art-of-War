			Tide-Dependent Access

* Type : Environmental / Hazard
* Mechanics :
	* Passage only accessible during lowest tide cycles (~2–4 hours in-game, randomized per day)
	* Higher tide prevents entrance and can damage players attempting to swim against currents
	* Pocket air bubbles provide temporary breathing windows
* Interaction :
	* Players must plan access according to tide cycles
	* Using water gear or gliding mounts can mitigate high-tide hazards

