---

	Player Interaction / Multiplayer Dynamics

* Single Player :
	* Complete puzzle → 30–60 sec timer → treasure spawn → open treasure
* Multiple Players :
	* Puzzle can be completed by one player, but any nearby player can reach treasure first
	* System Message alerts solver if another player opens treasure
	* Treasure remains available for short time window, encourages speed and coordination
* Party Mechanics :
	* Timer and treasure rewards scale slightly with party size
	* Party members can help with puzzle or guard against environmental hazards

---

	Environmental / Hazard Integration

* Currents and Air Pockets :
	* May push players off platforms while puzzle timer is active
* Minor Predators :
	* Spawn near puzzle / treasure location during countdown
* Lighting Effects :
	* Beam reflections change dynamically during countdown to increase tension

---

	Replayability / Randomization

* Puzzle sequence randomized for each attempt
* Countdown timer randomized between 30–60 sec
* Treasure spawn alcove randomized among 2–3 locations
* Encourages repeated engagement rather than farming the same spot

