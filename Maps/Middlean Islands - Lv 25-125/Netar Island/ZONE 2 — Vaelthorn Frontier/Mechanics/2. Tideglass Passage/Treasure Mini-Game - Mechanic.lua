# Tideglass Passage — Treasure Mini-Game

Location : Hidden alcove in Tideglass Passage (accessible during low tide)
Trigger : Interaction with pillar/statue (as hinted by Lorian “Tidefinger” Vesh)

---

		Game Concept

* Type : Environmental Puzzle / Social Competition
* Goal : Solve the light/rotation sequence puzzle on the pillar/statue to spawn the treasure
* Mechanics :
	* Puzzle requires rotating crystal arms or aligning light beams across reflective surfaces
	* Completion triggers 30–60 second countdown before treasure spawns
	* Treasure spawns in a separate pre-determined pool or alcove**, not directly at the pillar
* Social Element :
	* Any player can open the treasure once it spawns
	* The player who solved the puzzle receives system message if another player opens it:

    > “<PlayerName> solved the Tideglass puzzle — <OtherPlayerName> stole the treasure!”
	* Encourages friendly competition and social interaction

---

	Puzzle Mechanics

	A. Structure

* Pillar/Statue Components :
	* 3–5 rotating crystal arms with symbols on each
	* Mirrors / reflective plates on the wall or water surface
	* Beams of light must connect all mirrors/crystals in correct sequence

	B. Interaction

* Player Actions :
	* Rotate crystals to align beams
	* Rotate mirrors or touch light nodes to reflect beams correctly
	* Optional: cast elemental spell (fire, water, or light) to temporarily activate nodes
* Environmental Hazards :
	* Gusts from air pockets / small currents may temporarily misalign crystals
	* Minor aquatic predators may appear if countdown is active

	C. Timing

* Countdown :
	* Once puzzle is solved, treasure spawn timer: 30–60 seconds, randomized per attempt
	* Treasure remains available for 5 minutes before disappearing if unopened

---

	Treasure Spawn Location

* Spawned elsewhere in the passage (randomized among 2–3 secret alcoves to avoid camping at pillar)
* Requires swimming / navigation to reach
* If treasure is not opened in 5 minutes, it resets and puzzle can be retried

