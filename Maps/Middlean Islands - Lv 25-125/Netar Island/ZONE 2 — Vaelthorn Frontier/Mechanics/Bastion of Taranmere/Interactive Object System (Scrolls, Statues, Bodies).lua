			Interactive Object System (Scrolls, Statues, Bodies)

---

		I. SYSTEM PURPOSE

	The Interactive Object System exists to:

* Encourage exploration and investigation
* Provide lore, quests, and minor challenges
* Integrate with spirit reactive spawns
* Avoid interfering with key fragment collection or dungeon entry

	Core Principle :

> Players who investigate the world gain knowledge, rewards, and risk — even outside combat.

---

		II. OBJECT TYPES

	There are three primary object types, each serving different purposes:

| Object Type         | Example                                     | Purpose                                                             |
| ------------------- | ------------------------------------------- | ------------------------------------------------------------------- |
| Scrolls / Pages     | Ancient notes, battle journals              | Provide lore, optional quest progression, and minor buffs           |
| Statues / Monuments | Fallen crusader statues, guardian monuments | Lore reinforcement, can trigger spirit ambushes                     |
| Bodies / Remains    | Fallen crusaders, explorers, or civilians   | Quest hooks, clues, or fragment hints; risk of spirit manifestation |

---

		III. INTERACTION MECHANICS

	A. Detection & Interaction

* Objects are visually highlighted when players approach
* Can be inspected by clicking / using action key
* Some objects require:
	* Minimum proximity
	* No combat in immediate area
* Inspection triggers:
	* Lore text / journal entry
	* Optional quest progression
	* Possible enemy spawn (reactive mechanics)

	B. Inspection Outcomes

1. Lore Discovery
	* Text description
	* Optional XP or minor item reward
2. Quest Trigger
	* Adds objectives to quest log
	* May require multiple objects in sequence
3. Reactive Enemy Spawn
	* Restless Spirits may appear
	* Chance increases with Escalation Level and proximity to seal
	* Encourages fast engagement and movement

---

		IV. OBJECT DISTRIBUTION

	A. Scrolls / Pages

* Found in libraries, broken halls, or ruins
* Mostly safe but occasionally guarded by spirits
* Serve as clues for hidden content or faction lore

	B. Statues / Monuments

* Near courtyards or hallways
* Often linked to defensive or religious history
* May trigger:
	* Spirit ambush
	* Mini-puzzles for lore unlock

	C. Bodies / Remains

* Scattered in combat zones or collapsed areas
* Can be:
	* Purely lore
	* Quest-related (retrieve object or information)
	* Spawn trigger for spirits or pressure events

---

		V. INSPECTION TIMERS

* Interaction takes 2–4 seconds
* During this time:
	* Player cannot move or attack
	* Suspense and risk are introduced
* Interruptions:
	* If attacked, inspection fails, object remains interactable
	* Optional repeatable inspection after cooldown (30–60s)

---

		VI. QUEST HOOK INTEGRATION

* Objects can be flagged for quest objectives
* Quest example:
	* “Inspect 5 fallen crusader bodies to recover battle data”
	* “Examine 3 statues to unlock hidden corridor”
* Quest-related objects may:
	* Spawn additional enemies
	* Reward faction reputation
	* Grant minor consumables or hints for dungeon entry

---

		VII. REACTIVE SPAWN & ESCALATION LINK

* Spirits may spawn near inspected objects :
	* Higher chance for rare or valuable lore items
	* Increased spawn probability near seal or high-pressure zones
* Encourages strategic risk assessment :
	* Investigate carefully or move quickly

---

		VIII. VISUAL & AUDIO DESIGN

* Scrolls / pages: faint glow or particle effect
* Statues / monuments: subtle aura, cracks, or debris
* Bodies / remains: occasional flicker or whisper sound
* Interactions provide:
	* Text UI feedback
	* Optional lore audio
	* Enemy cue (if reactive spawn triggered)

---

		IX. ANTI-EXPLOIT RULES

* Objects are not lootable for trade items
* Cannot be farmed repeatedly for XP without cooldown
* Each object has:
	* Interaction cooldown
	* Spawn probability reset

---

		X. PLAYER EXPERIENCE GOALS

	Players should:
* Feel curiosity rewarded
* Gain story and lore context
* Experience risk vs reward during exploration
* Recognize objects as part of the Bastion’s ecosystem

---

		XI. SYSTEM INTEGRATION

	Interactive objects integrate with:

* Restless Spirit system (reactive spawn)
* Dynamic Spawn Pressure & Escalation (inspection may spike local pressure)
* Quest system (object flags for objectives)
* Seal Proximity Danger Zone (higher risk near seal)

	Does not affect:
* Key fragment drops (except optionally for lore hints)
* Rare Monster mechanics

