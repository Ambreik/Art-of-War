			Key Fragment Drop & Assembly System

---

		I. SYSTEM PURPOSE

	The Key Fragment system exists to:

* Gate access to the dungeon behind the seal
* Encourage exploration and combat in "Bastion of Taranmere"
* Discourage trading or farming exploits
* Allow party-based progression while respecting individual effort

	Core Principle :

> Players must actively engage with the world to forge their dungeon access key.

---

		II. FRAGMENT TYPES

	There are five Key Fragments per key:

| Fragment ID | Description         | Drop Rarity |
| ----------- | ------------------- | ----------- |
| Fragment 1  | Crusader Sigil      | Common      |
| Fragment 2  | Sealed Rune         | Uncommon    |
| Fragment 3  | Spirit Shard        | Uncommon    |
| Fragment 4  | War-Torn Medallion  | Rare        |
| Fragment 5  | Essence of the Seal | Rare        |

	Notes :
* Each fragment drops randomly from designated enemy types (see below).
* Players may accumulate multiple copies of the same fragment type before obtaining others.
* Fragments cannot be sold, auctioned, or exchanged normally; they are inventory-bound.

---

		III. DROP SOURCES

	1. Escaped Crusader Undead
* All standard types: low base chance (1–3%)
* Elite variants: medium chance (5–7%)

	2. Restless Spirits
* Rare chance (1–2%) for Fragments 3 & 5

	3. Rare Monster
* Guarantees 1 fragment drop of random type not yet in player inventory
* Can drop multiple copies if already has some fragments

	4. Ruin Wildlife
* No fragment drops

	Drop Notes :
* Drops are inventory-bound
* Only one key is required per dungeon entry, but players can collect multiples for convenience
* Randomization ensures non-linear fragment accumulation

---

		IV. FRAGMENT COLLECTION LOGIC

	Randomized Fragment Accumulation

* Players may end up with:
	* 3 of Fragment 1, 1 of Fragment 3, 0 of Fragment 4
* There is no sequence requirement; only total completion matters

	Inventory Rules

* Fragments occupy special inventory slots
* Cannot stack with other item types
* Non-tradable via normal trade
* Can only be transferred via trade inventory (prevents profit grinding)

---

		V. KEY ASSEMBLY

	Once a player collects all five fragments, the system automatically:

1. Removes the fragments from inventory
2. Creates 1 full dungeon key (name suggestion: "Sealbreaker Key")
3. The key is party-ready :

   * Only **one key per party is required** to trigger seal entry
   * Multiple keys may be held for convenience or backup

	Key Behavior

* Tradable only within a trade inventory
* Cannot be sold on market
* Cannot be duplicated
* Keys do not expire, but seal access triggers cooldowns

---

		VI. PARTY INTEGRATION

* Dungeon entry requires:
	* Full party
	* At least one key in the party
* Key mechanics:
	* Party leader initiates interaction at the seal
	* Other members **must click and accept** to enter
	* Single key is consumed or marked as used for that entry
* Supports multiple keys for multiple dungeon runs

---

		VII. ANTI-EXPLOIT MECHANICS

* Fragments are inventory-bound
* Market sale or normal trade is disabled
* Key trades allowed only via controlled trade inventory
* Random fragment drops prevent predictable grinding patterns
* Encourages:
	* Party collaboration
	* Exploration of multiple zones
	* Strategic farming rather than exploit farming

---

		VIII. PLAYER EXPERIENCE GOALS

	Players should:

* Engage with enemies across the zone naturally
* Enjoy the thrill of random drops
* Plan dungeon runs with a party or friends
* Avoid “farm-and-sell” exploits
* Feel reward when assembling the key after active participation

---

		IX. SYSTEM INTEGRATION

	The Key Fragment system interacts with:

* Enemy spawns (crusaders & spirits)
* Pressure & escalation system (higher pressure may yield elite variants with higher drop chance)
* Seal Proximity Danger Zone (prepares players for challenge)
* Dungeon access mechanics (party requirements and cooldowns)

	It does not interact with:
* Ruin Wildlife
* Quest object rewards (optional)
* Non-dungeon access gameplay

