			Seal Activation & Totem Mechanics

---

		I. SYSTEM PURPOSE

	The Seal Activation & Totem System exists to:

* Provide a clear, interactive ritual for dungeon entry
* Ensure party coordination is required
* Reward preparation and timing
* Tie the dungeon access to both combat and exploration progression

	Core Principle :

> Entering the dungeon is an event, not a simple click.

---

		II. ENTRY REQUIREMENTS

	To activate the seal:
1. Party must be full (all slots filled)
2. At least one player holds a Sealbreaker Key
3. All players must be present in the seal proximity zone
4. Seal is off cooldown (1-hour lock after last entry)

---

		III. SEAL INTERACTION MECHANICS

	Step 1: Leader Initiation

* Party leader clicks on the seal
* Options displayed: “Enter Dungeon”
* System validates:
	* Full party
	* Key presence
	* Cooldown

	If any requirement fails :
* Action is denied
* Clear on-screen message displays reason

---

	Step 2: Party Confirmation

* After leader initiates, 10-second countdown begins
* All party members must click the seal and accept the challenge
* Visual countdown appears for all members
* If any member fails to accept:
	* Countdown resets
	* Party must re-initiate

	Purpose : Ensures coordinated group entry

---

	Step 3: Totem Spawn

* Once all members accept, Seal Totem spawns in front of the entrance
* Suggested name: “Totem of Binding”
* Totem acts as a mini-damage objective :
	* Must be destroyed to complete activation
	* Small hit points to encourage fast teamwork
* During totem fight:
	* Enemies in seal proximity are alerted
	* Spirit ambush chance spikes
	* Patrol loops compress

---

	Step 4: Final Countdown

* After the totem is destroyed:
	* 20-second final countdown
	* Players prepare for teleport
	* Visual and audio cues indicate imminent dungeon entry
	* Nearby enemies may try to disrupt if party lingers

---

	Step 5: Teleport & Lock

* Party is teleported inside the dungeon
* Seal triggers 1-hour cooldown
* Seal is locked for all players outside party
* Totem is removed until next activation

---

		IV. VISUAL & AUDIO CUES

* Seal glows brighter as players approach
* Audible hum intensifies during countdown
* Totem pulses with energy while active
* Clear feedback ensures players know their next steps

---

		V. PARTY & KEY MECHANICS INTEGRATION

* Only one key needed per party
* Key is consumed or marked as “used” after entry
* Multiple keys in inventory: no effect on mechanics
* Keys cannot be exploited; can only trade via trade inventory

---

		VI. ANTI-EXPLOIT MECHANICS

* Seal cannot be spammed or farmed due to cooldown
* Totem cannot be destroyed prematurely by outside players
* Party must remain in seal zone during countdown
* Countdown resets if:
	* Any member leaves
	* Party is not full

---

		VII. PLAYER EXPERIENCE GOALS

	Players should feel:
* The seal is important and guarded
* Entering the dungeon is a team effort
* There is a sense of tension as patrols, spirits, and proximity danger escalate
* Coordinated action and timing matter

---

		VIII. SYSTEM INTEGRATION

	Seal Activation & Totem System interacts with:
* Seal Proximity Danger Zone (pressure, patrol compression, spirits)
* Key Fragment System (key validation, consumption)
* Party Mechanics (full party requirement, countdown acceptance)
* Dynamic Spawn Pressure & Escalation (alerting nearby enemies)

	It does not interact with :
* Ruin Wildlife
* Quest objects outside dungeon gating

