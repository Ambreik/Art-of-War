			Respawn & Reset Rules System

---

		I. SYSTEM PURPOSE

	The Respawn & Reset system exists to:
* Prevent infinite combat loops
* Avoid empty or overfilled zones
* Protect NPC-safe areas
* Ensure pressure escalation has natural release valves

	Core Rule :

> The Bastion reacts — but it always stabilizes if left alone.

---

		II. ENEMY RESPAWN CATEGORIES

	Each enemy type follows distinct respawn logic.

---

	A. Escaped Crusader Undead

	Respawn Timing :
* Base: 6–10 minutes
* Modified by:
	* Pressure level
	* Player density
	* Seal proximity

	Respawn Location Rules :
* Rotate between predefined nodes
* Never respawn at the same node twice consecutively
* Avoid respawning directly behind players

	Conditional Modifiers :
* Faster respawn at Escalation Levels 3–4
* Slower respawn if zone is abandoned

---

	B. Restless Spirits

	Respawn Timing :
* Base: 10–15 minutes
* Not affected by player count
* Slightly affected by pressure level

	Despawn Rules :
	
	Spirits despawn if:
* Ignored for too long
* Players leave the zone
* They complete their disruption cycle

	Anti-Clutter Rule :
* Spirit count per zone is capped

---

	C. Ruin Wildlife

	Respawn Timing :
* Long (15–25 minutes)
* Not pressure-dependent

	Population Logic :
* Wildlife populations feel persistent
* Not replaced quickly if wiped out

---

	D. Rare Monster

	Respawn Timing :
* Very long
* Randomized window
* Shared across entire Bastion

	Despawn Rules :
* If lured toward safe zones
* If not engaged for extended periods

	Cooldown applies even on despawn.

---

		III. RESET CONDITIONS (HARD & SOFT)

	Soft Reset Conditions

	A soft reset partially restores balance when:
* Players leave a zone
* Combat stops for a set duration
* Pressure decays naturally

	Effects:
* Patrols resume default routes
* Escalation levels drop gradually
* Respawn timers normalize

---

	Hard Reset Conditions

	A hard reset occurs when:
* Players enter the dungeon
* All players leave the Bastion
* Server/world reset
* Seal interaction completes

	Effects:
* Pressure reset to baseline
* Patrol routes reshuffled
* Spirit anchors reset

---

		IV. LEASHING & ANTI-EXPLOIT CONTROLS

	Leash Boundaries

	Enemies will reset if:
* Pulled into NPC courtyard
* Dragged toward entry arches
* Taken outside their pressure zone

	Reset Behavior
* Enemies return to:
	* Original spawn pool
	* Patrol route
* Health and state fully reset

---

		V. SAFE ZONE ENFORCEMENT

	Absolute Safe Areas
* NPC Courtyard
* Investigation staging points
* Dungeon seal activation platform

	Rules:
* Enemies cannot enter
* Aggro is dropped instantly
* Patrols reroute away

	This ensures:
* Clear player expectations
* Zero grief potential

---

		VI. PRESSURE RESET INTEGRATION

	Pressure Relief Events

	Pressure decreases faster if:
* Rare Monster passes through
* Players wipe or retreat
* No crusader kills occur for a time

	Pressure Lock Conditions

	Pressure cannot reset below a minimum if:
* Players are actively farming
* Seal is being contested

---

		VII. FAILSAFE SYSTEMS

	Overpopulation Safeguards
* Hard caps per zone
* Respawn delays enforced if caps reached

	Underpopulation Safeguards
* Emergency respawns if zone becomes empty
* Priority given to crusader patrols

---

		VIII. PLAYER EXPERIENCE GOALS

	Players should feel that:
* Retreating is smart, not failure
* Clearing an area buys time, not permanence
* Safe zones are truly safe
* The Bastion feels reactive, not unfair

---

		IX. SYSTEM INTEGRATION

	Respawn & Reset Rules integrate with:
* Patrol system
* Spawn pressure & escalation
* Seal access flow
* Rare monster control logic

