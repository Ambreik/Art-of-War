			Dungeon Access – Failure & Edge-Case Handling (LOCKED)

	Seal Countdown Phase (10 seconds)

---

	If any of the following occur during the countdown:

* A party member disconnects
* A party member refuses to interact with the seal
* A party member leaves the party

	Result :
* That player loses their Sealbreaker Key
* The dungeon entry fails
* Remaining players are not pulled inside
* No retry without reacquiring a key

	This enforces:
* Commitment
* Party readiness
* Anti-griefing / anti-leech behavior

---

	Totem (Pillar) Destruction Phase

* If a player dies during this phase:
	* Normal death rules apply
	* EXP loss follows the global system (Metin2-style chance-based loss)
	* No special penalty tied to the dungeon

---

	Key Consumption Rules

* Key Name : "Sealbreaker Key"
* Key Consumption Moment :
	Consumed once the seal is broken and the totem spawns

	Not consumed:
* On fragment collection
* On seal interaction
* On dungeon completion

	This prevents:
* Entry abuse
* Partial attempts
* Safe scouting

