			Seal Totem – Identity & Lore

	Totem Name : Totem of the Shackled Oath

---

	Lore Justification

	The Totem of the Shackled Oath anchors the curse binding the dungeon’s interior.
	While the seal bars physical entry, the totem maintains the spiritual lock that prevents passage beyond the threshold.

	Destroying the totem:
* Breaks the curse’s outer anchor
* Allows the dungeon interior to manifest safely
* Forces the seal into a dormant state

---

	Totem Mechanics
* Has a visible HP bar
* Does not scale with player level
* Spawns minor enemies during destruction
* No interrupt mechanics
* No escalation pressure beyond the countdown

	Purpose:

> A test of coordination, not combat difficulty.

