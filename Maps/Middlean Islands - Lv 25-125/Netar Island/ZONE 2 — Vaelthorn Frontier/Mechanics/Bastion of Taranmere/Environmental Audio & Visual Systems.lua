			Environmental Audio & Visual Systems

	Audio Cues
* Increased spawn pressure → distant echoes, armored movement
* Spirit manifestation → whispering, low-frequency distortion
* Rare monster nearby → unique, recognizable sound signature

---

	Visual Signals
* Seal area → fading runes intensify near proximity
* Torches → flicker under pressure or spirit activity
* Spirit zones → cold mist, subtle visual distortion

	These systems provide situational awareness, not warnings.

