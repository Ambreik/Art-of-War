			Seal Proximity Danger Zone System

---

		I. SYSTEM PURPOSE

	The Seal Proximity Danger Zone exists to:
* Signal escalating danger without UI warnings
* Prevent casual camping near the seal
* Enforce preparation before dungeon entry
* Create narrative weight around the seal’s instability

	Core Principle :

> The seal is not a door — it is a wound.

---

		II. ZONE STRUCTURE & LAYERS

	The area around the seal is divided into three invisible concentric layers, each with increasing danger modifiers.

---

	Layer 1 – Outer Influence Ring

	*(Early warning zone)*

	Distance :
* Furthest from seal entrance

	Effects :
* Slight increase in crusader patrol frequency
* Occasional spirit manifestations
* Patrol routes begin to shorten

	Player Perception :
* “This area feels watched.”

---

	Layer 2 – Inner Guard Ring

	*(Active danger zone)*

	Distance :
* Mid-range around seal

	Effects :
* High crusader density
* Increased elite crusader chance
* Spirits manifest reactively and faster
* Patrol overlap increases

	Player Perception :

* “We should not stay here long.”

---

	Layer 3 – Seal Threshold

	*(Critical danger zone)*

	Distance :
* Immediate seal vicinity

	Effects :
* Maximum crusader density cap
* Frequent spirit materialization
* Minimal idle time for patrols
* Near-instant enemy response to combat

	Player Perception :
* “We either commit or leave.”

---

		III. CRUSADER DENSITY MODIFIERS

	As players move closer to the seal:
* Patrol group count increases
* Group composition shifts:
	* Higher chance of Bulwarks
	* Elite variants more likely
* Respawn timers shorten (within caps)

	This ensures the seal is never undefended.

---

		IV. SPIRIT ACTIVITY MODIFIERS

	Spirit behavior intensifies near the seal:
* Reduced idle invisibility duration
* Larger reactive spawn radius
* Increased chance to spawn during:
	* Lore inspection
	* Combat aftermath
	* Pressure spikes

	Spirits reinforce:
* Memory
* Guilt
* Instability

---

		V. PATROL LOOP COMPRESSION

	Loop Compression Logic
* Patrol routes shorten closer to the seal
* Idle pauses decrease
* Patrols re-enter player paths faster

	Result:
* Area feels alive and oppressive
* Clearing enemies gives only brief relief

---

		VI. ENEMY RESPONSE TIME

	Detection & Response
* Sound and combat detection radius increases
* Investigation delay decreases
* Multiple patrols may converge

	Near the seal:
* Fights escalate rapidly
* Prolonged combat is dangerous

---

		VII. PRESSURE SYSTEM OVERRIDE

	The seal area applies baseline pressure, regardless of player behavior.

	Effects:
* Pressure never decays below a minimum
* Escalation levels activate faster
* Pressure decay is slower

	This prevents:
* Farming near the seal
* Safe staging near dungeon entry

---

		VIII. INTERACTION RESTRICTIONS

	No Long-Term Staging
* Enemies respawn quickly
* Spirit ambushes frequent
* Players discouraged from waiting

	Seal Activation Exception

	Once seal activation begins:
* Pressure escalation pauses
* No new patrols spawn in immediate area
* Existing enemies remain

	This ensures:
* Fair dungeon entry process
* Clear transition from open world to instance

---

		IX. FAILSAFE & FAIRNESS MEASURES

* No enemy spawns directly on players
* Patrols respect safe-zone boundaries
* Clear audio/visual cues indicate rising danger

	The system is oppressive, not unfair.

---

		X. PLAYER LEARNING OUTCOME

	Without tooltips or warnings, players learn:
* The seal is dangerous to approach casually
* Preparation matters
* Standing still invites death
* Dungeon entry is a commitment

---

		XI. SYSTEM INTEGRATION

	Seal Proximity Danger Zone integrates with:
* Dynamic spawn pressure
* Patrol compression
* Respawn timing
* Dungeon access flow

