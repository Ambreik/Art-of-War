			Shifting Corridors

* Type : Environmental / Navigation Challenge
* Mechanics :
	* Narrow passages are affected by airflow; walls “feel alive” as gusts alternate
	* Sudden gusts may temporarily reveal or hide alcoves with mining nodes or loot
	* Players must:
		* Time jumps or sprints
		* Move carefully to avoid being blown into spiked rock or shallow reflective pools
	* These corridors naturally encourage exploration and awareness of rhythm

