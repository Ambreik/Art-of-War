			Resonant Chambers

* Type : Environmental / Puzzle-like / Rare Creature Trigger
* Mechanics :
	* Some chambers produce low-frequency hums based on airflow and cavern geometry
	* Player interaction (jumping, walking, casting magic, throwing objects) can resonate with the chamber
	* Correct resonance unlocks:
		* Hidden mining veins (Ironvein Shards or Stormforged Ore)
		* Spawn of rare cave creatures
		* Activation of dungeon-related triggers (ancient markings pulse when in resonance)
* Optional Effects :
	* Minor sound-based illusions (ghostly whispers) to create tension

