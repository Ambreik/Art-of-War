			Pulsating Airflow

* Type : Environmental / Hazard
* Mechanics :
	* Air pressure in narrow corridors rises and falls rhythmically, like the cavern is “breathing.”
	* High-pressure bursts can:
		* Push players back or sideways
		* Knock over lightweight objects or barrels
		* Disrupt casting and channeling
	* Low-pressure periods reduce mobility slightly but allow safer passage
* Interaction :
	* Players can time their movement to pass safely
	* Certain skills (gliding, shield blocking) can mitigate knockback
	* Standing in airflow for prolonged periods can fatigue the player (minor stamina drain)

