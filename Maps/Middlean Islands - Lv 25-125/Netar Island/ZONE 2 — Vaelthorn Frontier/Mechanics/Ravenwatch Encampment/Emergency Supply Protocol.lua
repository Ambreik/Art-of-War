			Emergency Supply Protocol

* Type : Limited-Time Utility Shift

	During heightened threat periods:
* Quartermaster stock slightly changes
* Emergency consumables appear
* Prices may increase temporarily

No gear upgrades — only survival tools.

---

	4.1 Quartermaster Stock Changes

	Normal Stock Reduced : 
* Crafting materials ↓
* Trade goods ↓
* Luxury food removed

	Emergency Stock Added : 
* Defensive consumables
* Survival items
* Anti-undead tools

---

	4.2 Emergency Consumables List

* Fortified Rations (HP regen)
* Bitter Tonic (Fear resistance)
* Torch Bundle (Light radius boost)
* Crude Bandages (Out-of-combat heal)
* Signal Flares (Flavor / future use)

---

	4.3 Pricing Rules

* Prices increase by:
	* +10% during Alert
	* +25% during Lockdown
* Justified by:
	* Scarcity
	* Military requisition
	* Siege conditions

---

	4.4 Flavor Feedback

	Quartermaster Lines : 
* “Supplies are tight.”
* “Take what you need, not what you want.”
* “If this holds, we won’t last long.”

