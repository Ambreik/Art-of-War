			Night Raid Alert System

* Type : Global / Reactive Environmental Mechanic

	Trigger Conditions : 
* Crusader activity spikes inside the dungeon
* Time window: Night only
* Randomized nights, not predictable

	Mechanic : 
* A deep horn blast echoes across the encampment
* Multiple horn calls with short delays
* Torches intensify in brightness
* Soldiers move into formation (visual only)

	Player Impact : 
* Ambient tension increase
* NPC dialogue changes briefly
* Some services temporarily pause
* Reinforces that the dungeon is alive

---

		1.1 Alert Activation Triggers

* Nightfall + Dungeon unrest
* Crusader patrols detected outside the castle
* Player-triggered dungeon events (future)
* Randomized escalation window (to avoid predictability)

---

		1.2 Alert States & Effects

	WARNING STATE

* Horn sounds once (low, distant)
* Soldiers increase patrol density
* No services disabled yet

	ALERT STATE

* Repeating horn blasts
* Torches lit along walls
* Restricted services activate

	LOCKDOWN STATE

* Continuous horn pattern
* Gates sealed
* Non-essential NPCs disappear
* Airspace fully restricted

---

		1.3 Restricted Services During Alerts

| Service           | Warning | Alert         | Lockdown        |
| ----------------- | ------- | ------------- | --------------- |
| Flying Travel     | Enabled | Arrival only  | Disabled        |
| Vendor            | Enabled | Limited stock | Emergency stock |
| Quest Turn-ins    | Enabled | Enabled       | Disabled        |
| Non-military NPCs | Present | Reduced       | Gone            |

