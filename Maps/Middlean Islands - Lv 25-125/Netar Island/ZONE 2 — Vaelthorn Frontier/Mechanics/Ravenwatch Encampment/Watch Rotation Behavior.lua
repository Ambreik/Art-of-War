			Watch Rotation Behavior

* Type : Visual / World-Building Mechanic

	Guards:
* Rotate positions at set intervals
* Use lantern signals
* Exchange short, voiced lines

	At higher player progression:
* Guards may recognize returning players
* Minor acknowledgment lines only

---

	3.1 Rotation Intervals

* Shift change every in-game 2–3 hours
* Visible movement:
	* Guards swap wall positions
	* Scouts depart / return
	* Archers rotate towers

---

	3.2 Role-Specific Behaviors

	Guards : 

* Gate ↔ Wall rotation
* Pause for inspection animations
* Stop players briefly during alerts

	Soldiers : 
* Barracks ↔ Patrol routes
* Drill animations during calm
* Defensive formations during alerts

	Archers : 
* Tower swaps
* Adjust aim during wind changes
* Loose warning shots during alerts

	Scouts : 
* Leave encampment
* Return with urgency during alerts
* Deliver silent reports (non-interactive)

---

	3.3 Ambient Exchange Lines

	Guards : 
* “Your watch now.”
* “Quiet shift so far.”
* “Stay sharp.”

	Soldiers : 
* “You hear anything?”
* “Not yet. That’s worse.”

	Archers : 
* “Range looks clear.”
* “For now.”

	Scouts : 
* “Path’s not safe.”
* “They’re closer than before.”

---

	3.4 Player Recognition System

	Mechanic : 
* Guards track player presence over time
* After multiple visits:
	* Guards face player briefly
	* Minor acknowledgment lines triggered

	Examples : 
* “Back again?”
* “Still breathing, I see.”
* “You picked a bad place to linger.”

*No reputation gain, flavor only.*

