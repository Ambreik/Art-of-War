			Restricted Zones (Soft Control)

* Type : Environmental Access Control

	Certain areas of the encampment:
* Are blocked during high alert
* Cannot be entered at night
* Are guarded by non-interactive soldiers

	Purpose:
* Prevent player misuse
* Reinforce military discipline
* Maintain immersion without hard walls

---

		2.1 Flying Landing Zone Restrictions

	When Alert or Higher :
* Landing pad interaction disabled
* Flight Master NPC despawns
* Mounts vanish from platform
* Visual barriers activate (chains, banners, guards)

	Arrival Rule : 
* Players already airborne may land
* Cannot initiate new flight
* Flight UI locked

---

	2.2 Encampment Interior Restrictions

	Alert State : 
* Barracks closed
* Command tents guarded
* Certain paths blocked

	Lockdown State : 
* Only main corridor accessible
* Defensive formations occupy streets
* Players redirected through patrol-safe routes

