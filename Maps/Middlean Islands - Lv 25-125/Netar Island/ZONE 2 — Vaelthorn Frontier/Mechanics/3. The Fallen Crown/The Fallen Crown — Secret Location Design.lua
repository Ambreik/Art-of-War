			The Fallen Crown — Secret Location Design

Type : Secret / Boss Lair / Quest Trigger
Location Features : Massive hollow boulder shaped like an inverted crown, rugged basin, seat-like formations, faintly glowing mineral veins, spiraling tunnels

---

		Quest Activation Item

Item Name : "Corrupted Crusader Sigil"

* Type : Quest / Ritual Item
* Source : Drops from elite mobs or mini-bosses inside the Tomb of Great Crusaders dungeon
* Description :
	> A fractured sigil infused with lingering energy from the fallen Liberty Crusaders. Glows faintly when near cursed locations.
	
* Purpose : Used at the shrine inside The Fallen Crown to initiate quest and boss encounter
* Restrictions :
	* Only usable by players level 55+
	* Can be held in quest inventory only until used
	* Item consumed upon use

---

		Quest / Shrine Mechanics

	Quest Name : "Awakening the Crown’s Sentinel"

* Objective : Activate the ancient shrine at The Fallen Crown using the "Corrupted Crusader Sigil"

* Player Interaction :
	* Place the sigil on the central pedestal within the crown’s hollow
	* Pedestal emits pulsating glow and harmonic tones
	* Player must remain within the crown for 30–60 seconds as the ritual completes
	* During this time:
		* Mineral veins intensify glow, creating dynamic lighting
		* Small environmental hazards: falling stones, minor air bursts, echoing sounds to create tension
		* Occasionally, adds (corrupted spirits) spawn to challenge the player before boss emerges

* Completion Trigger :
	* Ritual completes → boss spawns at the center of the crown
	* Entrance to spiraling tunnels temporarily sealed with falling rocks or energy barriers

---

		Environmental Setup for Boss Fight

* Arena Features :

	* Hollow crown boulder with seat-like formations providing partial cover
	* Glowing mineral veins along walls act as visual cues for boss abilities (e.g., charge, shockwave, or AoE attacks)
	* Spiral tunnels can serve as escape routes for adds or environmental hazards
	* Ambient sound : low hums, occasional dripping, reverberating pulses

* Optional Environmental Mechanics During Boss Fight :

	* Falling mineral shards: avoidable, deal physical damage
	* Pulsating air bursts: knockback effect or brief stun if caught
	* Mineral vein interactions: players can use environmental buffs by touching specific veins during fight (e.g., temporary resistance to boss’s AoE)

