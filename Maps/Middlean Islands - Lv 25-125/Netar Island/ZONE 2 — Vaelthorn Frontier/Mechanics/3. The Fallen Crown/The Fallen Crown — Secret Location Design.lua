			The Crowned Warden

	Location : Location : Netar Island/Zone 2 - Vaelthorn Frontier/The Fallen Crown
---

Type : Rare / Boss / Elite
Location : The Fallen Crown — central hollow boulder

---

	Appearance & Lore

Appearance :
* Massive, humanoid-crustacean hybrid fused with minerals from the crown
* Carapace shaped like a fragmented crown, glowing faintly along cracks
* Eyes burn with violet flame, mineral veins pulse with each breath
* Size: ~3–4 meters tall, intimidating presence in the arena

Lore :
> Once a guardian of the Liberty Crusaders’ sacred relics, the Warden was corrupted by the curse emanating from the fallen legion. Bound within the hollow crown, it awakens only when an outsider disturbs the shrine with the "Corrupted Crusader Sigil".
> The Warden remembers the intruders as intruders and strikes with fury, using both brute strength and elemental mineral powers.

---

	Environmental Mechanics During Fight

* Arena Interaction :
	* Seat-like formations provide partial cover
	* Glowing mineral veins on walls: signal upcoming attacks; can be touched for temporary buffs
* Hazards :
	* Falling mineral shards (random intervals, small AoE damage)
	* Pulsating air bursts (knockback or brief stun if caught)
* Add Spawns :
	* Minor corrupted spirit adds spawn depending on party size
	* Adds are aggressive, low HP, but can interrupt player positioning

---

	Boss Mechanics
|
| Earth Shatter - Warden slams ground, dealing AoE damage and creating temporary fissures. Players hit by fissures are slowed.
| Crown Slam - Leaps onto arena center, dealing massive melee damage in frontal cone; knockback included.
| Mineral Pulse - Charges mineral veins along walls; emits pulse damaging all players in radius. Veins glow brighter before attack, giving visual cue.
| Summon Adds - Summons 2–5 corrupted spirit adds if players spread too much. Adds vanish if not engaged after 20 sec.
| Adaptive Fury (Enrage) - HP below 50% → gains increased attack speed, additional AoE effects, and can root one player with shockwave jump.
| Environmental Interaction - Can destroy seat-like formations to create debris on players; knocks them back.
| Ritual Disruption - If players touch mineral veins during fight, Warden targets nearest player with high-priority AoE attack.

---

	Phases & Adaptability

* Phase 1 (100–75% HP) :
	* Basic attacks (melee, Earth Shatter), occasional Mineral Pulse
	* Adds spawn occasionally

* Phase 2 (75–50% HP) :
	* Aggro increases, begins Crown Slam
	* Summons adds more frequently, environmental hazards intensified

* Phase 3 (50–0% HP) :
	* Adaptive Fury triggers
	* Faster attack speed, stronger AoE, random jumps to root players
	* Can temporarily seal arena exits with falling debris
	* Boss scales based on party size (more players = more adds, faster attacks)

---

	Combat Strategy / Weaknesses

* Weak Against :
	* Fire or piercing damage for mineral shell
	* Coordinated interrupts to prevent AoE attacks
* Environmental Advantage :
	* Using mineral veins during fight can provide temporary resistance buffs if timed correctly
	* Avoid air bursts and falling shards

