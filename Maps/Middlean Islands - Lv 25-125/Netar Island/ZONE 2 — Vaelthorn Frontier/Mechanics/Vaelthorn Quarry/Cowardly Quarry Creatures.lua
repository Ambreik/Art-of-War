			Cowardly Quarry Creatures

Type : Monster Behavior

	Some creatures living in the quarry:
* do not fight immediately
* retreat deeper when engaged
* attempt to lure players into tighter spaces

	Behavior
* They flee toward the collapsed tunnel or scaffolding
* If cornered, they scream or shriek

	That scream may:
* pull stronger creatures
* wake dormant ones
* trigger another mechanic 

This creates uneasy pursuit gameplay, not direct combat.

