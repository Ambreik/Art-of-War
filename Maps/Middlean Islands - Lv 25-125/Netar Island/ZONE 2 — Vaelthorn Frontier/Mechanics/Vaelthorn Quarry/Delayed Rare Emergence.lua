			Delayed Rare Emergence

Type : Rare Spawn Behavior

	A rare or elite creature:

* does not spawn immediately
* appears after players remain in the quarry for a while
* or after multiple disturbances (noise, mining, cart use)

	Notable
* It is stronger than expected
* It may arrive silently
* Or break through a wall or floor

This discourages farming and encourages awareness.

---

		Core Philosophy

	This rare is not farmable in a clean way.
	It exists to:
* punish prolonged presence
* react to industrial activity
* turn the quarry from “farm spot” into “leave now”

	Players should learn:
> “If we stay too long, something comes.”

---

		TRIGGERS — When Emergence Becomes Possible

	The rare does NOT spawn on entry.
	Instead, it becomes eligible when any combination of the following occurs:
* Continuous presence in quarry (time-based)
* Repeated mining actions
* Multiple cart movements or crashes
* Several collapses triggered
* Excessive monster noise (shouts, coordinator calls)

❗ Players never see a meter — only signs.

---

		WARNING SIGNS (VERY IMPORTANT)

	Before spawning, the quarry gives environmental tells :
* Distant stone grinding sounds
* Dust falling from ceilings
* Guide marks vibrating faintly
* Miners behaving oddly:
  * stopping mid-action
  * facing walls
  * refusing to advance

These signs are the only warning.

---

		RARE EMERGENCE ENTITY

	The Deepburied Foreman
Undead Constructed Miner — Rare Elite
Giant HP Bar. Boss-type monster.

> “The one who never left, because the quarry never let him.”

---

	Spawn Method (One Only, Chosen Randomly)
* Breaks through a quarry wall
* Smashes upward from beneath the floor
* Emerges from a sealed tunnel collapse

✔ No spawn animation warning
✔ Often enters combat immediately

---

	COMBAT IDENTITY
* Slow, unstoppable
* Extremely high strength
* Uses quarry itself as a weapon
* Does not chase far — controls territory

---

		ABILITIES

	*Stone Drag
* Drags a player toward rubble
* Applies Bleed + Slow
* If dragged into unstable ground, may trigger collapse

---

	*Foreman’s Slam
* Heavy overhead attack
* Causes AoE knockback
* Breaks scaffolding, carts, and weak walls

---

	*Call the Shift
* Summons 2–4 Rockie Miners from nearby tunnels
* If Coordinator Miner is alive, summons more

---

	*Buried Endurance
* Takes reduced damage while near stone walls
* Damage resistance drops if lured into open areas

---

		PASSIVE EFFECTS
* Immune to fear
* Resistant to poison
* Takes increased damage from fire and magic
* Becomes slower as HP decreases (cracking stone)

---

	GROUP INTERACTION (IMPORTANT)

	If undead miners are alive:
* They protect the Foreman
* Coordinators will try to reach him
* Haulers may push players toward him

	Killing miners first makes the fight easier — but increases spawn risk earlier.

---

	PLAYER COUNTERPLAY
✔ Fire damage breaks armor faster
✔ Silence stops "Call the Shift"
✔ Open areas reduce his defenses
✔ Destroy carts to prevent terrain abuse
✔ Retreating is valid — he does not despawn immediately

	Smart groups can:
* bait him
* weaken him
* disengage
* return later (if he hasn’t retreated)

---

		UNIQUE DROPS (VERY IMPORTANT)

	Guaranteed

* Foreman’s Chain Key
  * Opens sealed quarry storage rooms
  * One-time use per character

	Rare
* Stonebound Core
  * Crafting material
  * Used for heavy armor / shields

	Very Rare

* Blueprint: Reinforced Mining Cart
  * Usable in other quarries or mines
  * Improves cart mechanics

---

		DESPAWN & RESET LOGIC

* If not killed:
  * retreats back into stone after a long time
* Leaves:
  * cracked walls
  * broken floors
  * altered quarry layout

	This means:
> even failure changes the place

