			Old Quarry Rails

Type : Utility / Movement
Remnants of rail tracks and carts still exist.

	What players can do
* Push abandoned carts
* Ride them downhill
* Use momentum to:
  * break weak barriers
  * escape enemies
  * crash into mobs

	Risk
* Losing control
* Slamming into collapsed sections
* Triggering collapses

This adds a physical, non-magical traversal tool.

