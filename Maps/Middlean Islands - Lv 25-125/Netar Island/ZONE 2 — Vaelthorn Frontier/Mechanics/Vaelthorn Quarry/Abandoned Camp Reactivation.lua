			Abandoned Camp Reactivation

Type : World State Change
Old camps with fire pits and tools exist.

	If players light a campfire
* Some creatures avoid the area
* Others are attracted to the light
* Shadows move unnaturally around the quarry walls

	In rare cases:
* Former quarry workers (now feral, cursed, or altered) emerge from hiding
* They may appear confused, then hostile

This gives life to the idea that people never truly left.

