			The Sudden Darkness

Type : Light / Panic / Audio

	In certain interior sections:
* light sources may suddenly extinguish
* torches go out
* ambient light drops sharply

	This can be triggered by:
* crossing a certain threshold
* loud noise
* interacting with old machinery

	What follows
* dragging sounds
* stone scraping
* distant breathing or movement

No immediate attack is guaranteed.
Sometimes nothing happens.
Sometimes something does.

This mechanic is about psychological pressure, not damage.

