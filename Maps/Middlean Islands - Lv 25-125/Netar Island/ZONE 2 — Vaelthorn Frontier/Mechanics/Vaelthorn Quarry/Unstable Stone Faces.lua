			Unstable Stone Faces

Type : Environmental / Risk

Some quarry walls and half-cut stone blocks are structurally unstable.

	What happens
* Heavy combat
* Repeated movement
* Mining nearby

…may cause:
* small rockfalls
* partial wall collapses
* temporary path blockages

	Effects
* Damage to players or monsters
* New paths opening while others close
* Corpses or objects being buried

This is not predictable — players learn which areas are dangerous by experience.

