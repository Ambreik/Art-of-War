			Rare Amphibious Monster — The Gloomtide Mirelurker

---

	Type : 

* Amphibious Beast
* Rare / Elite
* Cave-dwelling, reflective-pool bound

---

	Appearance : 

* Large, hunched amphibian creature with a slick, semi-translucent hide
* Skin reflects surrounding light faintly, mimicking the shimmer of the pools
* Broad jaw lined with uneven teeth, glowing algae-like growths along spine
* Long, webbed forearms used to drag itself across stone
* Eyes sit high on the skull, glowing dim blue-green in darkness
* Water constantly drips from its body, leaving wet trails behind

---

	Spawn Conditions : 

* Triggered by stepping into specific reflective pools
* Only at correct times of day (independent of Mineral Vision timing)
* Pool must remain undisturbed for a short time before spawn
* Vision appears subtly in the water before the creature emerges

> If conditions fail, the pool returns to normal with no feedback.

---

	Behavior : 

* Begins submerged and hidden
* Ambush-based combat style
* Prefers to fight near water, retreating into pools when pressured
* Aggressive once revealed, but cautious and territorial
* Will disengage and vanish if pulled too far from water

---

	Core Mechanics : 

	Submerge
* Periodically dives into a reflective pool
* Becomes untargetable for a short duration
* Reappears from a different nearby pool

---

	Tidal Grasp

* Melee attack that:
	* Deals moderate damage
	* Slows movement
	* Briefly pulls the player toward the pool

---

	Mirror Ripple
* Causes nearby pools to ripple violently
* Spawns minor water manifestations (non-elemental adds)
* Adds are fragile but distracting

---

	Wet Hide (Passive)
* Takes reduced physical damage while standing in water
* Vulnerable to fire and lightning damage
* Loses mitigation if forced onto dry ground

---

	Ambush Leap
* Leaps from water onto a distant target
* Applies brief root or knockdown
* Can only be used if starting from water

---

	Weakness & Counterplay

* Strongly weakened when:
	* Pulled away from pools
	* Hit with fire-based attacks
* Players who control positioning gain advantage
* Poor performance in sustained ranged combat

---

	Difficulty & Scaling

* Intended for:
	* Solo players at appropriate level
	* Small groups (2–3 players)
* Not a tanky boss — relies on ambush and positioning
* No adds if solo; spawns adds only for groups

---

	Drops (Sample)

* Mirelurker Hide (crafting material)
* Reflective Pearl (rare reagent)
* Gloomtide Essence (used in water-related consumables)
* Small chance:
	* Cosmetic aquatic item
	* Utility item tied to underwater exploration

---

	Environmental Flavor

* Water pools darken briefly before spawn
* Low gurgling sounds echo through nearby tunnels
* After defeat, pools remain calm but lose reflective properties for a time

