			Reflective Pool Interaction

	Type : Environmental / Hidden Reward

* Pools on the floor are shallow but reflective, some with hidden magical properties.

	Mechanics
* Stepping on specific pools at correct times may reveal:
	* Hidden paths under the water
	* Submerged treasure chests
	* Summoning a rare amphibious or water elemental monster

	Optional
* Some pools reflect light anomalies similar to the mineral vision effect

