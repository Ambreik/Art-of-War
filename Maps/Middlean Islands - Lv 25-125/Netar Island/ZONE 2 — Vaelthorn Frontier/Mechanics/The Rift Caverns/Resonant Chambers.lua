			Resonant Chambers

	Type : Sound / Environmental Hazard
* Certain narrow faults resonate with low-frequency hums when players run, jump, or fight near them.

	Effects
* Can attract cave-dwelling monsters
* May disorient players slightly (subtle camera shake or low visual distortion)
* Can be used strategically to lure enemies into traps

	Optional Rare
* A “Resonance Elemental” may appear if players interact with multiple resonant points in sequence

