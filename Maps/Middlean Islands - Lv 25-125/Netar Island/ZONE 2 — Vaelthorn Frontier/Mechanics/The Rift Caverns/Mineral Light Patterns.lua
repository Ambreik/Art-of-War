			Mineral Light Patterns

	Type : Environmental Puzzle / Rare Interaction
* Mineral formations refract light differently based on player positioning and time of day
* Specific alignments could trigger:
	* Unlock hidden chamber doors
	* Reveal rare items or scrolls
	* Spawn rare creatures

	Example
* 3 reflective walls in sequence — casting light across the pool — may open a secret fissure

