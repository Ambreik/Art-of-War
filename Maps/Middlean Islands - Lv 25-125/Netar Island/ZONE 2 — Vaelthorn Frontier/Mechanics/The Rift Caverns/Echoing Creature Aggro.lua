			Echoing Creature Aggro

	Type : Monster Behavior Mechanic
* The cavern amplifies sound, altering monster behavior:
	* Monsters may aggro from long distances if spells, shouts, or combat sounds are made
	* Certain monsters can move faster along resonant paths, ambushing the player
	* Groups may react to echoes differently : some flee, some converge

	Optional Rare
* Certain rare cave beasts only appear when sound level is high

