		Seal Fragment — Flying Mount Path

---

	Item : "Riftbound Seal Fragment"

	Type : 
* Quest / Progression Item
* Tradeable (with restrictions)

---

	Drop Rules : 

* Dropped by: Aeralith, Warden of the Rift
* Account-bound drop cap :
	* Maximum **3 Riftbound Seal Fragments per account
* Drop chance:
	* Guaranteed drop until account reaches 3
	* After 3 → no longer drops for that account

> This prevents farming while still allowing trade/economy interaction.

---

	Economic Rule : 

* Vendor Sell Price : 10 Gold
* Creates a meaningful choice:
	* Immediate profit (10G)
	* Or long-term investment (collect all 3 for mount access)

---

	Requirement : 
* 3 Riftbound Seal Fragments
* All 3 must be obtained by the same account
* Fragments are consumed when exchanged

---

	Vendor : Kaelen Ridgeborne

* Role: Exotic Mount Broker / Seal Handler
* Location: north village (north side of Netar Island)
* Appearance:
	* Weather-worn traveler
	* Leather cloak reinforced with bone clasps
	* Carries carved seals and mount insignia

---

		Vendor Interaction Logic

	Dialogue Hook : 
> “Not many earn the Rift’s mark. Fewer still bring all three.”

---

	Purchase Condition : 

* Requires:
	* 3 Riftbound Seal Fragments
	* Gold cost (optional, tunable later)
* Result:
	* Unlocks purchase of a flying mount seal
	* Or allows petting/bonding interaction, depending on your system

> Vendor will not appear as mount vendor unless player has at least 1 fragment (optional hidden logic).

---

	Mount Unlock Flow (Clean & Lore-Friendly)

1. Defeat Aeralith
2. Obtain Riftbound Seal Fragments (max 3/account)
3. Choose:
	* Sell fragments (10G each)
	* Or save all 3
4. Travel to the north village
5. Exchange 3 fragments with Kaelen Ridgeborne
6. Unlock flying mount acquisition path

---

	Design Benefits : 
* Encourages exploration
* Prevents abuse / farming
* Adds economic tension
* Mount remains prestigious
* Vendor placement reinforces world traversal
* Vision of flying mount becomes foreshadowing, not deception

---

	Optional Flavor (Non-Mechanical)

* NPC reacts differently based on:
	* Number of fragments carried
	* Whether player sold fragments in the past
* Mount-related dialogue changes if player has seen the Rift Vision

