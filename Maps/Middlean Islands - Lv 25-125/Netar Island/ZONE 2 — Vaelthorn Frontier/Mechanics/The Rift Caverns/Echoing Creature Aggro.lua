			Rare Spawn / Puzzle Trigger — Rift Caverns Guardian

	Core Concept : 
The illusion of the player on a flying mount is not a reward fantasy, but a vision of potential — a projection of mastery, guidance, or challenge.
This vision leads to a rare cave guardian, not for cosmetics, but for progression, knowledge, and unique utility.

---
	
		Trigger Conditions (Puzzle-Based) : 

	All conditions must be met in sequence:

1. Interact with the Vision
	* Touch, attack, or cast magic at the illusion

2. Reflective Pool Trial
	* Jump across 3 specific reflective pools in the correct order
	* Order is hinted by:
		* Light refraction
		* Subtle ripples
		* Mineral glow intensity

3. Alignment Completion
	* When sequence is correct:
		* Cavern hum stops briefly
		* Light converges in a single chamber
		* The guardian spawns

> Failing the sequence resets the puzzle silently.

