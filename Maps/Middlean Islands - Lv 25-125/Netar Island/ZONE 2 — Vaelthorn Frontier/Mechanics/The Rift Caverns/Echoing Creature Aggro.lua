			Flying Mount — Skelvar Windrider

---

	Mount Type
* Flying Mount
* Large avian / wind-adapted beast

---

	Appearance : 
* Broad wingspan with layered feathers shaped like stone-carved blades
* Feathers shift from dark slate to pale sky-gray toward the tips
* Eyes glow faintly with pale blue light
* Talons reinforced with mineral growths from cliff nesting
* Subtle wind currents swirl around wings when airborne

	This mount looks powerful, ancient, and utilitarian, not decorative.

---

	Origin & Lore : 

* Native to the northern cliffs of Netar Island
* Rarely bonds with mortals
* Only those recognized by the Rift’s vision are tolerated
* Kaelen Ridgeborne acts as an intermediary, not an owner

---

	Acquisition Method : 

* Requires:
	* 3 Riftbound Seal Fragments
	* Interaction with "Kaelen Ridgeborne" in north village.
* Kaelen provides a "Windrider Bond Seal"
* Player must then:
	* Travel to a "Windrider Nesting Cliff"
	* Complete a short bonding interaction (non-combat)

> The mount is not “summoned instantly” — it must be bonded.

---

	Mount Behavior : 

* Calm on ground
* Restless in high winds
* Reacts subtly to:
	* Wind intensity
	* Cliff edges
	* Altitude

---

	Mount Mechanics : 

	Wind Adaptation (Passive)
* Reduced stamina drain while flying in high-wind zones
* Increased stability near cliffs

---

	Updraft Mastery : 

* Gains bonus lift from:
	* Natural updrafts
	* Windcaller Cliffs
	* Coastal arches

---

	Heavy Load Tolerance : 

* Slightly reduced penalty when carrying weight compared to other flying mounts

---

	Environmental Sensitivity : 

* Cannot be summoned:
	* Indoors
	* In unstable caverns
* Will refuse to land in unsafe zones

---

	Restrictions (Lore-Friendly) : 

* Cannot be used before completing Rift Caverns challenge
* Will not respond if seal fragments were sold without completing the set
* Bond is permanent once established

---

	Why This Mount Works : 

* No cosmetics, no illusion rewards
* Mount is contextual to the island
* Acquisition reinforces exploration, not farming
* Feels earned through understanding the world

---

	Optional Flavor : 

* NPCs in northern villages comment on the mount
* Windcaller Cliffs recognize the mount (ambient effects)
* Future zones may check for Windrider bond

