			Reflective Mineral Vision

	Type : Environmental / Rare Visual Event
* At certain times of the day (randomized within the day cycle), light hitting mineral formations creates an illusion in the main chamber.
* The image appears to be the player themselves, wearing shiny armor on a flying mount — completely personalized.

	Effect / Gameplay
* Purely visual / awe-inspiring
* Could act as a signal for a hidden mechanic : if players interact (touch, attack, or cast magic at the illusion), it may trigger:
	* Spawn of a mini-boss elemental
	* Appearance of a hidden treasure cache
	* Or temporary buff to the player

	Notes
* Encourages careful observation
* Rewards curiosity

---



---

	1. Base Event — The Vision

	Trigger Conditions

* Occurs at specific times of day, randomized within the day cycle
	(example: dawn, high sun, dusk — but never announced to players)
* Requires:
	* Light entering the main chamber
	* Mineral walls with reflective properties

### Visual Effect

* A spectral image of the player appears:
  * Same class & race
  * Wearing shining, idealized armor
  * Mounted on a flying mount
* The image is:
	* Semi-transparent
	* Silent
	* Slightly delayed in movement (feels unreal)
* Reflections ripple slightly across nearby water pools

> This is intentionally awe-only at first glance — no UI prompt.

---

	2. Interaction Layer — How Players Trigger It

	When the vision is active, any one of the following interactions can trigger an outcome :

	Possible Interactions

1. Touch the Vision
	* Walk directly into the image
2. Attack the Vision
	* Any melee or ranged attack
3. Cast Magic on the Vision
	* Any spell (damage, buff, or utility)
4. Interact via Water
	* Standing in a reflective pool while facing the vision

> Only one interaction per appearance is allowed.

---

	3. Outcome Roll — Randomized Result

Once interacted with, the system rolls one of three outcomes :

| Outcome                   | Weight   | Cooldown    |
| ------------------------- | -------- | ----------- |
| Water Elemental Mini-Boss | Rare     | 3–7 days    |
| Hidden Treasure Cache     | Uncommon | 24–72 hours |
| Temporary Buff            | Common   | 24 hours    |

> Cooldowns are per character , not per account.

---

		Outcome 1 — Water Elemental Mini-Boss

	Spawn Conditions

* Triggered by any interaction
* The illusion shatters into water and light
* Nearby pools begin to rise and ripple
* The elemental emerges from the largest reflective pool

---

		Mini-Boss: Refraction Warden

	Type
* Elemental (Water)
* Elite / Mini-boss

	Appearance
* Tall, slow-moving humanoid shape made of water and mineral shards
* Body refracts light like glass
* Smaller elemental fragments orbit it

---

	Behavior
* Slow movement speed
* Heavy, deliberate attacks
* Spawns small water-adds that resemble broken fragments of itself
* Controls space rather than chasing players

---

	Core Mechanics
* Hydraulic Slam
	Heavy AoE water strike, slows movement
* Mirror Flow
	Reflects light across pools, causing adds to spawn
* Condensed Form
	Gains armor when standing in water
* Fracture Spawn
	Periodically spawns 2–4 small elementals

	Adds
* Low HP
* Fast movement
* Explode into slowing puddles on death

---

	Weakness
* Vulnerable to lightning, fire, and burst damage
* Loses armor when pulled away from water

---

	Rewards
* Elemental essence
* Rift Cavern–specific crafting material
* Small chance at buff-related consumable

---

		Outcome 2 — Hidden Treasure Cache

	Trigger
* Vision dissolves peacefully
* Minerals glow brighter for a few seconds

	Effect
* A hidden cache becomes visible:
	* Under a pool
	* Behind a mineral wall
	* In a newly opened fissure

	Possible Rewards
* Rare gems
* Lore items
* Cavern-specific materials
* Small chance for utility items tied to island mechanics

---

		Outcome 3 — Temporary Buff

	Buff: "Breath of the Rift"

	Type
* Utility / Exploration Buff

	Effects
* Allows underwater breathing
* Reduced movement penalty in water
* Slight resistance to cold or slow effects

	Duration
* 30–60 minutes (tunable)

	Usefulness
	
* Exploring:
	* Coastal caves
	* Sunken paths
	* Underwater tunnels
* Synergizes with:
	* Stormfall Coves
	* Tideglass Passage
	* Future island content

> This buff cannot be refreshed until cooldown expires.

---

	4. Cooldown & Replayability Rules

* Each character can trigger:
	* Buff → every 24h
	* Treasure → 1–3 days
	* Mini-boss → up to 7 days
* Cooldown resets independently
* Vision may appear even if player is on cooldown → but interaction fails silently

---

	5. Design Intent (Why This Works)

* Not forced content
* Discovery-based
* Visual storytelling
* Encourages:
	* Observation
	* Experimentation
	* Returning at different times
* Scales naturally with curiosity, not power

