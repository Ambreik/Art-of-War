			Primary Mechanic: Abandoned Miner’s Diary

* Type : Lore / Psychological / One-Time Discovery
* Trigger : Interacting with a half-rotted satchel near a collapsed wall
* Availability : Once per character (or account, your choice)

---

	Diary Object

```
Name: Waterlogged Mining Diary
Condition: Pages swollen, ink smeared, but still readable
Found Near: A dead lantern and two broken pickaxes
```

---

	Diary Reading Mechanic

```
- Reading is done page by page (3–5 entries)
- Each page slightly darkens ambient light
- Subtle sound effects: distant water sloshing, faint breathing, stone creaks
- No combat triggers while reading (safe but oppressive)
```

---

	Diary Entries (Copy/Paste Ready)

	Entry I — The Collapse

```
Day unknown. The tunnel fell in behind us.

The stone screamed when it broke.
Dust everywhere. Couldn’t see my hands.
We tried to dig back, but the rocks kept shifting.

Someone laughed. I think it was Joren.
I don’t think he meant to.
```

---

	Entry II — The First Night

```
We heard them last night.

Metal dragging on stone.
Voices that tried to sound like prayers.
Torches flickered without fire.

One of the men ran.
We heard him scream once.
Then nothing.
```

---

	Entry III — The Water

```
The tide reaches us now.

We tried to swim out through the lower passage.
The water pulls too hard.
Creatures move beneath the surface — fast, sharp, hungry.

You can’t swing an axe and swim at the same time.
We learned that.
```

---

	Entry IV — Hunger

```
There are only two of us now.
Me… and Arvek.

We don’t talk anymore.
He looks at me differently.
I look at him too long.

I don’t remember the last time I ate.
```

---

	Final Entry — Acceptance

```
The water is rising again.

If it’s not the tide, it’ll be them.
If it’s not them, it’ll be the beasts.
If it’s not the beasts…

I’m not scared anymore.

I don’t know if I’ll pray.
I don’t know if I’ll swing the axe.
I just know I’m tired.

If someone finds this —
Tell the world we didn’t abandon the quarry.
The quarry abandoned us.
```

---

	Post-Reading Effect

```
- Diary crumbles into salt-dust after final page
- Ambient sound returns to normal
- Player receives:
   Item: “Miner’s Last Words” (lore item, no stats)
- Optional: Unlocks additional dialogue with Field Marshal NPC or Quarry NPCs
```

---

	Narrative Purpose

```
- Explains disappearance of quarry workers
- Links undead crusaders + aquatic threats
- Reinforces hopelessness and corruption of Netar Island
- Adds psychological weight without combat or rewards abuse
```

---

	Design Notes (Important)

```
- No combat spawn here (keeps it intimate)
- No repeat farming
- Purely narrative, but powerful
- Works as emotional contrast to boss-heavy quarry content
```

