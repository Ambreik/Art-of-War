			Resonant Chambers — Cave-Dwelling Monsters

	1. Echo Skitter

* Type : Arthropod / Cave Creature
* Small, fast, wall-climbing creature
* Reacts aggressively to sudden sounds
* Often appears in groups

---

	2. Deep Hummer

* Type : Beast
* Large, heavy-bodied creature with thick hide
* Naturally produces low-frequency vibrations
* Slow movement, high HP

---

	3. Rift Borer

* Type : Aberration
* Worm-like creature embedded in cavern walls
* Emerges when vibrations reach certain intensity
* Prefers narrow passages

---

	4. Resonant Stalker

* Type : Humanoid / Cave Predator
* Uses echoes to track prey
* Moves silently until close
* Often ambushes from darkness

---

	5. Crystalback Lurker

* Type : Beast
* Covered in mineral growths
* Blends into reflective walls
* Becomes active when resonance increases

---

	6. Hollow Screecher

* Type : Bat-like Creature
* Emits piercing shrieks that amplify cavern hums
* Appears in swarms
* Draws other creatures toward its location

---

	7. Fault Dweller

* Type : Elemental / Earth
* Lives inside narrow faults and cracks
* Responds to deep vibrations rather than surface noise
* Rare encounter

---

	8. Dripfang Crawler

* Type : Reptilian Cave Beast
* Lurks near reflective pools
* Sensitive to sound and movement
* Ambush predator

---

	9. Stoneveil Watcher

* Type : Construct / Ancient Creature
* Dormant unless resonance patterns are disturbed
* Awakens slowly
* Rare elite

---

	10. Murmurling

* Type : Aberration
* Small, whispering entity
* Feeds on echo and sound
* Often precedes stronger creatures

---

## Notes

* These monsters can:
	* Appear individually or in small groups
	* Be passive until resonance increases
	* Function as ambient threats or escalation triggers
* No overlap with water or wind mechanics
* All fit naturally into:
	* Narrow tunnels
	* Echo chambers
	* Sloping caverns

