			Vaelthorn Quarry — Undead Miner Ecosystem

	Monster Theme : 
* Undead quarry workers bound to tools, duty, and routine
* Slow, deliberate, but dangerous in groups
* Industrial combat style: pushes, pulls, bleeds, collapses
* Strong group synergy, weak alone

