		MONSTER ROSTER

---

*Rockie Miner : 
Undead Miner — Worker Type
Low–Medium HP. Low mobility. Melee attacks.
* Uses broken pickaxes and stone hammers
* Attacks apply “Fracture” (minor armor reduction)
* Occasionally slams ground causing short micro-knockback

	Weakness
* Vulnerable to fire damage
* Slower when affected by burn or fear

	Role
* Frontline fodder
* Body blockers

	Group
* 3–6 Rockie Miners
* Often accompany higher-ranked miners

---

*Hauler Miner(the one carrying stuff)
Undead Miner — Carrier Type
Medium HP. Slow movement. High pushback strength.
* Carries ore sacks, carts, or stone crates
* Uses shoulder charge and shove attacks
* Applies “Crushing Weight” (movement slow)

	Special Mechanic
* When killed, may drop carried materials:
	* stone fragments
	* ore chunks
	* unstable debris (can explode if ignited)

	Weakness
* Weak to piercing damage
* Very weak from behind

	Group
* 1 Hauler + 2–4 Rockie Miners

---

*Site Miner
Undead Miner — Skilled Worker
Medium HP. Medium attack speed.
* Uses sharpened tools
* Applies Bleed DoT
* Occasionally marks a player, increasing damage taken from other miners

	Behavior
* Targets wounded or isolated players
* Retreats behind Rockie Miners when pressured

	Weakness
* Vulnerable to magic damage
* Interruptible attacks

	Group
* 2 Site Miners
* Often paired with a Hauler

---

*Drunk Miner
Undead Miner — Unstable Type
Medium HP. Erratic movement and attack patterns.
* Swings wildly
* Occasionally stumbles, causing AoE knockback
* Can accidentally hit allies

	Special
* When low HP, enters “Rage Drunk”:
  * increased damage
  * reduced accuracy

	Weakness
* Highly vulnerable to stun and slow
* Easily kited

	Group
* 1–2 Drunk Miners
* Mixed into larger groups unpredictably

---

*Coordinator Miner
Undead Miner — Support / Control
Low HP. Low direct damage.
* Carries whistle or bell
* Periodically issues commands:
	* increases attack speed of nearby miners
	* calls reinforcements if not interrupted

	Special Mechanic
* If allowed to finish a call:
	* additional miners emerge from tunnels
	* or nearby groups link together

	Weakness
* Extremely weak to silence
* Panics under fear effects

	Group
* 1 Coordinator
* Always protected by 3–5 other miners

---

*Leader Miner
Undead Miner — Elite / Mini-Boss
High HP. Strong melee attacks.
* Uses reinforced pickaxe
* Heavy knockback attacks
* Periodic ground slam causing minor collapse effects

	Affects
* “Stagger”
* “Armor Break”
* “Fear” on low-health players

	Aura**
* Nearby miners take reduced damage
* Immune to fear while allies live

	Weakness
* Fire + magic damage combination
* Loses aura when alone

	Group
* 1 Leader
* 2 Site Miners
* 2–4 Rockie Miners
  *(Elite group)*

---

*The Buried Overseer (Rare / Event Monster)
Undead Miner — Rare Elite
Giant HP Bar. Boss-type.
* Emerges after:
  * prolonged quarry activity
  * multiple collapses
  * excessive mining

	Abilities
* Collapsing tunnel slam
* Pulls players toward debris
* Summons Rockie Miners mid-fight

	Weakness
* Highly vulnerable to fire and light-based effects
* Takes increased damage after collapses

	Group
* Solo (summons adds)

