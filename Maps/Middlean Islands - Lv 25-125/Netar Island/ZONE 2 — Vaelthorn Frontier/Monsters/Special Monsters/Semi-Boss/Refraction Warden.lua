			Refraction Warden

	Location : Location : Netar Island/Zone 2 - Vaelthorn Frontier/The Rift Caverns

---

	Type
* Elemental (Water)
* Elite / Mini-boss

	Appearance
* Tall, slow-moving humanoid shape made of water and mineral shards
* Body refracts light like glass
* Smaller elemental fragments orbit it

---

	Behavior
* Slow movement speed
* Heavy, deliberate attacks
* Spawns small water-adds that resemble broken fragments of itself
* Controls space rather than chasing players

---

	Core Mechanics
* Hydraulic Slam
	Heavy AoE water strike, slows movement
* Mirror Flow
	Reflects light across pools, causing adds to spawn
* Condensed Form
	Gains armor when standing in water
* Fracture Spawn
	Periodically spawns 2–4 small elementals

	Adds
* Low HP
* Fast movement
* Explode into slowing puddles on death

---

	Weakness
* Vulnerable to lightning, fire, and burst damage
* Loses armor when pulled away from water

---

	Rewards
* Elemental essence
* Rift Cavern–specific crafting material
* Small chance at buff-related consumable

