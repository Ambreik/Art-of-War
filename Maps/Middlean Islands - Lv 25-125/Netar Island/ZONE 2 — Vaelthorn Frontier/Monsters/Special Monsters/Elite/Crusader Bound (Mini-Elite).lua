			Crusader Bound (Mini-Elite)

	Location : Middlean Islands \ Netar Island \ Zone 2 Vaelthorn Frontier \ Bastion of Taranmere (inside the castle, patrolling seal proximity and inner halls)

---

	Appearance : "The Crusader Bound" is a towering figure among the undead, clad in corroded, battle-worn armor that seems fused to its spectral form. Faint green 
light seeps through the cracks in its plate, giving it an eerie glow in shadowed corridors. Its jagged two-handed sword radiates an unnatural chill, and its helm’s 
glowing eye slits scan relentlessly for intruders. A ghostly aura swirls around its frame, constantly shifting as if feeding off the seal’s unstable energy.

---

	Behavior : This mini-elite anchors patrol groups of undead, maintaining order among wandering crusaders while boosting their aggression. It prioritizes players 
nearest to the seal or displaying the highest threat. While highly aggressive, it patrols only within the seal proximity zones and inner halls; attempts to lure it 
outside are ineffective due to a strict leash system. It reacts dynamically during combat, escalating its attacks as its health diminishes, forcing coordinated team
engagement.

---

		Mechanics & Skills

	Mechanics :

* Category : Mini-Elite Crusader Undead
* Patrol Role : Anchor for nearby patrols; enhances aggression of other undead
* Dungeon Drops : None (can drop Sealbreaker Key to a lucky player)
* Solo Status : Cannot be soloed effectively
* Spawn Frequency : Every 3 hours
* Location Behavior : Patrolling inner halls and seal proximity zones only

		Phases :

	Phase 1 – Engagement :

* Detects players in medium aggro range
* Uses "Heavy Strike" and "Shield Slam"
* Boosts nearby patrol members’ attack speed by 15%

	Phase 2 – Seal Corruption (50% Health) :

* AoE fear effect for nearby players (2–3s)
* Summons 2 temporary "Crusader Watchers" if patrol group below max
* Gains +20% damage and +10% movement speed

	Phase 3 – Desperation (20% Health) :

* Gains lifesteal on all attacks
* AoE "Stagger Pulse" (knockback around self)
* Erratic charges at random players in patrol group

	Skills :

1. Heavy Strike : Single-target melee; applies bleed
2. Shield Slam : AoE damage; staggers nearby players
3. Summon Watchers : Calls temporary patrol minions (Phase 2)
4. Fear Aura : Short-range fear effect (Phase 2)
5. Stagger Pulse : AoE knockback at low health (Phase 3)

	Spawn & Respawn :

* Appears near seal zones inside the castle
* Respawns every 3 hours
* Anchors nearby patrols, triggering local escalation of undead

	Drops :
* No standard loot
* "Sealbreaker Key" can drop to a lucky player

