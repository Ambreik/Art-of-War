			Rare Guardian — Aeralith, Warden of the Rift

	Location : Location : Netar Island/Zone 2 - Vaelthorn Frontier/The Rift Caverns

---
	Type : 
* Spirit / Elemental Hybrid
* Rare Boss
* High HP, environment-driven combat

---

	Appearance : 
* Tall, floating entity made of condensed light, stone, and mist
* Wings are spectral projections, not true mounts
* Armor-like plates of mineral and crystal
* Eyes glow with shifting light patterns

---

	Behavior : 
* Calm and silent before engagement
* Becomes aggressive only after puzzle completion
* Does not chase players outside the Rift chamber
* Uses environment as part of combat

---

	Core Mechanics : 
* Lightbound Armor
	Reduces damage while standing in aligned light beams
* Rift Surge
	Environmental AoE using cavern walls and pools
* Spectral Summon
	Calls minor light-spirits when HP thresholds are reached
* Judgment of Potential
	Targets players who fail mechanics more aggressively

---

	Rewards (Lore & System Friendly)

	1. Seal Fragment (Flying Mount Path)

* NOT a mount itself
* A rare fragment required to:
	* Unlock a vendor option
	* Advance a mount-related questline
* Encourages exploration across regions

---

	2. Guardian’s Insight

* Permanent or semi-permanent character bonus:
  * Slight resistance to environmental hazards
  * Improved interaction with light-based mechanics
* Cannot stack infinitely

---

	3. Rift Essence

* Used for:
  * High-tier consumables
  * Special crafting recipes
  * Cave / water / exploration tools

---

	4. Environmental Achievement

* “Seen by the Rift”
* Unlocks:
	* Hidden dialogue options
	* Access to future secrets on Netar Island
* Not cosmetic — progression-oriented

