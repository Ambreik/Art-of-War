			Spiritual Manifest (Rare – Spirit Variant)

	Location : Middlean Islands \ Netar Island \ Zone 2 Vaelthorn Frontier \ Bastion of Taranmere (haunted wings, fallen battlements, and ruins near scrolls, statues, and bodies)

---

	Appearance : "The Spiritual Manifest" is a massive, ethereal entity composed of swirling, semi-transparent mist and flickering blue-white light. Its form is 
humanoid but constantly shifts and distorts, making it hard to focus on. Glowing runes float across its “body,” pulsing with energy, and spectral chains trail from 
its wrists, fading into nothingness. Its eyes shine as twin orbs of pure energy, radiating a chilling aura that warps the air around it.

---

	Behavior : This rare spirit wanders the ruins and haunted sections of the castle, appearing near high-lore locations like scrolls, statues, or bodies. It reacts
to players by phasing in and out, teleporting short distances to maintain pressure, and targeting anyone who lingers in its territory. The Spiritual Manifest cannot
be soloed and is intended to require coordinated party engagement. It is highly unpredictable, often appearing in areas of investigation or during high-pressure 
moments near the seal, creating both risk and tension. It does not drop standard loot but can grant a Sealbreaker Key to a lucky party member.

---

		Mechanics & Skills

	Mechanics :

* Category : Rare Spirit
* Spawn Frequency : Random window of hours to days
* Solo Status : Cannot be soloed
* Dungeon Drops : None (Sealbreaker Key may drop to a lucky player)
* Territory : Haunted ruins, battlements, and object-rich areas (scrolls/statues/bodies)
* Aggression : High; reacts dynamically to nearby inspections and player activity

	Skills :

1. Phantom Swipe : Melee range spectral attack; ignores armor, deals spirit damage
2. Ethereal Grasp : Pulls a single player toward itself, immobilizing briefly
3. Wailing Aura : AoE fear and minor continuous damage over 3s
4. Phase Shift : Teleports short distance unpredictably, avoiding attacks
5. Spectral Chain : AoE tether to multiple players; slows movement and reduces dodge chance

	Spawn & Respawn :

* Appears deep in ruined wings, battlements, and near object-heavy areas
* Respawn timer: randomized window of hours to days
* Only one active Spiritual Manifest at a time

	Drops :

* No standard loot

