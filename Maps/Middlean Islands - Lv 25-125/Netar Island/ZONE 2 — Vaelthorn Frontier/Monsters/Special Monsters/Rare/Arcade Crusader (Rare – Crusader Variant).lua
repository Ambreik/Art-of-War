			Arcade Crusader (Rare – Crusader Variant)

	Location : Middlean Islands \ Netar Island \ Zone 2 Vaelthorn Frontier \ Bastion of Taranmere (deep inner halls and collapsed chambers near the seal)

---

	Appearance : "The Arcade Crusader" is a massive, imposing undead warrior, clad in cracked blackened plate adorned with ceremonial golden trims dulled by age and
decay. Its face is hidden beneath a horned helm, the eye slits glowing an ominous crimson. Chains rattle across its chest and shoulders as it moves, each step 
resonating with a hollow, metallic echo. The Crusader wields a massive halberd that seems fused with spectral energy, leaving trails of flickering light with every 
swing.

---

	Behavior : This rare crusader is a territorial enforcer, patrolling deeper zones of the castle where the seal’s energy is strongest. It aggressively targets
players who linger near its patrol nodes or interfere with nearby Crusader groups. Arcade Crusader reacts dynamically to combat: it escalates its attack patterns if
players focus it, but cannot be lured far from its deep zone boundaries. Due to its rarity and strength, it is impossible to solo, and engagement requires a 
coordinated party. It is designed to interact with the seal environment, but will not drop standard loot, only giving a chance for a Sealbreaker Key to a lucky 
player in the party.

---

		Mechanics & Skills

	Mechanics :

* Category : Rare Crusader Undead
* Spawn Frequency : Random window of hours to days
* Solo Status : Cannot be soloed
* Dungeon Drops : None (Sealbreaker Key may drop to a lucky party member)
* Patrol Role : Stationary deep-zone enforcer with occasional patrols around collapsed halls
* Aggression : High; prioritizes players near seal and crusader patrols

	Skills :

1. Halberd Sweep : Wide frontal attack; knocks back all players in range
2. Crusader’s Judgment : Targeted heavy strike; deals high damage to one player
3. Chain Slam : AoE impact; stuns players briefly (2s)
4. War Cry : Buffs nearby undead; increases attack speed and damage for 10s
5. Spectral Charge : Charges a player; ignores obstacles, dealing damage and knockback

	Spawn & Respawn :

* Appears deep inside castle halls near the seal, often in collapsed or fortified areas
* Respawn timer: randomized window of hours to days
* Only one active Arcade Crusader at a time

	Drops :

* No standard loot
* Chance to drop "Sealbreaker Key" to one lucky player in party

