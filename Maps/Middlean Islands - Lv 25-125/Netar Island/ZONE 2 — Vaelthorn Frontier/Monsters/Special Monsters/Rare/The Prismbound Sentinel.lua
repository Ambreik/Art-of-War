			The Prismbound Sentinel
	
	Location : Location : Netar Island/Zone 2 - Vaelthorn Frontier/The Rift Caverns

---
	Type : 
* Construct / Ancient Mineral Entity
* Rare Elite

---

	Spawn Condition : 
* Triggered when 3 reflective mineral walls are aligned correctly
* Light must pass in sequence across walls and reflect into a pool
* The pool crystallizes briefly before the Sentinel emerges

---

	Appearance : 
* Tall humanoid construct made of layered crystal and stone
* Body refracts light into prismatic colors
* Cracks in its form glow softly
* Limbs are angular, slow-moving, precise

---

	Behavior : 
* Slow movement
* Highly defensive
* Guards the chamber where it spawns
* Does not pursue far beyond its summoning area

---

	Core Abilities : 
* Prismatic Cleave
	Wide arc attack that changes damage type based on light angle
* Refraction Shield
	Temporarily reduces incoming damage; stronger when standing in light
* Shard Pulse
	Emits crystal shards in a radial burst
* Light Anchor (Passive)
	Gains armor when light beams strike its body

---

	Weakness & Counterplay : 
* Vulnerable when pulled into shadow
* Light disruption reduces its defenses
* High resistance to physical damage

---

	Drops : 
* Prismatic Core
* Crystal crafting materials
* Rare scrolls tied to ancient knowledge

---

	Design Intent : 
* Feels like a guardian, not a hunter
* Rewards players who understand light manipulation

