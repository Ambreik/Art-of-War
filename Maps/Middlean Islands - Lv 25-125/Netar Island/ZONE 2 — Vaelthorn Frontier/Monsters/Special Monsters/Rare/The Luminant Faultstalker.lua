			The Luminant Faultstalker
			
	Location : Location : Netar Island/Zone 2 - Vaelthorn Frontier/The Rift Caverns

---
	Type : 
* Aberration / Light-Touched Predator
* Rare Elite

---

	Spawn Condition : 
* Triggered by misaligned light that briefly flashes across multiple mineral walls
* Requires light to flicker or distort rather than align perfectly
* Occurs more often at dawn or dusk

---

	Appearance : 
* Low, quadrupedal creature with elongated limbs
* Semi-transparent body that bends light unnaturally
* Faint glowing veins pulse under its skin
* Eyes emit sharp beams when it focuses on prey

---

	Behavior : 
* Fast, aggressive, ambush-oriented
* Moves unpredictably through shadows
* Attacks quickly, retreats, then re-engages

---

	Core Abilities : 
* Lightwarp Dash
	Short-range teleport between illuminated surfaces
* Glare Rend
	High-damage strike that briefly blinds the target
* Fault Phase
	Becomes semi-intangible when standing in fractured light
* Hunting Instinct (Passive)
	Increased damage against isolated targets

---

	Weakness & Counterplay : 
* Vulnerable to sustained AoE damage
* Light stability reduces its evasion
* Low defense compared to Sentinel

---

	Drops : 
* Luminant Fang
* Rare alchemical reagents
* Cosmetic or illusion-based items

