	Quotes *(Adaptive / Dynamic)*

* On spawn: “You dare disturb my quarry? Foolish mortals!”
* On low HP: “I’m not done yet… this stone remembers!”
* If stunned or roots: “Argh! You think a pile of rocks can stop me?!”
* After killing player: “Another fool for the stone.”
* Using Deathblow: “Feel the wrath of the deep veins!”
* Seeing barrels destroyed: “You play with my tools, and you’ll pay!”
* Taunting repeated players: “Back again? Did the last lesson not suffice?”

---

	Notes / Strategy for Players

* Solo low-level (<55) : almost impossible
* Solo high-level (>68) : can defeat with skill, use environment
* Party composition critical: avoid too many Shamans/Warlocks
* Environmental use is mandatory : carts, rocks, barrels
* Aggression and adaptive AI reward strategic positioning

