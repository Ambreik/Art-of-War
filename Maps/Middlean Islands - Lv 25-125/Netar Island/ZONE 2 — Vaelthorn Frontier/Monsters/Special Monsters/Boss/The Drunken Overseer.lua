			The Drunken Overseer

---

	Location : Netar Island/Zone 2 - Vaelthorn Frontier/Vaelthorn Quarry

---

	Appearance

* Massive undead miner, standing taller than any normal miner.
* Wears tattered miner’s clothes, reinforced with old iron plates.
* One arm wields a rusted axe, the other a massive stone pick.
* Face partially covered by a soot-stained helmet, with glowing green eyes.
* Covered in scars, dirt, and old mining residue; barrels of ale and tools strapped to belt.
* Heavily muscled, slow walk, but when enraged, moves with surprising agility.
* Shadow stretches across the chamber, flickering in the extinguished firelight.

---

	Behavior (Short Description)

* Territorial and reactive to light, noise, and number of intruders.
* Uses the environment as weapons: barrels, rocks, carts, pillars.
* Adapts dynamically depending on party size, composition, and player levels.
* Tanky, with huge HP pool and strong defense.
* Engages in a mix of melee, area control, and mobility bursts.
* Can spawn adds if the party is large.
* Becomes permanently enraged if multiple Shamans or Warlocks are present.
* Remembers players they fought previously (up to 5 times/month) and mocks them.

---

	Mechanics / Environmental Interaction

1. Extinguish Fire Trigger
	* Players must light the campfire in the ending chamber to trigger event.
	* Countdown begins (random: 30s–2min) to destroy 3 stone pillars to summon boss.
	* If pillars not destroyed in time, boss does not appear, entrance remains open.

2. Sealed Arena
   * Upon spawn, entrance is sealed with falling stones or iron door, trapping players in arena.

3. Environmental Weapons
	* Big rocks, carts, barrels can be thrown by players to damage boss.
	* Barrels**: 5 green (heal boss if close), 3 red (explosive, can be thrown by boss if party >2 players)
	* Boss uses barrels dynamically:
		* Only targets barrels when at distance, cannot use barrels if close.
		* Explosive barrels thrown only under multi-player conditions.
	
4. Adaptive Combat
	* Boss adapts to party size and composition :
		* Solo <55 → impossible to defeat
		* Solo 68+ → solo victory possible
		* Party 3×55 → survival possible
		* Party 5+ ×55 → required for efficiency
	* Multiple Shamans/Warlocks → boss permanently enraged (Deathblow stacks with Berserk)

5. Memory / Mocking Mechanic
	* Tracks last 5 player encounters per month.
	* Recalls players who lost before and taunts them dynamically.

6. Environmental Hazards
	* Jump/root stun skill: lands on player, stuns + massive damage.
	* Uses terrain: pushes players into walls, barrels, or pillars.
	* Adds spawn for large groups or high-level parties.

---

	Phases / Adaptability

1. Phase 1 — Base
	* HP > 75%
	* Uses melee swings, stone throws, and minor knockbacks
	* Targets nearest player

2. Phase 2 — Tactical
	* HP 50–75%
	* Adds more barrel interaction
	* Uses dashes to catch ranged players
	* Rotates priorities based on player distance

3. Phase 3 — Berserk
	* HP 25–50%
	* Damage doubles (Berserk)
	* Attack speed spikes (God)
	* Uses jump-root skill frequently
	* More aggressive environmental use (rocks, pillars, barrels)

4. Phase 4 — Deathblow / Enraged
	* Only triggers if multiple Shamans/Warlocks present
	* Random ultra-damage skill stacks with Berserk
	* Aggression maxed, adds summoned if applicable

5. Adaptive Behavior
	* Alters movement, skill usage, and barrel targeting dynamically
	* Uses dashes and jumps to control player spacing
	* Prioritizes weaker or isolated players first

