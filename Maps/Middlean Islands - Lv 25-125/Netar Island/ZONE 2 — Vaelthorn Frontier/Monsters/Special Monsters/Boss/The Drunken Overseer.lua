		The Drunken Overseer Arena Mechanics Map

	Arena Overview

* Shape : Rough rectangle, 30m × 20m
* Entrance : Top-center, sealed on boss spawn
* Chamber : Ending tunnel, dim light, extinguished fire at center
* Floor : Uneven stone, rubble, collapsed scaffolding areas

---

	1.Fire Trigger / Spawn

* Position : Center of the arena, small fire pit
* Mechanic : Lighting the fire starts countdown (30s–2min) to destroy 3 stone pillars to summon boss
* Note : If players fail to destroy all 3 pillars in time, boss does not spawn

---

	2. Pillars

* Number : 3
* Placement : Equilateral triangle around the fire (~5–7m from fire)
* Interaction : Destroy within countdown → triggers boss
* Mechanic : Can be destroyed by players’ melee, magic, thrown objects
* Notes : Represent visual cues for the ritual; only interactable during countdown

---

	3. Barrel Layout

* Total : 8 barrels
	* 5 green (healing for boss)
	* 3 red (explosive, thrown by boss if party >2)

	Placement :
* Spread across arena edges: avoid clustering in center
* Boss only interacts if distance >4–5m from barrels
* Players can use barrels as environmental tools

	Mechanic Notes :
* Green barrels heal boss (only at distance)
* Red barrels explode on contact (AoE), can be thrown by boss if criteria met

---

	4. Carts / Big Rocks Interaction Zones

* Position : Scattered along arena sides and corners
* Use : Players can push, throw, or roll for AoE damage
* Mechanic : Rolling carts can knock back boss, deal damage, or break weak walls
* Notes : Only usable when boss not standing directly on top

---

	5. Jump / Root Skill Zones

* Boss Leap / Root Target Areas :
	* Central fire area (landing spot)
	* Around pillars for root effect radius (~3–4m radius)
* Mechanic : Boss can jump over players, root them, and receive massive damage if hit during animation

---

	6. Boss Movement & Aggression Zones

* Pathing :
	* Patrols slowly around arena until engaged
	* Uses dash attacks to corner ranged players near walls
	* Avoids barrels if close, moves toward them if distance allows
* Hazard Zones :
	* Collapsed scaffolding → may trigger minor knockbacks
	* Rubble → slows movement, occasionally trips players

---

	7. Environmental Triggers / Interactions

* Lighting Fire : Starts boss countdown
* Destroying Pillars : Triggers boss spawn
* Barrels (green/red) :
	* Green heals boss, only if distance > threshold
	* Red explodes if boss throws, AoE damage
* Carts & Rocks : Pushable, throwable, damage boss or environment
* Collapsible Walls : Can be knocked down by boss slam, traps, or barrels

---

	8. Player Strategy Zones

* Safe Corners :
	* Near arena edges, far from pillars, barrels, or collapse points
	* Good for ranged combat
* High-Risk Areas :
	* Near fire pit (jump/root)
	* Between barrels if boss is enraged or multi-player fight
* Environmental Play :
	* Push carts from safe corner to hit boss
	* Lure boss near explosive barrels for damage
	* Use pillar destruction timing to trigger mechanics

