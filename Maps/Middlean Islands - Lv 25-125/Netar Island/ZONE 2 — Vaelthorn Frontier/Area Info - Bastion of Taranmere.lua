			CASTLE INTERIOR SECTIONS


	1. Outer Breach Gate (Collapsed Gatehouse)

	Purpose
The original main gate of the castle — never completed, never reinforced.

	Current State
* Massive stone arch half-built
* Rusted portcullis frozen mid-drop
* Broken chains embedded into stone
* Siege scaffolding still attached to the walls, petrified by time

	Narrative Role
	
	This gate tells players immediately:
* The castle was never meant to be lived in
* Construction stopped suddenly
* The threat came from inside the mountain, not outside

No enemies patrol here.
This is a psychological threshold.

