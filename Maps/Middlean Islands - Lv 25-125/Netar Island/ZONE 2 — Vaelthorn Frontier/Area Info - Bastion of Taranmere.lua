		THE CASTLE AS A WORLD LOCATION

	Bastion of the Last Oath
	South-west Netar Island, base of the Black Mountain

This step defines how the world understands the castle, not what players do there.

---

	1. How the Castle Is Perceived

	A. Soldiers of the Outpost

	To the soldiers stationed nearby, the Bastion is:
* A failed fortification
* A grave marker
* An ongoing duty

	They are taught three things:
1. The castle was built to hold the line
2. The line failed
3. Their role is to ensure it never fails again

	Most soldiers:
* Do not enter the castle
* Do not speak of what they hear at night
* Refer to it simply as “the Bastion” or “the Old Wall”

	Veterans say:

> “It still thinks it’s at war.”

	They believe the crusaders inside are:
* Still defending something
* Still obeying orders
* Still hostile to anyone not part of the Legion

---

	B. Scholars & Orders (Order of Restoration, Dawn Pact)

	To scholars, the Bastion is:
* A containment structure
* A historical crime scene
* A living seal

	They know:
* The castle is part of the binding ritual
* Destroying it would destabilize the island
* Studying it risks accelerating decay

Scholars are divided into two factions:

	Preservationists
* Believe the seal must remain untouched
* See exploration as dangerous arrogance

	Restorationists
* Believe understanding the seal is the only way to prevent catastrophe
* Quietly sponsor adventurers

This tension is unresolved — and intentional.

---

	C. Regular Travelers & Sailors

	To common folk, the castle is:
* A cursed ruin
* A bad omen
* A place to avoid

	Superstitions include:
* Birds refusing to land on its walls
* Wind dying unnaturally near its gates
* Lights appearing inside with no torches lit

	Sailors mark it on maps as:

> “Stone that remembers.”

Most refuse to approach after dusk.

---

	2. Why Players Are Allowed Near It

	Players are allowed near the Bastion because:
* The seal requires external pressure release
* Minor disturbances prevent catastrophic rupture
* Controlled exploration is safer than stagnation

	In-universe logic:

> The seal needs movement — not silence.

	The outpost operates under a "Containment Tolerance Doctrine" :
* Small groups allowed
* Limited access
* Heavy observation

	Players are seen as:
* Test subjects
* Pressure valves
* Replaceable, if necessary

No one says this aloud.

---

	3. Why the Castle Is Not Destroyed

	The Bastion cannot be destroyed because:

1. Structural Collapse Risk
	* The castle anchors unstable mountain layers
	* Removing it could cause massive subsidence

2. Totemic Dependency
	* Totems are integrated into walls and foundations
	* Breaking stone disrupts spiritual flow

3. Crusader Reaction
	* Any large-scale assault causes violent surge responses
	* Past attempts resulted in mass casualties

4. Unknown Depth Impact
	* Scholars fear the dungeon would “wake fully”
	* No one knows what that truly means

The safest option is containment through neglect.

---

	4. Why It Is Not Fully Evacuated

	Evacuation would:
* Allow corruption to stagnate
* Increase internal pressure
* Accelerate seal decay

The outpost exists because of the castle.

Soldiers rotate frequently.
No one stays long enough to “listen too closely.”

---

	5. The Castle’s Role in Island Psychology

	The Bastion acts as:
* A warning
* A boundary
* A reminder of disobedience

	It reinforces the moral taught to soldiers:

> Orders exist for a reason.

	And the darker truth whispered by scholars:

> Something wanted the Crusaders here.

---

	6. The Castle in the Island’s Natural Flow

The mountain breathes.
The caverns pulse.
The undead roam in patterns.

All of it radiates outward from the Bastion.

It is not the source —
It is the point of pressure.

