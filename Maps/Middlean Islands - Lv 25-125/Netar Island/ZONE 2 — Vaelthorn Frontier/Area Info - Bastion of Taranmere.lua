			Bastion of Taranmere

*Unfinished Castle at the Base of the Mountain*

---

	The Bastion of Taranmere lies in the south-western reaches of Netar Island, pressed against the lower spine of the mountain like a wound that never healed. 
From a distance, the structure does not immediately resemble a castle at all — instead, it appears as a jagged cluster of stone walls and broken towers, rising 
unevenly from the rocky ground where the mountain’s shadow never fully retreats. The land here slopes sharply upward, forcing the fortress into an awkward, 
defensive posture, as though it were built in haste and desperation rather than with pride or foresight.

	The mountain looms directly behind it, its stone face scarred by cracks and unnatural fissures. One such fracture yawns open behind the castle’s central 
courtyard — the concealed mouth of the "Tomb of the Great Crusaders", though no sign announces its true nature. To the untrained eye, it appears merely as a 
collapsed cavern sealed by rubble and ancient masonry. To those who know the history, it is the reason the bastion exists at all.

---

		Exterior & First Impression

	The outer walls of Taranmere rise no higher than two completed stories in most places, their intended third tier never finished. Stones are unevenly set, mortar
lines crude and inconsistent, suggesting construction by soldiers rather than masons. Scaffolding remnants still cling to certain walls — rotted beams lashed 
together with rusted iron hooks, frozen in place since the day the work abruptly ceased.
	Several towers stand incomplete, their upper levels open to the sky. Narrow arrow slits puncture their lower sections, but no battlements crown them. Rainwater 
has carved dark streaks down the stone, and moss grows freely in the cracks, thriving in the perpetual damp caused by the mountain’s presence.
	The main gate faces southwest, oriented toward the coastal approach rather than inland settlements. It consists of two massive oak doors reinforced with iron 
bands, now split and warped. One hangs partially off its hinges, while the other lies shattered on the ground, its iron fittings corroded into jagged teeth. Above 
the gate, a half-carved sigil of the Crusader Order remains unfinished — its lines shallow, its symmetry flawed, as if the carver was interrupted mid-stroke.
	Tattered banners once bearing Crusader colors still cling to broken poles, their cloth bleached and torn beyond recognition. When the wind blows down from the 
mountain, they stir faintly, producing a dry, rasping sound that resembles whispered speech.

---

		Courtyard & Inner Grounds

	Beyond the gate lies the central courtyard, wide but uneven, its ground fractured by roots and subsidence. The stone paving was never properly set, and 
several areas have collapsed inward, exposing dirt, gravel, and older layers of rock beneath. Scattered across the courtyard are abandoned construction tools: bent 
chisels, broken mallets, rusted nails, and lengths of chain once used to haul stone.
	Near the center stands the skeletal frame of what was meant to be a command tower. Only its lower foundation exists — a circular base ringed by half-finished 
columns. The interior is open to the sky, and rainwater pools inside, reflecting the broken silhouette of the mountain above. Some say this tower was intended to 
house the final warding mechanism meant to seal the mountain breach entirely.
	Along the courtyard’s edges, half-built staircases climb toward walls that end abruptly, forcing anyone ascending them to turn back. In some places, the stone 
steps simply vanish, leaving nothing but open air. These incomplete paths give the unsettling impression that the castle was never meant to be finished — or was 
never allowed to be.

---

		Walls, Sigils, and Corruption

	The interior walls of the bastion are carved with Crusader markings, though many are defaced or corrupted. What were once symbols of faith and unity now appear 
distorted — lines twisted, circles broken, and inscriptions overwritten by darker etchings. Some markings seem scratched hastily, as if made in fear or madness 
rather than devotion.
	In certain shadowed corners, the stone itself has taken on a faint green-blue discoloration, pulsing subtly when the air pressure shifts. These areas lie 
closest to the mountain fracture and respond to unseen forces below. When approached, the temperature drops, and faint vibrations can be felt through the soles of 
one’s boots.

	At night, these stones emit a dim, spectral glow, just enough to outline the corrupted sigils etched into them. No two glow patterns are ever quite the same.

---

		Sound, Wind, and Atmosphere

	The bastion is never silent.

	Winds descend constantly from the mountain pass behind it, funneling through gaps in the walls and incomplete towers. These gusts create low, resonant hums that
travel through the stone, echoing across the courtyard and into the surrounding foothills. At times, the sound resembles distant chanting; at others, it feels like 
the deep exhalation of something enormous sleeping beneath the mountain.
	Footsteps echo longer than they should. Voices carry oddly, sometimes returning from unexpected directions. Metal left unattended vibrates faintly, as if 
responding to a rhythm no one else can hear.

	Those who linger too long report the sensation of being watched — not from the walls, but from below.

---

		The Sealed Mountain Breach

	At the rear of the castle, where the stone foundation meets the mountain’s base, lies the true reason for Taranmere’s existence. A massive stone barrier, 
reinforced with ancient masonry and partially collapsed wards, blocks a natural cavern entrance. This barrier was constructed hastily, its stones mismatched and 
layered atop one another without artistry.
	Fragments of old totems and ritual anchors are embedded into the stonework, their power faded but not extinguished. Faint symbols still pulse when touched, 
reacting to living presence. Cracks run through the seal, wide enough for cold air — and something else — to seep through.

	Behind this barrier lies the "Tomb of the Great Crusaders", its presence pressing outward, straining against the incomplete defenses meant to contain it.

---

		Overall Impression

	The Bastion of Taranmere does not feel abandoned.

	It feels interrupted.

	Every wall, every stair, every tower speaks of urgency — of soldiers racing against time, aware that something beneath the mountain was awakening faster than 
they could stop it. The castle stands as both a shield and a gravestone, marking the moment when containment failed and sacrifice became inevitable.
	Even in ruin, it still serves its purpose — not by holding the enemy back entirely, but by warning all who approach that what lies beyond was never meant to be 
unearthed.

---

		Bastion of Taranmere — Interior

---

		Entry Hall – The Broken Watch

	Passing through the ruined main gate, travelers enter a broad stone hall once intended as the castle’s primary guard station. The ceiling rises higher here than
in any other completed interior space, supported by thick stone ribs that curve upward like the inside of a ribcage. Portions of the roof are missing, allowing pale
daylight — or moonlight at night — to spill in uneven shafts, illuminating drifting dust and the faint shimmer of disturbed air.
	Rusting weapon racks line the walls, many still holding cracked shields or blunted spears abandoned where they fell. The floor is marked by deep grooves where 
heavy stone blocks were dragged inward during construction, then left behind when the work ceased. Faded footprints are still visible in the dust, layered upon one 
another, as though the soldiers marched in circles during their final days.

	At the far end of the hall stand two unfinished staircases, one leading upward into collapsed quarters, the other descending deeper into the heart of the 
bastion.

---

		The Hall of Oaths

	Beyond the entry hall lies a long, rectangular chamber flanked by stone statues of Crusader knights. Each figure stands with head bowed, hands resting atop the 
pommel of a grounded blade. Their faces are worn smooth, not by time alone, but by deliberate defacement — eyes scratched away, mouths chipped, sigils broken.
	The walls are carved with fragments of oaths and vows, many incomplete, their inscriptions abruptly ending mid-sentence. Some lines appear overwritten, as if 
the original words were replaced by desperate additions etched with trembling hands.
	The air here is unnaturally still. Sound dampens, footsteps muted as though the stone absorbs noise. Even the wind from outside struggles to reach this chamber,
giving it the feeling of a place meant for final decisions.
	A massive circular seal is set into the floor at the hall’s center — dormant, cold, and partially cracked. Whatever ritual was intended to be performed here was
never fully completed.

---

		Side Chambers – Barracks of the Faithful

	Branching from the Hall of Oaths are several small side rooms, once serving as temporary barracks and supply storage. These chambers are low-ceilinged and 
claustrophobic, their walls close enough to brush against passing shoulders.
	Bunk frames lie overturned, bedrolls rotted away. Crates remain stacked against walls, many unopened, suggesting the soldiers never had time — or the will — to 
use what they brought. In one chamber, helmets and armor are arranged in a neat row, as though their owners removed them voluntarily and never returned.
	Faint scratch marks can be found along some walls, clustered near doorways and corners. They are not deep, but frantic, layered over one another in chaotic 
patterns.

---

		The Spiral Passage

	At the rear of the interior complex, a narrow spiral corridor descends gradually into the mountain. This passage is not a staircase, but a sloping stone ramp 
carved directly into the bedrock, spiraling inward and downward.
	The walls grow rougher the deeper one travels, transitioning from worked stone to natural rock veined with faintly glowing minerals. These veins pulse subtly, 
brightening and dimming like a slow heartbeat. The sound of wind changes here, becoming a low, rhythmic pressure that can be felt in the chest rather than heard.
	Occasionally, small alcoves open along the spiral, each containing fragments of broken tools, shattered ritual anchors, or half-carved statues abandoned mid-
creation.

---

		The Convergence Chamber

	The spiral passage opens into a vast circular chamber — the largest completed interior space of the bastion. This chamber was clearly designed as the final 
staging ground before the seal.
	A domed ceiling arches overhead, its apex fractured, allowing thin shafts of mountain light to pierce through drifting mist. The floor is carved with concentric
rings of symbols, intersected by radial lines leading toward a raised central platform.
	Around the chamber stand colossal statues of Crusader champions, far larger than those above.Unlike the bowed figures in the "Hall of Oaths", these stand 
upright, weapons raised — frozen in the act of defense. Their armor is heavily stylized, bearing insignias no longer recognized by any modern order.

	Some statues are cracked, others partially collapsed, their broken pieces scattered across the floor as though something passed between them with great force.

	The atmosphere here is oppressive but focused — a place meant for resolve, not retreat.

---

		The Threshold Maze

	Beyond the "Convergence Chamber" lies a short, deliberate labyrinth carved into the mountain itself. This maze is not complex in size, but in intention. Narrow 
corridors twist just enough to disorient, forcing travelers to pass close to the stone walls.
	Statues line these corridors as well, smaller and more abstract — faceless figures clutching tablets, chains, or broken blades. Some passages loop back briefly,
while others end in sealed stone doors etched with warnings and fractured prayers.

	The maze serves not as a challenge of navigation, but of patience and awareness — a final test before the mountain’s heart.

---

		The Mountain Fracture – The Sealed Wound

	At the end of the labyrinth, the stone opens into a raw, natural cavern — the mountain fracture itself.
	This is no ordinary cave. The walls are jagged and uneven, split apart by immense force, as though the mountain was torn open from within. Cold air pours 
outward in rhythmic pulses, matching the breathing cadence felt throughout the bastion.
	At the center stands the "Great Seal" — a colossal barrier of stone, metal, and ancient bindings fused together. Chains thicker than tree trunks anchor it into 
the surrounding rock, some snapped, others stretched tight and vibrating faintly. Embedded relics and warding pillars surround the seal, many shattered or inactive.
	Through the cracks, a deep, endless darkness can be seen, and from within it comes a presence — not a sound, but a pressure, pressing outward against the seal 
and against the minds of those who stand before it.

	This fracture is not merely an entrance.

	It is a scar left behind when something ancient was buried alive — and never truly stopped trying to escape.

