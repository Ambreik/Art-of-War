			RETURN TO ISLAND FLOW

	Netar Island: Structural & Narrative Continuity

This step ensures the castle, village, and dungeon do not overshadow the island, but instead bind it together.

---

	1. Netar Island After the Castle Is Placed

	With the "Bastion of the Last Oath" established:
* The island is no longer just “dangerous”
* It becomes contained danger
* Every zone now exists under influence, not chaos

	The Bastion is:
* The anchor point
* The pressure regulator
* The narrative gravity well

	Nothing on Netar is random.

---

	2. Zone Flow (High-Level)

Without changing any zone design you already made:

	Coastal Zones

(Stormfall Coves, Tideglass Passage, Silent Pier)

* Show early corruption signs
* Smuggling, secrets, remnants of escape
* Water as both refuge and trap

	These zones feel:
* Opportunistic
* Desperate
* Transitional

---

	Inland / Geological Zones

(Broken Stone Forest, Vaelthorn Quarry, Rift Caverns)

* Reflect long-term exposure
* Corruption has settled into patterns
* Creatures adapt instead of panic

	These zones feel:
* Uneasy but functional
* Exploited, not abandoned
* Haunted by routine, not shock

---

	Mountain-Adjacent Zones

	(Windcaller Cliffs, Breathing Cavern, Lost Quarry Tunnel)

* Show direct resonance
* Air, sound, pressure, and memory
* Nature reacting to something buried

	These zones feel:
* Alive
* Watchful
* Unwelcoming in subtle ways

---

	3. The Village’s Role in Island Flow

	The military outpost village exists as:
* A buffer
* A filter
* A narrative checkpoint

	Players:
* Enter Netar as explorers
* Reach the village as witnesses
* Approach the Bastion as participants

	The village controls:
* Information
* Access
* Tone

	It keeps the island from becoming a loot playground.

---

	4. Dungeon Placement (Without Opening It)

	The "Tomb of the Great Crusaders" now sits:
* Beneath the Bastion
* Beneath history
* Beneath obligation

	Its presence is felt through:
* Enemy behavior
* Environmental anomalies
* NPC attitudes

	Not through:
* Forced content
* Immediate access
* Overexposure

	This preserves its status as a megadungeon, not a hallway.

