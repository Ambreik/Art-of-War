			Military Outpost Village of Netar

	Name : "Ravenwatch Encampment"
	South-west Netar Island, north-east of the Bastion of the Last Oath

---

		1. Overview

	"Ravenwatch Encampment" is a fortified military settlement established to monitor, contain, and respond to threats emerging from the "Bastion of the Last Oath" and the mountain beneath it.

	It is not a civilian village.

	It exists for:
* Surveillance
* Containment
* Logistical support
* Controlled adventurer deployment

	The encampment is permanent, but no one considers it safe.

---

		2. Visual & Structural Description

* Built primarily from:
	* Reinforced timber
	* Black iron plating
	* Salvaged stone from failed fortifications

* Surrounded by:
	* Wooden palisades
	* Watchtowers with constant archer presence

* Banners display:
	* Neutral military insignia
	* No faction pride, only duty

	At night:
* Torches burn with steady blue-white flame
* Guards rotate frequently
* Horn signals are tested at regular intervals

---

		3. Population & NPC Presence

	Military Presence

* Heavily guarded at all times
* Includes:
	* Infantry soldiers
	* Archers
	* Scouts
	* Engineers
* Most soldiers:
	* Are non-interactive
	* Cannot be targeted or attacked
	* Exist as environmental reinforcement

	This reinforces:

> “This place is under constant watch.”

---

	Interactive NPCs (Limited)

Only a small number of NPCs interact with players:

	Command Authority

* "Field Marshal of Ravenwatch" 
* Oversees all operations
* Primary source of:
	* Island-wide intelligence
	* High-level dungeon-related quests
	* Emergency responses

	Quest Officers

* A few designated officers issue:
	* Recon quests
	* Recovery missions
	* Containment tasks

	These quests:
* Rarely involve “clearing”
* Often involve observation, retrieval, or survival

---

		4. Vendor & Services

	Quartermaster Vendor (Single Vendor)

* Sells:
	* Basic supplies
	* Rations
	* Light consumables
	* Emergency items (bandages, flares, markers)
* No advanced gear
* No luxury items

	This vendor exists to:
* Prepare players
* Not empower them excessively

---

		5. Atmosphere & Tone

	Ravenwatch Encampment feels:	
* Tense
* Disciplined
* Quietly exhausted

	Soldiers:
* Avoid unnecessary conversation
* Speak in short, formal phrases
* Do not share rumors openly

	Occasionally, players may overhear:
* Disputes between officers
* Warnings about patrols gone silent
* Mentions of “movement” near the Bastion

---

		6. Relationship to the Castle

	The encampment exists because of the Bastion.

* Patrols never enter the castle interior
* Scouts monitor changes in:
	* Sounds
	* Light
	* Undead movement
* Any anomaly is logged immediately

	The village is close enough to react — but far enough to retreat.

---

		7. Player Function in the Village

	For players, Ravenwatch is:
* A staging ground
* A warning sign
* A narrative checkpoint

	It reinforces:

> “You are not here by accident — and neither is the danger.”

---

		8. Narrative Role Summary

	Ravenwatch Encampment serves as:
* The human anchor of Netar Island
* The last line of order
* The place where players realize:
	* This island is being watched
	* But not fully understood

