## **D. Creature / AI Mechanics (Zone-specific, expanded)**

### **Cliff Ambushers**

* Creatures perched on elevated areas use vertical ambush tactics.
* They leap or glide downward toward players, gaining momentum and impact bonus.

### **Sound-Reactive Predators**

* Certain creatures in The Broken Stone Forest and Rift Caverns respond to noise.
* Running, mining, or dropping heavy items alerts them even behind walls.
* They may circle the noise source before attacking, behaving tactically.

### **Darkvision Hunters**

* Cave dwellers have enhanced detection range in darkness.
* Light sources may attract them or alter their patrol patterns.
* They navigate complex tunnel systems with precision, making tunnels risky at night.

