# ✅ **4. Vegetation & Natural Features (Redone, more descriptive)**

Vaelthorn Frontier’s flora is shaped by constant wind, salt spray, and rugged stone.
Instead of lush vegetation, the zone expresses nature in **lean, wind-hardened forms** that cling to the land for survival.

### **Trees & Large Flora (Descriptive Only)**

* **Coastal Windbent Trees** — gnarled, low-growing trees that never grow straight. Their trunks twist and lean sharply inland, shaped by generations of sea winds. Their bark is rough and muted gray, often flaking due to salt exposure.
* **Highland Pines** — tall, thin pines found slightly inland. Their needles form tight clusters to reduce wind drag, giving them a sleek silhouette. The tops sway constantly.
* **Deep-Rooted Ridge Oaks** — sturdy, wide-rooted trees growing between rocky hills. Their roots wrap around stone outcrops, stabilizing them against storms.

### **Undergrowth & Ground-Level Flora**

* **Grass mats** — short, dense grass growing close to ground level to avoid wind shredding. These grass beds often ripple like fur under strong gusts.
* **Bristled shrubs** — low, thorny shrubs with tiny leaves adapted to retain moisture. They form tight clustered groups on slopes.
* **Coastal moss beds** — thick moss growing on large stone faces. Their texture is velvety, absorbing mist and dew through the day.
* **Sea-salt ferns** — small ferns that thrive in cracks of cliff walls. Their fronds shimmer lightly when coated in dried salt.

### **Natural Landmark Formations**

* **Rock formations carved by ancient tidal pressure**, forming twisting shapes and hollowed arches.
* **Petrified tree-like pillars** of stone in The Broken Stone Forest, giving the area an eerie prehistoric atmosphere.
* **The Shattered Basin**, once a large hill that collapsed centuries ago, now a bowl-like geological scar filled with scattered boulders.

Overall, vegetation here feels **tough, sparse, resilient**, embodying the concept of a frontier resisting the sea’s relentless force.

