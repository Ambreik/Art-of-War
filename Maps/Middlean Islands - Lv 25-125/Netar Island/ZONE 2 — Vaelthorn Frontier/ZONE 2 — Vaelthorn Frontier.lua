			ZONE 2 — Vaelthorn Frontier

---

# **1. Overview / Introduction**

Vaelthorn Frontier is the **rugged western gateway** of Netar Island, positioned between the warm southern lowlands of Southfall Dominion and the colder, wind-carved heights of Greymarch Uplands. This zone acts as a transitional frontier — a place where the gentle terrain of the south begins to twist into jagged formations and uplifted rock spines as it pushes toward the island’s western edge.

The region is defined by its **wild, untamed coastline**, battered by powerful ocean currents. Deep coves, narrow fjord-like cuts, and tall sea stacks dominate the horizon. Inland, the terrain rises into **broken hills, scattered boulder fields**, and **dense thornwood pockets**, giving the entire region a harsh, frontier-like feeling. It is the least settled side of Netar Island, known for its isolation, unstable cliffs, and deep-rooted wilderness.

Vaelthorn Frontier is unique among the five zones because it is shaped by **relentless wind pressure** from the Great Sea. Trees bend inland, cliffs erode dramatically, and the air constantly carries the scent of salt and storm. This makes the region feel alive — an ever-moving frontier where nature is always in motion.

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

# **2. Geography & Terrain**

Vaelthorn Frontier stretches across the west-south expanse of the island, touching the Great Sea along a long and fractured coastline. Compared to the gentler slopes of Southfall Dominion, this zone is **sharply uneven**, with elevation shifting quickly as the land rises toward the center of the island.

### **Coastline**

* Steep, dark cliffs dominate most of the shoreline.
* A handful of beaches exist, but they are narrow, coarse, and usually tucked between jagged rock formations.
* Multiple **sea arches** and **natural stone bridges** form along the wave-carved coast.
* The ocean depth steepens rapidly, making waters here darker and colder than the southern sea.

### **Inland Terrain**

* The coastal rise leads into **broken moorlands** with rolling hills and scattered boulders.
* Low ridges mark the frontier’s interior boundary, gradually transitioning into the slopes of the **Central Mountain Range**.
* Several dry gullies and creekbeds snake across the zone, only filling during seasonal rain bursts.
* Caverns and fissures run beneath the earth, often interconnected by unstable tunnels.

### **Terrain Transitions**

* **South:** connects smoothly to Southfall Dominion’s lighter, greener lowlands.
* **North:** terrain steepens and cools approaching Greymarch Uplands.
* **East:** becomes rocky and rises toward mountain foothills.
* **West:** drops sharply into the Great Sea.

Vaelthorn Frontier is the most “wild frontier” portion of the island — unpredictable, dramatic, and visibly shaped by natural pressures.

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

# **3. Climate & Environmental Behavior**

The region receives the **strongest winds** on the entire island due to its exposed position facing the Great Sea.

### **Wind Systems**

* Persistent westward winds push inland, bending vegetation.
* Sudden gusts occur randomly, especially near cliff edges.
* Wind tunnels amplify sound in narrow coast passages.

### **Fog & Humidity**

* Morning fog rises from cold water and drifts inland.
* Visibility can drop significantly in ravines and lower moorland pockets.
* Fog tends to cling to the ground in swirling patches.

### **Day/Night Atmosphere**

* **Day:** Bright, crisp light with strong shadows due to elevation shifts.
* **Night:** Very dark due to lack of settlements; the sea carries faint bioluminescent foam along the coast.

The overall feeling is a zone shaped by relentless elements — wild, cold, and always moving.

