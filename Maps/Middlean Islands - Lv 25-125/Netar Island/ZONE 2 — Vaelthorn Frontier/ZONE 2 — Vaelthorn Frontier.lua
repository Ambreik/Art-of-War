# ✅ **7. Environmental Mechanics (A–D) — Fully Elaborated**

---

## **A. Global Mechanics (Rewritten in descriptive form)**

These mechanics apply across the entire game world, but here they are restated fully so the Zone 2 file is complete and standalone.

### **1. Day/Night Cycle**

* Daylight enhances visibility and reduces monster aggression.
* Night drastically increases threat levels: creatures become fully aggressive and gain +15% to all core stats.
* At night, drop chances increase by +10% across all creature types.
* Ambient light shifts significantly, with coastal zones experiencing stronger moon reflections on water surfaces.

### **2. Fatigue Waters**

* Extended swimming reduces stamina gradually.
* Rapid currents or cold waters accelerate this drain.
* Being swept too far from land applies a stacking fatigue debuff, eventually leading to drowning.
* Prevents players from bypassing exploration routes through uncontrolled sea travel.

### **3. Water Flow & Currents**

* Every body of water—including rivers, sea inlets, and certain pools—has directional flow.
* Currents affect movement speed, fishing cast trajectories, and pulled enemies.
* Strong offshore currents near Vaelthorn Frontier may drift players toward deeper danger zones.

### **4. Environmental Audio Layering**

* Soundscapes dynamically change depending on terrain: crashing waves, cave echoes, wind whistling.
* Audio cues help players detect approaching creatures or understand environmental hazards.
* Louder player movement creates detectable noise signatures in surrounding AI behavior.

### **5. Monster Awareness**

* Creatures leverage both sight and sound when detecting players.
* Low light boosts creature detection efficiency.
* Line-of-sight interruptions such as rocks or ridges reduce detection.
* At night, awareness radius increases further due to heightened senses.

### **6. Noise Awareness**

* Sprinting, combat, and harvesting produce noise detectable by nearby monsters.
* Certain surfaces (stone floors, hollow cliffs) amplify footstep noise.
* Stealth approaches must consider sound radius and environmental amplification.

---

## **B. Zone-Specific Environmental Mechanics (Greatly elaborated)**

### **1. Wind Pressure Zones**

* Certain high-elevation or cliff-edge areas emit constant wind gusts that subtly push the player toward the inland direction.
* Movement control requires slight compensation; gliding is affected, creating drift.
* Projectiles may deviate in these zones, slightly altering ranged attacks.

### **2. Updraft Columns**

* Strong vertical wind streams push upward from cliff gaps or chasms.
* Gliders can catch these updrafts to ascend higher or travel farther.
* Falling players may be cushioned, reducing fall damage if they land in an active updraft.
* Some updrafts fluctuate, creating timing-based traversal advantages.

### **3. Echo Tunnels**

* Narrow stone corridors that amplify sound vibrations.
* Player actions within tunnels may travel far, drawing creatures from distant chambers.
* Subtle audio cues (wind pitch, resonance) can indicate hidden openings or cave splits.
* Creatures using echolocation behave differently here, gaining bonus detection range.

