## **C. Resource-Based Mechanics (Expanded Details)**

### **Stormveil Lotus**

* Appears only in tidepools at low tide, vanishing completely once waters rise.
* Lotus petals retract into the root bulb during daytime cycles, making them inaccessible.
* The herb glows faintly at night, aiding detection.

### **Stormforged Ore**

* Its formation relies on rare wind surges: periods of intense, sudden atmospheric pressure.
* After a surge, new veins appear in deep caverns for a limited time.
* These veins are visually distinct with a metallic blue glow.

### **Saltwind Herb**

* Always grows in high-wind areas; calm patches never spawn it.
* Strong winds cause the herb to sway violently, making harvesting slightly slower.

### **Blackfoam Grouper**

* Only spawns when bioluminescent plankton is active at Blackfoam Shore.
* Fishing success increases when waves glow at their brightest.

### **Ironvein Shards (unstable nodes)**

* Some nodes may collapse after interaction, temporarily sealing parts of a cavern.
* Harvesting must be quick and deliberate.

