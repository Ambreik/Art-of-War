Netar Island rises from the ocean like a single broken continent, divided naturally by a tall mountain spine and stretched into an uneven form that resembles an **“L” with an elongated tail**. This southern–eastern extension, long ago shaped by tectonic uplift, forms the most accessible coastline of the island and hosts the only location suitable for a major port. It is here that most travelers first set foot on Netar, stepping from ship decks onto an island that immediately feels ancient, untamed, and shaped by unpredictable forces.

---

# 🔹 **THE SOUTHERN REGION — Lowlands, Arrival Coast, and the Island’s Warmest Lands**

The entire southern section of Netar Island is a **broad, uneven lowland** framed by rolling hills and backed by the towering central mountains to the north. As ships approach from the open sea, the land seems to rise gently before reversing abruptly into steep, forest-coated slopes where the mountain spine begins.

### **Arrival Coast and the Eastern Tail**

The southeastern “tail” of the island stretches outward like a bent arm of land, sheltered by natural coves and shallow bays. The coastline is dotted with sandbars, smooth beaches, tide pools, and clusters of large, curved rocks shaped by constant waves. The waters here are clear, with turquoise hues revealing sprawling **coral shelves** beneath them.

This portion of the south is the island’s most stable zone for maritime travel. It is also the location of the **main port** — a place where newcomers disembark before pushing deeper inland.

### **Southern Flora and Terrain**

The south is defined by:

* **Vast mixed forests**, dominated by tall broadleaf trees tapering toward the mountains.
* **Numerous streams** flowing down from the mountain meltwater.
* **Ferns, flowering bushes, and mossy undergrowth** thick enough to hide small clearings.
* **Open meadows**, created naturally when the river bends widen into calm floodplains.

The further west one travels, the more rugged the coastline becomes. Rocky outcrops begin replacing beaches, and the forests thicken in volume and height.

---

# 🔹 **THE WESTERN HALF — Split into Two Natural Zones by the Mountains**

The western side of Netar is **wild, harsher**, and significantly less forgiving than the gentler east. Because the central mountain range runs almost the entire length of the island, the western territories naturally split into **two distinct sections** — divided by altitude, terrain, and the way the cliffs meet the sea.

---

## **WEST-SOUTH REGION — Forest Ridges, Hidden Groves, and Broken Coastline**

Southwestern Netar feels like a world slowly returning to wilderness.
The land is dominated by:

* **Steep ridges**
* **Dense forests**
* **Long finger-like valleys** that push inland from the western shore

The air is thick with humidity, the sound of insects, and the distant crash of powerful waves. The western coast is particularly dramatic here — carved into **towering cliffs**, narrow beaches, and natural stone arches that bend over the sea. Large boulders sit half-submerged in the surf, suggesting the land has been fractured repeatedly over time.

### **Forests and Groves**

The trees in this region grow tall and gnarled, their roots gripping heavy rock beds. Under their canopy lie:

* **Shadowed ravines**
* **Streams that carve wandering paths**
* **Hidden groves**, where natural clearings allow sunlight to pool onto moss-covered soil

When winds blow from the west, the entire forest sways in long, rolling motions, like a single organism breathing.

---

## **WEST-NORTH REGION — Harsh Cliffs, Cold Winds, and Exposed Terrain**

The northwestern side of the island becomes more severe.
The air is colder, the cliffs higher, and the vegetation grows sparser as the terrain rises toward the northern cap of Netar.

This region is shaped by:

* **Jagged rocky shelves**, rising in uneven terraces
* **Grassland atop stone**, exposed heavily to salt winds and storms
* **Weaker tree growth**, consisting of wind-bent pines and stunted evergreens

Here the coastline is at its roughest:
Entire parts of land appear as though torn away, leaving behind dramatic **fracture lines**, giant slabs of stone, and sea-cut caves that breathe out cold mist.

---

# 🔹 **THE EASTERN REGION — The Calm Side, Slopes, Waterways, and Sunken Secrets**

The mountains that divide Netar create enormous contrast:
While the west is weather-beaten and wild, the **east** is calmer, brighter, and much more fertile.

### **Sloping Terrain**

Unlike the sharp descents of the west, the eastern side gently slopes down from the mountain foothills toward the sea. This creates:

* **Broad forest areas**
* **Open highlands**
* **Large stretches of soft, greenery-covered soil**
* **Long winding rivers** running southeast from the mountains

### **Calmer Coasts**

The eastern shores are well protected, leading to:

* **Stable beaches**
* **Long coves**
* **Natural harbors**
* **Accessible shallows**

Because of this calmer environment, many of the island’s **sunken ruins and shipwrecks** gather here — vessels from past eras that drifted into the island’s orbit and met the hidden reefs and rock shelves beneath the surface.

### **Submerged Sights**

Along the eastern coastline, the water is often clear enough to reveal:

* Broken hulls lying on the ocean floor
* Stone platforms overtaken by coral
* Remnants of forgotten structures now entwined with sea life

Some of these extend outward into the ocean like drowned roads or fallen walls, hinting at old shorelines that once lay higher.

---

# 🔹 **THE NORTHERN REGION — Wind-Shaped Plains and the Stone Pillars**

At the very top of Netar lies a place where the land opens wide.
The mountains taper off here, flattening into a broad, cold terrain shaped by unrestrained wind.

### **The Northern Plains**

This region is characterized by:

* **Open grass plains**
* **Sparse trees bent permanently by the coastal winds**
* **Broad plateaus** overlooking both seas
* **Cold morning fogs** that drift inland

Travel is easier here than in the west but harsher than in the east. The plains rise and fall in gentle waves, broken occasionally by groups of rocks and narrow streams.

### **The Stone Pillars**

Netar’s most defining northern feature is the cluster of **towering stone pillars** rising from both land and water.
These formations stand like natural spires, some crooked, some impossibly thin, reaching toward the sky with weathered surfaces chipped by centuries of storms.

Many pillars:

* Stand isolated in shallow water
* Are connected by natural stone bridges
* Form narrow canyons between them
* Create eerie echoes when wind passes through their gaps

When fog settles, the pillars loom like ghostly silhouettes, giving this northern region a presence unlike anywhere else on the island.

---

# 🔹 **THE CENTRAL MOUNTAIN SPINE — The Heart of Netar**

The mountains that run from **south to north** define the entire island.
Their peaks are jagged and steep, often lost in thick fog. The higher elevations remain cold even when the coasts are warm.

### **Climate and Atmosphere**

* Fog clings to the ridges both morning and evening.
* The upper air is constantly disturbed by harsh winds.
* Loose stones and cliffs make direct ascent treacherous.

At their highest points, the mountains flatten into narrow ridgelines where the wind howls and visibility drops. The atmosphere here feels isolated from the rest of the island — a world suspended between earth and sky.

---

# 🌿 **General Vegetation Distribution Across the Island**

* **South** — Warm mixed forests, many streams, vibrant undergrowth.
* **West-South** — Dense, wild forests with rugged ridges and shadowed groves.
* **West-North** — Sparse pine forests, sharp cliffs, cold grasses.
* **East** — Large forests, softer soil, rich greenery, calmer waterways.
* **North** — Grassy plains, wind-bent trees, low shrubs, cold coastal flats.
* **Mountains** — Rocky slopes, scattered hardy plants, high fog zones.

