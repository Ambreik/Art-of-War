		Eastern Coast – Coastal Creature Ecosystem

	Core Identity of the Coast

	This coastline is:
* Shallow-water rich
* Predator-heavy
* Highly reactive to tides, sound, and reflections
* A zone where **visual deception and ambush dominate survival**

	The ecosystem revolves around:
* Tides
* Silence
* Reflections
* Territorial predators
* Opportunistic scavengers

---

	1. Apex Predators : These creatures control population and define danger.

	A. Mireback Crocodiles

* Role : Apex shoreline predator
* Habitats :
	* Shallow water
	* River mouths
	* Mirror Sand edges
	* Near wrecks

	Behavior :
* Lie motionless, mimicking driftwood or rock
* Attack only when prey is within perfect range
* Prefer ambush over pursuit

	Gameplay Mechanics :
* First strike bonus damage
* Can drag targets into water
* Retreat if heavily injured rather than fight to death
* More aggressive at night and high tide

	Ecosystem Impact :
* Keeps crab, boar, and small beasts in check
* Scavenges corpses near wrecks

---

	B. Brinebound Maw (Rare Variant) : A monstrous crocodilian feeding near wrecks.

* Drawn by blood and shipwreck debris
* Territorial, but slow-moving
* Can fight other large predators
* Rare loot source
* Appears only in low tide or fog

---

	2. Territorial Mid Predators : These dominate land-water edges and hunt in small packs.

	A. Shorefang Boars

* Role : Coastal scavenger-hunter
* Habitats :
	* Grass near springs
	* Bluffs above Seastrand
	* Transitional zones between jungle and beach

	Behavior :
* Feed on crabs, carrion, plants
* Become aggressive if threatened
* Flee if isolated or wounded

	Gameplay :
* Charge attacks
* Can knock players into water or hazards
* Occasionally fight crocodiles over prey

---

	B. Tide Stalkers (Lizard-Beasts)

* Role : Agile ambushers
* Habitats :
	* Rocks
	* Shallow reefs
	* Cliff bases

	Behavior :
* Cling to surfaces
* Drop on prey from above
* Prefer isolated targets

	Gameplay :
* Leap attacks
* Bleed effects
* Vulnerable to fire and lightning

---

	3. Flying Coastal Creatures : These define the vertical layer of the ecosystem.

	A. Pelicans & Sea Gulls

* Role : Opportunistic scavengers
* Habitats :
	* Wrecks
	* Beach
	* Cliff edges

	Behavior :
* Non-hostile by default
* Attack when provoked (coconut mechanic)
* Swarm response

	Gameplay :
* Blinding pecks
* Steal small items or interrupt casting
* Can disrupt other mobs

	Ecosystem Role :
* Clean corpses
* Reveal hidden predators by agitation

---

	B. Reef Harriers (Predatory Birds)

* Role : True aerial predators
* Habitats :
	* Starwatch Ledge airspace
	* Luminous Ridge skies
	* Over Seastrand Mirror at dusk

	Behavior :
* Circle from afar
* Dive-bomb attacks
* Avoid ground combat

	Gameplay :
* High burst damage
* Vulnerable while ascending after dive

---

	4. Small Fauna & Prey Species : These creatures sustain the entire ecosystem.

	A. Mirror Crabs

* Role : Primary prey species
* Habitats :
	* Mirror Sand
	* Shallow waters
	* Under rocks

	Behavior :
* Flee from players
* Freeze when reflections are disturbed
* Gather in large clusters at low tide

	Gameplay :
* Resource: Shells, meat
* Trigger predator spawns if over-farmed

---

	B. Tide Frogs

	Habitats :
* Dawnchill Springs
* Marshy inlets

	Role :
* Feed insects
* Prey for birds and reptiles

	Gameplay :
* Used in alchemy
* Rare glowing variants appear during mist

---

	5. Scavengers & Cleaners : These ensure the ecosystem "resets" naturally.

	A. Sand Leeches

* Appear near blood and corpses
* Drain life
* Multiply quickly if ignored

	B. Reef Worms

* Appear after major fights
* Attract birds and small beasts
* Despawn once area stabilizes

---

	6. Environmental Interactions

	Tide Influence

| Condition | Ecosystem Effect                                         |
| --------- | -------------------------------------------------------- |
| High Tide | Crocodiles aggressive, fewer crabs, more aquatic spawns  |
| Low Tide  | Crabs abundant, rare predators emerge, wrecks accessible |
| Fog       | Rare monsters appear, birds less active                  |
| Night     | Predators more aggressive, ambush range increased        |

---

	Sound Influence : Because of the sound-absorbing cliffs:

* Loud abilities:
	* Attract birds
	* But not land predators
* Splashing water:
	* Attracts crocodiles instantly
* Bell from Starwatch Ledge:
	* Drives predators away temporarily
	* But attracts Crusader undead

---

	Reflection Influence

	On Mirror Sand:
* Certain predators become harder to detect
* Stealth effectiveness increases
* Flying creatures appear duplicated visually

---

	7. Inter-Species Dynamics

	This is where the ecosystem feels alive.

* Crocodiles hunt boars and crabs
* Boars scavenge wrecks after battles
* Birds steal from boars and players
* Reef Harriers steal prey from crocodiles
* Pelicans swarm wounded animals
* Frogs attract snakes and lizards
* Overhunting any prey species causes:

  * Predator aggression to rise
  * Migration from nearby zones

---

	8. Player Impact on the Ecosystem

	Players are not neutral.

	Their actions affect:
* Spawn rates
* Aggression levels
* Rare creature chances

	Examples:
* Killing too many crabs → crocodiles roam inland
* Using coconuts frequently → birds become hostile by default
* Farming crocodiles → boars overpopulate temporarily
* Leaving corpses → scavengers increase

