		Cragwallower Stats, Abilities & Environmental Interactions

---

	1. Scout Cragwallower
Role : Fast, light attacker; tests patrol and crowd mechanics
* HP : Low (~15–20% of Soldier)
* Damage : Low melee (~5–10 per hit)
* Speed : Very high, patrol/detection aware
* Abilities :
	* Quick Dash : Short burst to close gaps or evade guards
	* Stumble Trap : Occasionally knocks over barrels or market crates, causing temporary player pathing obstacles
* Environmental Interaction :
	* Fog : Scouts are slightly less accurate with any ranged attacks (if they carry small rocks)
	* Crowd Panic : Can blend into civilian movement, confusing guard AI temporarily
	* Heat Shimmer : Minimal effect

---

	2. Ranger Cragwallower
Role : Ranged harasser; interacts with environmental hazards
* HP : Moderate (~50% of Soldier)
* Damage : Moderate ranged (~10–15 per hit)
* Speed : Medium
* Abilities :
	* Rock Toss : Standard ranged attack
	* Poison Spit : Area effect, slows players or NPCs briefly (applied as minor debuff, like -5% movement for 5s)
* Environmental Interaction :
	* Fog : Ranged accuracy reduced by 15–20%
	* Wind Draft : Can occasionally miscalculate projectile landing
	* Crowd Panic : May hit civilians accidentally, escalating chaos
	* Light Shimmer : Minor projectile visual distortion

---

	3. Soldier Cragwallower
Role : Core melee pack unit; tanky frontliner
* HP : High (~200–250 base)
* Damage : Medium melee (~20–30 per hit)
* Speed : Medium
* Abilities :
	* Cleave : Hits multiple players in melee range (~3–4 units radius)
	* Guard Break : Temporarily interrupts guard AI if hitting them directly (mechanic reward for using AI interaction)
* Environmental Interaction :
	* Crowd Panic : Can push civilians to create dynamic obstacles
	* Fog : Minimal impact; melee focused
	* Wind Draft : No effect
	* Heat Shimmer : No effect

---

	4. Berserk Cragwallower
Role : High-threat melee; reacts dynamically to player/environmental stimuli
* HP : Medium-High (~150–200 base)
* Damage : High (~25–40 per hit)
* Speed : High when enraged
* Abilities :
	* Frenzy : Gains +20% speed/damage when near fire or explosive source (balloon fire, artillery)
	* Leap Attack : Jumps across short distances to reach elevated terraces
* Environmental Interaction :
	* Hot Air Balloons / Fireballs : Triggers Frenzy automatically if nearby, making them more dangerous
	* Crowd Panic : May ignore obstacles entirely in pursuit
	* Fog / Heat Shimmer : Minimal effect

---

	5. Mage Cragwallower
Role : Support / environmental manipulator
* HP : Moderate (~100–120)
* Damage : Low direct (~10–15) but area effect high (~20 per second in fire/smoke zones)
* Abilities :
	* Fireball Cast : Can ignite oil barrels or structures, causing area damage and smoke (affects player visibility)
	* Smoke Cloud : Creates localized fog, reducing ranged accuracy for players and guards (~10–15% effect radius)
* Environmental Interaction :
	* Fog / Mist Entry : Can reinforce fog temporarily
	* Light / Heat Shimmer : Fireball projectile visual distortion reduced accuracy for both sides
	* Crowd Panic : Fire or smoke may cause additional NPC movement

---

	6. Alpha Cragwallower
Role : Raid boss / wave leader
* HP : Very high (~500–600)
* Damage : High (~30–50 per melee hit)
* Speed : Medium (bursts to high when enraged)
* Abilities :
	* Roar : Fear effect on nearby NPCs/players for 2–3 seconds (causes temporary stagger or movement slowdown)
	* Smash : AoE melee attack (~5-unit radius)
	* Command Pack : Increases nearby Cragwallowers’ attack speed by 10% while active
* Environmental Interaction :
	* Hot Air Balloons : Alpha prioritizes attacking fireball sources; may cause miscalculated targeting if players glide or drop bombs mid-frenzy
	* Incendiary Artillery : Attempts to avoid projectiles, creating openings for players to manipulate guard AI
	* Fog / Mist : Reduces ranged threat to Alpha, but melee unaffected
	* Light Shimmer : Can misjudge distant player positions, creating emergent tactical windows
	* Crowd Panic : Alpha may use fleeing NPCs as “cover” to reposition, adding unpredictability

