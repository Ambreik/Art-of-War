			Sunveil Market Raid – Wave Spawn & Scaling

---

		General Rules

* Event Trigger : Players inside/near Sunveil Market Town; optionally quest-flagged
* Raid Duration : ~10–15 minutes depending on player count
* Scaling : Number of monsters and elite spawns scale with:
	* Number of players in town
	* Player average level / combat rating
* Spawn Points :
	* Surrounding hills / ridge edges (for Scouts, Rangers)
	* Forested approaches (Soldiers, Berserks)
	* Terrace ledges / higher ridges (Mages, Alpha)

---

	Wave 1 – Scout Wave

* Purpose : Introduce threat, test guard AI & environmental mechanics
* Duration : 2–3 minutes
* Spawn Numbers :
	* 8 × Scout Cragwallower
	* 3 × Ranger Cragwallower
* Spawn Logic :
	* Scouts spawn at two distant points around the town; Rangers slightly elevated to harass from range
	* If players use Hot Air Balloons early, Scouts may retreat temporarily, triggering Guard Suspicion State

---

	Wave 2 – Pack Assault

* Purpose : Core challenge; requires environmental manipulation
* Duration : 3–5 minutes
* Spawn Numbers :
	* 5 × Soldier Cragwallower
	* 3 × Berserk Cragwallower
	* 2 × Mage Cragwallower
* Spawn Logic :
	* Soldiers enter through primary valley entrances; Berserks flank terraces if Balloon / Artillery detected
	* Mages spawn from elevated platforms to ignite environmental hazards (oil barrels, crates)
* Dynamic Scaling :
	* If previous wave killed quickly, extra Berserks (+1–2) spawn to increase challenge
	* If many players inside town, additional Scouts may appear to harass NPCs

---

	Wave 3 – Elite Alpha

* Purpose : Climax; forces full player coordination
* Duration : 4–5 minutes
* Spawn Numbers :
	* 1 × Alpha Cragwallower
	* 3 × Elite Soldier Cragwallower
	* 2 × Elite Berserk Cragwallower
	* 2 × Ranger Cragwallower (support/distraction)
* Spawn Logic :
	* Alpha appears on elevated ridge behind town for tactical approach
	* Elite Soldiers and Berserks approach in staggered groups, creating multi-directional threats
	* Ranger Cragwallowers harass from terrace edges, occasionally triggering Guard Alert AI
* Dynamic Scaling :
	* Extra Elite Berserks spawn if players focus heavily on Alpha
	* Alpha reacts to Balloon and Artillery use; if skillfully dodged, may misstep on terraces (emergent opportunity)

---

	Spawn Timing & Emergent Flow

* Wave 1 : Scouts appear → ~30 seconds later, Rangers join → 1–2 minute engagement
* Wave 2 : Soldiers & Berserks enter in staggered groups → 1–2 minutes later, Mages spawn → full wave duration 3–5 minutes
* Wave 3 : Alpha + Elite units enter sequentially → Alpha waits for 50–70% of pack to arrive → multi-minute coordinated fight

	Emergent Elements

* Hot Air Balloons / Artillery : Use triggers adaptive AI for Berserks / Alpha
* Crowd Panic : Creates unpredictable paths for Scouts/Soldiers
* Fog / Shimmer / Wind : Randomly impacts ranged units and AI targeting, introducing variability each raid
* Guard AI : Patrols dynamically react, creating choke points or distractions
* Player Coordination : Needed to manipulate multiple mechanics for optimal survival

---

	Optional Special Variations

* Weather-Influenced Spawn : Rare “Fog Surge” may delay Ranger accuracy or reduce visual detection radius for Alpha
* Random Elite Modifier : Occasionally one Soldier or Berserk spawns with Frenzy-like speed buff (+10–15%)
* Reinforcement Chance : If Wave 2 takes >4 minutes, 1–2 additional Scouts appear, forcing players to re-prioritize

