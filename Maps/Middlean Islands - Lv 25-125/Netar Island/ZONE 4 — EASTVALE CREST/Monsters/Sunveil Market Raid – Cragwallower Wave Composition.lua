			Sunveil Market Raid – Cragwallower Wave Composition

	Wave 1 – Scout Wave

* Purpose : Introduce players to the threat; test guard AI & environmental mechanics
* Composition :
	* 8 × Scout Cragwallower (fast, low HP, moderate damage)
	* 3 × Ranger Cragwallower (ranged attacks, throws rocks or spits venom)
* Behavior :
	* Avoid heavy defenses at first
	* Target market stalls and paths, causing early crowd panic
	* Patrol-adapted: respond to guard AI, fog, and light mechanics

---

	Wave 2 – Pack Assault

* Purpose : Main challenge; environmental manipulation required
* Composition :
	* 5 × Soldier Cragwallower (heavily armored, melee)
	* 3 × Berserk Cragwallower (fast, high damage, frenzy when near artillery/fire)
	* 2 × Mage Cragwallower (casts fire or earth magic to interact with environment, e.g., ignite oil barrels or create smoke clouds)
* Behavior :
	* Aggressively hunt players
	* Avoid direct artillery line of fire if possible
	* Use crowd chaos for cover
	* Fog reduces their ranged attacks slightly
* Environmental Interaction :
	* Light shimmer affects Mage Cragwallower aiming (misses)
	* Wind draft may push them off terraces if not careful

---

	Wave 3 – Elite Alpha

* Purpose : Climax of raid; requires full player coordination
* Composition :
	* 1 × Alpha Cragwallower (huge HP, high damage, leader AI)
	* 3 × Elite Soldier Cragwallower
	* 2 × Elite Berserk Cragwallower
	* 2 × Ranger Cragwallower (support, distraction)
* Behavior :
	* Alpha coordinates pack, prioritizes artillery or balloon users
	* Guards’ alert AI reacts strongly, converging to main threat
	* Player must leverage wind draft, fog, and crowd dynamics to survive
* Special Hidden Interaction :
	* If players use gliding + balloon drops at correct timing, Alpha’s targeting can misfire temporarily, creating a skill-based exploit

