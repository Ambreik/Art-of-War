	7. SKILLS & ABILITIES

(Structured, not table)

---

	1. Abyssal Siphon (Passive – Vampiric Core)

	Effect:
• Every successful hit drains HP and resources
• Converts drained value into Sustain HP
• If hitting shielded or immune targets, still drains resources

	Special Rule:
• If players burst 20% HP in 1–3 seconds, the Warden channels an emergency siphon:
	* Locks target
	* Heavy drain over 3 seconds
	* Interruptible

---

	2. Cataclysmic Pulse (AoE – Massive Radius)

Type: Shadow AoE
Radius: Very large
	Effect:
• Deals heavy shadow damage
• Drains mana, energy, stamina
• 40% of damage dealt converted into Sustain HP

	Used:
• Periodically
• Or when Sustain HP drops below 20%

---

	3. Warden’s Call (Summon Skill)

	Summons spectral minions.

	Scaling:
• 1–2 players: 2 minions
• 3–4 players: 4 minions
• 5+ players: 6 minions

	Minions:
• Deal moderate damage
• Their hits generate Combo
• Their deaths normally do nothing…
	Except under Thunder of Inversion

---

	4. Thunder of Inversion (Storm Skill)

	Effect:
Summons storm pillars around battlefield.

	Special Mechanic:
• All damage dealt to minions while Thunder is active:
→ Converted into HP for the Warden
• Forces players to:
	* Stop cleaving
	* Or risk feeding the boss

	Also damages players and minions alike.

---

	5. Resonance Skill – 40 Combo

	Grasp of Echoes

	Effect:
• AoE pull toward the Warden
• Minor damage
• Drains resources from all pulled targets
• Adds to Sustain HP

---

	6. Resonance Skill – 60 Combo

	Wail of Hunger

	Effect:
• Cone attack
• Applies Fear + Slow
• Heavy HP drain
• Converts large amount into Sustain HP

---

	7. Resonance Skill – 80 Combo

	Spectral Ascension

	Effect:
• Warden becomes untargetable for 5 seconds
• Minions become empowered
• Sustain HP rapidly regenerates
• Combo decay is paused

---

	8. Resonance Skill – 100 Combo

	Dominion of the Void

	Ultimate Skill

	Effect:
• Massive AoE storm
• Heavy damage + full resource drain
• Converts huge amount into Sustain HP
• Resets Combo to 40 after use
• Visually fractures the air around the ledge

---

	8. PERFORMANCE-BASED DIFFICULTY

	This Elite adapts to player performance:

	If players:
• Burst too hard → Emergency siphons trigger
• AoE carelessly → Thunder feeds boss
• Ignore minions → Combo spikes rapidly
• Play slow → Sustain HP overwhelms them

---

