			The Ascendant Warden – Overwatch Ledge Elite

---

	1. BASIC INFORMATION

Monster Name: The Ascendant Warden
Rank: Elite
Classification: Spawner Elite / Vampiric Caster
Monster Type: Skillable
Race / Category: Ethereal Undead
Element: Shadow / Storm
Level Range: Scales with Zone 4 (High Tier Elite)
Aggression Type: Aggressive
Group Requirement: 2+ players mandatory for proper kill
Location: Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Luminous Ridge – Overwatch Ledge

	Spawn Type: Timed World Spawn
	Spawn Hours (Server Time):
• 06:00
• 12:00
• 17:00
• 23:00

	Despawn Rule:
If not engaged within 20 minutes of spawn, fades into wind and resonance.

---

	2. VISUAL & UI DESIGN

	Appearance:
A tall, floating spectral warrior composed of dark mist and storm-lit bone fragments.
Fragments of broken armor orbit its body slowly.

	Key Visual Indicators:
• Two HP bars visible:
* Primary HP (Red)
* Sustain HP (Purple/Shadow)
	• Combo Meter displayed under HP bars (Resonance Meter)
	• When absorbing HP or energy, veins glow bright violet
	• When Combo thresholds are reached, UI pulses and emits sound cue

	Environmental Presence: 
Wind currents bend around it.
Stone grooves on the ledge glow faintly when it is active.

---

	3. CORE ATTRIBUTES

Primary HP: Very High (Elite tier)
Sustain HP (Secondary Bar): Unlimited but decays slowly when not feeding
Defense: Medium
Magic Resistance: Medium–High
Physical Resistance: Medium
Mobility: Medium (Floating, not path-limited by terrain)
Attack Range: Long
Attack Speed: Moderate
Damage Type: Shadow + Storm
CC Resistance:
• Immune to Fear
• Resistant to Root & Slow
• Can be interrupted during channeling

---

	4. DUAL HP SYSTEM – VAMPIRIC CORE MECHANIC

	The Ascendant Warden has two health systems:

Primary HP:
• Standard boss HP
• Must be reduced to zero for kill

Sustain HP (Secondary Bar):
• Filled through vampiric effects
• Acts as an extra shield before Primary HP can be damaged
• Slowly decays when not actively feeding
• Can exceed 50% of Primary HP
• When Sustain HP is active, boss gains:

* +15% damage
* +10% movement speed
* Reduced CC vulnerability

Sustain HP is gained by:
• Draining players’ HP
• Draining mana/energy/stamina
• Damage dealt to its minions via Thunder ability
• Certain AoE skills

---

	5. COMBO SYSTEM – RESONANCE METER

The Ascendant Warden does not use mana.
Instead, it uses a Resonance Combo Meter (0–100).

	Combo is gained by:
• Successful attacks on players
• Minions landing hits
• Draining resources
• Taking heavy damage in short time (counter-pressure mechanic)

	Combo Thresholds:
40 Combo → First Resonance Skill
60 Combo → Second Resonance Skill
80 Combo → Third Resonance Skill
100 Combo → Ultimate Resonance Skill

	Combo does NOT reset fully — it drops gradually, forcing pressure-based combat.

---

	6. CORE COMBAT BEHAVIOR

This Elite is NOT a tank boss.
Its danger comes from:
• HP regeneration through vampirism
• Area control
• Resource denial
• Scaling difficulty based on player performance

	Against solo players:
• It becomes extremely difficult due to:
	* Regeneration
	* Resource drain
	* Minion feeding loops

