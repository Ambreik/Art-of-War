			Riftborn Mawlurker

---

		GENERAL INFORMATION

* Name : Riftborn Mawlurker
* Title : The Depthbound Devourer
* Classification : Elite Aquatic / Abyssal
* Type : Provoked Spawn
* Threat Level : High
* Zone : Zone 4 – Eastvale Crest
* Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Riftwell Chasm
* Spawn Type : Player-triggered only
* Respawn : 90 minutes global cooldown (per instance/channel)

---

		LORE

	The Riftborn Mawlurker is not a native surface predator, but a creature shaped by pressure, darkness, and mineral-infused waters beneath the island.
	It emerges only when the silence of the Riftwell Chasm is violently broken, responding to shockwaves as both threat and invitation.

	Its body carries crystalline mineral growths along its spine and jaw, shimmering faintly in mist, as if fused with the veins of the earth itself.

---

		SPAWN MECHANIC

	The Riftborn Mawlurker can only be summoned via:

	"Depthbreaker Provocation System"

	Trigger:
* Player throws Dynamite into the Riftwell Chasm

	Conditions:
* At least 1 Dynamite thrown
* Maximum effective dynamite: 52
* Spawn locked if monster was summoned within last 90 minutes

	Spawn Behavior:
* Erupts violently from the chasm
* Lands near the highest dynamite contributor
* Aggro priority:
	1. Highest dynamite contributor
	2. Nearby players
	3. Highest damage dealer

	Failsafe:
* If fewer than 3 players nearby:
	* Monster HP and damage reduced by 15%
	* Skill set remains intact

---

		SCALING SYSTEM (BASED ON DYNAMITE USED)

	1–5 Dynamite:
* Normal Elite Variant
* Base HP, base damage
* Limited skill rotation

	6–15 Dynamite:
* Enhanced Variant
* +20% HP
* Unlocks Mist Vein Drain

	16–30 Dynamite:
* Empowered Variant
* +35% HP
* Gains Lifesteal and crowd control effects

	31–52 Dynamite:
* Enraged Variant
* +50% HP
* Enrage Phase at 30% HP
* Full skillset active

---

		COMBAT PROFILE

* Combat Role : Area Controller / Sustained Threat
* Preferred Range : Melee with area denial
* Movement : Short lunges, water-assisted leaps
* Resistance : High resistance to water, poison, bleed
* Weakness : Fire, lightning, dry terrain debuffs

---

		CORE ABILITIES

	"Abyssal Surge" : The Mawlurker lunges forward in a straight line, dealing splash damage upon impact.

	Effects:
* Deals physical + water damage
* Knocks back targets hit
* Leaves a short-lived water slick that slows movement
Cooldown: Medium
Range: Medium
Area: Line impact + small AoE

---

	"Mist Vein Drain" : Drains vitality from players standing in mist zones.

	Effects:
* Deals damage per second to players inside mist
* Transfers 40% of damage dealt as HP to Mawlurker
* Mist zones expand slightly during the effect

Cooldown: Long
Range: Large radius
Special: Only unlocked when 6+ dynamite used

---

	"Chasm Howl" : The Mawlurker emits a resonant roar.

	Effects:
* Applies Fear for 2 seconds
* Reduces sound perception (temporary silence distortion)
* Interrupts casting and channeling

Cooldown: Long
Area: Large AoE
Utility: Disruption and crowd control

---

	"Abyssal Pull" : Pulls nearby enemies toward its core.

	Effects:
* Pulls players inward
* Applies brief root after pull
* Sets up for AoE follow-up

Cooldown: Medium
Area: Circular
Utility: Control and grouping

---

	"Enrage Phase" (30% HP) : Activated only in Enraged Variant (31–52 dynamite)

	Effects:
* +30% attack speed
* +25% movement speed
* All skills gain reduced cooldown
* Visual change: mineral veins glow violently

---

		PASSIVE TRAITS

	Depthbound Body : 
* Immune to knockdown
* Reduced effect from slows

	Mineral Fused Hide :
* Physical damage taken reduced by 10%
* Lightning damage taken increased by 15%

	Chasm Resilience : 
* HP regeneration increases while mist is present

---

		REWARD SYSTEM

	Drop quality scales with:
* Dynamite used
* Player contribution

	Possible Drops:
* Rift-scale Hide
* Veil-crystal Bone
* Abyssal Mineral Core
* Mist Condensate
* Rare chance: Core of Riftwell (used in Riftcaller Rod crafting)

	Gold:
* Medium-high gold drop

	Experience:
* High experience for elite

---

		ANTI-FARMING & ABUSE PREVENTION

* Spawn locked for 90 minutes after defeat
* Dynamite thrown during lock:
	* Consumed
	* No spawn occurs
* Repeated provocation attempts destabilize chasm:
	* No fishing possible for 30 minutes

---

		ENVIRONMENTAL INTERACTION

	While active:
* Chasm vibrations intensify
* Mist thickens
* Mineral veins glow faintly
* Fishing disabled until Mawlurker is defeated

