		Auralith, the Grotto Warden – Combat Abilities (Structured Format)

---

	Ability 1 – Resonant Slam
* Type : Area of Effect / Shockwave
* Trigger : Melee range or when 2+ players cluster
* Cooldown : Medium

	Effect :
Auralith slams the ground, releasing expanding shockwaves in all directions.

	Gameplay :
* Deals heavy physical + resonance damage
* Damage increases if the target is:
	* Sprinting
	* Dodging
	* Jumping
* Applies brief knockdown on unstable footing
* Breaks fragile terrain sections

	Synergy :
* Becomes more dangerous after terrain has fractured
* Combines with Seismic Pulse to punish mobility builds

---

	Ability 2 – Seismic Push
* Type : Crowd Control / Environmental Control
* Trigger : When players surround or flank
* Cooldown : Medium–Long

	Effect :
A violent resonance burst pushes all nearby players outward.

	Gameplay :
* Pushes players away from Auralith
* Can:
	* Throw players into water
	* Slam them into walls or debris
* Interrupts:
	* Casting
	* Channeling
	* Stealth entry

	Synergy :
* Used to reposition players into hazardous terrain
* Sets up follow-up Crystal Fracture or Resonant Slam

---

	Ability 3 – Echo Call
* Type : Summoning / Area Pressure
* Trigger : At 75% HP and 40% HP
* Cooldown : Long

	Effect :
Auralith awakens crystal-bound stone constructs from the cave walls.

	Gameplay :
* Summons multiple resonance constructs
* Each construct:
	* Pulses sound when destroyed
	* Emits minor AoE damage
* If constructs are ignored:
	* Auralith gains:
		* Increased resonance damage
		* Reduced cooldowns temporarily

	Synergy :
* Encourages target prioritization
* Punishes tunnel vision on boss

---

	Ability 4 – Crystal Fracture
* Type : Terrain Alteration / Zone Control
* Trigger : Random intervals or after Seismic Push
* Cooldown : Medium

	Effect :
Auralith shatters parts of the cave structure.

	Gameplay :
* Breaks:
	* Floor segments
	* Pillars
	* Walls
* Creates:
	* Gaps
	* Falling debris
	* Line-of-sight blockers
* Permanently alters arena for the duration of the fight

	Synergy :
* Makes later phases harder
* Changes safe zones dynamically

---

	Ability 5 – Seismic Pulse

* Type : Debuff / Control
* Trigger : When players gain advantage or over-damage
* Cooldown : Medium

	Effect :
A low-frequency vibration spreads through the chamber.

	Gameplay :
Applies debuff to all players:
* Reduced jump height
* Reduced dodge effectiveness
* Disrupts stealth briefly
* Slight casting instability

	Synergy :
* Makes positioning and timing critical
* Combines with Resonant Slam and Seismic Push

---

	Ability 6 – Resonant Suppression (Final Phase Mechanic)
* Type : Special Rule / Phase Mechanic
* Trigger : At 5% HP

	Effect :
Auralith becomes immune to solo damage.

	Gameplay :
* Only takes damage if:
	* At least 2 players hit him within a short time window
* Visual cue:
	* Crystal veins glow violently
	* Sound distortion fills the chamber
* If condition is not met:
	* Auralith regenerates back to 15%
	* Continues combat or resets

	Design Purpose :
* Enforces co-op organically
* Prevents solo exploitation
* Fits narrative as “resonance requiring multiple sources”

