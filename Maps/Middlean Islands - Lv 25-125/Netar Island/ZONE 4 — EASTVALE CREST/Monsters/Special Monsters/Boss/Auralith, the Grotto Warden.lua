			Auralith, the Grotto Warden

---

	Name : "Auralith, the Grotto Warden"

	Title : "The Resonant Sentinel of Whisperlight"

	Type : World Sub-Boss / Secret Location Guardian

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Sunken Grotto of Whisperlight Cove / Boss Chamber: "The Resonant Hollow"

---

	Description : 
* Auralith is an ancient cave-bound entity formed from stone, crystal, and resonance itself.
* It exists as both guardian and consequence — awakened whenever the balance of the grotto is disturbed by light, vibration, or repeated intrusion.
* Its body is fused with glowing mineral seams that pulse with seismic rhythm.
* Every movement it makes is echoed through the cavern, shaping the battlefield dynamically.

---

		Encounter Requirements

	Minimum Player Requirement

* The encounter can be initiated solo, but:
	* Boss cannot be killed by a single player
	* Final 5% HP must be dealt by at least 2 players simultaneously
* If only one player is present at 5%:
	* Auralith becomes immune
	* He mocks the player
	* Resets after a short duration

---

		Core Mechanics

	Resonance-Based Combat : 

Auralith does not fight with brute force alone — it controls sound, vibration, and terrain.
The battlefield is constantly shifting based on resonance intensity.

---

		Ability Set

	1. Resonant Slam

* Type : AoE / Shockwave
Auralith slams the ground, sending expanding shockwaves outward.

* Deals high AoE damage
* Damage increases if players are moving when hit
* Can knock down unstable players

---

	2. Seismic Push

* Type : AoE / Crowd Control
Auralith releases a violent resonance burst.

* Pushes all players away from him
* Can throw players into broken terrain or water
* Interrupts casting and channeling

---

	3. Echo Call

* Type : Summon / Area Control
Auralith awakens mineral constructs from the cave walls.

* Summons crystal-bound stone entities
* They emit sound pulses when destroyed
* If ignored:
	* They empower Auralith’s next abilities

---

	4. Crystal Fracture

* Type : Terrain Alteration
Shatters sections of the cave floor or walls.

* Creates:
	* Gaps
	* Falling debris zones
	* New line-of-sight breaks
* Permanently alters arena layout during the fight

---

	5. Seismic Pulse

* Type : Debuff / Control
A low-frequency vibration spreads through the chamber.

* Reduces:
	* Jump height
	* Dodge effectiveness
* Temporarily disrupts stealth
* Makes precise movement harder

---

	6. Resonant Suppression (5% Phase Mechanic)

When Auralith reaches 5% HP :
* Becomes immune to damage from single players
* Only takes damage if:
	* At least 2 players strike him within a short window
* Visual cue:
	* Crystal seams glow violently
	* Sound distortion fills the chamber

---

	Anti-Solo Fail State

If only one player is present during the final phase:
* Auralith stops taking damage
* He taunts the player
* After a short time:
	* He restores to 15% HP
	* Fight continues or resets

---

		Behavior System

	Aggro Style

* Prioritizes:
	1. Closest player during resonance surges
	2. Loudest actions (AoE, explosions, high-impact skills)
* Responds aggressively to:
	* Repeated movement
	* Excessive casting
	* Jumping during Seismic Pulse

---

	Audio & Environmental Integration

* Footsteps cause echoing ripples in sound
* Each ability has distinct resonance tones
* Cave visually vibrates during heavy skills
* Sound becomes a gameplay indicator

