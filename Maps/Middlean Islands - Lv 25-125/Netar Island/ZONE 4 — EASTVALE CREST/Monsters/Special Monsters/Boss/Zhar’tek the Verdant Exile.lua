			Zhar’tek the Verdant Exile

	Type :
Boss Monster
Ritual-Gated
Group Encounter (Non-Solo)

	Classification : 
Tribal Reptilian Warlord
Bipedal
Ritual Guardian

	Location : Middlean Islands → Netar Island → Zone 4 Eastvale Crest → Silentshade Hollow


	Visibility : 
Hidden
Only appears through ritual summoning

	Respawn : 
Instance-based
Cooldown: 8 hours
Only one active instance per zone shard

---

		Access Requirements

	Required Items : 

1. "Obsidian Seal of Binding"
	Dropped by: Boss in Tomb of Great Crusaders
	Use: Inserted into the wall indentation in Silentshade Hollow

2. "Parchment of Silent Claim"
	Dropped by: Elite / Mini-boss monsters in Tomb of Great Crusaders
	Use: Burned in ritual fire
	Effect:
	Only players who burn this parchment:
	✔ Can damage Zhar’tek
	✔ Can receive loot
   ✖ Others may be damaged but cannot deal damage nor loot
	
---

	Summoning Procedure : 

1. Player inserts "Obsidian Seal of Binding" into wall
2. Blue ritual fire spawns in center of cave
3. Cave entrance seals
4. Each player must burn "Parchment of Silent Claim"
5. Ritual fire extinguishes
6. Zhar’tek and minions spawn
7. Arena remains sealed until boss is defeated or all players die

---

		Combat Role : 

	Multi-Role Boss
• Requires tank(s)
• Requires healers
• Requires damage dealers
• Requires coordinated target prioritization

	Not designed for solo play

---

	Health Structure : 
• Primary HP Pool
• No regeneration outside combat
• Gains shields from healers
• Damage reduction active while tank minions live
• Enters special states based on statue system

---

		Core Combat Mechanics

	Damage Immunity Rule : 
Only players who burned "Parchment of Silent Claim" may damage Zhar’tek.
	Others:
• Take damage normally
• Cannot deal damage
• Cannot receive loot

---

		Boss Phases

---

	Phase 1 — Ritual Guardian

Active from 100% → 50% HP
Focus: Control, positioning, minion management

	Abilities:

"Verdant Cleave"
• Wide frontal arc
• Applies stacking armor reduction
• Forces tank swaps or movement

"Scaled Retribution"
• Reflects a % of damage received for 5s
• Triggers after heavy burst

"Emerald Shockwave"
• AoE knockback
• Silences players for 2s
• Used to disrupt ranged stacking

"Rite of Dominion"
• Marks a random player
• After 5s spawns tank minions near target

---

	Phase 2 — Blood of the Hollow

Triggers at 50% HP
Changes:
• Attack speed increased
• AoE frequency increased
• Healers strengthen
• Boss periodically drains HP from minions

	New Ability:
"Verdant Drain"
• Channels on minions
• Transfers their HP to boss
• Encourages killing minions first

---

	Phase 3 — Statue-Influenced Phase

Behavior depends on remaining statues (see below).

---

		Minions

	Tank Minions — Hollowward Sentinels (2)

Role: Damage absorption and control

	Abilities:

"Stonebound Guard"
• Shares a % of damage taken with Zhar’tek

"Unyielding Slam"
• Frontal stun cone

"Vigilant Stand"
• If both alive: Zhar’tek takes reduced damage

---

	Healer Minions — Verdant Channelers (3)

Spawn Points:
Fixed ritual locations around arena

	Abilities:

"Primal Mend"
• Strong single-target heal

"Spirit Link"
• Redirects damage from boss to themselves

"Life Bloom Totem"
• AoE healing over time

---

		Statue System : 

	Three statues spawn when boss appears.

---

	Statue of Ruin (Red)

	Effect while active:
• Increases Zhar’tek’s damage output
	Destroyed:
✔ Reduces boss damage
✔ Makes fight safer

---

	Statue of Renewal (Yellow)

	Effect while active:
• Increases healing from Verdant Channelers
	Destroyed:
✔ Weakens healers
✔ Reduces boss regeneration

---

	Statue of Balance (Orange)

	Effect while active:
• Prevents Berserk Mode
• Dampens boss’s final damage spikes
	Destroyed:
❌ Zhar’tek enters Berserk Mode at 30% HP
✔ Drop quality increases significantly

---

	Berserk Mode (Only if Orange Statue Destroyed)

Triggers at 30% HP

	Effects: "Savage Overlord"
• +40% damage
• Faster abilities
• Additional AoE pulses
• Tank minions revive once
• Healers cast at double speed
• Zhar’tek becomes immune to CC

	Purpose: Risk vs reward mechanic

---

		Loot Rules : 

	Only players who burned "Parchment of Silent Claim" are eligible.

	Standard Loot (Orange Statue Intact)
• Rare gear
• Ritual materials
• Zone crafting components
• Tomb-related items

	Enhanced Loot (Orange Statue Destroyed)
• High-tier crafting materials
• Unique relics
• Legendary component chance
• Ritual artifacts

---

		Reset Conditions : 

	If all players die:
• Boss despawns
• All items consumed
• Cave opens after cooldown
• Statues reset
• No loot granted

