		ENVIRONMENTAL SPAWN SYSTEM – The Brinebound Maw


	Spawn Zone : The Shattered Line (Wreck Cluster – offshore debris zone)

	1. Spawn Conditions : 

	The Brinebound Maw can only spawn if at least one of the following is active:
✔ Low Tide Phase
✔ Heavy Fog Weather State

	If both occur simultaneously, spawn chance is increased.

---

	2. Spawn Chance & Frequency : 

| Condition                 | Spawn Chance        |
| ------------------------- | ------------------- |
| Low Tide only             | 6–8% per tide cycle |
| Heavy Fog only            | 5–7% per fog cycle  |
| Low Tide + Heavy Fog      | 12–15%              |
| Clear weather / High tide | 0% (cannot spawn)   |

	Maximum Active Instances : Only one Brinebound Maw per island instance at a time.

---

	3. Respawn Logic

	After being defeated:

* Minimum Respawn Cooldown : 2 real-time hours
* Maximum Respawn Delay : 6 real-time hours
* Cooldown only progresses during valid spawn conditions (Low Tide / Fog)

	This ensures:
✔ No constant farming
✔ Feels rare and event-like
✔ Encourages exploration rather than camping

---

	4. Spawn Triggers

	The Maw does not spawn instantly when conditions are met.
	It is triggered by:
• Player presence near wreckage zones
• Prolonged activity around debris clusters
• Combat, looting, or movement disturbing the area

	This creates a sense that the creature is:

> “Drawn by noise and intrusion rather than simply appearing.”

---

	5. Anti-Farm Protections

	To prevent abuse:
✔ Cannot spawn twice within the same real-time tide cycle
✔ Loot quality slightly reduced if killed repeatedly within short periods
✔ Spawn location varies between multiple wreck sub-clusters
✔ Will retreat/despawn if no players remain nearby for extended time

