			The Brinebound Maw

	Location : Middlean Islands / Netar Island / Zone 4 — Seastrand Mirror / The Shattered Line

	Category : Rare World Creature (Apex Coastal Threat)

	Spawn Conditions :
* Spawns only during "Low Tide" OR "Heavy Fog"
* Appears among wreckage and debris
* Extremely rare; typically only one per instance of The Shattered Line

---

	Appearance : The Brinebound Maw is a massive,ancient crocodilian beast, overgrown with barnacles, driftwood, and seaweed that cling to its cracked, scaled hide.
Its eyes gleam with cold yellow intelligence, and its jaws are rimmed with razor-cut salt crystals fused into the bone.
	Its body is broad and muscular, built for power and ambush rather than speed. When it moves through shallow water or wreckage, the sound is deep, heavy, and 
foreboding — an unsettling rhythm that precedes its presence.

---

	Lore : Old salt-born mariners spoke of a beast that devoured tides as if they were prey. They claimed the sea spat it out centuries ago, a horror forged by 
storms, drowned souls, and time itself.
	Now, in the wreck-strewn shallows of The Shattered Line, the Brinebound Maw claims dominion. When the tide retreats and fog crawls ashore, it rises — drawn by 
the scent of surf, bone, and fresh blood.

---

		Behaviour : 

	AI Type :
Stealthy Ambush Predator / Territorial Apex

	Aggression :
Extreme when provoked or defending territory; otherwise silent and hidden.

	Movement :
Lurks in shallow water and debris, moving slowly until provoked.
Once aggroed: bursts of terrifying speed in short charges or lunges.

	Targeting :
Prioritizes:
1. Players near water’s edge
2. Small creatures and animals
3. Loot carriers
4. Other rare creatures

---

	Role : 
* Role : Rare Predator Boss / Environmental Apex
* Threat Level : High
* Purpose :
	* Deters reckless exploration
	* Creates high-risk, high-reward gameplay
	* Unique rare loot sources

---

		Mechanics : 

	Predatory Ambush : 
* Becomes active only when players approach wreck debris within a certain radius.
* Uses **shallow water camouflage** – almost invisible until it strikes.

	Water Warp Charge : 
* Lunges forward through water or wreckage with violent speed.
* Cannot be easily dodged without terrain awareness.
* Leaves a shockwave ripple on hit, knocking nearby players off balance.

	Debris Crush : 
* Smashes claws down, displacing wreckage and sharp metal.
* Causes area bleeding if players are near debris.

	Fog Cloak (Passive) : 
* In heavy fog or low visibility states, its outline fades.
* Reflection distortions on mirror sand reduce detection further.

