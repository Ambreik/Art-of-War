			Resonant Wraiths – Ascendant Warden Minions

---

	BASIC INFORMATION

Name: Resonant Wraith
Rank: Minion
Linked Elite: The Ascendant Warden
Classification: Spectral Servant
Type: Skillable
Race: Ethereal Undead
Element: Shadow / Storm
Aggression: Aggressive
Spawn Method: Summoned only by The Ascendant Warden
Despawn Rule: Despawn on Warden death or reset

---

	APPEARANCE

Floating distorted humanoid silhouettes made of wind, shadow, and crackling storm light.
They leave faint trails in the air as they move.

---

	CORE ATTRIBUTES

HP: Low
Damage: Moderate
Defense: Very Low
Movement Speed: High
Attack Type: Melee / Short-range Shadow
Attack Speed: Fast
CC Resistance: Low

	They are designed to be easy to kill, but dangerous when ignored.

---

	MECHANICS

	1. Resonance Feed (Passive)

	Every successful hit by a Resonant Wraith:
• Generates Combo for The Ascendant Warden
• Restores a small amount of Sustain HP to the Warden
• Applies minor resource drain to the player

	This makes minions a direct extension of the boss, not separate threats.

---

	2. Thunder Inversion Interaction

	When "Thunder of Inversion" is active:
• Any damage dealt to Resonant Wraiths
	→ Is converted into HP for the Ascendant Warden
• Killing them during this state is actively harmful

	This forces players to:
✔ Stop AoE
✔ Switch targets
✔ Or wait out the storm phase

---

	3. Storm Lash (Basic Attack)

	Shadow + Storm melee strike that:
• Deals moderate damage
• Applies a brief Slow
• Drains a small amount of mana/energy/stamina

---

	4. Death Effect – Shattered Resonance

	On death (outside Thunder phase):
• Releases a small resonance pulse
• Deals minor AoE damage
• Slightly reduces Warden’s Combo meter

	This rewards controlled killing at the right time.

---

		AI BEHAVIOR

• Aggressively targets:
1. Healers
2. Casters
3. Lowest defense targets
	• Avoid tanks when possible
	• Attempts to surround players
	• Repositions often to avoid predictable cleaving

---

	SCALING RULES

	The number of Resonant Wraiths summoned scales based on:
• Player count
• Damage output
• Fight duration

	This ensures:
✔ More pressure on strong groups
✔ Survival challenge for smaller groups
✔ No static difficulty

---

	QUOTES (Audio Whispers)

	Occasionally whispered when near:
• “Resonance… feeds…”
• “You bleed for the storm…”
• “Echo… echo…”

---

	PURPOSE IN FIGHT DESIGN

	Resonant Wraiths exist to:
✔ Feed the Warden’s Sustain HP
✔ Accelerate Combo generation
✔ Punish careless AoE
✔ Force target prioritization
✔ Enforce phase awareness
✔ Prevent brute-force bursting

	They are not meant to overwhelm by themselves, but to make the Warden impossible to brute-force.

