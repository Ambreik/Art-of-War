			Zhar’tek the Verdant Exile - Minions

---

		Hollowward Sentinel

	Type : Elite Minion / Tank Role

	Linked Boss : Zhar’tek the Verdant Exile

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Silentshade Hollow
	Quantity : 2 (Always)
	Role : Damage Absorber / Boss Protector

	Behavior : 

* Aggressive
* Prioritizes players attacking Zhar’tek
* Maintains proximity to boss

		Core Mechanics : 

	Stonebound Guard (Passive)
* Shares a percentage of damage taken with Zhar’tek
* While both Sentinels are alive:
	* Zhar’tek receives reduced damage

	"Unyielding Slam"
* Frontal cone attack
* Applies stun and knockback
* Used to disrupt tanks and melee DPS

	"Vigilant Stand"
* If one Sentinel falls:
	* The remaining one gains increased defense and threat generation

	"Death Effect"
* Upon death:
	* Zhar’tek loses damage mitigation
	* Boss takes increased damage for a short duration

	Notes : 
* Must be killed before focusing full damage on Zhar’tek
* Ignoring them heavily prolongs the fight

