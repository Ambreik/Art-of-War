		Verdant Channeler

	Type : Elite Minion / Healer Role

	Linked Boss : Zhar’tek the Verdant Exile

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Silentshade Hollow
	Quantity : 3 (Always)
	Role : Healing / Sustain / Damage Redirection
	
	Behavior : 

* Stationary
* Casts continuously
* Does not chase players

		Core Mechanics : 

	"Primal Mend"
* Powerful single-target heal
* Prioritizes Zhar’tek
* Can target Sentinels

	"Spirit Link"
* Redirects a portion of damage taken by Zhar’tek to the Channeler
* Forces players to split damage properly

	"Life Bloom Totem"
* Periodically summons a healing zone
* Heals all enemies standing inside

	Interaction with Statues
* While Yellow Statue is active:
	*  Healing power increased
	*  Totems persist longer
* If Yellow Statue destroyed:
	*  Channelers lose healing amplification
	*  Totems weaker

	"Death Effect"
* Each slain Channeler:
	*  Reduces Zhar’tek’s regeneration rate
	*  Increases vulnerability window

	Notes

High-priority targets
Left alive, they can make the boss nearly unkillable

