	Windrazor Gargoyles : 
* Type : Living Stone Flyers
* Role : Ambush Predators

	Appearance : 
* Statues fused into cliff walls
* Wake when caravans pass
* Stone wings with jagged edges
* Blue frost-glow eyes

	Behavior : 
* Remain inert until triggered
* Drop directly onto targets
* Shatter on death into terrain hazards

	Permanent Spawn Locations :
* Windrazor cliff faces
* Overhanging rock bridges
* Narrow choke corridors

	Event Role:
* Sudden high-damage bursts
* Force players to watch terrain, not just enemies

