	Broodspine Forest Spiders

* Type : Large Arachnids
* Signature Trait : Spawnlings

	Behavior : 
* Stationary ambush
* Upon death:
	* Releases spiderlings
* Forces AoE and positioning play

	Locations :
* Forest edge north of Sunveil Plains
* Deep brush before Crestfall Ridges
* Woodland border near ZONE 5
--------
	Cliffshade Smugglers

* Type : Rogue Humanoids
* Role : Opportunistic ambushers

	Behavior : 
* Avoid direct confrontation
* Use traps, smoke bombs, ranged weapons
* Target:
	* Cargo wagons
	* Stragglers
	* Isolated healers

	Variants :
* Smuggler Cutthroat (melee)
* Smuggler Sharpshot (ranged)
* Smuggler Saboteur (explosives/traps)

	Locations :
* Between cliff passes near Crestfall Ridges
* Hidden camps between Sunveil Plains & Windrazor Pass
* Forest-cliff transition zones

	Reusable in:
* ZONE 4 cliffs
* ZONE 5 passes
-------
	Dunehorn Grazers

* Type : Plains Beasts (Herbivores)

	Appearance : 
* Tall, horned quadrupeds
* Long necks
* Sandy fur tones

	Behavior : 
* Non-hostile
* Stampede when frightened
* Can accidentally disrupt caravan formation

	Locations :
* Open plains west and south of Sunveil Town
* Borderlands between plains and ridges
-----
	Bristleback River-Hippos

* Type : Massive Semi-Territorial Beasts

	(Not classical hippos — brutal fantasy variants)

	Appearance : 
* Thick, armored hide
* Spiked ridges along back
* Short tusks and powerful jaws

	Behavior : 
* Neutral unless approached
* Will aggressively defend water territory
* Can flip wagons or knock caravans

	Locations :
* Wide river crossings near Sunveil Plains
* Lowland lakes between ZONE 4 & ZONE 1
----
	Mirejaw Crocodiles

* Type : Semi-Aquatic Predators
* Role : Ambush predators in low water areas

	Behavior : 
* Lurk in shallow river bends and marsh pools
* Burst attack from water
* Will drag players or NPCs briefly

	Locations :
* River east of Sunveil Market Town
* Marshlands along road to Crestfall Ridges
* Flooded lowlands south-east of town

	Reusable for :
* ZONE 4 wetlands
* ZONE 1 river deltas
