			Crusader Invasion – Monster Stats & Abilities

---

		1. Berserk Crusader Leader

	Level : 77
	Role : Raid boss-style leader for event-triggered invasion
	Weapon : Massive 2-handed axe
	Behavior : Aggressive melee, prioritizes players using environmental mechanics (balloons, artillery), interacts dynamically with terraces, stairs, and water

	Stats :
* HP: Very high (~6000–7000)
* Melee Damage: High (~150–200 per hit)
* Movement Speed: Medium (can burst to high during skill activation)
* Defense: High (reduces incoming damage by ~25%)
* Aggro Radius: Large (targets any player/NPC within 25 meters)

	Abilities :
1. Cleave Swing (Melee AoE) :
	* Sweeps 2–3 targets in front of leader
	* Damage: 120–160
	* Knockback: Minor push (~2 meters)
	* Cooldown: 8 seconds

2. Ground Slam (Jump AoE) :
	* Jumps into air, lands with a shockwave radius of 6 meters
	* Damage: 150–200 to all in radius
	* Knockback: Strong (~4 meters)
	* Applies minor stun (~1–1.5s)
	* Cooldown: 20 seconds

3. Battle Roar (Fear / Aggro) :
	* Fear effect on NPCs for 2–3 seconds (stagger)
	* Increases attack speed of nearby Soldiers and Archers (+15%)
	* Cooldown: 25 seconds

4. Axe Slam Push (Directional AoE) :
	* Pushes players/NPCs in a 3-meter frontal cone
	* Damage: 100–120
	* Knockback: 3–4 meters
	* Cooldown: 12 seconds

	Environmental Interaction :

* Stairs/Terraces : May jump or slam from upper terraces for tactical advantage
* Crowd Dynamics : Uses fleeing NPCs as temporary cover or to push players
* Water : Wet surfaces reduce movement slightly but leader unaffected
* Balloon/Artillery : Aggro prioritizes players using fire attacks
* Fog / Shimmer : Slightly reduces ranged targeting from Archers but melee unaffected

---

		2. Crusader Soldiers

	Level : 77 (same as leader for coherence)
	Role : Melee frontline, follow leader, swarm players/NPCs

	Stats :
* HP: Moderate (~1200–1500)
* Melee Damage: Medium (~60–80 per hit)
* Movement Speed: Medium
* Defense: Medium (reduces incoming damage ~15%)
* Aggro Radius: Medium (~15 meters)

	Abilities :
* Slash : Standard melee attack (~60–80)
* Shield Bash (if using shields) : Minor stun (~0.5–1s)
* Pack Coordination : Gains +10% attack speed if 3+ nearby Soldiers are alive

	Environmental Interaction :
* Stairs/Terraces : Reduced speed while climbing
* Crowd : Can knock over fleeing NPCs, creating obstacles
* Water : Slip chance (~5–10%), slowing next attack
* Guard AI / Player Mechanics : React to balloons or artillery by changing paths

---

		3. Crusader Archers

	Level : 55 (weaker than Soldiers for ranged harassment)
	Role : Ranged support, pressure players from walls, terraces, or behind cover

	Stats :
* HP: Low (~800–1000)
* Ranged Damage: Medium (~40–60 per hit)
* Movement Speed: Medium
* Defense: Low (~5–10% reduction)
* Aggro Radius: Medium (~15 meters)

	Abilities :
* Arrow Shot : Standard ranged attack (~40–60)
* Volley : Fires 3 arrows in a cone (AOE ~3 meters)
* Poison Arrow (optional) : Reduces player movement speed by 5–10% for 5s

	Environmental Interaction :
* Fog / Mist : Accuracy reduced 15–20%
* Light / Heat Shimmer : Visual distortion can cause misfire
* High Terraces / Stairs : Gains minor accuracy bonus if firing from high-ground
* Crowd Dynamics : Civilians may block line-of-sight, forcing repositioning

---

		Spawn Behavior : 

* Leader spawns first at town entrance
* Soldiers & Archers spawn in waves around leader (15–20 sec intervals)
* Continuous spawn while leader alive; stops when leader dies
* Scaling : Number of soldiers/archers increases with player count (>20 players)

