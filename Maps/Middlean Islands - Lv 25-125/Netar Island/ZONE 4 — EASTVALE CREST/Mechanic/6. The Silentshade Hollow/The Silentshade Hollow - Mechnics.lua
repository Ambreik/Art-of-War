			The Silentshade Hollow - Mechnics

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Silentshade Hollow

---

	Boss: "Zhar’tek the Verdant Exile"

(Type: Tribal Reptilian Warlord, Bipedal)

A bipedal reptilian entity clad in tribal armor and bone ornaments, infused with primal ritual magic.
Designed for group play only.
Cannot be soloed.

---

		1. Access & Summoning Mechanic

	Ritual Item Required (Inserted into Wall)

	Item Name : "Obsidian Seal of Binding"
Dropped by: A boss in the "Tomb of Great Crusaders"
Type: Ritual Artifact
Use: Inserted into the circular wall indentation to activate the ritual.

---

	Activation Sequence

1. A player inserts "Obsidian Seal of Binding" into the wall.
2. A blue ritual fire appears in the center of the cave.
3. The cave entrance seals.
4. All players must burn a ritual paper in the flame.

---

	Ritual Paper

	Item Name : "Parchment of Silent Claim"
Dropped by: Elite enemies in Tomb of Great Crusaders
Use: Burned in the ritual fire
	Effect:
Only players who burn this parchment can:
✔ Damage the boss
✔ Receive loot
✖ Others can be damaged but cannot deal damage nor loot

	This enforces:
• Participation requirement
• Prevents abuse
• Forces preparation

---

		2. Boss Encounter Structure

	Once all papers are burned:
• Fire extinguishes
• Boss materializes
• Healers, tanks, and statues spawn simultaneously
• Arena remains sealed until:
✔ Boss defeated
✖ Or all players die

---

		Zhar’tek the Verdant Exile – Core Mechanics

---

	Health Structure : 
* Main HP Pool
* Enters empowered states based on statue status
* Gains temporary shields from healers
* Cannot be burst-killed

---

		Combat Phases

	Phase 1 – Ritual Guardian

Zhar’tek fights defensively, supported by healers and tank minions.

	Abilities:

• "Verdant Cleave" : Wide frontal attack, applies stacking armor shred

• "Scaled Retribution" : Reflects % of damage taken for 5s

• "Emerald Shockwave" : AoE knockback + silence

• "Rite of Dominion" : Marks a player → after 5s summons tank minions near that player

---

	Phase 2 – Blood of the Hollow (50% HP)

Boss becomes aggressive.
• Attack speed increased
• AoE frequency increased
• Healers gain stronger heals
• Boss periodically drains HP from minions

---

	Phase 3 – Choice Phase (Statue Influence)

Triggered dynamically based on which statues remain.
Details below.

---

	3. Minions

	Tank Minions – “Hollowward Sentinels”

Two spawn initially.

	Role:
• Absorb damage
• Body-block players
• Intercept ranged attacks

	Abilities:
• "Stonebound Guard" – Shares damage with Zhar’tek
• "Unyielding Slam" – Stuns players in frontal arc
• "Vigilant Stand" – If both are alive, boss takes reduced damage

	These MUST be managed or separated.

---

	Healer Minions – “Verdant Channelers”

	3 spawn in fixed ritual points.

	Abilities:
• "Primal Mend" – Strong heal on boss or tanks
• "Spirit Link" – Redirects damage from Zhar’tek
• "Life Bloom Totem" – Area heal over time

	Players must:
✔ Interrupt
✔ Focus
✔ Control positioning

---

	4. Statue System (Critical)

	Three statues appear around the arena.

* Red Statue – "Statue of Ruin"
Effect while active: Increases boss damage

	Destroying it:
✔ Reduces boss damage output
✔ Makes fight safer

---

* Yellow Statue – "Statue of Renewal"
Effect while active: Increases healing done by healers

	Destroying it:
✔ Weakens healer minions
✔ Reduces boss regeneration

---

* Orange Statue – "Statue of Balance" (The Choice)
Effect while active:
• Prevents Zhar’tek from entering Berserk Mode
• Reduces his final phase damage spikes

	Destroying it:
❌ Zhar’tek enters Berserk Mode at 30% HP:
• +40% damage
• Faster abilities
• Additional AoE
✔ Increases drop quality drastically

	This creates a risk vs reward system.

---

	5. Berserk Mode (Only if Orange Destroyed)

	At 30% HP:
• Savage Overlord
* Massive AoE pulses
* Tank minions revive once
* Healers cast double speed
	* No more crowd control possible on boss
	* Hard DPS and healing check

---

	6. Drops & Rewards Logic

	If Orange Statue is NOT destroyed:
• Standard rare loot
• Crafting materials
• Ritual items
• Zone-appropriate gear

	If Orange Statue IS destroyed:
• High-tier drops
• Unique materials
• Chance for:
• Legendary crafting component
• Ritual artifacts
• Zone-exclusive relics

	Loot only rolls for:
✔ Players who burned Parchment

---

	7. Reset Conditions

If all players die:
• Boss despawns
• Items are consumed
• Statues reset
• Cave reopens after cooldown

Cooldown: 8 hours per instance

---

	8. Hint NPC – Access & Guidance

	NPC: "Eremor the Stonebound"

Location: Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Luminous Ridge – lower terraces near Silentshade Hollow

Type: Wandering Scholar / Ex-Ritualist

	Functions:
• Explains existence of Silentshade Hollow
• Gives hints about:
✔ The Obsidian Seal
✔ Ritual Papers
✔ The statues
✔ The Orange statue risk
• Does NOT reveal exact drops

	Dialogue reveals:
• The ritual
• The sealing
• The role of burning parchments
• That only the “claimed” may strike the Verdant Exile

	He does NOT give items — only knowledge.

