		Secret Location Mechanics

	The Hidden Archive of Felgrace Shelf

---

		MECHANIC 1 — Concealed Access & Vertical Descent System

		1.1 Discovery Method

This location is not visible on the world map by default.

	It can be revealed through:
* A quest from "Field Marshal – Ravenwatch Encampment (Zone 2)" directing players to investigate abnormal terrain on Felgrace Shelf
* High Perception / Exploration skills detecting:
	* Broken vegetation patterns
	* Slight geometric stone edges among natural rock
* Environmental interaction: clearing vines, shifting rubble, or collapsing false stone plates

Once discovered, the entrance becomes permanently marked for the discovering player/account.

---

	1.2 Entry Mechanics

The entrance is a collapsed rectangular breach partially hidden by vegetation.

	Players must:
* Crouch or crawl to enter
* Certain armor types or mounts are blocked from entering
* Summoned large companions are dismissed or resummoned after entry

	Entry locks the player inside the sub-area until an exit condition is met (see Mechanic 1.5).

---

	1.3 Vertical Sinkhole / Fountain Descent System

The interior structure functions like a vertical excavation well, not a normal dungeon room.

	Movement is handled via:
* A circular descending shaft (the “fountain”)
* Spiral ledges carved manually by Aurelian Thorne
* Anchoring points in stone used for rope, chain, or climbing

	Players can descend using:
* Manual climbing (slow, stamina-based)
* Rope deployment (consumable or skill-based)
* Controlled fall points (short drops with fall-damage scaling)
* Class-based traversal (grappling, blink, levitate, etc.)

	Descending too quickly without support risks:
* Stagger
* Injury
* Alerting hidden mechanics in deeper layers

---

	1.4 Ascending & Exit Mechanics

	Players can exit by:
* Climbing back the same way
* Using mobility skills
* Or activating the "Pillar Recall Interaction" (see Mechanic 2.4)

	If a player is incapacitated inside:
* They respawn at the top entrance
* With durability damage and a debuff: "Crushed by Stone Memory" (reduced stamina regen for X minutes)

---

	1.5 Lock-In Behavior

	Once inside:
* Teleportation is disabled
* Hearthstones, portals, recalls are blocked
* The only exit methods are:
	* Physical ascent
	* Pillar Recall
	* Or group-assisted extraction (ropes, pull skills, etc.)

This reinforces the “sealed refuge” identity of the location.

---

		MECHANIC 2 — The Pillar of Failed Resurrection

The central pillar is a **unique interactive system**, not a cosmetic object.

---

	2.1 Pillar Structure & Visual Logic

	The pillar is covered in:
* Concentric engraved rings
* Runic fragments
* Interrupted incantation sequences

	By design and interpretation:
* The drawings indicate the Banshee Queen attempted to resurrect Aurelian Thorne here
* The pillar was either created or altered by her magic
* The ritual failed

	Environmental confirmation:
* A skeleton found near the wall:
	* Very old
	* Headless
	* A sword beside it
* Suggests Aurelian chose death before corruption or was partially reclaimed

---

	2.2 Pillar Interaction Phases

The pillar has three functional states, unlocked progressively.

---

	Phase I — Observation & Decoding

Available to all players upon first visit.

	Players can:
* Inspect runes
* Rotate concentric rings
* Activate “Read Pattern” interactions

	This grants:
* Lore entries about the Banshee Queen’s ritual
* Partial understanding of failed resurrection magic
* Unlocks Phase II

No combat or buffs occur in this phase.

---

	Phase II — Incantation Attempt (Player-Driven Ritual)

Players can attempt to replicate or disrupt the ancient ritual.

	This requires:
* Specific reagents (e.g. bone dust, spectral residue, cursed ink, soul fragments)
* Or class-based inputs (Necromancy, Light, Arcane, Shadow, etc.)

	Possible outcomes:
* Failed incantation → environmental backlash (fear, silence, stamina drain)
* Partial success → temporary buffs or debuffs tied to undead, memory, or resistance
* Corruption attempt → summons hostile remnants or spectral anomalies

This phase cannot resurrect Aurelian Thorne, only replicate fragments of the ritual effect.

---

	Phase III — Banshee Interference Event (Rare Trigger)

	Triggered when:
* Players repeatedly activate Phase II
* Or combine specific rare reagents
* Or fulfill hidden conditions tied to undead crusader lore

	Effects:
* The pillar emits a banshee resonance
* The area becomes partially phased
* Spectral echoes of Aurelian and/or the Banshee Queen manifest
* New lore, encounters, or future quest flags are unlocked

	This does NOT spawn the Banshee Queen herself — only traces or projections.

---

	2.3 Skeleton of Aurelian Thorne – World Anchor

The skeleton is not a loot object.

	It functions as:
* A world anchor preventing full resurrection
* A stabilizer against corruption of the location
* A narrative object that players cannot remove or destroy

	Interactions:
* Pay respects → grants small resistance vs fear/undead
* Desecrate → spawns hostile remnants or curses the player
* Ritual focus → increases Phase II power but raises corruption risk

---

	2.4 Pillar Recall (Emergency Exit Mechanic)

After interacting with the pillar at least once, players unlock:

	Pillar Recall : 
* A once-per-visit activation
* Teleports players safely to the entrance ledge
* Leaves a temporary exhaustion debuff
* Disabled during ritual attempts or active events

	This serves as:
* A controlled escape
* A design-safe fallback for players who misjudge descent difficulty

---

		MECHANIC 3 — Wall Story as a Living System

The engraved story is not static lore.

	It functions as:

* A progressive reading system
* With parts unlocking as:
	* Player explores deeper
	* Interacts with the pillar
	* Or triggers Phase II/III

	Advanced interactions:
* Some text changes based on world state (e.g. after Crusader dungeon is cleared)
* Certain inscriptions react to undead kills in the region
* Some lines glow faintly when Banshee-related content is active in the zone

---

		MECHANIC 4 — Environmental Threat & Stability

This is not a combat-heavy location, but it is not safe.

	Threats include:
* Structural instability (rockfalls if players rush or misuse abilities)
* Spectral pressure (sanity/mana drain over time)
* Ritual backlash effects
* Limited vision and sound distortion

	The longer players remain:
* The harder it becomes to safely perform Phase II
* The greater the chance of triggering Phase III unintentionally

