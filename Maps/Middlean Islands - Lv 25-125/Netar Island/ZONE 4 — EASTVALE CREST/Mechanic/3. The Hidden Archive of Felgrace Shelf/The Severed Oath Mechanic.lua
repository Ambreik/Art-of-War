			The Severed Oath

	(Aurelian Thorne’s Ghost Event – Warlock Exclusive Trigger)

Classification :
Hidden World Logic (LW)
Rare, class-specific, non-repeatable per character

---

		5.1 Trigger Conditions

	This mini-event is not visible in UI and has no quest marker.
	It is triggered only if all conditions are met:
* Player class: "Warlock"
* Player manually takes the Sword beside Aurelian Thorne’s skeleton
* The sword is placed onto the central pillar using a special interaction:

  > "Offer Blade to the Pillar"
* The Pillar must NOT be in Phase III at the time
* No ritual is currently active

	Once triggered, the sword becomes temporarily bound to the pillar and cannot be removed until the event concludes.

---

		5.2 Event Initiation

	Upon placing the sword:
* The pillar darkens
* The concentric rings stop rotating
* A low, distorted wind sound fills the chamber
* All other interactions are locked
* The environment enters a “silent phase” (no ambient sounds except echoes)

	Then the ghost of "Aurelian Thorne" manifests.

---

		5.3 Aurelian Thorne – Ghost Manifestation

	The apparition is:
* Non-hostile
* Semi-transparent
* Lacks head (visual detail: faint spectral mist where the head would be)
* Slightly flickering, unstable

	He addresses the Warlock directly.

---

		5.4 Aurelian Thorne’s Revelation (Dialogue Logic)

	Aurelian explains:
* That after finishing the wall engravings:
	* The noises from below grew louder and more persistent
	* The cave began reacting unnaturally
* That he accidentally dropped his sword while working
* That from above, he heard the Banshee and her minions returning into the depths
* That he realized he was being watched, hunted

	He describes:

> “I knew then… it was not a fight meant for men like me.”

	He reveals his final decision:

* He learned the Banshee could not bind or corrupt bodies that were:
	* Too severely destroyed
	* Or improperly whole
* So he chose to sever his own head instantly, ensuring:
	* His soul could not be used
	* His body could not be raised
	* He would never become part of her undead host

	He clarifies:
> “It was not bravery.
> It was the only way to deny her.”

---

		5.5 Warning to the Player

	Before fading, Aurelian issues a final warning, tailored for the Warlock:
	He tells the player:
* To stay away from these lands unless:
	* They are a well-prepared soldier
	* Or quick enough with blade or spell
	* Or capable of sustaining fire long enough to reach the sea
* That the Banshee — the "Red-Rose Witch" — does not merely kill
	* She keeps what she touches

	This ties directly into:
* Fire mechanics
* Coastal escape logic
* Future banshee-related content

---

		5.6 Event Resolution

	After dialogue ends:
* The sword dissolves into black spectral fragments
* The pillar releases a short shockwave (visual only)
* The skeleton remains unchanged
* The ghost vanishes permanently for that character

	This event cannot be repeated on the same character.

---

		5.7 Mechanical Rewards / Effects

	This event does NOT grant items directly.
	Instead, it unlocks:

	Hidden Passive Buff (Warlock Only):

	"Bearer of the Severed Oath"

Effects:

* +X% resistance to fear effects from undead and banshee-type enemies
* Enables detection of certain hidden banshee traces in the world
* Alters some Warlock-only interactions in future undead zones
* Certain ritual outcomes become safer or more potent

Additionally:
* Warlocks with this flag gain alternate dialogue with:
	* Banshee-related NPCs
	* Necromancers
	* Some undead elites

---

	5.8 Failure / Interruption Conditions

	The event fails if:	
* Player leaves the area mid-event
* Player is killed during manifestation
* Pillar enters Phase III forcibly

	On failure:
* The sword is returned to its original position
* Event may be retried later

---

	5.9 World Consistency Rules

* Other players cannot see Aurelian during this event
* The ghost only manifests for the Warlock triggering it
* Other players present will only notice:
	* Sudden silence
	* Pillar darkening
	* Sword dissolving afterward

---

	This mechanic:
* Reinforces Aurelian Thorne as a major narrative figure
* Ties Warlocks uniquely to banshee lore
* Adds a hidden, high-value story layer
* Preserves the tragic, grounded tone of Zone 4
* Deepens the Red-Rose Witch mythos without overexposing her

