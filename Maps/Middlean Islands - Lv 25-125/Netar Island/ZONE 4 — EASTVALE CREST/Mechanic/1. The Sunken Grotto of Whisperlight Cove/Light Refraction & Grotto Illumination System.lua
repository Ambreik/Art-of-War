			Light Refraction & Grotto Illumination System

	Name : "Refracted Veil"

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Sunken Grotto of Whisperlight Cove

---

	Description : The grotto is naturally lit by sunlight filtering through narrow cracks in the ceiling and fractured stone above.
Rather than providing constant visibility, light inside the cavern behaves dynamically, shifting with the time of day and the movement of water outside.
	Light is not only visual ambiance here — it actively alters exploration, navigation, and danger perception.

---

		Core Mechanics

	Dynamic Light Beams

Sunlight enters through ceiling fissures and refracts through mineral veins and seawater mist:
* Light beams move slowly across cavern surfaces
* Illumination changes based on:
	* Time of day
	* Tide level
	* Water surface agitation
* Certain areas are only visible when a light beam passes over them

---

		Hidden Object Revelation

	Some objects and paths are invisible or indistinct unless illuminated:

Light reveals:
* Faint wall markings
* Submerged ledges
* Climbable surfaces
* Hidden mineral seams
* Ancient etchings or symbols tied to future puzzles

Without light:
* These surfaces appear smooth or indistinguishable from surrounding rock

---

		Illusion & Distortion Effects

	Because light refracts through water and crystal:

* Some surfaces appear:
	* Deeper than they really are
	* Or closer than they are
* Players may misjudge:
	* Jump distances
	* Water depth
	* Enemy proximity

	This creates a natural environmental puzzle without artificial UI guidance.

---

		Player Interaction With Light

	Players can influence the illumination in limited ways:

* Splashing water:
	* Temporarily disrupts light patterns
	* Can reveal new reflections or obscure others
* Using fire or magical light sources:
	* Overpowers natural refraction
	* Reveals hidden elements instantly
	* But also:
		* Attracts certain cave creatures
		* Or destabilizes fragile mineral formations

---

		Combat Integration

	Light affects encounters inside the grotto:

* Creatures may:
	* Avoid strong light zones
	* Or ambush from shadowed areas
* Some enemies gain:
	* Accuracy or evasion bonuses while standing in shadow
* Others are weakened when fully illuminated

	This turns positioning and light awareness into a tactical layer.

---

		Exploration Impact

	This mechanic ensures that:

* The grotto is not meant to be rushed
* Careful movement and observation are rewarded
* The same location can feel different at:
	* Dawn
	* Midday
	* Sunset
* Repeated visits reveal new details based on light conditions

