			Tide-Sealed Entrance System

	Name : "Breath of the Cove"

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Seastrand Mirror → Whisperlight Cove → Sunken Grotto of Whisperlight Cove

---

	Description : The entrance to the grotto is completely hidden behind a wave-carved stone wall and only becomes accessible when the tide reaches a very specific 
height. At all other times, the wall appears solid and impenetrable, blending seamlessly with the surrounding cliff and sea foam.
	This makes the grotto naturally time-gated and prevents casual discovery.

---

		Mechanics

	Tide Dependency

The entrance becomes available only when:
* The tide is at a mid-low transitional state
	* Not full low tide
	* Not full high tide
* This state lasts approximately:
	* 6–10 minutes per cycle
* Occurs:
	* Roughly 2–3 times per in-game day

	During this window:
* A narrow crevice opens behind the curved stone wall
* Subtle water drainage animation reveals a crawl-sized opening
* The entrance cannot be forced open by skills, explosives, or strength

---

		Visual & Environmental Indicators

	When the entrance is about to open, players may notice:
* Water receding unnaturally fast along the cliff face
* Foam patterns forming a circular motion near the wall
* A brief shimmer effect across the stone surface
* Refraction of light behaving abnormally in that spot

	These clues:
* Are subtle
* Not marked on the minimap
* Intended for observant or returning players

---

	Access Restrictions

* The entrance:
	* Is not targetable
	* Cannot be interacted with outside the valid tide window
* Attempts to use:
	* Teleport
	* Blink
	* Clipping skills 
	will fail due to environmental collision rules

---

		Player Interaction

	Once accessible, players may:
* Enter freely until tide changes again
* Remain inside even after the entrance reseals
* Exit only through:
	* The same entrance (if tide allows)
	* Or deeper internal paths (if unlocked later)

	If players are outside when the tide shifts:
* The entrance closes silently
* No system message appears
* Players must wait for the next tide cycle

---

		Gameplay Impact

	This mechanic ensures:

* The location feels secret and natural, not artificially locked
* Players must:
	* Observe
	* Return
	* Or follow environmental patterns
* Encourages:
	* Exploration
	* Map knowledge
	* Time awareness
* Prevents mass-farming and overcrowding

---

	System Integration

* The tide state is synchronized globally
* Entrance availability:
	* Is shared across all players on the map
* Respawn or death:
	* Does not force re-opening
* Server restart:
	* Resets tide cycle but does not reveal the entrance by default

---

		Escape & Safety Measures

	Players who become locked inside the "Sunken Grotto" due to tide changes are not trapped permanently.

	Emergency Exit Options

If the entrance seals while players are inside, they may exit by:

	1. Teleport Abilities

* Any standard teleport skill, scroll, or recall effect works normally
* Teleport destination:

	* Last bound location
	* Or nearest discovered waypoint

	2. Flaming Crystal – Emergency Artifact

A special item found deep inside the grotto:

	Item : "Flaming Crystal of Whisperlight"

* One-use consumable
* When activated:
	* Instantly teleports the player outside the grotto entrance
	* Always places them at the base of the Whisperlight Cove cliffs
* Cannot be used in combat
* Cannot be traded
* Exists specifically to prevent permanent entrapment

