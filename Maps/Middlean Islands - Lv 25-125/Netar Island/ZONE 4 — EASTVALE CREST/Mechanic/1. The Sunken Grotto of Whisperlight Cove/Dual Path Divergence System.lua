			Dual Path Divergence System (Boss & Relic Route)

	Name : The Fork of Tides

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Sunken Grotto of Whisperlight Cove

---

	Description : Deep within the grotto, the main stone ledge splits into two ancient routes carved naturally by water and erosion.
These paths do not exist to favor builds — they serve two distinct narrative and mechanical purposes:
* One leads to a hostile chamber housing a unique boss
* The other leads to a forgotten relic chest tied to deeper dungeon progression

	Players must choose which path to take per visit, as natural changes prevent clearing both in one run.

---

		Path A – The Dry Ledge (Boss Route)

	Leads To:

* Boss Chamber : "The Resonant Hollow"

	A wide, echoing cavern where sound and vibration shape the battlefield.

	Boss Name: "Auralith, the Grotto Warden"
An ancient cave-dwelling entity fused with mineral and stone, awakened by repeated tremors and light disturbance.

---

	Auralith, the Grotto Warden – Concept

* A massive stone-crusted creature with glowing mineral seams
* Moves slowly but controls the terrain through vibration
* Attacks are tied to:
	* Sound
	* Ground resonance
	* Falling stone

	Core Boss Mechanics (High-level)

* "Resonant Slam"
	* Creates shockwaves through the floor
	* Damage increases if players are moving when it strikes
* "Echo Call"
	* Summons mineral constructs from cave walls
* "Crystal Fracture"
	* Breaks parts of the arena, changing safe zones
* "Seismic Pulse"
	* Temporarily disables jumping or dodging

	This boss reinforces:
* The cave’s sound-based identity
* The instability of ancient grotto formations

---

		Path B – The Flooded Corridor (Relic Route)

	Leads To: Ancient Chest : "The Jagged Reliquary of Tides"

A corroded, barnacle-covered chest fused into the rock at the end of the submerged corridor.
Its appearance suggests decay and worthlessness — but its contents tell another story.

---

		Jagged Reliquary of Tides – Chest System

	Access Conditions

* Located behind:
	* Deep water
	* Low visibility
	* Tight submerged passage
* Requires:
	* Careful navigation
	* Breath management
	* Or water-mob avoidance

---

	Loot System

The chest draws from a long randomized pool, including:

Possible drops:
* Rare crafting materials
* Ancient Runes
* Relic fragments usable inside: "Tomb of Great Crusaders"
* Grotto-exclusive consumables
* Low chance:
	* Unique dungeon utility item
	* Or Crusader-linked artifact

---

	Access Limits

* Per Account :
	Can be opened once every 1–24 hours (randomized cooldown per account)
* Per Chest Instance :
	Can be looted by up to 8 players before sealing again
* After the limit:
	* Chest visually seals
	* Emits dull light
	* Cannot be interacted with again until reset

