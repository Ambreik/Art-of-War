			Dawn/Dusk Light Show Mechanic

	Name : "Ethereal Horizon"

	Description : At dawn or dusk, the reflective minerals catch the sun’s rays and glow in long, soft bands of light, illuminating the ridge in a spectacle of 
pale luminescence.

	Mechanics :
* Provides vision buff for all players on the ridge (slightly increased perception and spotting range)
* Enhances navigation across narrow ridge paths, reducing risk of falling
* Could act as a minor buff zone for exploration or scouting the Sunwell Basin and distant sea
* Players observing this spectacle may trigger lore pop-ups or journal entries about the ridge’s minerals and history

	Flavor :
*As the first rays of sunlight strike the ridge, the stones awaken, stretching long bands of pale light across the path.The world seems to float beneath your feet.*

