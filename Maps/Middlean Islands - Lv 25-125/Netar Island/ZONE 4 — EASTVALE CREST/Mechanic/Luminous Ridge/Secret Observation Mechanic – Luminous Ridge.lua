			Secret Observation Mechanic – Luminous Ridge

	Name : "Ridgewatch Sightline"

	Description : From certain points along the ridge, the glowing minerals act as natural mirrors and lenses, reflecting moonlight and sunlight across the 
landscape. Players who position themselves carefully at these observation points can see distant POIs, secret pathways, or rare events—even things normally hidden 
without the ridge’s perspective.

---

	Mechanics : 

1. "Observation Points"

	* Specific locations along the ridge are marked by natural mineral clusters or stone outcrops.
	* Interacting here triggers a survey mode :
		* Players can rotate camera freely
		* Zoom toward "Sunwell Basin", "Starwatch Ledge", or other nearby POIs
		* Certain distant points glow faintly when visible (secret markers)

2. "Day/Night Effects"

	* Daytime : Reflections may highlight terrain features, hidden caves, or rare herbs
	* Nighttime : Moonlight and mineral glow allow spotting of:
		* Rare nocturnal creatures (e.g., flying predators, distant patrols)
		* Hidden POIs or floating loot markers
		* Distant celestial events aligned with Starwatch Ledge

3. "Discovery Rewards"
	* Observing certain secret points may unlock journal entries or lore:
		* Ancient navigational techniques using natural light
		* Hints for future dungeon entrances
		* Hidden resource locations
	* Some observations may unlock minor exploration buffs :
		* +5% movement speed along ridge paths for 15 minutes
		* +5% night vision when traversing other elevated POIs

4. "Environmental Interaction"
	* Players may manipulate small mirrors, reflective stones, or minerals to redirect beams to reveal additional secrets
	* Could later be tied to cross-POI puzzle sequences, linking Luminous Ridge to Starwatch Ledge and Sunwell Basin

---

	Flavor : 
*By crouching among the glowing minerals and gazing carefully along the ridge, the world stretches farther than your eyes could normally reach. A faint shimmer 
hints at paths unseen, secrets untold, and stories waiting to be uncovered in the distance.*

