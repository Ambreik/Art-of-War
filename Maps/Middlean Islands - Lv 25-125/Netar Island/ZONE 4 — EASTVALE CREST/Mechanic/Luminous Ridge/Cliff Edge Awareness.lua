			Optional Environmental Interaction / Hazard Mechanic

	Name : "Cliff Edge Awareness"

	Description : The ridge is narrow in spots, and the reflective minerals can cause temporary glare or optical illusions, especially during dawn/dusk.

	Mechanics :
* Players may misjudge ledges when not careful:
	* Failing perception checks → minor slip or fall (low damage, not deadly, unless combined with other hazards)
	* Could interact with future mechanics (e.g., triggering rockfalls or environmental traps)
* Makes cautious traversal rewarding
* Can combine with light reflection safe path mechanic for risk/reward traversal

	Flavor :
*The minerals glow almost too brightly, making edges shimmer and tricking your eyes. Step carefully, or the ridge may remind you that even beauty has sharp edges.*

