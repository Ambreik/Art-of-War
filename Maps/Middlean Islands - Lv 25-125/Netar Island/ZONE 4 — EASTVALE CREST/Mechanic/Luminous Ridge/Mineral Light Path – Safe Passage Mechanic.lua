			Mineral Light Path – Safe Passage Mechanic

	Name : "Glowing Path"

	Description :
The pale, reflective minerals in the ridge absorb sunlight during the day and faintly glow at night. This illumination creates a narrow corridor of soft light along the ridge.

	Mechanics :

* Players traveling on the illuminated bands are safe from ground-based creatures at night.
* Flying creatures (harpies, giant bats, night wraiths, or zone-specific aerial mobs) can still attack, maintaining tension.
* Light intensity depends on:
	* Moon phase (full moon enhances glow)
	* Weather conditions (fog or heavy rain dims the reflection)
* Players can use the path to safely traverse the ridge at night, even if surrounding terrain is hostile.

	Optional Enhancements / Extra Features :
* Light may briefly blind or disorient flying enemies passing near the ridge (minor debuff to accuracy or attack speed).
* Certain rare nocturnal plants along the mineral reflections only bloom when the glow is strong, giving a herbalism/mining collectible opportunity.

	Flavor :
*The minerals shimmer softly under the night sky, casting a pale ribbon of light across the ridge. It feels almost magical—enemies hesitate to tread here, as if the
stones themselves protect the path.*

