			Environmental Feedback System

	Name : "Rift Resonance Response"

---

	3.1 Chasm Reaction to Activity

	The Riftwell Chasm responds dynamically to player actions:

If:
* Multiple dynamite explosions occur
* Or repeated failed fishing attempts happen
* Or rare fish is caught

Then:
* Vibration frequency changes
* Mist density increases or fades
* Ambient sound distorts further

This is purely systemic but:
* Alters spawn rates
* Alters fishing success chance
* Affects monster strength slightly

---

	3.2 Visual & Audio Feedback

	Players may observe:
* Stone band glow shifts
* Mineral veins pulse
* Echo distortion increases
* Subtle gravitational sway

T	his warns experienced players that:
* The chasm is becoming unstable or active

