			Dynamite Provocation System

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Riftwell Chasm

	Name : "Depthbreaker Provocation"

---

	2.1 Trigger Conditions

	If players throw "Dynamite" into the "Riftwell Chasm" :
* The system tracks total dynamite used per short window
* Minimum: 1
* Maximum effective: 52

	The more dynamite used:
* The stronger the summoned creature
* The more aggressive its opening behavior

---

	2.2 Spawned Monster

	Name: "Riftborn Mawlurker"

(Aquatic / Abyssal Elite Monster)

	Classification:	
* Rare provoked aquatic entity
* Does NOT spawn naturally
* Only appears via dynamite interaction

	Spawn Behavior:
* Leaps violently from the chasm
* Lands near the throwing players
* Aggro locked on:
	1. Highest dynamite contributor
	2. Nearby players
* Cannot return to the chasm once emerged

---

	2.3 Dynamite Scaling Logic

The number of dynamite used determines:

| Dynamite Used | Effect                           |
| ------------- | -------------------------------- |
| 1–5           | Normal Mawlurker                 |
| 6–15          | Enhanced HP + AoE skill unlocked |
| 16–30         | Gains lifesteal + crowd control  |
| 31–52         | Elite Variant with enraged phase |

	If fewer than 3 players are nearby:
* The Mawlurker receives a soft stat reduction
* But retains full skillset

---

	2.4 Mawlurker Core Abilities

* "Abyssal Surge"
	Leaps forward dealing splash damage

* "Mist Vein Drain"
	Drains HP from players standing in mist zones

* "Chasm Howl"
	Fear effect + sound suppression

* "Enrage at 30% HP"
	Increases attack speed and knockback chance

---

	2.5 Reward System

	Drops scale with:
* Dynamite count
* Player contribution

	Possible Drops:
* Rift-scale hide
* Veil-crystal bone
* Abyssal crafting components
* Rare chance:
	"Core of Riftwell" (used in Riftcaller Rod crafting)

---

	2.6 Anti-Farming Rules

* Riftborn Mawlurker can only be summoned once every:
	* 90 minutes per channel/instance
* Dynamite thrown outside that window:
	* Is consumed
	* But no spawn occurs
* Players abusing resets will cause:
	* Chasm destabilization (no fishing for 30 minutes)

