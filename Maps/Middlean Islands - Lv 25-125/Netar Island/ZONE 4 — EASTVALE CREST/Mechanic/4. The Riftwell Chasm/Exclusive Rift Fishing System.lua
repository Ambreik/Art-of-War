			Exclusive Rift Fishing System

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Riftwell Chasm

---

 Name : "Abyssal Line" – Riftwell Fishing Mechanic

---

	1.1 Access Conditions

Fishing inside Riftwell Chasm is only possible if ALL are true:

* Player is using the fishing pole:
	"Riftcaller Rod"
* Player is using the lure:
	"Deepveil Lure"
* Player is positioned at specific ledges or the stone steps near the mist zone
* Normal fishing equipment will fail here

---

	1.2 Required Equipment

	Fishing Pole: "Riftcaller Rod"

	Properties:
* Designed for extreme depth and magical interference
* Allows fishing only in:
	* Riftwell Chasm
	* (Future: similar abyss-type fissures)
* Cannot be used on regular waters

	Acquisition:
* Rare drop from Zone 4 elites
* Or crafted by :
	* Eldric Sunwell - Fisherman Rank III. 

---

	Lure: "Deepveil Lure"

	Properties:
* Emits a low-frequency magical vibration
* Attracts creatures immune to surface fishing
* Consumed on use

	Crafting by Eldric Sunwell :
* Shadow Silk Thread
* Mist Condensate
* Abyss-Salt Crystal

---

	1.3 Riftwell-Exclusive Fish

	Name: "Veilscale Abyssfin" (Rare Fish)

	Properties:
* Can only be fished inside Riftwell Chasm
* Extremely low catch rate
* Cannot be caught by any other method

	Uses:
* High-tier alchemy ingredient
* Used in crafting:
	* Stealth-enhancing consumables
	* Sound-suppression elixirs
	* Shadow-infused gear
* Required in at least one legendary recipe

	Fishing Behavior:
* Only appears during:
	* Mist-active intervals
	* Or when the chasm vibration is active
* Has a chance to escape even after a successful hook

---

	1.4 System Restrictions

* Only one Veilscale Abyssfin may be caught per player per 24h
* The chasm “resets” fishing rarity every 4 hours globally
* Using wrong equipment alerts the chasm (visual ripple + vibration spike)

