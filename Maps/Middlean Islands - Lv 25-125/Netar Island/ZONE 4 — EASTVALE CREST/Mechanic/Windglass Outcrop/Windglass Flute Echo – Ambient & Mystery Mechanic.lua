			Windglass Flute Echo – Ambient & Mystery Mechanic

	Name : "Echoes of the Windglass"

	Description : The hollow pockets in the smooth, wind-sculpted boulders create natural flute-like tones when coastal winds pass through. At certain times of day,
the outcrop resonates as if the rocks themselves are singing, carrying music across the shore and cliffs.

	Mechanics :

* Active Hours :
	* Typically heard during dawn (05:30–07:30) and sunset (17:00–19:30).
	* The effect is stronger on windy days or when the coastal wind is above a certain threshold.
* Player Interaction :
	* Players can stand near certain boulders to focus on the echoes, which provides:
		* Temporary calmness buff :
			* +5% HP regen
			* +5% Mana/Stamina regen
			* Duration: 15 minutes
		* Optional journal/lore insight: a subtle audio whisper narrates fragments of coastal legends, ancient travelers, or lost songs of the island.
	* The echo is subtle for players just passing by, but interacting closely unlocks a more immersive effect, like hearing multiple overlapping flute tones.
* Environmental Effects :
	* Wind patterns can carry the melody across nearby cliffs, alerting wildlife:
		* Seabirds may fly closer or away depending on the tone
		* Small coastal predators may temporarily freeze or flee
	* Occasionally, distant harmonic resonance triggers when multiple pockets align with the wind, producing a rare, longer melody that can alert players to a hidden 
POI or secret spot nearby (future development opportunity).
* Rarity / Lore :
	* The phenomenon is natural but rare: only happens when winds, tide, and time align perfectly.
	* Creates a mysterious, almost spiritual atmosphere at the border between Zone 4 and Zone 5.
	* Could be used later to unlock hidden dungeons, secret fishing spots, or NPC encounters that only appear when the echo is active.

	Flavor Text :
*"The stones hum as if remembering centuries of wind. Stand still, and for a fleeting moment, you might hear the island’s own heartbeat."*

