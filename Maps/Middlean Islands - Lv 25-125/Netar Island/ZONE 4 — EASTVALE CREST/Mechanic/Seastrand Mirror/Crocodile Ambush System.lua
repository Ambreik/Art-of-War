			Crocodile Ambush System

	Name : "Stillwater Hunger"

	Description : Crocodiles lie almost invisible in shallow water and on reflective mirror sand, blending perfectly with the surface. The calm of the beach masks a
deadly predator. Players must carefully watch both the mirrored ground and the subtle ripples in the water to survive.

	Mechanics : 

	1. Stealth & Camouflage : 

* Crocodiles are visually indistinct until triggered.
* Detection triggers:
	* Player steps within a defined “ambush range” (3–7 meters, depending on player level)
	* Player splashes in water or casts AoE skills
	* Player disturbs mirror sand surface (running, rolling, dodging)
* First strike:
	* +25–40% bonus damage
	* Chance to root, grab, or drag player into water for secondary attacks

	2. Reflexive Behavior – Ambush Logic : 
* Crocodiles patrol slowly when water is shallow or tide is high
* When multiple crocodiles are in an area:
	* They coordinate ambushes indirectly via “stealth signaling” (tail splash or head movement)
	* Can corner players between mirror sand reflections, forcing movement mistakes
* Environmental synergy:
	* Mirror sand reflections can make crocodiles appear displaced
	* Sound suppression makes it difficult to hear splashes until too late

	3. Player Interaction & Counterplay : 
* Players can:
	* Throw objects (coconuts, rocks, or fish bait) to lure crocodiles out of hiding
	* Use ranged attacks from cliffs or high ground to safely thin numbers
	* Exploit reflection distortions to spot hidden ripples
* Risk/Reward:
	* Luring crocodiles can trigger chain ambushes
	* Baiting can make them cluster for AoE skills

	4. Environmental Hazards & Synergy : 
* Shallow water slows movement – perfect for ambush
* Mirror sand reflections:
	* Cause misjudgment of distance
	* Can "duplicate" crocodiles visually, increasing panic or misfire
* Tide levels influence activity:
	* Low tide: fewer crocodiles, but more visible
	* High tide: more crocodiles, less visibility

	5. Emergent Combat :
* Multiple crocodiles may gang up if players attack one
* Some crocodiles may retreat temporarily if heavily injured, reappearing later for ambush
* Rare variant (elite crocodile) can use water to create small localized waves, disrupting ranged attacks or casting

	6. Flavor / Immersion : 
* "A shadow under the glassy sand shifts. Nothing moves, yet something watches. The water whispers…"
* Crocodiles make subtle rippling motions before attacking, giving players a slight cue

	7. Optional Integration : 
* Interaction with Skyfall Frenzy:

	* Birds in frenzy may distract crocodiles
	* Players may manipulate bird attacks to safely cross ambush zones
* Linked with Focus Vision passive:
	* Players with the passive can detect subtle ripples earlier
	* Can reward tactical play for observant players

