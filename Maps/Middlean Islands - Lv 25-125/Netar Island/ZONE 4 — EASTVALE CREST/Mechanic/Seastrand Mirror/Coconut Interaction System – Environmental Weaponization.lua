			Coconut Interaction System – Environmental Weaponization

	Name: "Coastal Provocation"

	Description : Coconuts grow in small clusters along the upper beach and bluff edge.

	Mechanics

* Players can:
	* Pick up coconuts as throwable objects

	Throw Effects : 
* Against Animals:
	* Stuns briefly
	* May enrage predators (crocodiles, boars)

*Against Birds (Pelicans, Gulls)
	* Triggers:
		*Event: "Skyfall Frenzy"

	Birds become hostile and:
* Attack all creatures and players within radius
* Prioritize:
	1. Whoever threw the coconut
	2. Nearby animals
* Can:
	* Kill small creatures
	* Disrupt fights
	* Reveal stealthed enemies

	This allows players to:
* Clear areas creatively
* Trigger chaos for advantage
* Or accidentally wipe themselves

