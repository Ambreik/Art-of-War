			Wreck Cluster – Rare Encounter & Loot Zone

	Name: "The Shattered Line"

	A cluster of broken ships and debris partially submerged offshore.

	Mechanics : 

	Rare Monster Spawn : 

* Appears only during:
	* Low tide
	* Or heavy fog
* Example Rare:
	"The Brinebound Maw" – A massive crocodilian creature feeding among wreckage

	Rewards : 
* Sea-themed gear
* Unique crafting materials (salt-crystal bone, mirror-scale hide)

	Environmental Hazards : 
* Sharp debris causes bleeding if knocked into
* Waterlogged zones reduce movement speed

