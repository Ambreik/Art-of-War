			Silence Field – Sound Absorption Mechanic

	Name: "Cliffbound Quiet"

	Description : The cliffs absorb sound, leaving the beach eerily quiet.

	Mechanics : 

	Audio Suppression
* All combat sounds reduced by ~50%
* Spell and ability audio muted or softened
* Environmental audio dominates (waves, birds, wind)

	Gameplay Impact : 
* Enemies relying on sound detection are less effective
* Stealth builds gain increased advantage
* Certain predators rely more on vision here than hearing

	Psychological Effect : 
* Combat feels “wrong” and disorienting
* Makes sudden ambushes feel much more intense

