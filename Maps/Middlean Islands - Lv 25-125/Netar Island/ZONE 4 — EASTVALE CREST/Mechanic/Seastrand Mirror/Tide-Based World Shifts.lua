			Tide-Based World Shifts

	Name: "Breathing Shore"

	The POI changes depending on tide state.

	High Tide : 
* Less walkable area
* More aquatic creatures
* Crocodiles more aggressive
* Some wrecks submerged

	Low Tide : 
* Mirror sand exposed
* Hidden loot revealed
* Rare monsters more likely
* Easier access to wrecks

