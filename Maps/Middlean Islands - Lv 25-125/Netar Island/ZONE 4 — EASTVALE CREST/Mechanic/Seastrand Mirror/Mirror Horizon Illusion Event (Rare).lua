			Mirror Horizon Illusion Event (Rare)

	Name: "Vanishing Line"

	Occurs at:
* Sunset
* With low tide
* Clear sky

	Effect : The boundary between sea and sky visually disappears.

	During this:
* Players experience:
	* No visible horizon
	* Sky reflected perfectly underfoot
* For 60 seconds:
	* Movement speed increased
	* Fall damage reduced
	* Stealth effectiveness increased
	* Some enemies become confused, pausing briefly

	Grants buff:
"Between Sky and Sea"

* +10% evasion
* +10% movement speed
Duration: 15 minutes

