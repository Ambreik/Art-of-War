			The Abandoned Boat – Crusader Escape Lore Mechanic

	Name: "The Last Oar"

	Description : A half-buried, weather-worn boat rests near the bluffs — the escape vessel the final Crusader sought before being caught.

	Mechanics : 

Players can interact with the boat to:

* Investigate revealing : 
	* Scratched Crusader symbols
	* Blood traces near the waterline
	* A broken oar

	Event Interaction : 

* At night, near the boat:
	* A shadowy figure briefly appears pushing it toward the sea
	* The figure collapses before reaching the water and vanishes

	Witnessing this grants:
* +Temporary movement speed near shorelines (10 min)
* +Small bonus to evasion against Crusaders

