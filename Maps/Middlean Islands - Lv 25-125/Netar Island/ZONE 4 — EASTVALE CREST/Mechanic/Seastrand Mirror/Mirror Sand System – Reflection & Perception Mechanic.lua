✖ Dying to 1–4 normal mobs without active debuffs
✖ Falling due to poor positioning with no combat pressure
✖ AFK deaths or standing idle in hostile zones
✖ Repeated deaths in short intervals
✖ Deaths outside mirror-active states (even inside the POI)

