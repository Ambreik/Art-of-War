	Focus Vision → Stealth Expansion System

*(Passive Line: Perceptive Stealth)*

	Focus Vision does not become “Stealth” itself — instead, it augments how Stealth behaves for the player across all classes, in subtle, non-breaking ways.
	This creates a new cross-class stealth mastery layer, built on awareness rather than invisibility.

---

	Core Concept : Focus Vision evolves into a passive branch called:

	Passive Line Name : "Perceptive Stealth"

> You do not vanish — you become unimportant to the eye.

	This line does not grant "Stealth", but enhances:
* Detection control
* Entry/exit behavior
* Environmental blending
* Movement concealment

	Even non-stealth classes benefit — without replacing Assassin Stealth.

---

		How It Expands From Focus Vision

	Once Focus Vision is unlocked, hidden progress starts tracking toward Perceptive Stealth traits, unlocked gradually through stealth-related behaviors.
	This makes it feel like a natural evolution, not a separate skill.

---

		Perceptive Stealth – Passive Tiers

	These are NOT shown as levels; they are silently unlocked through play.

---

	Tier I – Shadow Reading

	Effect :
✔ Reduces enemy detection radius by 5–8% while:
* In tall grass
* In fog/mist
* In low light
✔ Detection delay increases slightly (enemies take longer to “notice” you)

	Flavor : "You learn when to move by watching how shadows breathe."

	Applies globally.

---

	Tier II – Silent Intent

	Effect :
✔ First hostile action from stealth or low detection:
* Generates 30% less threat
* Reduces chance of immediately drawing aggro
✔ Ranged attacks reveal position less clearly at long range

	This does NOT grant stealth — only softens exposure.

---

	Tier III – Environmental Cloaking

	Effect :
✔ Certain environments partially conceal you even while moving:
* Wet surfaces
* Snow
* Shallow water
* Dense foliage
✔ Footstep sound radius reduced by 20% in these terrains

	This synergizes perfectly with:
* Mirror Sand
* Dawnchill Springs
* Jungle/swamps

---

	Tier IV – Ghosting (Rare)

	Unlocked only by exceptional play.

	Effect :
✔ Brief stealth-like state (0.8–1.5s) triggers when:
* Breaking line of sight
* After dodging an attack
* After exiting mist/fog/water
✔ Does NOT work in direct frontal vision

	This gives non-Assassin classes momentary stealth behavior, but never full invisibility.

---

	Tier V – The Quiet Mind (Mastery)

	Very rare.

	Effect :
✔ Immune to “panic detection” (sudden mass aggro from noise/light spikes)
✔ Enemies must confirm your presence before alerting others
✔ Grants access to special stealth-based dialogue options and interactions

	This makes players feel like true environmental predators, not magical invisibility users.

---

		Relationship With Assassin Stealth

	This system does NOT replace Assassin stealth — it enhances it.

	Assassin with Perceptive Stealth gets:
✔ Longer effective stealth windows
✔ Fewer random stealth breaks
✔ More forgiving detection margins
✔ Cleaner re-entry into stealth after attacks

	But never:
✖ Infinite stealth
✖ Permanent invisibility
✖ Immunity to AoE detection

	So Assassin remains king of stealth — but perceptive players become masters of not being seen.

---

		How Players Progress It (Hidden)

	Progress is earned through:
✔ Avoiding detection repeatedly
✔ Breaking line of sight successfully
✔ Escaping combat without being seen
✔ Moving through hostile zones without engaging
✔ Using terrain instead of skills
✔ Performing stealth actions without Assassin class

	This keeps it:
* Rare
* Skill-based
* Non-grindable
* Non-obvious

	Many players will never realize they’re progressing it.

