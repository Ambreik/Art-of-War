			Mirror Sand System – Reflection & Perception Mechanic

	Name: "Skybound Reflection"

	Description : When the tide retreats, the wet sand becomes a near-perfect mirror of the sky and cliffs, blurring the line between ground and horizon.

	Mechanics

	Visual Distortion : 
* Player silhouettes, mobs, and flying creatures appear reflected beneath them
* At certain angles:
	* Flying enemies appear grounded
	* Ground enemies appear floating
* This makes target-reading harder in combat

	Combat Impact : 
* Ranged attacks have slightly reduced accuracy due to visual misalignment
* Stealth effectiveness is increased due to reflected silhouettes confusing detection
* Certain creatures (crabs, crocodiles) gain brief camouflage while standing on mirror sand

	Player Interaction :
*Players can:
	* Use the reflection to:
		* Spot flying enemies before they dive
		* Detect invisible or burrowed creatures via reflection distortions
		* Activate a “Focus Vision” mode (limited duration) that sharpens reflections and reveals:
			* Hidden movement trails
			* Submerged enemies
			* Buried loot

