		Tolerated Deaths – Hidden Survival Logic

	Not all deaths invalidate Focus Vision progress. The system silently classifies deaths based on combat context:

< Deaths That "Do NOT Reset Progress" >

	These are considered acceptable and do not invalidate accumulated progress:
✔ Player was engaged with 5+ standard enemies simultaneously
✔ Player was fighting an "Elite" or "Rare".
✔ Player was under effects of:
	* Crowd control chains (stun → root → fear)
	* Forced displacement into water or off cliffs
✔ Player was ambushed by:
	* A crocodile from water
	* A flying creature dive while already in combat
✔ Player was killed during:
* Low tide surge events
* Weather anomalies (fog, sudden storms, reflective glare bursts)

	These deaths are flagged as “environmental overwhelm” and are tolerated.

---

