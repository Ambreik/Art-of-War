			World Chest – “Crestfall Reliquary”

		Placement

	Located on:
* A high central terrace
* Or within a natural amphitheater

---

		Access Mechanic

	Instead of keys or puzzles:

	Chest becomes accessible only when :

* Area wildlife density is reduced
	OR
* Wind conditions stabilize (less gusts)
	OR
* Player clears enemies without triggering Echo Zone overpulls

	This ties chest access to:
* Mastery of the environment
* Not raw DPS

