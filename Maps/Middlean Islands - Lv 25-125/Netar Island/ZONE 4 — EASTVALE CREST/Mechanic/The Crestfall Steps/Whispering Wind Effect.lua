			Whispering Wind Effect

	Description : The constant whispering wind is not just audio flavor — it affects perception.

	Mechanics : 
* Random gusts:
	* Briefly mask sound
	* Or amplify it suddenly
* Stealth windows open and close dynamically
* Creatures may:
	* Lose track of targets briefly
	* Or become alerted unexpectedly

	Why It Fits : 
* Matches “constant whispering tone”
* Keeps encounters dynamic and unpredictable

