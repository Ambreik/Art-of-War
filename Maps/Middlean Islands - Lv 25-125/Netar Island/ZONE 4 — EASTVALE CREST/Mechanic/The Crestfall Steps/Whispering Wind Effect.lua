			Dense Wildlife Convergence**

### Description

Crestfall Steps act as a natural funnel for animals.

### Mechanics

* Higher-than-average spawn density
* Pack-based enemies more common
* Predators patrol vertically between steps
* Excellent grinding area, but:

  * High risk if overpulled
  * Noise attracts more enemies

### Why It Fits

* Terraces funnel movement naturally
* No artificial spawn justification needed

