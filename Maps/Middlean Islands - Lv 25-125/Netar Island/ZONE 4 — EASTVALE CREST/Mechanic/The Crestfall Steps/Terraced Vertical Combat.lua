			Terraced Vertical Combat

	Description : Combat across multiple steps creates a vertical battlefield.

	Mechanics : 
* Attacking from higher steps:
	* Slight damage or accuracy bonus
* Knockback abilities become more dangerous:
	* Enemies may fall to lower levels
* Some creatures prefer upper terraces
* Others lurk below, attacking upward

	Why It Fits : 
* Uses the staircase structure naturally
* Makes positioning more important than raw power

