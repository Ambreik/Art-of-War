			Safe Ascent Mechanic (No Trapping Players)

	Wind-Assisted Ascension

	In certain wind channels:
* Standing still briefly triggers:
	* A lift effect
	* Or reduced stamina climbing
* Allows players to return upward naturally
* No teleport, no ladders, no artificial tools

	This makes vertical traversal:
* Logical
* Wind-based
* Not frustrating

