			Shallow Pool Reflection

	Description : Pools on certain steps reflect light and movement, acting as natural sensory surfaces.

	Mechanics : 

* Enemies and players reflected in pools become:
	* Slightly easier to detect
* Some predators rely on reflections to ambush
* Certain rare creatures only appear near pools
* Reflections subtly indicate:
	* Wind direction
	* Approaching threats

	Why It Fits : 
* Builds on shallow pools and light
* Adds tactical awareness instead of raw stats

