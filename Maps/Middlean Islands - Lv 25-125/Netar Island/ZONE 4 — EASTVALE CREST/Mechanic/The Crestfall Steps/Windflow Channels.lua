			Windflow Channels

	Description : The constant wind flowing between the steps behaves like an invisible river, shaping movement and combat.

	Mechanics : 

* Certain narrow paths between steps become "Windflow Channels"
* While inside them:
	* Player movement speed increases downhill
	* Slight movement resistance uphill
* Ranged attacks fired *with* the wind gain:
	* Increased range or accuracy
* Ranged attacks fired *against* the wind:
	* Slightly reduced range or deviation

	Why It Fits : 
* Matches “wind flows like a river”
* Makes direction matter
* Adds spatial awareness without UI clutter

