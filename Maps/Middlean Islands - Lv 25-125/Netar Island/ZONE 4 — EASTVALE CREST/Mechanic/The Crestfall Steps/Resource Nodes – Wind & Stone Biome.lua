			Resource Nodes – Wind & Stone Biome

	Mineral Type: "Wind-Polished Veins"

* Appear on exposed stone faces
* Easier to mine during strong winds
* Harder to mine when calm (surface dust settles)

	Herbal Type: "Stepbloom / Crestvine"

* Grow on step edges and shallow pools
* Require careful positioning to gather safely
* Some only harvestable from above or below

	This reinforces:
* Vertical harvesting
* Environmental awareness

