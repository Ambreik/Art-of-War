			Whispering Wind Effect - Echoes of the Lost Crusade

		Core Mechanic (refined)

	The Whispering Wind is not only environmental noise — it is a carrier of memory, distortion, and residual will left by past tragedies.
	The wind does not speak continuously.
	It remembers.

---

	Concept : At specific moments, under specific conditions, the wind carries fragmented voices and visions of the fallen Crusaders — not as ghosts, but as 
imprints in the land itself.
	These are not NPCs, not interactables, not quests — but world echoes.

	They do not respond.
	They do not repeat predictably.
	They are not complete truths.

	They are memories bleeding through terrain.

---

		Fire Camp Whisper Event

	How It Triggers : A small, unmarked "Fire Camp" can be lit by players at certain terraces of Crestfall Steps.

	When:
* The fire is lit
* At least one player sits/rests nearby
* Wind intensity is medium or low
* No combat nearby
* 21:00 – 00:00 (Nightfall to Midnight)

	Then, after an undefined time window (not fixed, not predictable):

> The Whispering Wind changes tone.

	Presentation

	This can appear as :
* A screen narration window, semi-transparent, fading in and out

	Never marked as a quest.
	Never logged in a journal.
	Only experienced.

---

		Phase I — Before Entering the Dungeon

*(Player has not entered Tomb of the Great Crusaders)*

	Narrative Tone : Confusion, fear, early disappearances.
	Sample Whispers

> *“…another one’s gone… third this week… no blood… no tracks…”*

> *“…night swallowed him like he was never here…”*

> *“…the mountain breathes at night…”*

> *“…something watches us from inside the stone…”*

> *“…they came wearing our armor…”*
> *“…but hollow… like smoke trapped in steel…”*

> *“…we thought we’d raise a fortress here…”*
> *“…but the mountain took our walls before we built them…”*

	Purpose :
* Establish mystery
* Link Crusaders to the mountain
* Foreshadow dungeon horrors

---

		Phase II — After Clearing Half the Dungeon

*(Player reaches sections with the Spirits)*

	Narrative Tone : Understanding emerges. Paranoia. Strategic fear.
	Sample Whispers : 

> *“…I think they are part of something older…”*
> *“…spirits hiding from the wrath of the Old Gods…”*
> *“…and we disturbed their territory…”*

> *“…we must speak with the leader…”*
> *“…it is not safe to remain here…”*

> *“…from hundreds of us…”*
> *“…we are now forty-one…”*
> *“…with our groups, one hundred and twelve…”*

> *“…either we leave…”*
> *“…or this land will bury us…”*

	Purpose :
* Recontextualizes spirits
* Introduces Old Gods
* Suggests Crusaders were intruders
* Adds moral complexity

---

		Phase III — After Completing the Dungeon

*(Dungeon fully cleared)*

	Narrative Tone : Desperation. Collapse. Escape planning.
	Sample Whispers : 

> *“…now we are only eighty-three…”*

> *“…I will go north…”*
> *“…speak with the fellows beyond the ridge…”*
> *“…there are other islands…”*
> *“…we may yet be forgotten…”*

> *“…dragons still fly…”*
> *“…the war is not over…”*
> *“…they will not waste their strength on us…”*

> *“…you’re with me, right?”*

	Purpose :
* Establishes Zone 4 → Zone 5 transition
* Connects Crusaders to wider war
* Leads directly into the Journal

---

		Phase IV — The Fracture

*(Final Whisper Arc before Journal Discovery)*

	Narrative Tone : Betrayal, decay, realization.
	Sample Whispers : 

> *“…the captain from the northern group knows everything…”*
> *“…they will flee tonight…”*

> *“…you no longer speak…”*
> *“…your eyes are empty…”*
> *“…are you still human…”*
> *“…or already one of them?”*

> *“…you scare me…”*

	Purpose :
* Introduces the transformation theme
* Establishes undead infiltration
* Leads emotionally into the journal

---

		Mechanical Impact of the Whispers

	These are not just cosmetic.
	While whispers occur:

Player Effects
* Slight reduction to perception clarity
	*(fog, vignette, subtle distortion)*
* Or heightened awareness briefly
	*(detect nearby movement, enemies glow faintly)*

		Randomized per event.

	Creature Behavior Shift

Nearby creatures may:
* Pause briefly
* Change patrol routes
* Become unusually aggressive
* Or disengage suddenly

	This reinforces the feeling that:

> The land itself is reacting to memory.

---

	Outside this window:
* Only ambient wind exists
* No narrative echoes occur
* No whispers trigger

	This makes the mechanic:
* Rare
* Intentional
* Ritual-like

---

		Narrative Phases Based on Dungeon Progress

The Whispering Wind evolves as the player progresses through "Tomb of the Great Crusaders".

