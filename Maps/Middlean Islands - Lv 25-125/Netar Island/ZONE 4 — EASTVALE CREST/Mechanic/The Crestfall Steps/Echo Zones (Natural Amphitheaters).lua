			Echo Zones (Natural Amphitheaters)

	Description : Some steps form natural bowls where sound behaves unnaturally.

	Mechanics 

	Inside Echo Zones:
* Sounds travel further:
	* Combat noise
	* Footsteps
	* Abilities
* Pulls additional wildlife if combat is prolonged
* Stealth is harder, but:
	* Sound-based abilities are enhanced
	* Taunts and aggro skills have increased radius

	Why It Fits : 

* Directly from “natural amphitheaters where sound amplifies”
* Encourages:
	* Risk vs reward grinding
	* Awareness of positioning

