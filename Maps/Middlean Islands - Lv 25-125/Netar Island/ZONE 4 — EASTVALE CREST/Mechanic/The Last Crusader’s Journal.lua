		Mechanical Purpose of the Journal

	This is not just lore — it is a system element.

	Gameplay Function 

	Once read:
* Player permanently gains:
	* Awareness: *Undead influenced by spirits fear fire more strongly*
	* Slight perception bonus near undead in Zones 4–5 at night
* Unlocks unique environmental reactions in:
	* Crestfall Steps
	* Tomb of the Great Crusaders
	* Zone 5 Northspire Reach

	Not raw power — knowledge becomes advantage.

