		Narrative Form

	The Journal of the Last Crusader

*(Recovered Pages – Full Testimony)*

> I no longer know which day this is. The nights blend into one another, and even the wind has begun to speak with voices that are not its own.
>
> I struck him. Our leader. I did not wish to, but words were no longer enough. When he fell, the sound his body made against the stone was heavier than any blade could have been.
>
> The two constructors… they were gone before that. They did not leave. They did not fall. They simply were no longer among us — yet their voices lingered. I heard them behind me, whispering my name, though their steps made no sound upon the soil. That was when I knew: they were already no longer what they had been.
>
> I could not leave him there. Whatever he had become, he was still our leader, and so I bound him to a crude trolley of broken planks and rope and dragged him northward, away from the mountain that breathes death.
>
> The land itself resisted me.
>
> Beasts came in waves, as if drawn by something I carried. Wolves, first. Then creatures of the marsh. I avoided the forest where the shadows move even when the wind is still, and chose instead the shore.
>
> There, the waters betrayed me as well. Crocodiles surged from beneath the surface like living traps, and more than once I thought the sea itself would claim us both. Yet I pressed on, because stopping meant surrender.
>
> When I rested at last, just before nightfall, he awoke.
>
> At first, I thought he had merely slept. But when he spoke, his voice did not sound like the man I had followed into exile. His arms and legs bore strange markings, dark and branching, as though veins had learned new paths beneath his skin.
>
> We argued. He spoke of staying. Of returning. Of listening.
>
> I struck flint and raised a branch aflame — and in that moment, he recoiled. Not as a man afraid of fire, but as something wounded by its very presence. A sound escaped him then… not a scream, not a cry — something hollow and sharp, as if air were being torn.
>
> And he fled into the forest.
>
> I remained there, shaking, staring at the fire as if it were the last truth left in the world.
>
> It was then I understood: Jey had already been one of them. He had listened while I spoke of escape. I had led death to our door myself.
>
> Still, I continued north.
>
> When I reached the shore where the ship was meant to be, I found only ruin. Bodies lay tangled like driftwood, torn and twisted beyond recognition. No one lived. No voices called out. Only the sea remained indifferent.
>
> Despair took me then — until I remembered the boat I had hidden days before, not far from where I had fought my leader. A foolish hope, perhaps, but it was all that remained.
>
> I turned back.
>
> That is when they came.
>
> A great host of the dead moved through the trees, silent, tireless, led by a towering spirit whose form rose like mist shaped by hatred. With her arms she pulled them like strings, bending their will to hers.
>
> Among them… I saw him.
>
> My leader was no longer fleeing. He walked among them now, a hollow thing wrapped in what remained of command.
>
> I ran.
>
> The forest twisted. The ground betrayed me. No beasts came to my aid. No wind spoke in comfort. Even the world seemed to hide itself from me.
>
> I crawled beneath a great rock as night fell and wrote this by trembling hand.
>
> If this is found, know this: fire is their fear, but not their end. They do not die as men do. And the mountain was only the beginning.
>
> It is done.
>
> Instead of becoming part of them…
>
> I will take my life.

*(The final page is stained with darkened blood. The writing does not continue.)*

