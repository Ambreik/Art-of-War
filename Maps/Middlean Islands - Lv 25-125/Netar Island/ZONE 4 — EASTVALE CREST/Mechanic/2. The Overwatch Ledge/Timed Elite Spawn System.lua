			Timed Elite Spawn System

---

Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Luminous Ridge / Overwatch Ledge

---

	Mechanic : Timed Elite Spawn System

	Name : "The Ascendant Warden" (Ghostly Vampiric Elite)

---

		Spawn Conditions

	Appears only at fixed world times :

* 06:00
* 12:00
* 17:00
* 23:00

	Spawn Rules : 

* If not engaged, despawns after:
	* 20 minutes
* If engaged:
	* Remains until defeated or all players leave the ledge
* Only one instance active at a time
* Does not respawn outside these time windows

---

	Elite Classification

* Type: Ghostly / Vampiric / Summoner
* Difficulty: Elite (designed for 2–5 players, extremely punishing solo)
* Role: Sustain-based pressure Elite
* Combat Identity:
	* Not tanky by base HP
	* Becomes durable through vampiric absorption and sustain mechanics

---

		Core Resource Systems

	Dual HP System

	Primary HP Bar :
* Standard HP

	Secondary HP Bar – "Sustain HP"
* Stores all HP gained through vampiric abilities
* Acts as a buffer before true HP is affected
* Depletes first when taking damage
* Cannot be healed by external sources (only vampiric effects)
* If full:
	* Elite gains minor damage reduction

---

		Vampiric Passive System

	Passive: "Siphoning Presence"

	Effects :
* Absorbs:
	* Mana
	* Energy
	* Stamina (depending on player resource type)
* Converts part of drained resources into:
	* Sustain HP
* When taking heavy burst damage (within 1–3 seconds):
	* Can trigger emergency HP drain from players

---

		Core Combat Abilities

---

	Ability 1 – Abyssal Drain

* Type : Targeted Vampiric Channel
* Trigger : When Elite loses more than X% HP in 1–3 seconds

	Effect :
* Channels on nearest players
* Drains HP directly
* Converts to Sustain HP
* Interruptible, but resistant to soft CC

	Purpose :
* Anti-burst mechanic
* Prevents quick solo kill attempts

---

	Ability 2 – Cataclysmic Pulse

* Type : Massive Radius AoE
* Range : Very large

	Effect :
* Deals shadow + resonance damage
* Drains a percentage of player resources
* Adds Sustain HP per player hit

	Special :
* Cannot be fully avoided — positioning only mitigates

---

	Ability 3 – Warden’s Call

* Type : Summoning
* Effect :
	* Summons spectral minions.

	Minion Count Depends On :
* Number of players present
* Damage dealt to elite in last X seconds
* If players are performing very well → more minions spawn

	Minion Traits :
* Low HP
* Moderate damage
* Each hit adds combo points to the elite

---

	Ability 4 – Thunder of Inversion

* Type : Area Shock / Mixed Targeting

	Effect :
* Summons a thunder strike at random zones
* Damages:
	* Players
	* Minions alike

	Special Mechanic :
* All damage dealt to minions by this thunder:
	→ Is converted into Sustain HP for the Elite

	Purpose :
* Forces players to:
	* Either reposition
	* Or stop DPSing minions blindly

---

		Combo System (Secondary Bar)

	Under the Elite’s Sustain HP Bar appears a "Combo Meter".

	Combo Generation

Combo points are gained when:
* Minions hit players
* Elite abilities hit players
* Players fail mechanics (AoE, drains, etc.)

---

		Combo Thresholds & Skill Triggers

	At 40 Combo – "Grasp of Echoes"
* Short-range AoE pull
* Light damage
* Minor resource drain

---

	At 60 Combo – "Wail of Hunger"
* Cone-based scream
* Fear or slow effect
* Medium Sustain HP gain

---

	At 80 Combo – "Spectral Ascension"
* Elite becomes untargetable for a few seconds
* Summons empowered minions
* Combo resets to 40 instead of 0

---

	At 100 Combo – "Dominion of the Void"

	Major Skill :
* Massive AoE
* Heavy damage
* Drains HP and resources from all players
* Fills Sustain HP to a large degree
* Resets combo to 0

---

		Anti-Solo Design

	This Elite is intentionally extremely hard solo due to:
* Constant sustain through vampirism
* Combo generation accelerated by lack of minion control
* Thunder converting minion damage into Elite healing
* Burst punishment via Abyssal Drain
* Resource starvation mechanics

	Solo players can technically defeat it, but:
* Requires exceptional gear
* Perfect execution
* Long duration combat

---

		Player Performance Scaling

	The Elite adapts dynamically:

	If players:
✔ Kill minions too fast → Elite gains Sustain HP
✔ Ignore minions → Combo rises faster
✔ Burst Elite too hard → Drain abilities trigger
✔ Spread damage wisely → Best chance of success

