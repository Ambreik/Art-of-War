			Light Fracture Mechanic – Reflection Mapping

	Name: "Fracture of the Sky"

	Description : The glasslike stone reflects the sky with unnatural clarity. However, the geometric cracks distort the reflection into segmented patterns, 
creating false horizons and shifting light paths.

	Mechanics : 

* At different times of day, light aligns differently across the cracks:
	* Midday :
		* Reflection is blinding
		* Screen glare effect increases
		* Perception-based interactions are suppressed
	* Dusk & Dawn :
		* Light flows along the crack patterns
		* Certain crack intersections glow briefly
* When standing at specific intersections:
	* Players may:
		* Reveal hidden symbols etched faintly inside the stone
		* See projected silhouettes of long-gone figures crossing the terrace
		* Temporarily reveal invisible objects nearby (if any are present)

* These light alignments follow a fixed celestial pattern:
	* Repeats every in-game day
	* But only fully visible at dusk

	Purpose : This mechanic teaches players that not every secret is found by digging — some are found by standing still and looking carefully.

