			Passive Environmental Effect – Mental Stillness

	Name: "Glasswell Calm"

	Description : The silence, reflection, and slow light movements induce a subtle psychological effect.

	Mechanics : 

	While standing idle on the glass surface:
* Combat state drops faster
* Skill interruptions are slightly reduced
* Aggro range from wandering creatures nearby is reduced
* Screen effects become softer and quieter

	This makes Glasswell Terrace:

> A place of mental reset, observation, and preparation — not power.

