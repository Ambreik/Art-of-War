			Thermal Vein Mechanic – The Glowing Streaks

	Name: "Living Veins of Glasswell"

	Description : At dusk, glowing streaks slide beneath the glass surface like slow-moving veins of light.

	Mechanics : 

* These streaks are:
	* Temperature-reactive mineral currents
	* Not magical, but geologically alive
* When a player:
	* Tracks their movement patterns across several cycles:
		* They form recognizable routes
		* Converging toward the narrow crevice
* Standing above a convergence point:
	* Grants:
		* Brief clarity vision (environment becomes sharper)
		* Increased detection radius for secrets
		* Slight cooldown reduction for perception-based skills
* This is NOT a buff — it is a location-based effect, only active while on the terrace.

