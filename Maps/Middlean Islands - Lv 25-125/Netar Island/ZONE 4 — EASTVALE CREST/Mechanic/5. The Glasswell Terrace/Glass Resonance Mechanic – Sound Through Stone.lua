			Glass Resonance Mechanic – Sound Through Stone

	Name: "Echo Beneath the Glass"

	Description : The terrace surface acts as a resonance plate, transmitting vibrations through the polished stone.

	Mechanics : 

* When players:
	* Walk
	* Jump
	* Use heavy abilities
	* Or drop items

	The glass subtly sings in response.

* Certain actions cause:
	* Deep tones → indicating hollow space beneath
	* Sharp tones → indicating dense stone
	* Whispering vibrations → indicating ancient fractures

* Players can:
	* Use this to:
		* Locate the narrow crevice more precisely
		* Identify weak points in the stone surface
		* Detect if something is sealed beneath

* If multiple players move in rhythm:
	* A harmonic resonance occurs
	* Temporarily amplifies hidden patterns or reveals glowing streaks longer

