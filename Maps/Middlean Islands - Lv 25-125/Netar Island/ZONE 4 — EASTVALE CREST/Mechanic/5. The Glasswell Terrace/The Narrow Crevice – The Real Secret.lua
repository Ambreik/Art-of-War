			The Narrow Crevice – The Real Secret

	The crevice is not the reward itself — it is the gateway for future content.

	Name: "The Shardfall Passage"

	Mechanics : 

* The crevice cannot be entered normally at first
* It reacts to:
	* Certain light alignments
	* Certain resonance patterns
	* Or specific quest states (future)
* For now:
	* Players can:
		* Hear air flowing from inside
		* Feel cold currents at dusk
		* See faint light reflections escaping from it at dawn

	This lets the location exist as a mystery long before it becomes a dungeon, trial, or lore chamber.

