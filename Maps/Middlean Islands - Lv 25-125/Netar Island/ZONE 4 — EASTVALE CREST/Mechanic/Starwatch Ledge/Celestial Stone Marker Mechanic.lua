			Aligned Stones – Secret Unlock Mechanic

	Overview : The ancient stones are more than visual curiosities — they are a celestial puzzle. Players who study them over time and interact at the right moments
can unlock hidden POIs, secret chests, or lore entries across Zone 4 and beyond.
	The system is time- and observation-based, rewarding attention and experimentation.

---

	Mechanic Details : 

	1. "Light Alignment Puzzle"

* Players must observe the stones at specific times of day :
	* Dawn → Morning alignment
	* Dusk → Evening alignment
	* Solstice → Special solar alignment
	* Equinox → Balanced shadow alignment
* When a stone is correctly aligned:
	* A faint beam of light or star reflection points to:
		* Hidden treasure caches
		* Rare herb or mineral nodes
		* Future POI locations (like a small cliff cave, wrecked ship, or hidden ruins)
* Visual cues are subtle, requiring players to pay attention:
	* Light intensifies when pointing accurately
	* Shadows stretch in the correct direction

---

	2. "Sequence Activation"

* Certain stones must be interacted with in a specific order during the right time windows.
* Sequence discovery can be player-driven:
	* Trial and error
	* Observing community markings or environmental hints
* Completing a sequence unlocks:
	* Secret loot chests with rare materials
	* Lore entries about Netar Island and the ancient civilizations
	* Temporary buffs for exploration or vision in nearby POIs (optional)

---

	3. "Cross-POI Interaction"

* Light beams or reflections may point to other POIs :
	* Example: A reflection points to a hidden cove along the eastern shore
	* Example: Beam aligns with a natural terrace or stone formation
* Could be tied to a “star map challenge” in future development:
	* Players need to visit multiple POIs in correct order
	* Completing all alignments grants unique collectibles or triggers a hidden event

---

	4. Environmental & Lore Feedback

* Correct alignment triggers subtle effects:
	* Whispering winds increase near aligned stones
	* Ambient chimes or faint celestial music cues
	* Tiny glowing particles trace the path of the light beam
* Encourages exploration, observation, and lore immersion

---

	5. Optional Collectible / Achievement

* Players who complete all alignments or sequences may receive:
	* A "Constellation Journal Entry" (lore)
	* Rare collectible item (e.g., "Celestial Lens", "Starforged Trinket")
	* Minor permanent buff: +5% perception at night or minor movement speed bonus on high ledges

	Flavor for Collectible  :
*This delicate lens captures the alignment of stars, revealing paths hidden to the casual eye. The ancients intended it as a guide, and now it is yours.*

---

	Example in Gameplay : 

1. Player approaches "Aligned Stones" at dawn.
2. Observes a faint ray from the northern stone pointing to a distant cliff.
3. Interacts with the stone → beam intensifies.
4. Notes the direction or follows the light → finds a hidden herb cache.
5. During solstice, interacts with multiple stones in correct sequence → unlocks rare loot chest and a lore entry about Netar Island’s ancient astronomers.

