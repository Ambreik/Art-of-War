			Celestial Stone Marker Mechanic

	Name : "Aligned Stones"

	Description : Ancient stones dot Starwatch Ledge, arranged along precise axes that correspond to solstices, equinoxes, and other celestial events. Each stone is
weathered but etched with faint markings — some geometric, some resembling constellations. The arrangement is not random; the ancient architects intended these 
stones to interact with sunlight, moonlight, and starlight.
	The effect of the stones is most apparent during dawn, dusk, and astronomical events, when the rays of light align perfectly along certain axes, creating small 
but noticeable phenomena.

---

	Mechanics : 

1. "Timed Interactions"
	* Players can interact with the stones only at specific times :
		* Dawn
		* Dusk
		* Solstice
		* Equinox
	* Interaction triggers subtle environmental effects and may reveal hidden visual cues.

2. "Visual Effects"
	* Light Beams / Rays : When the sun or moon hits a stone at the correct angle, a faint beam of light projects toward:
		* Hidden treasures or rare resources
		* Nearby POIs, cliff paths, or distant landmarks
		* Future puzzle locations
	* Star Reflections : At night, stars reflect across specific stones, creating glimmering lines that may form patterns or hint at secret interactions.

3. "Puzzle & Lore Potential"
	* Stones may be part of a future constellation puzzle, requiring players to:
		* Activate stones in a particular sequence
		* Observe the alignment of light and shadow
		* Connect multiple POIs or hidden dungeon entrances
	* Could unlock secret loot, lore entries, or trigger hidden world events.

4. Environmental Feedback
	* Correct alignment may trigger subtle environmental cues:
		* Wind shift over the ledge
		* Soft ambient chimes
		* Shadows stretching along the ground to guide players

5. Exploration Reward
	* Encourages careful observation rather than combat
	* Supports roleplay and discovery mechanics
	* Players may leave marks or notes for others, forming a community-driven puzzle over time

---

	Flavor Text : 
*The stones seem silent, but when the light strikes just so, you feel as if they are watching… or guiding. Each angle and shadow whispers of a knowledge lost to time, and those who pay attention might just glimpse what the ancients intended.*

