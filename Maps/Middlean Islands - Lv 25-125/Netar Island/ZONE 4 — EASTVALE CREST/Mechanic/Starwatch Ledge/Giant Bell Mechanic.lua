			Giant Bell Mechanic

	Name : "Echoing Ledge Bell"

	Description : A massive bronze bell stands atop a stone pedestal. Its surface is weathered and ancient, inscriptions hint at a ritual or signal function.

	Mechanics :

* Players can strike the bell with a heavy object (or interact to ring it)
* Immediate Effects :
	* All nearby wild animals are scared, fleeing the area for a short time (e.g., 2–3 minutes)
	* Crocodiles along the eastern shore specifically react
* Nighttime Special Effect :
	* Ringing the bell at night triggers a "Crusader Spawn Event" :
		* Undead Crusaders appear across "Starwatch Ledge"
		* They spawn randomly but with warning sound cues for players to react
* Sound Propagation :
	* Bell sound travels over the zone
	* Nearby players can hear from far away, even on cliffs or beaches

	Flavor :
*The deep toll echoes across the ledge, shaking the earth beneath your feet. Wildlife scatters, but something unnatural stirs in the darkness…*

