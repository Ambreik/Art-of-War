			Telescope Observation Mechanic

	Name : "Astral Viewer"

	Description : A sturdy brass telescope is mounted on the ledge, aimed toward the eastern sea and horizon. It allows travelers to scan long distances during day 
or night, depending on weather and light conditions.

	Mechanics :
* Players can interact with the telescope to observe faraway locations :
	* Spot enemy encampments
	* Detect rare monster spawns along the coast
	* Locate hidden POIs in nearby zones
* Daytime : Long-distance visibility is clear, ideal for scouting islands or beaches
* Nighttime : Constellations visible; can trigger a small lore event if a player observes certain stars aligned
* Limited by fog, rain, or storms

	Optional Special Use :
* Certain celestial alignments (e.g., solstice or equinox) may reveal hidden symbols on the ground or activate secret POI interactions

	Flavor :
*Through the brass tube, the world stretches far beyond your footsteps. Stars and sea alike whisper of places yet unseen.*

