			The Crestfall Faultway

	Type: Dungeon Exit & World-Link Secret Location

This is not a reward location.
This is a world infrastructure location — and those are rare and powerful.

---

		Core Purpose

	The Crestfall Faultway serves as:
✔ A one-way (or semi-one-way) exit from the second half of the Tomb of Great Crusaders
✔ A world-consistent physical breach created by the same cataclysm that fractured the Crusaders’ citadel
✔ A spatial connection between:

> "Tomb of Great Crusaders" (Spirit Section) → "Eastvale Crest surface"

	This immediately:
*  Makes your dungeon feel embedded in the world
*  Avoids teleport-only logic
*  Adds realism and exploration value
*  Allows future quest hooks

---

		How Players Exit From the Dungeon

Inside the Tomb (Spirit Section), there is: "The Fractured Vein" (Internal Dungeon Area)

	A collapsed spiritual corridor where:
* Stone is warped
*  Walls bleed mist
*  Crystals identical to Crestfall Faultway appear
*  Wind can be heard long before seen

	At the end:
Players reach a broken wall revealing:
👉 a steep, natural fault sloping upward
👉 this is the internal side of the Crestfall Faultway

---

		How Players Physically Climb Up

	Now to your key question:

> How do players come up from this hole?

We do NOT use teleporting here.
We use "Environmental Traversal".

		Exit Mechanic: Faultway Ascension System

	Method 1 – Crystal Refraction Climb

	Those jagged, non-glowing crystals are the key.

	They:
*  Refract light into beams
*  Reveal hidden footholds and grips when illuminated

	Inside the dungeon:
Players receive or can use:
*  Torches
*  Fire spells
*  Spirit-light
*  Or dungeon lanterns

	When light hits the crystals:
✔ Temporary light-bridges appear
✔ Or climbing routes become visible
✔ Or spectral handholds manifest

	This turns the exit into:
A short environmental traversal puzzle, not a combat mechanic.

---

	Method 2 – Wind Channel Lift

	As players ascend:
*  Sudden updrafts occur from deep below
*  These allow players to:
	* Jump higher
	* Glide briefly
	* Or be carried upward through narrow shafts

	This ties directly with:
✔ The slanted spiral
✔ The wind described in the faultway
✔ The dungeon’s spirit-energy disturbances

---

	Method 3 – Group-Based Exit Assistance

	To prevent solo trivialization and to promote co-op:

	Certain ledges require:
*  A player to stabilize a crystal
*  Or channel energy into a fracture
*  Or hold a stone plate

	Allowing:
*  Another player to pass
*  Then roles switch

	This reinforces:
✔ Group identity
✔ Physical world logic
✔ No invisible walls or fake exits

---

		Surface Exit Logic

	When players finally emerge:

They exit from:

> Beneath the fallen tree
> On the correct side of its trunk
> As per your description

	From the outside:
✔ The entrance is invisible unless approached from that angle
✔ From the inside, it is always usable

	This means:
*  You can’t enter the dungeon from here
*  But you can leave it here
*  Which is perfect dungeon logic

