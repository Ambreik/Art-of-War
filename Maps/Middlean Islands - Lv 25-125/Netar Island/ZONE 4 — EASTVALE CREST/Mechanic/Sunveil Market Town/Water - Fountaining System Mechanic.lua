			Water / Fountaining System Mechanic

---

	Core Identity : The system represents town water infrastructure — wells, channels, and fountain terraces — that is both environmental and interactive, allowing 
players to manipulate it for combat and tactical purposes.

---

	Physical Structure : 
* Wells: Scattered around central market and residential terraces
* Water Channels: Connect wells to fountains and drains, flowing along streets and ramps
* Fountains / Terraces: Central plaza fountains and minor residential decorative fountains
* Slopes / Drains: Water can flow onto paths, ramps, and stairways, creating tactical zones

---

		Mechanic Details

	1. Primary Interactions : 

* Extinguish Fire :
	* Water can neutralize fire-based attacks (balloons, incendiary ballista)
	* Reduces damage and prevents environmental hazards from spreading
* Slippery Surfaces:
	* Water flowing on ramps or stairways can create slip zones, reducing movement control (-10–15% speed, occasional stagger)
* Stamina Drain:
	* Moving through channels or running on wet surfaces slightly drains player stamina faster, requiring tactical pacing

---

	2. Environmental / Raid Synergy : 

* Hot Air Balloons:
	* Dropped fireballs can be extinguished if landing on water channels
* Incendiary Ballista:
	* Fire attacks hitting water zones have reduced area effect
* Crowd Panic / Civilian Movement:
	* Crowds may slip or reroute on wet surfaces, creating emergent pathing challenges
* Guard AI:
	* Guards moving through wet areas experience minor movement penalties, making them more predictable or exploitable
* Cragwallower AI:
	* Certain units (Scout, Soldier) can slip on wet ramps, reducing mobility temporarily
	* Mage / Berserk units less affected, but may misstep if near fountain edges

---

	3. Player Tactical Use : 

* Defensive Counterplay:
	* Player-controlled water release (via fountain valves or channels) can mitigate fire hazards during raids
	* Can manipulate enemy positioning by creating slippery zones in choke points
* Combat Positioning:
	* Combine with stairs/terrace advantage : enemies moving uphill on wet surfaces may miss attacks or stagger
* Environmental Creativity:
	* Players may bait Cragwallowers into slipping zones while targeting them with balloons or artillery

---

	4. Hidden / Rare Interactions : 

* Flood Boost:
	* Fully opening multiple fountains in sequence creates temporary high water flow, knocking back or staggering enemies (especially Scouts or Soldiers)
* Crowd Synergy:
	* Panic + wet zones can redirect civilians in unexpected ways, potentially blocking paths for enemy AI
* Gliding Trick:
	* Players can glide onto wet channels to slide into strategic positions or escape combat
* Fire Dampening Chain:
	* Water combined with fog + shimmer can drastically reduce fireball threat during Alpha or elite waves, rewarding environmental mastery

---

	5. Purpose : 

* Introduces player/environmental combat synergy
* Rewards strategic thinking rather than pure stats
* Integrates with all Sunveil Market Town mechanics :
	* Hot air balloons
	* Incendiary artillery
	* Guard AI
	* Crowd dynamics
	* Fog / shimmer / terraces

---

	This mechanic makes "Sunveil Market Town" not just vertically strategic, but also environmentally reactive, giving players tools to manipulate combat and 
emergent events.

