			Crusader Invasion Mechanic

---

		Core Identity

	This mechanic introduces a reactive threat that scales dynamically based on:
* Player population in the town
* Player activity (alarm triggers: artillery, balloon, combat)
* Time of day (afternoon/evening favored)

	Unlike Cragwallower raids:
* No loot / EXP / gold
* Focus is REP gain, coordination, and emergent challenge

---

		Physical Structure : 

* Spawn Zones : Town outskirts and plains leading to main gate
* Town Entrance : Key choke point where leader (Berserk Crusader) spawns
* Patrol Zones : Inner town streets; waves of Soldiers / Archers continuously spawn while leader persists

---

		Trigger Conditions : 

* Player Count : ≥ 20 players inside Sunveil Market Town

* Time of Day : Afternoon / Evening (6 PM – 10 PM in game)

* Player Actions :
	* Hot air balloon ascent
	* Artillery firing
	* Loud combat in town (sustained over 10–15 seconds)

* Cooldown :
	* After event ends, minimum 30 minutes before next trigger

---

		Wave & Spawn Logic

	Leader Spawn : 

* 1 × Berserk Crusader Leader spawns near town entrance
* Behavior:
	* Aggressively attacks first target in town
	* While alive, continuous soldier/archer spawns every 15–20 seconds

	Wave Spawn (Continuous) 

* Soldiers spawn from the plains or outskirts
* Archers spawn on elevated positions / walls to pressure players
* Spawn numbers scale with player count :
	* 20–30 players: 3 Soldiers + 1 Archer per interval
	* 31–50 players: 5 Soldiers + 2 Archers per interval
	* 50+ players: 7 Soldiers + 3 Archers per interval

---

		Environmental & Mechanic Integration

* Hot Air Balloons : Can be used to attack waves or leader temporarily, but cooldowns prevent permanent dominance
* Incendiary Artillery : Can target spawning points, slows continuous wave pressure
* Crowd Dynamics : Civilians may flee and create obstacles
* Fog / Shimmer / Wind : Can influence ranged accuracy of Archers and player defenses
* Stairs/Terraces : Players can defend key points; leader AI may try to force paths through stairs
* Water System : Extinguishes any fire-based artillery misfires

---

		REP & Reward

* Same Sunveil Market Town REP as mini-event mechanic:
	* Wave participation = REP gain
	* Guards / Civilians protected = bonus REP
	* Environmental mastery = bonus REP (balloons, artillery, water, terrace positioning)
* No loot, no gold, no EXP — purely reputation-driven incentive

---

		Scaling & Dynamic Flow

* Event intensity scales with players :
	* More players = more frequent spawns, higher waves
* Leader alive : triggers continuous spawning
* Leader dies : spawn stops, but event persists for remaining waves (~1–2 minutes)
* Multiple events per day : afternoon/evening triggers may repeat, max 1 per 30 minutes

---

		Hidden / Emergent Interactions

* Berserk Crusader Leader AI :
	* Prioritizes players using environmental mechanics (balloons, artillery)
	* Can be baited into choke points / slippery surfaces / terrace traps
* Wave Exploit : Players who coordinate crowd + water + artillery + terrace positioning can delay or reduce spawn efficiency
* Noise Amplification : Multiple players triggering alarms simultaneously increases spawn frequency slightly (max +20%)
* Fog + Light Shimmer : Can obscure Archers’ line-of-sight, giving players tactical advantage if coordinated

