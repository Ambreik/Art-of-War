			Hot Air Balloon Environmental Combo

	Structure :
* Hot air balloons located on military terraces of "Sunveil Market Town"
* Balloons can be mounted temporarily during invasions or events
* Wind draft zones (ridge winds) influence balloon movement and trajectory
* Adjacent terraces and alleyways create vertical and horizontal AoE opportunities

---

	Trigger :
* Player must mount the balloon and drop water barrels or incendiary bombs at a precise timing
* Altitude and wind conditions must align:
	* Altitude: 5–10 meters above target area
	* Wind: moderate, aligned with enemy positions
* Works during Crusader invasion, "Cragwallower" raids, or other emergent events

---

	Effects :

1. AoE Damage / Splash :
	* Incendiary Bomb :
		* Radius: 6 meters
		* Damage: 150–200
		* Ignites enemies for 5 seconds (~15 DPS)
	* Water Barrel :
		* Radius: 4 meters
		* Pushes enemies back (~2–3 meters)
		* Can extinguish fires from other mechanics (ballista, firepots)

2. Environmental Combo / Drift :
	* Wind draft allows balloon to drift horizontally, covering multiple targets if timed correctly
	* Enables emergent multi-target effects when combined with other town mechanics:
		* Terrace Echo Exploit : funnel mobs into AoE zone
		* Water Channels : amplify splash knockback or steam cloud effects
		* Ballista Firepot : chain explosions with flame + AoE from balloon

3. Physics / Emergent Interaction :
	* Balloon can interact with verticality : drop items onto stairs, upper terraces, or choke points
	* Players must time release carefully to avoid missing or hitting unintended targets

---

	Hidden Use :
* Only triggers if:
	* Balloon reaches specific altitude
	* Wind draft is aligned correctly with target area
	* Player uses correct payload (water barrel or incendiary bomb)
* Mis-timed drops = minimal effect (water splashes, minor fire, or no AoE)
* Encourages skill-based experimentation and environmental mastery

---

	Integration with Mechanics :
* Water / Fountaining System : Enhances steam cloud effects when incendiary bomb hits water zone
* Incendiary Artillery : Chain explosions with firepot or ballista for massive AoE
* Crowd Dynamics : Balloon drops can push mobs into fleeing NPCs, creating emergent paths or blockages
* Guard AI : Guards respond dynamically, adjusting patrol paths or defending areas hit by balloon drops
* Multi-Mechanic Combo : Works with terrace echoes, water, artillery, and crowd interactions for rare emergent tactical advantage

---

	Reward / Incentive :
* REP Bonus : +15–20 for precise use during event scenarios
* Hidden achievement if combined with at least two other LCWM in a single chain reaction
* Temporary visual reward: plume of fire, water splash effect, or balloon drift trail

