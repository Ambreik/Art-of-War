			Wind Draft Gliding / Environmental Influence

		Core Identity

	This mechanic represents localized, terrain-bound wind behavior created by the elevated architecture of "Sunveil Market Town" and the natural ridge drafts of 
Zone 4.

	It is not:
* Permanent flight
* A travel replacement
* A mount system

	It is:
* A situational movement enhancer
* A knowledge-based traversal mechanic
* A tactical repositioning tool

	Players who understand the town’s shape and wind behavior gain movement advantages without raw power increases.

---

		Physical Structure & Placement

	The mechanic exists naturally at:
* Upper terraces
* Balcony edges
* Open stair landings
* Exposed parapets
* Market awnings and rooflines

	No glowing effects.
	No obvious indicators.

	Subtle environmental tells include:
* Hanging cloth movement
* Wind chimes
* Dust and leaf drift
* Sound pitch changes

---

		Activation Conditions

	The mechanic triggers when:

* A player moves from a height-exposed surface
* Into an active ridge-aligned wind draft
* At the correct angle and timing

	It is not a button press.
	It is a movement outcome.

---

		Player Interaction & Movement Behavior

	Short-Distance Gliding

	When aligned correctly:
* Fall speed is reduced
* Horizontal momentum increases
* Player can clear:
	* Streets
	* Courtyards
	* Defensive gaps

	This allows:
* Fast repositioning
* Escape from threats
* Access to elevated or otherwise awkward locations

---

		Directional Influence

	Wind drafts:
* Favor specific directions
* Shift subtly over time

	Experienced players:
* Learn optimal launch points
* Anticipate drift
* Adjust mid-air movement

	Inexperienced players:
* May overshoot
* May undershoot
* May land awkwardly

---

		Combat & Tactical Use

	During combat situations:
* Players can disengage vertically
* Melee enemies struggle to follow
* Ranged enemies must reposition

	This enables:
* Hit-and-run tactics
* Defensive retreats
* Ambush repositioning

	The wind does not guarantee safety — only opportunity.

---

		Environmental Integration (Zone 4)

	The mechanic directly responds to:
* Ridge Draft Winds
* Minor atmospheric fluctuations

	Effects include:
* Variable glide distance
* Slight altitude gain or loss
* Altered landing momentum

	These changes are:
* Consistent enough to learn
* Variable enough to prevent automation

---

		Risk & Failure States

	Poor usage can result in:
* Hard landings
* Temporary disorientation
* Forced ground exposure

	This keeps:
* The mechanic skill-based
* Failure meaningful but not punishing

---

		Town Design Synergy

	Sunveil Market Town is built to:
* Encourage vertical movement
* Reward spatial awareness
* Feel navigable in three dimensions

	This mechanic:
* Makes the town feel alive
* Turns architecture into gameplay
* Encourages exploration without quests

---

			Hidden Interactions – Wind Draft Gliding

	These are not explained.
	They are discovered by curiosity, accident, or mastery.

---

		1. Chain Draft Momentum

	Discovery : 
* Player glides from one draft into another

	Effect : 
* Momentum briefly stacks
* Second glide carries further

	Encourages:
* Route planning
* Town mastery

---

		2. Soft Landing Exploit

	Discovery : 
* Player glides into sloped surfaces or cloth awnings

	Effect : 
* Fall damage negated
* Immediate movement recovery

	Rewards:
* Environmental reading

---

		3. Wind Stall Misuse

	Discovery : 
* Player jumps straight upward into draft

	Effect : 
* Brief hover
* Then forced drop

	Purpose:
* Breaks assumption of vertical safety

---

		4. Combat State Interaction

	Discovery : 
* Player glides while recently in combat

	Effect : 
* Wind effectiveness reduced
* Prevents guaranteed escape

	Keeps PvE tension intact.

---

		5. Weight & Encumbrance Response

	Discovery : 
* Heavily encumbered players glide poorly

	Effect : 
* Shorter distance
* Faster descent

	Narratively logical, mechanically subtle.

---

		6. Directional Silence

	Discovery : 
* Landing behind enemies via glide

	Effect : 
* Reduced detection window
* Enables surprise positioning

	No stealth buff icons.
	Just behavior.

---

		7. Time-of-Day Drift Variance

	Discovery : 
* Glide behavior differs slightly at certain hours

	Effect : 
* Longer or shorter carry

	Only noticeable to attentive players.

---

		8. False Confidence Trap

	Discovery : 
* Repeated success from same ledge

	Effect : 
* Wind shifts slightly
* Player misjudges next jump

	Prevents muscle-memory abuse.

---

		9. NPC Awareness Reaction

	Discovery : 
* NPCs notice frequent aerial movement

	Effect : 
* Dialogue changes
* Guards comment on “reckless jumping”

	World reacts subtly.

---

		10. Non-Rewarding Experiments

	Some jumps:
* Do nothing special
* Fail quietly

	This is intentional:
* Preserves mystery
* Prevents mechanic from becoming formulaic

---

		Why This Mechanic Is Strong

* Zero UI dependency
* Fully diegetic
* Skill-based
* Exploration-driven
* Makes the town feel designed, not decorated

	t feels exactly like:

> “People who know this town move differently.”

