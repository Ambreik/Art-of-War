			Time-Based Social Roles Mechanic

> A town is not one place — it is many, depending on the hour.

---

		Core Principle

	NPCs do not just move — they change roles, influence, and behavior depending on time.

	Time alters:
* Who is present
* Who has authority
* What is possible
* What is risky

---

		Daily Social Phases (Example for Sunveil Market Town)

	1. Dawn (Low Activity / Reset Phase)

	Time : Early morning

	Town State :
* Low population
* Quiet streets
* Guards relaxed
* Merchants preparing stalls

	NPC Roles :
* Vendors = setup mode
* Guards = patrol + observation
* Smugglers = inactive / exiting

	Gameplay Effects :
* Best time for:
	* Clean pickpocketing
	* Silent crafting
	* Information gathering
* Rumors reset
* Loitering suspicion builds slower

---

	2. Midday (Commerce Authority Phase)

	Time : Late morning → afternoon

	Town State :
* Crowded
* Noisy
* Guard presence high
* Merchants dominant

	NPC Roles :
* Merchants control flow
* Guards enforce order
* Civilians form dynamic obstacles

	Gameplay Effects :
* Market penalties activate
* Crafting noise propagates further
* Smuggler recognition harder
* Ballista use draws attention faster

---

	3. Evening (Transition / Opportunity Phase)

	Time : Late afternoon → dusk

	Town State :
* Population shifts
* Guards rotate
* Civilians linger
* Lighting changes

	NPC Roles :
* Guards less predictable
* Merchants close stalls
* Smugglers begin observing

	Gameplay Effects :
* Rumor chains activate strongly
* Smuggler recognition easiest
* LCWM triggers more frequent
* Informal gathering zones active

---

	4. Night (Shadow Authority Phase)

	Time : Night

	Town State :
* Reduced visibility
* Quiet pockets
* Focused patrols

	NPC Roles :
* Guards = response units
* Smugglers = primary actors
* Certain NPCs disappear

	Gameplay Effects :
* Pickpocket risk high / reward high
* Crafting noise extremely risky
* Loitering suspicion fast
* Hidden events spawn

---

		NPC Spatial Reassignment

	NPCs change location, not just behavior:

| NPC Type   | Day           | Night              |
| ---------- | ------------- | ------------------ |
| Merchants  | Market stalls | Inns / homes       |
| Guards     | Walls, gates  | Patrol chokepoints |
| Smugglers  | Hidden        | Market edges       |
| Quest NPCs | Public        | Private interiors  |
| Civilians  | Streets       | Indoors            |

---

		Authority Shift System

	Who “controls” the town changes:

* Day → Merchants
* Evening → Guards
* Night → Smugglers / Shadows

	This affects:
* Who questions you
* Who notices you
* Who offers opportunities

---
	
		Mechanic Synergies

	With Loitering Awareness : 
* Day: slow suspicion
* Night: fast suspicion

	With Rumor Chains : 
* Rumors evolve by time
* Some rumors only exist at night

	With Smuggler Recognition : 
* Evening = optimal
* Day = risky
* Night = commitment test

	With Market Systems : 
* Stall specialization strongest at day
* Smuggler pricing strongest at night

---

		Hidden Interactions

	Time-Locked LCWM

	Certain LCWMs:
* Only trigger at dusk
* Require night + fog
* Require dawn silence

	Fake Time Manipulation

* Bells
* Alarm abuse
* Event overlap can desync roles temporarily

---

		Player Skill Expression

	Advanced players learn:
* Best hours to act
* Best NPCs to approach
* When to wait
* When to provoke

---

	Anti-Exploit Design

* No hard locks
* No forced downtime
* Time windows overlap
* AFK does nothing

