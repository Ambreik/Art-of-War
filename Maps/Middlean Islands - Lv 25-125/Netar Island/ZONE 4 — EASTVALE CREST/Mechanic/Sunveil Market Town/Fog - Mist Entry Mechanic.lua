			Fog / Mist Entry Mechanic

		Core Identity

	This mechanic represents a temporal environmental state where mist from the "Sunwell Basin" infiltrates "Sunveil Market Town" during specific low-light periods.

	It is not a weather system in the classic sense.
	It is a localized atmospheric condition that subtly alters perception, detection, and behavior, rather than raw combat numbers.

	The fog does not exist to obscure randomly — it exists to change how the town is experienced.

---

		Spatial & Temporal Structure

	Where the Fog Appears

* Narrow streets
* Market alleys
* Lower terraces
* Gate approaches
* Areas close to stone and water runoff

	It does not fully blanket the town.
	Upper terraces and high walls often remain clearer.

---

	When It Appears
* Early morning
* Early evening
* Transition moments (dawn / dusk)

	It is:
* Predictable in timing
* Unpredictable in intensity

---

		Visibility & Perception Effects

	Player Vision
* Reduced medium-range visibility
* Close-range clarity remains mostly intact
* Distant silhouettes visible before details

	This creates:
* Unease
* Anticipation
* Misjudgment opportunities

---

		Detection Range Alteration

	Fog modifies line-of-sight logic :

* Players detect enemies later
* Enemies detect players later
* Detection becomes angle- and movement-based

	Standing still:
* Makes players harder to detect

	Moving fast:
* Increases visibility footprint

---

		Combat & Movement Impact

* Ambushes become viable in town spaces
* Ranged combat effectiveness drops
* Melee engagement distances shorten naturally

	This changes:
* How players approach danger
* How quickly situations escalate

---

		Non-Combat Tension Use

	This mechanic is not combat-only.

	Fog supports:
* Clandestine actions
* Quiet movement
* Observation without commitment

	It encourages:
* Slower pacing
* Listening instead of scanning
* Spatial awareness

---

		Zone 4 Environmental Integration

	Directly linked to:
* Mist Surge at Dawn

	The fog:
* Moves inward from the basin
* Slowly dissipates as light strengthens
* Can retreat suddenly if winds shift

	This ties it into the same environmental logic as wind drafts and ridge effects.

---

		Failure & Risk

	Fog does not protect by default.

	Poor decisions may lead to:
* Walking into threats
* Losing orientation
* Being flanked unknowingly

	It rewards:
* Patience
* Sound awareness
* Familiarity with town layout

---

			Hidden Interactions – Fog / Mist Entry Mechanic

	These are never explained, never guaranteed, and not always beneficial.

---

		1. Sound-Based Awareness Shift

	Discovery
* Players rely on audio cues more than visuals

	Effect
* Footsteps, armor clinks, breathing matter more
* Loud actions increase detection

	Fog punishes careless movement.

---

		2. Silhouette Misreading

	Discovery
* Players mistake shapes for enemies or NPCs

	Effect
* False threat perception
* Hesitation or misplays

	Psychological pressure is intentional.

---

		3. Motion Contrast Exploit

	Discovery
* Slow-moving players blend into fog

	Effect
* Reduced detection radius
* Effective shadowing of targets

	Standing still can be safer than moving.

---

		4. Light Source Betrayal

	Discovery
* Torches, spell glows, fire effects become visible earlier

	Effect
* Light sources increase detection range
* Fire-based actions negate fog concealment

	Synergizes naturally with artillery & balloons.

---

		5. Fog Layer Memory

	Discovery
* Certain streets fog more often

	Effect
* Players learn “safe” mist paths
* Town mastery emerges organically

---

		6. Sudden Clarity Shift

	Discovery
* Wind drafts clear fog briefly

	Effect
* Visibility spike
* Exposes hidden movement

	Combines with wind mechanics without overlap.

---

		7. Height Advantage Reversal

	Discovery
* Fog pools low

	Effect
* Elevated players see more
* Ground-level players move unseen

	Vertical gameplay reinforced.

---

		8. Fog Fatigue

	Discovery
* Prolonged fog exposure subtly affects awareness

	Effect
* Slight reaction delay
* Tunnel vision

	Not debuff icons — felt, not shown.

---

		9. Enemy Behavior Drift

	Discovery
* Enemies hesitate more in fog

	Effect
* Slower patrols
* Shorter pursuit

	Creates windows for action, not safety.

---

		10. False Comfort Trap

	Discovery
* Players assume fog equals safety

	Effect
* Close-range threats become more dangerous
* Panic escalates faster

---

		11. Unrewarded Exploration

	Some fog interactions:
* Lead nowhere
* Do nothing
* Exist only to unsettle

	This preserves:
* Uncertainty
* Atmosphere
* Authenticity

---

		Why This Mechanic Belongs in "Sunveil Market Town"

* Reinforces mood and identity
* Alters familiar spaces without changing geometry
* Encourages mastery of town layout
* Supports multiple playstyles
* Interacts softly with other Zone 4 systems

	It makes the town feel:

> Alive, breathing, and occasionally untrustworthy.

