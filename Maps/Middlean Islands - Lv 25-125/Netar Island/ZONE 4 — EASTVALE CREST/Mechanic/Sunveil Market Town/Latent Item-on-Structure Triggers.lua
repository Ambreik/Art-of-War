			Latent Item-on-Structure Triggers

	Structure / Setup :
* Found on interactive structures in town: furnaces, ballista platforms, workshop alcoves, bell towers, water channels
* Items must be physically usable / throwable / interactable by the player
* Correct items trigger hidden beneficial effects, incorrect items trigger unexpected consequences

---

	Good Outcome Items :

1. Alchemist’s Flash Powder (crafted in market workshop)
	* Use : Tossed into furnace alcove
	* Effect : Creates enhanced flame burst, knocking back enemy soldiers and adding +5–10 REP if used on invading mobs

2. Volatile Water Barrel (special barrel filled with alchemical solution)
	* Use : Dropped from balloon onto water channels
	* Effect : Creates steam cloud, reducing ranged enemy accuracy by ~20% for 10 seconds; may also trigger AoE environmental synergy with furnaces

3. Crusader Signal Orb (crafted from rare market materials)
	* Use : Thrown onto bell tower or terrace echo point
	* Effect : Temporarily redirects enemy patrols, funnels soldiers into traps or into balloon / artillery AoE zones

---

	Wrong / Bad Outcome Items :
	
1. Generic Stone or Rock
	* Use : Tossed into furnace, ballista, or alcove
	* Effect : Produces minor smoke, harmless debris, may alert nearby enemies to player location without benefit

2. Spoiled Crafting Ingredients (rotten herbs, failed alchemical items)
	* Use : Dropped on balloon or terrace echo
	* Effect : Creates slightly delayed or misfired AoE, potentially damaging player or allies, no REP gained

3. Random Civilian Item (bread loaf, market crate, bucket)
	* Use : Interacted with workshop or LCWM structure
	* Effect : Causes chaotic NPC reactions, civilians flee unpredictably; may block path or interfere with player movement

---

	Hidden / Emergent Interactions :
* Certain rare combinations of good items may chain LCWM effects for a massive emergent result
* Incorrect item use may reveal hidden “safe combos” or teach players about structure interactions
* Encourages player experimentation and discovery

---

	Integration with Mechanics :
* Works in synergy with:
	* Ballista / Balloon / Furnace mechanics
	* Terrace Echo Exploit
	* Water Channel Tactical Exploit
	* Crowd dynamics and NPC AI

---

	Reward / Incentive :
* REP Bonus : +5–15 for successful hidden combinations
* Hidden achievement for discovering 3+ rare item-on-structure chains
* Provides emergent, rewarding gameplay without explicit guidance

