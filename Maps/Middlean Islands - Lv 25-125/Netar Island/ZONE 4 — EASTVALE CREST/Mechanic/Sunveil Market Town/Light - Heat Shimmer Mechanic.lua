			Light / Heat Shimmer Mechanic

		Core Identity

	This mechanic represents optical distortion caused by intense midday sunlight interacting with pale stone architecture and ambient heat.

It is not a debuff.
It is not blindness.
It is perceptual interference.

	The goal is to:
* Reduce long-range precision
* Encourage positional awareness
* Make time-of-day a meaningful factor

---

		Environmental Structure & Scope

	Affected Areas : 
* Pale stone terraces
* Sun-exposed streets
* Open plazas
* Surrounding approaches near the town

	Unaffected Areas : 
* Shaded alleys
* Covered walkways
* Interior spaces
* Mechanical structures (ballistas, artillery, balloons)

	This exception reinforces:
* The reliability of engineered tools
* The fallibility of organic perception

---

		Activation Conditions

	The shimmer activates during:
* Midday sun
* Peak light reflection periods
* Low wind conditions

	It fades naturally as:
* Sun angle changes
* Shadows lengthen

	No hard on/off switch — it ramps in and out subtly.

---

		Ranged Accuracy Impact

	What Is Affected : 
* Manual ranged attacks
* Line-of-sight targeting
* Visual aim precision

	Effects include:
* Slight reticle drift
* Target outline distortion
* Misjudged distance perception

	The player:
* *Feels* the inaccuracy
* Isn’t told numerically

---

		What Is NOT Affected

* Close-range attacks
* Area-of-effect abilities
* Mechanical targeting systems

	This preserves:
* Class viability
* Tactical diversity

---

		Combat & Tactical Implications

* Long-range sniping becomes unreliable
* Players are encouraged to:
	* Close distance
	* Change elevation
	* Reposition into shade

	Combat feels:
* More physical
* More situational
* Less static

---

		Zone 4 Integration

	Linked directly to:
* Heat Shimmer Distortion from Luminous Ridge

	The same optical logic:
* Applies regionally
* Feels consistent across Zone 4

	Players begin to associate:

> “This light lies.”

---

			Hidden Interactions – Light / Heat Shimmer

	These are subtle and not always beneficial.

---

	1. Distance-Based Error Curve

* Mid-range suffers most
* Extreme long-range shots may appear accurate but land off-center

	Breaks false confidence.

---

	2. Movement Compensation

* Moving targets are harder to track
* Stationary targets briefly appear clearer

	Encourages dynamic positioning.

---

	3. Shadow Edge Exploit

* Firing from shade into light slightly improves accuracy
* Firing from light into light is worst

	Rewards environmental positioning.

---

	4. Vertical Angle Distortion

* Shooting uphill or downhill exaggerates shimmer
* Flat trajectories perform better

	Ties into town elevation design.

---

	5. Heat Memory Zones

* Certain stone surfaces shimmer more
* Learned through experience

	Town mastery emerges again.

---

	6. Visual Fatigue

* Prolonged aiming worsens distortion
* Brief pauses reset clarity

	Encourages rhythm, not tunnel vision.

---

	7. False Target Alignment

* Reticles may align “correctly” but shots miss
* Teaches distrust of static aiming

---

	8. Wind Relief Window

* Brief gusts reduce shimmer momentarily
* Players who wait gain advantage

	Soft synergy with wind mechanics.

---

	9. Non-Combat Side Effect

* Reading distant signs, banners, or symbols becomes harder
* Encourages closer exploration

---

	10. Machine Exception Reinforcement

* Observant players notice machines ignore shimmer
* Reinforces design logic without explanation

---

	Why This Mechanic Is Strong

* Simple concept, deep consequence
* Zero UI clutter
* Time-of-day matters
* Does not punish melee
* Enhances immersion and realism

	It makes the town feel:

> Harsh, bright, and honest — the sun does not favor precision.**

