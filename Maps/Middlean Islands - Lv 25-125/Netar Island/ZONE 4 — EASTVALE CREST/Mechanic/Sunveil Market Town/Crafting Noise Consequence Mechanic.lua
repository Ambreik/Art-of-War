			Crafting Noise Consequence Mechanic

> Crafting is not silent. Towns listen.

---

		Core Concept

	Certain crafting actions generate audible noise signatures that propagate through the town environment.
	Noise does not automatically mean danger — it means attention.

	Attention can come from:
* NPCs
* Guards
* Merchants
* Smugglers
* Hidden systems (events, LCWM triggers)

---

		Noise Sources (Examples)

| Craft Type                           | Noise Level | Notes                  |
| ------------------------------------ | ----------- | ---------------------- |
| Light crafting (alchemy, cooking)    | Low         | Smells + subtle sounds |
| Smithing, heavy tailoring            | Medium      | Rhythmic hammering     |
| Enchanting / rare forging            | High        | Resonant pulses        |
| Emergency crafting (during invasion) | Extreme     | Chaos amplification    |

	Noise is context-sensitive :
* Time of day
* Market crowd density
* Alert state
* Weather (fog dampens sound, wind spreads it)

---

	Noise Propagation Rules : 
* Noise travels along terraces, stairs, and alleys
* Echo points amplify it (ties to Terrace Echo LCWM)
* Water channels dampen sound
* Open plazas spread noise widely

	Noise is not a radius bubble, but a path-based spread.

---

		Noise Consequence States

	1. Passive Attention : 

	Triggered by low–medium noise.

	Effects :
* NPCs glance over
* Merchants comment
* Crowd flow shifts
* Guard patrols reroute slightly

	Mostly atmospheric.

---

	2. Curious Attention : 

	Triggered by sustained or repeated medium noise.

	Effects :
* NPCs approach workstation
* Merchants ask questions
* Smugglers may mark you
* Hidden LCWM checks activate

	Opportunity state.

---

	3. Disruptive Attention : 

	Triggered by high noise or noise during alert state.

	Effects :
* Guards investigate
* Crowd disperses
* Temporary crafting slowdown
* Potential event hooks

	Risk–reward zone.

---

	Positive Outcomes (Good Play)

	Skilled Timing : 
* Crafting during fog reduces attention
* Crafting near water dampens noise
* Crafting while crowd is dense masks sound

➡ Grants:
* Bonus craft quality
* Merchant Favor
* Hidden recipe hints

---

	Intentional Noise Use

	Players may weaponize noise :
* Lure guards away
* Distract enemies during invasion
* Trigger patrol shifts
* Activate LCWM echo traps

	Noise becomes a tool, not a penalty.

---

	Negative / Neutral Outcomes (Bad Play)

* Crafting rare items loudly at night:
	* Guards arrive
	* Stall closes temporarily
* Repeated loud crafting:
	* Smuggler attention
	* Price shifts against you
* Crafting during fake alarm:
	* Auto escort out of town

	No permanent punishment.

---

		Hidden Interactions (LCWM Hooks)

	Echo-Triggered Craft Surge

	If crafting noise aligns with terrace echo points:
* Crafting speed increases briefly
* Or produces unstable items

---

		Smuggler Interception

	High-noise crafting of rare items:
* Smuggler NPC spawns nearby
* Offers alternative deal
* Refusing increases suspicion

---

		Guard Training Event

	Repeated loud crafting near barracks:
* Guards start training nearby
* Raises town alert readiness temporarily

---

		Invasion Synergy

	During Crusader attacks:
* Noise attracts enemies faster
* Or pulls defenders toward you
* Can be used to redirect waves

---

	Anti-Exploit Design : 
* AFK crafting generates less noise
* Macro repetition triggers diminishing attention
* Bots cannot control timing/context

---

	Environmental Modifiers : 

| Condition    | Effect                      |
| ------------ | --------------------------- |
| Fog          | Noise dampened              |
| Wind         | Noise spreads unpredictably |
| Crowd Panic  | Noise masked                |
| Heat Shimmer | Distorts sound source       |

---

	Player Feedback (No UI Bars) : 
* NPC voice lines
* Guard footsteps
* Crowd movement
* Camera audio reverb
* Subtle screen vibration

	Players feel attention.

