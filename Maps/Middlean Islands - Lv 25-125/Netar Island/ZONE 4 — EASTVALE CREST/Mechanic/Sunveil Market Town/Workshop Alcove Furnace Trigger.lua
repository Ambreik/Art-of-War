			Workshop Alcove Furnace Trigger

	Structure :
* Located in small workshops and furnace alcoves scattered on market terraces and residential sections of Sunveil Market Town
* Furnaces are functional environmental props — not just decorative
* Alcoves often line narrow alleys or ramps, making them tactical choke points
* Certain furnaces have subtle markers or soot patterns signaling LCWM potential

---

	Trigger :
* Player must throw a special crafted item (crafted with ingredients from market NPCs) into the furnace
* Item examples:
	* Volatile alchemical powders
	* Firepot fragments
	* Explosive enchanted oils
* Correct combination required to trigger LCWM; generic items produce only smoke (cosmetic)
* Works any time during combat or enemy events (Crusader invasion, Cragwallower raids)

---

	Effects :
1. Flame Burst :
	* Range: ~3–4 meters in front of furnace alley
	* Damage: Moderate (~80–120)
	* Knockback: ~2–3 meters, pushing Soldiers or Scouts into walls, stairs, or water channels
	* Can interrupt enemy attack patterns, especially melee mobs

2. Protective Bonus / REP :
	* If enemies are knocked back away from civilians or NPCs, player earns bonus REP
	* Encourages strategic positioning and timing

3. Environmental / Emergent Interactions :
	* Works in combination with:
		* Terrace Echo Exploit : funnel mobs into furnace alley
		* Water / Slippery Channels : extinguishes flame burst if wet, creating strategic fail/success interactions
		* Ballista Firepot or Balloon mechanics : chain explosions, AoE combos
	* Can be used preemptively to create traps along high-traffic zones for enemies

4. Visual / Audio Feedback :

	* Furnace flames flare brightly with light/shadow reflections along nearby walls
	* Short roaring sound signals successful activation
	* Players can spot successful LCWM use visually from adjacent terraces

---

	Hidden Use :
* Only triggers if:
	* Correct item combination is used
	* Furnace is intact and unobstructed
	* Player throws item into furnace at precise angle
* Generic items = smoke only (no damage or knockback)
* Encourages player experimentation and discovery

---

	Integration with Mechanics :
* Crowd Dynamics : Flame bursts can push mobs into fleeing civilians, adding emergent challenges
* Guard AI : Guards may intercept mobs redirected by furnace bursts
* Crusader / Cragwallower AI : Certain melee units may avoid furnace alcoves after repeated hits
* Multi-Mechanic Combo : Works with Ballista Firepot and Terrace Echo for powerful chain reactions

---

	Reward / Incentive :
* REP Bonus : +10–15 for successful execution
* Hidden achievement if furnace triggers combine with other LCWM in a raid scenario
* Optional visual reward (temporary flame glow or scorch mark on alley wall)

