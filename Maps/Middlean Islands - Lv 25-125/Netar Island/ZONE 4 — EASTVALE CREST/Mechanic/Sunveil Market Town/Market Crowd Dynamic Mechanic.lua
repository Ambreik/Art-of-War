			Market Crowd Dynamic Mechanic

		Core Identity

	This mechanic represents civilian crowd behavior as a physical, reactive system, not background decoration.

	The market is not static.
	It responds to player movement, urgency, and disruption.

	Crowds are treated as:
* Soft obstacles
* Dynamic terrain
* Behavioral indicators

---

		Spatial Structure

	Primary Zone
* Central Market Terrace

	Secondary spillover zones:
* Adjacent alleys
* Stall corridors
* Stair approaches

	Crowd density:
* Peaks during trade hours
* Thins during early morning / night

---

		Trigger Conditions

	The crowd reacts when players:
* Sprint repeatedly
* Jump through stalls
* Draw weapons
* Cast visually disruptive abilities
* Move erratically at close range

	Calm walking:
* Does not trigger displacement
* Allows natural navigation

---

		Crowd Behavior Logic

	Scatter & Flow

	When triggered:
* Civilians move away from perceived threat
* They do not vanish
* They relocate, causing congestion elsewhere

	Movement characteristics:
* Semi-predictable
* Directionally biased toward exits or open spaces

---

		Obstacle Formation

	As crowds shift:
* New choke points form
* Pathways narrow
* Movement becomes less efficient

	Players can:
* Trap themselves unintentionally
* Be delayed during pursuits or escapes

---

		Mobility & Combat Impact

* Sprinting through the market is risky
* Combat in crowded areas becomes chaotic
* Enemies and players alike must adapt

	This discourages:
* Reckless movement
* Thoughtless aggression

	And rewards:
* Route planning
* Awareness
* Calm traversal

---

		Zone 4 Integration

	The mechanic mirrors:
* Herbivore scatter behavior
* Wildlife panic responses

	This creates:
* Systemic consistency
* Predictable learning across zones

	Players who understand animal behavior:
* Instinctively understand crowd movement

---

		Hidden Interactions – Market Crowd Dynamics

	These interactions are subtle and often counterintuitive.

---

	1. Calm Presence Effect

* Standing still briefly reduces crowd agitation
* Civilians resume normal patterns

	Encourages composure.

---

	2. Chain Reaction Panic

* One disturbance can propagate
* Crowds move in waves

	Players may cause problems far from themselves.

---

	3. Stall Shielding

* Dense crowds can block line-of-sight
* Useful for escaping or repositioning

	Crowds are not just obstacles.

---

	4. Directional Bias Learning

* Crowds favor certain escape routes
* Experienced players predict congestion

	Town mastery again.

---

	5. Aggression Amplifier

* Drawing weapons escalates panic faster
* Even without attacking

	Visual language matters.

---

	6. Fog Synergy

* Fog increases misinterpretation
* Crowds scatter more aggressively

	Environmental layering, not stacking buffs.

---

	7. Crowd Compression Trap

* Multiple players trigger scatter from different angles
* Civilians compress instead of dispersing

	Creates sudden movement dead zones.

---

	8. Silent Passage

* Walking, sheathing weapons, slow turns
* Allows near-invisible navigation

	No stealth buff needed.

---

	9. Memory Without Punishment

* Civilians do not “hate” the player
* Panic dissipates naturally

	Prevents reputation overload.

---

	10. Non-Optimal Solutions Exist

	Some crowd reactions:

* Make paths worse
* Offer no advantage

	This preserves realism and unpredictability.

---

		Why This Mechanic Works

* Turns NPCs into terrain
* Adds challenge without combat scaling
* Makes the town feel alive
* Encourages player self-regulation
* Synergizes naturally with fog, wind, and light

	Sunveil Market Town becomes:

> A place where behavior matters, not just stats.

