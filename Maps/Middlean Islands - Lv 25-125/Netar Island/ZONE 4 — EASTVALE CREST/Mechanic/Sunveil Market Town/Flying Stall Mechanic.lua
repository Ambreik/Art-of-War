			Flying Stall Mechanic (Sunveil Market Town)

> “Reach distant settlements safely, if you follow the rules.”

---

		Core Concept : 

	Flying Stalls are NPC-operated mount rental points that allow players to traverse long distances automatically along predetermined paths.
	This is restricted by:
* Mount type (only certain mounts work here)
* Flight paths (straight, uninterrupted)
* Invisible island travel boundaries
* Player status (cannot be in restricted zones or in combat)

---

	Structure : 

* NPC : Flying Master
* Location : Sunveil Market Town central plaza / upper terrace
* UI : Flight map with selectable destinations
* Mounts Available : Giant birds (rented, flying path-compatible)
* Cost : Currency (gold, tokens, or reputation-based discount)
* Safety : Players cannot deviate; path is straight

---

	Flight Path Rules : 

1. Player selects destination before mounting
2. Mount auto-starts, path cannot be altered
3. Restricted zones :
	* Crossing the invisible island boundary automatically dismounts non-eligible mounts
	* Flying in combat or restricted zones is blocked
4. Emergent Risk :
	* If player dismounts mid-flight: free fall mechanics apply
	* Mount may die if environmental hazards exist along the path (storms, high winds)
5. Landing Zone :
	* Player lands automatically at destination
	* Cannot exit mount early unless for emergent mechanics

---

	Player Interaction : 

* Buying a ride :
	* Talk to Flying Master NPC
	* Choose destination from map
	* Pay fee (gold / token / REP discount)
* Flight Experience :
	* Automatic travel
	* Can view terrain, events, environmental effects (fog, wind shimmer)
* Emergent Mechanics :
	* Hot air balloon use can influence local air currents
	* Weather may slightly sway mount (cosmetic or minor buff/debuff)
* Hidden Interactions :
	* Flying through Fog / Thermal currents may reveal hidden LCWM structures or rare monsters
	* Certain timed events (raids, NPC migrations) visible only from flight

---

	Restrictions : 

* Mount type limited to Flying Stall-compatible mounts
* Cannot carry other players (unless using special mounts later)
* Invisible island boundary prevents misuse
* Alarm mechanics / fake emergencies may block paths temporarily
* Reputation level may unlock discounts or priority slots

---

	Synergy With Other Mechanics : 

| Mechanic                       | Effect                                                                                     |
| ------------------------------ | ------------------------------------------------------------------------------------------ |
| Time-Based Social Roles        | NPC schedules may make Flying Stall inactive at night, or open only during market hours    |
| Alarm / Fake Emergency         | All flying paths blocked temporarily                                                       |
| LCWM / Environmental Mechanics | Flying above certain terraces may trigger hidden mechanics or reveal visual hints          |
| Hot Air Balloon & Wind Draft   | Minor atmospheric effects during flight, only visual or slight mount sway                  |
| Social / Loitering Awareness   | Players loitering near Flying Stall during restricted hours may be escorted out or blocked |

---

	Player Risk / Emergent Event : 

* Intentional dismount : Player can exit mid-flight → free fall risk
* Environmental hazard : Storms or thermal drafts can knock players off small mounts
* Event overlap : Crusader invasion / LCWM emergent mechanics can trigger visibility of events from the air
* Boundaries enforcement : Invisible line separating north and south halves of island ensures no bypassing

