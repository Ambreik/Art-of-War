			Favor Points (FP) System

---

	Step 2 – Favor Points (FP) System

	1. Purpose : Favor Points (FP) quantify how much an NPC “likes” or “trusts” a player. FP is dynamic and influenced by:
* Rumors propagated through the cross-town system (Step 1)
* Direct interactions (quests, trades, services)
* Player choices (helping/harming NPCs, crafting, escort missions)
* Special events (festivals, invasions, emergencies)

	FP influences:
* Prices at shops (discounts or surcharges)
* Access to hidden quests or rare items
* NPC dialogue and rumors about the player
* Behavioral perks (assistance during invasions, private training, secret mechanics)

---

	2. Favor Points Mechanics : 

| Archetype       | Positive FP Actions                                                               | Negative FP Actions                                                      | Max FP | Min FP | Notes                                                                                 |
| Greedy      | Large purchases, delivering high-value goods, paying for services promptly        | Failing deliveries, stealing from them, bad trades                       | 100    | -50    | Rumors about wealth increase spread faster                                            |
| Cautious    | Following instructions precisely, helping NPCs or allies, avoiding trouble        | Breaking rules, ignoring instructions, reckless actions                  | 100    | -50    | Rumors propagate slower, FP changes more gradually                                    |
| Opportunist | Exploiting mechanics cleverly, profitable trades, uncovering hidden opportunities | Wasting time, exposing secret systems, failure in profit-making attempts | 100    | -50    | FP reacts strongly to unique or creative solutions                                    |
| Social      | Interacting frequently, participating in festivals/events, helping other NPCs     | Causing public disturbances, being rude to NPCs or other players         | 100    | -50    | FP affects how rumors propagate socially, increases tavern and town gossip efficiency |

---

	3. FP Gain/Loss : 

* Base FP change per action : +5 / -5
* Amplifiers :
	* Rumor reinforcement: +2 FP per NPC who hears about the action
	* Repeat positive action (same day): +1 FP stacking bonus
	* Event-based boost (festival, invasion, seasonal): +5 FP if action aligns with NPC archetype
* Decay :
	* FP decays by -1 per day if no interaction occurs
	* FP reset triggers if player performs direct betrayal, like stealing, attacking, or failing major quest

---

	4. Example of FP Effect

1. Player delivers rare Lumen Quartz to "Miner Trainer"
	* Direct FP gain: +5
	* Rumor propagation: Blacksmith (+2 FP), Town Guardian (+2 FP), Tavern Keeper (+2 FP)
	* Total FP gain: +11

2. Player buys a large batch of goods from "Weapon Merchant"
	* Greedy FP gain: +5
	* Rumor amplification to Accessories Merchant (+2 FP)
	* If Merchant archetype detects cheating or scams → FP drop: -10

3. Player frequently disrupts tavern fights → "Tavern Keeper"
	* Negative FP: -5 per incident
	* Rumor spreads: Guards (-2 FP), Mount Master (-1 FP)

---

	5. FP Threshold Effects : 

| FP Range          | Effect                                                                             |
| ----------------- | ---------------------------------------------------------------------------------- |
| 80–100       	    | NPC offers exclusive discounts, secret items, rare quests, or assistance in events |
| 50–79             | Standard interactions; may hint at hidden opportunities                            |
| 1–49              | No bonus effects; neutral NPC behavior                                             |
|   -1–-49          | NPC becomes suspicious or unfriendly; higher prices, refuses favors                |
|   -50 and below   | NPC will refuse service, alert guards, or spread negative rumors                   |

---

	6. Special Interactions Tied to FP : 

1. Discounts & Hidden Mechanics : 
	* Flying Master discount if smuggler player + positive FP with Smuggler NPC
	* Tavern Keeper special beverage unlock at FP > 50
	* Accessories Merchant secret rare items at FP > 80

2. Quest Unlocks : 
	* Certain quests only available if FP threshold met
	* Chain quests may have multiple routes depending on FP

3. NPC Behavior Modifiers : 
	* Positive FP → NPC may help in combat (like Town Guardian or Hunter NPC)
	* Negative FP → NPC may ignore, hinder, or report the player

