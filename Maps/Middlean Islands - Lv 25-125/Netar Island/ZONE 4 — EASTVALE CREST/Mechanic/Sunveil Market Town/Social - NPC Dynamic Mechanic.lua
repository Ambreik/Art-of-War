			Social / NPC Dynamic Mechanic

	Structure :
* Applies to market terraces, residential areas, and craft zones of Sunveil Market Town
* NPCs include:
	* Civilians (merchants, shoppers, children, artisans)
	* Guards (regular patrols, special alert behavior)
	* Workshop / Craft NPCs (alchemy, blacksmith, cooking)

---

	Trigger / Use :
* NPC behavior changes dynamically based on player actions, combat events, and environmental triggers :
	1. Chaos / combat : NPCs flee, hide behind structures, or panic when explosions, fire, or monsters occur
	2. Environmental mechanics : NPCs interact with balloons, artillery, terraces, or furnaces
	3. Crowd dynamics : Sprinting, collision, or aggressive actions from players push NPCs into other areas, forming emergent obstacles or blockades

---

	Effects :

1. Reactive Movement :
	* Civilians flee along predefined escape paths, but can be influenced by:
		* Player-induced chaos (fire, water, balloon AoE)
		* Environmental triggers (furnace explosions, terrace echoes)
	* NPCs may take cover behind obstacles if combat is nearby

2. Dynamic Interaction with Mechanics :
	* NPCs can block enemy paths unintentionally (crowd mechanics)
	* NPCs react to bell alarms, fog, and heat shimmer, influencing player strategy
	* Some NPCs may interact with LCWM directly :
		* For example, artisans may throw incendiary ingredients into furnaces if they see enemies nearby

3. AI Behavior :
	* Guards dynamically respond to NPC movement, positioning themselves along paths likely to protect civilians
	* NPC scatter mechanics influenced by:
		* Proximity to explosions or fire
		* Noise (shouting, terrace echoes)
		* Crowd density and bottlenecks

---

	Hidden / Emergent Use :
* Players can orchestrate chaos to manipulate NPC movement, creating:
	* Traps for invading enemies
	* Blockades in alleyways to funnel mobs
	* Opportunities to experiment with town defenses
* Observant players may discover unique patterns that maximize REP or trigger rare LCWM combos

---

	Integration with Mechanics :
* Crowd Dynamics : NPCs become moving obstacles; combo with terrace knockbacks or water channels
* Balloon / Artillery / Furnace LCWM : NPCs may inadvertently increase effect by pushing enemies into AoE zones
* Fog / Mist / Light Shimmer : NPC reactions may be delayed or obscured, increasing player challenge
* Guard Patrol / Alert Mechanic : NPC movement can influence guard response dynamically

