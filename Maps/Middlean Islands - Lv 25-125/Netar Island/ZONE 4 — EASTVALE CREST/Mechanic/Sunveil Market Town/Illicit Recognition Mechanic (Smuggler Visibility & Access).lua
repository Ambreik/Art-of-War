			Illicit Recognition Mechanic (Smuggler Visibility & Access)

> Smugglers don’t advertise. They recognize patterns.

---

		Core Idea

	Smugglers do not appear on demand.
	They become aware of players through behavior signals already present in the city.

	This mechanic links:
* Loitering Awareness
* Pickpocket Detection
* Rumor Chains into "Smuggler Recognition States".

---

		How Smugglers “See” Players

	Smugglers monitor behavior vectors, not reputation.

	Recognition Signals : 
* Repeated loitering near market edges (without suspicion)
* Standing near stalls but not trading
* Observing guards patrol patterns
* Triggering crafting noise without panic
* Successful pickpocketing without guard intervention
* Overhearing Tier 2+ rumors

	No single action is enough.

---

		Smuggler Recognition States

	State 0: Invisible
* Smugglers ignore the player
* No interaction possible

---

	State 1: Noted

* Triggered when 2–3 recognition signals occur.

	Effects:
* Smuggler NPC appears in peripheral vision
* Makes eye contact
* Walks away

	Test of awareness.

---

	State 2: Acknowledged

	Triggered by:
* Continued presence
* No guard suspicion
* Proper timing (fog, evening, crowd)

	Effects:
* Smuggler pauses nearby
* Ambient line:

	> “Busy place for doing nothing.”

	Player still cannot trade.

---

	State 3: Open Channel

	Triggered by:
* Player emotes
* Moves closer
* Follows but doesn’t chase
* Uses contextual items (coin flip, drink, note)

	Effects:
* Smuggler becomes targetable
* Opens dialogue, not vendor UI
* Tests player intent

---

	State 4: Transaction Enabled

	Triggered by:
* Correct dialogue choices
* Behavior consistency
* No recent guard suspicion

	Effects:
* Smuggler stall UI opens
* Unique goods unlocked
* Alternative pricing rules apply

	Access is temporary.

---

		Failure Conditions (Soft)

	Smugglers disengage if:
* Player sprints at them
* Guard alert triggered
* Fake alarm abused
* Suspicion level rises

	Result:
* Smuggler fades into crowd
* No hostility
* Cooldown before retry

---

	Hidden Player Techniques (Skill Expression)

| Technique           | Effect             |
| ------------------- | ------------------ |
| Crowd masking       | Faster recognition |
| Fog loitering       | Lower suspicion    |
| Rumor manipulation  | Smuggler curiosity |
| Crafting noise bait | Smuggler approach  |
| Water channel use   | Guard distraction  |

---

		Rumor Chain Integration

	Smugglers both:
* Consume rumors
* Create rumors

	Examples:
* “Someone’s moving rare metal quietly.”
* “Guards are stretched thin tonight.”
* “Market’s watching the wrong people.”

	Rumors can:
* Accelerate recognition
* Block recognition
* Redirect smuggler spawn points

---

		Pickpocket Link

	Successful pickpocketing:
* Increases smuggler interest
  Failed attempts:
* Delays recognition
* Raises risk threshold

	Smugglers prefer clean operators.

---

	Anti-Exploit Safeguards

* Recognition decays over time
* AFK ignored
* Macro patterns detected
* Cannot brute-force recognition

---

	Player Feedback (No UI Bars)

* Eye contact
* Pathing changes
* Whispered NPC lines
* Subtle camera framing

Players feel when they’re noticed.

