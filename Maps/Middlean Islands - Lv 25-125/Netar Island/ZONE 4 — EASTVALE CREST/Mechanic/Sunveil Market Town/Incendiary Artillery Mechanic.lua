			Incendiary Artillery Mechanic – Sunveil Market Town

		Core Identity

	This mechanic represents a manually operated, high-impact defensive structure embedded into the town’s main gate defenses.
	Unlike the hot air balloons, this is a ground-based, repeatable control mechanic designed for area denial, crowd shaping, and pre-emptive pressure, not burst 
damage.

	It exists to:
* Let players influence battlefield flow
* Create zones enemies avoid or are punished for entering
* Tie crafting, preparation, and defense together

---

	Physical Structure & Placement
* Two heavy incendiary ballistas
* Mounted symmetrically on either side of the main gate
* Reinforced with:
	* Fire-resistant plating
	* Stabilizing braces
	* Embedded aiming rails

	Visually:
* Larger than standard ballistas
* Fire reservoirs visible
* Scorch marks on nearby stone hint at prior use

---

		Availability & Mounting Rules

	Universal Availability

* Ballistas can be mounted at any time
* They are not restricted to invasion events

	This makes them:
* A training tool
* A preparation mechanic
* A curiosity attractor

---

		Mount Duration Limitation (Non-Invasion Use)

	If no active invasion event is present:
* Player may remain mounted for 150 seconds
* Countdown is not explicitly shown (soft learning)
* At expiration:
	* Player is automatically dismounted
	* Ballista enters a brief cooldown

	This prevents:
* AFK abuse
* Permanent control
* Out-of-context domination

---

		Projectile System

	Projectile Stack
* Each mount session grants a finite ammunition stack
* Recommended design:
	* 5 incendiary projectiles per session

	Once the stack is depleted:
* Player is dismounted automatically
* Encourages deliberate firing

---

		Fire Rate

* One projectile every ~7 seconds
* No rapid firing
* Enforces:
	* Timing
	* Target prioritization
	* Area planning

---

		Projectile Behavior & Effects

	Impact Effects

* Projectiles explode on impact
* Create:
	* Burning ground zones
	* Persistent heat fields
* Damage is:
	* Moderate upfront
	* Strong over time
	* Punishing to clustered enemies

	This makes the ballistas:
* Poor for single targets
* Excellent for wave control

---

		Environmental Influence (Zone 4 Integration)

* Ridge Draft Winds
	* Slight lateral drift in projectile trajectory
	* Experienced players compensate naturally

* Shifting Sand Glare
	* Visual distortion near ground
	* Can obscure exact impact point

	These effects are:
* Subtle
* Learnable
* Never frustrating

---

		Player Protection Rules

	While mounted:
* Player is fully immune to damage
* Cannot be interrupted
* Cannot be crowd-controlled

	This reinforces:
* The feeling of operating heavy machinery
* A clear risk-reward tradeoff:
	* Safe while mounted
	* Vulnerable immediately after dismount

---

		Machine Durability Rules

* Ballistas:
	* Cannot be damaged
	* Cannot be destroyed
	* Cannot be sabotaged

	This ensures:
* Reliability as a core town feature
* No griefing vectors
* No repair micromanagement

---

		Crafting & Resource Integration

	Ammunition Is Crafted, Not Infinite

* Projectiles are not magically generated
* Players must:
	* Gather materials
	* Deliver them to a specific NPC
	* Craft incendiary charges

	This creates:
* A loop between:
	* World exploration
	* Professions
	* Town defense readiness

---

		Material Quality Impact

	While the base mechanic stays consistent:

* Higher-quality materials may:
	* Improve burn duration
	* Alter explosion radius
	* Change secondary effects

	This is not random — it’s intentional preparation.

---

		NPC & Player Synergy

* During invasions:
	* NPC guards will operate unused ballistas
	* Their behavior is predictable, not optimal

	Players:
* Can outplay NPC usage
* Can coordinate fire zones
* Can intentionally overlap effects

---

		Battlefield Control Philosophy

	The ballistas are designed to:
* Shape enemy movement
* Force pathing decisions
* Punish reckless advances

	They are not kill buttons.

	A skilled player:
* Creates funnels
* Controls choke points
* Supports melee defenders

---

		Failure & Skill Expression

	Poor usage results in:
* Wasted shots
* Ineffective burn zones
* No hard penalty — just inefficiency

	Good usage results in:
* Slowed assaults
* Reduced pressure on walls
* Noticeable difference in town defense success

---

		Narrative Consistency

	These ballistas are:
* Permanent town fixtures
* Maintained by local forces
* A symbol of preparedness, not excess power

	NPC dialogue reinforces:
* “They burn slow, not fast.”
* “Aim where they will be.”
* “Fire shapes the fight.”

---

		Why This Mechanic Works

* Always accessible, but never abusable
* Ties crafting into defense meaningfully
* Encourages foresight and coordination
* Integrates naturally with Zone 4 conditions
* Complements, not replaces, other mechanics

---

			Hidden Interactions – Incendiary Artillery Mechanic

	These interactions are:
* Optional
* Undocumented
* Discovered through misuse, observation, or repetition
* Not required for success, but reward mastery

---

		1. Overheat Timing Window

	How It’s Discovered
* Player fires projectiles exactly on cooldown (7s rhythm)
* No delay, no panic spam

	What Happens
* After several perfect shots:
	* Ballista enters a brief “overheated but primed” state
	* Next projectile has:
		* Larger burn radius
	* Shorter fuse

	Cost
* Ballista locks briefly afterward
* Player is auto-dismounted

	Design Intent
* Rewards mechanical discipline
* Punishes greed

---

		2. Dead-Zone Ignition

	How It’s Discovered
* Player fires at empty ground
* No enemies present

	Effect
* Ground becomes a lingering fire hazard
* Enemies entering later:
	* Ignite immediately
	* Panic or reroute

	Why It’s Hidden
* Most players only shoot enemies
* Pre-emptive denial isn’t obvious

---

		3. Fire Pattern Memory

	How It’s Discovered
* Player fires multiple shots forming a recognizable pattern
	* Lines
	* Arcs
	* Overlapping zones

	Effect
* Burn zones last slightly longer
* Fire spreads between zones

	World Logic
* Heat accumulates in structured areas

---

		4. Wind Counterplay Exploit

	How It’s Discovered
* Player intentionally aims against wind drift

	Effect
* Projectile lands shorter but hotter
* Smaller area, higher DoT

	Skill Expression
* Environmental reading
* Not raw damage

---

		5. Silent NPC Override

	How It’s Discovered
* Player dismounts mid-session with ammo left

	Effect
* Nearby NPC guard immediately takes control
* Uses remaining projectiles defensively

	Narrative Payoff
* Town feels alive
* Defense doesn’t pause for players

---

		6. Ammunition Type Echo

	How It’s Discovered
* Player uses a certain ammo type repeatedly

	Effect
* Subsequent shots subtly inherit previous properties
	* Longer burn
	* Wider spread
	* Faster ignition

	Not Explained
* No buff icons
* Learned through repetition

---

		7. Ground Synergy with Terrain Objects

	How It’s Discovered
* Fire lands near:
	* Barricades
	* Rubble
	* Wooden structures

	Effect
* Fire propagates differently
* Creates unexpected denial zones

	Encourages
* Terrain awareness
* Town familiarity

---

		8. False Safety During Non-Event Use

	How It’s Discovered
* Player mounts ballista casually outside invasions

	Effect
* Nothing attacks the player…
* But shots may attract distant threats

	Delayed Consequence
* Enemies approach later
* Player already dismounted

---

		9. Intentional Ammo Waste Bonus

	How It’s Discovered
* Player fires last projectile at a harmless area

	Effect
* Ballista cooldown reduced slightly next time
* Representing heat bleed-off

	Meta Lesson
* Efficiency isn’t always optimal

---

		10. Hidden Failure State: Panic Fire

	How It’s Discovered
* Player fires rapidly without aiming

	Effect
* Shots scatter
* Burn duration reduced

	Why It Exists
* Skill ceiling reinforcement
* Calm > speed

---

		11. Cooperative Target Conditioning

	How It’s Discovered
* Multiple players coordinate artillery fire

	Effect
* Enemies adapt slower
* AI behavior becomes predictable

	Emergent Team Play
* No party requirement
* No UI

---

		12. Memory Without Reward

	Important Design Choice

	Some interactions:
* Do nothing obvious
* Exist only to mislead or test

	This:
* Preserves mystery
* Prevents datamining certainty
* Feels very WoW-like

