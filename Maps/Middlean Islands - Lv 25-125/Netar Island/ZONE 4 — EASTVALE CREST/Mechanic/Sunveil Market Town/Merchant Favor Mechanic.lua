			Merchant Favor Mechanic (Behavior-Based Trust System)

> Not reputation. Not a stat. A memory of how you behave.

---

		Core Philosophy

	Merchants do not like heroes or famous players.

	They like:
* Predictable traders
* Fair behavior
* Low disruption
* Respect for specialization

	This system tracks merchant trust patterns, not numbers.

---

	What Merchant Favor IS : 
✔ Per-merchant or per-stall memory
✔ Invisible to UI (no bar)
✔ Built from *behavior*, not quests
✔ Decays naturally if abused

---

	What Merchant Favor Is NOT : 
❌ Not faction REP
❌ Not tradable
❌ Not grindable directly
❌ Not global across towns

---

		How Merchant Favor Is Built

	Positive Behavior Signals

	Merchants quietly log:
* Selling appropriate goods to their stall
* Selling in moderate volumes
* Returning over time (not spam-selling)
* Trading during normal market hours
* Helping during invasions (indirect bonus)

	Each merchant has a soft memory window.

---

	Negative Behavior Signals : 
* Dumping same item repeatedly
* Using wrong stall categories
* Triggering market panic
* Exploit-pattern selling
* Fake alarms / social chaos near stall

	These do not punish — they just slow favor gain or pause it.

---

		Favor States (Hidden, Tiered)

	Merchants internally classify you as:
1. Unknown
2. Recognized Trader
3. Reliable Supplier
4. Preferred Partner

	No UI. Only behavioral feedback.

---
	
		Benefits by Favor State

	Recognized Trader : 
* Slightly faster transaction speed
* More polite NPC responses
* Less affected by overcrowded penalty

---

	Reliable Supplier

* Soft price rounding in your favor (+1–3%)
* Access to extra dialogue options
* Vendor gives hints:
	* “Try selling herbs tomorrow morning.”
	* “Blacksmith is short on flux.”

---

	Preferred Partner : 
* Access to hidden stock (not rare, just situational)
* Ability to:
	* Delay payment
	* Pre-sell upcoming goods
* Reduced suspicion during alert states

	This does not break economy — it optimizes efficiency.

---

		Important: Favor Is Contextual

	Favor:
* Applies only to that merchant
* Does not transfer automatically
* Can spread slowly between similar stalls
* Is harder to gain with smugglers (they don’t trust)

---

		Interaction With Smuggler Stalls

	Smugglers invert the logic:
* Favor gained through risk behavior
* Penalized for predictability
* Prefer chaotic sellers

➡ Creates two parallel economic playstyles.

---

		Synergy With Existing Mechanics

	Overcrowded Market Penalty : 
* Favor softens the penalty curve
* Trusted traders hit diminishing returns later

---

		Guard Patrol / Alert State

* High favor:
	* Guards ignore you during inspections
	* Faster clearance during evacuations

	Low favor:
* Guards watch you longer
* Merchants refuse to shield you during chaos

---

	Emergency Bell Abuse : 
* Merchants remember false alarms
* Favor temporarily frozen
* They won’t help you during next event

---

		LCWM Interactions

	Some LCWM triggers:
* Only activate if merchant favors you
* Furnace combos
* Ballista firepot crafting access

---

		Decay & Protection

	Favor:
* Decays slowly if unused
* Resets partially if player abuses mechanics
* Cannot be permanently lost

	No hard lockouts, ever.

---

	Anti-Exploit Design : 
* Selling too fast → no favor gain
* Multi-account dumping → neutral outcome
* Bot behavior → never gains favor

	Bots cannot behave well long-term.

---

		Example In-World Feedback (No UI)

	NPC lines:
* “I’ll make this quick for you.”
* “You’ve been fair to me.”
* “I don’t have that for everyone.”

	Body language:
* Faster animations
* Less crowd interference
* Guards step aside

