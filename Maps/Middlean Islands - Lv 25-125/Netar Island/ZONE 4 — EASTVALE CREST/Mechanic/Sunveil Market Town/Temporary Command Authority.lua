			Temporary Command Authority

	Structure / Setup :
* Command Post : Centralized, elevated structure near military terraces
* Accessible only by the player with the highest level currently present on Netar Island (excluding players in Tomb of Great Crusaders dungeon)
* Visually: wooden podium, war maps, and signaling instruments (flags, bells, horns)

---

	Trigger / Use :
* Player interacts with command post to assume temporary authority over NPC guards within the town
* Activation lasts limited time window (~5–7 minutes) during events (e.g., Crusader invasion)
* Only one player can assume command at a time

---

	Effects :

1. NPC Guard Control :
	* Player can issue simple orders :
		* Defend / Fortify : Guards take defensive positions on terraces, ballista, or choke points
		* Patrol / Intercept : Guards move toward specific corridors or gates
		* Focus Fire : NPCs prioritize specific enemies (leaders, ranged units, Cragwallower scouts)
   * Guards buffed temporarily during command (~+10% accuracy, +5% damage resistance)

2. Tactical Depth / Player Agency :
	* Player can manipulate battlefield flow, combining:
		* Ballista / Balloon mechanics
		* Terrace Echo / Furnace / Water channel LCWM
		* Crowd dynamics and choke points
	* Allows advanced players to create emergent combos and influence town defense strategically

3. Environmental / Emergent Synergy :
	* Player decisions affect:
		* Enemy AI response: enemies may avoid or redirect around fortified zones
		* NPC civilian safety: can protect non-combatants by directing guards
		* Multi-LCWM coordination: commanded guards can trigger or complement LCWM effects (ballista, terrace echoes, furnaces, etc.)

---

	Hidden / Rare Interactions :
* Certain rare combinations of commands during an event may:
	* Temporarily increase REP reward for the player (+5–10)
	* Trigger emergent enemy behavior, like forcing leaders into AoE traps
* Player may discover advanced tactical patterns not explicitly explained in the UI

---

	Restrictions / Limitations :
* Temporary command is exclusive to the highest-level player
* Duration limited (~5–7 minutes)
* Player cannot abuse commands to permanently buff town or NPCs outside event windows

---

	Reward / Incentive :
* REP Bonus : +15 for effective command coordination during events
* Optional hidden achievement for orchestrating multi-LCWM combos with guards
* Provides high-level player prestige and recognition for tactical mastery

