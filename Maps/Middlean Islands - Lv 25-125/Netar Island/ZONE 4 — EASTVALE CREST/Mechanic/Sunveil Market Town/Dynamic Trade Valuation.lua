			Dynamic Trade Valuation

	Structure / Setup :

* Centralized in the market terraces, with key vendors: blacksmith, alchemist, cooking, herbalist, tailoring, and general goods
* Prices of goods fluctuate based on:
	* Regional resource availability (harvested from farms, mines, or nearby dungeon loot)
	* Player actions (raids, dungeon clearing, gathering)
	* Supply & demand logic

---

	Trigger / Use :
* Player activities affect the stock of goods in the market:
	1. Positive contribution : players deliver rare crafting materials, defeat invading mobs, or escort caravans
		* Market prices drop slightly for common goods (easier to buy)
		* Prices of rare items increase slightly due to demand
	2. Negative impact : dungeon clearing or over-harvesting reduces regional stock
		* Vendors raise prices on some goods, lower on others (e.g., surplus in low-quality items, shortage in rare ingredients)

---

	Effects :

1. Economic Simulation :
	* Prices dynamically adjust every in-game day or after major events
	* Rare crafting ingredients may spike in price after raids or dungeon clears
	* Standard goods (food, tools, consumables) fluctuate slightly

2. Player Strategic Influence :
	* Players can invest in certain goods, predicting upcoming events or dungeon clears
	* Actions like supporting town defense or providing resources may stabilize or increase supply

3. Emergent Interactions :
	* Certain NPC vendors may comment on player actions, hinting at economic changes
	* Markets may have temporary discounts or surge events after rare LCWM are triggered
	* Multiple players coordinating resource input can manipulate prices temporarily

---

	Hidden / Rare Interactions :

* Overloading or understocking the market may trigger:
	* Bonus REP if the town is stabilized
	* Special vendor items that appear only after rare economic conditions are met
	* Minor town-wide events like traders bringing rare shipments or caravans

---

	Reward / Incentive :
* Players influencing supply in a positive way earn:
	* REP with vendors or town faction (+5–15 per event)
	* Access to rare crafting materials or discounted goods
* Players inadvertently disrupting supply may:
	* Raise prices or miss certain rare items
	* Learn about the hidden economic simulation mechanics

---

	Integration with Zone 4 Mechanics :
* Ties to Dungeon Cleansing / Event-Triggered Monster Spawns
* Linked with LCWM combos that produce rare materials or ingredients
* Integrates with NPC interaction mechanics (crafters and vendors respond dynamically)

