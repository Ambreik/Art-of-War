			Market Stall Specialization Mechanic

	Structure :
* Individual market stalls on "Sunveil Market Town" terraces
* Each stall is operated by a specialized vendor NPC
* Stalls are visually and mechanically tied to their specialization (tools, banners, smells, sounds)

---

	Core Rule : 
Each stall strongly favors specific item categories.
Selling items outside the stall’s specialization results in reduced gold value and fewer hidden benefits.

---

		Stall Specializations (Examples)

	Armor Stall

	Favored Items :
* Heavy / Medium / Light armor
* Shields
* Armor enhancement materials

	Penalty for Wrong Items :
* Weapons, jewelry, consumables sell for –20% to –40% value
* No REP gain
* Vendor dialogue reflects disinterest

---

	Weapon Stall

	Favored Items :
* Melee weapons
* Ranged weapons
* Weapon mods or sharpening items

	Penalty for Wrong Items :
* Armor or crafting junk sells for –25% value
* Reduced chance for special barter outcomes

---

	Crafting & Raw Materials Stall

	Favored Items :
* Ores, herbs, leather, cloth
* Monster crafting components

	Penalty for Wrong Items :
* Finished gear sells for –30% value
* Vendor may redirect player verbally to another stall

---

	Consumables & Alchemy Stall

	Favored Items :
* Potions, reagents, food buffs
* Incense and ritual materials

	Penalty for Wrong Items :
* Gear or raw weapons sell at base vendor trash value only

---

		Economic Interaction Effects

	Correct Item Sold : 
* Full gold value (100%)
* Chance for bonus gold (+5–10%)
* Small REP gain with vendor
* Contributes positively to "Dynamic Trade Valuation"

---

	Wrong Item Sold : 
* Reduced gold payout
* No REP gain
* Does not stabilize market supply
* May slightly increase demand elsewhere (subtle economy shift)

---

		Hidden / Advanced Interactions (LCWM Layer)

	Bulk Mis-Selling

* Flooding a stall with wrong items can:
	* Temporarily lower its buying prices further
	* Trigger vendor refusal or “come back later” dialogue

---

	Smart Routing Bonus

* Selling correct items to multiple specialized stalls in sequence:
	* Grants micro REP bonus
	* Slightly stabilizes market prices town-wide
	* May unlock special barter dialogue

---

	Expert Knowledge Reward

* High REP players or traders can:
	* Reduce wrong-item penalty slightly
	* Access vendor hints about better stalls for certain goods

---

		Integration with Other Mechanics

	Synergies with :
* Dynamic Trade Valuation (supply/demand precision)
* Social / NPC Dynamic Mechanic (vendors gossip about bad trading habits)
* Latent Item-on-Structure Triggers (wrong items may unlock hidden reactions)
* Temporary Command Authority (commanding guards can protect valuable stalls during chaos)

---

	Player Learning Curve

* Early players learn through loss of value
* Experienced players optimize routes and timing
* Master traders influence the town economy indirectly

---

	Design Outcome

* Encourages intentional trading behavior
* Prevents “dump everything at one NPC” gameplay
* Makes the market feel alive, specialized, and believable
* Reinforces Sunveil Market Town as a system-driven hub

