			Multi-Mechanic Emergent Combo

	Structure :
* Uses the entire "Sunveil Market Town" as interactive space
* Integrates:
	* Hot Air Balloons (aerial AoE or splash)
	* Ballista Firepots / Incendiary Artillery (chained explosions)
	* Terrace Corners / Echo Points (enemy redirection)
	* Water Channels / Fountains (slip, knockback, steam clouds)
	* Workshop Furnaces (short-range flame bursts)
	* Crowd Dynamics / NPC movement (fleeing or obstacle creation)
	* Fog / Mist (reduced visibility, ranged challenge)
	* Heat / Light Shimmer (line-of-sight distortion, tactical confusion)

	Trigger :
* Players must combine at least three different LCWM mechanics in sequence or simultaneously
* Can occur during "Crusaders" invasions, "Cragwallower" raids, or other town-wide emergent events
* Requires coordination :
	* Timing (simultaneous activation)
	* Positioning (terrace, alley, balloon placement)
	* Payload (correct items for firepots, furnaces, balloon bombs)

---

	Effects :
1. Enemy Misdirection / Immobilization :
	* Temporarily immobilizes or misdirects all invading soldiers for 10–15 seconds
	* Berserk / Alpha enemies cannot target specific player groups during effect
	* Causes enemies to collide with terrain, stairs, water channels, or each other

2. Tactical Advantage :
	* Players can clear areas, protect NPCs, or set up chain damage zones
	* Forces enemies to recalculate patrols / attack behavior, giving defenders an edge

3. Environmental Synergy :
	* Fire, water, wind, and echo interactions combine for unexpected emergent effects
	* Multi-level terraces, narrow alleys, and stairways amplify chaotic yet controllable battlefield outcomes
	* Crowd and NPC behavior can enhance or disrupt combos, rewarding adaptive thinking

4. Visual / Audio Feedback :
	* Small town banner flares, fireworks, dust clouds, or steam plumes indicate successful combo
	* Subtle sound cues signal chain reaction for observant players

---

	Hidden Use :
* Extremely rare; requires:
	* Three or more LCWM mechanics combined
	* Precise timing and positioning
	* Correct payload / item usage
* Encourages experimentation, communication, and high-level strategy
* Effect cannot be predicted fully, rewarding creative and adaptive teams

---

	Reward / Incentive :
* High REP Bonus : +30–40 for successful execution
* Hidden Achievement / Title : “Master of Sunveil Tactics”.
* Optional cosmetic effects visible only to participants of the combo
* Encourages teamplay and emergent gameplay mastery


