			Stairway / Terrace Tactical Advantage Mechanic

---

	Core Identity : This mechanic rewards positional awareness and strategic use of verticality in "Sunveil Market Town". It integrates with combat, environmental 
mechanics, and AI behavior, making terraces and stairways more than aesthetic features.

---

	Physical Structure :  

* Stone stairways connect terraces of different levels
* Ramps and elevated platforms provide high-ground vantage points
* Terraces vary in width and cover (some narrow, some wide) for tactical complexity

---

		Mechanic Details

	1. Buffs for Elevated Positions : 

* Accuracy Bonus : +10% ranged hit chance for defenders (players & NPCs)
* Vision Range : +20% detection radius for spotting enemies
* Melee Advantage : +5% damage when striking downwards

> Notes : Buffs apply only while positioned above enemies or on high terraces/stairways. Moving to lower terraces removes bonuses.

---

	2. AI Integration : 

* Guard AI :
	* Prefers high-ground positions when defending
	* Patrols along stairways and terraces for optimal vision
	* Uses vertical advantage to prioritize ranged fire and target selection
* Cragwallower AI :
	* May attempt to climb terraces but slower
	* Berserk or Alpha units may leap attacks from lower positions, creating emergent tactical interactions

---

	3. Player Interaction : 

* Combat : Players positioned on terraces gain buffed stats; can snipe enemies or protect key points
* Mobility : Stairs/ramps allow vertical repositioning, gliding from terraces interacts with Wind Draft Gliding Mechanic
* Strategy : Players can combine this with balloon or artillery mechanics for coordinated attacks from elevated vantage points

---

	4. Environmental Synergy : 

* Fog / Mist Entry : Reduced visibility may partially negate vision bonus, making positioning tactical
* Light / Heat Shimmer : Can temporarily reduce ranged accuracy even on terraces if glare is strong
* Crowd Dynamics : Crowds moving along stairways can block narrow paths, requiring player awareness
* Echo Resonance : Sound travels along stone stairways, alerting guards or triggering AI reactions for approaching enemies

---

	5. Hidden Interactions : 

* Fall Risk : Jumping or gliding from terraces without "Wind Draft" mastery may cause minor fall damage or stagger
* Noise Amplification : Heavy footsteps along stairs may increase guard alert state temporarily
* Environmental Exploit : Players can bait Cragwallowers into charging down stairs for knockback or AoE combo opportunities

---

	6. Purpose : 

* Reinforces verticality in town combat
* Encourages player tactical decision-making
* Integrates seamlessly with existing mechanics (balloons, artillery, crowd, fog, wind, guard AI)
* Creates emergent gameplay opportunities for clever positioning and timing

