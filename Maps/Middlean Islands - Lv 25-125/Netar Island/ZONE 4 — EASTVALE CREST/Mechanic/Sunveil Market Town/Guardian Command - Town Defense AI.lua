			Sunveil Market Town – Guards & Archers Synergy Mechanic

	Mechanic Name : Guardian Command: Town Defense AI

	Purpose : This mechanic ensures that Tharok Ironshield’s presence dynamically coordinates the town’s guards and archers during normal patrols and invasion 
events, creating emergent combat scenarios and rewarding strategic player interaction.

	Components :

1. NPCs Involved :
   * Tharok Ironshield (Town Guardian) – central AI coordinator.
   * Guards (Patrols along streets, watchtowers, central squares).
   * Archers (Walls, watchtowers, ballista platforms).
   * Scouts (outside town, gather intelligence on incoming threats).

2. Structure / Layout Integration :
   * Upper terraces, walls, stairways, ballista platforms.
   * Streets, market terraces, and key choke points.

3. Triggers / Use :
   * Event-Driven : Enemy invasion (Crusaders or Cragwallowers) or player-triggered alarms (ballista use, hot air balloon ascents, emergency bell).
   * Passive Coordination : Tharok’s presence enhances AI awareness and coordination even when no active raid is happening.
   * Player Synergy : Players can use mechanical structures (ballistas, balloons, terraces) to create combo effects, which Tharok reacts to, enhancing guard/archer efficiency.

4. Mechanic Details :
	* Alert Levels :
		1. Passive : Guards patrol fixed routes; archers watch towers. Tharok roams patrol paths.
		2. Suspicious : Triggered by minor disturbances (loud crafting, loitering, minor player attacks). Patrol paths are dynamically adjusted toward potential threats; archers focus nearby points of interest.
		3. Alert : Triggered by invasion, alarm bell, or balloon/ballista activation. Guards form defensive lines, archers prioritize targets based on threat hierarchy, and Tharok applies buffs/heals.
	* Dynamic Combat Behavior :
		* Archers use line-of-sight targeting and can focus fire on priority enemies, such as Berserk Leaders.
		* Guards block chokepoints, push enemies back, and interact with terrain mechanics (stairs, water channels, slippery surfaces).
		* Scouts report enemy positions, updating Tharok and AI guard/archer positioning.
	* Emergent Player Interactions :
		* Balloon or ballista combos can funnel enemies into choke points.
		* Water channel or workshop furnace triggers may create temporary AoE effects, which Tharok integrates into AI commands.
		* Players who assist in events may gain minor buffs or reputation, based on coordination with Tharok.

5. Hidden Interactions :
	* Tharok may dynamically redirect archers to cover a flank if a player sets up a multi-mechanic trap (e.g., balloon + water channel + ballista combo).
	* Archers may shoot flaming projectiles over barricades if a firepot or incendiary bomb is triggered correctly.
	* Guards may assist players who are retreating or healing, creating a temporary safe zone.

6. Player Feedback / Benefits :
	* Cooperative play with mechanics and AI is rewarded with small town REP gains.
	* Observing Tharok’s commands can teach tactical positioning for future raids.
	* Players who ignore coordinated defense may face tougher enemy waves, reinforcing emergent strategy.

