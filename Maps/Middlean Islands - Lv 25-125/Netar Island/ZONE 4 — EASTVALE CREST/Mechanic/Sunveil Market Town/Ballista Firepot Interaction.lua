			Ballista Firepot Interaction

	Structure : Ballista platforms

* Located near the main gate terraces and military terraces
* Each platform allows one player at a time to operate
* Platforms have elevation differences, affecting AoE impact if fired from upper vs lower terrace

	Trigger :

* Player must load a special firepot crafted from rare items obtained from market NPCs (e.g., alchemical fire, volatile oil, enchanted charcoal)
* Must fire from the correct terrace angle relative to enemies (line-of-sight + elevation)
* Can be triggered during Crusader invasion, Cragwallower raid, or mini-event

	Effects :

1. Primary AoE Explosion :
	* Radius: 6–8 meters
	* Damage: 200–250 to enemy units within radius
	* Ignites enemies for 5–7 seconds (DOT ~10–15 damage per second)

2. Berserk Crusader Leader Stun :
	* Temporarily stuns leader for 3–4 seconds, interrupting any AoE or melee abilities
	* Only affects leader if within explosion radius

3. Environmental Combo :
	* If fired onto flooded channels, explosion creates steam cloud :
		* Reduces ranged accuracy for all enemies by ~20%
		* Duration: 5–7 seconds
		* Encourages players to coordinate terrain + water mechanics

4. Physics / Emergent Interaction :
	* Explosion can knock back nearby Soldiers or Scouts, potentially pushing them into stairs, terraces, or water channels
	* Can trigger chain reactions with other LCWM (e.g., terrace echo or workshop furnace flame bursts)

	Hidden Use :

* Only triggers if:
	* Firepot is used from the correct terrace angle
	* Player uses the special crafted firepot (generic firepot = normal projectile, no AoE/leader stun)
* Encourages player experimentation and observation

	Integration with Mechanics :

* Works with Hot Air Balloon if timed: dropping firepot simultaneously from terrace + balloon creates extended AoE
* Synergizes with Water / Fountaining System : flooding terraces enhances steam cloud effect
* Guards may react dynamically to explosions (Guard Alert State)
* Fog/shimmer may obscure aim, making precise firing more challenging

	Reward / Incentive :

* REP Bonus : +15 for successful execution
* Optional cosmetic effect (small fire plume or town banner flare)
* Hidden achievement if combined with other LCWM (e.g., terrace echo + water channel combo)

