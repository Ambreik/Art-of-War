			Sunwell Microclimate Buff (Passive Environmental Mechanic)

	Description : Sunwell Basin’s unique geography—its concave slopes, sunlight concentration, and accumulated moisture—creates a microclimate that positively 
affects players within its bounds. The basin feels warmer and slightly more humid than the surrounding plains, and this subtle change benefits stamina recovery and 
environmental interaction.
	The microclimate also reinforces the living, responsive feel of the basin: grasses shimmer, water pools ripple gently, and small wildlife responds to player 
movement.

---

	Mechanic Features : 

1. Passive Buff :
	* Effect : +5% stamina regeneration while within the basin.
	* Radius : Approximately the basin’s natural depression (e.g., 40–50 meters in diameter).
	* Activation : Automatic upon entering; no player action required.
	* Visual/Audio Feedback :
		* Sunlight glimmers on grasses and pools.
		* Slight warmth haze or shimmering air effect near slopes.
		* Birdsong and water trickling sound slightly amplified.

2. Interaction with Other Mechanics :
	* Fishing Pools : Fish are more active in sunlit areas of the microclimate.
	* Herbalism : Herbs near sunlit or dew-covered slopes may have slightly faster respawn rates.
	* LCWM Potential : Certain herbs or hidden fish could respond specifically to microclimate conditions, such as rippling water or shimmering plants, hinting at 
special interactions.

3. Optional Visual Flavor :
	* When multiple players are present, minor environmental effects (e.g., grasses moving, faint echoes of footsteps) make the basin feel alive and responsive.
	* Light mist over stone basins or shallow pools adds to the immersive magical realism.

