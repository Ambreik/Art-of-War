			Hidden Herb Patches

	Description : Sunwell Basin’s unusually lush microclimate encourages the growth of rare and semi-hidden herbs. These patches are scattered across the basin, 
often near stone basins, shaded slopes, or damp edges of the pools. While small and subtle, the herb clusters offer valuable resources for Herbalism and can respond
to environmental conditions such as morning dew, rainfall, or "Sunwell Nocturne" melodies.
	Herb patches encourage careful exploration—players must approach quietly to avoid disturbing surrounding wildlife, and subtle visual cues like shimmering leaves
or faint pollen drifting help players spot them.

---

	Herbs Available : 

1. "Basinroot Cluster"
	* Primary herb for Herbalism.
	* Found near shallow pools or damp slopes.
	* Low respawn rate (~12–24 hours) to encourage return visits.

2. "Shimmering Moss"
	* Grows on stone basins and shaded rocks.
	* Useful for crafting minor potions or environmental buffs.
	* Appears only in locations that retain dew.

3. "Sunveil Fern"
	* Rare herb found in hidden nooks among tall grasses.
	* Respawns every 24–36 hours.
	* Collecting may occasionally trigger subtle LCWM interactions, e.g., a small sparkling effect or nearby ambient wildlife responding.

---

	Interaction Mechanics : 

* Harvesting :
	* Requires Herbalism profession to gather efficiently.
	* Approach carefully; excessive movement or noise may cause herbs to “hide” temporarily, respawning later.
	* Players can pick 1–3 units per patch per harvest.

* Environmental Conditions Affect Growth :
	* Morning dew → higher chance for "Basinroot Cluster" to spawn.
	* Rain → Shimmering Moss appears more prominently on stone surfaces.
	* Sunlight → Sunveil Fern slightly glows in shaded areas, hinting at hidden locations.

* Visual Feedback :
	* Herbs are marked subtly: glint on leaves, soft rustling in nearby grasses.
	* Some herbs respond to player presence with faint animation or particle effects (e.g., pollen drifting).

---

	LCWM Potential : 
* Certain herbs can interact with ambient Sunwell Nocturne:
	* Players present during 9–11 PM may notice herbs subtly shifting or glowing, hinting at special crafting or future interactions.
	* Rare herbs might only appear when environmental conditions align (e.g., dew + melody).

