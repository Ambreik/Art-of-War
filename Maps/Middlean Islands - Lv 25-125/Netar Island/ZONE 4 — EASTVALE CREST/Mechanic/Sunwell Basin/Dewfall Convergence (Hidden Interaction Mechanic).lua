			Dewfall Convergence (Hidden Interaction Mechanic)

	Description : "Sunwell Basin" contains smooth, shallow stone basins hidden among the grasses.These basins collect rainwater and morning dew, creating small 
pools. When players interact with these basins under specific conditions, a latent magical effect can trigger, temporarily altering the environment or revealing 
rare resources.
	This mechanic encourages exploration, timing, and observation, allowing players to discover hidden rewards without formal quests or events.

---

	Mechanic Features : 

1. Activation Conditions :
	* Environmental Requirements :
		* Basins must have collected enough water/dew (after rain or morning).
		* Certain basins respond only during specific times (e.g., morning 6–8 AM for dew, or evening 9–11 PM for subtle ambient effects).
	* Player Interaction :
		* Use the “interact” action on 1–3 basins in a sequence or combination.
		* The system provides subtle visual hints: shimmering water, faint glimmer, or minor ripples.

2. Possible Effects :
	* Enhanced Fish Spawns : Rare fish (e.g., "Glintscale Dace") appear temporarily in nearby pools.
	* Herbalism Boost : Hidden herbs (e.g., Sunveil Fern or Shimmering Moss) become visible or respawn early.
	* Ambient Feedback : Water ripples in concentric circles, faint echoes resonate, or grasses shimmer.

3. Player Feedback :
	* Successful activation gives subtle visual/audio confirmation : shimmering water, soft chimes, or amplified echo effects.
	* Rewards are curiosity-driven, optional, and do not block normal gameplay.

4. LCWM Philosophy :
	* Encourages observation and experimentation rather than quest-following.
	* Supports hidden world depth, consistent with the “responsive basin” theme.
	* Can later integrate with other mechanics (e.g., potion interactions, ambient melody cues).

