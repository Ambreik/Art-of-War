			Twilight Lull

	Description : Once a week, the "Sunwell Basin" experiences a natural lull in monster strength. During this period, all aggressive wildlife and minor hostile 
creatures in the basin are weakened due to the combined effects of the microclimate, moonlight, and resonant water echoes. This mechanic creates a safe exploration 
window for fishing, herb gathering, and environmental observation, rewarding players who pay attention to the basin’s rhythms.

---

	Mechanic Details : 

1. Activation Time :
	* Day of the Week : Wednesday
	* Time : Evening (6:00 PM – 9:00 PM) in-game local time

2. Effect :
	* All creatures in the Sunwell Basin:
		* Deal 30% less damage.
		* Have 20% reduced health.
		* Slightly slower movement speed.
	* Passive effect does not disable abilities or drops—just reduces challenge for resource gathering and exploration.

3. Environmental Flavor :
	* Grasses sway more gently, waters shimmer unusually bright.
	* Small animals become more active and visible, adding life to the scene.
	* Soft ambient music or wind-like sound cues could subtly signal the lull.

4. **Gameplay Purpose :
	* Provides a window of opportunity for players to fish, gather herbs, or explore the basin without constant threat.
	* Reinforces the living, responsive nature of "Sunwell Basin".
	* Can subtly hint at hidden mechanics or rare resource spawns (like LCWM interactions) due to lower monster interference.

