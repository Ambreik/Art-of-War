			Ambient Water Melody Mechanic

	Mechanic Name : "Sunwell Nocturne"

	Description : Between 9:00 PM and 11:00 PM, the ambient sounds of Sunwell Basin—rustling grasses, trickling water, and wildlife—combine naturally to create a #
harmonious, faint melody that seems to emanate only from the water pools. Players near or interacting with the pools can hear it, enhancing immersion and hinting at
hidden latent mechanics (LCWM) in the area.

---

	Mechanic Features : 

1. Time Window :
	* Only active daily from 9:00 PM to 11:00 PM (in-game local time).

2. Location :
   * Audible within 15–20 meters of the main pools.
   * Louder and more pronounced if the player is standing directly at the water’s edge.

3. Player Interaction / Feedback :
	* Passive effect: players can hear the melody even without action.
	* Subtle environmental cues appear during the melody:
		* Water ripples more clearly.
		* Light reflects off dew-covered grasses in a faint shimmer.
		* Wildlife pauses briefly or moves gently.
	* Can be a hint for LCWM triggers in future mechanics, e.g., certain herb growth, hidden fish spawns, or puzzle-like interactions that respond to melody timing.

4. Immersion & Flavor :
	* Creates a mystical, almost magical feeling unique to Sunwell Basin.
	* Reinforces the basin’s identity as a responsive, living environment.
	* Encourages exploration and observation, without being mandatory for progression.

