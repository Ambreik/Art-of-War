			Fishing Pools

	Description : "Sunwell Basin" contains a series of small, crystal-clear freshwater pools scattered among the tall grasses and shallow stone basins. These pools 
collect rainwater and morning dew, providing ideal conditions for freshwater fish. Despite their modest size, the pools teem with aquatic life that responds to time
of day, weather, and water level, creating a dynamic and rewarding fishing experience.
	The ambient warmth and sunlight concentrated in the basin accelerate fish activity during early mornings, making it the optimal time for rare spawns. Players 
can wade carefully into shallow edges or fish from grassy banks, with movement and sounds subtly influencing fish behavior.

---

		Resources (Fish Species)

	Using the Zone 4 Resource segment for aquatic life in freshwater pools:

1. "Crestwater Chorus Carp"
	* Common fish in all pools.
	* Increases Fishing Profession experience slightly.
	* Active after rainfall or early morning dew.

2. "Sunwell Minnow"
	* Small, quick fish; slightly harder to catch.
	* Common in shallow basins.
	* Provides low-level crafting material for potions or bait.

3. "Glintscale Dace"
	* Rare fish that only appears when stone basins are full of dew/water (LCWM tie-in).
	* Slightly higher Fishing experience and useful for special recipes.

4. "Basin Trout"
	* Appears only in deeper micro-pools in shaded basin corners.
	* Medium rarity, responsive to morning sunlight.

---

	Interaction Mechanics : 

* Pool Access : Players can fish from shallow edges; stepping too far in may startle fish, reducing catch chances.
* Time/Weather Dynamics :
	* Morning dew → increases spawn rates of Sunwell Minnow and Glintscale Dace.
	* Rain → replenishes pools, temporarily increases fish activity for 1–2 hours.
	* Night → only Crestwater Chorus Carp are active, minimal rare spawns.
* Fishing Difficulty :
	* Standard rod: 50% success on common fish, 20% on rare fish.
	* Improved rod: +10% success on all fish.
	* Player movement, sounds, and environmental interaction can subtly affect catch chance.

---

	LCWM Potential : 
* Hidden basins or clusters of pools may unlock temporary high-density fish spawns when interacted with in a particular sequence or timing, rewarding curiosity-
driven exploration.
* Fish behavior can subtly respond to ambient sounds, tying into the “Sunwell Basin responds to the traveler” flavor.

---

	Player Feedback : 
* Fish splashes, rippling water, and occasional visible schools provide visual and auditory cues for players.
* Sunlight shimmering across pools hints at rare spawn zones.
* Passive environment interaction encourages slow exploration and observation, reinforcing the immersive quality of Sunwell Basin.


