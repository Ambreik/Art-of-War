			Sunwell Resonance Fishing

	Description : At "Sunwell Basin", a few rare fish are extremely elusive and only appear when specific environmental and player-driven conditions align. These 
fish form the legendary "Moonlight School", a shimmering group that responds to ritualistic offerings to the water. The LCWM mechanic encourages player 
experimentation, timing, and use of special items to interact with the basin’s latent magic.

---

	Components : 

1. Rare Fish School
	* Name : "Moonlight School"
	* Type : Rare, high-value fish cluster.
	* Behavior : Appears only during exact environmental conditions (noon or midnight).
	* Reward :
		* High Fishing Profession experience.
		* Special crafting ingredient: "Moonlit Scale" (used in high-level potions or fishing-related recipes).

2. Special Potion
	* Name : "Lunar Liquor"
	* Source : Purchased from "Stormreach Token Vendor NPC" using "Stormreach Commendation Tokens".
	* Mechanics :
		* Only one potion can be used per interaction.
		* Must be thrown into a pool at exact noon or midnight (server time or in-game local time).
		* Potion evaporates after interaction, visually shimmering on water surface to signal activation.

3. Interaction Mechanics
	* Step 1 : Player buys "Lunar Liquor" from the "Stormreach Token Vendor".
	* Step 2 : At noon or midnight, the player targets a specific pool in "Sunwell Basin" and “uses” the potion.
	* Step 3 : If timed correctly:
		* Water shimmers, faint echoes resonate across the basin.
		* "Moonlight School" spawns for a limited duration (~5 minutes).
	* Step 4 : Player fishes normally; success rates:
		* Common fish: unaffected.
		* Moonlight School fish: base 20% chance, +10% per Fishing skill upgrade or special rod.

4. Environmental Feedback
	* Water sparkles with soft blue-white luminescence.
	* Basin acoustics amplify a subtle melodic hum, signaling correct activation.
	* Nearby animals pause briefly, enhancing the mystical effect.

