			Flora & Herbalism Integration

	Name : "Thermal Bloom Cycle"

	Description : Unique herbs grow only in proximity to specific spring types.

	Herb Placement

	Choose selectively from Zone 4 resources:

	1. Near Warm Springs
* Sunblossom Reed – used for stamina tonics
* Emberleaf – fire and warmth-based alchemy

	2. Near Cold Springs
* Frostpetal Bloom – mana and clarity potions
* Chillroot – frost and resistance brews

	3. Near Confluence Pools
* Veilflower – stealth, invisibility, perception

	Mechanics : 
* Herbs only appear when:
	* Spring conditions are correct
	* Mist is present or at dawn/dusk
* Overharvesting reduces spawn rate temporarily

	Flavor : 
*The plants here drink not just water, but temperature, memory, and silence.*

