			Mineral Mist Alchemy Interaction

	Name : "Essence of the Veil"

	Description : The mineral mist itself can be harvested and used for alchemical and crafting purposes.

	Mechanics : 
* Players can collect "Mist Condensate":
	* Appears only when mist density is high
	* Collected via bottles, cloths, or vials
* Used for:
	* Stealth potions
	* Sound-muffling oils
	* Camouflage salves
* Overharvesting mist causes:
	* Temporary clearing of camouflage zones
	* Increased predator activity

	Flavor : 
*The mist clings to your skin like a memory — fleeting, but powerful.*

