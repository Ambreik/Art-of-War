			Spring Fishing Network

	Name : "Mirrorwater Fishing Grounds"

	Description : The mineral-filtered waters create ideal micro-habitats for several Zone 4 fish species, some preferring warmth, others cold, and a few only 
appearing in the mixed pools.

	Mechanics : 

	Fishing spots vary by pool type:

	"Warm Springs Fish"
* Sunveil Carp
* Emberfin Minnow

	"Cold Springs Fish"
* Silverveil Trout
* Frostscale Darter

	"Confluence Pool Fish (Rare)"
* Prismback Koi
* Dawnshimmer Eel

	Additional Rules:

* Fish behavior changes with:
	* Time of day
	* Mist density
	* Thermal balance
* Some rare fish can only be caught:
	* At dawn when mist is thick
	* Or when players remain undetected for a period (synergy with stealth mechanic)
* Confluence Pools increase:
	* Chance for rare fish
	* Chance for double catch

	Flavor : 
*Where warmth and cold entwine, the waters shimmer with unseen life — some fleeting as mist, others patient as stone.*

