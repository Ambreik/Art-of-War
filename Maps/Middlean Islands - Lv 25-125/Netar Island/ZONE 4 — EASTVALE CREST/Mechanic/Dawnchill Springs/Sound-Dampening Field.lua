			Sound-Dampening Field

	Name : "Muted Hollow"

	Description : Mineral mists and water vapor absorb sound, creating an eerie quiet where even footsteps feel distant.

	Mechanics : 
* Inside mist zones:
	* Spell and ability sounds are softened
	* Enemy alert radius is reduced
* Some enemies rely on sound more than sight and become weaker here
* Conversely, certain creatures become harder to locate due to silence
* PvP interactions (if applicable):
	* Reduced combat noise
	* Makes ambushes more effective

	Flavor : 
*Here, the world speaks in whispers — and sometimes not at all.*

