			Stealth & Camouflage System – Spring Variant

	Name : "Mistbound Veil"

	Description : Thick grasses, drifting mineral mist, and flowering reeds create an environment where sound is dampened and visibility is warped.

	Mechanics : 
* Tall grasses and mist provide natural camouflage zones
	* Reduces detection radius of enemies
	* Slightly distorts enemy targeting
* While inside:
	* Players and mobs gain partial invisibility
	* Attacking or using abilities breaks concealment instantly
* Certain predators (e.g. mist panthers, reed stalkers) can:
	* Enter stealth themselves
	* Ambush from the mist
* Ranged reconnaissance becomes extremely valuable here

	Environmental Integration : 

* Fishing while hidden:
	* Increases rare fish chance
	* Reduces chance of attracting predators
* Some herb nodes only spawn inside mist zones

	Flavor : 
*In the drifting haze, shapes dissolve and reappear — a place where patience is deadlier than strength.*

