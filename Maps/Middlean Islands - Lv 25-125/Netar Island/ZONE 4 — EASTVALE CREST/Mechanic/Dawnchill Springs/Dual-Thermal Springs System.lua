			Dual-Thermal Springs System

	Name : "Thermal Confluence"

	Description : Warm and cold springs emerge side-by-side, fed by different mineral layers beneath the plateau. Where they meet, the water becomes uniquely 
reactive to time, weather, and player interaction.

	Mechanics : 
* Springs are categorized:
	* "Warm Springs" – faint steam, amber-toned water
	* "Cold Springs" – crystal clear, bluish reflections
	* "Confluence Pools" – where warm and cold waters mix
* Each spring type provides different effects:
	* Warm:
		* Slowly regenerates stamina and health when standing in
		* Slight increase to fire resistance
	* Cold:
		* Increases mana regeneration or cooldown recovery
		* Slight increase to frost resistance
	* Confluence:
    * Temporary buff to resource gathering (fishing/herbalism/mining) nearby
* Staying too long in either extreme without leaving:
	* Warm → causes fatigue (reduced movement)
	* Cold → causes numbness (slower casting/attack speed)

	Flavor : 
*The waters do not merely soothe or chill — they remember the path they traveled through the mountain, and they pass that memory into those who linger.*

