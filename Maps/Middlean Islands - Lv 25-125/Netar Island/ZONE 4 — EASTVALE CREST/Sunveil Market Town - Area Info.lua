			Sunveil Market Town

		Overview

	"Sunveil Market Town" is a fortified terrace settlement carved directly into the highland plateau of Eastvale Crest. Rather than sitting on the land, the town
feels as though it has grown out of the stone itself, shaped by wind, elevation, and long-term habitation.
	It serves as the primary civilian and trade hub of the region — a place of safety, exchange, and preparation — standing as a calm but resilient counterpart to 
the open and often deceptive tranquility of the surrounding crestlands.

---

		Location & Terrain Integration

	The town is built along a natural rise in the plateau, where the land naturally steps upward in wide stone shelves. These shelves have been shaped into broad 
terraces, each supporting a section of the settlement.

* The lowest terrace faces the open plains and serves as the main point of arrival.
* Higher terraces house commerce, crafts, and administration.
* The uppermost tier overlooks the region, offering long sightlines across Eastvale Crest and toward the eastern sea.

	Natural cliff faces form half-walls and sheer backdrops, reducing the need for excessive artificial fortification and giving the town a grounded, immovable 
presence.

---

		Fortifications & Defenses

	Sunveil Market Town is well fortified, but not imposing in a militaristic way.

* Stone ramparts follow the edges of terraces where natural drops are insufficient.
* Gatehouses are compact but thick, reinforced with pale iron fittings and creststone masonry.
* Watchpoints are positioned at:
	* Terrace corners
	* Natural chokepoints
	* Elevated ridges integrated into the town structure

	Rather than tall walls encircling the entire settlement, defense is achieved through elevation, layered access, and controlled approaches.
	The town feels secure without feeling closed off.

---

		Architecture & Materials

	The architecture reflects practical longevity rather than ornamentation.

	Primary Materials : 
* Pale stone blocks quarried locally
* Creststone slabs used for foundations and stairways
* Reinforced timber for roofs, balconies, and awnings
* Lumen Quartz accents embedded subtly in stonework to catch light

	Design Language : 
* Squared stone buildings with softened edges from age and wind
* Flat or gently sloped roofs to withstand highland gusts
* Deep-set windows and heavy shutters
* Buildings often share walls or are partially embedded into rock faces

	Nothing feels temporary — every structure appears meant to last generations.

---

		Town Layout

	Terraced Structure

	Sunveil Market Town is organized vertically rather than horizontally.

* Wide stone stairways and ramps connect terraces
* Each terrace feels like a self-contained district
* Sightlines are open, allowing players to see multiple layers of the town at once

	Movement through the town naturally creates a sense of ascent and descent, reinforcing its plateau identity.

---

	Central Market Terrace

	At the heart of the town lies the Market Terrace, the widest and most active level.

* Open-air market stalls with stone bases and fabric canopies
* Trade wagons parked along the edges
* Permanent merchant structures carved into the surrounding stone
* A central open space used for announcements, trade gatherings, and communal activity

	The market is lively but orderly, shaped by routine rather than chaos.

---

	Craft & Profession Quarters

	Surrounding and slightly above the market are the craft terraces, where profession-related buildings are clustered.

* Workshops open toward shared courtyards
* Chimneys, worktables, drying racks, and storage alcoves line the stone walls
* The soundscape shifts here: metalwork, grinding, brewing, stitching

These areas feel industrious and purpose-driven, forming the economic backbone of the town.

---

	Residential Terraces

	Higher and quieter terraces are reserved for:

* Stone homes
* Inns and lodging houses
* Administrative buildings
* Community spaces

	These areas are calmer, with narrower paths, overlooking the market and plains below.

---

	Atmosphere & Sensory Experience

	Sunveil Market Town has a distinct sensory identity :

* Wind is ever-present, moving banners, awnings, and loose fabrics
* Light reflects softly off pale stone, giving the town a warm glow during the day
* At dawn and dusk, long shadows stretch across terraces
* Sounds travel upward — distant market noise, footsteps on stone, muted voices

	Despite being fortified, the town feels open and breathable, never claustrophobic.

---

	Cultural Tone

	The town reflects a culture that values:

* Stability
* Craftsmanship
* Trade
* Preparedness

	There is no excess decoration, no grand monuments — only subtle symbols etched into stone, banners marking districts, and practical signage.

	Sunveil Market Town feels like a place that:

* Has endured
* Will endure
* And quietly supports everything that happens beyond its walls

---

		Role in Eastvale Crest

	Sunveil Market Town stands as:

* A safe anchor point in the zone
* A transition space between wild plateau and structured civilization
* A place where players regroup, prepare, and observe the broader region

	It does not dominate the landscape — it belongs to it.

---

		Outer Defensive Zone & Forward Camp

	On the outer side of the town, slightly offset from the main arrival approach and positioned on a broad, reinforced stone shelf, lies a forward defensive 
encampment integrated into Sunveil Market Town’s fortification network.

	This area is not part of the civilian terraces.
	It functions as a military staging ground, visually and spatially distinct from the market and residential levels.

	Stone platforms, anchored posts, supply crates, and reinforced pavements indicate that this zone is designed to endure sudden escalation rather than daily life.

---

	Hot Air Balloons – Aerial Defense Structures

	Within this forward defensive shelf stand three large hot air balloons, permanently stationed but only periodically active.

	Visual & Structural Description

* Each balloon is anchored to a heavy stone mooring ring embedded into the terrace
* Thick ropes, counterweights, and reinforced frames hold the balloon baskets in place
* The balloon fabric is:
	* Weathered
	* Patched
	* Reinforced with leather bindings
* Burner assemblies and fuel containers are mounted directly into the baskets

	When inactive, the balloons remain grounded, gently shifting with the wind — clearly visible from afar as unusual silhouettes against the plateau sky.

	Role in the Town’s Identity

	These balloons serve as emergency aerial defense assets, designed for:
* Wide battlefield observation
* Area suppression
* Temporary aerial advantage during large-scale threats

	Their presence reinforces the idea that Sunveil Market Town:
* Anticipates large assaults
* Adapts to open-terrain warfare
* Uses elevation and airspace as part of its defense doctrine

	They do not feel experimental or whimsical — they feel necessary.

---

		Primary Gate Approach & Heavy Artillery

	Directly in front of the main town entrance,positioned symmetrically on either side of the primary access route, stand two massive fixed artillery emplacements.

	These are not standard ballistas.

	Weapon Description : 
* Large torsion-based siege weapons
* Built on heavy stone bases
* Designed to launch incendiary payloads, not spears
* Equipped with:
	* Reinforced launch arms
	* Containment cradles for fire-based ammunition
	* Heat-scorched firing channels

	The weapons resemble advanced fire-throwing artillery, closer in spirit to incendiary launchers seen in large-scale warfare rather than precision weapons.


	Positioning Logic : 
* Slightly elevated relative to the approach path
* Clear, unobstructed firing arcs
* Overlapping coverage zones
* Protected from direct frontal assault by stone bulwarks

	Their placement makes it immediately clear:
	This entrance is meant to break charging forces, not merely delay them.

---

	Atmospheric Impact of These Structures

	The addition of these elements changes the feel of the town subtly but meaningfully:
* The presence of balloons adds vertical awareness to the skyline
* The artillery gives weight and seriousness to the gate
* Civilian areas remain calm, but the outskirts communicate readiness

	Sunveil Market Town now feels like:

> A peaceful settlement that knows exactly how fragile peace can be.

