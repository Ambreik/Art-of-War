# **7. Environmental Mechanics**

---

## **7A. Global Mechanics**

---

### **• Day/Night Cycle**

Standard world cycle applies: daylight improves visibility and reduces ambush risks; nighttime grants creatures increased aggression, +15% to all stats, and +10% drop rate.
In Eastvale Crest, the open terrain makes night-time more dangerous due to long shadows that hide fast-moving predators.

### **• Fatigue Waters**

All water bodies drain stamina while swimming. Stronger currents increase drain.
On this plateau, the Seastrand coastal waters pull northward, making long-distance swimming especially taxing and risky.

### **• Water Flow & Currents**

Rivers and coastal edges exert directional pull.
Here, even small coastal inlets funnel water sharply between rock shelves, making fishing slightly harder if players face against the flow.

### **• Environmental Audio Layering**

Terrain-based sound cues help players detect threats.
Open plains amplify creature calls and wind noise, while terraces cause echoes that hint at approaching creatures long before they appear.

### **• Monster Awareness**

Creatures detect players differently depending on light levels and movement.
In Eastvale Crest’s open land, awareness radius is naturally higher during daylight and extremely pronounced during full-moon nights.

### **• Noise Awareness**

Running, fighting, or harvesting increases the chance that nearby creatures will detect the player.
Sound travels far across the Crest, especially across terraced cliffs where echoes move vertically.

---

# **7B. Specific Environmental Mechanics (Unique to Eastvale Crest)**

*(These mechanics exist only in this zone.)*

---

### **• Terrace Echo Resonance**

Sound bounces between stacked cliff layers. A single action—jumping, mining, or combat—can alert creatures positioned several levels above or below. Echo intensity changes based on the size and depth of the terrace.

### **• Heat Shimmer Distortion (Luminous Ridge)**

Sun-heated minerals distort light, causing shimmering waves. Long-range targeting is less accurate, and small creatures appear warped at distance, making ambushes easier for them.

### **• Mist Surge at Dawn (Dawnchill Springs)**

Warm geothermal steam mixes with early cold air, producing dense fog for 10–15 minutes each dawn. Visibility drops drastically, but players can use the fog to bypass patrols or approach POIs unseen.

### **• Ridge Draft Winds**

Strong upward air currents form along the eastern mountain wall. These drafts can push gliders upward slightly but destabilize them randomly, making gliding unpredictable and dangerous here.

### **• Shifting Sand Glare (Seastrand Beach)**

The wet, mineral-rich sand reflects sunlight intensely during morning hours. Player accuracy suffers when looking east, while certain creatures become irritable or retreat into shaded rocky crevices.

### **• Moisture Pocket Humidity (Sunwell Basin)**

The concave basin traps moisture after rain, causing temporary growth surges for certain herbs. Humidity also affects stamina regeneration slightly, slowing it by ~5% while present.

### **• Quarry Resonance Pulse (Echo Quarry)**

Mining certain quartz nodes creates sonic vibrations. These pulses can attract geological creatures dwelling inside nearby cavern pockets, increasing ambush probability the longer a player mines uninterrupted.

---

# **7C. Resource-Based Mechanics**

---

### **• Lumen Quartz Overcharge**

Rare Lumen Quartz can store ambient sunlight. When harvested at peak daylight, the mineral emits a blinding flash, temporarily lowering player accuracy unless proper gear is used. At night, harvesting becomes harder but safer.

### **• Moisture-Triggered Herb Growth**

Certain herbs only become harvestable after rainfall or morning mist. These herbs appear semi-dormant otherwise and cannot be collected until climate conditions trigger their bloom cycle.

### **• Tide-Locked Fishing Pools**

Pockets of fish spawn only when the tide recedes, revealing shallow pools rich in prey. They disappear once the tide returns. Missing the window means waiting for the next tide shift.

### **• Echo-Sensitive Ore Veins**

Some ore types emit rhythmic vibrations detectable only when approached slowly. Sprinting past them can cause them to “sink” beneath loose soil temporarily, requiring players to wait several minutes for the vein to re-emerge.

### **• Ridge Herb Wind Dispersal**

High ridge winds blow lightweight herbs off their stems during storms or strong gusts. These herbs scatter downslope, meaning the highest terraces rarely retain herbs after windy days.

---

# **7D. Creature / AI Mechanics**

---

### **• Aggression Triggered by Sound Direction**

Creatures in terraced zones triangulate sound more effectively because of echo patterns. They may approach from higher or lower levels based on resonance rather than direct proximity.

### **• Ridge-Walker Pathing Logic**

Creatures that inhabit ridge lines constantly patrol narrow cliff edges. If combat starts on a ridge, these creatures instinctively reposition to height advantage, often circling to higher ground before engaging.

### **• Sand-Burrowed Ambushers**

Certain predators remain invisible underground near Seastrand Beach, reacting only when players walk directly over soft sand patches. Heavy steps or mounted travel increase trigger chances.

### **• Basin Territorial Saturation**

Creatures within Sunwell Basin become more territorial after rainfall because moisture heightens scent trails. Their aggression radius increases until the basin dries fully.

### **• Luminous Ridge Light Sensitivity**

Crystal-fed creatures react strongly to light. Sudden flashes (from quartz or spells) can either enrage or stun them depending on the creature type. Some may abandon combat entirely if disoriented.

### **• Herd Scatter Logic (Open Plains)**

Herbivore groups detect motion faster across open terrain. When startled, the entire herd flees in a cone spread pattern, potentially pulling predators with them and causing chain reactions across the plains.

