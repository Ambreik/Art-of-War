# **5. Points of Interest (POIs)**

### **5.0 Sunveil Market Town** 

The local town settlement of this zone. is very well fortified and gather all the profession trainers and some NPCs with questing.

### **5.1. Sunwell Basin**

A vast, natural bowl at the heart of Eastvale Crest where moisture gathers and vegetation grows unnaturally lush. Tall grasses shimmer with dew even late into the morning, and the basin feels almost warm to the touch due to sunlight concentrating on its curved slopes. The acoustics here create a faint echo to every sound—bird calls, footsteps, even the stirring wind—giving the impression that the land is responding to the traveler. Hidden among the grasses are smooth, shallow stone basins that collect crystal-clear water after every rainfall.

---

### **5.2. Starwatch Ledge**

A gently sloping bluff overlooking the eastern sea, famous for an uninterrupted view of the sky. At night, the elevation and clear air make constellations appear razor-sharp. Many travelers camp here to witness dawn break over the horizon—it arrives like a slow golden tide that washes across the plains. Stone markers of unknown age dot the ground, aligned eerily with celestial events such as solstices and equinoxes.

---

### **5.3. The Crestfall Steps**

A dramatic series of natural terraces carved into the earth. Each step drops just a few meters, but the effect is immense: a gigantic staircase stretching across the land. Some steps contain shallow pools, others are lined with wind-polished stone, and a few create natural amphitheaters where sound amplifies strangely. The wind flows between the levels like a gentle river, creating a constant whispering tone.

---

### **5.4. Luminous Ridge**

A narrow highland ridge where pale, reflective minerals run through exposed stone. At dawn and dusk, the ridge glows in long, soft bands of light. Travelers can walk the entire length of the ridge, which offers sweeping views of the Sunwell Basin on one side and the distant sea on the other. On particularly bright mornings, the ridge appears almost ethereal, as if suspended above the land.

---

### **5.5. Dawnchill Springs**

A collection of warm and cold springs hidden in a shallow valley. The waters travel through mineral channels under the plateau and emerge as clear pools surrounded by thick grasses and wildflowers. Some springs release faint mineral mists that drift low over the ground, creating a surreal landscape where colors appear muted and sound feels distant. Small stone alcoves near the springs hint at ancient habitation or ceremonial use.

---

### **5.6. Seastrand Mirror**

A thin stretch of beach under the eastern bluffs where the sand is so fine and pale that it reflects the sky. When the tide retreats, the flat wet sand becomes a perfect mirror surface. At certain angles, the boundary between sky and ground seems to disappear completely. The beach is almost unnaturally quiet; the cliffs absorb sound, leaving only soft waves and distant seabird calls.

---

### **5.7. Windglass Outcrop**

A cluster of smooth, curved boulders shaped over centuries by soft coastal winds. Their surfaces shine faintly, and many have hollow pockets that create flute-like tones when the wind passes through. At sunset, the outcrop turns warm amber, making it a favored place for travelers seeking calm or meditation. This is the border location between Zone 4 and Zone 5, on the east side of the island, by the shore.

