# **3. Ancient Warning Drum**

*A flavor relic with a mild crowd-control effect*

A massive stone drum sits partially buried near the tower’s upper floor.

### ✔ **Interaction**

Player chooses “Strike Drum.”

### ✔ **Results**

1. **Deep resonant boom** — heard across the ridge
2. **Hidden whisper text** appears briefly:
   *“The watchers endure. Eyes to the horizon.”*
3. **All creatures in a ~10m radius freeze for 1 second**

   * Not a stun
   * Not canceling combat
   * Purely flavor (animation freeze)

### ✔ **Usage Cooldown**

30 seconds to avoid spam

### ✔ **Purpose**

Atmospheric storytelling
Echo of ancient warning systems
Fun tiny interaction players remember

