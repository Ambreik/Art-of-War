			Watcher’s Wind Column

*Micro traversal boost — gentle vertical movement*

The old structure’s collapsed roof channels wind through broken beams.

### ✔ **Trigger**

Standing on a specific **rotted plank platform** activates a low updraft.

### ✔ **Effect**

* Player is lifted **1–1.5 meters upward**
* Enough to reach:

  * A small ledge
  * A broken arch
  * A hidden stash (if placed)

### ✔ **Characteristics**

* Not strong enough for gliding
* Only affects the player when directly above the airflow
* Has a soft whooshing sound effect

### ✔ **Purpose**

Makes the tower feel “alive” with environmental physics
Allows subtle vertical interactions without turning into a platforming zone

