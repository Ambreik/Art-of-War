			Vision Trigger

*Light supernatural cue toward deeper secrets*

At the highest platform, a carved symbol reacts to player focus.

### ✔ **Trigger Action**

Player uses **/focus** emote while standing on the center rune.

### ✔ **Effect**

* Faint **ghostly silhouettes** appear for 3 seconds
* They stand still, pointing toward a distant location:

  * A hidden cave entrance

### ✔ **Behavior**

* Silhouettes fade into drifting ash
* They do NOT guide directly
* Just hint at “there is something over there”

### ✔ **Purpose**

Encourages exploration
Adds mystery and ancient presence

