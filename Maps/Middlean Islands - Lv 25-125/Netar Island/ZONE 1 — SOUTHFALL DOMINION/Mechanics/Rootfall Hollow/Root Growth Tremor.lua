			Root Growth Tremor

*A living-forest micro-event*

Every few minutes the Hollow experiences a subtle “pulse,” as if the trees shift their weight.

### ✔ **What Happens**

* Roots slightly reposition
* Loose soil vibrates
* Leaves shower down
* Very faint rumbling groan resonates through the ground

### ✔ **Gameplay Effect**

None — purely atmospheric
Player movement is not interrupted

### ✔ **Purpose**

Gives the location a **living, breathing** ecosystem feel
Reinforces that the Hollow is not a static forest but a constantly growing entity

