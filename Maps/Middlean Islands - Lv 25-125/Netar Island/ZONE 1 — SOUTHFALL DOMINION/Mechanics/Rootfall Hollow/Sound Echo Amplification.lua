# **4. Hollow Heart Tree**

*Future quest hook — currently flavor only*

A massive ancient tree stands in the central clearing.
Its bark is split open, revealing a faintly luminescent “heartwood” cavity.

### ✔ **Interaction**

Player uses the interaction prompt **“Touch the Heartwood.”**

### ✔ **Result**

* Soft resonant tone plays
* A gentle ripple of leaves floats outward
* The tree’s light pulse quickens for a moment
* A cryptic whisper text appears:
  *“Life listens. Life remembers.”*

### ✔ **Current Purpose**

* Flavor interaction only
