			Sound Echo Amplification

*Stealth & awareness environmental mechanic*

The tightly interwoven canopy and hollow roots create **unnaturally clean acoustics**.

### ✔ **How It Works**

* Player footsteps echo slightly louder than normal
* These echoes propagate farther, causing:

  * Creatures to detect the player from +20–30% farther away
  * Certain ambient animals to flee sooner
  * Some passive creatures to look in the player’s direction

### ✔ **Note**

* Only footsteps echo — spells, combat sounds, tool swings behave normally
* Silent movement abilities negate the effect

### ✔ **Purpose**

Adds tension to exploration
Gives the Hollow its own identity: a forest where the **air carries sound wrong**.

