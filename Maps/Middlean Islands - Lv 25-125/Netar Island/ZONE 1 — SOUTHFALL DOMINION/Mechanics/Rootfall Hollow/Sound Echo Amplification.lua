# **2. Root Tunnel Shortcuts**

*Small mobility grid built into the terrain*

Massive roots bend and twist into natural crawlspaces that function as secret passages.

### ✔ **Design**

* Enterable “root maw” openings
* Narrow tunnels — player must walk/crawl (reduced speed)
* Exit into secluded clearings, glades, or hidden overlooks

### ✔ **Behavior**

* Some tunnels connect back to each other, forming little loops
* Some are one-way due to collapses or narrow squeezes
* Creatures rarely follow players inside

### ✔ **Environmental Flavor**

Inside the tunnels:

* Bioluminescent fungus
* Dripping sap
* Distant groaning of roots shifting overhead

