			Echo Chamber Effect

### **Purpose:**

Give the ruined citadel a feeling of age, emptiness, and eerie acoustics.

### **Gameplay Effect (Harmless):**

Creatures in this exterior zone have their **hearing radius increased by +10%**, but:

* Does **not** pull extra packs
* Does **not** chain-aggro
* Only causes **single-creature early detection**

### **Extra Flavor:**

When the player performs an action (jump, weapon swing, footstep, spell cast):

* A distant echo plays
* Sometimes a soft “ghostly whisper” responds (no function, just ambience)

