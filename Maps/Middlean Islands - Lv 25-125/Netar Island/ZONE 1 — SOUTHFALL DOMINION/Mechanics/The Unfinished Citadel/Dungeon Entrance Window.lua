			Dungeon Entrance Window

Every **30–120 minutes**, the entrance area displays a short atmospheric occurrence.

### **Event Visual:**

* Ghostly silhouettes of ancient crusaders appear briefly
* They march toward the sealed dungeon gate
* A faint warhorn echoes
* The lights fade
* No combat
* No interaction

### **Optional Extra Flair:**

A brief tooltip appears:

> *“You feel the weight of forgotten battles.”*

This has **no gameplay effect**, no reward, no buff.

