			Citadel Lantern Braziers

Lighting these braziers **does not start fights**, **does not open dungeon doors**, and **does not progress any dungeon path**.

They are **environmental storytelling tools**.

### **How It Works:**

* The Unfinished Citadel has **4 ancient braziers** placed along its ruined hallways.
* Each requires a **simple interaction** (“Ignite Brazier”).
* When lit:

  * Sends a warm burst of light outward
  * Lights up vines and carvings on nearby stone
  * Sometimes reveals faint **etched crusader symbols**
  * Plays a deep “ancient mechanism hum” (no mechanical effect)

### **Tiny Mechanical Reward:**

Lighting a brazier grants:

* ** an instant increase of +10% EXP filled for player's current EXP required.

OR

* **+1% movement speed permanent, inside the "Tomb of Great Crusaders"** (stacking up to 4%)
  A simple exploration buff with a great benefit inside the dungeon, whenever the player goes inside.

