			Crumbling Pathing Hazard

This is **not a trap**, **not a damage mechanic**, **not a fail condition**.

### **Trigger:**

The player steps on an unstable stone tile.

### **Result:**

Floor collapses **gently** (scripted), and the player falls into:

### **🟦 “Forgotten Alcove” (Safe Chamber)**

* No enemies
* Contains 1–2 interactive elements
* An exit staircase leads back up near the entrance

### **Possible Chamber Contents:**

#### **A. Lost Storage Chest**

Contains one of:

* Small crafting bundle (Herbs / Ore / Cloth / Bones)
* 1–2 “Ancient Brass Coins” (can be exchanged later in another area)
* Tiny amount of gold

#### **B. Lore Tablet**

Player reads a short Cronarian Crusader history piece:

> “The oathbinders lit these halls so the darkness would never reclaim our fallen.”

#### **C. Wall Mural Puzzle (Pure Flavor)**

Touch it → it glows for 3 sec → nothing else happens.
Useful for future secrets but harmless now.

