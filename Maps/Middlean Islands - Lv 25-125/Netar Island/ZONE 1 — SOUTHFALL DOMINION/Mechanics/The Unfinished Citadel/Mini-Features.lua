			MINI-FEATURES

---

### **1. “Dustfall Cascade” (Visual Physics Only)**

When the player walks under unstable beams, dust falls realistically.
Sometimes stones shift, but never collapse.

---

### **2. “Ancient Light Paths”**

When all 4 braziers are lit, a thin line of golden light connects them for 5 seconds.

Purely cosmetic.

---

### **3. “Fallen Banner Fragment” (Collectible Item)**

Very low chance (0.5%) when exploring rubble piles.
A cosmetic collectible with lore — no stats.

