			Hidden Cat Petting Mechanic – Southfall Cat

	Type Hidden / Optional Player Interaction

	Location Southfall Village – Rooftops near the Inn

	Mechanic Description
* Players can discover the ability to pet the Southfall Cat by approaching it carefully and using a **“/pet” emote** when standing on the same rooftop.
* Cat must not be startled; sudden movement or attacks nearby reset the interaction.
* Cat reacts with small animations: purring, rubbing against the player, tail flicks, ear twitches.

	Interaction Requirements
* Must approach quietly (within 2m of the cat).
* Must have no combat engagement nearby.
* Can only pet the cat once per day for XP or curiosity rewards.

	Reward / Effect
* Minor exploration XP: +25 XP per successful pet.
* Hidden buff: “Curiosity Spark (Cat Edition)” – +1% gathering chance for herbs or small crafting resources for 30 minutes.
* Unlocks subtle environmental dialogue from NPCs (e.g., villagers notice player bonding with the cat).

	Optional Extensions
* Petting all cats found on Netar Island could unlock a special “Whisper of the Wild” title or cosmetic item.
* Players could trigger minor lore whispers from the cat’s perspective (small flavor text).

