# **B.3 Threat Types Outside the Walls**

Guards behave differently depending on what type of presence is detected *beyond* the village boundary.
(This is not combat AI — it’s world-flavor behavior.)

### **1. Wildlife Threats**

Small animals (wolves, boars) wandering too close.

* Guards point spears or raise lanterns.
* Short voice lines:

  * “Shoo! Back to the woods!”
  * “Keep your distance!”

### **2. Unidentified Sounds**

Rustling bushes, distant movement, or odd silhouettes.

* Guard pauses patrol.
* Lantern raised; slow walking toward sound.
* After a few seconds:

  * They shrug and return with dialogue:
    “Probably just driftwood or a loose plank.”

### **3. “Dark” Threat Presence**

(Tied to your world’s Crusaders or other shadow-type dangers — but **no combat**, just flavor.)

Guards react more aggressively when they sense something abnormal.

* Sudden freeze, lantern flicker animation.
* Longer voice lines:

  * “Something feels wrong…”
  * “Shadows move strangely tonight.”
* Increased patrol frequency for ~1 minute afterward.
* Two guards may pair up temporarily instead of walking alone.

---

# **B.4 Escort or Proximity Reactions**

When players or NPCs walk near guards, they react naturally.

### **Player Proximity (Neutral or Friendly)**

* Guard briefly turns head toward player.
* If followed too closely:
  “Staying close? Don’t worry — we keep this road safe.”

### **NPC Escort Behavior**

Some villagers returning late at night walk near patrol routes.

* If a villager intersects a guard’s patrol, the guard **slows down** and stays slightly behind them.
* If the villager moves toward a darker zone:

  * Guard steps in front, as if “checking the path.”

### **Player Escort Behavior (Passive)**

Not an actual escort quest.

If the player walks alongside a patrolling guard:

* Guard occasionally comments:

  * “Quiet night… for now.”
  * “Stay alert; fog plays tricks.”

### **Aggressive Threat Proximity**

If a dangerous creature steps within a defined outer radius:

* Guard briefly points weapon.
* No combat initiation.
* Player gets a short warning line:
  “If you must go out there, keep your blade ready.”

