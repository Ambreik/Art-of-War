			Guard & Patrol System

Southfall Village maintains a small but disciplined defensive routine.
These mechanics are **purely atmospheric + behavioral**, not combat-heavy unless you choose later.

---

# **B.1 Night Patrol**

At night, guards switch from passive daytime walking to **structured patrol loops**.

### **Patrol Behavior**

* Guards walk with lanterns or torches.
* Paths cover:

  * The village entrance
  * Perimeter near wooden fences
  * Road toward the coastline
  * Back alleys between houses
* Walk cycles slow and cautious, with occasional pauses to “listen.”

### **Small Night-Only Interactions**

* If a player walks close behind a guard at night, the guard occasionally comments:
  “Easy… thought I heard something.”
* Guards turn their lantern slightly toward noises or movement.
* Rarely, the guard kneels to check footprints or debris.

### **Visibility Behavior**

* Guard lanterns illuminate a slightly wider field than normal player lanterns.
* They sometimes raise the lantern upward when fog is heavy.

---

# **B.2 Response to Alarm Bell**

Southfall has a small metal alarm bell near the entrance tower.

### **Trigger Conditions**

(Not tied to quests — only systemic behavior.)

When the bell is rung **(player or NPC)**:

1. **All guards stop and turn toward the sound**
2. They jog (not run) to the bell area
3. Once gathered:

   * They adopt a **ready stance**
   * Look outward toward the roads or coastline
   * Increase voice lines like:

     * “Eyes up!”
     * “Check the shadows!”
     * “Report anything unusual!”

### **Duration**

* 45 seconds of heightened alert
* Guards slowly return to their normal patrol routes afterward
* Guards mention the false alarm if nothing is found
  (“Another false ring… villagers need calmer nerves.”)

