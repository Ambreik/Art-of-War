			Village-Time Behaviors


---

Southfall Village follows a subtle daily rhythm that makes the settlement feel alive.
NPCs shift behavior based on **morning**, **afternoon**, **night**, and optional **seasonal/weather micro-changes**.
These behaviors are **purely atmospheric** and do not affect gameplay balance unless noted.

---

## **A.1 Morning Behaviors (Sunrise → Late Morning)**

* Villagers begin daily chores: sweeping porches, feeding livestock, pulling water from the well.
* Fishermen prepare nets and walk toward the coastline or small pond.
* Children run between houses for brief playful moments before lessons.
* Cooking smoke rises from chimneys as homes prepare breakfast stews.
* A mild increase in ambient bird sounds.

**Micro-Interactions:**

* Some NPCs greet players with more energetic dialogue.
* Shops open gradually — a few open slightly later depending on the owner’s routine.

---

## **A.2 Afternoon Behaviors (Midday → Sunset)**

* Farmers and gatherers return periodically to drop off supplies at storage sheds.
* Crafters (blacksmith, tailor, cook) work actively, causing background crafting sounds.
* Fishermen return with early catches; some clean fish outdoors.
* Children are mostly indoors or sitting near shaded areas.
* The village center has the highest NPC density.

**Micro-Interactions:**

* NPCs comment on weather or unusual sea conditions.
* Occasional gossip lines between villagers become audible when passing by.

---

## **A.3 Night Behaviors (Sunset → Midnight → Early Night)**

* Lights ignite: lanterns, torches, and windows glow with warm light.
* Most villagers retreat indoors; only guards and a few late-working NPCs remain outside.
* Dogs bark more frequently near the village’s edge.
* Distant sea sounds become louder in village audio.

**Micro-Interactions:**

* NPCs display tired or shorter dialogue.
* A few houses have silhouettes visible in windows (flavor only).
* Some windows dim suddenly if wind gusts blow.

---

## **A.4 Late Night Behaviors (Midnight → Sunrise)**

A quieter phase distinct from early night.

* Only guards patrol regularly.
* A rare NPC such as an insomniac villager may pace near their home.
* Soft distant whispers and creaking wood ambience intensifies.
* Occasionally, an NPC wakes briefly to check outside before returning inside.

**Micro-Interactions:**

* Very low chance of subtle, harmless “mysterious sounds.”
* Footsteps echo more in empty streets.

---

## **A.5 Seasonal Micro-Changes (Optional Flavor Layer)**

These changes are environmental and influence NPC flavor dialogue.

### **Spring**

* Children play more often outside.
* Villagers mention new planting cycles.
* More flower or herb bundles visible near certain houses.

### **Summer**

* Villagers stay outside longer into the evening.
* Water buckets appear outside houses.
* Fishermen comment about warm tides or increased insect activity.

### **Autumn**

* More crates, dried herbs, and firewood appear near homes.
* NPCs wear slightly thicker clothing.
* Dialogue mentions colder nights.

### **Winter**

* Fewer NPCs outside during morning and late night.
* Shorter outdoor routines.
* Small frost effects on roofs or fences (visual only).

---

## **A.6 Weather-Dependent Micro-Changes**

Each weather change slightly modifies NPC behavior or dialogue.

### **Light Rain**

* Villagers run to porches or indoor areas.
* Outdoor crafting slows or stops.
* Children no longer play outside.

### **Heavy Rain or Storm**

* No outdoor chores.
* Lanterns flicker more often.
* NPCs indoors may peek out from windows occasionally.

### **Fog**

* NPCs walk slower.
* Dialogue becomes cautious (“Strange morning, can barely see the well…”)
* Guards increase patrol speed slightly.

---

## **A.7 Small Villager Conversations (Ambient Dialogue System)**

Not full dialogue trees — simple overheard lines that trigger when two or more NPCs are near each other.

### Examples:

* “Did you hear something by the coastline last night?”
* “The tide brought in strange debris again…”
* “Wind is picking up — lanterns will struggle tonight.”
* “Hope the children stay away from the cliffs.”
* “Fog rolled in thicker than usual today…”

These do not trigger quests but **hint** at other mechanics in the village.

