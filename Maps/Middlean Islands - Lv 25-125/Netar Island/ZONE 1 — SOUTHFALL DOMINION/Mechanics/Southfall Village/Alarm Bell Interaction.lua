			Alarm Bell Interaction
			
## ✔ **Core Concept**

A bell in the village center that players can interact with to temporarily place Southfall in “Alert Mode”.

Great for flavor, safety, and immersion — but must be balanced to avoid abuse.

---

# ✔ **Bell Location**

**Southfall Village Center — near the well.**

Small wooden tower, rope hanging from the bell.

---

# ✔ **Activation Rules**

* Click interaction triggers bell animation + sound
* **Cooldown: 3 minutes (global)**
* Can only be used **at night**
* Cannot be used if the Lantern Keeper event is active
* Cannot be used while monsters are actively attacking the village walls (anti-abuse)

---

# ✔ **Effects Upon Ringing**

### **1. Villagers React**

* Villagers run inside homes
* Doors close
* Window shutters slam
* Some NPCs hide behind crates
* Shops close instantly

### **2. Patrol NPCs Spawn**

Three “Southfall Watchmen” appear and walk toward key choke points:

* Gate
* Eastern wall
* Well area

They despawn 45 seconds later.

### **3. Monster Freeze**

All monsters within **40m of village walls**:

* Freeze for **3 seconds**
* AI “alerted” animation
* Resume behavior afterward

This 3-sec freeze is enough for dramatic effect but not a gameplay exploit.

---

# ✔ **Rewards & Limits**

No reward for ringing the bell
(Prevents farming motivation.)

If the bell is rung too frequently:

**If 4 activations occur within 30 minutes:**
→ Bell becomes “Dulled” for 50 minutes, uninteractive.
Flavor text:
*"The bell hangs still, its metal warm and overused."*

