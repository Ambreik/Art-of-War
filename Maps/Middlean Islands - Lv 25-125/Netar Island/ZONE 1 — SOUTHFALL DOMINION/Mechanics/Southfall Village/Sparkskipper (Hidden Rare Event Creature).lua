			Sparkskipper (Hidden Rare Event Creature)

### **📌 Monster Name:**

**Sparkskipper**

### **📘 Type:**

**Ambient Rare Creature (Trigger Event — Nightly)**
Not a combat monster. Non-hostile. Flees when chased.

### **📌 Description:**

A tiny blue fire elemental roughly the size of a rabbit.
Appears playful, skittering across the roads and rooftops of **Lantern’s Watch Village**.
Leaves small streaks of blue light behind it as it runs.

### **📘 Spawn Conditions:**

* **Event Type:** Hidden Rare Event (1% chance, every night)
* **Condition:** During the lantern chain’s ignition, the **sixth lantern** sparks blue
* **Spawn Location:** Always inside the village boundaries
* **Duration:** 40 seconds before despawning
* **Behavior:** Runs in looping paths, making soft chime sounds
* **Interaction:** Players must click it (no skill check needed)

### **📘 Combat:**

* Non-aggressive
* Cannot be attacked
* Cannot be targeted
* Interaction-only creature

### **📘 Purpose in the World:**

* Adds life, mystery, and nightly ambience to the village
* Symbolic representation of “Lantern fire gaining temporary will”
* Foreshadows larger magical secrets in the region
* Offers rare lore items without creating power advantage

