# ✔ **Object: The Weathered Markstone**

Located inside the abandoned hut in Southfall Village outskirts.

A stone slab with a carved, swirling pattern.

### **Interaction Text:**

*“You place your hand on the cold surface.
Something echoes inside… but nothing happens.”*

---

## ✔ **3 Hidden Layers (No future commitments)**

### **Layer 1 — Visual Memory Puff**

When touched:

* Screen slightly desaturates
* A faint sound plays (wind + hum)
* Player sees a **brief flash**:

  * A silhouette carrying a lantern
  * A massive shadow behind them
  * Then nothing

Purpose: atmospheric mystery.

---

### **Layer 2 — Hidden Tracking**

The game records:

**“PlayerHasSeenMarkstone = TRUE”**

That’s it.

Later, if you want, this can open secret doors, add dialogue options, or unlock achievements.

But **nothing right now**.

---

### **Layer 3 — Small Environmental Change**

On interacting for the first time:

* The lantern nearest the hut flickers blue for 3 sec

This ties the object to the Lantern Network mechanic subtly.

---

### Optional Flavor (No effect)

Players who return during rain see a message:

*“Raindrops trace the carved grooves as though illuminating forgotten shapes.”*

