			Mini Hidden Events

All events are **rare, non-quest**, flavor-only for now — but with **seeded lore hooks**.

---

# **D.1 Lost Child Event**

A tiny, rotating hidden event that appears in Southfall Village **only at dusk**.

### **General Behavior**

* A small child NPC (random of 3 possible models) appears crying near:

  * the well
  * the fish-drying racks
  * behind the tavern
* The child speaks **ambient lines**, not interactable.

### **Ambient Dialogue**

* “Mama? … Where did you go?”
* “I wasn’t supposed to play near the beach…”
* “It’s getting dark… please find me…”

### **Environmental Cues**

* When this event is active, lantern flame color near the child becomes *slightly warmer*.
* A dog NPC sits nearby, watching the child quietly.

### **Future Quest Hook (not active)**

* A missing parent
* Connection to the tide debris event
* A unique wooden toy found later in The Shimmering Delta

For now: **purely atmospheric**.

