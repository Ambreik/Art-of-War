# **D.2 Cat Stuck on Roof**

### **Behavior**

* A black-and-white cat appears on a random roof in Southfall.
* It meows loudly every 10–15 seconds.
* Occasionally walks a few steps, sits, and flicks its tail.

### **NPC Reactions**

Nearby villagers comment:

* “Not again… someone’s pet wandered up there.”
* “That cat climbs better than the guards.”

### **Environmental Cue**

* If wind is strong, the cat crouches low and stops meowing.

### **Future Quest Hook (inactive)**

* The cat belongs to a future important NPC
* It may carry a small tied message during a later storyline

Right now: **no interactions**, just flavor.

