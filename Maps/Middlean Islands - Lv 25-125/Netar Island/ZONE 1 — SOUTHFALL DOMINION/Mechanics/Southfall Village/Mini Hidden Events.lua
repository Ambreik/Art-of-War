# **D.4 Tide Debris Containing Items**

A micro-event near the village shoreline.

### **Behavior**

Every in-game morning tide leaves behind a small debris pile:

Possible items (visual only for now):

* driftwood
* torn cloth
* ropes
* broken crate edges
* old fishing floats
* partial nameplate from a ship

### **Ambient Lines**

Nearby fisher NPCs say:

* “The sea always returns what it takes.”
* “Another wreck, or the same old one?”

