# **D.5 Whispering Stones Near the Well**

The **most mysterious** micro-event in Southfall.

### **Behavior**

Rarely, the stones around the well emit faint sound loops.

### **Audio**

* Echo-like whispers, barely audible
* One voice at a time, repeating a phrase that cannot be understood
* Occurs only at **midnight**

### **Visual Cue**

* Slight pulsing glow on the stone rims (not magical, just atmospheric)
* Nearby candles flicker even with no wind

### **Npc Reactions**

If a player stands longer than 3 seconds:

* An elderly villager walks by whispering:
  “The stones remember… even if we’ve forgotten.”

