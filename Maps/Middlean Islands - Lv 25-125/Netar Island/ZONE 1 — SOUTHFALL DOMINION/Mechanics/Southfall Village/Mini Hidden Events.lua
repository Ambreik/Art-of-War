# **D.3 Strange Footprints**

These appear **randomly outside the southern gate** of Southfall Village.

### **Visual**

* Humanoid prints, larger than human, leading **toward the beach**.
* Slight glowing dust inside some prints during night.

### **Behavior**

* Prints fade slowly over 2 minutes.
* Rain removes them instantly.

### **NPC Whisper Lines**

Guards occasionally react:

* “Tracks again… whatever it is, it’s watching us.”
* “These weren’t here an hour ago…”

### **Future Quest Hook**

The tracks match an upcoming **shoreline stalker** creature type in a later zone.

Currently: **visual-only clue**, no interaction.

