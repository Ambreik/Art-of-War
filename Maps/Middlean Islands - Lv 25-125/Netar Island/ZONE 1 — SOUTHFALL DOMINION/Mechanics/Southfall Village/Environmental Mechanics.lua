			Environmental Mechanics

Southfall Village is located near the coast, so its environment shifts frequently in subtle ways that affect immersion but not gameplay difficulty.

---

# **C.1 Weather Micro-Mechanics at Southfall**

These weather variations are **localized to the village area** — not the entire zone.

### **1. Light Drizzle**

* Occurs rarely and lasts 1–3 minutes.
* Villagers:

  * briefly pull hoods forward
  * run small lines like:
    “Rain again… figures.”
* Wooden surfaces glisten.
* Footstep sounds shift to “wet soft thuds.”

### **2. Short Sea Spray Gust**

Not a rain event — just bursts of salty wind from the shore.

* A mist-like flash rolls over the village.
* Cloth on houses and banners flutter strongly.
* Guard comments:
  “Waves must be hitting hard…”
* Slight temporary dimming of distant light sources.

### **3. Warm Cloudy Afternoon**

* Occasional fading sunlight with warm highlights on buildings.
* Creates dynamic shadows that shift every few seconds.
* Children NPCs become more active outdoors.

### **4. Cold Evening Breeze**

* Visible cold breath from villagers.
* Lanterns flicker more visibly.
* Dogs and cats near the houses curl up or move to sheltered spots.

---

# **C.2 Fog From the Sea**

Southfall Village is strongly affected by **coastal fog**, a signature feature.

### **Fog Types**

#### **1. Early Dawn Low Fog**

* Very thin, close to ground level.
* Lanterns glow with a soft halo effect.
* Footsteps sound muffled.
* Fisher NPCs comment:
  “Perfect morning for the nets.”

#### **2. Thick Evening Fog**

* Moves into the village in slow “waves.”
* Reduces visibility just slightly around street corners.
* NPCs react:

  * Guards pause slightly longer at intersections.
  * Villagers light hanging lanterns automatically.
  * Some NPCs choose to stay indoors.

#### **3. Sudden Fog Cross**

Fog rushes across the entrance road quickly.

* Dust motes visible in the air.
* Guard animation changes:

  * They shield their eyes.
  * Lantern is raised higher.
* Brief sound of a distant foghorn from the sea (pure flavor).

