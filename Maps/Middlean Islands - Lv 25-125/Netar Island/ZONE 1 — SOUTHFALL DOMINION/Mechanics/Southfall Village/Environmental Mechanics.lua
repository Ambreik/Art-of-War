# **C.3 Wind Affecting Lantern Brightness**

This is a **micro dynamic-light mechanic** and applies to:

* Player lanterns
* Guard lanterns
* Static lantern posts in the village

### **Wind Strength Levels**

#### **1. Light Breeze**

* Lantern flame slightly shifts direction.
* Flame brightness pulses softly.
* Hanging lanterns sway only 2–3 degrees.

#### **2. Moderate Coastal Wind**

* Lanterns flicker more noticeably.
* Light radius fluctuates briefly (small 5–10% variation).
* You may hear:

  * Wooden beams creaking
  * Cloth banners snapping
* Guard comment example:
  “Wind’s picking up—tie down anything loose.”

#### **3. Strong Gust Event**

A short, sudden gust that occurs rarely.

* Lanterns dim sharply for one second.
* Some lanterns on posts swing strongly.
* A few lose brightness for 3–5 seconds before returning.
* Player lantern effect:

  * Quick flicker and shake of the light cone
* Guard reaction:
  “Strong one! Better hold that lantern tight.”

