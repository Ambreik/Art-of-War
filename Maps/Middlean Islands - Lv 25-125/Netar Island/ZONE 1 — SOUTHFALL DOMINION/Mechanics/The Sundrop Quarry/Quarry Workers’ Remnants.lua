# 📜 **Lore: Sunbleached Ledger Page**

*The page is brittle, edges curled from heat. A faint smell of ash lingers.*

> “Shift 7: Harnel Brightrough — present.
> Jerrik Marn — present.
> Relvan Tor — present.
> *Durn Foldrake — …*”
>
> The rest of the line ends in a violent smear, the ink dragged sideways as if the hand writing it was yanked away.
> A second stroke crosses the page — not ink, but a scorched line, as though a hot chain or burning hand had struck it.

Whoever wrote this was one of the few workers who dared to continue mining after rumors began spreading of **shadows in the quarry walls** — silhouettes shaped like forgotten crusaders that wandered too far south.

The fact that the page is **sunbleached on one side and burned on the other** suggests only one thing:

The worker dropped it while **running from something**, and whatever followed left more than fear behind. A few steps away, the dust is sprinkled with **charred scraps of cloth**, too small to be a garment… unless the rest of it simply *didn’t survive*.

No body.
No tools.
Only this page, half-light, half-ash.

And Durn Foldrake’s name… never checked again.

