			Collapsing Lift Platform

The quarry has 3 old wooden lifts.

### **Mechanic:**

Standing on a lift has a **2% chance per second** to trigger a “Shaking” warning:

* Camera shakes a bit
* Rope fibers visibly stretch
* Dust falls from the pulley

### **Player Reaction Window:**

They have **4–5 seconds** to jump off before it collapses.

### **Class Interaction Flavor (Optional):**

* Rogues detect shaking earlier (+1.5 sec)
* Miners with Rank II profession feel the vibration earlier
* Sorcerers or Dragon Shamans see the rope spark magically

### **If It Falls:**

* Player softly falls into a sand pile → 25% HP damage
* At the bottom they find a:

  * Broken lift wheel (junk item)
  * Small ore node
  * 10% chance for *Lost Worker’s Satchel*

### **Satchel Contains:**

Random from:

* Miner’s Rope
* Small Ore Cache
* Tarnished Worker’s Badge (cosmetic)

