			Echo Mining Resonance

### **Trigger:**

When a player mines any ore node inside Sundrop Quarry.

### **Effects:**

1. **Stone Resonance Pulse**

   * The struck ore emits a deep pulse sound
   * Loose gravel visibly tumbles from nearby cliffs
   * Certain nodes vibrate for 1–3 seconds

2. **Rare Mineral Reaction Rolls**
   Each strike has new sub-procs:

   * **5% chance:** *Quarrysteel Fragment*
   * **1% chance:** *Sparked Sundrop Core* (used later)
   * **0.25% chance:** *Sundrop Crystal Spire* (cosmetic trophy item)

3. **Ambient Threat Warning**
   Some creatures in the quarry respond to sound:

   * Quarry Bats shift position
   * Stone Mites briefly wake (but do NOT auto-aggro)
   * Dust from high cliffs gives players danger feedback
     *(This is a warning, not a punishment.)*

4. **Group Mining Bonus**
   If two or more players mine the same vein within 3 seconds:

   * +10% yield for everyone
   * Small “resonance harmony” sound effect

