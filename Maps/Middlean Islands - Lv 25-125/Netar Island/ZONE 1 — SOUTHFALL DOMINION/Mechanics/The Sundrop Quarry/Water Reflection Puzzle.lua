			Water Reflection Puzzle

### **Location**

A hidden shallow reflective pool located in the **lower levels of Sundrop Quarry**, slightly off the main worker path.

### **Mechanic Overview**

This puzzle is purely optional, subtle, and has no quest attached for now.

Players must stand in **exact, narrow positions** where the water’s reflection aligns correctly.

### **Reflection Rules**

* Works **only** at **sunrise** or **sunset**.
* The watery reflection reveals **three unique glowing markings** when viewed from:

  * Angle A — Left ridge viewpoint
  * Angle B — Broken beam perch
  * Angle C — Fallen chain platform

Each reflection is partial.
When all three are mentally combined, they form a **Pickaxe Rune**.

### **Discovery Rules**

* A player can “discover” all 3 angles **quickly** (because appearance frequency is ×12).
* After discovering all 3 within a sunrise/sunset window, the player receives:

