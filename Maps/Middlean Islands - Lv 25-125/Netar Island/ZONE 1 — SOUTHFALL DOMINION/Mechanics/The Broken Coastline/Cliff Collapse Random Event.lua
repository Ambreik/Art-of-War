			Cliff Collapse Random Event

*Environmental hazard — zero combat requirement*

Large fragments of the sea-facing cliffside occasionally shear off due to erosion.

### ✔ **Trigger Frequency**

* Roughly once every 6–10 in-game minutes (zone-wide)
* Always happens **off-camera**, but within hearing distance

### ✔ **Visible Effects**

* A burst of dust clouds
* Falling stones
* Small temporary debris piles

### ✔ **Gameplay Impact**

* Debris piles block movement for **5–8 seconds**
* Collapses may expose:

  * A narrow crack revealing a tiny tunnel
  * A crab den

### ✔ **Extra Detail**

If a player *is very close* to the collapse spot:

* They are knocked down (short animation)
* No damage taken (this is a hazard, not a punishment)

This keeps it dynamic, not lethal.

