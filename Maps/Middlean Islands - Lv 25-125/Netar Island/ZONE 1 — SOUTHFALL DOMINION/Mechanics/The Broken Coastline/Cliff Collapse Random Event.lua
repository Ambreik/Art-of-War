# **2. Seagull Swarm Aggro**

*Wildlife behavior → chain reaction mechanic*

Groups of seagulls feed or nest on shoreline rocks.

### ✔ **Trigger**

If the player:

* Runs too close
* Uses ranged attacks nearby
* Rolls/dashes past them
* Kills a crab they are scavenging

### ✔ **Behavior**

The entire flock takes off at once:

* Loud cawing
* White feather burst
* Marks the player’s movement

### ✔ **Gameplay Consequence**

Nearby predators (wolves, shoreline prowlers, reef stalkers) gain:

* **+30% detection range**
* **+15% movement speed toward the noise** (only for 5 seconds)

### ✔ **Purpose**

Creates emergent chain reactions:

* Disturb seagulls → predators come
* Players may accidentally pull enemies
* Stealth classes need to adapt

