			Sharp Coral Hazard

*Terrain-based micro-punishment for careless movement*

Dead reefs jut out from the ground all around the tide line.

### ✔ **When it Triggers**

* Sprinting
* Rolling / dodging
* Mounting (if somehow managed close to waterline)

### ✔ **Effect**

Inflicts:
**Bleeding — 6 seconds, 3 ticks of minor damage**

### ✔ **Subtle Details**

* Walking causes no debuff
* Light-footed classes (rogue-like) get reduced duration
* Bleed cannot kill the player — reduces only to 1 HP baseline

### ✔ **Purpose**

Makes traversal thoughtful
Rewards controlled movement
Adds texture to the coastline without danger spikes

