# **4. Tidal Breath Effect**

*Wave collision → temporary vision + movement disruption*

When massive waves crash against the outer rocks, they generate explosive mist clouds.

### ✔ **Trigger Frequency**

Every **25–40 seconds**, based on tidal cycle.

### ✔ **Effects**

* **Mist cloud** obscures vision in a 12–18m radius
* **Pushback** (tiny) nudges the player 0.5–1 meter
* **Water sound peak** warns players 1 second before impact

### ✔ **Gameplay Uses**

* Fighters lose sight of monsters momentarily
* Stealth players use mist as soft cover
* Ranged attacks are reduced in accuracy (miss chance +8%)
* Can push careless players toward coral hazards

