			Shifting Sandbar Paths

*Zone-wide dynamic navigation mechanic*

Each in-game day, ocean currents subtly reshape the exposed sandbars:

### ✔ **What Changes**

* Some sandbars **extend**, creating new walkable shortcuts between islets.
* Others **sink back below the tide**, removing access to previous pathways.
* Rarely, a **fresh temporary sand ridge** appears, accessible for only a single day.

### ✔ **Gameplay Impact**

* Access to hidden herb nodes, fishing spots, or crates changes daily.
* Players familiar with yesterday’s layout must adapt.
* Speedrunners or explorers find optimized paths—but only until the next reset.

### ✔ **Extra Detail**

Sandbars realign behind-the-scenes based on a 5-pattern rotation.
Even without seeing the system, players gradually “feel” the rhythm.

