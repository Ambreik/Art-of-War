			Delta Erosion Mechanic

*Environmental hazard / movement flavor*

Certain channels of the delta have **strong lateral currents**.

### ✔ **Effect**

When players stand still in flowing water:

* They slowly drift sideways (0.2–0.3 meters per second)
* Correcting course requires movement inputs
* Standing still for too long can push the player down elevation steps or into deep pockets

### ✔ **Purpose**

* Makes the zone feel dynamic without being dangerous
* Adds mild positional challenge during gathering or combat
* Communicates visually through flowing reeds and wave patterns

### ✔ **Hidden Detail**

Some erosion currents reveal items after a shift—like driftwood, shells, or junk objects for later professions.

