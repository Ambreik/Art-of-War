# **Summary**

The Shimmering Delta functions as a **living landscape system** designed to:

* Encourage continuous exploration
* Reward observational gameplay
* Provide dynamic gathering opportunities
* Make the wetlands feel alive without “gamey” intrusions

Nothing here interferes with monster balance or dungeon designs—the mechanics are purely environmental.

