			Mud Nest Mounds

*Environmental ambience mechanic*

Clustered clumps of mud dot the delta’s shores.
They are **completely harmless** but add life to the wetlands.

### ✔ **Random Behavior**

Occasionally, a mound trembles and bursts open, releasing:

* Mud crabs
* Tiny frog critters
* Dust puffs
* Harmless mud sprites (visual only)

### ✔ **Purpose**

* Pure immersion
* Makes the area feel biologically active
* Can lightly scare players who don’t expect movement

### ✔ **Extra Detail**

Rarely (1% chance), a mound collapses to reveal a cosmetic **“Mud-stained Pebble”**, a small collector’s item with no gameplay benefit.

