			Mineral Glimmer Effect

*Subtle gathering aid*

Rare herbs and materials in the delta emit a faint energy that refracts through the water.

### ✔ **Trigger**

When a rare herb is within **35 meters**, shallow water gains a soft, shimmering sparkle effect.

### ✔ **Purpose**

* Helps dedicated herbalists avoid wandering aimlessly.
* Encourages exploration of shifting sandbars, since rare herbs spawn in newly revealed land.

### ✔ **Extra Detail**

The glimmer is **stronger at dawn and dusk**, when light angles sharpen reflections.

