			Bioluminescent Crystal Cycle

### **Description**

Crystals embedded in the cavern walls emit bioluminescent light. Their brightness follows a **night-time amplification cycle**.

### **Cycle**

* During **daylight**, crystals glow faintly.
* **At night**, the crystals intensify by **300%**, illuminating:

  * Hidden cracks in rock
  * Narrow tunnels
  * Faint ancient markings carved by unknown hands

### **Gameplay Function**

* **Night glow reveals hidden pathways**:

  * A narrow sidetunnel only visible when crystals are bright
  * A hidden platform above a water pool
* Can act as a natural guide for players who enter at night.

### **Note**

No interaction, collection, or crafting from these crystals at this stage.

