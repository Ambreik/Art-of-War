## **4. Cave Murmur Sound Cues (Expanded)**

### **Description**

The caverns warn of upcoming tide shifts via environmental audio changes.

### **Sound Cue (10 seconds before shift)**

1. Low rumbling echoes
2. Slight water sloshing noise
3. Tiny falling stones
4. A deep, hollow “breathing” resonance from cavern chambers

### **Why It Exists**

This acts as a **natural telegraph** instead of UI messaging.

### **Gameplay Impact**

* Gives players a chance to retreat from dangerous submerged zones.
* Helps players time dives to reach air pockets before a tide rise.

