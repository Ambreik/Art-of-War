			 Air Pocket Diving

### **Description**

Underwater sections include **natural bubble pockets** trapped in ceiling cracks.

### **Function**

* Reset oxygen depletion for divers.
* Reduce oxygen drain by **40%** while standing inside the pocket.
* Allow short rest moments in deeper water paths.

### **Placement**

* Typically found:

  * Under rocky overhangs
  * Inside narrow submerged tunnels
  * Along a circular underwater loop that connects two chambers

### **Risk Note**

Air pockets are **small**: players must remain partially submerged and cannot fight inside them.

