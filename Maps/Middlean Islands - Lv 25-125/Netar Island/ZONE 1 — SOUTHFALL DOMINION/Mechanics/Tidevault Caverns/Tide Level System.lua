			Tide Level System

### **Description**

The caverns are directly connected to the sea through cracks and tunnels, causing **dynamic tide cycles** that change the playable layout.

### **Cycle**

* A full tide cycle lasts **20 minutes**:

  * **Low Tide (10 minutes)** – Maximum exploration
  * **High Tide (10 minutes)** – Flooded passages, altered navigation

### **Gameplay Impact**

**Low Tide:**

* Reveals **side tunnels**, **ore pockets**, and **a small sandy nest area** where crustacean-type mobs spawn.
* Allows access to a natural stone bridge leading deeper.

**High Tide:**

* Water rises up to waist/chest height in many rooms.
* Some corridors become **entirely submerged**.
* Special **Tidefish** and **Hollowfin Eels** spawn only during high tide.

### **Safe Notice**

Before tides shift, the cavern walls shake slightly and pebbles fall — a **soft warning** for players exploring deep areas.

