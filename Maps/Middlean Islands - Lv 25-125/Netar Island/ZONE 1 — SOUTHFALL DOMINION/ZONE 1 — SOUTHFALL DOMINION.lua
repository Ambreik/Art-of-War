# **7. Environmental Mechanics**

## **A. Global Mechanics (Standard Across the World)**

1. **Day/Night Cycle** – Night increases monster aggression, stats, and drop rate.
2. **Fatigue Waters** – Swimming drains stamina; deep sea causes drowning stacks.
3. **Water Flow & Currents** – Rivers and tides push movement and affect fishing stability.
4. **Environmental Audio Layering** – Sounds shift depending on terrain.
5. **Monster Awareness** – AI sight and sensitivity increase at night.
6. **Noise Awareness** – Running, fighting, and harvesting attract nearby creatures.

---

## **B. Specific Environmental Mechanics (Zone-Exclusive)**

### **1. Coastal Wind Shear**

Areas around Broken Coastline create sudden lateral bursts of wind.
These winds can slightly shift player movement when near cliff edges.

### **2. Tidal Access Windows**

Tidevault Caverns are fully traversable only during **low tide**.
At high tide, inner chambers flood completely.

### **3. Aquatic Ambush Zones**

Fish and aquatic predators hide in deep seagrass beds.
Players cannot see nameplates of aquatic creatures **until they surface** or get close enough to them inside waters.

### **4. Fog Pockets**

Small fog clusters around the Unfinished Citadel reduce visibility sharply between 1–4 meters above ground.
(Does not affect gameplay unless used later.)

---

## **C. Resource-Based Mechanics**

### **1. Night-Bloom Cycle**
Certain herbs (Emberdew Lotus) appear only at night.

### **2. Tide-Triggered Resources**
Cliffshade Fungus appears only during **low tide** within the caverns.

### **3. Quarry Resonance**
At Sundrop Quarry, mining Quarrysteel Nodes has a 10% chance at night to “resonate,” producing double ore but depleting instantly afterward.

### **4. Delta Drifting**
Fishing at the Shimmering Delta may slightly shift the player’s position due to underwater currents.

---

## **D. Creature / AI Mechanics (Zone Exclusive)**

*(No monster stats, just behavior)*

### **1. Cliff-Stalker Behavior**
Creatures near the Broken Coastline prefer ambushing from elevated rock edges.

### **2. Sound Echo Sensitivity**
Inside root hollows, creatures detect players from longer distances due to sound bouncing off tree roots.

### **3. Waterline Predators**
Some beasts patrol strictly along the waterline, adjusting pathing with tides.

