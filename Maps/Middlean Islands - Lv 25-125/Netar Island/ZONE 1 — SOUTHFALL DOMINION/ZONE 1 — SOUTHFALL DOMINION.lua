			ZONE 1 — SOUTHFALL DOMINION

**(The Southern Region of Netar Island)**

---

# **1. Overview / Introduction**

Southfall Dominion forms the broad, sun-struck **southern expanse of Netar Island**, serving as the island’s first major region encountered by travelers crossing the Great Sea. Its terrain presents an elegant balance between rugged coastal cliffs and wide, sandy coves, gradually rising inland toward the looming central mountain range. The region is characterized by warm air currents, expansive beaches, shallow deltas, scattered cliffs, and early forest growth forming the “first layer” of Netar’s tropical vegetation.

Southfall Dominion is unique for hosting:

* The **main landing port** for all travelers arriving from the Capital Island
* A **small settlement** built near a jagged shoreline cliff
* The **unfinished citadel ruins**, which serve as the exterior entrance to the Tomb of Great Crusaders megadungeon
* A long, solitary **coastal road** that leads northward into the heart of the island
* The beginning of the **flying-mount allowed zone** before the invisible northern boundary

The atmosphere here is warm, bright, and deceptively peaceful — a welcoming introduction to the island that hides dangers in its cliffs, caves, and coastal waters. Southfall Dominion embodies the “starting point” feel while setting the tone for the varied wilderness ahead.

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

# **2. Geography & Terrain**

Southfall Dominion stretches across the entire southern length of the island, forming the lower base of Netar’s “L-shaped” silhouette. The zone’s defining terrain traits include:

### **Coastal Shape & Layout**

* The southern coastline bends gently, with a long stretch of sand interrupted by clusters of broken stone shelves.
* The **eastern extension (“the tail of the L”)** protrudes outward into the Great Sea — this is the area containing the landing port.
* Inland terrain rises steadily toward the **central mountain ridge**, though not sharply; the slope is gradual and forested.

### **Elevation & Land Gradients**

* The coastal perimeter is low and smooth, broken by occasional cliffs rising 10–30 meters.
* Moving inland, the ground transitions into forest foothills, with rolling elevations and uneven terrain.
* Several **natural terraces** exist due to ancient erosion against the mountain’s base.

### **Water Features**

* Two short rivers originate from the southern mountain slopes, cutting through the zone before reaching the sea.
* These rivers form shallow deltas with mixed sand and stone.
* A narrow waterfall descends from a mid-elevation cliff to the west, forming a freshwater pool hidden between trees.
* Tidal caves are found on both the southeast and southwest edges of the region.

### **Rock Formations & Cliffs**

* Jagged basalt formations emerge near the eastern port region.
* South-central cliffs reveal broken walls of sedimentary stone layered over volcanic rock from ancient eruptions.
* Inland boulders are scattered, forming natural barriers and clearing edges.

### **Biome Transition**

* The southernmost strip is open coastline.
* The mid-south transitions into **first-layer tropical forest**, with shorter trees and wide canopies.
* Approaching the mountain, the trees grow taller and denser, with roots gripping the slopes and large ferns dominating the ground.

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

# **3. Climate & Environmental Behavior**

### **Day/Night Differences**

* **Day:** Warm, bright, and clear. Good visibility over coastlines and open trails.
* **Night:** Light mist rolls in from the sea; the cliffs feel colder, and the forest becomes significantly darker due to dense canopy coverage.

### **Fog & Humidity**

* The region is moderately humid, with early-morning fog drifting from the sea toward the citadel ruins.
* Fog tends to cluster around the base of cliffs and river basins.

### **Wind Systems**

* Southfall Dominion receives strong, consistent **southern marine winds**.
* Cliffs on the southwest ridge generate **gust tunnels**, occasionally strong enough to push players slightly off balance.

### **Visibility**

* Coastal zones: High visibility during the day; moderate at night.
* Forest outskirts: Medium visibility due to foliage, lower at night due to canopy shadows.

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

# **4. Vegetation & Natural Features**

### **Forest Structure**

* **Outer Tropical Belt:** Shorter palms, dense shrubs, and broad-leaf trees.
* **Inner Foothill Vegetation:** Taller trees with thicker trunks, more moss, larger ferns, and thicker undergrowth.

### **Coastal Vegetation**

* Seagrass patches
* Thick salt-resistant shrubs
* Occasional flowering reed beds around delta waters

### **Undergrowth & Flora**

* Hanging mosses from tree branches
* Vines climbing low cliffs
* Fungus clusters thriving along shaded fallen logs
* Dense ferns forming natural “walls” at certain paths
* Red-shelled mushrooms growing in humid corners
* Natural stone arches near the water, carved smooth by wind and wave erosion

### **Unique Natural Formation**

* A long **root bridge** created by intertwined trees crossing a narrow ravine near the foothills.
* A **sunken grove**, where part of an old forest slipped due to erosion and now lies lower than the surrounding terrain, isolated and quiet.
* A **coastal blowhole** near the southwest cliffs that erupts with mist when waves crash strongly.

