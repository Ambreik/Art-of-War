**Bonus Drop:**

* *Warm Iron Chunk* (used later in heat-based forging recipes)

---

## ### **2. Quarrysteel Nodes**

* Concentrated near Sundrop Quarry’s deepest areas.
* **Rank III**

**Special Details:**

* Extremely dense metallic veins with blue-black cracks.
* **Requires a Reinforced Pickaxe** (Rank II pick)
* Has a 20% chance to resist the first mining strike.
* Produces the iconic echo sound that starts resonance reactions.

**Bonus Drop:**

* *Quarrysteel Fragments*
* Small chance to drop *Hardend Steel Core*

