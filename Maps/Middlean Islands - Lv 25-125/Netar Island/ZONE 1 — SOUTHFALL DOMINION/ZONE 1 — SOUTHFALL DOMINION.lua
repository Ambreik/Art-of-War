# **5. Points of Interest (POIs)**

### **1. The Landing Port of Southfall**

A small wooden port built on the southeast extension. Surrounded by fractured stone plateaus and sheltered shallow waters. First location players see when arriving.

---

### **2. Southfall Village**

Located west of the port, partially built on raised cliffs overlooking the coastline. Narrow stone steps link the upper houses to lower pathways. This is the primary settlement of the southern region.

---

### **3. The Unfinished Citadel**

A massive stone fortress built into the base of the mountain, abandoned long ago.

* Broken towers lean against rocky slopes.
* A shattered gate provides access to the megadungeon beneath the mountain.
* Scaffolding, collapsed bridges, and partially carved inner halls define the outward structure.

---

### **4. The Sundrop Quarry**

A half-abandoned stone quarry west of the village with carved terraces, lift platforms, and leftover chiseled blocks.
Its lower pits are partially flooded, forming small reflective pools.

---

### **5. Tidevault Caverns**

A network of tidal caves near the southwest corner:

* Accessible only during low tide
* Interior filled with smooth stone and crystal pockets
* Contains submerged tunnels and hidden chambers deeper inside the cliff wall

---

### **6. The Shimmering Delta**

At the outflow of the eastern river, sunlight reflects off mineral-rich silt, giving the waters a bright sheen.
Sandbars form shifting patterns, creating small natural paths that appear and disappear with tides.

---

### **7. The Broken Coastline**

Large fractured rocks, collapsed cliffs, and unstable edges define this dangerous coastal stretch west of the citadel.

---

### **8. Old Watcher’s Rise**

A tall rock pillar serving as a lookout point from ancient times. Carved footholds and remnants of an old signal system remain.

---

### **9. Rootfall Hollow**

A depression in the forest where massive tree roots have created a sprawling natural shelter. Moss-covered stone formations hide beneath the root canopy.

