			Discovery XP / Hidden Location Rewards

	Concept : Reward exploration in the deep pocket of the Mossroot Basin.

	Mechanics :
* Players gain hidden discovery XP when entering the cabin or pond area for the first time
* Visual cue: small golden shimmer around key environmental features (waterwheel, pier, drying racks)
* Can unlock future interactions, quests, or lore entries

---

	Concept : Reward players who explore off-path locations with non-intrusive progression, subtle visual feedback, and long-term narrative unlocks rather than 
immediate material gain.
	This system is silent, non-repeatable, and account-respectful.

---

	Discovery Triggers : 

	Discovery XP is granted only once per character upon fulfilling any of the following:
* Entering "Deepwater Clearing" for the first time
* Stepping onto the cabin porch
* Approaching the pond edge within interaction range

> The first trigger grants XP; the others enhance discovery feedback but do not stack XP.

---

	Discovery XP Mechanics

* XP Type: "Hidden Discovery XP"
* Does not display in standard XP bar
* Contributes to:
	* Exploration milestones
	* Environmental mastery tracking
	* Future unlock flags

	XP Grant Rules :
* Granted once per character
* Cannot be boosted
* Cannot be shared in party
* Does not trigger combat or quest UI

---

	Visual Feedback (Non-Intrusive) : Upon first discovery, players observe:

	Environmental Shimmer Effects

* Waterwheel
	* Soft golden motes drift from turning paddles
	* Glow fades after ~8 seconds

* Pier & Pond Surface
	* Gentle light ripple beneath water surface
	* No sound cue (silence reinforces calm)

* Drying Racks
	* Brief glint across hanging fish scales

> These effects do not repeat on future visits.

---

	UI & Player Feedback

* No pop-up
* No quest log entry
* Optional journal entry unlocked:
	* “Deepwater Clearing — a place where the island breathes slowly.”

---

	Unlockable Flags (Future-Proofing)

	Discovery enables latent flags that may later unlock:

* New dialogue lines from Oscar Fisher D.
* Access to:
	* Rare fishing lures
	* Hidden shop rotations
* Environmental reactions:
	* Increased rare fish emergence stability
	* Reduced aggression from certain aquatic entities
* Lore interactions:
	* Scroll fragments
	* D. Clan references

> None activate immediately — discovery marks the player, not rewards them instantly.

---

	Anti-Exploitation Safeguards : 

* Cannot be farmed
* Cannot be triggered by mounts
* Cannot be shared via summoning
* Logging out inside does not retrigger discovery

---

	Design Intent (Developer Note) : 

	This system exists to teach players:

> “Some places are not meant to announce themselves.”

	It reinforces:
* Exploration over efficiency
* Observation over interaction
* Memory over reward spam

