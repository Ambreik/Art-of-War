			Dynamic Pond Currents

	Concept : Subterranean water flow affects fishing, movement, and small environmental interactions.

	Mechanics :
* Streams occasionally shift debris or floating platforms (logs, lily pads)
* Players using fishing or standing on platforms must adapt timing to avoid being pushed into shallow hazards or out of range
* Subtle visual cue: small waves and bubbles indicate current direction and speed
* Optional: players can use skills (like gliders, mounts, or water manipulation abilities) to stabilize their position

---

	Concept : Hidden subterranean water flows subtly influence the surface, creating shifting micro-currents that affect fishing precision, platform stability, and 
player positioning.
	Currents are predictable through observation, not UI indicators.

---

		Current Behavior

	Flow Patterns :

* Currents shift:
	* Slowly
	* Periodically

* Pattern types:
	* Gentle lateral drift
	* Circular eddies
	* Sudden directional pulses

* Changes occur:
	* Every 5–12 minutes
	* Not synchronized across the area

---

		Environmental Effects

	Floating Objects : 

	Affected objects include:
* Lily leaves
* Drift logs
* Fishing debris
* Net lines

	Behavior:
* Objects drift within bounded paths
* Some rotate slowly
* Speed varies by current strength

---

		Player Interaction Impact

	Fishing : 

* Cast range subtly altered
* Hook tension fluctuates
* Certain fish:
	* Only bite when currents align
	* Escape more easily against strong flow

> Skilled players adjust cast timing, not position alone.

---

	Standing & Resting : 

* Players standing on floating objects:
	* Slowly repositioned
	* May drift out of optimal zones
* If drifted into:
	* Shallow hazards
	* Stream edges
		→ Player is gently pushed off

	No damage is dealt.

---

		Visual & Audio Cues

	Visual Signals : 
* Small directional ripples
* Rising air bubbles
* Light distortion beneath surface

	Audio Signals : 
* Subtle water pull sound
* Slight increase in pond movement noise

> These cues intensify before stronger shifts.

---

		Skill & Item Interaction

	Players may mitigate current effects by:
* Using gliders for repositioning
* Activating water-affinity skills
* Deploying weighted fishing tools
* Timing interactions during low-flow windows

	No skill fully nullifies currents — only reduces impact.

---

		Optional Advanced Interaction

* Certain lures:
	* Perform better when cast with the current
* Some rare fish:
	* Appear only during specific current patterns
* Floating platforms:
	* Become inaccessible during high-flow states

---

	Anti-Exploit Safeguards : 
* No permanent anchoring allowed
* Currents ignore player attempts to block flow
* AFK players drift off platforms naturally

---

		Teaching Layer

	This system teaches:

> “Water is never still — only patient.”

	Players learn to:
* Read movement
* Wait for opportunity
* Accept imperfect control

