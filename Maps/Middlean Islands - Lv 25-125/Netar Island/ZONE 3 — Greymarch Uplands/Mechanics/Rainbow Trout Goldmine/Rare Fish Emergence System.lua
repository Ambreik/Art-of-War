			Rare Fish Emergence System

	System Purpose : The "Rare Fish Emergence System" transforms the "Deepwater Clearing" pond into a low-frequency, high-value environmental interaction, rewarding
patience, awareness, and coordination while preserving the zone’s quiet, secluded identity.
	This system is non-repeatable on demand, non-farmable, and intentionally discourages crowding.

---

		Core Rules

	Spawn Logic : 
* Spawn Interval : Randomized between 2–6 hours (server-based)
* Active Limit : Only one rare aquatic entity may exist at a time
* Lifetime : ~15 minutes if unengaged
* Despawn Behavior : Silent fade beneath the water surface (no loot, no reset penalty)

---

		Environmental Cues

	Rare spawns are never announced globally. Instead, players must notice:
* Subtle surface ripples
* Light refraction or faint shimmer
* Occasional single splash or jump
* Ambient audio changes (soft harmonic tones, deep water resonance)

	These cues are visible only within the pond vicinity.

---

		Interaction Rules

	Engagement Restriction
* Only one player may interact with a rare fish at a time
* When engaged:
	* Pond becomes “claimed”
	* Other players may observe but not interact

	Crowd Penalty
* If multiple players enter the pond interaction radius:
	* The rare fish immediately escapes
	* No cooldown is consumed
	* No item loss occurs

	This enforces coordination, trust, and communication.

---

		Failure Conditions (Universal)

	A rare fish attempt fails if:
* The player breaks required interaction rules (movement, timing, tension)
* Another player enters the active zone
* The player cancels the interaction
* The rare fish’s timer expires mid-attempt

	Failure results in:
* Fish escape animation
* Temporary water disturbance
* No aggressive retaliation

