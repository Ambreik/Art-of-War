---

	The Gulpmaw Levi-Tad : 

* Type : Hostile Aquatic Monster
* Rank : III
* Aggression Scope : Solo-only (targets lure user exclusively)

	Trigger Condition : 

* Player repeatedly uses "Oscar’s Experimental Lure" in the pond
* Internal counter tracks lure overuse
* No visual warning beyond subtle deep-water ripples

---

	Description : A massive, bloated tadpole-like beast erupts from the depths.
	Its body is slick and muscular, tapering into a powerful tail. Two oversized fin-like extremities function as crude arms, allowing it to pull itself across land
briefly.
	Its mouth opens unnaturally wide, revealing rows of soft grinding ridges rather than teeth.

---

	Combat Rules : 

* Only the lure user is targeted
* Other players:
	* Cannot damage it
	* Cannot draw aggro
	* Cannot assist in combat
* The creature ignores mounts, pets, and companions

---

	Abilities : 

• Gulp Surge : A sudden forward lunge that knocks the player back.
• Fin Slam : Uses its extremities to slam the ground, causing short stun.
• Swallow Drag : Attempts to pull the player toward the pond, increasing danger near water.
• Amphibious Retreat : If player survives long enough, the creature retreats violently into the water.

---

	Failure & Outcome : 

* Defeating the creature grants:
	* No loot (by design)
	* Temporary fishing lockout in the pond
* Death causes:
	* Creature despawns immediately
	* Lure cooldown is reset

	Design Intent : This creature exists purely as a discipline check and lore reinforcement — not a farmable enemy.

