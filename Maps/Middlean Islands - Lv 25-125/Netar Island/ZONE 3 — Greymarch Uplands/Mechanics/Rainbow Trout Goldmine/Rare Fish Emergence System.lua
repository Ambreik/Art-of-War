		Rare Fish Pool (Deepwater Clearing Exclusive)

	1. Rainbow Veil Trout : 

* Rank : III
* Role : Signature rare fish of the location

	Visual Identity : 
* Semi-translucent body
* Subtle prismatic color shift
* Leaves faint light trails beneath water

	Spawn Cue : 
* Water surface gains soft rainbow refraction
* Low harmonic hum replaces ambient silence

	Interaction Mechanic : 
* Player must maintain balanced line tension
* Over-pulling or sudden inputs cause instability
* Calm, rhythmic input is rewarded

	Special Rule : 
* Any additional player entering the pond radius causes instant escape

---

	2. Goldmine Lurker : 

* Rank : III
* Role : High-risk, bottom-dwelling rare

	Visual Identity : 
* Large moving shadow along pond floor
* Occasional mud plumes

	Spawn Cue : 
* Water darkens slightly
* Deep, muffled bubbling sound

	Interaction Mechanic : 
* Requires "weighted bait"
* Applies periodic pull toward water edge
* Tests stamina and positioning control

	Failure Effect : 
* Player is knocked down (non-lethal)
* Lurker retreats into sediment

---

	3. Stillwater Phantom Fry : 

* Rank : II
* Role : Precision and discipline test

	Visual Identity : 
* Body never fully visible
* Detected only by concentric ripples

	Spawn Cue : 
* Pond surface becomes unnaturally still
* Ambient sound dampens

	Interaction Mechanic : 
* Player must remain completely motionless
* Any movement input breaks the attempt

---

		Supporting Aquatic Entities

	Passive System Creatures (Non-hostile)

	1. Netcoil Eel : 
* Occasionally tangles fishing lines
* Requires quick-release input
* Successful handling slightly improves rare spawn odds

	2. Lilyback Tortoise :
* Large, passive aquatic creature
* Stabilizes water currents when nearby
* Cannot be attacked or captured

	3. Glassfin Skimmer : 

*A thin, translucent fish with elongated fins resembling panes of glass. 
*It glides just beneath the surface, reflecting ambient light and occasionally breaking the water in short arcs.

* Moves in slow, looping paths near the surface
* Never interacts with fishing lines
* Flee behavior only if directly splashed or disturbed

* Serves as a false positive for rare fish cues
* Its surface movement may mislead impatient players
* Presence slightly increases ambient shimmer effects

---

	4. Rootcoil Amphipod

*A large, moss-covered crustacean-like creature clinging to submerged roots and stones. Its body slowly flexes as it filters nutrients from the water.

* Remains stationary for long periods
* Occasionally shifts position, releasing sediment clouds

* Sediment plumes may obscure rare fish visuals
* Cannot be hooked or netted
* Adds environmental depth without danger

---

		Hostile Aquatic Creatures

	*(Aggressive only if caught or provoked)*

	1. Mirejaw Snapper

* Type : Hostile Aquatic Monster
* Rank : II
* Aggression Trigger : Accidentally hooked or disturbed

	Description : A stocky freshwater predator with a wide, blunt jaw and overlapping stone-like scales. Its eyes sit high on the skull, allowing it to watch from 
just below the surface.

	Engagement Trigger :
* Can be hooked while fishing normally
* Immediately snaps the line and lunges toward the player

	Combat Behavior :
* Short-range lunges from water edge
* Applies "Bleeding Bite"
* Retreats briefly between attacks

	Special Rule :
* Will disengage if player retreats far enough from the pond
* Does not pursue beyond Deepwater Clearing

---

	2. Siltbound Lamprey

* Type : Hostile Aquatic Monster
* Rank : II
* Aggression Trigger : Net fishing or bottom disturbance

	Description : A long, pale, eel-like creature with a circular, toothed mouth. It emerges violently from the silt when disturbed.

	Combat Behavior :
* Latches onto the player, reducing movement speed
* Requires repeated actions to dislodge
* Leaves behind temporary stamina drain

	Environmental Tie-in :
* More likely to appear near Rootcoil Amphipod zones
* Sediment clouds increase its spawn chance

