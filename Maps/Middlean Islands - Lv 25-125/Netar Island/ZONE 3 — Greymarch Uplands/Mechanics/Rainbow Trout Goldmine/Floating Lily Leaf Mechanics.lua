			Floating Lily Leaf Mechanics

	Concept : As a mini environmental “puzzle” platform.

	Mechanics :
* Some oversized lily leaves float and bounce players periodically
* Only one player at a time may sit
* Mounts/pets break the leaf if used
* Visual cue: water vibrates or splashes before bounce
* Gameplay tie-in: Could assist reaching small islands, fishing spots, or hidden loot

---

	Concept : Oversized freshwater lily leaves act as temporary natural platforms, offering traversal, positioning, and playful risk.
	They are not permanent, not safe, and not meant to be crowded.

---

	Spawn & Placement

* Found along:
	* Stream edges
	* Slow-flowing pools
	* Narrow crossings in "The Sunken Run"
* Spawn frequency:
	* Sparse and deliberate
* Some leaves are:
	* Stationary
	* Slowly drifting within a limited radius

---

	Interaction Rules : 

	Occupancy : 

* Only one player may occupy a leaf at a time
* If:
	* A second player
	* A mount
	* A pet
		steps onto the leaf:
	* The leaf immediately sinks and despawns

> No bounce occurs when overloaded — the leaf fails outright.

---

	Stability Timer

* When a player steps onto a leaf:
	* A hidden stability timer begins (randomized per leaf)
* Typical duration:
	* **8–15 seconds**

---

		Bounce Mechanic

	Pre-Bounce Warning : 

Before the bounce triggers:

* Water beneath the leaf:
	* Begins vibrating
	* Produces surface splashes
* The leaf:
	* Flexes slightly
	* Tilts rhythmically

> This warning lasts ~2 seconds and is clearly readable.

---

	Bounce Execution

* Player is launched:
	* Vertically and slightly forward
* Launch height:
	* Enough to:
		* Reach small ledges
		* Cross narrow gaps
		* Reposition for fishing
* Bounce cannot be canceled

---

	Fall Resolution

After the bounce:

* Players must:
	* Use gliders
	* Use slow-fall passives
	* Accept fall impact
* Falling into water:
	* Is safe
	* Resets position downstream

---

	Leaf Lifecycle

| State    | Description                 |
| -------- | --------------------------- |
| Idle     | Floating normally           |
| Occupied | Stability timer active      |
| Warning  | Water vibrates, leaf flexes |
| Bounce   | Player launched             |
| Collapse | Leaf sinks after bounce     |
| Regrowth | New leaf appears later      |

* Regrowth timer:
	* Several minutes
	* Not synchronized across leaves

---

	Gameplay Tie-Ins

* Reaching:
	* Hidden fishing spots
	* Small islets
	* Resource nodes
* Temporary rest platform:
	* Safe from ground threats
	* Not safe from ranged attacks
* Environmental puzzles:
	* Chain bounces
	* Time positioning with currents

---

	Anti-Exploit Rules

* Cannot be frozen in place
* Cannot be anchored
* Cannot be used to block paths
* Cannot be farmed for movement abuse

---

	Player Learning Cue

The mechanic teaches:

> “Nature gives you time — not safety.”

Players who:

* Observe water behavior
* Time their movement
* Prepare mobility tools are rewarded.

