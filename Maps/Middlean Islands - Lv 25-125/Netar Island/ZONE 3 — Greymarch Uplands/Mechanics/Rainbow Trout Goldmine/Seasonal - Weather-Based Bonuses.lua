			Seasonal / Weather-Based Bonuses

	Concept : The pond’s ecology reacts to environmental conditions.

	Mechanics :
* Fog, rain, or daylight variations can:
	* Increase chance for rare fish
	* Alter visibility or pond interaction difficulty
	* Slightly adjust NPC dialogue or ambient sound cues
* Visual/audio feedback: ambient fog, soft glints in water, distant bird calls

---

	Concept : The pond and its ecosystem react to natural environmental conditions like fog, rain, and daylight.
	These conditions influence fish activity, platform interactions, and ambient cues, subtly rewarding observant players.

---

	Environmental Triggers : 

	Fog       
- Reduces visibility on pond surface
- Slightly increases rare fish spawn chance
- Lily leaf bounce timing harder to predict
	Rain      
- Water surface ripples stronger
- Increases general fish activity
- Some rare species appear only during rainfall
	Daylight  
- Morning / dusk glints may reveal fish schools
- Afternoon light may trigger rare aquatic creature emergence
- NPC dialogue subtly references light patterns 

---

	Visual & Audio Feedback : 

* Visual :
	* Ambient fog softly rolling across pond
	* Water glints, shimmering scales catching sunlight
	* Lily leaves and floating debris move naturally with wind/rain effects

* *Audio :
	* Distant bird calls or frogs
	* Rain hitting pond surface
	* Subtle splashes or gurgles indicating hidden fish movement

---

	Gameplay Effects

* Fishing :
	* Fog reduces line visibility and aiming range
	* Rain can slightly increase bite rates for specific fish
	* Certain rare fish are weather-gated

* Lily Leaf Platforms :
	* Strong wind or rain may make leaf bounces less predictable
	* Visual warning cues amplified (bigger splashes or leaf tilt)

* NPC Dialogue :
	* Oscar may comment on unusual weather patterns, giving flavor or hints
	* Dialogue may hint at rare species activity

---

	Design Philosophy : 

> “Nature rewards those who notice her moods.”

* Encourages players to observe and adapt rather than rely purely on mechanics.
* Creates dynamic exploration and repeat play incentives.
* Integrates naturally with all pond mechanics: currents, lily leaves, waterwheel, and rare fish emergence.

