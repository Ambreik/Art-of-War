			Environmental Interaction with Waterwheel

	Concept : Players can manipulate the waterwheel and connected pulley nets.

	Mechanics :
* Rotating wheel triggers:
	* Raises/lower nets (optional for future fishing minigames)
	* Activates mechanical pulses in the pond (disturbs fish, shifts lily leaves)
* Could require skill or teamwork to optimize fishing or rare fish spawn chances

---

	Concept : The waterwheel is a manual environmental lever that influences pond behavior, fish activity, and floating objects.
	It is not required, not optimal to spam, and best used deliberately.

---

		Waterwheel Interaction States

	Idle State : 
* Wheel turns slowly on its own
* Nets rest partially submerged
* Pond currents remain natural

---

		Player Interaction

* Players may:
	* Push
	* Pull
	* Lock briefly
* Interaction is manual — no UI prompt beyond “Interact”

---

		Mechanical Effects

	1. Pulley Net Adjustment (Passive)

	Rotating the wheel:
* Raises or lowers:
	* Submerged net frames
	* Guide ropes beneath water
* Effects:
	* Temporarily reshapes fish paths
	* Can expose or hide certain species

> Nets do not capture fish automatically.

---

	2. Pond Pulse Disturbance

	Each strong rotation creates a mechanical pulse :
* Sends a vibration through the pond
* Effects include:
	* Fish scattering
	* Lily leaves shifting position
	* Floating debris reorienting

	Pulse strength depends on:
* Rotation speed
* Number of players assisting

---

	3. Rare Fish Influence (Subtle)

* Controlled use:
	* Increases chance of rare fish emergence
* Excessive use:
	* Spooks fish
	* Temporarily suppresses rare spawns

> This creates a risk-reward rhythm.

---

		Teamwork & Skill Layer

	Solo Use : 
* Slow rotation
* Minor effects
* Safe but inefficient

	Group Use : 
* Faster rotation
* Stronger pulses
* Higher risk of disruption

	Optional future expansion:
* Two players stabilize wheel while one rotates

---

	Visual & Audio Feedback : 

* Wood creaks under strain
* Water churns briefly
* Nets shimmer faintly when repositioning
* Lily leaves visibly shift after pulses

---

	Cooldowns & Limits : 

* Wheel cannot be spammed:
	* Soft internal cooldown
* Overuse results in:
	* Wheel resistance
	* Diminished effects
* Wheel slowly returns to idle state when left alone

---

	Anti-Exploit Safeguards : 
* Cannot be locked permanently
* Does not grant direct loot
* Does not trigger combat or quests

---

	Design Philosophy

This mechanic exists to teach:

> “Influence, not control.”

	Players who:
* Observe water behavior
* Coordinate lightly
* Act sparingly gain subtle advantages — never guarantees.

---

		Hidden Mastery Interaction — Waterwheel

	(Rainbow Trout Goldmine – Deepwater Clearing)

---

	Concept : Players who carefully observe, time, and coordinate the waterwheel and pulleys unlock hidden environmental bonuses.
	No overt UI hints are given — learning is purely experiential.

---

	Trigger Conditions : 

1. Timing Mastery : 
	* Rotate the wheel exactly when the pond pulse aligns with a lily leaf bounce.
	* Consecutive successful alignments (3–5 pulses) are required.

2. Team Coordination : 
	* Two or more players interact simultaneously:
		* One stabilizes the wheel
		* One rotates at precise rhythm
	* Unlocks subtle fish attraction effect.

3. Observation : 
	* Players must notice:
		* Lily leaves shift timing
		* Net frames rise/fall
		* Water pulse patterns

> The system ignores brute force attempts — spamming wheel rotations does nothing.

---

	Effects of Hidden Mastery : 

* Temporary increase in rare fish emergence chance (+10–15% for 10–15 minutes)
* Subtle visual cue:
	* Golden shimmer along nets and lily leaves
	* Small glowing ripple around pond edges
* Unlocks hidden journal entry for players:
	* “A true fisherman dances with water, not against it.”
* Can be repeated after a soft cooldown (~1–2 hours)
* Does not give direct items, XP, or combat advantage

---

	Feedback Layer : 
* No pop-ups
* No quest notification
* Environmental visual cues only
* Players may notice increased fish activity naturally

---

	Anti-Exploitation Safeguards : 
* Solo mastery achievable but more difficult
* Brute-force wheel rotations ignored
* Cannot trigger rare fish immediately — effects are gradual and subtle
* Cooldown prevents repeated instant mastery

---

	Design Philosophy : 

> “True mastery is observed, not forced.”

	This rewards:
* Player patience
* Environmental awareness
* Timing skill
* Team coordination

