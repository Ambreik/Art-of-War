			Overhang Shelter Zones

	Description : Natural stone overhangs provide brief shelter from wind and exposure, forming safe pockets along the cliff face.

	Mechanics : 
* Inside shelter zones:
	* Wind effects are reduced
	* Casting interruption chance lowered
* Limited space prevents prolonged stays

	Player Interaction

* Used as:
	* Rest points
	* Observation spots
	* Recovery zones between hazards

	Feedback : 
* Audio: wind dampening effect
* Visual: calmer particle behavior

