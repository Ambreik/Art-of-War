			Narrow Path Exposure Risk

	Description : Hidden fissures and ledges form narrow descent paths toward the stone beaches below. These paths are unstable and exposed to wind and erosion.

	Mechanics : 

* Paths are:
	* Single-file
	* No combat-friendly space
* Sudden wind gusts increase fall risk
* Falling does not always kill but causes:
	* Severe damage
	* Forced recovery state

	Player Interaction : 

* Players must:
	* Move carefully
	* Avoid sprinting
* Certain classes or equipment may reduce slip chance

	Feedback : 
* Visual: cracked stone, loose gravel shifts
* Audio: stone scraping, falling pebbles

