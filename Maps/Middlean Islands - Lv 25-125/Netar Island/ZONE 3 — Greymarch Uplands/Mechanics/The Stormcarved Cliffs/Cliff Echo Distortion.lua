			Cliff Echo Distortion

	Description : The jagged stone walls reflect sound unevenly, creating false audio cues. Footsteps, combat noise, and creature calls echo unpredictably across 
the cliffs.

	Mechanics : 
* Sounds are replayed from altered positions
* Enemy awareness may be misdirected
* Some creatures may investigate echoes instead of players

	Player Interaction : 
* Loud actions increase echo range
* Stealth movement minimizes sound distortion
* Players can intentionally create noise to mislead enemies

	Feedback : 
* Audio: delayed or warped echoes
* Subtle vibration effect on nearby stone surfaces

