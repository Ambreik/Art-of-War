			Cliffside Hum Resonance

	Description : During strong winds, the cliffs emit a low, resonant hum caused by air passing through eroded stone channels.

	Mechanics :
* Hum intensity rises with wind strength
* The resonance:
	* Masks other sounds
	* Reduces detection accuracy for both players and creatures

	Player Interaction : 
* Stealth becomes less reliable
* Players must rely more on vision and positioning
* Certain abilities may fail or be delayed due to vibration

	Feedback : 
* Audio: deep, sustained tonal hum
* Visual: subtle camera vibration near resonant zones

