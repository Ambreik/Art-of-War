			Wave Surge Impact

	Description : Powerful waves periodically crash into the cove, sending surges of water across the stone floor.

	Mechanics : 
* Surge intervals are semi-predictable
* Waves can:
	* Push players backward
	* Interrupt actions
	* Briefly disorient vision

	Player Interaction : 
* Players can brace near rock formations
* Poor positioning may force repositioning or knockdowns

	Feedback : 
* Audio: rising wave roar before impact
* Visual: incoming foam wall and spray

