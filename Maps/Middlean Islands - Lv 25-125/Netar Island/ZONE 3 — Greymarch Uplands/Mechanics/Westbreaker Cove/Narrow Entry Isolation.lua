			Narrow Entry Isolation

	Description : The cove is accessible only through a tight cliff gap, cutting off line of sight and escape routes.

	Mechanics : 
* Entry is single or narrow-group passage
* Limited retreat options once inside
* Sound carries poorly back through the gap

	Player Interaction : 
* Encourages group commitment before entry
* Discourages hit-and-run tactics

	Feedback : 
* Visual: steep stone walls closing in
* Audio: muffled exterior wind once inside

