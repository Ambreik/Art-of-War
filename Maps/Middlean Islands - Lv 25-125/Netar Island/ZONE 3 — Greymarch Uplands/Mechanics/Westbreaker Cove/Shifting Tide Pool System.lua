			 Shifting Tide Pool System

	Description : The cove floor is covered in shallow pools formed by crashing waves. These pools constantly change as water surges in and drains out through 
cracks in the stone.

	Mechanics : 

* Pools shift position over time
* Water depth fluctuates in cycles
* Movement speed varies depending on pool state

	Player Interaction : 

* Players must adjust positioning as footing changes
* Standing in deeper pools increases stamina drain
* Certain abilities are delayed or weakened while partially submerged

	Feedback : 

* Visual: foam patterns reshaping the ground
* Audio: sloshing water mixed with wave crashes

