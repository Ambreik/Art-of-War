			Foam Concealment Zones

	Description : Thick sea foam accumulates in pockets where waves collide with stone, obscuring vision and movement.

	Mechanics : 
* Foam reduces visibility in affected zones
* Movement inside foam is slowed
* Entities can emerge suddenly from foam pockets

	Player Interaction : 
* Players must:
	* Move cautiously
	* Maintain formation
* Ranged targeting becomes unreliable

	Feedback : 
* Visual: dense, swirling white foam
* Audio: muffled sound while inside foam

