			Fog Spiral Distortion

	Description : The fog does not remain static — wind patterns twist it into slow-moving spirals that subtly shift visibility and battlefield shape.

---

	Mechanics : 
* Fog density zones move slowly
* Line of sight opens and closes dynamically
* Some enemies briefly vanish and reappear at range
* Boss silhouettes may appear distorted or delayed

---

	Player Interaction : 
* Encourages constant awareness
* Punishes tunnel vision
* Makes ranged enemies harder to fully suppress

---

	Feedback : 
* Visual: rotating fog walls, density pulses
* Audio: muffled wind rotation, distant echoing impacts

