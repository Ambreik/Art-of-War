			Boss Spawn Mechanic – Fog Sovereign

	Boss Spawn Rules

* Spawns every 6 hours
* Timer starts from last death
* Only one boss active at a time
* Cannot be soloed
* Boss exists only inside the fog

---

	Boss Concept (Visual Reference)

	Appearance Reference :
* Massive armored humanoid
* Heavy presence, imposing silhouette
* Dense musculature and plated armor
* Slow but overwhelming posture

---

	Fog–Boss Synergy

* Boss does not gain "Hyped Cord"
* Boss is balanced assuming players are buffed
* Leaving the fog mid-fight is heavily punished

