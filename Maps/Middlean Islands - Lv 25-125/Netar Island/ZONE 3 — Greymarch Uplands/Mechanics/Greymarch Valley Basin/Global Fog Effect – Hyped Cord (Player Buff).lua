			Global Fog Effect – "Hyped Cord" (Player Buff)

	Trigger :
* Automatically applied when entering dense fog zones

	Effects :
* Stamina does not decrease
* Attack Speed: Maximum cap
* Movement Speed: Maximum cap

	Duration Rules :
* Active only while inside fog
* Buff fades rapidly (1–2 seconds) after exiting fog
* Cannot be refreshed outside the basin

	Design Intent :
The fog turns the basin into a pressure cooker — power is granted, but danger scales faster.

