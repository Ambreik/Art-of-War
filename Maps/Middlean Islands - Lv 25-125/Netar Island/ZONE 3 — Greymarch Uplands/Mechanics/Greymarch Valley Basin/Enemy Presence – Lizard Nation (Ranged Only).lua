			Enemy Presence – Lizard Nation (Ranged Only)

	All standard enemies in the basin are ranged* using bows, javelins, or magic.

	Monster List : 
1. **Greymarch Lizard Archer**
2. **Fogscale Lizard Bowman**
3. **Stonecrest Lizard Spearer**
4. **Mistfang Lizard Hunter**
5. **Ashhide Lizard Thrower**
6. **Riftscale Lizard Skirmisher**
7. **Greycoil Lizard Marksman**
8. **Brokencrest Lizard Scout**
9. **Veilfang Lizard Sharpshot**
10. **Stormscale Lizard Warden**

---

	Shared Monster Mechanics : 
* Maintain distance at all times
* Constant repositioning inside fog
* Use elevated rubble and landslide stones
* Fire through fog with reduced accuracy, but high volume
	
**Design Intent :
Overwhelm players through pressure, not precision.

