			Torch Awakening Mechanic (Global Trigger)

	Type : Area State Mechanic

* Torches along tunnels:
	* Are unlit by default
	* Ignite sequentially as players move deeper
* Fully lit tunnels indicate:
	* The tunnels are “awake”
	* Ritual cooldowns are lifted
* If torches go dark again:
	* Summoning mechanics are disabled

