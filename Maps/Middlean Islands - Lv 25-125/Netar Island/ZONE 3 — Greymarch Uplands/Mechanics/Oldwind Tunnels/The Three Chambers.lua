			The Three Chambers (Ritual Design)

	Each chamber:

* Has a distinct color
* Hosts a statue/totem
* Uses a different interaction logic
* Can summon a Rare or Boss when conditions are met

---

		Chamber I — The Ember Hollow (Red Chamber)

* Theme : Motion, Rhythm, Heat
* Color : Deep ember red
* Ambient : Constant low flame, crackling stone

	Central Object

* The Cinderbound Totem

---

	Unique Mechanic — Flame Resonance Dance

* Trigger Conditions :
	* Tunnel torches must be fully lit
	* Players must possess:
		* "Ash-Worn Relic" (dropped by fire-aligned enemies anywhere on the island)

	Interaction : 

* A perpetual flame burns before the totem
* Players must:
	* Perform movement patterns (emotes / movement inputs)
	* Stay within flame radius
* Standing still causes failure

	Success State : 
* Flame rises
* Wind tone becomes sharp and fast
* Totem fractures

	Result : 

* "Rare Fire-Wind Entity" or "Vaelthryx, the Glyphbound Verdant" is summoned

---

		Chamber II — The Verdant Still (Green Chamber)

* Theme : Patience, Balance, Breath
* Color : Moss green
* Ambient : Slow wind pulses, soft stone creaks

	Central Object
* "The Breathroot Effigy"

---

	Unique Mechanic — Wind Stillness Trial

* Trigger Conditions :
	* Tunnel torches lit
	* Players must possess:
		* "Breath-Sealed Bark" (dropped by ancient beasts or trees on the island)

	Interaction : 

* Players must:
	* Remain motionless
	* Avoid attacking
	* Maintain spacing between party members
* Sudden movement resets progress

	Success State : 
* Wind drops to silence
* Fog condenses around the effigy

	Result : 

* "Rare Windbound Guardian" or "Vaelthryx, the Glyphbound Verdant" emerges from stone

---

		Chamber III — The Azure Depth (Blue Chamber)

* Theme : Memory, Echo, Awareness
* Color : Cold blue
* Ambient : Distant echoes, dripping water

	Central Object

* "The Echo-Sunder Obelisk"

---

	Unique Mechanic — Echo Alignment Ritual

	Trigger Conditions :
* Tunnel torches lit
* Players must possess:
  * "Resonant Shard" (dropped by echo-based or stone enemies)

	Interaction : 
* Players must:
	* Walk specific paths
	* Trigger echoes in correct sequence
* Incorrect sequence causes:
	* Echo backlash (minor damage / disorientation)

	Success State : 
* Echoes synchronize
* Obelisk hums steadily

	Result : 
* Ancient Echo Entity or "Vaelthryx, the Glyphbound Verdant" manifests

---

	Cooldown & Reset Rules : 
* Each chamber has its own cooldown:
	* Rare : several hours
	* Boss : longer window
* Cooldowns only count when:
	* Torches are extinguished after summoning
* Partial failures do not consume items

