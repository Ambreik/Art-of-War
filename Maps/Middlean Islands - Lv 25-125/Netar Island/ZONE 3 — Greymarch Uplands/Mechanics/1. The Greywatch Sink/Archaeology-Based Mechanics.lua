			Archaeology Questline Hook

	Questline Nature :
* Investigation-based
* Non-combat focused
* Multi-stage excavation logic

	Themes :
* Why the mine collapsed
* What was being extracted
* Who ordered its abandonment

	Progression Unlocks :
* New archaeology interactions island-wide
* Greywatch-specific artifacts
* Deeper access to sealed chambers

> Non-archaeologists may observe but cannot progress the core investigation.

