			Vertical Descent Mechanics

	Spiral Ledge System

* Broken stone ledges spiral downward
* Each level:
	* Narrow footing
	* Uneven geometry
* Players must:
	* Walk carefully
	* Use slow-fall, gliders, or controlled jumps

	Failure State :

* Falling causes:
	* Heavy stamina loss
	* Non-lethal damage
	* Forced landing on lower tier

