			Hidden Shrine – “Sanctum of the Fallen Crusaders”


	Concept : A concealed shrine embedded within the Wind-Hollow Path serves as a lore and reward hub. It reacts to players who have cleared or interacted with the 
"Wind-Hollow" boss encounter. The shrine links environmental cues, Crusader lore, and tangible rewards for exploration and engagement.

---

		Mechanics Overview

	Access & Activation : 
* Location: Midpoint of the Wind-Hollow Path, inside the perfectly round chamber.
* Activation Condition:
	* Only accessible after defeating the Wind-Hollow Path boss or observing the proper environmental triggers (fog, wind direction, light alignment).
	* Players must approach carefully, avoiding triggering environmental hazards or fog-triggered mini-adds.
* Interaction: Right-click or emote interaction prompts a “Read Shrine Pages” interface.

---

		Environmental Interaction : 
* Wind Response : The shrine reacts to player movement; fast or aggressive movement may cancel interaction.
* Light Alignment : Sunlight or ambient glow from torches interacts with shrine glyphs; reading pages requires proper illumination (either natural or via player torches/light sources).
* Fog Sensitivity : Heavy fog can partially obscure glyphs, requiring patience or multiple visits for full comprehension.

---

		Page-Based Lore System : 
* Pages : Three collectible pages (as we crafted) are found near or on the shrine.
* Interaction :
	* Players can read pages individually or sequentially.
	* Reading a page triggers a small Discovery XP bonus.
	* Completing all pages unlocks a hidden lore echo, which may hint at further tomb secrets or environmental interactions.
* Mechanic Tie-in : Encourages exploration, environmental awareness, and attention to detail.

---

		Reward System : 
* Conditional Rewards : Shrine provides random rewards once per post-dungeon completion. Rewards are scaled with the number of players and their class composition (
e.g., adventurers vs. scholars).
* Reward Examples :
	* Chests: Gold, crafting materials, or consumables.
	* Equipment: Shoes, bracelets, or small armor pieces imbued with temporary buffs.
	* Buffs: Activate upon returning to Wind-Hollow or another cursed zone, lasting 2 hours. Examples:
		* +5% movement speed
		* +10% attack speed
		* Minor stamina or health regeneration
	* Rune fragments or consumables for later crafting.

---

		Dynamic Interactions : 
* Environmental Cues : Shrine reacts differently depending on time of day, weather, or post-boss state.
* Party Coordination : Multiple players can read together, unlocking bonus Discovery XP and increasing chance for rare rewards.
* Replayability : Players may revisit shrine after cooldown or environmental reset to discover alternative lore echoes or trigger seasonal rewards.

---

		Gameplay Tie-in : 
* Serves as a bridge between exploration, dungeon combat, and reward mechanics.
* Encourages player curiosity, careful observation of the environment, and repeat visits for hidden lore and temporary bonuses.
* Integrates Crusader story elements, post-dungeon rewards, and environmental mechanics such as wind, fog, and light.

