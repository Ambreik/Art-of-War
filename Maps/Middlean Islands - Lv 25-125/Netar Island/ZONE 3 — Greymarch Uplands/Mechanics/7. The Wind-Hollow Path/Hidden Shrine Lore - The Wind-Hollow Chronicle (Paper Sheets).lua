			Hidden Shrine Lore: The Wind-Hollow Chronicle

---
	Location : Middlean Islands → Netar Island → Zone 3: Greymarch Uplands → The Wind-Hollow Path → Midpoint Round Chamber, on the floor scattered.

---
			Page 1 – The Opening of Secrets

	“The winds carry what the eyes cannot see, and the stones remember what the living have long forgotten.”

	It is said that long before the fortress atop the Greymarch Uplands fell, the Crusaders sought to carve their dominion across the western cliffs. They were 
warriors of unmatched skill, bound by oath and steel, yet even their resolve faltered in the face of forces unseen.
	The Wind-Hollow Path was once a sacred corridor, a place where the air itself was alive with the whispers of the divine. The Crusaders constructed here a small 
shrine, embedding glyphs into the stone, intending to bless their warriors with protection against the unseen.
	But darkness grew even in the light of their ambition. A shadow rose within the ranks: spirits, corrupted by betrayal, that had once walked as human allies. 
They learned the ways of the Crusaders, mimicking their tactics, twisting their oaths into chains of torment. The Crusaders’ magic and steel faltered where they 
were strongest.
	Those who built the shrine wrote that the wind itself became a sentry. It could carry warnings, shift obstacles, and protect sacred relics—but only for the 
worthy. Many tried to breach the path in arrogance, only to vanish in the fog, their voices merging with the whispers that haunted the cliffs.
	It is said that the glyphs etched into the shrine glow faintly when darkness approaches. Only those who respect the air, the light, and the remnants of the 
fallen may perceive the hidden runes that tell of victories and failures alike.

	“The winds carry what the eyes cannot see, and the stones remember what the living have long forgotten.”

---

			Page 2 – The Fall of the Crusaders

	The chronicles continue with accounts of the final expedition, when the Crusaders sought to purge the traitorous spirits from the Greymarch Uplands. Despite 
their numbers and skill, they were unprepared for the wrath of what they had created.
	The spirits, led by a figure later whispered of as the "Queen of Echoes", rose in the night. Their forms were twisted reflections of men and women once sworn to
the Crusaders’ cause. They wielded the very tactics and formations of the fallen, turning each strategy into a trap.
	The chronicles speak of three phases of the battle: first, the sentinel winds that protected the loyal Crusaders; then, the swirling defenses of the corrupted 
soldiers; and finally, the overwhelming storm of spectral force. One by one, the Crusaders fell or fled, leaving their oaths unfulfilled and their fortress a husk.
	In desperation, some scribes attempted to seal the spirits within the cliffside, creating wards and embedding glyphs into stones that would hold echoes of their
power. These wards could not fully contain them but slowed their spread, giving time for survivors to escape.
	It is here, the pages note, that the hidden shrine serves as a beacon for those brave enough to venture into the Hollow. If approached correctly, the glyphs 
awaken, providing protection and insight—but also revealing only fragments of the truth. Those who read too deeply without respect may draw the attention of 
lingering spirits, still bound to the betrayal of their former lives.
	Legends claim that the fallen left behind artifacts of light and fire, objects that the spirits fear, which can aid future adventurers. Yet the chronicles 
warn: even these blessings are temporary; they must be wielded with care and understanding, lest the same darkness consume the unworthy.

---

			Page 3 – The Whisper of Rewards and the Guiding Light

	The final page is a ledger of promises, written not in stone but in subtle ink, readable only in the gentle flicker of dawn or the illumination of true 
curiosity:

	Those who conquer the Wind-Hollow Path, defeating the guardian spirits and surviving the sentinel winds, may find rewards left by the Crusaders themselves:

* Ancient Chests : Containing currency, crafting components, or rare consumables.
* Blessed Equipment : Such as shoes or bracelets, etched with light-glyphs, granting temporary buffs when returned to the Hollow.
* Rune Fragments : A precursor to crafting unique consumables or activating environmental interactions.
* Active Buffs : Grants special protection, enhanced movement, or combat efficiency for two hours when venturing again into cursed zones.

	The scribes cautioned that these gifts are reactive, tied to the spirit of exploration rather than brute force. Only those who respect the path, move with care,
and pay homage to the legacy of the Crusaders may claim them fully.

	The page concludes with a final warning:

> “The Wind-Hollow is patient. It listens. It tests. Only when the seeker aligns with its rhythm will the legacy of the Crusaders reveal itself in full light. Fail, and the fog will forget your passage, as it has forgotten so many before.”

