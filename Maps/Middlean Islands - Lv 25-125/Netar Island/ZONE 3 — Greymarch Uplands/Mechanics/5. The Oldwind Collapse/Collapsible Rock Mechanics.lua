		Items / Materials Found via Collapsible Rock Mechanics

1. "Shattered Stone Fragment"
	* Type: Crafting Material
	* Description: Chunk of stone dislodged from boulders; can be used for masonry, minor crafting, or archaeology inspections.

2. "Glowsprout Fungus Cluster"
	* Type: Exploration / Quest Item
	* Description: Small patches of bioluminescent fungus revealed behind rocks; required for certain hidden quests or inspection objectives.

3. "Ancient Mining Pick"
	* Type: Quest / Lore Item
	* Description: Rusted tool left by old miners; can be collected for archaeology quests or to unlock story entries.

4. "Cave Crystal Shard"
	* Type: Crafting / Quest Material
	* Description: Sharp fragment of quartz or similar stone hidden in crevices; may be used in puzzles, crafting, or trade-ins.

5. "Buried Relic Token"
	* Type: Quest Item
	* Description: Small metal or stone object partially hidden behind collapsed rock; contributes to multi-stage archaeology quests.

6. "Dust & Debris" *(Environmental Hazard / Collectable)*
	* Type: Hazard / Optional Minor Item
	* Description: Collecting dust or debris is possible but only for curiosity or minor achievements; may trigger temporary obscured vision.

