			Collapsible Rock Mechanics

	Concept : Players can interact with unstable boulders to open new paths or reveal hidden areas.

	Mechanics :
* Certain rocks are weak points; can be broken via:
	* Heavy attacks / melee skills
	* Environmental tools (e.g., explosive barrels, levers from adjacent ledges)
* Breaking rocks may trigger:
	* Minor debris fall (minor environmental damage if hit)
	* Hidden items (crafting mats, quest objects)
	* Temporary hazards (dust clouds → reduces visibility)
* Some rocks require teamwork to move (pushing / coordinated destruction)

	Gameplay Tie-in : Encourages careful observation and interaction; prevents simple brute-force progression.

