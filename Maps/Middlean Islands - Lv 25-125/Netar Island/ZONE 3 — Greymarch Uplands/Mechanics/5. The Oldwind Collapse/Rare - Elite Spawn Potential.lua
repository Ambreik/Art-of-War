---
	
		Oldwind Collapse — Rare/Elite Creature Integration

	Concept : The elite creature is hidden within the Oldwind Collapse, and its spawn is gated behind environmental mastery, archaeology progression, and 
exploration. Players cannot stumble upon it by random chance; instead, interacting with the environment and completing parts of the questline triggers the 
conditions.

---

		Spawn Trigger Conditions

1. Environmental Mastery Requirements
   * Players must:
		* Interact with all "Glowsprout Fungus Clusters" in the collapse.
		* Examine "Ancient Mining Picks".
		* Collect or inspect "Buried Relic Tokens".
		* Clear collapsible rock passages leading deeper into the cavern.

2. Archaeology Rank Requirement
	* Only players with "Archaeology Rank II" or higher can fully decipher the clues that unlock the creature’s chamber.
	* Lower-rank players may observe or partially explore but cannot trigger the elite spawn.

3. Questline Progression Requirement
	* The creature only spawns after at least one player has completed the “Secrets Beneath the Oldwind” or “Tomb Survivors” questline objectives in the collapse.
	* The spawn is party-scaled, meaning a single player can trigger it, but larger parties adjust difficulty and adds optional mechanics.

4. Environmental Signal Cues
	* Visual : Glowsprout fungus clusters pulse brighter, dust clouds settle, faint glyphs glow.
	* Audio : Low rumbling echoes, shifting rock tones, faint growls from deeper within.

---

		Creature Spawn Mechanics

1. Elite Creature Den
	* Creature spawns in a hidden cavern unlocked by collapsing rocks and interacting with relics/fungi.
	* The den is gated: players must perform specific interactions to open the entrance.

2. Optional Combat/Engagement
	* Creature is highly aggressive, Rank IV+ difficulty.
	* Optional for parties to engage — soloing is extremely difficult.
	* Environmental hazards remain active (rock falls, fungus spores, unstable ledges).

3. Cooldown & Respawn
	* Spawn window: 6–12 hours, depending on server time and number of players who interacted with the chamber.
	* Partial failures do not reset spawn but require players to repeat the environment interactions to trigger the next chance.

---

		Gameplay Tie-in : 

* Encourages full exploration of the Oldwind Collapse.
* Rewards Archaeology-skilled players with access to rare content.
* Integrates environmental puzzles, collapsible rocks, and relics into a meaningful progression system.
* Bridges the POI exploration, questline, and elite content, giving players a strong sense of accomplishment.
* Future expansions: the elite creature could drop rare crafting mats, lore items, or even unique collectibles once combat mechanics are finalized.

