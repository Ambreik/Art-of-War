		Quest Unlock Mechanic — Oldwind Collapse

	Concept : Exploration and interaction within the Oldwind Collapse gradually unlocks major questlines. Players uncover lore, hidden passages, and secrets of the 
collapsed mine through environmental investigation.

---

		Mechanics Overview

1. Trigger Conditions

   * Players interact with "Glowsprout Fungus Clusters", "Ancient Mining Picks", or "Buried Relic Tokens".
   * Partial environmental interactions (breaking collapsible rocks, examining runes, or illuminating faint glyphs) update quest progress automatically.
   * Certain quests require players to have "Archaeology Rank II" or higher to access full content (e.g., inspect hidden runes or analyze fungi patterns).

2. Questlines Unlocked
   a. Secrets Beneath the Oldwind

	* Multi-stage archaeology + exploration quest.
	* Objectives include:
		* Examining fungi clusters and relics.
		* Clearing minor rock passages to reach hidden chambers.
		* Deciphering glyphs or markings left by ancient miners.
	* Rewards: Experience, discovery XP, minor crafting materials, and hints to elite monster locations.

   b. Tomb Survivors

	* Rescue or retrieve objectives from collapsed tomb areas within the site.
	* Objectives include:
		* Navigating dangerous rock passages safely.
		* Recovering lost items from fallen miners or adventurers.
		* Optional combat against environmental hazards (falling rocks, minor cave-in monsters).
	* Rewards: Experience, lore entries, archaeology XP, and potential rare crafting components.

3. Dynamic Quest Progression
	* Progress is updated automatically as players interact with environmental objects or clear passages.
	* Players do not need to manually “report back” until the major objective of the questline is complete.
	* Partial exploration or incomplete interaction can still provide discovery XP and minor rewards, encouraging repeated visits.

4. Environmental Feedback
	* Subtle audio cues: faint echoes or shifting rock tones indicate interactable areas.
	* Visual cues: glowing fungi intensifies, dust from collapsible rocks, faint glyph luminescence on walls.
	* Players can observe the environment and deduce next objectives without forced guidance.

5. Gameplay Tie-in
	* Integrates the Oldwind Collapse POI into the island’s archaeology system.
	* Encourages exploration, puzzle-solving, and careful environmental interaction.
	* Provides meaningful content even for non-combat players, while rewarding Archaeology-skilled players with additional clues and lore.
	* Unlocks access to further hidden chambers, elite monsters, and long-term exploration rewards.

