			Erosion Fault Edges (Risk vs Control)

	Type : Terrain Hazard (Soft)

	Some ravine walls are unstable where erosion and faulting meet.

	Mechanics : 
* Standing too long near certain edges:
	* Triggers small rock slides
	* Causes minor knockback or repositioning
* Does **not** deal lethal damage
* Primarily affects careless positioning

	Purpose
* Reinforces the ravine’s instability
* Adds tension without frustration

