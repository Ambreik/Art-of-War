			Unstable Buoyancy Surge (Lily Bounce Mechanic)

	Type : Timed Environmental Reaction

	After prolonged use, some lily leaves become unstable.

	Visual Warning : 
* Water around the leaf begins to:
	* Vibrate
	* Splash outward rhythmically
	* Produce subtle rippling rings

	Mechanics : 
* After a short warning period:
	* The leaf releases a buoyancy surge
	* The player is launched upward into the air
* Launch height is moderate and controllable
* Player can:
	* Use glider
	* Use slow-fall passive
	* Chain into cliff ledges or paths

	Design Purpose : 
* Vertical traversal option
* Soft surprise mechanic (not lethal)
* Encourages environmental awareness

