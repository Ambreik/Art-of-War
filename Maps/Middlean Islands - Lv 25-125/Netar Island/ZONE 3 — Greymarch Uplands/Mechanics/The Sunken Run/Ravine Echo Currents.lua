			Ravine Echo Currents

	Type : Movement Modifier

	Wind and water currents interact strangely within the narrow ravine.

	Mechanics : 

* Certain sections apply:
	* Light directional push
	* Momentum carry while jumping
* Effects are subtle but noticeable
* Can be used to:
	* Extend jumps
	* Reach narrow ledges
	* Adjust landing positions

	Player Skill Expression : 
* Rewards timing and positioning
* Non-punitive if misused

