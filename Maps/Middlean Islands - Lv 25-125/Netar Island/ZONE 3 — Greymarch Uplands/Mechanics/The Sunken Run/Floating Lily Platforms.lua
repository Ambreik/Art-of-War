			Floating Lily Platforms (Rest & Fishing Nodes)

	Type : Environmental Interaction

	Large, thick water lily leaves drift slowly along the shallow stream edges and calm pools.

	Mechanics : 
* A player may stand, sit, fish, or rest on a single leaf.
* Only one entity may occupy a leaf:
	* Player
	* Pet
	* Mount
* If more than one entity steps onto it:
	* The leaf submerges completely
	* All occupants are forced into the water
	* The leaf despawns temporarily

	Use Cases : 
* Safe fishing spot
* Temporary stamina recovery
* Observation point in the ravine

