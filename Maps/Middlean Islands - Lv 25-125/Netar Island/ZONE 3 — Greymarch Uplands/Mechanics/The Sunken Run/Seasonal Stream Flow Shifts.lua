			Seasonal Stream Flow Shifts

	Type : Environmental State Mechanic

	The stream inside the ravine subtly changes based on recent rainfall cycles.

	Mechanics : 

* During wet cycles:
	* Water flow increases
	* Some lily leaves drift faster
	* Certain shallow crossings become impassable
* During dry cycles:
	* Water recedes
	* Hidden stone paths emerge
	* New resting/fishing spots appear

	Gameplay Impact : 
* Alters navigation routes
* Encourages revisiting the area
* No hard lockouts

