		Rune Perchant: Veilfall Rank III

	Location : Veilfall Step – Hidden Fog Ledge, Netar Island

	Item Type : Consumable Rune (Rank III)

---

	Concept : A carved stone circle hidden in the fog acts as a conduit for the "Veilfall Rune*". Players interact with the environment to earn the Rune, which provides temporary, powerful bonuses for Netar Island.

---

		Mechanics / Interaction :

1. Collect Required Material :
	* Gather "Veilfall Shards" from local hazards:
		* Cliffside lizards
		* Minor elemental creatures
		* Rare fish from nearby streams

2. Activate Rune Circle :
	* Stand inside the fog-covered circle.
	* The fog density, wind direction, and falling water beams serve as environmental cues.
	* Players must perform a sequence of emotes and movements, representing alignment with the Veilfall energy:

		Sequence Example :

1. Emote: "Bow" – pay respect to the circle.
2. Movement: Walk clockwise 3 steps along the edge of the circle.
3. Emote: "Salute" – signal alignment with the Rune energy.
4. Movement: Step back twice from the center, pausing 2 seconds.
5. Emote: "Cheer / Clap" – complete the sequence to finalize the Rune’s activation.
	* Visual / Audio Feedback :
		* Fog glows brighter with each correct step.
		* Light beams converge slightly on the circle’s center.
		* Subtle water-fall-like tone increases in pitch as the sequence progresses.
		* If a mistake occurs (wrong step or emote), a soft wind gust disperses fog briefly, and the sequence must be restarted.

3. Reward Generation :
	* Successfully completing the sequence grants 1 Veilfall Rune (Rank III).
	* Item is consumable : right-click to activate bonuses.

---

		Item Effects / Bonuses :

* Duration: 30 minutes
* Only active on Netar Island
* Effects :
	* +5% movement speed
	* +10% attack speed
	* +15% stamina recovery

---

		Item Rules / Limitations :
* Not tradable or marketable
* Cannot be stacked — each Rune is single-use
* Additional Runes can be earned by repeating the environmental interaction
* Environmental conditions (fog density, light beams, wind gusts) slightly influence the success window, rewarding careful observation

---

		Gameplay Tie-In :

* Encourages exploration and observation of hidden environmental cues
* Rewards players with time-limited, tangible bonuses
* Integrates seamlessly into Zone 3 lore and environmental mechanics
* Can be expanded for future events : e.g., seasonal fog increasing spawn rate, rare variants of Veilfall Rune with higher bonuses

