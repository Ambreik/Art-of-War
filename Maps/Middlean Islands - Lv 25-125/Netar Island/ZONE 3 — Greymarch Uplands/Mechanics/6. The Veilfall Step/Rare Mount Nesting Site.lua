	Rare Mount Nesting Site – Terrestrial Rapture

	Location : Veilfall Step, Mossroot Basin

	Concept : A solitary terrestrial rapture (avian-like but terrestrial, with muscular limbs and short wings for gliding/jumping) nests on the fog-covered ledge. 
Its plumage shimmers with muted rainbow hues under sunlight, but it remains well-camouflaged in dense fog. Observers notice subtle movements: the tip of its tail 
flicks, its eyes track the player, and faint claw marks are etched in the stone ledge.
	The mount only appears for careful explorers — rushing or crowding the ledge will cause it to vanish until the next reset.

---

		Mechanics : 

* Spawn Conditions :
	* Must approach fog-covered ledge cautiously.
	* Rune Circle or fog ledge mechanics successfully activated (subtle environmental cues like moving mist patterns, faint harmonic tones).
	* Area must be free of aggressive monsters (mount refuses to appear if threat nearby).

* Interaction :
	* Use Mount Taming skill to interact.
	* Requires specific bait (purchased from Oscar Fisher D. or found in Mossroot Basin).
	* Player must perform a sequence of calm emotes / slow movements, mimicking the mount’s behavior to build trust.
	* Player’s passive abilities like slow-fall or stealth movement increase bonding chance.

* Taming Steps :
	1. Observation Phase : Watch the mount’s behavior for 10–15 seconds; note idle positions and resting patterns.
	2. Bait Placement : Lay bait on safe nearby rock; retreat to a marked distance.
	3. Calm Interaction : Use emotes such as sit, bow, or slow step while remaining in the mount’s line of sight.
	4. Bonding Success : Mount will approach gradually. Sequence must be uninterrupted — sudden movement, attacking, or rushing causes failure.
	5. Taming Completion : Mount accepts player; loyalty meter appears.

* Failure :
	* Mount flies/vanishes into fog, resetting interaction.
	* Cooldown: 6–12 hours for reattempt.

---

		Reward Outcomes : 

	As Pet :

* Cosmetic companion; mirrors mount appearance in miniaturized form.
* Loyalty meter tracks bonding progress (minor buffs unlocked with higher loyalty).
* Minor movement bonus when summoned near player.

	As Mount :

* Fully rideable terrestrial rapture.
* Cliff traversal: can safely navigate ledges.
* Slow-fall assistance: reduces falling damage when jumping from ledges.
* Minor jump boost: allows reaching elevated platforms during exploration.
* Loyalty system tracks taming progress, affecting responsiveness and mount special idle animations.

---

		Gameplay Tie-Ins : 

* Encourages exploration and careful interaction with environmental cues.
* Rewards observation skills and timed action coordination.
* Fog and ledge mechanics add natural risk/reward tension : rushing players can spook mount, but careful players get a rare reward.
* Integration with Rune Circle provides optional cross-mechanics between environmental puzzle and creature taming.

---

		Visual / Audio Flavor : 

* The mount’s claws tap rhythmically on the stone ledge, creating soft echoing sounds.
* Plumage shimmers with muted rainbow tones in sunlight, blending with fog.
* Subtle ambient mist swirls when mount is nearby, guiding players’ attention.
* Gentle cooing or bird-like warble signals player proximity once bonding begins.

