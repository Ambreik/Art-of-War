			Hidden Lore Stone Cluster

	Concept : Ancient Crusader stones partially embedded in the ridge hold fragments of lore.

	Mechanics :
* Players who inspect stones gain "Discovery XP".
* Stones may trigger a hidden lore entry about the Crusaders’ fall or their traitorous corruption.
* Interaction requires "Archaeology Rank I" or higher to fully translate or interpret inscriptions.
* Multiple stones must be examined to unlock a hidden lore echo, tying to deeper tomb secrets.

	Gameplay Tie-in : Reinforces the Crusader backstory and rewards dedicated exploration.

---

		Hidden Lore Stone Cluster – Lore Content

	Location : Veilfall Step, Mossroot Basin

	Description : A semi-circle of weathered stones rises from the ridge, their surfaces etched with faded runes, war sigils, and crude pictographs. Some stones are
cracked; others tilt precariously toward the cliff edge. The wind passing between them carries a low harmonic hum, almost like whispers of the past. Observers might
feel the heavy weight of history pressing from the stones, as if the events etched upon them still echo in the air.

---

		Lore Entries (Discovered Sequentially)

	Stone 1 – The Vanguard’s March
"Long before the betrayal, the Great Crusaders crossed the western islands with banners held high. Each step carved a path of unity and strength, yet shadows lingered at the edges of their order."

	Stone 2 – The Hidden Discord
"Whispers of dissent traveled through the ranks. Certain officers coveted power, striking secret pacts with unknown forces. What was once brotherhood now trembled on the edge of corruption."

	Stone 3 – The Fall of the Iron Sigil
"A storm of betrayal consumed the order. Commanders fell to unseen hands; loyal soldiers vanished. The Iron Sigil, once a beacon of discipline, became a mark of ruin."

	Stone 4 – The Spirit’s Curse
"Those who succumbed to dark forces did not die, but lingered. Their souls twisted by rage and unfulfilled duty, becoming the first of the restless spirits. The lands themselves mourned the corruption that spread like wildfire."

	Stone 5 – The Last Archivist
"A lone archivist chronicled what remained. Hiding fragments of truth in stone, he hoped future eyes might witness the betrayal, and perhaps, one day, right the course of history."

	Stone 6 – The Forgotten Pact
"The final rune speaks of a pact — one forged between mortal hands and ethereal influence, promising power to the unworthy and doom to the faithful. None are left to remember who broke it first."

---

		Environmental Flavor / Immersion Text

* Wind hums through the stones, almost like a chorus of voices lamenting the lost Crusaders.
* Occasionally, faint ghostly figures flicker in the periphery, as though reenacting fragments of the betrayal.
* The stones emit a cold, metallic scent, reminiscent of both blood and rusted armor.

