			Fog Veil Passage

	Concept : A hidden path behind the slow-falling fog provides access to the ridge platform.

	Mechanics :

* The fog creates partial visibility obstruction, making navigation tricky.
* Players must use movement skills, gliders, or mounts (or the future terrestrial rapture mount) to safely traverse.
* Falling from the ridge results in minor environmental damage and respawn at the fog entrance.
* Only players who discover the fog edge can access the hidden platform; casual traversal on the cliff doesn’t reveal it.

	Gameplay Tie-in : Encourages careful observation and rewards players for exploration.

