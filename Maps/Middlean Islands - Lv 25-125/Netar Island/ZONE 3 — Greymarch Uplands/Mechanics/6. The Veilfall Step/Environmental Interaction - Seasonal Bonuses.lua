			Environmental Interaction / Seasonal Bonuses

	Concept : Fog, wind, and light beams affect access and mechanics.

	Mechanics :
* Fog density : Denser fog reduces visibility, makes rune activation harder.
* Wind gusts : Can push players off ledges or assist glider/mount usage.
* Light beams : Align with Rune Circle or cliffs to trigger hidden effects, increasing success chance for rune or mount.
* Seasonal variation : Certain days increase chance for Veilfall Shard drops or rare mount appearance.

	Gameplay Tie-in : Encourages players to return at different times or weather conditions for optimal exploration.

