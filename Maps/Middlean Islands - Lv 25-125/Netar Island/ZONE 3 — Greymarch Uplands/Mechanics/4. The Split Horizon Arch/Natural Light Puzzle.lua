			Natural Light Puzzle

* Concept : Shafts of sunlight highlight faint symbols on chamber walls and floors.
* Mechanics :
	* Players must align themselves with light beams to “activate” symbols.
	* Sequence triggers a hidden mechanism or reveals loot caches.
	* Visual cue: glowing patterns appear when players correctly align.
* Optional Expansion : Could tie to a future hidden treasure room.

---
		
	Concept : Players use shafts of sunlight streaming through cracks in the chamber ceiling to align symbols etched in the stone walls and floors. Correct 
alignment triggers hidden mechanisms, reveals loot, or eventually awakens the elite tamable creature.

---

	Mechanics : 

	1. Light Beam Alignment

* Sunlight Shafts : Streams of natural light fall at different angles depending on time of day or seasonal position.
* Symbols / Glyphs : Etched on walls, floors, and ceiling; vary in complexity from simple lines to intricate patterns.
* Alignment Interaction :
	* Players must stand in specific spots or rotate small reflective surfaces (stone mirrors, crystal fragments) to redirect beams.
	* Correctly aligned beams illuminate symbols fully; partially aligned beams cause faint glows.

	2. Puzzle Logic & Feedback

* Progression Meter : Subtle UI hint or visual cue (glowing light) shows percentage of alignment completion.
* Failure States :
	* Misalignment triggers:
		* Small falling rocks or dust (cosmetic hazard).
		* Temporary shadow covers symbols; alignment must be retried.
* Hints :
	* Faint glyph outlines may shimmer to guide correct alignment.
	* Echoed light patterns indicate which symbols are connected.

	3. Player Skill Integration

* Optional Timing Element : Some light shafts shift slowly (like moving sun patches). Players must move or wait strategically.
* Team Coordination : For parties:
	* Multiple players may need to stand on different symbols simultaneously to complete alignment.
	* Signals or emotes can help synchronize timing.
* Passive Abilities Impact :
	* Gliders or slow-fall skills can assist in reaching elevated or precarious symbols.
	* Strength or dexterity skills may allow moving reflective stones faster.

	4. Environmental Interactions

* Natural Light Effects :
	* Glimmering particles highlight aligned symbols.
	* Light may reflect off water puddles or crystals for additional beams.
* Subtle hazards :
	* Sunlight through cracks may heat certain areas; standing too long may cause minor discomfort or reduced stamina (optional).

	5. Gameplay Tie-In / Rewards

* Completion Effects :
	* Hidden loot cache revealed (chests, rare crafting materials, or archaeology clues).
	* Optional clue for elite tamable creature spawn location.
	* Triggers light-based visual cue signaling puzzle solved.
* Future Expansions :
	* Seasonal light shifts change puzzle alignment.
	* Rare creatures or spirits could appear if alignment is perfect.
	* Puzzle could unlock further hidden chambers or secret environmental events.

	6. Audio / Visual Feedback

* Audio cues :
	* Subtle chimes or harmonic tones when a symbol is correctly aligned.
	* Shimmering wind sound intensifies as players approach full completion.
* Visual cues :
	* Symbols glow brighter with alignment accuracy.
	* Reflected beams move naturally across walls/floor, creating a dynamic, rewarding visual spectacle.

