			Hidden Elite Tamable Creature

* Concept : The chamber may house a rare, elite creature that can be tamed .
* Mechanics :
	* Appears only if environmental conditions or puzzle is solved correctly.
	* Tamable only with a high-level skill (Taming).
	* Creature behavior: initially evasive, reacts to puzzle alignment or player actions.

---
		
		Elite Tamable Creature — Auralith Winddrake

	Location : Middlean Islands \ Netar Island \ Zone 3 Uplands \ Split Horizon Arch (spawned after platforming + puzzle completion)

---

	Appearance :
* A medium-large draconic creature with sleek, aerodynamic body
* Scales shimmer with silver and pale blue streaks, reflecting the sunlight through the arch cracks
* Wings are semi-transparent with light-reflecting membranes, allowing glimmering patterns when in motion
* Sharp, elongated talons and a ridge of flexible fin-like spines along its back
* Head crest glows faintly during high wind or when agitated
* Eyes: deep amber with vertical slits, intelligent and alert

---

	Personality :
* Curious and reactive to player gestures
* Observes before engaging; will display caution to unknown players
* Can be playful if approached correctly, but will attack if provoked
* Responds differently based on taming attempts: calm and patient taming yields better bonding

---

	Behavior :
* Ranged aerial patrols over ledges and platforming areas
* Can perch or hover mid-air while scanning the player’s movements
* Uses burst wind abilities to knock back intruders or adventurers misstepping on ledges
* When tamed, maintains awareness of player movement; can assist in navigation and combat

---

	Mechanics :
* Spawn Condition : Only after completion of the "Cliffside Platforming Challenge" + "Light Puzzle", triggering a visual cue (wind spiral + glowing glyphs).
* Hostility : Aggressive until pacified for taming.
* Mount/Pet Eligibility : Can be tamed as either, but choosing one locks the other unless alternative taming methods are completed (special future quest).
* Special Interaction Mechanic :
	* Requires a combination of emotes / movement sequences to mimic the creature’s behavior pattern.
	* A “Wind Bonding Ritual” can be initiated near the creature:
		* Player stands on glowing ledge pattern
		* Uses special emotes (wave, bow, or salute) to sync with creature
		* Creature emits glowing wing signals in response
		* Multiple successful repetitions reduce aggression and increase taming progress
* Mount Taming Bonus : Players must complete an optional flight test, guiding the creature through wind tunnels over the arch. Successful completion allows mounting
and gliding assistance.

---

	Skills / Abilities :

* Wind Gust (Passive) : Automatically deflects minor projectiles or environmental debris during flight.
* Aerial Dash : Quick lateral movement, used in combat or platforming traversal.
* Wing Shield : Temporarily shields rider/pet from falling hazard damage when gliding.
* Tail Sweep : Knocks back enemies on the ground if agitated.
* Bonded Leap : When tamed, allows the player to boost across gaps using creature-assisted jump.
* Glide Assist : Smooths descent from high ledges when mounted.
* Environmental Awareness : Highlights interactive objects or hidden ledges in proximity when mounted or near player.

---

		Taming Process :

	1. Observation Phase :
* Creature monitors player from safe distance
* Player must avoid aggressive attacks for initial period (60–120 seconds)

	2. Ritual Bonding Phase :
* Player uses emotes and movement patterns synced with visual wing cues
* Must maintain proximity without sudden movement
* Optional items can improve taming success:
	* Wind-Sealed Relic – boosts affinity
	* Gale Feather Token – reduces aggression

	3. Mount Training Phase (Optional for Mount) :
* After bonding, player must guide the creature through a wind tunnel flight path over arch
* Failure resets mount taming attempt but preserves pet taming progress

	4. Final Acceptance :
* Creature emits a glow pulse, signaling bond success
* Creature can now be summoned as Pet or Mount, with cooldown restrictions for summoning

