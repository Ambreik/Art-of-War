		Auralith Winddrake — Taming Steps Blueprint

	Phase 0 — Spawn Requirement

* Condition : Player completes "Cliffside Platforming Challenge" + "Light Puzzle"
* Trigger : Creature spawns on the ledge, emits glowing wing signals
* Visual Cue : Wind spiral + glyph projection
* Player Notification : “A shadow flutters above the arch… approach carefully.”

---

	Phase 1 — Observation Phase (Passive Monitoring)

* Objective : Remain near creature without provoking it
* Duration : 60–120 seconds
* Mechanics :
	* Sudden movement or attacks → aggression increases → resets timer
	* Calm approach → creature begins subtle wing signaling
* Success : Unlocks "Ritual Bonding Phase"
* Failure : Creature flies to a perch; must wait cooldown to retry

---

	Phase 2 — Ritual Bonding Phase

* Objective : Sync player movement / emotes with creature’s cues
* Mechanics :
	* Stand on glowing ledge near creature
	* Use sequence of emotes/movements in response to wing signals:
		* Wave → Bow → Salute (example)
	* Maintain proximity without sudden movement
* Items (Optional Boosts) :
	* Wind-Sealed Relic – increases affinity points
	* Gale Feather Token – reduces aggression
* Success : Creature lowers aggression → emits faint glow → unlocks "Mount Training Phase"
* Failure : Creature flies away briefly → must restart bonding phase

---

	Phase 3 — Mount Training Phase

* Objective : Guide creature through wind tunnel flight path
* Requirements : Completion of Ritual Bonding Phase
* Mechanics :
	* Follow wind currents along marked path
	* Avoid gust hazards or cliff collisions
	* Skills like glide, slow-fall, mount control assist navigation
* Success : Creature accepts mounting → unlocks "Glide Assist" & "Bonded Leap" skills
* Failure : Mount taming resets, but pet taming progress preserved

---

	Phase 4 — Pet vs Mount Choice

* Option 1 — Pet : Creature can be summoned, fights alongside player, responds to commands
* Option 2 — Mount : Creature can be ridden, gliding and aerial mobility enabled, combat buffs when mounted
* Note : Choosing one locks the other unless a special future quest is completed

---

	Phase 5 — Final Acceptance

* Visual Cue : Full-body glow + wing flare
* Player Notification : “The Auralith Winddrake accepts your bond.”
* Outcome : Creature added to summon list
* Cooldowns :
	* Summon: Standard cooldown (like other pets/mounts)
	* Dismount / dismiss: No penalties
* Future Expansion Hooks :
	* Rare wind paths / hidden treasures activated only when mounted
	* Combat synergy in Zone 3 with strong wind / cliff mechanics
	* Environmental interactions like moving floating lily leaves, triggering gusts

---

		Optional Failure Handling

* Leaving area → resets to Observation Phase
* Aggressive actions → resets current phase
* Death during bonding → phase progress preserved, but must re-approach creature

