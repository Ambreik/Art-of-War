			Cliffside Platforming Challenge

* Concept : Players must navigate the narrow ledges and climb into the chamber.
* Mechanics :
	* Tightrope-like balance system for ledge traversal.
	* Wind gusts randomly push players slightly off course (affected by player passive abilities like gliders, mounts, or slow-fall skills).
	* Falling from ledges results in minor environmental damage and return to a safe ledge checkpoint.
* Gameplay Tie-in : Rewards careful traversal; optional paths may lead to hidden loot or clues for the elite creature.

---

	Concept : Players must traverse the narrow, jagged ledges beneath the Split Horizon Arch to reach the hidden chamber. The environment acts as both a physical 
obstacle and a skill-based puzzle.

---

	Mechanics : 

	1. Tightrope / Ledge Traversal System
* Ledges vary in width: some are wide enough for two players side-by-side, others barely enough for one.
* Players must maintain balance meter :
	* Leaning too far left/right reduces balance.
	* Balance adjustments can be made via keyboard/mouse input or controller stick.
* Some ledges have unstable sections that may slightly tilt or crumble after a player steps on them, forcing quick reaction.

	2. Wind Gust Hazards
* Random gusts push players laterally or backward along the ledge.
* Gust intensity varies by area: near the arch’s outer edge, gusts are strongest.
* Player mitigation:
	* Gliders or parachute passive skill reduces knockback.
	* Mounts or slow-fall passive skills decrease fall damage if knocked off.
	* Quick reaction / dodge skill allows regaining balance immediately.
* Audio/Visual Cues:
	* Whistling wind sound increases as gust approaches.
	* Slight movement of hanging moss, leaves, or dust indicates wind direction.

	3. Fall & Checkpoint System

* Falling from ledges results in:
	* Minor environmental damage (e.g., scrapes, bruises).
	* Reset to nearest safe ledge checkpoint, not all the way back to start.
* Optional “risk-reward”: certain hidden loot or clues for the elite creature are located on more dangerous ledges.

	4. Interactive Elements

* Some ledges have handholds, ropes, or protruding stones that can be used to stabilize balance.
* Occasional temporary platforms (fallen stone slabs or vines) appear/disappear in sync with wind gusts.
* Optional secret paths may require jumping skill or glider use to reach.

	5. Gameplay Tie-In

* Traversal encourages coordination and timing, especially for parties.
* Optional paths can reveal:
	* Hidden loot chests or ore veins.
	* Clues for the elite tamable creature.
	* Environmental storytelling (etched glyphs, bird nests, or worn carvings).
* Failure to navigate properly is forgiving but encourages practice and mastery.

	6. Audio / Visual Feedback

* Wind howls alert players to upcoming gusts.
* Dust, loose stones, moss movement indicate unstable sections.
* Glowing moss or sunlight beams hint at safer ledges and optional secrets.

