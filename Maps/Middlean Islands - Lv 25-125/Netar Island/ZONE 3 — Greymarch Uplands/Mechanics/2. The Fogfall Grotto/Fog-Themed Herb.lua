			Fog-Themed Herb

* Type : Herbalism Resource
* Rank : III (Rare)
* Zone : Zone 3 – Fogfall Grotto only

---

	Description : A pale, translucent flower that condenses directly from fog-laden stone. Its petals evaporate slowly when exposed to dry air.

---

	Spawn Mechanics

* Appears only when:
	* Fog density is high
	* Players remain inside grotto for a short duration
* Spawn interval:
	* Long (several hours)
* Limited nodes per cycle (1–2)

---

	Harvest Rules

* Harvesting too quickly:
	* Causes the bloom to dissipate
* Optimal harvest:
	* Requires brief stillness
	* Interrupting fog flow reduces yield

---

	Environmental Tie-In

* Mistveil Bloom is invisible without fog
* Clearing fog too aggressively prevents spawn

