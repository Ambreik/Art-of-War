			Echo Drift Zones

	Concept : Sound reflections are physically displaced.

	Mechanics :
* Certain pools and stone spikes act as sound anchors
* Walking near them:
	* Shifts echo origin
	* Can mislead other players nearby
* Standing still long enough:
	* Echo alignment stabilizes
	* True chamber layout becomes clearer

	Soft Teaching Layer :
* Movement = distortion
* Stillness = clarity

	This quietly mirrors mechanics from "Oldwind Tunnels" but in a non-combat way.

