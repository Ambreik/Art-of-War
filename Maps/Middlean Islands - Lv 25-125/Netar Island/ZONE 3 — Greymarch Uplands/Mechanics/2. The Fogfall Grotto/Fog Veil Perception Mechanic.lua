			Fog Veil Perception Mechanic

	Concept : Fogfall Grotto subtly alters player perception and navigation.

	Mechanics :
* Fog density increases deeper inside
* Player footsteps:
	* Echo with a slight delay
	* Sometimes echo from the wrong direction
* Minimap orientation subtly desyncs (no full disable)

	Gameplay Effect :
* Players rely more on:
	* Visual memory
	* Environmental cues
	* Sound rhythm (water drops)

> This trains players to read the environment instead of UI.

