			Sound-Sensitive Creatures (Passive Reaction)

	Type : Indirect Trigger

	Nearby wildlife and minor enemies react to sound, not sight.

	Mechanics : 

* Repeated echo triggers:
	* Cause distant creatures to:
		* Shift position
		* Change patrol routes
		* Become alert
* Does not auto-aggro
* Silent movement reduces reactions

	Player Agency : 

* Players can:
	* Intentionally create noise to redirect threats
	* Move slowly to avoid attention

