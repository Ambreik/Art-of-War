			Resonant Footfall System

	Type : Environmental Audio Mechanic

	Certain fractured stone slabs amplify footsteps unnaturally.

	Mechanics : 
* Walking, running, or jumping on echo slabs:
	* Produces delayed, repeating footstep sounds
	* Echoes propagate far beyond visual range
* Sound direction may:
	* Reflect off nearby stone
	* Appear to come from false locations

	Gameplay Impact : 
* Creates uncertainty:
	* “Is someone else here?”
	* “Did I just alert something?”
* Works on:
	* Players
	* Mounts
	* Larger enemies

