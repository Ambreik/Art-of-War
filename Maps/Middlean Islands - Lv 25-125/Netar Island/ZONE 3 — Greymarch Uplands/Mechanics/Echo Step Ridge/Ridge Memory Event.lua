			Ridge Memory Event (Rare, Non-Combat)

	Type : Ambient World Event

	Very rarely, the ridge replays past sounds.

	Mechanics : 
* Echoes may reproduce:
	* Old march rhythms
	* Distant shouts
	* Armor clinks
* These are:
	* Non-interactive
	* Non-hostile
	* Lore-only

	Purpose : 
* Deepens atmosphere
* Suggests the land remembers past movements
* No quest hooks, no rewards

