			False Presence Illusion

	Type : Psychological / Perception Mechanic

	Echoes occasionally layer and distort.

	Mechanics : 
* When multiple slabs are triggered in sequence:
	* Echoes may:
		* Sound heavier than the player
		* Sound like multiple entities
* No actual enemies are spawned
* This is pure environmental misdirection

	Design Purpose : 
* Tension without combat
* Reinforces paranoia and caution
* Excellent for night or fog cycles

