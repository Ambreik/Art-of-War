			Ritual Summoning Circle — Cradle of the Standing Stones

* Location : Greymarch Uplands \ Zone 3 \ Cradle of the Standing Stones (central oval)
* Type : Secret, optional, gated ritual site
* Theme : Harmony, resonance, wind, and ancient rites
* Difficulty : Puzzle & coordination-based, non-combat focused

---

	Concept : The circular formation of monoliths functions as a ritual focus once the stones are harmonized. Players align the harmonic frequencies of the stones 
and present the required ritual items to unlock one of several possible outcomes.
	This is intentionally optional, so players can explore, experiment, or prepare before attempting.

---

		Requirements : 

1. Harmonic Alignment of Stones
	* Players must successfully complete the "Harmonic Resonance Puzzle" (stone tones aligned in correct sequence and frequency).
	* Alignment is visualized by faint glow along cracks and audible by stabilized harmonic tones.

2. Ritual Items (must be in inventory)
	* "Resonant Shards" — fragments found in special locations across Zone 3 or gained from Rare monsters.
	* "Wind-Sealed Relics" — hidden items dropped by wind-themed monsters or found in Secret Locations.
	* "Archaeology Artifacts" — higher-rank archaeology players may unlock fragments providing hints or partial progress.

3. Player Coordination
	* Only a party or group of players can trigger the ritual effectively.
	* Each player interacts with specific stones (marked by color or glyph) to activate their contribution.

---

		Interaction Flow : 

1. Observation Phase
	* Players inspect the stone oval.
	* Stones emit distinct tones based on wind and current alignment.
	* Players may “tune” stones by stepping near them or using emotes / ritual item placements.

2. Alignment Phase
	* Players must activate stones in correct sequence, respecting:
		* Tone pitch
		* Color-coded glyphs
		* Position relative to the oval
	* Failure resets stone glow and harmonic sequence.

3. Ritual Offering Phase
	* Once harmonic alignment is achieved:
		* Each player presents a ritual item on a highlighted stone (or central totem if solo).
	* Only the correct combination triggers a result.
	* Visual feedback: energy pulses along stone surfaces; wind in the area softens or intensifies depending on sequence accuracy.

4. Outcome Trigger
	* One of three outcomes occurs, selected randomly or based on player input (optional branching logic):

	1. Ritual Rare Summon
		* A rare creature manifests at the center of the oval.
		* Appears intentionally, not aggressive unless provoked.
        * Could be part of a larger questline later.
	2. Quest Step Activation
        * Unlocks or progresses a ritual-related quest.
        * Could reveal a hidden artifact, lore scroll, or next puzzle.
	3. Lore Echo Unlock
        * Players experience a visual/audio lore event.
        * Provides backstory, hints for future content, or environmental history of Zone 3.

---

	Player Feedback : 

* Visual : Stone glow, rune pulses, wind shimmering around stones.
* Audio : Wind harmonics stabilize, a clear chime confirms success.
* Failure cues : Flickering glows, discordant tones, slight wind gust pushing players back.

---

	Cooldowns & Reset Logic : 

* Ritual can only be attempted once every 4–6 hours (server time) to prevent farming rare summon.
* Partial failures do not consume items, but a full reset may be triggered if all stones are incorrectly aligned.
* Visual/audio cues remain for players who wish to observe without attempting.

