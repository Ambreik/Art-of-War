			Ritual Summoning Circle — Optional Future Expansions

	1. Scaling with Party Size

* Concept : The ritual adapts based on the number of participating players.
* Mechanics :
	* Small party (1–2 players) : Fewer stones need to be activated, but timing windows are tighter.
	* Medium party (3–5 players) : Full oval puzzle active; coordination required.
	* Large party (6+ players) : Extra stones or glyphs appear; failure resets only a portion of the alignment.
* Gameplay Benefit : Encourages group exploration while still allowing solo or duo attempts for partial rewards.

---

	2. Archaeology Rank Integration

* Concept : Higher archaeology skill gives hints or puzzle advantages.
* Mechanics :
	* Rank I–II: Only visual cues (stone glow) for alignment.
	* Rank III: Subtle sound guides added (tones indicate sequence).
	* Rank IV: Partial alignment auto-fills; failed interactions reset fewer stones.
	* Rank V (Master): Full puzzle hints displayed temporarily (glyphs pulse in correct order).
* Gameplay Benefit : Rewards players investing in archaeology, encourages profession growth for access to lore content.

---

	3. Rare Summoned Creature — Future Loot / Environmental Impact

* Concept : Although Aeolynth is non-loot by default, future content could tie summons to temporary environmental or resource changes.
* Mechanics (Possible) :
	* Summoned creature triggers glyph-based buffs in surrounding area for a limited duration (e.g., +5% rare fish spawn, +5% exploration XP).
	* Future expansion may allow resource drops (rare crafting material tied to ritual or nature-themed items).
	* Summon could modify ambient weather or particle effects temporarily, e.g., wind intensifies, minor rainfall, visual aurora effects.

---

	4. Seasonal / Weather Modifiers

* Concept : Ritual success, sequence, and rewards may subtly vary with environmental conditions.
* Mechanics :
	* Rain / Moisture : Enhances glyph resonance, allowing easier puzzle alignment.
	* Fog / Mist : Obscures glyphs partially, requiring better player coordination.
	* Wind Speed : Strong winds may accelerate stone tone changes, increasing puzzle difficulty.
	* Time of Day : Some rare lore echoes only trigger at dawn or dusk.
* Gameplay Benefit : Encourages players to experiment with timing and environmental conditions, increases immersion and replayability.

