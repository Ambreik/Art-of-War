			Harmonic Resonance Puzzle (Primary Mechanic)

	Concept : Each standing stone emits a distinct tonal frequency when interacted with.

	Interaction

* Players may:
	* Touch
	* Strike
	* Channel wind through stones (passive environmental trigger)
* Incorrect activation causes:
	* Disharmonic pulses
	* Temporary disorientation
	* Wind backlash (knockback, not damage)

	Goal : 
* Align tones into a stable harmonic loop
* Successful alignment causes:
	* Wind to fall silent
	* Stones to glow faintly along cracks

