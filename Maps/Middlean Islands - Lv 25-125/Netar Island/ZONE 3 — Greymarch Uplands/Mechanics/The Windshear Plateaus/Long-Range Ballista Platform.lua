			Long-Range Ballista Platform (Windshot Arbalest)

	Description : Mounted at select plateau edges are massive, fixed arbalest mechanisms designed for extreme long-range firing. These devices are anchored into the
stone and aligned toward the open sky and distant terrain.

	Mechanics : 
* Interactable environmental weapon
* Interacting with the arbalest:
	* Locks player position
	* Switches camera to long-range zoom mode
*Player enters aiming mode when using it 
*Camera : 
	* Fully zoomable over extreme range
	* Locks player movement while active
	* Projectiles:
	* Travel long distances
	* Are affected by wind drift
* Zoom allows:
	* Extreme distance targeting
	* Precision aiming across the uplands
*Projectiles:
	* Travel long distances
	* Are affected by wind drift
* Projectiles can hit:
	* Birds
	* Flying creatures
	* Flying players using traversal systems
	* Any entity entering the arbalest’s effective zone

	Player Interaction : 
* Arbalest is:
	* Stationary
	* Direction-limited
* Requires manual aim and timing
* Wind conditions subtly affect projectile trajectory

	Feedback : 
* Visual: deep zoom with slight wind drift indicator
* Audio: heavy mechanical tension, sharp release snap
* Impact: distant hit confirmation with delayed sound return

