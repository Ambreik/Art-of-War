			Windlift Vortex Currents

	Description : At irregular intervals, powerful vertical wind currents form at the tops of the plateaus. These localized vortexes spiral upward like narrow, 
translucent tornadoes, lifting anything caught within them high into the air before releasing it over the surrounding uplands.

	Mechanics : 
* Windlift vortices:
	* Spawn intermittently
	* Persist briefly before dissipating
* When a player enters a vortex:
	* They are lifted rapidly upward
	* Then expelled in one of several preset directions
* Landing damage applies unless mitigated
* Vortices cannot be resisted or interrupted once caught

	Player Interaction : 
* Players are expected to:
	* Use gliders, parachutes, or slow-fall effects
	* Rely on passive landing skills if available
* Poor preparation results in:
	* Heavy fall damage
	* Forced recovery state

	Feedback : 
* Visual: spiraling air distortion and dust columns
* Audio: rising wind whine that crescendos before lift
* Camera: brief vertical pull effect during ascent

