# **4. Vegetation & Natural Features (Descriptive Rewrite)**

The vegetation in Greymarch Uplands reflects the region’s rugged climate and constant winds. Along the western cliffs, plant life grows low to the ground, clinging tightly to cracks in the stone. Small shrubs with tough, salt-resistant leaves form dense clusters in sheltered pockets, their branches bending permanently away from the prevailing winds. Grasses grow in clumps rather than uniform fields, shaped by airflow patterns that sweep across the cliff tops. Some grasses form long, soft mats that ripple like waves when the wind brushes over them.

Moving inland to the higher Uplands, vegetation becomes slightly more generous, though still shaped by hardship. Tall, wiry grasses dominate the landscape, spreading across gentle slopes in broad swaths. Their blades are thin but durable, producing a soft rustling sound as the wind passes through them. In places where the land dips or forms shallow basins, moisture collects enough for clusters of hardy wildflowers and mosses to grow, adding subtle touches of color to the otherwise muted palette of greens, greys, and earthy browns.

Isolated trees grow throughout the Uplands, but they rarely appear in groups. Most are twisted by the wind, their trunks angled and their branches stunted. They often grow in shapes that suggest they’ve spent their entire lives leaning into the prevailing breeze. Only in sheltered areas—behind ridges, within gullies, or in depressions shielded from direct storms—do slightly taller trees stand in ones and twos, forming miniature pockets of woodland.

As the land drops into the Greymarch Valley, vegetation shifts again. The valley’s higher moisture levels support thicker grasses, softer soil, and a surprising variety of small plants. Ferns grow along the base of stone walls, and long trailing vines crawl down from rocky ledges. The fog nourishes a soft layer of moss that spreads across boulders and fallen stone chunks. Here, the air feels cooler and more humid, and the vegetation reflects this by growing taller, denser, and softer in texture.

The natural features of the zone are equally striking. Wind-shaped stone pillars rise from the Uplands like ancient markers. Certain ridges are so worn by erosion that their surfaces display delicate patterns—swirls, grooves, and lines carved by centuries of storms. Natural arches appear where softer layers of rock have collapsed, leaving strange silhouettes against the sky. In the valley, massive boulders rest in quiet heaps, evidence of rockslides long past. Narrow fissures, some barely wide enough for a person to squeeze through, run deep into the stone, hinting at subterranean passages carved by water or ancient geological shifting.

Together, the vegetation and natural features of Greymarch Uplands create a landscape that feels alive in its stillness—resilient, weathered, and full of silent history.

