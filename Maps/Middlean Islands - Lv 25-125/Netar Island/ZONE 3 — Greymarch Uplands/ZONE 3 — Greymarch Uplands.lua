# **1. Overview / Introduction**

The **Greymarch Uplands** dominate the **northwestern flank** of the island, forming a broad, wind-swept expanse shaped by ancient erosion, relentless sea winds, and the shadow of the Central Mountain Range to the east.
This region feels **vast, exposed, and rugged**, defined by weathered stone ridges, grassy mesas, long rolling slopes, and natural terraces that march steadily inland until they collapse into the narrow Greymarch Valley at the mountain’s foot.

Compared to other zones:

* It is **the most open and windswept** region of the island.
* It has a unique **layered terrain**, from cliffside platforms to high Uplands and finally the sunken valley near the mountain’s edge.
* Its atmosphere is cooler, pale, and quiet — carrying a sense of ancient endurance, as if the land itself remembers storms and ages long past.
* The sea on the western side is **colder and more violent** than in the south, shaping steep coastlines and storm-carved coves.

Greymarch Uplands is defined by **three identities**:

1. **The Western Sea Cliffs** — rough edges battered by cold winds and booming surf.
2. **The High Uplands** — grassy, stony plateaus dotted with lone standing stones and fractured ridges.
3. **The Greymarch Valley** — a sunken inland corridor between the Uplands and the Central Mountains, filled with fog and strange wind patterns.

