### **8. Rainbow Trout Goldmine**

**Category : Hidden Wilderness Shop & Crafting NPC Site
**Type : Shop NPC, Craft NPC, Skillable (Non-targetable)
**Primary NPC : Oscar Fisher D.

**Description :
Deep within the shaded heart of the Greymarch Uplands, far beyond the marked paths and beneath a canopy so thick that sunlight filters through in narrow pale shafts, lies the isolated cabin known as **Rainbow Trout Goldmine**.
Built beside a crystalline pond fed by subterranean currents from the mountain range, the structure appears half reclaimed by moss and roots, blending into the forest as though grown rather than built.

A small pier juts into the water, along with drying racks filled with colorful freshwater fish. Barrels filled with bait and handcrafted fishing tools sit outside, and an old waterwheel turns lazily, powering nets and pulley lines anchored beneath the water surface.

The area is known as **Deepwater Clearing**, a secluded sub-pocket of the Mossroot Basin, recognizable by its quietness—no wind, no distant beast calls—just the ripple of the pond and the creak of the waterwheel.

**NPC Role :
Oscar Fisher D., the eldest member of the D. fishing clan, operates the shop.
He offers special handcrafted fishing goods, freshwater materials, and rare wilderness consumables.
He does not train fishing, cannot be targeted, and is considered a Craft & Shop NPC.
His knowledge of freshwater biology is unmatched on the island.

**Gameplay Relevance :
Players may visit for:

* Unique crafting materials
* Rare fish-based items
* Hidden recipes (future expansions)
* Side quests tied to river creatures or the D. Clan

His hut is also used for:

* Rare fish spawns near the pond
* Future fishing-related mechanics
