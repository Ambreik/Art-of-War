# **6. Environmental Mechanics**

---

## **A. Global Mechanics**

*(Full descriptive version repeated in every zone file as requested)*

### **1. Day/Night Cycle**

* Standard rotation changing brightness and visibility.
* Night increases monster aggression and stats by **+15%**, with **+10% drop chance**.
* In this zone specifically, nighttime winds calm slightly, increasing sound clarity.

### **2. Fatigue Waters**

* Swimming drains stamina progressively.
* Strong currents near cliffs increase drain significantly.
* Deep-water exploration triggers stacking drowning debuff.
* Prevents bypassing coastal dangers.

### **3. Water Flow & Currents**

* Western coast has strong outward and side currents.
* Fishing difficulty increases near cliff turbulence.
* Items dropped in water may drift noticeably.

### **4. Environmental Audio Layering**

* Wind, stone echoes, and ocean waves dominate soundscape.
* Moving from Uplands to Valley sharply increases echo effects.
* Audio cues indicate nearby cliffs, caverns, or wind tunnels.

### **5. Monster Awareness**

* Open terrain and strong winds affect detection.
* At night, creatures detect from farther distances due to reduced ambient noise.

### **6. Noise Awareness**

* Running across stone slabs echoes loudly.
* Combat near cliffs reverberates far across the Uplands.
* Breaking resources increases detection radius.

---

## **B. Specific Environmental Mechanics (Unique to Greymarch Uplands)**

### **1. Valley Fog Pulses**

* Fog in Greymarch Valley naturally thickens and thins in “pulses.”
* Visibility can drop drastically for short periods.
* Sound is muffled during thick pulses, reducing monster awareness and detection.

### **2. Wind Corridor Effect**

* The valley forms a natural wind corridor.
* Strong gusts push players slightly when crossing elevated ridges.
* Ranged attacks are mildly affected by crosswinds during storms.

### **3. Cliff Resonance**

* Certain cliff sections amplify vibrations.
* Heavy footsteps or combat against cliff walls produce large echo bursts.
* Echo bursts may temporarily confuse or misdirect creatures.

### **4. Plateau Updrafts**

* Some plateaus have upward wind currents.
* These can increase jump distance slightly.
* Glider mounts (once available) gain improved lift here.

---

## **C. Resource-Based Mechanics**

### **1. Stone Vein Durability**

* Veins found on the Uplands have higher durability due to mineral hardness.
* Mining takes longer but yields slightly more material.

### **2. Valley Moisture Influence**

* Plants in fog-heavy areas regrow faster.
* Herb respawn timers are reduced slightly in the valley.

### **3. Cliff Fishing Risk**

* Fishing from high cliffs carries a small probability of line snap from wind or surf.
* Rare fish sometimes appear only during periods of high tide.

### **4. Seasonal Stream Deposits**

* After rainfall events, temporary deposits appear in The Sunken Run.
* These deposits contain mixed low-tier minerals before drying out.

---

## **D. Creature / AI Mechanics**

### **1. Sound Confusion Behavior**

* Creatures in the valley or ravines may respond to echoes instead of the player's actual position.
* Some may investigate echo points instead of the origin.

### **2. Height Awareness**

* Monsters on plateaus react differently to height differences.
* Some will not jump down unless aggroed.
* Others may use slope paths intelligently to reach players.

### **3. Fog Stalking**

* Creatures in the valley use fog pulses to reposition.
* During thick fog, their movement becomes more erratic and unpredictable.

### **4. Cliff Edge Avoidance**

* Many creatures avoid cliff edges unless in direct pursuit.
* This prevents accidental falls but can create tactical advantages for players.

