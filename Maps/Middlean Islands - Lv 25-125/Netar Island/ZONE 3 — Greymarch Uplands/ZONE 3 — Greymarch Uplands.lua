# **2. Geography & Terrain (Descriptive Rewrite)**

The Greymarch Uplands form a sweeping, wind-battered expanse stretching across the island’s entire northwestern quarter, presenting a landscape that feels both ancient and stoic—shaped by time, wind, and the silent pressure of the sea. The region moves in a natural gradient, rising gently from the coastline in the west to the fractured foothills and sunken valley pressed against the Central Mountain Range in the east. From any position, the land appears layered: long terraces carved by prehistoric erosion, stone shelves raised like the steps of some lost giant, and broad rolling uplands that seem to ripple outward like the frozen crest of a vast green wave.

The western boundary meets the ocean not with gentle beaches, but with **tall cliffs and broken headlands**, some sharp and vertical, others eroded into slanted platforms that descend in irregular tiers. The cliffs are not uniform; they twist, curve, and break, with sudden crevices and abrupt drops, each shaped by centuries of storm winds and crashing surf. In several places, the cliff walls are marked by long horizontal lines—ancient tidal scars where the sea once reached higher levels. Large sections of the coastline ring with the echoing roar of water smashing against stone, creating an atmosphere of continuous motion and sound.

Moving inland from the cliffs, the Uplands rise steadily in long, sweeping slopes. The surface is a mosaic of **rock-studded grasslands**, patches of exposed stone, and natural terraces that run parallel to the sea. The land is not flat; it rises and dips with natural rhythm, with shallow valleys and sudden ridges forming a complex network of paths and barriers. Occasional plateaus stand above the landscape as natural observation platforms, offering panoramic views of the sea to the west and the looming mountains to the east. These higher regions often feel harsher—windswept and exposed—yet reveal subtle beauty in the delicate patterns etched into stone by ancient storms.

As the Uplands progress eastward, the terrain becomes increasingly uneven. Broad rises give way to **broken foothills**, where the land fractures into jagged shelves and sloping stone formations. Here the ground grows rockier, the soil thinner, and gentle contours become steep ridges. The land seems to buckle and twist as it approaches the mountains, as if resisting the geological weight of the massive range.

At last, the terrain drops sharply into the **Greymarch Valley**, a long depression running north–south along the mountain’s base. This valley is narrow in some places and wide in others, but always lower than the surrounding land, forming a natural trench where fog collects and lingers. Stone walls rise sharply on its eastern side, creating a sense of enclosure and isolation. The valley floor is uneven—partially carved by ancient riverbeds, partially shaped by past landslides—and filled with weather-worn boulders that have tumbled from the mountain over centuries.

The full terrain of Greymarch Uplands feels like a zone carved by time and shaped by the forces of wind, water, and stone. It is a place of quiet might, with each ridge and valley telling a story of geological endurance.

