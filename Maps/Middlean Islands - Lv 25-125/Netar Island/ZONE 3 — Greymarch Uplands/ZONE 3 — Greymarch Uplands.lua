# **3. Climate & Environmental Behavior (Descriptive Rewrite)**

The Greymarch Uplands carry a distinctly cooler climate than the southern regions of the island. Days often begin with a crisp chill that clings to the air even after the sun has climbed fully above the horizon. The western winds arrive daily without fail—strong, steady, and pervasive—sweeping across the cliffs and inland over the rolling terrain. These winds bring the scent of salt and the echo of crashing waves, giving the entire region a constant reminder of the vast Great Sea that borders it.

During the day, sunlight is sharp and clear, casting long shadows across the Uplands and illuminating the textured stone surfaces with pale highlights. The open terrain allows light to travel far, making the region appear vast and expansive even under overcast skies. Cloud cover is common, but rarely oppressive; instead, the clouds move quickly overhead, dragged along by the relentless wind currents.

Nightfall brings a noticeable change. The temperature drops swiftly, and the winds, though ever present, become softer and less chaotic. The sound of the waves becomes clearer without the competing rush of daylight breezes. The Uplands grow quiet, the grasses still except for an occasional sweep of wind that whispers across the ground. The cliffs and stone ridges cool rapidly, causing small drafts to slip through cracks and crevices, creating subtle tones that drift across the landscape.

The most distinctive environmental behavior occurs in the Greymarch Valley. Because of its sunken position beneath the Uplands and the shadow of the mountains, the valley behaves like a natural trap for moisture and cold air. Fog gathers here every morning and evening, rolling in gentle waves that rise and fall like breathing mist. This fog doesn’t simply appear—it **pulses**, thickening and thinning rhythmically as it follows the shifting temperature of the valley’s stone walls. During these pulses, visibility may shrink dramatically, and sound becomes muted, creating an uncanny stillness.

The zone’s atmosphere overall feels both harsh and serene: open skies, cold winds, distant thunder of the ocean, and the soft breath of the valley fog forming a symphony of natural motion. It’s an environment that demands respect from those who cross it, but rewards them with immense beauty and breathtaking views.

