			Greymarch Fog Sovereign

	Location : Middlean Islands / Netar Island / Zone 3 Greymarch Uplands / Greymarch Valley Basin (inside dense fog only)

	Spawn Cycle  :
* Spawns every 6 hours (counted from last death)
* Only one active at a time
* Cannot be soloed

---

	Appearance : "The Greymarch Fog Sovereign" stands as a colossal armored warlord, its silhouette barely contained within the rolling fog. Its body resembles a 
heavily plated humanoid general, with thick, overlapping armor segments etched by age and battle. The armor is crude yet imposing, reinforced around the shoulders 
and torso, giving the creature an immovable presence. Its helm is angular and severe, obscuring its face entirely, while faint, dull light leaks from narrow eye 
slits. Each step causes the ground to tremble slightly, stone grinding beneath its weight. When it moves, the fog parts reluctantly, as if resisting its advance.

---

	Behaviour : "The Fog Sovereign "is slow, deliberate, and oppressive. It does not chase recklessly but advances with inevitability, forcing players to reposition
constantly. It ignores minor threats and focuses on clusters of players, punishing poor coordination. The creature is fully aware of the fog-enhanced state of 
intruders and fights as if expecting extreme speed and stamina from its opponents. It never leaves the fog and will reset if drawn beyond its boundaries.

---

		Mechanics : 
* Type : World Boss (POI Boss)
* Soloable : ❌ No
* Combat Zone : Fog-covered basin only
* Player Buff Interaction : Balanced assuming players have "Hyped Cord"
* Leash Rule : Hard reset if fog boundary is crossed
* Drops : None (not defined)

---

		Core Mechanics : 

	Fogbound Authority : 
* The boss is unaffected by fog penalties or player buffs
* Visibility distortion affects players more than the boss
* Gains increased threat generation inside dense fog zones

---

		Hyped Cord Counterbalance : 

* The boss’s damage and control effects are tuned to:
	* High player movement speed
	* Max attack speed
	* Infinite stamina
* Standing still is heavily punished

---

		Combat Phases : 

	Phase I – Martial Presence (100%–70%)

	Description : The Sovereign asserts control, testing the group’s movement and spacing.

	Behavior :
* Wide, sweeping attacks
* Heavy frontal pressure
* Slow repositioning

	Environmental Interaction :
* Fog density remains stable

---

	Phase II – Crushing Advance (70%–35%)

	Description : The boss becomes more aggressive, using its mass to collapse formations.

	Behavior :
* Faster attack chains
* Increased knockback effects
* Punishes tightly grouped players

	Environmental Interaction :
* Fog spirals intensify
* Visibility fluctuates more rapidly

---

	Phase III – Fog Tyranny (35%–0%)

	Description : The Sovereign enters a relentless final state, overwhelming the basin.

	Behavior :
* Reduced downtime between attacks
* Area-denial focused
* Forces constant movement

	Environmental Interaction :
* Fog shifts more violently
* Visual distortion increases near the boss

---

		Skills : 

	Sovereign’s Cleave : 
* Description : A massive horizontal strike with extended reach.
* Effect : Heavy damage and knockback; punishes side clustering.

---

	Groundbreaker Stomp : 
* Description : Slams the ground, sending shockwaves through the basin floor.
* Effect : Area damage and brief stagger.

---

	Fog-Pressured Charge : 
* Description : A slow but unstoppable forward advance.
* Effect : Forces players to scatter or be knocked back.

---

	Tyrant’s Reprimand : 
* Description : Targets a player group and delivers a crushing overhead blow.
* Effect : High burst damage in a small area.

---

	Basin Domination (Passive)
* Description : The Sovereign’s presence warps the battlefield.
* Effect : Reduced reaction time for players near the boss due to fog distortion.

