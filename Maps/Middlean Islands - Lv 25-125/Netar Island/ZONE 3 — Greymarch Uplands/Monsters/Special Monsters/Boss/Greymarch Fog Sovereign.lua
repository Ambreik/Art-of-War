---

	Design Intent Summary

	This boss is designed to:
* Fully exploit "Hyped Cord"
* Punish hesitation and poor spacing
* Feel overwhelming without being unfair
* Function as a mechanical stress test, not a loot encounter

> The fog empowers the intruders — the Sovereign exists to prove that power alone is not enough.

