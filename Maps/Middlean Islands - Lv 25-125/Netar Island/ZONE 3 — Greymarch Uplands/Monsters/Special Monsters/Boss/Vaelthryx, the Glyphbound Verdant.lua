			Vaelthryx, the Glyphbound Verdant

* Type : Boss (Ritual-Summoned)
* Category : Nature / Ancient Entity
* Spawn Source : Oldwind Tunnels – Any of the Three Chambers
* Soloable : ❌ No
* Pet Use : ❌ Disabled (Tunnel Rule)
* Cooldown : Long (shared across all three chambers)

---

	Location : Middlean Islands → Netar Island → Zone 3: Greymarch Uplands → Oldwind Tunnels (Chambers)

---

	Appearance : "Vaelthryx" towers over the chamber floor, its body shaped from ancient bark, stone, and living root. Its form is asymmetrical, grown rather than 
built, as if the land itself forced it into existence.
	Across its torso, limbs, and crown-like head are **glowing nature hieroglyphs**, etched deep into hardened wood and stone. These glyphs pulse slowly, shifting 
color as it changes abilities — greens, ambers, blues, and earthen browns cycling through its form.
	Vines coil and uncoil with every breath, and fragments of soil constantly fall from its body, reforming at its feet. When near water, moss rapidly spreads 
across its legs and torso.

	Its presence bends the chamber:
* Wind slows
* Sound dampens
* Stone subtly cracks beneath its weight

---

		Behaviour : 

* Initially stationary upon summoning, observing players
* Becomes increasingly aggressive as glyphs activate
* Prioritizes:
	1. Area control
	2. Player displacement
	3. Attrition through terrain interaction
* Reacts strongly to environmental positioning:
	* Water proximity
	* Player clustering
	* Line-of-sight abuse

	Vaelthryx does not chase recklessly — it claims territory, forcing players to move.

---

		Core Mechanics : 

	1. Glyph Cycle System

* Vaelthryx possesses 7 Nature Glyphs
* Only 3 glyphs active at once
* Glyphs rotate every phase
* Each glyph alters both:
	* Boss abilities
	* Arena conditions

	Players must adapt rather than memorize.

---

	2. Environmental Regeneration (Water Proximity)

* If Vaelthryx stands very close to water :
	* Gains rapid health regeneration
	* Moss and vines visibly spread
* Regeneration stops when:
	* Boss is pulled away
	* Players block water access

	This mechanic teaches positional discipline.

---

	3. Add Summoning — Root Stumps

	Summoned Adds : Verdant Stumps
* Appear as living tree stumps erupting from the ground
* Immediately root nearby players
* Can be destroyed
* If ignored:
	* Spread roots wider
	* Increase control duration

	Stumps are priority threats, not filler adds.

---

		Phases : 

	Phase I — The Awakening Glyphs (100%–70%)

* Boss activates 3 basic glyphs
* Moderate damage
* Slow arena control
* Limited stump summons

	Purpose: Teach mechanics.

---

	Phase II — The Verdant Claim (70%–35%)

* Glyph rotation accelerates
* Adds spawn more frequently
* Terrain hazards intensify
* Boss actively positions toward water

	Purpose: Pressure management and coordination.

---

	Phase III — The Living Script (35%–0%)

* All 7 glyphs activate sequentially
* No fixed rotation
* Increased regeneration risk
* Larger root zones
* Aggressive displacement skills

	Purpose: Survival through mastery.

---

			Skills : 

	1. Glyph of Grasping Earth
* Roots burst outward from boss
* Slows and briefly immobilizes players
* Enhanced near water

---

	2. Verdant Surge
* Waves of living vines sweep forward
* Pushes players back
* Interrupts casting

---

	3. Runic Bloom
* Glyphs detonate in delayed sequence
* Area denial zones remain briefly

---

	4. Stoneleaf Carapace
* Boss hardens bark and stone
* Reduces damage taken
* Breaking stumps weakens this effect

---

	5. Echo of the Wild Script
* Random glyph activates twice
* Unpredictable overlaps
* Visual cue: glyph glow intensifies

---

	6. Rootbound Reclamation
* Summons multiple Verdant Stumps
* Roots multiple players simultaneously
* Stumps heal boss if left alive

---

	7. Verdant Renewal
* Massive regeneration pulse
* Only triggers if water proximity condition is met
* Visually obvious moss overgrowth

