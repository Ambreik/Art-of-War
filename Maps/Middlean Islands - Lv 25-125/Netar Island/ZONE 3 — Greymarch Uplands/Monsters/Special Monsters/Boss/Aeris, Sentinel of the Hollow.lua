			Aeris, Sentinel of the Hollow

	Location : Middlean Islands → Netar Island → Zone 3: Greymarch Uplands → The Wind-Hollow Path → Midpoint Round Chamber

---

	Appearance : Aeris towers over mortals at nearly 4 meters, a guardian forged from stone and infused with swirling air currents. Its body is carved with ancient 
Crusader glyphs glowing faintly in pale blue and white, pulsing with wind energy. Wisps of cloud-like mist curl around its shoulders and legs, reacting to its 
movements and the surrounding chamber.
	Its face is partially obscured by a stone mask etched with sigils; its glowing eyes pierce through the fog. Hands and forearms end in swirling gusts of energy, 
giving the impression of claws made from wind itself. Aeris constantly shifts weight, and every step on the stone floor produces harmonic echoes, as though the 
chamber itself is alive.

	Distinct Features :
* Flowing cape of swirling debris and cloud energy
* Glowing glyphs along arms, chest, and legs
* Mist constantly curling around body and limbs
* Stone mask etched with Crusader sigils

---

		Personality / Behavior : 

	Aeris is territorial, defensive, and relentless. It is not malicious by nature—it protects the Hollow Shrine with the intent of guarding Crusader secrets.

* Moves with purpose, using the chamber’s architecture to its advantage
* Constantly assesses threats, prioritizing ranged attackers, then melee
* Calls upon the chamber’s natural wind currents to disrupt player positioning
* Summons elemental and stone minions to maintain battlefield control
* Will never flee, standing its ground until defeated

---

		Mechanics :

	Phase 1 – Sentinel Awareness :
* Sweeps chamber with Wind Slash (cone AoE in front of boss, knocks players back)
* Passive Gust Aura : applies stacking slow to nearby players
* Summons Wind Sprites (fast elemental minions) every 60 seconds

	Phase 2 – Defensive Swirl (Triggered at 70% HP) :
* Summons Stone Sentinels (mid-tier adds, tanky, deal knockback)
* Creates Airborne Zone Control : gusts push players toward walls, requiring careful positioning
* Uses Energy Vortex : channels wind pulling players into center, causing moderate AoE damage

	Phase 3 – Crashing Tempest (Triggered at 30% HP) :
* Wind intensifies; platform edges become hazardous
* Rocks occasionally collapse, dealing AoE damage and applying brief stun
* Minion Wave Surge : summons both Wind Sprites and Stone Sentinels in rapid succession
* Vulnerable to light/fire skills, which deal +25% damage, reflecting Crusader lore

	Minions / Adds :
* Wind Sprites : small, fast, apply stacking slow, low HP
* Stone Sentinels : larger, tanky, knockback abilities, moderate damage

	Spawn Conditions :
* Appears randomly between 3 hours – 3 days
* Pre-spawn: 10-minute warning with high-altitude wind vortex, cloud twisting, faint lightning
* Cannot be soloed; requires coordinated party

