			Aelmyr, the Breathbound Warden

* Type : Rare Monster (Ritual-Summoned)
* Element : Wind / Nature
* Spawn Source : Oldwind Tunnels — Chamber II: The Verdant Still
* Soloable : ❌ No
* Pet Use : ❌ Disabled (Tunnel Rule)
* Cooldown : Several hours (Chamber-specific)

---

	Location : Middlean Islands → Netar Island → Zone 3: Greymarch Uplands → Oldwind Tunnels → Verdant Still (Green Chamber)

---

	Appearance : Aelmyr rises silently from the stone floor as if grown rather than summoned.
	Its body is tall and slender, formed from woven roots, pale bark, and compressed wind, with no sharp edges. Moss and small leaves cling to its limbs, 
unmoving even when it attacks. Its “face” is a smooth, mask-like knot of wood, marked only by faintly glowing runes that resemble slow breaths expanding and 
contracting.
	Translucent wind currents coil around its torso and shoulders, visible only when they catch the chamber’s dim light. The air around Aelmyr subtly bends, as if 
the space itself is breathing.
	When it moves, there is no sound of footsteps — only a gentle pressure change in the air.

---

		Behaviour : 

* Moves slowly, deliberately
* Rarely chases
* Punishes:
	* Erratic movement
	* Clustering
	* Over-aggression
* Appears passive until provoked

	Aelmyr does not rush players — it waits for mistakes.

---

		Core Mechanics : 

	1. Stillness Judgment Field : 

* Players who:
	* Move erratically
	* Attack excessively
	* Stack too closely are marked with **Disrupted Breath**
* Calm, measured movement clears the mark

	This mirrors the **Wind Stillness Trial** ritual.

---

	2. Breath Synchronization : 

* Aelmyr periodically “inhales”:
	* Pulling air inward
* Then “exhales”:
	* Releasing controlled wind bursts
* Timing matters more than reflexes

---

	3. Spatial Discipline : 

* Arena subtly enforces spacing:
	* Standing too close to allies increases wind pressure
	* Correct spacing reduces damage taken

---

		Phases : 

	Phase I — The Quiet Rise (100%–70%)

* Minimal attacks
* Large visual tells
* Tests player patience

	Purpose: Teach restraint.

---

	Phase II — The Pressing Calm (70%–35%)

* Wind pressure increases
* More frequent breath cycles
* Strong punishment for clustering

	Purpose: Enforce discipline.

---

	Phase III — The Held Breath (35%–0%)

* Longer silence periods
* Sudden powerful releases
* Mistakes are heavily punished

	Purpose: Precision and trust.

---

		Skills & Abilities : 

	Measured Gust : 

* Controlled wind wave
* Pushes players gently but decisively
* Stronger against clustered targets

---

	Breathbind : 

* Marks players who attack too frequently
* Marked players suffer delayed knockback
* Cleared by slowing actions

---

	Verdant Suspension : 

* Lifts selected players briefly
* Holds them in midair
* Drops them safely if spacing rules are respected

---

	Stillwind Veil : 

* Reduces incoming damage
* Weakens when players maintain proper spacing
* Strengthens when chaos increases

---

	Exhale of the Warden : 

* Large directional wind release
* Clearly telegraphed
* Lethal only if mishandled

---

	Final Skill: The Long Silence : 
	*(Phase III Only)*

* Chamber goes nearly silent
* Visual cues only
* After delay, massive wind release occurs

