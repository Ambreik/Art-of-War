			Pyrrhax, the Cinderbound Waltzer

* Type : Rare Monster (Ritual-Summoned)
* Element : Fire / Wind
* Spawn Source : Oldwind Tunnels — Chamber I: The Ember Hollow
* Soloable : ❌ No
* Pet Use : ❌ Disabled (Tunnel Rule)
* Cooldown : Several hours (Chamber-specific)

---

	Location : Middlean Islands → Netar Island → Zone 3: Greymarch Uplands → Oldwind Tunnels → Ember Hollow (Red Chamber)

---

	Appearance: Pyrrhax manifests as a humanoid figure formed of living embers and spiraling ash, its lower body never fully touching the ground. Its silhouette 
flickers constantly, as if dancing to an unheard rhythm.
	Its limbs are elongated and uneven, trailing sparks with every movement.
	A fractured mask of obsidian floats where a face should be, glowing from within with molten cracks that pulse in time with the chamber’s flames.
	Bands of burning glyphs coil around its torso and arms, rotating slowly like a ritual script caught in motion. Wind currents twist around its form, lifting ash 
and cinders into a perpetual spiral.
	When Pyrrhax moves, the ground beneath briefly scorches — never enough to ignite, but enough to warn.

---

		Behaviour : 

* Never remains still
* Constantly circles players
* Punishes immobility
* Becomes more aggressive when players hesitate

	Pyrrhax behaves like a ritual dancer, not a brute:
* Fast repositioning
* Sudden directional shifts
* Predictable only in rhythm, not direction

	Standing still near Pyrrhax is perceived as defiance.

---

		Core Mechanics : 

	1. Motion Enforcement Field : 

* Players within combat radius:
	* Gain stacking penalties if stationary
	* Movement clears stacks instantly
* Encourages:
	* Continuous motion
	* Rotational positioning
	* Awareness of surroundings

---

	2. Flame Rhythm Sync : 

* Pyrrhax attacks in rhythmic patterns
* Attacks align with:
	* Flame pulses
	* Wind tone shifts
* Skilled players can:
	* Learn the rhythm
	* Move with the pattern instead of reacting late

---

	3. Heat-Wind Convergence : 

* Fire and wind abilities overlap
* Fire zones drift with air currents
* Wind bursts can push players into lingering heat

---

		Phases : 

	Phase I — The First Steps (100%–65%)

* Wide sweeping attacks
* Large visual tells
* Moderate movement pressure

	Purpose: Teach rhythm and motion.

---

	Phase II — The Accelerated Waltz (65%–30%)

* Faster attack cadence
* Smaller safe zones
* Frequent repositioning by Pyrrhax

	Purpose: Stress coordination and stamina.

---

	Phase III — The Final Turn (30%–0%)

* Near-constant motion
* Overlapping fire-wind fields
* Severe punishment for hesitation

	Purpose: Execution under pressure.

---

		Skills & Abilities

	Emberstep Lunge : 

* Pyrrhax vanishes briefly
* Reappears behind a moving player
* Leaves a burning trail along its path

---

	Cinder Spiral : 

* Summons rotating rings of fire
* Rings drift unpredictably with wind
* Standing still guarantees contact

---

	Ashbound Kick : 

* High-speed strike
* Launches players sideways
* Often aimed at clustered targets

---

	Flamebeat Pulse : 

* Rhythmic fire shockwaves
* Pulses increase in speed over time
* Visual cue: glyphs flash rapidly

---

	Scorchwake : 
* Pyrrhax dashes across the chamber
* Ignites the ground briefly
* Forces rapid repositioning

---

	Finale: The Cinder Waltz
	*(Phase III Only)*
	
* Continuous spinning movement
* Fire trails form a rotating pattern
* Wind intensifies, reducing reaction time

