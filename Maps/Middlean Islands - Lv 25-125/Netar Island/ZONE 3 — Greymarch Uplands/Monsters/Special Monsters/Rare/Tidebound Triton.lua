			Tidebound Triton – Rare Monster

	Location : Middlean Islands / Netar Island / Zone 3 Greymarch Uplands / Westbreaker Cove

---

	Appearance : The Tidebound Triton rises from the sea foam as if sculpted by the waves themselves. Its towering form is humanoid but distorted, with elongated 
limbs wrapped in strands of kelp and hardened salt-crystal growths. Sections of its body appear semi-fluid, constantly reshaping as water flows through fractured 
armor plates made of coral and barnacle-encrusted stone. Its head is crowned with broken fin-like ridges, and its eyes glow with a cold, deep-sea luminescence, 
unfocused yet watchful. Sea foam continually spills from its joints, dripping back into the tide pools beneath it.

---

	Behaviour : The Triton is territorial and relentlessly aggressive once manifested. It does not patrol or wander beyond the boundaries of Westbreaker Cove. 
Instead, it advances methodically, using the environment to its advantage—herding players toward foam pockets and surge zones. It reacts strongly to wave surges, 
becoming more animated and violent when the sea is active. Retreat is impossible; the creature fights until destroyed or until all players are driven out of the 
cove.

---

	Mechanics

* Type: Rare Monster
* Soloable: ❌ No
* Spawn Frequency: Very rare (hours to days)
* Spawn Method: Emerges from dense sea foam during active wave cycles
* Active Limit: Only one Tidebound Triton can exist at a time
* Leash Area: Confined strictly to Westbreaker Cove
* PvP Interaction: Not applicable
* Drops: Not specified

---

		Core Mechanics

	Foam-Bound Presence : 
* Gains increased defense and regeneration while standing in active foam zones
* Foam zones shift naturally with wave mechanics

	Wave Synchronization : 
* Certain abilities are empowered immediately after wave surges
* Surge timing alters attack patterns

	Environmental Dominance : 
* Uses the cove’s shifting pools and limited space to pressure groups
* Forces constant repositioning

---

		Combat Phases

	Phase I – Emergence (100%–70%)
* Recently formed, body partially unstable
* Relies on wide, sweeping attacks
* Foam presence is moderate

	Behavioral Focus: Area denial and positioning pressure

---

	Phase II – Tidal Assertion (70%–35%)
* Body fully solidifies
* Foam density increases across the cove
* Attacks become faster and more punishing

	Behavioral Focus: Sustained pressure and forced movement

---

	Phase III – Breakwater Rage (35%–0%)
* The Triton becomes erratic
* Wave surges occur more frequently
* Foam zones rapidly shift and expand

	Behavioral Focus: Overwhelm and collapse formations

---

		Skills

	1. Tidebreaker Slam

* Description: Slams its arms into the ground, sending a wave of water outward.
* Effect: Knockback and movement disruption.

---

	2. Foam Veil

* Description: Pulls surrounding foam upward, obscuring vision.
* Effect: Reduced visibility and targeting accuracy for players.

---

	3. Undertow Grasp

* Description: Summons a pulling current beneath nearby players.
* Effect: Drags players toward active foam zones.

---

	4. Breaker’s Roar

* Description: Emits a deep, resonant roar synchronized with crashing waves.
* Effect: Brief disorientation and delayed actions.

