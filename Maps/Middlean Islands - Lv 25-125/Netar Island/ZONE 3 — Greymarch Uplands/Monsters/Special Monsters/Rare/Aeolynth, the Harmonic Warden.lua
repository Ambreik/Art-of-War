			Aeolynth, the Harmonic Warden

	Location : Greymarch Uplands \ Zone 3 \ Cradle of the Standing Stones (summoned via Ritual Summoning Circle)

---

	Appearance : 
* Towering, ethereal being of semi-transparent stone and living wind currents.
* Hieroglyphic glyphs shimmer along its limbs, pulsing in sync with wind and harmonic tones.
* Limbs are elongated and slightly curved, almost floating rather than walking.
* Eyes are like spinning vortexes of soft light, changing color with its resonance.
* Surrounding it is a faint harmonic aura, causing slight visual distortion of the air.

---

	Personality : 
* Non-hostile unless provoked.
* Appears calm, deliberate, and ancient.
* Responds to player presence with curiosity, but maintains its ritual focus.
* Exhibits a “guardian of harmony” demeanor — protects the Cradle from misuse of the stones.

---

	Behavior : 
* Patrols the ritual circle slowly if no players interact, orbiting stones.
* Activates environmental effects in sync with wind patterns (e.g., gusts of wind, vibrating ground).
* Can interact with summoned adds (stump-like elemental constructs) to maintain harmony.
* Passive interaction: players may approach, emotes may trigger visual responses from glyphs.

---

	Mechanics : 
* Summoning Requirements : Only appears after the Ritual Summoning Circle is successfully activated with proper items and alignment.
* Adds Summon : Can call elemental stumps that root players if combat occurs.
* Regeneration : Heals gradually if near water or rain (environmental tie-in).
* Non-Soloable : Designed for parties (1–3+ players suggested).
* Interaction : Can be observed, emotes performed near it, or minor buffs applied (like enhanced awareness in the ritual area).
* Cooldown : Ritual circle resets 4–6 hours after summoning; Aeolynth disappears after 30 minutes or upon ritual completion.

---

	Skills : 

* Skill 1 : Resonant Pulse
	* Description : Sends a harmonic wave around the ritual circle, amplifying or stabilizing glyphs on stones.
	* Effect : Adjusts puzzle difficulty or reinforces the circle’s alignment.

* Skill 2 : Windbind Stump
	* Description : Summons a temporary wooden stump with swirling glyphs that roots players in place if combat is attempted.
	* Effect : Controls party movement if ritual is disrupted; disappears after a few seconds.

* Skill 3 : Harmonic Gust
	* Description : Creates a soft wind that pushes players slightly in alignment directions.
	* Effect : Encourages correct positioning for rituals, or subtle environmental challenge.

* Skill 4 : Glyph Resonance Heal
	* Description : Heals itself over time if environmental water or rain is present.
	* Effect : Promotes environmental interaction awareness.

* Skill 5 : Sonic Reverberation
	* Description : Sends harmonic tones across the circle that can stun nearby hostile mobs or temporarily reveal hidden objects.
	* Effect : Bonus puzzle assistance or minor defensive mechanic.

* Skill 6 : Aura Synchronization
	* Description : Aligns all nearby rune glyphs with its internal frequency.
	* Effect : Resets puzzle progress if misalignment occurs, or enhances success visuals for players.

* Skill 7 : Wind Echo
	* Description : Generates audible feedback through the stones, guiding players to correct steps in the ritual.
	* Effect : Provides player hints; subtle auditory guidance.

---

	Quotes : 

* "Harmony… must be preserved."
* "The stones speak in tones… listen well."
* "Disruption here will not be tolerated… but understanding will be rewarded."
* "Approach with respect, and the wind will reveal its secrets."
* Emotes / non-verbal cues : Glyphs pulse brighter when players succeed or step correctly.

---

	Notes : 

* Aeolynth is lore-first; not meant to drop loot.
* Summoning reinforces player teamwork and environmental awareness.
* Can be used in future expansions for puzzle-linked buffs or questlines.
* Serves as a living, reactive centerpiece for the Cradle of the Standing Stones.

