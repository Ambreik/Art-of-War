			Echoris, the Resonant Sentinel

* Type : Rare Monster (Ritual-Summoned)
* Element : Sound / Stone / Arcane
* Spawn Source : Oldwind Tunnels — Chamber III: The Azure Depth
* Soloable : ❌ No
* Pet Use : ❌ Disabled (Tunnel Rule)
* Cooldown : Several hours (Chamber-specific)

---

	Location : Middlean Islands → Netar Island → Zone 3: Greymarch Uplands → Oldwind Tunnels → Azure Depth (Blue Chamber)

---

	Appearance : Echoris manifests as a tall, semi-transparent humanoid figure made of dark stone, etched with glowing blue glyphs shaped like sound waves and 
arcane runes.
	Its body floats slightly above the ground, each step creating a faint echo that lingers unnaturally in the chamber. Its eyes are two deep pools of liquid light,
reflecting the faint torch glow.
	Its arms end in long, chiseled appendages resembling tuning forks, which emit pulses of blue energy with each movement. Small fragments of crystal orbit around 
its body, shifting in sync with the chamber’s ambient echoes.
	When it moves, soft chimes and harmonic tones reverberate off the tunnel walls, giving a strong sense that the environment itself is reacting.

---

		Behaviour : 

* Moves deliberately, almost gliding
* Patrols in patterns tied to echo triggers
* Reacts to player-triggered sound:
	* Moving too quickly
	* Attacking randomly
	* Misstepping on echo slabs
* Prefers ambushing from echo misdirections
* Players must solve positional and rhythm puzzles under threat

---

		Core Mechanics : 

	1. Echo Alignment Sensitivity : 

* Echoes in the chamber are directly tied to Echoris’ attacks
* Stepping on the wrong stone slabs:
	* Creates false echoes
	* Temporarily buffs Echoris’ attacks
* Correct sequences weaken the monster briefly

---

	2. Resonant Pulse : 

* Echoris releases area sound waves
* Waves deal damage and disorient players, especially if multiple players are stacked
* Visual cue: glyphs glow before release

---

	3. Harmonic Displacement : 

* Can teleport short distances along echo lines
* Frequently repositions to maintain chamber control
* Makes direct line-of-sight targeting difficult

---

	4. Crystal Orbit Barrage : 

* Surrounding crystals fire slow-moving shockwave projectiles
* Projectiles can:
	* Stun briefly
	* Interact with chamber echo zones to amplify attack

---

	5. Chamber Memory Trigger : 

* Players must walk paths in a specific sequence
* Incorrect sequence triggers:
	* Minor damage
	* Temporary buffs to Echoris
* Echoes “corrected” if players succeed, weakening its attacks temporarily

---

		Phases : 

	Phase I — The Listening (100%–70%)

* Moves slowly
* Uses Resonant Pulse sparsely
* Introduces positional challenge

	Phase II — The Reverberation (70%–35%)

* Echo misdirections increase
* Crystal Barrage begins
* Disorientation stacks appear

	Phase III — The Harmonic Collapse (35%–0)

* Almost constant attack cycle
* Teleports frequently along echo paths
* Requires full party coordination
* Punishes mistakes harshly

