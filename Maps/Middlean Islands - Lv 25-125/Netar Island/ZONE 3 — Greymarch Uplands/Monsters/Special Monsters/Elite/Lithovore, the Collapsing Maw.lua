			Lithovore, the Collapsing Maw

	Location : Middlean Islands \ Netar Island \ Zone 3 Greymarch Uplands \ Oldwind Collapse

---

	Appearance : A massive, rock-encrusted beast that appears to be a hybrid of elemental stone and ancient living flesh. Its body is mottled with jagged boulders 
and glowing crystal veins that pulse faintly with energy from the surrounding collapse. Massive claws can tear through stone, and its tail ends in a spiked mass 
capable of smashing through rubble. A faint fungal growth covers parts of its back, echoing the glowing Glowsprout clusters in the cavern. Its eyes glow amber, 
reflecting intelligence and primal rage.

---

	Personality : 
* Territorial, highly aggressive to intruders.
* Reacts violently to sudden movements or attacks in its vicinity.
* Seemingly aware of traps and environmental hazards in its lair, occasionally using collapsing rocks or debris to its advantage.

---

	Behaviour : 
* Patrols the collapsed cavern slowly at first, becoming more aggressive if players approach deeper areas.
* Uses the terrain: may knock unstable rocks to create hazards for players.
* Adds (rock/stone elemental minions) may spawn if players linger too long near key zones.
* Cannot be soloed effectively; designed for small party or group encounters.
* Reacts to environmental cues: fungus clusters glowing brighter can make it more alert, while collapsed rocks reduce its line-of-sight.

---

	Mechanics : 
* Type : Rare Elite Boss
* Respawn : 6–12 hours after defeat; spawn gated behind Oldwind Collapse interactions (fungus + relics + collapsible rocks cleared).
* HP Scaling : Adjusted to party size; HP bar visible to players.
* Adds :
	* Stone Stumps – minor mobs that root players.
* Environmental Interaction : Can trigger debris falls or dislodge rocks, causing minor environmental damage if players are in range.
* Regeneration : If water from nearby streams is present, slowly regenerates HP.
* Loot : No immediate drops; future expansions may include rare crafting mats, lore items, or collectibles.

---

	Skills : 

* Skill 1: Earthquake Slam
	* Description: Smashes the ground with massive claws, causing a shockwave.
	* Effect: Area knockback + minor environmental damage; may knock players into debris.

* Skill 2: Boulder Toss
	* Description: Picks up loose rocks and hurls them toward players.
	* Effect: Ranged AoE damage; can also trigger environmental collapse if near weak ledges.

* Skill 3: Rooting Spike
	* Description: Summons stone stumps from the ground to immobilize players temporarily.
	* Effect: Roots players for 3–5 seconds; adds minor environmental hazards.

* Skill 4: Collapsing Tail
	* Description: Swings tail to knock down boulders or debris.
	* Effect: Heavy knockback + minor terrain damage; can alter player pathing.

* Skill 5: Crystal Pulse
	* Description: Emits an energy pulse from glowing crystal veins.
	* Effect: Small AoE stun or interrupts player casting.

* Skill 6: Fossilized Roar
	* Description: Roars to intimidate and stagger players in range.
	* Effect: Temporarily reduces player attack speed; increases threat of adds.

* Skill 7: Regenerative Flow
	* Description: Absorbs water from nearby streams to regenerate HP slowly over time.
	* Effect: Passive regen; can be interrupted if streams are blocked or diverted.

---

	Quotes : 

* Upon first encounter:
	* “The stones themselves cry out in warning…”
	* “You dare tread upon my domain?”

* During combat:
	* “Foolish mortals! The collapse will claim you!”
	* “Feel the weight of ages!”

* Upon taking heavy damage:
	* “You strike me, but cannot break me!”

* Upon being defeated:
	* “The cavern… claims me… but not… forever…”

---

		Gameplay Tie-in : 

* The creature is directly tied to the Oldwind Collapse POI and questline :
	* Players must interact with fungi, relics, and collapsible rocks to enable spawn.
	* Designed for team-based combat, cannot be soloed easily.
	* Environmental hazards and debris are integral to both the challenge and the lore of the location.
* Acts as a capstone encounter for the collapse, rewarding exploration, archaeology skill, and environmental mastery.

