			Thar’Kezhul, the Buried Overseer

* Type : Elite — Ancient Stone / Earthbound Entity
* Location : Greywatch Sink – Lower Spiral & Sealed Chamber Perimeter
* Visibility : Not always present (reactive spawn)
* Soloable : No (small coordinated group minimum)

---

	Lore Identity : "Thar’Kezhul" was not a guardian placed after the collapse.
	It was the last overseer construct, awakened during the excavation when the stone itself rejected removal.
	Rather than being destroyed, it was entombed alive, partially fused into the cavern walls — neither active nor dormant, but listening.

> “When the miners fled, the Overseer remained — not by order, but by obligation.”

---

		Spawn Logic (Keeps It Secret)

	Thar’Kezhul does not spawn on a fixed timer.

	Spawn Conditions (Any Two Trigger It) : 
* Prolonged excavation attempts without proper Archaeology Rank
* Repeated seal destabilization
* Stone pressure spikes caused by careless movement or combat
* Archaeologist reaching Rank III+ inspection threshold

> This ensures casual explorers feel watched, but don’t always fight it.

---

	Physical Description : 

* Upper torso partially embedded in stone wall
* Massive asymmetrical body — one arm fully formed, the other fused into rock
* Surface etched with:
	* Fracture lines
	* Old extraction glyphs
	* Sealing runes layered over each other
* Eyes glow dim amber when inactive — flare violently when engaged

	Stone dust falls constantly from its movements.

---

		Combat Philosophy

	This is not a DPS race.

	Thar’Kezhul is designed to:
* Punish positioning
* React to the environment
* Interfere with Archaeology mechanics
* Escalate if fought incorrectly

---

		Core Mechanics

	1. Stonebound Domain

	The arena is partially alive.

* Certain floor tiles harden or soften mid-fight
* Standing still too long causes:
	* Rooting
	* Slow petrification buildup
* Moving recklessly triggers:
	* Stone spikes
	* Falling debris

> The monster controls the battlefield, not the players.

---

	2. Excavation Backlash

	Triggered when players:
* Use high-impact skills
* Attack sealed structures
* Cause structural stress

	Effect : 
* Shockwave through the chamber
* Temporary seal reinforcement
* Elite gains defensive stacks

	Encourages controlled damage, not burst.

---

		3. Overseer’s Call

	Periodically summons Stonebound Remnants :
* Half-formed miner constructs
* Slow, relentless
* Explode into pressure waves if ignored

	These adds:
* Interrupt inspections
* Increase chamber instability

---

		4. Petrifying Gaze

	Directional cone ability.

* Applies stacking stone corruption
* At max stacks:
	* Player partially turns to stone
	* Movement & casting restricted

	Can be mitigated by:
* Breaking line of sight
* Standing near specific stone nodes

---

		5. Last Directive (Enrage Mechanic)

	If:
* Fight drags too long
* Players brute-force the encounter
* Chamber integrity drops too low

	Thar’Kezhul:
* Fuses deeper into the wall
* Becomes immobile
* Turns arena into collapsing hazard field

	This phase is about survival, not damage.

---

		Non-Combat Interaction Option (Advanced)

	At higher Archaeology Ranks:

* Players may stabilize pressure nodes mid-fight
* Successful stabilization:
	* Weakens Thar’Kezhul
	* Reduces add spawns
	* Prevents enrage escalation

	This rewards Archaeologists without making them mandatory.

---

		Defeat Outcomes

	Thar’Kezhul is not permanently killed.

	After defeat:
* It fractures and sinks back into stone
* Chamber pressure stabilizes for several hours
* Sealed chamber becomes safe for inspection

	Future respawns:
* Are slower
* Become more aggressive if repeatedly provoked

---

		Narrative Payoff

	Defeating Thar’Kezhul reveals:
* Overseer was never meant to protect the miners
* It was meant to protect what they were taking
* The seal is a compromise, not a prison

> “The stone did not fear theft.
> It feared what theft would awaken.”

---

		Passive Abilities : 

	Buried Authority (Passive)

* Thar’Kezhul is partially fused into the cavern
* Takes reduced damage while standing near stone walls
* Gains resistance stacks when players cause structural stress

> Encourages controlled movement and positioning

---

	Chamber Integrity Awareness (Passive)

* Tracks environmental destabilization
* High instability:
	* Increases add spawn rate
	* Accelerates ability cooldowns
	* Pushes encounter toward "Last Directive"

---

		Core Combat Abilities

	1. Fracture Sweep

* Wide horizontal slam with its free arm
* Sends jagged stone shards across the ground
* Shards:
	* Apply bleed + slow
	* Remain briefly as hazardous terrain

	Visual : Cracks race across the floor before impact
	Counterplay : Step back or reposition diagonally

---

	2. Pillar Rend

* Targets a stone pillar or wall section
* Rips part of it free and hurls debris forward
* Impact zone causes:
	* Knockback
	* Temporary rubble obstacles

	Interaction Note :
Breaking too many pillars accelerates chamber collapse.

---

	3. Petrifying Gaze

* Frontal cone, slow wind-up
* Applies "Stone Corruption" stacks

	At Max Stacks :
* Player partially petrified
* Movement speed severely reduced
* Casting interrupted periodically

	Mitigation :
* Break line of sight
* Stand near stabilized stone nodes (if active)

---

	4. Overseer’s Call

* Slams embedded arm into the wall
* Summons "Stonebound Remnants"

	Spawn behavior:
* Appear from cracks and collapsed seams
* Target random players or archaeology interactions

	Cooldown shortens as fight progresses.

---

	5. Excavation Backlash

* Triggered, not timed
* Activates when:
	* Players deal heavy burst damage
	* Seals are struck
	* Environmental objects are destroyed carelessly

	Effect:
* Shockwave through chamber
* Temporary damage reduction for Thar’Kezhul
* Minor stun or knockdown to nearby players

---

		Advanced / Pressure Abilities

	6. Stonebind Grasp

* Targets a distant player
* Stone arm erupts from the ground beneath them
* Roots and pulls slightly toward Thar’Kezhul

	If not broken:
* Applies stacking stone corruption

---

	7. Last Directive (Enrage / Fail-Safe Phase)

	Triggered when:
* Fight exceeds intended duration
* Chamber integrity critically low
* Archaeology mechanics are ignored

	Effects:
* Thar’Kezhul fuses deeper into the wall
* Stops moving
* Arena becomes unstable:
	* Falling debris
	* Collapsing floor sections
	* Narrow safe zones

	Victory condition here is survival + pressure stabilization, not raw DPS.

---

		Interaction Skill (Archaeology-Favored)

	Stabilize Pressure Node (Optional, Rank III+)

* Channel interaction during combat
* Temporarily:
	* Reduces add spawns
	* Weakens Overseer’s Call
	* Delays Last Directive

Failure causes backlash damage.

