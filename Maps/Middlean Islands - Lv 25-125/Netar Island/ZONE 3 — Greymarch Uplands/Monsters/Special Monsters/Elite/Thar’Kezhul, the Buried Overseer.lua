		II. Sub-Enemy — Stonebound Remnants

* Type : Elite Add / Environmental Threat
* Faction : Greywatch Constructs
* Role : Pressure, disruption, punishment for neglect

---

	Description :
* Half-formed humanoid shapes
* Miner tools fused into stone limbs
* Faces cracked, featureless, faintly glowing
* Drag themselves from walls or rubble piles

	They crumble constantly, yet refuse to fall.

---

		Behavior : 

* Slow but relentless
* Prioritize:
	* Archaeology interactions
	* Stationary players
	* Players afflicted by stone corruption
* Ignore taunts occasionally

---

		Abilities

	1. Crushing Advance

* Slow march toward target
* Each step creates minor shockwaves
* Standing directly in path causes stagger

---

	2. Pressure Detonation

* When killed or ignored too long:
	* Explodes into stone shards
	* Deals AoE damage
	* Increases chamber instability

	Encourages controlled elimination, not AoE spam.

---

	3. Binding Grip

* Short-range grab
* Roots player briefly
* Applies minor stone corruption

---

		Passive : 

	Unfinished Labor
* Takes reduced damage while Thar’Kezhul is active
* Damage increases if pressure nodes are stabilized

