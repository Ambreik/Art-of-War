# Quest Chain: **“Operation Liberty: Echoes of the Sealed Oath”**

**Quest Type:** Lore / Investigation / Dungeon-Linked
**Starting NPC:** *Field Marshal of Netar Village* (at dungeon entrance)
**Chain Length:** Medium–Long (6 main quests + optional branches)

---

## QUEST 0 – QUEST STARTER ITEM

### Item: **Note for Advancing Troops**

**Source:** Automatically given when player first speaks to the Field Marshal
**Flavor Text (Item):**

> *“If you read this, we failed. If you follow it, remember why.”*

Interacting with the item **starts the quest chain**.

---

## QUEST 1 – *“Orders Never Meant to Be Broken”*

**Given by:** Field Marshal of Netar Village
**Objective:**

* Read the *Note for Advancing Troops*
* Speak again with the Field Marshal

### Narrative

The Field Marshal explains:

* This note is a **reconstructed copy** of the original Liberty Crusaders orders
* The originals were lost — except for **symbolic carvings** found around Netar

> “They carved their regrets into stone… where retreat was impossible.”

**Unlocks:**
📍 Seabridge Arch added to quest tracking

---

## QUEST 2 – *“Where the Sea Witnessed the Lie”*

**Location:** Seabridge Arch (low tide)

### Objective

* Reach the hidden alcove under Seabridge Arch
* Examine the ancient symbols

### Mechanics

* Symbols are **not readable text**
* Interactions reveal:

  * A *sequence* of carvings
  * Some crossed out violently
  * One repeated symbol: **a broken standard over waves**

### Result

Player receives:

* **Crusader’s Memory Rune (Dormant)**

> The rune vibrates faintly near the symbols.

---

## QUEST 3 – *“The Rune That Remembers”*

**Given by:** Field Marshal
**Objective:**

* Bring the Dormant Rune back
* Learn its origin

### Lore Reveal

The rune:

* Was used by **officers** to record tactical decisions
* Reacts only where **decisions caused mass death**

> “These were not prayers. They were confessions.”

The Field Marshal directs you to **three sites** where the rune may awaken.

---

## QUEST 4 – *“Three Burdens of Liberty”*

**Multi-Location Quest**

### Objectives (Any Order)

Use the rune at:

1. **Seabridge Arch** – *The Betrayal*
2. **Stormfall Coves** – *The Landing*
3. **Broken Stone Forest** – *The Oath*

Each activation:

* Spawns a **Memory Echo** (non-hostile vision or weak hostile spirit)
* Adds a charge to the rune

After all three:

* Rune becomes **Crusader’s Memory Rune (Awakened)**

---

## QUEST 5 – *“The Seal Was Not Mercy”*

**Trigger:** First entry into the **Cursed Citadel of Netar** with Awakened Rune
**Passive Dungeon Interaction Quest**

### Effects Inside Dungeon

* Certain banners glow faintly
* Whispered dialogue becomes **clearer**
* Some undead **recognize the rune**

Example NPC whisper:

> “You carry the weight… but not the blame.”

### Objective

* Defeat **any one** named Crusader Captain boss
* Collect:

  * **Fragment of Mystic Stone**
  * **Crusader Banner Shard**

---

## QUEST 6 – *“Echoes of Command”* (Chain Finale)

**Given by:** Field Marshal
**Objective:**

* Combine the items
* Choose how the memory is preserved

### Player Choice (Permanent, Account-Noted)

#### Option A – **Preserve the Truth**

* Rune becomes: *Crusader’s Memory Rune – Unbroken*
* Effect:

  * Minor passive bonus vs Undead
  * Unlocks hidden dialogue with Order of Restoration NPCs
* Lore:

  * The Crusaders are remembered as **tragic, not traitors**

#### Option B – **Seal the Shame**

* Rune becomes: *Crusader’s Memory Rune – Bound*
* Effect:

  * Slight corruption resistance
  * Increased drop chance of Mystic Stone fragments
* Lore:

  * History remains officially unchanged

