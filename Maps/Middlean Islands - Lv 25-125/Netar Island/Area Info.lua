		Netar Island - Area Info
			
---

# **1. Island Overview**

* Netar Island is the biggest island, located in the north.
* Island shaped like an “L”.
* A long snake-like mountain spans from south to north.
* The Tomb of Great Crusaders megadungeon is inside this mountain.
* Entrance to the dungeon is in the south; exit is in the north.
* The island contains rivers that fragment some zones (partially dividing land masses).
* The island is divided into 2 sides via mechanics: a south side and north side, separated by an invisible line.
* Terrain on map is visually divided by rivers to reflect the mechanic line.
* Island level range: 40–85.

---

# **2. Zones**

* Island is divided into zones.
* Each zone contains specific herbs, mines/ores, and fishing families.
* One boss per zone.
* Each zone has 1 Mystic Stone (spawns every 2–3 hours).

---

# **3. Villages & Settlements**

* There are 3 villages on the island.
* One landing location exists in the south side (first zone).
* Other piers exist for fishing and exploration.
* One village is near the megadungeon entrance.
* The northern village near the dungeon exit is heavily fortified with stone walls and one gate on the east side.
* Each village has a Flying Master NPC.

---

# **4. Flying & Mount Restrictions**

* Flying mounts are permitted on the island.
* Flying mounts are disabled near the mountain radius.
* Flying mounts are disabled on the top side of the island.
* Only Flying Master transport works in restricted areas.
* Crossing the invisible line (north side) removes flying if using a mount.
* Strong Winds (Cliff Pushback) mechanic exists.

---

# **5. Flying Master NPC System**

* One Flying Master NPC per village.
* They have dedicated huts (similar to WoW).
* Provide travel between points.

---

# **6. Dungeon Connection (Tomb of Great Crusaders)**

* Dungeon located inside the mountain.
* Entrance: south, inside a destroyed castle built at the mountain edge.
* Exit: north, not far from the fortified village.
* Monsters can occasionally escape the dungeon (Order of Restoration & Field Marshal involvement).

---

# **7. Monsters – General Rules**

* Monsters are tough and group-focused.
* Rare monsters are solo-group difficulty.
* Aquatic monsters have invisible names when underwater.
* Monsters have unique CC depending on island side:

  * South side: **Stun**
  * North side: **Root**
* CC can stack.

---

# **8. Bosses**

* 3 major bosses with 12-hour respawn.
* 1 boss per zone (total 5).
* 1–2 smaller bosses per zone.
* No boss is soloable except smaller bosses.
* Boss-luring mechanic:

  * If boss crosses to another zone, respawn time reduces by 30%.

---

# **9. Rare Monsters**

* Total: **33 rare monsters**.
* If rare monsters are lured too far from spawn: a signal triggers (your idea list cuts here, so no further info to add).

---

# **10. Hidden NPCs & Random Encounters**

* 5 hidden NPCs exist on the island.
* 8 small caves exist in the mountain with:

  * Random monsters
  * Random chest
  * Random NPC spawn (day/week)
  * Random boss spawn (day/week)

---

# **11. Special NPC Types**

* Field Marshal NPC located in one village.
* Order of Restoration faction present.
* A unique NPC that teaches “Traps”.
* Beast Tamer NPC located in the north:

  * Provides taming-related quests.
  * Sells special traps.
  * Gives unique item (TBD).

---

# **12. Resources & Gathering**

* All resources spawn every 30 minutes.
* Depleted mechanic applies to mining and herbs (not fishing).
* During night:

  * Resources spawn every 15–30 minutes.
  * Depleted mechanic disabled.
* Mining veins are in the **south** side.
* Ore deposits are in the **north** side.
* Some veins can become buffed at night, dropping improved minerals.
* On the island, herbs include 3 poisonous herbs:

  * Harvesting them applies a lethal poison causing death.
  * Surrounded by a green glow.
* Fishing group bonus mechanic:

  * In southern village seaside pontoon cliffs:

    * Group fishing grants **+10 chance for rare fish**.
* Hunters (profession) can drop special items only they can loot.

---

# **13. Currency & Vendors**

* Local currency: **Powder of Stinky Pollen**.
* Vendors accept only this currency for special goods.
* Rank III+ monsters drop it at night.
* Potions are excluded (not bought with this currency).

---

# **14. Environmental Features**

* Tall trees around the mountain, some extremely large.
* Bioluminescent Environmental Light exists near mountains.
* Stealth & Camouflage vegetation exists only in the north side.
* NW area: ruins with aggressive monsters and poison DoTs.
* NW coastline: many sunken ships.
* SW area: underwater caves + underwater wrecks.
* NE shoreline: stone pillars scattered around.
* First island where players encounter flying monsters.

---

# **15. Travel & Respawn**

* “Restart in Town” teleports to nearest village.
* Flying Master NPCs handle safe vertical travel.
* No World Chest on this island.

---

# **16. Danger & Difficulty Modifiers**

* XP loss on death increased by **2.3%**.
* Monsters at night have enhanced drop rules (pollen).
* Group-required monsters for safety.

