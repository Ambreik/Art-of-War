			RESOURCE & LOOT IDENTITY — WINDHOLM SPIRES

		"The Wind Leaves Proof Behind"

	Windholm is not just dangerous — it produces materials that cannot exist elsewhere, shaped by :
* Extreme airflow
* Pressure
* Altitude
* Sound resonance
* Storm energy

	These resources feel aerodynamic, unstable, whispering, and alive.

---

	CORE RESOURCES OVERVIEW : 

| Resource             | Identity                    | Role                              |
| -------------------- | --------------------------- | --------------------------------- |
| **Aero-Crystals**    | Solidified wind energy      | Gliders, upgrades, power nodes    |
| **Windglass Shards** | Wind-polished crystal glass | Enchantments, optics, instruments |
| **Sky-Iron Ore**     | Lightweight metal alloy     | Mobility gear, weapons, armor     |
| **Whisper Feathers** | Sound-reactive feathers     | Runes, stealth, wind magic        |

	Each resource has unique gathering logic — not just mining nodes.

---

		AERO-CRYSTALS

	"Wind Made Solid"

	Lore : Formed where wind pressure compresses airborne mana over centuries.

	Acquisition :
* Found in wind tunnel cores
* Spawns after storms
* Appears inside stabilized vortexes
* Drops from elite wind-beasts

	Gameplay Identity :
* Fragile — can shatter if mishandled
* Can discharge wind bursts when struck
* Some float unless anchored

	Uses : 

	Gliders
* Increase endurance capacity
* Boost wind efficiency
* Power special glide abilities
* Enable mid-air burst boosts

	Wind Shrines
* Stabilize airflow
* Redirect storm currents
* Fuel ancient mechanisms

	Runes
* Create Wind Amplification Runes
* Temporary flight-enhancing buffs

---

		WINDGLASS SHARDS

	"Crystal Sculpted by Infinite Wind"

	Lore : Sand exposed to endless high-speed wind fuses into translucent glass.

	Acquisition :
* Found on spire edges
* Carved from wind-polished rock
* Dropped by "Whistling Drakes"
* Harvested from shattered wind vanes

	Gameplay Identity :
* Reflects airflow visually
* Vibrates when storms approach
* Used to track invisible wind currents

	Uses :

	Enchantments
* Wind resistance
* Fall damage reduction
* Sound dampening enchantments

#	Tools
* Wind lenses (show air tunnels)
* Glider airflow monitors
* Sound-direction tracking devices

	Alchemy
* Potion catalysts
* Volatility amplifiers

---

		SKY-IRON ORE

	"Metal That Wants to Fly"

	Lore : A rare ore infused with high-altitude magnetic fields, making it lighter than normal iron.

	Acquisition :

* Found only on high spire cliffs
* Requires climbing or gliding to reach
* Nodes sway in the wind
* Elite guardian enemies protect deposits

	Gameplay Identity :
* Lightweight
* Flexible
* Resistant to wind shear
* Can store airflow charge

	Uses :

	Mobility Gear
* Glider frames
* Grappling hooks
* Wind boots
* Lightweight armor

	Weapons
* Faster swing speed
* Air-slicing blades
* Throwing weapons with extended range

	Professions
* Blacksmithing
* Engineering
* Advanced tool crafting

---

		WHISPER FEATHERS

	"Sound-Carrying Relics of the Sky"

	Lore : Shed by creatures evolved to sense sound across miles.

	Acquisition :
* Dropped by Whistling Drakes
* Found in high nests
* Harvested from Sky-Stalker remains
* Looted from hidden roosts

	Gameplay Identity :
* React to sound
* Hum when enemies are nearby
* Can store echo signatures

	Uses :

	Runes (HIGH IMPORTANCE)
* Sound-masking runes
* Detection runes
* Movement silence buffs
* Combat echo amplifiers

	Stealth Systems
* Reduce sound detection
* Mislead predators
* Fake noise generators

	Parchants & Quest Items
* Forbidden letters
* Sound-coded maps
* Wind-language scrolls

