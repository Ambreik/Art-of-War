			ENVIRONMENTAL PUZZLES (NATURAL) — WINDHOLM SPIRES

	"Solve the Wind, Not a Button"

	Puzzles in this zone are based on :
* Airflow
* Pressure
* Momentum
* Sound
* Balance
* Wind redirection

	Instead of pressing switches, players manipulate wind behavior.

---

		CORE DESIGN PRINCIPLE

	These puzzles : 
✅ Look like part of the environment
✅ Follow physical logic
✅ Have visible cause-and-effect
✅ Teach players to understand wind patterns
❌ Avoid magical UI popups
❌ Avoid immersion-breaking mechanics

	Wind becomes a tool, lock, bridge, and timer.

---

		PUZZLE TYPE 1 — ROTATE SPIRE VANES (WIND REDIRECTION)

	Concept : Ancient wind vanes mounted on spires can be rotated to redirect airflow.

	Player Actions :
* Climb to vane platforms
* Rotate giant directional fins
* Align multiple vanes to create a stable wind corridor

	Results :
* Redirect wind to open new glider routes
* Create updrafts to reach higher spires
* Disable dangerous crosswinds

	Challenge Layer :
* Strong gusts can rotate vanes back
* Some require two players to hold position
* Timed puzzles where wind shifts mid-rotation

	Feels like engineering, not magic.

---

		PUZZLE TYPE 2 — TIMED CROSSINGS (WIND SHIFT WINDOWS)

	Concept : Some paths are only safe when wind direction changes.

	Mechanics :
* Wind shifts on a global timer
* Players must observe banners, dust, or sound pitch
* Crossing too early = pushed off
* Crossing too late = slammed into rock

	Variants :
* Solo precision crossings
* Group coordination crossings
* Escort missions through shifting wind corridors

	Skill Expression : Veterans learn to predict wind cycles by sound or movement.

---

		PUZZLE TYPE 3 — WEIGHTED AIRFLOW OBJECTS

	Concept : Players drop heavy objects to alter pressure currents.

	How It Works:

	Dropping weight :
* Creates downward air displacement
* Breaks unstable turbulence
* Anchors floating wind platforms
* Opens sealed airflow tunnels

	Example Objects :
* Ancient stone weights
* Broken statue fragments
* Cargo crates
* Fallen spire plates

	Puzzle Ideas :
* Drop weight to stabilize a glider launch ramp
* Anchor a floating bridge
* Open a sealed wind gate
* Collapse an unstable vortex

	Feels like real-world airflow manipulation.

---

		PUZZLE TYPE 4 — ANCIENT WIND SHRINES (STABILIZATION NODES)

	Concept : Old civilization built shrines that regulate wind flow.

	Activation Effects :
* Calm violent corridors
* Lock wind direction temporarily
* Create safe glide lanes
* Silence howling spires

	Gameplay Layers :
* Requires power source (runes, parchants, artifacts)
* Must be reactivated after storms
* Some attract wind-beasts when active

	Lore Flavor : These shrines are remnants of a civilization that engineered climate control.

---

		PUZZLE TYPE 5 — SOUND-BASED WIND LOCKS

	Because sound travels far here…

	Mechanic : Certain doors or airflow tunnels respond to specific wind tones.

	Examples :
* Align spires until the whistle pitch matches
* Play a wind flute to harmonize airflow
* Silence zones to disable hostile wind

	Wind becomes a musical lock.

---

		PUZZLE TYPE 6 — GLIDER-REQUIRED CHAIN PUZZLES

	Some puzzles require mastering glider physics :

	Examples :
* Ride sequential wind boosts to reach activation points
* Glide through rings to stabilize airflow nodes
* Maintain altitude long enough to charge wind crystals

	Fail State :
Fall → reset → try again
But failure feels physical, not artificial.

---

		PUZZLE TYPE 7 — COOPERATIVE WIND ENGINEERING

	Group-based puzzles :

	Examples :
* One player rotates vanes while another glides
* One holds airflow pressure while others cross
* Teams synchronize timed wind shifts

	Rewards :
* Secret vaults
* Rare parchants
* Rune crafting materials
* Hidden glider upgrades

---

		DYNAMIC PUZZLE VARIANTS (REPLAYABILITY)

	Puzzles change depending on :

| Condition      | Change                |
| -------------- | --------------------- |
| Storm          | More violent wind     |
| Time of day    | Direction shifts      |
| Player actions | Corridors destabilize |
| World events   | New routes open       |

	No static solutions — players learn wind logic, not memorization.

---

		REWARD THEMING

	Solving puzzles grants :
* Wind-imbued parchants
* Rune fragments
* Glider upgrades
* Lost maps
* Forbidden letters
* Expedition permits
* Secret traversal routes

	Rewards reinforce exploration + mastery.

