			WIND WHISTLING — SOUND STEALTH SYSTEM
		Windholm Spires Environmental Mechanic

	The spires act like giant sound amplifiers.
	Wind carries noise farther than normal, bending and echoing through corridors.

	Here, sound = danger.

---

		1. CORE SYSTEM — SOUND TRAVELS THROUGH WIND

	Unlike normal zones :
* Noise travels long distances
* Wind amplifies and redirects sound
* Echoes linger longer
* Enemies track direction + rhythm

	Sound becomes :
* A tracking signal
* A lure
* A threat
* A PvP intelligence tool

---

		2. PLAYER SOUND GENERATION TYPES

| Action                 | Sound Level | Risk              |
| ---------------------- | ----------- | ----------------- |
| Slow crouch walking    | Very Low    | Safe              |
| Normal walking         | Low         | Minor             |
| Sprinting              | High        | Alert risk        |
| Jumping / hard landing | High        | Danger            |
| Combat                 | Very High   | Predator beacon   |
| Falling impacts        | Extreme     | Instant detection |
| Breaking objects       | Extreme     | Zone-wide echo    |

---

		3. WIND MODIFIERS — SOUND BEHAVIOR

	Tailwind (Sound Travels Further)
Your noise is carried farther downwind.

	Headwind (Sound Dampened)
Noise travels shorter distance.

	Wind Corridors (Sound Funnels)
Narrow spire gaps channel sound like pipes — a whisper can travel hundreds of meters.

	Storm Winds (Chaotic Sound)

	Sound becomes :
* Distorted
* Misleading
* Multi-directional

	Enemies may react unpredictably.

---

		4. STEALTH GAMEPLAY — SOUND DISCIPLINE

	Silent Movement Options : 
* Crouch-walking reduces echo
* Soft-landing glider perk reduces impact noise
* Rope climbing avoids stone clatter
* Cloth boots reduce footstep reflection
* Wind timing: move during loud gusts

	Mistakes Result In :
* Wind-beasts tracking you
* Ambush predators arriving
* PvP players locating you
* Territory-wide danger escalation

---

		5. SOUND MEMORY — ENEMIES REMEMBER NOISE

	Enemies store :

* Last heard sound
* Direction
* Pattern type (footsteps vs combat)

	They :
* Investigate
* Patrol
* Lay ambushes
* Call others if repeated noise occurs

	Repeated loud actions = pack escalation.

---

		6. WIND-BEAST MONSTER FILE — WINDHOLM SPIRES

These integrate directly into your zone monster system.

---

	Sky-Stalkers

* Type : Agile Predator
* Behavior : Silent ambusher

	Mechanics :
* Leap between spires
* Track footstep rhythm
* Ambush from above after sound detection
* Retreat if target becomes silent

	Counterplay : Stop moving to break tracking.

---

	Whistling Drakes

* Type : Sound-Hunting Flying Predator
* Core Trait : Tracks echo tones

	Mechanics :
* Navigate using wind sound currents
* Attack loud gliding players
* Emit mimic whistles to confuse prey
* Become more aggressive during storms

	Trigger : Combat noise, falling impacts, loud landings

---

	Spire Leeches

* Type : Environmental Parasite
* Behavior : Silent trapper

	Mechanics :
* Detect vibration through rock
* Drop onto noisy climbers
* Drain stamina if attached
* Fall off if player moves silently afterward

---

	Aerial Predators

* Type : Dive Hunters
* Trigger : Falling or panicked movement

	Mechanics :
* Detect sudden vertical movement
* Divebomb falling players
* Retreat if player regains glide control

---

	Wind Howlers

* Type : Pack Predator
* Trait : Responds to repeated sound patterns

	Mechanics :
* Summon pack members after repeated noise
* Track player across corridors
* Lose trail if player stays silent long enough

---

	Echo Wisps

* Type : Sound-Feeding Entity

	Mechanics :
* Spawn where noise lingers
* Drain mana/stamina from loud players
* Multiply if players keep making noise
* Fade when silence returns

---

		7. PvP — SOUND TRACKING WARFARE

	PvP Players Can :
* Track enemies by footsteps echo
* Predict enemy direction from wind-carried noise
* Throw sound lures to bait enemies
* Use loud combat to fake presence elsewhere

	PvP Builds May Specialize In :
* Silent assassin movement
* Sound bait tactics
* Noise suppression gear
* Wind echo tracking

---

