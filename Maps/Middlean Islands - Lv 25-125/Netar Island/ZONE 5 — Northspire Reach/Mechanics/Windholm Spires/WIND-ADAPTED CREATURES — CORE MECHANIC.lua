			WIND-ADAPTED CREATURES — CORE MECHANIC
	
	"They Fly Better Than You Survive"

	In Windholm Spires, creatures :
* Treat wind as terrain
* Treat air as roads
* Treat falling players as prey

	Players are outsiders.
	Enemies are native aerial predators.

---

		DESIGN PHILOSOPHY

	These enemies do not "ignore physics" — they master it.

	They :
✅ Ride gusts with no stamina cost
✅ Turn turbulence into speed
✅ Use wind to reposition, escape, or attack
❌ Are rarely grounded
❌ Are punished less by falling

---

		GLOBAL SYSTEM — WIND-BOUND AI BEHAVIOR

		1. Gust Riding

	Creatures can :
* Drift on strong wind corridors
* Gain burst speed when entering wind tunnels
* Chain gusts to reposition mid-combat

	Result : Enemies feel mobile, elusive, and airborne-smart.

---

		2. Wind-Boosted Attacks

	Certain attacks scale with wind intensity :

| Wind Level | Enemy Bonus                |
| ---------- | -------------------------- |
| Calm       | Normal                     |
| Strong     | +Knockback                 |
| Gale       | +Damage +Range             |
| Storm      | +Multi-hit / Chain attacks |

	This makes weather a combat modifier.

---

		3. Anti-Glider Tactics (Core Pressure System)

	Enemies deliberately target glider stability :

	They can :
* Slam players mid-air
* Tear glider straps
* Force glider stall
* Create destabilizing turbulence bursts

	Player Effects :
* Forced landing
* Spin-out
* Emergency recovery events
* Durability loss on glider

---

		SPECIES-SPECIFIC WIND COMBAT BEHAVIOR

---

	Sky-Stalkers — Wind-Parkour Ambushers

* Role : Agile ledge predators
* Behavior :
	* Leap across spires using tail-wind boosts
	* Predict player landing paths	
	* Chain jump → slash → retreat
	* Knock players sideways into falls

	Special Ability :

* "Wind-Step Leap" : They jump farther when jumping into wind.

	Counterplay :
* Fight near walls
* Disrupt their jump timing
* Avoid open ledges

---

	Whistling Drakes — Sound-Guided Air Hunters

* Role : Apex mid-air predator
* Behavior :
	* Patrol wind tunnels
	* Dive toward loud players
	* Glide silently upwind
	* Exhale pressure shockwaves

	Wind Interaction :
* Faster when flying into gusts
* Can hover in turbulence
* Gains attack bonus during storms

	Special Ability :
* "Sonic Wind Burst"

	Creates a cone that :
* Disorients
* Pushes
* Drains glider endurance

---

	Spire Leeches — Environmental Parasites

* Role : Stamina control predators
* Behavior :
	* Cling to vertical rock
	* Drop onto passing climbers
	* Feed while player climbs or glides

	Wind Use :
* Release and reattach using drafts
* Drift to new spires
* Hide in wind-shadow cracks

	Special Effect :

* "Grip Drain" 

	Reduces :
* Stamina regen
* Climb speed
* Glider control

---

	Aerial Predators — Fall Executioners

* Role : Punish falling players
* Behavior :
	* Detect falling momentum
	* Dive faster than gravity
	* Strike mid-fall
	* Carry prey toward hazards

	Special Ability :

* "Mid-Air Tackle"

	If player is falling :
* Predator locks on
* Deals bonus damage
* Can spin player mid-air

---

		ADVANCED WIND COMBAT BEHAVIORS

---

	1. Player-Pushing Attacks

	Enemies use wind to :
* Push players off ledges
* Redirect glider paths
* Force panic landings

	Feels like fighting the storm itself.

---

	2. Wind Ambush Zones

	Some enemies :
* Hide in wind-blind spots
* Attack only when player crosses a gust line
* Use sudden pressure shifts to stun

---

	3. Wind Recovery Advantage

	If knocked off :
* Enemies recover mid-air
* Players must panic-glide

	This reinforces predator superiority.

---

		SKILL GAP INTENT

	New Players :
* Fall often
* Lose gliders
* Panic during wind shifts

	Veterans :
* Ride gusts like enemies
* Predict wind cycles
* Counter-push predators
* Use wind to bait enemy mistakes

---

		PLAYER COUNTER-SYSTEMS (FAIRNESS LAYER)

	Players eventually gain tools to compete :

	Anti-Wind Gear :
* Stabilized glider frames
* Wind-anchoring boots
* Sound-dampening cloaks

	Combat Skills :
* Gust Counter Slash
* Mid-Air Dodge
* Emergency Glide Recovery

	Environmental Tactics :
* Lure enemies into turbulence
* Fight in wind-shadow pockets
* Reverse gust positioning

---

		ECOLOGY TIE-IN — WHY THEY’RE STRONGER HERE

	These creatures evolved to :
* Hunt falling prey
* Feed on aerial creatures
* Live where walking is impossible

	They are not magical — they are perfectly adapted predators.

	The player is the anomaly.

---

		GAMEPLAY PAYOFF

	This mechanic creates :

✅ Combat that feels environmentally intelligent
✅ Monsters that feel native and superior
✅ Aerial combat identity unique to Windholm
✅ High skill expression
✅ Memorable “I survived the wind predators” moments
✅ A reason to master gliders and positioning

