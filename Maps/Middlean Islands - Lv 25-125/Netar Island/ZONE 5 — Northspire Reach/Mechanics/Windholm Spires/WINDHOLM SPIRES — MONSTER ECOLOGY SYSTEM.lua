			WINDHOLM SPIRES — MONSTER ECOLOGY SYSTEM
		A Vertical, Wind-Driven Predator Web

	Windholm is not a normal biome.
	It is a wind-powered hunting ground, where :
* Sound replaces scent
* Height replaces territory
* Wind replaces ground travel
* Mistakes fall downward into death

	This creates a high-skill, high-danger predator ecosystem.

---

		1. ECOLOGICAL TIERS (FOOD WEB STRUCTURE)

	Tier 1 — Micro-Life (Foundation Layer)

Small lifeforms that survive on wind residue, spores, and mineral dust.

	Wind Spores : 
* Float between spires
* Feed on magical wind residue
* Sustain insect and parasite species

	Dust Mites : 
* Live in rock cracks
* Feed on spores
* Attract leeches and small predators

---

	Tier 2 — Parasites & Rock Clingers

	Spire Leeches

* Role : Stamina parasites
	* Feed on vibration and body heat
	* Prefer climbers
	* Keep populations of climbers and animals weak

	Wind-Ticks 
* Attach to gliders or cloaks
* Drain stamina slowly
* Spread sickness if ignored

---

	Tier 3 — Small Agile Hunters

	Sky-Stalkers

* Role : Precision ambushers
	* Hunt smaller creatures across ledges
	* Track sound rhythms
	* Cull weak or noisy prey

	Ledge Runners : 
* Fast scavengers
* Steal prey remains
* Flee at loud noise

---

	Tier 4 — Aerial Predators

	Aerial Predators

* Role : Falling-prey specialists
	* Hunt falling targets
	* Keep population of reckless climbers in check
	* Feed on glider failures

	Whistling Drakes

* Role : Apex sound-hunters
	* Track echoes across corridors
	* Kill loud prey
	* Dominate mid-air territory

---

	Tier 5 — Pack Hunters

	Wind Howlers

* Role : Coordinated pursuit predators
	* Hunt in packs
	* Respond to repeated noise
	* Push prey into falls

---

	Tier 6 — Supernatural Sound Entities

	Echo Wisps

* Role : Sound-consuming lifeforms
	* Feed on lingering noise
	* Multiply when zones are chaotic
	* Keep sound pollution in balance

---

	Tier 7 — Territorial Apex Entities

	The Spire Sovereigns 
	
	Ancient wind-formed beings : 
* Control local wind patterns
* Punish overhunting or loud dominance
* Act as ecosystem balancers

---

		2. TERRITORY DISTRIBUTION (VERTICAL ECOLOGY)

| Height Level   | Dominant Species         | Role            |
| -------------- | ------------------------ | --------------- |
| Lower spires   | Leeches, Ledge Runners   | Scavengers      |
| Mid spires     | Sky-Stalkers, Howlers    | Active hunters  |
| Upper spires   | Drakes, Aerial Predators | Air dominance   |
| Wind corridors | Echo Wisps               | Sound control   |
| Storm peaks    | Spire Sovereigns         | Apex regulators |

---

		3. SOUND-BASED PREDATOR RELATIONSHIPS

	Noise Attracts :
* Whistling Drakes
* Wind Howlers
* Echo Wisps

	Silence Favors :
* Sky-Stalkers (stealth ambush)
* Spire Leeches (undetected attachment)

	Too Much Noise Causes :
* Predator overpopulation
* Zone danger escalation
* Rare apex spawns
* Temporary ecosystem hostility

---

		4. WIND-DEPENDENT HUNTING BEHAVIOR

	During Calm Winds :
* Sky-Stalkers dominate
* Sound travels shorter distances
* Stealth is easier

	During Strong Winds :
* Drakes gain range
* Howlers hunt wider
* Falling risk increases

	During Storms :
* Echo Wisps multiply
* Apex entities awaken
* Predators become hyper-aggressive

---

		5. FEEDING & CULLING BALANCE

	Each species plays a population control role :

| Predator     | Keeps In Check  |
| ------------ | --------------- |
| Leeches      | Climber stamina |
| Sky-Stalkers | Small prey      |
| Howlers      | Medium prey     |
| Drakes       | Large prey      |
| Sovereigns   | Apex imbalance  |

	If players overhunt one species :
* Another species grows dangerously
* Wind patterns destabilize
* Zone difficulty changes dynamically

---

		6. PLAYER IMPACT ON ECOLOGY (DYNAMIC WORLD SYSTEM)

	Players can influence the ecosystem :

	Killing Too Many Drakes →
* Sky-Stalkers overpopulate
* More ambush encounters
* Stealth becomes harder

	Excessive Loud Combat →
* Echo Wisps infest corridors
* Sound penalties increase

	Ignoring Howlers →
* Pack territories expand
* Travel routes become unsafe

	Rare Event: Predator Collapse →
Triggers a Zone Ecological Crisis Event

---

		7. NESTING, ROOSTING & LAIRS

	Nest Types :
* Drake wind-perches
* Howler echo dens
* Sky-Stalker cliff burrows
* Wisp sound vortex pools

	Destroying nests :
* Reduces spawn rate
* Triggers retaliation hunts
* Alters predator travel routes

---

		8. BREEDING SEASONS & MIGRATION

	Seasonal Behavior :

* Spring: Drake egg-laying
* Summer: Howler pack expansion
* Autumn: Wisp multiplication
* Winter: Sovereign dormancy

	Migration :
Some predators shift spire clusters, changing danger maps over time.

---

		9. SCAVENGERS, CLEANUP & RECYCLING

	Corpse Cycle :
* Ledge Runners consume remains
* Wind Spores grow from decay
* Wisps absorb leftover echoes
* Dust mites recycle minerals

	Nothing wastes — everything feeds the wind.

---

		10. LORE FOUNDATION — WHY THIS ECOSYSTEM EXISTS

	Windholm is :
* A natural wind organ
* A magical resonance field
* A place where sound becomes energy

	Creatures evolved to :
* Hear better than see
* Fly better than walk
* Hunt smarter than stronger

	The ecosystem exists because wind itself is alive here.

