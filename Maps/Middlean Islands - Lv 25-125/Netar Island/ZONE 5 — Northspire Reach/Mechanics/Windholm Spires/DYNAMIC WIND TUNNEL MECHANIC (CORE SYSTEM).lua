			DYNAMIC WIND TUNNEL MECHANIC (CORE SYSTEM)

	Wind in Windholm Spires is not atmosphere — it is navigable terrain.
	Wind tunnels form semi-stable air corridors between spires, behaving like invisible rivers of force.

	They can :
* Carry players
* Deflect them
* Boost them
* Punish mistimed movement

	This makes air a controllable but dangerous medium.

---

		1. STRUCTURE OF A WIND TUNNEL

	Each tunnel has properties:

	Core Attributes : 
* Direction Vector (push angle)
* Strength (force intensity)
* Stability (how predictable it is)
* Width (safe corridor size)
* Turbulence (random deviation chance)
* Cycle Timer (how often it shifts)

---

		2. WIND SHIFT CYCLE SYSTEM

	Wind corridors shift direction on timed cycles.

	Example States :

| Phase           | Behavior            |
| --------------- | ------------------- |
| Calm Flow       | Stable travel       |
| Rising Pressure | Increasing force    |
| Cross-Shear     | Sideways drift      |
| Turbulent Burst | Erratic pushes      |
| Reset           | New direction forms |

	Gameplay Effect :
* Players learn wind rhythms
* Skilled players time crossings
* Rushing = higher fall risk

	This turns traversal into timing-based mastery.

---

		3. INTERACTION WITH GLIDING (ADVANCED MOVEMENT)

	Wind tunnels directly affect glider physics.

	Boost Corridors : 
* Increase glide speed
* Extend glide endurance
* Allow long-distance aerial travel

	Drift Corridors : 
* Push players sideways mid-air
* Require manual steering correction

	Slam Corridors : 
* Forcefully throw players toward rock faces
* Can :
	* Damage gliders
	* Knock players down
	* Break concentration
	* Cause free-fall if stamina is low

	High-Skill Chain Mechanic

	Players can :
* Enter Tunnel A → gain speed
* Redirect into Tunnel B → gain altitude
* Exit into Tunnel C → land on hidden ledge

	This creates intentional route mastery.

---

		4. ON-FOOT WIND INTERACTION

	Wind tunnels also affect grounded players.

	Light Wind Corridors : 
* Increase sprint speed
* Reduce stamina drain

	Heavy Wind Corridors : 
* Push player sideways
* Can interrupt attacks
* Increase stamina usage to resist

	Extreme Gust Corridors : 
* Force knockback
* Trigger balance checks
* Push players off ledges if careless

	Equipment Influence : 
* Heavy armor = harder to push
* Light armor = easier to throw
* Cloaks / capes increase wind drag
* Special boots stabilize footing

---

		5. SKILL EXPRESSION & PLAYER MASTERY

	Beginner Experience : 
* Gets pushed
* Falls
* Misses jumps
* Learns by failure

	Experienced Player : 
* Reads wind patterns visually
* Anticipates shift cycles
* Chains tunnels efficiently
* Uses wind to skip terrain

	Elite Mastery : 
* Mid-air redirect timing
* Reverse wind riding
* Secret aerial routes
* Combat positioning via airflow

---

		6. VISUAL & SENSORY READABILITY (REALISTIC FEEL)

	Even if tunnels are invisible, players can read them :

	Visual Cues : 
* Mist ribbons flowing in a direction
* Dust streams bending
* Floating particles drifting in curves
* Cloth / banners reacting to airflow

	Audio Cues : 
* Pitch changes based on strength
* Whistling tone shifts before direction change
* Sudden silence before turbulence

	This keeps the system learnable and fair.

---

		7. COMBAT IN WIND TUNNELS

	Wind becomes a combat variable.

	Ranged Combat : 
* Arrows curve with wind
* Projectiles drift off target
* Skilled players lead shots using airflow

	Melee Combat : 
* Knockback becomes deadlier near edges
* Heavy attacks can overpower wind drift
* Dodges behave differently in strong currents

	PvP Tactics : 
* Push enemies into slam corridors
* Fight uphill into headwind to exhaust enemies
* Use side-wind corridors for strafing

---

		8. ENEMY & AI UTILIZATION

	Enemies can exploit wind :

	Examples :
* Creatures that ride gusts to reposition
* Predators that knock players into tunnels
* Wind-element mobs that reverse corridor flow
* Sniper enemies using wind for projectile drift mastery

	This makes the zone feel alive and hostile.

---

		9. PUZZLE & GATE DESIGN USING WIND

	Wind tunnels become natural locks.

	Puzzle Ideas :
* Wait for correct wind phase to cross
* Redirect airflow using rotating spire fins
* Disable turbulence generators
* Add weight objects to stabilize a tunnel

	This keeps puzzles environment-based, not UI-based.

---

		10. SECRET ROUTES & HIGH-SKILL REWARDS

	Only advanced players can reach :
* Hidden spire peaks
* Aerial ruins
* Secret wind sanctums
* Rare resource nests

	Reward :
* Unique loot
* Runes
* Wind-based upgrades
* Lore fragments

	Exploration becomes prestige content.

