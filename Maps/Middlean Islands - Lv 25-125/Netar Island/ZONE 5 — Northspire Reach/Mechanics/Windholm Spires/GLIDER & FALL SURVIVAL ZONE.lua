			GLIDER & FALL SURVIVAL ZONE

	Windholm Spires — The Glider Skill Trial Zone
	This zone becomes the official mastery proving ground for gliding, aerial control, and fall survival.

	It is designed so :
* Inexperienced players struggle
* Veterans flow
* Masters dominate the air

---

		1. CORE CONCEPT — THE SKY IS NOT SAFE

	Gliding here is not passive travel — it is:
* Risky
* Resource-managed
* Reactive
* Skill-based

	The air behaves like terrain :
* Some wind carries you
* Some fights you
* Some betrays you

---

		2. WIND DRAFT TYPES (AERIAL TERRAIN)

	Boost Drafts — Endurance Refill Zones

	Invisible air lanes that :
* Restore glider stamina over time
* Encourage chaining long-distance flights
* Reward smooth, controlled movement

	Skilled players plan routes through these.

---

	Turbulence Fields — Endurance Drain Zones

	Chaotic air pockets that :
* Rapidly drain endurance
* Shake glider stability
* Reduce steering precision

	Mistakes here = forced descent or crash.

---

	Updraft Spires — Vertical Lift Pillars

	Thermal columns rising along spires :
* Lift gliders upward mid-flight
* Allow altitude recovery
* Enable aerial re-routing

	Masters can :
* Climb vertically
* Reset failed runs
* Reach secret sky paths

---

	Stall Gusts — Glider Killers

	Sudden wind bursts that :
* Stall gliders mid-air
* Kill forward momentum
* Cause spinouts or freefall

	Requires :
* Fast correction
* Emergency dive recovery
* Or crash survival

---

		3. GLIDER ENDURANCE SYSTEM — HIGH TENSION

	Gliders consume stamina over time.

	Endurance drains faster when :
* Fighting headwinds
* Flying through turbulence
* Carrying heavy gear
* Making sharp directional turns

	Endurance recovers when :
* Riding stable drafts
* Entering updraft columns
* Landing safely on ledges
* Using rare recovery tools

	This turns gliding into resource strategy, not a glide button.

---

		4. SKILL EXPRESSION TIERS

	Beginner : 
* Burns endurance early
* Falls short of targets
* Crashes frequently
* Learns draft reading

	Experienced : 
* Chains wind boosts
* Avoids turbulence
* Uses updrafts to extend flight
* Reaches mid-tier routes

	Master : 
* Surfs wind sheets
* Manipulates stall gusts
* Performs aerial recovery loops
* Reaches hidden apex routes
* Lands on micro-ledges mid-flight

	Air becomes a playground.

---

		5. FALL SURVIVAL MECHANIC (FAILURE = GAMEPLAY)

	Falling doesn’t mean instant death — it becomes a skill event.

	Players can attempt :
* Emergency glide deploy
* Wind-grab recovery
* Cliff latch mid-fall
* Roll landing to reduce damage

	Failure outcomes :
* Injury
* Equipment durability loss
* Temporary debuffs
* Long fall death

	Mistakes teach — not just punish.

---

		6. MID-AIR INTERACTIONS

	Aerial Precision Landing

	Land on :
* Thin ledges
* Moving stone plates
* Wind-floating debris

	Mid-Air Anchors

	Ancient spikes or ropes let players :
* Halt falls
* Reset glide
* Swing to alternate routes

	Draft Jumping : 
Leap from wind tunnel → glide → updraft → ledge → glide again
Creates parkour flight chains.

---

		7. AERIAL COMBAT & THREATS

	Enemies exploit gliding players.

	Examples :
* Sky Drakes that force evasive dives
* Wind Wisps that drain endurance
* Cliff Snipers knocking players mid-flight
* Storm Spirits that alter wind patterns

	PvP Layer :
* Mid-air knockdowns
* Draft interception
* Glide ambushes
* Ledge traps targeting falling enemies

---

		8. GLIDER TRIALS & CHALLENGES

