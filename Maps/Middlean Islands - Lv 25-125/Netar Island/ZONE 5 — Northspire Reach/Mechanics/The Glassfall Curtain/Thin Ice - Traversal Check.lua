			Thin Ice / Traversal Check

		1. Core Behavior (Base Mechanics) 

* Certain sections of the ice curtain or pools cannot support full player weight.
* Players must judge safe paths across the ice.
* Risk/Reward : Some weak tiles hide shortcuts, loot caches, or hidden alcoves behind the ice.

---

		2. LCWM Hidden Mechanics

* Some “weak” tiles appear solid only from certain angles or when viewed in certain conditions (sunlight angle, shadow alignment, or frost patterns).
* Conditional mechanics:
	* Tiles may shift stability depending on prior player actions, e.g., triggering minor tremors or ice shard falls elsewhere.
	* Some tiles are only solid if a sequence of ice shards has fallen, creating a temporary pathway.

---

		3. Traversal Depth

* Players must balance speed vs. caution :
	* Sprinting or heavy load can break tiles prematurely.
	* Deliberate walking or positioning allows safer crossing.
* Optional: Sliding mechanic — slipping on thin ice can carry players to hidden locations or secret loot, but risks falling into deeper hazard zones.

---

		4. Environmental Interaction

* Water underneath frozen pools may contain:
	* Rare ice-locked flora
	* Environmental hazards (sharp ice shards, hidden crevices)
	* LCWM triggers: player can “see” shimmer through ice revealing secret passage if lighting or frost conditions align

---

		5. Potential Integrations (if fit)

* Glider : Can be used to bypass dangerous thin ice sections or glide to hidden alcoves
* Frost : Frost accumulation can make ice tiles slipperier, adding traversal challenge or revealing hidden paths
* Wind : Strong gusts could tip balance if the player is stationary on a thin tile

