			Frost Integration — Localized Accumulation & LCWM Effects

		1. Core Frost Behavior

* The ice curtain constantly sprays fine frost particles, creating localized frost accumulation on:
	* Ledges
	* Pools behind the curtain
	* Thin ice surfaces and rock outcrops

* Environmental Effects :
	* Surfaces become slippery, affecting traversal and combat stability
	* Frost can obscure small details, hiding LCWM secrets, pathways, or loot

---

		2. Player Exposure Mechanics

* Frostbite Debuff :
	* Prolonged exposure applies debuff:
		* Reduced movement speed
		* Slower stamina recovery
		* Mild visual distortion (frost on screen / icy overlay)
	* Debuff severity scales with proximity to spray, cumulative exposure, and environmental factors like wind intensity
* Optional Challenge : Players may traverse frost-heavy areas without protection for additional rewards (hidden caches, LCWM interactions)

---

		3. Latent Conditional Mechanics (LCWM Style)

* Hidden Flora / Resources :
	* Everwake Bloom appears only:
		1. On frost-laden ledges
		2. While player has Frostbite debuff active
		3. During specific time-of-day or sunlight conditions
	* Collecting bloom can unlock:
		* Crafting recipes for frost-infused consumables
		* Quest or lore fragments

* Ice Crystal Resonance :
	* Frost accumulation subtly changes sound resonance of ice shards or dripping water
	* Certain environmental cues only activate when Frostbite is active, unlocking hidden alcoves or puzzle triggers

* Frost Chain Reactions :
	* Prolonged accumulation on weak ice tiles can increase likelihood of thin ice collapse
	* Cascading environmental effects may reveal hidden resources or paths (LCWM)

---

		4. Environmental & Traversal Interactions

* Glider / Traversal Use :
	* Frost-heavy ledges provide launch points for Glider jumps to hidden alcoves
	* Slippery frost can assist controlled slides, revealing secret areas below
* Wind Interaction :
	* Gusts can disperse or concentrate frost patches, creating dynamic traversal challenges or exposing hidden tiles
* Ice Shard Synergy :
	* Frost accumulation increases ice shard fall frequency or slipperiness on impacted ledges

---

		5. Optional Mini-Event Integration

* Frost Bloom Event :
	* Rare event where Frostbite + correct light/wind alignment triggers multiple Everwake Blooms to bloom simultaneously
	* Can attract hidden creatures (ice sprites, frost spirits) or reveal special resource cache

* Hidden Path Activation :
	* Frost accumulation + ice shard sequence may unlock a secret alcove behind the curtain, connecting to later LCWM chains

