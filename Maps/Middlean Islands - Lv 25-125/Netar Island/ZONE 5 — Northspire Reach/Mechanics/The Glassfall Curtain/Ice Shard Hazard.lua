			Ice Shard Hazard

		1. Core Behavior (Base Mechanics)

* Randomized falling ice shards triggered by environmental instability in the curtain.
	* Proximity scaling : Shards fall more frequently or larger if the player is closer to the curtain’s wall or ledge.
	* Damage : Small shards = minor physical; larger chunks = moderate damage + knockback.
	* Audio cue : Subtle cracking sound before fall; directional audio lets perceptive players dodge.

---

		2. Player Interaction — Extended with Glider

* Glider Utility : Players equipped with the Glider can launch off ledges or thin ice platforms to :
	* Avoid shard impact zones
	* Glide past falling debris for speedruns or exploration
	* Access secret alcoves behind or below the curtain (LCWM trigger)

* Glider Skill Integration :
	* Certain shard patterns require timed gliding maneuvers to safely traverse.
	* Glider boosts or maneuvers are influenced by wind gust mechanics, making each approach dynamic.

---

		3. Environmental Dynamics

* Shard Chain Reactions :
	* Larger shards hitting lower ice layers can trigger secondary debris, creating cascading hazards.
	* Conditional: only triggers if player triggers nearby tremor or ice vibration (LCWM latent event).

* Wind Interaction :
	* Gusts can deflect smaller shards, altering trajectories.
	* Players can intentionally use wind currents with Glider to manipulate shard flow (creative gameplay).

* Frost Accumulation :
	* Frequent shard impacts generate localized frost patches, adding slippery zones.
	* Some patches are hidden paths to rare flora or items, revealed only when frost spreads far enough (LCWM conditional).

---

		4. Mini-Events / Rare Triggers

* Shard Avalanche Event : Rare large-scale fall event triggered by :
	1. Player weight on multiple thin ice points
	2. Nearby frostquake (Shattercrag Basin trigger)
	3. Wind intensity threshold

	Outcome :
* Creates a temporary hazard zone
* Reveals hidden POI behind the curtain
* Rare ice shard artifact drop usable in advanced crafting

* Ice Sprite Encounter (Hidden Creature) :
	* Only spawns during shard avalanches with specific light conditions
	* Players can Glider-dodge or chase to interact, unlocking temporary buffs or rare crafting materials

---

		5. Latent Conditional Mechanics (LCWM Integration)

* Hidden ice shards only fall if :
	* Frostquake in Shattercrag Basin has occurred
	* Player’s proximity triggers ice vibration sensors (unseen environmental markers)
	* Sunlight hits curtain at correct angle (daytime window)

* Outcomes unlocked via LCWM :
	* Access to hidden cave behind curtain
	* Unlock secret shard recipe for crafting frost-based weapons or tools
	* Trigger lore dialogue or environmental storytelling

