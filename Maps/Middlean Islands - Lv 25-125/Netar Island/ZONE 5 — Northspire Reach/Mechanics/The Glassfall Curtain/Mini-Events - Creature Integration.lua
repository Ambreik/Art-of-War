			Mini-Events - Creature Integration: The Glassfall Curtain

		1. Frost Lurker Ambush

	Behavior :
* Predatory creature hides behind or within the ice curtain, using natural cover and ice shards for stealth attacks.
* Ambush Mechanics :
	* Detects player presence via footstep sound, frost patterns, or ice vibration.
	* Uses ice shard impacts to distract or push players into hazards.
	* Conditional stealth zones: only appears if player enters certain weak ice or frost-laden areas.

	Environmental Integration :
* Ambush areas overlap with Ice Shard Hazard zones, making the encounter dynamic rather than scripted.
* Wind gusts may carry creature sounds, creating a player skill check : listen, avoid, or prepare traps.
* Frostbite exposure can slow player reaction, increasing ambush risk.

	Reward / LCWM :
* Rare drops: Frost-infused crafting materials, shard-tipped claws, or hidden lore fragments.
* Can trigger secret path unlocking if the player defeats it without being hit by ambient hazards.

---

		2. Wind Spirit Spawn (Rare)

	Behavior :
* Elemental spirit appears only during high wind + specific sun angle, creating a conditional, emergent encounter.
* Interaction Mechanics :
	* Player must reflect sunlight off ice shards or ice prism objects to trap / interact with the Wind Spirit.
	* Spirit can manipulate environmental hazards : increase gust intensity, redirect ice shards, or lift debris to reveal secrets.

	Environmental Integration :
* Appears in updraft corridors or wind funnel zones near the curtain.
* Optional risk/reward: players can use gusts + glider to reach the Wind Spirit’s hidden platform.
* Can activate LCWM environmental puzzle chains, unlocking hidden resource nodes or lore objects.

	Reward / LCWM :
* Rare crafting drops: Wind-infused shards, elemental reagents
* Environmental change: creates temporary wind currents or opens secret alcoves
* Optional bonus: triggers hidden sun-reflection puzzle in nearby alcove

---

		3. Integration with Other Systems

* Ice Shard Hazard : Both creatures may interact with shard patterns; the Lurker may use shards for stealth or knockback.
* Frost Integration : Frostbite slows player reaction, increasing challenge for both encounters.
* Wind Integration : Gusts may aid or hinder the player when navigating the Wind Spirit puzzle.
* Glider : Can be used to safely reach or evade creature spawn zones.

---

		4. Optional LCWM Triggers / Secrets

* Hidden Spawn Variants :
	* Frost Lurker may have rare albino variant dropping unique crafting materials.
	* Wind Spirit may split into multiple minor wind entities if the player completes a light-reflection chain.

* Environmental Feedback :
	* Ice shard vibration, frost accumulation, and gust sounds serve as clues to creature presence.
	* Observant players can avoid combat entirely or manipulate hazards to defeat the creatures indirectly.

