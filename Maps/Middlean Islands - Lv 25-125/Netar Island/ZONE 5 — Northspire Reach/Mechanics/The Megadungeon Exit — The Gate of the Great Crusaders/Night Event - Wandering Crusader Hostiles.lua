			Night Event: Wandering Crusader Hostiles

	Name : "The Relentless Crusaders"

---

	Spawn Conditions : 

* Time: Night
* Area: Entire Zone 5, with increased density near the Gate
* Spawn in groups, not solo

	Behavior : 
* Aggressive on sight
* Patrol routes across the zone
* Do not despawn on player death if player respawns locally

	Special Behavior: Anti-Camp Mechanic

If a player:
* Dies to these crusaders
	AND
* Uses "Restart Here"
	Then:
* The same crusader group remains nearby
* They will immediately re-engage the respawned player

	Player Counterplay
	
To escape this loop:
* Player must use "Restart in Town"
* This forces respawn in nearest human settlement instead

	Weakness Mechanic : 

Crusaders are:
* Highly sensitive to fire and fire-light sources
	* Torches
	* Fire spells
	* Burning consumables
* Fire causes:
	* Reduced aggression
	* Temporary stagger
	* Possible morale-like behavior (hesitation or partial retreat)

	Design Purpose : 
* Enforces danger of night travel
* Adds psychological pressure and risk
* Prevents trivial death recovery
* Encourages planning, grouping, or retreat

