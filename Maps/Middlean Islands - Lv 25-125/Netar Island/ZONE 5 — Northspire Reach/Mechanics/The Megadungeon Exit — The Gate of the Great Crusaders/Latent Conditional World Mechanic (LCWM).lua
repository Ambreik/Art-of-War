			Latent Conditional World Mechanic (LCWM)

	Name : "The Returning Mechanism" – Dungeon Completion Sync

	Description : 

The Gate contains a hidden, dormant mechanical structure that reacts only when players inside the "Tomb of the Great" Crusaders reach its final stage and exit.
Players outside the dungeon, near the Gate, may witness the activation sequence before the exiting players appear, serving as a powerful world-connection moment.

---

		Trigger Conditions

	This LCWM activates when:
* A dungeon group completes the Tomb internally
	AND
* The exit sequence is initiated inside the dungeon

	World Effects (External, Visible to Open World Players)

* Visual :
	* Frost-dark sigils briefly glow
	* Iron bands rotate or shift subtly
	* Stone anchors tremble or realign
	* Snow slides from cracks in the masonry
* Audio :
	* Low grinding stone sound
	* Deep resonant echo like a sealed chamber unlocking
	* Faint choral undertone or metallic reverberation
* Environmental Response :
	* Wind shifts direction toward the Gate
	* Snow particles swirl inward momentarily

	Outcome : 
* A temporary portal-like emergence effect appears at the gate seam
* Dungeon players exit into the world through the Gate
* After exit, the structure returns to its dormant, sealed state

	Design Intent : 
* Creates a shared-world moment
* Connects instanced dungeon content with the open world
* Reinforces the Gate as a monumental, living structure
* Encourages social curiosity and emergent player gatherings

