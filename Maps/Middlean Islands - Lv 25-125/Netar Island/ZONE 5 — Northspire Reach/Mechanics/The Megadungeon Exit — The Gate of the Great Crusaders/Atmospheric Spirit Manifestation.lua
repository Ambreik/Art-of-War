			Atmospheric Spirit Manifestation (Pure Visual & Sound)

	Name : "Echoes of the Crusaders" – Night Storm Phenomenon

---

	Conditions : 

	Appears only when:
* Time: Night
* Weather: Storm or heavy snowfall

	Effects : 

* Semi-transparent spectral figures of Crusaders appear near:
	* The Gate steps
	* Broken barricades
	* Siege remains
* They:
	* Walk silently
	* Stand in formation
	* Occasionally look toward the Gate
* Cannot be interacted with, targeted, or damaged
* Fade in and out naturally with storm intensity

	Audio Layer : 
* Distant marching sounds
* Faint whispers
* Occasional metallic clinks (armor memory)

	Design Purpose : 
* Represents Crusaders before corruption
* Reinforces the tragedy of their fate
* Adds narrative depth without turning them into enemies
* Serves as a pure world storytelling device

