			Core Settlement Mechanics (Always Active Systems)

---

		1.1 Wind-Shelter & Exposure System

	*(Environmental Combat & Navigation Layer)*

	"Stormreach Watch" is located on a wind-sheltered shelf, but only partially.
	The village is mechanically divided into "Wind Zones" based on terrain, structures, and elevation.

	Wind Zone Types

	A. Sheltered Zones

	Examples:
* Behind bulwarks
* Between tightly packed buildings
* Inside caverns and narrow alleys

	Effects :
* Reduced frostbite accumulation
* Reduced stamina drain
* No wind-based displacement
* Stable casting
* Ranged accuracy unaffected

	Design Role:
* Safe staging areas
* Recovery and regrouping points
* Natural fallback positions during storms or combat

---

	B. Exposed Zones

	Examples:
* Terraces
* Watchtower roof
* Lookout perches
* Open plaza edges

	Effects :
* Increased wind pressure
* Light displacement during strong gusts
* Increased stamina drain
* Frostbite accumulates faster
* Ranged attacks lose accuracy
* Cast times slightly increased
* Interrupt chance increased during storms

	Design Role:
* High-risk / high-reward positions
* Strong vantage points that require skill and preparation
* Makes terrain tactically meaningful even inside a village

---

	Dynamic Wind Behavior

	Wind is not static.

	Wind Strength States :
* Calm
* Strong
* Storm-grade

	Each state modifies:
* Push force
* Projectile deviation
* Casting stability
* Fire behavior (flickering, extinguishing, spreading)

---

	Combat & Environmental Interactions

	Wind can:
* Push players and enemies slightly off position
* Disrupt channeling abilities
* Amplify knockback effects
* Carry sound further (stealth implications later)

	This system is:
* Persistent
* Scales later across the whole island
* Starts being introduced mechanically here in Stormreach Watch

	Stormreach becomes the player’s first true "wind-combat literacy" zone.

---

		1.2 Storage Caverns – Shared Utility Nodes

	*(Multiplayer Logistics & Defense Integration)*

	The storage caverns carved into the slope are shared, non-instanced world objects, usable by multiple players simultaneously.
	They are a hybrid system between:
* World stash
* Defense logistics hub
* Crafting & survival node

	Core Properties : 
* Public access
* No personal storage here
* No theft between players
* All deposited materials become part of "Village Supply Pool"

---

	Player Interactions

	Players can:

* Deposit Materials
	* Examples:
		* Wood
		* Iron
		* Oil
		* Cloth
		* Alchemical reagents

	These are used for:
* Bulwark repairs
* Beacon refueling
* Emergency fortifications
* NPC defender equipment scaling

	Withdraw Limited Supplies

	Only during:
* Invasions
* Storm emergencies
* Special events

	Withdrawals are:
* Restricted
* Shared
* Subject to cooldowns and contribution priority

---

	Strategic Impact

	Village supply levels directly affect:
* How long Stormreach can hold during invasions
* Whether certain defensive options are available
* How fast structures can be repaired
* Whether emergency measures can be activated

	This makes:
* Crafters
* Gatherers
* Non-combat players
  equally important to village survival.

---

	Storage Caverns During Attacks

	If storage caverns are:

* Looted or destroyed
	Then:
* Defense options are reduced
* Repairs become slower
* Morale drops
* Next invasions become harder

	They are a primary strategic target for attackers.

---

	1.3 Lookout Perches & Watchtower

	*(Early Warning & Tactical Awareness System)*

	Stormreach Watch contains:
* A central watchtower (with permanent NPC archer)
* Several lookout perches carved into the rock

	These form a persistent detection and awareness network, not a toggleable device.

---

	How the System Works

	The watchtower and perches are:
* Always active
* Always functional
* Never disabled by player presence or absence

	However, players can enhance their effectiveness.

---

	Player Interaction Layer

	Players can:

* Scout From Perches
	*By standing on lookout points, players:
		* Extend enemy detection range
		* Reveal incoming patrols earlier
		* Identify invasion approach vectors

	Mark Threats

	From perches or tower:
* Players can manually mark approaching groups
* Marks propagate to:
	* Defenders
	* UI indicators
	* Defense AI prioritization

---

	Mechanical Impact

	Early detection results in:
* More prep time before invasions
* Slower enemy advance (defenders reposition sooner)
* Lower chance of surprise breaches
* Increased reward multipliers for defenders

	Failure to use perches:
* Reduces warning time
* Increases breach probability
* Makes invasions harder

---

	Integration With NPC Archer

	Since the archer NPC:
* Is always present
* Always active

	They provide:
* Baseline detection and response

	Players do not replace this system — they augment it.

	This ensures:
* Stormreach never feels dead
* But always feels better when players engage with it

