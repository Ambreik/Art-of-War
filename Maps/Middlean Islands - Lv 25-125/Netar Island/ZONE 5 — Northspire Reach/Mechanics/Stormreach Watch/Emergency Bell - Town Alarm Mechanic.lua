			Emergency Bell / Town Alarm Mechanic

	Structure :
* Bell towers located on upper terraces of Sunveil Market Town
* Towers are reachable by stairs or ladders, providing high visibility of the town
* Bells are large, functional, and prominently visible, signaling their purpose

---

	Trigger / Use :
* Player can manually ring the bell by interacting with the tower lever or rope
* Activates town-wide alert state, affecting:
	* NPC guards (changes patrol patterns, increases vigilance)
	* NPC civilians (take cover, flee, or gather in safe zones)
	* Enemy AI (if invasion event is active, they react more aggressively or redirect)
* Intended use : defense, warning of raids, or coordinating town-wide tactics

---

	Effects :
	
1. Guard & NPC AI :
	* Guards enter Alert State (patrols tighten, focus on strategic points)
	* Temporary buff to guard defenses :
		* +15% accuracy
		* +10% damage resistance
		* Extended patrol coverage and faster reaction to enemy proximity
	* Civilians move to safe zones, reducing risk of accidental harm

2. Enemy AI Interaction :
	* If invasion is in progress, enemies detect bell noise and may:
		* Change patrol paths
		* Prioritize attacking central town zones
		* Increase alertness temporarily

---

	Hidden / Exploit Use :
* Player can ring the bell without enemy presence to simulate a “fake emergency”
* Consequence for trickery:
	* Player is escorted out of town by guards
	* Town entrance blocked for 1 hour (invisible wall)
	* All flying paths blocked by Flying Masters NPCs for this location
	* No REP lost, quests remain active, no other penalties
* Encourages tactical or mischievous experimentation, creating dynamic world interactions

---

	Environmental / Emergent Interactions :
* Works with Balloon mechanics :
	* Players can trick enemies or NPCs by simulating defense scenarios
* Fog or heat shimmer can obscure AI detection of the bell source if timed carefully
* Guards and NPC AI reset patrols after 10–15 minutes if bell was rung without real threat

