			Morale State System

	Morale represents the psychological and structural condition of the village.
	It is a shared zone-wide state, dynamically affected by events, player actions, and outcomes.

---

		6.1. Morale Tiers

	1. Calm

	State Description : Stormreach Watch is stable, secure, and fully functional.
	Effects :
* NPCs:
	* Full civilian presence
	* Relaxed patrol patterns
	* Casual ambient dialogue
* Shops:
	* Normal prices
	* Full stock availability
* Defense:
	* Standard NPC defender count
	* Normal fire post effectiveness
* Events:
	* Invasion chance: Low
	* LCWM triggers: Harder to activate

---

	2. Alert

	State Description : Tension is rising; scouts sighted, rumors spread.

	Effects :
* NPCs:
	* Guards double patrols
	* Civilians move indoors more often
	* Defensive chatter & warnings
* Shops:
	* +5–10% price increase
	* Some luxury goods temporarily removed
* Defense:
	* Faster beacon fire lighting
	* Slight buff to NPC reaction time
* Events:
	* Invasion chance: Moderate
	* LCWM triggers: Easier

---

	3. Besieged

	State Description : The village is actively under threat or attack.
	Effects :
* NPCs:
	* Civilians flee or hide
	* Only guards, medics, and key vendors remain visible
	* Pale Matron can be summoned (if LCWM met)
* Shops:
	* Limited stock
	* +15–25% prices
	* Some services disabled (cosmetics, trading)
* Defense:
	* Bulwarks reinforced automatically
	* Beacon fires stronger
	* Fire barrels spawn more frequently
* Events:
	* Invasion active or imminent
	* Maximum LCWM responsiveness

---

	4. Broken

	State Description : Stormreach has fallen — temporarily conquered.

	Effects :
* NPCs:
	* Most civilians dead or missing
	* Only military NPCs respawn regularly
	* Merchants/trainers respawn after long delays
* Shops:
	* Closed or extremely limited
* Defense:
	* Fire posts disabled
	* Bulwarks breached and inactive
* Events:
	* No new invasions
	* Instead: Liberation mini-phase
* Recovery:
	* Village returns to Alert only after:
		* All Crusaders cleared
		* Repairs completed
		* Morale actions performed

---

		Morale Modifiers

	Morale shifts based on:
* Positive Influences
	* Successful defense
	* Repairs & fortifications
	* Lighting and maintaining fires
	* Saving civilians
	* Activating LCWM buffs
	* Presence of Pale Matron

* Negative Influences
	* Civilian deaths
	* Storage caverns looted
	* Bulwarks breached
	* Fires extinguished
	* Invasion success
	* Player abandonment during invasion

---

		6.2 Player-Driven Fortification System

	This system allows players to physically reshape Stormreach’s defensive layout.

	It is :
* Persistent
* Collaborative
* Destructible
* Visually reflected in the world

---

		Fortification Actions

	Barricade Placement

	Players can place :
* Wooden barricades
* Iron spike barriers
* Mobile shields

	Usage :
* Block narrow alleys
* Funnel Crusaders into kill-zones
* Protect fire posts or civilians

	Limits :
* Limited per zone
* Requires materials from Storage Caverns

---

	Debris Manipulation

	Players can :
* Drag broken carts
* Push fallen stone
* Collapse damaged walls intentionally

	Uses :
* Seal alternate paths
* Create improvised chokepoints
* Form emergency cover

---

	Stairway Reinforcement

	Players may :
* Reinforce steps with metal grating
* Add guard rails
* Install trap hooks

	Effect :
* Increases choke-point durability
* Allows trap placement
* Prevents collapse during storms/invasions

---

	Beacon Post Upgrades

	Players can upgrade beacon fires to :

| Tier        | Effect                               |
| ----------- | ------------------------------------ |
| Basic       | Normal light & hesitation effect     |
| Fortified   | Increased radius + morale bonus      |
| Overcharged | Fire bursts + crusader fear          |
| Sanctified  | Enables inscription resonance faster |

---

	Persistence Rules

	All fortifications :
* Persist through sessions
* Remain until destroyed by Crusaders or reset by server event
* Visually degrade over time if not maintained
* Are logged into the Morale system

---

	Strategic Consequences

	Well-fortified villages:
* Trigger fewer invasions
* Receive better defense NPC spawns
* Unlock rare merchant stock
* Allow access to secret mechanics (LCWM easier)

	Neglected villages:
* Suffer more frequent & harder invasions
* Lose access to certain services
* Require player intervention for recovery

