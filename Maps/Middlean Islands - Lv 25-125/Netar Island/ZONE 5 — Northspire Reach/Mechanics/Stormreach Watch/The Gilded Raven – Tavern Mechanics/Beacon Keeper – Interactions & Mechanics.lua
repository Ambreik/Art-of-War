			Beacon Keeper – Interactions & Mechanics

	Location : Stormreach Watch – Outdoor torches / plaza / tavern area

	NPC Type : Skillable, Immortal (while PvP flagged duel or during invasions)
	PvP : Can be flagged for duels
	Behavior : Roams between all beacon fires and torch posts, especially at night

---

	Functions & Mechanics : 

1. Fire & Light Maintenance : 
	* NPC wanders to all torches and beacon posts at night and in adverse weather.
	* Periodically relights torches if extinguished (due to wind, frost, or enemy sabotage).
	* Interaction with NPC may briefly boost nearby beacon light intensity for 30 seconds.

2. Player Interaction : 
	* Players can approach NPC to:
		* Learn Fire/Light mechanics (tutorial-style popups).
		* Temporarily activate "Resonance Buffs" (Oath of the Fallen / Crusader’s Last Stand / Vigil of the Watch).
	* Buff duration: short, tactical, local to plaza / beacon radius.

3. PvP Duel Mechanics : 
   * NPC can be challenged for friendly PvP duels.
   * During duel, all nearby guards assist the Beacon Keeper automatically.
   * NPC is immortal outside of invasions; cannot die from duel.

4. Integration with Tavern / Village Mechanics : 
	* When village state = Broken or Besieged, Beacon Keeper prioritizes outdoor fire posts for defense.
	* Interactions cannot be exploited to bypass invasion mechanics; temporary buffs vanish if beacon post is extinguished or destroyed.

---

	Flavor & Visuals : 
* Holds a large torch or fire staff.
* Walks with careful steps to avoid tripping while carrying fire.
* Emits warm light aura around NPC when stationary.
* Uses fire-based emotes when performing actions: waving torch, relighting, small fire sparks.

