	Mechanics Notes

* Tipsy Player Counter :
	* Each beverage consumed increases a “Tipsy Level” counter.
	* Threshold triggers Guardian of the Lodge push mechanic.

* Morale / Ambient Effects :
	* Light beverages: small passive buff to player ambient morale (+5%)
	* Medium beverages: moderate buff (+10%)
	* Heavy beverages: may cause tipsy effect, triggers ambient tavern animations (laughing, stumbling)

* Safety :
	* Tavern is PvP restricted
	* Consuming beverages does not deal damage
	* Effects are local and temporary

* Integration with Tavern Mechanics :
	* Tipsy mechanic interacts with Guardian NPC push
	* Tavern door shuts automatically if village state = Besieged or Broken
	* Players inside receive prompt: stay inside for safety or leave to assist the village

