			Lodge Owner & Wife – Lore & Quest Interface

	Overview : These NPCs provide narrative depth, lore immersion, and quest hooks. They are the primary interface for storytelling within the tavern. Dialogue 
reacts to village morale, invasion state, and player participation.

---

	A. Dialogue & Lore : 

* Reactive Dialogue :
	* Varies depending on village morale state (Calm, Alert, Besieged, Broken)
	* Reacts to player reputation, quests, or items carried
	* Examples:
		* Calm: “It’s quiet tonight… enjoy it while it lasts.”
		* Alert: “Keep an eye out, I saw shadows moving near the wall.”
		* Besieged: “The village is under attack! You better help!”
		* Broken: “Everything’s in ruins… I pray someone survives this.”

* Owner’s Wife Special Lore :
	* Shares Crusader tragedies, island myths, and hidden history.
	* Unlocks progressively as players complete mini-events or interact repeatedly.

---

	B. Quest Interface : 

* Quest Prompts :
	* Mini-events (e.g., local invasion, hidden LCWM shafts)
	* Lore-related side quests (finding artifacts, helping villagers, delivering messages)
	* Tavern-based puzzles (e.g., tipsy player interactions, hidden ingredients for potions or mount food)

* Interactive Mechanics :
	* Prompts appear when player is within talk range
	* Players can accept or decline quest immediately
	* Visual quest icon appears above NPC when new quests are available

---

	C. Tavern Lore Stories (Owner’s Wife)

1. Crusader Origins & Tragedy
	* "Long ago, these warriors were proud and disciplined… but the curse of this island changed them. Families vanished, memories twisted. Some nights, I hear their lament, echoing through the mountains…"

2. Island Mysteries
	* "There are stones on the cliffs… petrified soldiers, some say Crusaders, others whisper spirits trapped forever. They guard secrets, and those who disturb them… do not return unchanged."

3. Hidden Hero Tales
	* "A small group of villagers resisted once, using traps and clever tactics. They saved a caravan of refugees from the north winds. Some say their spirits still watch over Stormreach, helping those who defend it faithfully."

* Players can select story options interactively:
	* "Tell me more about the Crusaders" → deeper lore
	* "Any secrets or treasures?"→ triggers hints for LCWM or hidden quests
	* "I want to help" → unlocks mini-event participation

---

	D. Mechanics Notes : 

* Tavern Safe Zone Integration :
	* Tavern NPCs cannot be killed or PvP flagged.
	* Owner & Wife remain immortal during invasion, but doors shut when siege begins.

* Dialogue Frequency :
	* Random idle chatter when no players nearby (ambient flavor)
	* Increased dialogue frequency when players are inside, reflecting morale or event state

* Quest Trigger Conditions :
	* Proximity-based
	* Village morale state (e.g., some quests only available during Calm or Alert)
	* Player REP or previous quest completion

---

	E. Visual & Audio Feedback : 

* Dialogue icons indicate story, quest, or gossip availability
* Wife may be seen gesturing, sewing, or preparing meals
* Owner may shuffle papers, clean glasses, or tend to patrons
* Voiceovers or text dialogue reflect emotional state (fear, worry, pride)

