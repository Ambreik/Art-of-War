			Fire & Light Interaction Mechanics

		4.1 Beacon Fires & Torch Posts

	Placement Design 

	Beacon Fires & Torch Posts are fixed defensive structures placed at:

* Bulwarks :
	Control approach paths
* Stairway chokepoints :
	Deny vertical pressure
* Plaza edges :
	Protect civilian clusters and core NPCs

	They create safe-light corridors and defensive perimeters.

---

	Passive Effects (While Lit)

	When active, beacon fires project a "Light Influence Radius".

	Crusader Effects

Within radius:

* Hesitation Behavior
	* Crusaders:
		* Delay charges
		* Break formation more often
		* Prefer alternate routes if available
* Reduced Spawn Density
	* Local spawn tables near lit beacons:
		* -20% to -40% enemy reinforcement probability
* Elite Spawn Suppression
	* Elites have reduced chance to appear near heavy light zones

	Defender Effects

* NPC Morale Boost
	* Guards & Archers gain:
		* +Accuracy
		* Faster respawn time
		* Slight damage resistance
* Civilian Survival Bonus
	* Civilians near light:
		* Lower targeting priority by Crusader AI
		* Reduced fear-based panic behavior

---

	Player Interaction

	Players can actively manage beacon states:

	Light : 
* Activates effects
* Consumes fuel resource
* Makes the area strategically safer

	Extinguish : 
* Can be done intentionally or by Crusaders
* Removes all benefits
* Enables higher spawn density

	Refuel : 
* Restores fuel
* Increases duration and radius
* Over-refueling creates temporary high-intensity light zones

	Overload (Risk / Reward) : 

	Players may overload a fire source:
* Result: Fire Burst Event
	* AoE fire damage
	* Strong fear & knockback on Crusaders
	* Temporarily disables the beacon afterward

* Tactical Use :
	* Emergency crowd control
	* Area reset during heavy waves
	* Breaking elite pushes

---

	Strategic Layer

	Beacon Fires introduce:

* Map control
* Resource management
* Soft zoning through light rather than walls

	This avoids static " " feel and keeps player agency high.

