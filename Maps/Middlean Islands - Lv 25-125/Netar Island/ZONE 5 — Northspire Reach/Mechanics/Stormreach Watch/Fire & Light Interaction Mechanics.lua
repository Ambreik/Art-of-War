		4.2 Emergency Fire Barrels

	These are mobile fire weapons integrated into terrain and verticality.

	Placement

	Located at:
* Upper terraces
* Wall platforms
* Cliffside paths
* Watchtower storage platforms

---

	Usage Mechanics

	Players can:

* Roll Downhill
	* Fire barrels gain:
		* Momentum-based damage scaling
		* Increasing AoE size the longer they roll

	Ignite

Before or after rolling:

	* Creates:
		* Area Denial Zones
			* Burning ground
			* DoT
			* Reduced Crusader movement speed
		* Panic Effect
			* Low-tier Crusaders scatter
			* Ranged units lose formation
			* Elite units momentarily stall

---

	Crusader Reactions

* Lesser Crusaders:
	* May flee or stop advancing
* Veterans:
	* Try to bypass or destroy barrels
* Elites:
	* Can break through but suffer debuffs

	This reinforces the idea that Crusaders are military but not fearless or mindless.

---

	Synergy With Wind System

* Wind can:
	* Spread fire zones
	* Alter barrel trajectory
	* Extinguish weak flames
	* Amplify overloaded fire bursts

	So when you later add the Wind Core System, this module integrates naturally.

---

	AI Integration (Crusader Behavior vs Fire & Light)

	Crusaders are:
* Military-trained
* Not intelligent tacticians
* But tactically responsive

	Behavior Rules

	Crusaders:
* Avoid sustained light if alternative routes exist
* Attempt to:
	* Extinguish fires in groups
	* Target unlit zones
* Will:
	* Push through fire only when:
		* In elite formations
		* Or during final wave desperation

	This preserves their identity as disciplined, not mindless.

