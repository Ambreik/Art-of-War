			Stormreach Watch — Defensive Mechanics

	*(Applies during Invasion Events, Storm Conditions, or Defensive States)*

---

	2.1 Bulwark Integrity System
	*(Reinforced Earthen Walls as Primary Defense)*

	Structure & Behavior : 

* Bulwarks have durability tiers :
	* Intact → Damaged → Breached
* Crusaders attack the weakest sections first
* Players and NPC defenders can:
	* Repair bulwarks with materials
	* Reinforce specific segments for bonus durability
	* Barricade gaps temporarily during waves

	Mechanical Effects : 
* Intact bulwarks funnel enemies into chokepoints
* Breached walls allow deep incursion and trigger secondary defense AI
* Players can interact with bulwarks anywhere outside combat too (e.g., preparatory stages)

	Integration : 
* Material availability from "Storage Caverns" influences bulwark repair speed
* Bulwarks visually reflect damage (cracks, broken wood, displaced earth)

---

	2.2 Narrow Stairway Chokepoints
	*(Terrain-based Tactical Defense)*

	Behavior : 
* Crucial vertical access routes into upper terraces
* Enemies move slowly and predictably through them

	Player Interactions : 
* Collapse sections to block or redirect enemy flow
* Set traps (fire pots, pit traps, spiked barricades)
* Create kill zones for area control

	Design Role : 
* Makes terrain matter — combats here are about positioning and delay, not pure damage
* Controls invasion pacing

---

	2.3 Watchtower Signal & Early Alert System
	*(Integrated Watchtower Mechanics — Enhanced)*

	Improved from Lookout System
* The watchtower now includes:
	* Emergency Alarm Bell (Market Town mechanic integrated)
	* Signal Flares
	* Beacon Fire triggers

	Function
* When enemies detected:
	* Players or NPCs can trigger alarm
	* Sends system-wide warning
	* Boosts guard AI responsiveness and buffs defenders

	Effects
* Activates Alert State for village defenses:
	* Guard repositioning
	* NPC defenders mobilize faster
	* Defensive buffs applied

	(Note: The Bell/Alarm will be detailed further below)

---

	2.4 Hot Air Balloon Defense Mechanic
	*(Brought from Zone 4; fits perfectly here)*

	Stormreach Watch possesses "Hot Air Balloon Defense Platforms" mounted along military terraces.

	Core Identity : 
* Balloons are defensive tools only
* Not for travel or exploration
* Active only during Invasion or Defensive State

	Activation : 

Triggered when:
* Crusader invasion is active
* Village is in defensive alert

	Player Interaction Flow
1. Mount the Balloon
	* Short prep time
	* Burner ignites
2. Ascension to fixed altitude
	* Limited horizontal movement
	* Affected by Stormreach wind system
3. Payload Deployment
	* Incendiary payloads
	* Tactical bombs or crafted projectiles

	Mechanic Integration : 

* Wind system impacts:
	* Drop dispersion
	* Balloon stability
	* Payload accuracy
* Encourages timing, coordination, and awareness of terrain wind drafts

	Risk / Reward
* Limited fuel
* Strong wind can disable drop
* Balloon users are vulnerable during prep / landing phases

---

	2.5 Hot Air Balloon Environmental Combo
	*(Synergy Layer / Emergent Tactics)*

	By combining balloons with existing terrain and village mechanics, emergent gameplay arises:

	Combo Examples : 
* Wind Draft Direction + Payload Timing
	* Align with wind vectors to extend AoE
* Water Barrel + Balloon Drop
	* Creates steam cloud for crowd control
* Coordination with Ballista Firepots
	* Time balloon drops to coincide with firepot bursts for maximal effect
* Terrace Echo Tactics
	* Use elevated drops + wind to push enemies into kill zones

	Design Purpose : 
* Encourages skill and environmental mastery
* Feels emergent, not scripted

---

	2.6 Ballista Firepot Interaction
	*(Artillery-like Defensive System)*

	Platforms :
* Scattered at terrace levels
* One player operates per platform

	Trigger : 
* Fire only during invasion / defense
* Requires specific firepot crafted with rare ingredients

	Effects : 
* AoE explosion + continuous burn
* Ignites enemies (damage over time)
* Can stun a Crusader leader temporarily
* Physical pushback possible

	Advanced Outcomes : 
* If fired onto certain terrain (water channels), creates steam clouds that reduce enemy accuracy
* Knockback interactions with choke points or stairways

	Integration Points : 
* Combines with Balloon mechanics for extended AoE
* Synergizes with Wind Zones (dispersal patterns change)

---

	2.7 Emergency Bell / Town Alarm System
	*(Adapted from Sunveil Market Town)*

	Structure : 
* Giant bell atop the highest village terrace or watchtower

	Activation : 
* Manually pulled by players or NPC defenders

	Immediate Effects : 
* Triggers Alert State :
	* Guard AI becomes aggressive and tactical
	* NPC civilians take cover

	Enemy AI Response : 
* During invasion:
	* Enemies detect bell
	* May shift attack patterns
	* More aggressive targeting

	Smart Use / Abuse : 
* Players can ring bell outside invasion:
	* NPC guards react aggressively (false alarm)
	* Temporary minor penalty (e.g., guard escort, blocked paths)
	* Encourages thoughtful use

