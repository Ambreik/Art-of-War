			Latent Conditional World Mechanics (LCWM)

		5.1 The Bricked-Over Shaft Mechanism

	*(High-Value Secret Hook)*

	Base Structure : 

	Chain : Bricked Shaft ? Cold Empty Chamber ? Mountain Roots

	Default State

	The shaft appears as:
* A sealed, bricked wall
* No interaction prompt
* Looks like abandoned infrastructure or old reinforcement

	Players can pass by it dozens of times without suspecting anything.

---

	LCWM Activation Conditions

* The shaft does NOT open from a single trigger.
* It requires a latent condition sequence, meaning multiple environmental and situational factors must align.

	Required Condition Set (not order-dependent)

	Any 3 of the following must occur within a short time window :
* A storm is active
* An invasion is ongoing
* Player interacts with faded crusader inscriptions
* At least 2 specific beacon fires are lit
*  A fire barrel explodes nearby
* A spectral unit is currently active in the village

	This ensures:
* Not grindable
* Not predictable
* Only triggers in meaningful moments

---

	Activation Event

	When conditions are met:
* The bricked shaft:
	* Emits cold mist
	* Stone fractures slowly (audio + visual)
	* Opens for a limited time only
* No UI alert is given
* Players must notice it naturally

---

	Outcome

When opened, the shaft leads to :

* Hidden Chamber : 
	* A cold, carved chamber in the mountain roots containing:

* Artefill ľ The Sly Merchant (Smuggler NPC ľ Secret Trader)
	* Role :
		* Appears only while shaft is open
		* Trades :
			* Rare invasion-related items
			* Black-market gear
			* Information fragments
		* Disappears once shaft reseals

* Rare Cache : 
	* Contains :
		* Stormreach Tokens
		* Unique crafting reagents
		* Cosmetic fragments
		* Rare defensive items

---

		Design Philosophy

	This is:
* Not a dungeon
* Not a quest
* Not a boss
* A world secret

	It reinforces:

> The world reacts, but does not explain itself.

