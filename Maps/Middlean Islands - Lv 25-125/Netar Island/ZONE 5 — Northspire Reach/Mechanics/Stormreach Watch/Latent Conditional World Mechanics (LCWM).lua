		5.2 Crusader Inscription Resonance
			*(Lore-reactive LCWM)*

	Based on the faded crusader inscriptions scattered across "Stormreach Watch".
	These are not lore-only objects Ś they are resonant anchors.

---

	Trigger Interactions

	Certain actions near inscriptions can awaken their resonance :
* Valid Resonance Actions
	* Firelight illuminating the inscription
	* Blood spilled nearby (player or crusader)
	* Player kneeling / prayer emote
	* Playing a horn
	* Extinguishing a nearby beacon fire

	Each action does NOT guarantee activation.

	Instead:

* Builds hidden resonance charge
* Only visible through subtle effects:
	* Whispered audio
	* Faint glow
	* Frost forming on stone

---

	Outcomes : 

	A. Temporary Buffs

	When resonance is achieved, players nearby receive one of the following :
* Buff: "Oath of the Fallen"
	* +Defense vs Crusaders
	* Reduced fear effects
	* Slight HP regen near beacon fires
	* Duration: Short, but powerful

---

* Buff: "Crusaderĺs Last Stand"
	* Increased damage against elites
	* Resistance to frost & wind effects
	* Reduced stamina cost for blocking
	* Duration: Moderate

---

* Buff: "Vigil of the Watch"
	* Increased accuracy for ranged units
	* Faster reload / cast speed near walls
	* Duration: Tactical

	These buffs are :
* Local
* Temporary
* Stackable only through different inscriptions
* Never permanent

---

	B. Spectral Defender Summon
	*(Massive Invasion Only)*

	This is a conditional LCWM outcome, not a regular mechanic.

	Conditions
The Spectral Defender appears only when :
* Invasion scale is Massive
* At least 2 inscriptions have resonated
* Village defenses are near collapse
* At least 10 players are actively defending

---

		Spectral Defender:

	The Pale Matron of Stormreach

	Visual :
* Tall spectral woman
* Crusader-era armor
* Flowing pale-blue ethereal cloth
* Carries a broken banner instead of a weapon

	Behavior :
* Does not follow players
* Anchors herself in the village center
* Buffs NPC defenders
* Weakens Crusader morale
* Emits periodic defensive pulses

	She does NOT:
* Replace players
* Win the event alone
* Attack bosses directly

	She exists to:

> Prevent total collapse, not guarantee victory.

