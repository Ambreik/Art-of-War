## **7A. Global Mechanics**

*(Standard world mechanics, described in how they apply here)*

* **• Day/Night Cycle**

  * *Description:* The standard day/night rotation applies. In Northspire Reach this cycle has pronounced effects: daylight provides sharper illumination due to reflective ice surfaces, improving long-range visibility, while night increases creature aggression (global +15% to stats) and reduces player thermal comfort. Cold exposure mechanics (if present in your engine) intensify at night.

* **• Fatigue Waters**

  * *Description:* Any swimming or wading in partially frozen waters induces stamina drain. In this zone, submerged currents beneath thin ice can pull a player under quickly; prolonged underwater exposure accelerates drowning stacks. Cold water also accelerates fatigue drain vs. temperate regions.

* **• Water Flow & Currents**

  * *Description:* Sub-ice currents move unpredictably through fissure pools and under Winterglass Lake. Fishing lines and loose gear will drift or snap under strain unless weighted. Surface currents in narrow channels can carry small debris and conceal openings.

* **• Environmental Audio Layering**

  * *Description:* Sound behavior is unique: the cold air and reflective ice cause sounds to carry farther and sharper. Footfalls on packed snow create distinct crunches; metal clanks echo sharply. Wind-moving notes through spires produces haunting tonal cues that signal incoming storms or shifts in air pressure.

* **• Monster Awareness**

  * *Description:* Creatures here adapt to visibility and sound. During clear days they rely more on sight; during blizzards or fog they favor close-range sensing. Cold reduces scent trails, meaning tracking is less reliable unless snow is fresh.

* **• Noise Awareness**

  * *Description:* Noise alerts carry extremely far due to acoustic conditions. Heavy activities (mining, combat) will likely attract attention from distant creatures, especially across frozen plains or into cavern mouths where echoes amplify.

