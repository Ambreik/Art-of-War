## **8.1 The Frostveil Underpath**

A narrow, easily overlooked crevice near the mid-slope of the Windrazor Pass opens into a downward spiraling tunnel of compacted ice and bluish froststone. The walls are translucent in places, showing the slow movement of ancient trapped air bubbles drifting upward like reverse snowfall.

At the bottom, the tunnel splits into two natural galleries:

* **The Whispering Vault:** A chamber where wind slips through narrow slits in the ice, producing soft, flute-like tones. The chamber feels alive, reacting to air movement.
* **The Frostveil Arch:** A curved ice bridge dangling above a deep fissure, accessible only by shimmying along a narrow ledge. Mist rises from below, hinting at unseen warmth deeper underground.

No visible footprints or animal paths lead here, making it a pure player-discovery site.

