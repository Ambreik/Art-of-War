## 5. Points of Interest (POIs)


### **5.1. The Megadungeon Exit — The Gate of the Great Crusaders**

Carved straight into the Northspire Wall, the Gate of the Great Crusaders is a monumental stone portal of cyclopean scale. Oversized slabs of carved granite form twin doors bolted with iron bands and inset with frost-dark sigils. The frame is flanked by relieved crusader iconography: chiseled helms, faded banners, and a row of ascending warriors in low relief. Snow clings to the crevices between the blocks; wind whistles through the gates’ seams.

From outside, the gate reads as both tomb and fortress. Heavy, frost-burnished steps lead up to a shallow esplanade where the gate watches over the valley. The gate structure appears partially rebuilt over epochs: iron braces and crude buttresses have been added to support ancient masonry. At night the gate throws long shadows across the snow; during the day sunlight glints off carved inlay work that the frost has not yet eaten away.

**Spatial details and hooks:** the gate’s outer plaza contains scattered ruins of barricades and broken siege implements, narrow drainage channels from ancient defences, and a series of stone anchors that hint at once having supported a drawbridge or large portcullis. The gate’s seam shows signs of repeated chisel marks — as if someone attempted to reopen it in eras past.

---

### **5.2. Stormreach Watch (POI — Village near the Gate)**

Perched on a wind-sheltered shelf below the Northspire Wall, Stormreach Watch is the small, hardy settlement that serves as the immediate support community for the Gate. Built into terraces and reinforced with stone buttresses, the outpost’s compact buildings huddle close to reduce wind exposure. Heavy furs, smoked hides, and ironwork are common. A watchtower keeps vigil over the gate’s approach.

**Spatial details and hooks:** the Outpost includes a packed plaza used for provisioning and drying, small storage caverns carved into the slope, and a ring of solid earthen bulwarks that shield lower houses from prevailing gusts. Narrow stairways crawled into the rock connect the outpost to several lookout perches. There are faded inscriptions on the outer stones celebrating past crusader companies, and below one of the storage caches sits a bricked-over shaft that descends into a cold, empty chamber leading toward the mountain roots — a potential hidden route or sealed cache.

---

### **5.3. Windholm Spires**

A field of needle-like spires rising north of the Gate, their sheer faces catching wind and throwing up spectral wind-sheets. The spires create narrow corridors where wind tunnels form and whistling tones carry for miles. Between the spires are narrow ledge routes and precarious crossing points.

---

### **5.4. The Glassfall Curtain**

A series of frozen falls descending from a high terrace into the Shattercrag Basin. The frozen curtains glitter with internal striations; when the sun strikes them they sparkle like cut glass. Behind partial draperies of ice are small ledges and alcoves — places where snow accumulates in pockets and where seasonal melt can form tiny caves.

---

### **5.5. The Hollowed Terrace**

An ancient man-made terrace cut into the stone where old watchworks once stood. The terrace contains low foundations and fragments of iron fittings. Long ago it served as staging for siege defenses and now provides a sheltered walkway below wind-swept ridgelines.

---

### **5.6. Frostveil Cavern Mouths**

A string of cave openings halfway up the Northspire Wall, rimed with frost and pocked with mineral deposits. Many alcove mouths are small and lead into deeper galleries; others are wide, obvious cutouts that look like deliberate openings carved by hands. Some exit small vents where warm air breathes out from deep inside the mountain.

---

### **5.7. The Frozen Causeway**

A narrow, ancient stone path crossing a frozen trench between two ridges. The causeway’s stones are worn smooth; under the ice, thin fissures occasionally breathe cold mist, making footing precarious. Alongside the causeway are carved markers and chained anchors — evidence of long-ago traffic and deliberate fortification.

