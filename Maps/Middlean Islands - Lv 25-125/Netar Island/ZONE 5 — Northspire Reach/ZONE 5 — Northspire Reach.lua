   * Needs careful digging to avoid breaking; often used in potent restorative recipes.

**Rare Herbs**

8. **Everwake Bloom (Rare, Rank IV)**

   * Appears only at dawn on the inner ledges of the Gate plaza after an extended calm (no high winds) period.
