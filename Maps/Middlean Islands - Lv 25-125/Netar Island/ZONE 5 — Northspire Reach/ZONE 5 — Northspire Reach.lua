## **6.1 Fishing (6 species + 2 rare)**

1. **Iceglint Minnow** — *Rank I*

   * Small schooling fish found in shallow fissure pools along the valley margins and lower ledges.
   * Easy to catch with basic tackle; schools move quickly when disturbed.
   * Spawns at regular intervals; not tide-dependent.

2. **Frostscale Herring** — *Rank I*

   * Cold-water herring found in thin ribbon currents near the Windrazor Pass outflows.
   * Bites most strongly at midday when sunlight briefly warms the surface ice.

3. **Shardfin Trout** — *Rank II*

   * Lives in partially frozen running streams and under thin ice edges.
   * Requires careful ice-fishing technique (punch holes or find natural fissures).
   * Lines can freeze if left too long in subzero exposure.

4. **Glassback Char** — *Rank II*

   * Found beneath winterglass lake’s thinner ice near visible fractal cracks.
   * More active during calm, pale-sun mornings; harder to catch during heavy wind.

5. **Blue-veil Ray** — *Rank III*

   * A low-slung, slow-moving ray that inhabits deeper pockets under frozen ledges.
   * Requires a mid-tier rod and weighted line to reach.

6. **Crystal Halibut** — *Rank III*

   * Lives in deep fissures along the base of the Northspire Wall where cold currents converge.
   * Often takes long, heavy fights; requires patience and strength.

**Rare Species**

7. **Nightflare Lanternfish (Rare)** — *Rank IV*

   * Only appears in deep crevice pools beneath the Gate plaza at true midnight (game time) and during new-moon nights.
   * Emits dim bioluminescence; over-harvest depletes local spawns for 48 hours.
   * Catch window is narrow (≈5–10 minutes per spawn).

8. **Glassmirror Sole (Event-Rare)** — *Rank III*

   * Appears in the Winterglass Lake ONLY after a rare “thin-ice tremor” event that opens breath holes in the ice.
   * Harvest window closes as the ice re-freezes; spawn linked to seismic event frequency.

