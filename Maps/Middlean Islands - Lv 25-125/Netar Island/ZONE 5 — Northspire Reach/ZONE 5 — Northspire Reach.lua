## **8.2 The Twin Cavities of Shattercrag**

Within the fractured terrain of the Shattercrag Basin lie two nearly symmetrical sinkholes hidden under snow layers. These openings appear only after minor frostquakes.

**Upper Cavity:**
A bowl-shaped depression where the snow has melted into a thin spiral pattern. The cavity leads into a hollow chamber of smooth, egg-shaped stone. The roof is thin enough for sunlight to glow through, producing a diffuse golden light even at dusk.

**Lower Cavity:**
Located slightly north, this one has a jagged, teeth-like entrance. Below lies an underground slope of loose froststone chunks, leading toward deeper cracks that disappear into darkness. Touching the stone produces faint crystalline vibrations.

Both cavities are unconnected but appear carved by the same ancient geological event. Excellent potential for hidden miniboss entrances or collectible relic locations.

