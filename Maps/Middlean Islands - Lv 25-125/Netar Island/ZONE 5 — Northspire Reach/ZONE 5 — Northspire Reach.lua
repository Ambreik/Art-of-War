  * *Description:* Aggressive mining can cause brittle Blue-cerulean nodes to shatter, sending sharp crystalline fragments outward and creating temporary hazard zones where ice shards puncture the snowpack. This can force resource-gatherers to retreat and may reveal hidden pockets beneath.

