			Frost Lurker

	1. Basic Information

* Monster Name : Frost Lurker
* Level : 62
* Rank : III
* Monster Class : Superior
* Monster Type : Skillable / Ambush Predator
* Monster Category / Race : Beast / Elemental
* Element Type : Frost / Ice affinity
* Spawn Timer : 15s (Rank III)
* Location / Sub-Area : The Glassfall Curtain (behind ice curtain, weak ice zones)
* Group Pattern : Solo
* Aggro Type : Aggressive
* Special Notes : LCWM-triggered ambush; interacts with ice shards, frost accumulation; rare albino variant

---

	2. Appearance

* Size : Medium
* Coloration / Skin / Scales / Fur : Icy blue-gray fur with frost-encrusted spines along back
* Eyes / Markings / Luminescence : Pale icy eyes; faint glow under frost accumulation
* Special Features / Appendages : Sharp claws, jagged icy spikes along forearms, tail tipped with ice shard
* Distinctive Traits / Visual Indicators for Mechanics : Shivers/frost trails indicate ambush readiness

---

	3. Core Attributes

* HP Bar : Medium
* Defense / Armor : Medium
* Evasion / Dodge : High
* Movement Speed / Mobility : High, agile over ice/frost surfaces
* Chase / Leash Radius : 20m
* Attack Speed : Medium-Fast
* Damage Type : Physical (with frost debuff on hit)
* Damage Pattern : Burst + Knockback on charge
* Core Status Mechanics : Passive frost aura slows nearby players
* CC Resistances : High resistance to slow; moderate to knockback

---

	4. Combat Behavior / AI

| State                  | Description                                                                                                                                    
| Idle / Patrol          | Hides behind ice curtain; moves silently along frost-covered ledges; occasionally shifts ice shards to test for player presence.
| Detection              | Detects players via sound (footsteps) or ice vibration; aggressive on visual contact.
| Combat                 | Uses claw/bite combos; charges to knock players into weak ice tiles; may trigger ice shard falls as ambush tactic.
| Enrage / Phase Change  | HP <50%: increases movement speed, uses short leap attacks onto unstable ice patches, enhanced frost aura slowing players further.
| Death                  | On death, releases a burst of frost shards damaging nearby area; can break weak ice revealing hidden path / resource cache (LCWM conditional). 

---

	5. Skills & Abilities

* Frost Claw : Melee, direct physical + frost slow, cooldown 5s
* Ambush Charge : Targets player on weak ice, knocks back and triggers ice shard falls, cooldown 12s
* Frost Aura (Passive):  Slows movement of nearby players within 5m
* Ice Shard Manipulation : Can push shards to create hazard or block paths, cooldown 15s

---

	6. Status Effects / CC Applied

* Frostbite: slows movement 20%, 6s duration
* Knockback: target displacement 2-3m
* Minor DoT: frost ticks on exposed weak ice 3s duration

---

	7. Mechanics Interaction

* Interacts with Ice Shard Hazard : uses shards to ambush or push players
* Traversal hazard: weak ice tiles can collapse under charge attacks
* Frost accumulation increases stealth effectiveness
* Optional LCWM triggers: death may reveal hidden caches or paths if triggered conditions are met

---

	8. Quotes / Lore Lines

* “The frost hides more than just ice…”
* “Move quietly, or become part of the winter.”

---

	9. Respawn & Encounter Rules

* Respawn Timer: 15 minutes
* Solo combat lock; area remains active for player exploration
* Can reset if players retreat >50m from curtain

---

	10. Special Rules

* Rare albino variant spawns if environmental frost accumulation reaches threshold
* Death may trigger LCWM hidden path or resource cache

