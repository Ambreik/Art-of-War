			Frostveil Burrower

---

		BASIC INFORMATION

Name : "Frostveil Burrower"
Type : Rare Monster
Creature Class : Insectoid
Faction : Neutral Hostile
Zone : Netar Island – Northspire Reach
Location : Frostveil Cavern Mouths
Spawn Type : Conditional Rare Spawn
Difficulty : Solo+ / Small Party
Threat Level : Medium–High


---

		LORE DESCRIPTION

	The Frostveil Burrowers are ancient insectoid miners that tunneled the Northspire Wall long before the watchworks fell.
	They feed on mineral crystals and react violently when their food sources are disturbed.

	Some believe they were bred by the spirits of the dungeon, to weaken stone foundations beneath enemy fortresses.

---

		SPAWN CONDITIONS

	Trigger Mechanic :
* Crystal Smash Event – Rare Spawn Chain

	Counter :
* 4–30 smashed crystal deposits

	Spawn Delay :
* 3–8 seconds after trigger

	Night Bonus :
* Spawn chance x2 during night cycle
* Elite Variant Chance : 5% at night


---

		BEHAVIOR LOGIC

Aggression Type : Target Crystal Smashers First
Combat Style : Hit-and-Burrow Ambush
Escape Logic :
    • Burrows if HP < 40%
    • Repositions behind target
Territory Radius : Medium
Leash Distance : Short


	Special Behavior :
* Sensitive to vibrations → reacts to mining tools.
* Ignores stealth players unless crystals are smashed.

