			Ventshade Lurker

---

		BASIC INFORMATION

Monster Name : Ventshade Lurker
Type : Rare Creature
Family : Elemental / Insectoid Hybrid
Behavior : Passive → Ambush Trigger
Difficulty : Solo / Duo
Spawn Type : Conditional Rare Spawn
Location : Frostveil Cavern Mouths – Vent Chambers


---

		DESCRIPTION

	The "Ventshade Lurker" is an ancient hive parasite infused with frost-elemental energy.
	It hides near warm air vents, feeding on mineral heat and frost currents.
	It appears only when heat, light, and celestial alignment disturb its hiding place.
	Players often mistake it for a rock formation until it suddenly strikes.

---

		SPAWN CONDITIONS

Spawn Type : Triple Conditional Alignment
Requirements :
    • Warm Vent Active
    • Torch or Light Source placed near vent
    • Correct Time of Day Window
Chance :
    • 40% spawn chance on correct alignment
Cooldown :
    • 60 minutes per cave instance


	Time-of-Day Alignment Examples : 
* Dawn frost shimmer
* Midnight frost glow
* Sunset mineral flare

	Rotation is random per server cycle.

---

	APPEARANCE : 

* Semi-transparent icy shell
* Heat-glowing core veins
* Mineral spikes along back
* Emits faint vapor mist

	While hidden, appears as cracked mineral cluster.

---

		BEHAVIOR

	Passive State : 
* Does not attack
* Moves slowly near vent
* Watches player with glowing eye

	Ambush Trigger : 
Occurs if :
	* Player harvests mineral node nearby
	* Player extinguishes vent
	* Player damages creature

	Then creature bursts from hiding and attacks.

---

		ABILITIES

---

	1. Frost Vapor Snare
Type : Crowd Control
Effect :
    • Releases cold mist cone
    • Slows players 60% for 4 sec

Telegraph: Frost vapor swirl.

---

	2. Thermal Spike Shot
Type : Ranged Attack
Effect :
    • Shoots heated mineral shard
    • Hybrid frost + fire damage

Bonus Damage if player stands in vent airflow.

---

	3. Burrow into Vent
Type : Escape / Reposition
Effect :
    • Disappears into vent
    • Reappears behind player
Cooldown : 20 sec

---

	4. Mineral Shell Harden
Type : Defensive
Effect :
    • Gains 50% damage reduction for 5 sec
Trigger :
    • When HP below 40%

If broken early → creature stunned 2 sec.

