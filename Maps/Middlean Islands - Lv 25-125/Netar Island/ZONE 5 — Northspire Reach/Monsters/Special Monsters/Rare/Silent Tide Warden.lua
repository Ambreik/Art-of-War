			Silent Tide Warden

---

		Classification

* Type : Rare Sea Elemental
* Rank : Rare Elite
* Habitat : The Frostmirror Grotto
* Faction : Ancient Ocean Warden
* Temperament : Neutral until approached

---

		Lore Description

	Tidewarden is an ancient guardian bound to the frozen shoreline long before the Crusaders landed. It awakens only when the sea recovers fragments of forgotten 
relics and returns them to the grotto.
	The creature protects a hidden path once used by crusader scouts to reach a secret vault beneath the island.
	Its presence is rare and deliberate — appearing only when tides, stars, and cold currents align.

---

		Spawn Conditions

	Primary Spawn Rules : 
1. Grotto must be accessible (Low Tide).
2. Nighttime OR heavy starlight sky.
3. Random spawn window every 2–5 real days.
4. Only one spawn active per world instance.

	Visual Warning Signs : 
* Still seawater pools begin glowing faint blue.
* Mirror froststone reflects moving water patterns.
* Distant whale-like echo heard.

	This gives players discovery clues without UI spam.

---

		Combat Behavior

	Tidewarden fights slowly but powerfully — more like a guardian than a monster.

	Abilities : 
• Frozen Undertow : Pulls players toward a water pool, slowing movement.
• Mirrorwave Reflection : Reflects a portion of spell damage back using froststone mirrors.
• Tidal Prison : Encases one player in ice bubble for 4 seconds.
• Ancient Wake (Low HP Phase) : Summons freezing mist that reduces visibility and attack speed.

	No overcomplication — memorable but fair.

---

		Special Mechanics

* Will not chase players outside grotto.
* Despawns if tide rises.
* Cannot be forced spawn.

	Encourages natural discovery.

---

		Drops

	Primary Drop (100% – One Only)
* Key of the Sunken Crusade
	* Unique item.
	* Binds on pickup.
	* Opens a hidden side wing inside "Tomb of Great Crusaders"

	This side wing may contain :
* Lore tablets
* Crusader relic chest
* Secret miniboss
* Reputation tokens for hidden faction

	Exactly the meaningful reward you wanted.

---

	Secondary Drops (Small Chance)

* Frost-Salt Pearl
* Mirrorstone Fragment
* Oceanic Rune Dust
* Small Gold + XP

No gear spam.

---

		Designer Notes

	Purpose of this monster :
✔ Encourage players to revisit island
✔ Create world myth/rumor feeling
✔ Provide meaningful dungeon shortcut
✔ Tie ocean lore to crusader story

	Avoid turning this into farm content.
	Spawn rarity is intentional.

---

		Visual Design Notes

* Body made of frozen tidewater.
* Star reflections moving inside torso.
* Broken crusader anchor chain wrapped around arm.
* Emits low ocean hum.

