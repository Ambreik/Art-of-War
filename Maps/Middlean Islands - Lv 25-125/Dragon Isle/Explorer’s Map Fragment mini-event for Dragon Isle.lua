## **Rewards from Seafarer’s Hidden Cache**

**Local Resources (common):**

* **Sunken Reed** – Herb for crafting potions, found rarely in swampy/lake areas.
* **Crocodile Hide** – Leather from lake monsters, rare drop.
* **Dragonbone Fragment** – Mining resource, used for low-tier weapons or crafting.

**Small Chance of Unique Item (rare):**

* **Sage’s Compass** – Temporary buff item: +10% movement while exploring Dragon Isle for 1 hour.
* **Glowing Scale Pendant** – Cosmetic item, faintly illuminates surroundings in darkness.

**Occasional Fun “Event Loot” (very rare):**

* **Miniature Dragon Kite** – Cosmetic mount glider for fun; purely aesthetic.
* **Floating Lantern of the Lost** – Summons 3 small floating lanterns that follow the player for 10 minutes.
* **Potion of Phantom Echoes** – Temporary effect: faint ghostly visual trail follows the player, minor fun effect but no combat benefit.

---

## **Integration & Exploration Incentives**

* Encourages revisiting all **major and secret POIs** on Dragon Isle: cliffs, lake, jungle, dock, underwater cove.
* Can be shared between **party members** to encourage cooperative exploration.
* Completes a “hidden treasure” system rewarding players **daily**, while promoting **dynamic island traversal**.
* Adds depth to the **Dragon Isle environmental mechanics**, encouraging use of zip-lines, boat rentals, and careful jungle navigation.

