## **Explorer’s Map Fragment – Dragon Isle**

**Item Type:** Quest / Event Item
**Rarity:** Uncommon / Hidden Event Item
**Drop Source:**

* Rank II–IV monsters across Dragon Isle
* Higher chance near **secret POIs** or environmental hazards (cliff paths, jungle thickets, riverbanks)

**Number of Fragments:** **5 pieces**
Each fragment has a unique name and thematic placement:

1. **Fragment of the Shrouded Cliff** – Found near the **Dragon’s Eye Peak** cliffside.
2. **Fragment of the Whispering Jungle** – Dropped by ambush predators in dense jungle paths.
3. **Fragment of the Crocodile Lake** – Near the lake guarding the **Dragon God Dungeon** entrance.
4. **Fragment of the Sunken Totem** – Hidden in shallow underwater areas around small coves.
5. **Fragment of the Lost Harbor** – Occasionally dropped near **Blackwater Landing**, encourages revisiting the dock.

**Usage:**

* Assemble fragments via a quest interface to form the **complete Explorer’s Map**.
* Partial maps provide directional hints toward the treasure location (vague compass arrows, obscured “X marks the spot”).
* Encourages full exploration of Dragon Isle’s secret locations, vertical paths, and waterways.

---

## **Treasure Chest – “Seafarer’s Hidden Cache”**

**Location:**

* **Secluded Cove** on the north-western side of Dragon Isle, behind a cluster of jagged rocks and partially hidden by dense vegetation.
* Only accessible after assembling all 5 fragments.
* Chest **spawns once per day per server**, and only one can exist at a time.

**Mechanics:**

1. Chest appears when map is completed and a player interacts with the map interface.
2. Chest is **time-limited**: despawns if unclaimed within 15 minutes.
3. Can be opened **solo or in a party**; loot is distributed individually.
4. Partial map holders cannot trigger chest spawn.
5. If a non-map player interacts with the chest, a harmless environmental trap occurs (e.g., sudden minor debris fall or splash of water, just cosmetic or small stun).

