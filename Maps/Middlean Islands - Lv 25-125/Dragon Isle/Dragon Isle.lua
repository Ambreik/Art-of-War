**Location:** A secluded crescent-shaped beach on the island’s western side, surrounded by driftwood and broken ship remains.

**Mechanic Overview:**
An old **pirate cannon**, half-buried in sand and coral, can be interacted with by players. It’s aimed directly out to sea, toward the deep blue horizon. When loaded and fired using a **special bait item**, it acts as a lure — summoning one of the island’s aquatic terrors from the depths.

