### **Global Mechanics – Dragon Isle**

1. **Global Noise System**
   *Description:* All creatures on Dragon Isle can detect player-generated noise. Sounds from running, gathering, combat, or environmental interactions propagate in a radius around the player.
   *Player Impact:*

   * Noisy actions (e.g. mining, or firing ranged weapons) can alert nearby predators or pirate NPCs.
   * High-rank monsters have an increased detection radius.
   * Stealthy movement in dense foliage or using stealth buffs can mitigate detection.
     *Environmental Integration:*
   * Jungle foliage amplifies or muffles sounds depending on density.
   * Rocky cliffs or caves can echo sounds, increasing detection radius.

2. **Monster Awareness System**
   *Description:* Creatures actively monitor their surroundings for players or threats. They adapt to repeated patterns, such as retreating after losing aggro or calling reinforcements.
   *Player Impact:*

   * Mobs can avoid ambushes or use terrain advantages.
   * Certain pirate NPCs will regroup after spotting players, creating dynamic combat.
   * Repeated harassment of the same mob type may cause them to flee to higher terrain or swampy areas.
     *Environmental Integration:*
   * Jungle animals and swamp predators have unique awareness behaviors; e.g., crocodiles in Crocodile Lake strike only when players step too close to water’s edge.

3. **Night-Day Behavior System**
   *Description:* The island’s ecosystem dynamically shifts between day and night.
   *Player Impact:*

   * Day: Jungle wildlife behaves normally; pirates patrol paths on predictable routes.
   * Night: Stealth predators (jungle cats, spectral birds, or pirate ambushers) become active, gaining buffs like increased aggro range, damage, or movement speed.
   * Certain fishing pools and resource nodes only appear during specific time cycles.
     *Environmental Integration:*
   * Nighttime fog and limited visibility affect exploration and ambush strategies.
   * Lightning flashes on mountain peaks create temporary lighting that can reveal hidden paths or mobs.

4. **Stealth & Camouflage System**
   *Description:* Dense jungle foliage, tall grasses, and swamp reeds provide natural camouflage.
   *Player Impact:*

   * Grants partial invisibility to mobs, reducing detection radius.
   * Attacking or using abilities breaks camouflage immediately.
   * High-rank jungle predators can also use stealth to ambush players, making exploration riskier.
     *Environmental Integration:*
   * Certain pirate camps are shielded by natural cover, requiring players to use stealth or ranged reconnaissance.

5. **Weather & Lighting System**
   *Description:* Environmental visual effects enhance atmosphere without affecting mechanics like rain or storms.
   *Player Impact:*

   * Nighttime fog reduces visibility; torches or light skills increase detection radius for players.
   * Lightning flashes highlight mountain peaks or jungle clearings, temporarily exposing hazards or hidden mobs.
   * Ambient lighting affects fishing visibility and accuracy for skill-based mini-events.
     *Environmental Integration:*
   * Jungle canopy creates patches of light and shadow; swamp areas have persistent mist.

6. **Fatigue Waters**
   *Description:* Open water areas, particularly around Crocodile Lake and the island’s shoreline, impose a stacking debuff when swimming too far from shore.
   *Player Impact:*

   * Debuff reduces stamina and health gradually.
   * Players risk drowning if they remain too long without a boat, rope, or teleport escape.
   * Adds tension for resource gathering or reaching secret POIs on small islets.
     *Environmental Integration:*
   * Crocodile Lake is an extreme hazard: fatigue combined with predator attacks amplifies risk.
   * Boats rented from Blackwater Landing are strongly encouraged for safe travel.

7. **Flying Mount Restrictions (Island-Specific Global Mechanic)**
   *Description:* Flying mounts are disabled on Dragon Isle due to dense jungle canopy, mountain obstruction, and magical interference around the Dragon God dungeon.
   *Player Impact:*

   * Forces ground-based exploration and strategic pathfinding.
   * Players must use climbing, zip-lines (Flying Fox Tiroliana), or boats for navigation.
   * Prevents bypassing environmental hazards like pirate ambushes or Crocodile Lake.
     *Environmental Integration:*
   * Adds value to jungle trails, mountain paths, and strategic POIs for mobility.

