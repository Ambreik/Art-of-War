### **Fishing**

Dragon Isle offers both saltwater and freshwater fishing opportunities along its shores, river tributaries, and crocodile-filled lake.

* **Silverfin Pike** – Found in shallow river tributaries and jungle streams; prized for cooking recipes.
* **Moonlit Snapper** – A faintly luminescent fish, common along the south-eastern beaches near **Blackwater Landing**.
* **Reefscale Grouper** – Found near rocky outcrops along the north-western coast and near pirate shipwrecks.
* **Golden Croc Eel** – Rare and elusive; inhabits the deeper waters of the **Crocodile Lake**.

**Special Mechanic:** 
  * **High Tide:** Increases chance for rare fish like *Stormfin Barracuda* and *Golden Eel*.
  * **Low Tide:** Easier common catches like *Sandperch* and *Moonscale Carp*.
* During **Full Moon (in-game)**, players can catch *Phantom Koi* — a glowing, ethereal fish used in event recipes.

**Special Mechanic:** Some fishing spots have a chance to trigger **Resource Harvesting Events**: a school of rare fish spawns temporarily, encouraging players to compete for the resource.

**Location Hotspots:**

* **South Coast Tidal Flats:** Low-tide common fish.
* **Crescent Beach:** Rare tide-dependent spawns.
* **Near the Baited Cannon:** Chance for *Abyssal Guppy* during high tide (bait-only).
