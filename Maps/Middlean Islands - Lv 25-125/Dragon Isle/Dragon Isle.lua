### **3. Jungle Regrowth Cycle**

**Mechanic Overview:**
The jungle ecosystem of Dragon Isle behaves cyclically. Overharvesting or excessive killing of wildlife temporarily reduces spawn density and resource yield, simulating a living recovery system.

**How It Works:**

* If players excessively gather or hunt within a localized area, a **“Depleted Zone”** state activates.
* Within a depleted zone:

  * Herb and ore nodes temporarily stop spawning.
  * Common creatures migrate away, reducing spawn frequency.
* After **30–60 minutes**, the ecosystem recovers and nodes return gradually.

