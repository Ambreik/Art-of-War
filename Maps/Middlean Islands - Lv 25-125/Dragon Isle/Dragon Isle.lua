### **Resource Harvesting Events – Fishing (Dragon Isle)**

**Event Name:** **“Mystic Fish School”**

**Trigger & Spawn:**

* Can spawn at random intervals (≈ every 45–60 minutes) in **lakes, rivers, or coastal areas** of Dragon Isle.
* Only **one school per body of water** can exist at a time.
* Visible only to players within a certain radius; small shimmering water effect signals the presence.

**Event Mechanics:**

1. **Limited Catch:** The school has a finite number of fish (≈ 20–30 per spawn). Once depleted, the school disappears.
2. **Competition:** All nearby players can attempt to catch fish from the same school, creating a **player-driven race**.
3. **Rare Spawns:** Each Mystic Fish School guarantees at least **one rare or unique fish** spawn.
4. **Buff Effect:** Players participating in the event gain a temporary **“Angler’s Focus” buff**: +10% fishing skill success for 5 minutes.

**Example Fish in Mystic Fish School:**

* **Golden Croc Eel** – rare; found only in Crocodile Lake.
* **Moonlit Snapper** – uncommon; glowing fish, drops materials for alchemy.
* **Silverfin Pike** – common; used in general cooking.

**Rewards & Drops:**

* **Event Fish:** Some have a chance to drop **unique crafting ingredients**, such as:

  * **Glowing Scales** – for potion crafting.
  * **Pearl Shard** – rare jewel for crafting jewelry.
* **Occasional Fun Loot:** Small chance to obtain:

  * **Fishing Hat of the Lost Sailor** – cosmetic hat for players.
  * **Mini Pirate Companion** – temporary pet for 30 minutes.

**Notes:**

* Schools **deplete faster** if multiple players are fishing simultaneously.
* Mystic Fish Schools are **level-scaled**, ensuring high-level players can participate without trivializing the event for others.
* Event encourages exploration, as schools can appear **near hidden coves, jungle rivers, or pirate shipwrecks**, tying the mechanic into the island’s environment.

