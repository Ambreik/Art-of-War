### **1. Rare Mineral Pulse System – “The Heart of the Isle”**

**Mechanic Overview:**
Deep beneath Dragon Isle’s volcanic bedrock lies a dormant power source that occasionally pulses with residual draconic energy.
When this occurs, nearby mineral veins react — glowing faintly and temporarily upgrading their quality.

**How It Works:**

* Every **3–5 hours (server-based random interval)**, a “Draconic Pulse” event occurs.
* All mining nodes across the island gain a brief shimmer effect and change tier for 10 minutes.

  * Example: *Iron Veins → Enriched Iron Veins*
* During this window, gatherers can extract **Draconic Dust** (a rare crafting reagent used in advanced forging and enchanting).

**Location Hotspots:**

* Cliff bases around **Ashen Ridge Pass**
* Shallow caverns near **Dragon’s Rest Basin**
* Mining chance slightly higher near **Dragon God’s Lair Entrance**

**Gameplay Purpose:**
Encourages player movement and competition across the island and reinforces the feeling of a “living world” affected by the Dragon God’s energy.

