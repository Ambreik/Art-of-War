**Mechanics:**

* Upon firing, a loud echo reverberates across the sea.
* After ~45 seconds, the surface begins to churn violently, and the summoned monster appears.

**Monster:**

* **Name:** *Abyssal Devourer – “The Maw of Tides”*
* **Description:** A colossal eel-like beast with a maw full of phosphorescent teeth and barnacle-encrusted scales.
* **Mechanics:**

  * Emerges from the sea near the cannon.
  * Can submerge briefly to reposition.
  * Deals heavy knockback damage with tail sweeps.
  * Occasionally “swallows” a player for 5 seconds before spitting them out, causing minor debuff (“Drenched” – -10% movement speed).

**Loot Table:**

* Common: Salted Scales, Thick Hide, Obol (during event periods)
* Rare: *Pearl of the Deep Current* (used in alchemy to boost water-based potions)
* Unique Drop (1%): *Tidecaller’s Fang* – rare crafting reagent for aquatic gear upgrades.

**Respawn Timer:**

* Can only be triggered once every 2 hours per server.
* If not defeated, *The Maw of Tides* will despawn after 15 minutes.

