### **4. Vegetation**

Dragon Isle’s ecosystem is both lush and hostile — a living jungle that has reclaimed everything, from shipwrecks to ancient ruins. The dense foliage thrives under the island’s extreme humidity, volcanic soil, and constant mist drifting from the inner lake.

---

#### **General Environment**

The island’s flora forms three natural layers:

1. **Coastal Ring (Tropical Fringe)**

   * **Vegetation:** Thick mangroves, twisted palms, seaweed-draped roots, and flowering reeds that line the shore.
   * **Function:** Natural traps for boats and a perfect ambush zone for pirates or crocodilian creatures.
   * **Common Flora:** *Bristleweed*, *Saltbloom Palm*, *Reeffern*.
   * **Gameplay Note:** Hard to traverse — roots slow movement unless specific boots or mobility skills are used.

2. **Jungle Interior (Dense Heartland)**

   * **Vegetation:** Enormous trees with canopy coverage that blocks sunlight; vines, ferns, moss carpets, and glowing fungal patches thrive beneath.
   * **Common Flora:** *Verdant Viperleaf*, *Glowcap Mushrooms*, *Heartvine*, and *Spore Ferns*.
   * **Visuals:** Constant green haze, shafts of sunlight breaking through the canopy, occasional blue luminescence from bioluminescent fungi.
   * **Mechanics:**

     * Dense vegetation triggers the **Stealth Zone Mechanic**, granting camouflage to both players and certain predators.
     * Randomized vine “snare points” — players can get momentarily entangled if sprinting recklessly.

3. **Central Basin (The Fan Lake & Surroundings)**

   * **Vegetation:** Wetland flora — thick cattails, lotus-like flowers, and algae mats. Crocodile nests hide among the reed clusters.
   * **Special Flora:** *Sundew Bloom* — a carnivorous flower that reacts to movement (part of herbalism nodes).
   * **Gameplay Note:** Restricted visibility; movement through the swamp water drains stamina faster.

4. **Mountain Slopes (Dragon’s Spine Ridge)**

   * **Vegetation:** Sparse trees and blackened grass due to volcanic minerals; creeping thorn vines wrap around the rock faces.
   * **Special Flora:** *Ashthorn Ivy* — a poisonous herb used in high-level alchemy, rare and dangerous to collect.
   * **Visuals:** Faint blue lightning often cracks across the peaks, illuminating the twisted vegetation momentarily.

---

#### **Unique Vegetation Traits**

* **Bioluminescence Cycle:** At night, select plants (Glowcap Mushrooms, Heartvine) emit faint light that reacts to player movement — creating eerie glowing trails that help or hinder stealth.
* **Reactive Flora:** Some plants, like *Sundew Bloom*, react to proximity by spitting pollen clouds that blur vision or cause hallucinations (minor visual distortion effect).
* **Herbalism Tier:** Plants here are **Tier III–IV**, requiring advanced tools or profession ranks to gather safely.

