			The Ashscale Sentinel – Guardian of the Dragon God’s Seal

---

### **Overview**

The **Ashscale Sentinel** is the **island boss** of Dragon Isle — a powerful draconic guardian that watches over the **Fan-Shaped Lake**, where the entrance to the Dragon God’s dungeon lies hidden beneath the waters.
Born from the magical residue that seeps through the island’s ground from the Dragon God’s prison, this creature is a blend of dragon blood, hardened scales, and molten spirit energy.

The Sentinel is not enormous, but still imposing — roughly the size of a two-story creature, easily dwarfing any humanoid opponent. It serves as a **solo or small-party challenge**, requiring skill and cooperation from groups of up to **five players**.

---

### **Location & Behavior**

**Region:** Dragon Isle
**Specific Location:** The Fan-Shaped Lake (central area of the island)

**Behavior:**

* The Sentinel patrols the shallow parts of the lake, often submerged with only its back visible.
* Becomes aggressive if players enter the water or approach the center of the lake.
* When engaged, it lets out a deep, guttural roar that echoes through the jungle, warning nearby creatures (cosmetic sound only, no gameplay effects).
* If not defeated within 10 minutes of combat, the creature submerges and despawns, reappearing at full health after a cooldown.

**Respawn Timer:** Once every 24 in-game hours.
**Difficulty:** Designed for 3–5 players, mid-to-high level.
**Type:** Elite Beast (Dragon-type)

---

### **Appearance**

* **Size:** Large (approx. twice the size of a horse).
* **Body:** Dark, greyish scales with glowing red fissures beneath them — traces of the Dragon God’s lingering power.
* **Eyes:** Amber-red, constantly flickering.
* **Wings:** Short and decorative; used to balance or lash water, not for flight.
* **Movement:** Slow but powerful — creates small waves when emerging or slamming the ground.

---

### **Combat Mechanics**

#### **Phase 1 – The Lake Guardian (100% → 60% HP)**

* **Tail Sweep:** Wide attack that can knock players back into the water.
* **Water Surge:** The Sentinel slams its claws into the water, sending ripples that deal AoE physical damage.
* **Ashspit Strike:** Hurls a ball of heated ash that explodes on contact, dealing fire-type damage.
* Periodically dives underwater for a few seconds, reemerging in a new position to surprise players.

---

#### **Phase 2 – Scales of Fire (60% → 30% HP)**

* Scales begin to glow, increasing its damage slightly.
* Gains **Flame Veins**, a passive aura that burns nearby players if they stand too close.
* Every 30 seconds, the Sentinel releases **Molten Pulse**, dealing minor fire damage in a small area.
* Occasionally summons **Lesser Ashlings** (small draconic elementals) that assist for a short duration before dispersing.

---

#### **Phase 3 – Last Stand (30% → 0% HP)**

* Roars, causing a brief shockwave (knockback only, no fear or stun).
* Gains **Frenzy**, increasing its attack speed and aggression.
* Combines Tail Sweep and Ashspit in quick succession.
* At 10% HP, enters a **Protective Rage**, reducing damage taken slightly but slowing its movements.

When defeated, the Sentinel collapses into the water, leaving behind a glowing set of scales that mark its loot.

---

### **Rewards & Drops**

**Guaranteed Drop:**

* **Ashscale Heartstone** – A consumable item usable *only inside the Dragon God’s Dungeon*.

  * Grants a 10% resistance against the dungeon boss’s elemental damage for 30 minutes.
  * Single-use, cannot be traded.

**Possible Additional Drops (Small Chance):**

* **Ashscale Fragments** – Crafting material used in certain high-tier weapon coatings.
* **Burnt Claw** – Material used in rare alchemy fire-resistance potions.
* **Scaled Meat Chunk** – Can be cooked into *Embergrilled Filet*, a recipe exclusive to this island.

**Experience / Reputation:**

* Yields a significant amount of combat XP.
* Increases *Tavern Keeper* reputation slightly if players bring proof of the kill (item: “Ashscale Tooth”).

---

### **Combat Reset & Limits**

* **Respawn:** Once per in-game day (24-hour cycle).
* **Despawn:** If unengaged for 5 minutes or not defeated within 10 minutes of active combat.
* **Respawn Indicator:** Small bubbling in the lake surface appears ~10 minutes before it returns.

---

### **Design Notes**

* The Ashscale Sentinel is **not** a world event, dungeon raid, or story trigger — it serves as an open-world mini-boss and environmental guardian.
* Designed to encourage teamwork for a small group rather than mass raids.
* Its defeat helps players entering the dungeon by granting the **Ashscale Heartstone**.
* Respawn and limited access maintain its prestige and repeatable challenge.

