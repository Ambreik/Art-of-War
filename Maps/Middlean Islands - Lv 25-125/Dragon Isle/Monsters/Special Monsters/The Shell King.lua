			The Shell King

**Location:** Eastern shoreline near shallow tide pools.

**Mechanic Overview:**
A dense colony of crabs populates the area. Normally harmless, they respawn endlessly. However, over-harvesting (killing more than 300 in under 10 minutes) triggers a hidden escalation event.

**Event Trigger:**
When the crab population threshold is exceeded, the **Shell King** spawns.

**Monster:**

* **Name:** *Shell King Krakkar*
* **Description:** A massive, mutated crab with coral armor and glowing orange eyes. Its shell carries remnants of pirate wreckage — lanterns, anchors, and bits of gold fused into its back.
* **Mechanics:**

  * Extremely aggressive; will chase players inland if provoked.
  * Deals high AoE splash damage by slamming claws.
  * Occasionally burrows into the sand and emerges behind players.
  * If left alive, remains indefinitely and can migrate along the coast or into the jungle.

**Loot Table:**

* Common: Crab Meat, Cracked Shells
* Rare: *Golden Barnacle Fragment* – used in crafting underwater breathing potions.
* Unique (2%): *Crown of the Shell King* – decorative vanity helm (cosmetic).

**Respawn Logic:**

* If defeated, new crab swarms reset after 30 minutes.
* Multiple Shell Kings can coexist if previous ones are not killed. (Creates potential chaos.)