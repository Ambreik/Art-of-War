			Heart of the Overgrowth

---

		1. Basic Information

	Name : Heart of the Overgrowth
	Type : Plant
	Rank : Boss (POI World Boss)
	Role : Environmental Control / Multi-Phase Encounter
	Location : Ruined Temple — Arliton Island

---

		2. Lore

	Beneath the temple lies the true organism — a massive root-heart that has spread through every crack of stone for centuries.
	The Temple Rootbound Stranglers are merely defensive growths. But when the network is overfed beyond control, the core is forced to surface to protect itself.
	The Heart of the Overgrowth is not a creature.
	It is the entire root system of the temple given a single will.
	When it rises, the temple is no longer ruins.
	It becomes a living organism.

---

		3. Appearance 

	The ground of the temple ruptures outward as enormous root-columns push the stone floor aside. From the center rises a colossal mass of roots woven into a 
pulsating core the size of a small hut. Thick vine-tentacles spread in all directions, anchoring themselves into pillars, walls, and broken statues. Veins of 
glowing moss run across its surface, pulsing rhythmically like blood through arteries. The air fills with luminous pollen, and every step around it causes the earth
to tremble slightly. The ruin no longer feels like architecture — it feels like standing inside the chest cavity of a living being.

---

		4. Behavior & Combat Role

	This is a multi-phase environmental boss.

	Players are not just fighting the boss.

	They are fighting the entire temple while the boss is present.

	The fight requires :
* Constant movement
* Awareness of ground
* Managing spawned growths
* Rotating around the arena

---

		5. Phase Structure

	Phase 1 — Awakening Roots (100%–70%)

	The boss is anchored in the center.

		Abilities : 

	Tendril Slam : Vines slam random areas, forcing players to keep moving.

	Living Ground Surge

	The "Living Temple Ground" mechanic becomes extremely aggressive :
* Idle timer reduced to 3s
* Roots last longer

	Pollen Saturation : Pollen clouds frequently fill sections of the arena, limiting visibility.

	Goal of phase : Players learn they cannot stand still anywhere.

---

	Phase 2 — Spreading Overgrowth (70%–35%)

	The boss spreads roots to the ruins.
	Root anchors attach to :
* Pillars
* Walls
* Rubble

	These anchors must be destroyed.

	While anchors exist :
* Boss takes reduced damage
* Nutrient patches spawn continuously from the ground

	New Ability — Nutrient Absorption : If patches are not cleared, boss heals.

	Goal of phase : Players must split roles — some attack boss, some clear anchors and patches.

---

	Phase 3 — Temple Heart Exposed (35%–0%)

	The root mass opens, exposing a glowing core.

	Boss becomes mobile (slowly).

	Core Pulse : Rhythmic shockwaves force players to rotate around the arena.
	Vine Grasp Waves : Waves of roots travel across the ground in lines — players must dodge paths.
	Overgrowth Frenzy : All previous mechanics happen faster.

	Goal of phase : Movement, rotation, spatial awareness.

---

		6. Environmental Interaction

	This boss amplifies all temple mechanics:

| Mechanic             | Boss Interaction            |
| -------------------- | --------------------------- |
| Living Temple Ground | Becomes faster and deadlier |
| Nutrient Patches     | Boss uses them to heal      |
| Vines                | Become attack tools         |
| Ruins                | Used as root anchor points  |

	The POI itself becomes the encounter.

---

		7. Strengths

* Extreme area control
* Punishes lack of awareness
* Self-healing if mechanics ignored
* Forces group coordination

---

		8. Weaknesses

* Slow movement in Phase 3
* Vulnerable when anchors destroyed
* Predictable pulse patterns once learned

---

		9. Group Composition

	No adds in classic sense.
	The environment is the add.
	Root anchors act as secondary targets.

