			Golden Stream Serpent — Elite Creature

---

		1. Basic Information

* Monster Name: Golden Stream Serpent
* Level: 65–70
* Rank:** IV (Elite)
* Monster Class: Superior
* Monster Type: Melee / Patrol / Aquatic Predator
* Monster Category / Race: Reptile / Aquatic
* Element Type: Water
* Spawn Timer: 2–3 hours in real-time (tied to underground stream currents)
* Location / Sub-Area: Rainbow Trout Goldmine — underground waterfall basins and deep stream channels
* Group Pattern: Solo (sometimes accompanied by juvenile trout predators as environmental hazard)
* Aggro Type: Aggressive
* Special Notes: Elite variant; interacts heavily with aquatic environment; uses currents and trout to manipulate fight space

---

		2. Appearance

* Size: Large (6+ meters long)
* Coloration / Skin / Scales / Fur: Iridescent golden scales with subtle rainbow streaks mimicking trout reflections
* Eyes / Markings / Luminescence: Pale amber eyes with vertical pupils; bioluminescent spots along the spine pulse rhythmically
* Special Features / Appendages: Dorsal fins along its back for high-speed swimming, strong muscular tail for sweeping attacks
* Distinctive Traits / Visual Indicators for Mechanics: Faint glowing patterns intensify before Rapid Swim Ambush; tail curls in water before Current Lash

---

		3. Core Attributes

* HP Bar: Giant
* Defense / Armor: Medium (physical resistance from scales)
* Evasion / Dodge: Medium
* Movement Speed / Mobility: Medium, with sudden burst speed underwater
* Chase / Leash Radius: 25 meters
* Attack Speed: Medium-Fast
* Damage Type: Physical (main), Water Element (environmental knockback)
* Damage Pattern: Burst with Knockback; occasional bleed from tail lash
* Core Status Mechanics: Passive regen while in water, threat generation towards closest target, camouflage in shallow streams
* CC Resistances: Medium resistance to Poison; weak to Frost

---

		4. Combat Behavior / AI

| State                 | Description                                                                                                                              |
| --------------------- | ---------------------------------------------------------------------------------------------------------------------------------------- |
| Idle / Patrol         | Moves silently along streams, curling among rocks and ledges, using currents for propulsion. Rarely surfaces in pools.                   |
| Detection             | Detects players by movement in water or disturbing trout. Uses ambush stealth when unseen.                                               |
| Combat                | Combines tail lash, ambush surge, and environmental disruption. Prioritizes isolated targets. Uses currents to reposition dynamically.   |
| Enrage / Phase Change | At 50% HP, Golden Strike triggers: scales shimmer, attack speed and movement increase; more frequent use of Rapid Swim Ambush.           |
| Death                 | Collapses in water, sending a final shockwave; trout in nearby pools scatter, briefly obscuring vision and altering terrain temporarily. |

---

		5. Skills & Abilities

* "Current Lash" (Melee AoE): Swings its tail across a cone in front, dealing medium physical damage, applying minor bleed for 3 seconds and a knockback. Can hit 
multiple targets; timing allows partial avoidance by moving behind rocks or ledges. Cooldown: 8s.
* "Rapid Swim Ambush" (Single Target Burst): Detects lone players and accelerates using water currents. Deals high single-target damage and briefly blurs vision for
2–3 seconds. Ends behind target if swimming space allows. Cooldown: 12–18s depending on stream layout.
* "Trout Disruption" (Environmental Interaction): Every 20–30s, sweeps nearby trout pools. Fish scatter, causing minor knockback and temporary visual obstruction 
from splashing. Strong currents amplify the push. Does not damage trout directly; purely hazard.
* "Ambush Camouflage" (Passive Stealth): Blends into shallow pools or shadowed streams; movement is nearly silent. Breaks if attacked or attacking. Can reposition 
unnoticed when retreating.
* "Golden Strike" (Phase Buff): Activates below 50% HP; scales glow intensely, increasing movement and attack speed for 10–12 seconds. Encourages tactical focus by 
players and careful use of currents.

---

		6. Status Effects / CC Applied

* Bleed: From Current Lash, 3s duration, moderate physical damage over time
* Vision Blur: From Rapid Swim Ambush and Trout Disruption, 2–3s
* Knockback: From tail lash and trout splashes, pushes 2–3 meters downstream

---

		7. Mechanics Interaction

* Player Mechanics : Stealth partially negates detection; mounted players risk knockback into rocks or currents; consumables like Frost potions increase damage 
(slows serpent).
* Environment : Currents, pools, and ledges directly affect combat; trout presence triggers hazard mechanics.
* Spawn Mechanics : Appears during real-time spawn timer; may patrol area randomly until detected.

