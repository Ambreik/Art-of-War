		9. Respawn & Encounter Rules

* Respawns every 2–3 hours; spawns tied to active water currents
* Solo combat lock: Pools prevent escape from Serpent during engagement
* Reset: Retreat out of stream for 30 seconds; serpent returns to patrol
* Interaction with other monsters: Juvenile trout predators flee; no interference from other aquatics

---

		10. Special Rules

* Aggro prioritizes isolated targets
* Golden Strike phase requires rotation and use of environmental cover
* Currents modify skill trajectory and push distance

