			Temple Rootbound Strangler

---

		1. Basic Information

	Name : Temple Rootbound Strangler
	Type : Plant
	Rank : Elite
	Role : Area Control / Environmental Predator
	Location : Ruined Temple — Arliton Island

---

		2. Lore

	Deep beneath the cracked stones of the Ruined Temple, the Vine Strangler Plants are not separate organisms but extensions of a vast root network that has grown 
for centuries beneath the ruin.
	When the temple floor is fed with enough organic matter in a short time, the network reacts defensively. It concentrates nutrients into a single growth point 
and forces a larger body to the surface — a guardian grown from the temple itself.
	The Temple Rootbound Strangler is not a creature that lives in the temple.
	It is the temple, briefly given form.

---

		3. Appearance

	From the fractured floor of the ruin, thick roots push upward, tearing apart moss-covered stones as a mass of interwoven vines rises into a towering shape. Its 
body resembles a twisted pillar of roots and thorns, constantly shifting as if struggling to hold a humanoid silhouette. Patches of bioluminescent moss pulse 
faintly along its surface like a heartbeat beneath bark-like skin. Long vine-whips drag across the ground around it, moving independently, searching for movement. 
Where its head should be, a hollow knot of roots opens and closes slowly, releasing clouds of green pollen into the air. The ground around it never stays still — 
pebbles tremble and small roots continuously emerge and retract as if the earth itself breathes with the creature.

---

		4. Behavior & Combat Role

	The Temple Rootbound Strangler is an area denial elite.
	It does not chase players far. Instead, it turns the immediate environment into a hostile zone where movement, positioning, and awareness are constantly 
pressured.
	It fights best when players attempt to remain stationary.

---

		5. Abilities

	Root Lash : Swings vine whips in a wide arc, dealing Nature damage and applying "Slow".

	Grasp of the Floor : Targets players who have not moved recently. Roots erupt beneath them, applying a "Root" for 2 seconds.
(Synergizes directly with "Living Temple Ground".)

	Pollen Exhale : Releases a cloud of glowing pollen around itself :
* Heals nearby plant creatures
* Slightly obscures player vision
* Lingers for several seconds

	Nutrient Draw : If standing on a "Nutrient Patch", the Strangler absorbs it :
* Gains temporary damage increase
* Slight self-heal

	This rewards players who manage the patches properly.

---

		6. Environmental Interaction

	This Elite becomes more dangerous because of the temple mechanics :
* Players rooted by the ground are easy targets
* Pollen clouds force movement
* Nutrient patches empower it if ignored

	The fight space becomes chaotic and alive.

---

		7. Strengths

* Excellent area control
* Punishes stationary players
* Self-sustain if nutrient patches are present
* Hard to fight in tight spaces

---

		8. Weaknesses

* Slow turning speed
* Vulnerable to ranged kiting if players manage movement well
* Cannot move far from its spawn point
* Struggles if players destroy Bloom Node early (advanced tactic)

---

		9. Group Composition

	Usually fought alone, but may be surrounded by :
* Remaining Vine Strangler Plants
* Thorn Whip Plants
* Sporeburst Fungus

	These make the area more dangerous if not cleared.

---

		10. Rewards

* High chance to drop plant-based crafting materials
* Chance to drop rare alchemy/herbalism ingredient: "Overgrown Heart Fiber"
* Increased experience reward
* Contributes to temple ecosystem progression

