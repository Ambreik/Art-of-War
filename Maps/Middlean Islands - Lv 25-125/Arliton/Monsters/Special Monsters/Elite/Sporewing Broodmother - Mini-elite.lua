---

	Design Purpose

	This creature ensures :
* Players cannot brainlessly farm minerals
* Noise has meaningful consequences
* The cave feels alive and reactive
* Combat becomes environmental and memorable
* The mechanic has a visible “boss” embodiment

		She is not a boss by quest.
She is a boss by player behavior.
