			Sporewing Broodmother

---

		Basic Information

	Name : Sporewing Broodmother
	Type : Animal
	Role : Mini-Elite Roost Controller / Add Summoner
	Habitat : Western Sea Caves ceilings and deepest chambers
	Health Bar : Mini-Elite (Thick segmented bar)
	Aggression : Reactive (spawns only from Disturbed Roosts Stage 3)

	The Sporewing Broodmother is not normally present in the cave. She **emerges only** when players create excessive noise through mining, combat, or spore 
triggering. She is the apex organism of the cave ceiling ecosystem and the biological reason the roost reacts to disturbance.

---

		Appearance

	At first glance she looks like an enormous bat fused with fungus and stone. Her wings are ragged membranes veined with faint bioluminescent lines, as if 
Shadowmoss has grown inside her flesh. Clusters of pale spores pulse along her back like swollen eggs. Her head is crowned with layered cartilage plates resembling 
a fungal cap, and when she screeches, visible spore mist spills from her mouth.
	When clinging to the ceiling she is almost indistinguishable from rock and moss. Only when she releases her grip does her true size become clear, unfolding into
a grotesque canopy of wings and spores that fills the cave air.

---

		Spawn Condition — Disturbed Roosts Mechanic

	She spawns when :
* Roost Disturbance Meter reaches "Stage 3"
* Players remain inside the cave
* A delay of ~8–12 seconds passes after the warning dust/falling pebbles

	She drops from the ceiling with a heavy impact, scattering spores in a wide radius.
	If players leave the cave before she lands, the meter slowly resets and she does not spawn.

---

		Combat Behavior Overview

	The Broodmother does not fight alone. She is designed as a living roost, constantly calling and spawning smaller creatures to assist her.
	The fight becomes chaotic, vertical, and positional rather than a simple damage exchange.

	She alternates between :
* Grounded phases (summoning and spore control)
* Ceiling cling phases (add spawning and dive attacks)

---

		Abilities & Skills

	"Spore Impact" (Spawn Ability)

	What it does : When she lands, she releases a circular burst of dense spores.

	Effect :
* Applies "Spore Blindness" (reduced vision radius)
* Applies "Mild Poison DoT"
* Forces players to reposition immediately

	Purpose : Announces the fight and sets environmental pressure from the first second.

---

	"Brood Call" (Core Mechanic — Add Spawning)

	What it does : The Broodmother screeches toward the ceiling, awakening the roost.

	How it works :
* Every 12–18 seconds she calls for assistance
* Spawns:
	* 2–4 "Sporewing Skulkbats"
	* 1 "Ceiling Creeper Arthropod" (every second call)

Adds drop from random ceiling points, not near her.

	Important :
If players ignore the adds, the fight becomes unmanageable.

	Purpose : Forces target prioritization and spatial awareness.

---

	"Ceiling Cling Phase"

	What it does : At 70% and 35% HP, she flies back to the ceiling.

	While clinging :
* Becomes untargetable for melee
* Continues using **Brood Call**
* Drops targeted **Spore Drips** on players below

	Players must deal with adds while waiting for her to descend.

---

	"Spore Drip"

	What it does : Globs of spore mass fall from above.

	Effect on impact :
* Creates small spore clouds (like Shadowmoss trigger)
* Poisons and blurs vision
* Zones areas of the cave temporarily

---

	"Wing Buffet "

	What it does : A heavy wing slam in frontal cone.

	Effect :
* Knockback
* Interrupts casting
* Can push players into spore clouds or uneven cave terrain

---

	"Feeding Frenzy" (Add Interaction)

	What it does : If Skulkbats remain alive near her for too long, she consumes one.

	Effect :
* Heals 8% HP
* Gains temporary attack speed

	This punishes players who ignore the adds.

---

		Environmental Synergy

	This fight synergizes with :
* Shadowmoss spore clouds already in cave
* Uneven terrain and mineral veins
* Roost disturbance system
* Limited vision and cave darkness

	The cave itself becomes part of the fight.

---

	Death Behavior

	Upon death :
* Remaining adds panic and flee
* Roost Disturbance Meter instantly resets to Stage 0
* Ceiling dust settles visually

The cave feels calm again.

---

		Loot & Rewards Philosophy

	She does not drop standard loot. She drops rare cave ecosystem materials :
* Broodmother Spore Sac (alchemy/crafting)
* Hardened Wing Membrane (light armor crafting)
* Bioluminescent Vein Thread (used with Glowleaf recipes)
* Chance for Roost Egg (pet/mount crafting component later)

