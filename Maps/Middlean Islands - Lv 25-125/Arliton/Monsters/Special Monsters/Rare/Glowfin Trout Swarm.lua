---

		5. Skills & Abilities

* "Nipping Torrent" : Each fish bites rapidly at the nearest target, dealing minor physical damage. The swarm can coordinate up to 5 bites per second per player in 
water areas.
* "Swarm Dispersal" : On AoE damage, 40–60% of the swarm disperses, reducing direct hits but creating turbulence, which slows movement and slightly knocks back 
players.
* "Water Rush" : The swarm surges forward when a player crosses a narrow channel, knocking players toward shallow rock edges or environmental hazards. Damage is 
minor, but knockback can cause falls or trap interruptions.
* "Bioluminescent Flash" (Passive) : The swarm pulses its glowing scales at regular intervals, briefly blinding players in murky water, reducing visibility and 
accuracy of ranged attacks by 15% for 3s.

	Notes: The swarm’s mechanics are designed to reward careful navigation and awareness of currents. Attacking a single fish can escalate the encounter quickly, 
forcing players to manage crowding and positioning.

---

		6. Status Effects / CC Applied

* Slow: Water turbulence slows player movement by 15–25% during dispersal.
* Minor Knockback: Water Rush knocks players 1–2 meters toward stream edges or shallow rocks.
* Visibility Debuff: Bioluminescent Flash reduces sight range and target clarity.
* Tick Damage: N/A

---

		7. Mechanics Interaction

* Player Interaction: Players using fire or light spells have no effect; however, fishing nets or area traps can reduce swarm numbers efficiently.
* Environment Interaction: Water currents amplify movement speed of swarm; narrow channels enhance knockback effects.
* Special Triggers: Presence of a "Golden Stream Serpent" nearby increases aggression and coordination; the swarm can act as a "bait" mechanic.
* No minions or adds; swarm is self-contained.

---

		8. Quotes / Lore Lines

* "A flicker of silver darts past your legs, like liquid moonlight turned fish."
* "Disturb the stream, and the waters themselves bite back."
* "Even the smallest trout here seem to move as one… a silent, shimmering storm."

---

		9. Respawn & Encounter Rules

* Respawn Timer / Conditions : 9s per individual fish; swarm reforms fully in ~2–3 minutes.
* Single / Group Combat Lock: N/A; players can fight individually or in groups.
* Escape / Reset Conditions: Swarm will retreat if fewer than 3 fish remain or water is disturbed beyond their channel.
* Interaction with other monsters or players: Acts as environmental trigger for Golden Stream Serpent ambush; can funnel players into its attack zone.

---

		10. Special Rules

* Environmental hazard mechanics: Water currents and turbulence interact with player movement and positioning.
* Swarm dispersal mechanics reward AoE management and timing.
* No day/night dependency; fully functional in both.

