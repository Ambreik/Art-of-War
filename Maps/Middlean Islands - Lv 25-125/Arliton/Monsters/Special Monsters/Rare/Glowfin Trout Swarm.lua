			Glowfin Trout Swarm

---

		1. Basic Information

* Monster Name : Glowfin Trout Swarm
* Level : 14–16
* Rank : II
* Monster Class : Rare Monster
* Monster Type : Agile, Swarm, Reactive
* Monster Category / Race : Aquatic / Fish
* Element Type : Water
* Spawn Timer : 9s
* Location / Sub-Area : Underground Water Channels, Rainbow Trout Goldmine – mainly near small pools and flowing underground streams
* Group Pattern : Swarm of 6–12 individuals
* Aggro Type : Neutral until provoked; becomes aggressive if disturbed or attacked
* Special Notes : Acts as both environmental hazard and indirect threat; triggers predator mechanics if a Golden Stream Serpent is nearby

---

		2. Appearance

	The Glowfin Trout Swarm is composed of dozens of small, shimmering fish, each no larger than a player’s forearm. Their scales refract the dim underground light 
in faint, iridescent patterns that pulse rhythmically, creating a living, flowing tapestry in the streams. Their fins are long, almost filament-like, trailing 
behind them like underwater ribbons, giving the illusion of motion even when still. When they move in unison, the swarm undulates like liquid silver, making it 
difficult to target individual trout. Their mouths are small but sharp, designed for nipping rather than heavy damage, and their eyes glimmer faintly with 
bioluminescent light, giving an eerie glow in darker water pockets.

---

		3. Core Attributes

* HP Bar : Small
* Defense / Armor : Low
* Evasion / Dodge : High
* Movement Speed / Mobility : Very High in water, Medium on wet rocks or shallow streams
* Chase / Leash Radius : N/A (swarm disperses rather than chases long distances)
* Attack Speed : Medium
* Damage Type : Physical (minor bite)
* Damage Pattern : Direct, nipping multistrike, small but cumulative if multiple fish hit simultaneously
* Core Status Mechanics : Dispersal on area-of-effect attacks; triggers secondary environmental hazards if agitated 
(water turbulence, pushing players toward edges or hazards)
* CC Resistances : Slight resistance to knockback and push effects due to swarm dispersion

---

		4. Combat Behavior / AI

| State                 | Description                                                                                                                                            |
| Idle / Patrol         | Moves in synchronized schools, following water currents. Slight ripple effects alert nearby players to their presence.
| Detection             | If a player enters their immediate water area or attacks one fish, the entire swarm becomes agitated.
| Combat                | Swarm breaks into multiple clusters, nipping at players’ feet or legs. Each cluster targets a separate player when possible.
| Enrage / Phase Change | When 50% of the swarm is killed, remaining fish disperse rapidly, creating water turbulence that slows player movement and reduces						| visibility. 
