			Stillness Reward

		Concept

	The Northern Ponds are a place where wildlife constantly evaluates danger.

	Most players arrive, move, gather, fight, and leave.

	But the pond reveals its secrets only to those who do nothing.

	Stillness here is interpreted by nature as safety.

---

		Mechanic	

	When a player remains completely still near the pond edge (no movement, no skills, no camera turning) for 8–12 seconds :
* The environment begins to change subtly around them.
* This only works out of combat.

---

		What Happens During Stillness

	Fish Return to Shore :
* Fish silhouettes slowly approach the shoreline
* Fishing chance slightly increases
* Rare species become temporarily available

The water “trusts” the player.

---

	Small Creatures Emerge :
* Frogs, insects, and tiny critters appear from vegetation
* Some are lootable for minor alchemy or crafting materials
* Normally hidden unless stillness is achieved

---

	Hidden Herbs Become Noticeable :
* Glowleaf and Rainfern near the player emit slightly stronger glow
* Easier to spot in vegetation clutter
* Occasionally, an extra herb node spawns nearby

---

		Temporary Player Buff — "Pond’s Acceptance"

	[Pond’s Acceptance]

How to get: Remain motionless near pond edge for ~10 seconds out of combat
Effect: +Fishing Luck, +Herbalism Detection Radius
Duration: 2 minutes
Suffer debuff: Breaks immediately if player sprints or enters combat
UI Icon: Soft water ripple circle

---

		Visual & Audio Feedback

	Players notice :
* Ambient sounds become clearer
* Water ripples calm
* Subtle glow intensifies on nearby plants
* Wildlife sounds increase

	No UI prompt explains it.

	Players feel the change.

---

		Failure Condition

	If the player :
* Moves suddenly
* Enters combat
* Uses loud abilities

	The effect instantly breaks and wildlife retreats again.

---

		Purpose

* Rewards patience in a game that promotes constant action
* Encourages immersion and observation
* Makes the ponds feel spiritually alive
* Creates a secret interaction players discover organically

	In this place, doing nothing is an action.
