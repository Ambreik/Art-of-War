			Freshwater School Behavior

		Concept

	Fish in the Northern Ponds are not passive ambient life like coastal schools.

	These are territorial, alert freshwater species that evolved in tight ponds, shallow pools, and predator-rich vegetation.
	They react to movement, shadows, splashes, and combat vibrations above the water.

	The water here is alive and aware.

---

		Mechanic

	Fish schools inside the ponds constantly evaluate what is happening above the surface.
	They respond to three player behaviors :

	1. Sudden Movement Near Water Edge

	If a player sprints, fights, or lands heavily near the pond edge :
* Fish schools scatter rapidly
* Fishing success chance drops for a short time
* Water surface shows visible disturbance trails

	Slow movement keeps fish calm.

---

	2. Player Shadow Over Water

	When a player stands at the edge casting a shadow into the pond :
* Fish slowly drift away from that shoreline
* Fishing from that position becomes less effective
* Moving to a different angle restores chances

	Players learn to reposition naturally.

---

	3. Combat or Loud Activity Near Ponds

	If players fight, mine, burn Shadowmoss, or cause repeated disturbances nearby :
* Fish retreat into deeper pond areas
* Some species temporarily disappear from catch table
* Rare fish become impossible to catch until water "calms"

	If the area stays calm for some time, fish behavior returns to normal.

---

		Fishing Interaction Layer

	Players who :
* Walk instead of sprint
* Fish from low rocks, logs, or crouched terrain
* Avoid fighting near water

	Will experience noticeably better fishing results without any UI explanation.
	Observation is rewarded.

---

		Visual Feedback

	The system is readable without UI :
* Ripples when fish scatter
* Shadows visible on water surface
* Fish silhouettes moving away
* Occasional surface splashes when alarmed

	The pond visibly reacts.

---

		Fish Species to Add in Area Info (Resources → Fishing)

	Add these freshwater species to the Northern Ponds resource list :
* "Silverstream Minnow" — Common schooling fish, highly reactive to movement
* "Broadfin River Carp" — Medium fish that retreats to deeper water when disturbed
* "Spotted Glass Perch" — Semi-rare, only catchable when water is calm
* "Northern Pondrunner" — Fast schooling fish, impossible to catch during disturbances

	These exist only in this POI.

---

		Purpose

* Prevents brainless AFK fishing
* Makes fishing feel like hunting, not waiting
* Connects player movement with aquatic life
* Makes the ponds feel sensitive and alive
* Encourages calm, attentive behavior in a peaceful-looking area

	Players who treat the pond gently are rewarded.
