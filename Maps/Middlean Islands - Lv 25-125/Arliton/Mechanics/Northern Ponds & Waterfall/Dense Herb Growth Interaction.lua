			Dense Herb Growth Interaction

		Concept

	This POI is one of the richest herbalism locations on the island for a reason.

	The humidity, constant freshwater flow, filtered sunlight, and mineral-rich soil create overgrown, layered vegetation where herbs do not grow cleanly apart from
one another. They grow entangled.

	Here, herbs are not just resources.
	They physically affect movement, vision, and combat space.

	Players are not walking through plants.
	They are walking inside vegetation.

---

		Where It Happens

	This occurs in :
* Thick patches around pond edges
* Between clusters of "Glowleaf", "Rainfern", and "Shadowmoss"
* Along the stream banks
* Near fallen logs and shaded rock walls
* Around the waterfall’s outer mist zone

	These are visibly denser, greener, slightly overgrown zones.

---

		Baseline Effect (Always Active in These Patches)

	When a player walks through dense herb clusters :
* Movement speed slightly reduced (soft slow, not a debuff icon)
* Footstep sound becomes muffled
* Vision slightly obscured at ground level
* Character model partially hidden by vegetation

	It feels like pushing through thick plants.

---

		Gathering Interaction (Important Layer)

	When a player gathers an herb from inside these patches :

* Nearby vegetation shifts and opens briefly
* Hidden ground becomes visible
* Small insects, frogs, or critters scatter (visual life)
* Sometimes reveals :
	* Hidden extra herb node
	* Small lootable natural container (roots, bark, sap)
	* A safe footing path through slippery rock (synergy with Mechanic 1)

	Gathering becomes environment interaction, not a click action.

---

		Combat Interaction

	Fighting inside dense herb zones changes behavior :
* Small creatures can hide and ambush easier here
* Ranged attacks have slightly reduced clarity (visual obstruction)
* Some creatures will retreat into herbs to reposition
* Players who backpedal may unknowingly step into thicker vegetation and slow themselves

	The plants create soft terrain control.

---

		Hidden Depth — Herb Layering

	Because "Glowleaf", "Rainfern", and "Shadowmoss" grow together here:
	Disturbing one type can influence the others.

	Examples :
* Stepping on Shadowmoss here may trigger spores (ties to earlier mechanic)
* Burning Shadowmoss with fire slightly clears vegetation temporarily
* After heavy combat, patches look trampled for a short time

	The environment remembers interaction briefly.

---

		Visual Indicators

	Players can learn this naturally :
* Overlapping leaves and ferns
* Taller-than-normal herb models
* Audible rustling when walking
* Fireflies or moisture particles hovering

	It looks alive and thick.

---

		Purpose

* Makes herbalism immersive and physical
* Adds natural terrain-based slow without debuffs
* Creates soft ambush zones for future creatures
* Rewards careful movement and observation
* Connects resource gathering with environmental gameplay

	This POI starts to feel like a living freshwater jungle, not a static map.

