			Mist Veil Pockets

		Concept : 

	The constant crash of the waterfall fills the surrounding ponds with drifting curtains of mist.

	This is not just ambience.

	The mist collects, moves, and thickens in certain pockets between rocks, roots, and vegetation, forming natural visual veils where things can remain hidden in 
plain sight.

	Players are not meant to immediately notice this.
	They slowly realize that *some mist is different*.

---

		Mechanic

	Across the pond edges and waterfall base, several "Mist Veil Pockets" naturally form and dissolve over time.

	These pockets :
* Slightly distort vision when looking through them
* Obscure what lies inside unless the player walks into the mist
* Shift location slowly with time (wind + water pressure influence)

	They are environmental concealment zones.

---

		What Mist Can Hide

	When a Mist Veil Pocket is active, inside it may be :

	Hidden Resources

* Extra clusters of "Glowleaf"
* Patches of "Rainfern"
* Rare herb spawns that do not appear outside mist

	Players who walk into mist are rewarded with unexpected finds.

---

		Hidden Creatures

	Certain creatures prefer mist cover and only spawn or idle inside these pockets :
* Passive amphibians
* Rare pond insects
* Small predators waiting for prey

	From outside, they are almost invisible.

---

		Hidden Collectibles

	Occasionally :
* Washed-up containers
* Lost tools from prospectors upstream
* Small lootables or crafting curiosities

	Only visible when inside the mist.

---

		Player Interaction

	Players who :
* Avoid mist because they think it’s visual clutter → miss resources
* Walk through mist deliberately → discover secrets

	Torch or light sources slightly reduce the visual distortion, but do not fully remove it.
	This keeps the mystery intact.

---

		Dynamic Behavior

	Mist pockets are not static :
* They relocate slowly over time
* They become denser during early morning and late evening
* During midday, they thin out and reveal less

	Time of day subtly affects discovery chances.

---

		Visual Feedback

	Players learn to recognize Mist Veil Pockets by :
* Thicker swirling mist close to ground
* Slight shimmer effect in the air
* Sound dampening when inside
* Water droplets visibly condensing on screen edges

	No UI. Pure environmental reading.

---

		Purpose

* Encourages exploration inside what looks like simple visual fog
* Rewards curiosity and slow movement
* Makes herbs and small creatures feel naturally hidden
* Turns a visual effect into a gameplay layer
* Creates discovery moments without quests or markers

	The mist is not decoration.
	It is nature’s curtain hiding quiet secrets.

