			Slippery Wet Terrain

		Concept

	The constant spray from the waterfall, the humidity, and the never-dry stone surfaces turn parts of this POI into naturally hazardous footing. This is not a 
trap. It is not obvious. It is simply the environment behaving like wet rock should.

	Players who rush, fight, or sprint across rocks will feel it.
	Players who move carefully will barely notice it.

	This mechanic makes terrain itself part of gameplay without feeling artificial.

---

		Where It Happens

	This effect is present in very specific micro-locations :
* Rocks directly under waterfall mist
* Flat stone surfaces between ponds
* Moss-covered stones near **Glowleaf** patches
* Sloped rock surfaces where water flows thinly over them
* Stream crossing stones players use as shortcuts

	Not everywhere — only where it visually makes sense.

---

		Trigger Condition

	When a player :
* Sprints
* Dodges
* Fights
* Is pushed / knocked
* Turns rapidly while moving

…while standing on slippery rock.

	Walking slowly or standing still does not trigger it.

---

		What Happens (Effect)

	The player experiences a brief loss of footing :
* Short uncontrolled slide (1–2 meters)
* Small stamina drain
* 0.5s balance recovery animation
* If near edge → may slide into shallow water pool

	No damage. No punishment. Only positional inconvenience.
	During combat, however, this becomes meaningful.

---

		Combat Interaction

	If fighting near these rocks :
* Knockbacks become dangerous
* Dodging sideways may cause a slide instead
* Root-based creatures or mechanics become stronger here
* Players may slide into water, changing fight positioning

	This makes the terrain tactically relevant without adding difficulty artificially.

---

		Visual Indicators (Very Important)

	Players can learn to read it :
* Glossy stone reflections
* Thin water flow over rock
* Moss shine
* Water droplets splashing continuously
* Slight footstep splash sound when stepping

	Experienced players will recognize safe vs unsafe footing.

---

		Interaction With Other Mechanics (future synergy)

	This mechanic will later combine beautifully with :
* Root-Tangle Ground (players slide into roots)
* Mist Veil Pockets (reduced visibility + slippery ground)
* Stillness Reward (players learn to slow down here)

---

		Purpose

* Makes the environment feel real and physical
* Teaches careful movement
* Adds depth to herbalism and gathering routes
* Makes combat positioning dynamic without adding mobs
* Rewards observation over rushing

	This is environmental gameplay, not a hazard.

