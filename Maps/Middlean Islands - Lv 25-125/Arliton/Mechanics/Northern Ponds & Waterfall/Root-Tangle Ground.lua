			Root-Tangle Ground

		Concept

	Around the Northern Ponds and waterfall basin, ancient trees have pushed their roots outward over decades, forming a chaotic lattice across the moist soil.

	What looks like natural vegetation is, in reality, a living obstacle field that punishes careless movement—especially during combat.

	This ground was never meant for fast fighting.

---

		Mechanic

	Certain areas around the ponds are flagged as "Root-Tangle Zones".
	While walking calmly, players move normally.
	But during combat behavior, the roots begin to matter.

		Triggers

	The snare risk increases when players :
* Strafe or backpedal repeatedly
* Sprint through vegetation while in combat
* Get knocked back or dodge frequently
* Fight while standing inside dense root patches

		Effect

	When triggered :
* Player gets briefly "Snared" (rooted for ~1.5–2s)
* Small stumble animation
* Minor stamina drain
* No damage, but dangerous during fights

	This cannot happen while out of combat.

---

		Escalation During Fights

	The longer a fight lasts inside a Root-Tangle Zone :
* The higher the chance of triggering the snare
* Especially if multiple players move erratically

	Players learn to control their positioning instead of panicking.

---

		Environmental Reading

	Root-Tangle Zones are readable without UI :
* Thick exposed roots on ground
* Moss-covered twisted patterns
* Slightly uneven terrain visuals

	Experienced players start recognizing safe spots instinctively.

---

		Interaction With Creatures

	Creatures native to this POI :
* Ignore the root effect completely
* May even push players into root-dense areas via knockbacks or fear

	The terrain favors the wildlife.

---

		Counterplay

	Players can reduce risk by :
* Fighting on rocks, logs, or clear mud patches
* Avoiding unnecessary sprinting
* Using elevation or natural platforms
* Luring creatures to safer footing

	This rewards terrain awareness.

---

		Purpose

* Makes positioning matter without artificial barriers
* Punishes panic movement, not skillful play
* Turns terrain into a silent combat participant
* Reinforces the feeling of an old, overgrown, untamed place

	The danger is not the enemy.
	It is the ground beneath the fight.
