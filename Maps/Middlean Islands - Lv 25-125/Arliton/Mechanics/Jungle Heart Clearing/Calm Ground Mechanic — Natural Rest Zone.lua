			Calm Ground Mechanic — Natural Rest Zone

		Concept

	The "Jungle Heart Clearing" is not only biologically unique — it feels different.
	Sunlight, open space, gentle wind through tall grass, and the absence of oppressive canopy create a place where both animals and players instinctively lower 
their guard.
	This is the only location on the island that behaves like a natural sanctuary, without being a town, camp, or safe zone.

---

		Mechanic

	When players are out of combat and remain :
* Sitting
* Resting
* Standing idle without hostile actions

	for a short duration (~6–8 seconds), they enter a "Calm Ground State".

	While in this state :
* HP regeneration is noticeably increased
* Stamina regeneration is increased
* Mount ability cooldowns recover slightly faster
* Negative minor debuffs (poison, bleed, slow, fear residue, vision blur, etc.) fade more quickly

	This effect persists only while remaining inside the clearing and out of combat.
	The moment combat starts or the player leaves the clearing, the effect ends.
	No UI buff is shown — the player simply feels the difference.

---

		Environmental Feedback

	Subtle cues reinforce the feeling :
* Softer ambient sound
* Wildlife walking calmly nearby (ties into Wildlife Confidence)
* Warm sunlight tone across the ground
* Reduced aggressive creature density compared to jungle

	Players unconsciously learn :
	
> "If I’m in trouble, I can retreat here to recover."

---

		Interaction With Other Mechanics

	This mechanic complements :
* "Sun Saturation" → players move between shade and light while resting
* "Wildlife Confidence" → animals are calm unless provoked
* "Sunblossom Bloom Cycle" → herbalists prefer to gather and rest here during bloom hours

This POI becomes a natural regrouping location for adventurers without any artificial safe-zone rules.

---

	Purpose : 
* Provides emotional and gameplay contrast to hostile jungle and caves
* Creates an organic resting hub without NPCs or structures
* Encourages players to recognize and remember the location
* Reinforces the identity of the Jungle Heart as a living, breathing sanctuary on Arliton

	Here, players don’t feel safe because the game tells them.
	They feel safe because the world does.

