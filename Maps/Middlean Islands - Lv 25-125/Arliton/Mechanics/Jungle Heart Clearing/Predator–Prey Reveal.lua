			Predator–Prey Reveal

		Concept

	The Jungle Heart Clearing is wide, bright, and open enough for players to see nature happening in real time.
	Unlike dense jungle where hunts are hidden in foliage, here the entire chase unfolds in plain sight.
	This is not scripted combat.
	This is the ecosystem performing its natural cycle.

---

		Mechanic

	At random intervals, a "Predator–Prey Event" begins somewhere in the clearing.
	A prey creature suddenly sprints across the open field, followed seconds later by its predator in full pursuit.
	The chase path cuts visibly through the clearing, often passing near players.

---

	How the Event Unfolds : 
1. Prey runs in panic across open terrain
2. Predator follows at high speed
3. The chase ignores players unless they interfere
4. If uninterrupted, the predator eventually catches and kills the prey

	This happens dynamically and in different parts of the clearing each time.

---

	If Players Do Nothing

	When the predator succeeds :
* Predator stops and feeds
* Enters "Satiated State" for a short time
* Becomes non-aggressive and slow
* Can be safely observed or bypassed

	Players who simply watch are rewarded with a safer environment temporarily.

---

	If Players Interfere

	If a player :
* Attacks the predator
* Blocks its path
* Damages the prey
* Uses abilities near the chase

	The predator immediately switches aggro to the player.

	The natural event turns into a dangerous encounter.

---

		Environmental Impact

	During the chase :
* Nearby animals enter alert behavior (ties into Wildlife Confidence mechanic)
* Birds may take flight
* Flora may rustle as creatures pass through
* The clearing feels alive and reactive

---

		Visual & Audio Feedback

	Players recognize the event by :
* Sudden movement in distance
* Distinct running sounds
* Prey distress calls
* Predator growls

	This draws attention naturally without UI.

---

	Purpose : 
* Makes the clearing feel like a real ecosystem
* Encourages players to observe instead of attack
* Creates dynamic, unscripted world moments
* Rewards patience with safer traversal opportunities

	Here, players are not the center of attention.
	They are witnesses to nature’s routine.


