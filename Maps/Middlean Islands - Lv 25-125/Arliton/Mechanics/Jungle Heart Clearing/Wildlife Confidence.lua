			Wildlife Confidence

		Concept

	In most of "Arliton", animals are cautious, skittish, and defensive because the jungle is dense and dangerous.

	The "Jungle Heart Clearing" is different.

	Here, visibility is high, sunlight is abundant, and predators are few.
	Animals have grown confident, curious, and less afraid of movement.

	They do not immediately assume danger.
	They observe first.

---

		Mechanic

	Creatures native to the clearing do not instantly aggro or flee when a player enters their proximity.

	Instead, they enter a short "Observation State".

	For 3–5 seconds, they :
* Turn toward the player
* Watch
* Slowly reposition
* Continue grazing, walking, or idling

	Only after this window do they decide how to react.

---

		Possible Reactions

	Depending on player behavior during the Observation State :

	Player remains calm / does not attack
* Some creatures ignore the player
* Some slowly approach out of curiosity
* Passive creatures may walk very close without aggro
* Wildlife sounds increase slightly

	The clearing feels alive and unafraid.

---

	Player attacks immediately : 
* Target creature reacts normally
* But nearby creatures notice

	This triggers the second layer of the mechanic.

---

	Escalation — Clearing Turns Hostile

	If players kill or attack multiple creatures in a short time window :
* Nearby wildlife shifts behavior
* Observation State disappears
* Creatures become immediately aggressive on sight
* Some species may move toward the disturbance instead of away

	This hostile state lasts for a few minutes.

	After no aggression, wildlife returns to confident behavior.

---

		Environmental Feedback

	Players can read the state of the clearing by :
* Animal posture (relaxed vs alert)
* Increased or reduced wildlife sounds
* Creatures watching from a distance
* Movement patterns becoming more tense

	No UI indicator.
	The jungle’s mood changes.

---

		Interaction With Other Mechanics

	During Hostile State :
* Sun Saturation becomes more dangerous because fights happen in open areas
* Creatures may chase players into exposed sunlight
* Predator–prey events stop occurring temporarily

	The ecosystem shifts from harmony to alarm.

---

	Purpose : 
* Makes player behavior influence the entire area
* Creates emotional feedback from the environment
* Encourages restraint and observation
* Reinforces the feeling that this clearing is a living habitat, not a combat zone

	In this place, animals are not afraid of you.
	Until you teach them to be.

