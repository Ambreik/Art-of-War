			Sun Saturation

		Concept : 

	The Jungle Heart Clearing is the only place on "Arliton" where the canopy breaks completely and sunlight dominates the ground for most of the day.
	The plants here thrive because of it.
	Players are not adapted to it.
	This mechanic is not about heat damage — it is about exposure, fatigue, and light pressure.

---

		Mechanic

	When a player stands or fights in direct sunlight (areas with no tree shade, tall flora, or rock cover), they begin accumulating "Sun Saturation".
	This starts after ~3 minutes of continuous exposure.
	The effect builds gradually, not instantly.

---

	Sun Saturation Stages : 

	Stage I — Warm Exposure
* Slight stamina regeneration reduction
* Very subtle screen brightness increase
* No danger, only discomfort

	Stage II — Light Fatigue
* Minor stamina drain while moving or fighting
* Slower HP/Stamina regen
* Slight heat-haze visual effect on screen edges

	Stage III — Overexposed
* Noticeable stamina drain during combat actions
* Shorter sprint duration
* Mild blur from brightness
* Player feels pressured to seek shade

	This never kills the player.
	It pressures repositioning.

---

	How to Remove Sun Saturation

	The effect rapidly fades when the player moves into :
* Tree shade
* Tall dense flora patches
* Rock shadows
* Edges of the clearing where canopy returns

	After 5–6 seconds in shade, the effect starts dropping quickly.

---

	Combat Interaction

	During fights in the open :
* Players who stand still in sun too long get punished
* Players who use terrain, trees, and shade fight more efficiently

	This subtly teaches positional awareness.

---

	Environmental Reading

	Players can visually understand safe zones :
* Clear bright grass = exposure
* Flower patches, bushes, trees = protection
* Rock shadows = safe rest spots

	No UI is required.

---

	Time of Day Influence : 
* Strongest effect at midday
* Much weaker during morning/evening
* Almost absent at night

	The clearing feels very different depending on time.

---

	Purpose : 
* Turns sunlight into a gameplay element
* Encourages natural movement across the clearing
* Rewards players who use terrain intelligently
* Reinforces that this POI is defined by light, not darkness

	In this place, the enemy is not the wildlife.
	It is the sky above.

