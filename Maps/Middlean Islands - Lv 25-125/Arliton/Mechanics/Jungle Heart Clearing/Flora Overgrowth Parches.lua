			Flora Overgrowth Patches

		Concept

	The "Jungle Heart Clearing" is where sunlight allows vegetation to grow wildly and densely without the competition of towering canopy trees.
	Here, "Sunblossom", thick flowers, tall grasses, and lush undergrowth form natural overgrowth carpets that are beautiful to look at—but inconvenient to move 
through.

	This flora is not decoration.
	It is living terrain.

---

		Mechanic

	Certain areas of the clearing are covered in "Flora Overgrowth Patches" — dense clusters of flowers, tall grass, and Sunblossom growth.

	When players walk through these patches :
* Movement speed is slightly reduced
* Footsteps become muffled
* Visibility at ground level becomes cluttered

	The player feels like they are pushing through vegetation, not walking on clear ground.

---

		Hidden Elements Inside Patches

	These overgrowth zones often conceal :

	Herbalism Opportunities
* Higher chance for "Sunblossom" spawns
* Occasional hidden Glowleaf or minor herbs beneath the flowers
* Herbs are harder to see unless the player enters the patch

	Herbalists are naturally rewarded for walking into the vegetation.

---

	Small Creatures and Insects : 
* Tiny critters, insects, or passive animals live inside
* Some may be lootable for minor alchemy or crafting materials
* Others scatter when disturbed, adding life to the patch

---

	Pollen Release Interaction

	When players :
* Sprint through
* Fight inside
* Roll, dodge, or get knocked into the patch

	The flowers release a soft pollen cloud.

	This causes :
* Brief visual obstruction (light haze)
* Increased brightness from reflected sunlight in pollen
* No damage or debuff, only disorientation

	The effect lasts a few seconds and fades naturally.

---

		Combat Implication

	Fighting inside Flora Overgrowth is inconvenient :
* Slight slow from vegetation
* Vision clutter from pollen
* Creatures native to the clearing ignore these penalties

	Players learn to either pull enemies out or accept the disadvantage.

---

		Environmental Reading

	Players recognize these patches by :
* Extremely dense flower visuals
* Tall grass and heavy color saturation
* Noticeable Sunblossom presence

	They become recognizable terrain features over time.

---

	Purpose : 
* Turns vegetation into interactive terrain
* Rewards herbalism and exploration
* Adds mild inconvenience without frustration
* Makes the clearing feel rich, alive, and overgrown by light-fed life

	Here, the flowers are not just scenery.
	They are part of the ground you must navigate.


