			Herb Bloom Cycle — Sunblossom

		Concept

	The "Jungle Heart Clearing" is the only place on the island where "Sunblossom"	 grows under uninterrupted, direct sunlight for most of the day.
	Here, the plant does not behave like a static herb node.
	It follows a daily bloom cycle tied directly to the intensity of the sun overhead.
	Players who pay attention to time of day are rewarded.

---

		Mechanic

	During specific daytime windows (late morning to mid-afternoon in game time) :
* Sunblossom flowers physically open
* Petals stretch outward and emit a soft golden glow
* The plant becomes highly visible even from distance

	This is the "Bloom State".

	During Bloom State :
* Herbalism yield from each node is increased
* Harvest time is slightly reduced (easier gathering)
* Chance to obtain "Rare Sunblossom Variant" appears
* Surrounding flora appears slightly more vibrant

	As evening approaches :
* Petals slowly close
* Glow fades
* Plant returns to its normal, common herb behavior

	At night, Sunblossom looks almost dormant.

---

		Rare Variant Spawn

	Only during Bloom State :

* Some Sunblossom nodes are replaced by a "Radiant Sunblossom"
* Visually brighter, slightly taller, with intense golden hue
* Provides rare alchemy and energy-based crafting material

	This variant cannot appear outside the bloom window.

	Players gradually learn :

> "If I want the good Sunblossom, I come here when the sun is strongest."

	No UI, no timers — only observation.

---

		Environmental Integration

	This mechanic ties directly with :

* "Sun Saturation" (players already moving between light and shade)
* "Flora Overgrowth Patches" (flowers hiding nodes)
* Open visibility of the clearing (players can scan for glowing blooms)

	The entire clearing subtly changes tone during bloom hours.

---

	Purpose : 
* Introduces meaningful time-of-day gameplay for herbalists
* Makes a herb node feel alive and cyclical
* Rewards knowledge of environment rather than map markers
* Reinforces that this clearing is botanically unique on the island

	In this place, herbs don’t just grow.

	They wake up with the sun.

