			Ancient Carvings Respond to Light

	Concept : The temple carvings are not decorative — they are part of an old ritual system that reacts to illumination.

	Mechanic : 
* At night, carvings on pillars, walls, and fallen slabs emit a faint bioluminescent shimmer.
* If a player uses a "Torch", "Lantern", or fire-based spell within close range:
	* The symbols flare brightly for several seconds.
	* Nearby vines recoil and retract from the stone surface.
	* Small hidden paths, cracks in the wall, or overgrown alcoves briefly open.
* These openings may reveal :
	* Loot spots
	* Shortcuts
	* Hidden chests
	* Lore fragments

	Rules : 
* Works only during night cycle or heavy shade/rain darkness.
* Retraction lasts a short time before vines slowly reclaim the space.
* Not every carving triggers it — only specific ritual-marked ones.

	Purpose : 
* Rewards player observation and curiosity
* Encourages carrying light sources in dense jungle areas
* Makes the environment feel reactive rather than static
* Feels organic and logical, not like an artificial puzzle system

