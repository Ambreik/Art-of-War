			Glowing Flora Pollen

---

	Concept : 
* The glowing flora covering the Ruined Temple is part of the same plant ecosystem as its monsters.
* When disturbed, it releases reactive pollen that turns the battlefield into a living, shifting hazard.

---

	Mechanic : 
* When plant-type monsters die, they burst into a visible pollen cloud.
* These clouds linger in the air and slowly drift with the natural wind currents of Arliton.
* Multiple clouds can overlap, creating denser zones.

---

		Pollen Effects

	On Plant Creatures

	Plant monsters standing inside pollen clouds :
* Gradually regenerate health
* Gain a minor attack speed increase
* Become slightly more aggressive in behavior radius (they “sense” nourishment)

	This makes killing monsters in tight spaces potentially dangerous if the area is not managed.

---

		On Players

	Players standing inside pollen clouds experience escalating effects based on exposure time :

| Time in Cloud | Effect                                                        |
| ------------- | ------------------------------------------------------------- |
| 1–3s          | Slight screen blur, soft glow on edges                        |
| 4–6s          | Movement speed slightly reduced, muffled sound                |
| 7–10s         | Short disorientation (minor control delay / sluggish turning) |
| 10s+          | Brief cough / stagger animation that interrupts actions 	    |

	This does not deal damage.
	It incapacitates through discomfort, not harm.

---

	Wind Interaction (Arliton Environmental Link) : 
* Wind slowly pushes pollen across the temple grounds.
* Players may think an area is safe, only for pollen to drift into it.
* Strong gusts can suddenly clear an area… or flood it.

	The battlefield never stays the same for long.

---

	Player Counterplay : 
* Moving out of the cloud clears effects quickly.
* Elevated stone, broken pillars, or stairs are often above pollen density.
* Fire abilities or torches briefly disperse pollen in a small radius.

---

	Result : 
* Combat space becomes dynamic, organic, and messy.
* Players must be aware of air, not just ground.
* Encourages repositioning and spatial awareness.
* Reinforces the idea that the temple ecosystem itself fights back.
* Naturally synergizes with :
	* Living Temple Ground
	* Light-reactive carvings
	* Plant-type enemies
	* Wind mechanics of Arliton

	The air of the temple is as alive as the floor.

