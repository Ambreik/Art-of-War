			Temple Awakening State — Escalation Mechanic

---

	Concept : 
* The Ruined Temple is not a static farming spot.
* It notices repeated disturbance.

	The more players harvest, clear, and kill the plant ecosystem, the more the temple shifts from dormant ruin into defensive organism.

---

	Mechanic : 
* The temple tracks cumulative plant-creature deaths within its bounds over a rolling time window.
* As the count rises, the temple moves through "Awakening Tiers".

---

	Tier 0 — Dormant Temple (Default)
* Normal vine behavior
* Standard monster spawns
* Living Temple Ground at base intensity

---

	Tier 1 — Stirring Roots

	Triggered after moderate sustained kills.
* Vines appear thicker on walls and rubble
* Living Temple Ground roots trigger faster
* Slight increase in plant monster spawn rate

	Players begin to feel the place getting hostile.

---

	Tier 2 — Defensive Growth

	Triggered after heavy farming.
* Additional roots visible across the floor (larger rooting zones)
* Increased chance for "Temple Rootbound Strangler" (Elite) to spawn
* Pollen clouds linger longer before dispersing
* Ambient sound changes (deeper root creaks, plant movement)

	The temple is now actively resisting intrusion.

---

	Tier 3 — Awakening State

	Triggered after prolonged, intense clearing.
* High elite spawn frequency
* Living Temple Ground becomes very aggressive
* Nutrient patches appear more often
* Visual overgrowth intensifies across the ruins
* Chance to trigger conditions that lead toward the Boss emergence

	At this stage, the POI feels completely different from its default state.

---

	Decay / Calm Down

	If players stop killing plant creatures for a period of time :
* The temple slowly regresses through tiers
* Vines recede
* Root activity reduces
* Spawn rates normalize
* Ambient tension fades

	The temple “heals” itself.

---

	Purpose : 
* Prevents the POI from feeling like a static grind spot
* Makes repeated farming increasingly dangerous
* Creates a natural path toward Elite and Boss presence
* Makes the world feel reactive to player behavior
* Reinforces the idea that the temple is a living organism, not scenery
