			Flora Feeds on Death

---

	Concept : The plant life in the temple does not simply grow.

	It feeds.
	Every corpse left on the temple ground becomes nutrients for the underground root network controlled by the same organism type as the "Vine Strangler Plant".
	If enough biomass is provided in a short time, the network reacts by growing a larger defensive organism.

	This is not magic.
	This is accelerated plant predation.

---

		Mechanic Flow

	Step 1 — Nutrient Patch Creation

	Whenever a plant-type monster dies inside the temple bounds :
* A "Nutrient Patch" appears on the ground
* Visible as dark green, wet, root-infested soil
* Lasts 20 seconds

	This is not loot. It is environmental.

---

	Step 2 — Patch Accumulation Check

	If "X patches" exist simultaneously within temple bounds before older ones expire :
* They begin to :
	* Send visible roots toward each other
	* Merge into a single point

	Players can literally see this happening.

	This forms a "Bloom Node".

---

	Step 3 — Bloom Node Formation

	The Bloom Node is :
* A thick knot of roots rising from the ground
* Pulsating plant mass
* Targetable but very high HP (players usually don’t kill it in time)

	After ~6 seconds :
* It erupts.

---

		Resulting Spawn

	Most of the time it spawns :

	1. Elite : "Temple Rootbound Strangler", a grown, evolved version of the "Vine Strangler Plant".

	Rarely, if the accumulation was very fast (players cleared extremely efficiently):

	2. Boss: "Heart of the Overgrowth" , the central nervous mass of the temple’s plant network.

---

		What Players Learn

	Without UI, players realize :

> "If we clear these plants fast enough, something bigger grows."

	This turns normal grinding into an intentional event trigger.

---

		Important Rules

| Rule                                        | Behavior                         |
| ------------------------------------------- | -------------------------------- |
| Only plant-type deaths count                | Reinforces ecosystem logic       |
| Non-plant mobs killed here                  | Do not create patches            |
| Players can see patches                     | Understand cause/effect          |
| Slow killing                                | Nothing happens                  |
| Fast clearing                               | Event happens                    |
| Players can interrupt by killing Bloom Node | Prevents spawn (advanced tactic) |

---

		Visual Language

	This mechanic must be very readable :
* Roots crawling between patches
* Soil turning darker and alive
* Low rumbling plant sound
* Birds/insects flee the area when merging starts

	The temple looks like it is growing in real time.

---

		Synergy With “Living Temple Ground”

	These two mechanics together create brilliance :
* Players must keep moving (ground roots)
* Players must clear fast (nutrient patches)
* The more they fight, the more the temple reacts

	The POI feels alive.

---

		Lore Justification

	The Vine Strangler species is not individual plants.
	It is one organism spread across the temple through roots beneath the stone.
	Killing parts of it feeds the whole.
	If overfed → it defends itself by creating a larger body.

