			Forgotten Offering

---

	Concept : 
* The Ruined Temple was once a place of communion between druids and the living flora.
* The ritual was not words — it was offering plant life back to the source.

	This is a true "Latent Conditional World Mechanic*".
	Nothing hints at it directly. It is discovered by coincidence, curiosity, or experimentation.

---

		Hidden Trigger Conditions

	All conditions must be true at the same time :
* Night time within the temple bounds
* No plant-type monsters alive inside the temple area
* A player drops (manually discards) any herb / plant crafting item onto the central altar stone

	No prompt. No interaction icon. No feedback until it works.

---

	Sequence of Events (World Reaction)	

1. The moment the item touches the altar :
	* A low root-rumble sound plays through the ground.
2. Vines across the entire temple slowly retract from :
	* Walls
	* Pillars
	* Rubble
3. The glowing flora dims.
4. The altar stone splits open with roots pulling it apart.
5. A spectral ancient druid spirit rises from within.

The spirit is calm, translucent, and non-hostile.

---

	Spirit Interaction

The spirit does not speak through UI.
It performs a slow animation of placing a hand over the altar and channeling energy into the ground.

Players inside the temple receive one of :

* A temporary nature-attunement buff (movement through vines unhindered, reduced pollen effect, slight regen)
	or
* A rare plant-based crafting material appears on the altar

	After ~30 seconds :
* The spirit fades
* Vines slowly reclaim the temple
* The altar closes

	The dropped herb is consumed.

---

	Rules : 
* Can only occur once per night cycle
* Fails if any plant monster respawns during the process
* Players may not even realize what exactly triggered it

---

	Purpose : 
* Deep lore tie to carvings and rituals
* Rewards experimentation and observation
* Makes the temple feel ancient and reactive
* Perfect use of LCWM: hidden, logical, flavorful, not mandatory

	This is something players discover years later and say :

> "Wait… the temple reacts if you give it a plant?"

