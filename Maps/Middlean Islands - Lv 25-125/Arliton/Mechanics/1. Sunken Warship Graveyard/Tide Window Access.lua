			Tide Window Access

		Core Idea

	The "Sunken Warship Graveyard" is not a static POI.
	It breathes with the ocean.
	Players are not gated by level, keys, or mobs.
	They are gated by the sea itself.

---

		Tide Cycle Structure

	A full tide cycle runs slowly in the background (long enough that players feel it, short enough that they can plan around it).

| Tide State                  | World State                       | Player Experience            |
| --------------------------- | --------------------------------- | ---------------------------- |
| Receding Tide               | Water visibly pulling back        | Wreck shapes begin to appear |
| Low Tide (Prime Window)     | Wreck fully exposed               | Safe exploration phase       |
| Rising Tide                 | Water creeping back through hulls | Increasing tension           |
| High Tide                   | Wreck mostly submerged            | Dangerous / limited access   |

	No UI timer.
	Players learn by reading the coastline.

---

		Low Tide — Exploration Phase

	This is when the graveyard is meant to be explored.

	Players can :
* Walk on decks and ribs of ships
* Enter hull breaches
* Reach sand-buried crates
* Access captain’s quarters for Archaeology
* Stand in interior pools safely (for a time)

	Shallow interior pools form containing :
* "Moonfin Snapper"
* Hidden "Saltwater Lurker"

	* Important : Even at low tide, water is present inside ships — teaching players this place is never fully safe.

---

		Rising Tide — The Tension Phase

	This is where the mechanic becomes memorable.

	While players are inside wrecks :
* Water slowly starts flowing back into corridors
* Footing areas become slippery / wet
* Some paths that were walkable become waist-deep water
* Interior pools connect to the sea

	Players who stay too long feel :

> "I should leave now."

	But greed (crates, archaeology, fish) makes them hesitate.

---

	High Tide — The Punishment Phase

	At high tide :
* Only masts, ribs, and highest deck points remain above water
* Most interiors are completely submerged
* Movement becomes swimming

	This directly exposes players to the island’s core rule :

* "Fatigue Waters"

	Swimming between wrecks now :
* Drains stamina
* Slows movement
* Makes escape from creatures harder

	And in this state, players are exposed to :

* "Saltwater Lurker"
* Increased risk of falling into deep channels between ships

	What was an exploration zone becomes a hazard zone.

---

		Player Learning Pattern (very important)

	First visit :

> "Cool wrecks!"

	Second visit :

> "Oh… the sea is coming back."

	Third visit :

> "We should only come here at low tide."

	You never tell them.
	The ocean teaches them.

---

		Environmental Clues (No UI)

	Players can read tide state by :
* Wet sand lines moving up/down shore
* Barnacles glistening when recently submerged
* Water dripping from decks during receding tide
* Audible wave intensity changes
* Fish flopping in newly formed pools

	The world communicates the mechanic organically.

---

		Interaction With Other Mechanics

	This tide system amplifies everything else here :
* Birds are calmer at low tide, chaotic at high tide
* Crabs are more active as water returns
* Fishing is best at low tide pools
* Archaeology only realistically doable at low tide
* Night + high tide + fog = extremely dangerous navigation

---

	Design Purpose (deep level)

	This mechanic :
* Makes the Secret Location feel alive
* Makes time a gameplay resource
* Forces decision making :
	*Do I risk staying for one more crate?
* Naturally creates stories :
	* "We got trapped inside when the tide came back."

