			Barnacle Nest Reaction

		Core Idea

	The ships are no longer wrecks.

	They are artificial reefs, claimed by crustacean colonies over many years.

	Barnacles, coral growth, shells, and mineralized salt have fused wreck wood and metal into a living habitat.
	When players try to salvage, they are not opening containers.
	They are breaking into nests.

---

		What Counts as a “Disturbance”

	Any of the following actions near hull surfaces, crate piles, or scrap clusters triggers the system :
* Digging sand around wreck ribs
* Opening sealed or half-buried cargo crates
* Mining scrap metal piles fused with barnacles
* Repeated movement across dense barnacle patches
* Fighting on top of heavy barnacle growth

	The system is area-based, not object-based.

> The wreck section itself tracks disturbance.

---

		Hidden "Nest Agitation" Meter (per wreck section)

	Each wreck segment has an invisible agitation value.

| Player Action                | Agitation Gain         |
| ---------------------------- | ---------------------- |
| Open crate                   | Medium                 |
| Dig sand                     | Low                    |
| Mine scrap                   | Medium                 |
| Fight on barnacles           | Low (repeated adds up) |
| Multiple players interacting | Multiplies gain        |

	If players pause for ~20–30 seconds, agitation slowly drops.

	If they continue interacting, it escalates.

---

		Spawn Wave Pattern

	Stage 1 — Initial Response

	Small cracks and movement appear in barnacle clusters.

	Spawn:
* Groups of "Barnacle Crab" (6–10)

> These are the "workers" of the colony.

	They emerge from :
* Inside hull cracks
* Beneath scrap piles
* From under sand glued to barnacles

	They do not run from distance — they pour out of the wreck itself.

---

	Stage 2 — Defensive Response

	If players continue salvaging without clearing or waiting :

	Spawn shortly after :

* Groups of "Rustclaw Crab" (2–4)

	These are territorial defenders.
	They apply Bleed and are harder to ignore.
	Now the fight becomes meaningful, not cosmetic.

---

	Stage 3 — Colony Swarm State

	If players ignore combat and keep looting  :

* Barnacle Crab waves increase in size
* Rustclaw spawns happen faster
* Crabs begin spawning from multiple sides of the wreck

	At this point it feels like :

> "The ship is alive and trying to push us out."

---

		Interaction With Tide Mechanic

	This is where the design becomes elegant.

	During low tide :
* Spawns are manageable
* Space to fight exists

	During rising tide :
* Water begins filling the wreck
* Crabs still spawn
* Fighting space becomes tighter
* Escape routes shrink

	During high tide :
* Crabs can still spawn in submerged sections
* Players are forced into water while dealing with them
* Leads to exposure to "Fatigue Waters"

	The two mechanics combine to punish greed.

---

		Environmental Feedback (no UI)

	Players understand what’s happening through visuals :
* Barnacle clusters visibly crack and shake before spawns
* Sand shifts as crabs crawl out
* Clicking sounds and scraping from inside wood/metal
* Rust flakes falling from disturbed scrap piles

	The wreck warns before it reacts.

---

		Player Behavior This Creates

	Players naturally learn to :
* Clear area before continuing salvage
* Rotate between wreck sections instead of farming one
* Assign one player to watch for crabs while others loot
* Respect the place instead of speed-running it

	It creates organic group play without forcing it.

