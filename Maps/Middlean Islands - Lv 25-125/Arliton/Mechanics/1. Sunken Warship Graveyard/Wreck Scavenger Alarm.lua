			Wreck Scavenger Alarm

		Core Idea

	The broken decks are perches and feeding platforms for "Wreck Scavenger Bird".
	They are not randomly placed mobs.
	They are watchers.
	They treat the wreck as their territory and react to sudden disturbance, not proximity.

---

		What Triggers the Alarm

	This system does not trigger from simply walking nearby.

	It triggers from player behavior on deck surfaces :
* Sprinting across planks
* Fighting on the deck
* Opening crates on deck
* Jumping between broken hull sections
* Attacking birds directly
* Disturbing nests (small bone/shell piles)

	Slow walking does not trigger it immediately.
	Running does.
	Noise does.

---

		How Linked Aggro Works Here

	When triggered :
1. Nearby perched "Wreck Scavenger Bird" instantly take flight
2. Their Linked Aggro pulls :
	* Birds from adjacent wreck sections
	* Birds perched on masts or ribs
3. Within seconds, 6–12 birds may be circling above

	They do not land.

	They enter a dive harassment loop.

---

		Bird Behavior During Alarm State

	Once airborne :
* They circle above players
* Perform repeated dive attacks
* Reposition to masts, ribs, and broken spars
* Re-aggro if players try to ignore them and continue looting

	They behave like persistent aerial pressure, not one-time attackers.

	Players must either :
* Clear them
* Or leave the deck

---

		Night + Jungle Fog Interaction

	This is where the mechanic becomes atmospheric.

	At night, when "Jungle Fog" reduces visibility :
* Players hear wings and screeches first
* Shadows pass through fog above
* Dive attacks come from poor angles of visibility

	The birds become a sound-based threat.

	You react to audio cues, not sight.

---

		Environmental Feedback

	Before alarm triggers, players can notice :
* Birds pecking at wood or bones
* Small nests made of rope fibers and shells
* Birds watching from mast tips

	After trigger :
* Loud synchronized takeoff sound
* Rapid wingbeats overhead
* Continuous circling shadows

	The deck feels exposed and unsafe.

---

		Player Behavior This Encourages

	Players naturally learn to :
* Walk slowly on wreck decks
* Clear birds before looting
* Avoid fighting on top of decks
* Pay attention to sound at night
* Lure birds down one by one instead of triggering the flock

	It creates a stealth-like movement rule without saying so.

---

		Ecosystem Tie-In

	This mechanic uses :

* "Wreck Scavenger Bird" as the Linked Aggro trigger species

	They are already defined as chain-aggro creatures.
	Here, that trait becomes an environmental rule, not just a combat trait.

