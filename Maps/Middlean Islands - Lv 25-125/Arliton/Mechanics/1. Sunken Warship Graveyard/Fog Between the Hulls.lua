			Fog Between the Hulls

		Concept

	When night falls, the wreck field stops being a shoreline and becomes a maze of silhouettes swallowed by "Jungle Fog".
	During the day, players navigate by sight.
	At night, they navigate by memory and sound.
	The spaces between wrecks become more dangerous than the wrecks themselves.

---

		What Changes at Night

	As Jungle Fog rolls across the southern shore :
* Visibility between hulls drops heavily
* Ship ribs, masts, and tilted decks appear as dark shapes
* The waterline becomes hard to read
* Gaps between wrecks look like solid ground until approached

	Players begin misjudging distance, depth, and footing.

---

		Environmental Risk — Missteps Into Water

	Because of poor visibility :
* Players walking between wrecks can easily step into deeper channels
* Shallow reflective pools look like sand
* Sudden drops force players into swimming state

	This is where "Fatigue Waters" becomes the real threat.

	Not because players dive —
	but because they misstep.

---

		Sound Becomes Navigation

	With vision reduced, audio becomes the warning system :
* Wings flapping overhead (Wreck Scavenger Birds)
* Scratching and skittering from hull interiors (crabs and rats)
* Water sloshing inside broken ships
* Echoes bouncing between wood and ribs

	Players start moving slower, listening before advancing.
	This is a rare case where the game teaches players to trust sound over sight.

---

		Interior Shift — Drowned Deck Rat Activity

	At night, the inside of hulls changes behavior.

	Damp, dark interiors begin spawning more :
* "Drowned Deck Rat"

	They become :
* More numerous
* More active
* More likely to swarm when players enter tight hull corridors

	During the day, rats are background scavengers.
	At night, they feel like owners of the wreck interior.

---

		Spatial Disorientation

	Because every wreck looks similar in fog :
* Players lose sense of direction
* Previously safe paths feel unfamiliar
* Returning to shore becomes harder than expected

	The graveyard stops being a set of POIs and becomes a continuous dangerous field.

---

		Behavioral Impact on Players

	Players naturally start to :
* Avoid jumping between wrecks
* Move slowly and cautiously
* Stick to known routes
* Avoid interior exploration unless necessary
* Wait for daylight if they want safe salvage

	You are not forbidding night exploration.

	You are making it psychologically uncomfortable.

---

		Purpose Reinforced

	This creates a very clean contrast :
* "Day" → visual exploration, salvage, navigation
* "Night" → sound-based survival, disorientation, environmental danger

	The wrecks do not change.

