			Shadowmoss Spore Ecosystem

		Core Concept : 

	Shadowmoss is not just a harvestable herb.
	Inside the "Western Sea Caves", it exists in a defensive fungal-plant state caused by :
* Constant humidity (Rainfern environment)
* Mineral dust from Copper/Quartz veins
* Darkness and lack of airflow

	This makes the moss extremely reactive to disturbance.
	Players are not stepping on a plant.
	They are stepping on a living spore bed.

---

		How Shadowmoss Appears in the Cave

	Shadowmoss grows :
* In dark wall corners
* Along the base of mineral veins
* In cracks between rocks
* On damp cave floor near dripping ceilings

	Visually :
* Thick, dark green-black carpet
* Subtle pulsing
* Tiny faint spores floating above it (visible in torchlight)
* Looks “soft” compared to stone

	This is the visual warning. No UI needed.

---

		Trigger Conditions (What disturbs it)

	Shadowmoss releases spores when :
1. A player walks or sprints over it
2. Combat happens within ~2–3m of a patch
3. Mining a vein directly next to it
4. A creature dies on top of it
5. A player falls/stumbles onto it (from slippery terrain)

	This connects it directly with the slippery cave and mining resonance mechanics.

---

		Spore Cloud Behavior

	When triggered :
* A thick green spore cloud expands in a 3–4m radius
* Lingers for ~6 seconds
* Slowly spreads outward if no airflow
* If cave wind/tide airflow exists (from other mechanic), it drifts

	The cloud is physical space control, not just a debuff.

---

		Effects on Players Inside the Cloud

	While inside :
* Mild Poison DoT (stacking if staying)
* Slight vision blur (edges of screen haze)
* Reduced accuracy / focus (small hit chance penalty)
* Breathing sound effect intensifies (immersion cue)

	If player stays for full duration:

* Gains "Spore Intoxication" for 10s after leaving
	* Minor slow
	* Reduced stamina regen

	So the danger is not instant — it’s lingering exposure.

---

		Interaction with Fire and Light

	This is where it becomes beautiful design.

	If a player uses :
* Torch
* Lantern
* Fire spell
* Fire arrow
* Fire trap

	On a Shadowmoss patch BEFORE it is triggered :
→ The moss burns safely for ~15 seconds
→ Creates a clear safe path
→ Prevents spore release

	If burned AFTER spores release :
→ Cloud ignites briefly
→ Small fire burst (minor damage to players & creatures inside)
→ Instantly clears the cloud

	So fire is both preventive and reactive.

---

		Interaction with Creatures (Very Important)

	Cave creatures are adapted to it.

	Some creatures :	
* Do not trigger moss
* Are healed slightly by spore clouds
* Try to fight players inside moss patches deliberately

	This makes players realize:

> "They are using the environment against us."

---

		Interaction with Herbalism	

	If a player harvests Shadowmoss carefully (no sprinting, no combat nearby) :
* No cloud triggers
* Yields normal herb

	If harvested carelessly :
* Guaranteed spore release

	So even gathering requires awareness.

---

		Terrain Synergy

	Because cave floor is slippery :
* Players may stumble into Shadowmoss accidentally
* Fighting on slopes near mineral veins becomes risky
* Mining in a bad spot can force combat inside spores

	Everything links together naturally.

---

		Player Learning Curve (No Tutorial Needed)

	First time :
"Why did the air turn green?"

	Second time :
"Wait… it’s that plant."

	Third time :
"Burn it first."

	That is excellent environmental learning.

---

		Purpose (Now Fully Realized)

	This mechanic now :
* Makes Shadowmoss a true environmental actor
* Rewards light and fire usage
* Punishes careless movement and combat positioning
* Links mining, terrain, herbs, and monsters together
* Creates tactical space control inside tight caves
* Feels logical, immersive, and memorable

	This becomes something players will say :

> "Be careful of the moss in the sea caves."


