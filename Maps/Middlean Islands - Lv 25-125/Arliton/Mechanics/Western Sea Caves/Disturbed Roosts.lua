			Disturbed Roosts

	Core Concept : The ceilings of the "Western Sea Caves" are not empty rock.

	They are home to colonies of :
* Cave bats
* Hanging arthropods
* Ceiling-dwelling insectivores adapted to darkness

	These creatures are normally passive, sleeping, and unnoticed.
	Players never see them.
	Until they behave badly.

---

		What Counts as "Disturbance"

	The cave tracks a hidden "Disturbance Level" caused by :
* Mining mineral veins (loud strikes)
* Triggering Shadowmoss spore clouds
* Prolonged combat
* Fire usage inside cave
* Explosions / heavy abilities
* Multiple players moving together

	Each action adds to the disturbance value.

	This is not LCWM.
	This is a visible escalation players feel.

---

		Escalation Stages

	Stage 1 — Stirring

	Players hear :
* Screeches
* Flapping sounds
* Dust falling from ceiling

	No enemies yet. Warning phase.

---

	Stage 2 — Falling Shapes

	Small shapes begin dropping intermittently from the ceiling into nearby areas : 
* 1–2 creatures at a time join fights.

	Players start realizing :

> "The cave is reacting to us."

---

	Stage 3 — Roost Break

	If disturbance continue:
* A portion of the ceiling colony fully breaks.
* Multiple creatures drop at once into the cave section.

	Now players are fighting :
* Their original enemies
* Spore clouds
* Slippery terrain
* And falling roost creatures

	This becomes controlled chaos.

---

		Calm Down State

	If players stop mining/fighting for a while :
* Sounds fade
* No more drops
* Roost “sleeps” again

	The cave forgives.

---

		What Creatures Live in the Roost

	There are creatures that make sense living on ceilings, in humidity, in darkness, feeding on spores and insects.

	Primary Roost Creature — "Sporewing Skulkbat"

	A fungal-adapted cave bat species.

	Purpose : 
* Harassment
* Vision disruption
* Synergy with Shadowmoss clouds

	Behavior : 
* Drops from ceiling onto players
* Flies erratically in tight spaces
* Emits spore dust when hit

	They make fighting in spore clouds worse.

---

	Secondary Roost Creature — "Ceiling Creeper Arthropod"

	A long-legged cave insect/spider hybrid.

	Purpose : 
* Area denial
* Forces players to reposition

	Behavior : 
* Drops directly on top of players
* Applies slow/root briefly
* Attempts to crawl back to wall if ignored

	They punish standing still.

---

	Rare Roost Drop — "Roost Broodmother" (mini-elite)

	Very rare at max disturbance.

	A larger ceiling predator that falls with force.

	Purpose : 
* "You pushed the cave too far" moment
* Mini-elite surprise encounter

---

		Why These Creatures Work Perfectly Here

	They:
* Live on ceilings logically
* Feed on spores (Shadowmoss link)
* Hate fire/light (player interaction)
* Punish noisy mining and chaotic combat
* Make vertical space matter

	Players start looking up.
	That is excellent environmental immersion.

---

	Player Learning Pattern : 

First time: random bat drops.
Second time: more drops after mining.
Third time: players say:

> "Stop mining for a bit, let the cave settle."

	That is exactly the behavior you want to create.

---

	Purpose (Now Fully Realized)

	This mechanic :
* Prevents brainless farming
* Connects all cave mechanics together
* Makes sound/activity meaningful
* Introduces vertical ecosystem
* Adds dynamic combat without artificial spawns
* Feels organic, immersive, and memorable

	The cave is no longer a place.

---


			UI Concept — "Roost Disturbance Meter"

	Purpose : To give players ambient feedback on how noisy the cave environment has become as they mine, fight, or trigger moss spores — without intrusive UI.

	Design Philosophy

	It should :
✔ Be subtle and thematic — not a glaring bar
✔ Fit cave ambience — made of dust, echo, or bat silhouettes
✔ Change organically — not precise numbers but stages

---

	UI Location : Bottom-left of the screen, just above the chat/tooltips, small and semi transparent.

	This area is :
* close enough to monitor
* away from combat ability bars
* unobtrusive to exploration visuals

---

	UI Visual Elements

	Ambient Cave Overlay : 
* A faint dust/fog texture gradually darkens or flickers with cave noise.
* Not full screen — only in a small arc near the bottom-left.

---

		Roost Disturbance Indicator Design

	Icon Style : A small bat silhouette on a rocky bar, with three flickering soundwaves emanating upward.

	This reinforces :
* Noise
* Ceiling creatures
* Cave ambience

---

		Stage Behavior & Feedback

	Stage 0 — Calm

* Icon State : Dim, single flicker
* Subtle Visual :
	* No dust flicker, silent cave

**Hint : The cave is undisturbed
(No creatures drop)

---

	Stage 1 — Murmurs

**Icon State : Two soundwave pulses active
* Subtle Visual :
	* Very light dust flicker, slight rumble audio cue

**Hint : Ceilings stirred
Rare bat flutters overhead audio

---

	Stage 2 — Stirred

* Icon State : All soundwave arcs bright
* Subtle Visual :
	* Dust swirls, echo reverberation audio

**Hint : Roost senses activity
Small creature drops possible

---

	Stage 3 — Breakpoint

* Icon State : Pulsing glow + tiny falling rocks animation
* Subtle Visual :
	* Dust burst effect, sharp echo crack

**Hint : Roost breaking
Multiple creatures may fall

---

		Optional Add-On Feedback (Audio Only)

	A subtle increasing hum of chittering or flapping whenever the meter rises.
	No UI text, no bar — just atmosphere.

---

	This UI meets your philosophy:

> "Feels like world reacting, not game system forcing attention."

