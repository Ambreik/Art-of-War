			Rainbow Trout Schools

	Concept : The prized trout aren’t just scenery; they actively interact with the tunnels’ ecosystem.

	Mechanic : 
* Large schools of "Rainbow Trout" swim in the mine’s streams.
* Attacking or netting fish provokes nearby aquatic predators (Grotto Lurker, Shadowmoss Leech).
* Trout can alert predators : fast movement creates ripple effects, making ambushes more likely.

	Fish Panic Mechanics : 
* Large groups of trout can trigger ripple effects : 
    * Nearby predators become aggressive or reposition
    * Water currents shift slightly due to mass movement

	Resource Management :
* Overfishing temporarily reduces trout density, changing predator spawn patterns.
* Certain rare fish only appear after player has cleared predators in area → "safe fishing windows."

	Environmental Synergy :
* Certain plant monsters or fungi in tunnels react to trout movement (like spores floating in disturbed water).

	Result : 
* Players must manage environmental risk if targeting rare fish
* Reinforces the mine as a living ecosystem
* Creates mini "stealth/fishing" moments

