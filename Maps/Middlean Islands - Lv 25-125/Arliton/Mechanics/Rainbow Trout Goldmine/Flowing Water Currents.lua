			Flowing Water Currents

	Concept : The mine’s underground streams aren’t static — the water moves, affecting both players and creatures.

	Mechanic : 
* Players wading or swimming in streams are pushed slightly in the current direction, requiring skill to navigate tunnels safely.
* Heavy objects (loot, barrels) can be carried downstream if dropped.
* Certain ambush predators (like Cave Eels or Pond Serpents) use currents to launch surprise attacks from behind corners.

	Purpose : 
* Makes the water feel alive
* Forces players to adapt movement and positioning
* Adds risk/reward to exploring deep water channels

	Current Variability :
* Water speed changes based on recent player activity or nearby mine events (like explosions or cave-ins).
* Stronger currents push players faster, creating a “risk/reward” for traversing the streams quickly.

	Objects & Obstacles  :
* Floating debris (logs, barrels, broken carts) can block currents or be used as temporary platforms.
* Players can manipulate these to create bridges or trap predators.

	Environmental Combat Integration :
* Some aquatic monsters ride the currents to launch surprise attacks.
* Certain attacks or AoE spells can temporarily slow or divert water flow, giving tactical options.

