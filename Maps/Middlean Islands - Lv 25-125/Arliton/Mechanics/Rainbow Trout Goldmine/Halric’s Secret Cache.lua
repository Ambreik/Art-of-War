			Halric’s Secret Cache

	Concept : Halric hides rare gold nuggets and enchanted trout eggs deep in the mine. Not obvious to players.

	Hidden Trigger Conditions : 
* Player reaches deepest excavation chambers
* Night or low-light conditions
* Player leaves a mining tool near a "marked rock" (subtle visual cue)

	Result : 
* Hidden door slides open
* Rare loot cache appears: gold nuggets, rare trout eggs, or crafting materials
* May trigger a "Guardian Creature" (medium-level threat, soloable)

	Purpose : 
* Adds hidden lore tie to Halric’s obsession
* Encourages exploration and curiosity
* Provides surprise reward without UI hints

