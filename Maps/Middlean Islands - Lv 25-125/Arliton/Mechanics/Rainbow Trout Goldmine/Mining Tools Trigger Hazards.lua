			Mining Tools Trigger Hazards

	Concept : Prospector Halric’s tunnels are old and unstable; interacting with equipment or mining veins can have consequences.

	Mechanic : 
* Players swinging pickaxes or using explosives near unstable ore veins can trigger minor cave-ins :
	* Falling debris damages players
	* Creates temporary blockages that alter tunnel navigation
	* Occasionally reveals hidden ore pockets or trout pools
* Some interactive points (abandoned mining carts, sluices) can be pushed or rotated to access loot or shortcuts.

	Purpose : 
* Encourages engagement with the environment
* Rewards clever exploration and risk-taking
* Adds natural tension for mining-based gameplay

	Mining Tool Effects Vary :
* Pickaxe → minor cave-in / rockfall
* Explosives → larger cave-in, can reveal hidden ore pools or trout pockets
* Water bucket → diverts minor streams, can affect aquatic monster positioning

	Dynamic Miner NPC Response :

* Nearby hostile or neutral miners notice activity:
	* Rogue miners may appear if too much ore is disturbed
    * Halric may comment ("Careful with that pick, friend!")
* Adds lore immersion and subtle risk escalation

	Chain Reaction :
* Collapsing rock can alter mine layout temporarily, block paths, or trap players.
* Debris may trap aquatic monsters, changing spawn behavior temporarily.

