			Electrified Mineral Veins

	Concept : Some gold and crystal veins carry residual minerals that can react to water and metal tools.

	Mechanic : 
* Touching certain crystal veins while holding metal tools triggers electric shocks :
	* Small damage + brief stun
	* Can chain through water to nearby players (risk in group)
* Environmental clue: faint sparkling glow and tingling sound when near

	Phase Hazard :
* Veins can charge/discharge periodically.
* Players must time attacks or movement to avoid shocking themselves.

	Combo Effects :
* Water → shocks chain to nearby players
* Metal objects (tools, weapons) amplify risk, can stun monsters too
* Players can intentionally trigger to damage monsters, introducing risk-reward

	Environmental Feedback :
* Sparks leave brief light and sound cues, helping players predict hazard cycles.

	Purpose : 
* Introduces careful navigation and situational awareness
* Natural hazard tied to mining activity
* Makes players respect the environment rather than just clearing monsters

