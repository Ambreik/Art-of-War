			Mining Disturbance

	Concept : The mine reacts to over-harvesting both minerals and trout.

	Mechanic : 
* The more players extract ore or fish over time :
	* Minor cave-ins increase
	* Predator spawn rates (Grotto Lurker, Reef Stalker Ray) rise
	* Water flow intensity increases in streams (faster currents)
* If players leave the area for a while: 
	* Veins stabilize
	* Predators disperse
	* Streams calm down

	Mine Instability Visuals :
* Dust, falling rocks, shaking walls increase with over-harvesting
* Minor debris can trip players or block paths temporarily

	Dynamic Predator Behavior :
* Increased mining activity → predators become more aggressive or hunt in packs
* Rare "elite" aquatic predators may spawn if disturbances persist

	Event Windows :
* High disturbance periods → temporary bonuses (rare loot or fish spawn) if players can survive the hazards
* Encourages strategic farming and rotation


	Purpose : 
* Prevents exploitative farming
* Creates dynamic, living mine ecosystem
* Encourages pacing and tactical engagement

