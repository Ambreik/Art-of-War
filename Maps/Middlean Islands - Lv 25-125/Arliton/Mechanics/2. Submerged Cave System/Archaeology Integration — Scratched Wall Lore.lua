			Archaeology Integration — Scratched Wall Lore

	Inside the submerged passages, many stone walls contain deliberate scratch markings left by a stranded sailor who survived inside the cave network for days.

	These are not decoration.
	They are functional Archaeology nodes.

---

		How Players Discover Them

	Markings appear as :
* Repeated scratch lines forming crude arrows
* Etched symbols near air pockets
* Tally marks beside still pools
* Rough drawings of flowing water and "glowing patches"

	They are always placed at decision points :
* Tunnel intersections
* Near inward/outward current splits
* At entrances to still chambers
* Close to bioluminescent algae growth

	This placement is intentional : the walls teach navigation.

---

		Archaeology Interaction

	Only players with "Archaeology" can properly inspect them.

	Interaction grants :
* "Sailor Log Fragment" (journal entry piece)
* A short line of text written in distressed, practical tone
* Subtle hint about the nearby water behavior

	These fragments are stored in the Archaeology journal under :

* "Cave Survivor Log"

---

		Example Fragment Lines

	The tone is survival-focused, not poetic lore.

* "Water that pulls is death. Water that rests is breath."
* "Follow the glow if your lungs burn."
* "The quiet pools hide life… and eyes."
* "When the bubbles race, do not fight them."
* "Light grows on water where air waits above."
* "I scratched this where I almost drowned."

	Each line is a navigation hint disguised as a desperate note.

---

		Functional Teaching Role

	By collecting fragments, players indirectly learn :

| Clue in Log                  | Teaches Player                              |
| ---------------------------- | ------------------------------------------- |
| "Follow the glow"            | Bioluminescent algae marks air pockets      |
| "Water that rests is breath" | Still pools are safe zones (and fish spots) |
| "Do not fight the bubbles"   | Don’t swim against currents                 |
| Marks near intersections     | Currents decide paths                       |
| Marks near still chambers    | Rare fish locations                         |

	Archaeology becomes a guidebook to surviving the cave.

---

		Fragment Collection Progression

	Fragments are scattered across multiple chambers.

	As players collect more :
1. Early fragments warn about currents
2. Mid fragments mention glow and air
3. Later fragments mention still pools and creatures watching

	The log slowly reconstructs the survivor’s path through the cave.

---

		Archaeology Reward Layer

	Completing the "Cave Survivor Log" grants :
* Archaeology XP
* A minor permanent knowledge perk while inside this cave :
	* Slightly slower breath drain
	* Faster detection of air pockets (subtle visual cue)

	This represents learning from the survivor.

