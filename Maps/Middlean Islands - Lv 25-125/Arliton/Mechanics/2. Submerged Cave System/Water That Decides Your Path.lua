			Water That Decides Your Path

	This is the true navigation system of the submerged caves.
	Players do not choose directions by turning their character.
	They choose directions by understanding the water.

---

		Core Rule

	Every flooded tunnel is flagged with one of three current types :
1. "Inward Current" → pulls players deeper into the cave network
2. "Outward Current" → pushes players toward air pockets or exits
3. "Circular Drift" → traps players in looping side chambers

	Swimming against a strong current is intentionally ineffective and drains breath faster.
	The cave decides the route. The player learns to read it.

---

		How Players Read the Water (no UI, only environment)

	Currents are telegraphed visually :
* Suspended particles drifting in a direction
* Algae strands bending with flow
* Bubbles traveling along the current path
* Subtle sound of rushing water in stronger tunnels

	Experienced players start recognizing flow before entering.

---

		Breath Management Interaction

	Fighting the current :
* Increases breath drain rate
* Slows movement drastically
* Causes panic if players try to brute-force navigation

	Following the current:
* Conserves breath
* Naturally leads to either :
	* Deeper chambers with resources and danger
	* Safe air pockets

	The cave rewards cooperation, not resistance.

---

		Exploration as a Puzzle

	At intersections, players must choose tunnels based on :
* Strength of the pull
* Visual flow clues
* Whether they want :
	* Safety (outward flow)
	* Risk & reward (inward flow)

	This turns intersections into decision points, not corridors.

---

		Synergy With Riverglide Trout Mechanic

	Still pools (where "Riverglide Trout" exist) :

* Are always located outside strong current paths
* Found in chambers reached by leavin* main current routes

	Players who blindly follow currents will never find the fish.
	They must intentionally look for water that does not move.
	Fishing teaches current awareness.

---

		Danger Layer — Current Into Darkness

	Some inward currents lead into :
* Narrow, darker tunnels
* Predator ambush zones
* Chambers without immediate air pockets

	Players feel the dread of being carried somewhere unknown.
	The water becomes a source of tension.

---

		Outward Currents as Relief

	Tunnels with outward currents :
* Gently guide players to air pockets
* Often pass by quartz/crystal veins
* Feel like the cave “letting you breathe”

	These routes are subtle safe paths.

---

		Dynamic Changes After Heavy Rain

	After rainfall :
* Some currents strengthen
* Some reverse direction
* Previously safe routes may lead deeper
* New air pocket routes may open

	The same cave layout feels different depending on weather.
	Players cannot memorize — they must re-read the water.

