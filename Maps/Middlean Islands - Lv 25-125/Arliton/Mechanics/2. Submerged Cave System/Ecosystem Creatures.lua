* "Golden Eel" → ambush predator in darker tunnels
* "Mistwing Bat" → air pocket ceilings, linked aggro if disturbed
* "Shadowmoss Leech" → attaches to players in slow water sections

	These populate the cave naturally depending on :
* Light level
* Water stillness
* Air pocket presence
