			Riverglide Trout & Glowgrub Lure

	This is not just a rare fish with a special bait.
	This is a location-locked fishing behavior system that teaches players how the cave works through fishing.

---

		Riverglide Trout — Behavioral Design

	"Riverglide Trout" do not live in open water sections of the cave.

	They exist only in :

* Inner still pools
* Chambers where current does not reach
* Areas lit by bioluminescent algae
* Near air pockets above the waterline

	These pools are visually calm compared to the rest of the cave.
	That visual calm is the clue.

	Why they ignore normal bait

	These trout evolved in :
* Darkness
* Still water
* Environments without surface insects

	They hunt by reacting to faint underwater glow, not motion on the surface.

Normal bait = invisible to them.
Glow = food.

---

	Glowgrub Lure — Design Purpose

	Item Name : "Glowgrub Lure"
	Rank : II Rare Fishing Bait
	Vendor : Oscar Fisher D.

	Shop Description (ready to paste)

> A bioluminescent cave grub preserved in resin. Emits a faint glow underwater. Irresistible to deep freshwater species that avoid surface light.

### What makes this bait special mechanically

	When equipped :
* The fishing float emits a soft glow
* Only visible clearly in darker water
* Acts as a visual hint to players that this bait belongs in caves, not rivers or ponds

	Players start associating :

> Glow water + glow bait = something rare lives here.

---

		Player Discovery Loop (very important)

	First time players reach these pools they will :
1. Try normal bait → catch nothing or common cave fish
2. Notice algae glow and calm water
3. Remember Oscar sells a “glowing cave bait”
4. Come back prepared

	This creates memory-based gameplay, not tooltip gameplay.

---

		Exact Spawn Conditions for Riverglide Trout Schools

	A Riverglide Trout school can only spawn when all are true :

* Chamber has bioluminescent algae
* Water is marked as "Still Pool" (no current flag)
* There is an air pocket in the same chamber
* Player is using "Glowgrub Lure"

	If any of these are missing → the school does not appear.
	This prevents random fishing and makes it intentional.

---

		School Behavior

	When active :
* Small, slow-moving school
* Does not scatter like normal fish
* Stays near the glowing float
* Longer bite time than normal fish (rarity tension)

	Catching one slightly reduces school duration.
	Players feel like they are depleting a hidden ecosystem pocket.

---

		Environmental Storytelling

	Why are they near air pockets?

	Because :
* Glowgrubs live in cave ceilings near air
* They fall into these pools
* Trout evolved to wait under these spots

	Fishing spot = ecological relationship, not random spawn.

---

		Regeneration Logic

	Riverglide Trout schools :
* Respawn slowly
* Prefer chambers that have not been fished recently
* Encourage players to explore deeper instead of camping one pool

---

		Why this fish matters (design-wise)

	This fish teaches players :
* Still water is important
* Bioluminescent algae marks special chambers
* Air pockets are not just for breathing — they signal life
* Oscar’s bait has a real purpose tied to the world

	Fishing becomes a navigation teacher for the cave.

---

		Hidden Synergy With Archaeology

	Some scratched wall notes (for Archaeology) can say things like:

> "Where the water does not move, the glowing worms feed the silver swimmers."

	Players who read this can deduce the fishing rule without trial and error.
	Archaeology becomes a hint system for fishing.
