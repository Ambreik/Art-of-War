			The Fisherman Who Never Returned

	A message in a bottle reads :

> "If you find this, the tides took me before I could return. My net is still buried where the rocks meet the sand…"

		Quest Flow 

	Trigger : Loot "Message in a Bottle" from debris.

	Objective (implicit) :
* Players must explore rocky beach edges to find a half-buried old fishing net.

	Interaction Result :
* Spawns a small school of rare fish in nearby water for a short time
* Grants a hidden buff (below)

	No markers. Pure exploration.

---

	Buff Reward

**[Fisherman’s Instinct]**

How to get : Free the old fishing net from the rocks after reading the bottle message
Effect : Increased chance to detect rare fishing spots and +15% fishing yield
Duration : 20 minutes
Suffer debuff : None
UI Icon : Old fishing hook

