			Shells of the Tidemother

	A strange spiral of shells appears in debris piles occasionally.

		Quest Flow

	Trigger : Interact with shell spiral

	Objective (implicit) :
* Players must place 5 additional shells found nearby into the spiral.

	Result :
* Gentle wave washes over the shore
* Heals player
* Grants rare fishing luck buff

If player disturbs spiral randomly → hazard triggers (see hazards).

---

	Buff Reward

**[Tidemother’s Favor]**

How to get : Complete the shell spiral without disturbing it
Effect : Greatly increased chance to fish rare coastal species
Duration : 25 minutes
Suffer debuff : None
UI Icon : Spiral shell

