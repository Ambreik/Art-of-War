			Cargo from the Broken Skiff

	A cracked crate in debris contains trade goods and a soaked note:

> "Deliver this cargo… if the sea hasn’t claimed it already…"

		Quest Flow

	Trigger : Loot "Broken Cargo Manifest"

	Objective (implicit) :
* Players must carry the crate (movement slowed slightly) to the campfire area or safe shore spot.

	Result :
* Crate breaks open safely
* Spawns crafting materials
* Grants temporary beach movement buff

	If player drops it in water → crate dissolves, nothing gained.

---

	Buff Reward

**[Shore Runner]**

How to get : Safely carry the broken cargo crate across the beach without entering water
Effect : +25% movement speed while on sand or shore terrain
Duration : 15 minutes
Suffer debuff : None
UI Icon : Small drifting plank

