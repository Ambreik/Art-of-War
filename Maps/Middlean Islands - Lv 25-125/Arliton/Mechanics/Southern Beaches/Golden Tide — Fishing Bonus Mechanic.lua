			Golden Tide — Fishing Bonus Mechanic

	Concept : At specific times of the day, sunlight angle, water clarity, and tide alignment create a natural phenomenon where the shallow coastal waters begin to 
reflect golden glimmers across the surface.

	This is not magical. It is a mix of :
* Micro-bubbles from the tide shift
* Dense fish schools swimming near the surface
* Light refraction on scales
* Calm water pockets

	Players who pay attention notice :

> "Something is different with the water here…"

	These spots are temporary, subtle, and move along the shore.

---

	Mechanic : 
* Golden light reflections appear on water surface for ~2–3 minutes.
* Only visible from certain camera angles or when close to water.
* Fishing inside this glimmer zone changes the fish table completely.
* After some time, the glimmer fades and reappears elsewhere along the beach.

	This turns fishing into :

> Observation → movement → reward.

	No AFK fishing.

---

		What Actually Causes the Glimmer (Ecosystem Logic)

	This is created by specific fish schools that swim extremely close to the surface during feeding cycles.
	These fish are already part of the coastal ecosystem, but were missing from your list.

---

		What Happens When Fishing in Golden Tide

	Fishing table changes to include :
* Golden Shore Trout (rare)
* Increased chance of Moonfin Snapper
* Chance to catch double fish
* Chance to catch “Prismatic Scales” crafting material

	Outside Golden Tide → normal table.

---

	Purpose Reinforced : 
* Prevent AFK fishing
* Reward awareness
* Tie fishing to ecosystem behavior
* Make shoreline feel alive and reactive

