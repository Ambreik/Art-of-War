			Driftwood and Debris

	Mechanic :
* Players can collect driftwood, shells, or washed-up goods.
* Occasionally, rare items or messages in bottles appear, triggering:
	* Mini-quests
	* Hidden buffs (e.g., fishing luck, speed buff along the beach)
	* Environmental hazards (e.g., unstable sand piles collapsing, hiding small crabs that scratch)

	Purpose :
* Adds curiosity-driven rewards
* Blends casual resource gathering with minor exploration gameplay

---

		Environmental Hazards from Debris

	These happen when players interact carelessly or greedily.

---

	Hazard 1 — Collapsing Sand Pocket

	Trigger : Looting driftwood too quickly or repeatedly in same spot.

	Effect :
* Sand collapses
* Player falls knee-deep, Rooted for 3 seconds
* Small sand crabs emerge and apply minor bleed

	Teaches: don’t spam interactions.

---

	Hazard 2 — Tangled Rotting Net

	Trigger : Interacting with seaweed/net piles.

	Effect :
* Player gets Slowed heavily
* Applies stacking debuff "Salt Rash”"(minor DoT)
* Must move to water to cleanse it

	Teaches: environment awareness.

---

	Hazard 3 — Disturbed Shell Circle (failed mini-quest)

	Trigger : Player loots shells from spiral without completing it.

	Effect :
* Sudden wave knocks player back
* Applies "Daze" (auto-dismount)
* Spawns territorial crabs

	Teaches: observation before action.

---

	Hazard 4 — Bottle with Rotten Air

	Some bottles are old.

	Trigger : Opening wrong bottle.

	Effect :
* Noxious air cloud
* Blurred vision
* Minor poison DoT for 10 seconds

	Pure flavor, but memorable.

