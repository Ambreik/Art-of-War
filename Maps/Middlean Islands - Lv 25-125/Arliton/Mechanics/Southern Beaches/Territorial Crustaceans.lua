			Territorial Crustaceans

	Mechanic :
* Sandy crabs, small crabs, or territorial sand dwellers guard nests near water or rock pools.
* If players dig in nests or stand too close, the crabs swarm or throw sand (blind/slow effect).
* Some may carry rare crafting materials (shells, pigments).

	Purpose :
* Adds a combat-light threat to an otherwise tranquil area
* Rewards careful observation and timing

