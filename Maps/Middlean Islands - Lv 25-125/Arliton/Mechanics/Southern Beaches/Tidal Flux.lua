			Tidal Flux

	Mechanic :

* Tides rise and fall on a real-time schedule (or accelerated in-game schedule).
* High tide: Some fishing spots are submerged or temporarily inaccessible; rare aquatic creatures appear closer to shore.
* Low tide: Exposes shallow pools with crustaceans, shells, or hidden mini-chests; increases risk of sand traps or hidden holes.

	Purpose :

* Encourages players to observe tide cycles
* Dynamic resource gathering depending on the time of day
* Makes the beach feel alive rather than static

