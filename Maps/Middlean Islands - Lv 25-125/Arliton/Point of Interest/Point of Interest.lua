			Points of Interest (POIs)

1. "Ruined Temple" – The central landmark of the island, partially collapsed and overgrown by vines, moss, and glowing flora. Ancient carvings hint at forgotten rituals.
2. "Rainbow Trout Goldmine" – A mining operation hidden deep in the eastern jungle. Owned and operated by "aster Prospector Halric", a grizzled dwarf obsessed with 
gold and freshwater fishing. The mine is a strange mix of tunnels and underground streams, where miners harvest both ore and the prized rainbow trout that thrive in
its waters.
3. "Southern Beaches" – Wide sandy shores with gentle waves, where travelers often make landfall. Resource-rich with fishing spots.
4. "Western Sea Caves" – Small cave systems accessible from the cliffside trail. Contain mineral veins and glowing fungi.
5. "Northern Ponds & Waterfall" – A series of freshwater pools formed by the stream from the highlands. Surrounded by dense vegetation, it’s home to unique herbs 
and rare fish.
6. "Jungle Heart Clearing" – A naturally open area in the forest where sunlight pours through the canopy. Known for its unusual flora and animal presence.

