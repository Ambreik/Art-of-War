		Environmental Mechanics

	Global Mechanics (already core for your world game)

1. "Fatigue Waters"
   * Swimming too far into the deep sea applies a stacking debuff until death by drowning - fatigue.
   * Prevents exploitation of endless ocean travel and ties into ocean danger.

2. Stealth Zones (Dense Vegetation Camouflage)
   * Entering dense jungle, tall grass, or thick vegetation grants a camouflage buff.
   * Buff works like stealth: temporary invisibility to mobs/players (not total— detection radius reduced).
   * Breaks upon attack or ability use.
   * Certain high-rank monsters also use this, creating ambush danger.

---

		Arliton-Specific Environmental Mechanics

3. "Strong Winds" (Beach & Cliff Pushback)
   * On exposed cliffs/beaches, gusts of wind apply a slight push to players.
   * Push is minor, but affects positioning in combat, fishing, or gathering.
   * Could cause players to slip into shallow water → minor damage.
   * Adds tension when fighting near edges.

4. "Jungle Fog" (Reduced Visibility at Night)
   * A fog effect applies across the jungle interior during night.
   * Visibility radius reduced (shorter target/interaction range).
   * Encourages players to use torches, light sources, or specific buffs.
   * Makes ambush predators (see below) more dangerous.

---

		Resource-Based Mechanics

5. Rare Spawn Herbs / Minerals
   * Only one rare herb and one rare mineral vein can exist on the island at a time.
   * Herbs respawn faster (≈ 15 min).
   * Minerals respawn much slower (≈ 30 min+).
   * Mining system mechanics → veins have HP, must be “attacked down.”
   * Creates competition and player-driven economy.

6. Fishing Pools / Schools
   * Fishing pools appear in rivers, ponds, and coastal areas.
   * Pools grant unique fish for the island (e.g., Rainbow Trout, Golden Eel).
   * Regular fishing (outside pools) yields only common/low-value fish.
   * Pools deplete after X catches → force exploration.

7. Environmental Gathering (Weather-Tied Resources)
   * Certain herbs (e.g., Mistbloom) only spawn during specific weather events (rain, storm, heavy fog).
   * Encourages repeat visits and dynamic harvesting.

---

		Creature / AI Mechanics

8. Ambush Predators (Stealth Mobs in Jungle)
   * Certain creatures hide under stealth in dense vegetation.
   * Aggro when players approach within reduced detection radius.
   * Adds danger to exploration/gathering.

9. Linked Aggro (Wildlife Alert)
   * Disturbing flocks of birds, monkeys, or other “ambient” mobs can alert nearby hostile packs.
   * Creates chain reactions → chaotic combat in jungle areas.

10. Day/Night Behavior
* Day: wildlife behaves normally.
* Night: mobs either change spawn types (ghosts, predators) or gain buffs (+aggro range, +damage).
* Makes night exploration more dangerous but rewarding.

---

		Exploration & Lore Mechanics

11. Lore Interactions (Archaeology Objects)
* Clicking hidden ruins, tablets, or statues provides lore text, visuals, or archaeology progress.
* Standard across all Middlean Islands for profession tie-in.

12. World Chest System (Global)
* A rare treasure chest spawns once per hour or at fixed intervals.
* Appears at random or semi-fixed “chest spots” on the island.
* Only players of the appropriate level range can open.
* Rewards range from utility consumables to poor-quality filler 

