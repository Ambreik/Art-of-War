		Resources

	Fishing
	Arliton offers both saltwater and freshwater fishing opportunities, thanks to its beaches, ponds, and underground streams.

- Rainbow Trout – Found in the underground streams of the Rainbow Trout Goldmine and northern ponds.
- Moonfin Snapper – A silvery fish that glows faintly at night, common along the southern beaches.
- Reefclaw Grouper – Found near rocky shores and western sea caves.
- Golden Eel – Rare, found in still waters within the jungle pools; slippery and prized for cooking recipes.

	Herbs (Herbalism)

- Glowleaf – Bioluminescent plant found near the Ruined Temple and shaded caves; used for magical and alchemy recipes.
- Rainfern – Thrives in moist areas near waterfalls and streams; key for healing potions.
- Shadowmoss – Grows beneath dense jungle roots; harvested carefully due to its poison-tipped spores.
- Sunblossom – Found in the Jungle Heart Clearing; its bright petals are useful in crafting energy potions.

	Mining (Minerals)

- Copper Veins – Common along the western cliffs and in shallow caves.
- Iron Deposits – Found deeper in the Rainbow Trout Goldmine.
- Quartz Crystals – Sometimes clustered within the waterfall cavern walls.
- Runestone Fragments – Rare mineral-like shards near the Ruined Temple; used in advanced crafting recipes.

	Skins / Leather (Optional, Wildlife Resources)
	Not a primary gathering focus here, but some local wildlife may provide materials:

- Scaled Hide – Dropped from reptilian jungle beasts (to be defined later in the monster list).
- Toughened Leather – Rare drops from larger predators roaming near the temple ruins.

