		Arliton – Secret Key Locations

	These are hidden or hard-to-reach areas that reward curious exploration. They aren’t marked on maps and require either environmental awareness, mechanics, or 
timing to uncover.

---

1. Sunken Warship Graveyard
	- Where : Scattered along the southern shore, partially visible only at low tide.
	- Description : The broken hulls of ancient vessels lie half-buried in sand and coral. Some sections still hold sealed cargo crates, while the sea around them 
teems with barnacle-crusted debris. Small pools form in the ship remains, attracting fish.
	- Mechanics / Circumstances :
- Accessible only at low tide; at high tide most wrecks are submerged.
- Diving too deep triggers "Fatigue Waters" (core mechanic).
- Hostile seabirds and crabs use "Linked Aggro" if startled on deck.
- Exploration is easier at day; at night "Jungle Fog" and poor visibility make wrecks hazardous.

	What can be found :
- Salvage materials (scrap metal, rotted wood, rare fragments).
- Archaeology tablets in captain’s quarters.
- Rare fish schools (*Moonfin Snapper*).
- World Chest spawn chance.

---

2. Submerged Cave System
	- Where : Entrances near the island’s northern river mouth and under a hidden coastal cliff.
	- Description : A twisting network of flooded passages with air pockets and bioluminescent algae lighting the way. Some chambers hold still pools and small 
crystal formations.
	- Mechanics / Circumstances :
- Requires underwater exploration with breath management.
- Currents push players deeper or toward safe pockets.
- After heavy rain, some entrances flood shut, while others open.
- Ambush predators lurk in the darker tunnels.

	What can be found :
- Rare herbs (Mistbloom in damp alcoves).
- Quartz and crystal veins.
- Unique fish (*Riverglide Trout*) in inner pools.
- Lore fragments scratched into stone walls.

---

3. Hidden Forest Statue
	- Where : Buried in vines within the densest part of the jungle interior.
	- Description : A mossy, half-toppled statue of an old deity, so covered in foliage that it looks like a boulder until closely examined. At night, faint glowing
runes appear across its surface.
	- Mechanics / Circumstances :
- Only revealed clearly during nighttime (Day/Night Behavior mechanic).
- Runes are interactable for archaeology profession progress.
- Area has increased ambush predator spawns due to unnatural aura.
- Using Stealth Zone camouflage reduces predator aggro while exploring the site.

	What can be found :
- Archaeology fragments and lore.
- Occasional rare herb (*Shadowmoss*) growing on the statue’s base.
- Hidden runestone fragments under loosened stone.

---

4. Saltwater Grotto (Tidal Sink)
	- Where : A small cavern in the northern cliffs, fully submerged during high tide.
	- Description : A glittering grotto lined with natural salt formations. Pools inside trap rare reef fish and host glowing fungi on the walls.
	- Mechanics / Circumstances :
- Accessible only at low tide; at high tide the entrance is completely underwater.
- Strong Winds can make entry from boats risky.
- Stealth predators stalk the cavern waters, ambushing divers.

	What can be found :
- Salt crystals (used in cooking/preservation).
- Rare fish (*Reefclaw Grouper*).
- Small crystal veins for mining.

---

5. Bioluminescent Grove
	- Where : A secluded clearing north of the Temple Highlands.
	- Description : At night, the grove glows with rings of fungi and Glowleaf plants, creating a surreal light show. During the day, it appears like a regular 
thicket.
	- Mechanics / Circumstances :
- Herb nodes (*Glowleaf*) spawn more frequently here, but only at night.
- Attracts ambush predators who use the light to hunt.
- Lore markers inscribed on stones appear faintly visible only under fog conditions.

	What can be found :
- Dense Glowleaf clusters.
- Rare fish pools (*Golden Eel*) in sinkholes.
- Archaeology fragments tied to the temple’s forgotten worshippers.

