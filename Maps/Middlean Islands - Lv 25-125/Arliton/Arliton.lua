			Arliton – Middlean Islands

		Overview

	Location : South-east of the Middlean Islands cluster.
	
	Summary : Arliton is a medium-sized crescent-shaped island covered in lush tropical jungle. Its most prominent landmark is the "Ruined Temple", an ancient 
structure that crowns the central highlands. The island is divided by narrow paths cutting through the dense jungle, leading to beaches, caves, and hidden places of
interest. While seemingly idyllic with its golden sands and calm lagoons, the island conceals dangers in the form of wild nature, forgotten ruins, and human greed 
— most notably the "Rainbow Trout Goldmine", deep in the forest, where miners battle not only the earth but the island itself.

---

		Deep Description - Island Shape & General Layout

	Arliton is shaped like a crescent moon, with steep cliffs on the northern side and broader, sandy beaches on the south-east. The inner curve of the crescent 
shelters calmer waters, while the outer side faces rougher seas. The island rises toward the center, where the "Ruined Temple" stands on elevated ground, partially 
visible from the coasts. Jungle sprawls across most of the island, broken by rivers, small ponds, and rocky formations.

	Paths & Natural Routes

- Main Southern Path : A beaten track starting from the southern beach, cutting through the jungle toward the "Ruined Temple".
- Eastern Jungle Trail : Leads from the coast into the heart of the forest, ending at the "Rainbow Trout Goldmine".
- Western Cliff Path : A narrow trail that runs along cliffs and leads to small caves overlooking the sea.
- Northern Stream Route : A stream flows from the highlands down to the northern beach, forming natural ponds and a small waterfall. A faint path follows this 
stream and connects to hidden alcoves.

		Points of Interest (POIs)

1. "Ruined Temple" – The central landmark of the island, partially collapsed and overgrown by vines, moss, and glowing flora. Ancient carvings hint at forgotten rituals.
2. "Rainbow Trout Goldmine" – A mining operation hidden deep in the eastern jungle. Owned and operated by "aster Prospector Halric", a grizzled dwarf obsessed with 
gold and freshwater fishing. The mine is a strange mix of tunnels and underground streams, where miners harvest both ore and the prized rainbow trout that thrive in
its waters.
3. "Southern Beaches" – Wide sandy shores with gentle waves, where travelers often make landfall. Resource-rich with fishing spots.
4. "Western Sea Caves" – Small cave systems accessible from the cliffside trail. Contain mineral veins and glowing fungi.
5. "Northern Ponds & Waterfall" – A series of freshwater pools formed by the stream from the highlands. Surrounded by dense vegetation, it’s home to unique herbs 
and rare fish.
6. "Jungle Heart Clearing" – A naturally open area in the forest where sunlight pours through the canopy. Known for its unusual flora and animal presence.

---

		Vegetation

	Arliton’s vegetation is dense, humid, and layered. Towering canopy trees form a roof of wide leaves, their roots breaking through the soil in twisting 
formations. Beneath them, thick underbrush of ferns, thorn-vines, and moss carpets the jungle floor, making paths narrow and sometimes difficult to follow. Along 
streams and ponds, reeds and flowering plants bloom in vibrant colors, while rare glowing fungi cling to the shaded ruins of the temple and caves.
	The forest canopy creates alternating zones of light and shadow. In clearings, tall grass and sun-loving flowers thrive; in the shaded jungle heart, creeping 
vines and parasitic plants dominate. The ruins of the temple and the mine tunnels are overgrown with mosses and strangler-vines, giving them an atmosphere of decay 
reclaimed by nature.

	Unique Environmental Mechanic (Vegetation)
- Choking Vines : Certain tangled areas of the jungle are covered in thick vine formations. Players must interact with (cut/burn) vines to open hidden shortcuts or 
reveal paths to herb clusters or small caves.

