	3. Environmental Detail

* The chest on the ship **resets every 2–3 hours** (server time).
* Players entering the wreck risk reduced oxygen efficiency, encouraging use of consumables like *Deepwater Vial*.
* Occasionally, spectral echoes (non-hostile, visual ghosts of sailors) appear, whispering fragments of Mirath’s betrayal story — adding atmosphere, not gameplay impact.

