### **Forest Glade of Secrets – Loot / Rewards**

**Loot Categories:**

1. **Herbs (for Alchemy / Healing)**

   * Forest Dewleaf – Restores minor HP when used in potions
   * Shadowroot – Component for poisons or temporary buffs
   * Whispervine – Grants temporary +5% movement speed when consumed
   * Glade Blossom – Ingredient for rare consumables (+stat boosts)

2. **Small Ores (for Crafting / Blacksmithing)**

   * Iron Pebble – Low-tier crafting resource
   * Silver Shard – Mid-tier crafting material; slightly rare
   * Glimmer Stone – Rare, small chance; used for enhancement of weapons/armor

3. **Wildlife Loot (from small ambient creatures)**

   * Feathers of Forest Bird – Used in minor buffs or crafting arrows
   * Fangs / Claws – Minor combat consumables or low-tier upgrade components
   * Hide Scraps – Can be used for tailoring or leather-based recipes

4. **Rare Consumables / Temporary Buffs (limited drop)**

   * Hunter’s Ration – +10% damage against creatures for 10 minutes
   * Ranger’s Focus – +15% critical chance for 5 minutes
   * Elixir of Glade – Restores 25% HP and 25% MP instantly
   * Whisperwind Charm – +5% evasion for 15 minutes

**Loot Mechanics:**

* Loot spawns randomly when the mini-event is triggered.
* Each player can receive **1–2 items per interaction**.
* Rare consumables have a **5–10% drop chance** per trigger.
* Herbs and small ores can **respawn every in-game day**.
* Wildlife loot requires defeating or interacting with minor creatures within the glade (ambient encounter).

