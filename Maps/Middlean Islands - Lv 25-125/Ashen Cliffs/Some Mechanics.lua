	4. Random Mini-Event Trigger – Forest Glade of Secrets**

**Trigger Chance:** 10–20% upon entering the glade

**Possible Events:**

1. **Hidden NPC Encounter**

   * **Types of NPCs:**

     * Wandering hermit
     * Lost scout
     * Mysterious traveler
   * **Interactions:**

     * Talk / gather information
     * Help NPC with small task (e.g., collect herbs, repair equipment)
     * Optional combat if NPC is guarded by forest creatures
   * **Rewards:**

     * Temporary buff (+5–10% attack, movement speed, or defense for 5–15 min)
     * Small consumables (HP/Mana potion, rare herb)
     * Minor gold or repeatable quest for the next day

2. **Environmental Puzzle**

   * **Puzzle Types:**

     * **Runestone Alignment:** Rotate stones to match a hidden pattern
     * **Vine Manipulation:** Move/cut vines in the correct sequence to access a cache
     * **Forest Echo:** Step on correct tiles in sequence based on wildlife sounds
   * **Mechanics:**

     * Players must solve within a limited time (e.g., 5 min)
     * Failure resets the puzzle; reward quality slightly decreases
   * **Rewards:**

     * Rare consumable (e.g., “Glade’s Essence” – temporary stat buff)
     * Herb bundles or small ores
     * Temporary buff tokens

**Optional Bonus:**

* Traps or ambushes may trigger during events (e.g., hidden predators, spike traps), adding minor combat risk

