			Mirath, the Eternal Blight

### **General Overview**

* **Name:** Mirath, the Eternal Blight
* **Type:** Humanoid Boss Encounter
* **Location:** Fort Mirath Lake – central island basin
* **Recommended Group Size:** Up to 5 players
* **Respawn Timer:** 3 hours (real time)
* **Aggro Range:** Wide within lake perimeter; can be pulled slightly onto the shore but retreats if dragged too far.

---

### **Appearance**

Mirath still bears traces of his once-human form. His flesh shows the marks of the **Living Water**—pale veins glowing faintly beneath cracked, translucent skin.
He wears a ruined commander’s coat bound with scavenged armor plates, his right arm wrapped in iron-chain bindings from the old fortress prison. His eyes shimmer an unnatural blue, leaking faint mist as if the water itself tries to escape him.

Weapons:

* **Runebound Sabre** – A heavy curved sword corroded by sea salt, glowing faintly when charged.
* **Rusted Lasso Launcher** – a hybrid between grappling hook and rifle stock, used for mid-range control.

---

### **Combat Behavior & Mechanics**

Mirath’s fight has **three phases** depending on HP percentage.
He mixes physical aggression with control and brief ranged pressure.

#### **Phase 1 (100 %–70 %) – Command of the Shore**

* **Cleave Strike:** A wide arc slash dealing frontal AoE damage.
* **Anchor Pull:** Throws his lasso at a distant player, pulling them close and applying *Root (3 s)*.
* **Roar of Defiance:** Emits a short-range fear effect for 2 s and slows movement by 20 %.

> *Tactical Note:* During this phase he remains mostly in the shallows; melee range is safe but positioning matters.

#### **Phase 2 (69 %–35 %) – The Cursed Water**

* **Living Surge:** Summons bursts of corrupted lake water that deal small AoE damage every 2 s for 10 s.
* **Blighted Armor:** Temporarily reduces incoming damage by 25 % for 8 s.
* **Mirath’s Rifle Burst:** At range, fires three quick rifle shots; the third has a 25 % chance to stun for 2 s.

> *Tactical Note:* Players must handle alternating melee/ranged pressure and short bursts of AoE damage.

#### **Phase 3 (34 %–0 %) – Wrath of the Immortal**

* **Final Stand:** Upon reaching 30 %, Mirath roars, gaining +10 % attack speed and spawning two “Cursed Guards” (human elites).
* **Blight Overload:** Every 20 s Mirath releases a pulse of corrupted water, dealing minor damage and applying a stacking –3 % healing received debuff (5 s).
* **Last Lunge:** At 10 %, he charges a random player, performing a heavy knockback attack before collapsing.

---

### **Environmental Interaction**

* During combat, the **lake water darkens**, and faint glowing ripples appear beneath Mirath’s feet.
* The **bioluminescent flora** around the lake flicker in intensity according to his HP thresholds, providing natural light feedback.
* If dragged too far from the lake, he **submerges and resets**, restoring HP after 10 seconds out of combat.

