# **Region II: Fanthe’kar**

**Level Guidance:** 20–40
**Environment Type:** Desert transitioning to savanna
**Design Intent:** Mid-level zone for knights faction; introduces resource management (water, travel across dunes), environmental hazards, and quest hubs with moderate combat challenge.
**Monster Tier:** Lv. 20–40. Desert-adapted creatures, sand predators, minor elemental enemies (earth/fire).
**Ecosystem Notes:** Harsh arid climate, sparse vegetation, oasis points. Predatory and scavenger species, few forests or water sources. Supports early profession progression (mining, herbalism) and pet hunting with desert-adapted pets.

---

### **Sub-Area: Anghal Desert**

**Type:** Easternmost desert region
**Points of Interest:**

1. **Golden Coasts (East):** Sandy beaches, low monster density, quest grind zones
2. **Black Sands (West):** Barren terrain with stone blocks and hills; minor dungeon potential
3. **Goldcap Town (SW):** First desert settlement, quest hub, local merchant NPCs
4. **Sundry Lake (Central):** Oasis, water resource for survival mechanics, fishing points

**Monster Tier:** Lv. 20–30
**Ecosystem Notes:** Predominantly sand, rocks, minimal vegetation. Desert creatures and wandering packs. Some unique desert herbs for gathering professions.

---

### **Sub-Area: Golden Fields**

**Type:** Central desert-savana transition
**Points of Interest:**

1. **Alddan Field (West):** Major grinding area, diverse mid-level monsters
2. **Ancient Ruins (North):** Stone-walled ruins, access to **Barh Khaon Cave**, moderate dungeon
3. **Verian Stable (Intersection of territories):** NPC hub for travel, mounts, quests
4. **Arid Savanna (East):** Sparse trees, wildlife spawns, gathering spots
5. **Southern Abyll Town (South):** Historical human settlement, minor quest hub

**Monster Tier:** Lv. 25–35
**Ecosystem Notes:** Transition zone with patches of vegetation, small rivers. Supports both desert and savanna-adapted creatures.

---

### **Sub-Area: Fierdon**

**Type:** Easternmost savanna, near the Great Sea
**Points of Interest:**

1. **Riven Fields:** Fertile riverside area, hunting and questing hotspot
2. **Stumb Stables (West):** NPC hub studying wildlife, player mounts, and pet interactions
3. **Greenden Savanna (East):** Dense flora, diverse fauna, quest hubs, and rare resource nodes
4. **Riddleport (Coast):** Coastal town, trade hub, docking for Traveling Ships

**Monster Tier:** Lv. 30–40
**Ecosystem Notes:** Lush savanna near rivers, higher density of monsters and pets. Environment provides hunting and gathering diversity for players before transitioning to the next high-level region.

