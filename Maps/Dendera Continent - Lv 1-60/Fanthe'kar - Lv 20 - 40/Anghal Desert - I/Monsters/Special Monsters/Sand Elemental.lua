			Sand Elemental  

	General Description : A massive, ever-shifting creature formed from the restless sands of Anghal Desert. The Sand Elemental is an ancient guardian, awakened 
when the land is disturbed by excessive mining or gathering. Its body constantly reforms, absorbing the desert itself, making it incredibly resilient. Though slow-
moving, it can create powerful sandstorms, blind its enemies, and crush opponents under the weight of solidified sand.  

---

		Monster Details & Mechanics :  
- Rank : II  
- Race : Elemental  
- Type : Tanky, God, Skillable, Deathblow, Melee  
- Element : Earth  
- Class : Superior  
- Aggro Type : Proximity Aggro (Appears near players and attacks if they enter its range)  
- Spawn Condition : Triggered by excessive mining or gathering in one area.  
- Absorb 10% of the damage receive, being an "Elemental Monster".
- Doesnt spawn any monster.

---

		Combat Mechanics & Abilities  

	Passive Abilities :  
- Sand Absorption : Every 30 seconds, the Sand Elemental absorbs nearby sand, increasing its armor and health regeneration. If not interrupted, this makes it 
increasingly harder to kill.  
- Immune to Stuns & Knockbacks : As a creature made of loose sand, it cannot be stunned or pushed.  
- Resistant to Physical Damage : Swords and arrows pass through it with reduced effectiveness.  
- Weak to Water & Wind Attacks : Water magic can harden its body, making it temporarily vulnerable to physical strikes, while wind magic can scatter its form, 
interrupting its regeneration.  

---

	Active Skills :  
	Crushing Grip – The Sand Elemental extends an arm of solid stone, grabbing and slamming a target into the ground.  
- Deals high single-target damage.  
- Has a small chance to cause “Buried” status, where the player is trapped and must be freed.  

	Sandstorm Wrath – Summons a violent sandstorm, reducing visibility, causing random movement distortions, and lowering accuracy for everyone inside.  
- If the storm lasts long enough, the elemental regains health.  

	Shifting Form – Every 20% HP lost, the Sand Elemental shapeshifts, growing larger and tougher while changing attack patterns.  
- At 40% HP, its body turns partially into stone, increasing defense.  
- At 20% HP, it starts using double attacks, dealing more damage.  

	Earthquake Stomp – The Sand Elemental slams the ground, causing an area-wide tremor that knocks down players within range.  

	Sand Spikes – Raises jagged sand spikes from the ground, impaling enemies in a line.  
- High piercing damage.  
- Players must dodge by moving to the sides.  

