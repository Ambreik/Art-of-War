			Area Overview: Anghal Desert

	"Anghal Desert" is the first area of the Fanthe'kar region, located in the western part of this arid land. As the second most extensive area in 
the region, it encompasses a diverse landscape characterized by its deep crater, sandy dunes, and a unique combination of natural features and 
remnants of past civilizations. The area is divided into two main sub-zones, "Golden Coasts" and "Black Sands", each offering its own challenges and 
opportunities for adventurers.

---

	Key Features:

- Geography : 
	- The Anghal Desert is surrounded by steep cliffs and mountains, creating a natural barrier that isolates it from the surrounding areas, except 
for a passage to the west that leads to the sea. A deep crater runs from the northeast to the southwest, forming a distinct arc that separates the two
sub-zones.
	- The landscape is dominated by reddish stone roads, sandy terrain, and sparse vegetation, with vast stretches of barren land punctuated by large 
cacti and palm trees. The roads connecting points of interest are made of stone, flanked by small shrubs and patches of grass.

- Climate : 
	- The area experiences a harsh, arid climate, necessitating the introduction of the "Blazing Heat" debuff, which reduces movement speed by 20% in 
the Anghal Desert and 40% in the Golden Fields. Players must navigate the environment with caution, using special items or mounts to alleviate stamina
loss.

---

	Sub-Areas:

1. "Golden Coasts"
	- Description : Occupying the eastern side of the Anghal Desert, the Golden Coasts is the largest zone in the area, characterized by rolling sand 
dunes and a significant lack of vegetation. The landscape is shaped by ancient fortifications that hint at its storied past, despite its current 
barren state.
	- Points of Interest : 
		- The "Sundry Lake", shaped like the letter "L," serves as a vital safety zone amidst the desert's dangers, surrounded by palm trees and 
	other greenery.
		- The "Crater", with its steep walls, offers a perilous yet scenic view of the surrounding terrain, serving as a boundary between the Golden 
	Coasts and the Black Sands.

2. "Black Sands"
	- Description : Located in the western part of Anghal Desert, Black Sands is marked by ruins of ancient stone buildings scattered throughout, 
forming labyrinthine pathways that create ideal grinding areas for adventurers. This sub-area is known for its high concentration of unique monsters 
and resources.
	- Points of Interest :
		- "Goldcap Town" : This first human settlement in the region is fortified by a robust stone wall and surrounded by a small forest, offering 
	shelter and supplies for players. Inside, it features administrative and military areas, with vendors and a grand warehouse for trading.
		- "Choopmine Rigger" : An abandoned mine resembling a small fortress, this site is heavily protected by rank III monsters. The mine's 
	external wall and watchtowers add to its castle-like appearance, while inside, remnants of mining equipment tell tales of its former glory.
		- "Sundry Lake" : A central feature of the Anghal Desert, this lake provides a rare respite from the harsh environment, teeming with life and 
	surrounded by lush vegetation.

---

	Mechanics:

- Movement Restrictions : Players cannot use flying mounts to traverse the crater or navigate near the cliffs. All roads radiate from Goldcap Town, 
leading towards points of interest while ensuring that monsters do not spawn along these paths.
- Environmental Hazards : The "Blazing Heat" debuff adds an additional layer of difficulty, encouraging players to strategize their movements and 
utilize resources effectively. Special items or mounts can mitigate these effects, enhancing gameplay dynamics.