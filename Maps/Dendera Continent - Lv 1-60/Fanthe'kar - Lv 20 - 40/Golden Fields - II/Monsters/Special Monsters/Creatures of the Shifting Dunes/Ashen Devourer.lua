			Ashen Devourer (Legendary Sand Wyrm – Pit Apex Predator) 

- Type : Unique Mini-Bosses (One-of-a-Kind Encounters)  
- Size : Large  
- Threat Level : High  
- Tactics : Unique Abilities, Unpredictable Movement, Area Control  
- Weakness : Varies per creature  

	Description :   
- Massive, centipede-like beast that moves beneath the sand.  
- Emerges from pits suddenly, coiling around players to constrict them.  
- Sandstorm Roar → Kicks up a localized sandstorm, reducing vision and making ranged attacks inaccurate.  
- Burrow & Strike → Can retreat underground mid-fight and emerge behind players.  
- Rare Drop : Chitin-Plated Core (used in crafting desert-resistant armor).  

