			Serpent Maw Traps (Carnivorous Desert Plants – Environmental Hazard & Predator)  

- Type : Stationary Predatory Plant  
- Size : Large (extends 2-3 meters underground)  
- Threat Level : Moderate  
- Tactics : Camouflage, Latch & Hold, Poisonous Bite  
- Weakness : Slashing Damage (Swords, Axes)  
	
	Description : These carnivorous desert plants disguise themselves as ordinary sand patches but snap shut like a bear trap when disturbed. Their roots extend 
deep into the sand, acting like tentacles to grasp and hold victims, while their flower-like mouth secretes digestive acid to soften prey.   

	Combat Mechanics :  
- Latch Attack → If a player steps too close, the plant instantly grabs and holds them, immobilizing them for 5 seconds unless they break free.  
- Poisonous Bite → If not escaped in time, the plant delivers a venomous bite, inflicting a Poison DoT for 10 seconds.  
- Root Pull → If a player tries to run past, the plant lashes out with its roots, dragging them toward its maw.  
- Defensive Spore Cloud → If attacked, it may release spores that obscure vision for 5 seconds.  

	Rare Variant – Sandfang Maw  
- A much larger version of the Serpent Maw that can pull multiple players in at once.  
- Drops rare alchemical ingredients, such as Venomous Petals (used in poison crafting).  

