			Sand Drifters (Burrowing Insectoids – Ambush Predators)

- Type : Elite Insectoid  
- Size : Medium (about the size of a large wolf)  
- Threat Level : Moderate to High  
- Tactics : Burrow, Ambush, High Burst Damage  
- Weakness : Fire & Blunt Force Attacks  

	Description : The "Sand Drifters" are vicious, chitinous insectoids that burrow beneath the sand, lying in wait for prey. Their segmented bodies are covered in 
hard, cracked exoskeletons, mimicking the color of the desert to remain undetected. Their heads feature rows of serrated mandibles, designed to latch onto victims 
and tear through flesh quickly.  

	Combat Mechanics :  
- Burrow Ambush → They remain underground until players step near, then erupt from the sand to inflict high burst damage.  
- Claw Rend → A quick triple-slash attack that inflicts Bleed over time.  
- Acid Spray → Some larger variants spit corrosive venom, degrading armor durability.  
- Pack Hunters → Often found in groups of 2-4, making them more dangerous if not handled quickly.  

	Rare Variant – Dune Stalker  
- Larger, more armored version of the Sand Drifter.  
- Can briefly burrow during combat to reposition and strike from behind.  
- Drops rare crafting materials, such as Chitin Shards (for armor crafting).  

