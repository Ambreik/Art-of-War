			Point of Interest

	*Golden Fields : second area, biggest of the region. Located in the middle and consist in barren lands.
		*Alddan Field : located in the western side of the area, biggest.
			*Ancient Ruins : located in the north side of the area and is surrounded by stone walls. Lead to "Barh Khaon Cave" a dungeon deep in the mountains.
			*Verian Stable : tavern, located in the middle of the map, right where the two territories meet.
		*Arid Savanna : located in the eastern side of the area. In this place can be found trees and savanna wild animals.
			*Southern Abyll Town : located in the southern side of the area, surrounded by ruins of a settlement and huge blocks of stones.