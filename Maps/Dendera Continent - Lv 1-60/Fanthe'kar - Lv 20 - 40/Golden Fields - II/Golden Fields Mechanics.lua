			Mechanics of Golden Fields  


		1. The Blazing Heat (Environmental Hazard & Player Adaptation)  
- Mechanic : A persistent debuff, "Blazing Heat," affects all players in the Golden Fields. This debuff:  
	- Reduces movement speed by 5-10% based on the time of day.  
	- Increases stamina consumption for sprinting and combat actions.  
	- Causes periodic health loss if players fail to manage hydration (every 5 min).  

- Adaptation Methods :  
	- Consumables : Drinking Desert Berry Juice (purchasable from Verian Stable) or crafting Cactus Water (gathered from cacti).  
	- Shade Buffs : Standing under certain palm trees or inside Verian Stable temporarily negates Blazing Heat.  
	- Heat-Resistant Equipment : Special light armor pieces crafted, mitigate heat effects.  

---

	2. Shifting Dunes (Dynamic World Terrain Changes)  
- Mechanic : The sand dunes across Alddan Field shift gradually over in-game days, altering the landscape:  
	- Burying certain small paths or chests.  
	- Uncovering new potential ruins or entrances. 
	- Obscuring enemy positions, forcing players to navigate differently.  

- Special Effect : In some locations, the shifted dunes expose "Sinking Pits," which act as environmental traps:  
	- Falling into a pit leads to a minor DoT (damage over time) effect and temporary movement debuff.  
	- Some pits contain buried enemies, such as Sand Drifters (burrowing insects).  
	- Rarely, players may discover lost artifacts buried in the sand.  

