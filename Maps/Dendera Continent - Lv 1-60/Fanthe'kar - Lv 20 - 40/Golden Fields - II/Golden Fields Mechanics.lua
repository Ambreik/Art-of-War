		4. Giant’s Echo (Echoes of the Ancient Ruins Mechanic)  
- Mechanic : Within Ancient Ruins, a powerful latent magic lingers, occasionally manifesting as Giant’s Echoes (ghostly manifestations of the former inhabitants).  
	- These echoes do not attack, but if a player stays near them for too long, they gain the "Cursed Vision" debuff, which:  
		- Causes illusions of giant warriors to appear in the distance.  
		- Increases aggro range for nearby monsters.  
		- Occasionally forces players into a mini-battle where they must defeat a phantom entity to cleanse the curse.  

- Hidden Effect :  
	- Some echoes whisper in the giant’s language, providing clues to hidden entrances or Barh Khaon Cave pathways.  
	- Players who study enough echoes unlock a rare questline tied to uncovering more about the lost civilization.  

