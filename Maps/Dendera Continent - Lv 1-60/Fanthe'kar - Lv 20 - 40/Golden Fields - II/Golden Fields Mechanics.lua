		3. Nomadic Caravan System (Dynamic NPC & Trading System)  

	Core Mechanic :   
	Nomadic Caravans traverse Golden Fields, Arid Savannah, and desert roads on a randomized cycle, stopping at designated rest areas such as:  
- Ancient Ruins (Potential hidden artifacts, rare item vendors)  
- Bridges & Crossroads (High trade activity, risk of ambushes)  
- Southern Abyll Town (Main trade hub affected by caravan success/failure)  

	Caravan Features:  
- Sell Unique Heat-Resistant Gear & Survival Tools  
	- Desert Cloaks & Cooling Wraps (reduce heat exposure effects)  
	- Special Water Containers & Rations (grant buffs for survival in harsh conditions)  
	- Rare Desert Crafting Materials (fibers, special herbs, animal hides, and minerals from traders who travel far)  
- Offer Limited-Time Quests  
	- Caravan merchants rotate unique quest offers based on the player’s reputation or recent world events.  
	- Some caravans provide highly rewarding fetch or delivery quests that expire when the caravan moves.  
- Provide Temporary Heat Protection  
	- Players standing within the caravan’s shade receive a temporary heat resistance buff, reducing stamina drain from the sun.  

---

	Caravan Risk & Events  
	Caravans aren’t just roaming shops; they face dynamic threats, making them valuable world events for players:  

	1a. Bandit Ambush Events  
- Bandit NPCs may target caravans, triggering escort-style defense missions.  
- Players can join the caravan as defenders, earning coin, reputation, and unique loot if they prevent its destruction.  
- Bandit AI adapts to player numbers:  
  - Small ambushes if fewer players are present.  
  - Larger-scale raid events if many players are escorting.  

	2a. Caravan Destruction & Economic Impact  
- If a caravan is successfully destroyed, it disappears until the next cycle.  
- This failure affects the economy of Southern Abyll Town:  
  - Certain goods & items become unavailable until another caravan arrives.  
  - Prices for desert survival gear increase due to scarcity.  

	3a. Special Caravan Encounters (Randomized Events)  
In addition to bandits, caravans may encounter other surprises:  
- Lost Travelers: A lone NPC needing rescue or offering a one-time reward.  
- Merchant Rivalries: Traders within the caravan may offer exclusive deals if players pick sides in a trade dispute.  
- Rare Exotic Traders: Sometimes, an unusual NPC vendor appears, selling mystical goods, rare mounts, or hidden artifacts.  

---

		Caravan Reputation System  
	Players who consistently protect caravans or assist traders can unlock:  
- Exclusive Caravan Titles (e.g., "Desert Guardian")  
- Discounts on rare goods  
- Access to special nomadic gear & mounts  

