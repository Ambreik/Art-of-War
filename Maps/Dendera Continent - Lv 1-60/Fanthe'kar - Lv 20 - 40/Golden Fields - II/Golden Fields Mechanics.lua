		Southern Abyll Town Defense (Living Town System – Extended Version)  

	Southern Abyll Town is a fortified desert settlement that relies on player intervention to maintain stability. It is a semi-dynamic town where NPC behaviors, 
town functions, and economy are influenced by player actions.  



	1. Core Mechanics of the Living Town System  

- NPC Guards & Defenses:  
	- Patrolling NPCs actively watch for threats, scanning the nearby desert for dangers.  
	- Mounted Lookouts on towers signal warnings when hostile creatures or bandits approach.  
	- Defensive Ballistae & Traps can be maintained by players to boost protection.  

- Siege & Attack Triggers:  
	- Attacks happen at randomized intervals, but threat level increases if players neglect the town’s defenses.  
	- Aggressors can be desert raiders, corrupted beasts, or sandstorm-driven horrors.  
	- If left undefended, the town's economy weakens, NPCs disappear, and trade routes become riskier.  

---

	2. Siege Events – How They Work  

- Trigger Conditions:  
	- A siege event may be triggered based on:  
		✅ Enemy Raids: Bandit Lords, Rogue Mercenary Groups, or Nomadic Warbands.  
		✅ Monster Attacks: Large-scale assaults from sand creatures or mutated desert beasts.  
		✅ Environmental Hazards: Violent sandstorms attracting corrupted entities.  

- Event Flow:  
	1. Phase 1 – Warning & Preparation :  
		- Players in the region get a "Call to Arms" message.  
		- NPC guards shout warnings, and defensive NPCs prepare for battle.  
		- If enough players reinforce the town, the defenses hold.  
   
	2. Phase 2 – Battle for the Town :  
		- Waves of enemies attack, each with different behaviors:  
			- Bandits target storage & loot merchants.  
			- Monsters attack NPCs & break walls.  
			- Storm creatures aim to corrupt land & spread chaos.  
		- Players must defeat key enemy leaders to stop the invasion.  

	3. Phase 3 – Victory or Defeat:  
		- If players win : Town remains functional, and a reputation boost is awarded.  
		- If players lose :  
		- Some NPC vendors become unavailable (e.g., the Blacksmith might be kidnapped).  
		- Trade routes temporarily increase in cost or danger.  
		- Players must launch a counter-attack quest to restore town stability.  

---

	3. Town Reputation & Player Influence  

- Town Standing & Reputation System :  
	- Players can build a personal reputation with Southern Abyll by defending the town.  
	- Higher reputation unlocks unique vendor items, quests, and desert-themed rewards.  

| Reputation Rank | Title | Benefits |
|----------------|------------|--------------------|
| Neutral | Outsider | Limited access to town goods. |
| Friendly | Defender of Abyll | Discounts on survival gear. |
| Honored | Guardian of the Walls | Access to special caravan trades. |
| Revered | War Leader of Abyll | Can command small NPC squads in sieges. |
| Exalted | Champion of the Sands | Unlocks elite desert mount & armor sets. |

- Economic Impact of Reputation:  
	- If the town is frequently defended, more vendors appear, and better gear is sold.  
	- If the town is neglected, trade weakens, and some vendors leave.  

---

	4. Player Contribution to Town Stability  

- Reinforcing Walls & Defenses:  
	- Players can gather materials (wood, stone, metal) to repair town walls.  
	- Engineers & Blacksmith players can upgrade defensive weapons like ballistae & barricades.  

- Guard Assistance Quests:  
	- Patrols: Players can escort guards on desert patrols to reduce enemy aggression.  
	- Recon Missions: Players can scout enemy camps before an attack, allowing for preemptive strikes.  
	- Prisoner Rescues: Some sieges result in kidnapped NPCs. Players can rescue them for bonuses.  

