		6. Sandstorm Incursions (Unpredictable Hazard Events)  
- Mechanic : Randomly occurring sandstorms sweep through Golden Fields, causing:  
	- Reduced visibility (similar to fog of war mechanics).  
	- Increased enemy spawns as sand creatures emerge.  
	- Movement speed penalties, forcing players to take shelter.  

- Event Interaction :  
	- Certain enemies become stronger in a sandstorm, gaining temporary buffs to attack speed and evasion.  
	- Verian Stable becomes a safe zone, and NPCs will warn players when a sandstorm is near.  
	- Some hidden dungeons or ruins entrances only appear during a sandstorm.  

