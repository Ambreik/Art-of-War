			Fierdon – Mechanics Development  

	1. Regional Environmental Mechanics  
	Fierdon is the transition point between desert and maritime ecosystems, leading to the following mechanics:  

	Dynamic Water Levels :  
- Rivers in Fierdon rise or lower based on seasonal rainfall.  
- During rainy periods, riverbanks overflow, opening hidden caves or buried relics.  
- During dry seasons, smaller rivers shrink, forcing wildlife to relocate and revealing shipwrecks or lost treasures.  

	Wildlife Migration System :  
- Some aggressive wildlife species migrate from Riven Fields to Greenden Savanna when resources change.  
- If certain animal populations overgrow, they threaten NPC villages, triggering hunting missions or player-influenced culling events.  

	Shipwreck & Relic Discovery :  
- Tide changes affect the accessibility of shipwrecks in Greenden Savanna.  
- Players can scavenge shipwrecks for rare crafting materials or historical lore items.  
- Some relics grant temporary buffs or unique status effects when studied.  

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

	2. Riven Fields – Mechanics  

	Wild Fauna & Archaeological Exploration  

	Dynamic Wildlife System :  
- Players may encounter packs of wild creatures that react based on weather & player actions.  
- Certain creatures become more aggressive at night or when resources are low.  

	Ancient Campsites & Ruins System :  
- Players can analyze ruins for historical insights and temporary buffs (e.g., increased defense from understanding ancient battle formations).  
- Some ruins unlock puzzle-based quests, where players decipher old symbols to find hidden treasure.  

	Mount & Taming System (Stumb Stables Feature)  
- Taming Quests : Players can capture & train regional mounts, including:  
	- Swiftland Runners (fast but stamina-draining)  
	- Horned Elk (durable & good for traversal)  
- Tamed mounts can be bred or traded with specialized NPCs.  

	Rare Apex Predators :
- Fierdon’s wilderness hides Apex Creatures that appear randomly and roam freely.  
- Example: The Dreadfang Leopard  
  - A highly mobile, nocturnal predator that ambushes players and disappears into the shadows if not defeated quickly.  

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

	3. Greenden Savanna – Mechanics  

	Survival & River-Based Exploration  

	Suspended Rope Bridge System :  
- Some rope bridges are weak & can collapse, forcing players to repair them or swim across.  
- If broken, ferries temporarily replace bridges but require a small fee to use.  

	Greenden Valley Predators :  
- Players must balance hunting & conservation, or else local species overpopulate and become hostile.  
- If overhunting happens, Pindar/Knight conservation NPCs intervene, adding new moral decision quests.  

	"Living Shipwrecks" Discovery Mechanic :  
- Some shipwrecks are inhabited by ghostly past crews, leading to interactive spectral storytelling.  
- Players must solve mini-quests related to these wrecks before claiming valuable loot.  

	"Gringgle Lake Fishing Challenge" (Precursor to Riddleport Event)  
- Players can compete in catching rare fish that migrate to the lake once per month.  
- Some fish grant alchemy ingredients, while others provide buffs if consumed.  

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

	4. Riddleport – Mechanics  

	Portside Trade, Fishing, and Naval Adventures  

	Faction-Aligned Trade Economy :  
- Players can engage in maritime trade based on faction reputation.  
- Knights & Pindars control different trade routes, affecting what goods are available.  
- Smugglers appear when law enforcement is weak, offering black market quests.  

	"Fish Trap Mania" (Monthly Fishing Event)  
- Hosted by Crazy Marlo – The Mad Fisher.  
- A high-speed fishing challenge where players must catch the most rare species in limited time.  
- Special fish-based weapons & armor skins are given as rewards.  

	Legendary Sea Beasts (World Boss Encounter)  
- Some giant sea creatures appear during storms and must be defeated before reaching the port.  
- Players must use siege ballistae to help defend the port town.  

