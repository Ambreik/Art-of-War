			Legendary Sea Beasts (World Boss Encounter)

	Overview : The waters near "Riddleport" are home to colossal sea creatures—ancient beasts that dwell beneath the waves, surfacing unpredictably to challenge 
those who dare to sail or fish in their territory. These creatures are not tied to a world-scale event but instead appear based on *natural in-game triggers, 
creating a dynamic and immersive world.  

	These creatures can:  
✅ Threaten ships and fishing activities  
✅ Disrupt trade routes and impact Riddleport’s economy  
✅ Force players to use naval defenses and siege weapons  
✅ Create temporary changes in fish migration and merchant prices  

	Global Announcement :  
- "[Continent-Wide] The tides shift, and an ancient terror rises from the abyss! Will the warriors of Fierdon stand their ground?"  

	"The Beast Rises" (Boss Reveal)
- The Legendary Sea Beast emerges from the depths, towering above the waves and roaring as it approaches Riddleport.  
- Massive tentacles, spiked fins, or armored scales determine each sea beast’s unique attacks.  
- NPC ballistae crews scramble to prepare defenses, calling for player reinforcements.  

	Legendary Sea Beasts Variants :  
1.Torrak, The Abyss Leviathan – A serpentine horror with lightning-infused attacks that chain to multiple players.  
2.Dreadjaw, The Hungering Maw – A colossal shark-like beast that swallows players whole, forcing them to fight inside its stomach to escape.  
3.Krala’Thul, The Nightmare Below – A monstrous tentacled kraken, with limbs that crush ships and drag players into the sea.  

