		How the Mechanic Works  

	Triggers for Legendary Sea Beasts to Surface  
	
	Sea beasts do not follow a fixed timer. Instead, they appear naturally when specific conditions are met in the world.  

1. Stormy Weather Conditions  
- When severe storms hit Riddleport, the creatures rise from the depths, disturbed by the raging sea.  
- Players may notice NPCs reacting, such as fishermen evacuating the docks and guards preparing siege weapons.  
- Some shipwrecks may wash ashore, hinting at past battles with these creatures.  

2. Overfishing & Ecosystem Disturbance  
- If players and NPCs overfish in the region, depleting certain fish stocks, the beasts may surface to defend their hunting grounds.  
- Fishing spots around Riddleport temporarily dry up, forcing players to travel further for rare fish.  
- Some legendary fish only appear after a sea beast is defeated, creating an incentive to fight them.  

3. Large-Scale Naval Activity or Pirate Battles  
- When player-led or NPC-driven ship battles occur near Riddleport, the noise and blood attract massive sea predators.  
- These creatures do not discriminate—they attack both pirates and the town’s defenders.  
- If left unchecked, they sink merchant ships, causing a temporary delay in trade routes.  

4. High-Ranking Players or Faction Warfare Nearby  
- If multiple high-level players or famous sea captains enter the area, the beasts may view them as a challenge.  
- This makes the system scale with player progress, preventing weaker players from encountering impossible fights too early.  

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

		Player Interaction & Mechanics  

	Defending Riddleport with Siege Weapons  
- Riddleport’s harbor is equipped with stationary siege ballistae and harpoon launchers.  
- Players can manually control these weapons to weaken the beast.  
- Harpoons can pin down parts of the creature, while ballistae deal armor-piercing damage.  

	Naval Combat Mechanics  
- Players with ships can engage the beast at sea, using cannons, fire arrows, and explosive barrels.  
- Damaged ships must return to port for repairs, adding a risk vs. reward element.  
- Some beasts attempt to capsize smaller boats, forcing players to adapt their strategy.  

	Trade Disruptions & Merchant Reactions  
- If a sea beast lingers too long, merchant ships will refuse to dock, causing:  
	Increased prices for imported goods.  
	Rare items disappearing from vendors.  
	NPC traders offering bounties to clear the sea.  

- If players defeat the beast, merchants lower their prices for a short time as a reward.  

	Fishing Impact & Rare Spawns  
- During a sea beast encounter:  
  ❌ Some fishing areas become unsafe, forcing players to relocate.  
  ✅ If the beast is defeated, new legendary fish spawn for a limited time.  
  ✅ Special "Beast Remains" fishing spots appear, where players can fish up rare resources from the battle.  

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

		Outcomes & Effects  

	If players successfully repel the beast:  
✔️ Riddleport’s trade resumes, and fish return to normal.  
✔️ Some rare fish species spawn, rewarding those who participated.  
✔️ Merchant NPCs lower prices, showing gratitude to defenders.  

	If the beast remains unchecked:  
❌ Merchant ships avoid Riddleport, making trade more expensive.  
❌ NPC dialogue changes, with townsfolk fearing the sea.  
❌ Fishing resources are disrupted, forcing players to travel for certain fish.  

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

	Rewards & Aftermath  

	Player Rewards :  
- Legendary crafting materials (used for sea-themed armor, weapons, or enchantments).  
- Unique titles ("Defender of Riddleport" / "Beastslayer of the Tides").  
- Faction reputation boosts with local naval forces.  
- A chance to obtain a rare pet or mount related to the defeated beast.  

	World Impact (Success or Failure) :  
	If players succeed :  
- Riddleport remains safe, and fishermen resume their daily activities.  
- The Great Sea’s economy is stable, and trade routes are restored.  

	If players fail :  
- The monster destroys parts of Riddleport, disabling key vendors & trade for a week.  
- Sunken debris and shipwrecks block maritime access, forcing players to clear wreckage via special quests.  
- Some NPCs are lost at sea, requiring rescue missions to retrieve them.  

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

		Why This System Works as a "Living World Mechanic"  

- No Artificial Events – This is a natural part of the game world, triggered by player actions and environmental factors.  
- Scales with Player Presence – The mechanic adjusts based on who is in the region, preventing unfair encounters for lower-level players.  
- Multiple Playstyles Included – Players can engage in siege warfare, naval combat, trade, and fishing in meaningful ways.  
- Adds Depth Without Forcing Participation – Those who ignore the creatures will feel their impact indirectly through trade and fishing changes, but they aren’t 
required to fight them.  

