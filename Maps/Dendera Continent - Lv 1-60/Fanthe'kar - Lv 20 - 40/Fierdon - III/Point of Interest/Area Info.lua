			Area Info: Fierdon

	"Fierdon" is the third and smallest area of the Fanthe'kar region, strategically positioned on the eastern side. This area boasts direct access 
to the "Great Sea" at its southeastern edge, providing a vital maritime link despite the arid desert lands to the east, separated by a towering 
mountain range. Fierdon thrives with lush vegetation and abundant wildlife, contrasting sharply with the surrounding barrenness.

	Sub-Areas:

1. "Riven Fields"  
	Occupying the western side of Fierdon, Riven Fields is the largest sub-area, characterized by a network of rivers originating from the nearby 
mountains. These rivers flow eastward, converging into the "Great Sea", and are interspersed with pockets of savannah and forest.

	- "Stumb Stables" : A small village situated near the western mountains, Stumb Stables is home to NPCs studying the local wildlife. The village 
features several cozy houses nestled among rocky outcrops and tall trees, creating a natural habitat for both humans and animals. The centerpiece is a
large stable accompanied by blacksmith tools, making it a hub for adventurers seeking to learn more about the region's fauna. A massive tree with a 
towering trunk serves as a prominent landmark visible from afar.

	Riven Fields is rich in archaeological significance, with various ancient campsites and relics scattered throughout. The area’s diverse ecosystems
support aggressive wildlife and hidden treasures for players to discover.

2. "Greenden Savanna"  
	This southern sub-area is filled with abundant greenery, including shrubs and tall trees, offering a striking contrast to the arid landscape of 
the north. The territory slopes downward toward the south, creating a valley-like impression that culminates in "Gringgle Lake", fed by one of the 
river's arms.

	The landscape here is beautifully interwoven with a network of rivers connected by suspended rope bridges, allowing for scenic crossings. The 
terrain gradually descends toward the lake, providing a picturesque view of the surrounding flora and fauna.

	As players traverse the Greenden Savanna, they will encounter remnants of shipwrecks and traces of sea monsters that once inhabited the area, 
along with evidence of human activity in the form of small wooden houses built on stilts. These homes are particularly prevalent in the northeastern and central parts of Greenden Savanna, where the land is flatter and more hospitable.

3. "Riddleport"  
	Located in the eastern section of Fierdon, Riddleport serves as the region’s primary human settlement and port. The port is formed by the estuary 
created where one of the rivers meets the Great Sea, providing an essential hub for trade and fishing.

	Riddleport is well-guarded, featuring armed soldiers patrolling the harbor and a fleet of warships anchored nearby to fend off potential 
incursions by hostile forces. The harbor's design includes several pontoons for docking fishing vessels, while NPC fishermen engage in their daily 
catch at the northern part of the harbor.

	The port also acts as a social and economic center for adventurers, offering quests, trade opportunities, and a gateway to explore the maritime 
world beyond the Great Sea.

	Here, "Crazy Marlo – The Mad Fisher" is wondering the streets of the port to find the best fishermen for his competition that take weekly. He start the event 
called "Fish Trap Mania" a monthly event that draws in players eager to test their skills for unique prizes.

