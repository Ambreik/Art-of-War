			District-Specific Mechanics

		The Royal District
	Key Mechanics:  
1. Nobles & Military Clashing  
	- Faction Dynamics : Lightbright Shields may have ideological differences with nobles, leading to questlines where the player mediates disputes or even prevent	
assassination plots. 
	- Quest-Based Impact : Players can align with either the Lightbright Shields or Noble Factions (depending on player choice), influencing city politics.   
	- Stealth Missions : Certain assassination plots can be discovered by spying or listening to noble gossip.Players might have to investigate, gather information,
and uncover assassination attempts on high-ranking officials or royalty.

2. Royal Decrees & Bounties  
	- Bounty Board : A public bounty board will be available within the Royal Palace or Hall of Heroes, where royal decrees are issued. Players can pick up bounties
for criminals or rogue faction members such "Outlaws".  
	- Factional Bounties : Players may also encounter personalized bounties that affect their standing with different factions within the Royal District, such as 
Military Leaders, Noble Houses, or Assassins Guilds.

3. Political Influence :  
	- Political Intrigue : Interaction with noble NPCs could unlock side quests related to political maneuvers, assassination plots, or espionage. Players’ 
decisions can sway the political landscape in subtle ways.
	- Royal Favor : Completing missions for nobles or the royal family may increase a player’s favor with the monarchy, unlocking special quests, alliances, or even
exclusive items.

---

		The Military District
	Key Mechanics:  
1. Recruitment System :  
	- Join the Military : Players can join Dravenhold’s Army (through the Military Academy) for some questlines or side quests, that can provide some substantial 
REP with the faction of "Dendera".
	- Combat Training : In the Arena for Elite Training, players can engage in mock battles or PvE challenges that increase combat skills and provide rewards such
as weapons or military titles.

2. Siege Warfare :  
	- Siege Workshops : The Siege Workshop could provide opportunities for crafting siege equipment or participating in city defenses. There might be city-wide 
events where players help defend the city from external threats.
	- Siege Testing Grounds : Players can test new siege weapons in special combat zones outside the city.

3. Military Bounties :  
	- Defend the City : Players may encounter military-related bounties, such as protecting key figures from attack, hunting down deserters, or fighting off hostile
forces outside the city.  
	- Guarding Missions : Quests in the Barracks or Armory may focus on training new recruits, engaging in surveillance missions, or even tracking down spies.

---

		The Port District
	Key Mechanics:  
1. Ship Trading & Naval Warfare  
	- Fishing & Trade Ships : Players can interact with fishing boats or merchant ships to gain unique materials or trade goods, some of which may only be available
binteracting with NPC sailors in the Port District.  
	- Naval Warfare : Players may occasionally be sent to outer regions like the Middlean Islands on naval missions, which involve boarding ships or defending trade
routes from pirates.

2. Secret Smuggler’s Alley :  
	- Underground Trade : Smugglers operate in the Secret Smuggler’s Alley, a hidden dock area that connects to underground passages within Dravenhold. Players can 
access it through stealth missions or bribery.  
	- Quest Integration : Smugglers may offer underground quests, including stealing goods, dealing with black market vendors, or avoiding the attention of city 
guards. These quests can unlock rare items or black market goods not found in regular shops.

3. Import/Export Licenses (Reputation-Gated Access)
	- Certain vendors or trade ships only appear for players with merchant licenses, granted by doing reputation tasks or bribes in the Smuggler’s Alley.
	- Without the license, players are denied access or charged 200% for trade goods.

4. Pirate Bounties & Naval Infiltration
	- Occasionally, pirate ships dock disguised as traders. Players with the right intelligence (or REP with naval factions) can uncover plots, leading to combat or
stealth missions aboard the ship.
	- Reward: black market goods, smuggler caches.

5. Sea-Worn Artifacts (Daily Collection)
	- Players can comb the docks and waterfronts for lost goods like crates or messages in bottles. Some open daily quests, others are part of hidden chain lines.

---

		The Residential District
	Key Mechanics:  
1. Hidden Merchants & Quests :  
	- Secret Locations : Some hidden merchants may pop up in the Residential District, selling rare or unique items that are not available elsewhere in the city. 
These merchants could be connected to underground factions or even criminal organizations.
	- Cultural Quests : Players may discover local stories or hidden secrets through interaction with the residents, who may send them on quests related to lost 
heirlooms, hidden treasures, or forgotten family histories.

2. Community Events :  
	- Festivals & Markets : The Public Squares could host community events, such as festivals, celebrations, or spontaneous markets. These events could offer 
limited-time quests, discounts, or unique items from local artisans.

3. Resident Loyalty (REP increase method mechanics)
	- Helping locals boosts a hidden "REP" in the Residential District. With high enough loyalty:
* A secret merchant appears.
* Locals warn of nearby bounties or secret trades.
* A rare pet questline unlocks.

---

	Additional Mechanics to Consider:

- Faction Reputation :  
	- Players can influence various factions (e.g., Lightbright Shields, Royal Family, Nobles, Military, Pirates). Each faction can unlock specific quests, items,
and locations. Reputation can affect interactions, and quests might change depending on the faction’s view of the player.

- City Events :  
   - City-wide events might occur, such as royal parades, military drills, or pirate attacks, which can affect all districts. Players can join or react to these 
events, earning rewards or affecting city reputation.

- Crime System :  
   - A detailed crime system where NPCs can report crimes, leading to bounty hunters hunting down offenders. Players can also choose a side—help law enforcement 
or join the underground criminal world.

