			Dravenhold Mechanics
			
		City-Wide Mechanics  
	These mechanics apply to the whole of "Dravenhold" and affect every district.

1. Windows & Interiors :  
	- Only major buildings are accessible.  
	- Player Interactions : Players can enter key buildings, such as the Royal Palace, Command Center, Taverns, Guild Halls, and Marketplaces, but not every 
building in the city will be interactive. 
	- Visual Appeal : Some windows allow players to see NPC interactions or ongoing events inside certain areas, adding atmosphere.

2. Mount Restrictions :  
	- No flying mounts allowed inside the city.  
	- Ground-Based Mounts Only : Players must use ground mounts or travel by foot within the city. Flying mounts are restricted to areas outside the city walls, 
including the wilderness, ensuring a more grounded and immersive city environment.  
	- Special Mounts in City : Certain areas like the Royal Gardens or Port District might allow access to water mounts, like small boats, for exclusive quests or 
activities.  
   
3. Guard Vigilance :  
	- Aggressive players will be punished.  
	- Combat Restrictions : Guards will engage hostile players in certain areas, and violent actions in public spaces (e.g., Royal District, Military District) will
attract a guard response.  
	- Consequences of Aggression : If a player attacks a guard or non-hostile NPC, a city-wide alert goes off, and NPC guards will pursue the criminal, dealing 
significant damage. If the player is not defeated, they'll be arrested and taken to a prison or bounty hunter. 
	- Defend the City : Players who stop criminals or aid the guards may gain reputation or bounties as rewards.
	- Guardians may "sense" if an "Outlaw" it will chase right away.
	- Guardians may look for players which have a bounty on them and try to catch them.
	- Guardians may co-op together for the safety of others.
	
4. Guidance System :  
	- Guards provide directions.  
	- NPC Guidance : Guards at key locations in the city (Market Square, Royal District) will offer directions for players who need assistance.  
	- Quest Integration : Guards may also guide players to active quests in specific districts, such as the Royal Decree or Military Missions, making it easier to 
find hidden or key locations.

5. PK Restrictions :  
	- City is a non-PvP zone.  
	- Criminal Behavior : While PvP isn’t enabled, players can engage in criminal activity (e.g., theft, assassination). Those who break the law may have bounty 	
placed on them by city factions (e.g., Royal Guard, Lightbright Shields, Military).  
	- Consequences for Crimes : High-profile criminals may be restricted to come inside the city for a certain period of time, sending outside of the walls, an 
amount of money as payment for the crimes and a debuff, and other players can track down criminals to collect bounties.  
	- Faction Conflict : Rival factions might interact with criminals in ways that could impact reputation.

6. District Phasing Based on REP or Story Progress : 

	Certain districts may visually change depending on player actions:
	- Helping nobles might beautify the Royal District, while helping criminals results in more shady NPCs or graffiti appearing.
	- Residential District homes might get boarded up if crime increases.

7. Watchtower Alert System (Zone-Wide Layer) :
	If a crime is committed and guards are killed:
	- Nearby watchtowers send out a warning flare.
	- Area becomes locked down (more guards, no fast travel).
	- After a set time, it resets unless another crime happens — making the area harder to move through temporarily.

8. Disguises & False Identity (Criminal Tools) :
	- Players aligned with criminal factions can buy or earn disguises to avoid detection in high-security zones (Royal, Military).
	- If caught, disguise is burned, and a debuff is applied.

9. Public Services / Vendors with Cooldowns : 
	- Some vendors (e.g., in Royal or Residential) only appear at certain times of day, night vendors or traveling salesmen.
	- Certain vendors might be tied to festivals, parades, or district conditions (e.g., military vendors appear after war drills).

10. Vampire Scouts : 
	- At night, around certain districts (Royal District, Military District) and nearby walls of the city, Vampire NPCs are spawned and guard those location.
	- Have similar mechanics as normal Guardians, but they can fly and doesnt offer indications to different places.
	- Those are elite Guards and have an increase speed.
	
