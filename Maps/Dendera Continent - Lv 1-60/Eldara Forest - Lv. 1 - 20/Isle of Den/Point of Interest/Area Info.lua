			Isle of Den Overview

	Location :  
- An island situated close to the "Port of Ricko".  
- Accessible only by a specialized boat obtained from a marine trader NPC.

	Shape :  
- The island resembles the letter "L" open to the mainland, with a mountainous region featuring stony peaks on the far side.

---

	Key Features

1. "Fortified Villages"  
	- Description : Scattered around the island, these villages were established by traitors fleeing from the king's army.  
	- Construction : Comprised of small wooden houses surrounded by wooden fences shaped like an "X" with sharp points, providing defense against 
intruders and aggressive wild animals, particularly at night.  
	- Survival Structures : Along the rivers, there are pontoon structures where the colonists likely fished to sustain themselves.

2. "Dunrall Temple"  
	- Location : Situated in the corner of the island's mountainous region, at the center of the island.  
	- Description : A large, multi-tiered temple built from stone blocks in the form of a stepped pyramid, featuring tall pillars at each corner that 
hold burning torches.  
	- Significance : Named after "Duran, the Gimp", the leader of the rebellion during the "Second Great War", who led soldiers to flee here after 
plundering supply ships. The temple now serves as a prison for him and his commanders, who drank the "Immortal Wine" and are cursed to live forever.  
	- Access : The road leading to the temple is made of stones, adding to its historical significance.

3. "Natural Features"  
	- River System : A river flowing from the northern part of the island creates a lagoon in the south, where a small, seemingly abandoned port 
exists.  
	- Beaches : The expansive beach along the mainland side is inhabited by various wild creatures, including wild crabs, giant turtles, and aquatic 
beings that patrol for potential prey.

---

	Gameplay Mechanics

- Aggressive Monsters : All creatures on the island are hostile, providing a constant threat to players.  
- Human Monsters : Human-like monsters utilize ranged attacks that deal critical damage. (those are the traitors)
- Mount Restrictions : Flying mounts are prohibited on the island, although players can reach it using a flying mount.
- On the beaches, sometimes, players can see "Human" monsters (traitors) fighting with wild animals.

---

		Summary

	The "Isle of Den" serves as a hidden refuge for traitors and outcasts, surrounded by a mix of natural beauty and danger. With its fortified 
villages, the imposing "Dunrall Temple", and a variety of aggressive wildlife, players will face challenges and uncover secrets of the past while 
navigating this treacherous island. The combination of aggressive monsters and rich lore makes the "Isle of Den" a compelling destination for 
exploration and conflict.