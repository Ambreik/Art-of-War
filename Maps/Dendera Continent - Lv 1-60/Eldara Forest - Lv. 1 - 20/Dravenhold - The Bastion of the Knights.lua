			Dravenhold - The Bastion of the Knights 

	Region Overview : Dravenhold is the capital of the "Kingdom of Knights", a formidable city that stands as the stronghold of warriors who once hunted the 
"Old Gods". This region consists of four distinct areas:  

- "Dravenhold City" – The capital itself, a symbol of strength and discipline.  
- "Forest of Trevale" – A dark and enigmatic woodland where vampires dwell.  
- "Isfield Territory" – The largest and most resource-rich area of the kingdom.  
- "Isle of Den" – A secluded island where thieves and traitors hide from the wrath of the army.  

	Dravenhold is situated in the middle-east of the realm, serving as the first major area where players begin their journey. The city itself is located in the 
southern region of the kingdom, surrounded by vast forests and rugged terrain.   

	The capital of Dravenhold is a massive city, enclosed by titanic stone walls with three heavily fortified gates, each leading to key strategic points in the 
kingdom. These gates connect the city to the wilderness, the port, and the Isfield Territory, offering different challenges and opportunities for adventurers.   

	Geographical Features :   
	The region of Dravenhold is surrounded by natural defenses :  

- To the west, the land slopes into the "Isfield Territory", home to fertile lands, hidden ruins, and old battlefields.  
- To the east, the "Forest of Trevale" stretches out, an eerie and mist-filled expanse where vampires claim dominion.  
- To the south, the capital’s walls are met by a narrow beachline, except for the "Port of Ricco", the kingdom’s main connection to the sea.  
- To the north, towering mountains form a natural barrier separating Dravenhold from the harsh "Fanthe'kar Desert", with only a single portal and the teleportation NPC, "Assuro", providing access to the arid lands beyond.    

	The southwestern coastline is dotted with scattered islands, utilized for "Island Expeditions", a feature that unlocks at level 40 through the NPC Ignatio. 
	These islands remain inaccessible until a player starts the mini-chain quest, at which point unique monsters spawn with valuable loot.   


