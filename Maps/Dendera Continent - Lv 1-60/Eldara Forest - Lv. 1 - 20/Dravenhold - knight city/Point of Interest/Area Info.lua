			Dravenhold City - The Stronghold of the Knights

		City Overview :    
	Dravenhold, the heart of the "Kingdom of Knights", is a fortress city steeped in tradition, discipline, and warcraft. It was founded during the age of the 
"Old Gods'" downfall, serving as the stronghold for the knights who hunted them down.    
	Its architecture reflects its history—tall, pointed-roof buildings, massive stone structures, and fortified towers dominate the skyline. The city is divided 
into distinct districts, each serving a unique function in the kingdom’s daily life.   

	Two main gates control access to the city:  
- The "Western Gate" leads toward the training fields and open wilderness.  
- The "Southeastern Gate" connects to the "Port of Ricco", the kingdom’s maritime hub.    

	The streets of Dravenhold are paved with aged stone, and despite its military origins, it is far from a grim city. Trees line the streets, banners of the 
"Royal Order of Knights" flutter in the wind, and the city is alive with commerce, training, and governance.  

		Districts of Dravenhold  

	1. Western District - The Training Grounds  
	Located just beyond the fortress gates, this area is dedicated to the art of combat. Here, aspiring warriors, knights, and hunters train under the watchful eyes
of seasoned instructors. The "Silvershock Shields", the city’s elite guards, patrol the district, ensuring order.  

- Structures & Features :  
	- Barracks where soldiers are stationed.  
	- Combat Arenas where duels and training exercises take place.  
	- Weapon Storage Areas, where old and damaged weapons are stacked for inspection.  

- Creatures & NPCs :  
	- Chickens, roosters, cats, and dogs roam freely.  
	- Trainers offering combat skills and quests.  

---

	2. Northpointed District - The Royal Quarter  
	At the city’s northern edge, this district serves as the seat of power for the kingdom. The grand statue of "King Alter" as a young warrior dominates the 
district, a tribute to the knights' legacy.  

- Structures & Features :  
	- "The Royal Castle", home to the king and council members.  
	- "The Council Chamber", where political matters are debated.  
	- "The Grand Library", housing historical tomes.  

- NPCs :  
	- "Royal Guards", defending the castle.  
	- Nobles and diplomats, providing high-level quests.  

---

	3. Central Plaza District - The Heart of Commerce  
	The city’s most bustling and lively area, filled with vendors, traders, and entertainers. Colorful buildings and market stalls fill the streets, with a grand 
artesian fountain at the center.  

- Structures & Features :  
	- Weapon & Armor Shops run by blacksmiths.  
	- Alchemy & Herbalist Stores selling potions and reagents.  
	- Tailor & Leatherworker Shops, crafting gear.  
	- Taverns & Inns, offering food, rest, and rumors.  

- NPCs :  
	- Vendors, selling all manner of goods.  
	- Trainers, offering profession advancements.  
	- Quest Givers, providing trade-related tasks.  

---

	4. Barrack Divisions - The Military Stronghold  
`Located in the southern part of the city, this is the military’s main base of operations, heavily fortified and restricted to personnel.   

- Structures & Features :  
	- Barracks, housing commanders and officers.  
	- Training Grounds, where elite warriors refine their skills.  
	- War Rooms, where battle plans are drawn.  

- NPCs :  
	- "Silvershock Shields", maintaining order.  
	- "Weapon Masters", training players in melee combat.  

---

	5. Marine District - The Gateway to the Seas  
	Connecting the city to "Port of Ricco", this district is home to sailors, fishermen, and maritime traders.  

- Structures & Features :  
	- "Fisherman’s Market", where fresh catches are sold.  
	- "Harbor Storage Warehouses", filled with supplies.  
	- "Docks", where ships are moored.  

- NPCs :  
	- "Fishermen", providing fishing-related quests.  
	- "Merchants", selling seafood and trade goods.  

---

	Port of Ricco - The Lifeline of Dravenhold  
	East of the city, this expansive harbor ensures the kingdom’s access to trade, resources, and naval defense. The port features a long wharf, with docks for 
fishing boats and warships. A lighthouse stands tall, guiding ships safely.  

- Structures & Features :  
	- "Trading Post", where imported goods are sold.  
	- "Watchtowers & Cannons", for port defense.  