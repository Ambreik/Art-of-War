			The Royal District  

	The Royal District is where the rulers of Dravenhold reside. Majestic palaces, lavish gardens, and grand halls fill this area. The "Royal Palace", standing at
the very center of the district, towers above the rest of the city, a symbol of Dravenhold’s power and history. It is here that the city’s politics play out, where 
the council meets, and where the ruling family controls the fate of "Dravenhold".  
	This district is both a home and a fortress, with the wealthiest citizens, influential figures, and important nobles making their homes here. The streets are 
lined with marble and gold, and the atmosphere is one of quiet opulence. The city’s highest leaders and ambassadors from foreign lands make their way to the Royal 
District, whether for diplomatic discussions or to attend grand feasts and celebrations.  
	The "High Court of Dravenhold" stands as the center of justice, where important trials and royal decrees are issued. Here, judges and legal scholars oversee 
cases involving nobles, military officers, and high-profile criminals. Political tensions and faction-based disputes often play out within its grand halls, 
influencing the fate of Dravenhold’s power structure.  
	The "Hall of Heroes", a monumental structure within the "Royal District", honors the greatest knights, rulers, and champions in the city’s history. Statues, 
historical texts, and murals fill its vast halls, depicting past wars, victories, and betrayals. Some of these statues hold mystical significance, granting unique 
buffs, ancient knowledge, or secret quests to those who interact with them.  
	Encircling the palace, the "Royal Gardens & Celestial Fountain" provide an oasis of tranquility and splendor, accessible only to nobles, high-ranking officials,
and those granted special permission. The gardens flourish with rare, magical flora, tended by expert botanists and royal gardeners. At its heart stands the 
"Celestial Fountain", a mystical source said to purify and grant wisdom to those deemed worthy. Some mysterious NPCs can only be found here, offering exclusive 
political or mystical quests.  

