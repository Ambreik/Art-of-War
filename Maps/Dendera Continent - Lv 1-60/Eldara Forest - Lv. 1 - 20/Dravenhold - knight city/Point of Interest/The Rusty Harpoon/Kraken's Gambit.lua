			Kraken's Gambit

	Overview : "Kraken's Gambit" is a high-stakes dice game exclusive to "The Rusty Harpoon". Combining elements of chance, strategy, and the ever-present threat of
the mythical Kraken, this game offers players a thrilling experience where fortunes can be made or lost in a single roll.

---

	🧩 Game Components

- Custom Dice Three six-sided dice (3d6) with unique symbols
	- Anchor (⚓) Represents stability.
	- Wave (🌊) Represents unpredictability.
	- Kraken Tentacle (🐙) Represents danger.

- Betting Tokens Used to place wagers. 
- Kraken's Eye A glowing orb that reacts to cheating attempts.

---

	📜 Rules of Play

1. Ante Up Each player places an initial bet into the pot.
2. Roll the Dice Players take turns rolling the three custom dice. 

3. Evaluate the Outcome
	- Three Anchors (⚓⚓⚓) Jackpot! The player wins the entire pot.
	- Two Anchors and One Wave (⚓⚓🌊) Solid win. The player gains double their bet.
	- One Anchor, One Wave, One Tentacle (⚓🌊🐙) Neutral outcome. No gain or loss.
	- Two Tentacles and One Wave (🐙🐙🌊) Dangerous waters. The player loses their bet.
	- Three Tentacles (🐙🐙🐙) The Kraken awakens! The player loses all their tokens and is barred from playing for the next hour.

4. Cheating Mechanism
	- If a player attempts to cheat (e.g., sleight of hand), they must succeed on a high-difficulty Dexterity check.
	- Failure triggers the Kraken's Eye, which glows ominously, alerting the tavern staff.
	- Consequences range from being thrown out to facing the tavern's resident enforcer in a bar brawl.

---

	🧙 Enchanted Features

- Anti-Cheating Enchantment The Kraken's Eye detects magical or physical tampering with the dice.
- Dynamic Pot The pot's value can fluctuate based on the number of players and rounds played, encouraging prolonged engagement.

---

	🎉 Player Engagement

- Reputation System Winning multiple rounds increases a player's reputation, unlocking exclusive quests or discounts at the tavern.
- Tournaments Weekly Kraken's Gambit tournaments draw in crowds, offering rare items or significant gold prizes.

---

	This game not only provides entertainment but also integrates seamlessly with Dravenhold's lore and the ambiance of The Rusty Harpoon. It encourages player 
interaction, risk-taking, and can serve as a catalyst for various in-game events or storylines.