			The Minnow That Could

 Genre Uplifting sea shanty  
 Vibe Energetic, humorous, tavern-friendly  
 Instrumentation (optional in-game ambiance) Fiddle, accordion, stomping boots, and tankard clinks

---

	[Verse 1]
Oh we set out sail on the Minnow Bright,  
A dinghy no bigger than a barroom fight,  
With a leaky deck and a patched-up sail,  
We chased the breeze like a drunk on ale.  

The cap’n was sober — once, I swear,  
Till the gulls stole his hat and his last clean pair.  
But the sun came up and the wind held true,  
And the dolphins laughed as we skipped on through!

---

	[Chorus]
Heave-ho, roll and ride,  
The Minnow sails with luck and pride!  
Storm or calm, we’re never stuck,  
‘Cause fools like us survive on luck!  
Hey! Tankards up and clap your hands,  
We’ve danced through doom on sea and sands!  
So sing it loud and sing it good,  
For the little old Minnow that could! 🎵

---

	[Verse 2]
We once got caught in a hurricane,  
Spun us ‘round like a tipsy train,  
But we surfed that wave with a crate of cheese,  
And a mermaid winked as we cleared the seas.  

We’ve raced sea turtles for a wager bold,  
Lost the race but found some gold,  
Then ran aground near a kelpy bog —  
Where the first mate kissed a talking frog!

---

