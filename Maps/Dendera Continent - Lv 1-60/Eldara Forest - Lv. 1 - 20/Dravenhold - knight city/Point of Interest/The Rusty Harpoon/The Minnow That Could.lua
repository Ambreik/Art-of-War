---

	[Verse 3]
In Port Dravenhold we made our way,  
With empty pockets and debts to pay,  
But the tavern master, bless his beard,  
Said “You lads look drunk, and weird — but cheered!”  

Now we drink for tales and toast each storm,  
By the hearth where the luck’s still warm,  
And every fool who sails the tide,  
Hears the song of the crew who never died.

---

	[Final Chorus – everyone sings!]  
🎵 Heave-ho, roll and ride,  
