	[Secret Winner’s Verse] 
(Sung only for champions of the tavern game)*  

  
They say one soul beat the Kraken’s hand,  
With a grin too wide and a plan unplanned.  
No cheat nor charm, just wit and flair,  
And the Harpoon roared like a dragon’s lair!  

Now their name’s etched deep in ale-stained pine,  
Next to fools, legends, and salted swine.  
Raise your mugs, let the game be told —  
Of the luckiest rogue this port could hold!  
🎵

