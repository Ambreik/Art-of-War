	Optional Mechanics & Flavor Integration:

- Tavern Reaction : After winning, patrons raise tankards, chanting the chorus *“Heave-ho, roll and ride…”* in your honor.
- Trophy Unlock : A special seat reserved for the “Minnow’s Gambler” at the corner table with a carved placard.
- NPC Gossip : Barmaids, sailors, and cardsharks refer to the player as *“Kraken-Touched”* or *“Minnow’s Gambler”*.

