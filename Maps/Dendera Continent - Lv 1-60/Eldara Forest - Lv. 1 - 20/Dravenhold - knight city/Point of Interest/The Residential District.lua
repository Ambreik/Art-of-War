			The Residential District  


		The Residential District - “Where Dravenhold breathes.”

	The "Residential District" is the living, breathing soul of Dravenhold — vibrant by day, veiled by secrets at night. It serves as the backbone of the city's 
civilian life, populated by working-class families, artisans, local merchants, and hidden factions operating in the shadows.  
	Though modest in architecture compared to wealthier districts, its tangled streets, layered rooftops, and narrow alleys hide stories, secrets, and opportunities
for both humble players and ambitious schemers.  
	Due to its lower economic status, guard presence is minimal, making it one of the only districts where players may attack or kill NPCs (with consequences). 
	Black-market dealings, secret duels, and vigilante justice are common here — but so are familial bonds, festivals, and street wisdom.

