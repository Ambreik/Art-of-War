			Key Features & Buildings  

		1. 🛠️ **Worker’s Guild – “The Pulse of Dravenhold’s Labor” 

- New Mechanics :  
	- Guild Reputation System : Players can gain ranks within the Worker’s Guild (Apprentice → Journeyman → Master Builder) by completing contracts. Higher ranks 
unlock exclusive schematics and privileges (e.g., faster construction time for player-owned housing, cheaper repairs).
	- Community Projects : Server-wide construction efforts (e.g., rebuilding a burned-down structure, public defenses) that offer faction-wide bonuses and reward 
contributing players.
	- Sabotage Options (PvP): Rival players/factions may attempt to sabotage building projects — stealth skill checks and sabotage mechanics apply here.
	
- Black Market Option : The underground crafter here may also forge stolen goods with falsified serials (used to hide the origin of high-tier crafted items stolen or traded illegally). These items are detectable by certain NPCs or enchanted detection stones.

---

		2. Taverns & Inns  

	A. The Humble Hearth  
- Faction Influence : Players belonging to different factions may receive different interactions here. For example, a player aligned with a thieves’ guild may see 
alternative dialogue or backdoor options not visible to neutral players.
- Debt/Ledger System : Players may use favors (earned from NPCs here) as currency. These favors can unlock unique dialogue trees or protection from local 
consequences.

	B. The Sleeping Oak  
- Night Encounters : Rare NPCs only appear in player-rented rooms after midnight game-time (e.g., a demonologist selling cursed charms, a runaway prince).
- Lockpicking Challenge : Players can sneak into others’ rooms or attic spaces to uncover hidden loot or secrets — alerting the innkeeper may summon the few guards 
stationed nearby (low-level response force).

---

		3. Public Squares  

- Local Festivals & Faction Holidays : Mini-games, limited-time quests, and special food buffs available during local holidays.
- Mini-Fame System : Players who solve disputes, entertain with instruments, or help citizens here gain minor fame in the district, leading to:
	- Discounts at vendors.
	- Friendly NPC reactions.
	- Temporary protection (one-time warning before being attacked when caught doing crimes).
- Dueling Circles : Players can challenge NPCs or other players in non-lethal duels that draw public crowds. Winning streaks may result in whispers from 
underground factions or rare item bets.

---

		4. Workshops & Studios  

- Crafting Rivalries : Competing NPC artisans may ask players to sabotage their rivals’ workshops (e.g., planting cursed items, spreading false rumors). Players 
must choose sides carefully.
- Legacy Craft System : Some family-run studios hold ancient blueprints or "Legacy Tools", which can only be unlocked through long quest chains and high reputation.
- Unique Materials : Some artisans may offer materials you can’t find anywhere else — barkcloth, starlaced thread, or moldstone clay — all used for specialized gear
aesthetics or buffs.

---

		5. Hidden & Underground Areas  

	A. The Forgotten Catacombs  
- Mini-Dungeon Instance : Players can explore as a low-tier dungeon with randomized layouts, spawning either:
	- Forgotten relic guardians.
	- Rogue necromancers conducting experiments.
	- Tunnels to Smuggler’s Alley with stealth-based challenges.

- Light/Darkness Mechanic : Bringing torches, lanterns, or light spells alters encounter difficulty. Some dark spirits become invisible without light; others only 
awaken when light is present.

- Corruption Meter : Remaining in the catacombs too long or disturbing certain relics increases corruption, affecting visual screen filters, character dialogue, and
eventually triggering hallucination effects or phantom invasions.

	B. The Shrouded Market  
- Currency Conversions : Items are priced in **Whispers** — an underground currency earned by completing black-market quests or trading illegal goods.
- NPC Death Consequences : Killing a merchant here may grant you their stock, but will **lock you out of the market** permanently or until you offer a rare peace 
token. A bounty is placed on your head, visible in both faction and black-market bounty boards.
- Rare Events : Occasionally, legendary figures (e.g., “The Hollow Broker”) visit this market, offering one-time trades like:
	- Soul-bound relics.
	- Time-limited contracts with hidden side effects.
	- Clues to world-altering questlines.

