		District Environment, Sound, and Visual Mechanics

	Environment
- Narrow streets, hanging laundry lines, warm window glows at night.
- Chickens, dogs, and cats roam the streets; children run from alley to alley.
- Rooftops are traversable for agile classes — often used by spies, messengers, or players trying to evade guards.

	Guard Presence
- Sparse and often corrupt. Guards patrol in twos, and only along main roads.
- Players can attack and kill NPCs here, especially during nighttime.
- Killing major NPCs or repeat offenses will spawn Guardians or cause district hostility, locking players out of quests or vendors.

	Sound Design
- Day: Hammers striking metal, street chatter, occasional gossip barks from NPCs.
- Night: Dogs barking, drunken songs from taverns, footsteps in empty alleys, murmurs from hidden passages.
- Special: During underground exploration or high corruption, the sound distorts slightly — whispers echo, ambient noise drops unnaturally.

	Visuals
- Warm earth-tone buildings with patches of colored cloth from different artisan guilds.
- Lanterns flicker at night. Rain effects darken the district, with puddles reflecting torches and moonlight.
- Hidden passages shimmer faintly with magical auras if a player has the right items or perception.

