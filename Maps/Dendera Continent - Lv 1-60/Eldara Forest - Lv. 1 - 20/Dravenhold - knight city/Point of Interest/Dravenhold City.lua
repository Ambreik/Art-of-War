			The Military District – Dravenhold’s Stronghold of Might and Order  

	The "Military District" stands as the backbone of "Dravenhold's" defenses, a place where discipline, duty, and loyalty shape the city's elite forces. Encased 
within high stone walls, the district is a fortress within a fortress, a zone where only the dedicated men and women of the military reside, train, and prepare for 
any potential threat. The echoes of steel clashing, boots stomping in unison, and the stern voices of commanders barking orders fill the air, creating an atmosphere
of unwavering vigilance.  

	Here, two distinct military groups oversee security and ensure the city's might remains unquestioned:  

	1. The Lightbright Shields – "Knights of the Eternal Oath"  
	Once the personal royal guard of "King Alter", the "Lightbright Shields" were among the most devoted and powerful warriors of their time. Despite the fall of 
the old kingdom, many of them survived and remain fiercely loyal to the new monarchy, working closely with the king on missions requiring absolute  and skill.  

	Characteristics & Role  
- These knights are no ordinary guards; they are veterans, having stood on the front lines of Dravenhold’s most decisive battles.  
- They serve directly under the king and are tasked with high-priority missions, such as escorting nobles, retrieving artifacts, and neutralizing threats that 
ordinary soldiers could not handle.  
- Unlike the city’s guards, they are not stationed on gates or patrols.Instead, they operate from within the "Command Center", awaiting orders from the royal court.
- Some are assigned to protect key figures, while others act as strategists, advising military generals.  
- They wear ornate plate armor, typically engraved with golden insignias that symbolize their noble duty.  

	Notable Traits  
- Many of them are battle-scarred veterans, yet their spirit remains unbroken.  
- They speak in a formal, noble manner, emphasizing duty over personal matters.  
- Lightbright Shields rarely interfere with city life, unless an extreme threat arises.  

---

	2. The Silvershock Shields – Sentinels of the Fortress  
	While the "Lightbright Shields" serve in special missions, the "Silvershock Shields" handle city defense, particularly the fortress entrances and district 
borders. They are formidable warriors, handpicked from the army’s best soldiers whose active duty is nearing its end. Instead of retiring, they are granted the 
honor of safeguarding Dravenhold itself, ensuring that no enemy enters its walls.  

	Appearance & Equipment  
- Clad in heavy silver plate armor, with massive shields and long spears that gleam in the sunlight.  
- No helmets—these are old warriors, their white beards, mustaches, and bald heads a testament to their experience.  
- Their armor is not just decorative—it is reinforced with enchantments, making them extremely durable in combat.  

	Behavior & Combat Mechanics  
- They are "Elite" NPCs, meaning their combat stats are higher than standard guards.  
- Their Aggro Radius is increased, making them more alert to suspicious activity.  
- If a player exhibits criminal behavior, such as attacking civilians, stealing, or drawing weapons in restricted areas, they will become suspicious and may issue a
warning or immediate attack.  
- They cannot be killed by low-level characters or by outside monsters—they are among Dravenhold’s strongest line of defense.  
- Their stamina is high, allowing them to chase intruders aggressively, but they slow down significantly if the pursuit moves toward the "Central District".  

	Guarding Duties  
- They are primarily found at fortress gates, key district entrances, and high-security locations such as the Armory and Command Center.  
- Unlike common city guards, "Silvershock Shields" do not patrol—they stand guard, motionless, but are always ready to act.  
- They do not engage in casual conversation with civilians; their duty comes first.  

