		Marketplace District – Mechanics & Environmental Description

	The "Marketplace District" is the heart of economic life in the "Dravenhold", where traders, artisans, and adventurers converge daily. It is a dense, noisy, and
ever-changing environment filled with merchants, street criers, local guards, traveling buyers, and wandering entertainers. While safe in appearance, the district 
hides layers of intrigue and smuggling beneath its well-polished surface.

---

		Daily Market Banners & Rotating Trade Quests

	Each morning, five prominent "Market Banners" located near the most important buildings display the day’s rotating trade needs. These are local delivery quests 
tied to shops, vendors, and crafters across the district.
	Only the first players to complete these quests receive the full rewards, encouraging early participation. The quests often include:

- Delivering rare herbs to the "Alchemy stall"
- Supplying prepared meals to the "Tavern Keeper"
- Bringing refined ore to the "Blacksmith"
- Supplying fresh cloth to the "Tailor"
- Running scrolls and ink to the "Scribe"
	These quests refresh daily and may change dynamically depending on recent world events or local happenings. During weekends, the demand rises—new rare items 
arrive, and quests may require higher-quality or larger-volume deliveries.

---

		Crafting Synergy Bonuses

	Craftsmen from four major professions—Blacksmithing, Alchemy, Cooking, and Tailoring—benefit from working within the Marketplace District itself.
	By visiting specific vendor types in succession within a short time, players earn temporary synergy bonuses, such as:
- Reduced material requirements
- Increased rare item crafting chance
- Faster crafting speed

	These bonuses reflect the advantage of operating within a concentrated trade hub and are limited to once per day. They remain active for a brief window, 
encouraging strategic movement and profession-based efficiency.

---

		Scheduled Marketplace Events

	Two recurring events shape the rhythm of the district and reflect the tension between legal trade and shadow networks:

	1. Market Seizure (Every Monday) - On Mondays, Guardians sweep the area and shut down known smuggler access points via the sewage tunnels. All black market 
vendors vanish for 3 days, and entrances to the "Sewage District" are blocked by armed patrols.  
	The banners change to reflect the crackdown, and the atmosphere becomes tense. Vendors avoid certain topics, and street performers relocate away from central 
alleys.

	2. Rare Trader Arrival (Monthly or Event-Triggered) - A cloaked figure may appear on the streets for 24 hours, moving slowly through the district. This Rare 
Trader offers a selection of time-limited and exotic goods, including mounts, rare dyes, strange potions, or transformation scrolls.  
	This mysterious merchant never stops in one place long, often walking among the crowd and drawing quiet attention. Only players who explore or follow city 
whispers may find them before they disappear.

