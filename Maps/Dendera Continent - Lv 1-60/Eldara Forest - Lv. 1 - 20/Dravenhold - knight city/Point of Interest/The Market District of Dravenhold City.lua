		Combat Restrictions & Guardian Enforcement

	The Marketplace is a protected zone by local law. Vendors and merchants cannot be harmed by players of the same faction under normal circumstances. However:
- Enemy faction players (Pindars) can initiate raids, killing vendors and triggering aggressive Guardian response.  
	- Guardians appear in waves.
	- Area alarms sound.
	- The attacking player’s Reputation drops significantly, and they are marked for local bounty hunters.

- Players attempting smuggling, pickpocketing, or black market trades may draw attention from patrolling Guardians. Once exposed:
	- A temporary bounty is placed on the player.
	- Other players may assist in apprehending them for a reward.
	- The player may be forced to flee into the Sewage District to escape.


