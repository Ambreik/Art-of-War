- Resident Threat : "MycoQueen Solentha", a semi-immobile fungal entity that controls nearby flora.  
- Mechanics :  
	- Spore Clouds cause hallucinations or temporary control loss.  
	- Must burn or freeze fungal walls to reach inner chambers.  
	- Spawns rare ingredients for high-tier Alchemy and Herbalism.

---

	5. The Rusted Throne (Abandoned Control Room) 
- Description : The original water pressure control room for Dravenhold, now corroded and partially buried.  
- Key NPC :  
	- "Ravvok", an exiled Warforged who guards the ancient controls and believes himself ruler of the “Undercity.”  
- Player Impact :  
	- Completing Ravvok’s questline unlocks fast-travel pipe systems to the other districts.  
	- Choices affect the balance of power underground (ally Ravvok or destroy the controls).  

---

