- Low Guard Presence : There are no guards in the Sewage District—only traps, monsters, and informal factions.
- Lighting System : Players must bring their own torches or lanterns. Without them, vision is drastically reduced.
- Dynamic Darkness : Some areas are covered by magical darkness that requires specific items or spells to illuminate.
- Day/Night Variation : Certain enemies (e.g., ghosts, cursed bosses) spawn only at night.  
- Audio Design :  
	- Constant ambient drips, echoing screams, whispering voices (for haunted sections), scuttling noises.  
	- Sudden musical cues for jumpscares or boss proximity.

---

	Visual & Sound Design

