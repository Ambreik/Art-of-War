	Key Areas Within the Sewage District

---

	1. The Overflow Core (Toxic Reservoir and Boss Chamber) 
- Description : A massive circular chamber filled with swirling green sludge, broken walkways, and floating corpses.  
- Resident Threat : "Putris the Undrowned" – a mutated ooze-horror boss that drops rare crafting materials and cursed loot.  
- Mechanics :  
	- Random sludge waves knock players into toxic water.  
	- Must rotate valves around the chamber to weaken the boss.  
	- Adds spawn from pipes every 30 seconds unless blocked.

---

