	- “The Coinmaster” – Secret boss NPC who appears only during a special moon phase event.  
- Player Interaction :  
	- Unlock black-market quests for smuggling, assassinations, and spying.  
	- Unique daily PvP bounty board (player bounties).  
	- Shadow duel area where rogue-like classes can challenge each other for ranks or tokens.

---

	3. Ghostlight Vault (Haunted Noble Crypts) 
- Description : An old burial chamber flooded during the construction of the sewer. Now overrun by vengeful ghosts and cursed artifacts.  
- Features :  
	- Constant debuff: “Chill of Regret” – Reduces healing unless the player carries a sacred candle.  
	- Puzzle-based ghost encounters (must solve the past of the buried family).  
