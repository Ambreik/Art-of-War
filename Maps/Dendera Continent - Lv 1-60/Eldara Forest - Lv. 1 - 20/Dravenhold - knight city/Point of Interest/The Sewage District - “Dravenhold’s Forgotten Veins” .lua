			SEWAGE DISTRICT: DESIGN & STRUCTURE DESCRIPTION  
	*(Zone Design Breakdown)*

	Zone Type :  
- Indoor / Underground  
- Massive, interconnected labyrinth with multi-layered verticality  
- Mixture of flooded tunnels, sealed crypts, engineering chambers**, and arcane-forbidden zones

	Entrances & Exits :
1. Dockside Grate (East): Hidden behind crates and fishing nets; used by smugglers. Accessible only at low tide or using a sewer key.
2. City Wall Outflow Tunnel (Southwest): Used by creatures from outside. Overgrown, rarely patrolled. Leads to marshes.
3. Market District Manhole (North): Sealed by the city, needs engineering tools to open.
4. Residential District Basement Access (West): A haunted house has a cracked floor, leading into a forgotten well.
5. Emergency Hatch (Interior)**: Deep inside, this hatch leads up to the Cathedral Crypts (secret and requires a ghostly key).

---

		GENERAL ARCHITECTURE & ZONE SHAPE :

	Layout Structure:
- Three Primary Corridors form the spine of the zone (East, Central, West).
- Eight Secondary Corridors branch from them, linking to chambers or dead ends.
- Over 30+ rooms including:
	- 4 boss chambers
	- 6 hidden chambers
	- 3 trap gauntlets
	- 1 flooded underground prison
	- 1 arcane reactor room
	- 1 sacrificial chapel
	- 1 cursed necromancer lab
	- 1 buried vault

	Navigation Style :
- Classic maze-like design with loopbacks, locked gates, and vertical passageways (broken stairs, hanging ladders, old elevators).
- Corridors are wet, clogged, and sometimes collapsing, forcing detours.
- Doors are a mix of:
	- Rusted grates (require brute force or acid)
	- Arcane seals (dispel or sequence puzzles)
	- Mechanical locks (gear puzzles or requiring keys)

---

		THEMING OF EACH SUB-ZONE :

	1. Rotstream Artery (Main Spine Corridor):
- Central corridor with active sewage flow.
- Protected by mechanical sluice gates that close periodically (timing puzzle).
- Ghosts of workers wander this area, wailing.

	2. Vault of Forgotten Names (North Chamber):
- An ossuary crypt twisted by necrotic magic.
- Floating bones and spectral priests roam.
- Doors are covered in sigil seals; must collect “Remnants of the Dead” to open.

	3. Pumpheart Engine Room (South Engine Zone):
- Home to malfunctioning constructs and sludge elementals.
- Area mechanics:
	- Gear puzzles to reroute power.
	- Conveyor belts and crushing pistons.
- Environmental hazard: steam burst zones.

	4. The Drowned Gallery (Flooded Lower Chambers):
- Half-submerged rooms, treasure vaults now underwater.
- Home to waterlogged spirits and aquatic undead.
- Must swim or drain water using control levers.
- Slippery paths and drowning risk.

	5. Sacrificial Pipe Temple :
- Hidden behind a valve chamber wall.
- Room of broken statues and black candles.
- Boss: Wailing Remnant of Edran, a ghost priest that shatters sanity.
- Mechanics:
	- Light suppression zones.
	- Unseen spirits that ambush from behind unless countered with Light Orbs.

