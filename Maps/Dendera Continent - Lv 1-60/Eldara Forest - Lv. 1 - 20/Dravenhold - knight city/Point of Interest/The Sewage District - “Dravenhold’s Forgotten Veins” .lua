	Lore Justification : According to the lore you’ve established:

* The "Sewage District" was once a highly functional undercity, used for waste, emergency flood routing, dark rituals, and forgotten prison vaults.
* Over centuries, it collapsed inward, making the entire area a multilayered death trap, now infested by filth, decay, and cult activity.
* The lower level—where the dungeon takes place—was sealed off by the city long ago, with only a few risky access points remaining.
* Most inhabitants of the surface avoid this area. Only the desperate, brave, or insane venture into the deep sewer system.

