			Isfield Territory Overview

	Location : Second area of "Dendera Zone", situated on the "west side" of the region.  
- Boundaries : Defined by a deep notch in the ground extending eastward to the mountains in the north.  
- Access : Only reachable via a single old wooden bridge suspended over the hollow ground.

	Geography :  
- A vast forest divided into three sub-zones:
	- "Pirsky Village" : Southeastern region.
	- "Fortte Lake" : Northeastern part, guarded by "Perky Hook", a Rank II expert fisherman.
	- "Stonefields" : Northeastern area, leading to the "Rugged Mine" at its extreme point.
	- "Whiskey Forest" : The largest area, located in the west and northwest.

	Barrier :  
- Separated from "Dendera City" by a high mountain, with access through two wide valleys created by an ancient army. The mountain stretches from south
to north, creating a natural division from the rest of the continent.

---

	Key Locations

1. "Pirsky Village"  
	- Description : A medium-sized village fortified with walls made of tree trunks, wooden blocks, and animal bones.  
	- Population : Includes villagers, a blacksmith, the village leader, a hunter, two merchants (one for armor, the other for weapons), and guards. 
	- Defense : A central guard is joined by three heavily armored guards with long swords and two archers at the entrance.
	- Natural Features : A natural rock formation near "Fortte Lake" in the northern part.

2. "Fortte Lake"  
	- Description : A serene lake surrounded by forests with some felled trees for wood exploration.  
	- Guardian : Protected by "Perky Hook", a Rank II expert fisherman.

3. 	"Stonefields"  
	- Description : A rocky area characterized by uneven terrain and large stones, where the ground level is lower due to a significant crack.  
	- Access : Traversable through specific areas not hollowed out by the crack or via numerous bridges. The eastern part has a cliff that prevents 
direct access to the sea.
	- Significance : Home to the "Rugged Mine", an ancient mine of special minerals, now occupied by wild people who use it as a refuge.

4. "Rugged Mine"  
	- Location : Found at the extreme point in the northeastern Stonefields.  
	- Importance : Historically exploited for minerals, now it serves as a hideout for wild people.

5. "Whiskey Forest"  
	- Description : The largest sub-zone, located in the west and northwest, separated by a significant crack that raises the terrain.  
	- Access : Passage is facilitated by ancient stone steps built by the army.  
	- Terrain : Features several layers of forest and a valley descending southwest towards a settlement of ruins, where monsters launch surprise 
attacks.  
	- Fortifications : Contains wooden and stone fortifications serving as a settlement for those who have become wild since the "Second Great War". 
This settlement forms a labyrinthine structure for defense.
	- Temple Ruins : In the northern part, remnants of a temple dedicated to Giants, destroyed by **" Alucard" and now occupied by savages, serving as
a fallback point against the king's army.