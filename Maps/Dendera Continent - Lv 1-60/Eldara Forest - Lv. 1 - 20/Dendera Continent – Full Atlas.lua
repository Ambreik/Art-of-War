			Dendera Continent – Atlas v1

### Continent Dendera

Faction Knights
Location West side of world map, starting zone for players
Level Guidance 1–60 (per region)
Playable Races Humans, Vampires (vampires start in Forest of Trevale)

---

## Region I Eldara Forest

Level Guidance 1–20
Environment Type Forest, mixed plains, river crossings
Design Intent Starting region for Knights faction. Gentle combat curve, tutorial areas, early quest hubs, and first faction-specific training zones.
Monster Tier Low-level creatures (Lv. 1–20). Forest beasts, basic elemental creatures.
Ecosystem Notes Dense forest, rivers, small lakes. Abundant wood resources. Wildlife suitable for low-level pet hunting (e.g., small wolves, boars, forest birds).

### Sub-Area Dravenhold City

Type Capital City, starting hub
Points of Interest & Districts

1. Western District – Training Grounds Combat tutorials, sparring NPCs
2. Northpointed District – Royal Quarter Quest-giving NPCs, lore hubs
3. Central Plaza District – Heart of Commerce Merchants, crafting NPCs, general trade
4. Barrack Divisions – Military Stronghold Advanced combat training, faction missions
5. Marine District – Gateway to the Seas Port of Ricco (Traveling Ships dock)

Design Notes City is a tutorial-friendly environment with clear navigation. Safe zones for new players. Travel network activated at Lv. 25.

