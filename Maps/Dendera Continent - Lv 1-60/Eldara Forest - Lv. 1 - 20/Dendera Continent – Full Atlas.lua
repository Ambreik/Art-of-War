### Sub-Area Forest of Trevale

Type Vampire starting zone
Key Locations

1. Fallcrest Castle Central settlement for vampires, story hub
2. Ancient Ruins (North) Dungeon entry point, lore-related
3. Blackrock Grotto (North) Level 20 dungeon
4. Lake of Spectrals (West) Water resource, fishing and quest activities

Monster Tier Lv. 1–20, mostly forest creatures, few undeadminor vampiric NPCs
Ecosystem Notes Dense forest, ruins create dungeon ecosystem. Nightday cycle encourages exploration and early stealth mechanics.

---

### Sub-Area Isfield Territory

Type Leveling and quest area
Key Locations

1. Pirsky Village Medium-sized village, fortified, quest hub
2. Fortte Lake Fishing, small gathering spots
3. Stonefields Rocky terrain, low-level combat, exploration rewards
4. Rugged Mine (NE Stonefields) Mining resources, small dungeon
5. Whiskey Forest (WestNW) Largest forest section, high density of quest monsters

Monster Tier Lv. 5–20
Ecosystem Notes Forest and rocky terrain, balanced wildlife and monster spawns. Rich hunting and gathering area for early professions.

---

### Sub-Area Isle of Den

Type Hidden  Lore-focused
Key Features

1. Fortified Villages Refuge for traitors and outcasts
2. Dunrall Temple Dungeon, multi-tiered stone temple, high-level mobs for this zone

Monster Tier Lv. 10–20
Ecosystem Notes Island separated by water. Isolated ecosystems allow rare spawn mechanics.

