			Mooring Point (Boat Rental & Dock)

	Overview : Nestled along the western shore of Sunnymead Island, Mooring Point is a modest wooden dock that serves as a key hub for boat rentals and island 
travel. Whether players seek a peaceful fishing trip, a quick way to navigate the coastline, or a daring escape from danger, Mooring Point offers various vessels 
for rent.  
	Despite its small size, this dock plays a vital role in maritime travel and trade, attracting fishermen, travelers, and smugglers alike. It is often busy during
the day but becomes eerily quiet at night, save for the distant calls of seabirds and the lapping of waves against the pier.  

---

	Key Features & Mechanics :   

	1. Boat Rental Services  
- Players can rent different types of boats, including:  
	- Rowboats (cheap, slow, 1-2 passengers).  
	- Fishing Vessels (moderate speed, specialized for fishing activities).  
	- Cargo Barges (larger, for transporting goods in trade-related quests).  
- Boat rental fees vary based on the boat type and duration of use.  
- Some boats require a "Boating License", which can be earned through a tutorial quest.  

	2. Fishing & Exploration Opportunities  
- Deep-water fishing spots are accessible only via boats rented from this location.  
- Hidden small islands and coastal caves can be discovered while sailing.  
- Occasionally, mysterious shipwrecks appear, containing loot or clues to hidden treasures.  

	3. Travel & Navigation  
- Fast travel mechanics allow players to use rented boats to reach other key ports and fishing hubs.  
- Wind and tide mechanics may affect travel speed and boat control.  
- Smugglers sometimes use the docks at night, opening black-market trade opportunities.  

	4. Random Events & Dangers  
- Occasional pirate or sea creature attacks make traveling by boat risky but rewarding.  
- Players might encounter lost sailors needing rescue missions.  
- Fog and storms may reduce visibility, requiring navigation skills to stay on course.  

---

	Possible NPCs & Quest Hooks  

- Dockmaster Orwin Gale – Manages boat rentals, enforces dock rules, and teaches players boating mechanics.  
- Fisherman Tobin Netswift – A local fishing expert who offers deep-sea fishing quests and rumors about rare fish.  
- Shady Figure (Name TBD) – A smuggler contact who occasionally appears at night, offering illegal transport jobs.  
