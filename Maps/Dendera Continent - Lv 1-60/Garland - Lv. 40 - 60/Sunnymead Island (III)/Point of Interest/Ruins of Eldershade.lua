			Ruins of Eldershade  

	Location : Southern Peak of North High Peak, Sunnymead Island  

---

	Overview : Nestled within the dense overgrowth and rocky cliffs of the southern peak, the Ruins of Eldershade are the remnants of an ancient civilization long 
forgotten. Massive stone pillars, partially collapsed archways, and weathered carvings hint at a once-thriving culture that mysteriously disappeared.  
	These ruins are a hotspot for archaeological discovery, drawing scholars, treasure hunters, and adventurers seeking knowledge or lost relics. However, the ruins
are also shrouded in eerie silence, with unsettling whispers drifting through the wind at night.Some say that the spirits of the past still guard the sacred ground,
warning off those who come seeking treasure rather than wisdom.  

---

	Key Features  
- Excavation & Discovery – Players can dig for relics and artifacts, unlocking ancient knowledge and rare items for the archaeology profession.  
- Spirit-Touched Grounds – Certain areas of the ruins seem to react to arcane energy, triggering visions of the past or spectral encounters.  
- Forgotten Carvings & Runes – Scholars believe that the symbols inscribed on the ruins could reveal a hidden chamber buried deep within.  

---

	Secrets & Mysteries  

	1. The Sealed Chamber  
- Discovery : An ancient stone doorway, partially buried under rubble, is believed to contain the last great relic of the civilization.  
- Mechanic : Players must decipher runes scattered across the ruins to unlock the chamber.  
- Hazards :  
	- Incorrect rune sequences trigger magical traps or summon ethereal guardians.  
	- A mysterious barrier of force prevents unworthy adventurers from passing.  
- Reward :  
	- Rare Relics – Usable in high-tier crafting or sold to scholars.  
	- Ancient Knowledge Buff – Grants increased archaeology efficiency for a limited time.  

	2. The Guardian Spirits  
- Haunting Presence : At night, spectral figures roam the ruins, whispering in an unknown tongue.  
- Mechanic :  
	- Players who approach with respect may receive a blessing or hidden knowledge.  
	- Those who disturb the ruins aggressively will be cursed or attacked by vengeful spirits.  

---

	NPCs Found at Ruins of Eldershade  

1. Professor Elira Moonglade – Archaeology Trainer  
	- Offers archaeology training and excavation quests.  
	- Provides lore about the ruins.  

2. Thalyn Goldleaf – Apprentice Archaeologist  
	- Collects artifacts and relics for study.  
	- Has a personal questline involving a lost family heirloom hidden in the ruins.  

3. Tara Ironroot – Apprentice Archaeologist  
	- Focuses on environmental preservation and ancient flora study.  
	- Offers gathering-based quests related to the plants growing in the ruins.  

4. Lioran Stoneguard – Guardian of the Ruins  
	- Protects the site from hostile creatures and grave robbers.  
	- Can challenge players who try to forcefully take relics.  

