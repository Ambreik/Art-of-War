			South Ram Port  

	Overview : Located in the southeastern region of Sunnymead Island, South Ram Port serves as the island’s primary docking hub for ships arriving from the 
mainland. Though smaller than the major harbors found in the kingdom’s capital cities, it remains a bustling trade point where merchants, fishermen, and travelers 
gather. The port is built against a cluster of rocky outcrops, which act as a natural barrier, shielding it from the harsher waves of the Outland Ocean.  
	Unlike heavily fortified city ports, South Ram Port lacks an official military presence or stationed guards. Instead, its security relies on a mix of local 
traders, sailors, and dockworkers, who form an informal network of law enforcement. This has led to the port having a somewhat lawless reputation, where smuggling 
and black-market dealings thrive in the shadows of legitimate trade.  

---

	Key Features & Mechanics  

- Ship Docking & Travel Hub  
	- South Ram Port provides boat access to and from the mainland and other islands. Players can purchase passage on trade ships or rent smaller boats for personal
use.  
	- Some routes may be affected by storms, sea creatures, or piracy, creating dynamic events for players.  

- Market Stalls & Trading Post  
	- The port features several market stalls, selling fresh seafood, rare goods, and supplies for travelers.  
	- Players can engage in bartering mechanics, where prices fluctuate based on supply and demand.  

- Hidden Smuggler Routes  
	- While the main trade operates in daylight, hidden cave tunnels beneath the docks serve as smuggler routes. Players who gain the trust of certain NPCs can 
access these tunnels for black-market trading or secret deliveries.  
	- Some faction-based events may involve tracking smugglers or working for them.  

- Dockworker Jobs & Player Involvement  
	- Players can take on temporary work, such as loading cargo, repairing boats, or fishing off the piers, earning small but steady income.  
	- These tasks also serve as reputation-building mechanics for those seeking favor with local traders.  

---

	Encounters & Events  

- Merchant Ship Arrivals (Dynamic Trading System)  
	- Every few in-game days, a merchant ship arrives at the port, bringing rare goods and limited-time items for trade.  
	- Players must act quickly to buy valuable resources before they are sold out.  

- Storm Warnings & Shipwreck Events  
	- Occasionally, violent ocean storms cause ships to crash near the port. Players can recover lost cargo, rescue sailors, or fight off opportunistic pirates 
scavenging the wrecks.  

- The Black Market Night Trade  
	- At nightfall, certain unsanctioned traders set up temporary stalls, offering contraband, rare exotic goods, and stolen wares.  
	- Players may need to bribe or convince NPCs to access these deals.  

---

	Possible NPCs & Quest Hooks  

- Captain Elias Thorn (Dockmaster & Ship Vendor) – Manages port operations, assigns cargo-hauling quests, and sells boats for personal use.  
- Saria Crestwell (Smuggler Contact) – Runs the hidden black-market trade, offering secretive quests and exclusive deals for those willing to break the law.  
- Old Merrik (Retired Sailor & Storyteller) – Shares tales of legendary sea beasts, leading players to underwater treasures or dangerous sea encounters.  
- Vex "Quickhands" Malone (Thief & Informant) – A pickpocket NPC who may attempt to steal from players, but can also sell valuable underworld information.  

