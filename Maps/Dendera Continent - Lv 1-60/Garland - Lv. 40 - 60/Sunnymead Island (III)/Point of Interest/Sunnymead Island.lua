		New Locations for Sunnymead Island (Linked to "Treasure Hunt Extravaganza")  


	Sunken Wreck of the “Iron Gull” (Event Location – Treasure Hunt Extravaganza) 

	Location : Off the western coast of Sunnymead Island, near Sandy Shores  

	Overview : Beneath the shimmering waters of the "GreatSea" , the "Iron Gull", once a proud merchant ship, now rests in a watery grave. The ship was said to have
been carrying an important cargo of gold and rare artifacts, but it met its demise during a violent storm decades ago. Ever since, rumors have spread of hidden 
treasures locked within the wreck, waiting for daring adventurers to uncover them.  

	Key Features :  
- Diving Exploration – Players must dive underwater to explore the remains of the ship, searching for clues, lost cargo, and possible dangers lurking below.  
- Hostile Marine Life – The area is home to aggressive sea creatures, such as "Sunken Tide Serpents" and "Ghostly Mariners", making it a hazardous but rewarding 
venture.  
- Interactive Puzzle – Players must solve a wreckage puzzle to unlock a hidden compartment where the treasure is stored.  

	Secrets & Mysteries :  
- The Captain’s Chest : A locked chest rumored to contain a golden map pointing to even greater riches elsewhere on the island.  
- Drowned Crew Members : Ghostly spirits of the Iron Gull’s former crew sometimes appear, offering cryptic clues about the treasure’s exact location.  

---

	Eldershade Ruins (Event Location – Treasure Hunt Extravaganza & Future Lore Ties) 

	Location : Deep within the forests of Sunnymead Island, northeast of Gryveer Field  

	Overview : The "Eldershade Ruins" are the overgrown remains of an ancient civilization that once inhabited the island. Weathered stone structures and moss-
covered carvings hint at a once-prosperous society that mysteriously disappeared. The ruins hold secrets, puzzles, and remnants of an age-old power waiting to 
be unearthed.  

	Key Features :  
- Ancient Carvings & Runes – Players can interact with the stone monoliths to decipher messages and lore tied to the island’s history.  
- Buried Relics – Fragments of forgotten artifacts can be found here, some of which might be part of special treasure maps.  
- Wild Guardians – The ruins are not unprotected—stone golems and enchanted wildlife roam the area, attacking intruders.  

	Secrets & Mysteries :  
- The Sealed Chamber : A hidden underground vault rumored to contain a lost treasure from the island’s forgotten civilization.  
- The Forgotten Emblem : A mystical artifact that, when restored, grants access to a unique dungeon hidden elsewhere on the island.  

