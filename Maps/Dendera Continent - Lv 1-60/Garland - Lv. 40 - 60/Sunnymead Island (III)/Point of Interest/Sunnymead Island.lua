			Sunnymead Island – The Jewel of the Garland Region  

	Overview : Sunnymead Island, the third major area of the Garland Region, is a land of lush greenery, golden shores, and hidden mysteries, shaped by time, tides,
and history. Once part of the mainland, it was severed by the encroaching waters of the Outland Ocean, turning it into a secluded yet thriving landscape. The island
is renowned for its rolling meadows, deep forests, and towering mountains, making it a destination for both travelers and scholars alike.  
	Adventurers who set foot on Sunnymead Island will find bustling trade ports, secluded ruins, and perilous peaks, each offering their own set of challenges, 
stories, and opportunities.  

---

	Geography & Landscape  

	1. The Rolling Heartland  
	At the center of the island, vast plains stretch across the horizon, forming a balance between dense forests and open fields. The land is fertile and vibrant, 
dotted with small ponds, scattered rock formations, and hidden alcoves. The winds blow gently, carrying the scent of salt and wildflowers, while herds of wild 
animals roam freely.  

- Notable Creatures : Deer, wild hares, grazing beasts, and small predatory birds.  
- Flora : Healing herbs, tall wheatgrass, fruit-bearing trees, and rare medicinal flowers.  

	2. The Coastal Beauty – Sandy Shores  
	Encircling the island, the Sandy Shores offer some of the most picturesque coastlines in the Garland region. These beaches are a haven for fishermen and shell 
collectors, as treasures from sunken ships and driftwood from distant lands often wash ashore. Some areas are peaceful, while others hide dangerous sea creatures 
lurking beneath the waves.  

- Activities : Beachcombing, fishing, gathering rare shells, and uncovering buried treasures.  
- Hazards : Occasional sandstorms, aggressive wildlife, and hidden whirlpools.  

---

	Key Locations & Settlements  

	1. North High Peak – The Mountain of Trials  
	Towering over the island, North High Peak is one of the two major mountain peaks that define Sunnymead’s northeastern landscape. Its rocky cliffs and 
treacherous trails make it a challenging yet rewarding destination.  

- The Windmere Rest : A tavern built into the hillside, offering rest, food, and local legends to travelers. Many adventurers visit to prepare for their ascent or 
seek out local quests tied to the mountain’s mysteries.  
- Ironhorn Quarry : Located on the mountain’s lower western side, this mining site is known for its rich iron deposits and ancient tunnels rumored to hold forgotten
secrets. Miners and craftsmen frequent the site, working tirelessly to extract its resources.  
- Challenges : Wild mountain beasts, unpredictable weather, and legendary trials hidden within the peak’s caves.  

---

	2. South Ram Port – The Gateway to the Mainland  
	Positioned in the southeastern corner of the island, South Ram Port is Sunnymead’s primary trading hub, serving as the main access point to and from the 
mainland. Unlike some of the larger ports in the Garland region, South Ram Port has a more rugged, unpolished feel, with wooden piers built against rocky outcrops. 

- Commerce & Trade : Fishermen, traders, and travelers make use of the port daily. The economy thrives on seafood exports, ferry services, and black-market dealings
hidden beneath the surface.  
- Notable NPCs : Dockmasters, merchants, ship captains, and the occasional smuggler offering risky but lucrative opportunities.  
- Unique Events : Occasional pirate raids, contraband smuggling quests, and shipwrecked survivors washing ashore with stories of distant lands.  

---

	3. Gryveer Field – The Farmstead of the West  
	Nestled in the western plains, Gryveer Field is a sprawling farmstead owned by Gryveer Tall and his trusted companions. Surrounded by wooden fences and strong 
defenses, the farm thrives despite the constant threat of wild beasts and thieves.  

- Crops & Livestock : The farm cultivates wheat, vegetables, and fruit trees, while also raising sheep, bulls, and chickens for trade and sustenance.  
- Defensive Measures : Due to frequent attacks from aggressive creatures, Gryveer and his allies have fortified the farm with makeshift defenses.  
- Player Involvement : Players may be asked to assist in farm maintenance, animal care, and repelling hostile threats.  

---

	4. Ruins of Eldershade – The Forgotten Past  
	Tucked away in the dense forests of the island,the Ruins of Eldershade are the remnants of an ancient civilization that once thrived on Sunnymead centuries ago.
	These crumbling structures, moss-covered altars, and engraved pillars tell the story of a long-lost society.  

- Archaeology Site : Scholars and archaeologists frequently excavate the ruins, searching for forgotten relics, cryptic inscriptions, and lost artifacts.  
- Dangerous Intrusions : Hostile spirits and ancient guardians still roam the ruins, protecting whatever secrets lie within.  
- Unique Mechanics : Players may need special tools, skills, or knowledge to fully unlock the ruins' hidden treasures.  

---

	5. Ironhorn Quarry – The Ore Veins of Sunnymead  
	Carved into the eastern side of North High Peak, Ironhorn Quarry is Sunnymead’s primary mining operation, rich in precious minerals and valuable ores. However, 
the site is more than just a resource hub—it hides something far older beneath its tunnels.  

- Mining & Crafting Hub : Miners, blacksmiths, and craftsmen gather here to extract resources and trade materials.  
- Unexplored Depths : Some tunnels descend into forbidden areas, rumored to contain strange relics and buried horrors.  
- Hazards : Cave-ins, aggressive creatures, and supernatural anomalies make mining a perilous yet profitable venture.  

---

	6. The Windmere Rest – Tavern of the Travelers  
	Located on the hillside of North High Peak, The Windmere Rest is a rustic tavern known for its warm atmosphere, hearty meals, and mysterious clientele. 
	Travelers stop here before venturing into the mountains or embarking on long voyages.  

- Services : Lodging, food, drinks, and local lore.  
- Quest Hub : The tavern provides various quests, from hunting expeditions to solving old legends.  
- Unusual Visitors : Some patrons are not of this world, hinting at deeper supernatural ties.  

