			The Windmere Rest  

	Location : North High Peak, Sunnymead Island  

---

	Overview : Perched on the eastern slopes of "North High Peak", "The Windmere Rest" is a secluded tavern offering warmth and rest for weary travelers. 
Overlooking the vast ocean, the tavern is known for its welcoming atmosphere, local legends, and breathtaking views. Strong winds frequently howl through its beams,
adding to the mysterious ambiance.   
	Many adventurers stop here before continuing to "Ironhorn Quarry", "Ruins of Eldershade", or the treacherous cliffside trails leading to hidden secrets.
	The tavern provides quests, food, drinks, and rumors about the ancient ruins and strange echoes that disturb the silence of the mountains at night.  

---

	Key Features :   
- Rest & Buffs – Players can rest here to recover stamina and receive temporary buffs to improve endurance in harsh mountain conditions.  
- Quests & Exploration – Various NPCs offer exploration-based quests, including uncovering relics, hunting mountain creatures, and solving old mysteries.  
- Unique Food & Drinks – Offers Windmere Brew, a specialty ale that grants cold resistance, and Summit Stew, which temporarily increases stamina 
regeneration.  
- Gathering Hub – A favored spot for adventurers, archaeologists, and those preparing for dangerous expeditions into the peaks.  

---

	Secrets & Mysteries :   

	1. The Hidden Path to the Ruins of Eldershade  
- Discovery : In one of the tavern’s backrooms, an old, half-buried map hints at a forgotten route leading down the southern peak towards the Ruins of Eldershade.  
- Mechanic : Players can only uncover this path if they interact with the right NPC storyteller, who shares cryptic clues about "the wind that whispers the past".  
- Hazards : The hidden path is treacherous and unstable, requiring players to carefully descend, avoiding loose rocks, hidden pitfalls, and aggressive mountain 
creatures.  
- Reward : If successfully navigated, the path offers early access to the ruins without needing specialized excavation tools, potentially leading to rare artifact 
finds.  

	2. The Mystery of the Echoes  
- Strange Phenomenon : At night, deep howling echoes can be heard throughout the peaks. Some claim they’re the voices of the dead, while others insist they belong 
to something alive.  
- Mechanic : Players who investigate at midnight will encounter "Echo Wraiths", spectral beings that feed on sound and grow stronger in loud environments (like combat).  
- Combat Interaction :  
	- Staying silent weakens them, allowing for easier passage.  
	- Making too much noise (casting spells, using loud weapons) triggers aggressive attacks.  
- Lore Clue : An old tavern keeper might hold knowledge on how these echoes tie into the ancient ruins, hinting that the spirits might be protectors of something 
long forgotten.  

---

	NPCs Found at The Windmere Rest  

1. Gareth Hollowbrook – Tavern Keeper  
	- Shopkeeper selling food, drinks, and local lore.   
	- Provides rumors and a quest to investigate the echoes.  

2. Lyra Windmere – Storyteller & Explorer  
	- Tells myths and legends about North High Peak and the Ruins of Eldershade.  
	- Provides the first clue about the hidden path.  

3. Corvan Bristlemane – Mountain Hunter  
	- Offers bounty quests to eliminate hostile creatures near the peak.  
	- Specializes in tracking Echo Wraiths.  

4. Eldric Runeweaver – Historian & Scholar  
	- Studies ancient texts found near the ruins.  
	- Can translate relics discovered by players.  

