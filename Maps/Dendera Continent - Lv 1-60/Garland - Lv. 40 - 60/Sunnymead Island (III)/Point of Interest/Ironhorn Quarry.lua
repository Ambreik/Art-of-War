			Ironhorn Quarry  

	Location : Northern Peak of North High Peak, Sunnymead Island  

---

	Overview : Carved into the northern slopes of North High Peak, Ironhorn Quarry is a rugged mining site known for its rich iron and rare mineral veins. The 
quarry is an essential resource for blacksmiths, jewelers, and adventurers looking to gather valuable ores. However, deep within its tunnels, miners have uncovered 
strange, glowing crystals with unknown properties, sparking interest from scholars and mages alike.  
	Once a thriving excavation site, the quarry has become partially abandoned, as miners fear the mysterious collapses and strange occurrences that 
have plagued the tunnels. The deeper shafts remain largely unexplored, rumored to be haunted by spectral figures or crawling with creatures from beneath the 
mountain.  

---

	Key Features :   
- Mining Resource Hub – Players can mine ores and minerals essential for crafting, provided they have a mining profession and proper tools.  
- Hazardous Deep Tunnels – Certain areas of the quarry require torchlight or magical illumination, as the depths become pitch black.  
- Crystalline Caverns – A hidden chamber contains pulsating blue crystals, attracting interest from alchemists and researchers.  

---

	Secrets & Mysteries :   

	1. The Strange Crystal Veins :   
- Discovery : Miners have recently stumbled upon glowing veins of an unidentified blue mineral.  
- Mechanic : Players with mining or magical expertise can extract samples and bring them to a scholar for further study.  
- Potential Uses :  
	- Alchemical Enhancements – Can be crafted into rare elixirs.  
	- Weapon Infusion – Used in forging weapons with unique properties.  
	- Portal Catalyst – Some scholars believe they might be remnants of ancient teleportation magic.  

	2. The Vanishing Miners :   
- Mystery : Several miners have gone missing, leaving behind their tools and unfinished work.  
- Mechanic : Players investigating the abandoned tunnels may encounter:  
	- Collapsed pathways that require clearing with explosives or magic.  
		- Echoing whispers that lead them deeper, sometimes misguiding them.  
  - Shadowed Figures that disappear when approached, hinting at a lurking presence.  

