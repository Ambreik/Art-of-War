			Area Info: Sunnymead Island

	Overview : Sunnymead Island is the third area of the Garland region, a vibrant landmass that appears to have been torn from the mainland by the encroaching 
waters of the Outland Ocean. This picturesque island is characterized by its sandy shores, lush forests, and rolling plains, creating a unique environment for 
adventurers seeking exploration and camaraderie.

	Geography :
- Surrounding Landscape : The island is largely surrounded by sandy beaches that transition into rich plains and dense forests, offering diverse habitats. The 
northeastern part is dominated by North High Peak, a striking mountain with two distinct peaks, the highest of which provides breathtaking views of the island and 
the surrounding sea.
- Mountain Features : North High Peak is not only a natural landmark but also home to a small tavern perched on its hillside, serving as a popular rest stop for 
travelers and adventurers. The tavern offers refreshments, local lore, and quests for those daring enough to explore the peak's rugged terrain.
  
	Key Locations :
- South Ram Port : Located in the southeastern part of Sunnymead Island, South Ram Port serves as the main access point for boats arriving from the mainland. The 
port is somewhat secluded, tucked behind a cluster of rocky outcrops that provide natural shelter. While lacking formal guardians, the port is frequented by local 
fishermen and traders, adding to the island's bustling economy.
- Gryveer Field : Situated in the western part of the island, Gryveer Field is a sprawling farm run by Gryveer Tall and his two companions. This vibrant farm is 
enclosed by a sturdy wooden fence, protecting its crops and livestock. Gryveer and his friends cultivate the land and raise various animals, including sheep, bulls,
and chickens. The farm is a hub for local produce and offers quests related to farming and animal husbandry, providing players with the chance to engage in the 
island’s agricultural community.

	Terrain Features :
- Rolling Plains and Forests : The island's plains are interspersed with patches of forest, creating a harmonious blend of open space and shaded areas. This 
terrain supports a variety of wildlife, from birds and small mammals to larger creatures that roam the forests.
- Coastal Beauty : The sandy shores of Sunnymead Island are perfect for beachcombing and fishing. Players can engage in activities such as gathering shells, hunting
for treasures washed ashore, or even embarking on quests to aid local fishermen in their pursuits.
  
	Flora and Fauna :
- Diverse Ecosystems : The island boasts a rich variety of plant life, including flowering shrubs, medicinal herbs, and towering trees that provide a habitat for 
various species. Players may encounter unique creatures, from playful woodland animals to mystical beings that inhabit the deeper parts of the forests.
  
	Quests and Exploration :
- Tavern Quests : The tavern on North High Peak serves as a quest hub where players can receive tasks related to the island's history, folklore, or challenges 
involving local wildlife.
- Farming and Animal Care : Gryveer Field offers quests centered around farming, where players can assist with planting crops, tending to animals, or even 
protecting the farm from wild beasts that threaten the livestock.
- Exploration Opportunities : Adventurers are encouraged to explore the hidden trails around North High Peak, discover scenic viewpoints, and uncover the secrets of
Sunnymead Island, which may include ancient ruins or hidden treasures waiting to be found.

