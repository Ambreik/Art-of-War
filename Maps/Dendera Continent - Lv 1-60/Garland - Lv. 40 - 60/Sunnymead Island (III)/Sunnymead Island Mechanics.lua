	Hidden Chambers – Puzzle-Solving Mechanics

	The ruins of Eldershade are not just a treasure trove of knowledge; they are also a labyrinth of secrets, with hidden chambers that players can access only 
through a series of intricate puzzles. Here are some of the puzzles players may encounter:

- Lunar Alignment Puzzle :  
	One of the most complex chambers in the ruins is locked behind a puzzle involving the alignment of the sun and moon. Players will need to rotate large stone 
discs or use mirrors to reflect beams of light onto specific points in the room. When the light hits the right place, a hidden door will open, revealing a new 
chamber. This puzzle changes with the time of day and requires the player to observe the environment carefully to determine the optimal positioning of the light 
sources.  
	- *Hint* : The symbols on the discs might correlate with moon phases, requiring players to match the correct moon phase to open the chamber.

- Rune Activation Mechanism :  
	Some chambers are sealed behind ancient runes that must be activated in the correct sequence. The runes themselves can be found scattered throughout the ruins, 
often in difficult-to-reach places. Players must solve riddles or find clues within murals or inscriptions that provide hints about the rune order. Once activated, 
the runes may cause hidden doors to shift or even reveal hidden alcoves containing artifacts or lore scrolls.  
	- *Hint*: Professor Moonglade mentions a long-forgotten text that explains how the runes are tied to the Elders’ magic and their use of elemental forces.

- Weight-Based Pressure Plates :  
	In certain parts of the ruins, chambers are locked behind pressure plates that require players to place specific objects (often relics found in other parts of the ruins) on them. These plates might be tied to an inventory puzzle, where players must collect the right items and place them in the correct order on the plates to unlock the chamber. Each item represents a facet of the Elders’ magic: Earth, Air, Fire, Water, and Spirit.  
	- *Hint*: Players may need to use their knowledge of the elemental forces referenced in the ruins’ murals to figure out which object corresponds to each plate.

- Mirror of the Elders :  
	A final, particularly challenging puzzle involves the use of a "Mirror of the Elders", an ancient, enchanted artifact found in one of the ruins. This mirror 
reveals hidden details when players reflect light off of certain objects in the room. To solve this puzzle, players must find specific locations in the ruins where 
hidden glyphs or pathways are revealed by the mirror’s light, exposing the entrance to the next chamber.  
	- *Hint*: Look for old murals or inscriptions that depict mirrors or reflective surfaces, as these offer clues for the puzzle’s solution.

