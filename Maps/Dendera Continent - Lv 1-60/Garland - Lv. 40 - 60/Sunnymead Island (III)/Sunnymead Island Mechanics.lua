	Moonlight-Activated Statues

One of the most fascinating features of the "Ruins of Eldershade" is the set of statues scattered throughout the site. These statues appear to be more than mere sculptures—they are connected to ancient magical forces tied to the moon. Here are some details about the statues and their reactions to moonlight:

- The Moonlit Sentinels :  
	Some statues are positioned in a way that they only come to life—or reveal hidden pathways—when bathed in moonlight. The statues represent various celestial 
beings: the "Guardian of Dawn", the "Harbinger of Dusk", and the "Watcher of the Night". As the moon rises in the sky, the statues begin to glow faintly, with their
eyes or symbols lighting up. When the moon reaches its zenith, the statues either unlock doors or activate hidden passageways.  
	- *Hint*: Professor Moonglade notes in her research journal that the statues are particularly reactive during a "Full Moon", making it a key time for players to
attempt unlocking certain chambers.

- Moonlit Pathways :  
	Some statues don’t just unlock doors—they reveal hidden moonlit pathways. These pathways lead to long-forgotten chambers filled with valuable loot, secrets,
or even ancient relics. Players must navigate these moonlit paths carefully, as they only appear for a short time when the moon is at the right phase and height. 
The paths are often marked by glowing runes or ethereal lights, guiding players to their destinations.  
	- *Hint*: There are "Moonstone" items scattered throughout the ruins that can be used to extend the moonlit period, allowing players more time to explore.

- Revealing Messages :  
	Some statues are engraved with symbols that remain hidden until bathed in the correct moonlight. These symbols, when exposed to the moon, reveal important 
messages or clues—perhaps directions to hidden treasures or warnings about the dangers lurking in the ruins. The messages might be written in the ancient language 
of the Elders, and players can decipher them with the help of **Lunar Glyphs**, which can be found in various parts of the ruins.  
	- *Hint*: The lunar messages are cryptic and often need to be pieced together from different sources around the ruins, including ancient texts, murals, and 
statues themselves.

- The Watcher’s Blessing :  
	One particularly significant statue, known as the "Watcher’s Blessing", can only be activated during the "Lunar Eclipse". This rare event causes the statue to 
release a burst of magical energy, unlocking a powerful artifact or hidden chamber containing knowledge of the Elders.  
	- *Hint*: Players must be aware of in-game lunar cycles to take advantage of this rare event.