# **Region III: Garland**

**Level Guidance:** 40–60
**Environment Type:** Dense forests, hills, rivers, mountains, and coastal areas
**Design Intent:** High-level zone for knights faction; diverse terrain encourages exploration, complex questlines, and advanced monster types.
**Monster Tier:** Lv. 40–60. Forest, hill, and river-adapted creatures; stronger predators and environmental hazards.
**Ecosystem Notes:** Rich flora, rivers, waterfalls, and arable lands. Supports advanced gathering (herbs, rare minerals) and hunting for high-tier pets.

---

### **Sub-Area: Sharpthorne**

**Type:** Eastern part of Garland, protected by western mountains
**Points of Interest:**

1. **Stone Statue:** Hilltop statue of a soldier with a spear; hidden quests and lore hub
2. **Silverhammer Harbour:** Major port in Bayswick for trade, fishing, and quest interaction
3. **Ruins of Ports:** Scattered small ports, some in ruins; potential dungeon or quest locations
4. **Landscape:** Hilly terrain with dense forests and northern plains; minor river systems

**Monster Tier:** Lv. 40–50
**Ecosystem Notes:** Forested hills, moderate rivers; monsters are forest predators and aggressive fauna. Pet hunting viable with local wildlife.

---

### **Sub-Area: Azurr Forest**

**Type:** Largest area in Garland; dense forest with rivers and streams
**Points of Interest:**

1. **Primrose Lake:** Central lake where two rivers meet; fishing and quest hub
2. **Red Flue Stable:** Stone tavern by a river branch; guarded by spearmen
3. **Anjan’s Green Stable:** Built in a giant tree trunk; tavern with horse stables and blacksmith
4. **Vygor Port:** Smaller port at continent’s edge; strategic trade and travel point
5. **Landscape:** Intersected by rivers, waterfalls, valleys, rugged mountains; ideal for exploration

**Monster Tier:** Lv. 45–55
**Ecosystem Notes:** Dense forests support rare herbs, advanced forest monsters, and exotic pets. High-level quests and hidden areas are abundant.

---

### **Sub-Area: Sunnymead Island**

**Type:** Island off Garland; sandy shores, forests, and unique mountain
**Points of Interest:**

1. **North High Peak:** Mountain summit with tavern; panoramic view, hidden quests
2. **South Ram Port:** Small unguarded port; arrival point from mainland
3. **Gryveer Field:** Farmstead run by Gryveer Tall; pet-related quests and livestock
4. **Landscape:** Sandy beaches, patches of forest, isolated plains; small rivers and hills

**Monster Tier:** Lv. 50–60
**Ecosystem Notes:** Mixed environment: forests, sandy shores, and highlands. Supports unique pets, advanced resource gathering, and elite monsters.

