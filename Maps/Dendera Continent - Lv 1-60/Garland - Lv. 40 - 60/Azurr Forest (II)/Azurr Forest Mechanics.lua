	Secret & Hidden Mechanics (Encouraging Exploration)  
These are hidden elements that reward players for thorough exploration or interaction.  

8. Sunken Haven’s Forgotten Treasure  
   - Deep in "Sunken Haven", players can find an old diver’s corpse that contains a half-buried map.  
   - The map leads to a hidden treasure chest buried in the lakebed, but players must figure out the right sequence of underwater currents to reach it safely.  

9. Whispering Wind Puzzle
   - Inside "The Whispering Glade", the wind carries faint voices that whisper cryptic phrases.  
   - If players follow the whispers in the right order, they unlock a hidden glade with an ancient druid’s relic, granting a special nature-based buff.  

10. The Lost Smuggler’s Crypt  
   - Near "Lost Harbor’s Crypt", a hidden entrance can be found by stepping on three pressure plates in a specific order.  
   - Inside, players discover a cursed relic that reduces health over time but increases loot drop rates when held.  
   - Players must choose to either cleanse the relic or keep it for greater rewards at a risk.  

11. Echoing Caves of Shadowspine  
   - "Shadowspine Cave" features a hidden chamber that can only be accessed by singing a specific melody (learned from an NPC bard).  
   - Once unlocked, players find a mysterious tome that grants temporary night vision inside dark areas.  

12. Legend of the River Spirit  
   - Occasionally, players near the rivers of Azurr Forest may hear soft singing in the distance.  
   - If they follow it carefully, they may encounter a benevolent river spirit who grants a one-time blessing, such as faster swimming speed or a rare fish.  
   - However, if players attack the spirit, it disappears permanently for them.  

