			Core Mechanics of Azurr Forest  

	These are mechanics that directly fit into the region based on its geography, settlements, and quest potential.  

1. River Navigation & Small Boats  
   - Players can acquire or rent small boats at "Vygor Port" or "Greencoast Wharf" to navigate the rivers.  
   - Certain areas (such as hidden caves or secret islands) are only accessible by boat or other ways such scuba-diving.  
   - Some rapids or waterfalls may damage boats if players aren’t careful.  

2. Fishing System Enhancements  
   - Dynamic Fishing Spawns : Different fish species appear based on the time of day, weather, and location.  
   - Rare Fishing Events : At "Primrose Lake", a "giant spectral fish" appears only at midnight on foggy nights.  

3. Tavern & Stable Interactions  
   - Tavern "Red Flue Stable" serve as hub for rumors, quests, and hidden lore.  
   - Players can "rest in the tavern", which grants temporary buffs such "Well Rest" or "Drunken Bravery" (Temporary Strength Boost but reduced accuracy). 
   - "Anjan's Green Stable" offer mount care services where mounts can receive temporary speed boosts if properly groomed.  

4. Port Trade System  
   - At "Greencoast Wharf", players can engage in trade contracts for goods like exotic herbs, spices, and seafood.  
   - Smugglers operate in secret, offering illegal trade opportunities at night, which could lead to risk/reward scenarios with authorities.  
   - Smugglers are in direct contact with faction of other smuggles located in "Capital City" in the tavern called "Sailor's Tavern".

5. Bandit Ambushes & Forest Traps  
   - Some forest paths trigger ambush mechanics, where bandits attack unsuspecting travelers.  
   - Players can disarm traps using a "Survival Skill", potentially finding loot caches left behind by other victims.  

6. Weather & Environmental Effects  
   - Heavy rain makes rivers overflow, blocking some paths while opening new shortcuts.  
   - Thunderstorms can occasionally strike trees, creating fallen logs that block roads.
   - Thick fog at night reduces visibility, making navigation and combat more challenging.  

7. Spectral Stag of Whispering Glade  
   - A rare, ghostly stag appears at "The Whispering Glade", which can be tamed under special conditions.  
   - Players must stand still and not attack for 30 seconds while listening to the whispers of the forest to gain the stag’s trust.  
   - If startled, the stag disappears and won’t return for a full in-game day.  

