			The Salty Anchor Tavern  

	Location : Greencoast Wharf, nestled between the bustling docks and the cliffs of the coastline.  

---

	General Overview : "The Salty Anchor" is a lively,rustic tavern built into the side of a weathered stone building, with large windows facing out toward the sea.
The scent of saltwater and rum permeates the air, and the sound of waves crashing against the nearby rocks can be heard as the tavern’s patrons enjoy a drink or 
tell tall tales. The building itself is an essential part of Greencoast Wharf, offering a warm, welcoming space for sailors, dockworkers, and travelers alike. Its 
exterior is aged, with ivy creeping up the stone walls, but it’s sturdy and well-maintained—just like its owner, Hugo "Three-Finger" Lorne.

---

	Exterior Description :
- Size and Shape : A two-story building with a wide, low roof made of weathered wood and shingles. The front of the building faces the ocean, with a large wooden 
sign hanging from a rusted iron bracket, depicting a ship's anchor and a frothy mug of ale. The sign sways gently in the ocean breeze.
- Facade : The building has rough-hewn stone walls that have been bleached by salt and sun, with a few small wooden balconies for the upper floor. The first floor 
has wide, open windows that let in the salty air and offer a view of the sea. Wooden shutters are often open during the day but closed at night.
- Entrance : The front door is made of heavy oak, with iron hinges that creak in the wind. A brass knocker in the shape of a fish decorates the door, and a small 
bell rings when it opens. Outside, a small cobblestone patio with a few wooden benches sits under a canopy, providing a nice space for patrons to sit during warm 
weather.

---

	Interior Description :
The interior of "The Salty Anchor" is dark and cozy, with a sense of history in every corner. The warmth from the fireplace mingles with the salty air that flows through the open windows. The tavern is filled with the hum of conversation, clinking mugs, and the occasional burst of laughter or rowdy challenge.

- Main Room :  
	- The tavern's main room is spacious but not overly large, with a few long wooden tables scattered throughout. Most patrons sit at a bar that runs along the 
far wall, where Hugo and his staff serve drinks and exchange banter with regulars.
	- Wooden beams stretch across the ceiling, supporting the roof above. The floor is worn wood, smoothed down over years of use, with a few worn patches from 
dancing, spills, and dragged furniture.
	- Lighting : The room is lit by a combination of wall-mounted lanterns and a large, crackling hearth in the corner, with a few nearby tables offering a soft, 
golden glow. The lanterns are made from rusted iron, giving off a warm amber light, while the fire provides both warmth and light in the evenings.
	- Decoration : Fishing nets and nautical trinkets hang from the rafters, alongside maps of the coastline and various old ship parts. There’s a large painting of
a shipwrecked vessel on the far wall, covered in salt damage and looking somewhat faded. Shells, driftwood, and old anchors are scattered across the shelves. The 
air carries the faint smell of smoke, salt, and the lingering scent of the sea.
	- Atmosphere : A lively, yet relaxed ambiance fills the room—familiar faces chatting over mugs of ale, gambling happening at some tables, and the occasional 
burst of laughter at a well-told sea story.

---

	Bar Area :
- Bar Counter : The bar itself is a long, dark wood counter that gleams in the firelight. Behind it, shelves are stacked high with bottles of rum, whiskey, wine, 
and local spirits. Wooden kegs line the wall behind the bar, labeled with different types of alcohol, and an old tap is situated at one end of the counter where 
Hugo serves the drinks.
- Bar Stools : A dozen or so stools are scattered along the bar, many worn smooth by years of use. Some have missing slats or uneven legs, giving the place a rugged
charm.
- The Drinks : The tavern specializes in salty rum, aged whiskey, and the occasional obscure concoction made by Finnley "Salt" Dawson. There’s also a small 
selection of local wines and ales, with a special emphasis on seafood-inspired flavors.

---

	Dining Area :
- Tables and Seating : In addition to the bar stools, several long wooden tables fill the tavern, each one surrounded by mismatched chairs that reflect the eclectic
clientele of dockworkers and sailors. The tables are often shared, with patrons moving to accommodate others, creating a sense of camaraderie and community.
- Meals : Nora "Quickhands" Bell serves hearty meals, often featuring fresh fish, roasted meats, and a variety of locally sourced fruits and vegetables. The tavern 
is known for its seafood stews, fresh oysters, and the occasional special dish brought in by sailors who share their catches.  
	- Specialty Dish : The tavern’s signature dish is "Barnacle Stew," a hearty seafood stew filled with mussels, fish, and potatoes, often served with a side of 
rustic bread. It’s the perfect comfort food after a long day on the docks.

---

	Upstairs :
- Second Floor : The second floor is quieter than the main room, with a few smaller rooms for patrons who want a more private experience or simply wish to sleep off
their drinking. The stairs creak underfoot as one ascends, with a low wooden railing on either side. The rooms are sparsely decorated with minimal furniture—simple 
wooden beds and a small writing desk or chair.
- Common Room : The upstairs hallway leads to a large common area where patrons can sit and talk or play cards in a more intimate setting than downstairs. Several 
long windows provide a lovely view of the ocean.
- Private Rooms : Two private rooms are available for rent, often occupied by travelers or sailors in need of a rest. The rooms are small but functional, with cozy 
blankets and thick curtains to block the light.

---

	Other Features :
- The Stage : In the corner of the main room is a small stage where, on occasion, performers or traveling minstrels entertain the patrons. It’s usually reserved for
impromptu performances, but the occasional bard or dancer can be found here, adding to the lively atmosphere.
- Secret Backdoor : There is an old, weathered door at the back of the tavern that leads to the alleyway behind the building. Some say it’s used for deliveries, but
others speculate it’s used for more private meetings, though very few know what goes on there.

---

	Overall Atmosphere :
"The Salty Anchor" has a warm, welcoming vibe, but it's also a place where hard work and tales of adventure go hand in hand. The patrons are a mix of rough 
sailors, curious travelers, and hardworking dockworkers, all sharing a common bond over the sea. While the tavern may seem gruff on the outside, it’s a place where 
friendships are made, secrets are shared, and adventures begin.

