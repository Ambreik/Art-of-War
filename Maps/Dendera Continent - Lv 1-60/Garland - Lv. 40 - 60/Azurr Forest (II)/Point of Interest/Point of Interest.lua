			Azurr Forest Point of Interest
			
	Azurr Forest : This area is the largest and most extensive in the region. It covers the northeast of the territory, along the "Sharpthorne" area beyond the 
mountain, extends in the west and descends in the south, surrounding the entire continental part of this area. It has access to water to the north and west and the 
whole area is covered by forests of trees with wide branches. Moreover, it is crossed by several rivers, all of which intersect in the southwest, reaching the 
"Outland Ocean" forming here a big bay.
	*Greencoast Wharf : a small port located in southwest, where all the rivers intersect.
	*Red Flue Stable : is a tavern located in the north-east of the area close to the shore.		
	*Anjan's Green Stable : is a tavern located on a meadow, in south of the area.
	*Primrose Lake : located in west-center of the area with a small pier. Two rivers flow into this lake, through waterfalls, and form a single one at the 
exit from this lake, heading south.
		*Vygor Port : located in north-west of the area, a small port from where can be rented boats to go to the island.
		
