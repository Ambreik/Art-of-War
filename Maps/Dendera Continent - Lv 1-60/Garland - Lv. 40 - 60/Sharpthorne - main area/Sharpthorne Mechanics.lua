	General Quest Mechanics :
- Hidden Lore : Each location could have scattered journals, notes, and murals that expand the backstory of the area or provide hints for hidden quests.
- Environmental Interactions : Each hidden area should have unique environmental factors (e.g., underwater visibility, spectral phenomena, or the dangerous terrain of the crypt) that require players to use special items or gear.
- Boss Encounters : Certain areas could introduce world boss mechanics, where a rare or powerful creature can be found, defeated, and potentially turned into a 
companion.

	When we expand on these quests later, I’ll aim for a mix of exploration, combat, puzzles, and environmental interactions, with each quest being designed to feel
like a rewarding and immersive experience. These quests will also encourage exploration and discovery, keeping players invested in uncovering Sharpthorne’s secrets.
	Here are some ideas based on *WoW* quest styles to give you a preview of what I could create for the 20 hidden quests:

	1. Echoes of the Forgotten
   - Type : Exploration, Lore
   - Description : Hidden notes scattered across Sharpthorne hint at an ancient civilization that once inhabited the area. Players must piece together these 
fragmented stories to unlock a location or artifact.
   - Mechanic : Find lore fragments, interact with old ruins, and uncover hidden symbols.

	2. The Ghost’s Lament
   - Type : Environmental Puzzle, Combat
   - Description : A spectral figure haunts an old ruin, wailing in pain. To help the spirit pass on, players must solve a riddle that will require them to traverse
hidden parts of the map and return specific artifacts to the ruins.
   - Mechanic : Players solve puzzles related to the history of Sharpthorne, discovering artifacts scattered in hidden areas.

	3. Revenge of the Forest Spirits
   - Type : Combat, Exploration
   - Description : A powerful spirit is enraged due to a disturbance in its sacred forest. Players are tasked with finding and eliminating the source of the 
disturbance—an invading force of bandits or rogue animals.
   - Mechanic : Tracking down the cause of the disturbance, navigating dense forest areas, and eliminating the threat.

	4. The Unseen Path
   - Type : Stealth, Exploration
   - Description : A hidden trail in the forest reveals itself only to those who can move silently and without disturbance. Players must follow the path without 
triggering traps or alerting hidden sentinels to gain access to a long-lost treasure.
   - Mechanic : Stealth mechanics where players must avoid detection by enemies and environmental hazards.

	5. Smuggler’s Secrets
   - Type : Combat, Puzzle
   - Description : A smuggler’s crypt holds a treasure chest that is locked by a complex mechanism. To open it, players must explore the crypt, battling skeletal 
guardians and deciphering old locks and riddles.
   - Mechanic : Combat encounters with undead, solving locks and traps, finding hidden keys.

	6. The Sunken Relic
   - Type : Underwater Exploration
   - Description : A submerged relic lies at the bottom of the lake. Players must dive into the depths, contend with aquatic monsters, and retrieve the artifact 
from its watery resting place.
   - Mechanic : Underwater exploration mechanics, with limited air supply and underwater combat against hostile creatures.

	7. The Whispering Stones
   - Type : Puzzle, Exploration
   - Description : Hidden stone tablets scattered throughout the forest whisper cryptic messages. Players must decipher the clues and use them to unlock a secret 
location within the forest, which holds rare items or lore.
   - Mechanic : Interaction with stone tablets, solving riddles to reveal new locations.

	8. Stolen Memories
   - Type : Combat, NPC Interaction
   - Description : A mysterious NPC appears, claiming their memories were stolen by a powerful foe. Players must track down the culprit in the forest and retrieve 
the stolen memories, fighting enemies along the way.
   - Mechanic : Tracking down an enemy NPC, combat, and recovering special items to return the memories.

	9. The Drowned King
   - Type : Boss Fight, Exploration
   - Description : Deep in a sunken ruin lies the crypt of a long-dead king. His restless spirit must be laid to rest, and his treasures recovered. Players will 
need to dive into flooded areas to find the key to his crypt.
   - Mechanic : Underwater boss fight with unique mechanics tied to the environment.

	10. Familiar Footprints
   - Type : Tracking, Exploration
   - Description : A rare creature has been spotted in Sharpthorne, but no one has been able to catch a glimpse of it. Players must follow its footprints through 
various hidden areas, eventually catching up to it.
   - Mechanic : Tracking gameplay where players must interpret footprints, sounds, and other signs to locate the creature.

