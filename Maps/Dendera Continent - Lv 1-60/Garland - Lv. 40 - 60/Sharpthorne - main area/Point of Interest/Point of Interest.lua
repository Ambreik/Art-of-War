			Sharpthorne Point of Interest
			
	Sharpthorne : it is the main area, concentrated in the eastern part and towards the center and north of the continent. It is separated from the second area, to 
the north by a high mountain that descends to the northwest. In the west to the south, the territory is crossed by several rivers, making the passage in this region 
no longer through portals or through NPC teleporters, but by traversing these rivers by flight or by other methods. Is de second biggest area of the region.
		*Bayswick : located in the east of the region, is a big city with a big port.
		*Irvien Stable : located in the extreme north point of the area is a mount stable.
		*Silverhammer Harbour : located in the east, as an extension of the city.