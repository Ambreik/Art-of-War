			Sharpthorne

	Overview : Sharpthorne, the primary area of Garland, stretches across the eastern part of the region into its northern and central territories. 
This expansive landscape is a rich tapestry of rugged terrain, dense forests, and coastal beauty. Shielded by a formidable mountainous wall to the 
west, Sharpthorne enjoys a natural boundary that enhances its mystique. The Great Sea to the east and a serene southern lake further add to its 
geographical allure, making it a hub for adventurers and traders alike.

---

		Bayswick - The Heart of Sharpthorne :
	"Bayswick" is the crown jewel of Sharpthorne,a sprawling city second only to Dendera City in size and influence. As a center of commerce, culture,
and governance, Bayswick thrives on its strategic coastal location and dynamic trade routes.

- Architecture and Layout :  
	Bayswick is a blend of maritime charm and bustling urban life. Its towering lighthouses, cobblestone streets, and stone bridges over flowing 
canals offer a picturesque setting. The city is divided into districts for trade, residential life, and governance, ensuring organized functionality.

- Key Features of Bayswick :  
	- "Silverhammer Harbour" : This massive port is the city’s lifeblood, facilitating trade and fishing. With its extensive docks, vibrant market 
stalls, and frequent maritime traffic, it is always alive with activity.  
	- "Governor’s Hall" : A grand structure overseeing the city’s governance, where political matters and town events are managed.  
	- "Merchant’s Row" : A bustling commercial district filled with shops offering wares from across Garland and beyond, including exotic items from 
the Great Sea.  
	- "The Grand Square" : A central meeting place adorned with fountains, statues, and occasional festivals or events that draw crowds from across 
the region.  
	- "The Ghost Travelling Ship" : A mysterious spectral vessel that docks at Silverhammer Harbour on specific moonlit nights. Legends say it ferries
adventurers to distant, hidden locations or delivers cryptic messages about ancient curses and quests. Encountering this ship is considered a rare and
valuable opportunity for daring adventurers.  

		Irvien Stable  
	Description : Located at the extreme northern tip of Sharpthorne, Irvien Stable is a renowned mount stable nestled near the edge of the highland 
plains. The stable is a hub for adventurers seeking to acquire or upgrade mounts, with a range of options from hardy forest steeds to rare breeds 
capable of navigating Sharpthorne's rugged terrain.  
- Features :  
	- Training Ground : Adjacent to the stable is a training field where players can test and improve mount handling skills.  
	- Unique Vendor : Irvien Stable also boasts a mount vendor offering specialized gear, saddles, and barding, enhancing mount performance.  
	- Quests : Occasionally, the stable caretaker, "Old Irvien", offers quests tied to mount lore, including rare breeding tasks or taming 
challenges in nearby regions.  

---

	Geography and Key Features :
- Mountainous Wall : Running along the western edge, this towering range provides a natural fortress, with winding paths and treacherous cliffs. 
Rumors of ancient caves and hidden ruins are abundant among explorers.  
- Lake Haven : The southern boundary of Sharpthorne is defined by a tranquil lake, offering fishing spots and opportunities to discover unique aquatic
monsters.  
- Great Sea Coastline : To the east, the coastline is dotted with both functioning ports and remnants of bygone harbors, some said to conceal untold 
treasures.  

---

	Terrain Features :
- Forested Wilderness : Thick, ancient woods dominate the landscape, filled with towering trees, rare herbs, and hidden clearings.  
- Hilly Plains : Northward, the forests give way to undulating plains, ideal for encountering grazing wildlife and migratory creatures.  
- Rugged Trails : Winding paths weave through forests and hills, leading to concealed landmarks and rewarding careful explorers.  

---

	Quests and Exploration :
- Hidden Quests : Sharpthorne’s dense forests and ruins are replete with opportunities for hidden lore, unique rewards, and challenging puzzles.  
- Adventurer’s Challenges : Navigating the varied terrain requires skill and resourcefulness,with frequent encounters against wild beasts and bandits.

---

	Hidden Areas :
1. "Shadowspine Cave" :  
	A cavern system hidden within the western mountainous wall, teeming with rare minerals and guarded by Shadowfang Wolves. Adventurers may uncover 
ancient murals and secret chambers.  

2. "Sunken Haven" :  
	Located near the southern lake, this submerged ruin offers underwater exploration. Sunken relics and aquatic creatures, including a rare tamable 
sea serpent, await daring players.  

3. "The Whispering Glade" :  
	Deep within the forest lies a secluded clearing where the wind carries faint whispers. It is said to be home to a unique spectral stag that can be
tamed with the right approach.  

4. "Lost Harbor’s Crypt" :  
	Among the ruined ports along the eastern coast lies a hidden underground crypt. Once a refuge for smugglers, it now houses skeletal guardians and 
treasures of their ill-gotten gains.