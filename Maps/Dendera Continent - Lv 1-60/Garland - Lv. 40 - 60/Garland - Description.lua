			Region: Garland

	Overview : Garland is the third region of the Dendera continent and is the largest region overall. It is characterized by dense forests, river 
courses, and a diverse landscape that includes mountains, lakes, and coastal areas. The region is separated from the central area by a high mountain 
range shrouded in fog and clouds.

	Areas :

1. "Sharpthorne"
	- Description : Sharpthorne is the main area of Garland, extending into the eastern part and towards the north. It is protected by a mountainous 
wall on the west, which descends from the central north to the south. This area features dense forests, rolling hills, and several small ports along 
the coastline.
	- Key Features :
		- Stone Statue : A tall stone statue of a soldier holding a spear stands on a hill, hidden among trees near the small port. It is a focal point for hidden quests.
		- Silverhammer Harbour : A prominent port part of the town of Bayswick, known for its trade and fishing activities.
		- Ruins of Ports : Several small, unnamed ports are scattered along the coastline, with some in ruins.
		- Landscape : The area is marked by its hilly terrain, dense forests, and some plains in the northern part.

2. "Azurr Forest"
	- Description : The Azurr Forest is the largest area in Garland, completely covered by forests intersected by river courses. It extends from the 
center of the region, flowing southwest towards the Outland Ocean and forming a large archipelago.
	- Key Features :
		- Primrose Lake : A central feature where two rivers meet, creating a famous fishing location with a harbor.
		- Red Flue Stable : An ancient stone tavern located near a river branch, known for its distinct chimneys and guarded by spearmen.
		- Anjan's Green Stable : A tavern built in a giant tree trunk, equipped with a stable for horses and a blacksmith shop.
		- Vygor Port : A smaller port located at the continent's edge, facilitating trade and travel to the other areas. It is well-guarded and 
	serves as a strategic point.
		- Landscape : The area features numerous rivers, waterfalls, valleys, and a rugged mountainous terrain that adds to its beauty.

3. "Sunnymead Island"
	- Description : Sunnymead Island is a small section of the continent, nearly surrounded by water and characterized by sandy shores and plains 
hidden among forests. It has a high mountain in the northeastern part known as North High Peak.
	- Key Features :
		- North High Peak : A prominent mountain with a tavern located at its summit, offering breathtaking views.
		- South Ram Port : A small, unguarded port behind rocks, where boats arrive from the mainland.
		- Gryveer Field : A farm run by Gryveer Tall and his friends, surrounded by a wooden fence and known for raising livestock.
		- Landscape : The area is mostly sandy and forested, with the mountain creating a unique geographical feature.

	Additional Information :
- Island Expeditions : Garland is known for its "Island Expedition" missions, where players explore small islands along the northern coast to 
investigate various objectives. These islands hide numerous caves and are guarded by various monsters.
- Hidden Quests : Throughout Garland, especially in Sharpthorne and Azurr Forest, players can find hidden quests related to the history and lore of 
the region.