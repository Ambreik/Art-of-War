# **Region III: Askania**

**Level Guidance:** 40–60
**Environment Type:** Dense forest with uneven terrain, rivers, and coastal areas
**Minimum Level to Access:** 40
**Design Intent:** High-level leveling zone; forested challenges, resource diversity, and advanced monster tiers
**Monster Tier:** Lv. 40–60; forest and river-adapted creatures
**Ecosystem Notes:** Rich flora and fauna; multiple rivers; forest pets, mount resources, and hidden quest locations

---

### **Sub-Area: Copthorne**

**Type:** Northeastern forest; second largest in region
**Description:** Forested area with rolling hills, fertile lands, and the winding **River Elysia** creating natural barriers
**Points of Interest:**

1. **Fortress of Eldergate:** Main settlement; fortified hilltop fortress; hub for quests, NPCs, and crafting
2. **River Elysia:** Vital river running east–west; irrigation, fishing, and water-based pet food sources
3. **Rivermouth Bay:** Southwestern estuary; trade hub and fishing location; potential pet food source
4. **Rothesoll Mountain Peak:** Western natural barrier; ancient ruins and high-level monster spawn

**Monster Tier:** Lv. 40–50
**Ecosystem Notes:** Forest monsters, river-adapted creatures, rare pets; natural resources for crafting and profession gathering

---

### **Sub-Area: Brontes Hoods**

**Type:** Central and southern forest; largest area of Askania
**Description:** Dense forest with uneven terrain; river branch to the east separates it from Coppervale
**Points of Interest:**

1. **Lowtown:** Southern settlement; small population; ruins for quest exploration
2. **Greenlyf Tavern:** Northeastern tavern; rest and interaction for travelers; minor quest hub
3. **Creepfyg Lake:** Central forest lake; fishing spot; water-based pets spawn; quest integration

**Monster Tier:** Lv. 45–55
**Ecosystem Notes:** Forest predators, mystical creatures; quests involving flora, fauna, and pet taming

---

### **Sub-Area: Coppervale**

**Type:** Eastern coastal forest; smallest area
**Description:** Forest transitioning into sandy beach; river separates area from mainland; strategic port on south coast
**Points of Interest:**

1. **Long Beach Port:** Coastal port for Traveling Ships; key trading hub
2. **Bayside Haven:** Human settlement near port; local guild hall; marketplace for resources
3. **Rosdown Woods:** Western forest area; dense flora; hidden pet spawns and quests
4. **Creepfyg Lake (extension):** Northern access to lake from forest edge; fishing and quests

**Monster Tier:** Lv. 50–60
**Ecosystem Notes:** Coastal and forest monsters; high-level pets; hidden treasures; quest-specific encounters

