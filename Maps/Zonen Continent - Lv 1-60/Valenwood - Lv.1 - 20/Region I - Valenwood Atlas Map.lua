# **Region I: Valenwood**

**Level Guidance:** 1–20
**Environment Type:** Forested northern region with rivers, hills, and human settlements
**Design Intent:** Starting zone for Pindars faction; introduces gameplay mechanics, quests, and early pet and monster interactions
**Monster Tier:** Lv. 1–20; forest and river-adapted creatures
**Ecosystem Notes:** Rich in timber, herbs, and small wildlife; supports early pet hunting and gathering

---

### **Sub-Area: Dreadhollow Fortress**

**Type:** Main city and starting hub for humans and Pindar faction
**Districts:**

1. **Western District:** Largest district; multi-level structures, quest NPCs, crafting NPCs, and combat instructors
2. **East District (Port Sector):** Extends along the sea; harbor protected by watchtower; players can board ships for sea travel
3. **South Side (Battalion Quarters):** Military area; restricted access; training grounds and barracks for Silver Fangs elite
4. **Central Plaza:** Business hub; vibrant market with crafting and trade NPCs
5. **Northside District:** Extension of Western District; friendly atmosphere; quest NPCs

**Key Port:** **Amaska Harbor** – West side; docking point for Traveling Ships (level 25+ for use)

**Monster Tier:** Lv. 1–10 (outside city)
**Ecosystem Notes:** City-adjacent forest areas with low-level monsters for early quests; suitable for novice pet acquisition and leveling

---

### **Sub-Area: Arsshis Woods**

**Type:** Starting area for werewolf playable class
**Key Locations:**

1. **Garrin Walls:** Central village area for werewolves
2. **Ancient Ruins:** Northern section; contains deep cave, dungeon area with cursed werewolves
3. **Cave of Maniah:** Dungeon; home of "Bulzek Bloody Fang"; unlocks werewolf transformation mastery at Lv. 20+

**Monster Tier:** Lv. 5–20
**Ecosystem Notes:** Dense forest with hidden clearings; high chance for early forest-type pets; quest-rich for playable werewolves

---

### **Sub-Area: South Valley**

**Type:** Largest area in Valenwood; for general leveling and questlines
**Key Locations:**

1. **Thorn Hills:** Initial hills and forest terrain; thorny vegetation, small rivers
2. **Greenard Rocks:** Mountain chain stretching toward the sea; difficult terrain, hidden monsters
3. **Amaska Harbor (South Valley side):** Fortified harbor; strategic transport and quest hub

**Monster Tier:** Lv. 10–20
**Ecosystem Notes:** Forest and hill biome; supports early-medium tier monsters and beginner pets; fishing and resource nodes

---

### **Sub-Area: Ash Isle**

**Type:** Lore/quest-focused hidden refuge for traitors and outcasts
**Key Features:**

1. **Golden Sands Beach:** Coastal area; small campfires, quest interactions
2. **Tropyca Forest:** Dense tropical forest; increasingly challenging navigation
3. **Cave of Rungdy:** Dungeon; guarded entrance, hidden treasures, and quest content

**Monster Tier:** Lv. 15–20
**Ecosystem Notes:** Tropical forest with rare resource nodes; beginner-medium pet taming zone; hidden quest opportunities

