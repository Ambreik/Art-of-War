			District-Specific Mechanics – Dreadhollow Fortress

	1. Western District (Market Quarter)

	Key Mechanics :
1. 	Market Dynamics

* Shifting Stalls : Some merchants rotate during day/night cycles. At night, werewolf-run stalls sell meat, hides, and rare alchemy reagents. By day, human 
merchants run textile, metalwork, and crafted goods stalls.
* Economic Factions : Rivalry exists between the "Amber Trade Consortium" (humans) and "Fang-Claw Merchants" (werewolves). Players can align with one to unlock discounts, rare items, or unique quests.

2. Black Market (Hidden Layer)
* A hidden "Moonlit Alley" appears only after dusk. Smugglers and shady vendors operate here, selling illegal items: poison brews, outlaw gear, and forbidden 
werewolf charms.
* Access requires completing an introductory quest or bribing the right guard.

3. Bargaining & Bartering
* Certain vendors allow barter trade instead of coin (e.g., exchanging wolf pelts, claws, or herbs for goods).
* Werewolf traders prefer raw resources, while humans prefer crafted goods. This mechanic encourages mixed gathering and crafting.

---

	2. Eastern District (Port Sector)
	Key Mechanics :

1. River & Sea Trade
	* Fishing and trade boats dock regularly, bringing rotating stock of fish, seaweed-based reagents, and exotic trade goods.
	* Naval quests send players upriver or to hidden isles for combat, smuggling, or escort missions.
2. Smuggler’s Wharf
	* Secret wharfside tavern basements allow access to outlaw quests. Black market vendors appear here at night, selling contraband goods like illegal potions, 
smuggled ores, or foreign weapons.
3. Import/Export Licenses (Reputation-Gated)
	* Merchant licenses are required for fair prices. Without a license, smugglers may overcharge or deny goods.
	* REP with either the "Amber Consortium" or "Fang-Claw Merchants" unlocks exclusive imports (human-crafted jewelry or werewolf talismans).

---

	3. Southern District (Battalion Quarters)
	Key Mechanics :

1. Recruitment & Training
   * Players can enlist in the fortress guard or werewolf battalions. Quests range from basic patrols to blood rituals for werewolf warriors.
   * PvE challenges like sparring or mock sieges offer rewards: titles, small REP boosts, or weapon upgrades.
2. Military Bounties
   * A bounty board lists deserters, rogue werewolves, or external threats.
   * Rewards include REP with the fortress, silver currency, or exclusive “military-issue” gear.
3. War Rituals
   * Werewolf tradition is blended into training. Bloodmoon Rituals are held monthly, boosting morale and offering temporary buffs to players who participate.

---

	4. Central Plaza (Civic Heart)
	Key Mechanics :

1. Faction Presence
   * Both human and werewolf leadership meet here. Public speeches, ceremonies, and proclamations occur regularly.
   * Players may witness scripted events like trials, celebrations, or executions.
2. Reputation Showcases
   * Players with high REP may be publicly recognized during ceremonies.
   * Outlaws risk being displayed in chains or publicly judged.
3. Festivals & Civic Events
   * Seasonal markets, moonlit feasts, or hunting contests are hosted here.
   * Vendors tied to these events sell unique items: festival food, decorative gear, or talismans.

---

	5. Northern District (Residential Quarter)
	Key Mechanics :

1. Community Reputation
	* Helping locals with quests (delivering goods, mediating disputes, clearing pests/rogues) builds REP.
	* With high REP:
		* Secret merchants open their doors.
		* Residents warn players of dangers.
		* Special werewolf-family quests unlock (e.g., bloodline trials, moon rituals).
2. Hidden NPCs & Merchants
	* At night, werewolf-only merchants appear. By day, human artisans host hidden sales.
	* Blended family homes provide cultural quests (helping mixed families balance traditions).
3. Residential Events
	* Spontaneous events like a howl ritual, wedding, or funeral might occur, pulling players into story-driven interactions.

