			Dreadhollow Fortress – Mechanics

		City-Wide Mechanics
	These mechanics apply to the whole of "Dreadhollow Fortress" and affect every district.

---

	1. Windows & Interiors

* Only major buildings are accessible.
* Player Interactions : Players can enter key locations, such as the Moonfang Keep (central hall), Command Barracks, Taverns, Guild Halls, and Marketplace Halls, 
but not every house or structure will be interactive.
* Visual Appeal : Certain windows and doorways reveal silhouettes of humans and werewolves going about daily life—sharpening weapons, sharing drinks, or in council 
meetings—adding atmosphere.

---

	2. Mount Restrictions

* No flying mounts allowed inside the fortress.
* Ground-Based Mounts Only : Wolves, horses, or ground beasts may be used. Flying mounts are restricted to wilderness zones outside the walls, ensuring a contained,
immersive city.
* Special Mounts in City : In the Port District, small boats or river-creatures may be used for quests. In the Military Quarters, trained war wolves can be ridden for certain scenarios.

---

	3. Guard Vigilance

* Aggressive players are punished.
* Combat Restrictions : Guards (human sentries and werewolf wardens) react instantly to violence in public spaces (Central Plaza, Military Quarters).
* Consequences of Aggression : Hostile players trigger alarm howls, and squads of guards (including elite werewolf enforcers) chase them down.Arrest means a debuff,
fines, or being handed to bounty hunters.
* Extra Layers :
	* Guards "sense" outlaw status by smell; hidden players can still be tracked.
	* Players with a bounty are actively hunted.
	* Guard packs cooperate, surrounding offenders rather than fighting one-on-one.

---

	4. Guidance System

* Guards provide directions.
* NPC Guidance : Sentinels stationed at key hubs (Western Market, Central Plaza) will guide players verbally, pointing claws or weapons toward the correct street.
* Quest Integration : Guards sometimes direct players toward ongoing missions or story events—such as military drills, noble councils, or hidden hunts.

---

	5. PK Restrictions

* City is a non-PvP zone.
* Criminal Behavior : PvP combat is disabled, but outlaw actions (smuggling, theft, assassination) remain possible.
* Consequences for Crimes : Criminals may be expelled from the fortress temporarily, fined, or hunted. Severe offenders receive a debuff marking them as 
"Criminal Charge", making them more visible to bounty hunters.
* Faction Conflict : Reputation outcomes differ—criminals might gain standing with outlaw packs or smugglers but lose favor with the Pindar Guard.

---

	6. District Phasing Based on REP or Story Progress
	Districts visually shift based on player impact:

* A strong guard reputation brings more patrols, banners, and celebrations.
* Criminal alignment increases shady stalls, graffiti with claw marks, and nocturnal activity.
* Residential zones may change tone—doors bolted shut, or nightly howling rituals visible.

---

	7. Watchtower Alert System (Zone-Wide Layer)
	When crimes escalate:

* Howl & Flare System : Watchtowers send up flaming beacons and sound a werewolf howl that echoes across the city.
* Lockdown Mode : More guards patrol, checkpoints appear, and fast-travel to districts is restricted.
* Reset : After time passes without new crimes, the fortress returns to normal.

---

	8. Disguises & False Identity (Criminal Tools)

* Criminal Factions : Players aligned with outlaws can buy or craft disguises to slip into guarded districts (Central Plaza, Military Quarters).
* Werewolf Twist : Smell-based detection is a threat; special potions or talismans are needed to mask a player’s scent.
* Penalty : If caught, disguises are destroyed, and debuffs (like “Branded by the Watch”) make the player easier to track for hours.

---

	9. Public Services / Vendors with Cooldowns

* Night Vendors : At dusk, unique merchants appear—selling moon-themed wares, enchanted talismans, or illicit brews.
* Festivals & Rituals : Certain vendors arrive only during Bloodmoon rites or seasonal feasts.
* Military Drills : After army training events, specialized arms dealers briefly open for players.

---

	10. Werewolf Wardens (Night Patrols)

* At night, elite werewolf sentinels patrol the walls and districts.
* Mechanics: faster than human guards, able to leap obstacles, immune to intimidation.
* Unlike humans, they do not give directions—only act as silent guardians.
* These NPCs symbolize the fortress’s werewolf heritage and keep the city secure after dark.

