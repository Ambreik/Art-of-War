			Additional Mechanics – Dreadhollow Fortress

---

		Faction Reputation

* Players can align with multiple factions inside the fortress, each unlocking different quests, goods, and perks:
	* The Ironfang Guard (werewolf-heavy military corps): Rewards focus on combat gear, blood-imbued weapons, and werewolf rituals.
	* Amber Trade Consortium (human merchant guild): Unlocks access to imports, rare trinkets, and discounted goods.
	* Fang-Claw Merchants (werewolf traders’ network): Offers barter mechanics, rare hides, alchemical reagents, and talismans.
	* Silver Fang Council (blended ruling body of humans + werewolves): Reputation here influences city politics and grants access to high-level civic quests.

	Reputation influences access to buildings, discounts at vendors, and even how NPCs react (respect, fear, suspicion).

---

		City Events

* The fortress hosts both military and cultural events :
	* War Drills : Large-scale city drills where guards mobilize, and players can join for REP or military rewards.
	* Bloodmoon Rituals : Werewolf-led ceremonies with unique buffs or quests (boosts to stamina, regeneration, or stealth for limited time).
	* Trade Caravans : Merchant convoys arriving at the port trigger temporary stalls with foreign goods and rare imports.
	* Public Trials : Criminal NPCs (or sometimes players flagged as criminals) may be tried in the Central Plaza, drawing crowds and roleplay opportunities.

	Events affect vendors, security levels, and district atmosphere, just like Dravenhold parades or pirate attacks.

---

		Crime System

* "Law & Order Mechanics" :
	* Guards (both human and werewolf) patrol heavily and react quickly to crimes.
	* Criminal players may be dragged to the "Silver Fang Tribunal", facing fines, REP loss, or public humiliation.
	* Some werewolf guards use their heightened senses to track disguised/outlaw players faster than human guards.
* "Outlaw Options" :
	* Criminals can hide in Moonlit Alley (Western District) or Smuggler’s Wharf (Eastern District).
	* Quests from outlaw NPCs let players sabotage patrols, smuggle goods, or sell stolen items.

* "Bounty System" :
	* Military and Civic bounty boards list criminal players and NPCs.
	* Werewolf hunters may track criminals by scent, adding a unique pursuit mechanic.

