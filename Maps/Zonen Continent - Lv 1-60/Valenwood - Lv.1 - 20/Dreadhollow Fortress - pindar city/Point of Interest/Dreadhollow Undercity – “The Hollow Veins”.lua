			The Hollow Veins : DESIGN & STRUCTURE DESCRIPTION

	*(Zone Design Breakdown)*

	Zone Type
- Indoor / Underground
- Massive, interconnected labyrinth with multi-layered verticality
- Mixture of flooded tunnels, sealed crypts, engineering chambers, and arcane-forbidden zones

---

	Entrances & Exits

1. Dockside Grate (East) : Hidden behind crates and nets near the eastern docks. Flooded at high tide; requires smuggler’s key or low tide to access.
2. Forest Tunnel (South / Outside Walls) : Stone tunnel overgrown with roots. Leads to the forest outskirts. Walls occasionally weak, prone to collapse.
3. Market District Hatch (North) : Concealed in a back alley of the market. Requires lockpicking, diversion, or special key.
4. Residential Basement Access (West) : Cracked floors and rubble in a residential courtyard lead to a forgotten well. Haunted with rats and faint ghostly whispers.

---

		GENERAL ARCHITECTURE & ZONE SHAPE

	Layout Structure
- Three primary corridors form the main spine of the underground labyrinth (East, Central, West).
- Multiple secondary corridors branch off, creating loops, dead-ends, and hidden chambers.
- Includes flooded rooms, arcane chambers, engineering areas, and sealed crypts.

	Navigation Style
- Maze-like design with loopbacks, locked gates, and vertical passageways (broken stairs, hanging ladders, old elevators).
- Corridors are wet, clogged, and sometimes collapsing, forcing detours.
- Doors are a mix of:
	- Rusted grates (require brute force or acid)
	- Arcane seals (dispel or sequence puzzles)
	- Mechanical locks (gear puzzles or requiring keys)

---

		THEMING OF EACH SUB-ZONE

	1. The Iron Spires (Flooded Dock Section)
- Waterlogged warehouses, rusted cranes, floating debris.
- Secrets: submerged side rooms accessible by diving, hidden smuggler caches.

	2. The Shattered Bazaar (Market Hideout)
- Collapsed stalls, scattered stolen goods, flickering lanterns.
- Secrets: hidden black-market trade rooms, trap-laden loot stashes.

	3. The Hollow Warrens (Rat-Infested Tunnels)
- Narrow, twisting tunnels overrun with vermin.
- Secrets: collapsed walls hide stash rooms, secret rat nests with rare loot.

	4. The Rune Sanctum (Arcane Crypt Chambers)
- Burial chambers, glowing Pindar runes, shadowy apparitions.
- Secrets: rune-locked doors, hidden lore chambers, minor arcane traps.

	5. The Fungal Maw (Bioluminescent Cavern)
- Twisted fungal growth, glowing mushrooms, creeping spores.
- Secrets: rare alchemy/herbalism ingredients, mini-chambers protected by spore clouds.

---

		MONSTERS & NPCs (Named and Mechanic-based)

1. Brinejaw the Tide Stalker
- Ambush aquatic beast in flooded areas.
- Weak to lightning; immune to physical while submerged.
- Drops: bio-luminescent scales and rare smuggler loot caches.

2. Specter of Valvewright Orvin
- Semi-transparent cursed engineer.
- Appears in engine rooms after rerouting pumps.
- Weak to Light or Holy magic.

3. Plaguegrub Broodmother
- Insectoid boss in overgrown tunnels.
- Mechanic: spawns Plaguegrubs over time.
- Weak to fire or holy effects.

4. Arcano-Ghast Prototype
- Failed mage experiment, phases in/out every 10s.
- Immune to physical damage while phased.

5. Edran’s Wailing Remnant
- Boss of sacrificial chambers.
- Stacks “Sanity Fade” debuff.
- Only vulnerable when temple bells are rung.

6. Rotling Swarmers
- Small insectoid hybrids.
- Explode on death; weak to AoE or freeze.

7. Chained Horror “The Gurgler”
- Vault boss; breaks chains during fight.
- Sluice floods room mid-battle unless valves are manipulated.

---

		MECHANICAL SYSTEMS

- Sluice Gate Puzzles : Used to drain areas, reveal paths, or drown players.
- Rust Mechanic : Weapons and armor decay over time unless protected with “Anti-Corrosive Oil.”
- Light vs. Darkness : Some spirits or secrets visible only in darkness; others require light to weaken.
- Valve Chains : Wrong order locks doors; correct sequence opens sealed areas.
- Soul Locks : Certain rooms require ghostly items (e.g., Wedding Ring of the Lost, Severed Tongue of the Whisperer) to open.

