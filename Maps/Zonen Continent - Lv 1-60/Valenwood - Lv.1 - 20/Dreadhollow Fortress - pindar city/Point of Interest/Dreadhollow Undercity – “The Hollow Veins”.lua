	Lore Justification

* The Hollow Veins were once an extensive undercity system for waste disposal, secret transactions, and criminal hideouts.
* Over centuries, portions collapsed, forming toxic, fungal-infested labyrinths.
* Only the desperate, brave, or insane explore these depths.

