		Dungeon Name : The Hollow Veins – Forgotten Chambers

	Zone Type : 5–10 Player Dungeon 

> Accessed through any of the four main entrances of "The Hollow Veins" (Dockside, Forest Tunnel, Market Hatch, Residential District). Once a player enters, the 
instance is phased.
> Can be completed solo (scales difficulty) but optimized for party play.

---

	General Design & Layout Structure (5 Segments – Progressive Dungeon System)

	Each section is gated with environmental hazards, locked doors, puzzles, or mini-bosses.

1. The Sooted Channels – Trash mobs, sewer beasts, and wet floor hazards.
2. The Forgotten Docks – First mini-boss zone, partially flooded tunnels patrolled by vermin and spectral rats.
3. The Rotted Spine – Maze of broken walkways, hanging ladders, and mechanical locks.
4. The Runebound Vaults – Arcane-infused crypts and hideouts for criminals.
5. The Hollow Maw (Boss Chamber) – Final boss, a bioluminescent fungal lake chamber where decay and magic converge.

---

		Encounter Scripting & Mechanics

	Trash Encounters

- Mire Rats (Pack mobs) : Swarm mechanics; appear in waves when players walk near certain tunnels.
- Sludge Oozlings (Ranged caster-type) : Leave minor AoE puddles; interruptable.
- Cave Vermin (Swarm mechanics) : Appear in clusters in narrow corridors; slow but high numbers.
- Spectral Scavengers : Patrolling ghosts that push players toward traps.

---

	Mini-Boss 1: Grimbite, the Dock Devourer

Location : The Forgotten Docks
Role : Mutated rat-like creature enhanced by arcane filth.
Mechanics :
	- Tunnel Ambush : Burrows and appears behind players to interrupt ranged attacks.
	- Acidic Claw : Frontal cone attack, leaves stacking DoT on tank.
	- Nest Call : Summons vermin at 75% and 40% HP.

---

	Mini-Boss 2: Veil Sister Zoranna

Location : The Rotted Spine
Role : Ghostly cultist that haunts collapsed warehouses.
Mechanics :
	- Chains of the Lost (Single-Target Root) : Breakable with 3 hits or dispel.
	- Floodwave : Temporarily raises water level, forcing vertical navigation.
	- Possessed Vermin : Summons rat spirits that explode on death.
	- Enrage Phase (20% HP) : Becomes fully visible, blinking between players and doubling damage.

---

	Mid-Encounter Mechanic: Rotted Spine Navigation Event

* Parkour-style navigation over broken walkways, hanging ladders, and pipe platforms.
* Players rotate levers to open paths while avoiding environmental hazards (spikes, falling debris, sludge pools).
* Falling triggers minor trash spawns (Cave Vermin).

---

	Boss 3: The Hollow Watcher

Location : Runebound Vaults
Role : Arcane-corrupted criminal overlord fused with spectral filth.
Mechanics :
	Phase 1 :
	- Runic Burst : Targets multiple players with damaging sigils.
	- Summon Minions : Creates spectral criminals to attack.
	- Environmental Hazard : Rooms partially collapse over time; players must navigate safe zones.
	
	Phase 2 (50% HP) :
    * Splits into "Watcher Flesh" (tank buster) and "Watcher Mind" (caster with fear and confusion).
    * Must be killed in coordination within 10 seconds or one revives with 25% HP.

---

	Final Boss: Mycoheart, the Hollow Maw

Location : Core Chamber / Bioluminescent fungal lake
Type : Amorphous fungus-corrupted horror infused with Pindar magic.

	Phase 1 :
	- Toxic Spore Vortex : Rotating AoE zone.
	- Leech Fungus : Steals healing from players until cleansed.
	- Exploding Spore Pustules : Random AoE damage zones.

	Phase 2 (60% HP) :
	- Corrupt Heartbeats : Pulses damage and slows players.
	- Summon Spore Horrors : Small adds explode after 15 seconds.
	- Environmental Puzzle : Redirect fungal toxins by activating 3 runic valves.

	Phase 3 (25% HP, soft enrage) :
	- Expands to cover 80% of chamber.
	- Spawns hallucination fungi that must be destroyed by coordinated players.
	- Timer Enrage : Will consume chamber if not defeated in 2 minutes.

