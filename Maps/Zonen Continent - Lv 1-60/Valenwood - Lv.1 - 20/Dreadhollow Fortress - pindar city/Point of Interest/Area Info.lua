			Dreadhollow : The Capital of the Pindars

	Overview : Dreadhollow stands as the heart of the Pindar kingdom, strategically positioned in the northeastern part of the region. This fortified city serves as
the starting point for players, surrounded by lush forests, meandering rivers, and scenic waterways that flow into the Great Sea and Outland Ocean. With its rich 
history and intricate architecture, Zonen is designed to provide players with an immersive experience as they embark on their adventures.

	Geography :
- Location : Northeastern part of the continent, enveloped by dense forests and rivers, with direct access to the Great Sea and Outland Ocean.
- Natural Features : Branches of rivers originate from nearby mountains, creating beautiful lagoons and enhancing the scenic beauty of the area.

	City Structure : Dreadhollow, is a large citadel constructed with fortified walls made from greenish stone blocks. The city is divided into several distinct 
districts, each showcasing unique characteristics and architectural styles.

---

	Districts of Dreadhollow :

1. Western District :
	- Description : The largest district, structured on two levels. The lower level features smaller buildings, while the upper level boasts larger, more intricate 
structures.
	- Features : Home to quest-giving NPCs, crafting NPCs, and combat instructors. The atmosphere buzzes with pedestrians and guards providing assistance.

2. East District (Port Sector) :
	- Description : This district extends along the sea and includes a prominent harbor protected by a watchtower. The area is lively with fishermen trading goods.
	- Features : A wide street leading to the port, with anchored ships and warships. Security is maintained through selective outposts. Players can board ships for
sea travel during specific times.

3. South Side (Battalion Quarters) :
	- Description : An exclusive military district with restricted access, housing the command post, training camps, and soldier barracks.
	- Features : Guarded by the elite group known as the "Silver Fangs." The training area is equipped with tools for player interaction, available for those at 
level 20.

4. Central Plaza :
	- Description : The vibrant business hub of Zonen, characterized by smaller buildings, lush plants, and a bustling market atmosphere.
	- Features : NPC merchants offer weapons, armor, and crafting materials. The ambiance is lively, with pedestrians engaging with merchants.

5. Northside District :
	- Description : An extension of the Western District, known for its friendly atmosphere and quest givers, welcoming new players.
	- Features : Houses the "City Store" in a prominent building, staffed by helpful NPCs ready to assist.

---

	City Mechanics :

- Destruction and Interactions :
	- Windows within the city are indestructible, ensuring the city remains intact during conflicts.
	- Flying mounts are prohibited within city limits.

- Guardians and Player Interaction :
	- Guards remain vigilant at night; engaging in player duels may provoke their aggression.
	- Guards provide guidance to major locations, marked by blue dots on the player’s radar.

- PvP Regulations :
	- Certain districts prohibit dueling; players engaging in player killing (PK) are attacked by soldiers.

- NPC Interaction :
	- Each district features unique NPCs with distinct roles, from quest givers to merchants, enhancing the player's experience.

---

	Additional Features :

- Island Expeditions : 
	- Accessible to players once they reach level 40, these expeditions allow exploration of five unnamed islands for quests and treasure hunting.

- Rich Lore : 
	- Each area in Dreadhollow Fortress is steeped in history, providing a backdrop for quests and player interactions.

