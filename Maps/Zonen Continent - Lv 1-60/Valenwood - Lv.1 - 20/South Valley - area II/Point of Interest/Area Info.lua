			Area Info: South Valley

		Overview :
	South Valley is the largest area within the Zonen Region, situated in the southern part and characterized by its mountainous terrain and proximity to the Great 
Sea. This sub-area features a variety of points of interest and serves as a key passage to the capital.

	Geography :
- Location : Southernmost area of the Zonen Region.
- Terrain : Dominated by a large mountain range, with forests, lakes, and rivers defining the landscape.
- Separation : The area is split from the central region by a river, with two heavily guarded bridges allowing access to the capital.

	Key Locations :
1. Thorn Hills :
	- Description : The first area encountered after crossing the bridges, characterized by vast hills and forests filled with thorny trees.
	- Features :
		- Ardion Village : A well-fortified settlement with thick stone walls and watchtowers. Sentinels patrol regularly, prepared to defend against wild beasts.
		- Market Area : Home to NPCs offering merchandise and army garrison services. The village has a statue that serves as a place of worship.

2. Greenard Rocks :
	- Description : A mountain chain stretching toward the sea, known for its challenging terrain and ancient legends.
	- Key Features :
		- Windrock Peaks : A notable height offering stunning views but infamous for the dangers posed by hidden beasts. The peaks are barren and rocky, with no vegetation.
		- Ruined Village : At the base of the peaks, players can find a ruined village inhabited by a boss and his rock minions.

3. Amaska Harbor :
	- Description : A fortified harbor located in the southwestern part of South Valley, constructed during the Second Great War for strategic transport.
	- Features :
		- Fortifications : Surrounded by a long stone wall with a single entrance guarded by armed soldiers.
		- Naval Presence : Contains several warships and fishing boats, including the wreck of a large warship that enhances the harbor's picturesque view of the 	Great Sea.
		- Fishermen NPCs : Rank II fishermen provide guidance to adventurers visiting the harbor.

	Atmosphere and Sound :
- Upon entering the Greenard Rocks, players experience a change in ambient music that heightens the sense of danger. The sound of howls and growls can be heard, 
especially at night, creating an eerie and immersive atmosphere.
- Flaming torches are scattered throughout, providing light and helping players navigate toward the edge of the mountains.

