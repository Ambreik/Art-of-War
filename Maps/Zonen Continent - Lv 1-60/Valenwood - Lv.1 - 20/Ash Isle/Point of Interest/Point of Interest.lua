			Point of Interest
			
	*Golden Sands - located in the right side of the island, close to the continent.
	*Tropyca Forest - located in the left side of the island, cover almost 2/3of the island and surround the central tall hill.
	*Cave of Rungdy - located in the western part of the island, at the foot of the hill protected by high and sharp rocks with strange paintings on them.