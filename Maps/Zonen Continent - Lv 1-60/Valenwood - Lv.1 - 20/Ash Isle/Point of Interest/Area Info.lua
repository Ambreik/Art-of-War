			Ash Isle

	Location : 
- Situated in the northwestern part of the continent, close to "Amaska Harbor". The isle's hilltop can be seen from the port, and players can reach it using 
specialized boats provided by a marine trader NPC at the harbor.

	Overview : 
- Ash Isle serves as a refuge for traitors and thieves who have fled from the army's wrath after deserting or raiding supply boats. The island is heavily fortified 
by its inhabitants, who have tamed the wild animals to protect their territory and instill fear in potential intruders.

	Key Features :
1. Golden Sands Beach :
	- The sandy beach on the island where several campfires are lit at night, creating visibility for their nefarious activities. 
	- It is a strategic point for ambushing small boats attempting to approach the harbor.

2. Tropyca Forest :
	- A dense forest surrounding the island,characterized by tall tropical trees. The forest grows thicker and more challenging to navigate as players venture deeper.
	- The island's unique "J" shape allows for several secluded mini-settlements made of straw or wooden tents covered with large palm leaves. 

3. Cave of Rungdy :
	- Located in the western part of the island at the hill's base, protected by sharp rocks adorned with strange paintings.
	- Players must traverse the island and navigate traps to reach this cave, where ancient ruins and hidden treasures may await.

	Wildlife : 
- Home to aggressive monsters, including tigers, crocodiles, and anacondas, all of which pose a threat to adventurers.
- Mechanics :
	- All monsters are aggressive, with human-like adversaries employing ranged attacks that have critical hit potential.
	- Flying mounts are prohibited while on the island, but players can use them to travel to the island initially.

