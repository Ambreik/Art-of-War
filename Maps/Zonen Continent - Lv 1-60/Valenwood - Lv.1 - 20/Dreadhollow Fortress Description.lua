			Eastern District of Dreadhollow Fortress – Port Sector
	
	The Eastern District of Dreadhollow Fortress is its lifeline to the sea — a storm-beaten port carved into jagged cliffs, with reinforced stone docks stretching 
into dark waters. The air is thick with brine, smoke, and the cries of gulls. Ships dock constantly: merchant galleys, pirate sloops, and war barges. Smugglers and 
privateers thrive here, and while the Pindar maintain some order through patrols, much of the district runs on coin, contraband, and blade.
	This is the gateway for players to access sea-based content, exotic trade, and the darker underbelly of commerce.

---

