			Dreadhollow Fortress – Overview

	Dreadhollow Fortress stands as the unyielding heart of the Pindar Dominion, a citadel-city carved from the wild strength of Zonen’s northeastern lands. Unlike the
polished grandeur of Dravenhold, Dreadhollow embodies raw power, survival, and warlike pride. Its massive walls of dark greenish stone loom over the surrounding 
forests and rivers, symbolizing the Pindar people’s will to dominate both land and sea.
	The fortress is more than a city—it is a bastion of savagery and order, where ancient tribal traditions fuse with disciplined militarism. Within its walls, 
clans gather beneath war banners, forging weapons, raising beasts for battle, and preparing for constant conflict. The scent of iron, smelted ore, and animal hide 
mixes with the salt of the sea breeze, reminding all who enter that Dreadhollow is not a place of comfort—it is a place of endurance.
	For the Pindar people, "Dreadhollow" is a sacred inheritance. Built upon the grounds of an old werewolf settlement, the citadel reflects both primal heritage 
and modern warcraft. Its guardians, the "Silver Fangs", serve as enforcers of law and protectors of the capital, ensuring that strength—not weakness—defines the 
life within its walls.

	Adventurers beginning their journey here will find themselves immersed in an atmosphere of wild unity, where survival is celebrated, loyalty is rewarded, and 
war readiness defines every heartbeat of the city.

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

		Geography & Setting of Dreadhollow Fortress

	Location : "Dreadhollow Fortress" rises in the northeastern expanse of the Zonen continent, nestled between jagged mountain ridges and dense, untamed forests. 
Its position is not just symbolic but tactical—offering both defensive strength and easy access to the "Great Sea" and the "Outland Ocean". This strategic placement
allows the Pindars to strike by land or sea, and to funnel resources into their war-driven economy.

	Natural Surroundings

* "Arsshis Woods" : Towering, mist-choked pines and ancient oaks surround the fortress. These woods were once home to powerful werewolf clans and still echo with 
primal energy. The Pindars treat the forest as both resource and hunting ground, extracting lumber, herbs, and wild game while guarding against beasts that still 
prowl its depths.
* "South Valley" : Is the largest area within this place, known for its rolling hills and fertile plains. This region supports agriculture and provides resources 
essential for the kingdom. The valley is crisscrossed by rivers flowing from the Great Sea, enhancing its agricultural potential but also serving as a natural 
barrier against invaders.
* "The Fangriver System" : Branching rivers flow down from the "Ironmaw Mountains", converging near Dreadhollow to form lagoons, small waterfalls, and natural 
canals. These waterways provide fresh water, fertile fishing grounds, and defensive barriers, as well as routes to move troops and goods quickly.
* "The Great Sea" & "Outland Ocean" : From the city’s eastern port district, the open sea stretches to both trade routes and enemy coasts. Warships line the harbor,
while fishing boats and merchant vessels slip between them, creating a constant sense of tension between war and survival.

	Terrain & Construction

	The fortress is built on a natural stone plateau that rises slightly above the river delta, making it difficult for enemies to assault. The walls are forged 
from green-gray stone, quarried directly from the nearby Ironmaw range, reinforced with iron bands and tribal carvings that serve both as decoration and 
intimidation.

	Atmosphere & Tone

	Unlike the polished serenity of Dravenhold, Dreadhollow radiates a wild vitality. Wolf howls can sometimes be heard echoing across the forests at night. The air
smells of burning pitch, salted sea spray, and forge smoke. The environment is both beautiful and savage—a constant reminder that the Pindars thrive in the balance 
of nature’s bounty and nature’s wrath.

