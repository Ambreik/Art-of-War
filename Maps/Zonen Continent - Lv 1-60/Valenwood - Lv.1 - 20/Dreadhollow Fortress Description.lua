### 🧭 Western District Character

* Human-Werewolf Fusion : instead of being the "market street," it’s the soul of everyday life.
* The goods here are basic survival & lifestyle, not luxuries: bread, tools, gear, simple potions.
* Keeps Central Plaza unique as the fortress’ *commercial and political* core.

