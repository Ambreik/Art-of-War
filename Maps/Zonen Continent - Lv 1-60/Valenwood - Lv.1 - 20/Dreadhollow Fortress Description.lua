		The Districts of Dreadhollow
		
	1. Western District

	Description : The Western District is the largest district of Dreadhollow, sprawling across two levels. The lower level features compact buildings, primarily 
constructed from greenish stone with timber accents, reflecting a sturdy yet practical design. Upper-level structures are larger, with open balconies and communal 
terraces, showcasing Pindar architecture influenced by both human urban planning and werewolf territorial aesthetics. Streets are wide enough for patrols of mounted
units, and bridges connect elevated walkways, allowing swift movement across the district. Lanterns and torches line the streets, giving the area a lively yet 
slightly wild atmosphere during the evening hours.

	Features & Mechanics :

* NPCs : The district hosts a variety of NPCs, including quest-givers, crafting masters, and combat instructors.
* Player Interaction : Players can engage in profession training, pick up faction-specific quests, and purchase basic supplies.
* Guards & Security : The “Silver Fangs,” an elite unit of hybrid human-werewolf soldiers, patrol the district. They maintain order, provide guidance, and enforce 
city rules.
* Environmental Detail : Open-air sections allow mounted travel, but certain narrower streets restrict larger mounts.

	Role in the City & Lore : The Western District is the heart of civic life in Dreadhollow, representing the fusion of human discipline with werewolf vigor. Its
dual-level architecture symbolizes the hierarchical yet cooperative society of the Pindars, offering both a living space for residents and a hub for newcomers to 
interact with the faction. Quests from this district often reflect the ongoing integration of werewolf traditions with Pindar governance, emphasizing collaboration 
and strength.

---
	
	2. Eastern District (Port Sector)

	Description : The Eastern District stretches along the coastline of Dreadhollow, forming the city’s primary harbor. A sturdy watchtower guards the entrance to 
the port, overseeing both merchant and military traffic. Buildings here are functional, mostly constructed from greenish stone and reinforced timber, designed to 
withstand harsh coastal weather. The district bustles with fishermen, shipwrights, and merchants, giving it a lively, industrious atmosphere. The streets are lined 
with warehouses and storage yards, while the docks accommodate a mix of fishing boats and small warships. Bridges and wooden piers allow for easy access to anchored
vessels.

	Features & Mechanics :

* NPCs : Fishermen, shipwrights, harbor guards, and quest-giving NPCs are located throughout the district.
* Player Interaction : Players can board ships for trade or travel during scheduled departure times. Maritime quests and transport missions originate here.
* Security & Patrols : Guard posts along the harbor and watchtower provide surveillance. Only registered ships may dock without inspection.
* Environmental Detail : Players can interact with watercraft, but flying mounts are prohibited. Certain quests may require navigating treacherous waters or assisting in naval defense.

	Role in the City & Lore : The Port Sector is the lifeline of Dreadhollow, reflecting the Pindar’s mastery over both land and sea. The district is a symbol of 
trade, diplomacy, and military readiness. Its integration of human architectural order with the rugged coastal style favored by werewolves creates a unique 
environment where commerce and defense coexist. Players engaging here often experience the strategic importance of the harbor, including quests that reinforce the 
faction’s control over trade routes and naval security.

---

	3. South Side (Battalion Quarters)

	Description : The South Side is Dreadhollow’s exclusive military district, designed to house the city’s elite forces and command infrastructure. Access is 
strictly controlled, with gates manned by Silver Fangs, the Pindar faction’s renowned elite soldiers. The district features barracks, training grounds, armories, 
and a central command post. Buildings are robust, built from greenish stone and reinforced timber, with defensive towers providing oversight of the city and 
surrounding forests. The atmosphere is disciplined, with soldiers constantly patrolling and training, creating a palpable sense of order and readiness.

	Features & Mechanics :

* NPCs : Commanders, training instructors, and elite guards populate the district. Players may interact with NPCs for advanced combat training, military quests, and
faction-specific missions.
* Training Grounds : Areas equipped for sparring, archery, and mounted combat, allowing players to improve their skills and complete training objectives.
* Restricted Access : Only players of certain levels (typically 20+) or those on approved quests can enter freely. Unauthorized entry triggers Silver Fang patrol 
intervention.
* Security Mechanics : The district is heavily guarded, and PvP interactions are restricted; engaging guards results in immediate combat.

	Role in the City & Lore : The Battalion Quarters symbolize the martial strength and discipline of the Pindar faction. It is here that humans and werewolves work
together to maintain city defense, train recruits, and prepare for frontier expeditions. The district reflects the faction’s warlike character, emphasizing the 
blend of military precision and the raw physicality inherited from the werewolf lineage.

---

	4. Central Plaza

	Description : The Central Plaza is the vibrant heart of Dreadhollow, serving as the primary commercial and social hub of the city. It is an open square, lined 
with smaller, intricately designed buildings showcasing the Pindar architectural style, with greenish stone accented by timber and banners displaying the city’s 
emblem. Lush plants and ornamental fountains add a touch of natural beauty, contrasting with the fortress-like character of the city. The plaza is bustling with 
activity, filled with merchants, quest-givers, performers, and visiting NPCs, creating a dynamic environment for players.

	Features & Mechanics :

* Merchants & Services : NPCs provide weapons, armor, crafting materials, consumables, and specialty goods unique to Dreadhollow.
* Quest Interaction : Numerous quest-giving NPCs are positioned here, providing both main storylines and side missions.
* Player Interaction : The open layout allows for social interaction, trading, and guild gatherings. Players can set up temporary stalls or interact with city 
events.
* Environmental Design : While open and busy, the plaza remains fortified, and windows or structures cannot be destroyed during conflicts.

	Role in the City & Lore : The Central Plaza reflects the Pindar faction’s organized and community-oriented side. It is the convergence point for humans and 
werewolves alike, showcasing trade, cooperation, and the city’s cultural vibrancy. The plaza also functions as a hub for players to plan expeditions, purchase 
essential items, and engage with the city’s lore.

---

	5. Northside District

	Description : The Northside District is an extension of the Western District, designed as a welcoming area for new players and visitors. The streets are 
slightly narrower than in the Western District but maintain a structured layout, with greenish stone buildings accented with timber and banners representing local 
guilds and minor Pindar factions. The district has a calm, friendly atmosphere, with open courtyards, fountains, and shaded seating areas where NPCs and players can
interact.

	Features & Mechanics :

* City Store : A central building hosts the “City Store,” staffed by helpful NPCs offering essential items, consumables, and information.
* Quest NPCs : A number of quest-givers populate the district, guiding new players through introductory missions and storylines that explain the history and culture
of Dreadhollow.
* Social Interaction : Open courtyards encourage guild gatherings, trade, and casual player interactions.
* Security & PvP : Guards patrol the district to ensure safety. Player duels are allowed in designated areas, but any PK activity triggers a response from the 
city’s Silver Fangs.

	Role in the City & Lore : The Northside District is a bridge between the bustling Western District and the rest of Dreadhollow, symbolizing the Pindar faction’s
effort to integrate newcomers and maintain a balance between warlike strength and community life. Its design reflects the faction’s mixed heritage of humans and 
werewolves, showing harmony between diverse races in a controlled environment.

