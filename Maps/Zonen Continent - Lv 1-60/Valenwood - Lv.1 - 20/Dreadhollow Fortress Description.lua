### 🏘️ Key Locations in the Western District

1. The Hearthbound Commons

   * A large public square where both races gather around great braziers at night.
   * Humans treat it like a neighborhood park; werewolves see it as a communal fire circle.
   * Informal meetings, recruitment for hunting parties, local performances happen here.

2. The Emberfang Smithy *(artisan smith, not military)*

   * Small-scale workshop producing tools, cookware, horseshoes, nails, and repairing armor/clothing.
   * Distinct from military blacksmiths: it’s practical, civilian work.
   * Run by a werewolf craftsman who blends sturdy ironwork with bone-carved handles.

3. Moonwhistle Tavern

   * Popular neighborhood tavern, much humbler than the grand ones in Central Plaza.
   * Food: hearty stews, roasted root vegetables, spiced mead.
   * Basement used as an informal meeting ground for scouts and hunters, where rumors of nearby beasts circulate.

4. Stonemill Bakery

   * Family-run human bakery with a communal oven — daily bread for the district.
   * Known for hearty rye bread and sweet honey loaves that werewolves oddly love.

5. Wolf’s Tail Training Yard

   * Small open space for cub and child training — young werewolves practice shifting control, human children practice archery and spearplay.
   * Serves as a cultural blending ground where youths of both races grow up together.

6. The Loomfire Hall

   * Tailor’s guildhouse / communal workshop.
   * Produces basic clothing, cloaks, leather harnesses, and more importantly, winter gear for soldiers and scouts.
   * Firepits inside keep the hall warm, said to mimic the ancient caves werewolves once crafted in.

7. Fangroot Herbalist

   * Local herbalist shop, smaller than the apothecaries in Central Plaza.
   * Provides minor healing poultices, teas, salves, and some shapeshift-calming tonics used by younger werewolves.

