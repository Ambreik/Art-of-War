			Area Info: Arsshis Woods

	Overview : Arsshis Woods is a dense forest area located southwest of Zonen Fortress, separated from the main city by a branch of the river that flows through the 
region. This area serves as the natural habitat for the werewolves and is characterized by its wild animal population and scenic rivers that flow into the Great Sea.

	Geography :
- Location : Southwest of Dreadhollow Fortress.
- Environment : A dense forest with two rivers running through it, abundant in wild animals.
- Size : Relatively small but rich in flora and fauna.
- Significant Locations :
	- Garrin Walls : The primary settlement of the werewolves, featuring protective digs, ditches, and stone megaliths that create a fortified barrier around the 
	village.

	Notable Features :
- Ancient Ruins : In the northernmost part, near the mountains that separate the regions, lie the ruins of a city that holds a deep cave. This is where ancient 
werewolves, cursed and unable to coexist with the current werewolf population, take refuge.
  
- Cave of Maniah : A dungeon located within the ruins, this is where the cursed ancient werewolves, led by "Bulzek Bloody Fang", plot their revenge against those they
believe have betrayed their race. The cave serves as a hiding spot for these ancient werewolves who are under a curse, preventing them from coexisting with their kin.

	Lore :
- Curse of Bulzek Bloody Fang : Bulzek, the leader of the cursed werewolves, was said to have made a pact with the Old Gods, leading to the curse that prevents him 
and his legion from coexisting peacefully with others. This act of betrayal stems from his belief that his nation needed to stop the gods and their minions during the
war.

	Gameplay Elements :
- Level 20 Dungeon : The Cave of Maniah is a dungeon accessible to players at level 20. Adventurers can explore this dungeon to seek high-grade items and defeat 
Bulzek Bloody Fang, which grants them the ability to master their wolf nature, allowing them to transform into human form at will.

