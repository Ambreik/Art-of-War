			Area Info

	"Fragran Canyon" is the third area of the region, positioned on the eastern side and serving as the gateway to the shores on the western edge of the continent. 
While it is the smallest area, it is rich in diversity and intrigue. The area is bordered to the west by the "Maldiff Oasis", which acts as a natural boundary 
separating it from the "Crystal Desert". The stunning landscapes and hidden secrets of Fragran Canyon are waiting to be uncovered.

	"Maldiff Oasis" marks the western boundary of Fragran Canyon, offering a serene escape filled with lush greenery and vibrant wildlife. This sprawling oasis 
serves as a crucial access point between the two areas, providing players with a peaceful retreat amidst their adventures. It features a small unnamed village where
a few NPC fishermen offer boat rentals for fishing in the calm waters.

	As players venture eastward, they will encounter the "Ranpold Finger Path", a savannah-like terrain characterized by vast vegetation, tall trees, and a dynamic 
landscape. This area leads to a series of intersecting roads that guide players toward the entrance of the "Labyrinth of Ranpold". The entrance is marked by a grand
gate carved into the rock, flanked by statues of ancient soldiers, creating an imposing threshold into the maze that lies beyond.

	The "Labyrinth of Ranpold" is a sprawling and intricate maze located on the eastern side of Fragran Canyon. This area is teeming with Rank III aggressive 
monsters, making it both a challenge and an adventure for players. The labyrinth's pathways twist and turn, leading to a central chamber where a towering statue of 
a shaman stands with a glowing scepter held aloft, providing a momentary reprieve from the relentless creatures within. Players must navigate carefully, as the 
labyrinth's corridors are lined with vegetation and can easily lead to dead ends or ambushes from lurking foes.

	Finally, the journey culminates at the "Eastport Piers", where players arrive after traversing the labyrinth. This bustling port is safeguarded by stone walls 
and patrolled by guards, creating a vibrant atmosphere filled with the sounds of waves and the calls of merchants. The piers serve as a hub for fishing vessels and 
trade, with multiple docking points for boats. The stone road leads players to various NPC homes and vendors, providing a rich environment for quests and 
exploration.

	Environmental Mechanics

- High Mountain Wall :  
	The area is framed by a high mountain wall in the shape of a “C,” creating a natural barrier and defining the boundaries of Fragran Canyon. 
- Water Features :  
	The "Maldiff Oasis" enhances the biodiversity of the region, featuring rich vegetation and serving as a tranquil fishing spot.
- Bridges and Pathways :  
	Players can traverse the uneven landscape along the "Ranpold Finger Path", which connects to various locations leading to the labyrinth.

	Challenges for Players

- Environment Affect :  
	Navigating through the labyrinth can be disorienting, especially during nighttime when monsters are more vigilant, requiring players to be cautious of their 
surroundings.
- Wildlife Encounters :  
	The labyrinth is home to numerous aggressive monsters that can absorb Mana, Energy, or Rage from players, making battles intense and strategic. Players must 
prepare for fast-paced encounters where the enemies can pierce defenses easily.
  
