			Point of Interest
			
*Crystal Desert : second area and the biggest and is located in the center of the region spread to the south and cover most of the region.
	*Sunny Fields : eastern side of the region consist mostly in sand environment and giant skeletons of dragons and giants between the huge structures in the 
	center of the area.	
		*Brailey's Pier : located near the center of the area, on the river path. Property owned by "Brailey" a NPC who start a mountly Event called 
		"Fish Trap Mania".
		*Fort Breenk : located in the far south east surrounded by giant walls and towers that lead to this fort full of giants.
	*Badlands : located in the center to west side of the area, the biggest area. The ground here is affected by craters that interrupt the routes in this area in certain places, and the vegetation is made up of huge cactuses. 
		*Strongfield Castle : far in the west side, this castle is the main settlement of the area and also the region.
		*Maldiff Oasis : is a giant oasis located right in the east delimitation of the area.