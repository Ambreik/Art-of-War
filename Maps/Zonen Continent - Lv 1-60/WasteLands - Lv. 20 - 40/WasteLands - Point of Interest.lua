			Point of Interest
	
	*Sandee Lanes : is the first area and second biggest. Is located in the northern west side of the region that have access to the shore in west and desert in the 
east.
		*Tandara : Located in the west of the area, streatches as a vast savanna field.
			*Tandara Port : located in the middle west side of the area,at the shore and is surrounded by tall hills of dunes that leave the impression are lanes of 
		sand.
			*Tandara City : located close to the mountain in the nort-east and surrounded by tall vegetation and giant cactuses.
		*Zenarry Dunes : is located in the center of the area and goes to the east side. Is a vast desert.
	*Crystal Desert : second area and the biggest and is located in the center of the region spread to the south and cover most of the region.
		*Sunny Fields : eastern side of the region consist mostly in sand environment and giant skeletons of dragons and giants between the huge structures in the 
	center of the area.	
			*Brailey's Pier : located near the center of the area, on the river path. Property owned by "Brailey" a NPC who start a mountly Event called 
			"Fish Trap Mania".
			*Fort Breenk : located in the far south east surrounded by giant walls and towers that lead to this fort full of giants.
		*Badlands : located in the center to west side of the area, the biggest area. The ground here is affected by craters that interrupt the routes in this area in certain places, and the vegetation is made up of huge cactuses. 
			*Strongfield Castle : far in the west side, this castle is the main settlement of the area and also the region.
			*Maldiff Oasis : is a giant oasis located in the east side delimitating the area.
	*Fragran Cannion : is the third area and leads to the shores in the west side of the continent. Is located in the east side of the area and is the smallest area.
		*Ranpold Finger Path : is the main location that starts from the west side and leads to the depts of the labyrinth ahead.
		*Labyrinth of Ranpold : east side of the cannion here is transforms into a maze for the momst part of the area and leads to the shore far in east.
			*Eastport Piers : located at the east side of the region, the port here is surrounded by the mountains that separate de regions.
		
