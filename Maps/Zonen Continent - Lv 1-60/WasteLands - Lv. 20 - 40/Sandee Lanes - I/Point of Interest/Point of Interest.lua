			Point of Interest
			
*Sandee Lanes : is the first area and second biggest. Is located in the northern east side of the region that have access to the shore in east and desert in the west.
	*Tandara : Located in the east west of the area, streatches as a vast savanna field.
		*Tandara Port : located in the middle west side of the area,at the shore and is surrounded by tall hills of dunes that leave the impression are lanes of sand.
		*Tandara City : located close to the mountain in the nort-east and surrounded by tall vegetation and giant cactuses.
	*Zenarry Dunes : is located in the center of the area and goes to the east side. Is a vast desert.