# **Region II: WasteLands**

**Level Guidance:** 20–40
**Environment Type:** Arid desert with cliffs, sparse vegetation, and harsh terrain
**Minimum Level to Access:** 20
**Design Intent:** Mid-tier leveling zone; introduces desert survival mechanics, mid-level monsters, and resource-gathering challenges
**Monster Tier:** Lv. 20–40; desert-adapted creatures
**Ecosystem Notes:** Sparse water sources, occasional oases, scattered settlements; early desert pets and unique desert monsters

---

### **Sub-Area: Sandee Lanes**

**Type:** Eastern desert edge; first accessible area for Pindars in this region
**Description:** Savanna-like desert with minimal vegetation, rolling sand dunes, and occasional rock formations
**Points of Interest:**

1. **Tandara:** Western expanse of the area; vast savanna for grinding mid-level monsters
2. **Tandara Port:** Central-west shore; docking point for Traveling Ships; surrounded by hills of dunes
3. **Tandara City:** Northeast near the mountain; surrounded by tall vegetation and giant cactuses
4. **Zenarry Dunes:** Central-east desert stretch; challenging terrain and roaming desert monsters

**Monster Tier:** Lv. 20–30
**Ecosystem Notes:** Desert creatures, low water availability; players may encounter scavenger-type pets and desert flora for foraging

---

### **Sub-Area: Crystal Desert**

**Type:** Central region; largest and harshest desert section
**Description:** Vast, barren land with giant sand stretches and skeletal remains of dragons and giants
**Points of Interest:**

1. **Sunny Fields:** Eastern sand plains; scattered monster dens and quest NPCs
2. **Brailey's Pier:** Near river; property of NPC Brailey; hosts monthly "Fish Trap Mania" event
3. **Fort Breenk:** Southeast; fortified structure surrounded by walls and towers; desert monsters patrol vicinity
4. **Badlands:** Central-west; cratered terrain, giant cactuses, ideal for mid-level grind
5. **Strongfield Castle:** Western fortification; key quest hub and dungeon entrance
6. **Maldiff Oasis:** Eastern desert oasis; rare water source and mid-tier pet taming spot

**Monster Tier:** Lv. 25–35
**Ecosystem Notes:** Arid desert; monster types include sand dwellers, elemental earth monsters, and mid-level desert pets

---

### **Sub-Area: Fragran Canyon**

**Type:** Smallest area; leads to coastal shore
**Description:** Canyon maze with narrow paths, rocky formations, and limited vegetation
**Points of Interest:**

1. **Ranpold Finger Path:** Main western path; leads into labyrinth-like canyon
2. **Labyrinth of Ranpold:** Eastern maze; challenging navigation, rare mid-level monsters, and hidden treasure
3. **Eastport Piers:** Coastal port on the east; surrounded by mountains separating the region; docking point for Traveling Ships

**Monster Tier:** Lv. 30–40
**Ecosystem Notes:** Canyon-specific monsters; hidden pet spawns; suitable for desert-specialized pets; quest and treasure-focused area

