			Unholy Temples Mechanics
	
	NPC for Teleportation and Ticketing

	Name : "Gareth the Gatekeeper"

	Location : "Bayswick" (for Knights) or "Fortress of Eldergate" (for Pindars)

	Description : "Gareth the Gatekeeper" is a stoic, weathered figure who stands at the entrance of each respective teleportation point in the game, providing 
access to the treacherous "Unholy Temples". With a deep, commanding voice and a gaze that seems to pierce through the souls of those who approach, he exudes an aura
of both authority and mystery. A large, intricately carved staff rests in his hands, topped with a glowing orb that pulses with dark energy, as if attuned to the 
world beyond. Gareth wears a cloak of deep indigo with ancient symbols etched along the hem, and his attire hints at his long association with powerful, 
otherworldly forces.

	When approached, he speaks in a voice that echoes with a quiet urgency, offering the adventurers a chance to enter the "Unholy Temples" for a brief yet 
challenging experience. His solemn face never betrays emotion, but his words hold a weight that suggests he understands the dangers that await beyond. Gareth offers
each traveler a "Ticket of Passage", a mystical item that grants access to the Temples, but with strict time limits. 
	Despite his stoic appearance, a subtle sadness lingers in his eyes, as if he knows the price of tampering with the cursed realms and the consequences it can 
bring. Gareth’s role is crucial: he is both the gatekeeper and the one who ensures that those who enter can only do so within the rules, maintaining the balance of 
the dangerous realm he watches over.

