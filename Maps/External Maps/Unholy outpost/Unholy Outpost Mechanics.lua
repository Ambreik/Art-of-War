		Mechanics for Monsters in the Unholy Temples

	The "Unholy Temples" map is filled with a variety of monster types, each with unique and challenging mechanics that demand strategy and adaptability from 
players. Here’s an elaboration on those mechanics:

1. "Shadow Damage (Common Damage Type)"  
   All monsters in the "Unholy Temples" deal "Shadow Damage", a potent and eerie force that corrodes the very essence of the player’s health. This damage type is 
similar to what players experience in the "Underworld Realm", meaning that characters without specific "Shadow Resistance" may find themselves taking heavy damage 
over time. Players need to manage their resistances carefully, as Shadow Damage bypasses conventional defenses and drains health in subtle, relentless bursts.

2. "Piercing Damage (Ranged Monsters)"  
   Ranged monsters on the map, including both mages and archers, deal "Piercing Damage", which is particularly effective against players who lack "Armor". Piercing 
Damage can ignore certain defensive layers and can hit with accuracy over long distances. Mages in particular focus on magic-based piercing attacks, while archers 
utilize arrows or bolts with supernatural precision, making these ranged attackers dangerous for players who are not quick to react.

3. "Magic Damage (Mage Monsters)"  
   The mage monsters within the temples deal magic-based damage, which can overwhelm players who are unprepared. These attacks often come in bursts and are capable
of ignoring "Physical Defense", making them especially deadly against players with low "Magic Resistance". Given their power, players must stack 
"Magic Resistance" in order to absorb and mitigate these incoming magical assaults. "Magic Resistance" becomes a key stat for survival in this area, with players 
needing to adapt their equipment and abilities to reduce the harm from these enemies.

4. "High Attack Speed (Melee Monsters)"  
   "Melee monsters" in the "Unholy Temples" are quick, relentless fighters with high attack speed. While they lack the raw power to deal massive single-hit damage, 
they make up for it with rapid, continuous strikes that can overwhelm an unprepared adventurer. These monsters are relatively easier to kill in terms of their 
health pools, but players must be cautious not to get cornered, as their speed and attack frequency can chip away at a character’s health rapidly if not dealt with 
efficiently.

5. "Tank Monsters (Critical Damage)"  
   Some monsters in the "Unholy Temples" are designed as tanky foes, able to withstand significant amounts of damage. These monsters deal "Critical Damage", meaning
that if players are not careful or do not avoid their attacks, they can land devastating, high-damage hits. These creatures require a different approach, often 
necessitating players to find ways to break through their defenses, either by applying debuffs, using high burst damage abilities, or focusing on their weaknesses.

6. "Slowdown (Mage Monsters)"  
   Mage monsters in the "Unholy Temples" have the ability to "Slowdown" players, a status effect that reduces movement speed, attack speed, and casting speed. This 
mechanic is dangerous because it prevents players from escaping the battlefield quickly, or performing their abilities at their usual efficiency. "Slowdown" can 
make a fight feel drawn out, especially against fast-moving melee monsters or when trying to dodge the powerful attacks of ranged enemies.

7. "Stun (Archer Monsters)"  
   "Archer monsters" possess the ability to "Stun" their targets, rendering players unable to move or act for a brief period. This can lead to disastrous 
consequences, as players are left vulnerable to subsequent attacks from other monsters. A "Stun" can often mark the difference between a victory and a swift defeat,
requiring players to have access to **Stun Resistance** or the ability to avoid the incoming shots altogether.

8. "Tremble and Daze (Bosses)"  
   The bosses of the "Unholy Temples" are incredibly powerful entities that affect the environment and players in profound ways. Their "Tremble" effect causes the 
ground to shake violently, disrupting player control and causing damage to anyone within range. The "Daze" effect causes players to lose focus, impairing their 
ability to see clearly and react quickly to incoming attacks. These effects can turn the tide of a fight if not carefully avoided, forcing players to adapt their 
positioning and timing during these encounters.

9. "Restrain (Bosses, Rare Occurrence)"  
   Rarely, bosses in the "Unholy Temples" can trigger the "Restrain" effect, which immobilizes players for a short period. While restrained, players are unable to 
move, attack, or use abilities, leaving them completely vulnerable to further attacks from the boss or nearby enemies. "Restrain" is often used by the bosses as a 
last-ditch effort to ensure that players are caught in a vulnerable position during the fight, making it critical for players to be on high alert.

10. "Dual HP Bars (Boss Mechanics)"  
   Each boss in the "Unholy Temples" has "two HP Bars", which is a unique mechanic that significantly alters the flow of combat. As the boss loses health from the 
first bar, the fight shifts into a new phase, often with altered abilities and more aggressive behavior. This phase transition adds complexity to the fight, forcing
players to adapt their tactics on the fly and remain vigilant as the boss becomes more dangerous after the first HP bar is depleted.

11. "Berserk and Vampiric (Final Phase of Bosses)"  
   Once a boss reaches the final HP bar, it enters a "Berserk" state, increasing its damage output and aggression. In addition to this, the boss becomes "Vampiric",
meaning it begins to heal from the damage it inflicts on players. This makes the final phase of the fight a race against time, as players must dish out enough 
damage to overcome the boss’s healing, all while managing the significantly increased threat level.

---

	These mechanics make the "Unholy Temples" a challenging and dynamic environment where players must carefully plan their strategies, manage various resistances, 
and adapt on the fly in order to survive and succeed in their encounters. The combination of environmental hazards, powerful monster abilities, and the time-limited
farming opportunities creates a high-stakes atmosphere that is perfect for seasoned adventurers seeking to test their skills.