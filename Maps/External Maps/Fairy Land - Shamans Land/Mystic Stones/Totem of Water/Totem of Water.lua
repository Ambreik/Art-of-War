			Totem of Water
			
	This totem has the shape of a whirlpool made of water that spins continuously, surrounded by a blue smoke.
	This totem is located in second area in "Lake of Zarh" random spawn location.
	Once attacked, this totem spawns groups of aggressive monsters that attack the one who hits it.
	HP 15.000.000. tank type and vampiric.
	It always spawns at the fixed time, 1am, 2am and so on, whether it is destroyed or not.
	Have a 5% chance to spawn "Water Lord".


	Groups of spawn : 
*3x "Water Defender" & 4x "Water Enchanter".
*3x "Water Soldier" & 7x "Water Hunter".
*2x "Water Guardian" & 11x "Water Enchanter".
	those groups spawn random, every 5%HP lost.
	
