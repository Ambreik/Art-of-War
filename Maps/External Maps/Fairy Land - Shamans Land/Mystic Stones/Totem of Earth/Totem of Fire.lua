			Totem of Fire
			
	This totem has the shape of a round stone on fire, floating in the air, surrounded by a combined red and orange smoke.
	This totem is located in second area in "Flaming Peaks" random spawn location.
	Once attacked, this totem spawns groups of aggressive monsters that attack the one who hits it.
	HP 15.000.000. tank type and vampiric.
	It always spawns at the fixed time, 1am, 2am and so on, whether it is destroyed or not.
	Have a 5% chance to spawn "Fire Lord".


	Groups of spawn : 
*3x "Fire Defender" & 4x "Fire Enchanter".
*3x "Fire Soldier" & 7x "Fire Hunter".
*2x "Fire Guardian" & 11x "Fire Enchanter".
	those groups spawn random, every 5%HP lost.
	
