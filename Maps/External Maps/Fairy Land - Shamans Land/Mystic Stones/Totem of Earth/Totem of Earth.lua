			Totem of Earth
			
	This totem has the shape of a pyramid made of magma that float in air surrounded by a reddish smoke.
	This totem is located in second area in "Enchanted Plains" random spawn location.
	Once attacked, this totem spawns groups of aggressive monsters that attack the one who hits it.
	HP 15.000.000. tank type and vampiric.
	It always spawns at the fixed time, 1am, 2am and so on, whether it is destroyed or not.
	Have a 5% chance to spawn "Earth Lord".


	Groups of spawn : 
*3x "Earth Defender" & 4x "Earth Enchanter".
*3x "Earth Soldier" & 7x "Earth Hunter".
*2x "Earth Guardian" & 11x "Earth Enchanter".
	those groups spawn random, every 5%HP lost.
	
