			Totem of Wind
	
	This totem has the shape of a tornato made by wind that spins continuously surrounded by a silver smoke.
	This totem is located in first area in "Tangle Spring" random spawn location.
	Once attacked, this totem spawns groups of aggressive monsters that attack the one who hits it.
	HP 15.000.000. tank type and vampiric.
	It always spawns at the fixed time, 1am, 2am and so on, whether it is destroyed or not.
	Have a 5% chance to spawn "Wind Lord".

	Groups of spawn : 
*3x "Wind Defender" & 4x "Wind Enchanter".
*3x "Wind Soldier" & 7x "Wind Hunter".
*2x "Wind Guardian" & 11x "Wind Enchanter".
	those groups spawn random, every 5%HP lost.
	
