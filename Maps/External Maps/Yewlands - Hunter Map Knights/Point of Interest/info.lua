			Yewlands - Hunter’s Haven
	
		Overview :  
	"Yewlands" is an expansive and exclusive farm map designed for Hunter-class players. It’s a forested region that feels both enchanting and isolated, accessible 
only through teleportation or a special item called **Aziro's Ring**. Once players obtain this ring and complete the initiation quest from "Hunter Master Aziro", 
they unlock Yewlands as a training ground.

	In Yewlands, the atmosphere remains eternally bright, with no darkness, rain, or wind, creating a timeless and almost magical landscape. This map is perfect for
leveling up the Hunter profession and gathering rare resources while facing increasing challenges from wildlife and powerful factions like the Amazons.

---

		Main Areas

	1. "Village of Dirr"  
	Location : Southwestern corner, sheltered by high mountains with a single road for access.  
	Description : Dirr is the starting point in Yewlands, serving as a small, self-sustaining human settlement and a refuge for Hunters. It has a few homes, a main community building, and several strategic NPCs who assist Hunters in their training. Guards patrol the village entrance, both inside and outside, ensuring its safety from external threats. A small lake fed by the mountains provides fresh water, adding a sense of tranquility to this starting zone.

		Notable NPCs :
	- "Jhon – The Village Leader", handling the community’s welfare and introducing players to Yewlands.
	- "Hunter Master Aziro" – The primary quest-giver, who also provides the item “Aziro’s Ring,” enabling access to Yewlands after an initial mission.
	- "Army Captain Tan Vill" – Responsible for combat training and defensive tasks within the village.
	- "Harley the Innkeeper" – Manages the village inn, providing Hunters with rest, food, and supplies.
	- "Craftmaster Antonio" – A master of creating unique items and gear from resources found in Yewlands.
	- "The Leader of the Researchers" – Offers lore about Yewlands, diving into its hidden secrets and ancient history.
	- "Apprentice Alfred (Area 2)" & "Apprentice Vronta (Area 1)" – Each stationed in their respective forest areas to guide players in gathering rare resources.
	- Villagers "Banny" and "Joan" – Perform daily duties, giving the village an active and realistic feel.

---

	2. "Jargas Forest"  
	Location : Southern part of Yewlands.  
	Description : The Jargas Forest is an ancient, dense woodland area filled with towering trees and shadowed paths. Statues of giants and ancient ruins line the 
forest pathways, hinting at the region’s mysterious past. Wildlife abounds here, with wild animals lurking in the foliage. "River Spring" originates here, flowing 
through the forest and creating a natural division before it heads north into the other areas.

	Points of Interest :
	- Giant Statues and Ruins – Scattered throughout Jargas Forest, these relics speak to a forgotten age, giving Hunters points of interest to explore and 
potentially triggering quests.
	- "Apprentice Vronta’s Camp" – An NPC camp within Jargas Forest, where Apprentice Vronta helps Hunters identify rare plants and resources unique to this 
southern area.

---

	3. "Northern Woods"  
	Location : Central and northwestern parts, occupying the largest area on the map.  
	Description : Northern Woods is the heart of Yewlands and serves as a base for the "Amazon faction", who have built guarded camps and erected fortifications 
throughout the forest. These camps are surrounded by campfires, rock walls, and strategically placed tree trunks, forming a defense against intruders. Hunters need 
caution here, as they’ll face elite Amazon warriors defending their territory.

	Points of Interest :
	- "Amazon Camps" – Dotted across Northern Woods, these camps provide challenging encounters with "Amazon warriors" and opportunities for rare loot and 
experience.
	- "Apprentice Alfred’s Outpost" – Alfred offers guidance and quests related to the "Amazon faction" and survival tactics for Hunters venturing into the central 
woods.

---

	4. "Red Forest"  
	Location : Eastern and northeastern side.  
	Description : Known for its unique red foliage, the Red Forest is a hauntingly beautiful but dangerous place. It’s home to the elite "Amazon warriors", who have
built fortified structures among the trees. Log and stone houses, wooden fences, and connecting bridges give the area a complex layout. The northern path leads to 
the "Temple of Linar", a mysterious and sacred location locked for future content updates. The temple’s entrance is guarded by "Guard Son", a silent yet 
formidable protector.

	Points of Interest :
	- "Sacred Ruins" – Ancient stone blocks and ruins, remnants of collapsed structures, lead to the entrance of the Temple of Linar.
	- "Temple of Linar" – A dungeon accessible only in future updates for Hunters level 60 and above, requiring a group to explore its mysteries and potential 
rewards. Guard Son stands vigil at the entrance, awaiting those worthy of entry.

---

		Unique Environmental Features

- River Spring : Flowing from the mountains near Dirr Village, this river crosses Jargas Forest and travels north. Along its route, it divides Yewlands and 
eventually forms a lake in the Northern Woods. Tree-trunk bridges connect different areas, enabling traversal across the river and encouraging exploration.
- Surrounding Mountains : The towering mountains surrounding Yewlands isolate it from the main world, adding to the map’s mystique and creating a hidden paradise 
for Hunters.
- Perpetual Daylight : Yewlands remains perpetually bright and serene, with no nighttime or weather changes. This enchanted atmosphere sets it apart from other 
regions and underscores its magical, safe-haven quality.

---

		Purpose in Hunter Progression

	"Yewlands" is designed exclusively for Hunter players, who arrive at level 20 and train through the areas, encountering progressively tougher wildlife and 
"Amazon forces". The unique geography, wildlife, and rare resources cater to Hunters seeking specialized gear and materials for crafting. Moreover, the "Amazon faction" offers challenging encounters, giving Hunters the chance to earn faction-specific rewards. The area also holds the promise of the "Temple of Linar", a high
-level dungeon with rare rewards for advanced players.

