	Yewlands is a map made up of 3 distinct areas. Once here, the hunter begins his action in the "Village of Dirr", located in the left corner, surrounded by a high 
mountain with only one access road. The village consists of few houses and the main building. It is guarded by several NPC hunter guards who patrol the entrance inside
and outside it. Moreover, it also contains a small lake, the water in it springs from the mountain.
	From here, through the northern gate, you can see a vast forest, full of tall trees with a huge crown, in the "Jargas Forest". Here the forest has two areas 
surrounded by ruins and statues of giants that can be reached here by the main road coming from the village. The forest is full of wild animals that roam in search of 
prey. "River Spring" that springs from the mountain, right next to the village, runs its course through this area, going down to the north. It cuts this forest in two 
and descends towards the "Red Forest", where it can be reached through a wide bridge made of tree trunks. Moreover, this river goes from the center to the northwest of
the map through a branch and forms a lake here.
"Northern Woods" is the central area that also occupies the western part up to the north where it is separated from the "Red Forest" by a high hill. This forest is 
similar to the previous one, consisting of these tall trees. It is studded with Amazons who have built camps here and fortifications in the center of the map. These 
areas are otherwise protected from large campfires surrounded by rock walls or tree trunks. This area is the most extensive of the three. From here towards the west of
the map, going down towards the river, you cross a bridge with the same architecture as the previous one. Here you reach the "Red Forest", the place where the elite of
the Amazon army have a fortified and protected center throughout the place. Here you can find houses created with logs and stone, with wooden fences, between areas, 
bridges between small areas.
	From here, on the way to the north, you reach the entrance of the "Temple of Linar", surrounded by several stone blocks, ruins left from collapsed ancient 
buildings. In front of the entrance, there is "Guard Son", an NPC who watches over it using his long steel swords.