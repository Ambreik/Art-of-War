			Totem of Smoke
			
	"Totem of Smoke" - drop : Hunter's Banner
	
	This totem has the shape of a snake coiled around a pole, the snake is dark green, surrounded by a thick green smoke.
	This totem is located in the first area, and it spawns in the two plains in this forest.
	Once attacked, this totem spawns groups of aggressive animals that attack the one who hits it.
	HP 1.000.000. vampiric type.
	
	Groups of spawn : 
*3x "Giant Tree Lemur" & 2x "Hungry Gorilla"
*3x "Giant Tree Lemur" & 4x "Hungry Tiger"
*2x "Giant Tree Lemur" & 3x "Stonehead Frezen Gorilla"
	those groups spawn random, every 5%HP lost.
	
