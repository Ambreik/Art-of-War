			Imperial Statue of Amazon
			
	This totem has the shape of an amazon with two long swords crossed down.
	This totem is located in the third area, in the middle of "Saccred Ruins".
	Once attacked, this totem spawns groups of aggressive animals that attack the one who hits it.
	HP 15.000.000. tank type and vampiric.

	Groups of spawn : 
*7x "Blood Oath Crossbow Amazon" & 4x "Blood Oath Guard Amazon"
*3x "Battle Oath Longbow Amazon" & 6x "Battle Oath General Amazon"
*4x "Battle Oath Longbow Amazon" & 5x "Battle Oath Longsword Amazon",
	those groups spawn random, every 3%HP lost.
	
