			Totem of Endurance
		
	"Totem of Endurance" - drop : Hunter's Flaming Banner
	
	
	This totem has the shape of a shield with two swords crossed, surrounded by a thick brawn smoke.
	This totem is located in the first area, close to the bridge that lead to the "Northern Woods".
	Once attacked, this totem spawns groups of aggressive animals that attack the one who hits it.
	HP 4.000.000. tank type.

	Groups of spawn : 
*3x "Malefic Tree Lemur" & 3x "Giant Furry Gorilla"
*3x "Malefic Tree Lemur" & 6x "Wild Tapir"
*2x "Malefic Tree Lemur" & 4x "Stonehead Frezen Gorilla",
	those groups spawn random, every 5%HP lost.
	
	DROP : 
*Hunter's Flaming Banner - 5% drop rate.
