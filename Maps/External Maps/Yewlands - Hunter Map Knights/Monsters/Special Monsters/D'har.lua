			D'har 
	
	D'har it is a white tiger with thick fur and upper fangs protruding from its jaws, with which it tears prey.
	He doesn't have an increased movement or attack speed, but his attacks are impressive and the damage given is critical on a normal attack.
	Located in "Jargas Forest".
	
		Info : 
*Animal type
*Special monster type
*Aggro type
*Skillable monster
*High Damage Dealer
*Melee monster
*Affect with "Stun"

		Mechanics : 
*High HP Bar.
*Medium movement and attack speed.
*Critical strike damage.
*Solo group.
*Spawn same location every 30 minutes, in each 3 areas.

		Skills : 
*Sharp Claws : use his claws and deal multiple wounds to the target.
*Painfull Bite : jump forward and bite the target inflicting massive damage.
*Fast Snatch : jump at the target hitting hard affecting with "Stun" using his head and dealing damage.
	
