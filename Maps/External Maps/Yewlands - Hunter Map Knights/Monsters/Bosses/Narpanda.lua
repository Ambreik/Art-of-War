			Narpanda 
	
	Narpanda is a female monster, a wild amazon who wears a short robe over red pants with a bare back. He wears chain mail gloves and leather shoes. he carries a 
golden bow that is used both for close combat but prefers ranged attacks. Her blond hair is tied in a tail, which helps her to have a very high mobility and to 
defend herself faster against enemy attacks.
	Use skills with a wide range and AoE that can easily knock down the target if its defense is low. end
	Located in "Jargas Forest".

		Info : 
*Human monster
*Boss type
*Berserk type
*Skillable monster
*AoE skills
*Range monster
*Aggro monster
*Affect with "Slowdown"
*Affect with "Stun"
*Affect with "Knockback" using skills.

		Skills : 
	Close Range : 
*Bow Strike : strike the bow to its target dealing damage and affect with "Knockback" pushing backwards.
*Knee Blow : use the left knee and throw a charge of poison powder where she is jumping backwards avoiding the poison. Skill affect with "Poison" and also damage. 

	Long Range : 
*Precision Shot : launch a fast arrow that deal damage to the target and affect with "Knockback".
*Arrow Nocking : send an arrow to the target that explode and sent all over small pieces that deal massive damage.
*Quick Shot : send a fast arrow to the target dealing small damage but affect with "Slowdown".
*Stunning Arrow : send an arrow to the target dealing massive damage and affecting with "Stun".

