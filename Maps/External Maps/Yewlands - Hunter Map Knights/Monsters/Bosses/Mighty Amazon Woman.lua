			Mighty Amazon Woman

	Mighty Amazon Woman is the queen of the Amazons, a tall woman, beautiful in appearance, with long blond hair left loose. She wears a gold crown on her head and is 
dressed in a white robe with white gloves and silver boots on her feet.
	She wields a long golden sword with which she penetrates the target with every attack.
	Located in "Red Forest".

		Info : 
*Human monster
*Boss type
*Berserk type
*Skillable monster
*High Damage Dealer
*High Mobility
*Melee monster
*Affect with "Pull"
*Affect with "Daze"
*Affect with "Hold"

		Skills : 
*Puncture : use the long sword and point the target hard dealing damage and affecting with "Stun".
*Fast Forward : use her mobility to reach the target/s and deal a blow attack then a dast in the side exposed most and stab with the dagger affecting with "Hold"
	jumping at the back of the target and deal another blow attack that affect with "Hold".
*Bang Slash : delivers a quick evasive slash using her long sword, do a rotation in the air with it and land an explosive strike at the target. end 
*Dominance : jump and land her sword in the ground creating a massive impact area that pull her target/s to her.
*Side Strike : deal a great dash where she gather power and strike the target dealing damage and affecting with "Daze".
	(used often when target ride a horse or mount)
*Dancing Blade : she uses her sword skills and launches multiple very fast attacks targeting each enemy separately, affecting with "Stun" leaving behind a brown 
	trail that affects with "Slowdown" the targets that pass through it. If is only one target, create a shape of triangle that do the same. end

