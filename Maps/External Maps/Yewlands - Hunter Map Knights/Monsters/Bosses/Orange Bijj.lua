			Orange Bijj
			
			
	Orange Bijj it is a huge orange bear, which gives it its name. In its attacks, it can also stand on its hind legs and use its claws on its upper limbs to do great 
damage to its target. It has a symbol on its back in the shape of a leaf, which shows its element of nature. end
	It has a low movement speed, which makes the target escape easily, but despite this, it affects the target with "Slowdown" and "Stun". He has a very high HP Bar 
and his defense is impressive, which makes him a Tank type monster.
	He uses his skills to immobilize his target but sometimes also affects with "Knockback" when he is attacked by multiple targets. 
	Located in "Jargas Forest".
	
