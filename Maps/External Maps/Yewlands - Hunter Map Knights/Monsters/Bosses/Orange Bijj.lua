		Skills : 
*Fierce claws : jump forward and use his claws to deal massive damage to the target affecting with "Slowdown".
*Brutal Mauling : jump at the target and use his big claws to strike multiple wounds.
*Crippling Attack : jump at the target and use upper claws to hit with multiple strikes that affect with "Stun" and also damage.
*Giant Stomp : use his big leg to stomp the ground creating a huge AoE damage that "Knockback" the target/s and inflict damage.
	
