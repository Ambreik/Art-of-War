			Sharp Amazon

	Sharp Amazon is a general amazon, with quickness in attacks but also high mobility. She is dressed in thin chain mail armor that gives her mobility and her face is
white and beautiful with her hair braided in a bun.
	She uses short knives with which he attacks quickly.
	Located in "Northern Woods".
	
