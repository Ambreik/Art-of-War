			Curse Dagger Amazon
			
	Curse Dagger Amazon - Human type monster. High HP Bar. High movement and attack speed. Melee attacks. Boss type monster. Berserk type monster. Solo group. Affect
with "Poison" and "Stun". Special monster.
	
	Curse Dagger Amazon is a young amazon of small stature, with short hair and a white face and small blue eyes.
	She wears a long brown robe covering a white suit with a knife in a sheath.
	This Amazon is very agile and uses a long and thin sword that she carries in her left hand. Sometimes she plays with it and throws it from one hand to the other,
even during the fight. She seems very confident in battle and sometimes after each skill, she retreats with a dash and uses a gesture with her sword towards the 
target and laughs.
	Located in "Red Forest".
	
		Info : 
*Human monster
*Boss type
*Berserk type
*Skillable monster
*High Damage Dealer
*High Mobility
*Melee monster
*Affect with "Poison"
*Affect with "Stun"

		Skills : 
*Fast Cut : use her long sword and deal a few fast cuts to the target dealing pierce damage.
*Puncture : use the long sword and point the target hard dealing damage and affecting with "Stun".
*Fast Forward : use her mobility to reach the target/s and deal a blow attack then a dast in the side exposed most and stab with the dagger affecting with "Poison"
	jumping at the back of the target and deal another blow attack that affect with "Stun".
*Frontal Impact : do a backward move and fast move to the target landing a blow straight in the nose of the target affecting with "Stun". 
	(most used skill,to mock the enemy) end 
*Dancing Blade : she uses her sword skills and launches multiple very fast attacks targeting each enemy separately, affecting with "Stun" leaving behind a brown 
	trail that affects with "Slowdown" the targets that pass through it. If is only one target, create a shape of triangle that do the same. end

