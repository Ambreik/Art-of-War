			Mechanics 
			
	All Bosses in "Targas" have specific mechanics while in combat.
	
*when HP is bellow 30% enter in Rage Mode when all abilities increases with 15%.
*In Rage Mode all Bosses become Berserk and spawn every 5 seconds groups of monsters.
*REGEN HP increase with 4% per 10 seconds.
*Defence against weapons increase with 5% (meaning defence against sword/dagger/etc)
*Ussual most of the spawned monsters are range and their attacks are pierce attacks.