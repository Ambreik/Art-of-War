			Red-Eyes Jik
			
	Red-Eyes Jik is a troll leader similar to "Vicious Belly Cutter", tall, muscular and his face is similar. It is said that they are brothers but he has no hair 
and his eyes are big and red. He wears a red and torn blouse, with torn brown pants. Like his brother, he wields a large and heavy broadsword with which he crushes 
his enemies.	
	Located in ""Farbus Jungle""
	
	
		Info : 
*Human monster
*Boss type
*Berserk type
*Skillable monster
*High Damage Dealer
*High Mobility
*High HB Bar
*Melee monster
*Spawn 
*Affect with "Slowdown"
*Affect with "Daze"
*Affect with "Stun"

		Skills : 
*Troll Spirit : become anger and strike wider with all his hits affect with "Penetration Damage" and "Slowdown".
*Berserker's Rage : swaps from using AoE to melee attacks. Melee attacks have a chance to "Stun", preventing the target from moving or using mobility spells. 
Berserker's Rage also grants additional movement speed, armor, and decreases base attack time.
*Enrage : each continuous blow on the same target, he gains increased attack speed. If he changes targets, the stacks drop to zero. Affect with "Daze".
*Battle Trance : he enrage himself and start banging ground with his broadsword dealing massive damage. (used when is encountered by more enemies).
*Battle Fury : use his broadsword and make a dance in a circular motion with which he push and crushes his enemies, and if during this time the enemies hit an 
	obstacle, the damage is increased.end 

