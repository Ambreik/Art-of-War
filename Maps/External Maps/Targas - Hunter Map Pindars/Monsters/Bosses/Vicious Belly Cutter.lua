			Vicious Belly Cutter
			
	Vicious Belly Cutter is a troll chieftainship. A tall monster with a tuft of hair caught in a short tail and very large rear fangs. He is muscular and wears only
pants. His body is full of scars but his skin is painted with white paint. He carries a large broadsword in his hand that he uses to knock down his enemies.
	Located in ""Farbus Jungle""
	
		Info : 
*Human monster
*Boss type
*Berserk type
*Skillable monster
*High Damage Dealer
*High Mobility
*High HB Bar
*Melee monster
*Spawn 
*Affect with "Bleed"
*Affect with "Taunt"
*Affect with "Daze"
*Affect with "Hold"

		Skills : 
*Troll Spirit : become anger and strike wider with all his hits affect with "Penetration Damage".
*Berserker's Rage : swaps from using AoE to melee attacks. Melee attacks have a chance to "Taunt", preventing the target from moving or using mobility spells. 
Berserker's Rage also grants additional movement speed, armor, and decreases base attack time.
*Whirling Axes (Ranged) : hurls a broadsword of five strikes in a cone shape, affecting with "Hold" and damaging enemy.
*Whirling Axes (Melee) : hurls broadsword around him in a close range area of effect, damaging enemy and causing them to miss some attacks and affect with "Bleed".
*Enrage : each continuous blow on the same target, he gains increased attack speed. If he changes targets, the stacks drop to zero. Affect with "Daze".
*Battle Trance : he enrage himself and start banging ground with his broadsword dealing massive damage. (used when is encountered by more enemies).

