			Streek Grand Shaman Trollus
			
	Streek Grand Shaman Trollus is a tall and thin troll, green in color with yellow spots. He has a long nose and big fangs. He wears a dark blue robe with lighter 
blue stripes on it. On his shoulders he has long epaulettes with thorns protruding upwards.
	He wields a wooden scepter with a ruby on its head and uses powerful magical attacks.
	
	Located in "Crescent Mountain"
	
