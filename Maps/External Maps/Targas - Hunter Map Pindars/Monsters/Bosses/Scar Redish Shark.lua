			Scar Redish Shark
			
	Scar Redish Shark is a humanoid shark that walks on two legs and carries a huge spear with two points in its head. His body is full of scars and his tail is split.
He wears a leather armor and pieces of metal that cover his body up to his hips and at his shins there are iron plates that protect him.
	Located in "Crystal Beach".

		Info : 
*Animal monster
*Boss type monster
*Tank type monster
*Skillable monster
*Spawner monster
*Aggro monster
*Melee monster
*AoE skills
*Affect with "Slowdown"
*Affect with "Knockback"
*Affect with "Stun"

		Skills : 
*Fierce Bite of Shark : push himself with his legs at the target and deal a big bite dealing damage.
*Big Stump : dash at the tarhet and once is close enough, use his right leg to strike the ground dealing massive damage in an AoE range.
*Rampage Shark : Become harder in strikes and push with his hits from trident (used when is encountered by more enemies).
*Trident Potent : use his trident and strike the target three times in a row dealing massive damage.

