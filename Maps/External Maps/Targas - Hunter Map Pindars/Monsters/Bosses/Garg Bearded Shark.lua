			Garg Bearded Shark
			
	Garg Bearded Shark it is a huge bearded shark, walking on two legs, with a pointed head and eyes positioned on the side and fangs coming out of its mouth. He 
wears a chain link tunic that covers a white garment on the inside and brown pants torn at the knees.He does not wear shoes and his soles are as wide as a duck legs.
	Is a magical monster that use magic projections from range but also can use his teeth to bite hard.
	Located in "Crystal Beach".
	
