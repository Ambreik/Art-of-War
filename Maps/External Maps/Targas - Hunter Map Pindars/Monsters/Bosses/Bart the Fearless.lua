			Bart the Fearless
			
	Bart the Fearless is a huge troll with scars on his face and very large rear fangs. Big red eyes and two earrings in the left ear.
	He is dressed in a brown leather armor that covers only the left side of his chest and legs. He has many scars on his body and wields a long sword.
	
		Located in "Farbus Jungle".
	
		Info : 
*Human monster
*Boss type
*Berserk type
*Skillable monster
*High Damage Dealer
*High Mobility
*High HB Bar
*Melee monster
*Affect with "Bleed"
*Affect with "Hold"

		Skills : 
*Troll Spirit : become anger and strike wider with all his hits affect with "Penetration Damage".
*Enrage : each continuous blow on the same target, he gains increased attack speed. If he changes targets, the stacks drop to zero. Affect with "Bleed".
*Troll Spirit : dash into enemy (enemies) pushing affecting with "Hold" and start cutting with a few strikes affecting with "Bleed".
*Battle Trance : he enrage himself and start banging ground with his broadsword dealing massive damage. (used when is encountered by more enemies).
*Battle Fury : use his sword and make a dance in a circular motion with which he push and crushes his enemies, and if during this time the enemies hit an 
	obstacle, the damage is increased.end 

