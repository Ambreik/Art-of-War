			Streek Grand Chief Trollus
			
	Streek Grand Chief Trollus he is a huge troll with reddish skin and huge fangs.
	He is muscular, and full of scars on his chest and back. He only wears pants of a reddish orange color and
	He carries in his left hand a long sword with an arched shape.
	
	Located in "Crescent Mountain"
	
		Info : 
*Human monster
