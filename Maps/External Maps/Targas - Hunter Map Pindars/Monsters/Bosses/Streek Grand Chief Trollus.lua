*High Mobility
*High HB Bar
*Spawn
*Melee monster
*Affect with "Pull"
*Affect with "Daze"
*Affect with "Hold"

		Skills : 
*Troll Spirit : become anger and strike wider with all his hits affect with "Penetration Damage".
*Enrage : each continuous blow on the same target, he gains increased attack speed. If he changes targets, the stacks drop to zero. Affect with "Daze".
*Troll Spirit : dash into enemy (enemies) pushing affecting with "Hold" and start cutting with a few strikes affecting with "Hold".
*Battle Trance : he enrage himself and start banging ground with his broadsword dealing massive damage. (used when is encountered by more enemies).
*Battle Fury : use his sword and make a dance in a circular motion with which he push and crushes his enemies, and if during this time the enemies hit an 
	obstacle, the damage is increased.end 
*Roar of Streek : roar at the target/s and then jump into them dealing AoE damage and affect with "Pull".
