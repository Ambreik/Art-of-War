			Little Tunny
	
	Little Tunny it is a fish that walks on two legs, dressed in a chain dress and duck feet. He carries in his left hand a trident of his size. He has big, bulbous 
eyes and a big mouth.
		
	Info : 
*Animal type
*Special monster type
*Aggro type
*Skillable monster
*High Damage Dealer
*Melee monster
*Affect with "Stun"

		Mechanics : 
