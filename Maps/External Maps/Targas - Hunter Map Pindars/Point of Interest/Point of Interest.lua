			Point of Interest
		
		
	"Village of Gus" - human settlement. located in south-west side of the map.
	"Crystal Beach" - area 1. south and east side of the map.
	"Farbus Jungle" - area 2. central and west side of the map. biggest area.
	"Crescent Mountain" - area 3. northern side of the map. smallest area.
		"Crescent Peak" - lead to the entrance to the "Zen Castle".
		"Zen Castle" - available in next patch, from level 60 with a group.
	"Red River" - river that cross the entire map, starting from "Crescent Mountain" and goes to south and flows into the sea.
	
	Whole map is surrounded by an endless sea.
	
	Targas is an external map, which means that it is not located on the "World Map", but you get here using a teleporter or a special item. This area is accessible 
to pindars, from level 20, only to those who have chosen the first job, to be "Hunter".	
	Targas is an island surrounded by the sea, which has the shape of a shell with the opening towards the east, where there is a vast beach that is bordered by the 
sea, which forms a cut in it.
	This island consists of three distinct areas, each separated by the "Red River" on its way to the beach. These areas are separated by the course of the river, but 
the passage is made through bridges made of planes woven with vines.
	Its shape tends to form a huge plain in the center crossed by the "Red River" that springs from the base of the "Crescent Mountain" and flows right into the water 
tongue of the sea that enters "Crystal Beach" on the south-eastern side of the island. This central plain is surrounded by a forest of trees with tall trunks and their
crowns create a shadow in the path. In this plain, the only road coming from the village forms a winding path along it and leads to the peaks of "Crescent Mountain". 	All over the plain are camps made of wood with thatched roofs, where trolls have this sacred ground as their place of residence.
	At the base of the mountain, the road bends and is made of larger stones with high edges about half a meter, you reach the top of the mountain, right up to the 
entrance to "Zen Castle" where "Rypere the Guard" guards the sealed doors.