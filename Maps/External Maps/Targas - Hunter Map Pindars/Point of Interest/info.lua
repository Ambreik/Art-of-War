			Targas – Hunter's Territory of the Pindars

	Targas is an expansive, secluded map not part of the World Map, accessible only to hunters of the Pindar faction who have selected “Hunter” as their primary 
profession. As a farming map, Targas offers hunters a unique landscape filled with challenges, rare monsters, and the chance to gather fragments for the highly 
coveted “Runes” item class. To access Targas, players must complete a primary quest and acquire the "Emblem of a Hunter" from the NPC "Han Herik". Once obtained, 
hunters can teleport to Targas simply by activating the emblem.

		Atmosphere and Environment	

	The atmosphere in Targas is distinctively darker than that of Yewlands. The skies shift between dusky, deep shadows over the ocean’s edge and vibrant, warm 
light that bathes the jungle plains. The weather remains calm and unchanging—no storms, rain, or wind disturbs the quiet intensity of the jungle. These conditions 
allow hunters to roam freely, without disruptions, as they seek rare items and elusive prey.

		Map Layout and Key Areas

	Targas features three primary areas and a settlement village, all interconnected but diverse in terrain and challenges:

	1. "Village of Gus"  
	Located on the southwestern edge of Targas, the Village of Gus is the hub for Pindar hunters, a safe zone protected by hunter guards who watch the village and 
its main entrance. The village has only a few structures, including the central building, and a small freshwater spring fed by a nearby mountain. The villagers here
offer various services to hunters, from food and lodging to special crafting. Key NPCs include:
	- "Pampi", the village leader, who provides guidance and general support.
	- "Han Herik" , the Hunter Master, who offers quests and guidance for rune crafting.
	- "Fist Bampi" , the Army Captain, who organizes patrols and leads the defense.
	- "Lesley" , the innkeeper, offers rest and provisions for weary hunters.
	- Other villagers, like the "Leader of the Researchers" , provide background information and clues about the land.

	2. "Crystal Beach"  
   The first major area on Targas’s southern and eastern coast, Crystal Beach is known for its pristine sands and the ocean’s turquoise waters. Though beautiful, 
the beach is populated with lower-tier monsters that serve as valuable targets for new hunters gathering initial materials for their "Rune crafting". In Crystal 
Beach, hunters may also encounter the Gnols—an indigenous tribal group with deep connections to the land. 

	3. "Farbus Jungle"  
	As the largest section of Targas, Farbus Jungle occupies the central and western parts of the map, comprising dense and humid jungle paths. Giant trees, with 
massive canopies, shadow most of the area, creating a labyrinthine environment. This area is teeming with wildlife, but it is the Gnols who truly dominate Farbus 
Jungle. Organized into clans, the Gnols fiercely protect their territory, often ambushing unwary hunters. The jungle's humid atmosphere and difficult terrain 
increase the challenge of tracking prey and extracting valuable materials for crafting.

	4. "Crescent Mountain"  
	The smallest area, located on the northern side of the map, Crescent Mountain is a rocky, towering region that includes *Crescent Peak*, a steep cliff that 
serves as the entrance to the fabled *Zen Castle*. Only accessible in future updates and for high-level hunters, Zen Castle promises greater dangers and powerful 
rewards. For now, the Crescent Mountain area itself provides hunters with challenging bosses and elite monsters, demanding skillful play and offering rare item 
fragments.

	5. "Red River"  
	Flowing down from Crescent Mountain, the Red River snakes through Targas, cutting across Farbus Jungle before reaching the southern coastline and flowing into #
the ocean. The river offers hunters natural access routes and serves as a landmark, though it also attracts hostile Gnol scouts, who use it to navigate their 
territory.

		The Gnols: Indigenous Tribe of Targas**

	The Gnols are a tribal, beast-like people native to Targas, living in hidden settlements and fortified camps across the region. Known for their savagery and 
territorial instincts, they wield primitive but effective weapons, often employing ambush tactics against intruders. With deep connections to the natural world, the
Gnols possess a unique knowledge of the jungle, which allows them to avoid traps and sneak up on even experienced hunters.

	Gnols are skilled in tracking, evasion, and camouflage, making them formidable opponents for hunters. For those willing to brave the danger, however, the Gnols 
carry rare materials and items essential for crafting Runes. This makes hunting Gnols particularly attractive for Pindar hunters, though it requires cunning and 
resilience. 

	Purpose and Mechanics**

	Targas serves as a high-risk, high-reward environment for Pindar hunters to gather materials and engage with the unique Rune crafting system. Each area of 
Targas holds stronger monsters and progressively challenging Gnol camps, where hunters can farm rare fragments, complete specialized quests, and face difficult 
bosses.

	In Targas, hunters are continually tested on their tracking, survival, and combat skills, making it a fitting counterpart to Yewlands.