			Majestic Statue of Trollus
			
	This totem has the shape of a troll with a long staff hold it in front of it.
	This totem is located in the third area, on the top of "Crescent Mountain".
	Once attacked, this totem spawns groups of aggressive animals that attack the one who hits it.
	HP 15.000.000. tank type and vampiric.

	Groups of spawn : 
*7x "Streek Shaman Troll" & 4x "Hunter Troll"
*3x "Streek Watcher Troll" & 6x "Streek Seeker Troll"
*4x "Streek Watcher Troll" & 5x "Streek Seeker Troll",
	those groups spawn random, every 3%HP lost.
	
