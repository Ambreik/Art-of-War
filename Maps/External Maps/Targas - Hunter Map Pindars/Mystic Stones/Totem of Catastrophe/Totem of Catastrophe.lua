	Totem of Catastrophe - drop : 
	
	This totem has the shape of a stone with a trunk in it placed as was fallen into it, surrounded by a thick brawn smoke.
	This totem is located in the first area, on the road that lead to the "Farbus Jungle" just before to enter here with the place of spawn that look burnt.
	Once attacked, this totem spawns groups of aggressive animals that attack the one who hits it.
	HP 4.000.000. tank type.

	Groups of spawn : 
*3x "Snail Hunter" & 3x "Tarpon Warrior"
*3x "Snail Hunter" & 6x "Tarpon Warrior"
*2x "Scavenger Sailfish" & 4x "Seaweed Walker",
	those groups spawn random, every 5%HP lost.
	
