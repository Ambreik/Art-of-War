			Oasis Point: The Knight's Trial for Guardian Companions

	"Oasis Point" is a remote, challenging area situated within a narrow corridor between two towering, rugged hills, forming a natural barrier that isolates it 
from the rest of the land. This hidden domain is accessible only to knights, who come here to either test their strength against a fierce dragon or to acquire rare 
materials needed to equip their summoned Guardian companions. These Guardians, powerful protectors brought forth by the "Seal of Protection", act as loyal 
companions to knights and can be outfitted and leveled up through items gained from the trials in this area.

	To enter Oasis Point, knights must seek out "Lenard the Brave" in "Forest Albina",a seasoned warrior wandering the woods who grants access to this hidden arena.
Only knights possessing the strength and courage to undergo the trials are allowed entry, emphasizing Oasis Point as a place of skill and valor.

		Area Layout and Atmosphere

	Upon teleporting to Oasis Point, knights find themselves in front of a heavy, locked gate, which seals off the entryway to ensure no one exits before completing
their chosen trial. The area is shaped like a triangle, with three stone circles at each of the triangle’s corners, each pulsating with a faint, mysterious glow. In
the center stands the "Guardian Summoner", a solemn NPC who oversees the trials and offers two distinct challenges that each knight must choose between.

	This desolate realm is bathed in an eerie, perpetual twilight, with a strange ambient glow that casts the rocks in shifting shadows. The atmosphere here is 
still and heavy, with no rain, no wind, and no passage of day into night, creating an almost timeless, otherworldly experience. The stark stillness of Oasis Point 
reflects the seriousness of the trials, drawing on ancient powers that resonate with the stones marking each corner.

		Trials Offered by the Guardian Summoner

	When speaking to the "Guardian Summoner", knights are given two options:

	1. "Summoning the Dragon"
	- Choosing this path leads to an intense solo battle, as the Guardian Summoner disappears and, after a brief delay, a formidable dragon emerges in the center of
the clearing. The knight has five minutes to defeat this beast in a trial that tests both skill and endurance.
	- Upon defeating the dragon, the knight has a chance to acquire a **Seal of Protection**, though this reward is not guaranteed on the first try. The Seal of 
Protection can appear in one of 4 colors, each corresponding to a different Guardian type:
		- "Yellow Seal" : The rarest, symbolizing the "Shaman Guardian".
		- "Green Seal" : Representing the "Archer Guardian", the third-rarest.
		- "Orange Seal" : The "Defender Guardian", a moderately common seal.
		- "Red Seal" : The "Berserker Guardian", the most commonly obtained.
	- Each Seal of Protection is highly valued, as it enables the knight to summon a Guardian—a personal protective companion who aids in battle and grows stronger 
with each level. Collecting different seals allows knights to unlock various Guardian types, adding flexibility and strategy to their journey.

	2. "Pillars of Secrets"
	- This option spawns three statues within the stone circles located at each corner of the triangle. Known as the Pillars of Secrets, these statues are imbued 	
with magical properties and, when destroyed, release various rare resources and crafting components.
	- Knights have fifteen minutes to destroy as many of these statues as they can, with each pillar offering valuable materials essential for Guardian equipment 
and development. The items gathered from the pillars include equipment items and rare crafting pieces, though their exact nature remains a secret until they are 
looted. Once the time is up, the knight is transported back to the starting area, where they may return to Lenard the Brave to try again another day.

		Purpose of Oasis Point

	"Oasis Point" is a domain where knights can gather rare resources to empower their summoned Guardians, with the ultimate goal of obtaining Seals of Protection 
or vital crafting materials. This small yet crucial area offers players a balance between combat and resource collection, providing materials that are otherwise 
difficult to obtain. It is a realm that demands skill, patience, and determination, rewarding those who can overcome its challenges with resources critical to their
progression.

	Through its trials and unique atmosphere, Oasis Point serves as a vital location for knights looking to strengthen their Guardians, enabling them to create a 
powerful ally who stands by them in every future battle.