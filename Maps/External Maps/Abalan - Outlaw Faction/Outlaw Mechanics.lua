			Mechanics
			
		Interaction Mechanics with Outlaw NPCs :

1. Stealthy Approach : 
	- The NPCs will only appear under specific conditions—when the player is alone, away from high streets, important buildings, or visible guards. Each NPC subtly 
watches for players who seem like potential recruits, and once the coast is clear, they make their move.
	- With their faces hidden in the depths of their hoods, they open a quiet, almost whisper-like dialogue window that appears directly on the player's screen, 
adding a sense of immediacy to the interaction.

2. Initial Conversation :
	- The NPC quietly asks the player, "Do you wish to learn of mystical powers? Of hidden secrets that lie beyond the laws of nations?" Players can either respond 
positively, showing intrigue, or decline.
	- Acceptance : If players accept, the NPC speaks further, subtly introducing the concept of the Outlaw faction, a hidden group that has “access to powers 
unbound by the rigid laws of your homeland.” The NPC hints at techniques such as poison crafting, trap building, and the deeper knowledge that the Outlaws possess. 
They hand the player an item, the "Seal of the Outlaw".

3. Receiving the Seal of the Outlaw :
	- This seal serves as a one-time-use teleport item that allows the player to access a hidden portal, randomly located in Alaban. Upon activation, the portal 
takes the player directly to a secret Outlaw base within the heart of Alaban, where they can meet with the NPC who originally recruited them.

4. Arrival in Alaban – The Outlaw Induction :
	- Once the player arrives through the portal, they find themselves in a dimly lit, heavily wooded area with the distant sound of cascading rivers echoing 
through the trees. Here, the original Outlaw NPC is waiting, blending into the shadows. 
	- The NPC provides further orientation, explaining Alaban’s history and the Outlaw way of life. They guide the player through the surrounding terrain, 
describing the location's dangers, hidden resources, and the factions within Alaban that have grown around Aranil’s ideals.

5. Options for Loyalty :
	- Joining the Outlaws : If the player decides to proceed, they officially join the Outlaw faction, gaining access to specific abilities, crafting techniques, 
and a whole suite of clandestine missions. They also receive an Outlaw grade item, allowing them to disguise their allegiance when traveling outside Alaban.
	- Refusal and Guard Alert : Players who wish to remain loyal can refuse or even “Shout for a Guard” during the initial conversation. If they choose to shout, a 
nearby guard arrives, startling the Outlaw NPC, who quickly disappears into the shadows, leaving the player with no Outlaw affiliation. The guard acknowledges the 
player’s loyalty, potentially with a minor reputation boost.

