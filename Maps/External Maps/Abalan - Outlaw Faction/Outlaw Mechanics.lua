	1. Bounty Hunter Profession  
	This profession is designed specifically to hunt down and capture outlaw players who have been detected or declared criminals within the world. The Bounty Hunter 
would have unique skills, quests, and progression related to tracking down and capturing Outlaws.

- "Bounty Hunter Role" :  
	- Bounty Hunters are tasked with tracking and capturing Outlaws for "rewards". The longer an Outlaw evades capture, the greater the "bounty" placed on them. This 
makes the hunt increasingly lucrative over time.
	- As a "Bounty Hunter", players can "accept bounties" placed on Outlaws. This is tracked in a special "Bounty Board" found in the major cities' Guard Posts or 
Taverns.
	- Tracking Mechanism : Bounty Hunters have access to tracking abilities, special maps, and even magical tracking items to narrow down the location of their 
targets. The tracking efficiency improves as they gain experience, making it easier to locate high-ranked Outlaws.
	- Bounty Rewards : Each successful capture brings in gold, rare items, or fame in the "Bounty Hunter" faction. The bounty amount increases every real-time day 
the Outlaw remains uncaught, creating a sense of urgency.
	- PvP Involvement : Bounty Hunters can choose to either capture Outlaws alive or eliminate them. Capturing an Outlaw alive may yield higher rewards but carries 
more risk (i.e., the Outlaw can escape or retaliate).

---

	2. Capture and Punishment for Outlaws  
	When an Outlaw is caught by the guards, they face significant consequences that reflect their criminal behavior.

	Capture Process :
   - Once an Outlaw is detected or reported (through their actions), a "Bounty" is placed on their head. Guards, NPCs, and "Bounty Hunters" will actively track the 
Outlaw.
   - Upon being caught, the Outlaw is transported to a prison beneath one of the big cities. These prisons are notorious, secure, and only accessible by high-ranking
  guards or "Bounty Hunters". Guards escort the Outlaw to the prison, where they face their punishment.

---

	Punishment Mechanics :
	Upon capture, the Outlaw faces the following consequences:

1. "Loss of Experience" :
	- EXP Reset : The Outlaw "EXP" is reduced to "0", meaning the player loses all progress gained through questing, defeating enemies, or completing objectives. 
They will need to start from scratch, creating a major setback in progression.
   
2. Gold Confiscation :
	- 30% of Gold : The guards confiscate "30" of the Outlaw’s entire gold in their inventory, which is a significant financial loss. The player is still allowed to 
keep 70%, but this loss serves as both a practical and strategic penalty.

3. Equipment Loss :
	- One Random Piece of Equipment : A random item from the Outlaw's equipment set (armor, weapon, etc.) is seized by the guards. This item is either locked away in 
the prison or sold as a confiscated good in the city's black market. For this to happen, the Outlaw must have a high rating.
	- 2-5 Items from Inventory : The guards also take 2-5 random items from the Outlaw’s inventory. These could be consumables, crafting materials, rare items, or 
quest items. The randomness adds a layer of unpredictability to the punishment. For this to happen, the Outlaw must have a high rating.

4. Identity Exposure :
	- Item that Hides Rank : If the Outlaw possesses an item that hides their rank or keeps their criminal activities concealed (like a special mask, cloak, or 
artifact), this item is also taken by the guards, revealing their identity as an Outlaw.

5. Seal of the Outlaw :
	- Rare Seal Confiscation : There is a small chance that the "Seal of the Outlaw", an item that marks them as a criminal, can also be taken by the guards. Losing 
the seal could make it harder for the Outlaw to track their own reputation and progress as an outlaw. Without it, their criminal status may not be as easily 
recognized until they re-establish it.

---

	3. "Prison System"  
	All the major cities in the "World Game" have prisons beneath them, hidden in the depths of the urban areas. These prisons serve as the primary location where 
captured Outlaws are detained.

- Prison Design : 
	- Each city prison has its own design, ranging from basic stone dungeons to elaborate underground facilities, with guards stationed at every entrance and exit.
	- Some prisons have layers of security, with higher-level prisons for repeat offenders or Outlaws who have committed severe crimes.
   
- Prison Time :
	- Outlaws must serve a set amount of time (based on their crimes) in prison. This time varies but can range from a few hours to days depending on the severity of 
their actions.
	- The player cannot interact with other NPCs or perform most actions while incarcerated, except for a few limited options like chatting with fellow prisoners or 
accepting "escape quests".

- Escape Attempts :
	- Outlaws may attempt to escape from the prison through stealth, bribing guards, or special skills (e.g., lockpicking or illusion magic). There are escape 
mini-games that give players the chance to break free.
	- Escaping successfully allows the Outlaw to avoid further punishment but may come with consequences like higher bounties or more aggressive pursuit by 
"Bounty Hunters".

- Escape Quests :
	- Redemption Quests : Some Outlaws may undertake "redemption quests" while imprisoned, offering to help the guards with special tasks in exchange for reduced 
sentences or clearing their records.
	- Bounty Hunter Challenges : Some players can join forces with a Bounty Hunter to escape together for mutual benefit, creating alliances or even potential 
betrayals between players.

---

	4. "Bounty System and Dynamic Gameplay"  
	As a result of the Bounty Hunter profession, the world will feel much more alive with dynamic and interactive systems.

- Bounty Growth :  
	- As time passes, the "bounty" on an Outlaw’s head increases daily until they are caught. This encourages Bounty Hunters and other players to actively 
participate in the pursuit, knowing that the reward becomes greater the longer the Outlaw evades capture.
	- Outlaws can attempt to reduce their bounty by completing special quests or missions to clear their name, but these are not always successful.

- PvP Dynamics :
	- With the existence of "Bounty Hunters", Outlaws will have to carefully consider their actions in cities and on the open map. They could be targeted by players 
for bounties, making them much more vulnerable in highly populated areas.

---

		Summary  
	This mechanic creates a multi-layered system of consequences for Outlaws, rewarding Bounty Hunters and creating meaningful tension in the world. It promotes 
dynamic PvP interaction, resource management, and the use of in-game alliances and rivalries. Outlaws will need to be strategic in evading capture or risk facing 
harsh punishments, including the loss of progress, valuable items, and reputation. Meanwhile, Bounty Hunters will be able to carve out their niche in the game world, 
hunting down criminals for fame and fortune.