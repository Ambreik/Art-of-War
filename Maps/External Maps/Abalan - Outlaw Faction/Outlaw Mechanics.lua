		Outlaw Recruitment and Player Encounter Mechanics

	In the secretive recruitment system of the Outlaws, players have limited chances to join this hidden faction, with specific NPCs approaching them in secrecy and
testing their resolve and intellect. Here’s how the intricate encounter and recruitment process unfolds:

1. The Secretive Approach and Riddle Challenge :
	- The Outlaw NPCs will seek out players only under specific conditions, such as when the player is alone, away from high streets or important NPCs, and out of 
sight from any guards. If conditions are met, a cloaked figure will step forward, opening a discreet interaction window with the player.
	- Limited Attempts : Each NPC has only three chances to approach the player, who must be below level 95. If all three encounters pass without the player 
choosing to join, the opportunity to join the Outlaws vanishes until a much later level.
	- Opening Dialogue : The NPC whispers, "Do you seek the power that lies beyond the laws? Knowledge forbidden and strength hidden? Answer my question correctly, 
and the path may be yours. Fail, and this chance fades like smoke in the wind."

2. The Riddle of the Outlaws :
	- To receive the "Seal of the Outlaw", the player must answer a riddle, symbolizing the first test of becoming an Outlaw. Here’s an example of the riddle posed:

		> "I am both feared and desired, my presence lies beneath all that the eye can see. Though hidden, I am felt by all, and to uncover me is to betray your roots. What am I?"

	- Answer Choices (the correct answer being intentionally unclear):
		- A) Power  
		- B) Freedom  
		- C) Truth (correct answer)  
		- D) Death  

	- Players must think deeply; though “Power” and “Freedom” may seem tempting, “Truth” is the answer that aligns with the Outlaws’ mission of uncovering hidden 
realities. Answering incorrectly will cause the NPC to fade away with a parting, cryptic phrase: “Not all can walk in the shadow of truth.”

3. Receiving the Seal of the Outlaw :
	- Upon answering correctly, the player is given the "Seal of the Outlaw", an item granting a one-time teleport to a hidden portal in Alaban. Here, they can 
meet the NPC again and begin their initiation.

4. Outlaw Mechanic – The Danger of Discovery :
	- Disguise Mechanism : Outlaw players use an item called “Outlaw Servant,” which masks their status. If this item fails or is inspected closely by a Guardian 
NPC, the title above their heads appears in "RED" as “OUTLAW Servant,” alerting nearby guards and players.
	- NPC Aid and Escape Skills : If exposed, the nearest Outlaw NPC will step in to help the player escape, using area-of-effect (AoE) and pushback skills to hold 
off attackers. In extreme cases, such as an overwhelming 1vX situation, the NPC will initiate an emergency teleport, instantly transporting the player back to 
Alaban’s safe zone.
	- NPC Disappearance : If the NPC assisting the player is also discovered and their disguise broken, they will disappear into the shadows, avoiding detection for
a period before reappearing.

5. Return to Alaban – The Final Phase of Recruitment :
	- Once safely teleported to Alaban, the NPC greets the player and commends their survival skills. They provide further instructions, guiding the player through 
the Outlaw hierarchy, resources, and training areas.

