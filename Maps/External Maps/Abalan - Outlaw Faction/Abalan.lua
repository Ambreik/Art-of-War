			Abalan
			
	Alaban, hidden within the protective embrace of towering mountains, is not just a mystical land of beauty and forgotten ruins—it is also the dark refuge of the 
"Outlaws". Known by few as "The Forsaken Land," Alaban has become a sanctuary for traitors, misfits, and rogues from the main continents, a realm where they can 
hone their forbidden skills and gain powers unknown to those loyal to the outside world.

		The Hidden Refuge of Outlaws

	Once players reach level 25, they may encounter mysterious NPCs in each of the primary continent factions. These NPCs appear inconspicuous, blending into the 
crowd, but they harbor a secret: they are recruiters for Alaban. They offer players a tantalizing choice—to abandon their original faction, join the ranks of the 
Outlaws, and gain access to hidden abilities, unique buffs, and new methods of combat. The price? Loyalty to their former allies and the designation as enemies in 
their former lands.

	Upon joining Alaban’s faction, players receive a second faction alignment, "Outlaw", marking them as adversaries to their initial faction. They adopt new ways 
of combat, strategies tailored to the hostile environment, and a distinctive skill set that reflects the land’s ruthless edge. "Outlaw players" operate under a code
of secrecy, needing to hide their status when venturing back to the outside world. To accomplish this, they must obtain a critical item called "Grade Concealment Badge", available from Alaban's "General Vendor". This badge allows them to blend in with their former allies without revealing their Outlaw rank.

		The Trials of Alaban

	In Alaban, strength is paramount. The land is brimming with formidable creatures and intense challenges that rival any seen outside its borders. Monsters roam 
freely, their powers heightened by the unique energies of the land, creating a relentless proving ground for those who dare to call themselves Outlaws. Surviving 
here requires the utmost skill and resilience; players must adapt to the volatile environment or face annihilation.

	Alaban’s NPCs are seasoned Outlaws themselves, veterans of betrayal and battle who guide new recruits through a series of rigorous chain quests. These quests 
are more than mere tasks—they are trials that test a player’s cunning, strength, and loyalty to the Outlaw faction. Each quest line imparts unique buffs, new 
abilities, or combat techniques that cannot be learned anywhere else in the world. At the final stages of these quests, players unlock skills that enhance their 
combat effectiveness, empowering them with strategic advantages in battle against their former factions.

		The Land of Secrecy and Power

	While Alaban’s natural beauty and mystique enchant some, the heart of this land beats for those who reject convention and seek power beyond the reach of their 
original faction. This land is designed to reward cunning, adaptability, and a willingness to embrace the life of an Outlaw. Players who pledge their loyalty to 
Alaban gain access to secrets and abilities that set them apart, but they also inherit a legacy of secrecy, betrayal, and rivalry with their former comrades. For 
those bold enough to forsake the known for the hidden power within, Alaban offers a journey like no other—a path paved with both peril and promise.

