	The Central and Eastern Woodland
1. "Esorin Falls"
	- Point of Interest : "The Whispering Pools" 
	Just beyond the falls and pools lies "The Falling Leaf Tavern", nestled within a rocky grove. This rustic, moss-covered tavern is run by the recluse 
"Jasper Leaf", a former adventurer who provides both rest and information to weary travelers. Jasper has extensive knowledge of the surrounding forest, but he only 
shares it with those he deems worthy.

2. "Yalith Village"
	- Point of Interest : "The Elder’s Hearth"  
	At the heart of Yalith Village, the Elder’s Hearth serves as a communal hub for locals and travelers alike. It includes "The Woodstone Brewery", a small brewing
area where villagers make elixirs from local herbs. These elixirs are sold to players, offering unique benefits that aid in battles and exploration. It’s also where
players can find "Eldra", an herbalist and potion-maker who specializes in concoctions crafted from Esorin’s rarest plants.

3. "The Depths of Aeloria"  
	- Point of Interest : "The Depths of Aeloria"  
	Located near the eastern foothills of the central woodlands, this ancient dungeon is partially concealed by undergrowth and thick tree roots. Once an 
underground sanctuary, it was overtaken by dark forces and now hosts fierce, corrupted guardians and rare resources. Adventurers must navigate its labyrinthine 
chambers, uncovering powerful artifacts and facing increasingly perilous enemies. Rumor has it that the deepest level holds an artifact tied to Alaban’s ancient 
magic, which is fiercely protected by an enchanted construct.

4. Hidden NPC: "Ahlos the Rune Keeper"  
	- Deep within a secluded glade, "Ahlos the Rune Keeper" has a modest hut lined with ancient tomes and runestones. Known to be eccentric yet wise, he shares 
knowledge of lost magical techniques with those who bring him rare materials. His guidance is valuable for players seeking to enhance their magical abilities.

