	The Western Stronghold
1. "Ilarian Falls"
	- Point of Interest : "The Echo Caverns"  
	Behind Ilarian Falls, within the Echo Caverns, lies "The River’s Roar Tavern", hidden in a spacious cave chamber. This unique tavern is carved directly into the
cavern walls and is frequented by mercenaries, bounty hunters, and explorers. It’s run by "Dara the Songstress", who provides rest, food, and access to rare 
supplies for adventurers. The tavern is known for its haunting melodies, echoing throughout the caverns and creating an atmosphere of mystery.

2. "Caerlis Keep"
	- Point of Interest : "The Ironclad Vault" 
	In addition to storing powerful relics, the Ironclad Vault is overseen by "Master Guard Tiberon", an NPC who holds key information about Alaban’s history and 
the dungeon’s origins. Players with high enough reputation or rare relics may earn access to special quests related to defending Alaban from encroaching magical 
forces.

3. Hidden NPC: "Harra of the Highlands"  
	- In a hidden mountain alcove just west of Caerlis Keep, "Harra of the Highlands" keeps a watchful eye on the valley. Harra is a survivalist who left the keep 
years ago, yet her knowledge of Alaban’s natural dangers and hidden caves is unmatched. Players can trade rare forest materials with her for unique, handcrafted 
weapons and survival gear, or learn stealth techniques specific to the Alaban wilderness.

