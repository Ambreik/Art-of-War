			Point of Interest

	The Southern Woods
1. Glarinora Lake
	- Point of Interest : "Moonshadow Isle"   
Accessible by a bridge that appears only in moonlight, this central isle contains "The Mistsong Tavern", a hidden refuge for travelers and Outlaws alike. The tavern
is known for its rare brews made from forest herbs and lake water, providing unique benefits to those who drink them. Adventurers come here to rest, trade stories, 
and share clues about nearby secrets.

	- Point of Interest : "The Silver Shrine"  
	Found within the Lirasen Ruins, this shrine now doubles as a meeting ground for "Elaris", a reclusive sage skilled in lunar magic and ancient lore. Elaris 
offers unique potions and enchanted relics to travelers who earn her trust, though she demands offerings of rare herbs in exchange.

2. The Lirasen Woods
	- Point of Interest : "The Solitude Hollow"  
	Hidden deep in the silver-barked forest, this hollow is the home of "Narik the Wanderer", a veteran knight who has renounced society. Narik is knowledgeable 
about Alaban’s history and hidden pathways, and he teaches rare combat techniques to those who can find him. His cave is lined with relics of past battles, lending 
to its eerie, solemn atmosphere.

