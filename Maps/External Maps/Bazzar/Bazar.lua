			Bazzar – The Magical Marketplace

		Overview :  
	"Bazzar" is a peaceful, neutral marketplace realm, created by the "Bubbles of Magic" and guided by "Noctis", the "Elder of Night". It serves as a hub for 
players to establish their own personal shops, meet NPC merchants, and engage in trade. This neutral zone is open to both factions, and it offers a controlled 
environment for commerce without the disruptions of PvP. The map is divided into three distinct areas: the left side for shops, the middle for NPC merchants and 
services, and the right side for players' private shops, creating a seamless trading environment.

		Layout of Bazzar :

1. Left Area: Player Shops (Offline Private Shops  
	The left side of Bazzar is dedicated entirely to "player-owned shops", where players can set up their own "Offline Private Shops" to sell their items. This 
system allows players to leave their shops open while they are offline, with others visiting the shop to purchase items. Features of this area include:
	- Offline Private Shops : Players can set up their personal shops to sell items when they are not actively online.
	- Customization : Players can name their shops, set prices, and organize items for sale to attract buyers.
	- Shop Organization : Shop owners can organize their inventory, making it easier for customers to browse and purchase items. end 

2. Middle Area: NPC Merchants and Services  
	The center of Bazzar houses the NPC merchants and various essential services for players. Here, players can interact with NPCs to buy necessary items, take on 
quests, and gain reputation with the "Guardian of Bazar". Key NPCs and services include:
	- Mira the Trade Mistress : Sells items like the "Market Toolkit" required to set up a personal shop.
	- Armor Merchant : Offers armors for all classes, with higher-tier items unlocked based on reputation with the "Guardian of Bazar".
	- Weapon NPCs : Specialized NPCs for each class of weapon, offering weapons that players can purchase depending on their reputation.
	- Crafting and Profession NPCs : Includes "Tailor Master", "Herbalist Master", "Fishing Master", "Cooker Master", "Mine Trainer", "Farmer Trainer", and 
"Legendary Blacksmith", providing materials and recipes for various crafts and professions according with their Ranking REP.
	- Guardian of Bazar : Offers quests and manages the reputation system for the marketplace.
	- Bazar Patrollers : NPCs that patrol the area, helping players find the locations of other NPCs by marking them on the minimap when requested.

3. Right Area: Player Shops (Offline Private Shops  
	Similar to the left side, the right side is dedicated to "player-run shops", where players can also set up their own "Offline Private Shops" to sell items. This
provides an additional area for players to set up their shops and offer goods. Key features include:
   - Offline Private Shops : Another area for players to establish shops while they are offline, allowing buyers to browse and purchase items at any time.
   - Shop Customization : Players can choose shop names, organize their inventory, and set prices to entice buyers.
   - Item Listings : Players can list a wide variety of items for sale, from crafting materials to rare weapons and armor.

