			Bazzar – The Magical Marketplace

	The "Bazzar" is a neutral marketplace designed specifically for players to trade items, set up private shops, and interact with various NPC merchants. This area
is an oasis of commerce where PvP is disabled, and various shop functionalities are integrated, offering a safe trading zone for both factions. 

		Bazzar Map Overview

- Layout : The map consists of three primary areas :
  - Left Area : Reserved for "Player Private Shops" (Offline Private Shop area).
  - Middle Area : Dedicated to NPC "Merchants" and vendors for essential items and services.
  - Right Area : Another section for "Player Private Shops" (Offline Private Shop area).

---

		System Mechanics for Bazzar

1. Neutral Zone :  
	- PvP Disabled : No PvP combat is allowed within Bazzar. This ensures the marketplace is a safe space for trade and interactions.
	- Mount/Pet Restrictions : Players are not allowed to summon mounts or pets within Bazzar to ensure smooth movement and avoid clutter.
	- Guardian/Baron Restrictions : Summoning "Guardians" or "Barons" is prohibited within the marketplace to prevent any unfair advantages and keep the focus on 
trade.

---

2. Private Shop System :  
   - Players can set up their "Offline Private Shop" in either the Left or Right Area of Bazzar.These shops allow players to sell their items when they are offline.
   - Shop Placement : Players must use the "Market Toolkit" to create and manage their private shop. Shops are placed in designated areas, and players can choose 
from a variety of locations.
   - Shop Slots : Players begin with a basic 12 shop slot when they first acquire the "Market Toolkit". Additional slots are unlocked through the "Reputation System", allowing players to expand their shop capacity and sell more items simultaneously with a maximum 40 slots placed in 2 windows.
  
---

3. Market Toolkit :  
	- Main Item : The "Market Toolkit" is required to create a private shop. It serves as the tool to set up and manage a player's shop while offline.
	- Obtaining : The toolkit can be obtained from the "Mira the Trade Mistress". 
	- Customization : Players can use the "Market Toolkit" to:
     - Customize their shop's "name" and "theme".
     - Select items for sale from their inventory and set prices.
     - Organize items into categories for easy navigation.

---

4. Reputation System – Unlocks and Progression :  
   The "Reputation System" with the "Guardian of Bazar" offers players various rewards based on their activity and quest completion within Bazzar. Reputation helps 
  unlock additional shop slots, exclusive skins for shops, and further bonuses.

	- Reputation Quests :  
		- Players can complete quests provided by the "Guardian of Bazar" or other NPCs in Bazzar. These quests often involve trading, helping NPC merchants, gathering rare items, or crafting.
		- Each completed quest increases the player's **Reputation Points** with the **Guardian of Bazar**, which unlocks various rewards.

	- Rewards for Reputation :
		1. Additional Shop Slots : 
			- Every level of reputation, players unlock additional shop slots for their private shops, increasing their selling potential.
		2. Shop Skins :
			- Special skins for the "Market Toolkit" can be unlocked. These skins add unique visual effects to the player’s shop, allowing for personal customization (e.g., "Mystic", "Frost", "Runic", etc.).
		3. Discounts and Offers :  
			- Players with high reputation may receive discounts from NPC merchants or unlock exclusive items for purchase.

	- Reputation Levels :
		- The more the player engages in trading, crafting, and completing quests, the more "Reputation" they gain.
		- Higher Reputation grants access to premium NPC merchants and exclusive goods, and enhances the player's ability to run multiple shops simultaneously.

---

5. Time-Based Shop Placement :  
	Players must pay a small fee to place their private shop in Bazzar, based on how long they wish to keep it active. The pricing is time-based and is as follows:

	- 1 Hour : 50 Silver (S)
	- 2 Hours : 1 Gold (G)
	- 3 Hours : 1 Gold and 50 Silver (1.5G)
	- Custom Duration : Players can pay for longer shop durations, with prices increasing as the time extends.
	- Pay-to-Place : Players pay a fee to keep their shop active, and once the time runs out, the shop will automatically close, and the items will be stored in the
"Market Toolkit" or they can pay again the fee to extend the time direct from the "Market Toolkit".

---

6. NPC Merchants and Services :  
	Various NPCs are scattered throughout Bazzar middle area, offering essential services and items:

   - Mira the Trade Mistress : Sells the "Market Toolkit" and basic items for trading. At higher Reputation Levels, new items are unlocked such 
"Shop Slot Extension" and more.
   - Armor Merchant : Sells armor for all classes, with higher-level armor available as the player increases their reputation with the "Guardian of Bazar".
   - Weapon Vendors : Separate NPCs for each class of weapons. Access to high-tier weapons is based on reputation.
   - Crafting Masters : Includes "Tailor Master", "Herbalist Master", "Fishing Master", "Cooker Master", "Mine Trainer", "Farmer Trainer", offering various 
materials and recipes.
   - Legendary Blacksmith : Offers unique or high-quality crafting recipes, unlocked at higher reputation levels.
   - Guardian of Bazar : Offers quests and has a reputation system. As players complete quests, they gain "Reputation Points" and unlock rewards.
   - Bazar Patrollers : NPCs who patrol the Bazzar and help guide players to various NPC merchants when asked. They cannot be interacted with except for guidance 
purposes.

---

7. Seasonal and Event-Based Changes :  
	- During special events or seasons, new merchants or limited-time items may appear in Bazzar, adding exclusivity and variety to the marketplace.  
	- "Event-Specific Vendors" may offer unique cosmetics, event-themed items, and rare materials not typically available.

