			Garland Area Info

	Location : Central region of the "Legrand" continent, serving as the primary entry point for players.
	Description : "Garland" serves as the first major area players encounter upon arriving in "Legrand". With its rocky terrain and cliffs, it provides both 
protection and a vibrant setting for adventure. "Emryn Bay" is the crown jewel of this region, where players can gather, trade, and embark on quests.

		Sub-Areas : 

1. "Grosvenor" : 
	- Geographical Location: Center of "Garland", surrounding the eastern edges of "Emryn Bay".
	- Description : This rugged mountain area features steep cliffs and rocky outcrops. Players can discover hidden paths and shortcuts, perfect for those seeking 
adventure or avoiding enemy encounters. The landscape is dotted with rare flora, providing valuable crafting materials.
		Hidden Location : 
	"Grosvenor Heights" - A lookout point offering breathtaking views of Emryn Bay and the surrounding landscape, often visited by players for peaceful reflection.

	"Emryn Bay" : At the estuary of "Garland", connecting to the "Great Sea". Is a bustling port city at the estuary, split into four distinctive quarters:
	- "North Quarter" : Characterized by cozy homes and friendly taverns. The "Market Square" here is vibrant, with vendors selling local delicacies and unique items.
	- "East Quarter (Knights)": The knights stronghold, featuring the "Guardians' Barracks" where players can join the knights' army. Training grounds and 
sparring arenas allow players to hone their skills and learn combat techniques.
	- "South Quarter": A commercial hub bustling with trade. The "Emryn Marketplace" offers a range of shops selling everything from weapons to potions. Players can
engage in trading quests and improve their bargaining skills.
	- "West Quarter (Pindars)": A culturally rich area known for art and festivals. Players can find the "Pindar Museum", showcasing the history of "Legrand" and 
its inhabitants.

	"Cavern of Whispers" : Located at the foot of the cliffs near "Emryn Bay".
	- Description : A hidden cave filled with echoes of past heroes and legends. The walls are adorned with ancient carvings, and players can uncover rare 
artifacts and lore through exploration.

	"Forgotten Passage" : Underwater, along the coastline of "Garland".
	- Description : An underwater cavern that connects various locations along the coastline. Players can embark on treasure hunts, facing unique underwater 
challenges and enemies. This passage is rumored to hold treasures of lost adventurers and ancient sea creatures.

	"Caves of Solitude" : Scattered throughout the cliffs of "Garland".
	- Description : Secluded caves ideal for players seeking refuge. Each cave has its own unique resources, like rare crystals or herbs, making them prime spots 
for resource gathering. Some caves may also hide secrets or mini-bosses.

	"Sapphire Lagoon" : Adjacent to the coastline near "Emryn Bay".
	- Description : A picturesque lagoon known for its sparkling blue waters and lush vegetation. Players can fish for rare species, engage in crafting materials, 
or simply relax and enjoy the scenery.

	"Stonebreaker Ruins" : Northeastern part of "Garland", accessible through winding paths from "Emryn Bay".
	- Description : Ancient ruins that tell the story of a long-lost civilization. Players can explore these ruins for treasure and artifacts but must also be wary 
of traps and guardian spirits that protect the site.

---
		
		Overall Significance : 

	"Garland" serves as the first point of contact for players arriving on the "Legrand" continent, rich in history and culture. "Emryn Bay" stands as a testament 
to human resilience and adaptability, providing a hub for commerce and exploration. The distinct quarters cater to various factions,fostering community and rivalry,
while the surrounding mountainous terrain and secret caves add layers of adventure and lore, inviting players to uncover hidden treasures and stories.