			Dark Knight (Bladebearer of Ashkar)

	General Information

* Name : Bladebearer of Ashkar (aka Dark Knight)
* Type : Skull Monster / Boss-tier
* Rank : Skull (Overpowered, stronger than standard Bosses)
* Location : Legrand – Lv 120+, Sudeon, Dark Temple (Outdoor)
* Minimum Party Requirement : 5+ players to deal damage
* Respawn Timer : Low (exact time TBD, could be tied to server reset or lore)
* Special Notes : Drops dungeon key to Dark Temple

---

	Attributes & Scaling

* Extremely high HP pool, resistances, and damage output
* Ignores certain crowd control (CC) until debuff stacks applied
* Scales slightly with region/continent progression

---

	Behavior Mechanics

* Aggro & Threat : Immune to taunts; unpredictable targeting
* Minimum Party Mechanic : Solo/small groups cannot damage; triggers "Rage Mode" instantly
* Rage Mode : Permanent escalation if under-leveled or insufficient party size
* Signature Ability : "Shake" – Applies 5–10% debuff reducing stats/damage to attacking players (solo or group)

---

	Combat Mechanics & Abilities

* Rage Mode : Escalates damage, attack speed, and attack pattern intensity
* Shake : Affects all attacking players, reducing their combat efficiency
* Elite Creature Synergy : Nearby Elite Creature spawns periodically; drops "Ashkar’s Wardstone" which reduces Dark Knight’s damage by 5% per stack for all engaged 
players and can be used by all players, stacking the debuff for the Skull up to 40%.

