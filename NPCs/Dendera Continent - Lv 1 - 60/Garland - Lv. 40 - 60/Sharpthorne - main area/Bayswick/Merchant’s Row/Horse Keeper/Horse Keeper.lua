			Horse Keeper

	Location : "Merchant’s Row", The Trainer’s Lodge  

---

	Appearance : A tall, wiry man with a broad-brimmed hat and a jovial smile. He wears simple but practical clothing: a brown vest, white shirt, and sturdy 
trousers. By his side are a small, shaggy pony and a magnificent black stallion, both of which he frequently brushes or feeds.  

---

	Personality : Cheerful and talkative, he’s always ready with a story about one of his horses. He has a deep bond with his animals and treats them as trusted 
partners rather than mere beasts of burden.  

---

	Role :  
- Roles : Quest Giver, Trainer, Shopkeeper  
	The Horse Keeper offers quests related to horses, sells riding supplies, and provides services for storing and caring for horses.  

---

	Mechanics :  
- Type : Trainer, Quest Giver, Shopkeeper  
- Reputation : None  
- Interaction Limitations : None; open to all players.  
- PvP : Can be targeted for PvP and killed.  
- Respawn Time : 30 minutes after being killed.  
- Other Mechanics :  
	- Offers services to store up to 12 mounts for players, with a daily fee.  
	- Feeds mounts for an additional cost or requests specific food items for the mount.  
	- Skillable NPC: Uses basic defensive skills when attacked, such as:  
		- *Kickback*: A strong kick to knock attackers back.  
		- *Gallop Charge*: Charges forward with his stallion to deal moderate damage.  
		- *Rearing Strike*: Uses his pony to strike with its front hooves, causing slight stagger.  

---

	Quotes :  
- "Every horse is a partner, not just a mount."  
- "Ponies might be small, but don’t underestimate their spirit."  
- "Take good care of your horse, or you’ll regret it in the wild."  