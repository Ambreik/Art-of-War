			Mount Trainer

	Location : "Merchant’s Row", The Trainer’s Lodge  

---

	Appearance : A burly man in his 40s with a rugged beard and a weathered face. He wears a long leather coat and sturdy riding boots. His gloves are always 
covered in dust and hay, showing his care for mounts. Near him stand two magnificent and rare mounts—a luminous stag with glowing antlers and a sleek, black 
griffin—both of which cannot be found elsewhere in the game.  

---

	Personality : Gruff but good-hearted, he speaks in a straightforward, no-nonsense manner. Though he’s tough with players, he shows a surprising gentleness when 
handling mounts, often murmuring to them under his breath.  

---

	Role :  
- Roles : Quest Giver, Trainer, Shopkeeper  
	The Mount Trainer provides mount-related quests, sells mount supplies, and offers services for storing mounts. He also takes care of stored mounts, including 
feeding them for a fee or requiring specific food items.  

---

	Mechanics :  
- Type : Trainer, Quest Giver, Shopkeeper  
- Reputation : None  
- Interaction Limitations : None; open to all players.  
- PvP : Can be targeted for PvP and killed.  
- Respawn Time : 30 minutes after being killed.  
- Other Mechanics :  
	- Offers services to store up to 12 mounts for players, with a daily fee.  
	- Feeds mounts for an additional cost or requests specific food items for the mount.  
	- Interacts with his two rare mounts, playing tricks or brushing their fur/feathers.  

---

	Quotes :  
- "Horses, stags, griffins—it doesn’t matter. They all deserve proper care."  
- "Take care of your mount, or you’ll find yourself on foot."  
- "These beauties here? Sorry, friend. They’re not for sale."  

