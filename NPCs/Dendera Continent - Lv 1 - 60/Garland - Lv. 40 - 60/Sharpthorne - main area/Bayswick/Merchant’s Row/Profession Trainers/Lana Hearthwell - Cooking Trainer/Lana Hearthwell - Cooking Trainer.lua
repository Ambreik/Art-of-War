			Lana Hearthwell - Cooking Trainer  

	Location : "Merchant's Row", Commercial District

---

	Appearance : Lana is a cheerful woman in her mid-40s, with auburn hair tied back in a loose bun. She wears a practical yet charming outfit consisting of a white
apron over a colorful blouse and skirt, adorned with patches of flour and grease from her culinary work. Her hands are slightly calloused from years of chopping, 
stirring, and baking.

---

	Personality : Friendly, warm, and passionate about cooking, Lana takes pride in teaching others how to create delicious meals. She enjoys helping players grow 
their skills and sharing stories about her culinary adventures.  

---

	Role :  
- Role(s) : Cooking Trainer, Shopkeeper  
	Lana serves as a trainer and vendor in the Cooking profession. She offers Rank II quests related to creating intricate dishes and gathering rare ingredients.  

---

	Mechanics :  
- Type : Trainer, Vendor  
- Reputation : Players with the Cooking profession can gain reputation with Lana through quests and purchases. Higher reputation unlocks exclusive recipes and 
discounts.  
- Interaction Limitations : Available only to players with the Cooking profession.  
- PvP : Can be targeted and killed.  
- Respawn Time : 5 minutes if killed.  
- Defensive Behavior : Lana defends herself with low-damage melee attacks using a frying pan or rolling pin.  

