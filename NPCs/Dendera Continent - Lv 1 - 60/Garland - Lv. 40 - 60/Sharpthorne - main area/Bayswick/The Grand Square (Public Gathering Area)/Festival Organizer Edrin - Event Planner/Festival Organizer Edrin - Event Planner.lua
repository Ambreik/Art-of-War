			Festival Organizer Edrin - Event Planner

	Location : "Baywick City - Grand Square" (Public Gathering Area)

---

	Appearance : Edrin is a middle-aged man in his early 40s, with short-cropped silver hair and a neatly trimmed beard. He wears a well-tailored vest in vibrant 
colors, with intricate patterns that reflect the festive nature of his role. A long scarf in the city’s signature colors wraps around his neck, and he carries a 
scroll case with plans for upcoming events. His posture is upright, and he exudes an air of authority mixed with the excitement of his work.

---

	Personality : Edrin is organized, efficient, and a bit of a perfectionist. He takes great pride in his role as the city’s event planner and is deeply invested 
in ensuring every festival is a success. Though he may seem serious at first, his enthusiasm for the events is contagious, and he’s quick to share details about 
upcoming festivities with anyone who asks. He’s approachable, but he always has his mind on the next big event. 

---

	Role :  
- Role(s) : Quest Giver, Information Provider  
	Edrin coordinates city-wide festivals and celebrations, ensuring the smooth execution of events. He offers quests related to organizing festivities, including 
gathering resources, recruiting performers, or completing tasks that contribute to the event's success. Additionally, Edrin provides information about current and 
upcoming city events, such as festivals, celebrations, or competitions.

---

	Mechanics :  
- Type : Quest Giver, Information Provider  
- Reputation : No specific reputation system associated with him.  
- Interaction Limitations : Open to all players but only provides quests related to festivals or event preparations.  
- PvP : Edrin is not flagged for PvP. If attacked, he will immediately attempt to flee towards nearby guards for help.  
- Respawn Time : 15 minutes after being killed or leaving the area.  
- Other Mechanics : Edrin is a valuable source of information about both current and upcoming events. Players can interact with him to learn about future 
festivities and what preparations may be needed for participation.

