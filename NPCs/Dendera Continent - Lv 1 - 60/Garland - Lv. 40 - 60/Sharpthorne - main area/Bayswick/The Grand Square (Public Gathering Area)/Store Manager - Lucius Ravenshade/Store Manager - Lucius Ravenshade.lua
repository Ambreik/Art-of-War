			Store Manager - Lucius Ravenshade

	Location : "Baywick City", The Grand Square

---

	Appearance : Lucius is a tall, composed man in his early forties with sharp, angular features. His skin is pale, and his silver hair, always neatly combed back,
contrasts with his dark blue attire. He wears a well-tailored black coat with intricate embroidery along the cuffs and collar, and a set of small silver-framed 
glasses rests on his nose. His piercing gray eyes watch over the store with a keen sense of order, and his posture is straight, radiating authority. Lucius’s 
expression is calm, with an ever-present air of professionalism, but his lips are often curved in a small, knowing smile.

---

	Personality : Lucius is efficient, methodical, and always professional. He has a no-nonsense attitude when it comes to managing the store and prefers to keep 
things organized. While he can be a bit reserved at first, he is courteous and polite to every customer, always offering his services with a polite nod. Though he 
doesn’t engage much in small talk, those who take the time to speak with him will find that he is surprisingly knowledgeable about the inner workings of the store 
and the items within. Lucius is a man of few words, but when he speaks, his words carry weight.

---

	Role :  
- Store Manager : Lucius oversees the Store Manager function, allowing players to store their items and access their personal inventory, much like a personal vault.
He facilitates the organization of items and offers management services for players to store and retrieve goods as needed. He is crucial for players who wish to 
keep their inventories organized without needing to carry everything with them.

---

	Mechanics :  
- Type : Store Manager  
- Reputation : None  
- Interaction Limitations : Available to all players.  
- PvP : Not flagged for PvP, cannot be attacked.  
- Respawn Time : Instant, as he is always present and functioning within the store.  
- Other Mechanics : Lucius allows players to store a wide variety of items in the personal vault, which can be accessed whenever needed. He has no combat abilities 
or other special functions, simply managing the storage and retrieval of goods for players. Players can store equipment, materials, and other items in the vault to 
free up inventory space.

---

	Skills : Lucius is not skillable and does not possess any combat abilities or special skills.  

---

	Quotes :  
- "Your items are safe with me. Feel free to store anything you don’t need at the moment."  
- "I’ll handle the organization, you focus on your adventures."  
- "Everything is in order; your inventory is always accessible whenever you need it."

