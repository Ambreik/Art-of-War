			Thalyn Goldleaf

	Location : The Forgotten Altar, Ancient Ruins

---

	Appearance : Thalyn is a young, sprightly elf with a messy shock of golden hair. His clothes are a mix of adventurer's attire and scholarly robes, often 
stained with earth or fruit juices. He carries a small satchel filled with tools and artifacts. His expression is usually one of eager curiosity, and he is rarely 
seen without a wide grin on his face.

---

	Personality : Thalyn is youthful, energetic, and somewhat mischievous. He tends to be more interested in having fun than focusing on the academic side of
archaeology, which sometimes leads to distractions. His love for collecting things goes beyond just relics, as he enjoys gathering unusual items for his own 
amusement. Thalyn is always in search of new friends and often asks players to assist him with odd tasks.

---

	Behaviour :  
- Frequently talks to players about his adventures and the things he's found.
- Sometimes gets distracted by things around him, such as fruit-bearing trees or flowers.
- Enjoys pranking other NPCs or attempting to strike up fun, sometimes meaningless, conversations.

---

	Role :  
- Role(s) : Apprentice Archaeologist, Quest Giver  
	- Provides players with small, fun tasks related to archaeology, such as fetching fruits or helping him with his personal collection.

---

	Mechanics :  
- Type : Quest Giver  
- Reputation : None  
- Interaction Limitations : Only available to players with the Archaeology profession or those who have a certain reputation with him.  
- PvP : Cannot be killed in PvP, though he may be temporarily incapacitated by monsters.  
- Respawn Time : Immediate after death.  
- Other Mechanics :  
	- Can offer a variety of simple fetch quests related to his personal hobbies or minor archaeological tasks.  
	- Occasionally rewards players with minor archaeology tools, snacks, or fun trinkets.

---

	Quest :  
- Quest Title : Apple for Thalyn  
	- Quest Type : Side  
	- Objective(s) : Gather five ripe apples from the nearby orchard and deliver them to Thalyn.  
	- Rewards : Small archaeology-related items and a bit of gold.  
	- Conditions : No prerequisites.  
	- Quest Dialogue :  
		- Opening : "You know, I’ve been thinking… apples are the perfect snack for an adventurer like me. Mind helping me get some from the orchard?"  
		- Mid-Quest : "Did you find those apples? They’re *perfect* for making the best fruit salad in the land!"  
		- Completion : "Ah, these apples are perfect! You’re a lifesaver. I’ll make you something nice, don’t you worry."  
	- Additional Notes : This quest is lighthearted and designed to give players a break from more intense archaeology tasks.

