			Jarek Watchstone  

	Location : Azurr Forest / Vygor Port / Dockside Patrol Route  

---

	Appearance : Jarek is a muscular, broad-shouldered man with dark brown hair tied into a rough ponytail. He has deep scars along his left cheek and neck, 
evidence of past battles. His dark blue patrol uniform is reinforced with chainmail beneath a leather vest, and he wears a heavy iron belt with multiple pouches. 
His primary weapon is a long, single-edged cutlass, and he carries a secondary dagger strapped to his thigh.  

---

	Personality :  
- Highly protective of the port and its people.  
- Suspicious of newcomers, always watching their movements.  
- A man of few words, but his actions speak louder.  
- Has no tolerance for criminal activity, especially smuggling.  
- Cynical but deeply loyal to the law.  

---

	Behaviour :  
- Moves between the docks and shipyards, ensuring merchants and sailors remain safe.  
- Stops players who look suspicious, asking about their purpose at Vygor Port.  
- If a player is caught smuggling or assisting criminals, he will attack immediately.  
- Participates in the mini-event involving Magnus Holt & the smugglers, engaging in combat if necessary.  
- Uses aggressive combat tactics, focusing on control and counters.  

---

	Role :  
- Role(s) : Patrol Guard, Enforcer of the Law  
	- Protects Vygor Port from illegal activities.  
	- Hunts down criminal players with a negative reputation.  
	- Responds to calls for backup from Seris Stormguard or Magnus Holt.  

---

	Mechanics :  
- Type : Patrol Guard, Skillable NPC  
- Reputation :  
	- Players with negative reputation will be attacked on sight.  
	- Players with good standing in Vygor Port may receive minor assistance.  
- Interaction Limitations :  
	- If a player is known to have helped smugglers, Jarek will react with hostility.  
- PvP :  
	- Skillable NPC.  
	- Will call reinforcements from other guards if outnumbered.  
- Respawn Time :  
	- 20 minutes if killed.  
- Other Mechanics :  
	- Can counterattack melee strikes.  
	- Can parry attacks, reducing incoming damage.  
	- Has an AoE "Sweeping Strike" ability.  

---

	Skills:  

- Skill 1: Sweeping Strike  
	- Description : A powerful horizontal slash that knocks enemies back.  
	- Effect : Deals damage and interrupts spellcasting.  

- Skill 2: Shield Counter  
	- Description : Blocks an incoming melee attack and delivers a powerful counterblow.  
	- Effect : Stuns the attacker for 2 seconds.  

- Skill 3: Vygor’s Law  
	- Description : Calls for reinforcements, summoning two additional guards.  
	- Effect : Summons patrol reinforcements who fight alongside him.  

---

	Quest :  
- Quest Title : "Dockside Justice"  
	- Quest Type : Side Quest  
	- Objective(s) :  
		- Investigate a suspected smuggler’s stash.  
		- Confront and defeat the smuggler ringleader.  
		- Report findings to Jarek Watchstone.  
	- Rewards :  
		- Gold, experience, and increased reputation with Vygor Port Guards.  
	- Conditions :  
		- Available only to players with no smuggling ties.  
	- Quest Dialogue :  
		- Opening :  
			- "Too many ships dock here without proper inspection. I know there’s contraband moving through this port—I need proof."  
		- Mid-Quest :  
			- "Don’t come back until you’ve got solid evidence."  
		- Completion :  
			- "You did well. That scum won’t be smuggling anything through Vygor again."  
	- Additional Notes :  
		- Tied to larger smuggling questlines.  

---

	Quotes :  
- "Stay out of trouble, and we won’t have a problem."  
- "I know a criminal when I see one."  
- "You think you can outfight the law? Try me."  
- "The sea takes fools, and so do I."  
- "I don’t trust your kind… not yet."  

