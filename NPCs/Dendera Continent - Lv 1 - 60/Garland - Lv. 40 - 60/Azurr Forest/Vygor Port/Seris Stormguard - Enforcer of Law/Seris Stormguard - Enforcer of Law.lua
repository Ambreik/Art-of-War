			Seris Stormguard  
	
	Location : Azurr Forest / Vygor Port / Port Entrance & Main Docks  

---

	Appearance : Seris is a tall, broad-shouldered man with piercing gray eyes and a strong jawline, always clean-shaven. His dark blue uniform is lined with silver
trim, denoting his senior position. He wears sturdy steel pauldrons, a reinforced leather chestplate, and a black cloak with the Vygor Port emblem. A long, curved 
sword hangs at his side, and a metal bracer on his left forearm bears deep scratches—marks from past encounters.  

---

	Personality :  
- Disciplined and strict, follows orders without hesitation.  
- Has no patience for smugglers, pirates, or those who challenge the law.  
- Respects strength, both in words and combat.  
- Doesn’t tolerate laziness among his guards.  
- Rarely smiles, except after a hard-fought battle.  

---

	Behaviour :  
- Patrols the docks methodically, checking on merchants and inspecting ships.  
- Stops and questions suspicious individuals, especially new arrivals.  
- Intervenes immediately if players attack NPCs in the area.  
- Will engage in combat if provoked, using advanced sword techniques and AoE abilities.  
- Issues direct orders to other patrol guards, making them coordinate in combat.  

---

	Role :  
- Role(s) : Patrol Guard, Enforcer of the Law  
	- Ensures Vygor Port remains safe.  
	- Hunts down criminal players or those with negative reputation.  
	- Provides warnings before engaging in combat.  
	- Can summon reinforcements if outnumbered.  

---

	Mechanics :  
- Type : Patrol Guard, Skillable NPC  
- Reputation :  
	- Players with positive reputation in the port receive his approval and occasional assistance.  
	- Players with negative reputation will be attacked on sight.  
- Interaction Limitations :  
	- Players with a criminal record (such as known smugglers) will be immediately confronted.  
- PvP :  
	- Skillable NPC.  
	- Will call for reinforcements from other guards.  
- Respawn Time :  
	- 20 minutes if killed.  
- Other Mechanics :  
	- Can disarm players for a short duration.  
	- Can parry attacks, reducing incoming damage.  
	- Immune to stun and slow effects.  

---

	Skills:  

- Skill 1: Blade Arc  
	- Description : Performs a wide, sweeping slash, damaging multiple enemies in front of him.  
	- Effect : Hits all enemies in a cone in front of him, dealing moderate damage.  

- Skill 2: Tactical Disarm  
	- Description : Targets one enemy, disarming them for a short duration.  
	- Effect : Prevents weapon-based attacks for 5 seconds.  

- Skill 3: Call to Arms  
	- Description : Lets out a sharp whistle, summoning other patrol guards to his location.  
	- Effect : Summons 1-2 additional guards who will assist in combat.  

---

	Quest :  
- Quest Title : "The Smuggler’s Shadow"  
	- Quest Type : Side Quest  
	- Objective(s) :  
		- Find evidence of smuggling at Vygor Port.  
		- Interrogate suspicious merchants.  
		- Defeat a smuggler lieutenant hiding in the docks.  
	- Rewards :  
		- Gold & experience.  
		- Increased reputation with Vygor Port Guards.  
	- Conditions :  
		- Available only to players with no criminal record.  
	- Quest Dialogue :  
		- Opening :  
		- "You look like someone who can handle themselves. There's filth in this port, and I need proof before I can clean it up." 
		- Mid-Quest :  
			- "Did you find anything yet? I know they're hiding something."  
		- Completion :  
			- "Good work. I'll take it from here. This port will be cleaner, thanks to you."  
	- Additional Notes :  
		- Leads into a larger smuggling investigation.  

---

	Quotes :  
- "If you’re here to cause trouble, turn around. Now."  
- "I’ve seen smugglers with better discipline than some of these rookies."  
- "Break the law, and I’ll break more than just your spirit."  
- "Stay out of my way, and I’ll stay out of yours."  
- "You think you can outfight a man who’s trained for war? Try me."  

