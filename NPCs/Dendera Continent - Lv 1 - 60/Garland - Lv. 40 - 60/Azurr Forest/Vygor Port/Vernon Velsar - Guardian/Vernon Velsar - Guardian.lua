			Vernon Velsar  

	Location : Azurr Forest / Vygor Port / Guard Barracks & Main Entrance  

---

	Appearance :  
A burly man with a thick beard, streaked with gray, and piercing steel-blue eyes. He wears a reinforced leather coat over a chainmail shirt, signifying both authority and durability. A wide belt holds a large, well-worn cutlass, and his arms are thick with muscle from years of enforcing the law. His posture is always firm and imposing, and his expression rarely softens, giving him an air of unwavering authority.  

---

	Personality :  
Vernon is strict, no-nonsense, and a firm believer in order. He speaks in a deep, commanding tone, rarely entertaining jokes or small talk. He despises smugglers and anyone who disrupts the peace of Vygor Port. However, he respects those who follow the rules and is willing to help travelers navigate the region—if they can handle his gruff attitude. His loyalty to his job is unmatched, and he is known to show no mercy to criminals.  

---

	Behaviour :  
- Vernon is stationed near the main port entrance, offering directions to key locations such as the customs office, the shipwright, and boat rentals.  
- If approached, he will provide orientation dialogue for the entire area.  
- If he spots troublemakers, he immediately enters a combat stance.  
- When off-duty, he patrols the barracks and drinks at a quiet spot near the docks.  

---

	Role :  
- Role(s) : Area Guardian, Orientation NPC, Skillable NPC  
	- Provides navigation help for players unfamiliar with the port.  
	- Guards key NPCs and locations, ensuring no illegal activity occurs.  
	- Enforces justice, attacking criminals and defending allied NPCs if necessary.  

---

	Mechanics :  
- Type : Orientation NPC, Combat NPC  
- Reputation : No direct reputation rewards, but attacking him will cause instant hostility with Vygor Port’s authorities.  
- Interaction Limitations : Available to all players unless their reputation is hostile with the port.  
- PvP : Can be attacked, triggering an alert to other guards, who will assist him.  
- Respawn Time : 10 minutes after death.  
- Other Mechanics :  
	- Will warn players engaging in illegal activities before attacking.  
	- If killed, patrol guards increase in number for a limited time to secure the port.  

---

	Skills :  
- Skill 1: Sweeping Cut  
	- Description : Vernon swings his cutlass in a wide arc, hitting all enemies in front of him.  
	- Effect : Deals AoE slash damage, with a chance to cause Bleed.  

- Skill 2: Shield Bash  
	- Description : Vernon raises his reinforced bracer and slams it into an enemy’s face.  
	- Effect : Deals medium damage and stuns the target for 2 seconds.  

- Skill 3: Authority Roar  
	- Description : Vernon lets out a battle command, causing all guards nearby to rush to his aid.  
	- Effect : Summons two additional patrol guards to assist him in combat.  

---

	Quest :  
- Quest Title : "Keep Your Nose Clean"  
	- Quest Type : Orientation Quest (Side)  
	- Objective(s) : Speak to key NPCs in "Vygor Port" (Customs Officer, Shipwright, and General Store Owner).  
	- Rewards : Small amount of XP, minor gold reward, and an updated map marker for Vygor Port.  
	- Conditions : Available to all players upon first arrival.  
	- Quest Dialogue :  
		- Opening :  
			- _"New here? Then listen up. Vygor Port runs on order, not chaos. If you want to stay out of trouble, go introduce yourself to the Customs Officer, the Shipwright, and Lira at the General Store. You’ll need to know them if you plan on sticking around."_  
		- Mid-Quest :  
			- _"Still wandering around like a lost fish? Get to it—time is money here."_  
		- Completion :  
			- _"Good. Now you know who keeps this place running. Don’t give me a reason to throw you out."_  
	- Additional Notes : Completing this quest prevents Vernon from treating the player as an unknown outsider.  

