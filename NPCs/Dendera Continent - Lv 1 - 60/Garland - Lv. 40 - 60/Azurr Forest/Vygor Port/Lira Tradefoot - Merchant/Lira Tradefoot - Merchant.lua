			Lira Tradefoot  

	Location : Azurr Forest / Vygor Port / General Store  

---

	Appearance : Lira Tradefoot is a lean and wiry halfling, standing at about 3 feet tall with short, chestnut-colored hair tucked under a wide-
brimmed green hat. She has sharp, calculating eyes, and her fingers are always moving, either counting coins or rearranging stock. She wears a dark 
leather vest over a long-sleeved linen shirt, with several pouches strapped to her belt. Her feet are bare, a common trait of her kind, and they are calloused from 
years of travel.  

---

	Personality :  
- Friendly but shrewd, always looking for a good deal.  
- Has a sharp tongue, quick to mock bad negotiators.  
- Loves haggling, considers it a friendly competition.  
- Dislikes pirates and smugglers, but won’t turn away a good deal.  
- Always smiling, but her eyes constantly scan for thieves.  
- Hates being cheated, and will blacklist players who try to scam her.  

---

	Behaviour :  
- Always cleaning, organizing, or counting coins behind her stall.  
- Keeps an eye on players, tapping her fingers impatiently if they browse too long.  
- Adjusts prices based on a player’s reputation (higher REP = discounts, lower REP = increased prices).  
- Flees if attacked, dropping caltrops behind her to slow pursuers.  
- Occasionally chats with other merchants, sharing news about trade routes.  

---

	Role :  
- Role(s) : General Store Owner, Vendor, Survival Goods Merchant  
	- Sells basic supplies like rations, torches, repair kits, and small weapons.  
	- Specializes in survival gear for sailors and adventurers.  
	- Adjusts prices based on a player's reputation.  
	- Offers limited barter trade for rare goods.  

---

	Mechanics :  
- Type : Vendor  
- Reputation :  
	- Higher Reputation (Friendly or higher) → 5-10% discount on goods.  
	- Lower Reputation (Unfriendly or worse) → Prices increase by 10-20%.  
- Interaction Limitations :  
	- Smugglers and Pirates pay more unless they bribe her.  
- PvP :  
	- Non-skillable NPC, cannot be attacked.  
	- If threatened, drops caltrops (slows movement for 5 seconds) and vanishes for 30 minutes.  
- Respawn Time :  
	- Instant if not attacked.  
	- 30 minutes if forced to flee.  
- Other Mechanics :  
	- Occasionally changes her stock, depending on world events.  
	- Buys rare trade goods at good rates, but only in bulk.  

---

	Skills : (Used only for interactions, not combat.) 

- Skill 1: Silver Tongue  
	- Description : Lira can detect deception in trade deals.  
	- Effect : Players who try to sell fake or low-quality goods get instantly blacklisted from her store for 24 hours.  

- Skill 2: Merchant’s Insight  
	- Description : Adjusts prices dynamically based on the player's reputation and past purchases.  
	- Effect : Frequent customers get better deals, while newcomers or pirates pay more.  

- Skill 3: Quick Escape  
	- Description : When attacked, drops caltrops and vanishes, making her untargetable for 30 minutes.  
	- Effect : Players who try to attack her suffer slowed movement, and she disappears from the shop.  

---

	Quest :  
- Quest Title : "Supply Chain Crisis"  
	- Quest Type : Side Quest  
	- Objective(s) :  
		- Find and deliver missing goods from a lost caravan.  
		- Negotiate with a rival merchant to lower their prices.  
		- Protect a shipment from bandits on its way to Vygor Port.  
	- Rewards :  
		- Increased Reputation with Vygor Port traders.  
		- Permanent 5% discount at her store.  
		- Gold & consumable supplies.  
	- Conditions :  
		- Requires Neutral or higher Reputation with Vygor Port.  
	- Quest Dialogue :  
		- Opening :  
			- "Prices are rising, and I don’t like it. You interested in fixing that for me?"  
		- Mid-Quest :  
			- "If you’re late, I swear I’ll make you pay the difference."  
		- Completion :  
			- "Now that’s more like it! Stick around, I’ll make sure you get the best deals."  
	- Additional Notes :  
		- Completing this quest permanently unlocks better prices at her store.  

