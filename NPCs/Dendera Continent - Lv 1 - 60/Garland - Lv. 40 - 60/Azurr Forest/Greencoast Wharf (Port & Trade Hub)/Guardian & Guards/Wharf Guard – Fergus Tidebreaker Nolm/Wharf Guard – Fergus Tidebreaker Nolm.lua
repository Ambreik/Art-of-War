			Wharf Guard – Fergus "Tidebreaker" Nolm

	Location : "Greencoast Wharf" – East Pier

---

	Appearance :  
- Age: Late 40s  
- Attire: Wears a combination of worn leather armor and a sailor’s jacket. His armor is more practical than decorative, reflecting his days as a sailor.  
- Distinguishing Features: Weathered skin and a salt-and-pepper beard. His eyes are always scanning the horizon, a habit from his days at sea.  
- Equipment: A cutlass at his side, a small shield, and a rope coil for tying down ships.  
- Posture: Relaxed but vigilant, often leaning against a wooden post or railing, watching the ships dock and depart.

---

	Personality : Fergus is a seasoned veteran, his life spent on the seas before he became a guard. He has a deep love for the ocean and is highly attuned to the 
flow of life around the wharf. He’s laid-back but deadly serious when it comes to security. He has a dry sense of humor, often making sailor jokes or giving advice 
to younger guards.

---

	Behaviour :  
- Fergus spends most of his time at the East Pier, keeping an eye out for any trouble on the waters.  
- He’s not as formal as Captain Vail but still commands respect. He will often offer advice or stories about his sea adventures when spoken to.  
- He’s especially alert when it comes to any unusual ship movements, suspecting smuggling or hidden dangers on the water.

---

	Role :  
- Role(s): Veteran Guard, Smuggling Investigator, Quest Giver  
	- Fergus serves as a guardian of the East Pier and is particularly focused on ships coming into the port. He may give quests involving tracking down smuggling 
rings or handling pirates.

---

	Mechanics :  
- Type : Guard, Quest Giver, Investigator  
- Reputation : Offers Reputation rewards with the Greencoast Wharf Guard faction.  
- Interaction Limitations : Available once the player has reached a certain level of reputation with the Greencoast Wharf Guard faction.  
- PvP : Fergus is not flagged for PvP, but any attack on him will prompt a response from surrounding guards.  
- Respawn Time : Not applicable.  
- Other Mechanics :  
	- Smuggling Investigation : Fergus will offer quests related to investigating suspicious ships. Players may need to follow leads, inspect cargo, or deal with 
pirate crews. This could involve complex stealth or combat mechanics.  
	- Weather Sensing : Fergus has an innate ability to sense incoming storms or changes in the weather. He can provide quests that involve tracking down weather-
dependent threats or assisting with shipwrecks during storms.

---

	Skills :  
- Sea Sense  
	- Description: Fergus has spent his life at sea, giving him an uncanny ability to predict maritime dangers.  
	- Effect: Temporarily boosts the player's awareness of sea-related threats (e.g., pirates, sea monsters, storms). This might also increase the chance of 
encountering special sea-based encounters.

- Tidebreaker Strike  
	- Description: Fergus strikes with the force of the ocean’s tide, knocking enemies off balance.  
	- Effect: Deals physical damage and reduces the target’s mobility for a short duration.

---

	Quotes :  
- "The sea’s got a way of hiding its secrets, but I’ve learned to spot 'em."  
- "You’d be wise to listen to an old sailor. The sea doesn’t give second chances."  
- "Smuggling's not just a land game... it’s a sea game too."

