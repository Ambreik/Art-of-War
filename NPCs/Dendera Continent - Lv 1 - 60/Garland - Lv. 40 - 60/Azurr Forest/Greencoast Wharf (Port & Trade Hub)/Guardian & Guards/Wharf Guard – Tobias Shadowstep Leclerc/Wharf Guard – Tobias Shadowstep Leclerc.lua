			Wharf Guard – Tobias "Shadowstep" Leclerc
			
	Location : "Greencoast Wharf" – Upper Deck Watchtower

---

	Appearance :  
- Age: Early 30s  
- Attire: Wears lightweight leather armor designed for mobility and stealth. He carries a pair of daggers, perfect for quick, silent strikes.  
- Distinguishing Features: Tobias has a mysterious air about him. His eyes are sharp, and his movements are fluid, almost as if he’s always in motion. His dark 
brown hair is kept short, and his face is often hidden in shadow.  
- Equipment: Twin daggers, shortbow, and a cloak that blends into the night.  
- Posture: Always in motion, shifting between shadows or crouched in an alert position while patrolling the area.

---

	Personality : Tobias is a quiet, reserved individual with a keen sense of observation. He prefers working in the shadows and often handles tasks that require 
discretion. Though not particularly sociable, he’s highly effective in his role, and those who need his help find him to be sharp and reliable.

---

	Behaviour :  
- Tobias spends most of his time on the upper deck of the watchtower, surveying the entire wharf and its surroundings. He’s always scanning for trouble, 
particularly at night when the wharf is quieter.  
- He doesn’t engage in casual conversation, but when approached, he’ll share sharp insights about the wharf's security or give warnings about smugglers and 
thieves.  
- He’s vigilant when it comes to sneaky operations and often helps foil smuggling attempts by catching them before they happen.

---

	Role :  
- Role(s): Stealth Guard, Night Watch, Smuggler Tracker, Quest Giver  
	- Tobias serves as a silent guardian, keeping watch over the wharf at night and taking care of any stealth-related issues, such as smuggling. His quests usually
involve tracking down illicit activity or eliminating hidden threats.

---

	Mechanics :  
- Type : Guard, Quest Giver, Stealth Specialist  
- Reputation : Offers Reputation rewards with the Greencoast Wharf Guard faction.  
- Interaction Limitations : Available at night or after certain in-game events. Requires reputation with Greencoast Wharf Guard faction.  
- PvP : Tobias is not flagged for PvP, but attacking him will result in an immediate response from other guards.  
- Respawn Time : Not applicable.  
- Other Mechanics :  
	- Night Vision : Tobias has enhanced vision at night, allowing him to spot hidden enemies or objects that others cannot see.  
	- Silent Pursuit : Tobias excels at sneaking, allowing him to track enemies silently and gather information without being detected. This mechanic may be crucial
for certain quests, where stealth is necessary to gather evidence or apprehend criminals without alerting them.

---

	Skills :  
- Silent Strike  
	- Description: Tobias delivers a quick, precise strike that incapacitates the enemy with minimal noise.  
	- Effect: Deals moderate damage and silences the target for a short time, preventing them from calling for help.

- Cloak of Shadows  
	- Description: Tobias blends into the shadows, becoming nearly invisible.  
	- Effect: Grants temporary invisibility or increased stealth for a brief period.

