			Sister Eleanor Fayne  

	Location : Greencoast Wharf – Chapel of the Waterside

---

	Appearance :  
Sister Eleanor is an older woman, with a serene and gentle appearance. She wears a simple yet elegant white robe adorned with symbols of the sea and light, the fabric is embroidered with delicate golden threads. A rosary of small, polished seashells hangs from her neck. Her silver hair is neatly pulled back into a tight bun, and her soft eyes reflect a lifetime of wisdom and compassion. Her hands are weathered but steady, with rings representing various blessings she’s received throughout her life.

---

	Personality : Sister Eleanor is calm, spiritual, and always welcoming. She speaks with a slow and measured tone, offering words of encouragement and faith. 
Despite her serene exterior, she has a fierce devotion to protecting the sailors and travelers that pass through the region. She is very protective of her clients 
and treats them as if they were part of her extended family. Her manner is always gentle, and she speaks with an aura of authority that only those of great faith 
possess.

---

	Behaviour : Sister Eleanor is often found at the chapel, offering blessings to sailors before their voyages. She can be seen gently handing out small holy and 
protective charms to those who seek them. While not overly interactive, she does take the time to bless those who approach her. She will occasionally speak of the 
sea and the spirits that protect it, especially when asked for advice or guidance.

---

	Role :  
- Role(s) : Merchant (Holy & Protective Charms for Sailors)  
	Sister Eleanor sells a variety of charms, amulets, and talismans blessed with the divine protection of the sea gods. These items are believed to safeguard 
sailors from storms, sea monsters, and misfortune while traveling over water.

---

	Mechanics :  
- Type : Vendor  
- Reputation : Does not offer any reputation system or discounts.  
- Interaction Limitations : Available to all players, though the charms are priced based on the player’s reputation with local factions, particularly among sailors 
and travelers.  
- PvP : Sister Eleanor is not flagged for PvP, and players cannot attack her.  
- Respawn Time : Instant respawn if killed.  
- Other Mechanics : Players who have completed specific quests related to the sea or sailors might get a special charm or a small discount on items.

---

	Skills : This NPC have no skill or abilities.

---

	Quest :  
- Quest Title : Protection of the Waters  
	- Quest Type : Side Quest  
	- Objective(s) :  
		1. Collect rare sea shells or sacred coral from specific locations.  
		2. Return to Sister Eleanor with the items to be blessed for use in charms.  
	- Rewards :  
		- 200 XP  
		- A special charm for protection on the water  
		- Increased favor with local sailors and merchants  
	- Conditions :  
		- Requires Rank I reputation with local sailors or sea-based factions.  
	- Quest Dialogue :  
		- Opening : "The sea is a harsh mistress. I offer protection to those who sail her depths. Will you help me gather the sacred shells that the sea has given to me?"  
		- Mid-Quest : "Ah, the tides are shifting... can you sense it? I need these shells to bless your journey."  
		- Completion : "May the tides favor your journey, and may your path remain safe. Wear these charms, and remember: The sea is always watching."

---

	Quotes :  
- "May the winds guide your sails and the waters keep you safe."  
- "These charms are more than simple trinkets. They carry with them the blessings of the sea gods themselves."  
- "Come, traveler. The seas are dangerous. Let me protect you with a charm of the tides."

