			Oswald "The Riddlekeeper" Nix  

	Location : Greencoast Wharf – Riddle Cart

---

	Appearance : Oswald is a thin, eccentric man with wild, unkempt hair and a sharp, clever glint in his eyes. He wears an elaborate robe decorated with patterns 
of riddles and symbols, and a large, colorful hat adorned with feathers and jewels. He carries a staff topped with a crystal that seems to shimmer with light as he 
speaks. His appearance is both mysterious and charming, drawing curious onlookers to his cart where he displays his odd collection of herbs and cooking supplies.

---

	Personality : Oswald is a playful, enigmatic figure who loves puzzles, riddles, and word games. He speaks in riddles himself, challenging anyone who wishes to 
buy from him to first solve one of his cryptic puzzles. He delights in testing others’ wits and often gives subtle hints if the player seems particularly stuck. 
While he’s a bit of a trickster, he’s not malicious—just eager to see how clever his customers can be.

---

	Behaviour : Oswald’s cart is constantly surrounded by curious customers, each trying to crack one of his riddles. He doesn’t engage in typical conversations but
instead presents his riddles as challenges to anyone interested in purchasing his wares. If players fail to solve a riddle, he will simply repeat it or offer them 
another one, but always with a playful smile.

---

	Role :  
- Role(s) : Merchant (Herbalism & Cooking Supplies, but only if players solve his puzzles)  
	Oswald sells a variety of herbal and cooking supplies, but players must first solve one of his riddles before they can purchase anything from him.

---

	Mechanics :  
- Type : Vendor  
- Reputation : None—Oswald is more about intellectual skill than reputation.  
- Interaction Limitations : Players must solve one of his riddles to access his goods.  
- PvP : Oswald is untargetable, as he is a non-combat NPC.  
- Respawn Time : Instant respawn if killed.  
- Other Mechanics : Oswald’s riddles will change frequently, offering new challenges to returning players.

---

	Skills : This NPC have no skills or abilities.can be used for powerful potions.

---

	Quest :  
- Quest Title : The Riddle of the Sea  
	- Quest Type : Side Quest  
	- Objective(s) :  
		1. Solve Oswald’s special riddle about the sea.  
		2. Gather rare ingredients for a special recipe.  
	- Rewards :  
		- 200 XP  
		- A special herb or recipe for cooking  
		- A significant boost to reputation with herbalists and chefs.  
	- Conditions :  
		- Requires solving at least three of Oswald’s riddles to start.  
	- Quest Dialogue :  
		- Opening : "Ah, a seeker of riddles! Let me test your wits with one of my favorites. Solve it, and perhaps I’ll share something rare with you."  
		- Mid-Quest : "Ah, the sea is a mysterious place, full of riddles and answers. Find the right ingredient, and you might uncover its secret."  
		- Completion : "Well done, well done! You’ve cracked the code. Here’s your reward, and may the riddles of life always be in your favor."

---

	Quotes :  
- "Solve my riddle, and the goods are yours. Fail, and you must try again."  
- "A puzzle a day keeps the mind sharp. Are you up for the challenge?"  
- "Riddles are not just games—they’re keys to unlocking the secrets of the world."

