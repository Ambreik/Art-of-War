			Hildred "Needle" Carris  

	Location : Greencoast Wharf – Tailoring Booth

---

	Appearance : Hildred is a stout and sprightly woman with a knack for crafting and an eye for the finest fabrics. Her short, curly hair is tucked under a well-
worn bandana, and her hands are perpetually covered in thread and fabric stains. She wears a simple brown apron over her worn leather clothing, with a variety of 
sewing tools hanging from her belt. Despite her no-nonsense appearance, her bright smile and quick laugh make her approachable to any who are willing to learn the 
art of tailoring.

---

	Personality : Hildred is a warm, practical, and highly skilled tailor with a no-nonsense attitude. She’s proud of her work and has a sharp eye for quality. Her 
straightforward demeanor and witty remarks make her well-liked among those who seek her expertise. She’s quick to point out the flaws in others' work but is 
equally quick to offer advice or praise when deserved. She’s often busy with multiple projects at once, stitching up garments and creating items for travelers.

---

	Behaviour : When not working on orders or crafting, Hildred is busy organizing her collection of fabrics and sewing kits. She enjoys offering advice to aspiring
tailors or showing travelers how to fix a torn cloak. While she’s not one for idle chatter, she enjoys helping others and offering crafting tips. Her booth is a 
constant whirlwind of activity, with bolts of fabric, needles, and thread scattered around.

---

	Role :  
- Role(s) : Merchant (Fabrics & Sewing Kits for Crafting)  
	Hildred sells a variety of high-quality fabrics and essential tailoring supplies to those who wish to craft their own clothing, bags, or other fabric-based 
items.

---

	Mechanics :  
- Type : Vendor  
- Reputation : No specific reputation system, but players with a higher crafting skill might find that she gives them special crafting advice or unique items to 
help with their craft.  
- Interaction Limitations : Available to all players who wish to buy crafting materials.  
- PvP : Hildred cannot be attacked by players, as she is a non-targetable NPC.  
- Respawn Time : Instant respawn if killed.  
- Other Mechanics : Hildred’s stock refreshes frequently, and she occasionally offers special items during crafting events or festivals.

---

	Skills : This NPC have no skills or abilities. 
	
