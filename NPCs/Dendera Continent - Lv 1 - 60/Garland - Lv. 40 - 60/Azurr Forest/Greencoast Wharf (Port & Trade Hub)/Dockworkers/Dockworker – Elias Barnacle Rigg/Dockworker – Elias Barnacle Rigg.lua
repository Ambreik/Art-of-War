			Dockworker – Elias "Barnacle" Rigg  

	Location : "Greencoast Wharf" – Dockside

---

	Appearance :  
- Age: Mid 60s  
- Attire: Worn and weather-beaten clothes, often layered with a heavy coat that has seen better days. His hands are calloused and rough from years of work on the docks.  
- Distinguishing Features: A long, unkempt beard with bits of salt and grime, and a wrinkled face covered in laugh lines.  
- Equipment: A rusted hook used as a prosthetic in place of his missing hand, which he claims was lost in a battle with a giant squid.  
- Posture: Often hunched over, he moves slowly but with a certain rhythm, as if every movement is tied to a memory of the sea.

---

	Personality : Elias is the type to talk your ear off with exaggerated tales of adventure, sea monsters, and the "good ol' days." While his stories are often 
fanciful, they’re laced with the wisdom of someone who’s lived through rough seas and even rougher conditions. He has a heart of gold and is always eager to share 
his experiences, even if no one believes them.

---

	Behaviour :  
- Elias spends his days on the dock, regaling younger workers with tales of the sea and his "glorious" adventures. He rarely works, preferring to supervise and tell
stories instead.  
- He has a habit of muttering about “the Kraken” and "the Leviathan" during his breaks, though few pay attention anymore. Despite his tall tales, he’s respected for
his old age and experience, having seen the dock grow from a humble pier to the bustling wharf it is today.

---

	Role :  
- Role(s): Storyteller, Dockworker, Lorekeeper  
	- Elias may not be the most reliable worker, but his stories bring life to the docks and offer unique lore about the area. His insights into the wharf's 
history can be invaluable to those looking for clues or secrets of the past.

---

	Mechanics :  
- Type : Dockworker, Lorekeeper  
- Reputation : Can provide small reputation boosts with the Greencoast Wharf faction due to his long history with the port.  
- Interaction Limitations : Available for all players who visit the wharf.  
- PvP : Not flagged for PvP.  
- Respawn Time : Not applicable.  
- Other Mechanics :  
	- Lore Quests : Elias occasionally offers "Lore Quests," where players must track down lost artifacts or forgotten items connected to his sea stories. These 
quests often involve scavenger hunts or interactions with other NPCs, who may hold pieces of the puzzle.

---

	Quotes :  
- "Aye, the Kraken's tentacles were as wide as this dock, lad! Don’t believe me? Ask the captain!"  
- "You call it a fish? I’ve seen monsters bigger than your ship in the deep blue!"  
- "One hand’s enough when you’re quick enough... but I lost it fair and square to the sea."

