			Dockworker – Dale "Shifty" March  
	
	Location : "Greencoast Wharf" – Dockside

---

	Appearance :  
- Age: Mid 30s  
- Attire: A basic worker’s tunic, but always seems a bit too clean compared to others. He carries an air of always being ready to slip away.  
- Distinguishing Features: Dark, narrow eyes that are always darting around, a perpetually sly grin on his face. He’s well-groomed but seems out of place with his 
constant scanning of the area.  
- Equipment: A small pouch of coins hidden under his tunic, often flicking it in his hands when idle.  
- Posture: He’s constantly shifting, never fully at ease, as though he’s always waiting for the next opportunity to present itself.

---

	Personality : Dale is a gossip, and he knows every rumor circulating through the docks. He loves to eavesdrop, and his penchant for sharing gossip has earned 
him the nickname “Shifty.” He’s a bit of a schemer, always looking for the next bit of leverage or the next deal. He’s charming in his own way but can’t be trusted 
to follow through on promises unless there’s something in it for him.

---

	Behaviour :  
- Dale is often found loitering around the edges of the dock, listening in on conversations and offering up bits of information to the highest bidder. He’s good at 
making friends but even better at making enemies.  
- He’s not particularly good at physical labor but will get his hands dirty when it suits his agenda. His specialty is providing useful gossip or trading secrets 
for a price.

---

	Role :  
- Role(s): Gossipmonger, Dockworker, Quest Giver  
	- Dale may provide quests that involve collecting information or performing tasks based on what he’s overheard from other workers or travelers.

---

	Mechanics :  
- Type : Dockworker, Gossipmonger, Quest Giver  
- Reputation : May cause slight annoyance with other workers, especially if his rumors spark tension between factions.  
- Interaction Limitations : Available for players who enjoy gathering information or trading in rumors.  
- PvP : Not flagged for PvP.  
- Respawn Time : Not applicable.  
- Other Mechanics :  
	- Gossip Quests : Players may be tasked with verifying rumors or spreading rumors to gain favor with various factions or NPCs around the dock.  
	- Trade Secrets : Dale occasionally offers quests where players must retrieve items or information that he can use to his advantage in trading deals.

---

	Quotes :  
- "Oh, you didn’t hear? I’ve got the scoop on something big. Just need a little help with the details."  
- "Secrets aren’t worth much until you’ve got a price tag on them."  
- "You’ll want to know who’s looking for what around here. Trust me."

