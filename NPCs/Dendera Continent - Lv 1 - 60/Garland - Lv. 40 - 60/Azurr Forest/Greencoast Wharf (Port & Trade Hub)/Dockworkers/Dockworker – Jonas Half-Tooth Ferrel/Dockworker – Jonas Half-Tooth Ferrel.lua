			Dockworker – Jonas "Half-Tooth" Ferrel  

	Location : "Greencoast Wharf" – Dockside

---

	Appearance :  
- Age: Early 40s  
- Attire: A faded vest, patched trousers, and a wide-brimmed hat that never seems to come off.  
- Distinguishing Features: His most notable feature is his missing front tooth, hence his nickname. His eyes are always darting, as if looking for something—perhaps
his next gambling opportunity.  
- Equipment: A deck of cards always in his pocket, ready for a game.  
- Posture: He stands hunched, perpetually looking around with a suspicious air, ready to slip into a game of chance.

---

	Personality : Jonas is loud, brash, and never without a story or a gamble. He’s constantly chasing the next big win, often bragging about how he’s going to 
"hit it big" one day. His charm can get him out of tight spots, but his luck usually runs out. He has a tendency to run his mouth, and his gambling habits get him 
into trouble more often than not.

---

	Behaviour :  
- Jonas is a social creature, constantly surrounded by workers trying to get in on his bets or games. When he’s not working, he’s gambling, and he’s usually found 
in a heated discussion over who owes what. He’s often found at the dockside tables, eagerly dealing cards or dice for a wager.  
- He’s always looking for ways to make quick money, and he’s known to offer dubious deals or bets to players passing through.

---

	Role :  
- Role(s): Gambler, Dockworker, Quest Giver  
	- Though he’s not reliable for hard work, Jonas is adept at organizing games or offering betting opportunities. He also gives players quests involving gambling 
or tracking down lost bets and items related to his activities.

---

	Mechanics :  
- Type : Gambler, Dockworker, Quest Giver  
- Reputation : None specifically, though his actions can occasionally draw the ire of others at the wharf, particularly if he cheats.  
- Interaction Limitations : Open to all players willing to engage in his games or bets.  
- PvP : Not flagged for PvP.  
- Respawn Time : Not applicable.  
- Other Mechanics :  
   - Gambling Quests : Jonas may offer quests that involve tracking down someone who owes him a gambling debt or finding rare items that are part of his betting 
  schemes.  
   - Mini-Games : Players can engage in mini-games such as dice, cards, or betting games with Jonas, where the outcome may depend on luck or skill.

