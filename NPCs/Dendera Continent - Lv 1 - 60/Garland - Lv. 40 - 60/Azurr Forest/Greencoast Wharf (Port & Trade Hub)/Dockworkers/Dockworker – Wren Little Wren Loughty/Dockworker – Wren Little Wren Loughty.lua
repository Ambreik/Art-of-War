			Dockworker – Wren "Little Wren" Loughty  
	
	Location : "Greencoast Wharf" – Dockside

---

	Appearance :  
- Age: Late teens  
- Attire: Tight, practical clothing meant for agility, often dirtied from climbing and scurrying around the docks.  
- Distinguishing Features: A small, wiry frame, short brown hair, and bright, mischievous eyes. She has a perpetual grin and is always in motion, hopping from place
to place.  
- Equipment: A small satchel for carrying tools, a set of climbing gloves tied around her belt.  
- Posture: Always crouching or climbing, Wren seems to move faster than the others and often takes advantage of her small size to access hard-to-reach places.

---

	Personality : Wren is quick-witted, fast on her feet, and never afraid to get into places the others can’t reach. She’s often the one sent to retrieve tools 
from high spots or shimmy up a rigging to make sure everything is secure. While small in size, she’s a fierce worker and isn’t afraid to show it. Her energy and 
speed make her one of the best dockworkers, despite her size.

---

	Behaviour :  
- Wren is always climbing something, whether it’s the ropes of a ship or the rigging of a crane. She’s often seen darting up and down the docks, fetching tools or 
taking shortcuts through the scaffolding.  
- She enjoys teasing the other workers, especially the older men, and often pulls pranks when no one’s looking. But despite her antics, she’s highly respected for 
her speed and efficiency.

---

	Role :  
- Role(s): Agility Specialist, Dockworker  
	- Wren is critical to any tasks that require someone to get into tight or difficult spaces, making her indispensable. She may offer quick, agility-based quests 
that require the player to climb, jump, or navigate challenging terrain.

---

	Mechanics :  
- Type : Dockworker, Agility Quest Giver  
- Reputation : She earns respect from those who admire her skills but may be dismissed by others who think she’s too young or too quick for her own good.  
- Interaction Limitations : Wren is always moving, making it harder to pin her down for long conversations.  
- PvP : Not flagged for PvP.  
- Respawn Time : Not applicable.  
- Other Mechanics :  
	- Climbing Quests : Players may be tasked with climbing obstacles, retrieving items from high places, or assisting with securing ropes or rigging that only 
someone with quick reflexes and agility can handle.  
	- Race Challenges : Wren may challenge players to speed-based races around the dock, rewarding them with a boost in her favor if they win.

---

	Quotes :  
- "You think you can climb this faster than me? Better bring your A-game, mate!"  
- "Hey, I might be small, but I get places you can’t even dream of."  
- "Don’t mind me, just got to zip up there and fix this rigging. Be back in a jiff!"

