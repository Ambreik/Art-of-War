			Barkeep – Roland "Aleheart" Turner

	Location : Red Flue Stable Tavern, near the river.

---

	Appearance : Roland Turner is a large, jovial man in his late 30s with a thick, scruffy beard and a broad, welcoming smile. His face is round, with rosy cheeks,
and he has a booming voice that carries over the noise of the tavern. He wears a simple tavern keeper's tunic, with a stained apron and a patch of flour on his 
trousers, evidence of his many roles. His hair is dark, though a touch of gray is starting to creep in around his temples, and his hands are calloused from years of
serving drinks and handling mugs. His posture is relaxed, and he’s always ready with a laugh or a story for anyone who comes to the bar.

---

	Personality : Roland is the embodiment of warmth and hospitality. He has an infectious laugh, a sharp wit, and a deep love for his tavern and the people who 
visit. He is quick to offer a drink to anyone in need and can usually be found at the center of conversations, guiding them with his humorous anecdotes and 
boundless energy. Though he enjoys a good time, Roland also has a great sense of responsibility and will not allow troublemakers in his tavern. He’s a reliable 
source of gossip and rumor, often knowing the latest happenings in town before anyone else.

---

	Behaviour : Roland is constantly moving, whether he’s serving drinks, cleaning a table, or chatting with a regular. He stands behind the bar, offering drinks 
with a wide grin. He makes sure his customers are well-served and frequently checks in on them, keeping the mood light and festive. He is especially attentive to 
the stables, often talking to Garret about what’s going on with the horses and guests at the tavern.

---

	Role :  
- Role(s) : Barkeep, Quest Giver  
	Roland’s main role is to serve as the tavern’s barkeep, ensuring that everyone has a drink and a good time. He also has occasional side quests and rumors to 
offer.

---

	Mechanics :  
- Type : Vendor  
- Reputation : None  
- Interaction Limitations : Players can only purchase drinks, listen to gossip, or accept quests.  
- PvP : Roland cannot be attacked by players.  
- Respawn Time : No respawn time necessary, Roland is always present in the tavern.  
- Other Mechanics : Roland occasionally offers discounts for drinks to players who help him with tavern-related tasks, such as clearing out pests or retrieving a 
special ingredient.

---

	Skills : This NPC is not skillable. 

---

	Quest :  
- Quest Title : "Ale for the Road"  
	- Quest Type : Side Quest  
	- Objective(s) : Roland asks the player to retrieve a special ingredient from the nearby forest to make a rare brew for an important guest.  
	- Rewards : 200 XP, a special brew, and a one-time discount on tavern services.  
	- Conditions : Player must be level 5 or higher.  
	- Quest Dialogue :  
		- Opening : "I need a rare herb to brew a special drink for a guest. You wouldn’t mind running to the forest, would you?"  
		- Mid-Quest : "Did you find it yet? That herb’s a tricky one to come by."  
		- Completion : "You’ve returned! Let’s see what you’ve got. The brew will be perfect now."  
	- Additional Notes : The herb is guarded by a wild animal, making the quest a little more challenging.

