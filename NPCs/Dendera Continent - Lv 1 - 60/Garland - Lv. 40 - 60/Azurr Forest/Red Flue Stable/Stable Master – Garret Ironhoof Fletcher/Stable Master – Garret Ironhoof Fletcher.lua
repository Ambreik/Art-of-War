			Stable Master – Garret "Ironhoof" Fletcher  

	Location : Red Flue Stable Tavern, overseeing the stables near the tavern’s entrance.  

---

	Appearance : Garret is a tall, broad-shouldered man in his late 40s, with sun-weathered skin and short, salt-and-pepper hair. His sharp green eyes give him a 
no-nonsense look, and his calloused hands show years of working with horses. He wears a simple leather vest over a sturdy tunic, with thick riding gloves always 
tucked into his belt. A whip hangs at his side, though he rarely uses it—his presence alone is usually enough to keep the horses in check. His legs are slightly 
bowed from years of riding, and he moves with a slow but confident gait.  

---

	Personality : Garret is gruff but deeply knowledgeable about horses and mounts of all kinds. He has little patience for foolishness, but he respects those who 
take care of their animals properly. He has a soft spot for rare or unusual mounts and enjoys talking about their history and lineage. Though quiet, he has a sharp 
wit and a deep appreciation for the bond between riders and their animals. Despite his serious demeanor, he treats the tavern's regulars like family, always making 
sure travelers’ horses are well-fed and rested.  

---

	Behaviour : Garret spends most of his time tending to the horses, ensuring they are well-fed and properly groomed. He moves between the stable and the 
surrounding paddock, checking on the animals and making sure nothing is out of order. Occasionally, he’ll take a break to sit near the tavern entrance, watching 
over the road and listening in on conversations.  

---

	Role :  
- Role(s) : Stable Master, Vendor, Quest Giver  
	Garret oversees the stable, ensuring the well-being of the mounts. He also provides mount-related services and minor quests for players.  

---

	Mechanics :  
- Type : Vendor, Quest Giver  
- Reputation : None  
- Interaction Limitations : Players can interact with Garret to stable their mounts, purchase mount-related items, or accept small quests.  
- PvP : Garret cannot be attacked by players.  
- Respawn Time : Always present at the stable.  
- Other Mechanics : Players who have a reputation with the tavern can unlock discounts on stable services or gain access to special mounts.  

---

	Skills :  
- Skill 1 : Mount Care  
	- Description : Garret can heal, feed, or provide temporary buffs to a player's mount.  
	- Effect : Restores mount stamina or provides a speed boost for a limited time.  

- Skill 2 : Rare Mount Knowledge  
	- Description : Garret provides information about rare mounts in the world, helping players track down special creatures.  
	- Effect : Reveals hints about unique or hidden mounts in the game.  

---
	Quest :  
- Quest Title : "A Wild Ride"  
	- Quest Type : Side Quest  
	- Objective(s) : Garret asks the player to help tame a wild horse that has been causing trouble near the tavern.  
	- Rewards : 250 XP, a special saddle, or a chance to tame the wild horse.  
	- Conditions : Player must have a mount and at least Level 10 in Riding.  
	- Quest Dialogue :  
		- Opening : "There’s a wild stallion out there, strong as steel but stubborn as a rock. Think you can handle him?"  
		- Mid-Quest : "Careful now! That horse won’t go down easy."  
		- Completion : "Ha! You did it. That’s one fine steed. Take good care of him."  
	- Additional Notes : If successful, the player gains a rare horse. If they fail, they receive a consolation reward.  

---

	Quotes :  
- "A good horse is worth more than gold—if you treat it right."  
- "Keep your reins steady and your heels down, and you might just make it back in one piece."  
- "Most people think they ride the horse. Truth is, the horse lets them."  

