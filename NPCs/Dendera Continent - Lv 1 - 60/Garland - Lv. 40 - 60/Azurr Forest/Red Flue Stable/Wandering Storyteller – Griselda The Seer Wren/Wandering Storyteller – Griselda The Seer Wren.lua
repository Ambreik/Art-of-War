			Wandering Storyteller – Griselda "The Seer" Wren

	Location : Red Flue Stable Tavern, near the river.

---

	Appearance : Griselda is an elderly woman, hunched over slightly from age but still possessing a mysterious presence. Her long white hair is tied back in a 
loose braid, and her clothing consists of faded robes adorned with strange trinkets and symbols. Her eyes, though clouded with age, gleam with a knowing wisdom. She
carries a walking staff carved with runes and often holds a small pouch of items she uses for her stories. Her weathered face is lined with both age and countless 
tales.

---

	Personality : Griselda is enigmatic, speaking in riddles or cryptic phrases. She rarely reveals much about herself and is always vague about her past. She 
enjoys spinning tales and can be a little eccentric, especially when she gets caught up in her own stories. Though she appears harmless, there’s a strange aura 
around her, as though she knows more than she lets on. She’s always willing to share a story or a piece of advice for those who ask.

---

	Behaviour : Griselda is often found near the hearth in the tavern, her voice rising above the sounds of clinking glasses as she tells her stories. She’ll 
sometimes engage with players in conversation, hinting at events yet to come or sharing whimsical tales of the past. She’s mostly passive but can become animated 
when discussing lore, legends, and prophecies.

---

	Role :  
- Role(s) : Storyteller, Vendor  
	Griselda is a wandering storyteller who offers both entertainment and knowledge. She can share tidbits of the world’s history, lore, and secrets to players. 
Occasionally, she offers items tied to her stories.

---

	Mechanics :  
- Type : Vendor  
- Reputation : None  
- Interaction Limitations : All players can listen to Griselda’s stories, but only those with certain knowledge or items may gain further insights.  
- PvP : Griselda cannot be attacked by players.  
- Respawn Time : Griselda is always present when the player enters the tavern.  
- Other Mechanics : Griselda offers tales that may hint at upcoming quests, hidden locations, or items.

---

	Skills: This NPC is not skillable.

---

	Quest :  
- Quest Title : "Whispers of the Past"  
	- Quest Type : Side Quest  
	- Objective(s) : Griselda asks the player to gather rare artifacts from ancient ruins scattered across the world,which she will use to complete her latest tale.
	- Rewards : 300 XP, a rare trinket, and access to a special storyline.  
	- Conditions : Player must have explored at least two major regions.  
	- Quest Dialogue :  
		- Opening : "I need something… something lost to time. Find it, and I will weave your name into the tapestry of the old tales."  
		- Mid-Quest : "Do you have the artifacts? The past must be restored before the future can be written."  
		- Completion : "You’ve brought them back to me… now, the tale can be told."  
	- Additional Notes : The quest offers an insight into the world’s hidden history, unlocking new locations for exploration.

---

	Quotes:  
- "The past has a way of whispering to those who are willing to listen."  
- "A story is never just a story… it’s a key to something far greater."  
- "In every legend, there’s a grain of truth—if you know where to look."

