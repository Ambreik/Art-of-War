			Corvan Bristlemane  

	Location : The Windmere Rest, North High Peak, Sunnymead Island  

	Appearance : A rugged, middle-aged hunter with a weathered face, piercing amber eyes, and long, unkempt brown hair streaked with silver. He wears a thick fur-
lined cloak over hardened leather armor, reinforced with metal plating on the shoulders. A quiver of specialized hunting arrows rests on his back, and he carries a 
heavy crossbow strapped across his chest. His gloves are fingerless, revealing rough, scarred hands—evidence of years spent tracking and battling the dangers of the
mountain. His boots are caked in dirt, showing he rarely stays indoors for long.  

---

	Personality : Gruff but fair, Corvan is a man of few words. He values practicality over pleasantries and prefers actions over idle talk. He respects those who 
prove themselves capable in the wild but shows little patience for reckless adventurers who underestimate the dangers of North High Peak. He has a deep respect for 
nature and believes that every predator, even the deadly Echo Wraiths, has its place in the world.

	Behavior :
- Often found sharpening his crossbow bolts at a corner table in The Windmere Rest.  
- Frequently steps outside to scan the mountain slopes for signs of movement.  
- Will only engage in meaningful conversation if the player shows interest in hunting or tracking.  
- Occasionally mutters under his breath about "the things that lurk in the night."  

---

	Roles :  
- Wild Creature Hunter (Non-Bounty Hunter Players)  
	- Offers bounty quests to eliminate hostile creatures near the peak.  
	- Specializes in tracking dangerous wildlife (e.g., Echo Wraiths, mountain predators).  
	- Provides players with valuable survival tips for mountain exploration.  
	- Unlocks the passive "Tracking" ability after completing multiple quests.

- Bounty Hunter (For Players in the Bounty Hunter Organization)  
	- Offers quests to track and eliminate PK players or outlaws who have bounties on their heads.  
	- Provides critical information on high-value targets and locations.  
	- Specializes in tracking outlaws or PKs in remote areas.  
	- Can train players in tracking techniques and other bounty-hunting skills.  

---

	Mechanics :

	Wild Creature Hunter (For Non-Bounty Hunter Players) :
- Quest Type : Bounty Hunts for Creatures  
- Quest Frequency : One random daily quest, revolving around a wild creature in the North High Peak area.  
- Tracking Ability :  
	- After completing a series of hunts, players gain the passive ability "Tracking" which helps identify and locate species of monsters (including wild creatures)
within their region on the map. The ability will become more effective as players complete more hunts with Corvan.
- Reputation : Repeated assistance in wild creature bounties increases Corvan's trust in the player, unlocking better rewards, advanced hunting gear, and rare crafting materials.  
- Rewards for Wild Hunts :  
	- Hunter’s Cloak (minor stealth bonus)  
	- Echo Residue (for enchanted crafting)  
	- Special tracking tools  
- Interaction Limitations : Players must have at least basic combat or tracking skills to speak with Corvan; he dismisses those without survival skills.
- Behavior :  
	- Occasionally offers a mini-game or skill-based challenge to track and hunt the creatures successfully.

---

	Bounty Hunter (For Players in the Bounty Hunter Organization) :
- Quest Type : Tracking and eliminating outlaws, PK players, or high-profile bounties  
- Reputation : Players must earn favor with Corvan to unlock higher-tier bounties. The more successful bounties they complete, the more lucrative the rewards.  
- Training Mechanic : Corvan will train players in advanced bounty hunting techniques, including tracking hostile targets and navigating hostile territories.
- Rewards for Bounty Hunts :  
	- Bounty Hunter’s Cloak (grants increased movement speed while on target)  
	- Echo Residue (for crafting)  
	- Exclusive tracking tools  
- Behavior : Corvan offers players critical information on targets, including their last known location and specific behaviors. He will only train players 
affiliated with the Bounty Hunter Organization.
- Special Interactions : Occasionally warns of particular players with high bounties, offers special “dangerous” quests with extra rewards, and provides tips for 
ambushing outlaws.  

---

		Advanced Tracking Mechanic (For Both Hunters) :
	After completing a specific number of quests for Corvan, both types of players (Wild Creature Hunter and Bounty Hunter) unlock a passive skill called "Tracking"
that aids them in locating specific creatures or players, depending on their focus:
- Wild Creatures : Players can track down animal species or dangerous creatures on the map.
- Outlaws (Bounty Hunters Only) : Bounty Hunters can track down specific outlaws or PKs based on Corvan’s intel.

