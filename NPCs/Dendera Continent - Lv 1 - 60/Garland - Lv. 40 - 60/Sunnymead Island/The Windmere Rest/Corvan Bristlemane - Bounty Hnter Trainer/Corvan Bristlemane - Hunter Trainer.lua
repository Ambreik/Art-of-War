	Quest Example :  

	Quest Title : The Silent Stalkers 
- Quest Type : Hunting/Survival (Wild Creature Hunter)  
- Objective(s) : Track and eliminate a pack of Echo Wraiths haunting the mountain paths.  
- Rewards :  
	- Hunter’s Cloak (grants minor stealth bonus in wilderness areas)  
	- Echo Residue (rare crafting material)  
	- Increased Reputation with Corvan  
- Conditions :  
	- The hunt can only be attempted at night.  
	- Players must complete a mini-game to locate the wraiths.  
- Dialogue :  
	- Opening : "You hear them, don’t you? The whispers in the wind. They’re watching, waiting. If you think yourself capable, I’ve got a job for you."  
	- Mid-Quest : "Stay low, stay quiet. They see through the mist, but they listen more than they look. A wrong step, and you're the hunted."  
	- Completion : "Hmph. You’re not as reckless as I thought. Maybe there’s hope for you yet."  
- Lore : Adds to the mystery of Echo Wraiths, contributing to the overall lore of North High Peak.

---

	Bounty Hunter Quest Example :  

	Quest Title : Wanted: Falren the Outlaw  
- Quest Type : Bounty Hunt (Bounty Hunter)  
- Objective(s) : Track and eliminate Falren, a notorious outlaw with a bounty on his head.  
- Rewards :  
	- Bounty Hunter’s Cloak (movement speed increase while on target)  
	- Echo Residue  
	- Exclusive Bounty Hunter Tools  
- Conditions : Player must be affiliated with the Bounty Hunter Organization.  
- Dialogue :  
	- Opening : "Falren’s a slippery one, but he’s been seen up in the cliffs to the north. Get to him before someone else does."  
	- Mid-Quest : "Keep your distance. He’s got a nasty crew with him. Strike when the time’s right."  
	- Completion : "Impressive. I’ll be seeing more of you, I hope. There are bigger bounties out there."  

