			Professor Daeron Whitlock  

	Location : Alddan Field – Temporary Excavation Site  

---  

	Appearance : A tall, lean man in his late 50s, with silver-streaked brown hair tied back in a loose ponytail. His face is weathered, with deep lines around his 
sharp blue eyes, a sign of years spent studying ancient ruins under harsh conditions. He wears a long, dust-covered beige coat over a layered explorer’s vest, 
equipped with pockets filled with tools and parchment. A leather satchel, visibly worn from years of use, hangs at his side, and a pair of reading glasses rest on 
his nose. His hands are often smudged with dirt and ink, showing his dedication to fieldwork.  

---  

	Personality : Professor Whitlock is methodical, pragmatic, and deeply passionate about uncovering lost knowledge. He values discipline and careful study over 
reckless exploration, often lecturing his apprentices about patience and accuracy. He is approachable but has little tolerance for those who lack respect for 
history. When discussing discoveries, he speaks with enthusiasm, his usual stern demeanor softening when he shares his knowledge.  

---  

	Behaviour :  
- Often found poring over excavation maps and notes, discussing theories with his apprentices.  
- Occasionally stops mid-conversation to jot down ideas in his notebook.  
- Tends to sigh in frustration when players ask him about treasure rather than historical significance.  

---  

	Role :  
- Role(s) : Trainer (Archaeology), Quest Giver  
	- Teaches players the Archaeology profession, allowing them to learn excavation techniques and artifact restoration.  
	- Offers archaeology-related quests, rewarding players with knowledge, tools, or rare historical relics.  

---  

	Mechanics :  
- Type : Trainer, Quest Giver  
- Reputation : Associated with the Archaeologists' Guild; players can earn reputation through excavation quests.  
- Interaction Limitations : Only players who have unlocked the Archaeology profession can train with him.  
- PvP : Cannot be attacked.  
- Respawn Time : N/A (permanent NPC).  
- Other Mechanics :  
  - Occasionally offers special excavation contracts for advanced archaeologists.  
  - Has unique dialogue if the player has a high reputation with the Archaeologists' Guild.  

---  

	Quest :   
- Quest Title : "Buried Knowledge"  
	- Quest Type : Side Quest  
	- Objective(s)**:  
		1. Locate and excavate three buried relics in Alddan Field.  
		2. Return to Professor Whitlock for an analysis of the artifacts.  
	- Rewards : Archaeology experience, a rare historical journal entry, and a small relic pouch.  
	- Conditions : Must be at least Rank I in Archaeology.  
	- Quest Dialogue :  
		- Opening :  
			- "History is not just written in books, but in the soil beneath our feet. If you truly wish to call yourself an archaeologist, prove it—unearth three relics from Alddan Field and bring them to me intact."  
		- Mid-Quest :  
			- "Careful, these artifacts are delicate. One mistake, and we lose centuries of history in an instant."  
		- Completion :  
			- "Ah, fascinating! These relics belonged to a forgotten sect of scholars. Their inscriptions... remarkable! This will take time to decipher, but you've done well."  
	- Additional Notes :  
		- If the player has a high reputation, they receive an extra lore entry about the artifacts.  

