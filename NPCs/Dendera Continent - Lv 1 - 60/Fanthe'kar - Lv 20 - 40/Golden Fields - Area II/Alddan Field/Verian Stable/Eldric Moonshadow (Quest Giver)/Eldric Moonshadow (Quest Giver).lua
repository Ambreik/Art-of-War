			Eldric Moonshadow (Quest Giver)

	Location : "Golden Fields Tavern" (By the fireplace) 

---  

	Appearance : A tall, thin man with silver-streaked hair and a dark cloak that appears almost magical. His pale blue eyes have an otherworldly 
intensity, and his fingers are ink-stained from years of scholarly work. Eldric carries a satchel filled with scrolls and tomes.  

---  

	Personality : Calm and enigmatic, Eldric speaks with measured precision, often pausing to consider his words. He is deeply curious and passionate 
about uncovering ancient mysteries, eager to share his knowledge with like-minded individuals.  

---  

	Role :  
- Scholar and Hidden Location Guide :  
	Eldric provides quests to explore the nearby ruins, offering historical context and guidance for adventurers seeking hidden treasures or lore.  

---  

	Mechanics :  
- Type : Quest Giver, Lore Keeper  
- Reputation : Rewards exploration-focused players with new questlines and historical insights.  
- Interaction Limitations : Accessible to all players.  
- PvP : Cannot be attacked.  
- Respawn Time : N/A  
- Other Mechanics : Unlocks new lore-based questlines as players complete his tasks.  

- Function :  
	- Quest Giver : Provides quests to explore the "Ancient Ruins" and locate the entrance to the "Barh Khaon Cave" dungeon.  
	- Lore Keeper : Shares in-depth historical insights about the ruins and their magical remnants.  

---  

	Quotes :  
- "The ruins are more than stone and dust; they are echoes of a forgotten age." 
- "Knowledge is the greatest weapon in any adventurer’s arsenal." 
- "If you’re brave enough to seek the truth, I can guide you."