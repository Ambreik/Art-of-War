			Edric Vayne  

	Location : Golden Fields – Northern Area  

---  

	Appearance : Edric Vayne is a middle-aged man with a strong, weathered frame, the result of years spent in the field. He has short, graying brown hair, a neatly
trimmed beard, and piercing green eyes that seem to scrutinize everything around him. His attire consists of a sturdy, earth-toned archaeologist’s coat layered over
a reinforced leather vest, designed for both practicality and protection. His belt holds various excavation tools, including a brush, chisel, and a small notebook 
filled with notes and sketches. A silver pendant depicting an ancient symbol hangs from his neck, a relic from one of his past discoveries.  

---  

	Personality : Edric is a seasoned archaeologist with a deep respect for history and an unwavering dedication to uncovering the past. He is methodical, patient, 
and has little tolerance for reckless behavior, especially when it comes to handling artifacts. Though he values tradition, he is not entirely against using modern 
techniques if they serve the purpose of preserving historical integrity.He expects discipline from his apprentices and believes that true knowledge is earned 
through perseverance. Despite his strict nature,he has a deep passion for storytelling and will often share tales of past expeditions with those who earn his trust.

---  

	Behaviour :  
- Often seen carefully examining artifacts or sketching notes about nearby ruins.  
- Occasionally sighs in frustration when his apprentices argue about excavation methods.  
- Will stop to speak with players who show a genuine interest in archaeology, but dismisses those who seem careless.  

---  

	Role :  
- Role(s) : Trainer, Quest Giver  
	Edric serves as a mentor to young archaeologists, teaching excavation techniques and historical preservation. He also provides players with archaeology-related 
quests, rewarding them with knowledge, experience, and sometimes rare historical artifacts.  

---  

	Mechanics :  
- Type : Trainer, Quest Giver  
- Reputation : Tied to the Archaeologists' Guild. Players with high reputation can access exclusive excavation sites.  
- Interaction Limitations : Requires at least a basic Archaeology skill to train with him.  
- PvP : Cannot be attacked.  
- Respawn Time : N/A (persistent NPC).  
- Other Mechanics : Occasionally provides insight into hidden locations or lost relics based on the player's reputation and completed quests.  

---  

	Quest :  
- Quest Title : "The Lost Script of Eldoria"  
	- Quest Type : Side Quest  
	- Objective(s  Recover fragments of an ancient script from a nearby excavation site, carefully cleaning and reconstructing them.  
	- Rewards : Archaeology experience, historical notes, and a chance to uncover a hidden lore entry.  
	- Conditions : Requires at least Rank I in Archaeology.  
	- Quest Dialogue :  
		- Opening : "Ah, you have the hands of a seeker, but do you have the patience of a scholar? This script is more fragile than it looks. Treat it with care, and you may unlock secrets lost to time."  
		- Mid-Quest : "Patience, patience. Rushing history is the fastest way to lose it."  
		- Completion : "Well done. You handled these fragments with the respect they deserve. Perhaps you are ready for a greater challenge."  

---  

	Quotes :  
