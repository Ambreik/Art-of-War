			Lira Sunhoof (Stablekeeper)
			
	Location : "Southern Abyll Town"

---

	Appearance : "Lira" is a tall, graceful woman with long, dark hair tied into a practical braid. Her skin is sun-kissed from hours spent outdoors, 
and her bright green eyes sparkle with a deep connection to the animals she cares for. She wears a simple leather tunic with patches of hay and dirt, 
always carrying a small pouch of herbs and treats for the horses. A soft smile is always present as she interacts with the mounts and adventurers 
alike.

---

	Personality : Lira is warm-hearted and patient, with a natural affinity for horses and all creatures of the land. She’s known for her nurturing 
demeanor, speaking gently to both animals and players. Lira is a hands-on person, often found mucking out stables or gently brushing down a tired 
mount. Her calm and encouraging nature extends to her mount-related quests, where she acts as both a mentor and a guide, helping players understand 
their mounts and improve them.

---

	Role :  
- Stablekeeper & Quest Giver :  
	Lira manages the stable, providing players with food and items for their mounts. She offers quests aimed at improving the quality of horses and 
mounts through Metin2-style improvement mechanics, which may involve feeding the mounts, training them, or completing specific tasks to raise the 
horse’s level and stats.

---

	Mechanics :  
- Type : Stablekeeper & Quest Giver  
- Reputation : No specific reputation, but successful completion of mount training quests increases players' bond with Lira and their mount's 
capabilities.  
- Interaction Limitations : All players with mounts can interact with Lira, though certain mount improvement quests may require specific mount levels 
or conditions.  
- PvP : Lira is not flagged for PvP and cannot be attacked by players.  
- Respawn Time : N/A (Lira is a permanent NPC.)  
- Other Mechanics : Lira offers various food items for mounts and provides quests for improving the mounts’ stats, with special training sessions and 
care requirements, in line with the Metin2-style system.

