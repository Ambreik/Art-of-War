			Jena Hallowstone (Storyteller)

	Location : "Southern Abyll Town"

---

	Appearance : "Jena" is a woman in her late fifties, with graying hair tied back in a loose bun. Her pale skin and deep-set eyes speak of years 
spent poring over ancient texts and artifacts. She wears simple yet elegant robes decorated with symbols of history, and carries a scroll case with 
her at all times. Her glasses are perched on the edge of her nose as she often reads from them or consults her notes.

---

	Personality : Jena is a calm, thoughtful individual with a deep passion for the history of the region. She speaks with authority but does so in a 
warm, welcoming manner. Her voice carries a sense of reverence for the past, and she enjoys sharing her knowledge with those who show interest. She is
eager to impart her wisdom, though she can become lost in her thoughts when discussing the ancient events of the land.

---

	Role :  
- Storyteller :  
	Jena serves as the keeper of Southern Abyll’s history. She shares fascinating stories about the region’s past, including ancient ruins, long-
forgotten events, and the origins of the town itself. She does not offer quests, nor does she run a shop or crafting service. Her primary purpose is 
to enrich the players' experience with the lore of the area.

---

	Mechanics :  
- Type : Storyteller  
- Reputation : None  
- Interaction Limitations : Players can interact with Jena to hear her stories, but no quests, items, or other services are offered.  
- PvP : Not flagged for PvP  
- Respawn Time : N/A (Permanent NPC)  
- Other Mechanics : Jena's stories provide valuable lore and context for the game world but do not influence gameplay mechanics directly.

