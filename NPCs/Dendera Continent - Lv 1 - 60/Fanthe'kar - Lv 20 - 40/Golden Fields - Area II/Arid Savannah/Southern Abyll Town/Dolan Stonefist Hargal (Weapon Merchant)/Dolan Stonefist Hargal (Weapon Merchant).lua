			Dolan "Stonefist" Hargal (Weapon Merchant)

	Location : "Southern Abyll Town"

---

	Appearance : "Dolan" is a burly man with thick, muscular arms, evidence of his craft as a weaponsmith. His hands are large and calloused, and his 
stone-gray eyes gleam with the sharpness of a seasoned fighter. He wears a leather apron over his weathered tunic, his wide belt adorned with various 
tools and a sheathed weapon.

---

	Personality : Dolan is direct, with a no-nonsense attitude. He speaks with a deep, rumbling voice and often gives curt nods when addressing 
players. While not overly warm, he is respectful and values strong, dependable adventurers. His demeanor suggests that he respects skill and grit over
charm.

---

	Role :  
- Role(s) : Weapon Merchant  
	Dolan offers weapons and basic combat gear for those preparing for adventures. He is known for providing affordable, functional weapons, primarily
focused on swords, axes, and shields, all crafted with care and practicality in mind. 

---

	Mechanics :  
- Type : Vendor  
- Reputation : No reputation associated.  
- Interaction Limitations : Available to all players regardless of faction or reputation.  
- PvP : Dolan is not flagged for PvP. He cannot be attacked by players.  
- Respawn Time : Instant respawn upon death.  
- Other Mechanics : Sells basic weapons, shields, and combat gear for low-level adventurers. Does not offer any special items, enchantments, or 
crafting services.

---

	Quotes :  
- "If you need a blade that won't break, you're in the right place."  
- "Crafting simple weapons is no art, but I make sure they hold up when the time comes." 
- "Take care of your weapon, and it'll take care of you."