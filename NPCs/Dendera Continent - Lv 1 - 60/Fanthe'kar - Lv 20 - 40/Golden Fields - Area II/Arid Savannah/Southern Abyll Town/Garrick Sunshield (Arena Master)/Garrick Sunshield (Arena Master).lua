			Garrick Sunshield (Arena Master)

	Location : "Southern Abyll Town Arena"

---

	Appearance : "Garrick" is a tall, broad-shouldered man with a sun-weathered face, his skin a shade darker from countless hours in the open arena. 
He wears golden armor, symbolizing his role as the protector of the town’s combat grounds. His piercing eyes reflect years of experience in battle, 
and his posture is firm and commanding, standing tall as an embodiment of strength and discipline.

---

	Personality : Garrick is stern, no-nonsense, and highly disciplined. He commands respect with his calm yet authoritative demeanor. He is a teacher
at heart, focused on honing the skills of those who seek his training. While not overtly friendly, he has a quiet admiration for those willing to push
their limits in the arena.

---

	Role :  
- Role(s) : Arena Master & Trainer  
	Garrick oversees the arena where players can engage in combat training to enhance their defensive abilities. He does not offer quests or crafting,
but his role as a trainer in defense techniques makes him an important NPC for those looking to improve their combat survivability. He also serves as 
a point of reference for players looking to prove their strength in the arena.

---

	Mechanics :  
- Type : Trainer  
- Reputation : No specific reputation required, but players must show readiness for advanced combat training.  
- Interaction Limitations : Available to all players who wish to train in defensive combat techniques.  
- PvP : Garrick is not flagged for PvP but may occasionally challenge players to practice duels in the arena (non-lethal).  
- Respawn Time : Immediate respawn if killed.  
- Other Mechanics : Provides training that improves the player’s defensive abilities,including techniques for blocking, dodging, and counterattacking.

---

	Quotes :  
- "You’ve come to the right place if you seek to sharpen your defenses. A good defense is the key to victory."  
- "The arena is not for the weak. Only those who can stand their ground are welcome here."  
- "Remember, the key to survival is not how hard you strike, but how well you defend."