			Nerak Highmane (Stable Master)

	Location : "Stumb Stables"

---

	Appearance : "Nerak Highmane" is a robust man in his early 50s, with a weathered complexion and sharp, piercing green eyes. His long, graying hair
is tied back into a practical braid, and his heavy leather armor, adorned with horsehair tassels, speaks of years spent working with mounts. He 
carries a thick, braided whip and wears sturdy riding boots,giving him the commanding presence of someone deeply attuned to the beasts under his care.
A small emblem depicting a galloping steed is pinned to his chest.

---

	Personality : Gruff yet approachable, Nerak is a no-nonsense individual who respects hard work and dedication. He has a soft spot for adventurers 
who show genuine care for their mounts. His sharp tongue is balanced by his warm laughter, which surfaces when he recounts tales of his early taming 
days. While he can be strict in his dealings, his guidance is invaluable for those seeking to improve their riding or taming skills.

---

	Role :  
- Stable Master :   
	Nerak serves as the Stable Master at Stumb Stables, providing key services for players, including quests for taming mounts, upgrading mount 
capabilities, and offering a wide range of mount-related equipment. He also shares insights on regional wildlife that can be tamed.

---

	Mechanics :  
- Type : Quest Giver, Vendor, Trainer  
- Reputation : Offers rewards based on favor progression, including advanced mount equipment and unique mount-taming quests.  
- Interaction Limitations : Requires players to have achieved a minimum of Favor Rank I with Stumb Stables to access advanced services.  
- PvP : Non-combat NPC; cannot be attacked.  
- Respawn Time : Not applicable, as Nerak is essential and invulnerable.  
- Other Mechanics :  
  - Provides mount training sessions to improve riding skill.  
  - Occasionally triggers local events involving wild mount stampedes.  

