			Korrik Emberhoof (Blacksmith) 

	Location : "Stumb Stables"  

---  

	Appearance : "Korrik" is a large, muscular figure with a long, fiery-red mane that matches his thick beard. His broad shoulders are covered in 
soot-streaked leather armor, with tools strapped to his waist. His eyes glow amber, like the forge fire he works by, and his hands are calloused from 
years of hammering metal. A smoldering glow seems to emanate from his hands when he works, adding a faint aura of heat to his surroundings.  

---  

	Personality : Gruff and practical, Korrik speaks in a deep, rumbling voice. He has little patience for idle chatter, but his loyalty to his craft 
and the village is unmatched. His demeanor softens when discussing metalwork, as he takes great pride in his skill. He’s quick to offer advice, but 
always with a hint of sternness, believing in the value of hard work.  

---  

	Role :  
- Blacksmith  : Provides services for repairing, upgrading, and crafting items. He also offers quests related to crafting and materials found in Riven
Fields.  

---  

	Mechanics :  
- Type : Craft NPC, Quest Giver, Skillable NPC  
- Reputation : Reputation earned through crafting quests, unlocking enhanced crafting recipes, and exclusive upgrades.  
- Interaction Limitations : Available to all players, with some crafting services restricted by item tiers or level of progression.  
- PvP : Cannot be attacked or flagged for PvP.  
- Respawn Time : Resets after 10 minutes if killed during an event.  
- Other Mechanics :  
	- Allows item upgrades and repairs, though there is a risk of failure, causing the item to be destroyed.  
	- Offers quests to gather rare materials and upgrade equipment, with difficulty scaling based on the player’s crafting level.  
	- Can craft unique items with special bonuses, requiring rare materials or components.  

---  

	Quotes :  
- "Metal is my language, and it speaks the truth. Only the strongest survive the fire."  
- "Careful with your gear—one wrong strike, and you might lose more than you bargained for."  
- "I’ll make sure it’s done right, but don’t expect perfection without sacrifice."  