			Jorlin Deeppouch (Merchant)

	Location : "Stumb Stables"  

---  

	Appearance : "Jorlin" is a tall, burly dwarf with a thick black beard and a friendly smile. He wears a leather vest with pouches hanging from
every available strap. His hands are always busy arranging his wares, and his eyes glimmer with the excitement of a good trade. He carries a large 
pouch at his side, constantly replenished with rare items.  

---  

	Personality : Jorlin is pragmatic, business-minded, and jovial. He enjoys haggling and often offers deals based on a player's reputation with him. 
His personality shines through in his interactions, always offering a cheerful word or a wink when striking a deal.  

---  

	Role :  
- Shop : Sells various items, including rare materials, gear, and occasionally mysterious goods that change based on the player's favor rank.  

---  

	Mechanics :  
- Type : Shop  
- Reputation : Stock changes based on Favor Rank; higher favor unlocks exclusive goods.  
- Interaction Limitations : Available to all players; items vary depending on player’s relationship with Jorlin.  
- PvP : Cannot be attacked or flagged for PvP.  
- Respawn Time : Immediate reset after each transaction.  
- Other Mechanics :  
	- Rotating stock based on player’s favor rank.  
	- Items range from basic wares to rare and exclusive goods.  

---  

	Quotes :  
- "Ah, you’ve got an eye for fine things, don’t you?"  
- "A good deal is a fine art—one I’ve mastered!"  
- "You’ll find no better stock in all of Dendera, for a price worth your coin!"