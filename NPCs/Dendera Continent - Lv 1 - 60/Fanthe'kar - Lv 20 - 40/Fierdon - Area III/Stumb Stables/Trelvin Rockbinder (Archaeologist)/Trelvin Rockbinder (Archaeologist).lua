			Trelvin Rockbinder (Archaeologist)

	Location : "Stumb Stables"  

---  

	Appearance : "Trelvin" is a middle-aged gnome with a salt-and-pepper beard and round glasses perched on his nose. He is clad in a patched 
explorer’s coat with pockets filled with relics, maps, and tools. His fingers are often stained with ink or soil, and he has a well-worn satchel 
filled with archaeological finds. His curious eyes sparkle with excitement when discussing ancient ruins or artifacts.  

---  

	Personality : Trelvin is inquisitive, passionate, and a bit scatterbrained. He’s always eager to share knowledge and stories about his discoveries
but can sometimes lose focus when a new relic catches his attention. Despite his absent-mindedness, his deep love for archaeology drives him to seek 
knowledge and share it with others.  

---  

	Role :  
- Quest Giver : Provides quests tied to archaeology, unlocking relics and achievements. He also serves as the Archaeologist Profession Trainer, 
teaching new players the intricacies of the trade.  

---  

	Mechanics :  
- Type : Quest Giver, Profession Trainer  
- Reputation : No specific faction-based rewards, but offers achievements for relic collection.  
- Interaction Limitations : Available to all players; Archaeology profession must be unlocked.  
- PvP : Cannot be attacked or flagged for PvP.  
- Respawn Time : Reset after 5 minutes if killed during a specific event or archaeological expedition.  
- Other Mechanics :  
	- Unlocks quests that require the player to discover and retrieve rare artifacts.  
	- Allows players to train and level up in the Archaeology profession.  

---  

	Quotes :  
- "Every relic tells a story, and it's our job to listen." 
- "The past is waiting to be uncovered, if you’ve got the patience to dig."  
- "Ah, another piece of history! You’ll be amazed what it can teach you."  