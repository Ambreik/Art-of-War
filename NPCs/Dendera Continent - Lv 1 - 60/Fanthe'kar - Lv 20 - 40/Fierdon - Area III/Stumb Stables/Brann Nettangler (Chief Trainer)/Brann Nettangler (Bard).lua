			Brann Nettangler (Bard)

	Location : "Stumb Stables"

---

	Appearance : "Brann" is a stocky, middle-aged man with a ruddy complexion and a thick beard peppered with grey. His apron is splattered with flour and stains from countless meals, and his hands are always busy with a spoon or knife. He wears a simple yet practical tunic and carries an assortment of spices in small pouches tied to his belt.

---

	Personality : Brann is cheerful and boisterous, often sharing stories of his culinary adventures. He has a hearty laugh and a competitive streak, 
challenging others to create dishes as flavorful as his. He values effort and creativity in cooking above all else.

---

	Role :  
- Quest Giver :  
	Brann provides players with quests tied to the Cooking profession. His tasks range from gathering rare ingredients to preparing elaborate feasts 
for the village.

---

	Mechanics :  
- Type : Quest Giver  
- Reputation : Cooking profession reputation rewards, including special recipes and tools.  
- Interaction Limitations : Accessible to players pursuing the Cooking profession.  
- PvP : Cannot be attacked or flagged for PvP.  
- Respawn Time : N/A.  
- Other Mechanics :  
	- Offers quests to find rare ingredients and unlock advanced cooking techniques.  
	- Rewards players with unique recipes and culinary tools based on their Favor rank.  
	- Provides buffs or temporary boosts through prepared meals after completing certain quests.  

