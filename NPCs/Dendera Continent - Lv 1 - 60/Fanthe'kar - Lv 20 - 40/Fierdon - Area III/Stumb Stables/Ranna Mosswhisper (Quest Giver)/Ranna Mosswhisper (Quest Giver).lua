			Ranna Mosswhisper (Quest Giver)

	Location : "Stumb Stables"  

---  

	Appearance : "Ranna" is a calm, elderly woman with silver hair tied in a loose braid, her skin weathered by years of exposure to the elements. She
wears flowing robes of earthy greens and browns, adorned with nature-themed patterns. Her soft blue eyes sparkle with wisdom, and she carries a staff 
that seems to hum with energy, marked by the faint glow of ancient runes.  

---  

	Personality : Ranna exudes a serene aura, speaking in gentle tones, with an almost hypnotic quality. She has a deep connection to nature and its 
cycles, often offering cryptic advice to those seeking her guidance. Her kindness is apparent, but there is an underlying strength in her presence, 
hinting at the many secrets she has learned throughout her life.  

---  

	Role :  
- Quest Giver : Offers repeatable, faction-based quests that often involve gathering, exploration, or the defeat of specific enemies. She plays an 
important role in advancing the main questline, offering players insight into the larger mysteries of the world.  

---  

	Mechanics :  
- Type : Quest Giver  
- Reputation : Faction-based rewards, unlocking more powerful quests and exclusive lore.  
- Interaction Limitations : Available to all players, though quests might vary based on faction or progress in the main questline.  
- PvP : Cannot be attacked or flagged for PvP.  
- Respawn Time : Resets after 15 minutes if killed during an event.  
- Other Mechanics :  
	- Provides repeatable quests for gathering, exploring, and dealing with specific creatures.  
	- Unlocks special quests that lead deeper into the main story.  

