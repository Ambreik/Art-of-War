			Dock Guards (Skillable NPCs)

	Location : "Riddleport Docks"  

---

	Appearance : A disciplined group of guards, aged between 25 to 40. They wear dark leather and chainmail uniforms bearing the Riddleport insignia. 
Equipped with standard swords, shields, and occasionally spears, they project a sense of readiness and authority.  

---

	Personality : Focused and no-nonsense, these guards are professional, courteous, but firm in maintaining order. They are approachable for basic 
inquiries but do not tolerate disruptions or unlawful behavior.  

---

	Role :  
- Skillable NPCs  
	Guards tasked with ensuring the safety and order of the docks. They have no quest, shop, or crafting mechanics but contribute to the immersive 
world as interactive NPCs.  

---

	Mechanics :  
- Type : Skillable NPCs  
- Reputation : None.  
- Interaction Limitations : Open to all players, no specific requirements.  
- PvP : Not flagged for PvP.  
- Respawn Time : Immediate respawn for maintaining area security.  
- Other Mechanics : Patrol the docks, respond to nearby threats, and assist in combat against hostile NPCs.  

