			Erya Saltfoot (Side Quest Giver)

	Location : "Riddleport", near the western dock, often seen sitting on a weathered crate by the shoreline.  

---

	Appearance : A weathered older woman with gray-streaked hair tied in a loose bun. She wears a patched cloak and carries a small satchel filled with 
maps and relics. Her hands are calloused, showing her years of experience as a scavenger.  

---

	Personality : Curious and animated, with a love for storytelling. Erya often shares tales of sunken ships and lost treasures. She is approachable 
but has a shrewd side when discussing treasure splits.  

---

	Role :  
- Side Quest Giver  
	- Provides players with quests tied to local legends, underwater explorations, and recovering relics from shipwrecks around Riddleport.  

---

	Mechanics :  
- Type : Quest Giver.  
- Reputation : No reputation system tied to her quests.  
- Interaction Limitations : Available to all players, regardless of faction or skill set.  
- PvP : Cannot be attacked.  
- Respawn Time : Immediate if needed for quests.  

---

	Quotes :  
- "The sea holds secrets beyond your wildest dreams. Care to help me uncover them?"  
- "If you find anything shiny down there, bring it back to me!"
- "Careful out there. The ocean can be as ruthless as it is beautiful."  