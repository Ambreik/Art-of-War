			Myrin Seaborn (Main Questline Giver)

	Location : "Riddleport", in a modest office near the docks, surrounded by naval charts and correspondence.

---

	Appearance : A middle-aged woman with sharp features and piercing blue eyes. She wears a tailored naval uniform adorned with subtle insignias, 
signifying her authority. Her hair is neatly tied back, and her demeanor is both commanding and approachable.

---

	Personality : Focused, diplomatic, and pragmatic. Myrin speaks with purpose, offering players both guidance and challenges. She shows genuine 
concern for the people of Riddleport and the stability of the region.

---

	Role :  
- Main Questline Giver  
	- Provides pivotal quests tied to the overarching story of Riddleport, advancing the main plotline involving regional conflicts, hidden dangers, 
and the player’s involvement in these events.  

---

	Mechanics :  
- Type : Quest Giver.  
- Reputation : Utilizes the favor system tied to the "Dendera Army" faction and the current settlement.  
- Interaction Limitations : Requires a minimum favor rank for advanced story progression.  
- PvP : Cannot be attacked.  
- Respawn Time : Immediate, as necessary for story progression.  
- Other Mechanics : Quests often unlock new areas, allies, or faction-based rewards.  

---

	Quotes :  
- "Riddleport’s future rests on our ability to weather the coming storm. Are you ready to do your part?"  
- "We need someone resourceful—someone like you—to lead this effort."  
- "Your actions here could determine the fate of the entire region." 