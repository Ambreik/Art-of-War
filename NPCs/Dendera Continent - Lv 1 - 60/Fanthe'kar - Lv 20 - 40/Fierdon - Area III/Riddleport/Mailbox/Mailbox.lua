			Mailbox  

	Location : Near the plaza fountain in Riddleport.  

---

	Appearance : sturdy, weathered metal mailbox with decorative engravings resembling ocean waves. Its exterior is painted blue and gold, with a slot 
for depositing letters and parcels.  

---

	Personality : None, as it is a static, non-interactive object beyond its intended function.  

---

	Role :  
- Mailbox : Allows players to send and receive in-game mail, including items and gold transfers.  

---

	Mechanics :  
- Type : Functional Object.  
- Interaction Limitations : Accessible to all players.  
- Other Mechanics : Supports faction-based mailing and delivery of system messages, player-to-player trades, and auction house notifications.  

