			Eran Netcaster (Fisherman Trainer)
			
	Location : "Main Dock"  

---

	Appearance : A middle-aged man with a sun-worn face and a lean, muscular build. He wears a wide-brimmed hat and a vest covered in fishing hooks 
and bait. His hands are calloused, and he carries a finely carved fishing pole. A large net is slung over one shoulder.

---

	Personality : Eran is a jovial and patient mentor who enjoys sharing his knowledge with aspiring fishermen. He’s deeply passionate about fishing 
and always has a story about the "big one" that got away. Friendly and approachable but fiercely protective of the fishing community.

---

	Role :  
- Role(s) : Fisherman Trainer, Quest Giver, Shopkeeper.  
	- Teaches fishing mechanics and skills for players choosing the Fisherman Profession.  
	- Provides quests, including those tied to fishing-specific tasks and a general quest for all players to catch a rare fish.  
	- Sells exclusive bait types, fishing gear, and other supplies unavailable elsewhere.  

---

	Mechanics :  
- Type : Trainer, Shop, Quest Giver.  
- Reputation : Uses Favor mechanics to unlock additional bait types, special fishing gear, and rare recipes.  
- Interaction Limitations : Requires players to have the Fisherman Profession for advanced training and shop access. Non-profession players can accept
general quests.  
- PvP : Cannot be attacked by players.  
- Respawn Time : Standard NPC respawn time.  
- Other Mechanics : Advanced fishing skills unlock upon reaching higher Favor ranks.  

---

	Quotes :  
- "Patience, my friend! The best catches come to those who wait."  
- "This bait? Only the finest for the sharpest anglers."  
- "Think you can outfish me? Let’s see what you’ve got!"