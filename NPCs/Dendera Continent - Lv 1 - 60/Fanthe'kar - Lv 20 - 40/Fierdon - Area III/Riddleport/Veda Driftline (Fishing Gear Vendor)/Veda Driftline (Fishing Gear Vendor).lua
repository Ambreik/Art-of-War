			Veda Driftline (Fishing Gear Vendor)

	Location : "Close to the Shipyard"  

---

	Appearance : A middle-aged man with a sun-weathered face and a friendly demeanor. Veda wears a vest adorned with fishing lures and hooks, and his 
wide-brimmed hat shades his eyes from the bright port sun.  

---

	Personality : Veda is cheerful and easygoing, with a knack for making conversation about anything related to fishing or the sea. He often offers 
helpful tips to budding fishermen.  

---

	Role :  
- Vendor :  
	- Sells fishing gear, such as rods, hooks, and bait, alongside unique decorative items for personal boats.  
	- Stock evolves based on the player's **Favor Rank**, unlocking higher-tier items and exclusive decorative goods as relationships improve.  

---

	Mechanics :  
- Type : Vendor.  
- Favor System : Rewards players with access to better inventory items as they build a relationship with Veda through quests or purchases.  
- Interaction Limitations : No crafting or quest-giving mechanics.  

---

	Quotes :  
- "Need a good rod or a touch of flair for your ship? I’ve got just the thing."  
- "A fisherman’s tools are as important as his patience."  
- "Keep building your reputation with me, and you might see something truly special in stock."  