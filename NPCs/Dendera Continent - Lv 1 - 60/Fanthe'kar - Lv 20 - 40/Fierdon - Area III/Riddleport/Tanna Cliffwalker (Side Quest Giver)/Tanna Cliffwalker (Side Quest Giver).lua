			Tanna Cliffwalker (Side Quest Giver)

	Location : "Riddleport", near the cliffside trail leading to nearby caves.

---

	Appearance : A rugged adventurer in her late 30s, Tanna has sun-weathered skin, windswept dark hair, and a sturdy build. She wears practical 
climbing gear with a touch of flair, including a bright scarf and a carved walking stick adorned with feathers.

---

	Personality : Enthusiastic and adventurous, Tanna exudes a passion for exploration. She is encouraging and eager to share her love for uncharted
paths but can be impatient with those who lack her zeal.

---

	Role :  
- Side Quest Giver : Offers quests focused on exploring the cliffs and caves surrounding Riddleport,uncovering hidden treasures, and charting new paths.

---

	Mechanics :  
- Type : Side Quest Giver.  
- Reputation : Quests improve favor with Riddleport settlers and explorers.  
- Interaction Limitations : Available to all players but offers additional rewards for explorers and adventurers with high favor.  
- PvP : Cannot be attacked.  
- Respawn Time : Immediate for quest interactions.  
- Other Mechanics : Unlocks exploration-themed achievements and provides guidance for finding rare collectibles.

---

	Quotes :  
- "The cliffs hold more secrets than most imagine—are you ready to find them?"  
- "Careful with your footing. One slip, and it’s a long way down."
- "I’ve marked some caves worth investigating. Bring back anything unusual."  