			Lira Sandwell (Child with Dog)

	Location : "Gringgle Lake"  

---  

	Appearance : A small girl with curly auburn hair tied into messy pigtails. Wears a slightly tattered dress and sturdy boots, holding a stick in 
one hand. Her dog is a scrappy mutt with mismatched fur, always alert and at her side.  

---  

	Personality : Lira is mischievous but kind-hearted. She has a fearless curiosity and speaks with childlike enthusiasm, often boasting about her 
dog’s bravery. She’s determined to help protect her lakeside home.  

---  

	Role :  
- Role(s) : Quest Giver  
	Provides a few playful but meaningful quests related to protecting the lake and exploring the area.  

---  

	Mechanics :  
- Type : Quest Giver  
- Reputation : Non-skillable NPC.  
- Interaction Limitations : Available to all players; quests are aimed at low to mid-level adventurers.  
- PvP : Non-PvP NPC but will defend herself and call her dog for assistance if attacked by monsters.  
- Respawn Time : 5 minutes if killed.  
- Other Mechanics :  
	- Lira’s stick attack deals light damage to nearby enemies.  
	- Her dog lunges at attackers, dealing moderate damage and applying a brief slow effect.  

---  

	Quotes :  
- "Wanna see my dog’s best trick? He can scare off anything!"  
- "You think monsters are scary? They’ve never seen a stick like mine!"  
- "Gringgle Lake is our home, and I’ll fight to keep it safe!"  