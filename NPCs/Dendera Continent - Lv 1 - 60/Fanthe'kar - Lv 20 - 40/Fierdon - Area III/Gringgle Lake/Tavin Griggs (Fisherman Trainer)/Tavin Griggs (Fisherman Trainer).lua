			Tavin Griggs (Fisherman Trainer)

	Location : "Gringgle Lake"  

---  

	Appearance : A sturdy middle-aged man with weathered skin and sun-bleached hair. Wears a wide-brimmed hat, a patched tunic, and sturdy boots. 
Carries a fishing rod over one shoulder and a tackle box strapped to his belt.  

---  

	Personality : Tavin is patient and jovial, always ready to share a story or fishing tip. He speaks with a warm, gravelly voice and has an encouraging demeanor toward novice and experienced fishers alike.  

---  

	Role :  
- Role(s) : Fisherman Trainer, Quest Giver, Shop  
	Offers fishing-related quests and training to those in the fisherman profession. Provides gear and bait for players via his shop.  
	Have a quest for regular players to offer.
	
---  

	Mechanics :  
- Type : Trainer, Shop, Quest Giver  
- Reputation : Skillable NPC with Favor rewards for Fisherman players.  
- Interaction Limitations : Fisherman profession players receive exclusive quests, but one quest is available for all players.  
- PvP : Non-PvP NPC.  
- Respawn Time : 10 minutes if killed.  
- Other Mechanics : Offers items such as rods, lures, and unique baits, as well as advice on fishing hotspots around Gringgle Lake.  

---  

	Quotes :  
- "Out here, it’s just you, the water, and the fish. No better way to spend a day."  
- "Cast your line where the ripples break—that’s where the big ones lurk."  
- "Even a greenhorn can catch a trophy with the right bait."