			Escort Soldier (Dual Blades)



	Location : 
*Dynamic : 
Only appears during "Stormreach Supply Run"
Spawns attached to individual caravans

	Travels through:
* Sunveil Plains
* Windrazor Pass
* Frostbreak Valley
* Stormreach Watch

---

	Role & Function : 

* Primary mobile melee defender for assigned caravan
* Specializes in:
	* Fast engagement
	* Intercepting flanking enemies
	* Eliminating fast or light threats
* Protects only the caravan it is assigned to
* Can assist:
	* Other escort NPCs (soldiers, archers, constructors)
* Cannot:
	* Transfer protection to other caravans

---

	Appearance : 

* Medium armor, flexible and lightweight
* Twin short swords (one in each hand)
* Minimal plating, reinforced leather and chain mix
* No shield
* Face partially covered by a hood or half-mask
* Lean, agile posture
* Always in motion, rarely stands idle

---

	Combat Classification : 

* Skillable NPC
* High movement speed
* Fast attack speed
* Lower defense than spear soldiers
* Designed to hunt agile or light enemies

---

		Skills & Abilities

	Twin Cascade Combo

* Type : Multi-hit melee combo
* Animation :
	* 4 consecutive slashes
	* Alternating blades in fluid motion

	Effect :
* Delivers rapid strikes to a single target
* Final hit applies "Bleed" (minor DoT)
* Slight forward movement during the combo
* Can chase moving targets briefly

	Usage Behavior :
* Used when engaging:
	* Fast enemies
	* Casters
	* Light armor targets
* Will attempt to finish weakened enemies quickly

---

	Behavior & AI : 

* Prioritizes:
	* Enemies approaching caravan sides or rear
	* Fast attackers
	* Ranged or caster threats
* Avoids:
	* Tanking elite monsters head-on
* Will reposition constantly
* Rarely remains stationary
* Will disengage and re-engage fluidly

---

	Assistance Logic : 

	Can assist:
* Other escort NPCs from nearby caravans
	Cannot assist:
* Other caravans directly
* Will not abandon its assigned caravan radius

	If it strays too far:
* Returns automatically to its own caravan

---

	Interaction : 

* Not directly interactable by players
* Cannot receive player commands
* Affected by:
	* Rally effects from Caravan Leader
	* Defensive buffs from players

---

	Scaling : 

	Scales with:
* Number of players participating
* Caravan tier
* Event difficulty

	At higher tiers:
* Faster combo execution
* Higher bleed chance
* Slight survivability increase

---

	Flavor Quotes (Combat Barks)

*(Short vocal cues during battle)*

* “You’re not touching the cargo!”
* “Try getting past me!”
* “Too slow!”
* “Clear the flank!”
* “I’ll cut you down!”

