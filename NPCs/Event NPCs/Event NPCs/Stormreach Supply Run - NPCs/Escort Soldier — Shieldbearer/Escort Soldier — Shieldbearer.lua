			Escort Soldier — Shieldbearer


	Location : 

*Dynamic : 
Only appears during "Stormreach Supply Run"
Spawns attached to individual caravans

	Travels through:
* Sunveil Plains
* Windrazor Pass
* Frostbreak Valley
* Stormreach Watch

---

	Role & Function : 

* Primary frontline defender for assigned caravan
* Specializes in:
	* Crowd control
	* Disrupting enemy advances
	* Holding choke points
* Acts as the main tank unit of escort formations

	Protection Rules:
* Protects only its assigned caravan
* Can actively cooperate with other Shieldbearer units

	* Will assist nearby caravans if their frontline is collapsing
	* Will form temporary shield lines with other Shieldbearers

---

	Appearance :
* Heavy plated armor with reinforced pauldrons
* Large rectangular or kite shield
* One-handed sword
* Thick leather belt with spare straps and pouches
* Helmet with narrow visor slit
* Broad, grounded stance
* Frequently braces shield into the ground when idle

---

	Combat Classification : 

* Skillable NPC
* High defense
* Moderate movement speed
* Moderate attack speed
* Designed to absorb damage and control space

---

	Skills & Abilities : 

---

	1) Shield Bash Wave

* Type : AoE frontal control
* Animation :
	* Raises shield
	* Slams it forward into the ground

	Effect :

* Pushes enemies backward in a frontal cone
* Interrupts casting and channeling
* Applies brief *Dazed* (movement slow)
* Generates high threat

	Usage Behavior :
* Used when:
	* Enemies cluster in front of caravan
	* Caravan is under pressure
* Used to reset enemy positioning

---

	2) Shield Breaker Combo

*(Primary / Most-used Skill)*

* Type : Control + burst damage
* Animation :
	* Charges forward with shield
	* Smashes enemy
	* Follows with heavy sword overhead strike

	Effect :
* Shield hit:
	* Pushes target backward
	* Stuns for short duration
* Sword strike:
	* Deals high single-target damage
* Generates very high threat

	Usage Behavior :
* Preferred opener on:
	* Elite monsters
	* Large beasts
	* Leaders / Matriarch enemies
* Used more frequently than Shield Bash Wave

---

	Behavior & AI :

* Always positions at:
	* Front of caravan
	* Choke points
* Prioritizes:
	* Largest enemies
	* Enemies attacking caravan HP
* Uses Shield Breaker Combo aggressively
* Uses Shield Bash Wave defensively

---

	Cooperation Logic : 

* Detects nearby Shieldbearers within range
* If another caravan’s frontline is failing:
	* Moves to assist temporarily
	* Forms dual shield wall behavior
* Returns to its assigned caravan once threat stabilizes

---

	Interaction : 

* Not directly interactable
* Can be buffed and healed by players
* Reacts to:
	* Caravan Leader rally buffs
	* Player defensive auras

---

	Scaling : 

	Scales with:
* Event difficulty
* Number of participants
* Caravan tier

	Higher tiers grant:
* Longer stun duration
* Wider Shield Bash Wave cone
* Increased survivability

