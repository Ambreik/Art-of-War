			Escort Archer — Boar Rider

	Location : 

*Dynamic : 
Appears only during "Stormreach Supply Run"
Assigned to specific caravans
Moves with the caravan from:

* Sunveil Plains
* Windrazor Pass
* Frostbreak Valley
* Stormreach Watch

---

	Role & Function : 

* Primary ranged support for caravans
* Designed to:
	* Engage enemies at medium–long range
	* Control air and flanking threats
	* Harass fast or fleeing enemies
* Provides mobile ranged cover

	Protection Rules:
* Protects only its assigned caravan
* Can assist other NPCs (soldiers, leader) but:
	* Never switches caravans
	* Will not abandon its caravan position

---

		Mount — Wild Boar

	Appearance : 
* Large armored boar with iron plates on shoulders and tusks
* Thick bristles along spine
* Leather harness and saddle
* Scarred hide, battle-trained look

	### Mount Traits : 
* Fast acceleration
* Short burst charges
* Resistant to fear and knockback
* Can trample smaller enemies
* Will not flee when rider is attacked

---

	Appearance : 
* Archer in medium armor
* Hooded cloak flowing behind
* Composite bow or heavy longbow
* Quiver with barbed and weighted arrows
* Light spear or dagger as backup

---

	Combat Classification : 

* Skillable NPC
* High mobility
* Medium damage
* Low defense
* Designed for kiting and ranged pressure

---

		Skills & Abilities

---

	1) Boar Charge Shot

* Type : Mobility + damage
* Animation :
	* Boar charges forward
	* Archer fires multiple arrows while moving

* Effect :
	* Damages enemies in path
	* Slows enemies hit
	* Can break weaker enemy formations

	Usage Behavior :
* Used to:
	* Clear flanks
	* Intercept fast enemies
	* Escape when surrounded

---

	2) Piercing Tusk Arrow

* Type : Single-target high damage
* Effect :
	* Fires a heavy arrow
	* Deals high damage
	* Applies armor reduction debuff

	Usage Behavior :
* Used on:
	* Elite enemies
	* Leaders
	* Armored targets

---

	3) Suppression Volley

* Type : Area denial
* Effect :
	* Fires rapid arrows in a small zone
	* Enemies inside suffer:
		* Reduced movement speed
		* Reduced attack speed
	
	Usage Behavior :
* Used when enemies cluster
* Supports Shieldbearers and Dual Sword soldiers

---

	Behavior & AI : 

* Always circles around the caravan
* Maintains medium distance from enemies
* Prioritizes:
	* Flying enemies
	* Casters
	* Fast attackers
* Avoids direct melee unless forced

---

	Cooperation Logic : 

* Supports:
	* Shieldbearers holding chokepoints
	* Dual Sword soldiers engaging elites
* Focus fires with:
	* Other Boar Riders
	* Caravan Leader

---

	Scaling

	Scales with:
* Number of players
* Event tier
* Caravan difficulty

	Higher tiers grant:
* Faster charge cooldown
* Wider volley area
* Increased mount durability

