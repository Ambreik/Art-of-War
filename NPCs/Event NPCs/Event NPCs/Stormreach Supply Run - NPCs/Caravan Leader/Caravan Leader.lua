			Caravan Leader

	Location : 
*Static : "Sunveil Market Town – Caravan Staging Area" (before event start)
*Dynamic : During "Stormreach Supply Run" event, travels with caravan through:
1. Sunveil Plains
2. Windrazor Pass
3. Frostbreak Valley
4. Stormreach Watch

---

	Role & Function : 
* Leads all caravans during the "Stormreach Supply Run" mini-event
* Acts as:
	* Quest giver & turn-in NPC
	* Event phase controller
	* Combat support NPC
* Provides:
	* Location-based dialogue
	* Tactical callouts
	* Emergency buffs
* Required interaction for:
	* Event sign-up (solo or auxiliary players)
	* Guild coordination confirmation
* Can temporarily command allied NPC escorts

---

	Appearance : 
* Tall, broad-shouldered mercenary frame
* Wears reinforced leather-and-steel hybrid armor
* Twin swords mounted on his back
* Long dark cloak bearing Stormreach insignia
* Scarred face, stern eyes
* Carries himself with disciplined authority
* Slightly taller than escort soldiers and archers

---

	Combat Classification : 

*Skillable NPC
* Assists players in combat
* Does not replace player roles
* Will actively engage priority threats
* Cannot be controlled by players

---

	Skills & Abilities : 

	1. Twin Arc Cleave (AoE)

* Type : Circular AoE
* Effect :
	* Spins with both swords, damaging all enemies in radius
	* Applies "Armor Fracture" (minor defense reduction)
	* Used when surrounded

---

	2. Rallybreaker Surge (AoE)

* Type : Shockwave AoE
* Effect :
	* Slams blades into the ground
	* Deals damage + slows enemies
	* Grants nearby allies brief movement speed bonus
	* Used during heavy waves or elite threats

---

	3. Vanguard Push (Frontal Push & Damage)

* Type : Frontal Cone
* Effect :
	* Charges forward
	* Pushes enemies back
	* Deals moderate damage
	* Interrupts enemy casts
	* Used to break enemy formations near caravan

---

	Behavior & AI

* Prioritizes:
	* Threats closest to caravan
	* Elite or leader enemies
* Will reposition constantly
* Will not chase fleeing enemies far from caravan
* Avoids pulling enemies toward caravan path
* Enters defensive stance if caravan HP drops below 40%

