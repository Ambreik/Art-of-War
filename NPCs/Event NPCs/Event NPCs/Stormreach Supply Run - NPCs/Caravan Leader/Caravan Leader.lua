---

		Special Event Mechanics

	Tactical Callouts :

	Rhegar will issue short warnings:
* “Ambush incoming!”
* “Hold the line!”
* “Protect the rear!”
* “Form up!”

	These can trigger:
* Minor buffs
* Aggro shifts
* Visual alerts

---

		Emergency Intervention

	If caravan HP drops below 25%:
* Rhegar gains temporary:
	* Increased damage
	* Faster cooldowns
* Activates “Last Stand Protocol”:
	* Prioritizes elite enemies
	* Becomes more aggressive

---

	Relationship to Other Caravan NPCs

* Commands:
	* Escort Soldiers
	* Mounted Archers
	* Constructors
* Can call them to reposition
* Will protect wounded escort NPCs first before players

---

	Rewards & Reputation

* Grants:
	* Reputation with Sunveil Market Town
	* Reputation with Stormreach Watch
* Unlocks:
	* Rare caravan-related titles
	* Dialogue recognition on future runs

