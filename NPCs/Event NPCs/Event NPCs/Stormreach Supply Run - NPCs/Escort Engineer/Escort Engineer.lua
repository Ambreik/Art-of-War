			Escort Engineer



	Location : 
*Dynamic : 
	* Spawns only during "Stormreach Supply Run"
	* Assigned to specific caravans
	* Follows the caravan along the full route:
		* Sunveil Plains → Windrazor Pass → Frostbreak Valley → Stormreach Watch

---

	Appearance : 

* Wears reinforced leather overalls
* Protective helmet with built-in goggles
* Tool belt with hammers, wrenches, tongs, and repair kits
* Shoulder guards made from scrap metal
* Sturdy boots with metal tips

	Optional visual touches :
* Smudges of grease and ash on arms
* Small backpack carrying spare wheels, nails, rope

---

	Role & Function :

* Primary Role : Caravan support & repair
* Combat Role : Defensive only — fights back when attacked
* Tankiness : High health, medium armor
* Behavior :
	* Stays close to wagons / caravan
	* Moves slowly but steady
	* Prioritizes repairing damage and reinforcing wagons when possible
	* Will not pursue enemies unless directly threatened

---

	Combat / Interaction :

* Engagement Trigger : Only attacks monsters that attack them or caravan wagons
* Weapon : Heavy spanner / hammer / makeshift wrench
* Attack Style : Single target melee swing with knockback

	Special mechanics :

* Can repair damaged wagons while moving slowly
* Repair speed depends on player proximity — players near the engineer slightly boost efficiency

---

	AI Behavior :

* Keeps a tight radius around wagons
* Avoids terrain hazards and obstacles when moving
* Will follow caravan path automatically
* Retreats slightly if attacked heavily, but never abandons wagon

	Repair Priority :

1. Wagon HP < 50%
2. Caravan Leader’s wagon / health
3. Nearby NPC units (soldiers, mounted archers) if they are close

---

	Combat Scaling :

* Health and armor scale with event tier and player count
* Damage reflected is low but sufficient to deter early attacks
* Resilient to crowd control (stagger / knockdown) to maintain caravan protection

