			Garrin Muld – Daily Mining Questline (5 Days)

#### **Quest 1 – Ore Reconnaissance**

* **Objective:** Collect 10 **Copper Veins** from the cliffs near the waterfall streams.
* **Mechanic:** Mines have **higher HP** on this island. Players must break the node completely to gather ores.
* **Reward:** 50 gold + minor XP for profession progression.

#### **Quest 2 – Iron Vein Survey**

* **Objective:** Gather 8 **Iron Deposits** from specific cave nodes.
* **Mechanic:** Some nodes spawn only during the day; can remain until night if unmined.
* **Reward:** 60 gold + small consumable (Energy Draught).

#### **Quest 3 – Quartz Crystal Excavation**

* **Objective:** Mine 5 **Quartz Crystals** from waterfalls’ cavern walls.
* **Mechanic:** Random spawn locations near streams; encourages exploration.
* **Reward:** 70 gold + minor potion.

#### **Quest 4 – Rare Shard Collection**

* **Objective:** Collect 3 **Runestone Fragments** found near fortified walls or rocky outcrops.
* **Mechanic:** Rare nodes; can appear once per in-game day; encourages cautious exploration.
* **Reward:** 80 gold + minor XP + small chance for **Miner’s Ration**.

#### **Quest 5 – Master Miner Challenge**

* **Objective:** Mine 1 **special node** (could be any high-value ore) located in a hazard zone with increased monster density.
* **Mechanic:** Node has **highest HP** among daily quests; players must stay alert for hostile encounters while mining.
* **Reward:** 100 gold + minor XP + consumable reward (Energy or Health Potion).

