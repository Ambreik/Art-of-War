			Garrin Muld – Miner Trainer

**Role:** Miner Trainer, Daily Gathering Quest Giver (5-day mini-line), Ore-Related Tasks

**Location:** Sunken Encampment, Isle of Ashen Cliffs

**Appearance:**
Garrin Muld is a burly, weathered man in his mid-50s, with calloused hands and a streak of coal dust across his face. He wears reinforced leather mining gear, a belt filled with small pickaxes and chisels, and sturdy boots. His helmet has scratches from years of work in caves and cliffs. Despite his rough exterior, his eyes reflect a careful intelligence and pride in his craft.

**Behavior:**

* Non-hostile, interacts safely with players.
* Can perform combat (skillable), using a mining pick as a weapon if attacked by monsters in the area.
* Provides mining training to players of relevant professions (primary focus: Mining).
* Offers daily ore-gathering quests for **5 consecutive days**, after which the daily quests stop until reset (for new players or after weekly reset).

---

### **Chat / Interaction Lines** (Non-quest):

* “These cliffs hold more than just stone… watch your back while gathering.”
* “Not all veins are easy to break, some will test your strength.”
* “I’ve seen miners lose more than tools out here… keep your wits about you.”

