			Commander Edran Holt

### **1. General Overview**

**Name:** Commander Edran Holt
**Role:** Storyline Quest Giver (Main Questline Initiator)
**Faction:** Human Alliance (Capital City Military Division)
**Location:** *The Sunken Encampment*, Lost Island (Eastern shoreline zone)
**NPC Type:** Questgiver / Event NPC
**Map Marking:** Golden Exclamation Mark (Storyline)
**Appearance:** Veteran human commander in aged armor with a dark blue cloak and a sigil of the Capital’s army on the chestplate. He carries a longsword and a broken banner strapped to his back — a remnant from the Second Great War.
**Behavior:**

* Paces near a broken cannon overlooking the sea.
* Turns toward the player when approached, occasionally glancing toward the horizon.
* Idle voice lines: “Still no word from the scouts… something’s wrong.” / “Mirath’s shadow lingers here. I can feel it.”

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

### **2. Narrative Context**

Commander Edran Holt was dispatched from the Capital City to investigate the disappearance of scouting units sent to the Lost Island. Unknown to most, he served under Aric himself during the Second Great War and once fought alongside Mirath before his betrayal. Edran’s mission is personal — not only to confirm Mirath’s survival but to ensure the traitor’s curse ends once and for all.

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

### **3. Behavior and Interactions**

* **Interaction Distance:** Standard (4 meters).
* **Combat Flag:** Non-combat NPC (cannot be attacked).
* **Animation Loop:**

  * Idle → Patrol → Observe → Speak → Idle.
  * Occasionally interacts with the nearby cannon (kneels, inspects, adjusts strap).
* **Quest Availability Indicator:** Golden Exclamation Mark (visible from minimap).

