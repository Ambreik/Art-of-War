			Commander Edran Holt – Main Storyline Quest
			
---

### **1. Questline: “Echoes of the Traitor’s Oath”**

#### **Quest Type:** Main Storyline

#### **Quest Level Range:** Mid to High-Level (Balanced for 3–5 player groups)

#### **Repeatability:** One-time (storyline)

#### **Quest Giver:** Commander Edran Holt

#### **Quest Receiver:** Same NPC (Edran Holt)

#### **Associated Locations:**

* *The Sunken Encampment* (starting and ending point)
* *Northern Ruins* (document recovery)
* *Cavern of Shattered Faith* (evidence gathering zone)
* *Fort Mirath* (optional combat objective)

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

### **2. Quest Stages**

#### **Stage 1 — Introduction**

**Dialogue:**

> *Commander Holt stands by the remnants of a rusted cannon, eyes locked on the horizon.*
>
> “So you’ve come from the Capital. Good. We’ve lost contact with my scouts, and the island reeks of something… foul. The legends spoke of Mirath’s betrayal during the Second Great War, but I fear they weren’t just legends.”
>
> “Your orders are simple — confirm if Mirath’s army still walks this cursed land. Bring me proof — documents, insignias, or whatever you can find tied to his name.”

**Objectives:**

* Search the *Northern Ruins* for signs of Mirath’s army.
* Collect **3x “Mirath’s Insignias”** (dropped by human encounters shouting Mirath’s name).
* Find **1x “Tattered Banner Fragment”** inside the ruin’s chest.

**Environmental Notes:**

* Insignias drop from *Elite Human Encounters* (Mirath’s Loyalists).
* Banner Fragment is guarded by an *Elite Guard Captain*.

**Stage Reward:** Medium XP, 1000 Gold.

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

#### **Stage 2 — Evidence of the Living**

**Dialogue (Upon returning):**

> *Edran inspects the insignias, eyes narrowing.*
>
> “These are no replicas. His men still carry his crest… Mirath truly lives. Or worse — refuses to die.”
>
> “We need more proof. Something tangible that links him to this place. My scouts mentioned an ancient cavern, east of here — one they called the ‘Cavern of Shattered Faith’. If any trace of him remains, it’ll be there.”

**Objectives:**

* Enter the *Cavern of Shattered Faith*.
* Retrieve **“Mirath’s Journal Fragment”** from a locked chest deep within.
* Defeat the **Cursed Lieutenant** guarding the chest.

**Stage Reward:** Medium XP, rare crafting material (randomized).

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

#### **Stage 3 — Confirmation**

**Dialogue (Completion):**

> *Edran’s eyes narrow as he inspects the items brought back.*
>
> “These… these are Mirath’s banners. His crest, unchanged. He *is* alive then… trapped perhaps, but still breathing.”
>
> “You’ve done well, soldier. I’ll prepare a report to the Capital — and we’ll plan the next move carefully. This isn’t over.”

**Objectives:**

* Deliver Mirath’s Journal Fragment and Banner to Edran Holt.
* Speak with Archer Rhenna Vale for secondary objectives (unlocks *Chains of the Faithless* quest).

**Final Reward:**

* **Gold:** 2500
* **XP:** High-tier
* **Item:** *Living Water Sample* (Unique Consumable) — Restores full HP and Mana instantly (Cooldown: 30 minutes).
* **Unlocks:** Island Event *“The Last Breath of Mirath”* (periodic boss spawn event tied to island rotation).

