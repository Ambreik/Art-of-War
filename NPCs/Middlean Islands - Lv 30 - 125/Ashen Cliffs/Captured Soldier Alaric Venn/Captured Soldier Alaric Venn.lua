			Captured Soldier Alaric Venn

### **1. General Overview**

**Name:** Alaric Venn
**Role:** Storyline Quest NPC / Rescue Target
**Faction:** Human Alliance (Capital City Military Division)
**Location:** *The Sunken Encampment*, Lost Island
**NPC Type:** Captured Soldier / Quest Objective
**Map Marking:** Hidden until quest unlock (appears on minimap when close)
**Appearance:**

* Young human soldier, mid-30s, with short brown hair partially matted with sweat and dirt.
* Wears tattered Capital City armor — chestplate scratched, shoulder pads bent.
* Hands bound with rope, knees slightly bruised from prolonged kneeling.
* Expression shows fatigue, but eyes are alert, scanning for threats.
* Carries a short sword at his side (rusted but still usable).
* Small satchel with supplies barely intact, hinting at past battlefield experience.

**Behavior:**

* Kneels inside a makeshift cage built by Mirath’s men.
* Occasionally groans or whispers to himself about the past battle.
* Upon player proximity, attempts to signal or call quietly for help.
* Cannot move until rescued by the questline progression.

**Idle Lines / Dialogue:**

> “Please… get me out of here before they return.”
> “Mirath… he took everything. If they find me, I’m finished.”
> “A friend from the Capital? Thank the gods…”

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

### **2. Quest Integration**

**Quest Type:** Side Storyline / Rescue Mission
**Associated Quest:** “Chains of the Faithless” (secondary objective to main storyline)
**Objective:** Rescue Alaric Venn from captivity.
**Trigger:** Player interacts with Commander Edran Holt during main questline and receives instructions to free the captured soldier.
**Location:** Cage inside *The Sunken Encampment*, guarded by 2–3 human enemies (Mirath’s Loyalists).

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

### **3. Quest Stages**

#### **Stage 1 — Discovery**

**Dialogue (upon approaching cage):**

> “Who… who’s there? Please, help me! I’ve been trapped here for days.”

**Objectives:**

* Approach cage without alerting guards immediately (stealth optional).
* Observe guard patrol patterns.

---

#### **Stage 2 — Rescue Operation**

**Dialogue (after engaging guards):**

> “Hurry… the lock is old but I can’t do it alone!”

**Objectives:**

* Defeat 2–3 guarding soldiers around the cage.
* Unlock or break the cage (player can use interaction mechanic).
* Escort Alaric to a safe point near *The Sunken Encampment* or back to Commander Holt.

**Special Mechanic:**

* During escort, Alaric provides **combat support**: swings sword at nearby enemies, blocks attacks, but has low health (players must protect him).

---

#### **Stage 3 — Debriefing**

**Dialogue (upon return to Commander Holt):**

> “Thank you… I owe my life to you. Mirath’s men… they are worse than the stories say. This intel will help turn the tide.”

**Stage Reward:**

* Medium XP
* 1500 Gold
* Consumable: *Ration Pack* (Restores 25% HP and 10% Mana)

--------------------------------------------------------------------------------------------------------------------------------------------------------------------

### **4. Additional Notes**

* **Combat Flag:** Non-hostile until guards are engaged.
* **Movement:** Fixed until rescue. Post-rescue, follows player if escorting back.
* **Interaction Animation:** Kneeling → Unlock / Free → Stand → Follow.

