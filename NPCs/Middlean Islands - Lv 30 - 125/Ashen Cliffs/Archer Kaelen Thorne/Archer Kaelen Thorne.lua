			Archer Kaelen Thorne

### **1. General Overview**

**Name:** Kaelen Thorne
**Role:** Quest Sidekick / Skillable Combat NPC
**Faction:** Human Alliance (Capital City Military Division)
**Location:** *The Sunken Encampment*, Lost Island
**NPC Type:** Skillable Quest Ally / Ranged Support
**Map Marking:** Visible only when players are nearby or during quest stages
**Appearance:**

* Tall, lean human male, mid-30s, with sun-kissed skin and sharp green eyes.
* Wears leather armor reinforced with steel studs for mobility and protection.
* Equipped with a longbow strapped across his back and a quiver filled with arrows.
* Carries a short dagger at his belt for close combat.
* Has a green hooded cloak partially torn, suggesting past skirmishes in the island’s forests.
* Wears fingerless gloves, light boots, and a utility belt with small pouches for supplies.

**Behavior:**

* Idle: Observes the surrounding area, occasionally adjusting his bow.
* During quest: Provides ranged combat support, firing at enemies while maintaining distance.
* Skillable: Will actively target hostile NPCs or animals near the player.
* Follows player commands automatically during escort missions.

**Idle Lines / Dialogue:**

> “Stay behind me if you value your life.”
> “Keep your eyes open — this island is crawling with traitors.”
> “A well-placed arrow solves most problems quietly.”

---

### **2. Quest Integration**

**Quest Type:** Side Storyline / Rescue Mission Support
**Associated Quest:** “Chains of the Faithless” (secondary objective of main storyline)
**Objective:** Assist the player in rescuing Captured Soldier Alaric Venn.
**Trigger:** Automatically accompanies the player when the rescue mission begins.

---

### **3. Quest Stages**

#### **Stage 1 — Patrol Assistance**

**Dialogue (upon first interaction):**

> “I’ll cover you from a distance. Let’s make this quick.”

**Mechanics:**

* Kaelen provides ranged support with basic AI targeting.
* Can target up to 3 hostile enemies simultaneously.
* Attacks scale with the player’s level or party level.
* Uses a mix of standard arrows and occasional special arrows (armor-piercing or slowing effect).

---

#### **Stage 2 — Cage Engagement**

**Dialogue (when engaging guards):**

> “I’ve got their heads in my sights. You move in and free him!”

**Mechanics:**

* Skillable AI activates fully during combat:

  * Prioritizes enemies with high damage output.
  * Can stun or slow enemies temporarily with special arrows (5% chance per shot).
  * Maintains safe distance, avoids melee damage when possible.

---

#### **Stage 3 — Escort Support**

**Dialogue (while escorting Alaric):**

> “Stay close. Don’t let them surround us.”

**Mechanics:**

* Follows the player and the rescued soldier.
* Continues to provide ranged combat support while avoiding traps or debuff zones.
* If Alaric takes too much damage, Kaelen focuses on enemy attackers until threat is neutralized.

---

### **4. Additional Notes**

* **Combat Flag:** Non-hostile to players; hostile to Mirath’s men and island encounters.
* **Skillable:** Yes, scales with player level and provides real combat support.
* **Movement:** Follows player; keeps optimal distance for ranged attacks.
* **Interaction Animation:** Draws bow on engagement, switches to dagger for melee only if enemy reaches him.
* **Special Ability:** Occasionally fires a *Volley Arrow*, dealing AoE damage to clustered enemies.

