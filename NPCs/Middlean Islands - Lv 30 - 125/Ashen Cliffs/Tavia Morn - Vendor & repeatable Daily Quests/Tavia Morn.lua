			Tavia Morn

### **1. General Overview**

**Name:** Tavia Morn
**Role:** Vendor / Minor Quest Giver
**Faction:** Neutral (operates independently on Ashen Cliffs)
**Location:** *The Sunken Encampment*, Lost Island
**NPC Type:** Vendor, sells consumables and island-exclusive goods
**Map Marking:** Visible on map as “Vendor – Tavia Morn” near the tents of the encampment
**Appearance:**

* Female human, mid-30s, with short brown hair tied back with a leather cord.
* Wears a sturdy leather vest and apron over a simple tunic, stained with herbs and alchemical residue.
* Carries a small pouch belt with coins, herbs, and a notebook for tracking inventory.
* Boots are reinforced for traversing the rocky island terrain.
* Has a wooden staff with a hooked end used to carry supplies or support while walking.
* Expression is warm but watchful; constantly glances toward the forests as if wary of attacks.

**Behavior:**

* Idle: Arranges her goods, checks stock, occasionally waves to passing players.
* Combat: Non-hostile, will flee if engaged.
* Interactions: Provides information on her items, gives hints on island survival tips, and can initiate a short local mini-quest for players.

**Dialogue Lines (Idle / Chat):**

> “Stock up if you’re heading into the forest — you’ll need it.”
> “Some of these herbs are unique to Ashen Cliffs, handle with care.”
> “Ever wonder why Mirath’s men fear the cliffs? I can tell you… for a price.”
> “The Living Shard Fragment is rare, only a few make it to my hands.”

---

### **2. Additional Notes**

* **Combat Flag:** Non-hostile, cannot be attacked for quest purposes.
* **Interaction:** When approached, opens the Vendor UI and displays dialogue hints about island mechanics and dangers.
* **Special Feature:** Only sells **limited quantity island-exclusive items**. Once purchased, items are removed from her inventory for that player.
* **Environmental Awareness:** Occasionally comments on nearby hazards:

> “Watch the northern cliffs — gorillas patrol near the rocks.”
> “Some monsters get more aggressive at night, keep your wits about you.”

