### **Tavia Morn – Repeatable Quests**

#### **1. Gather Ashen Ingredients (Forest Herb Run)**

* **Objective:** Collect 10 units of **Glowleaf** or **Rainfern** near the forest edges or waterfalls.
* **Mechanic:** Herbs spawn faster on this island (every 10 minutes), making it suitable for repeatable gathering.
* **Reward:** 100 gold + 1 minor consumable (Health Potion / Mana Potion / Energy Draught).
* **Optional Bonus:** Small chance to unlock **Living Shard Fragment** for purchase.

---

#### **2. Fishers’ Supply Boost (Fishing Quest)**

* **Objective:** Catch 5–10 fish from a designated fishing spot near the waterfalls or rivers.
* **Mechanic:** Fishing in groups grants +5% higher chance of success; fish spawn both day and night depending on type.
* **Reward:** 100 gold + minor consumable; small chance for **Scout’s Ration**.

---

#### **3. Lost Supply Run (Scavenger Quest)**

* **Objective:** Search the forest for **abandoned crates or bottles** scattered by previous expeditions.
* **Mechanic:** Random locations near riverbanks or forest clearings. Can be repeated in different spawn points each day.
* **Reward:** 150 gold + Health/Mana potion, occasionally **Miner’s Ration**.

---

#### **4. Island Defense Report (Observation Quest)**

* **Objective:** Locate 3 points where island monsters (gorillas, elite soldiers) are unusually aggressive and report back to Tavia.
* **Mechanic:** Encourages players to explore various parts of the island and track monster behavior.
* **Reward:** 120 gold + minor consumables; sometimes grants **Gorilla Steak**.

---

#### **5. Local Flora Survey (Herbalist Assist Quest)**

* **Objective:** Count or harvest rare flowering plants near cliffs or waterfall pools, such as **Sunblossom** or **Shadowmoss**.
* **Mechanic:** Plants only spawn during certain periods (daytime), encourages timing visits.
* **Reward:** 100 gold + minor consumables; occasionally a small amount of XP for profession progression.

---

#### **6. Special Request – Island Exploration**

* **Objective:** Collect small items or fragments that washed ashore (driftwood, small crystals, or bottles).
* **Mechanic:** Random spawns around beach areas; encourages exploring the island safely.
* **Reward:** 120 gold + chance for **Living Shard Fragment** purchase; consumable reward included.

---

**Notes for repeatable quests:**

* Can be completed multiple times per day (limits can be set per quest, e.g., 3–5 per day).
* Rewards are designed to be minor but useful for players grinding or exploring the island.
* Some quests provide **temporary access to unique consumables**, like **Living Shard Fragment**, linking her role to both utility and progression without creating a new currency.

