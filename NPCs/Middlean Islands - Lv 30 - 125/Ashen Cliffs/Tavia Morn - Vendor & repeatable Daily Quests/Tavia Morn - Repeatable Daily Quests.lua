			Tavia Morn - Repetable Quests

### **1. Quest Integration**

**Mini-Quest Chain:**

* Tavia provides a small, repeatable quest to gather **local herbs** or **collect rare fragments** that spawn around the island.
* Completion rewards: small amount of gold, minor consumables, and occasionally a chance to unlock **Living Shard Fragment** for purchase.
* Daily Limit: Quest can be repeated up to **5 times per week** (resets at server daily reset).

**Example Quest Flow:**

1. **Quest Name:** “Gather Ashen Ingredients”

   * Objective: Collect 10 Glowleaf or Rainfern near the forest edges.
   * Reward: 100 gold + minor consumables; small chance to unlock Living Shard Fragment for purchase.

