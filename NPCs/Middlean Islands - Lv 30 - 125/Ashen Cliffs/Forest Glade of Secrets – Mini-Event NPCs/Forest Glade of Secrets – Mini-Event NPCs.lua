			Forest Glade of Secrets – Mini-Event NPCs

**1. Sylvaris Leafshade – Hidden Ranger**

* **Role:** Quest giver / temporary ally
* **Location Trigger:** Appears randomly among dense foliage when the player explores a certain area of the glade.
* **Behavior:**

  * Provides a **temporary escort quest** or **collection task** (e.g., gather herbs or track a rare animal).
  * Can engage in combat briefly if hostile creatures appear.
  * Disappears after quest completion or if the player leaves the area.
* **Dialogues / Hints:**

  * “The forest hides more than meets the eye… tread carefully.”
  * “I need help collecting Glade Blossoms before the predators do.”
  * “Follow the whispers of the wind; they will guide you.”
* **Rewards:**

  * Random rare consumable from loot table
  * Minor experience for exploration / profession tasks

---

**2. Mossback Tinkerer – Environmental Puzzle NPC**

* **Role:** Puzzle activator / reward NPC
* **Location Trigger:** Appears at a moss-covered stone circle or ancient ruin inside the glade.
* **Behavior:**

  * Provides a **small environmental puzzle**: align stones, activate runes, or craft a minor alchemical potion.
  * Remains passive unless player interacts with the puzzle.
  * Can drop small crafting materials upon puzzle completion.
* **Dialogues / Hints:**

  * “The forest speaks in symbols… can you read them?”
  * “Twist, turn, and watch the runes glow.”
  * “Only the observant may claim the glade’s secret.”
* **Rewards:**

  * Small ores or crafting materials
  * Occasionally drops rare consumables

---

**3. Ambush Predator Scout – Elite Creature NPC**

* **Role:** Environmental hazard / combat encounter
* **Location Trigger:** Spawned when players step near certain hidden paths or treasure caches.
* **Behavior:**

  * Attacks player on sight but can be avoided if stealth or movement is used wisely.
  * Drops **wildlife loot** upon defeat.
* **Dialogues / Hints:**

  * No dialogue (ambient predator), but can trigger emotes or sounds alerting players.
* **Rewards:**

  * Feathers, claws, hide scraps

