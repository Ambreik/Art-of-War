			Sereth Lume – Herbalist Trainer

**Role:** Herbalism Trainer, Profession Trainer

**Location:** Sunken Encampment, Isle of Ashen Cliffs

**Appearance:**
Sereth Lume is a slender, middle-aged woman with long, dark hair streaked with silver. She wears practical forest-green robes with pockets for herbs, pouches of seeds, and small satchels hanging from her belt. Her hands show slight stains from constant work with plant materials. A faint herbal scent surrounds her, and her calm, deliberate movements reflect her deep connection to the flora around the island.

**Behavior:**

* Non-hostile, interacts safely with players.
* Can perform combat (skillable) if monsters approach while players are gathering near her area.
* Provides **herbalism training** and access to **recipes, crafting materials, and potions** for players who pursue the profession.
* Can give **minor herbalism-related tasks**, but does not have a daily questline like Garrin Muld.

---

### **Interaction / Dialogue Lines** (Non-quest):

* “The island grows what you seek… if you know where to look.”
* “Some herbs bloom only under the moon’s light, others hide in the waterfalls’ mist.”
* “Be careful with the bioluminescent plants; they glow beautifully but can sting if mishandled.”
* “If you wish to learn, follow the roots and observe… patience is key.”

