			Harbormaster Selrik Tidewalker

**Location:**

* *Emerald Bay Dock*, south-eastern coast of Asku’lan Island.
* Positioned near the small harbour where boats arrive and depart.

**Role / Functions:**

1. **Boat Vendor / Rental NPC**

   * Players can rent boats to:

     * Travel **around Asku’lan** for fishing, exploration, and resource gathering.
     * Travel **to/from Capital City** (requires cost).
   * Rental fee: Gold or island-specific resource (see below).
   * Boats have minor mechanics: travel time, limited durability, or fatigue system for long trips.

2. **Questgiver**

   * Provides **2 island-specific quests**:

     1. *“Mapping the Hidden Shores”* – Explore 3 secret coves around Asku’lan; reward: gold + rare fish bait.
     2. *“Cargo Run Chaos”* – Deliver a cargo chest to a POI under a time limit while avoiding environmental hazards; reward: gold + island materials.

3. **Currency Conversion / Reward System**

   * Converts **“Storm Shards”**, the local monster currency dropped by Asku’lan’s high-level monsters, into a chest.

     * Conversion rate: **10x Storm Shards → 1 Thunder Chest**
   * **Thunder Chest**:

     * Contains random rewards: rare resources, herb bundles, fishing supplies, or puzzle pieces for the Totem of the Thunder Guardians.

**Appearance / Flavor:**

* Tall, rugged sailor with sun-tanned skin.
* Wears a leather coat reinforced with ropes and small metal clasps.
* Sea-worn tricorn hat, boots for wet decks, and a belt with hooks and small tools.
* Carries a spyglass and a small ledger for rentals and chest conversions.
* Speaks with a gravelly voice, often muttering about “storms, tides, and treacherous waters.”

**Interactions / Dialogue Examples:**

1. Boat Rental:

   * “Ahoy! Ready to brave Asku’lan’s seas? Ten gold for a safe ride around the island, or a dozen if you aim for the capital.”
2. Currency Conversion:

   * “Got some Storm Shards? Hand ‘em over and I’ll trade ye a Thunder Chest… might hold treasures, might hold trouble.”
3. Quest Giving:

   * *Mapping the Hidden Shores*: “The coast hides secrets… show me what ye find, and I’ll make it worth yer while.”
   * *Cargo Run Chaos*: “Time’s ticking! Can ye haul this crate across the shore without it sinking or getting torn apart?”

**Other Mechanics / Notes:**

* Serves as **spawn point for “Resurrect in Town”**.
* All interactions tied to Asku’lan island mechanics: fatigue, hazards, exploration, and resource nodes.
* Optional: can provide **minor hints for secret POIs** or nearby rare resource locations.

