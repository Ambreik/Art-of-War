			“Mira the Deckhand” - Supply Vendor

**Role:** Basic Goods Merchant
**Type:** Vendor / Utility NPC
**Function:** Sells base supplies for exploration and travel; some goods become available based on player reputation with the Middlean Explorers’ Guild.

---

### **Overview**

Once a stowaway turned crew assistant, **Mira** runs the makeshift dockside stall. She trades supplies salvaged from cargo runs, offering essentials for anyone brave enough to step off the dock.

---

### **Appearance**

Young woman with short auburn hair, simple sailor’s gear, and a leather pouch belt. A small knife hangs at her hip for cutting ropes and bait.

