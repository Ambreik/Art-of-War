			Shipmaster NPC – “Captain Doran Vale”

**Role:** Travel Manager / Ferry Captain
**Type:** Transport NPC
**Function:** Handles boat travel between Capital City ↔ Dragon Isle; also manages short-distance island transfers (if unlocked).

---

### **Overview**

A seasoned sailor from the Old Kingdom fleet, **Captain Doran Vale** was one of the few to brave the Dragon Seas before safe routes were established. He now runs the sanctioned ferry service between the Capital and Dragon Isle under a private charter.

---

### **Appearance**

Rugged middle-aged man with sea-weathered skin, tied-back gray hair, and a navy overcoat bearing the faded insignia of the Old Fleet. Wears fingerless gloves and carries a brass spyglass.

---

### **Functions & Mechanics**

* **Travel Interface:**

  * "Sail to Dragon Isle" (from Capital)
  * "Return to Capital City" (fee-based)
  * "Hire Coastal Skiff" – allows limited inter-POI short-range boat use, unlocked after completing his introduction quest.
  * Displays travel times and costs dynamically.

* **Resurrection Anchor:**

  * Player resurrection near Doran Vale reactivates travel dialog.
  * A “Safe Harbor” buff protects the player for 20 seconds after respawning.

* **Quest Hook:**

  * Offers one-time quest “First Tide Voyage” introducing travel safety and resource loading.

---

### **Idle Dialogue**

* “Winds here bite harder than steel. You best keep your ropes tight.”
* “The sea don’t forgive, but she remembers those who listen.”
* “We leave when the tide says, not before.”

