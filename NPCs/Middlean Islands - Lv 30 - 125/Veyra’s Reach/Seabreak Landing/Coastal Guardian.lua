			Coastal Guardian

**Type:** Guardian / Defender NPC
**Location:** Seabreak Landing, standing near the pier and occasionally fishing.
**Role:** Area protector, ensures player safety, fights off monsters that approach the landing.

**Mechanics & Functions:**

1. **Combat AI:**

   * **Tanky NPC** with high health and armor stats.
   * Uses **AoE abilities** to deter monsters or aggressive players.
   * Has **protection radius**: any hostile NPC within 15 meters will be targeted first.

2. **Skills & Abilities:**

   * **Whirlpool Slam:** AoE attack hitting all enemies around the pier, moderate damage.
   * **Shield Wall:** Temporarily reduces damage taken by 50%, active every 30 seconds.
   * **Taunt:** Forces nearby hostile NPCs to attack him instead of other NPCs or players in the area.

3. **Interaction:**

   * Non-tradeable. Cannot be used for quests or purchases.
   * Automatically respawns if killed by monsters; respawn timer ~10 minutes.

4. **Behavior:**

   * **Fishing animation** when idle to make the NPC feel alive.
   * Moves along the pier but rarely leaves Seabreak Landing.
   * Prioritizes monsters first, aggressive flagged players second.

5. **PvP Notes:**

   * Cannot be flagged for permanent PvP consequences.
   * AoE attacks **do not harm friendly players**.

