			Dockhand Marven, the Boat Vendor

**Type:** Vendor / Travel NPC
**Location:** Seabreak Landing — standing beside the small dock, near a stack of barrels and folded sails.
**Role:** Sells boats to players for travel back to **Capital City** or for **exploration and fishing** around the nearby coastal waters.

---

#### **Appearance & Personality**

* Wears light coastal clothing with rolled-up sleeves, a straw hat, and a weathered tool belt.
* Often seen polishing a small model ship or coiling rope.
* Personality: Gruff but friendly. Known for saying things like *“If it floats, I can sell it… if it sinks, that’s your problem.”*

---

#### **Vendor Services**

**Sells the following items:**

| Item Name            | Type        | Description                                                                   | Price      | Cooldown                   |
| -------------------- | ----------- | ----------------------------------------------------------------------------- | ---------- | -------------------------- |
| **Rowboat Charter**  | Travel Pass | Allows player to travel back to Capital City. Single-use.                     | 5 gold   | None                       |
| **Exploration Boat** | Mount Item  | Deploys a small controllable boat mount for coastal travel (up to 5 minutes). | 1,2 gold | 24h cooldown per character |
| **Fishing Skiff**    | Mount Item  | Allows stationary fishing from boat on sea surface.                           | 3,5 gold   | 12h cooldown per character |
| Big Health Vial | Potion | Potion that regen HP, when stationary. | 12 Silver | Can be bought in bulk | 
| Big Mana Vial | Potion | Potion that regen MP, when stationary. | 12 Silver | Can be bought in bulk | 

---

#### **Mechanics & Interactions**

1. **Boat Travel System:**

   * When purchasing **Rowboat Charter**, a prompt appears:
     *“Are you sure you wish to depart to Capital City?”*
     Once confirmed, the player is faded out and loaded into the **Capital City harbor**.
   * Boats purchased for exploration/fishing can be **summoned from inventory** when near a body of water.

2. **PvP & Punishment Mechanics:**

   * **Marven** can be attacked by players flagged for PvP.
   * If defeated:

     * The player **loses access to his services for 7 real days** (tracked server-side per account).
     * Nearby Guardian NPC marks the attacker as “Hostile” for 1 hour, attacking on sight.
   * While dead, **Marven respawns after 20 minutes** (to prevent prolonged griefing).

3. **Behavior:**

   * **Skillable NPC** – can be attacked and defeated easily, has no strong combat abilities.
   * **Idle actions:**

     * Polishes boat hulls
     * Coils ropes
     * Whistles sea shanties at random intervals.
   * Occasionally says:

     * *“You break it, you row it.”*
     * *“Storm’s brewing, better not push too far out today.”*

4. **Additional Features:**

   * If another player purchases a **Rowboat Charter**, any nearby player within 10m can **join their departure** (prompt appears: “Join voyage to Capital City?”).
   * Boats deployed by multiple players nearby **share visible movement paths** (small aesthetic waves or wakes).

