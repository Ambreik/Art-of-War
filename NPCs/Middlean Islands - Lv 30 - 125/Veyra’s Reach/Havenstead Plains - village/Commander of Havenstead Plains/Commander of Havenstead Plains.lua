### ⚖️ **Player Interaction Mechanics**

* **Invulnerability to Players:**
  Players cannot kill the Commander; his HP threshold locks at 10%.
  However, repeated aggression or killing nearby guards **flags the player as a traitor** to the Order of Restoration.

* **Reputation Penalty:**

  * Killing allied guards or attacking the Commander reduces the player’s **Order of Restoration reputation to zero**.
  * The player is **banned from Order-related quests and vendors for 7 real-time days**.
  * Reputation can only be regained through completing unfinished neutral quests tied to the faction.

* **Guidance Function:**
  Interacting with the Commander allows players to choose between four dialogue options:

  1. “Show me the nearby points of interest.”
  2. “I need information about the Crusaders.”
  3. “Where is the Rune Alchemist?”
  4. “Tell me more about Havenstead’s defenses.”

  Selecting (1) places a marker on the map for 15 minutes; only one marker can be active at a time.

---

### 🏕️ **Environmental Role**

The Commander represents the **central pillar of the island’s defense AI network**. If he dies:

* Monsters increase in aggression within 200 meters of Havenstead Plains for 10 minutes.
* Villagers panic (some civilian NPCs hide in huts).
* Guard patrols double frequency until he respawns.

