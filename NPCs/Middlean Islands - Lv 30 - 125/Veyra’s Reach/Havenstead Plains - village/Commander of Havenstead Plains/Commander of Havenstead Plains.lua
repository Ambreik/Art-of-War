			Commander of Havenstead Plains

**Location:** Central Square, Havenstead Plains – Veyra’s Reach
**Faction:** Order of Restoration
**Type:** Command NPC / Quest Giver / Defender
**Level Range:** 85–90
**Rank:** Elite NPC

---

### 🧍 **Description**

The **Commander of Havenstead Plains** stands tall and vigilant at the village’s center, clad in reinforced steel armor trimmed with blue and gold insignias of the **Order of Restoration**. His long spear, polished but battle-worn, rests firmly at his side.
He serves as both the **strategic overseer** of Veyra’s Reach and the primary authority for all expeditionary forces stationed on the island. Beneath his calm discipline lies the burden of command — the knowledge that he must guard the few living souls who remain against the horrors that stalk the cliffs and forests beyond.

---

### ⚔️ **Core Role**

* **Primary Role:** Defense leader and guide for players on Veyra’s Reach.
* **Quest Function:**

  * Involved in the **Tarnished Crusader Insignia** quest chain.
  * Initiates two personal questlines:

    * *“Echoes from the Shore”* (3-chain storyline).
    * *“Oath of the Plains”* (5-chain storyline).
* **Faction Impact:**
  Directly tied to **Order of Restoration** reputation. His opinion and actions affect player access to certain vendors and quests across the island.

