# **5. RELATIONSHIPS**

### **With Fisherman NPC**

* The only villager he likes
* They both enjoy silence
* Fisherman brings him beverages
* Alyndor sometimes listens to Fisherman’s complaints about the Chef
* Responds with:
  “Emotional noise is a curse. You endure much.”

### **With Chef NPC**

* None.
* He avoids him.
* Calls him:
  “The emotionally radioactive one.”

### **With Hunter NPC**

* Mutual respect
* Hunter protects walls
* Alyndor kills monsters that get too close
* They nod silently when crossing paths

### **With Herbalist NPC**

* Professional curiosity
* Occasionally trades arcane residue for herbs
* Speaks to her more politely than to others

---

# **6. NIGHTTIME MECHANICS**

### **Night Cycle Behavior**

At night he:

* Leaves his hut
* Patrols around the village walls
* Hunts monsters solo
* Gathers materials from arcane anomalies

### **Player Can Encounter Him in Combat**

If he is fighting:

* Player can join
* Loot rules:

  * If NPC tags first → player can still loot
  * If player gives loot to him → small Reputation / Rune Dust reward
* Rare monsters trigger special behavior:

### **Rare Monster Detection**

If a **rare monster** appears near him:

* System message:
  **“Alyndor Redveil calls for assistance against a powerful foe!”**
* He points his staff toward the monster
* Begins a multi-stage battle
* Drops special *Arcane Residue* used for Rank III runes

---

# **7. TELEPORT MECHANIC (THE BELL SYSTEM)**

At his hut is a small hanging rune-bell.

### **If the player rings the bell:**

* His eyes flash wherever he currently is
* He **instantly disappears from combat**, even mid-cast
* Teleports to his hut with a *boomph* sound
* Monsters he was fighting reset

### **Upon arrival**

He says:

* “You called. State your purpose.”
  Or if annoyed:
* “This had better be important.”

### **If a player rings the bell during battle with him nearby:**

He teleports out instantly
(this surprises players and looks mysterious)

