			Alyndor Redveil	— Rune Crafter (Rank III)

**Name:** *Alyndor Redveil*
**Profession:** Rune Artisan (NOT a trainer)
**Rank:** III
**Role:** **Crafter • Vendor • Combat-Assist NPC • Event Trigger NPC**
**Location:**
His personal hut sits **near the village walls**, opposite the gate, close to the central well.
The hut glows faint blue at night from residual rune energy.

---

# **1. APPEARANCE & AMBIENCE**

### **Appearance**

* Wears a long red-and-blue enchanted robe with traces of arcane weaving.
* Has glowing tattoos on his hands resembling runic circles.
* Hair tied back, pale eyes, stern expression—always looks “done with everyone.”
* Floats slightly above the ground when walking over stones or small debris.

### **Animations**

* **Idle:** Turning a small rune between his fingers, making it levitate.
* **Crafting:** Draws glowing glyphs in the air with his fingertip.
* **Combat:** Casts arcane missiles, mini-rune explosions, protective shields.
* **Teleport vanish:** Folds his arms, runes flash, *pop* — he disappears.
* **Nighttime roaming:** Walks along walls, scanning the surroundings with glowing monocle-like lens.

---

# **2. PERSONALITY**

* Stoic, mysterious, quietly sarcastic.
* Speaks minimally, every sentence has weight.
* Rarely interacts socially — except with Fisherman NPC, who is the **only one he tolerates**.
* He finds most villagers “emotionally loud and inefficient.”

### **He does NOT care about:**

* Chef’s love drama
* Hunter’s rivalry
* Herbalist’s plants (though he acknowledges her expertise)

### **He DOES care about:**

* Order
* Mystical energy
* Collecting rare components
* Killing monsters near the walls because “they disrupt the natural flow”

---

# **3. UNIQUE RUNE SYSTEM (CORE MECHANIC)**

### **Alyndor crafts RUNES of three Ranks:**

### **Rank I — Basic Runes**

* Small temporary buffs (Attack, Defense, Speed)
* Cosmetic glow effects
* Minor utility (faster gathering, slight dodge boost)

### **Rank II — Enhanced Runes**

* Medium combat boosts
* Damage shields
* Short-cooldown effects
* Temporary elemental damage infusions

### **Rank III — Advanced / Special Runes**

* Strong temporary buffs comparable to rare elixirs
* Unique toy-like effects (spectral wings, glowing trail, levitation jump)
* Mini transformation runes
* Rune of Arcane Burst (combat utility)
* Dungeon-aligned runes (megadungeon synergy buffs)

### **Crafting Access is Based on Player Knowledge**

Players must:

* Learn rune “patterns” (drops, quests, discoveries)
* THEN he can craft the rune for them
* Otherwise: “You lack the pattern. Return when you understand what you request.”

### **He also sells pre-made runes**, but:

* Only ones the player has learned
* Rare runes rotate daily
* Rank III runes only appear if the player is Rank III in ANY profession or class mastery

---

# **4. VENDOR & CRAFTING SERVICES**

### **Vendor Tabs**

* **I** = Rank I Runes
* **II** = Rank II Runes
* **III** = Rank III Runes
  Additional tab: **✦ Crafting Requests**

### **Examples of Runes He Sells**

(Adapted to your game’s style)

#### **Rank I Rune Examples**

* Rune of Minor Ferocity
* Rune of Quickstep
