* Rune of Spirit Balance (regen boost)

#### **Rank III Rune Examples**

* Rune of Dragon Pulse (temporary AoE ability)
* Rune of Ethereal Wings (visual effect with small glide)
* Rune of Titanhide (huge temp defense buff)
* Rune of Aether Focus (greatly enhances cooldown speed for 12 sec)

### **Crafting Process**

Player provides:

* Rune Core
* Monster essence
* Special herb / crystal
* Gold fee

Alyndor performs:

1. Levitation of materials
2. Arcane binding
3. Finalization explosion *“pop”*

Player receives crafted rune.

