# **9. OPTIONAL QUESTS (He isn’t a quest NPC, but can have 2 unique ones)**

He does not give “normal” quests, but **two special tasks** fit his personality:

---

## **QUEST 1 — “Runic Disruption” (Night Only)**

A rare anomaly appears near the village.
He requests the player to help stabilize runic energy and kill a special creature.

**Reward:**

* Rune Pattern (random)
* Rune Core
* +Rune Crafter Reputation

---

## **QUEST 2 — “Silence the Walls”**

A gathering quest tied to nighttime patrols.

Collect:

* 4× Arcane Echo Shards
* 2× Lizardman Tails
* 1× Fellmoss (from Herbalist’s herb list)

**Reward:**

* Free Rank II crafted rune
* Unlock Rune Vendor special item

