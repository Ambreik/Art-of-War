			Eldran Ironbrow — The Humble Smith

**Type:** Crafting Vendor (Blacksmith & Item Upgrader)
**Profession Category:** Standard Blacksmith (non-Legendary)
**Faction Affiliation:** Order of Restoration (Friendly)
**Location:** Havenstead Plains, in the **western corner of the village**, inside a modest smithing hut opposite to his brother’s **Legendary Forge**.
**Level Range:** N/A
**Skillable:** ❌ No (cannot be trained on or fought)
**Reputation Dependency:** ✔️ Provides **discounts and crafting bonuses** to players with high Order of Restoration reputation.

---

### 🧱 **Appearance & Personality**

Eldran Ironbrow is an **older man**, well into his sixties, yet still broad-shouldered from decades of hammering steel. His thick **gray beard** is braided with copper wire, and his eyes gleam with both wisdom and mischief.
He wears a soot-stained apron over a simple tunic, and his forge hat (a battered leather cap with a metal insignia of a hammer) sits proudly on his head — he calls it his “thinking helmet.”

Eldran has a **booming laugh**, a tendency to talk even when no one’s listening, and an unshakable pride in his craft. Despite his jealousy of his younger brother — the Legendary Blacksmith — he genuinely respects him and occasionally mutters that “the lad only shines because I built the anvil he works on.”

---

### ⚙️ **NPC Mechanics**

* Faction-Based Discount System : Players with higher reputation in the "Order of Restoration" receive scaling discounts on upgrades and materials (up to 20%).
* Crafting Services : Can craft and upgrade standard weapons, armor, and reinforcement materials. Does not handle legendary-tier items.
* Dynamic Dialogue States : Changes quotes and greetings depending on player reputation, time of day, or whether Legendary Blacksmith’s forge is busy.
* Shop Closing Mechanic : If monsters breach the village, Eldran immediately shuts his door, locking himself in the hut until combat ends.
* Non-Skillable : Cannot be targeted for combat or used for skill training. Attacking him results in reputation loss with the "Order of Restoration".
* Smith’s Temper Meter : Small humor mechanic — if a player spam-clicks him too often, he mutters angrily for 30 seconds before refusing interaction for a short period.
