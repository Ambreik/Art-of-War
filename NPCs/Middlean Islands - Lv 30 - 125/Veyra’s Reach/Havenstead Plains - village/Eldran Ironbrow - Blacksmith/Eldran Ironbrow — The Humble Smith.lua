### 🪓 **Gameplay Notes**

* **AI Routine:**

  * Works at forge from sunrise to sunset.
  * Occasionally walks out to splash water on his forge or mutter about his brother.
  * Shuts shop instantly if monsters breach village, reopening 10 minutes after combat ends.

* **Visuals:**

  * Glowing forge beside his hut.
  * Occasional sparks and hammer sounds.
  * Small smoke plume visible from village center.

