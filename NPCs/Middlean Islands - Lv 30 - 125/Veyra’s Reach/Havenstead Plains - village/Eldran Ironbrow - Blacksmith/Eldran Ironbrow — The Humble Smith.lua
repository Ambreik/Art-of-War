### 💡 Developer Notes

* Eldran adds **atmospheric realism** and a **lived-in rivalry** dynamic to Havenstead.
* His humor, practical advice, and mild bitterness make him memorable.
* He grounds the profession NPC lineup with believable human emotion, not just mechanics.

