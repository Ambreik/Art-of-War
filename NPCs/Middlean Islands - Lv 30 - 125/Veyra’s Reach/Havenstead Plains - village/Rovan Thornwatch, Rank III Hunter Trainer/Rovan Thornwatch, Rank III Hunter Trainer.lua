# **7. QUESTLINE**

Rovan offers 4 quests total:

* **2 Hunting Quests** (serious, island-focused)
* **1 Timed, Multi-Phase Monster-Slay Quest with unique mechanics**
* **1 Comedy Jealousy Quest involving the Chef NPC**

---

# **QUEST 1 — “Culling the Skytalon Lizard Archers”**

**Type:** Hunt / Slay
**Target:** *Archer Skytalon Lizard* (Rank II)
**Objective:** Kill **50** Skytalon Lizard Archers
**Reward:** Hunter XP + mid-tier bowstring + coins

**Quest Text:**
“The Skytalon Archers are testing our perimeter.
Their arrows grow bolder.
Cull them—cleanly, quickly.”

Simple hunt.

---

# **QUEST 2 — “Marked for Silence”**

**Type:** Hunt / Track
**Target:** *Shadowcrest Stalker* (rare island monster)
**Mechanic:** Player follows tracks using “Hunter’s Insight” (temporary buff)
**Objective:** Kill **1** Shadowcrest Stalker
**Reward:** Rare spear oil + Hunter XP

**Quest Text:**
“A Shadowcrest Stalker hunts near the ridge.
Find it before it finds us.”

---

# **QUEST 3 — “Tide of Claws” (THE BIG ONE)**

### **The 500 Monster Quest with Timed Mechanic & Semi-Boss Trigger**

**Quest Type:** Multi-Phase Hunt
**Target:** *Archer Skytalon Lizards* (Rank II)
**Objective:** Kill **500**

BUT the quest changes mid-progress.

---

## **Phase 1 — Kill 120/500**

When the player reaches **120 kills**, the system triggers:

**NEW OBJECTIVE:**
*“A massive surge of lizards is forming. Finish the cull within 30 minutes!”*

**Buff Applied:**
**Hunter’s Tempo** – +3% movement speed and +5% ranged speed.

---

## **Phase 2 — 30-Minute Timer**

Player must reach **500 kills** within the timer.

BUT if the player gets close (like 480/500), a system message appears:

> **SYSTEM:** “Hunter Trainer Rovan urgently requests your return to Havenstead! A large beast is attacking the village gates!”

Quest instantly updates:

**NEW OBJECTIVE:**
Return to Rovan within 2 minutes.

---

## **Phase 3 — The Semi-Boss**

Player arrives to find:

### **SEMI-BOSS:** *Clawbound Tyranax*

Rank III elite hunt-beast
Unique to this quest only.

Rovan is fighting it alone.

**Mechanics:**

* Tyranax deals heavy slash damage
* Can roar to stun
* Occasionally targets the Herbalist’s shop direction
  (Rovan goes into rage mode when it does)

**Objective:**
Defeat Tyranax with Rovan.

---

## **Ending**

Rovan:
“You returned. Good.
I knew you would not abandon Havenstead.
…Thank you.”

Reward:

* **Rovan’s Spear Oil (rare)**
* **Hunter XP**
* **Unique Title:** *“Thornwatch’s Ally”*

---

# **QUEST 4 — “Operation: Impress the Herbalist” (Comedy Quest)**

This is the jealousy quest involving the Chef NPC.

**Quest Type:** Humorous Interaction / Trick Quest
**Rovan’s Goal:**
Impress the Herbalist without fighting the Chef.

**Quest Text:**
“The Chef keeps bringing her soups.
Ridiculous.
I need something… impressive.
Help me. But nothing violent.
She dislikes unnecessary aggression.”

---

## **Objective:**

Outshine the Chef by completing embarrassing tasks for Rovan:

### Step 1 — “Carry This”

Rovan forces the player to carry a **massive bouquet of forest herbs**
(so big it covers the player’s screen)

Player becomes slowed.
Villagers comment:
“That’s the biggest bush I’ve ever seen carried.”

---

### Step 2 — “Deliver the Bouquet”

Player brings the bouquet to the Herbalist.

She laughs:
“Oh my goodness—who gave you this? This is… enormous!”

Player returns.
Rovan:
“…She laughed?
W-was it a good laugh? Or a bad laugh?”

---

### Step 3 — The Chef Interferes

Chef appears during turn-in:

Chef NPC:
“I also brought her flowers! I made soup! I—”
Rovan (furious but restrained):
“Cook. Leave. Now.”

---

### Step 4 — Player Helps Rovan “Outshine” the Chef

**Funny mechanic:**
The player must adjust Rovan’s “pose” for 10 seconds so he looks heroic.

UI mini-game:

* “Tilt chin”
* “Straighten back”
* “Hold bow more impressively”
* “Stop looking angry”
* “Stop looking at the Herbalist”
* “Stop looking at the Chef”
* “STOP LOOKING AT EVERYTHING”

When done:
**Success Sound:** *twang*
Rovan:
“…Perfect. I look completely natural.”

He absolutely does not.

---

## **Reward**

* Coins
* A cosmetic item: **“Hunter’s Ridiculous Herb Bouquet” (back-slot cosmetic)**
* Slight reputation boost with Rovan

