			Rovan Thornwatch, Rank III Hunter Trainer

### **Rank:** III (Master Hunter)
### **Type:** Trainer / Combat Mentor / Duel NPC / Questgiver
### **Location:** Havenstead Village – beneath the tallest tree near the eastern perimeter, overlooking the forest edge.

---

# **1. OVERVIEW**

**Rovan Thornwatch** is the village’s Master Hunter — serious, disciplined, observant, and absolutely dedicated to the protection of Havenstead and the people living in it…
…especially *one person* in particular: the Herbalist.

He is respected, feared by monsters, admired by some villagers, and avoided by others because of his intense “I can hear your heartbeat” stare.

He keeps mostly to himself—
except when the Herbalist passes by.
Then he pretends to examine the sky… or the wind… or the entire philosophical concept of leaves.

The Chef NPC?
Rovan considers him a “harmless kitchen mouse”—
but also despises him because the Herbalist sometimes *smiles* at his food.

---

# **2. LOCATION SETUP & ANIMATIONS**

Rovan is found under a **tall old oak** beside:

* his **warhorse**, fully saddled
* a **wooden water bucket**
* a **pile of hay**
* a training dummy behind the tree
* a small hunting campfire
* a rack of monster bones he’s studying

**Idle Animations:**

* Cleaning his longbow
* Sharpening his spear
* Feeding his horse (rare)
* Looking toward the Herbalist’s shop (then quickly pretending he wasn’t)
* Crouched, listening to distant noises

**Combat Animation (special):**
He uses **bow at long range**, switching to **spear** if anything closes in.

---

# **3. APPEARANCE**

* tall, lean, extremely athletic
* long dark hair tied back
* green mantle made of lightweight beast hide
* tribal hunter necklace made of Rank III monster talons
* two scars: one across the jaw, one across his left arm
* calm, sharp eyes
* longbow in hand, spear strapped diagonally on back

When angry (usually because of the Chef), his right eyebrow twitches.

---

# **4. PERSONALITY**

* **Professional** – almost too serious
* **Protective** – especially over Herbalist NPC
* **Focused** – always in “hunt mode”
* **Stoic** – except in romantic matters
* **Territorial** – toward monsters *and* the Chef NPC
* **Socially awkward** – but only around the Herbalist

He is the type who can kill a beast twice his size but panics when someone says:
“So… do you like her?”

---

# **5. TRAINER MECHANICS**

### ✔ Hunter Trainer – Rank III

Teaches:

* advanced ranged techniques
* spear precision combos
* monster tracking skills
* stamina efficiency
* passive bonuses to ambush attacks

### ✔ Duelable NPC

The player can duel him:

* He uses real combat AI with stance switching
* If the player wins, he respects them
* If the player loses, he gives advice and no shame

### ✔ Village Defense AI

Rovan is one of the NPCs who **actively engages monsters** during village raids.

He gains a temporary buff:
**“Predator’s Pulse”** – 20% attack speed and doubling tracking distance.

### ✔ Relationship Influence Mechanic (hidden)

If the player completes *any* of his love-related quests, he gains unique dialogue changes toward both the player **and** the Chef.

