			Osric “Deepbreath” Wavereed — Fishing Trainer (Rank III)

**Name:** *Osric “Deepbreath” Wavereed*
**Profession:** Rank III Fishing Trainer
**Role:** Trainer • Vendor • Questgiver
**Location:** Near the **village well**, sitting on a small wooden stool with a fishing rod across his lap, a bucket full of fresh fish, and a lantern that glows brighter at night.

---

# **1. NPC APPEARANCE & ENVIRONMENT**

### **Appearance**

* Tall, skinny man with sunburnt skin and constantly tired eyes.
* Wears a patched fisherman’s vest, waterproof boots, and a small knife strapped to his thigh.
* Has a fishing rod carved from Driftwood Birch and a net tied to his belt.
* Smells faintly of fish (he can’t help it).

### **Animations**

* **Daytime:** Calmly fishing into the well, humming, dozing off mid-sentence.
* **Nighttime:** Pacing in circles angrily while mumbling about the Chief NPC.
* **When annoyed:** Pulls his hat down aggressively and mutters curse words about “love-sick idiots.”
* **When happy:** Swings his fishing rod like a lute and pretends to play music.

### **Environment**

* A small setup:

  * Bucket of fish
  * Old wooden stool
  * A rope line for drying fish
  * Lantern on a stick
  * A crate of fishing tools
  * Tiny barrel where he “stores emergency drinks”

---

# **2. PERSONALITY**

* **Day:** Polite, friendly, hyper-focused on fish.
* **Night:** Absolutely fed up with the Chief NPC and his love stories.
* **Secret fear:** He believes the Chef NPC might poison the fish shipments “out of emotional turbulence.”
* **Loyal to Herbalist:** Respects her deeply, but she never gives him any beverages.
* **Perpetual victim:** Always stuck between the Chef NPC and his endless romantic rants.

---

# **3. UNIQUE MECHANICS**

### **📌 Time-based NPC Personality Swap**

* **Day:**
  Osric offers training, sells goods, gives normal quests.
* **Night:**
  Osric refuses normal conversation and only offers the **Night Questline**, speaking in angry rants.

### **📌 Fear of the Chef Mechanic**

* If the Chef NPC is nearby (or during a certain quest step) Osric:

  * Stutters
  * Tries to hide his fish bucket
  * Repeats: “Don’t poison me, don’t poison me, don’t poison me…”

### **📌 Special Trainer Perk**

As a **Rank III Trainer**, he teaches:

* Advanced fishing techniques
* Special bait crafting
* Underwater fishing skill
* “Silent Strike” technique (faster catch chance)

