# **6. QUESTS**

## **QUEST 1 — “Night of the Fishless Fury” (Night-Only Quest)**

**Type:** Comedic / Interaction Quest
**Requirement:** Must be night.
**Objective:** Help Osric scare the Chef NPC into stopping the nightly romantic obsession rants.

---

### **Quest Description**

Osric is fed up. Every night, when he finally stops fishing and goes to get a drink, the Chef NPC traps him in a corner and spills his heart out for HOURS.

Osric desperately wants that to stop.

**Your job:**
**Scare the living soul out of the Chef**, so he backs off.

---

### **Quest Steps**

1. **Speak with Osric at night.**
   “I can’t handle him anymore. Please. I beg you. Scare him.”

2. **Prepare the disguise:**
   Collect:

   * 1× Nightveil Scale (bioluminescent)
   * 2× Duskscale Bones
   * 3× Jungle Leaves

3. **Craft a “Bog-Loom Horror Mask.”**
   (Crafting UI interacts with Osric’s fishing crate)

4. **Sneak behind the Chef NPC**
   (Stealth area auto-grants stealth)

5. **Jump out and scream “BOO!”**
   A special animation plays:

   * Chef NPC falls backward
   * Drops his cooking spoon
   * Screams: “THE HERBALIST WILL NEVER LOVE A MONSTER AAAAA—”

6. **Return to Osric**

---

### **Quest Ending (Funny Outcome)**

Osric bursts into laughter until he nearly chokes on his own breath.

**Reward:**

* *Osric’s Secret Mix (rare bait)*
* +100 Fishing EXP
* Title: **“Bog-Loom Menace”** (cosmetic)

