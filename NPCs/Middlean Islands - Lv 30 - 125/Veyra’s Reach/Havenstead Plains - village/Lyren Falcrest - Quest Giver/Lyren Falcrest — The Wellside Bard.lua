			Lyren Falcrest — The Wellside Bard

**Type:** Quest Giver (Unique NPC)
**Faction:** Order of Restoration (Civilian Tier)
**Location:** Havenstead Plains – beside the central **village well**
**Level Range:** N/A (non-combatant)
**Respawn:** 15 minutes if slain
**Skillable:** ❌ No (cannot be engaged in skill combat)
**Attack Reaction:** Runs, hides, or cowers behind nearby structures

---

### 🌾 **Appearance & Behavior**

Lyren Falcrest is a **tall, slender man** with long, neatly combed **blonde hair** cascading past his shoulders. He wears **bright silken garments** of blue and green tones, adorned with gold-trimmed cuffs and a weathered **straw hat**. A lute hangs lazily on his back, and his boots are always dusted from pacing near the well.

Each morning, Lyren **sings softly**, performing melodies that can be faintly heard across the village square. Between songs, he grabs apples from a small **basket near the well** and tosses them at a **wooden bat effigy** planted in the dirt — his peculiar way of “training his aim,” as he claims.

When players approach or pass him, he pauses mid-tune, gives a courteous bow, and calls out with a cheerful line — always eager to charm or amuse.

---

### 🧩 **Personality & AI Behavior**

* **Disposition:** Cheerful but cowardly. Loves conversation, hates confrontation.
* **Daily Routine:**

  * **Morning:** Sings and plays lute near the well.
  * **Afternoon:** Throws apples at his bat target; occasionally interacts with villagers.
  * **Night:** Sits on the well’s edge, humming softly, visibly nervous when shadows fall.
* **Reactions to Events:**

  * Loud noise or combat near him → flinches, drops an apple, hides behind the well.
  * Monsters breach village → runs toward Commander or hides in storage hut.
  * Players emote near him → responds with random friendly gestures (wave, bow, laugh).
  * In PvP stance → flees immediately, often shouting comically for help.

