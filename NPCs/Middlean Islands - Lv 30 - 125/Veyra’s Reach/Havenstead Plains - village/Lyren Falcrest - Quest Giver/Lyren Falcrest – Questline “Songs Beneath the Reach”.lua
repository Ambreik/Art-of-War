## ⚒️ **Quest II – “The Weight of Forgotten Tools”**

**Quest Type:** Profession / Resource Discovery
**Required Level:** 55+
**Starting NPC:** Lyren Falcrest
**Area:** Grimstone Quarry

---

### 📜 **Quest Description:**

Lyren has composed a song about the island’s miners — but lacks the rhythm of their toil. He’s convinced that somewhere in the **southern quarry**, abandoned tools still carry the echoes of their owners’ work.
He asks you to recover what remains of their craft and maybe, just maybe, their spirit of perseverance.

---

### 🎯 **Objectives:**

1. Retrieve **3 “Ancient Quarry Tools”** from mining ruins in **Grimstone Quarry.**
2. Use the **Catapult Device** to reach the elevated cliff (where the final relic lies).
3. Return to Lyren Falcrest with the relics intact.

---

### ⚙️ **Mechanics:**

* One of the relics requires using the **catapult traversal mechanic**.
* If the player collides mid-launch or dies, relic progress resets.
* Successfully obtaining all relics grants a small bonus item.

---

### 💬 **Lyren Dialogue Snippets:**

> “You found them? Wonderful! I’ll polish these until they sing again!”
> “It’s strange… I can almost hear the clang of their hammers when I touch the metal. Perhaps the island remembers every strike.”

