## 💀 **Quest III – “Heart of the Veil”**

**Quest Type:** Timed / Combat / Lore Conclusion
**Required Level:** 69–70
**Starting NPC:** Lyren Falcrest
**Area:** Veyra’s Edge → Submerged Passage near the Sunken Warship

---

### 📜 **Quest Description:**

During one of his late-night performances, Lyren claims to have seen **lights pulsing beneath the sea’s surface**, near the edge of the island. He believes the echoes are *alive* — trapped in a crystalline chamber beneath the waves.

He asks you to descend into the coastal depths, recover what lies within, and bring proof that the “song of Veyra’s Reach” truly exists.

---

### ⏳ **Quest Conditions:**

* **Time Limit:** 25 minutes.
* **Failure Count:** 3 total allowed (quest becomes permanently interrupted after 3 fails).
* **Failure Triggers:**

  * Timer expires.
  * Player dies before retrieving the item.
  * Player fails to defeat the semi-boss guarding the relic.

---

### 🎯 **Objectives:**

1. Dive into the **submerged passage** near the **Sunken Warship**.
2. Defeat **Seraphine the Hollowed Voice** (Semi-Boss, Level 70).
3. Retrieve the **Heart of the Veil Crystal** within the time limit.
4. Return to Lyren Falcrest.

---

