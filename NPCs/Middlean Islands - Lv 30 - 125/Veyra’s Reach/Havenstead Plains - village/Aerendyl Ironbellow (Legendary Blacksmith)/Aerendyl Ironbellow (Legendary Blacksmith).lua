## 5 — Forge Environment (gamefeel)

* Ambient: low hum of the anvil, measured hammer strikes, occasional ring of a tuned metal — gives a unique soundscape.
* Players see small runic glyphs flare when Aerendyl refines materials (visual cue for refining availability).
* Opposite Eldran’s humble forge — clear visual/symbolic rivalry in layout (players can visit both for recipe chains and material handoffs).

