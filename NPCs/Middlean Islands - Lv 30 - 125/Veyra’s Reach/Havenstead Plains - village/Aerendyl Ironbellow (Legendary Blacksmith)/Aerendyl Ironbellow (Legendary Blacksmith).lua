			Aerendyl Ironbellow (Legendary Blacksmith)

**Role:** Legendary Blacksmith (Master Refiner & Rank III Trainer)
**Location:** Havenstead Plains — elevated forge opposite **Eldran Ironbrow**’s smithy
**Faction:** Order of Restoration (professional affiliation)
**Skillable:** No
**Quests:** None (does not give or start quests)
**Primary Functions:**

* Crafts & refines **Legendary-tier** equipment (requires recipes/materials)
* Refines rare island materials into components required for high-tier crafting
* Acts as **Blacksmith Rank III trainer** (teaches Rank III skills; linked with other profession trainers)
* No “evaluate player-forged items for skill advancement” function

---

## 1 — Appearance & Forge

* Tall, compactly built; controlled, severe expression. Wears heavy leather and a rune-etched smith’s apron.
* Forge is high-walled, elevated, and opposite Eldran’s hut (visual: steady red glow, controlled sparks rising in rhythmic pulses).
* The forge area includes large anvils, a stone trough with cooling runes, and display racks of sample legendary work (cosmetic).

---

## 2 — Services & Limits

**Services Provided**

1. Legendary equipment crafting (requires recipe + required refined materials).
2. Refinement service: turns rare island raw materials into high-tier crafting components.
3. Rank III Blacksmith training (teaches the Rank III skill tier; acts as the Rank III trainer hub and coordinates with other profession trainers for cross-profession unlocks).

**Requirements to use the forge / craft with Aerendyl**

* Player must be Blacksmith Rank III **or** be on a specific permission path (e.g., sanctioned commission brought by NPCs — not a quest, but an approval flag).
* Must bring required materials (many must be refined by Aerendyl’s brother Eldran first).
* Recommended: Order of Restoration reputation >= Friendly (practical gating; Aerendyl may refuse service to players with very low rep).

**No quests originate from him.** He will not initiate, offer, or gate storyline quests.

---

## 3 — Attempting to Attack / Protections

Players cannot kill Aerendyl. If a player attempts hostile action:

1. Aerendyl **effortlessly parries / disables** the attack (scripted non-lethal counter).
2. The aggressor receives:

   * **Knockdown (6s)** and brief reputation penalty (−50 Order rep), and
   * A system message: **“Aerendyl: Your rage is loud. Your skill is not.”**
3. Aerendyl ignores the player for a cooldown period (10 minutes) unless the player performs a reconciliation emote or otherwise restores rep.
4. Attempting to grief Aerendyl causes wider faction consequences (flagged in player record) — discourages abuse.

---

## 4 — Trainer (Rank III) — Unlocks & Perks

As the Rank III trainer for Blacksmithing, Aerendyl provides the following **Rank III skill perks** (these are unlocked by reaching Rank III and training with him):

* **Precision Strike (passive):** Improved success chance on high-tier crafts (+X% success).
* **Metal Resonance (passive):** Reduced rare-material consumption for Legendary crafting.
* **Anvil Mastery (passive):** Reduced craft time for high-tier items.
* **Heat Memory (passive):** On craft failure, higher material refund chance.

> Implementation note: these are trainer-granted passives. Aerendyl is the Rank III anchor — other trainers can link to or reference these perks for cross-profession synergy as needed.

