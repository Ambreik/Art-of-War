# **UNIQUE MECHANICS**

## **1. “Faction Loyalty Scan” — Hidden mechanic**

If a **Zonen player** walks too close (even without interacting), he mutters insults:

* “Get lost, swamp-blood.”
* “Your coin smells of lizards.”

If a **Dendera player** stands still near him for 10 seconds, he plays an animation:
**saluting with pride**
and grants a hidden buff:

**“Dendera Merchant Favor” — +5% vendor discount for 15 min**

*(This triggers only once every 2 hours.)*

---

## **2. “Score the Day” — Hidden Competition Mechanic**

Once every hour he shouts to the Zonen vendor:

> “I’ve served more warriors today than you! Admit defeat!”

The system checks:

* Amount of players who opened his vendor window
  VS.
* Amount who opened the Zonen vendor’s window

If Gorrim is currently **winning**, he gains a temporary aura:
**“Victory of Dendera” — glowing gold around him**
which gives **Dendera players +50 temporary Dendera REP** when interacting.

If he is **losing**, he sulks and gets a debuff:
**“Market Shame” — 10% price INCREASE**
for Dendera players (for 10 minutes).

---

## **3. “Duel System”**

Only Dendera AND enemy faction players can duel him.

✔ **If a Dendera player wins:**

* Gains PvP Points
* Gets **3 minutes of 15% discount**
* He says:

  > “Good! You fight like a true Denderan!”

✔ **If a Dendera player loses:**

* Loses PvP Points
* Gorrim says:

  > “Train harder! Dendera needs strength!”

✔ **If a Zonen player wins:**

* Gains PvP Points
* Gorrim is furious:

  > “No! I demand a rematch! Right now!… after I drink my tea…”

✔ Cooldown: **3 hours**

