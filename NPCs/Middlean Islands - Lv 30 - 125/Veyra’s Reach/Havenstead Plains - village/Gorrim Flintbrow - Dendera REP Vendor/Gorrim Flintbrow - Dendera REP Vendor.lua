			Gorrim Flintbrow

### **Faction Tie:** **Dendera** (Human Continent)
### **Location:**

Located on the **left side of the village main road**, directly across from the Zonen Vendor.
He stands beside a sturdy **wooden market stand** wrapped in Dendera-style cloth patterns (deep green + gold motifs).
His crates are marked with the Dendera Lion Crest.

He and the Zonen vendor face each other **like two rival street performers**, yelling, bragging, competing for passing customers.

