# 🌑 **MINING TRAINER — DAILY QUEST CHAIN (5 DAYS)**

### *“Quarry Days” — Given by Garrin Muld (Rank III Miner Trainer)*

Each quest unlocks the next one. Only **one attempt per day**, unless otherwise noted.

---

# **📘 DAY 1 — “Scout the Veins”**

### **Quest Start (Garrin Muld)**

> “If you want to mine like a real Rank III, you start by listening to the stone.
> The quarry shifts every morning — new veins rise, old ones crack.
> Head in there and bring me samples from today’s live veins.
> Don't chip your fingers.”

---

## **Objective**

✔ Collect **10 Fresh Ore Samples** from Grimstone Quarry.

---

## **Quest Mechanics (Day 1)**

1. **Dynamic Vein Shift:**

   * Only special “Fresh Veins” nodes drop quest samples.
   * They despawn after 90 seconds after someone mines them.

2. **Node Warmup:**

   * Players must “prime” the vein with a 1-sec channel before mining (teaches new players rhythm).

3. **Rock Echoes:**

   * Occasionally a vein hums. Mining a humming vein gives +1 bonus sample (flavor, not guaranteed).

4. **Failure Condition:**

   * Leave quarry zone for 3 minutes → progress resets to 0.

---

## **Quest Turn-In (Garrin)**

> “Good! You brought me enough to smell the whole mine.
> Tomorrow, we deal with the crystals chewing up the tunnels.”

---

## **Reward**

* Small Mining XP
* Small Ore Pouch (5–12 random ores)
* 5% increased mining speed inside Grimstone Quarry for 30 minutes (buff)

---

---

# **📘 DAY 2 — “Shatter the Crystals”**

### **Quest Start**

> “Crystals have surfaced in the lower tunnels — sharp ones.
> They grow fast and block half the pathways.
> Take your pick and *shatter* them before someone trips and becomes a permanent decoration.”

---

## **Objective**

✔ Destroy **6 Razor Crystals** in Grimstone Quarry.

---

## **Quest Mechanics (Day 2)**

1. **Crystal HP Mechanic:**

   * Crystals have high durability and take **5–9 hits** depending on player mining level.
   * They cannot be destroyed by combat skills — *only mining swings*.

2. **Crystal Shriek:**

   * When a crystal is hit, there is a **10% chance** it releases a loud shriek, spawning a harmless dust cloud (visual only).

3. **Sharp Fragments:**

   * Every mined crystal has a **20% chance** to drop a “Sharp Fragment” that can be used to skip 1 hit on another crystal (optional mechanic).

4. **Failure Condition:**

   * If 2 crystals respawn before completion, the quest auto-fails (teaches players efficiency).

---

## **Quest Turn-In**

> “Heh! Those crystals won’t scratch anyone today.
> Tomorrow, you help the boys with the heavy work.”

---

## **Reward**

* Medium Mining XP
* Rockbreaker Oil (increases mining damage by 10% for 20 min)
* 1–3 Sharp Fragments (non-tradeable, optional utility)

---

---

# **📘 DAY 3 — “Cart Escort”**

### **Quest Start**

> “The miners are moving a load out of the lower tunnels.
> The cart’s a stubborn old mule — it sticks, tips, and screams when you touch it.
> Walk with it. Keep it safe. Push it if it stops yelling.”

---

## **Objective**

✔ Escort the **Mining Cart** safely through the lower tunnels.
(The cart moves through 3 segments.)

---

## **Quest Mechanics (Day 3)**

### **1. Cart Momentum System**

The cart has three states:

* **Stuck** — player must strike the wheels with their pick (3 hits).
* **Normal** — moves slowly along rails.
* **Fast** — after a successful downhill push, moves quickly.

Players must manage momentum so the cart doesn’t derail.

---

### **2. Tunnel Hazards**

* Small cave-ins randomly occur (**non-lethal**, just push cart back on track).
* Falling rocks briefly slow the cart.
* Minor critters run across the rails (cosmetic).

---

### **3. Escort Threats**

* 3 mini “Stone Elemental Fragments” spawn at specific points.
* These enemies **cannot kill the cart**, but **delay it** unless defeated quickly.

---

### **4. Failure Condition**

* Cart derails 3 times
  OR
* Cart gets stuck for more than 40 seconds.

---

## **Quest Turn-In**

> “Cart made it up here in one piece?
> Good. Last time it decided to nap on the tracks.
> Let’s see if you can find something rarer tomorrow.”

---

## **Reward**

* Medium-High Mining XP
* Cartwheel Grease (temporary buff: +15% reduced stamina drain while mining)
* Chance for 1 Rare Ore (2–4%)

---

---

# **📘 DAY 4 — “The Lost Lump”**

### **Quest Start**

> “Once a day the Quarry reveals a ‘Lost Lump.’
> A half-buried node from the deep layers pushed up by pressure.
> Find it before it cools — or someone else steals it.”

---

## **Objective**

✔ Find and mine **1 Lost Lump** node.

---

## **Quest Mechanics (Day 4)**

1. **Randomized Spawn:**

   * The Lost Lump appears in **one** of 8 fixed locations.
   * Only *one* active per player at a time.

2. **Heat Meter:**

   * The node begins “hot.”
   * Each minute outside the ground it loses heat.
   * Mining a cold Lump yields **1 ore**.
   * Mining a warm Lump yields **3 ore**.
   * Mining a hot Lump yields **5 ore**.
     (Total time: 3-minute window.)

3. **Seismic Clues:**

   * Every 20 seconds, players hear directional rumbling pointing towards the correct area.

4. **Failure Condition:**

   * If heat reaches 0, the Lump shatters → quest fails.

---

## **Quest Turn-In**

> “Still warm! Good work.
> Tomorrow… the real test.
> Hope your arms slept well.”

---

## **Reward**

* High Mining XP
* Warm Stone Token (used only for Day 5)
* Bag of Quarry Finds (contains 8–14 ores)

---

---

# **📘 DAY 5 — “Mastery Strike”**

### **Quest Start**

> “Rank III isn’t about strength — it’s about one perfect hit.
> I’ve marked a deep vein under the Quarry.
> Use this charge to expose it.
> Then break it before the tunnel closes again.”

---

## **Objective**

✔ Use Garrin’s Explosive Charge to expose a **Reinforced Vein**
✔ Mine the Reinforced Vein before the dust collapses the chamber

---

## **Quest Mechanics (Day 5)**

### **1. Timed Explosion Phase**

* Player places the explosive → 4-second fuse → dust cloud.
* A Reinforced Vein appears and the player gets a **40-second timer**.

---

### **2. High-HP Vein Mechanics**

* Requires **8 perfect hits**.
* Perfect hits occur when:

  * A glowing “strike marker” appears briefly
  * Player attacks during the glow window
  * Otherwise deals reduced damage

---

### **3. Tunnel Collapse Pressure**

At 20 seconds remaining:

* The chamber begins rumbling
* Vein becomes unstable
* Missed hits have a chance to cause player stagger

---

### **4. Warm Stone Bonus (from Day 4)**

* Player may use the token ONCE to:

  * Freeze timer for 4 seconds
    OR
  * Add 1 free perfect hit

(Encourages finishing previous day’s quest well.)

---

### **5. Failure Condition**

* Timer reaches 0
* Player misses 4 perfect-hit opportunities
* Player leaves chamber

---

## **Quest Turn-In**

> “You struck true.
> That’s the arm of a Rank III Miner.
> Come back any day — the Quarry always has new lessons.”

---

## **Reward (Final Day)**

* Very High Mining XP
* High-Tier Ore Bundle (10–16 ores)
* Chance (5–7%) to receive a **Refined Rare Ore**
* Temporary passive for that day: **+15% node stability** (inside Grimstone Quarry)

