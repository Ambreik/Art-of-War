			Seraphine Willowmire — Herbalism Trainer (Rank III)

### *“The Green-Eyed Scribe of Roots & Remedies”*

**Type:** Profession Trainer (Herbalism Rank III)
**Profession Category:** Herbalism / Alchemical Harvesting
**Faction Affiliation:** Order of Restoration
**Location:** Havenstead Plains — **eastern side of the village**, in a vine-covered boutique named **“The Verdant Nest”**
**Skillable:** ❌ No
**Combat Role:** Flees and hides if threatened
**Specialty:** Rare island flora, advanced combination hints, extractor techniques

---

# **🌱 Appearance & Personality**

Seraphine Willowmire is a tall, graceful woman in her early forties, with
long **moss-green hair**, usually tied with braided vines. Her eyes are bright
emerald, said to “glow” at dusk due to years of handling luminescent herbs.

She wears a layered herbalist robe, decorated with pressed flowers and
silver-thread embroidery depicting leaves, roots, and spores. She moves
slowly and deliberately, as if every step is part of a ritual.

Her personality:

* **Calm, scholarly, highly intelligent**
* **Cryptic**, often speaking in riddles when giving hints
* **Incredibly passionate** about plants, to the point of lecturing players
  for harvesting “improperly”
* Has a **dry sense of humor**, usually directed at careless alchemists
* Known for giving cryptic *recipe hints*, not full instructions

Her boutique **“The Verdant Nest”** is filled with hanging herbs, bubbling essence jars,
a small indoor aquaponic pool for Coralvine Bloom, and an enormous leaf-shaped
worktable stained with years of potion residue.

---

# **🌿 NPC Mechanics**

### ✔ **Rank III Herbalism Training**

Provides training for high-tier herbalism techniques, including:

* **Regrowth Prediction** — teaches players when a node will regrow
* **Dual-Harvest Mastery** — increases yield from double-regrowth herbs (Ashen Dewleaf)
* **Aquatic Flora Handling** — required to safely harvest Coralvine Bloom
* **Stabilizer Knowledge** — makes Fellmoss-based recipes more effective

### ✔ **Hint System (Special Feature)**

Seraphine occasionally provides **veiled recipe hints** when spoken to.
These are NOT guaranteed — she offers them randomly when greeting a player.

Example hints:

* “Silverpetal Root thrives only when paired with *cooling agents*.”
* “Never mix Verdant Bloom with raw Ironbark. One heals; the other hardens.
  Together? They bite.”
* “Coralvine Bloom sings only underwater. Listen closely — its rhythm tells
  you when to harvest.”
* “Ashen Dewleaf’s second bloom is its strongest, but only if you leave
  the stem intact.”
* “Serpentvine hates company. Mix it alone unless you desire… unintended
  reactions.”
* “Silverpetal tonics shine brightest when brewed under moonlight.”
* “A pinch of Fellmoss solves more alchemical disasters than any healer.”
* “Ironbark Spores whisper. If they hiss, leave.”

These hints tie to actual crafting behaviors, but don’t reveal exact recipes.

### ✔ **Boutique Closing Mechanic**

If monsters enter Havenstead or get too close to her boutique:

* Seraphine instantly closes the door
* Curtains drop
* A green mist fills the windows
* She refuses interaction for **8 minutes after combat ends**

### ✔ **Daily Herb Forecast (Flavor Only)**

Once per day, the player may ask:

> “Seraphine, what does the island grow today?”

She will give one of 10 random atmospheric lines describing herb regrowth,
spawn zones, or weather affecting plant growth.
(This has NO gameplay effect; pure immersion.)

### ✔ **Hidden Interaction — “The Whispering Shelf”**

Inside The Verdant Nest is a dusty wooden shelf.
If a player clicks it **three times**, Seraphine reacts:

> “Ah… you found the Whispering Shelf. Curious fingers earn curious knowledge.”

She gives a **1-hour buff**:
**⟡ Botanic Insight — Allows detecting rare herbs within 40 meters.**
(Does not highlight nodes; gives a faint sound cue only.)

Cooldown: Once per day.

