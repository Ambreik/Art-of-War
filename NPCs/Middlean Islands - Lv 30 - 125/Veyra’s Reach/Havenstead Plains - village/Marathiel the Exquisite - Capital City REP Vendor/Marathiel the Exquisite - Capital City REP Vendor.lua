			Marathiel the Exquisite

### **Faction Tie:** Capital City
### **Location:**
Placed **near the small stone bridge** leading toward the upper side of the village, slightly **away from the two male vendors**.
She stands under a **bright red silk canopy**, surrounded by crates with ornate stamps from Capital City.

From her position, she can easily **watch the two male vendors argue**, and she constantly smirks at them.

---

# **IDENTITY & PERSONALITY**

* Elegant, playful, sarcastic.
* Professional when it comes to business, but mischievous enough to **provoke the two arguing vendors** by praising the opposite faction whenever one of them comes nearby.
* Very aware of village gossip and secretly **keeps score** of which vendor is winning the “player traffic competition.”

She also sees herself as the “civilized one” among them.

---

