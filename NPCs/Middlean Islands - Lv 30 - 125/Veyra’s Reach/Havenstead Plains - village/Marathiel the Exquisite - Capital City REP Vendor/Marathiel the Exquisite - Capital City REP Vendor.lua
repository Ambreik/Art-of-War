# **COMBAT AI TEMPLATE**

### **Combat Style:**

Elegant duelist — fast, evasive, uses agility and light magic.

### **Abilities**

1. **“Capital Parry”**
   Blocks one incoming attack every 10 sec.

2. **“Silkstep Dash”**
   Short-distance dash to reposition.

3. **“Noble’s Bloom”**
   Small AoE knockback of glittering light.

4. **“Market Price Slash”**
   Light damage, but applies a “Price Cut” debuff that reduces player damage by 3%.

5. **“Elegant Disengage”**
   At 20% HP, she leaps backward and restores a small shield.

### **Behavior**

* Never kills players: stops at 1 HP.
* Never flees.
* Will not fight monsters.
* Will restart duel if the player tries to exploit terrain.

