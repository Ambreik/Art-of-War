# **UNIQUE MECHANICS**

### **1. “Capital Pledge” — Hidden REP Bonus**

Once per day, if the player bows to her (emote `/bow`), she responds:

> “Ah, respect. A rare treasure. Your courtesy will be remembered.”

This grants:
✔ **+25 Capital City REP**

**But only once per day per player.**

If the player tries again:

> “Flattery is lovely… but not *that* lovely.”

---

### **2. “Merchant’s Glance” — Hidden Discount Trigger**

If a player **walks near her canopy carrying a cosmetic item from Capital City**, she notices it:

> “Oh! You’ve brought elegance to this barbaric island.”

She automatically grants the player a **10-minute 5% discount**.

This is **separate from duel discounts** and can stack.

---

### **3. “Trade Gossip” — Dynamic Interactions**

If she sees the two male vendors arguing, she makes comments, such as:

* “There they go again… like roosters fighting over an empty nest.”
* “Someone should tell them the village doesn’t revolve around their pride.”
* “If only their goods were as sharp as their tongues.”

If both male vendors have more than **50 players interacting with them that day**, she becomes jealous and complains:

> “People these days… no appreciation for refinement.”

---

### **4. Duel System**

Players from **any faction** can challenge her, but she fights using **evasion and finesse**, not brute force.

If the player **wins**:

* gains **PvP Points**
* gets a **3-minute 15% discount** on all her goods
* she says:

  > “Congratulations… though I may have let you win. Maybe.”

If the player **loses**:

* loses **PvP Points**
* she taunts lightly:

  > “Grace beats strength. Every time.”

Cool-down: **3 hours**.

