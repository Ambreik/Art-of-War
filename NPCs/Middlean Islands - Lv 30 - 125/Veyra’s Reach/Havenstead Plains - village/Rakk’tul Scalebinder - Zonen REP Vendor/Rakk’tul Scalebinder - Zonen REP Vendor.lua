# **UNIQUE MECHANICS**

## **1. “Cold-Blooded Insight” — Hidden mechanic**

If a **Zonen player** stands near him without interacting (8+ seconds):
He whispers secret hints about Zonen crafting, survival, or history.

If a **Denderan player** stands too close:
He slowly turns his head and stares until they leave.

Zonen players get a hidden buff:
**“Zonen Precision” — +3% item durability for 20 min**
*Triggers once every 90 minutes.*

---

## **2. “Market Rivalry System”**

Once per hour he announces calmly:

> “Another Zonen warrior honors me. Your move, human.”

System checks number of interactions:

* Zonen Vendor interactions
  VS
* Dendera Vendor interactions

If Rakk’tul is **winning**, he gains an aura:
**“Dominance of Scales” — blue glow**
that gives Zonen players **+60 Zonen REP** upon interacting.

If he is **losing**, he activates a snark session:

**“Cold Rebuke” — Prices are +15% for 10 minutes**
(because he is “correcting emotional imbalance”).

---

## **3. “Duel System”**

Zonen players **and enemy faction players** can challenge him.

✔ **If a Zonen player wins:**

* Gains PvP Points
* Unlocks **3 minutes of 15% discount**
* Rakk’tul says:

  > “Skill acknowledged. Conduct your trade.”

✔ **If a Zonen player loses:**

* Loses PvP Points
* Rakk’tul says:

  > “Do not return until you sharpen your instincts.”

✔ **If a Denderan wins:**

* Gains PvP Points
* Rakk’tul hisses:

  > “Unacceptable. I must meditate.”

✔ Cooldown: **3 hours**

