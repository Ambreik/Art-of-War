			Rakk’tul Scalebinder

### **Faction Tie:** **Zonen** (Lizard-Folk Continent)
### **Location:**

Placed directly across from the Dendera Vendor on the opposite side of the village road.
His stall is decorated with **woven scale-pattern mats**, bone ornaments, and hanging dried herbs used in Zonen shamanic rituals.

A carved wooden sign shaped like a **lizard head** hangs above him.

He stands with perfect posture, arms crossed, and a smug half-smile aimed constantly at Gorrim (Dendera vendor).

