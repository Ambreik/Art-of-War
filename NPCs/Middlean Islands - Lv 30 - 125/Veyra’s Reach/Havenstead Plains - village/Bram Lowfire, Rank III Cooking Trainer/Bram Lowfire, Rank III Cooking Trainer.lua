			Bram Lowfire, Rank III Cooking Trainer

### **Rank:** III (Master Chef)
### **Type:** Trainer / Vendor (No default questline, but has optional comedic quests)
### **Location:** Havenstead Village – beside the central smoking stove hut, where aromas constantly drift out.

---

# **1. OVERVIEW**

Bram Lowfire is Havenstead’s eccentric, wide-eyed, anxious—but extremely talented—Master Chef.
He is one of the few Rank III trainers who:
✔ sells **all Rank I, II, AND III cooking goods**
✔ provides **starter ingredients**, **advanced preps**, **full meal kits**, and **rare island-based dishes**

He is a **coward in love** with the Herbalist Trainer, shy to confess, but bold in every other situation — especially when monsters threaten the village or, worse… *when they scare her*.

He also becomes part of a future **love-triangle questline** with the Hunter NPC.

---

# **2. LOCATION & SHOP ENVIRONMENT**

His boutique is a half-open cooking stand built around:

* a **big clay oven**, always glowing
* a **hanging rack of herbs** (all stolen purchased from the Herbalist… with excuses)
* various drying meats
* crates of vegetables in messy piles
* a sign reading: *“No Tasting Unless You Want To."*

Occasionally, bubbles erupt from a pot beside him — he apologizes every time, even when it’s not his fault.

---

# **3. APPEARANCE**

* tall, sandy-blonde hair tied in a messy bun
* light apron splattered with 100 stains but immaculately clean hands
* carries a pan like a shield
* Rank III Chef badge pinned proudly — slightly crooked
* smells like garlic and mild panic

His eyes sparkle whenever the Herbalist NPC walks by.
When she looks back?
He panics and drops something sizzling.

---

# **4. PERSONALITY & BEHAVIOR**

**General Traits:**

* kind, warm-hearted, expressive
* extremely shy around the Herbalist
* freezes when she compliments anything
* a coward in romance
* but *shockingly brave* in combat when she’s in danger
* humming melodies when cooking (poorly)

**Combat Behavior (unique):**

* throws hot pans, ladles, and blazing oil in a fight
* gains a temporary buff called **“Heroic Courage (Triggered by Love)”** if the Herbalist NPC is within 25m
* shouts dramatic lines like “NOT THE HERBALIST!!—I mean—NOT THE VILLAGE!!”

---

# **5. TRAINER MECHANICS**

### ✔ Cooking Trainer – Rank III

He teaches all cooking ranks and recipes.

### ✔ Vendor Mechanics – 3-Tier Shop UI

Vendor window has **three buttons**:

---

## **[ I ] – Rank I Goods**

Basic ingredients & early cooking items.

**Examples:**

* Simple Wheat Flour
* Coarse Salt
* Field Vegetables
* Clear Water Flask
* Basic Spice Mix
* Beginner’s Cooking Kit

---

## **[ II ] – Rank II Goods**

Intermediate cooking reagents & meal components.

**Examples:**

* Aromatic Herb Bundle
* Sliced Tender Meat
* Island Fish Filet
* Sun-Dried Pepper Crates
* Slow-Burn Broth Base
* Mid-Tier Chef’s Set

---

## **[ III ] – Rank III Goods**

High-end, elite reagents crafted on the island or refined by Bram himself.

**Examples:**

* Smoked Skymeat (elite mount-protein)
* Ember-Marinated Reeffish
* Fellmoss-Infused Oil (from Herbalist’s stall… she knows)
* Silverpot Cooking Pan (tool)
* Feast of Havenstead Heroes (major food buff)
* “Heartfire Spice” (secret recipe he’ll never admit is inspired by Herbalist NPC)

