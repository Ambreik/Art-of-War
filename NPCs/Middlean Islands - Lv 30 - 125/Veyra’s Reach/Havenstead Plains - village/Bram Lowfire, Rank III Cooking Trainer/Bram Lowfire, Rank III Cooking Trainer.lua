# **7. QUESTS (Optional, Funny & Light)**

You asked for 1–2 quests, comedic, non-serious, aligned with island tone.

### **Quest 1 — “THE GREAT POT CATASTROPHE”**

**Start Level:** 40
**Quest Giver:** Bram Lowfire
**Type:** Comedy / Errand

**Quest Text:**
“Listen… the pot is angry. I don’t know why. I might’ve insulted its soup.
Could you — um — take it outside before it explodes? I’m scared. Of the pot.”

**Objective:**

* Pick up **Unstable Cooking Pot**
* Carry it 40 meters away from the village
* Throw it into a small pit

**Mechanic:**
Carrying the pot gives you a buff:
**“Unstable Contents” – random burping steam clouds every 5 seconds.**
Steam pushes the player slightly upward. Visual comedy.

**Ending:**
The pot ignites in a small harmless explosion and a crab pops out running away.

**Bram’s Turn-In Dialogue:**
“You survived!?
…I mean of course you survived! Easy job for a brave adventurer.
Also don’t mention the crab. It’s complicated.”

**Reward:**
Coins + small food bundle.

---

### **Quest 2 — “HOT SOUP, HOT HEARTS”**

(This lightly ties to the future love-triangle.)

**Start Level:** 45
**Quest Type:** Delivery / Embarrassing Comedy

**Quest Text:**
“I made a special warming soup for the Herbalist.
It’s not romantic! (It is.)
Can you deliver it? I’m too… uh… busy hiding.”

**Objective:**
Take **Warming Heart-Soup** to the Herbalist Trainer.

**Mechanic:**
The soup constantly emits steam shaped like hearts.
You cannot mount while carrying it.

**Funny Twist:**
When delivering, the Herbalist says:
“Oh, this smells lovely! Did Bram make it?
…He’s so sweet.
But I thought the Hunter was bringing me dinner tonight.”

*Player returns.*

**Bram’s Reaction:**
“Oh… he is?
Well… well GOOD! I mean… good for them.
…excuse me I need to go scream—stir! I meant stir.”

Rewards: Cooking XP + 3 random foods.

