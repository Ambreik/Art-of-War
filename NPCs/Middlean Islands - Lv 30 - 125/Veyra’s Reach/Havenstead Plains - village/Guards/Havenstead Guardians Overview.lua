### ⚙️ **Shared Guard Mechanics**

| Mechanic              | Description                                                                                                                      |
| --------------------- | -------------------------------------------------------------------------------------------------------------------------------- |
| **Skillable NPCs**    | Can enter combat when provoked or when monsters breach the perimeter. They defend NPCs, players under attack, and the Commander. |
| **Knockback Skill**   | All three share a shoulder-based Knockback CC skill to interrupt or push back enemies.                                           |
| **Patrol AI**         | Move in semi-random paths within a fixed patrol radius; pause intermittently (3–15 seconds) before rerouting.                    |
| **Combat Priority**   | Target aggressive creatures first, then hostile or PvP-flagged players.                                                          |
| **PvP Behavior**      | In PvP stance, their attacks pierce and negate armor bonuses; minimal damage but high control.                                   |
| **Engagement Speed**  | Movement speed increases by 25% during combat; they pursue enemies briefly before returning to patrol routes.                    |
| **Team Coordination** | If one guard is attacked, nearby guards and the Commander may join combat within a 40m radius.                                   |

