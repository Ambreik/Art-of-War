			Havenstead Guardians Overview

**Type:** Defensive NPCs / Village Guards
**Faction:** Order of Restoration
**Rank:** Elite Soldiers
**Level Range:** 80–85
**Location:** Havenstead Plains – Gate and Perimeter Patrol
**Common Mechanics:** Shared patrol AI, Knockback skill, dynamic aggression, pathing randomness, and protective behavior toward Commander and villagers.

---

### 🛡️ **1. Guardian Alric Stoneveil**

**Appearance & Equipment:**
Broad-shouldered, wearing heavy bronze-steel armor with a blue Order tabard. His helmet visor is often raised, showing a scarred jawline and weathered expression. Keeps his hand near his longsword hilt, alert even when resting.

**Behavior & Personality:**
Alric is the senior among the three — steady, methodical, and cautious. He often pauses at corners or gates longer than the others, scanning the horizon before continuing. Known for muttering tactical notes like “Winds carry ill omens…” or “Commander won’t like this silence.”

**Patrol Pattern:**

* Starts from the **northern gate**, patrols to the **central square**, then walks outside the gate perimeter briefly before returning.
* Occasionally stops at the fishing bridge, standing watch silently for 10–15 seconds before moving again.
* Moves slower than the others, symbolizing discipline and experience.

**Combat Behavior:**

* **Attack Type:** Defensive sword strikes (slow but heavy).
* **Special Skill:** **Shoulder Bash (Knockback)** – pushes back enemies 5 meters, stunning them for 1 second.
* Prioritizes monsters attacking NPCs first.
* Rarely engages in PvP unless provoked by a flagged player inside guard jurisdiction.

**Notes:**

* High defense, moderate damage.
* Increases the morale of nearby guards (+5% attack speed) when within 10 meters.
* Occasionally salutes the Commander when paths cross.

---

### ⚔️ **2. Guardian Ryn “Ironstride” Daleris**

**Appearance & Equipment:**
Slimmer and younger than Alric, with twin short spears crossed on his back. Wears lighter armor with silver engravings and an exposed helm revealing sharp eyes and short dark hair.

**Behavior & Personality:**
Ryn is restless, alert, and often circles the walls faster than his comrades. He’s known to whistle or hum during patrols, creating ambient life within the village. Occasionally comments on player actions (“You seem far from home, traveler.”).

**Patrol Pattern:**

* Random wandering around **eastern perimeter**, moving between the **blacksmith area**, **outer fences**, and **village gates**.
* Frequently exits the gate briefly, scanning the cliffs before returning.
* Often pauses near torches or fences for 3–5 seconds, then turns sharply to change direction.

**Combat Behavior:**

* **Attack Type:** Fast piercing thrusts; ignores 10% of armor during combat.
* **Special Skill:** **Shield Rush (Knockback)** – short sprint and shoulder bash that interrupts attacks and forces enemies backward 4 meters.
* Engages rapidly in combat; movement speed +25% during fight.
* Focuses on disabling enemies to protect other NPCs or the Commander.

**Notes:**

* Slightly lower defense but higher attack speed.
* Will retreat and regroup if surrounded by more than 5 enemies.
* Occasionally responds to chat emotes or greetings from players.

---

### ⚔️ **3. Guardian Mareth Halvorn**

**Appearance & Equipment:**
A tall woman in polished armor trimmed with gold filigree. Her helmet is fully enclosed, and her cape bears the insignia of the **Order of Restoration’s First Crusade** — marking her as a veteran of the old war.

**Behavior & Personality:**
Mareth is calm, disciplined, and speaks rarely. She occasionally kneels in front of the central statue in Havenstead Plains, showing reverence before resuming patrol. She observes players silently but attentively, tracking anyone who lingers too close to storage buildings or the alchemist’s hut.

**Patrol Pattern:**

* Moves between the **western outer gate**, **training grounds**, and **village well**.
* Occasionally exits beyond the gate for short periods, observing the plains outside before turning back.
* Stops longer (up to 20 seconds) near the well or statue before continuing.

**Combat Behavior:**

* **Attack Type:** Defensive halberd swings; slightly longer reach and moderate armor penetration.
* **Special Skill:** **Repel Strike (Knockback)** – shoulder check that pushes enemies 6 meters back, briefly stunning them.
* Uses defensive tactics, prioritizing cover and guard coordination.

**Notes:**

* Balanced stats; excels at defending static positions.
* Gains +10% armor when near Commander (loyalty effect).
* Cannot be lured far from the gates (leash radius 40m).

