			Eldric Vaelthorne – Archaeologist Rank III

	Location : Middlean Islands / Netar Island / Zone 2 Vaelthorn Frontier / Bastion of Taranmere (Courtside, near excavation and documentation area)

---

	Appearance : An experienced middle-aged scholar dressed in layered expedition robes reinforced with light leather padding. His clothing is weathered and stained
with dust, ash, and old ink. He carries a satchel filled with scrolls, parchment rolls, charcoal sticks, and measuring tools. A monocle-like lens hangs from his 
neck, used to examine inscriptions and relics. His posture is slightly hunched from years of study, yet his eyes remain sharp and alert, constantly scanning the 
surroundings.

---

	Personality : Calm, methodical, and deeply focused on his work. He speaks in a measured tone, often pausing to consider his words carefully. While not 
unfriendly, he is clearly more interested in discoveries than casual conversation. He shows visible concern about the dangers deeper inside the Bastion and 
frequently warns players not to underestimate the ruins. His curiosity outweighs his fear, but he knows when retreat is necessary.

---

	Behaviour : When hostile monsters approach the courtside area, the Archaeologist immediately abandons his position and flees toward the main castle entrance, 
seeking safety behind Restoration forces.

---

	Role :
* Role(s) : Quest Giver
	The Archaeologist oversees the study of the Bastion’s ruins, relics, and fallen remains. He provides investigative tasks focused on uncovering the history, 
purpose, and corruption tied to Taranmere.

---

	Mechanics :

* Type : Quest Giver
* Reputation : Yes – Restoration faction
* Interaction Limitations : Available to all players present in the area
* PvP : Cannot be attacked; not PvP-flagged
* Respawn Time : Standard NPC respawn rules if removed by scripted events
* Other Mechanics :
	* Flee mechanic when hostile enemies enter the courtside area
	* Will not follow players or move deeper into the castle

