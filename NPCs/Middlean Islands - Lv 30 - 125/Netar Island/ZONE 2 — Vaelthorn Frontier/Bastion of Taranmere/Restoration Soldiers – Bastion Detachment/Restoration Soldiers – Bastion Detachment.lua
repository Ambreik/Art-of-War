			Restoration Soldiers – Bastion Detachment

	Location :
Middlean Islands / Netar Island / Zone 2 Vaelthorn Frontier / Bastion of Taranmere (courtyards, inner corridors, defensive chokepoints)

---

	Named Personnel :

* Captain Alric Venn
* Sergeant Ilyra Cross
* Corporal Bren Hollow
* Shieldbearer Toren Falk
* Shieldbearer Maelis Rowe
* Watchman Corren Hale

---

	Appearance : The Restoration soldiers wear standardized plate-and-mail armor bearing the Restoration insignia, dulled by soot and age. Their armor shows clear 
signs of prolonged deployment—dented shields, chipped blades, and faded tabards. Each soldier carries a spear or sword and shield, with minimal ornamentation. 
Helmets remain on at all times during patrol, reinforcing their disciplined presence.

---

	Personality : Professional, disciplined, and largely silent. Soldiers speak only when necessary, maintaining a focused and vigilant demeanor. They treat 
civilians and researchers with protective seriousness and adventurers with cautious respect.

---

	Behaviour :

* Maintain fixed patrol routes and guard positions
* Actively engage hostile entities
* Do not flee
* Prioritize protection of non-combat NPCs
* Reform defensive lines after combat

---

	Role :

* Role(s) : Guard / Defender
	Restoration Soldiers secure the Bastion, protect research personnel, and hold key positions against hostile incursions.

---

	Mechanics :

* Type : Skillable NPCs (Group)
* Reputation : Yes – Restoration faction
* Interaction Limitations : Available to all players
* PvP : Cannot be attacked; not PvP-flagged
* Respawn Time : Standard NPC respawn rules
* Other Mechanics :
	* Assist players in nearby combat
	* React dynamically to seal proximity danger zones
	* Provide ambient combat support without overshadowing players

---

	### Skills :

* Shield Wall Formation
	* Description : Soldiers form a defensive line to halt enemy advances.
	* Effect : Reduced damage taken and improved control of enemy movement.

* Focused Thrust
	* Description : Coordinated spear or sword attacks against priority targets.
	* Effect : Increased damage against elite or charging enemies.

