			Thalos Merrin – Examiner

	Location :
Middlean Islands / Netar Island / Zone 2 Vaelthorn Frontier / Bastion of Taranmere (inside the castle, near sealed chambers, execution halls, and ritual-marked floors)

---

	Appearance :

Thalos Merrin is a lean, sharp-featured man clad in darkened scholar’s armor reinforced with metal inlays and rune-etched plates. His attire blends academic robes with light defensive gear, allowing movement while offering minimal protection. A satchel filled with measuring tools, charcoal sticks, and parchment hangs at his side. His gloves are stained with ash and old residue, evidence of frequent examination of cursed surfaces and remains. His gaze is constantly focused, scanning walls, floors, and objects rather than people.

---

	Personality :

Thalos is detached, analytical, and emotionally restrained. He speaks with clinical precision, rarely raising his voice or indulging in speculation. He treats the Bastion as a case study rather than a tragedy, which can make him appear cold—but his dedication to understanding the corruption is absolute.

---

	Behaviour :

When hostile entities approach, Thalos immediately disengages and flees toward the main entrance**, relying on Restoration soldiers and scouts for protection. Outside of danger, he remains stationary near points of interest, examining markings, seals, and structural damage.

---

	Role :

* Role(s) : Examiner
	Thalos specializes in identifying corruption patterns, structural anomalies, and ritual remnants within the Bastion, serving as a key analytical figure for 
Restoration efforts.

