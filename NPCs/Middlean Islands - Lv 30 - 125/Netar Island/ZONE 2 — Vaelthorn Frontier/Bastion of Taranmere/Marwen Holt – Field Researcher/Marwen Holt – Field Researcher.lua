			Marwen Holt – Field Researcher

	Location : Middlean Islands / Netar Island / Zone 2 Vaelthorn Frontier / Bastion of Taranmere (Courtside, near temporary research setups and document tables)

---

	Appearance : A young but seasoned field researcher dressed in practical travel attire reinforced with light leather guards. His cloak is frayed at the edges and
dust-covered from constant movement around the ruins. He carries a small ledger, ink vial, and charcoal sticks secured to his belt, along with a compact satchel 
filled with collected notes and sketches. His posture is alert and slightly tense, often glancing around as if expecting danger at any moment.

---

	Personality : Nervous but dedicated. Marwen is clearly aware that he is out of his depth in such a dangerous location, yet he takes his responsibilities 
seriously. He speaks quickly, sometimes stumbling over his words, especially when discussing the undead presence. Despite his anxiety, he shows genuine enthusiasm 
when talking about discoveries and is eager to assist anyone helping with research efforts.

---

	Behaviour : If hostile monsters approach the courtside area, Marwen immediately abandons his position and flees toward the main entrance of the castle, seeking 
protection among the Restoration soldiers.

---

	Role :
* Role(s) : Quest Giver
	Marwen assists senior investigators by delegating minor research and documentation tasks to players, helping gather information from the Bastion without 
venturing into deeper, more dangerous areas himself.

---

	Mechanics :

* Type : Quest Giver
* Reputation : Yes – Restoration faction
