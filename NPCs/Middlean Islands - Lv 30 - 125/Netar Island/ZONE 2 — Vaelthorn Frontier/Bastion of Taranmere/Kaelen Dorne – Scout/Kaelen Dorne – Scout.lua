			Kaelen Dorne – Scout

	Location : 
Middlean Islands / Netar Island / Zone 2 Vaelthorn Frontier / Bastion of Taranmere (inside the castle, outer corridors, breached walls, and inner courtyard edges)

---

	Appearance : Kaelen Dorne wears reinforced scout leathers layered with chain-thread stitching along the shoulders and forearms. His armor bears scratches and 
faded heraldic markings from prior campaigns, partially obscured by a weathered cloak dyed in muted stone-gray tones. A long hunting bow rests over one shoulder, 
its string wrapped in protective cloth, while multiple signal flares and chalk markers hang from his belt. His stance is grounded and steady, often leaning against 
broken masonry as he surveys distant movement.

---

	Personality : More outspoken than his counterpart, Kaelen is pragmatic and blunt. He values preparation over heroics and is openly critical of reckless 
adventurers. Despite this, he shows loyalty to those who prove competent and displays a strong sense of duty toward protecting the research effort within the 
Bastion.

---

	Behaviour : Kaelen actively monitors breached sections of the Bastion and outer interior approaches. He does not flee when monsters attack. Instead, he 
prioritizes ranged suppression, attempting to draw enemies away from vulnerable NPCs and key access points.

---

	Role :
* Role(s) : Scout
	Kaelen specializes in long-range surveillance and threat interception, providing early warnings and battlefield control during hostile incursions.

---

	Mechanics :

* Type : Skillable NPC
* Reputation : Yes – Restoration faction
* Interaction Limitations : Available to all players
* PvP : Cannot be attacked; not PvP-flagged
* Respawn Time : Standard NPC respawn rules if killed during scripted events
* Other Mechanics :
	* Engages enemies at range
	* Prioritizes targets approaching non-combat NPCs
	* Does not flee during combat events

---

	Skills :

* Skill 1 : Suppression Shot
	* Description : Kaelen fires a heavy arrow designed to pin or stagger enemies advancing through choke points.
	* Effect : Brief movement slow and reduced attack speed on affected enemies.

* Skill 2 : Signal Flare
	* Description : Launches a flare to mark hostile movement paths or alert nearby allies.
	* Effect : Reveals enemies in the affected area and slightly increases ally awareness range.

