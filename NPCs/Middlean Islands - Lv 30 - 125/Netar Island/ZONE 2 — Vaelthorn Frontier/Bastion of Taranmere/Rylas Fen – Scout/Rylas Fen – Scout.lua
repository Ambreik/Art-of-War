			Rylas Fen – Scout

	Location : Middlean Islands / Netar Island / Zone 2 Vaelthorn Frontier / Bastion of Taranmere (inside the castle,shallow inner corridors and observation points)

---

	Appearance : Rylas Fen is a lean, sharp-eyed scout clad in lightweight leather armor reinforced with dark cloth layers for silent movement. His gear is worn but
meticulously maintained, with minimal metal to avoid noise. A short blade rests at his side, while a compact crossbow is strapped across his back. His hood is often
pulled low, shadowing his face, and his posture is perpetually alert, weight shifted forward as if ready to move at a moment’s notice.

---

	Personality : Quiet, disciplined, and observant. Rylas speaks only when necessary, choosing his words carefully. He trusts action over conversation and shows 
respect toward capable adventurers. Though calm on the surface, he harbors deep concern about the crusader presence deeper within the Bastion and treats the seal 
area with visible caution.

---

	Behaviour : Rylas patrols shallow interior sections of the castle, stopping at vantage points to observe crusader movements. He does not flee when monsters 
approach; instead, he engages them directly to protect investigative personnel and maintain safe observation routes.

---

	Role :
* Role(s) : Scout
	Rylas gathers intelligence on enemy patrols and secures safe passage routes within the Bastion’s upper and inner sections.

---

	Mechanics :

* Type : Skillable NPC
* Reputation : Yes – Restoration faction
* Interaction Limitations : Available to all players
* PvP : Cannot be attacked; not PvP-flagged
* Respawn Time : Standard NPC respawn rules if killed during scripted events
* Other Mechanics :
	* Does not flee when enemies approach
	* Actively engages hostile mobs within assigned patrol zones

---

	Skills :

* Skill 1 : Tactical Mark
	* Description : Rylas marks a hostile target, exposing weak points and improving ally accuracy against it.
	* Effect : Marked enemy takes increased damage from players for a short duration.

* Skill 2 : Rapid Disengage
	* Description : A swift backward maneuver allowing Rylas to reposition while avoiding incoming attacks.
	* Effect : Brief evasion boost and threat reduction.

