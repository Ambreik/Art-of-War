		Questline — “Lorian’s Smuggling Run”

	Quest 1: “Trial of the Tides”

* Objective : Deliver rare resources (Stormforged Ore or Glassfin Coral) from Stormfall Coves to hidden inlet in Tideglass Passage without being detected by ambient predators or environmental hazards
* Mechanics :
	* Low tide required
	* Avoid currents, aggressive sea creatures, and pulsating air pockets
	* Optional stealth success bonus
* Rewards :
	* Gold (5–10G)
	* Minor consumables (healing salves, stamina potions)
	* Small chance for rare crafting material

---

	Quest 2: “The Pillar Challenge”

* Objective : Lorian hints at the pillar/statue mini-game for treasure
* Mechanics :
	* Solve the sequence puzzle on the pillar or statue
	* Starts treasure spawn countdown (30–60 sec)
	* Environmental hazards may occur during countdown (gusts, currents, minor sea creature interference)
* Rewards :
	* Treasure chest spawns somewhere in Tideglass Passage
	* System message notifies puzzle-solver if another player opens it
	* Treasure may include:
		* Gold (100–200G)
		* Rare crafting materials (Stormforged Ore, Glassfin Coral)
		* Small chance for Seal Fragment (Flying Mount Path)

---

	Quest 3: “Secret Smuggler Route”

* Objective : Use the hidden underwater passage to transport goods between locations (future chain quest linking to other POIs)
* Mechanics :
	* Access only during low tide
	* Avoid environmental hazards and predators
	* Completion may temporarily increase spawn rate of Moonlit Pearls or other aquatic resources in the passage
* Rewards :
	* Gold (200–300G)
	* Rare crafting nodes
	* Chance for hidden lore notes (crusader fragments, map hints)

