			Kellan “Saltbeard” Merrow

* Type : Questgiver / Trader / Smuggler
* 	Location : Location : Netar Island/Zone 2 - Vaelthorn Frontier/Silent Pier

	Appearance :
* Weathered human male, mid-60s
* Barnacle-encrusted boots and tattered sailor’s clothing
* Long, graying beard braided with small trinkets
* Monocle on left eye, leather belt with pouches of tools and scrolls
* Carries a worn fishing rod and a small rusty cutlass

	Behavior / AI :
* Roams slowly along the exposed planks of the pier
* Occasionally inspects crates or kneels near the water as if checking nets
* Reacts to player presence with caution or curiosity
* Provides visual hints to interact with hidden crates or map puzzles
* Can move slightly to avoid high tide hazards

	Dialogue Examples :
* “These waters hide more than fish… some say old Liberty Crusaders left secrets here.”
* “Timing is everything. Tides will show you what the ocean wants you to see.”
* “I’ve seen treasure slip through fingers more times than I can count… maybe today’s your day.”
* “Careful on the planks, they bite if you’re not watching.”

	Role / Function :
* Questgiver for treasure hunting or lost cargo quests
* Provides lore related to the Liberty Crusaders or Netar Island’s hidden history
* Sells or trades minor crafting materials collected from the pier (driftwood, Glassfin Coral, rare plankton)
* Offers hints for treasure map mini-games

	Quests :
1. Recover Sunken Cargo — retrieve items from rusted crates before the tide returns
2. Treasure Map Chain — solve mini-game puzzles to reveal hidden caches; completion spawns system message if another player opens the treasure
3. Tag Rare Fish — help Kellan locate rare aquatic resources for research; rewards include gold or crafting mats
4. Special Delivery — deliver collected items to other NPCs on Netar Island for bonus rewards

	Rewards / Loot :
* Gold (50–150G per quest)
* Crafting Materials: Driftwood, Glassfin Coral, Rare Plankton
* Consumables: Minor stamina potions, temporary buffs
* Treasure Map Fragments: used to trigger hidden treasure caches or rare NPC interactions
* Fun / Flavor: Unique trinkets or decorative items for cosmetic purposes

	Additional Notes :
* Interaction is optional but highly rewarding for exploration-focused players
* Works as a hub for hidden Pier mechanics and environmental puzzles
* Can provide hints or small buffs to aid with tide timing or underwater exploration

