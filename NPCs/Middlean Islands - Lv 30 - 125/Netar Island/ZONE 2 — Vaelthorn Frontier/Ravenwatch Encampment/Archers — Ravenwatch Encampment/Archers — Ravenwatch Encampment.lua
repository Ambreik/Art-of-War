	Appearance : Archers are light-armored for mobility: leather jerkin reinforced with chain at vital points, bracers, and boots for silent movement. They carry 
longbows, quivers with a variety of arrows (standard, fire, and poisoned for flavor), and sometimes short swords or daggers for close combat. Some wear cloaks with 
hoods or feathered adornments to signal rank or specialization. Their stance is relaxed yet ready to draw and fire at a moment’s notice.

---

	Personality :
* Observant, patient, and precise
* Often quieter than soldiers, but will call warnings loudly if danger is spotted
* Loyal to the Ravenwatch command structure
* Slightly playful with fellow archers during downtime

---

	Behaviour :
* Patrol along high ground or walls, especially near cliff edges and watch points
* Take position on guard towers or elevated platforms
* Respond to alerts with bow ready and signals to soldiers below
* Recognize returning players and nod or call out a short acknowledgment
* Use ranged attacks if a monster or event is triggered nearby

---

	Role :

* Role(s): Ranged Defense NPC, Alert NPC
	* Cover open areas and approaches to the outpost
	* Engage intruders or monsters with ranged attacks
	* Work in tandem with soldiers and scouts for coordinated responses

---

	Mechanics :

* Type: Combat-ready NPC, non-aggressive unless triggered by events
* Reputation: Standard; no bonus
* Interaction Limitations: Cannot be attacked or targeted normally
* PvP: Non-attackable
* Respawn Time: N/A
* Other Mechanics:
	* Patrol or tower rotations every 12–20 minutes
	* React to night raid alerts by firing warning shots or using signal flares
	* Signal soldiers when intruders or monsters appear
	* Minor acknowledgment lines for returning players

