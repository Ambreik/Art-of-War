---

	Appearance : Guards are stocky, well-built men and women, dressed in polished leather and iron plate for mobility and protection. They wear the Ravenwatch crest
on their chestplates and carry longswords or halberds. Their helmets have open visors for visibility, and their belts hold pouches for rations and signal flares. 	
	They stand upright, often at corners or along walls, scanning their surroundings vigilantly.

---

	Personality :
* Disciplined, stoic, and alert
* Low tolerance for slackers, but fair with players showing respect
* Polite, short, and firm with inquiries
* Slightly superstitious, often muttering about past night raids

---

	Behaviour :
* Patrol assigned sections, rotate positions at intervals
* Engage in call-and-response with other guards
* React to noise, unusual movement, or intrusions
* Recognize returning players with minor acknowledgment
* Give alerts during night raids or dungeon breaches

---

	Role :

* Role(s): Defender NPC, Alert NPC
	* Protect the village and castle approaches
	* Monitor restricted zones
	* Provide short, situational dialogue to enhance immersion

---

	Mechanics :

* Type : Combat-ready NPC, non-aggressive to players unless provoked by game events
* Reputation : Standard for all players; no bonus
* Interaction Limitations : Cannot be attacked or targeted by normal players
* PvP : Non-attackable
* Respawn Time : N/A
* Other Mechanics :
	* Rotate patrols every 10–15 minutes in-game
	* Respond to night raid alerts (triggers quotes and minor animations)
	* Observe restricted zones, visually blocking flying mounts

