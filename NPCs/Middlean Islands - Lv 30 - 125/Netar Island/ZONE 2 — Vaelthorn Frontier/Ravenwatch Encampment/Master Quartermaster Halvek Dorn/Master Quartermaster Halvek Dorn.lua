			Quartermaster — Ravenwatch Encampment

---

	NPC Name: Master Quartermaster Halvek Dorn

	Location : Middlean Islands → Netar Island → Zone 2 → Ravenwatch Encampment → Supply Pavilion

---

	Appearance : Halvek Dorn is a broad-shouldered, middle-aged man with a permanently tired expression and ink-stained fingers. He wears reinforced logistics armor
layered with utility straps, pouches, and ledger hooks. A thick supply ledger is chained to his belt, and a short hammer hangs beside it — used more often for 
emphasis than combat.
	His pavilion is stacked with sealed crates, emergency rations, weapon bundles, and marked barrels bearing warning sigils.

---

	Personality : Gruff, pragmatic, and sharply observant. Halvek treats war like mathematics — supplies in, casualties out. He has little patience for idealism but
deeply respects preparedness.
	He speaks bluntly and often mutters inventory numbers under his breath even while talking to players.

---

	Behaviour :
* Regularly inspects crates and adjusts stock
* During Night Raid Alerts, actively seals or opens emergency containers
* Shouts at nearby soldiers to “move faster” or “count twice”
* Temporarily refuses casual conversation during emergencies

---

	Role :
* Role(s) : Vendor, Emergency Supply Controller
	Halvek provides all essential expedition supplies and manages emergency stock during dungeon-related escalations.

---

	Mechanics :

* Type : Vendor
* Reputation : Order of Restoration
* Interaction Limitations :
  * Some items only available during Night Raid Alerts
  * Emergency stock requires minimum reputation rank
* PvP : Cannot be attacked
* Respawn Time : Not applicable
* Other Mechanics :
  * Stock changes dynamically during alert states
  * Prices increase during emergencies
  * Uses "Powder of Stinky Pollen" as exclusive currency

