			Binner the Flight Master — Flying Master

---

		1. NPC Identity

* Title : Flying Master
* Role Type : World Travel / Logistics NPC
* Faction Alignment : Neutral Military Authority
* Availability : Conditional (see Restrictions)

	Visual Description : 
* Wears reinforced leather with wind-scorched cloaks
* Carries signal flares and horn whistle
* Often seen near grounded mounts or perched lookout posts
* Calm, disciplined, minimal speech

	This NPC is not a civilian flight handler, but a military air coordinator, trained to operate under siege conditions.

---

		2. Flight Network Connections

	Active Routes (When Unrestricted)

	From "Ravenwatch Encampment", players may fly to:

1. "Southfall Village"
2. "Stormreach Watch"
   * Northern defensive settlement
   * Closer to open terrain and coast
   * Acts as a forward reconnaissance hub

---

	Route Structure (World Logic)

* Ravenwatch → Stormreach Watch
	* Follows the mountain edge
	* Visuals: cliff winds, ravens, ruined watchposts

* Ravenwatch → Southfall Village
	* Low-altitude military route
	* Avoids the castle airspace
	* Uses signal pylons and horn towers

---

	Route Restrictions (Critical Rule)

	Ravenwatch Encampment is a TERMINAL-ONLY node under threat

* Players may arrive here by flight
* Players cannot depart from here during restriction states
* This reinforces the idea of containment, not convenience

---

		3. Flight Zone & Landing Area

	Location : 
* Elevated wooden-and-stone platform
* Positioned away from main barracks
* Clear line of sight to the castle and mountain base

	Environmental Details : 
* Wind chimes (low metallic tone)
* Grounded mounts restless at night
* Lanterns extinguish automatically during alerts

---

		4. Restriction Integration

	When Restrictions Are Enabled:

*(Night Raid Alert / High Crusader Activity)*

* Flight platform becomes inactive
* No takeoffs or departures allowed
* Mounts are removed or grounded
* Signal fires extinguished

	NPC Behavior : 
* Flying Master disappears entirely
* No dialogue, no interaction
* Area guarded by silent soldiers

> This is intentional:
> The army does not allow air traffic during crusader movements.

---

		5. Player Arrival Exception Rule

	Players who are already airborne :
* May still land during restriction onset
* Are immediately grounded
* Cannot re-initiate flight until restrictions lift

	This prevents:
* Soft-locking players
* Breaking immersion
* Exploits during alerts

---

		6. Narrative Justification

	In-World Reason : 
* Crusader entities interfere with flight paths
* Aerial detection draws hostile attention
* Several historical losses blamed on airborne patrols

	Military Doctrine : 

> “Better to strand a rider than invite the dead skyward.”

---

		7. Flying Master — Ambient Dialogue (Normal State)

*(Short, professional, minimal)*

* “Routes are clear. Choose quickly.”
* “Wind’s unstable near the mountain.”
* “No lingering. Mounts don’t like this place.”
* “We don’t fly after dusk. Officially.”

---

		8. Flying Master — Dialogue (Restrictions Approaching)

* “Not tonight.”
* “Orders came down.”
* “Sky’s closed. Don’t ask.”
* “If you’re here, you stay.”

---

		9. Flying Master — World Function Summary

	Purpose : 
* Connects Zone 2 settlements
* Reinforces military realism
* Supports island traversal without trivializing danger

	Design Pillars : 
* Always present in villages
* Never guaranteed availability
* Responds to world state, not player demand

