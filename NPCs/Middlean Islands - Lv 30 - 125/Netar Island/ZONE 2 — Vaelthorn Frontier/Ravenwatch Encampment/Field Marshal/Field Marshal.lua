			Field Marshal

	Location : Middlean Islands → Netar Island → Zone 2 → Ravenwatch Encampment → Command Pavilion (central fortified platform)

---

	Appearance : "Field Marshal" is a battle-hardened human in his late forties, tall and broad-shouldered. He wears reinforced command plate armor bearing faded 
sigils of the High Command of Aric, scratched and dulled by years of combat. A heavy crimson cloak hangs from his shoulders, pinned by a broken crusader insignia — 
deliberately left unrepaired.
	His face is scarred across the jaw and temple, his beard trimmed short and streaked with grey. He always stands upright, hands often clasped behind his back or 
resting on the pommel of a ceremonial longsword planted into the ground before him.

---

	Personality : Calm, controlled, and unyielding. Commander Vaust does not raise his voice unless absolutely necessary. He treats players with measured respect 
but no warmth. To him, Netar Island is not a place of heroics — only survival and containment.
	He despises recklessness, values discipline, and has little patience for curiosity that risks the encampment’s safety.

---

	Behaviour : 
* Remains stationed at the command pavilion
* Observes alert state changes without visible panic
* During Night Raid Alerts, his posture stiffens and nearby soldiers snap to attention
* Does not follow or move toward players

---

	Role :

* Role(s) : Quest Giver, Command Authority NPC
	Field Marshal serves as the strategic leader of Ravenwatch Encampment. He provides narrative context, operational directives, and oversees all military 
responses tied to the dungeon and island threats.

---

	Mechanics  :

* Type : Quest Giver
* Reputation : Increases reputation with "Order of Restoration"
* Interaction Limitations :
	* Available to all factions
	* Some dialogue and quests require prior dungeon progression
* PvP : Cannot be attacked; protected NPC
* Respawn Time : Not applicable
* Other Mechanics :
	* Dialogue changes based on global alert state
	* Acknowledges players who repeatedly survive dungeon runs
	* Becomes uninteractive during full Lockdown events (visual only)

---

	Skills  :

* Skill 1 : Tactical Command Presence
* Description : Commander Vaust radiates authority, increasing morale among nearby soldiers during alert states.
* Effect : Nearby guard and soldier NPCs gain increased readiness (faster rotations, tighter formations).

* Skill 2 : Emergency Muster Order
* Description : When Night Raid Alert triggers, Vaust issues a silent command gesture.
* Effect : Instantly accelerates watch rotations and activates horn signals.

* Skill 3 : Seal Awareness
* Description : Vaust senses fluctuations from the dungeon seal.
* Effect : Enables certain quests or dialogue only when dungeon corruption thresholds are reached.

