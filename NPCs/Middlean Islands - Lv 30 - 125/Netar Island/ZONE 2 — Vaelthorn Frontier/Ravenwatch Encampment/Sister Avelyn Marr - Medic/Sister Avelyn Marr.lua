			Field Medic — Ravenwatch Encampment

---

	NPC Name: Sister Avelyn Marr

	Location : Middlean Islands → Netar Island → Zone 2 → Ravenwatch Encampment → Triage Tent (near inner wall)

---

	Appearance : "Sister Avelyn" is a calm, sharp-eyed woman in her late thirties, wearing layered medical robes reinforced with light chain beneath. Her sleeves 
are rolled up, forearms marked with old scars and healing sigils.
	She carries a satchel filled with vials, bandages, bone needles, and faintly glowing charms. Her tent is clean but worn, with blood-stained planks carefully 
scrubbed and rows of stretchers lining the walls.

---

	Personality : Measured, compassionate, and unflinching. Avelyn does not panic — ever. She treats soldiers and players alike with the same calm efficiency, but 
has little tolerance for recklessness.
	She speaks softly, even during crises, which only makes her authority more absolute.

---

	Behaviour :
* Actively moves between wounded NPCs during alerts
* Kneels to heal injured soldiers after raids
* Applies bandages and sigils in visible animations
* During heavy attacks, may shout short medical orders

---

	Role :

* Role(s) : Medic, Support NPC
	Provides healing, status removal, and emergency assistance to players and NPC defenders.

---

	Mechanics :

* Type : Support / Healer NPC
* Reputation : Order of Restoration
* Interaction Limitations :
	* Full services require minimum reputation
	* Emergency healing only available during alerts
* PvP : Cannot be attacked
* Respawn Time : Not applicable
* Other Mechanics :
	* Heals nearby NPC soldiers automatically
	* Can revive downed NPC defenders after raids
	* Limited healing charges per alert cycle

---

	Skills :

* Skill 1 : Battlefield Triage
* Description : Rapidly heals nearby wounded NPCs and players.
* Effect : Restores HP over time in a small radius.

* Skill 2 : Purifying Salve
* Description : Removes poison, bleed, and shadow corruption effects.
* Effect : Cleanses negative status effects.

* Skill 3 : Last Breath Stabilization
* Description : Prevents death of critically wounded NPCs.
* Effect : Keeps NPCs alive at minimal HP until healed.

