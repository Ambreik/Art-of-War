	Appearance : Soldiers are medium-built, trained for combat and mobility. They wear chainmail reinforced with leather straps and carry swords, shields, and 
sometimes short spears. Their helmets are light, often with visors that can be lifted. They wear Ravenwatch insignias and belts with small satchels for rations or 
signal flares. Their posture is disciplined but less rigid than guards, reflecting readiness to move into action quickly.

---

	Personality :
* Courageous, loyal, and disciplined
* Often slightly boisterous when not on alert
* Respectful toward superiors and players displaying initiative
* Some carry quiet superstitions about the undead or cursed lands

---

	Behaviour :
* Patrol assigned areas, sometimes moving in small squads
* Respond to alerts with combat-ready animations (draw weapons, shout commands)
* React to unusual noises or monster sightings
* Provide minor guidance to players entering the outpost (e.g., “Watch your step near the cliffs!”)
* Recognize returning players through small acknowledgment gestures

---

	Role :

* Role(s): Combat NPC, Alert NPC
	* Protect the village, outpost, and key access points to the castle
	* Respond to dungeon or night raid alerts
	* Enhance immersion via call-and-response with guards and archers

---

	Mechanics :
* Type: Combat-ready NPC, non-aggressive unless triggered by events
* Reputation: Standard; no bonus
* Interaction Limitations: Cannot be attacked or targeted normally
* PvP: Non-attackable
* Respawn Time: N/A
* Other Mechanics:
	* Patrol rotations every 12–20 minutes in-game
	* Respond to night raid alerts with visual animations and voice lines
	* Alert nearby NPCs (guards, archers, scouts) when monsters are detected
	* Minor acknowledgment of returning players (nod, short line)

