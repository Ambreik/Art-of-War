	Appearance : Scouts wear light, camouflaged clothing designed for blending into forests, cliffs, or shorelines. They carry short bows, slings, or small 
crossbows, plus daggers for close encounters. Some have small satchels, ropes, or climbing hooks. Boots and gloves are muted in color for stealth, and cloaks may 
have hoods or feathers for rank signaling. They often wear masks or scarves covering the lower face when on patrol.

---

	Personality :
* Observant, cautious, and independent
* Often silent, speaking only when necessary
* Highly aware of their surroundings
* Loyal, but sometimes mischievous with fellow scouts
* Keen trackers, able to follow trails of monsters, enemies, or lost travelers

---

	Behaviour :
* Patrol border areas, cliff paths, and hidden trails around the outpost
* Report unusual activity to archers or soldiers
* React quickly to environmental hazards (falling rocks, tidal surges, monster spawns)
* Can recognize returning players, providing minor acknowledgment lines
* Use stealth movement to approach or monitor intruders

---

	Role :

* Role(s): Recon NPC, Alert NPC
	* Detect intruders, monsters, or rare spawns in the area
	* Communicate with archers and soldiers for coordinated response
	* Act as environmental observer NPCs for lore flavor

---

	Mechanics :
* Type: Recon/Alert NPC
* Reputation: Standard; no bonus
* Interaction Limitations: Cannot be attacked or normally targeted
* PvP: Non-attackable
* Respawn Time: N/A
* Other Mechanics:
