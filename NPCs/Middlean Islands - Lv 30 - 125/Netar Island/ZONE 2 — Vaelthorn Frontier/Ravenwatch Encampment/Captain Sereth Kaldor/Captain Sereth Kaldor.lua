			Tactical Officer — Ravenwatch Encampment

---

	NPC Name : Captain Sereth Kaldor

	Location  : Middlean Islands → Netar Island → Zone 2 → Ravenwatch Encampment → Tactical Platform (overlooking the castle approach)

---

	Appearance : Captain Sereth Kaldor is a lean, sharp-featured human in his early thirties. He wears lighter tactical armor reinforced with leather and chain, 
optimized for mobility rather than brute defense. His armor is marked with chalk symbols, etched arrows, and faint rune-scratches used to track patrol routes and 
enemy movements.
	A rolled tactical map case hangs from his belt, and a short command blade rests at his side. His eyes constantly scan the horizon, and he rarely stands still 
for long.

---

	Personality : Focused, analytical, and impatient with inefficiency. Kaldor speaks quickly and precisely, often assuming the listener understands military 
terminology. He respects competence over rank and shows visible approval toward players who adapt quickly to Netar’s dangers.
	He treats Netar Island like a living battlefield, not a static location.

---

	Behaviour : 
* Walks between observation points and signal markers
* Pauses to adjust markers or review patrol paths
* During Night Raid Alerts, remains fixed at the tactical platform
* Occasionally gestures or gives silent orders to scouts

---

	Role :
* Role(s) : Quest Giver, Tactical Operations NPC
	Captain Kaldor coordinates scouting, patrols, and response strategies against undead movements and dungeon spillover threats.

---

