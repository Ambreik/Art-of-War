			Eldrin Shorewatch

*(Villager Witness)*

	Location :
Southfall Dominion / Southfall Village / South Gate & Shoreline Path

---

	Appearance : Eldrin is a lean, weather-worn man in his early 40s with sun-darkened skin and pale eyes strained from years of watching the tide. He wears a faded
cloak patched at the shoulders, simple trousers, and salt-stained boots.

	Notable details:
* Slight limp in his right leg
* Keeps his hood half-raised even in clear weather
* Often gazes toward the water or the road beyond the gate

---

	Personality : Cautious and uneasy. Eldrin speaks in fragments, often pausing as if measuring his words. He does not seek attention and becomes uncomfortable 
when questioned directly but will share what he saw if approached calmly.

---

	Behaviour :
* Patrols slowly between the south gate and shoreline
* Stops periodically to stare at the ground or sea
* Leaves the area briefly during night alarms
* Avoids eye contact after dark

---

	Role :

* Role(s) : Environmental Story NPC
	Eldrin exists to reinforce unfolding events through observation. He provides contextual hints rather than instructions, helping players piece together the 
narrative organically.

---

	Mechanics :
* Type : Flavor NPC
* Reputation : None
* Interaction Limitations :
	* Dialogue changes based on questline progression
* PvP :
	* Cannot be attacked
	* Not flagged for PvP
* Respawn Time : N/A
* Other Mechanics :
	* Dialogue unlocks after certain quest stages
	* Night-time lines differ from day-time

---

	Skills :

*Not applicable.*

