---

	Quotes :

	Before Questline Trigger :
* “Tracks don’t always lead where you think.”
* “Sea carries more than fish.”

	During Questline :
* “Boots like that don’t belong to villagers.”
* “They moved fast. Didn’t look back.”

	After Questline Completion :
* “Shore feels quieter now.”
* “Some footprints should stay buried.”

	Night Time :
* “I don’t linger here after sundown.”
* “Water remembers.”

