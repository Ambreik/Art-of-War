---

	Lore Tease (No Full Reveal Yet) : 
* Chamber was sealed from the inside
* Something remained behind willingly

---

	Completion Reward : 
* Greywatch Relic: "Resonant Keystone"
* Unlocks:
	* Deeper sealed content (future update)
	* New archaeology interactions across Zone 3

---

