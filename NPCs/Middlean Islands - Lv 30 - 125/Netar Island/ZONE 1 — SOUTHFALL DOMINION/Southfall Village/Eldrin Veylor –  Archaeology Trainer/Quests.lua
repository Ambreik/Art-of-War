		Design Philosophy

	This questline:
* Rewards patience
* Builds mystery gradually
* Makes archaeology feel dangerous
* Creates future elite & dungeon hooks naturally

---

		The Sealed Chamber of Greywatch

* Location : Beneath the lowest spiral ledge of Greywatch Sink
* Status : Partially Accessible (Initial State)
* Access Type : Archaeology-Gated, Multi-Phase

---

		1. Chamber Overview

	The sealed chamber is not a single room, but a pressure-stabilized cavity carved into reactive stone.

	Visual Identity : 
* Walls etched with non-linguistic glyphs (pre-symbolic markings)
* Stone surfaces subtly pulse when moisture is present
* Floor layered with sediment and abandoned tools arranged deliberately

> The chamber does not feel abandoned — it feels paused.

---

		2. Initial Access State (Current Content)

	What Players Can Access Now : 
* Outer Antechamber (≈30% of total space)
* View into inner chamber through:
	* Fractured seal
	* Narrow crevices
* Interaction with "Resonant Keystone" socket

---

		Environmental Mechanics (Initial)

	Pressure Seal Field : 
* Invisible stabilization field prevents entry deeper
* Disturbing it causes:
	* Wind reversal
	* Low-frequency hum
	* Sudden dust collapse

	Moisture Sensitivity : 
* Water presence causes:
	* Glyphs to glow faintly
	* Stone veins to resonate

	This links back to:
* Boss regen mechanics
* Pond ecology
* Waterwheel manipulation

---

		3. Archaeology Interactions (Initial Phase)

	Interaction Nodes

* Tool Arrangement Cache
	* Reveals workers stopped mid-task
	* Tools placed intentionally, not dropped
* Glyph Study Surface
	* Allows rubbing, not translation
* Seal Edge Examination
	* Shows seal formed after evacuation

---

	Key Lore Discoveries (Teasers Only) : 
* Chamber was sealed from inside
* The stone itself assisted the seal
* Extraction target was never removed

---

		4. Future Access Paths (Designed Hooks)

	Access Path A — Archaeology Rank IV+

	Trigger :
* Advanced pressure calibration techniques

	Unlocks :
* Partial dissolution of pressure seal
* Entry into Inner Ring (≈60%)

	Reveals :
* Central extraction pit
* Core stone growth

---

	Access Path B — Environmental Manipulation

	Trigger :
* Long-term waterwheel adjustments
* Controlled moisture saturation

	Unlocks :
* Temporary glyph activation
* Stone phase shift windows

	Reveals :
* Stone memory projections
* Collapse vision event

---

	Access Path C — Elite Monster Resolution

	Trigger :
* Defeating or pacifying Greywatch elite

	Unlocks :
* Permanent seal weakening
* Safe access during low-risk periods

---

	Access Path D — World Event / Expansion

	Trigger :
* Island-wide geological event
* Story progression milestone

	Unlocks :
* Full chamber access
* Inner sanctum opening

---

		5. Inner Chamber (Locked – Future Content)

	Intended Design (Hidden) : 
* Central pillar of living stone
* Slow, rhythmic pulse matching island ley rhythms
* Embedded remains:
	* Not corpses
	* Stone-merged forms (voluntary)

> Suggests guardianship, not sacrifice.

---

		6. The Resonant Keystone

	Current Function : 
* Stabilizes seal
* Prevents catastrophic collapse

	Future Function : 
* Key to:
	* Boss summoning alterations
	* Island-wide stone reactions
	* Archaeology mastery unlocks

	Removing it early is impossible — attempting to do so causes full chamber lockdown.

---

		7. Risk & Failure States

* Excessive disturbance:
	* Elite agitation increases
	* Tunnel instability
* Incorrect tool use:
	* Seal reinforcement (temporary lockout)

	This teaches players:

> Some secrets resist force.

---

		8. Why This Works Long-Term

* Chamber remains relevant across expansions
* Ties archaeology, environment, elites, and bosses together
* Encourages revisiting, not rushing
* Supports multiple player types:
	* Explorers
	* Lore hunters
	* Completionists

---

		9. Subtle Player Messaging

	Environmental text fragments:

> “The stone remembers pressure.”
> “Not all seals are meant to be broken.”
> “They stayed so the island wouldn’t fall.”

