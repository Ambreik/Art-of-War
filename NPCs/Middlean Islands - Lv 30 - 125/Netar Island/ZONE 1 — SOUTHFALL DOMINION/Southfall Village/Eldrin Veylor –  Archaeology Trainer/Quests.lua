			Archaeology Questline

	“The Collapse Beneath Greywatch”

* Quest Type : Profession Questline (Archaeology)
* Required Rank : Archaeology Rank II (minimum)
* Quest Giver : *Indirect* (environment-triggered → later reported to Eldrin Veylor)
* Location : The Greywatch Sink

> Non-archaeologists may witness discoveries, but cannot interact with core objectives.

---

		QUEST I — Fractures in the Stone

	Trigger Condition : Inspect collapsed mining marks inside Greywatch Sink (Rank II Archaeology)

---

	Objective : 

* Examine 3 distinct structural fracture points
* Record stress directions and tool impressions

---

	Mechanics : 

* Interactive inspection nodes:
	* Wall fissures
	* Crushed support beams
	* Collapsed ledge anchor points
* Players rotate camera and align tools to:
	* Trace fracture propagation
	* Identify collapse sequence

	Failure State :
* Rushing inspection causes:
	* Dust cloud
	* Temporary vision reduction
	* Collapse instability meter increase

---

	Lore Reveal : 
* Collapse was not natural
* Support beams were weakened before failure

---

	Completion Reward : 
* Archaeology XP
* Item: "Stone Fracture Rubbing"
* Unlocks Quest II

---

		QUEST II — *What the Earth Hid*

	Trigger : Completing Quest I and descending to lower spiral tier

---

	Objective : 
* Identify extracted material remnants
* Analyze residue embedded in rock

---

	Mechanics : 

* Archaeology interaction:
	* Scrape stone veins
	* Compare mineral patterns
* Players must avoid:
	* Over-extraction (causes tremors)
	* Noise buildup

	Hidden Mechanic :
* Correct analysis reduces elite agitation (future tie-in)

---

	Lore Reveal : 
* Mine was extracting non-metallic, organic stone
* Material reacts faintly to moisture and pressure

> Suggests purpose beyond construction or trade

---

	Completion Reward : 
* Greywatch Artifact Fragment I
* Unlock: "Reactive Stone Identification" (passive archaeology bonus)

---

		QUEST III — Marks Without Names

	Trigger : Returning findings to Eldrin Veylor
(Dialogue unlocked only if both prior quests complete)

---

	Objective : 
* Decipher abandoned quarry records
* Identify authority markings

---

	Mechanics : 
* Puzzle-based decoding:
	* Symbol alignment
	* Partial sigil reconstruction
* Requires:
	* Quiet environment
	* No hostile presence nearby

---

	Lore Reveal : 
* Mine was overseen by:
	* A private order
	* Operating outside local governance
* Orders were sealed, unsigned, and time-restricted

---

	Completion Reward : 
* Archaeology XP
* Unlocks Quest IV
* Journal Entry:
	> “No names endure, only intentions.”

---

		QUEST IV — The Day They Left

	Trigger : Return to Greywatch Sink with decoded sigils

---

	Objective : 
* Locate emergency abandonment markers
* Trace evacuation route

---

	Mechanics : 
* Environmental trail following:
	* Chalk symbols
	* Broken tool caches
* Players must:
	* Reconstruct movement direction
	* Avoid destabilizing paths

	Timed Element :
* Lingering too long increases collapse risk

---

	Lore Reveal : 
* Evacuation was sudden
* Workers left tools and samples behind
* Collapse was expected, not accidental

---

	Completion Reward : 
* Greywatch Artifact Fragment II
* Unlock: "Structural Awareness"
	*(Reduces fall damage in ruins & caves)*

---

		QUEST V — Sealed Below

	Trigger : Possessing both Greywatch Artifact Fragments

---

	Objective : 
* Locate sealed chamber below lowest ledge
