			Eldrin Veylor –  Archaeology Trainer

	Location : Southfall Village – Near ruined watchtower

---

	Appearance : Middle-aged male, weathered skin from years outdoors. Wears layered brown and beige linen robes with leather belts holding small pouches. Glasses 
perched on his nose, a small magnifying lens hangs from a chain around his neck.Carries a small wooden case for relic fragments and a notebook filled with sketches.
Posture is slightly stooped but alert.

---

	Personality : Curious, meticulous, and somewhat eccentric. Speaks quickly when excited about artifacts, yet patient with learners. Enjoys telling short stories 
about the relics he studies and often mutters notes aloud while inspecting items.

---

	Behaviour : Moves slowly around the appraising table, gestures toward items while explaining. Occasionally examines the ground near ruins for fragments.

---

	Role :
* Role(s): Archaeology Trainer / Flavor NPC
* Function: Teaches archaeology basics, identifies relic fragments, offers insight on local Crusader-era ruins. Supports future archaeology-related quests.

---

	Mechanics :
* Type: Trainer / Flavor NPC
* Reputation: Can grant archaeology XP and unlock crafting/identifying recipes for relics.
* Interaction Limitations: Any player interested in archaeology; no faction restriction.
* PvP: Can be attacked but flagged NPC. Skillable if attacked; may retain grudge against disrespectful players.
* Respawn Time: 10 minutes if killed.
* Other Mechanics: Can identify special relic fragments for quest progression; occasionally offers hints about hidden caves or mini-events in the area.

---

	Skills :
*(No special combat skills; purely flavor/training unless needed later.)*

---

	Quest :

* Quest Title: “Fragments of the Past”
  * Quest Type: Side / Archaeology introduction
  * Objective(s): Examine 5 relic fragments found around Southfall Village and the nearby ruins.
  * Rewards: 1500 XP, Archaeology Recipe: “Basic Relic Identification”
  * Conditions: Level 52+
  * Quest Dialogue:
    * Opening: “Ah, a curious mind! Care to learn the stories these fragments tell?”
    * Mid-Quest: “Excellent, you’ve found another piece! Keep an eye out near the ruined watchtower.”
    * Completion: “Well done! These pieces begin to tell the tale of the Crusaders’ final days. Here, take this recipe to continue your studies.”
  * Additional Notes: Can be repeatable if new fragments spawn periodically for exploration.

---

	Quotes :
* “Every relic has a story, if you know how to listen.”
* “Careful with that shard—it’s older than most of the buildings here.”
* “Some fragments… seem ordinary, but they hide secrets for the observant.”
* “Watch your step near the ruins; the stones remember.”
* “If you’re serious about archaeology, you’ll want a keen eye, not just strong hands.”

