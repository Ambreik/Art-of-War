			NIGHT LANTERN DUTY MINI-EVENT

### **Event Name:**

**“Lights Before Nightfall”**

### **Event Type:**

• Dynamic Village Mini-Event
• Triggered by **Guardian NPC** of Southfall Village
• Occurs **ONLY during the evening transition** (just before world night)

### **Event Frequency:**

• Not every night
• Chance to trigger when **at least 1 player is in Southfall Village boundaries** during dusk

---

# ✔ **Trigger Conditions**

1. Evening approaches (world time)
2. Lanterns around village gates, walls, and main road MUST be lit
3. Guardian checks for **“Lantern Keeper NPC”** (your wandering worker responsible for lighting the lanterns)
4. If event triggers, Guardian shouts publicly:
   *“Where is that lazy torchbearer again?! The night is coming—someone find him, quickly!”*
5. Player(s) in range receive the event pop-up.

---

# ✔ **Player Objective**

**Find the missing Lantern Keeper within 10 minutes.**
If time expires → mission fails → lanterns begin **sparking dimly** → village lighting will be auto-corrected once night fully arrives.

If found → one of **3 Possible Scenarios** occurs.

---

# ✔ **Possible Lantern Keeper States**

## **1. NPC is FISHING**

Location: The small lake just outside Southfall Village (north-west or south-east, you choose later).

**Flavor:**
The NPC is struggling to catch a rare local fish.
He forgot his duty entirely.

**Dialogue Theme:**
The player scolds him → NPC panics → asks the player to help.

### **Special Fish Name:**

**Moonfin Gleamer**
A rare evening-active fish with slight bioluminescent scales.

### **Player Task:**

• Catch **1× Moonfin Gleamer** before NPC finishes lighting **all lanterns** (he runs back immediately).
• The NPC will *try to finish his route fast*, usually around 3–4 minutes.

### **Rewards:**

#### **NPC Reward (only if player catches the fish in time):**

• **Moonlit Scale Pouch** — small bag containing 3 random fish or bait
or
• **Glowing Reed Thread** — crafting item used for Tailoring rare cloaks
(random 50/50)

#### **Guardian Reward:**

Given regardless (as long as player found NPC before timer):
• EXP
• Gold

Rare Chance (~5%):
• **Sash of the Lantern Runner** (non-equip item, cosmetic sash tied around waist)
• **Skill Page: “Nightwatcher’s Insight”** (passive: +2% visibility radius at night & +2 Perception during nighttime exploration)

