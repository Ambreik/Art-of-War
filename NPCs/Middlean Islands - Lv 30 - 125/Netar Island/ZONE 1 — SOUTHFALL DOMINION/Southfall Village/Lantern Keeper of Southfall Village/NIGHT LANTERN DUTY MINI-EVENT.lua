## **2. NPC is DRUNK**

Location:
Leaning against the inside of the village wall or near a barn.

**Flavor:**
He is holding a bottle, slurring, laughing, eyes half-open.

**Dialogue Theme:**
He confesses he can’t walk straight and hands the job to the player.

### **Player Task:**

• Light all lanterns using items he gives:

### **Tools Provided:**

* **Keeper’s Oil Flask**
* **Long Wick Cord**
* **Sparkstarter Flame**

### **Gameplay:**

Player interacts with lantern posts to light them manually.

### **Rewards:**

#### **NPC Reward:**

Gives player a **Perchant** (profession-use crafting piece)

**Perchant Name:**
**“Brass Ember-Linked Perchant”**
• Used exclusively by **Blacksmiths**
• Required to forge a **special ring** (we’ll develop the ring later)

#### **Guardian Reward:**

• EXP
• Gold
• *(Rolled rare 5% chance)* same two rare rewards:

* **Sash of the Lantern Runner**
* **Skill Page: Nightwatcher’s Insight**

---

---

## **3. NPC is SURROUNDED BY MONSTERS**

Location:
Along the outer wall, near bushes or forest edge.

### **Monsters:**

Local creatures usually roaming outside Southfall Village:

**Proposed Monster Type:**
**Duskhollow Scravels**
• Small wolf-like scavengers
• Quick and weak but hunt in packs
• Drop: fangs, pelts, raw meat, small gold

### **Player Task:**

• Kill all monsters
• Escort NPC to nearest lantern
• Once he lights the first lantern → event ends successfully

### **Rewards:**

#### **NPC Reward:**

None (he’s embarrassed)

#### **Guardian Reward:**

• EXP
• Gold
• Rare reward chance (5%):

* **Sash of the Lantern Runner**
* **Skill Page: Nightwatcher’s Insight**

---

# ✔ **Event End Condition**

The event ends the moment:

### **• Either the NPC lights the FIRST lantern**

**or**

### **• Player lights ALL lanterns (drunk scenario)**

**or**

### **• Time expires (FAIL)**

---

# ✔ **Fail State**

If the player fails to find the NPC within the 10-minute countdown:

• Lanterns begin **sparkling weakly**
• The Guardian grumbles publicly
• No rewards
• The village will eventually auto-light once full night arrives

---

# ✔ **Items Introduced Here**

### **1. Sash of the Lantern Runner**

• Equip sash item
• Golden cloth with faint magical thread
• Symbolizes helpfulness during dusk emergencies

### **2. Skill Page: Nightwatcher’s Insight**

Passive skill:
• +2% vision radius at night
• +2 Perception while exploring in nighttime
• Pages vanish when learned; can be sold for high gold

### **3. Brass Ember-Linked Perchant**

Blacksmith crafting material used to forge a special ring
