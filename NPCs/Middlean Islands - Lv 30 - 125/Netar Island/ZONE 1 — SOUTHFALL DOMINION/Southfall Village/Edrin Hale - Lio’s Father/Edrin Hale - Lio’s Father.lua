			Edrin Hale - Lio’s Father

	Location :
* Pre-Quest : Referenced only (Southfall Village)
* During Questline :
	* Coastal trail outside Southfall (footprint investigation phase)
	* Hidden Crusader holding site (off-map / instanced encounter)
* Post-Quest : Memorial site near Southfall cliffs (if deceased) or infirmary corner of Southfall Inn (if rescued – weakened state)

> Final location depends on questline resolution flags.

---

	Appearance : Edrin Hale is a weathered middle-aged laborer, early 40s. He has broad shoulders from years of dock and stone work, though his posture is slightly
hunched. His hair is dark but streaked with gray, tied back with a simple cord.
	His clothing is scorched and torn :
* Burned linen shirt with ash marks
* Leather work gloves (one missing)
* Heavy boots with distinctive tread marks (matching the Strange Footprints)

	In later quest stages, his skin shows faint blackened vein patterns along the neck and arms — early signs of Crusader corruption or ritual exposure.

---

	Personality : Quiet, protective, and deeply practical. Edrin is not a hero by nature — he works to survive and protect his child. He carries guilt for 
leaving Lio alone and shame for being captured without resistance.
	If rescued alive, his voice is strained, and he avoids direct eye contact when speaking about what happened.

---

	Behaviour :
* Pre-Quest : Not physically present
* During Questline :
	* Bound, weakened, or restrained
	* Minimal movement
	* Reacts strongly to Crusader symbols or chanting
* Post-Quest :
	* Limited movement
	* Often seated or leaning against walls
	* Occasionally gazes toward the sea in silence

---

	Role :

* Role(s) : Story Target NPC, Quest Resolution NPC
	Edrin is the narrative core of the Southfall mini-arc. His fate determines the emotional outcome of the questline and permanently disables related mini-events.

