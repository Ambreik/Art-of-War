			Marrek Durn

*(Southfall Innkeeper)*

	Location :
Southfall Dominion / Southfall Village / Southfall Inn / Main Hall

---

	Appearance : Marrek Durn is a broad-shouldered man in his late 50s with a thick salt-and-pepper beard. His hair is thinning on top, usually slicked back with 
oil. He wears a worn wool vest over a linen shirt, sleeves rolled up, with a stained apron tied at the waist.

	Notable details:
* Burn scar along his left forearm (old, healed)
* Always polishing a mug or wiping the counter
* A lantern hangs permanently behind the bar, kept lit even during the day

---

	Personality : Grounded, observant, and quietly protective of the village. Marrek speaks calmly, rarely raising his voice. He listens more than he talks, but 
when he does speak, it’s often loaded with implication rather than direct statements.
	He avoids gossip unless pressed and never exaggerates what he has seen.

---

	Behaviour :
* Stays behind the counter during most hours
* At night, occasionally steps outside to check the street
* Stops serving briefly during alarm bell events
* Lowers his voice when discussing sensitive matters

---

	Role :

* Role(s) : Innkeeper, Flavor NPC, Optional Quest Commentary
	Marrek provides food, drink, and rest services. He also serves as an ambient lore anchor, offering subtle observations tied to nearby events and questlines 
without directly starting or completing quests.

