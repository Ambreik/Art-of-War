			Lio

	Location :
Netar Island / Southfall Dominion / Southfall Village / Central Square / Old Stone Well

---

	Appearance : Lio is a young village boy, around 8–9 years old. He wears a worn linen tunic, slightly oversized, patched at the elbows, and simple leather 
sandals. His clothes are dusty, especially around the knees, as if he’s been pacing or kneeling near the well for hours.
	His hair is messy and unkempt, as though he’s run his hands through it repeatedly. His eyes are red and swollen from crying, and he often clutches a small 
wooden token carved with a simple family mark — his father’s.
	He avoids direct eye contact, glancing frequently toward nearby streets and rooftops.

---

	Personality : Fearful, anxious, and emotionally overwhelmed. Lio is not dramatic — he is quietly distressed, trying to be brave but clearly failing. He speaks 
in short sentences, often hesitating or repeating himself when nervous.
	Despite his fear, he is observant. Small details stand out to him, especially anything related to his father or their home.

---

	Behaviour :
* Remains close to the well at all times
* Paces in short loops
* Occasionally sits on the well’s edge
* Looks up at rooftops or toward the village gate when guards pass
* Does not move locations unless escorted during quests

---

	Role :

* Role(s) : Questline Clue NPC, Story Catalyst
	Lio serves as the emotional entry point into the Southfall questline. His concern for his missing father triggers the investigation that unfolds through the 
Lost Child, Cat on the Roof, and Strange Footprints events.

---

	Mechanics :

* Type : Quest NPC / Story Trigger
* Reputation :
	* Indirectly tied to Southfall Village Trust (no direct REP gains)
* Interaction Limitations :
	* Only interacts meaningfully during the questline
	* Post-quest, becomes non-interactive ambient NPC
* PvP :
	* Cannot be attacked
* Respawn Time :
	* N/A (persistent NPC)
* Other Mechanics :
	* Dialogue changes as questline progresses
	* Becomes silent and withdrawn after questline completion

