			Guard Kael Ironwatch

	Location :
Netar Island / Southfall Dominion / Southfall Village / Inner Wall Patrol Route

---

	Appearance : A broad-shouldered guard clad in heavy leather armor reinforced with iron studs. He carries a kite shield emblazoned with the Southfall insignia, 
visibly scratched from repeated use, and a short arming sword worn low at his hip. His helm is usually left open, revealing close-cropped dark hair and a 
perpetually stern expression. His stance is firm and immovable, often standing still longer than necessary while on watch.

---

	Personality : Stern, disciplined, and uncompromising. Kael believes vigilance is the only thing keeping Southfall alive. He speaks little and wastes no words, 
treating both villagers and outsiders with equal seriousness. While not unfriendly, he has little patience for foolishness or complacency.

---

	Behaviour :

* Patrols the inner wall and gate-adjacent paths.
* Takes extended shifts during nighttime hours.
* Immediately redirects his patrol route when the alarm bell is triggered.
* Briefly halts movement to observe the sea during fog or heavy wind conditions.

---

	Role :

* Role(s) : Patrol Guard
	Kael Ironwatch serves as a frontline village defender, maintaining order along the walls and responding rapidly to external threats.

