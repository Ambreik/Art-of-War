			Southfall Watchmen

* Type : Perimeter Scouts / Hunting-Style NPCs
* Faction : Southfall Village
* Count : 3 NPCs (shared AI, individual identity)

	Global Rule : They never enter the village, even while retreating.

---

	1. Watchman Edrik Hale (Senior Watchman)

	Location : Southfall Village – Outer Perimeter (Forest-facing patrol ring)

---

	Appearance
* Older man, weathered face, short grey-brown beard
* Leather scouting armor (worn but well-maintained)
* Short sword at left hip
* Longbow slung across the back
* Slight limp when walking, but steady stance when aiming

---

	Personality
* Calm, experienced, quietly authoritative
* Speaks rarely, but with certainty
* Treats the two younger watchmen as protégés
* Acknowledges players with a nod, not conversation

---

	Behaviour
* Leads perimeter patrol patterns
* Pauses frequently to scan treeline
* Does not chase recklessly
* Retreats earlier than the younger watchmen if overwhelmed

---

	2. Watchman Kellen Ridge (Junior Watchman)

	Location : Southfall Village – Coastal Perimeter (shoreline patrol arc)

---

	Appearance
* Young adult, sharp features
* Same leather armor and weapons as Edrik
* Cleaner gear, fewer repairs
* Slightly restless posture

---

	Personality
* Alert, eager, sometimes impulsive
* Tends to overcommit during chases
* Respects Edrik but tries to prove himself
* Occasionally mutters to himself while on patrol

---

	Behaviour
* More aggressive chase radius
* Will pursue monsters slightly farther than intended
* Retreats later than optimal if unsupported

---

	3. Watchman Toren Vale (Junior Watchman)

	Location : Southfall Village – Southern Wall & Gate Exterior

---

	Appearance
* Young adult, rounder face than Kellen
* Same equipment and armor
* Keeps bow in hand more often
* Frequently adjusts gloves or bowstring

---

	Personality
* Nervous but attentive
* More cautious than Kellen
* Relies on ranged combat
* Clearly uncomfortable when alone

---

	Behaviour
* Maintains closer distance to the walls
* Retreats faster than the others
* More likely to use escape mechanics early

---

		Shared Mechanics & Functions (All Watchmen)

---

	Role :
* Perimeter scouting and threat thinning
* Early-warning combat presence
* World realism reinforcement (non-quest NPCs)

---

	Combat Mechanics
* Weapons : Short Sword (melee), Bow (ranged)
* Combat Style :
	* Opens with ranged attacks
	* Switches to melee if engaged
* Stats :
	* Increased movement speed
	* Increased agility
	* Lower durability than guards

---

	Patrol Logic
* Patrol outer perimeter paths only
* Paths slightly randomized per cycle
* Never cross village boundary
* Never idle inside walls

---

	Monster Engagement Rules
* Engage monsters near village walls
* Chase distance is limited
* Do not pursue deep into wilderness

---

		Emergency Escape Mechanic

	Trigger Condition :
* Engaged with 3+ monsters
* No nearby player or NPC support detected

	Action Sequence :
1. Throws "Smoke Pot" (temporary vision obstruction)
2. Performs long dash away from combat
3. Retreats toward nearest gate exterior
4. Breaks combat and resumes patrol after recovery

	Smoke has no mechanical benefit to players — purely NPC survival logic.

---

	Player Interaction Rules

* Players can assist in combat
* Players can heal them
* Players can give food items as a gesture (flavor interaction only)

	Important :
* No rewards
* No reputation
* No tracking

	This reinforces world authenticity, not farming.

---

	PvP & Attack Rules

* Cannot be attacked by players
* Not PvP-flagged
* Immune to griefing mechanics

(If you later want a “consequences” system, this can be toggled.)

---

		Dialogue / Flavor (Shared Pool)

	Used contextually, not guaranteed.
* “Tracks are fresh.”
* “Too close to the walls.”
* “That smoke wasn’t wind.”
* “We hold the line — not the glory.”
* “If you’re helping, stay sharp.”

---

	Design Intent (Why They Matter)
* Makes Southfall feel actively defended
* Supports night danger escalation
* Adds believable NPC survival logic
* Encourages organic player cooperation
* No reward = no exploitation

