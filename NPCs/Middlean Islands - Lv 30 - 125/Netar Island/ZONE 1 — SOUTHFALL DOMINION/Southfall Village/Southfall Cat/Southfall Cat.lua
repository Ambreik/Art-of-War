			Southfall Cat

	Location : Southfall Village – Rooftops near the Inn

---

	Appearance :

* Small, agile feline with dark gray fur and faint stripes.
* Bright green eyes that reflect lantern light at night.
* Often seen perched on rooftops or weaving between wooden beams.
* Occasionally swishes its tail, leaving subtle scratch marks on tiles.

---

	Personality :

* Curious and alert, occasionally meows when players approach.
* Appears skittish but will occasionally interact indirectly with players.
* Acts as an environmental storytelling NPC, hinting at hidden questlines.

---

	Behaviour :

* Roams rooftops, sometimes jumping from one building to another.
* Leaves scratch marks and minor environmental clues (e.g., knocked-over pots).
* Can react to player presence: meows, crouches, or darts away.
* Serves as a visual cue for the “Cat Stuck on Roof” mini-event tied to the questline.

---

	Role :

* Role(s) : Environmental NPC / Questline Hint
* Function :
	* Provides visual storytelling and subtle hints for the Southfall questline.
	* Interacts indirectly with “Lost Child” and “Strange Footprints” events.

---

	Mechanics :

* Type : Environmental NPC
* Reputation : None
* Interaction Limitations : Passive; cannot be directly interacted with except via quest triggers.
* PvP : Cannot be attacked
* Respawn Time : N/A – always present in the same environmental locations.
* Other Mechanics :
	* Visual environmental trigger for questline.
	* Scratch marks and positioning hint player to search the surrounding area.

---

	Skills : None

---

	Quest :
* Not a quest giver, serves purely as environmental clue for questline.

---

	Quotes / Emotes (Optional Player Observations)  :
* The cat meows softly from the rooftop, tail flicking nervously.
* “You notice small scratches on the tiles… perhaps someone climbed here recently?”
* The cat darts away, revealing a faint Crusader plate fragment beneath the eaves.

