			Guard Brennar Stonehelm

	Location :
Netar Island / Southfall Dominion / Southfall Village / Outer Wall Ramparts

---

	Appearance : Brennar wears well-maintained chainmail beneath a muted blue surcoat marked with Southfall’s crest. A solid steel helmet with a narrow visor 
shadows his eyes, giving him an unreadable presence. He carries a long steel-tipped spear, held upright or resting against the stonework, and rarely shifts his 
footing while on duty. His armor bears fewer marks than most—evidence of caution rather than inexperience.

---

	Personality : Calm, quiet, and highly observant. Brennar watches more than he speaks, taking in details others overlook. He does not jump to conclusions, but 
once he notices something amiss, he never forgets it. Villagers trust him because he never exaggerates danger—if Brennar is concerned, it is already serious.

---

	Behaviour :

* Assigned exclusively to wall rotations and elevated watch points.
* Periodically stops to scan beyond the village perimeter.
* Turns his attention toward disturbances outside the walls, even if no alarm has been sounded.
* During night hours, his patrol pace slows, increasing detection radius rather than movement speed.

---

	Role :

* Role(s) : Wall Guard
	Brennar Stonehelm is responsible for early detection of threats approaching Southfall, serving as the village’s first warning line against external danger.

