			Shadd Baitille (Dockside) – Rank III Trainer

	Location : Southfall Village – Dockside Pier

---

	Appearance :

* Middle-aged fisherman with tanned skin and calloused hands.
* Wears a water-stained leather apron over casual clothes.
* Wide-brimmed hat with seaweed tucked under the band.
* Carries a staff-like fishing pole, often leaning against the pier.
* Has a small satchel with baits and minor fishing tools.

---

	Personality :
* Patient and observant, often hums old sea shanties.
* Friendly but strict when teaching fishing techniques.
* Reacts dynamically to weather, tide, and time of day.

---

	Behaviour :
* Walks along the dock during the day, occasionally inspecting nets.
* Comments on tides, wind, and rare fish sightings.
* Provides tips to players on optimal fishing times.

---

	Role :
* Roles : Fishing Trainer / Rank III Profession Trainer
* Function :
	* Teaches advanced fishing techniques and recipes.
	* Sells Rank III fishing consumables, baits, and rods.
	* Provides fishing profession quests for Rank III advancement.

---

	Mechanics :

* Type : Trainer / Vendor
* Reputation : None, but Rank III Fishing required to purchase some items.
* Interaction Limitations : Requires Rank III Fishing to access advanced recipes or rods.
* PvP : Can be attacked, flagged for PvP.
* Respawn Time : Standard NPC respawn (5 minutes).
* Other Mechanics :
	* Can craft select high-rank fishing rods for players with materials.
	* Gives dynamic hints for rare fish spawns based on tide or weather.

---

	Goods / Craftable Items (Rank III Fishing Focus) :

* Stormcaller Rod – High-quality fishing rod for rough currents.
	* Effect : +10% chance to catch Rank III fish in strong currents.

* Moonfin Gleamer Bait – Special luminescent bait for event fish.
	* *Effect : Increases chance to catch Moonfin Gleamer.

* Echofish Net – Humming net that signals rare fish bites.
* Reflective Water Prism – Crystal that reveals hidden fish for 10 min.
* Nightbait Flask – Concentrated nocturnal scent; 1-hour cooldown.
* Tidewalker Boots – Reduces river drift while fishing.

*(Additional consumables or crafted rods can be unlocked through Rank III fishing questlines.)

---

	Skills :

* Skill 1 – Fishing Insight
  * *Description : Temporarily reveals rare fish spawns nearby for 5 minutes.
  * *Effect : +15% chance to catch rare Rank III fish in the area.

* Skill 2 – Tide Sense
  * *Description : Announces sudden tide changes affecting nearby fishing zones.
  * *Effect : Players in the area receive a visual cue for optimal fishing timing.

---

	Quest :

* Quest Title : Advanced Fisher Techniques
	* Quest Type : Profession Rank III Advancement
	* Objective(s) : Catch 5 Rank III fish using specific baits or rods.
	* Rewards : Experience, Rank III recipe unlocked, small monetary reward.
	* Conditions : Player must be Rank III Fishing.
	* Quest Dialogue :
		* Opening : “Ah, you’ve honed your skills well! Let me see if you’re ready for the master’s techniques.”
		* Mid-Quest : “Keep an eye on the currents. The tide waits for no one!”
		* Completion : “Well done! You’ve earned your mastery of these waters.”
	* *Additional Notes : Quest unlocks access to high-rank crafting rods.

---

	Quotes :

* “The tide is patient, but it tests us all.”
* “Use the right bait at the right hour, and you’ll catch more than fish.”
* “Stormy weather? That’s when the rare ones bite.”
* “Remember, a good fisherman respects the water.”
* “You’ve got the skill, but the sea tests your patience still.”

