	1. Buying Regular Goods (Coins)

 “Ah, a practical choice! This will serve you well.”
 “Coins in hand, supplies in pack. Good adventuring!”
 “May this little bottle keep you breathing and your sword swinging.”
 “Another day, another coin spent wisely.”

	2. Asking About Special Goods (Powder of Stinky Pollen)

 “Ah, the good stuff? Only the pollen will get you these treasures.”
 “Not everyone understands the value of Stinky Pollen. Do you?”
 “These items are for those who dare to venture at night… or those clever enough to farm.”
 “Careful! Spending your pollen too quickly is like lighting your coin on fire.”

	3. Buying Special Goods (Powder of Stinky Pollen)

 “Excellent choice! May this aid your adventures in Southfall.”
 “Powder well spent. You’ll see the difference soon enough.”
 “Ah, a wise collector of rare supplies! Good on you.”
 “I like the way you think—smart spending always pays off.”

	4. Insufficient Coins / Pollen

 “Looks like your purse—or pouch of pollen—is too light.”
 “Come back when you have enough, adventurer.”
 “Don’t leave me hanging! You need more than that to take this home.”
 “I’d sell it to you if I could… but your coin (or pollen) says otherwise.”

	5. Curiosity / Inquiry about Items

 “You want to know about that? Only a seasoned adventurer will truly understand its value.”
 “Every item here has a story. Some are quiet, some… quite explosive.”
 “These supplies aren’t magical, but they’ll keep you alive long enough to find real magic.”
 “Ah, always the curious one! A good trait, if a dangerous one.”

	6. General Idle / Environmental Quotes

 “Wind smells like the sea today. Best to stock up before heading out.”
 “Lanterns, rope, potions… a simple life, but safer than the wilds.”
 “Some say the monsters of Southfall fear nothing. I say, keep a small bottle on you.”
 “Even in a peaceful village, coins and pollen change hands faster than the tide.”

