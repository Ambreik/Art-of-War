			Guard Thalen

	Location :
Netar Island / Southfall Dominion / Southfall Village / Main Gate

---

	Appearance : A veteran human guard in reinforced steel-and-leather armor, worn smooth by years of patrol duty. His left pauldron bears the faded insignia of 
Southfall, partially scratched and repaired. He carries a long spear with a hooked blade, ideal for pulling enemies away from the gate. A lantern hangs from his 
belt during night patrols. His posture is rigid and alert, rarely relaxing even when idle. A thin scar runs across his cheek, partially hidden by a short, trimmed 
beard.

---

	Personality : Protective, disciplined, and quietly compassionate. Thalen takes his duty seriously and shows visible concern when civilians are at 
risk—especially children. He speaks plainly, without flourish, and dislikes unnecessary chatter. While wary of outsiders, he respects those who prove helpful to the
village. When discussing Crusader activity, his tone becomes tense and controlled.

---

	Behaviour :

* Remains stationed near the main gate during daytime.
* At night, occasionally walks short inspection paths near the gate towers.
* Reacts immediately to alarm bell activation by issuing commands to nearby guards.
* During related questline progression, his idle dialogue and stance subtly change to reflect urgency.

---

	Role :

* Role(s) : Quest Giver, Guardian NPC, Skillable NPC
	Guard Thalen oversees the security of Southfall Village, coordinates investigations into nearby threats, and serves as the primary authority figure for local 
incidents involving villagers and hostile activity.

---

	Mechanics :

* Type : Quest Giver, Guardian
* Reputation : None directly (may indirectly influence future Southfall or regional systems)
* Interaction Limitations :
	* Full interaction available from Level 52+
* PvP :
	* Cannot be attacked by players
	* Not flagged for PvP
* Respawn Time :
	* Not applicable (protected NPC)
* Other Mechanics :
	* Coordinates guard responses during village alarm events
	* Acts as the anchor NPC for a multi-quest village investigation storyline
	* Dialogue changes based on questline completion state

---

	Skills :

* Skill 1: Gate Command
	* Description : Thalen issues a sharp command, signaling nearby guards to tighten formation around the gate.
	* Effect : Nearby guard NPCs gain increased alertness and faster response to hostile entities for a short duration.

* Skill 2: Protective Stance
	* Description : Thalen plants his spear and adopts a defensive posture when enemies approach the village entrance.
	* Effect : Reduces damage taken by nearby civilian NPCs within a small radius.

* Skill 3: Alarm Coordination
	* Description : Automatically triggers when the village alarm bell is rung.
	* Effect : Synchronizes guard patrol paths and prevents delayed reactions during village alert states.

---

	Quest :

* Quest Title : *Whispers at the Well*
	* Quest Type : Storyline
	* Objective(s):
		* Speak with the frightened child near the village well
		* Report findings back to Guard Thalen
* Rewards :
	* Experience (3–5% of level)
	* 50 Silver
	* Cooking Profession Perchant
* Conditions :
	* Level 52+
* Quest Dialogue :
	* Opening : “I don’t like this. A child wandering alone, asking questions no child should have to ask.”
	* Mid-Quest : “Find him. And keep your eyes open — the dark doesn’t move without leaving marks.”
	* Completion : “That confirms my fears. This isn’t a simple disappearance.”
* Additional Notes :
	* Completing this quest unlocks the next stage of the investigation questline
	* Mini-events in the village become contextually linked to the storyline

