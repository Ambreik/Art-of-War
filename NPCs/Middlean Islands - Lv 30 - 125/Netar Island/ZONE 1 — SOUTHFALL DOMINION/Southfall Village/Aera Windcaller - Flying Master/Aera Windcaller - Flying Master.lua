			Aera Windcaller

	Location :
Netar Island / Southfall Dominion / Southfall Village / Coastal Rise / Windcaller Hut

(A small elevated clearing near the vendor cluster, marked by wind chimes, tether posts, and circling birds.)

---

	Appearance : Aera Windcaller wears dark, weathered skin-leather armor, stitched with bone clasps and wind-carved symbols. His long dark hair flows freely, often
lifted unnaturally by unseen currents. His eyes are reddish, sharp, and reflective, glowing faintly at dusk or during storms.

	He carries:
* A wood-and-bone perch staff, wrapped in feathers and leather straps
* A wind flute, etched with spiral glyphs, always within reach

	At idle, Aera may:
* Stand perfectly still as cloth and feathers move around him
* Sit on his perch while birds land nearby
* Vanish briefly into a swirl of wind, reappearing moments later a few steps away

	His presence subtly alters the environment: wind direction shifts, dust lifts, lantern flames bend.

---

	Personality : Detached, proud, and observant. Aera does not see himself as a servant or merchant — he is a keeper of passage, not a taxi. He respects courage, 
patience, and awareness of the skies.

	He remembers disrespect.
	Aera is calm by default, but becomes cold, sharp, and dismissive toward players who:
* Attack him
* Interrupt rituals
* Abuse flying mechanics
* Repeatedly cancel flights

	Forgiveness is possible, but never automatic.

---

	Behaviour :
* Rotates through multiple idle positions (standing, perched, pacing, flute-playing)
* Occasionally vanishes for 1–2 seconds, relocating nearby
* Watches players silently before responding
* Will refuse service to players he holds a grudge against
* During storms or fog events, his behavior becomes more active and alert

---

	Role :

* Role(s) : Flying Master, Skillable NPC, Transport Unlocker
	Aera Windcaller enables aerial travel between island locations, but only for players who have earned the "Brevet of Wind". He also serves as the narrative 
anchor for all flying-related mechanics on Netar Island.

---

	Mechanics :

* Type : Trainer / Transport NPC / Skillable NPC
* Reputation :
	* Hidden personal reputation: "Aera’s Regard"
* Interaction Limitations :
	* Flying requires completion of "Brevet of Wind" quest
	* Service can be denied if Aera holds a grudge
* PvP :
	* Can be attacked
	* If attacked and not killed, Aera remembers the attacker
	* Killing him triggers temporary flying shutdown for the area
* Respawn Time :
	* Long respawn (Island Event Tier)
* Other Mechanics :
	* Island-only flying
	* Restricted airspace enforced (no-fly zones, storm locks)
	* Wind conditions affect flight paths
	* Flying routes unlock progressively per island

---

	Skills :

* Skill 1: Windstep Shift
* Description : Aera dissolves into a spiral of wind and reforms nearby.
* Effect : Short-range teleport; used during idle, combat, or when disengaging.

---

* Skill 2: Gale Ward
* Description : Surrounds himself with a rotating wind barrier.
* Effect : Reduces incoming damage; knocks back nearby enemies.

---

* Skill 3: Skycaller’s Flute
* Description : Aera plays his flute, calling wind currents or creatures.
* Effect :
  * Summons flying mounts
  * Alters local wind behavior
  * Used to initiate flights

---
* Skill 4: Mark of the Disrespectful (Hidden)
* Description : Applied to players who attack or insult Aera repeatedly.
* Effect :
	* Flying costs increased
	* Possible service denial
	* Special dialogue changes

---

	Quest :

* Quest Title : "Brevet of Wind"
* Quest Type : Storyline / Unlock Quest
* Objective(s) :
	1. Observe wind patterns at three island vantage points
	2. Recover a broken perch-token from coastal cliffs
	3. Endure a short scripted flight trial (no control, trust-based)
	4. Return during active wind conditions
* Rewards :
	* "Brevet of Wind" (Flying License – Island Tier)
	* Unlock Flying Routes on Netar Island
	* Access to future Flying Masters
* Conditions :
	* Level requirement (Island tier)
	* Must not have attacked Aera prior
	* Cannot be completed during storms

---

