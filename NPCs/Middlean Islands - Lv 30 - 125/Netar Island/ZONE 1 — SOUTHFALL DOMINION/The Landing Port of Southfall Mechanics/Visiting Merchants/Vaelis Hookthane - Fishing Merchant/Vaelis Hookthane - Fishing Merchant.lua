			Vaelis Hookthane
			
**Type:** Fishing Goods Merchant
**Spawn Location:** The Landing Port of Southfall (dockside, near crates area).
**Rotation Position:** #1 (first in cycle)

---

# **1. NPC Description**

**Vaelis Hookthane** is a tall, wiry man with sea-weathered skin, eyes that shimmer like wet scales, and a calm, quiet posture. Like all merchants of his kind, he wears the **strange foreign outfit** — long layered cloth, deep blues and greys, reinforced with thin braided threads of unknown material.

He carries:

* A foldable bamboo-like pole
* A weathered crate with carved wave patterns
* A sealed scroll container tied with wax-black rope

Nobody knows where he comes from. His language has an accent unknown on Capital Island.
Villagers whisper he might be from **beyond the Great Sea**, from a land “where storms never sleep.”

**NPC is:**

* Non-attackable
* Cannot be moved, stunned, or displaced
* Forces **auto-dismount** within 4 meters
* Tagged as **Neutral • Foreign Merchant**

---

# **2. Spawn & Behavior**

### **Spawn Window**

Appears every **2–3 in-game days**, stays **6 in-game hours**.

### **Dual-Spawn Compatibility**

Can appear together with **Seraphelle Mossweft** (rare 8% chance).

### **Movement**

Stationary. Performs small ambient animations:

* Adjusts his fishing crate
* Sharpens hook tips
* Briefly inspects the water
* Releases a quiet whistle at random intervals

---

# **3. Currency Used**

### **Powder of Stinky Pollen (PSP)**

Used for ALL merchants on the island.

### **Stock Rules**

* Each item has limited stock per visit.
* If sold out → Vaelis gives special “out of stock” quotes.
* Stock resets only next visit.

