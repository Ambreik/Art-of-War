			Runecarver Elthron – Rune Merchant

---

# **Appearance & Personality**

Elthron is calm, deliberate, and speaks with a scholarly tone.
His clothes resemble stitched runecloth with faint glowing seams, matching the same cultural origin as all visiting merchants — unknown, yet refined.

He carries a scroll case embedded with runic channels that pulse based on the items he sells.

---

# **Vendor Window (Rune Merchant)**

**Currency Used:** *Powder of Stinky Pollen*
**Tabs:**

* **Runes** (Rank I–II)
* **Rune Crafting Materials**

Each visit he brings **5–7 random items**, with limited stacks.

