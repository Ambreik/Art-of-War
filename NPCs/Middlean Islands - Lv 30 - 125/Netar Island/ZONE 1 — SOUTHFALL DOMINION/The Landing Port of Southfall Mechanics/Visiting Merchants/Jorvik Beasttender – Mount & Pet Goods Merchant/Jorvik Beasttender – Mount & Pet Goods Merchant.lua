			Jorvik Beasttender – Mount & Pet Goods Merchant

*(Rotating Merchant – Appears at The Landing Port of Southfall)*

---

## **• Type:** SHOP NPC
## **• Function:** Sells pet food, pet evolution items, pet skill manuals, mount food, mount revival reagents, mount essences, and mount gear.

---

# **Overview**

Jorvik Beasttender is a mysterious traveling merchant belonging to the *Unknown Caravan*, a group whose origin no one on Netar Island can truly explain. Arriving unpredictably at **The Landing Port of Southfall**, Jorvik specializes in goods related to pets and mounts — many of which cannot be found anywhere else in the Middlean Islands.

He may appear alone or with a **20% chance** alongside *Runecarver Elthron*, creating temporary access to a unique double-merchant window. He is fully neutral, cannot be attacked, and carries the same signature uniform shared by all rotating merchants.

Jorvik offers a rotating selection of items, all purchased with **Powder of Stinky Pollen**. Each visit includes limited stock items, pet and mount consumables, rare gear, revival reagents, and a unique **Secret Item** available only to early visitors or players with Rank II Navigator’s Lodge reputation.

He is a favorite among beastmasters, tamers, and adventurers who rely heavily on their companions.

---

# **Appearance**

Jorvik wears the mysterious and uniform attire characteristic of all rotating merchants:

* A deep teal hooded mantle with gold-thread border.
* A heavy traveler’s cloak with embroidered sigils that do not match any known faction or kingdom.
* Leather satchel and broad belt with hanging pouches containing herbs, grains, feathers, small bones, runic stones, and vials.
* A beast-bone staff capped with a polished amber orb (glows slightly when creatures are nearby).
* His eyes are amber-gold and reflect light in a way resembling nocturnal predators.
* Despite carrying no visible weapons, a faint aura of protection surrounds him.
* Animals around the dock tend to calm down in his presence.

---

# **Behavior & Interaction**

* Auto-dismount bubble: any player approaching within 6 meters automatically dismounts.
* Immune to all forms of damage and cannot be targeted by monsters.
* Has two shop tabs: **PET GOODS** and **MOUNT GOODS**.
* Stocks rotate randomly each time he appears.
* Limited stock per visit (3–10 units per item depending on rarity).
* Has distinct quotes based on time of day, stock amounts, dual-spawn condition, and secret item activation.
* Offers a cryptic hint about which merchant will arrive next.

Spawn rules:

* Appears every 2–3 in-game days.
* Stays for 6 in-game hours.
* 20% chance to spawn with *Runecarver Elthron*.
* Effects upon arrival: faint beast-call echo + soft amber glow.

