# **6. SECRET ITEM**

Unlocked if:

* The player interacts with Miraleen within **the first 60 minutes of her arrival**, **OR**
* The player has **Navigator’s Lodge REP Rank I**

---

### **Secret Item: “Tailor’s Guiding Spool”**

**Cost:** 18 PSP
**Stock:** 1

**Effect:**
For 25 minutes, tailoring craft attempts gain **+12% chance to create an Improved Cloth Item**.
(Timer continues during death. Non-stackable.)

