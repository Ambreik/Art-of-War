			Miraleen Threadsage - Tailoring Merchant


**Tailoring Goods Merchant**
**Rotation Position:** #4
**Spawn Location:** Southfall Landing Port — north-eastern pier, beside the stacked linen bundles and drying racks.

---

# **1. NPC Description**

Miraleen Threadsage is a calm, soft-spoken traveler dressed in the same unique foreign uniform as all rotating merchants — but with textile-themed embellishments:

* Long woven sleeves with geometric embroidery
* A sash decorated with spindle charms
* Light pouches filled with threads, needles, and fabrics
* A traveling loom strapped neatly across her back

She is tidy, meticulous, and almost ritualistic in her movements.
Locals whisper that her clothing techniques resemble none from the known continents, and she sometimes mutters stitch patterns that no Tailor recognizes.

Miraleen frequently inspects the air for “thread tension,” a habit nobody understands.

---

# **2. Spawn & Behavior**

### **Spawn Window**

Appears after Bront Tavaspan, every **2–3 in-game days**.

### **Dual-Spawn Chance**

8% chance to spawn with Bront or the next merchant.
If dual-spawned with Bront, she complains about the smoke ruining fabrics.

### **Ambient Animations**

* Smoothly pulls thread between her fingertips
* Measures imaginary cloth with floating gestures
* Adjusts a loom strap as if preparing to weave
* Folds and refolds the same piece of cloth endlessly

---

# **3. Currency**

**Powder of Stinky Pollen (PSP)** — same universal merchant currency.

Each item has **limited stock**.

---

