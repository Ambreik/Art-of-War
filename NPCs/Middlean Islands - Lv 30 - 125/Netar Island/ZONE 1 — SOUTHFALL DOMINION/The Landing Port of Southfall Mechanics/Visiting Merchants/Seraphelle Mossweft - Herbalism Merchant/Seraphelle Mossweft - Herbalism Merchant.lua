			Seraphelle Mossweft - Herbalism Goods Merchant

**Herbalism Goods Merchant**
**Rotation Position:** #2
**Spawn Location:** The Landing Port of Southfall (usually near the stack of vine-covered crates on the eastern side of the dock)

---

# **1. NPC Description**

Seraphelle Mossweft appears almost ethereal — a tall, slender woman wrapped in the same **mysterious foreign attire** shared by all roaming merchants.
Her clothing has subtle **green-tinted fibers** woven into the sleeves, and she carries:

* A bundle of preserved herbs tied with vine-rope
* A leaf-woven satchel at her hip
* A strange, smooth seed-pod that glows faintly when she moves

Her mannerisms are soft, precise, and deliberate.
She speaks with calm confidence, as if she understands the forest better than those who live on the island.

Villagers whisper she might come from **a land where trees whisper warnings** — a folklore rumor we will later use in the Zone 4 town gossip files.

**NPC Traits:**

* Cannot be attacked or targeted by monsters
* Auto-dismount radius: ~4 meters
* Foreign Merchant Tag
* Calm, slow gestures; ambient scent particles drift around her

---

# **2. Spawn & Behavior**

### **Spawn Window**

Appears every **2–3 in-game days** following Vaelis Hookthane.

### **Dual-Spawn Chance**

8% chance to appear *together* with Vaelis Hookthane.
When dual-spawned, they occasionally exchange subtle dialogue lines.

### **Ambient Animations**

* Sorts herbs on the ground
* Gently grinds petals in a small mortar
* Listens to the wind direction
* Sniffs leaves to verify freshness

---

# **3. Currency**

**Powder of Stinky Pollen (PSP)**
Used by *all* visiting merchants.

**Stock Limits:**

* Every item has a limited per-visit supply
* Sold-out state triggers specific dialogue lines
* Stock resets next arrival only

---

