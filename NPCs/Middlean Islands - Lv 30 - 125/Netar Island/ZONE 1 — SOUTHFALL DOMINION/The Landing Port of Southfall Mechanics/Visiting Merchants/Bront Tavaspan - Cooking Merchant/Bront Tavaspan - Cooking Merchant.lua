			Bront Tavaspan - Cooking Merchant
			
			
**Cooking Goods Merchant**
**Rotation Position:** #3
**Spawn Location:** Southfall Landing Port — western docks, beside the fish crates and portable stove setup.

---

# **1. NPC Description**

Bront Tavaspan is a broad-shouldered, warm-voiced traveling cook whose foreign attire matches the unique uniform worn by all rotating merchants.
His version features:

* A heat-resistant outer robe tied with burnt-orange sashes
* Heavy leather cooking gloves
* A small foldable stove strapped to his back
* Numerous spice jars attached to a wide belt

He smells faintly of smoked herbs and roasted meat the moment he spawns.

Bront’s demeanor is hearty, friendly, and slightly chaotic — he constantly multitasks by stirring imaginary pots or checking spice tins as he speaks.
Travelers have noted that he cooks dishes from lands unknown and refuses to reveal their origins.

---

# **2. Spawn & Behavior**

### **Spawn Window**

Appears after Seraphelle Mossweft, every **2–3 in-game days**.

### **Dual-Spawn Chance**

8% chance to spawn simultaneously with "Miraleen Threadsage - Tailoring Merchant".
If both are present, Bront often tries to convince Seraphelle to let him use her herbs — she politely refuses.

### **Ambient Animations**

* Lights and extinguishes a portable stove
* Tosses seasoning into the wind to “test the gust”
* Checks small jars and mutters about flavor profiles
* Fans smoke off imaginary pans

---

# **3. Currency**

**Powder of Stinky Pollen (PSP)** — same shared traveling-merchant currency.

Every item has **limited stock** that resets only on his next rotation.

