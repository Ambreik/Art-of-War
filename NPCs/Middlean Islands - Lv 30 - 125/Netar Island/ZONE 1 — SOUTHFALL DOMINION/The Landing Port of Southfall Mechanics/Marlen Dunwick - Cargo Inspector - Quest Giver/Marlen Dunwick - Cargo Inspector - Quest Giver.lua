# **QUEST 2 — “The Dockside Pursuit”**

### **Quest Type:**

Solo Mini-Encounter / Chase

### **Quest Giver:**

Marlen Dunwick

---

## **Quest Offer Dialogue**

**Marlen Dunwick:**

> “I saw someone skulking near the eastern supply racks earlier—hood up, gloves on, carrying something small.
> When I called out, he bolted.
> If he’s tied to that ledger, he’ll try to flee the port entirely.”

> “Find him. Bring back whatever he’s hiding.”

---

## **Objectives:**

* **Chase the Suspicious Runner**
* Interrupt him before he reaches the end of the pier
* Loot the stolen item: **Contraband Manifest Token**
* Return to Marlen

### **Mechanic:**

A non-combat NPC appears and begins running a set escape route.

Player must:

* Interact with **Footprints** to trigger his spawn
* Sprint after him
* Use any movement ability or a “Shout” interaction (appears on screen) to slow him
* When caught, he despawns in smoke and drops a token

---

## **Item Looted:**

### **Contraband Manifest Token**

A triangular brass token with the initials **“R.M.”** engraved.
Used by smugglers to approve illegal cargo.
Clearly *not* from the port authority.

---

## **Quest Completion Dialogue**

**Marlen Dunwick:**

> “A smuggler manifest token…
> These are used only by the outlaw groups operating out of the Great Sea’s shadow routes.”

> “The Navigator’s Lodge will want this immediately.
> You’ve done excellent work.”

**Rewards:**

* **700 Navigator’s Lodge REP**
* **60 Gold**
* Unlocks final quest.

