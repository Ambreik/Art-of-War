# **Overview**

Marlen Dunwick is the senior Cargo Inspector in **Southfall Port**, responsible for ensuring that every crate, barrel, and shipment entering Netar Island complies with island trade law.
Experienced, sharp-eyed, and notoriously difficult to deceive, Dunwick plays an early role in the player’s exposure to the island’s criminal undercurrent.

Marlen is the NPC who responds to items and events generated by the “Suspicious Cargo” mechanic—especially the **Contraband Ledger**, which he immediately recognizes as coded smuggler intel.

He stands near the central inspection platform, where workers bring crates for tagging and approval.

This NPC is essential because:

* He introduces the player to the smuggler threat.
* He acts as the anchor NPC for the dock sub-storyline.
* He awards minor early-game reputation with the Dockworkers’ Syndicate.
* He provides access to “dock maintenance” and “inspection” themed side content.

---

# **Appearance**

Marlen Dunwick has the look of a man who has worked the docks for decades:

* Wears a **dark blue port authority coat** with brass clasps.
* Thick leather belt with a bundle of **inspection tags** and a small seal punch.
* Brown work trousers tucked into waterproof boots.
* Short gray hair, close-trimmed beard.
* Eyes narrowed, analytical, constantly scanning his surroundings.
* Holds a heavy **clipboard carved from driftwood**, etched with his personal seal.
* A quill tucked behind one ear.
* A large magnifying monocle hanging from a chain on his chest.

Idle animation:

* Taps the clipboard.
* Inspects crates even when not interacting with players.
* Occasionally shouts at workers to “lift properly” or “watch the edges.”

