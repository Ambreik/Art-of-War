# **QUEST 1 — “Ledger of Lies”**

### **Quest Type:**

Story / Investigation

### **Quest Giver:**

Cargo Inspector **Marlen Dunwick**

### **Quest Start Item:**

**Contraband Ledger**
A small water-damaged notebook with coded marks and unusual shorthand symbols. A faint smell of seawood and bitter oils clings to it.

---

## **Quest Start (Hand Item to Marlen)**

**Marlen Dunwick:**

> “Let me see that… This isn’t one of ours. These codes don’t belong to any sanctioned merchant route.
> Someone at this port thinks they can move goods under my nose.”

> “This ledger’s incomplete. Whoever dropped it must have been in a hurry.
> We’ll need more evidence if we’re to act on this.”

---

## **Objectives:**

* **Inspect 3 Suspicious Crates** around the Landing Port
* **Find “Illicit Parcel Fragment”** inside one of the crates
* Return to **Marlen Dunwick**

### **Mechanic:**

3 crates with faint markings will spawn in fixed but cycling locations near the dock.
Player interacts → 2 crates contain nothing → 1 contains:

**Illicit Parcel Fragment**
A torn scrap of thick fabric stamped with a foreign wax seal.

---

## **Quest Completion Dialogue**

**Marlen Dunwick:**

> “A torn parcel with a foreign seal… this confirms it. These smugglers aren’t local.
> They’re routing goods from Capital Island without Navigator’s Lodge approval.”

> “If the Lodge hears about this, they’ll want names.
> And they’ll compensate those who assisted.”

**Reward:**

* **500 Navigator’s Lodge REP**
* **30 Gold**
* Unlocks next quest.

