# **Behavior & Interaction**

### **General Behavior**

* Patrols a short 6–10 meter loop around the inspection platform.
* Stops to inspect crates periodically.
* Occasionally berates lazy dockhands.
* Cannot be harmed or engaged in combat.

### **Player Interaction**

Marlen reacts differently based on context:

**1. Basic Interaction**

* Provides minor lore about cargo routes, ship arrivals, and port rules.
* Offers general tips about interacting with dock systems (crates, merchants, etc.)

**2. When handed a Contraband Ledger**

* Immediately shifts tone into urgency.
* Starts the quest continuation event chain.

**3. Reputation Effects**
As players gain reputation with Dockworkers:

* He gives more friendly dialogue.
* Later unlocks a luggage-tag cosmetic or a “Dockworker’s Gloves” cosmetic prize.

