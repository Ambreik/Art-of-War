			Navigator’s Lodge Officer — Lira Seastone

## **• Role:**

Navigator’s Lodge Representative (Capital Island Authority)

## **• Type:**

Story NPC / Reputation NPC / Quest NPC

## **• Function:**

Represents the **Navigator’s Lodge** at the Landing Port of Southfall.
Handles official reports, oversees trade legitimacy, accepts contraband evidence, and provides reputation to players who assist port security matters.

---

# **Overview**

Lira Seastone serves as the **official delegate of the Navigator’s Lodge** in Southfall Dominion.
Her primary duty is to oversee maritime operations connected to Capital Island, ensuring that all trade going through the Landing Port complies with lodge-sanctioned regulations.

She acts as the **final authority** for investigations, including smuggling and illegal cargo flows detected on Netar Island.

Placed on the **upper dock balcony**, she has a wide view over arriving ships, dock workers, and cargo transfers. Players who finish the “Contraband Ledger” micro-questline will report directly to her.

Lira is calm, perceptive, and carries the air of someone used to managing chaos without raising her voice.

She does NOT move from her post.
She is NOT targetable.
She belongs to **Capital Island faction**, not Netar Island locals.

---

# **Appearance**

* Tall, composed woman in her late 30s.
* Wears the formal **Navigator’s Lodge navy-blue coat**, reinforced with brass buttons and shoulder straps marked with rank.
* High boots polished to a near-mirror sheen.
* Carries a **rolled maritime map** tucked under one arm at all times.
* A brass compass hangs from her belt.
* Her hair is dark blonde, tied into a tight naval knot.
* Her eyes are sharp sea-green — constantly observing the dock.
* Occasionally, she wears thin reading gloves for handling documents.

---

# **Behavior & Interaction**

* Stands on the **upper balcony platform** overlooking the docks.
* Performs a loop of small animations:

  * Studying maps on a wooden table.
  * Watching the sea horizon.
  * Making notes in a leather-bound folder.
  * Adjusting her lodgemaster compass.
* Turns her gaze to the player when approached.
* Cannot be pushed or moved.
* Reacts instantly when given official items (such as quest objectives).
* Grants a profession-friendly aura:
  **“Lodge Discipline” — reduces vendor prices in ports by 1% (cosmetic effect, no functional discount unless you want one later).**

---

# **Mechanics & Functions**

### **1. Quest Interaction**

* Final hand-in NPC for the “Contraband in the Current” questline.
* Accepts:

  * **Contraband Manifest Token**
  * **Illicit Parcel Fragment**
* Awards Navigator’s Lodge reputation.

---

### **2. Reputation Delegation**

* Players gain **Navigator’s Lodge REP** when completing port-related quests.
* REP tiers may unlock nothing for now (as instructed), but can be expanded in future.

---

### **3. Port Authority Presence (Passive Mechanic)**

* While the player is within 25 meters of her:

  * Hostile creatures are less likely to path aggressively toward the dock area.
  * Smuggler NPCs will **not** spawn near the upper platform area.

---

### **4. Document Examination Mechanic**

* When given a quest item, she plays a short “document inspection” animation:

  * Opens notebook
  * Scans item
  * Makes notes on a parchment scroll

---

### **5. Lore Anchor (Non-Mechanical)**

* Lira quietly represents the influence of Capital Island.
* Provides context for why smugglers fear documented evidence.
* Demonstrates that official maritime governance extends onto Netar Island.

