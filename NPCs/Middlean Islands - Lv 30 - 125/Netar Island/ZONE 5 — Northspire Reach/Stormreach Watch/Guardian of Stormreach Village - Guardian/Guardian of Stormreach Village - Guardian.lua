			Guardian of Stormreach Village

---

	Name : "Tharion Stoneveil"  "The Living Shield of Stormreach"

---

	Type : Elite Defensive NPC - Skillable / Non-PvP / Civilian-Protector / Event-Driven

---

	Faction : "Stormreach Watch" - "Order of Restoration" (Allied)

---

	Location : Stormreach Village
	
	Usually patrols:
* Civilian quarters
* Market edges
* Inner plaza

	At night:
* Found in the tavern with Captain Aldric Stormward
* Drinking and sharing war stories until:
	* Midnight normally
	* Later during winter seasons

---

	Role : 
* Primary "Protector of Civilians & Infrastructure"
* Tharion Stoneveil is not a battlefield commander — he is the immovable presence that ensures Stormreach’s people survive whatever the Crusaders bring. Where the 
Captain leads soldiers, Tharion guards the soul of the village.
* He does not fight for glory, and he does not fight for players — he fights for Stormreach alone.

---

	Appearance : 
* Same age range as the Captain (early 50s)
* Broad, heavily built, slow but unyielding
* Wears armor similar to Aldric’s but:
	* More reinforced
	* Less ceremonial
	* Scarred and darkened from countless impacts
* Helmet often off when not in combat
* Carries a massive long spear taller than himself
* Armor always dusty or worn — never pristine

---

	Weapon Suit

	1. Primary Weapon : "Stoneveil Longspear"
* Heavy, thick-shafted spear with a wide leaf blade
* Designed for :
	* Formation control
	* Area denial
	* Pinning enemies
* Emits a dull resonance when striking elites

	2. Secondary
* None — never uses shield or secondary weapon
* His spear is his defense.

---

		Combat Profile

	Combat Style

* Pure Defensive Vanguard
	* Zone Control + NPC Protection Specialist

	Excels at:
* Holding chokepoints
* Preventing NPC deaths
* Absorbing extreme damage
* Disrupting elite movement

---

	Core Abilities

	"Impenetrable Stand"

	Roots himself in place:
* Massive damage reduction
* Prevents enemy knockback or displacement
* Creates a “no-pass” zone behind him

---

	"Spear of Interdiction"

	Wide piercing thrust:
* Hits multiple enemies in a line
* Applies:
	* Heavy slow
	* Armor fracture
* Especially effective against elites and siege units

---

	"Guardian’s Vow"

	Passive aura:
* Reduces damage taken by civilians and nearby NPCs
* Automatically redirects some incoming damage to himself

---

	"Groundbreaker Thrust"

	Slams spear into ground:
* Creates a shockwave
* Knocks enemies down
* Interrupts Crusader channeling abilities

---

	PvP Rules : 

❌ Cannot be flagged for PvP
❌ Cannot be damaged by players
❌ Cannot assist players in PvP

	If players are attacked near him:
* He will not intervene
* He will reposition to shield NPCs instead

	This ensures:
✔ He is not exploitable
✔ His loyalty is purely to Stormreach
✔ PvP balance remains intact

---

		Invasion Behavior

	During Crusader invasions :
* Moves immediately to:
	* Civilian clusters
	* Market zone
	* Emergency bell
* Will ignore players entirely
* Prioritizes:
	1. Civilians
	2. Vendors & trainers
	3. Non-military NPCs
* Becomes one of the last NPCs standing if the village falls

---

	Durability

	Extremely high :
* Massive HP pool
* Damage scaling resistance
* Cannot be burst down
* Designed to hold against full invasion waves

	He is meant to outlast, not overpower.

---

	Relationship with Captain Aldric Stormward

* Old battlefield companions
* Mutual respect, different philosophies:
	* Aldric: Tactical, commanding
	* Tharion: Stoic, immovable
* Seen together in the tavern at night:
	* Drinking
	* Reminiscing
	* Occasionally arguing about past battles

	This relationship adds:
✔ World depth
✔ Visual storytelling
✔ Seasonal behavior immersion

---

	Behavior States : 

	1. Calm
* Patrols civilian areas
* Stands silently near markets
* Occasionally nods to players

	2. Alert
* Positions near chokepoints
* Stops moving randomly
* Keeps spear planted

	3. Besieged
* Locks down civilian zones
* Uses Impenetrable Stand frequently
* Never retreats

	4. Broken
* Becomes hyper-defensive
* Stands alone if needed
* Protects even fallen NPC bodies

