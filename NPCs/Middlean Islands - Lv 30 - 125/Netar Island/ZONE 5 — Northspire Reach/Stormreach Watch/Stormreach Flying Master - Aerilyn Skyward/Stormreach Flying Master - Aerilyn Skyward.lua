			Stormreach Flying Master

---

	Basic Information : 

* Name : Aerilyn Skyward
* Title : Master of Aerial Paths — Stormreach Branch
* Role : Transportation NPC (Flying Stall Operator)
* NPC Type : Civilian Service / Transit Guide
* Faction : Neutral (Stormreach Transport Authority)
* Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach / Stormreach Watch
* Exact Placement : Outdoor platform near the village entrance (flight pad), adjacent to the travel hut

* Disposition : Polite, Welcoming, Business-minded
* PvP Status : Not PvP-flagged
* Skillable : No
* Reputation : Non-REP NPC
* Questgiver : No

---

		Visual Identity : 

	Appearance :
* Wearing leather aviator gear with brass buckles
* Thick goggles resting on brow
* Light fur-lined jacket suitable for high-altitude flight prep
* Wears a pilot’s scarf that flows even in calm wind
* Has a small satchel of flight papers and charts

	Mount :
* Perched beside her is a tethered "Skywing Harnessed Drake" (non-combat transport mount), ready for riders

	Hut :
* Simple yet clean structure with maps, flight logs, and route charts
* Wind chimes outside to indicate current wind strength conditions

---

	Role in Village / Functions : 

* Aerilyn Skyward is the designated "Flying Master" for "Stormreach Watch", granting access to controlled flight travel along predefined routes throughout Middlean 
Islands and Netar Island.

	Her core functions are :
* Interface for "Flight Route Selection"
* Validation of currency and route eligibility
* Denial or pause of service during major world events
* Visual and audio maps for route planning

	She cannot be attacked and is purely a service NPC.

---

		Mechanics : 

	1. Flight Route Access : 

	Players interact with Aerilyn to:
* Open the "Flight Map UI"
* View available destinations and costs
* Select a flight path
* Pay the required fee
	* Silver (standard flight fare)
	* Alternative currency options (special items or event tickets)

	Once confirmed:
* Player mounts the Skywing drake automatically
* Flight path begins along predefined aerial corridors

	Behavior :
* Cannot be used if:
	* Invasion is active
	* Village Morale is Broken
	* Player is in combat status
* Returns a prompt if service is denied

	Flight paths are one-way or multi-leg as designed by the world map.

---

	2. Invasion / Alarm Response : 

	When an invasion is active in "Stormreach Watch" :

* Aerilyn’s hut door closes
* Flight services are disabled
* She shouts warnings to players:
	* "Flights are grounded — Stormreach is under siege!"
	* "Come back once the village is secure."
* UI is disabled for flight routes until the village returns to at least "Alert" morale

	This integrates with your invasion and morale systems to ensure the mechanic feels cohesive rather than abstract.

---

	3. Barrier Override System : 

* Stormreach is subject to an invisible flight barrier on "Netar Island" , preventing free flying.
* However, Aerilyn’s flight routes are special authorized corridors that :
	* Bypass the island’s invisible anti-flight barrier
	* Allow fast travel across wider distances
	* Are only usable via her service interface
	* Prevent unintended exploits (no manual flying outside system routes)
* This echoes typical flight master functionality seen in open-world games where flight is bound to fixed points rather than player-controlled models 

---

	4. Cost & Route Modifiers : 

* Flight service cost and availability may vary based on :
	* Time of day
	* Zone Event states (some routes cheaper at night)
	* Weather intensity (strong winds can increase fare or delay departures)
	* Player’s faction or event reputation

	Aerilyn provides friendly explanatory dialogue when such modifiers apply.

---

	5. UI & Interaction : 

	Flight UI Elements :
* Route list with :
	* Destination names
	* Fare cost
	* Estimated travel time
* Confirmation button
* Cancel button
* Real-time weather indicator for flight influence
* Notes on event conditions that may affect departure

	Interaction is disabled during invasions or when village Morale is Broken.

