* Name : Roderic Ironbind
* Title : Master of Field Traps
* Role : Combat Utility Trainer
* NPC Type : Specialized Trainer / Vendor
* Faction : Neutral (Stormreach Defense Corps – Auxiliary)
* Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach / Stormreach Watch
* Exact Placement : Near the outer bulwark, close to chokepoints and stairway access

* Disposition : Stern, Precise, Tactical
* PvP Status : Not PvP-flagged
* Skillable : Yes (Utility / Control-based)
* Reputation : No direct REP
* Questgiver : Yes (Trap Introduction & Mastery Quests)

---

		Visual Identity : 

	Appearance :
* Wears reinforced leather armor mixed with light metal plates
* Numerous trap parts hanging from belt: springs, claws, coils, runes
* Fingerless gloves with scorch marks and oil stains
* Short, graying hair, scar across cheek from a failed trap test

	Workshop :
* Small fortified shack with:
	* Trap racks on walls
	* Practice targets outside
	* Burnt and frozen patches on the ground from testing

---

		Role in Village : 

	Roderic Ironbind introduces and governs the "Trap System", a new combat utility mechanic focused on :
* Area control
* Enemy disruption
* Defensive preparation
* Tactical combat support

	He is the only NPC on Netar Island that teaches and enables this system.

---

		Mechanics — Trap System (Core Design) : 

	This section defines how traps function globally in your game.

---

	1. Trap Type: Consumable Ground Items : 

	Traps are:
* Active consumables, not passive abilities
* Placed directly on the ground
* Trigger when enemies enter their radius
* Have limited duration and charges
✔ Traps do NOT require a class
✔ Traps are usable by all players
✔ Certain classes may enhance them later

---

	2. Trap Categories : 

	Roderic teaches and sells schematics for the following trap families :

	A. Control Traps

	Root Trap : 
* Immobilizes enemy for X seconds
* Breaks on heavy damage
* Best vs melee

	Slow Trap : 
* Reduces movement speed by %
* Does not break on damage
* Stacks with frost effects

---

	B. Disable Traps

	Freeze Trap :
* Encases enemy in ice
* Breaks on first hit
* Enemy becomes immune to freeze for a short time afterward

	Shock Trap : 
* Stuns target briefly
* High cooldown trap
* Visual lightning discharge

---

	C. Terrain Traps

	Oil Trap : 
* Slows and makes ground flammable
* Can be ignited by fire spells or fire barrels

	Spike Trap : 
* Deals physical damage over time
* Hidden visually until triggered

---

	D. Utility Traps

	Fear Trap : 
* Causes enemies to flee randomly
* Less effective on elites
* Ineffective on bosses

	Reveal Trap : 
* Reveals stealth or invisible enemies
* No damage
* Critical for PvP and rogue enemies

---

	3. Trap Placement Rules : 

	Traps can be placed :
✔ On ground
✔ On stairs
✔ At chokepoints
✔ Near doors, barricades, walls

	Traps CANNOT be placed :
✖ In mid-air
✖ On vertical walls
✖ On NPCs
✖ Inside protected buildings

---

	4. Trap Crafting System : 

	Traps are crafted via :
* Trap Schematics

	Each schematic unlocks :
* Trap type
* Trap quality tiers
* Visual style

	Schematics are :
* Sold by Roderic
* Earned via quests
* Dropped rarely from elites

---

	Crafting Process : 

	To craft traps, players need :
* Basic materials (metal, wood, frost cores, etc.)
* Trap Components :
	* Springs
	* Runes
	* Cores
* Optional Enhancers :
	* Duration extender
	* Radius amplifier
	* Damage modulator

	Crafting occurs at :
* Trap Tables (near Roderic)
* Or portable kits at higher mastery

---

	5. Trap Quality Tiers : 

| Tier                         | Effect                                 |
| ---------------------------- | -------------------------------------- |
| Crude                        | Low duration, small radius             |
| Standard                     | Balanced                               |
| Refined                      | Improved effects                       |
| Mastercrafted                | Enhanced control, visuals              |
| Stormbound (Stormreach Only) | Special interactions with wind & frost |

---

	6. Trap Mastery System : 

	Players level Trap Mastery by :
* Successfully triggering traps
* Crafting traps
* Completing trap-related quests

	Trap Mastery unlocks:
* Faster placement
* Reduced resource cost
* Multi-trap chains
* Hidden trap placement
* Elite-resistant traps

---

	7. Trap Interaction With World Systems : 

	Wind System : 
* Strong wind reduces trap trigger accuracy
* Wind may displace lightweight traps
* Heavy traps resist displacement

	Frost System : 
* Frost traps gain bonus duration
* Freeze traps stronger during storms

	Fire & Light : 
* Oil traps ignite
* Beacon fires can trigger nearby oil traps accidentally

	Invasion System : 
* Traps are more effective on scouts and waves 1–2
* Reduced effect on elites
* Special elite-counter traps unlock later

---

	8. Trap Limits

	To prevent abuse :
* Max active traps per player: X (scales with mastery)
* Trap decay over time
* Only one Freeze Trap active per player at a time
* Traps do not stack infinitely in same tile

---

