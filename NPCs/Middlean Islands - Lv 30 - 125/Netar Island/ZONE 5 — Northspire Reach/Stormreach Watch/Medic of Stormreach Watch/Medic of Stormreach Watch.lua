			Medic of Stormreach Watch

	Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach\Stormreach Watch — Village Plaza / Healing Tent

	Role : Healer / Support Unit

	Functions :
* Provides instant healing and curing for players on interaction
* Supports NPCs during invasions (healing military units)
* Acts as an emergency support in high-damage scenarios
* Moves quickly through the village to reach wounded or affected NPCs
* Cannot be PvP-targeted by players, but may be killed during invasions if left in the open too long


	Appearance :
* Human, mid-40s, wearing reinforced cloth and leather vestments with red cross motifs
* Carries a satchel with herbs and potions
* Hands glow faintly when casting healing
* Moves briskly with an alert, focused gait

	Behavior / AI :
* Automatically detects nearby wounded NPCs and players
* Stops players who cross his path and prompts a “Receive Healing?” dialogue
* Raises hands and casts healing effect, curing debuffs and restoring HP instantly
* During invasions, prioritizes military NPCs first, civilians second
* Can be killed by elite crusaders or mobs if left unprotected during siege
* Immortal outside invasions
* Quick response time for movement and targeting

	Mechanics :

* Healing Interaction :
	* Player stops, receives instant full heal
	* Removes debuffs such as Poison, Bleed, or minor curses
	* Visual: glowing hands on player, gentle animation of healing wave
* Movement :
	* +20% speed compared to normal NPCs
	* Automatically pathfinds to injured units or players
* Invasion Mechanics :
	* Priority target for crusaders if player healing is heavily affecting combat
	* Can die if left exposed during prolonged battles
* Interaction Prompt :
	* "Medic: I will tend to your wounds."
	* Player must accept to receive healing
* Immortal Flag : Active outside invasion
* PvP Flag : None

	Quotes / Emotes :
* "Hold still, I’ll have you patched up in a moment."
* "This won’t hurt… much."
* "Keep moving! We need every hand to defend the village."
* "Another wound? You’re lucky I’m quick today."
* Emotes : raises hands, kneels to examine, runs briskly between units

	Skills / Abilities :
* Instant Full Heal (player or NPC)
* Cure Minor Debuffs
* Fast Movement AI for auto-targeting wounded units

	Persistence :
* Present permanently in village (unless killed during invasion)
* Respawns after a set timer if killed in combat

