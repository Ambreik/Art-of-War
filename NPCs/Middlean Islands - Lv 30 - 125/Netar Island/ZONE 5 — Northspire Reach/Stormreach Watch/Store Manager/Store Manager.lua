			Store Manager

---

	Basic Information : 

* Name : Store Manager
* Title : Keeper of Supplies
* Role : Village Economic Control
* NPC Type : Systemic Manager / Economic Controller
* Faction : Stormreach Watch
* Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach / Stormreach Watch
* Exact Placement : Central Plaza, near clustered vendors and warehouses

* Disposition : Pragmatic, calculating, stern but fair
* PvP Status : Not PvP-flagged
* Skillable : No
* Reputation : Indirect (affects village-wide perception)
* Questgiver : No (system-driven NPC)

---

		Visual Identity : 

	Appearance :

* Heavy fur-lined coat over merchant’s attire
* Ledger book always in hand
* Iron-rimmed spectacles
* Small ink-stained gloves
* Keys hanging at belt (symbolizing control over supply)

	Environmental Presence :
* Constantly seen inspecting stalls, storage doors, or talking to vendors
* Often stands near notice boards and stock crates

---

		Core Role in Stormreach Watch : 

	The Store Manager governs all economic flow within "Stormreach Watch", functioning as a system-level NPC, not a traditional vendor.

	He does not sell items directly — he controls :
* What is available
* In what quantity
* At what condition
* Under what morale state

---

	Mechanics — Economic Control System

This NPC introduces the "Village Economic State System", tied directly to Morale and Invasions.

---

	1. Stock Regulation : 

	The Store Manager dynamically adjusts :
* Vendor inventories
* Restock rates
* Item tier availability
* Scarcity or surplus states

	Stock is affected by :
✔ Village Morale
✔ Invasion success/failure
✔ Player participation
✔ Caravan Events
✔ Stormreach Supply Run outcome

---

	2. Morale-Based Shop Behavior : 

| Morale State | Effect on Shops                            |
| ------------ | ------------------------------------------ |
| Calm         | Full stock, normal prices                  |
| Alert        | Reduced restock, slight price increase     |
| Besieged     | Limited stock, defensive items prioritized |
| Broken       | Shops closed, no trade possible            |

✔ During "Broken", the Store Manager triggers forced closures
✔ Vendors become unavailable except for emergency NPCs

---

	3. Emergency Economic Overrides : 

	During invasions:
* Defensive consumables are prioritized
* Luxury and cosmetic items temporarily removed
* Fortification materials become more common
* Balloon payloads and ballista recharges appear faster

	If village falls:
* All non-essential vendors disabled
* Only:
	* Stormreach Token Vendor
	* Trap Trainer
	* Military NPCs
		remain operational

---

	4. Player Interaction Effects : 

	Players indirectly affect the Store Manager by :
* Defending caravans
* Completing Supply Run events
* Participating in village defense
* Sabotaging or aiding smugglers

	High player contribution results in :
✔ Better stock
✔ Faster restock
✔ Temporary discounts
✔ Rare items appearing

	Neglect results in :
✖ Shortages
✖ Higher prices
✖ Shop closures
✖ Delayed upgrades

---

	5. Anti-Exploit / Stability Mechanics : 

	The Store Manager ensures :
* No infinite restock loops
* No stock flooding
* Controlled rare item appearance
* Fair economy pacing

	He is essentially the economic governor AI of "Stormreach Watch".

---

	6. Smuggler Interaction (Mr. B & Mr. D / Artefill) : 

	The Store Manager :
* Is actively aware of smuggling activity
* Can :
	* Restrict goods
	* Trigger patrol investigations
	* Temporarily ban certain items
* Occasionally allows controlled black-market leaks during Besieged state to avoid total collapse

	This creates :
✔ Tension
✔ Dynamic stock shifts
✔ Emergent player decisions

