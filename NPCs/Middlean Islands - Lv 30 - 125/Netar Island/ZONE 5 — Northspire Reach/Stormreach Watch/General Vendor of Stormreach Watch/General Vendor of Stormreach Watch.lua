			General Vendor of Stormreach Watch

---

	Basic Information : 

* Name : General Vendor
* Title : Quarter Supplier
* Role : Basic Supply NPC
* NPC Type : Civilian Merchant / Survival Vendor
* Faction : Stormreach Watch
* Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach / Stormreach Watch
	* Exact Placement : Small open stall near central plaza, close to storage caverns

* Disposition : Practical, cautious, resilient
* PvP Status : Not PvP-flagged
* Skillable : No
* Can Die in Invasions : ✔ Yes
* Reputation : None
* Questgiver : No

---

		Visual Identity : 

	Appearance :
* Thick wool cloak and layered furs
* Leather apron full of pockets
* Wears gloves even indoors
* Tool belt with hammer, knife, small hatchet
* Often leaning on a crate or sorting goods

	Environmental Presence :
* Always near stacked barrels, sacks, or crates
* During storms, pulls tarp over stall
* During invasions, visibly panicked but stays unless fleeing is triggered

---

		Core Role in Stormreach Watch

	The General Vendor is the foundation supply NPC, ensuring :
✔ Food access
✔ Tools for crafting and survival
✔ Entry-level gear
✔ Daily utility for players

	This NPC ensures Stormreach Watch is playable even when under pressure.

---

		Mechanics — General Vendor Systems

---

	1. Survival Supply Role

	The General Vendor provides:
* Food and drink
* Basic tools (knives, hammers, ropes, lanterns)
* Low-tier weapons and armor
* Repair kits
* Simple consumables

	He is the only guaranteed access point for survival goods when conditions worsen.

---

	2. Morale-Based Stock Limitation

| Village State | Vendor Behavior                              |
| ------------- | -------------------------------------------- |
| Calm          | Full basic stock available                   |
| Alert         | Reduced restock, defensive items prioritized |
| Besieged      | Only survival & defensive goods sold         |
| Broken        | Vendor disappears or stall closed            |

✔ Controlled by Store Manager
✔ Stock reacts dynamically to invasions

---

	3. Invasion Vulnerability

	During invasions :
* He may:
	* Be killed
	* Flee into storage caverns
	* Or barricade himself inside a nearby shack
* If killed:
	* He respawns after 1–2 hours real-time
	* With limited stock until village stabilizes

	This adds :
✔ Stakes
✔ Real consequence
✔ Living settlement feeling

---

	4. Tool-Based Combat (Last Resort)

	While not skillable and not PvP flagged :

If attacked by enemies:
* He uses tools as improvised weapons:
	* Hammer swings (low damage)
	* Throwing small tools
	* Shielding behind crates
* Cannot defeat elites
* Only meant for :
	✔ Survival flavor
	✔ Desperation defense
	✔ Visual storytelling

---

	5. Civilian Panic Interaction

If civilian panic mechanics trigger :
* The General Vendor :
	* Abandons stall
	* Attempts to flee toward storage caverns or lodge
	* Leaves behind scattered goods (non-loot, visual only)

	This reinforces:
✔ Vulnerability of non-military NPCs
✔ Importance of defense

---

	6. Anti-Exploit Safeguards

* Cannot be farmed for loot
* Does not drop items
* Does not respawn instantly
* Stock cannot be accessed while dead or absent

