			Guardian of the Lodge

	Role : Tavern Enforcer / Lore Witness
	
	Functions :
* Patrols tavern interior, interacts with visitors
* Prevents disturbances from overly intoxicated players
* Recognizes spectral phenomena or strange events in tavern / village
* Can trigger flavor interactions with drunk players (push mechanic)

	Appearance & Behavior :

* Middle-aged, grizzled veteran in weathered armor and cloak
* Carries a staff or short halberd for show, not combat
* Moves slowly around tavern; pauses near busy areas
* Notices spectral phenomena from LCWM or invasion aftermath

	Mechanics :

1. Intoxicated Player Push
	* Trigger : Player consumes 3+ beverages in a short period (configurable)
	* Effect : Guardian walks toward player, initiates a series of short pushes toward the door
	* Outcome : Player ends up at tavern entrance; temporarily blocked from re-entering (cooldown ~2-5 min)
	* UI Feedback : Warning message appears: "Guardian of the Lodge says: ‘You’ve had enough for tonight, friend… time to step outside."
	* Flavor : Adds realism and tavern dynamics without PvP

2. Spectral Awareness Interaction
	* Trigger : Player casts fire, light, or triggers LCWM inside tavern
	* Effect : Guardian comments, points, or reacts to phenomena
	* Flavor : Adds story depth, encourages exploration

3. Ambient Tavern Behavior
	* Wanders near the hearth, near bar, near tables
	* Observes player actions and NPC activities
	* Occasionally converses with the Tavern Owner or Bard

	Quotes :
* On player being too loud or tipsy:
	* "Careful there, friend, this isn’t a battlefield!"
	* "I’m not here to fight you, just… out you go, for your own good."
	* "Don’t make me escort you… step lively now!"

* On spectral phenomena or strange activity:
	* "I’ve seen worse on the front lines, but that… that’s uncanny."
	* "Hmph. Reminds me of the old crusader nights… the spirits still wander."

* Idle / tavern ambient:
	* "The ale flows strong, but the tavern still stands."
	* "A quiet night… best for keeping trouble out."
	* "I’ve faced armies, but not the tales these walls could tell."

	Combat / PvP Flag :
* Not skillable, cannot be killed by players normally
* Push mechanic is flavor-based, not damaging
* Can observe LCWM or invasion effects without engaging

