			Tharic Hearthwarden

	Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach\Stormreach Watch — Tavern

	Role : Tavern Owner / Local Figure

	Functions :
* Provides lore and gossip about the island and local events
* Keeps track of travelers, rumors, and incoming dangers
* Cannot be PvP-targeted or skillable
* Tavern door prevents enemies (including crusaders) from entering during siege
* Provides social and narrative interactions

	Appearance :
* Human male, early 60s, rugged, weathered face
* Wears simple but sturdy clothes, apron with faint burn marks
* Often polishing mugs or serving drinks
* Slight limp, a scar across left cheek

	Behavior / AI :
* Wanders within tavern, interacts with players via dialogue
* Delivers lore, rumors, and tips about hidden areas or upcoming events
* Reacts dynamically to the village morale and events (e.g., worried during invasion alerts)
* Keeps track of player reputation and can adjust dialogue based on player REP with the Order of Restoration

	Mechanics :
* Tavern is PvP-safe zone; interactions cannot be interrupted by combat
* NPC cannot be attacked or killed outside events
* During siege, tavern door is magically reinforced; NPC and patrons safe
* Offers optional dialogue prompts to players for lore stories, rumors, or minor flavor quests

	Quotes / Dialogue Examples :

* "Ah, another traveler! Sit, sit… the hearth is warm, and my ale is colder than the Windrazor Pass."
* "Did you hear about the last scouting party? Ha! Barely made it back before the first frostbite set in."
* "Those cursed crusaders… can’t trust a day without them stirring trouble."
* "You look like you’ve seen a fight or two. Care to swap stories?"
* "I know every path, every hidden cave, and most importantly… who to trust out there."
* "The children say they saw ghosts near the old watchtower… don’t tell them I told you."
* "Ah, nothing like a hot stew and a good tale on a stormy night."
* "If you’re looking for the Skelvar Windrider rumors… that’s not a tale for the faint-hearted."
* "Some people come in here thinking they can drink their way to wisdom… I prefer the quiet observation."
* "The frostbite this morning was worse than a crusader’s cold glare."
* "Rumor has it, the field marshal himself has new plans… but shh, don’t repeat that here."
* "I’ve seen soldiers come and go… the trick is knowing when to speak and when to stay silent."
* "Even in the calm, keep your eyes open. The wind carries secrets, not just snow."
* "Ale’s on the house if you’ve survived the north ledges… and lived to tell the tale!"
* "My wife always says I talk too much… I say, the world is worth talking about."

	Emotes :
* Polishing mugs
* Serving drinks
* Gesturing with hands while telling a story
* Nodding to acknowledge player greetings
* Shrugging or sighing when rumors of danger come up

	Persistence :
* Always present inside tavern
* Dialogue evolves as players complete quests, reputation changes, or village events occur

