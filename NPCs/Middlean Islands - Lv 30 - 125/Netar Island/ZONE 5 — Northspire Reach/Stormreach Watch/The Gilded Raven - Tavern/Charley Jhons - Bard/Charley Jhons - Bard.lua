			Charley Jhons - Bard

	Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach → Stormreach Watch → Tavern stage

	Role : Morale & Flavor NPC
	
	Functions :
* Buffs village morale slightly when Calm or Alert (stackable with Beacon/Vigil buffs)
* Performs songs related to Crusaders and island lore
* Adjusts behavior and tone according to village state (Calm / Alert / Besieged / Broken)
* Sings, laughs, and interacts with Lodge Owner and Owner’s Wife
* Provides ambient tavern life, encouraging players to stay, listen, and interact

	Appearance & Behavior :
* Middle-aged male, flamboyant tunic with soft leather boots
* Carries a lute or harp; occasionally a small drum
* Often seated on a tavern platform or near the hearth
* Uses gestures and emotes while performing
* Not skillable, not PvP flagged

	Mechanics :

1. Morale Buffs
	* Provides "Inspiring Melody" when Calm/Alert
		* Effect: Minor temporary boost to nearby NPCs’ attack and defense during normal village state
		* Duration: Short, refreshed periodically while the bard is performing

2. Reactive Performance
	* Sings different songs depending on village state:
		* Calm : Cheerful tunes, increases morale, minor healing for NPCs
		* Alert : Encouraging war songs, alerting nearby players to Crusader threats
		* Besieged : Intense, dramatic tunes that bolster NPC defense for short periods
		* Broken : Sings sorrowful tunes; no buffs, flavor only

3. Ambient Interaction
	* Laughs and chats with the Lodge Owner and Owner’s Wife
	* Reacts to player actions (approach, applaud, drink, or cheer)
	* May comment on invasions, spectral events, or visible Crusaders

4. Song Mechanics 

   * "Ode of the Fallen"
		* Buff: Slight defense increase to nearby NPCs
		* Duration: Tactical (temporary, repeatable)

   * "March of the Watch"
		* Buff: Slight attack bonus to nearby NPCs
		* Duration: Tactical

	* "Ballad of Stormreach"
		* Flavor only; increases ambient morale for players
		* Duration: Passive while performing

	Quotes / Greetings :

* Greeting:
	* "Ah, travelers! Come, lend an ear, and perhaps some coin for a song?"
	* "If the Crusaders march, I’ll march with my lute!"
* Idle:
	* "A toast to those who survived another night…"
	* "Even the stones whisper of the Red-Robe Banshee Queen"
* Event-specific:
	* "Hurry! The village needs its defenders!" (Alert/Besieged)
	* "The tavern is quiet tonight… for now…" (Calm)

	Notes :
* Player interaction limited to emotes or dialogue; cannot be attacked
* Works with Owner, Owner’s Wife, and Guardian for tavern mechanics
* Tavern remains a safe zone for PvP; Bard reinforces flavor and minor village bonuses

