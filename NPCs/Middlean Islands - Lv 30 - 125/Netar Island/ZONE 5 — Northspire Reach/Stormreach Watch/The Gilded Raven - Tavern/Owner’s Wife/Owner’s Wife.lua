			Owner’s Wife

	Role : Lore Conduit / Storyteller
	
	Functions :
* Shares history of the Crusaders and island myths
* Provides emotional depth and context for the village and invasions
* Can give hints about hidden mechanics (LCWM, spectral events) without being a quest-giver
* Interacts with players in tavern, reacts to invasion states

	Appearance & Behavior :

* Mid-40s, wearing warm practical clothing with subtle embellishments from the old Order
* Gentle demeanor but strong presence; often seen near the hearth
* Talks to visitors, occasionally wringing hands or gesturing to illustrate story points
* Not skillable, not PvP flagged

	Mechanics :

1. Lore Interaction
	* Triggered by proximity or player interaction
	* Offers 3 different stories, selectable by dialogue wheel
	* Stories provide flavor and sometimes hint at mechanics or hidden locations

2. Spectral / Crusader References
	* Recognizes the arrival of elite or spectral enemies in the village
	* Comments dynamically on the ongoing invasion

3. Ambient Dialogue
	* Idle comments on the village, tavern, and NPCs
	* Reacts to invasion, panic, or player actions

	Quotes :

* On Crusaders:
	* “The Order of Restoration… they were once proud, now shadows haunt their memory.”
	* “Even in the quiet, you can hear their echoes along the cliffs.”

* On village life:
	* “Stormreach survives because people care, even when fear grips the streets.”
	* “Sometimes I wish the winds would carry all sorrow away.”

* Idle tavern chatter:
	* “Ah, adventurers… every one of you brings a new story here.”
	* “The hearth keeps us warm, but memories keep us wiser.”

