			Owner’s Wife — Lore Stories

	Story 1: The Fall of the Red-Robe Banshee Queen

> "Long ago, the Red-Robe Banshee Queen led the northern Crusader battalion. She was ruthless, but cunning… when the curse took hold, she could not rest. Her spirit now rises only at the worst storms, and the villagers say she whispers the fate of those who fight alone. I once saw her shadow pass through the village square… I shudder still."

	Story 2: The Tragic Crusader Children

> "There was a small company of Crusaders, young recruits barely old enough to hold a sword. When the plague of the undead struck, they were trapped in the chapel atop the cliffs. Some say you can still see their lanterns flickering along the path at night. I warn visitors… do not follow them, for they do not lead anywhere but sorrow."

	Story 3: Mystic Stones and the Island’s Secrets

> "The Mystic Stones… oh, those strange petrified crusader shapes scattered across the cliffs. No one knows why they stand frozen, as if the island itself punished them. Some brave souls claim they can summon a spectral guide if they arrange them correctly. I dare not try… but I tell these tales to remind you that this island holds more than just wind and snow. It holds memory."

