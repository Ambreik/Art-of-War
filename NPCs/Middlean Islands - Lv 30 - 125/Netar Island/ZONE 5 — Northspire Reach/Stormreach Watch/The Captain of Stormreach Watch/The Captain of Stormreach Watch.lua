			The Captain of Stormreach Watch

---

	Name : "Captain Aldric Stormward" - "Commander of Stormreach Watch"

---

	Type : Elite Military NPC / Skillable / Combat-Capable / Reputation-Linked / PvP-Enabled

---

	Faction : "Order of Restoration" (Allied) / Stormreach Watch (Local Command)

---

	Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach\Stormreach Watch – Central Plaza : Near the main beacon fire and overlooking the primary defensive chokepoint.



---

	Role : 
* Military Commander & Defensive Anchor NPC
	Oversees all military operations in Stormreach Watch and acts as the highest-ranking authority within the village. He embodies discipline, resilience, and
tactical mastery, serving both as a symbol of hope and a living bulwark against Crusader invasions.

---

	Appearance : 
* Tall, broad-shouldered man in his early 50s
* Weathered face with deep battle scars and a stern gaze
* Wears full bronze command armor, engraved with the sigil of the Order of Restoration
* Cape torn at the edges from past campaigns
* Always stands upright, hands resting on his weapon, surveying the village
* Subtle golden glow around armor seams during invasions

---

	Weapon Suit : 

* Primary Weapon : "Stormward’s Halberd of the Bronze Oath"
	* A long bronze halberd with a wide crescent blade
	* Reinforced shaft etched with restoration runes
	* Functions as both:
		* Heavy cleaving weapon
		* Focus conduit for AoE skills and healing waves

* Secondary : "Command Shield – “Aegis of the Watch”"
	* Large bronze shield
	* Used during defensive stances and protection phases
	* Emits shockwave pulses when blocking Crusader elites

---

	Combat Profile : 

* Combat Style
	* Hybrid : AoE Vanguard + Defensive Healer
	
	Excels at :
* Area denial
* Sustained frontline presence
* Supporting allied NPCs and players during invasions

---

		Core Abilities

	1. "Bronze Tempest"
Wide halberd sweep hitting all enemies in a cone
Applies: Armor Shred + Minor Knockback

	2. "Aegis Pulse"
Emits a radial shield wave :
	* Grants temporary shields to nearby allies
	* Removes minor fear effects

	3. "Restoration Surge"
AoE healing wave centered on himself : 
	* Prioritizes NPC defenders first
	* Scales stronger while invasion is active

	4. "Commander’s Stand"
Temporary state:
	* Increases defense of nearby NPCs
	* Boosts their damage and morale
	* Visual: golden aura and glowing runes

---

	PvP Interaction : 
* Can be targeted for PvP
* Extremely dangerous to most players
* Will easily defeat low–mid level players
* Only realistically defeatable by:
	* High-level players
	* Players accompanied by a "Guardian"

	If attacked:
* Does not chase outside Stormreach bounds
* Will not kill civilians
* Will prioritize protecting NPC defenders over finishing players

---

	Reputation Integration

Helping Stormreach Watch during invasions :
✔ Increases "Reputation with the Order of Restoration"
✔ Contributes globally to the Order’s standing
✔ Unlocks future chain content tied to Field Marshal

---

	Quest & Chain Integration : 
* Does NOT offer quests directly
* Is a major active node in the quest chain initiated by:
	"Field Marshal – Leader of the Order of Restoration"
* Presence and survival in invasion events dynamically affects later quest stages

---

	Behavior States

	1. Calm
* Observes patrols
* Engages players with greetings
* Occasionally drills soldiers

	2. Alert
* Issues commands
* Moves between defensive points
* Gives rally speeches

	3. Besieged
* Constantly active in combat
* Uses Restoration Surge frequently
* Patrols key chokepoints

	4. Broken
* Fights relentlessly
* Will personally defend civilians and beacon fires
* Becomes one of the last NPCs standing

