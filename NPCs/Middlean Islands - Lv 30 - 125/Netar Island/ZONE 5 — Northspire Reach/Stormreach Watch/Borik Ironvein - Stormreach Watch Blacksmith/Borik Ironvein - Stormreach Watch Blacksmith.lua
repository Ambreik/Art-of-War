			Borik Ironvein - Stormreach Watch Blacksmith

---

	Basic Information : 

* Name : Borik Ironvein
* Title : Stormreach Blacksmith
* Role : Repairs & Gear Maintenance
* NPC Type : Civilian Artisan / Fortification Contributor
* Faction : Stormreach Watch
* Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach / Stormreach Watch
* Exact Placement : Small stone forge near bulwarks and storage caverns

* Disposition : Gruff, practical, fiercely loyal to Stormreach
* PvP Status : Not PvP-flagged
* Skillable : No
* Can Die in Invasions : ✔ Yes
* Reputation : None
* Questgiver : No

---

		Visual Identity

	Appearance :
* Broad-shouldered, muscular
* Heavy leather apron over chainmail
* Burn-scarred gloves
* Beard braided with metal rings
* Hammer larger than average, visibly worn

	Forge Setup :
* Constant fire glow
* Bellows pumping smoke into the cold air
* Anvil marked with crusader-era runes
* Piles of scrap metal and weapon parts

---

		Core Role in Stormreach Watch

	Borik ensures:
✔ Gear maintenance
✔ Emergency equipment
✔ Fortification crafting
✔ Last-line survival readiness

	He is a keystone NPC for long sieges and reinforces the frontier survival identity.

---

		Mechanics — Blacksmith Systems

---

	1. Gear Repair System :

	Borik offers:
* Standard durability repairs
* Partial repairs at reduced cost during Besieged state
* Emergency repairs during invasion (limited to weapons & armor only)

	Repair behavior by morale:

| Village State | Repair Availability  |
| ------------- | -------------------- |
| Calm          | Full repair services |
| Alert         | Increased cost       |
| Besieged      | Reduced scope        |
| Broken        | No service           |

---

	2. Fortification Crafting Contributor

	Borik passively contributes to:
* Bulwark reinforcement
* Ballista repair
* Beacon post durability

	If Borik dies during invasion:
* Fortification repair speed reduced by 40%
* Ballista repairs disabled until respawn

---

	Hidden Mechanic — "Hammer of Stormreach"
*(LCWM / Event-Conditional Legendary Mechanic)*

---

	Activation Conditions : 

	Borik’s special mechanic becomes available ONLY if :
✔ An invasion is active
✔ Player is flagged as a defender
✔ Player has contributed meaningfully (kills, repairs, lookout, etc.)
✔ Borik is alive
✔ Player speaks to Borik during invasion

	When active:

* Borik’s hammer emits a greenish glow, visible only to eligible players.

---

	Effect : 
	
	Borik will:

> Upgrade ONE item for FREE and with 100% success chance

	Applicable items:

* Weapons or armor
* Level range: 1–100
* Item types:
	✔ Adventure Gear
	✔ Ascended Gear

	Restrictions:
* Only ONE item per invasion per player
* No stacking or repeat use
* Cannot be saved or delayed
* Must be done during the invasion window

---

	Narrative Justification : 

	Borik reforges the item :

> “For Stormreach… and for those who stood when others ran.”

	This is a morale-based blessing, not a crafting mechanic.

---

	Anti-Exploit Safeguards : 
* Player must be flagged as defender
* Must have active participation score
* Only available during active invasion
* One use per event
* No trading upgraded item during invasion window

---

	4. Invasion Behavior : 

	During invasions:
* Borik:
	* Continues working at forge
	* Shouts warnings
	* Repairs NPC gear visually
* Will NOT flee unless village is Broken
* If killed:
	* Forge goes dark
	* Hidden mechanic disabled
	* Respawns after 1–2 hours

---

	5. Tool-Based Combat

	Borik defends himself with :
* Heavy hammer strikes (slow, strong)
* Knockback effects
* No elite combat ability

	Purely for :
✔ Flavor
✔ Visual storytelling
✔ Desperation defense

