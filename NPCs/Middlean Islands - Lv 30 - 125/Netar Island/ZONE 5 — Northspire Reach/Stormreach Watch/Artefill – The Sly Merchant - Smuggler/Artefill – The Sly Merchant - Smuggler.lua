			Artefill – The Sly Merchant 

* Location : Stormreach Watch — Hidden Bricked-Over Shaft / Secret Chamber
* Role : Hidden Smuggler / Rare Trader

---

	Appearance & Behavior : 
* Small, wiry humanoid, cloaked in dark leathers and a hood, face partially masked.
* Always carries a small satchel of rare goods; dagger strapped to belt.
* Moves cautiously, rarely leaving the chamber.
* Skillable; can use self-defense skills if threatened, including minor AoE knockback and smoke screen.
* Appears only via LCWM mechanism (bricked-over shaft activated).
* Independent of Mr. B & Mr. D; does not interact with casual players.

---

	NPC Functions : 
* Rare Item Trader : Sells high-tier consumables, tools, and smuggler-only equipment.
* Faction Integrity : Attends invasions only for self-protection, avoids aiding NPCs or players unless they belong to his organization.
* LCWM Activation : Only accessible when bricked shaft opens based on hidden sequence triggers.

---

	Mechanics : 

* Access Mechanic :
	* Activated via bricked-over shaft sequence (Storm + Invasion active + Beacon fires + Inscription interaction).
	* Chamber opens for a limited time (~30 minutes per activation).

* Combat Mechanic :
	* Can attack intruders using:
		* Smoke Screen → Area blind for 3 seconds
		* Quick Daggers → Fast melee attacks
		* Minor AoE Knockback → 2-3 m radius
	* Non-PvP flagged; cannot be killed outside events triggered by player invasions.
* Trading Mechanic :
	* Only smuggler/outlaw flagged players can trade.
	* Sells:
		* Rare smuggler consumables (anti-detection items, potions)
		* Rare crafting items
		* Mount components (high-tier, island-exclusive)
		* Special trinkets for stealth or pickpocket enhancements
	* Stock refresh: every 12 in-game hours.

* Event Interaction :
	* Attends invasion events but only defends self or chamber.
	* Ignores regular NPCs and non-organization players.

* Hidden Lore / Flavor :
	* Artefill may give rare hints about crusader activity or hidden caches if approached carefully.
	* Speaks cryptically; rewards observation and repeated visits.

---

	Quotes & Dialogue : 

* Greeting :
	* "Ah, a discerning eye. You don’t wander here by accident, do you?"
	* "Quiet… lest you disturb the wrong ears."

* Suspicious / Threatened :
	* "Back away, or you’ll regret meddling in matters above your rank."
	* "A little too close… I prefer my hands clean."

* Trading / Smuggling Interaction :
	* "You’re in the right place… if you know how to handle it."
	* "I don’t deal in trifles. Only the bold or desperate may purchase."

---

	Skills (for self-defense) : 

* Quick Daggers : Single-target melee attack; moderate damage + chance to slow movement.
* Smoke Screen : Area effect around Artefill; blinds enemies for 3 seconds.
* Minor Knockback : 2-3 m radius; pushes intruders away from chamber entrance.

---

	Notes : 
* Does not trigger REP with any faction.
* Cannot be flagged for PvP outside invasion events.
* Only interacts with smuggler/allied players; otherwise hostile if detected intruding.

