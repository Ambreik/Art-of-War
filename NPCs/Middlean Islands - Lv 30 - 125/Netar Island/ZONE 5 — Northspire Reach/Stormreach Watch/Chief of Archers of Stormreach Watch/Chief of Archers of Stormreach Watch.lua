			Chief of Archers of Stormreach Watch

---

	Basic Information : 

* Name : Arcturion Windeye
* Title : Chief of Archers, Stormreach Watch
* Race : Human
* Faction : Order of Restoration (Stormreach Garrison)
* Rank : Officer
* Location :  Middlean Islands → Netar Island → Zone 5 — Northspire Reach\Stormreach Watch – Watchtower of Stormreach Watch
* NPC Type : Military Commander (Ranged Specialist)
* Disposition : Disciplined, Observant, Calm
* PvP Status : Non-hostile (cannot be flagged for PvP)
* Questgiver : No
* Trainer : No
* Skillable : Yes

---

		Visual Identity : 

	Armor :
* Reinforced leather-and-bronze hybrid armor
* Fur-lined mantle for wind protection
* Stormreach crest stitched in silver thread
* Lightweight for mobility, not ceremonial

	Weapons :
* Primary: Longbow “Gale-Seeker” (composite bow, stormsteel limbs)
* Secondary: Short saber (last resort)
* Quiver: Always visible, never empty visually

	Distinguishing Features :
* Pale, weathered eyes
* Scar across left brow
* Keeps one eye closed slightly when scanning horizons (habitual aiming stance)

---

		Role in the Village : 

* Arcturion Windeye commands all ranged defense units in Stormreach Watch.
* He does not lead troops directly into melee, but instead shapes the battlefield through:
	* Positioning
	* Fire discipline
	* Wind-aware ranged tactics

	He embodies the village’s first line of detection and ranged response.

---

		Mechanics : 

	1. Ranged Command Authority

Arcturion passively affects all archer NPCs in Stormreach Watch.

	Effects :
* Increases:
	* Archer accuracy
	* Reaction time to enemy presence
	* Target prioritization (casters, elites, siege units)
* If Arcturion is incapacitated or absent:
	* Archer NPCs lose bonus accuracy
	* Slower response during invasions

	This makes him a strategic node NPC rather than just flavor.

---

	2. Wind-Aware Combat Logic

Arcturion is directly linked to your wind system.

	Mechanical Effects :

* His commands dynamically adapt to:
	* Wind direction
	* Wind intensity
	* Storm state
* During high wind:
	* He orders volley arcs adjusted
	* NPC archers reduce long-range fire and favor mid-range barrages
* During calm:
	* Full-range precision firing enabled

	Extension Hook :
Later, this integrates directly into your Wind System (accuracy, projectile drift, cast interference).

---

	3. Far-Sight Detection Sense

Arcturion possesses a unique perception system.

	Mechanic: "Windeye Sense"
* Detects hostile units at a much longer radius than standard NPCs
* Especially sensitive to:
	* Large troop movement
	* Heavy armor
	* Undead crusaders
* Triggers:
	* Early alert to the Watchtower
	* Enables pre-invasion preparation bonuses
	* May activate the village alarm earlier than normal

	Special :
This detection is not visual-only — it is described as:

> “Reading the wind, not just the land.”

---

	4. Sparring Duel System (Friendly Duel Mechanic)

Players may challenge Arcturion to a non-lethal duel.

	Conditions :
* Only outside invasion events
* No death possible
* No durability loss

	Duel Characteristics :
* Ranged-only combat
* Player cannot close into melee easily (Arcturion uses evasive footwork)
* Teaches players:
	* Wind compensation
	* Leading targets
	* Ranged positioning

	Rewards :
* Small ranged skill bonus for limited time
* Stormreach reputation
* Occasional training tokens (non-currency)

	Design Purpose :
This NPC functions as a ranged skill benchmark, not a loot source.

---

	5. Invasion Phase Behavior

	During Crusader Invasions :
* Remains in Watchtower or elevated position
* Issues ranged commands
* Buffs archers instead of directly engaging
* Will only fight personally if:
	* Watchtower is breached
	* Or archer ranks are wiped out

	If defeated:
* Archer NPCs:
	* Lose formation discipline
	* Fire randomly for a short period
* Defense becomes noticeably weaker

---

	6. Player Interaction Restrictions

* Cannot be exploited for:
	* Aggro pulling
	* Body blocking
	* Farming
* Does not follow players
* Will not assist players outside village combat boundaries

---

		AI Behavior : 

	Default State :
* Scanning horizon
* Occasionally adjusting archer positions
* Practicing aim animations

	Combat State :
* Never reckless
* Repositions constantly
* Prioritizes elite and siege units

	Village Calm State :
* Trains archers
* Observes weather
* Interacts minimally

