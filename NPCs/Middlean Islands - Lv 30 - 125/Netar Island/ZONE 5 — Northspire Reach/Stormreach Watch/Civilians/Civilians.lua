			Civilians

* Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach → Stormreach Watch
* Role : Casual Population
* Functions :
	* Victims during invasions
	* Affect village morale
	* Respawn hours later
	* Provide ambient life

	Appearance :
* Mixed gender and ages, wearing fur-lined simple clothes, boots, scarves.
* Common props: baskets, buckets, small tools, bags.
* Hairstyles and clothing vary slightly to avoid uniformity.

	Behavior / AI :
* Roam the village plaza, stairways, and near dwellings.
* Stop to interact with local vendors (token vendor, general vendor, blacksmith).
* Panicked when invasion starts, flee toward safe zones or shelters.
* Interact with environmental mechanics: running near fires, stairway barricades, etc.
* React to weather (wind / frost) with minor debuffs: slower movement or seeking shelter.

	Mechanics :
* Invasion Victims : Civilians can be killed by invading crusaders or environmental hazards.
	* Casualties increase village “Broken” state faster.
	* Player or NPC protection of civilians increases morale and rewards.

* Respawn : Civilians respawn 3-6 in-game hours after death, randomized to prevent predictable repopulation.

* Morale Influence :
	* Calm: Civilians move freely, interact normally.
	* Alert: Civilians move cautiously, avoid danger zones.
	* Besieged: Civilians hide indoors, some stop commerce.
	* Broken: Majority flee village or become temporarily unavailable.

* Environmental Interaction :
	* React to beacon fires and torch posts (gather around for warmth / safety).
	* React to wind/frost mechanics: slowed movement in strong gusts or freezing zones.

* Loot / Interaction :
	* Civilians cannot be targeted for loot, but may give flavor emotes like waving, pointing, or begging.
	* Optional hidden flavor: minor buffs from helping civilians (e.g., “Morale Boost +5%” for 10 minutes to players nearby).

	Combat / PvP :
* Civilians cannot attack, not flagged for PvP, and have minimal durability.
* Death only impacts invasion outcomes and morale systems.

	Names / Variations :
* Generic names assigned at spawn from a pool (e.g., Elara, Thoren, Miris, Joren, Livia, Corin, etc.)
* No permanent tracking, randomly applied for each respawn.

