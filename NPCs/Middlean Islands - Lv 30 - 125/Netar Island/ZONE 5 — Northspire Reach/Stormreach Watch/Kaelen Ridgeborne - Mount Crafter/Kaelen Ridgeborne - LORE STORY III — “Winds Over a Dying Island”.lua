			Kaelen Ridgeborne - LORE STORY III — “Winds Over a Dying Island”

* NPC : Kaelen Ridgeborne
* Type : Invasion Lore / Hidden Threats
* Availability : After Story II or during/after a large-scale invasion
* Delivery Style : Urgent, confidential
* Tone : Ominous, strategic, restrained panic

---

> Kaelen Ridgeborne says :

You see…
Invasions here are not like elsewhere.

When the horns sound and the bells scream, most hide behind walls.
I do not.

I take to the skies.

From above, Stormreach Watch looks like a spark struggling against an ocean of dark.
But what I saw… was not just crusaders.

Not just mindless undead obeying old commands.

There is something else now.

A second spirit.

Not like the Red-Robe Banshee Queen the veterans whisper about.
This one is different.

She moves like the wind itself —
A woman, wrapped in dark green robes, her veil long and torn like storm-clouds trailing across the sky.
Where she passes, the air twists.
The winds scream.

I saw her from above once.
And she saw me.

She did not chase as a beast would.
She reached.

The air around me pulled, violently — as if the sky itself tried to drag me down to her grasp.
Had my bird not been faster…
I would not be speaking to you now.

And behind her…

The crusaders followed.

Not wandering.
Not scattered.

Thrown forward like weapons.

That is when I knew:
They are no longer merely remnants of a dead army.

They are being directed.

And that is not all.

In recent weeks, strange objects began appearing across the island.
Totems… stones… statues shaped like crusaders frozen mid-march.

The scholars on the mainland would likely call them "Mystic Stones".
Anchors, perhaps.
Or markers.

Whatever they are, they do not belong to the past.
They belong to what is coming.

I have not yet sent this to the "Field Marshal".

Not because I doubt it.

But because once it is written…
It becomes real.

Tell me, adventurer…
When the wind itself turns against us…

Who do you think is truly besieged?

