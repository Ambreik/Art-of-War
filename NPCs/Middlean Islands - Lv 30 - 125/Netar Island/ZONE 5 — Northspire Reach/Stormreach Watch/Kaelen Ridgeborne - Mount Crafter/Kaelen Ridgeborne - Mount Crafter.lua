			Kaelen Ridgeborne

---

	Basic Information : 

* Name : Kaelen Ridgeborne
* Title : Windrider Chronicler
* Role : Crusader Historian / Mount Crafter
* NPC Type : Lore Anchor / Specialized Crafter
* Faction : Neutral (Aligned with Stormreach Watch)
* Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach / Stormreach Watch
* Exact Placement : Small elevated platform near the village edge, overlooking the valley and wind currents

* Disposition : Calm, observant, elusive
* PvP Status : Can be PvP-flagged
* Skillable : ✔ Yes
* Reputation : None
* Questgiver : ✔ Yes (Lore Quest Chain — Crusaders)

---

		Visual Identity

	Appearance :
* Tall, lean build
* Long coat reinforced with wind-leather and light plates
* Short, thin blade at his side (rapier-like)
* Goggles or wind-scarred eyes
* Carries scrolls and strapped tools for mount maintenance

	Mount Presence :
* His flying mount, "Skelvar Windrider", is often seen nearby or circling overhead
* Mount occasionally lands behind him during idle animations

---

		Core Role in Stormreach Watch

	Kaelen is the village’s :
✔ Living archive of Crusader history
✔ Gateway to Crusader lore
✔ Exclusive mount crafter
✔ Aerial world interaction NPC

	He bridges :

> Lore ↔ Crafting ↔ World Mobility ↔ Exploration

---

		Mechanics — Kaelen Ridgeborne Systems

---

	1. Crusader Lore Anchor

	Kaelen provides:
* Lore dialogue
* Fragmented historical insight
* Leads into:
	✔ Crusader downfall
	✔ Undead conversion
	✔ Banshee Queen legacy (indirectly)
	✔ The Tomb of Great Crusaders

	Accessed through:
* Progressive dialogue
* Small lore quests 
* Environmental hints tied to Zone 5 and dungeon

✔ He does NOT reveal full truths at once
✔ Encourages investigation and cross-zone exploration

---

	2. Specialized Mount Crafter

	Kaelen is the ONLY NPC able to craft:

* Mount: Skelvar Windrider
	* Type : Flying Mount
	* Use : Island-only flying (Netar Island system compliant)
	* Special Traits :
		* Can cross invisible barrier
		* Stable in extreme wind
		* Designed for long aerial routes

	Crafting Requirements:

* Rare materials (Crusader relics, wind cores, bones, etc.)
* Special food crafted by Kaelen
* Progression gated by lore quests

---

	3. Mount Sustenance Crafting

	Kaelen also crafts: "Skelvar Feed Types"

* Wind-Resistant Feed
* Long-Flight Rations
* Storm-Endurance Mix

	These affect:
✔ Mount stamina
✔ Flight duration
✔ Resistance to wind disruption

	Mount food is exclusive to Kaelen.

---

	4. PvP / Threat Evasion Mechanics

	Kaelen is NOT a fighter, but extremely hard to pin down.

	A. Windstep Escape (Teleport)

* Trigger : When attacked
* Effect :
	* Instantly teleports a short-to-medium distance away
	* Leaves behind a gust visual effect
	* Breaks combat lock
	* Cooldown-based

✔ Works on players and monsters

---

	B. Disabling Strike (Flavor Control Skill)

	Using his thin sword:

* Skill Name : "Windbind Thrust"
* Effect :
	* Knocks attacker back
	* Applies long stun:
		2–10 minutes (randomized or scaling)
	* Works on players and monsters
	* Purely flavor / narrative enforcement skill

✔ This is NOT a combat mechanic
✔ It exists to enforce “Do not harass this NPC”
✔ Prevents griefing without killing players

---

	5. Aerial Escape Mechanic

	If pursued repeatedly or heavily threatened:

	Kaelen may :
* Mount "Skelvar Windrider"
* Take off immediately
* Become inactive for several hours
* Resume later after flying cycle

	While airborne:
✔ Cannot be interacted with
✔ Cannot be attacked
✔ Cannot be accessed for crafting

---

	6. World Aerial Route Mechanic

	Kaelen periodically flies around "Netar Island" for up to 3 hours continuously.

	This flight :
* Has a fixed but complex route
* Passes over:
	✔ Northspire Wall
	✔ Zone boundaries
	✔ Dungeon gate
	✔ Wind corridors
* Can be followed by players in the air
* Acts as:
	✔ A moving world marker
	✔ A living navigation reference
	✔ A hidden lore carrier

	Players following him may:
* Discover hidden locations
* Trigger world hints

---

	7. Skillable but Non-Combat Focused

	Kaelen is skillable, but :
* Has low damage output
* Uses defensive/escape mechanics only
* Cannot be farmed or exploited

	Skillable here means :
✔ Can be targeted
✔ Can be interacted in combat terms
✔ But is NOT a progression combat target

