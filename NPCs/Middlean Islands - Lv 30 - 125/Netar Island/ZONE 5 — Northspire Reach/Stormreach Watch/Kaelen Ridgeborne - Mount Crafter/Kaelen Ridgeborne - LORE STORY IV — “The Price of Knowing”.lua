			Kaelen Ridgeborne - LORE STORY IV — “The Price of Knowing”

* NPC : Kaelen Ridgeborne
* Type : Interactive Lore / World-State Trigger
* Availability : After Story III
* Delivery Style : Conditional, choice-driven
* Tone : Personal, strategic, revealing
* Outcome : Triggers Kaelen’s flight behavior & Field Marshal report

---

> Kaelen Ridgeborne says :

You return again…

Either you truly care about this island…
or you simply enjoy listening to an old man speak of storms and ghosts.

Either way — I respect both.

There are things I have seen that do not belong in tavern whispers.
Movements.
Patterns.
Decisions made not by chance, but by will.

And such knowledge… rarely comes freely.

---

