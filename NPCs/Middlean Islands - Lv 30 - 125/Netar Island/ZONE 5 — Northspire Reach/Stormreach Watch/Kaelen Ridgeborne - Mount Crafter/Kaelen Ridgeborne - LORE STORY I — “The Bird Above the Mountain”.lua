			Kaelen Ridgeborne - LORE STORY I — “The Bird Above the Mountain”

* NPC : Kaelen Ridgeborne
* Type : Personal Memory / Mount Origin Lore
* Availability : After completing at least Quest 1 or Quest 2
* Delivery Style : Spoken, cinematic pacing
* Tone : Reflective, slightly regretful, adventurous

---

> Kaelen Ridgeborne says :

Ah… adventurer, come closer.
Let me tell you a story. You may find it interesting… or useful.
Depending on what your intentions are on this island.

When I first came here, I was not what you see now.
I was a young mount trainer, a novice breeder, barely trusted with anything more than stable work and wounded horses.
But now… heh… now I can tame almost anything.
Even those Crusaders, in a sense — though they still walk, trapped in strong memories and stronger curses.

Back then, when the army was still organizing this place, I was given responsibility for the mounts — horses, beasts, anything that carried a rider.
But during my travels across the island, I saw something that no ledger ever recorded.

High above the mountain, soaring as if it ruled the sky itself…
A bird-like creature.
Not a gryphon. Not a wyvern.
Something… purer.
Majestic in a way that made every other mount feel like a shadow.

The moment I saw it, I knew.
That was my true quest.

It took me two months just to find where it hid.
Two months of climbing cliffs, chasing winds, sleeping in caves, and avoiding Crusader patrols that roamed even back then.

When I finally found them…
There was not just one.

A mother… and three fledglings.

I won’t pretend I was brave enough to face that without preparation.
I contracted a few soldiers — well trained, disciplined — not because I feared battle…
but because I feared being caught unguarded, taken by surprise by something larger than me.

And yet… even with them…
Things went wrong.

I won’t tell you all the blood and chaos.
What matters is this:
I could save only one.

My bird.

We fled.
I ran, wounded, breathless, holding that creature while the mountain screamed behind us.

Sometimes… I think I was selfish.
I could have tried to save more.
Or all of them.

But I didn’t.

Now, I am the only one who has one of their kind.
Or… perhaps not.

You see, adventurer…
Sometimes I wonder if somewhere on this island…
Another survived.

