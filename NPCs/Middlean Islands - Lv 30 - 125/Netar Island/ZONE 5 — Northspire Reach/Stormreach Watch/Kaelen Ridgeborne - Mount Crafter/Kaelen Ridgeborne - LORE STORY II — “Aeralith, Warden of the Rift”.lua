			Kaelen Ridgeborne - LORE STORY II — “Aeralith, Warden of the Rift”

* NPC : Kaelen Ridgeborne
* Type : Secret Knowledge / Rare Boss Lore
* Availability : After completing Quest 3 or after Story I
* Delivery Style : Quiet, conspiratorial
* Tone : Dark, restrained, vengeful

---

> Kaelen Ridgeborne says :

You return again…
Either you enjoy my company, or you’re looking for something more than words.

Good.
Because I know more than I should — and far more than I report.

There is a place west of here, in the broken cracks of the mountain, in that wet, mist-bound region soldiers call "Vaelthorn Frontier".
Most believe the caves there are empty.
They are not.

The army whispers of a rare monster, a thing they named the "Rift Caverns Guardian".
But that is not its true name.

I have seen it with my own eyes.

It is called "Aeralith, Warden of the Rift".

An elemental beast, bound not to stone… but to the breath of the mountain itself.
Wind, crystal, and rage shaped into a will that does not forget.

It was that creature that destroyed the family of my bird.

I hunted it once.
And I defeated it.

But creatures like Aeralith do not die as others do.
They retreat… waiting for the world to forget them.

I would not allow that.

So I did something the army does not know.
I bound its return to a trial — not of strength, but of perception.

To summon Aeralith again, one must not simply enter the cavern.
They must understand it.

First, there is an illusion — a vision, flickering like broken glass.
Touch it.
Strike it.
Acknowledge it.

Then comes the water.
Three reflective pools, scattered like fragments of a shattered mirror.
You must cross them in the correct order — not guessed, but observed.
By how the light bends…
How the ripples whisper…
How the minerals glow brighter in one than the others.

Only when the sequence is right…
The cavern will fall silent for a breath.
And the light will converge.

Then…
Aeralith returns.

Fail, and the mountain says nothing.
It simply forgets you were ever there.

Now tell me, adventurer…

Would you be courageous enough to seek that place?
And if you are…
If you find anything that belonged to my bird’s family…

Bring it to me.

Perhaps then…
I will use my powers to call one for you.

Or perhaps not.

Hope, you see, is a dangerous thing in the Rift.

