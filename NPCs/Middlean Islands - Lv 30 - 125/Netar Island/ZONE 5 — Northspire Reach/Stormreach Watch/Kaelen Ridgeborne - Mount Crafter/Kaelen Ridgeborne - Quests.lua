			Kaelen Ridgeborne

* NPC : Kaelen Ridgeborne
* Location : Zone 5 — Stormreach Watch, Netar Island
* Quest Type : Multi-zone Utility & Lore Preparation Chain
* Progression Role : Prepares player for Crusader lore, Mount access, and aerial content

---

	QUEST 1 — "Fragments of the Fallen"

	Quest Type : Collection / World Immersion
	Description : Kaelen requests proof of the physical remnants of the Crusaders — weapons that once symbolized their discipline and now reflect their decay.

> "They carried blades once forged with purpose… bring me what remains of that purpose."

	Objectives : 
Collect: "4x Cracked Crusader Sword"

	Source

	Dropped by :
* Crusader-type enemies wielding sword-class weapons
* Found throughout Netar Island

	Drop Rate : Low

	This enforces:
* Extended combat exposure to Crusaders
* Environmental storytelling through repetition

	Rewards : None

	Purpose : 
✔ Introduces Crusader enemy identity
✔ Establishes Kaelen’s interest in their remnants
✔ Encourages organic grinding without power reward
✔ Serves as narrative credibility gate for later quests

---

	QUEST 2 — "Breath of the Skies"

	Quest Type : Elemental Gathering / World Exploration
	Description : Kaelen seeks pure Wind Essences — remnants of elemental life, essential for understanding the island’s aerial currents.

> "You cannot tame the sky without first listening to its breath."

	Objectives : 
Collect : 5x Wind Essence

	Source

	Dropped by :
* Wind-elemental monsters
* Found throughout Netar Island
  (Cliffs, open plains, wind corridors, elevated areas)

	Drop Rate : Medium

	Rewards : None

	Purpose : 
✔ Introduces elemental ecology of the island
✔ Connects Kaelen to aerial and wind systems
✔ Soft-prepares player for mount and flight logic
✔ World-building through resource placement

---

	QUEST 3 — "Echoes Beneath Crystal"

	Quest Type : Cross-Zone Rare Hunt / Secret Location Discovery
	Description : Kaelen sends the player beyond Netar Island to retrieve rare crystals from a hidden creature — reinforcing his knowledge of the wider world.

> "Not all truths are found under open skies. Some hide where light breaks."

	Objectives : 
Collect : 2x Resonant Crystal

	Source

	Dropped by:
* "Crystalback Lurker"	
	Location :
* "Resonant Chambers"
* ZONE 2 — Vaelthorn Frontier
* Secluded, secret location

	Drop Rate : Low-Medium
(Designed to feel rare but not punishing)

	Rewards : 
* 1 Gold
* Experience
* "Icy Pearl" *(Key future crafting material for Kaelen)*

	Purpose : 
✔ Introduces secret location gameplay
✔ Expands Kaelen’s reach beyond Zone 5
✔ Seeds materials for later mount & unique crafts
✔ Establishes Kaelen as a long-term progression NPC

---

	QUEST 4 — "The Courier of Winds"

	Quest Type : Multi-NPC Relay / World Integration Quest
	Entry Condition : This quest is NOT started directly from Kaelen.
	How to Start : Player must first present "Scroll No. 2" to the "Field Marshal" located in : "ZONE 2 — Vaelthorn Frontier - Ravenwatch Encampment"

	The Field Marshal then instructs the player to assist Kaelen by delivering a map.

---

	Description : Kaelen entrusts the player with a map meant for a skilled navigator — establishing diplomatic and informational ties between regions.

> "Some paths must be walked before they are flown."

	Objectives : 

Deliver:

* "Stormreach Route Map" to : "Lira Seastone – Navigator’s Lodge Officer" located in "ZONE 1 — Southfall Dominion - Landing Port of Southfall"

	Rewards : 
From Lira Seastone : "Scroll No. 2" *(Quest item, starting point for larger narrative chain)*

	Purpose : 
✔ Ties Zones 1, 2, and 5 together
✔ Establishes Kaelen’s political relevance
✔ Connects him with military command (Field Marshal)
✔ Introduces information logistics into the narrative layer

