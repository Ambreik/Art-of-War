			Glider Item Types & Rarity Tiers

	System File — Glider Equipment Classification

* Gliders are equipment items, not mounts.
* They provide utility, control, safety, and traversal, not raw combat power.

	They are:
✔ Optional
✔ Skill-enhancing
✔ Environmental tools
✔ Rarely combat-adjacent
✔ Non-mandatory for progression

---

	Glider Item Slot Type

* Equipment Slot : Utility / Back Device
* Category : Aerial Tool
* Usage Type : Active (Deploy while falling or jumping)

---

	CORE GLIDER ITEM TYPES

* Each type defines playstyle & behavior.

---

	1. Featherfall Gliders

* Purpose : Survival & safety
* Strengths :
	* Strong fall damage reduction
	* Slow descent	
	* Stable control

* Ideal For : Exploration, casual travel, vertical zones
* Weakness : Short horizontal travel

---

	2. Drift Gliders

* * Purpose : Balanced traversal
* Strengths :
	* Moderate distance
	* Moderate descent control
	* Stable in light wind

* Ideal For : General exploration
* Weakness : No specialization

---

	3. Long-Range Gliders

* Purpose : Distance & travel
* Strengths :
	* Maximum horizontal distance
	* Efficient stamina use
	* Optimized glide ratio

* Ideal For : Crossing valleys, long jumps
* Weakness : Worse vertical control

---

	4. Speed Gliders

* Purpose : Fast repositioning
* Strengths :
	* Increased glide speed
	* Faster deployment
	* Strong dive momentum

* Ideal For : Quick escapes, PvE reposition
* Weakness : Less stable

---

	5. Wind-Synergy Gliders

* Purpose : Environmental mastery
* Strengths :
	* Boosted performance in wind
	* Better lift from gusts
	* Reduced wind penalties

* Ideal For : Wind-heavy regions
* Weakness : Normal performance outside wind

---

	6. Experimental Gliders

* Purpose : Risk–reward fun
* Strengths :
	* Potential bonus effects
	* Unstable modifiers
	* Unique flight behavior

* Weakness : Random quirks, failure events

---

	7. Stealth Gliders

* Purpose : Subtle travel
* Strengths :
	* Reduced noise
	* Lower detection while gliding
	* Smooth landing

* Ideal For : Rogues, scouting
* Weakness : Shorter range

---

		GLIDER RARITY TIERS

---

	Common — “Basic Frame”

* Role : Entry utility
* Traits :
	* Basic glide
	* No special effects
	* Reliable
* Easy to craft

* Use Case : First-time glider users

---

	Uncommon — “Refined Frame”

* Role : Improved control
* Traits :
	* Better glide ratio
	* Slight stamina efficiency
	* Minor stability bonuses

---

	Rare — “Optimized Wing”

* Role : Travel efficiency
* Traits :
	* Longer flight
	* Reduced stamina drain
	* Environmental synergy (wind OR descent OR speed)

---

	Epic — “Mastercrafted Frame”

* Role : Specialized excellence
* Traits :
	* Strong specialization bonus
	* Unique passive modifier
	* Superior control

---

	Legendary — “Skyshard Prototype”

* Role : Prestige & peak performance
* Traits :
	* Best-in-class glide behavior
	* Unique visual effects
	* Experimental or signature mechanics
	* Rare materials required

---

	RARITY STAT SCALING (UTILITY-FOCUSED)

| Stat               | Scaling    |
| ------------------ | ---------- |
| Glide Distance     | Medium     |
| Descent Slow       | Medium     |
