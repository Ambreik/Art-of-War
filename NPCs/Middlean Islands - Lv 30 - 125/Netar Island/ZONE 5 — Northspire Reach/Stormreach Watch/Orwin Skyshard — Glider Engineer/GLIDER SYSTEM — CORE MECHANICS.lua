			GLIDER SYSTEM — CORE MECHANICS
	(Traversal, Survival, Skill-Based Mobility)

		Design Goal

	Gliders are not mounts and not flight — they are :

* A controlled fall tool
* A short-distance travel mechanic
* A survival tool when falling
* A wind-reactive traversal system
* A skill-based movement enhancer, not mandatory but rewarding

	They do NOT replace walking, mounts, or "Flying Masters".

---

		1.How Gliding Activates

	Activation Conditions

	A player can deploy a glider if :
* They are falling
* They step off a ledge
* They jump from a height
* They manually activate the glider hotkey mid-air

	Cannot activate if : 
* Stunned
* Rooted
* Carrying heavy objective items
* In no-glide zones
* Indoors (unless dungeon allows it)

---

		2. Core Glide Movement Behavior

	When gliding

	Player enters "Glide State" :
* Fall speed reduced heavily
* Horizontal movement enabled
* Vertical descent slow but constant
* Turning speed moderate
* Camera slightly zooms out for spatial awareness

	Movement feels like : 
* Slow falling
* Air surfing
* Wind drifting
* Directional steering

---

		3. Glide Physics (Not True Flight)

	Gliders do NOT gain altitude naturally.

| Action              | Result                                  |
| ------------------- | --------------------------------------- |
| Falling             | Slowed descent                          |
| Moving forward      | Extends travel distance                 |
| Facing wind         | Gains glide efficiency                  |
| Facing against wind | Reduced glide range                     |
| Diving downward     | Gains short forward burst               |
| Pulling up          | Slows descent further but reduces speed |

---

		4. Wind Interaction (Links to Wind System)

	Wind affects glide performance :

	Wind Boost Effects : 
* Tailwind = +20–40% distance
* Crosswind = lateral drift
* Headwind = reduced range
* Storm wind = unstable flight, risk of spin

	Strong wind zones : 
* Extend glide range
* Allow reaching hidden ledges
* Enable secret traversal routes

---

		5. Stamina / Glide Energy Resource

	Gliders consume "Glide Endurance"

| Action        | Cost         |
| ------------- | ------------ |
| Basic gliding | Low drain    |
| Sharp turns   | Medium drain |
| Speed dive    | Higher drain |
| Wind riding   | Lower drain  |

	When endurance reaches 0 : 
* Glide collapses
* Player resumes falling
* Soft landing damage applies if low enough

---

		6. Landing Mechanics

	Safe Landing : 
* Smooth touchdown
* No damage
* Bonus if landing on marked zones

	Rough Landing : 
* Small stumble animation
* Minor stamina loss

	Hard Landing : 
* Fall damage reduced but not removed
* Possible knockdown

---

		7. Emergency Fall Save (Critical Survival Role)

	If player falls from lethal height :
* If glider equipped → automatic emergency deploy
* Converts lethal fall → heavy stagger instead of death

	This makes gliders life-saving tools.

---

		8. Dismount Boundary Rule (Island Flight Restriction)

	This matches your rule :

	On this island
* Skelvar Windrider = only true flying mount allowed
* Glider = allowed anywhere

	Crossing restricted flight boundary : 
* Flying mounts force dismount
* Gliders remain functional
	→ Reinforces glider as legal traversal tool

---

		9. Skill Expression (Player Mastery)

	Players can learn to :
* Extend glide distance
* Ride wind currents
* Dive-boost forward
* Land on small ledges
* Chain glides between rooftops or cliffs
* Escape enemies using terrain

	This makes gliding :
* Skillful, expressive, rewarding — not automatic.

---

		10. Combat Interaction Rules

	Allowed while gliding :
* Light item use
* Some abilities (class-defined)
* Trap drop (Outlaw synergy)
* Emergency potion

	Not allowed :
* Heavy casting
* Full melee combos
* Mount summoning

---

		11. PvP Interaction Rules

| Behavior                 | Rule                    |
| ------------------------ | ----------------------- |
| Gliding                  | Not PvP exploit         |
| Being attacked mid-air   | Damage applies normally |
| Getting stunned          | Glide collapses         |
| Killing a gliding player | Possible                |

Gliding = escape tool, not invulnerability

---

		12. Glider Quality Differences (Item Tiers)

| Tier      | Bonus                       |
| --------- | --------------------------- |
| Common    | Basic glide                 |
| Uncommon  | +Distance                   |
| Rare      | +Endurance efficiency       |
| Epic      | +Wind synergy               |
| Legendary | +Stability, emergency boost |

---

		13. Failure & Chaos (Links to Experimental Failure)

	Low-quality gliders may :
* Shake mid-air
* Lose control briefly
* Drop altitude faster
* Trigger funny NPC comments

	Adds charm without frustration.

---

		14. Integration With Orwin Skyshard

	Orwin teaches :
* Safe landing
* Wind reading
* Emergency deployment
* Glide efficiency awareness

	The system feels earned, not handed out.

---

		15. Environmental Integration

	Gliding interacts with :
* Wind system
* Storm weather
* Frost slip zones
* Canyon drafts
* Beacon light updrafts
* Dungeon vertical shortcuts
* Hidden loot ledges
* Secret routes

