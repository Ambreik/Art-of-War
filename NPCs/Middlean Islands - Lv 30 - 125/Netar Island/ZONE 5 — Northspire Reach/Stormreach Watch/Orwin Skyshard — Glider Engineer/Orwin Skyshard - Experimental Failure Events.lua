			Experimental Failure Events

* NPC Owner : Orwin Skyshard
* System Type : Flavor / Risk / Fun / Prototype Instability
* Category : Glider Mechanics Support System

---

		System Purpose 

	This mechanic exists to:

✔ Add humor and personality to glider gameplay
✔ Reinforce Orwin Skyshard’s identity as an obsessed inventor
✔ Make glider testing feel alive and experimental
✔ Introduce non-punishing chaos
✔ Keep gliders fun, not grindy or hardcore
✔ Add immersive flavor without frustrating players

	This system is never required, never lethal, and never punishing.

---

		Core Concept

	Some gliders, components, or test states are marked as:

> Prototype / Experimental / Unstable

	When used, they have a small chance to trigger a "Failure Event".

Failure =
🪂 Funny
🌀 Chaotic
🔧 Unstable
😄 Memorable
❌ Not deadly

---

		When Failure Events Can Trigger

	Trigger Sources :
* Using "Prototype Gliders"
* Testing glider parts crafted by "Orwin Skyshard"
* Completing "Aerial Trial quests"
* Using unfinished glider schematics
* Equipping "Experimental Wind Modules"
* Rare chance during strong wind weather
* Rare chance during invasion stress events

---

	Failure Chance Guidelines : 

| Source Type                 | Failure Chance |
| --------------------------- | -------------- |
| Basic Prototype Part        | 3–6%           |
| Experimental Glider         | 6–10%          |
| Trial Test Flight           | 5%             |
| Extreme Weather Flight      | +3%            |
| Invasion Active             | +2%            |
| Mastercrafted Stable Glider | 0%             |

---

		Failure Event Categories

---

		Flight Instability Events

	Spiral Drift : 

	Player spins mid-air briefly : 
* Camera wobble
* Spin animation
* Regains control automatically

> Orwin: "Ah. Aerodynamic rebellion."

---

	Wind Overboost

	Glider surges forward too fast : 
* Overshoots landing
* Forced slide roll on impact
* No damage

---

	Directional Misalignment

	Controls briefly invert : 
* 2–3 seconds confusion
* Regains control

---

		Mechanical Malfunctions

	Rattling Frame : 
* Audio rattle
* Slight steering wobble
* Cosmetic only

---

	Soft Crash Landing : 
* Player staggers
* Dust impact effect
* No lethal fall

---

	Loose Control Lever : 
* Brief delayed steering response

---

		Wind & Physics Anomalies

	Reverse Draft

	Wind flips direction : 
* Sudden sideways drift

---

	Turbulence Pocket : 
* Random air bump
* Slight altitude drop

---

	Cosmetic Chaos Events

	Smoke Burst

Glider emits smoke : 
* NPC reacts
* Cosmetic only

---

	Spark Shower

	Small sparks flicker : 

> Orwin: "Decorative… yes… decorative."

---

		Rare Fun Event (Ultra Rare)

	Prototype Autonomy

	Glider briefly flies on its own : 
* Player struggles
* Forced zig-zag flight
* Regains control safely

> Orwin: "IT’S DEVELOPING PERSONALITY AGAIN!"

---

	Safety Rules : 

✔ Failure CANNOT kill player
✔ Failure CANNOT cause fall death
✔ Emergency slow-fall auto-trigger if needed
✔ No PvP interference
✔ No gear durability loss
✔ No EXP loss

	This is pure flavor + fun.

