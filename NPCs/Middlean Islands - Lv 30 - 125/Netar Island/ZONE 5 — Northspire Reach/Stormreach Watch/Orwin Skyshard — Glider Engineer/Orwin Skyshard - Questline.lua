			Orwin Skyshard - Questline

		"Aerial Trials of the Windwright"

* Questgiver : Orwin Skyshard 
* Quest Arc Name : "Trials of Air & Descent"
* Purpose : Teach traversal mastery → unlock glider proficiency → build trust with the engineer

---

		QUEST 1 — "Learning How to Fall"

* Type : Intro Trial
* Theme : Landing control & gravity respect

	Objective : 
* Jump from a marked height
* Land safely inside 3 designated landing zones
* Avoid heavy fall damage

	Mechanics : 
* Falling from high ledges
* Shrinking landing circles
* Soft-landing timing window

	Failure Condition : 
* Miss landing zones
* Take heavy fall damage

	Reward : 
* EXP
* Minor currency
* Unlock next trial

	Engineer Quote : 

> "Flight begins with learning not to die on impact."

---

		QUEST 2 — "Reading the Wind"

* Type : Environmental Awareness Trial
* Theme : Wind direction & movement prediction

	Objective : 
* Identify correct wind currents
* Follow airflow ribbons
* Reach the final platform using only wind paths

	Mechanics : 
* Visual airflow cues
* Moving wind streams
* Wrong routes push player backward

	Reward : 
* EXP
* Wind Insight Token

	Engineer Quote : 

> "The air speaks — most people just don’t listen."

---

		QUEST 3 — "Momentum is Everything"

* Type : Movement Mastery Trial
* Theme : Speed & carry-through motion

	Objective :
* Chain downhill jumps
* Maintain speed through 3 momentum gates
* Reach destination without losing momentum

	Mechanics : 
* Sprint-based travel
* Momentum decay if player slows
* Jump chaining

	Reward : 
* EXP
* Minor speed bonus token

	Engineer Quote : 

> "Speed is borrowed time. Waste it, and gravity collects."

---

		QUEST 4 — "Precision Landing Drill"

* Type : Accuracy Trial
* Theme : Controlled descent

	Objective : 
* Leap from elevated platforms
* Land inside shrinking target zones
* Complete 3 perfect landings

	Mechanics : 
* Timing-based descent
* Narrow landing windows
* Visual focus test

	Failure Condition : 
* Miss more than once

	Reward : 
* EXP
* Precision Mark

	Engineer Quote : 

> "Landing is art. Falling is clumsiness."

---

		QUEST 5 — "Draft Dancing"

* Type : Advanced Wind Navigation
* Theme : Multi-wind chain traversal

	Objective : 
* Ride 3 different wind currents in sequence
* Avoid turbulence pockets
* Reach final floating marker

	Mechanics : 
* Direction-changing airflow
* Turbulence knockback
* Multi-layer air movement

	Reward : 
* EXP
* Unlock next stage

	Engineer Quote : 
	
> "The sky isn’t still — it’s a moving puzzle."

---

		QUEST 6 — "Trial of Trust"

* Type : Engineer Confidence Test
* Theme : Trust & discipline

	Objective : 
* Complete a blind wind route
* Navigate using:
	* Dust drift
	* Cloth markers
	* Sound cues

	Mechanics : 
* Limited visual guidance
* Environmental signal reading

	Reward : 
* EXP
* Engineer Approval Token

	Engineer Quote : 

> "If you hesitate mid-air, you fall. Confidence matters."

---

	QUEST 7 — "First True Glide"

* Type : Capstone Trial
* Theme : Pre-glider glide simulation

	Objective : 
* Use temporary wind-lift device
* Glide across a canyon
* Land safely at a marked platform

	Mechanics : 
* Temporary simulated glider effect
* Glide control training
* Direction steering test

	Reward : 
* EXP
* "Unlock: Glider Handling Passive (Rank I)"
* Access to glider crafting services

	Engineer Quote : 

> "Now you’re not just falling. You’re learning to fly."

---

		PASSIVE REWARD — "Glider Handling (Rank I)"

	Effect : 
* Increased glide stability
* Reduced fall impact
* Improved wind control while gliding

	Flavor : 

> "You’ve learned to negotiate with the air."

