			GLIDER UPGRADE, DURABILITY & REPAIR SYSTEM
		(Progression Without Grind Pressure)

	Design Intent

* Gliders should feel :
	* Maintainable
	* Upgradable
	* Valuable
	* Not disposable
	* Not punishing

---

		1. Glider Durability System

	Durability Loss Triggers : 

| Event              | Durability Cost |
| ------------------ | --------------- |
| Every glide use    | Minor           |
| Hard landing       | Medium          |
| Crash / collapse   | High            |
| Turbulence stress  | Minor           |
| Combat hit mid-air | Minor           |

---

		2. Durability States

| Durability % | Effect                        |
| ------------ | ----------------------------- |
| 100–70%      | Full performance              |
| 70–40%       | Slight stamina drain increase |
| 40–15%       | Reduced glide distance        |
| Below 15%    | Risk of malfunction           |
| 0%           | Glider unusable               |

---

		3. Failure Risk (Flavor, Not Punitive)

	At low durability :
* Glide shakes
* Wind control weaker
* Chance of early stamina collapse
* Visual cloth tearing effect

	This ties into your "Experimental Failure Events" — fun, not rage-inducing.

---

		4. Repair System

	Repair Locations : 

* "Orwin Skyshard Workshop"
* Legendary Blacksmith
* Portable repair kits (limited)

---

	Repair Costs Scale With : 

| Factor             | Effect                  |
| ------------------ | ----------------------- |
| Glider rarity      | Higher repair cost      |
| Damage level       | Higher repair cost      |
| Special components | Requires rare materials |

---

		5. Upgrade System (Not Mandatory — Rewarding)

	Upgrade Categories

* Endurance Upgrade : 
	* +Longer glide time

* Wind Efficiency Upgrade : 
	* Better performance in headwinds
	* More benefit from tailwinds

* Stability Upgrade : 
	* Less turbulence effect
	* Lower failure chance

* Weight Optimization : 
	* Faster directional response
	* Lower stamina drain

	Experimental Mod Slot (High-tier only)

	Adds fun effects :
* Speed burst
* Emergency save boost
* Reduced durability loss

---

		6. Upgrade Method

	Upgrades require :
* Crafting materials
* Wind-related items
* Rare mechanical parts
* Essence-based enhancements

	Upgrade Chance Scaling : 

| Tier      | Success Chance |
| --------- | -------------- |
| Low       | High           |
| Mid       | Medium         |
| High      | Low            |
| Legendary | Risky          |

	Failure may :
* Consume materials
* Slightly damage durability
* Trigger humorous malfunction

---

		7. Repair vs Upgrade Philosophy

| Repair             | Upgrade              |
| ------------------ | -------------------- |
| Maintains function | Improves performance |
| Low cost           | Medium / high cost   |
| Routine            | Optional progression |

	No forced grind — only optional mastery.

---

		8. Integration With Crafting & Economy

	Gliders become :
* Tradeable assets
* Upgrade investment items
* Rare collectible gear
* Prestige traversal items

	Supports :
✔ Economy
✔ Crafting loops
✔ Exploration reward
✔ Player identity

