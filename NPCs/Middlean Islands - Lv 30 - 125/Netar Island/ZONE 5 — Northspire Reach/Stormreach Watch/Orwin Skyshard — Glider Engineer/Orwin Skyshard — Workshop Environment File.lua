			Orwin Skyshard — Workshop Environment File

* Location Type : Specialized NPC Workspace
* Theme : Experimental Aviation / Wind Engineering / Controlled Chaos
* Tone : Inventive, Noisy, Curious, Slightly Dangerous
* Gameplay Role : Immersion, Flavor, Minor Interactive Systems

---

		Workshop Identity

* Name :

> "The Windwright’s Workshop"

	Purpose : A semi-chaotic engineering space where gliders are:
* Built
* Tested
* Repaired
* Failed
* Reinvented

	The workshop should feel like :

> A genius’ lab that barely stays together.

