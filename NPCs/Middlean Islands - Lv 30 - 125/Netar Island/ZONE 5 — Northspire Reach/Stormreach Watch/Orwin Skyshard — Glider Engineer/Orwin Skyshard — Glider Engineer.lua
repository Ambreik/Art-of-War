		PvP & Combat Behavior

	PvP Status : 
* Cannot be PvP flagged
* Neutral during normal conditions

	Invasion Behavior : 
* Becomes skillable ONLY if attacked by monsters during invasion
* Defends himself using:
	* Wind bursts
	* Tool-based strikes
	* Smoke bursts
	* Emergency escape hops
* Does not assist other NPCs or players
* Fights only to protect himself and his workshop

> "If they break my workshop, I swear I’ll ground the sky itself."

---

	PRIMARY FUNCTION — "Aerial Trials & Flight Training"

	Purpose : 
* Teaches players aerial awareness, wind reading, landing control, and glide preparation, even before owning a glider.
* These quests subtly train players how to use gliders effectively later.

---

		AERIAL TRIAL QUEST TYPES

	1. Wind Flow Navigation Trial

	Player must :
* Jump across wind currents
* Ride natural updrafts
* Land on marked platforms

	Teaches :
✔ Wind direction awareness
✔ Controlled landing
✔ Timing jumps with airflow

---

	2. Precision Landing Trial

	Player must:
* Leap from height
* Land inside shrinking target zones
* Avoid impact damage

	Teaches :
✔ Fall timing
✔ Landing control
✔ Risk evaluation

---

	3. Momentum Carry Trial

	Player learns to:
* Convert downhill movement into forward travel
* Chain momentum-based jumps

	Teaches :
✔ Speed conservation
✔ Traversal planning

---

	4. Blind Wind Reading Trial

	Player navigates using:
* Visual wind cues
* Dust trails
* Hanging ribbons

	Teaches :
✔ Environmental awareness
✔ Reading invisible mechanics

---

	Trial Rewards : 
* Small EXP
* Wind-themed items
* Discounts on glider crafting
* Unlock "Glider Handling Passive"

