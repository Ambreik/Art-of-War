			Orwin Skyshard — Glider Engineer

	Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach → Stormreach Watch

---

		Core Identity

	A brilliant but distracted wind mechanic and glider craftsman, obsessed with airflow, lift dynamics, and aerial stability.
	He treats gliders not as tools — but as living machines that must obey the language of the wind.

	He speaks rarely about politics or war — his mind is almost always lost in calculations, wind currents, broken wings, and flight experiments.

> "The wind never lies. People do."

---

