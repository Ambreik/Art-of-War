			Orwin Skyshard — Glider Schematic Vendor File

* Vendor Type : Schematic Seller (Crafting Unlocks)
* NPC : Orwin Skyshard
* System Role : Unlock glider crafting designs
* Tone : Experimental, aviation-focused, semi-unpredictable

---

		Vendor Overview

	Orwin does NOT sell finished gliders.
	He sells SCHEMATICS, which unlock crafting recipes.

	Schematics are:
* Account-bound (optional)
* Craftable via Glider Crafting NPC/System
* Tiered by Complexity & Performance
* Some require quest progress or Experimental Failure unlocks

---

		Shop Structure — Tabs

	Tab 1 — Basic Flight Schematics
	Tab 2 — Advanced & Specialized Schematics
	Tab 3 — Experimental / Risk Designs
	Tab 4 — Rare & Discovery Schematics

---

	TAB 1 — Basic Flight Schematics
	(Entry-level, practical, stable)

	Simple Canvas Glider Schematic : 
* Basic glide
* Moderate descent slow
* Short flight distance
* Stable, low failure rate

* Use Case : Safe travel, beginners
* Cost : Low gold + basic materials

---

	Windframe Glider Schematic : 
* Slightly better control
* Improved lateral movement
* Reduced stamina drain

* Use Case : Controlled navigation
* Cost : Low–Medium

---

	Driftwing Harness Schematic : 
* Optimized for short jumps
* Quick deploy time
* Faster takeoff animation

* Use Case : Emergency slow-fall
* Cost : Low

---

	TAB 2 — Advanced & Specialized Schematics : 
	(Improved performance & control)

	Skycurrent Rider Schematic : 
* Longer glide duration
* Better wind current efficiency
* Minor speed boost while gliding

* Use Case : Travel optimization
* Unlock Condition : Complete Orwin Questline

---

	Stormdraft Glider Schematic : 
* Performs better in windy conditions
* Slight lift bonus during storms
* Less affected by wind penalties

* Use Case : Wind-heavy zones

---

	Featherfall Strider Schematic : 

* Greatly reduced fall speed
* Designed for vertical descent
* Emergency survival utility

	Use Case : Safety / exploration

---

	TAB 3 — Experimental / Risk Designs
	(Unstable — interacts with Experimental Failure Events)

	Unstable Prototype Wing Schematic : 
* Higher glide speed
* Chance to wobble mid-flight
* Can trigger fun failure animations

---

	Overcharged Lift Harness : 
* Extremely strong lift
* Short flight time
* Risk of sudden hard landing

---

	Dual-Sail Glider Schematic : 
* High speed
* Sensitive to wind shifts
* Can gain bonus speed OR lose control

---

	TAB 4 — Rare & Discovery Schematics : 
