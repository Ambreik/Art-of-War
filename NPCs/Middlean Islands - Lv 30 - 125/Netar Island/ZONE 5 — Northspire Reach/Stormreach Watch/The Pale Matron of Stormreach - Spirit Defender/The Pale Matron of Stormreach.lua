			The Pale Matron of Stormreach

---

	Location : Stormreach Watch — Village Plaza (only during Massive-Scale Invasion events)

---

	Appearance : A tall, ethereal woman of translucent form, clad in ancient Crusader-era armor that seems forged from moonlight and frost. Her features are soft 
yet solemn, with hollow, glowing blue eyes and a faint mist that trails from her feet. Instead of a weapon, she carries a tattered spectral banner, its cloth 
shifting like wind-swept smoke. She emits a pale blue aura that pulses rhythmically, and her presence slightly flickers like a mirage.

---

	Personality : Silent yet resolute. The Matron does not speak, but her presence instills calm determination in defenders and unease in her foes. She moves with 
purpose and grace, never wavering or showing fear, embodying an unwavering watch over the village she once protected. She does not interact like a regular NPC — she
is a guardian force, quietly influencing the battlefield.

---

	Behaviour : 
* Only spawns once during a massive invasion and when specific inscription resonance conditions are met.
* Does not roam — stands in the heart of the village, radiating influence outward.
* Her aura affects nearby allies and enemies according to her mechanic profile below.

---

	Role : 
* Role(s) : Spectral Defender & Combat Support Entity
* Function :
	* Provides tactical support to NPC defenders and players
	* Temporarily bolsters morale
	* Helps stabilize faltering defenses during peak invasion moments
	* Does not attack directly but empowers others who do

---

	Mechanics : 
* Type : Event-Specific Support Entity
* Reputation : N/A (cannot be befriended or interacted with directly)
* Interaction Limitations :
	* Players cannot initiate interaction like dialogue or quests
	* Only appears under precise LCWM conditions during invasions
* PvP :
	* Cannot be targeted or damaged by players
	* Immune to PvE damage beyond scripted/desired mechanics
* Respawn Time :
	* Only reappears if all LCWM conditions are re-met in a future invasion
* Other Mechanics :
	* Emits a "Morale Pulse" every 15 seconds
	* Radius increases temporarily if multiple resonance conditions are active
	* Does not prevent defeat but reduces the chance of total collapse

---

		Skills 

	Skill 1: "Morale Beacon"

* Description : Radiates a calming energy that bolsters defenders.
* Effect : Allies within 15m receive:
	* +10% Damage Resistance vs Crusaders
	* +8% Accuracy
	* Minor HP regeneration boost over time

---

	Skill 2: "Resolve Echo"

* Description : Temporarily weakens Crusader assault vigor near her position.
* Effect : Enemies within 10m suffer:
	* -15% Attack Speed
	* Reduced morale (AI behavioral modifier, making them more hesitant)

---

	Skill 3: "Guardian Pulse"

* Description : Once the invasion enters its final wave phase, emits a powerful pulse.
* Effect : Temporary rally effect that:
	* Restores partial bulwark integrity
	* Slightly reduces cooldowns of player defensive skills (fire lighting, barricading, etc.)

---

	Quest : 
* The Pale Matron does not give quests.
* Her presence is an emergent event trigger — not a quest hub.

---

		Quotes : 
	(Ambient voice / echoes heard when she appears)

* “Stand firm, for we watch still.”
* “Let the fallen be your strength.”
* “The watch never ends.”
	(These phrases are heard as whispers in wind and echo lightly in the vicinity.)

