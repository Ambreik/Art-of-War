	Shop Mechanics / Special Rules

* Only players flagged Smuggler / Outlaw can see or buy items. Others see an empty stall.
* Mr. B & Mr. D will hide the shop if village is under siege (Broken state).
* Stock refresh every 6 in-game hours.
* Certain items (e.g., Coin Ward) cannot be resold or traded outside smuggler network.
* NPCs may pickpocket unaware players near the shop, adding flavor and risk.

