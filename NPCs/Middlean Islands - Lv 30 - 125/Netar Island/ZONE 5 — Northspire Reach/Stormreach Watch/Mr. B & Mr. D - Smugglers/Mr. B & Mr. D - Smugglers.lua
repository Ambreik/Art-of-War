			Mr. B & Mr. D

	The Goblin Smuggler Twins of Stormreach Watch

---

	General Identity

	Names :
* Mr. B — “Brixle Blinkfuse”
* Mr. D — “Dazzik Blinkfuse”

* Race : Goblin
* Faction : Smuggler Network (Independent Cell)
* Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach → Stormreach Watch
Primarily: alleys, storage caverns, secluded stairways, behind the lodge

* Role : Chaotic Smuggler NPCs
* Alignment : Neutral Chaotic
* PvP Flag : ❌ Cannot be PvP flagged
* Skillable : ❌
* Killable : ❌ (Expelled instead)

---

	Appearance : 
* Short, wiry goblins in layered coats stuffed with pockets
* One-eyed goggles (Mr. B), cracked monocle (Mr. D)
* Always holding or adjusting:
	* vials
	* wrapped trinkets
	* stolen goods
* Carry small shoulder satchels that visibly grow/shrink based on activity
* Often animated:
	* whispering
	* gesturing wildly
	* pointing fingers at each other

	Visual Contrast :
* Mr. B: red accents, twitchy movements
* Mr. D: blue accents, smoother posture

---

	Core Behavior : 
* Rarely stationary
* Frequently split up, then reconvene
* Avoid main plaza during daytime
* Appear more often:
	* At night
	* During storms
	* After invasions
* Instantly scatter when guards approach
* Never appear together in open spaces for long

---

	Mechanics :

	1. Pickpocket System (Unique NPC Mechanic)

	Trigger Conditions

They attempt pickpocketing when :
✔ Player is idle
✔ Player is in secluded areas
✔ Player is browsing inventory/shop
✔ Player is AFK
✔ Player is low on HP
✔ Player is carrying high-value items

	Effects

When successful:

* Steal:
	* Small gold
	* Random consumable
	* Rare crafting mat (low chance)
	* Item upgrade stone (very rare)
* Player receives:

	* "Lightened Purse" debuff

	Counterplay

Players can:
✔ Catch them mid-act → triggers chase dialogue
✔ Use emotes (threaten / stare / shout)
✔ Stay near guards
✔ Use Light / Fire
✔ Wear anti-theft trinkets

	If caught:
They drop a random stolen item and flee laughing.

---

	2. Harassment of Isolated Players

When player is alone and away from guards:

	They may:
* Block path briefly
* Demand:
	* Gold
	* Food
	* Potions
* Fake-threaten violence but never attack
* Trigger intimidation dialogue

	Player Options :
* Pay them off
* Refuse (they flee)
* Call guards
* Chase them

---

	3. Twin Blame System (Signature Mechanic)

Whenever accused, caught, or expelled :

	They ALWAYS:
* Blame each other dynamically

	Example:

> Mr. B: “Wasn’t me! That sack’s stitched in blue, that’s HIS!”
> Mr. D: “Blue? BLUE?! You stitched it after stealing the thread!”

	This mechanic ensures:
✔ Humor
✔ Replayability
✔ Personality persistence

---

	4. Guard Expulsion System

	Stormreach Guards will:
* Periodically chase them out of the village
* Escort them to outskirts
* They return later via:
	* Sewers
	* Cliffs
	* Storage caverns
	* The bricked shaft (rare)

	They are never permanently removed.

---

	5. Illegal & Rare Goods Vendor System

	They sell items not found in normal shops: 

	Example Goods
* Stolen Ballista Parts
* Counterfeit Fortification Materials
* Smuggled Potions
* Wind-Resistant Cloaks
* Forged Stormreach Tokens (low quality)
* Rare trap schematics
* Broken relics (used in hidden crafting)

	Vendor Rules :
✔ Prices fluctuate wildly
✔ Sometimes sell junk disguised as rare
✔ Occasionally offer real rare items
✔ Inventory rotates after expulsions

---

	6. Dynamic Twin Presence

	Sometimes:
* Only one twin is present
* The other “vanished” with goods
* Player may encounter only one and later meet the other in a different location

	This creates:
✔ World continuity
✔ Emergent storytelling

---

	7. Relationship With Artefill

✔ They belong to same smuggler faction
✔ They do NOT cooperate directly
✔ Occasionally mention Artefill
✔ Artefill openly despises them
✔ They mock Artefill for being “too honest for a smuggler”

