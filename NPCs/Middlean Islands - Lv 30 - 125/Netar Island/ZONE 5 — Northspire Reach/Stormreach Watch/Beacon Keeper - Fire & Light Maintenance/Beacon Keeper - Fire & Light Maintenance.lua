			Beacon Keeper

	Location : Middlean Islands → Netar Island → Zone 5 — Northspire Reach → Stormreach Watch

	Role : Fire & Light Maintenance / Trainer
	
	Functions :
* Maintains beacon fires and torch posts
* Teaches fire mechanics to players
* Can be challenged to duels
* Enhances defensive buffs during invasions

	Appearance :
* Wears a flame-resistant leather and bronze vest, gloves, and boots
* Carries a torch in one hand and a fire-maintenance kit on his belt
* Occasionally has soot on his face from tending fires
* Flame-emblazoned hood for night visibility

	Behavior / AI :
* Wanders along key beacon posts, bulwarks, stairway chokepoints, and plaza edges
* Tends to light or refuel fires at night or during storm events
* Reacts to invaders or player PvP duels by using fire-based skills
* Uses environmental awareness to avoid hazards, but actively positions himself near torches and beacon fires

	Mechanics :
* Fire & Light Maintenance :
	* Automatically lights extinguished torch posts or beacon fires
	* Can be interacted with to teach players:
		* Lighting / extinguishing
		* Refueling
		* Fire overload (area burst)
	* Buffs players within radius during invasions:
    * "Beacon’s Warmth" : +Morale for NPCs, +Defense vs Crusaders

* PvP / Duel Interactions :
	* Flagged players can challenge him to duels
	* All guards in the village assist if flagged in PvP
	* Cannot be killed; temporary damage is mitigated via skill or teleport
	* Uses fire-based skills in duels:
		* Flame Whirl (AoE knockback)
		* Torch Strike (single-target knockdown)

* Environmental Synergy :
	* Moves to unlit torches automatically
	* Reacts to storm conditions, ensuring fires remain lit
	* Triggers special buffs if multiple beacon fires are active simultaneously

	Durability / Flagging :
* Not killable
* Immortal in PvP duels
* Skillable — primarily fire-based attacks
* Can assist or hinder players tactically based on light positioning

	Quotes / Flavor Text :

* "A torch unlit is a village unprotected!"
* "The flames will guide you… if you know how to use them."
* "Step carefully, adventurer, the fire bites as sharply as a crusader’s sword."
* "Even in darkness, a watchful flame stands eternal."

	Optional Player Training Interaction :
* Mini-tutorial quests for:
	* Torch lighting
	* Beacon fire placement strategy
	* Fire barrel usage for area denial

