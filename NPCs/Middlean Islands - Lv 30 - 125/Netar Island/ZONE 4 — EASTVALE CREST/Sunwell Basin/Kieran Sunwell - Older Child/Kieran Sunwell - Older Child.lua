			Kieran Sunwell

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Sunwell Basin

---

	Appearance : A lanky teenage boy of about 15–16 years, wearing patched but practical clothing suitable for fishing. Slightly sunburned face, bright inquisitive 
eyes, and carries a small fishing rod and personal satchel. Moves with eager, slightly nervous energy, often glancing at his younger brother, Liam, who seems to 
naturally outperform him.

---

	Personality : Kieran is serious, determined, and somewhat insecure because of his younger brother’s natural talent. He is competitive and constantly tries to 
prove himself, even asking for player assistance in producing special baits or practicing tricky fishing techniques to win contests against Liam. Despite his 
frustration, he is diligent, curious, and secretly proud of his brother.

---

	Backstory : Kieran and his younger brother Liam were found as children in the forest near Sunwell Basin with no memory of their parents. Villagers named them 
“Sunwell” because of their proximity to the lake. They grew up around the basin, learning fishing from Eldric Sunwell and surviving with his guidance.

---

	Behaviour :

* Practices fishing constantly, always comparing his catches to Liam’s.
* Approaches players to request assistance for his “competition” : making special baits, testing new fishing techniques, or helping him strategize for catching rare
fish.
* Frequently observes Liam and shows signs of both frustration and determination.
* Interacts casually with Eldric and the dogs but is focused on improving his skill.

---

	Role :

* Role(s) : Apprentice Fisherman, Player Interaction (favor requests for fishing competition)
* Focus : Supports Fishing Pools mechanic and adds social flavor via sibling rivalry.

---

	Mechanics :

* Interacts with "Fishing Pools".
* Player can assist him in:
	* Creating or testing special bait recipes.
	* Practicing catch techniques for rare fish.
	* Observing the environment to identify better fishing spots.
* Does not provide formal quests; interactions are personal favors linked to his competition with Liam.

---

	Skills :

* None 

