			Kieran’s Favor System (Informal Quests)

	These favors are personal requests, not official contracts.

---

	1. Favor: “Better Bait Than His”

	Objective : Bring Kieran materials for crafting "Improvised Gleam Bait" x5

	Required Items : 
* Shimmerfin Scales x5
* Basinroot Fibers x3
* Insect Husks x10

	Mechanic : Unlocks a temporary fishing buff in Sunwell Basin:

* Increased chance to catch rare fish
* Buff only works with Kieran’s crafted bait

	Narrative Role : Introduces bait-based fishing mechanics and Kieran’s reliance on tricks over mastery.

---

	2. Favor: “The One That Got Away”

	Objective : Catch and deliver a rare Sunwell Basin fish:

* Example: "Sunveil Glider" *(fast, reflective, elusive fish)*

	Mechanic :

	Appears only:
* At specific times
* Or after rainfall

	Purpose : Represents Kieran’s obsession with proving himself through a single legendary catch.

---

	3. Favor: “Don’t Let Him Know”

	Objective : Deliver Kieran’s fish secretly to Sunveil Market Town

	Mechanic : 
* Avoid being seen by his brother
* Time-limited
* If spotted, reward reduced

	Purpose : Introduces social stealth and deepens sibling rivalry.

---

	4. Favor: “Make It Look Bigger”

	Objective : Help Kieran build a fish display rack

	Required Items : 
* Lightwood Planks x6
* Gradded Salt x2
* Crestgrass Twine x4

	Effect : 
* Adds a visible fish rack near the basin
* NPCs comment on it
* Younger brother reacts negatively

	This permanently alters the environment.

---

	5. Favor: “Beat Him Fair… Just Once”**

	Objective : Help Kieran win a timed fishing contest against his brother

	Mechanics

	Players assist by:
* Distracting Ripple
* Clearing wildlife
* Providing special bait

	Outcome : Temporary confidence boost or visible frustration, depending on result.

---

		Kieran’s Unique Reward System

* Kieran gives no gold.
* Instead, he rewards players through trust, rare objects, and access.

---

		Reward Layer 1 – Rare Items

	Sunwell Knot Charm *(Rare Trinket)*

	Unlocked after completing 2 favors.

	Effect :
* Slightly increases fishing success in Sunwell Basin
* Reduces bait consumption chance
* Cosmetic shimmer near water

	Lore: Handmade by Kieran as a lucky charm.

---

	Bent Hook of the Basin *(Upgradeable Tool)*

	Reward for “The One That Got Away”.

	Effect :
* Increases chance to hook elusive fish
* Lower break chance on rare fish
* Upgradable later by Fishing Master Rank III

---

		Reward Layer 2 – Secret Quest Trigger

	Weathered Fishing Notes *(Hidden Content Key)*

	Unlocked after 3 favors.

	Contains:
* Fish movement patterns
* Night water sounds
* Reflection patterns

	Unlocks hidden questlines tied to:
* LCWN fishing school
* Night melody mechanic
* Rare noon/midnight fish events

---

	Reward Layer 3 – Environmental Access

	Quiet Spot Access : 

Unlocked after “Make It Look Bigger”.

	Reveals:
* A hidden fishing nook
* Protected from Ripple’s interference
* Increased rare fish chance

	Invisible to players who lack Kieran’s trust.

---

	Reward Layer 4 – Passive Basin Buff

	Kieran’s Trust *(Soft Reputation Buff)*

Unlocked after completing all favors at least once.

	Sunwell Basin only:
* Slight increase to rare fish chance
* Reduced wildlife aggression near water
* Dialogue changes from NPCs
* Brann and Lyria acknowledge you as “a friend of the boys”

	No formal REP UI needed — purely organic.

---

		Character Growth Through Progression

	Early Kieran :
* Whiny
* Trick-focused
* Dependent on players

	Later Kieran :
* More confident
* Begins learning true fishing skills
* Teaches small tips
* Less reliant on shortcuts

	This makes him a living, evolving NPC, not just a quest node.

