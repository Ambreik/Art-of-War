			Liam Sunwell

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Sunwell Basin

---

	Appearance : A small and agile boy of about 12–13 years, wearing light, well-worn clothes suited for moving quickly through tall grass and along the pools. His 
fishing rod is shorter and custom-fitted for his size, but meticulously maintained. His eyes are sharp and observant, and he almost always has a relaxed, confident 
posture despite his young age.

---

	Personality : Liam is naturally gifted, playful, and confident without being arrogant. Fishing seems to come effortlessly to him, which often frustrates Kieran.
He enjoys the friendly rivalry and sometimes teases his brother, but he is not cruel—he simply loves the challenge and the joy of mastering his craft.He has a deep,
intuitive connection to Sunwell Basin and treats the environment with instinctive respect.

---

	Backstory : Like his brother, Liam was found near Sunwell Basin with no memory of his parents. Because they were discovered close to the water, the villagers 
named them “Sunwell.” While Kieran worked hard to learn fishing, Liam’s natural affinity for the basin and its rhythms quickly set him apart as a prodigy under 
Eldric’s guidance.

---

	Behaviour :

* Moves lightly around the basin, often appearing in unexpected spots near the pools.
* Rarely asks players for help, preferring to solve problems on his own.
* Frequently catches fish with surprising ease, sometimes before Kieran even prepares his rod.
* Occasionally observes environmental changes (dew, ripples, light) before choosing a fishing spot.
* Teases Kieran in a playful, non-malicious way.

---

	Role :

* Role(s) : Apprentice Fisherman, Environmental Flavor NPC
* Focus : Adds personality, immersion, and reinforces the Fishing Pools mechanic through demonstration rather than instruction.

---

	Mechanics :

* Interacts with "Fishing Pools" in a visible, almost “scripted” way:
	* Appears more often near rare fish spawn zones.
	* Can indirectly hint at better fishing spots through his positioning.
* Does not give quests or favors.
* Acts as a natural guide through observation rather than dialogue or instructions.

---

	Skills :

* None

