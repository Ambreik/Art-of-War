			Brann the Basinwatch

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Sunwell Basin

---

	Appearance : A tall, broad-shouldered man in his late 30s, with unkempt dark hair and a short, rough beard. He wears a mix of leather and cloth armor that looks
worn but functional. His most striking feature is a long wooden staff reinforced with metal rings, which he leans on as much as he fights with. His posture suggests
strength, but his relaxed stance often gives away his lazy nature.
	Two scars cross his forearms, hinting at past fights, and his boots are always muddy from wandering between the basin edges and the surrounding forest.

---

	Personality : Brann is capable, experienced, and undeniably strong — but also lazy and opportunistic. He prefers letting others handle the “boring” parts of his
duties and is quick to ask players for favors instead of dealing with issues himself. Despite this, he genuinely cares about the safety of Eldric and the boys and 
will intervene personally if a serious threat appears.
	He hides his sense of responsibility behind sarcasm and a carefree attitude.

---

	Backstory : Brann arrived in Sunwell Basin years ago as a wandering fighter and chose to remain after realizing the basin needed a guardian more than another 
sword for hire. Though not formally tied to any faction or military, he took it upon himself to protect the fishermen and the children found near the basin.
	Rather than becoming a strict soldier, he shaped himself into a local protector — informal, independent, and deeply familiar with the wildlife and dangers of 
Eastvale Crest.

---

	Behaviour :
* Patrols the edges of Sunwell Basin and nearby forest paths.
* Frequently leans against trees or rocks, watching the pools lazily.
* Intervenes only when threats grow serious.
* Sends players to deal with minor problems instead of doing them himself.
* Occasionally trains with his staff when he thinks no one is watching.

---

	Role :

* Role(s) : Area Protector, Minor Quest Giver (favours), Combat Trainer (flavor only)
* Focus :
	* Keeps the basin safe
	* Delegates tasks to players
	* Connects the basin to local monster population control

---

	Mechanics :

* Not a soldier, but functions as the local defender of "Sunwell Basin".
* Requests player favors instead of issuing formal quests, such as:
	* Reducing the local wolf population
	* Bringing firewood
	* Delivering supplies or food from "Sunveil Market Town"
* Appears more active during "Twilight Lull", when monsters are weaker, reinforcing his control of the area.
* Not tied to reputation or military systems.

---

	Skills :

	"Long Staff Mastery" : 
* Brann is highly skilled with a long staff.
* Uses sweeping strikes, disarming motions, and defensive stances.
* Designed more for crowd control and area denial than lethal force.
* Flavor-wise, he can knock back or disable enemies rather than kill them outright.

