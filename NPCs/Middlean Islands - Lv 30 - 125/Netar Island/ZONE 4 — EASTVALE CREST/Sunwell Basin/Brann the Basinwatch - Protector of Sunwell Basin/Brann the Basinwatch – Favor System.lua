			Brann the Basinwatch – Favor System

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Sunwell Basin

	Role : Protector of the Basin / Informal Task-Giver

	System Type : World-integrated favors (no UI-heavy quest chains)

---

	Core Identity of Brann’s Favors

	Brann uses favors to:
* Keep Sunwell Basin safe
* Avoid doing tedious work himself
* Outsource problems to capable players
* Maintain control without becoming a formal authority

	His favors are practical, repetitive, and grounded in survival and logistics rather than story drama.

---

	Brann’s Favor Categories

	Brann’s favors fall into three main functional groups :
1. Wildlife Control
2. Resource & Logistics
3. Basin Stability & Patrol Support

---

	1. Wildlife Control Favors

	Favor: “Thin Their Numbers”

	Objective : Reduce the local wolf population around Sunwell Basin.

	Mechanics : 
* Wolves spawn more frequently around:
	* Forest edge
	* Rocky ridges overlooking the basin
* Brann tracks population thresholds rather than specific kills
* If wolves exceed a certain number, Brann becomes more active in the area

	Outcome

* Reduces wildlife aggression temporarily
* Makes fishing and gathering safer
* Improves NPC behavior (dogs patrol less aggressively, Lyria moves more freely)

	Brann’s Flavor

> “Not all wolves are bad… but too many is a problem.”

---

	Favor: “Something Bigger Stalks” *(Occasional)*

	Objective : Deal with a rare alpha wolf or predator threatening the basin.

	Mechanics :
* Triggered only when wildlife pressure is high
* Rare mini-encounter rather than repeatable grind
* Brann visibly joins the fight if nearby

	This reinforces him as capable, not passive.

---

	2. Resource & Logistics Favors

	These reflect Brann’s laziness and reliance on players for “boring” tasks.

---

	Favor: “Wood for the Watch”

	Objective : Bring firewood to Brann.

	Mechanics :
* Firewood can be gathered from:
	* Dead trees
	* Fallen branches
	* Forest clearings
* Firewood stock affects:
	* Night warmth
	* NPC idle behavior
	* Campfire availability

	If players ignore this:

* Brann complains
* Basin feels colder and less welcoming at night

---

	Favor: “Basin’s Belly”

	Objective : Deliver food supplies from "Sunveil Market Town".

	Mechanics : 
* Players choose:
	* Fish crates
	* Dry rations
	* Fresh produce
* Affects:
	* NPC dialogue
	* Idle animations
	* Cooking by Lyria

	This creates a soft supply chain system without formal economy.

---

	3. Patrol & Stability Favors

	These relate directly to Brann’s control over the area.

---

	Favor: “Eyes Where I Can’t Be”

	Objective : Scout specific routes or forest sections and report back.

	Mechanics
* Players must reach:
	* Ridge paths
	* Tree clusters
	* Basin perimeter points
* If threats are detected:
	* Brann increases patrol
	* Dogs become more active
	* Wildlife aggression adjusts

	This makes the world feel reactive.

---

	Favor: “Quiet the Basin”

	Objective : Restore calm after disturbances (Outlaws, monster surges, events).

	Mechanics : 
* Triggered dynamically when:
	* Outlaws are active
	* Monsters overrun parts of the basin
* Completion restores:
	* Fishing stability
	* Herbalism safety
	* NPC routines

---

		Twilight Lull Integration

	Brann becomes noticeably more active during "Twilight Lull" :

	During this period:
* Brann patrols visibly instead of resting
* Dogs gain heightened alertness
* Favors related to combat become easier
* Wildlife is weaker, reinforcing Brann’s dominance over the area

	This visually and mechanically reinforces him as the basin’s anchor.

---

	Brann’s Unique Rewards (No Gold Focus)

Brann doesn’t reward in money primarily. Instead:

---

	1. Basin Safety Buffs

	Completing his favors improves Sunwell Basin for everyone:
* Reduced monster aggression
* Increased fishing stability
* Safer gathering zones
* More predictable wildlife behavior

	These are world rewards, not personal loot only.

---

	2. “Basinwatch Mark” *(Invisible World Flag)*

	After repeated help:
* NPCs recognize player as “trusted”
* Dogs no longer growl at you
* Lyria and Kieran reference Brann’s trust
* Players can access certain patrol-only paths safely

	No UI reputation — just environmental recognition.

---

	3. Rare Item: “Worn Basinwatch Whistle”

	Description : A battered whistle Brann once used to signal danger.

	Effect :
* Once per day:
	* Summons Brann or the dogs nearby (if in Sunwell Basin)
	* Or scares off minor wildlife

	This is a utility reward, not combat power creep.

