	SYSTEM INTEGRATION NOTES

• Riftcaller Rod is intentionally rare and limited
• Fishing without Rod or Lure always fails
• Even with both:
	* Success rates are moderate
	* Rare fish remain rare
		• Occasional monster agitation while fishing reinforces risk

