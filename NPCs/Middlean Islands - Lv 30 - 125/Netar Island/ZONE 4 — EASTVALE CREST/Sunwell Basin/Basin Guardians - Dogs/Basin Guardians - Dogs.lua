			Basin Guardians

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Sunwell Basin

	Type : Companion Creatures / Area Defenders

---

	General Description : The Basin Guardians are two powerful, semi-feral dogs that roam Sunwell Basin alongside Brann the Basinwatch. Though not trained like 
military hounds, they are fiercely territorial and deeply loyal to those they consider “of the basin.”
	They patrol the waterside, grassy slopes, and nearby forest paths, acting as the first line of defense against intruders, wild beasts, and outlaws.
	They often move together, attacking in coordination and overwhelming targets through relentless pressure rather than refined tactics.

---

		Common Behaviour & Mechanics

	Aggression Rules : 
The Basin Guardians will automatically attack:
* Players flagged as "Outlaws"
* Hostile or wild animals entering the basin
* Creatures threatening NPCs or children

	They do not attack neutral or lawful players unless provoked.

---

	Pack Combat Behaviour : 
* Always fight together when possible
* Prefer flanking and surrounding targets
* Will pursue enemies out of the basin but not far into deeper forest zones

---

	Brann’s Presence Buff – “Guardian’s Oath”

When "Brann the Basinwatch" is nearby, the dogs gain:

	Guardian’s Oath (Rarely Seen State) :
* Increase in size (visibly bulkier and taller)
* Significant increase in health and defense
* Enter a berserk-like state with faster, more aggressive attacks
* Glowing eyes and louder growls during combat

This form is rare and usually only triggered during serious threats or major area disturbances.

---

		Individual Profiles

---

	1. Name : Ripple

* Role : The Playful Protector
* Temperament : Energetic, curious, and bonded to the youngest Sunwell child

	Appearance : A medium-sized dog with silver-grey fur and dark, alert eyes. Its coat shimmers slightly when wet, and it is almost always damp from jumping into 
the water. Its ears are uneven, one standing upright and the other flopping down.

	Personality : Ripple is playful and mischievous, especially around the children. He frequently disrupts fishing attempts by splashing into the water, chasing 
fish, or stealing bait. Despite this, he is extremely protective when danger arises.

	Special Behaviour : 

* Frequently seen playing with the youngest Sunwell child at the water’s edge
* Jumps into the lake, splashing and scattering fish schools
* Often “accidentally” interferes with the brothers’ fishing competition
* First to alert Brann when something feels wrong

	Quotes (Flavor / Observation)

* *“Ripple barks excitedly and splashes into the water.”*
* *“Ripple circles you curiously, tail wagging… but eyes sharp.”*
* *“Ripple growls low, stepping between the child and danger.”*

---

	2. Name : Thorn

* Role : The Silent Enforcer
* Temperament : Calm, dominant, and highly territorial

	Appearance : Larger and darker than Ripple, Thorn has deep black and brown fur with a scar running across his muzzle. His eyes are steady and unblinking, and he
rarely makes unnecessary movement.

	Personality : Thorn is disciplined and observant. He rarely plays and prefers to remain on watch, often sitting atop small rises overlooking the basin. When he 
moves, it is usually for a reason.

	Special Behaviour :

* Patrols the basin edges and forest line
* Positions himself between threats and NPCs
* Often initiates combat with Outlaws or wild beasts
* Rarely interacts with players unless hostility is detected

	Quotes (Flavor / Observation)

* *“Thorn watches you in silence, unblinking.”*
* *“Thorn’s growl is low and final.”*
* *“Thorn steps forward, blocking your path.”*

---

	Environmental Integration

* The dogs are part of the identity of Sunwell Basin, not generic animals
* They reinforce that the area is protected, not abandoned
* Their behavior supports the idea that the basin reacts to presence and threat
* Their loyalty to Brann strengthens his role as true guardian

