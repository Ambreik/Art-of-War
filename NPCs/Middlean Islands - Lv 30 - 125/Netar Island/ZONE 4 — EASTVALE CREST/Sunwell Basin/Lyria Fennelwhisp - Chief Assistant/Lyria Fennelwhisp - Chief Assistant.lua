			Lyria Fennelwhisp

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest / Sunwell Basin

---

	Appearance : A young woman in her early 20s with light brown hair tied into a loose braid, usually messy from constant work. She wears a practical travel outfit
with a short apron covered in herb stains and faint burn marks. Small pouches hang from her belt, filled with dried leaves, spices, and tiny jars of preserved 
ingredients.
	Her hands often smell of herbs and smoked fish, and her eyes sparkle with curiosity whenever she finds something new to cook with.

---

	Personality : Lyria is enthusiastic, curious, and endlessly talkative when it comes to food. She sees every plant, fish, and fungus as a potential recipe 
waiting to be discovered. Though not reckless, she often underestimates the dangers of the wilderness in her eagerness to collect rare ingredients.
	She is respectful toward Brann and the fishermen, but constantly pushes them to “try just one more strange recipe.”

---

	Backstory : Lyria is an apprentice cook from a tavern in "Sunveil Market Town", sent by her master to explore the wilds and learn the secrets of regional 
cuisine. Sunwell Basin, with its rich waters and strange vegetation, quickly became her favorite place on Eastvale Crest.

She believes that the best dishes are born where nature is at its most unpredictable.

---

	Behaviour :

* Spends time near the water and among the tall grasses gathering herbs.
* Frequently experiments with small campfire recipes near the basin.
* Returns to Sunveil Market Town periodically with new ingredients.
* Avoids combat and relies on Brann or players for protection.

---

	Role :

* Role(s) : Flavor NPC, Ingredient Specialist, Future Cooking Content Anchor
* Focus :
	* Gathering rare cooking ingredients
	* Linking Sunwell Basin to Sunveil Market Town’s tavern
	* Introducing future regional recipes

---

	Mechanics :

* Acts as a narrative bridge between wilderness resources and town cooking systems.
* Highlights unique fish and herbs found in Sunwell Basin.
* May unlock special cooking recipes in Sunveil Market Town in the future, depending on player actions or discoveries here.
* Not a vendor or trainer — strictly flavor and progression-based.

---

