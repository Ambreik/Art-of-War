			Lyria Fennelwhisp – Chief Assistant Quests

	All quests are:

* Available to any player, regardless of profession
* Focused on gathering, timing, danger, and zone identity
* Designed to reinforce Sunwell Basin’s ecological and logistical identity

---

		Quest Set – Lyria Fennelwhisp (Chief Assistant, Sunwell Basin)

---

	1. Quest: “Roots Beneath Still Water”

	Objective : Collect "Basinroot Cluster" x8

	Description : 

Lyria needs a rare root that grows only in damp soil near the shallow stone basins scattered across Sunwell Basin.
She is too afraid to search for them herself, as:
* Wild animals roam those areas
* The dogs might leave her unprotected
* The tall grasses make ambushes likely

	Mechanics :

* Basinroot Clusters spawn:
	* Near stone water basins
	* Along marshy patches at basin edges
* Moderate respawn time
* Can attract nearby wildlife when harvested

	Reward : 
* Gold
* Experience
* Small chance at rare cooking ingredient (future use)

---

	2. Quest: “Crestgrass for the Light Tonic”

	Objective : Collect "Crestgrass Tuft" x25

	Description : 

Lyria uses Crestgrass to brew light tonics served in Sunveil Market Town’s tavern — refreshing, mildly invigorating drinks favored by travelers.
These plants grow deep in the forest beyond the basin, forcing players to leave the safe zone.

	Mechanics : 

* Crestgrass spawns:
	* In shaded forest clearings
	* Near sloped ridges of Eastvale Crest
* Often guarded by wildlife
* Players must venture well outside Sunwell Basin

	Reward : 
* Gold
* Experience
* Minor reputation with Sunveil Market Town tavern (future system hook)

---

	3. Quest: “Salt of the Ridgecloak”

	Objective : Collect "Gradded Salt" x10

	Source : Dropped by "Ridgecloak Smugglers"

	Description : 

Lyria needs coarse Gradded Salt for preserving meats and enhancing flavor.
Ridgecloak Smugglers hoard it for their black-market food trade.

	Mechanics :
* Salt drops from Ridgecloak Smugglers
* Smugglers operate:
	* Along forest paths
	* Near cliff-side routes
* Encourages PvE combat and exploration

	Reward : 
* Gold
* Experience
* Chance for Smuggler-related loot

---

	4. Quest: “Fresh or Forgotten” (Timed Quest)

	Objective : Deliver "Fresh Wolf Meat" x12 before it spoils

	Spoil Mechanic : 

Once Fresh Wolf Meat drops:
* Each piece has a 10-minute spoil timer
* If timer expires:
	* Item becomes "Spoiled Meat"
	* Cannot be used for this quest

	Quest Limits : 
* Must complete within 1 real-time week after accepting
* Partial turn-ins allowed

	Rewards :
* 2 Gold per valid piece delivered
* Large EXP bonus upon completion
* Flavor reward: “Lyria’s Respect”

	Narrative Note : Lyria insists on freshness for proper curing and cooking — reinforcing realism and urgency.

---

	5. Quest: “Cracking the Basin Feast” (Advanced Timed Quest)

	Objective : Deliver "Crab Claws" x50 before they spoil

	Spoil Mechanic : 

* Each Crab Claw has a 30-minute spoil timer
* Longer than wolf meat, but quantity is much larger

	Rewards : 
* 1 Gold per claw
* Large EXP reward
* Unlocks 2 Cooking Recipes
* Considered one of Lyria’s major personal requests

---

		Unlocked Cooking Recipes

	1. Recipe: “Sunwell Basin Crab Stew”

	Description : A hearty stew made from fresh crab claws, Basinroot, and Crestgrass.

	Effect (example) :
* Increases HP regeneration
* Minor cold and water resistance
* Favored by fishermen and guards

---

	2. Recipe: “Golden-Claw Skillet”

	Description : Crab claws pan-seared in Gradded Salt and herbs, served sizzling hot.

	Effect (example) :
* Boosts strength or stamina
* Short-term attack speed bonus
* Highly prized tavern dish in "Sunveil Market Town"

