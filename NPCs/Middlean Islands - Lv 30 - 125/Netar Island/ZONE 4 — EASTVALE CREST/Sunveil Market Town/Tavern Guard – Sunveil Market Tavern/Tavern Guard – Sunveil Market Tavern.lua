			Tavern Guard – Sunveil Market Tavern

	Appearance :
* Tall, lean, imposing
* Wears a long black coat open from top to floor
* Leather boots, black gloves
* Black wide-brim hat, reminiscent of an undertaker
* Carries a heavy wooden club strapped to the back (used for AoE pushes and rooting)
* Grumpy, permanent frown; eyes constantly scanning the crowd

---

	Primary Functions :

* Roams inside the tavern only, high movement speed
* Observes player behavior for:
	* Fighting or causing chaos
	* Excessive drunkenness
	* AFK players causing obstruction
* Uses AoE **pushback and root skills** to control rowdy players
* Targets **only players**, NPCs and allies in AoE are unharmed
* Keeps track of **crowd density** at night; more aggressive when tavern is full

---

	Mechanics / Skills :

1. Club Sweep (AoE Pushback)
	* Area radius: 3m
	* Pushes all players in radius away from him
	* Damage only affects the primary target, others only pushed

2. Rooting Slam (Targeted)
	* Targets a player who is flagged as “rowdy” or causing fights
	* Roots them in place for 5–8 seconds
	* Does minor damage

3. Crowd Control Awareness
	* Moves faster when tavern is crowded
	* Aggression scales with number of intoxicated or rowdy players

4. Patrol Route
	* Random wandering across tavern tables and bar areas
	* Avoids obstacles but reacts instantly if a fight or player chaos occurs

5. Night Grump Mechanic
	* If more than 10 players are present after sunset,
		* Increases AoE push frequency
		* May call minor staff intervention (bartender NPC assists)

---

	Behavior :

* Always inside the tavern, never leaves
* Prioritizes crowd control over damage
* Observes NPCs but does not attack or interfere
* Reacts to funny player behavior, prank attempts, or fights immediately
* Can chase down a player who intentionally tries to disrupt tavern peace

