			Merchant’s Gratitude Token
			
	This item can be optained as reward from "Tharion Grayfang - Hunter Trainer" for completing his quest.
	
	This item start a chain-quest when is used at "Steward NPC".
	
---

	Chain Quest: “Lights of Gratitude”

* Starting Item : Merchant’s Gratitude Token
* Starting NPC : Any town-based NPC who interacts with player quests (e.g., Town Council Scribe, or Market Quarter Steward)

---

	Quest 1: Deliver the Token

* Objective : Give the Merchant’s Gratitude Token to the "Steward NPC".
* Narrative : The Steward notices that the merchants were saved from Cragwallowers, and wants the town to commemorate the heroism.
* Reward : Small EXP, Gold, unlocks access to next step of chain.

---

	Quest 2: Light the Memorial Lamps

* Objective : Go to three lamp posts in Market Terrace and light them using a special lantern given by the Steward.
* Narrative : Each lamp post honors a different group: fishermen, traders, and hunters. Lighting all three completes the tribute.
* Mechanic : Player must approach each lamp and interact. There’s a time window of 5 minutes per lamp to prevent easy skipping; if done too fast, the lamps reset.
* Reward : Medium EXP, minor Gold, cosmetic effect (glow around player for 1 hour).

---

	Quest 3: Pattern of Shadows

* Objective : Observe the shadows cast by the lamps at dusk to reveal a hidden symbol on the Market Terrace wall.
* Narrative : The shadows align to create a symbol that points toward a hidden cache.
* Mechanic : Player must stand in the right spot to “read” the pattern; can be done solo or with a friend.
* Reward : Unlocks minor crafting material or small consumable reward.

---

	Quest 4: The Missing Scout

* Objective : The Steward informs the player that a scout NPC went missing while checking the outskirts near the lamp-lit area. Player must find them.
* Mechanic : Scout is nearby, hiding behind objects in the Market Terrace or alleys. Player must find them by interacting with environmental cues (e.g., small 
footprints, dropped gear, or faint sound).
* Reward : EXP, Gold, and item: Scout’s Map Fragment (can be used for optional treasure hunting or as a collectible).

---

	Quest 5: Return to Steward

* Objective : Escort the Scout back to the Steward safely.
* Mechanic : Player must keep NPC safe while minor environmental hazards occur (falling crates, startled NPCs, small aggressive birds like the crows).
* Reward : Large EXP, Gold, unique title: “Friend of the Market”, minor cosmetic (flag/banner effect near player for 24h), and optional bonus: “Gratitude Chest” 
containing random consumable/crafting items.

