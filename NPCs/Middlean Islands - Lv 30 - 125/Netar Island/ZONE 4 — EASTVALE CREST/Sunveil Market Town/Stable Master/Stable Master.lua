			Stable Master

**NPC Type : Universal Service NPC
**Name Displayed In-Game : "Stable Master"
**Faction : Neutral / Town Service
**Skillable : No
**Location : Sunveil Market Town – Stable Grounds (near Mount Master)

---

	Appearance : 

* Middle-aged, weathered but strong
* Wears:
	* Reinforced leather coat
	* Riding boots
	* Gloves always worn
* Carries:
	* Whistle
	* Rope
	* Ledger
* Constantly:
	* Checking horses
	* Adjusting hay packs
	* Cleaning hooves
* Movements are calm, precise, professional

---

		Core Functions

	1. Mount Storage : 

* Stores player mounts safely
* Prevents:
	* Mount fatigue
	* Mood decay
* Universal system across all settlements

---

	2. Mount Retrieval : 

* Allows summoning of stored mounts
* Faster retrieval if:
	* Player regularly uses this Stable Master
* Slower if mounts were left injured or starving

---

	3. Mount Condition Management : 

* Passively restores:
	* Stamina
	* Calmness
* Does **NOT** train or upgrade mounts
	(keeps separation from Mount Master)

---

	4. Mount Slot Expansion : 

* Unlocks additional stable slots via:
	* Items
	* Rare quest rewards
* No gold-based progression abuse

---

		Zone-Specific Mechanics (Sunveil Only)

	Hay Pack Management : 

* Hay is a limited resource
* Overuse of stables:
	* Slows service
	* Increases hosting costs
* Encourages rotation and planning

---

	Invasion Behavior : 

* During town invasions:
	* Stable Master releases mounts
	* Mounts flee to safe zones
	* Some mounts assist guards by:
		* Blocking paths
		* Panicking enemies

---

	Smuggler Interaction (Hidden) : 

* Stable Master pretends ignorance
* Smuggler mounts:
	* Are quietly handled
	* Never questioned
* No discounts — just silence

