			2 PM Customer Pack (Sunveil Market Town)

	General Info : 

* Name / Group Name : Midday Shoppers
* Spawn Type : Time-Based / Location-Specific
* Spawn Time : 14:00 – 15:30
* Group Size : 6–10 NPCs, mixed genders
* Zone / Location : Market & Residential Terraces
* Persistence : Spawned only during this time, despawn afterward

---

	Appearance : 

* Clothing: Lighter fabrics (linen, cotton), aprons for stall helpers, straw hats, colorful scarves
* Accessories: Baskets, bags, small crates of goods, handheld fans
* Distinctive Features:
	* One elderly man carries a small walking stick and has a satchel
	* A young woman has a basket of fruits and flowers
	* One child in the group has a handmade toy (like a small wooden bird)
* Unique hairstyles, hair colors, and heights for realism

---

	Behavior / AI : 

* Movement Route :
	1. Spawn near main square or central market terrace
	2. Wander along market stalls for 10–15 minutes, inspecting goods
	3. Interact with small houses or NPCs in side streets
	4. Exit town through side streets near fountains or walls after 25–35 minutes
* Actions / Animations :
	* Inspecting and pointing at goods
	* Talking or laughing with each other
	* Sitting briefly at benches or near fountains
	* Reacting dynamically to environmental mechanics (balloons, artillery, chaos)
* Group Interaction :
	* Naturally split into pairs or trios while walking
	* Avoid clustering for realism
* Interaction With Player :
	* Can provide gossip or rumors if approached politely
	* Players can assist NPCs with carrying goods for minor rewards
	* Flee or scatter if player causes chaos

---

	Mechanics / Triggers : 
* Interaction Triggers :
	* Helping NPC carry items → small reward, minor EXP, or "Merchant’s Gratitude Token"
	* Loud abilities or attacks near them → NPCs scatter, may trigger LCWM
* Environmental Triggers :
	* Heat shimmer or bright sunlight slows NPC movement slightly
	* Fog/Mist → NPCs huddle or walk slower, comment about weather
* Hidden Interactions :
	* If player positions near terrace corners and uses loud abilities → NPCs may move to reveal a hidden LCWM interaction
	* NPC noticing rare items or dropped goods may trigger secret dialogue

---

	Quotes / Chatter : 
* Ambient Chatter :
	* “The baker sold out before noon today!”
	* “Did you see the travelers passing the east gate?”
	* “These fruits are fresher than yesterday, I swear it!”
* Player Interaction Quotes :
	* “Could you help me with this crate?”
	* “Ah, you look like a capable adventurer. Care to assist?”
	* “Mind the crowd, please.”
* Hidden Interaction Quotes :
	* “Did that crate just roll on its own?” (LCWM triggered)
	* “Strange… I think I saw something move over there.”

---

	Rewards / Loot (Optional) : 
* Minor EXP for assisting NPC
* Chance for small crafting materials (herbs, minor fish, fruits)
* Occasional "Merchant’s Gratitude Token"

---

	Notes / Misc : 
* Reflects mid-day activity: more relaxed than early morning pack
* Integrated with other town NPC mechanics (guards, shimmer, fog, LCWM)
* Wanders in randomized short routes to simulate dynamic market environment

