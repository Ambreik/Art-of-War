			MOUNT MASTER

**Role : Mount Handling, Hosting & Care
**Location : Sunveil Market Town – Outer terrace / near Flying Stall outskirts
**Faction : Neutral / Wildfolk
**Combat Role : Non-combat (mounts may react defensively)
**Persistence : Permanent NPC

---

	Appearance : 

* Looks like a wild man, almost feral:
	* Long, messy hair and thick beard
	* Scarred arms, sunburned skin
	* Ravaged leather clothing patched with fur and rope
* Always smells of hay, sweat, and beasts
* Stall is chaotic but functional:
	* Several mounts tied or resting
	* Hay piles everywhere
	* Buckets, brushes, iron hooks, salves, ropes
* Often seen:
	* Feeding mounts
	* Calming aggressive beasts
	* Cleaning hooves with brutal efficiency

---

	Primary Functions

	1. Mount Hosting (Island-Specific System) :

* Players can host / stable mounts here
* Hosting is required to:
	* Safely log out with mount
	* Prevent mount fatigue penalties
	* Maintain mount loyalty on this island

	Cost (No Gold)

Hosting requires a special item, not money.
	Required Item: "Sun-Dried Beast Fodder"

*(Island-specific currency item)*

	Description:

* Bundled, dried feed mixed with local herbs and bone dust
* Smells awful, mounts love it

	How to Obtain :

* Dropped from:
	* Rhynos
	* Cragwallowers
	* Large island beasts
* Crafted by:
	* Herbalists + Hunters (optional later system)
* Found rarely in wilderness chests

	Usage : 

* Hosting a mount consumes:
	* 1–3 Fodder (based on mount size & rarity)
* Rare mounts require more

---

	2. Mount Care & Recovery : 

* Restores:
	* Mount stamina
	* Loyalty decay
* Removes:
	* Fear effects
	* Combat stress
* Care time scales with mount rarity

---

	3. Mount Behavior Adjustment : 

* Can temporarily:
	* Reduce mount noise
	* Improve handling
	* Stabilize aggressive mounts
* Effects last **24–48 hours

---

	4. Emergency Mount Retrieval : 

* If mount is lost, injured, or stranded:
	* Mount Master can retrieve it
	* Costs large amount of "Sun-Dried Beast Fodder"
	* Cooldown applies

---

	5. Mount Interaction Flavor : 

* Players can:
	* Watch him train mounts
	* See failed attempts
	* Witness mounts rejecting weak handlers

---

		Hidden / Advanced Mechanics

	Mount Recognition : 

* If player hosts same mount repeatedly:
	* Mount Master acknowledges it
	* Reduced fodder cost
	* Faster services

---

	Smuggler Interaction : 

* Smugglers may:
	* Request illegal mount transport
	* Bribe Mount Master with rare fodder
* Visible only to players in Smuggling System

---

	Invasion Behavior : 

* During town invasions:
	* Releases mounts from stalls
	* Some mounts may:
		* Rampage
		* Defend players
		* Flee into wilderness
* Hosted mounts have higher survival chance

---

	Behavior & AI : 

* Rough, loud, intimidating
* Respects:
	* Hunters
	* Players with well-treated mounts
* Dislikes:
	* Neglectful players
	* City folk who complain about smell
* Will shout at guards if they interfere with beasts

