			Kalden Ironbolt - Emergency Supply Officer

---

	Location : Market / Town Outskirts (Zone 4, near Ballista platforms and stockpiles)

---

	Appearance : 
* Wears a brown leather apron over sturdy wool clothing, slightly worn from constant labor
* Short-cropped hair, streaked with gray, rugged face with a pair of protective goggles on forehead
* Carries a tool belt with hammers, measuring tools, and vials
* Boots are mud-stained, gloves leather-bound, fingers marked with black soot and mineral dust
* Always has scrolls and maps of supply stockpiles tucked in pockets

---

	Primary Functions : 
* Assigns and tracks Emergency Supply Stockpile quests
* Coordinates town defense stockpiles: projectiles, mining materials, healing supplies
* Escorts NPC supply caravans (when part of quests)
* Provides REP rewards to players for completing supply missions
* Monitors supply routes and may trigger hidden mechanics if players use special interactions (water, firepots, LCWM mechanics)

---

	Behavior : 
* Walks carefully around stockpiles, inspecting barrels and crates
* Stops at Ballista platforms, checking the projectiles and firing readiness
* Occasionally shouts reminders to players about stockpile safety or materials needed
* During emergencies, patrols the town, moving from stockpile to stockpile, interacting with players who volunteer
* Can react dynamically if stockpile is attacked: calls guards and signals players

---

	Mechanics / Skills : 
* Assigns quests to collect mining materials, sap, leaves, stones for ballista projectiles
* Can escort NPCs or player-contracted supply caravans; NPC has HP bar for attacks from wildlife
* Supplies can trigger hidden REP bonuses if environmental mechanics are exploited
* Interacts with LCWM points: water, terraces, balloons can boost quest efficiency
* Tracks completion for daily/weekly rotations

---

	Quests : 
* Daily and weekly supply missions (Emergency Stockpile Quests)
* Tasks: collect materials, escort NPCs, deliver stock to other settlements or siege points
* Rewards: REP, crafting materials, chance for rare items or buffs

---

	Story : 
* Branik has been assigned to this town after serving as a technician during the construction of the Sankis Outpost in Outland Territories
* He is meticulous, likes to see everything in order, and treats players as apprentices in logistics
* Dreams of one day establishing a permanent supply network across islands

