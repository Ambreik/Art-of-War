			Kalden Ironbolt - Emergency Supply Quests

	Quest 1 – Stones for Projectiles

* Objective : Collect 50 Rounded Stones from the cliffs near "Sunveil Market Town", use them to craft projectiles for the ballista.
* Mechanic : Stones are found in small clusters; collecting triggers a minor slippery ground hazard (~pushback if careless).
* Reward : 150 REP, 5 Silver, 1 Random Crafting Material

---

	Quest 2 – Harvesting Tree Sap

* Objective : Gather 5 Vials of Sticky Sap from tallest trees around Sunveil Market.
* Mechanic : Players must climb or use LCWM points to reach sap; chance of falling if multiple players are on the same tree (small knockback).
* Reward : 200 REP, 10 Silver, 1 Minor Healing Potion

---

	Quest 3 – Wood Planks Delivery

* Objective : Collect 10 Large Wood Planks from the forests and deliver to Ravenwatch Encampment.
* Mechanic : Player escort; path may include wandering Cragwallowers (random encounter)
* Reward : 250 REP, 15 Silver, Minor Crafting Material

---

	Quest 4 – Wild Leaf Collection

* Objective : Gather 3 Giant Forest Leaves from deep Ravenwatch Forest.
* Mechanic : Rare spawn points; leaves may be guarded by small herbivores (passive hazard).
* Reward : 200 REP, 5 Silver, 1 Special Ingredient for Ballista Projectiles

---

	Quest 5 – Emergency Shipment

* Objective : Deliver a package of supplies from Sunveil Market to Southfall Village within 20–30 minutes (no flying allowed).
* Mechanic : Timed delivery; random wandering NPCs may block paths, causing minor delays.
* Reward : 350 REP, 20 Silver, 1 Small Mount Repair Kit

---

	Quest 6 – Flying Shipment

* Objective : Deliver special ballista ammo to Stormreach Watch using Flying Master services.
* Mechanic : Only available during daytime; player must pay normal flying cost or use a special token.
* Reward : 200 REP, 15 Silver, 1 Projectile Upgrade Component

---

	Quest 7 – Quarry Minerals

* Objective : Collect 100x Raw Ore from The Sundrop Quarry and return to Branik for testing in the ballista projectiles.
* Mechanic : Escort mini-mechanic: NPC assists player but can be damaged by wildlife (HP bar shown). UI tracks total ore collected.
* Reward : 400 REP, 30 Silver, Chance for Rare Crafting Material

---

	Notes / Mechanics Integration :

* All quests give REP toward town, reinforcing the Emergency Supply Stockpile mechanic.
* Some quests integrate LCWM / hidden interactions (sap, stones, projectile crafting).
* Quest locations are fully island-spread, encouraging players to traverse Sunveil → Ravenwatch → Southfall → Stormreach.
* Delivery quests include timed challenge and escort mechanics, adding tactical depth.

