			Crafting Test Duel Mechanic — Bromir Ironfist

		Overview : 

	This mechanic allows players to test newly crafted equipment in a controlled duel with Bromir. It is intended to provide feedback on the quality and performance
of crafted items while being a fun, interactive spectacle for other players in Sunveil Market Town.

---

	Trigger : 
* Player crafts a new item (weapon or armor) at Bromir’s workshop.
* Only items crafted on-site can trigger the duel.
* Once crafted, a 5-minute window begins where the player may challenge Bromir.
* NPC announces:
	*“Ah, a new piece! Let’s see how it fares in the field. Step forward for a proper test!”*

---

	Location : 
* Duel occurs in front of Bromir’s blacksmith store.
* Boundaries defined by marked duel zone (10–15 meters around forge entrance).
* NPC assistants, guards, and spectators appear automatically and watch the duel.

---

	Player Interaction : 

1. Player **initiates duel** from NPC dialogue menu.
2. Bromir accepts automatically:
	* Dialogue: *“We’ll test this properly — no shortcuts!”*
3. Player equips the crafted item; Bromir equips a standard test weapon or armor.
4. Optional: Player may invite up to **3 friends** to watch or participate as sparring partners if duel is cooperative.
5. Duel ends when:
	* Player defeats Bromir’s AI (simulated challenge rating)
	* Bromir defeats player
	* 5 minutes expire

---

	Duel Mechanics : 

* Bromir AI Behavior :
	* Uses realistic combat simulation for weapon/armor type
	* Defensive and offensive skills match the crafted item class
	* Minor randomness to simulate “human-like testing”
* Spectators :
	* NPC assistants and guards comment on duel progress
	* Guards will not intervene, unless player moves outside duel zone
* Environmental Interaction :
	* Nearby anvils, tools, and workshop props are immune to collision
	* Duel occurs safely without harming NPC or damaging environment

---

	Feedback / Outcome : 

* After duel, Bromir provides verbal feedback based on performance:
	* *Excellent performance*: “This weapon is perfectly balanced. Well done!”
	* *Moderate performance*: “Solid work, but it could be sharper.”
	* *Poor performance*: “Hmm… needs refinement. Try again.”
* Optional temporary buff to crafted item in combat for 5–10 minutes post-duel, to encourage real-world testing outside the duel zone.
* **XP / Reputation : Players may earn minor REP with Bromir for participating in duel successfully.

---

	Hidden Interactions : 

* If player uses rare or unusual crafted items, Bromir comments specifically:
	* *“Ah, an exotic alloy! Very interesting…”*
* Spectators may trigger small reactions based on outcome (cheers or whispers).
* If player performs exceptionally, NPC may unlock bonus dialogue or short story snippet.

---

	Spectator Behavior : 

* Assistants comment during duel:
	1. “Notice how he balances the swing…”
	2. “That armor withstands well — impressive!”
* Guards and town NPCs gather at edges, react dynamically:
	* If player nearly defeats Bromir: “Goodness, he’s skilled!”
	* If Bromir dominates: “Masterful work, as expected!”

---

	Safety Rules : 
* Player cannot die in duel
* Bromir cannot die in duel
* Other players cannot interfere in duel zone
* Duel is purely testing mechanic, no rewards for PvP kills

---

	Reward / Incentive : 
* Player gains direct feedback for crafted item
* Temporary combat enhancement on tested item (optional)
* Minor REP increase with Bromir
* Spectator reaction / flavor dialogue adds immersion

---

	Visual / UI Cues : 
* Duel menu appears after crafting, with “Challenge Bromir” button
* Countdown timer for 5-minute duel window
* Marked duel zone with subtle visual indicator
* NPC dialogue pops up dynamically during duel

