			Bromir Ironfist - Blacksmith Trainer


	Location : Sunveil Market Town

* Large blacksmith store with multiple furnaces, anvils, and worktables
* Two assistants always present, helping with crafting and sales
* Store cluttered with tools, metal ingots, and finished goods
* Occasionally interacts with players testing crafted items

---

	Visual Description : "Bromir" is a stout dwarf, broad-shouldered with a braided dark beard streaked with gray. His hands are calloused, but precise — every 
hammer strike is perfect. He wears a leather apron, reinforced with metal plates, and his eyes reveal intelligence and experience.

* Assistants: two younger dwarves (male/female), running minor errands and tidying tools
* Always surrounded by the smell of coal and molten metal

---

	Personality :
* Kind, gentle, and serious about craftsmanship
* Respects skill and effort in players
* Protective of his assistants and his store
* Encourages creativity and quality in crafting
