			Bromir Ironfist Quest Chain — Sunveil Market Town

	Quest 1 — “Supplies from Afar”

	Objective :
* Acquire "Salted Water" from the northern side of the island.
* Gather "Waste Rock" from The Sundrop Quarry (southern region).
* Collect "Old Weapon Fragments" scattered in the quarry.

	Mechanics & Notes :
* Salted Water: Can be found near northern rivers; some areas guarded by local wildlife.
* Waste Rock: Mined from Sundrop Quarry; requires minor mining skill.
* Old Weapon Fragments: Spawn rarely in quarry, drop guaranteed only if quest is active.

	Rewards :
* Burning Iron Orb x3 — usable in crafting or temporary buff item.

	Flavor / Dialogue :
* “Ah, the quality of my forge depends on your legs as much as my hands! Bring me the best water and stone, and maybe, just maybe, your efforts will be rewarded.”
* “Do not mix up the old weapons with junk. Precision matters!”

---

		Quest 2 — “Rhino Horn Harvest”

	Objective :
* Hunt "Rhinoceroses" in the forested areas.
* Collect 3 Strong Horns.

	Mechanics & Notes :
* Rhinos spawn very slowly (1 per group every few minutes).
* Drop rate for horns is low — encourages persistence.
* Solo players may need multiple attempts; groups can coordinate.

	Rewards :
* 2x Magic Broom Sweep OR 1–3x Magic Steel (randomized reward, scalable by effort).

	Flavor / Dialogue :
* “Those beasts are tougher than my apprentices’ attention spans! Bring me their horns, or do not return.”
* “Ah, a horn well-earned! Your skill grows sharper, I see.”

---

		Quest 3 — “Crusader Shield Samples”

	Objective :
* Enter the "Tomb of Great Crusaders".
* Collect shield samples from "Crusader Soldiers" wielding shields.
* Drops only if quest is active.

	Mechanics & Notes :
* This is the only way to acquire the shield items needed for Bromir’s study.
* Encourages dungeon participation.

	Rewards :
* Ticket for entry into "Tomb of Great Crusaders"
* Very low chance for Primary Slot Bag (GOLD)

	Flavor / Dialogue :
* “Ah, now you venture where steel meets steel! Study those shields well; the quality will tell tales of the Crusaders’ discipline.”
* “Bring me the samples, and perhaps the Tomb itself may open its secrets to you.”

---

	Quest Flow : 
1. Q1 → Completion triggers Q2.
2. Q2 → Completion triggers Q3.
3. Q3 → Final reward issued, opens advanced crafting and dungeon access opportunities.

---

	Optional Mechanics to Integrate : 

* Certain items (horns, old weapon fragments) can only drop if the player has the active quest, preventing farm exploits.
* Rhinos: rare spawn reinforces exploration and patience.
* Shield sample quest ties Bromir’s crafting and testing mechanics with dungeon mechanics, giving more immersion.

