* “Your weapon could be deadly…if handled properly.”

	Event / Combat:
* “Sharpened and ready—don’t let anyone surprise you!”
* “Every swing counts; waste none!”
* “Stay behind me if you don’t want a blade in your back!”

	Cross-NPC Interaction:
* “Thalric, you old flirt! Go polish your armors and leave my sister alone!”
* “Kiera, don’t let him fool you—armor can’t save a fool from a sword!”

---

	Placement / Notes :
* Located in a weapon shop adjacent to Accessories Merchant, near the market hub.
* Interaction radius for training: ~5 meters.
* Reacts dynamically to Armor Merchant and Accessories Merchant’s presence.

