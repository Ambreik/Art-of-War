			Roran Steelbane - Weapon Merchant NPC

	Appearance :
* Tall, broad-shouldered human male, wears a dark leather vest with steel studs, heavy boots, and gauntlets.
* His shop is cluttered with swords, axes, maces, and bows, some hanging, some on racks.
* Always carries a small whetstone and a dagger on his belt, constantly polishing weapons.
* Stern expression, short black hair, slight scar over left eyebrow, always frowning at Armor Merchant.

---

	Primary Functions :
* Weapon Sales : Sells swords, axes, maces, bows, and crossbows, staff, fan, bell and others.
* Repair Services : Repairs weapons for a fee; speed depends on material quality.

---

	Behavior :
* Always grumbling about Armor Merchant: “Old fool thinks shiny plates make a warrior…they don’t!”
* Protects his sister (Accessories Merchant) from Armor Merchant’s flirtatious attempts.
* Constantly adjusts weapons in the shop, swings training swords to test balance.
* Occasionally steps outside to check his supply delivery or scold apprentices.

---

	Mechanics :
* Reputation-Based Discounts : Completing town quests or buying regularly unlocks minor discounts.
* Weapon Quality Check : Crafted weapons can be inspected; high-quality results may unlock hidden effects 
(e.g., chance to trigger extra damage, minor elemental effect).
* Sibling Rivalry Dialogue : Dialogue and reactions dynamically change depending on proximity of Armor Merchant or Accessories Merchant.

---

	Skills :
* Weapon Assessment : Buffs a player’s damage for a short duration after inspection.
* Quick Sharpen : Temporarily improves attack speed for player’s weapon for 5 minutes.

---

		Quotes :

	Idle / Shop Interaction:
* “Looking for a real weapon, not some tin-plated junk?”
* “Step carefully, every sword here has a story…and a sharp edge!”
* “Armor Merchant again? He’ll be talking to the wrong person if he thinks women like shiny plates!”
* “Kiera, stay away from him, or I’ll be forced to cover your ears!”

	Player Interaction / Training:
