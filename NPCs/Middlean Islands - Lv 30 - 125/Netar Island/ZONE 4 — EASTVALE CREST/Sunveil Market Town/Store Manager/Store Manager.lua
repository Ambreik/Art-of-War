			STORE MANAGER

**Role : Core Town Utility NPC
**Location : Sunveil Market Town – Near central store building
**Faction : Neutral / Town Authority
**Combat Role : Non-combat (protected by dogs & guards)
**Persistence : Permanent NPC

---

	Appearance : 

* Well-dressed, wealthy-looking man
* Taller than his sister (General Merchant Lady), similar facial features
* Clean, refined clothing with fur-lined coat and leather gloves
* Always seated on a sturdy wooden chair beside his store entrance
* Gold-trimmed ledger always on his lap
* Two large guard dogs sit on either side of him, alert and calm

---

	Primary Functions (Core Systems NPC)

	1. Item Storage System : 
* Allows players to store items in a personal virtual storage.
* Storage access is character-bound.
* Requires a one-time fee per character to unlock base storage.

	Storage Expansion Mechanics :
* Players can expand storage by:
	* Using "Storage Expansion Tokens" → permanently unlock additional pages.
	* Using "Grand Ledger of Holding" (account-bound):
		* Grants 10 shared storage windows
		* Duration options:
			* 30 days
			* 60 days
			* 90 days
			* Permanent (very rare quest reward)

---

	2. Currency Conversion
* Converts Silver → Gold
* Conversion applies a small service fee
* Used heavily by traders, smugglers, and high-level players.

---

	3. Bounty Management System : 
* Players may place a bounty on another player’s head.
* Requires:
	* Gold payment
	* Minimum level requirement
	* Cooldown to prevent abuse
* Bounty details are registered into the world bounty system.

---

	4. Item-Shop Inventory : 
* Manages each character’s private Item-Shop Inventory.
* Inventory is:
	* Character-specific
	* Invisible to other players
	* Accessible only through this NPC.

---

	5. Cash Holding : 
* Holds up to 1000 Gold per character for free
* Acts as a secure gold vault
* Excess gold may require a fee or special items (future expansion).

---

		Guard Dogs – Mechanics

	General Behavior : 
* Two large, trained guard dogs:
	* Always remain near the Store Manager
	* Constantly scan nearby players
* Growl subtly at suspicious behavior.

---

	Detection & Protection : 

* Dogs react to:
	* Pickpocket attempts
	* Aggressive actions near the Store Manager
	* Players lingering too long without interaction
* Can:
	* Bark loudly (alert guards)
	* Temporarily root or slow hostile players
	* Reveal stealth briefly

---

	Hidden Mechanic – Feeding the Dogs

* Players can feed dogs with "Fresh Meat".
* Effects:
	* Dogs remember the player
	* Player gains “Marked as Friendly” status for this NPC

	Long-Term Effects :
* During invasions:
	* Store Manager may despawn
	* Dogs remain active
	* Dogs will:

		* Assist the town
		* Protect fed players within radius
		* Escort wounded players briefly

---

	Invasion & Emergency Behavior :

* When town invasion triggers:
	* Store Manager despawns safely
	* Dogs remain and assist guards
	* Dogs prioritize:
		1. Fed players
		2. Low-health players
		3. Town NPCs

---

	Behavior & AI :
* Calm, observant, rarely speaks unless interacted with
* Will not engage in combat personally
* Dogs handle all threat responses
* During high crowd density:
	* Dogs reposition
	* Guards may be summoned automatically

