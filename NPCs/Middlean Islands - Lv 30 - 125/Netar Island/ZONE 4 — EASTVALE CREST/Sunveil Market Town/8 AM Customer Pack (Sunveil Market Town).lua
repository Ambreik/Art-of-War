			8 AM Customer Pack (Sunveil Market Town)

	General Info : 
* Name / Group Name : Morning Customers
* Spawn Type : Time-Based / Location-Specific
* Spawn Time : 08:00 – 09:30
* Group Size : 6–8 NPCs, mixed genders
* Zone / Location : Market & Residential Terraces
* Persistence : Spawned only during this time, despawn afterward

---

	Appearance : 
* Clothing: Colorful tunics, aprons for traders, simple shoes
* Accessories: Baskets, satchels, walking sticks, small pouches
* Distinctive Features:
	* One NPC has a straw hat and a green scarf
	* One elderly woman carries a walking stick and a basket
	* One young man has a satchel over his shoulder and carries a small hammer
* Each NPC slightly different in height, body type, and hair color to feel unique

---

	Behavior / AI : 
* Movement Route :
	1. Spawn at town entrance near main gate
	2. Randomized wandering along market terraces for 5–10 minutes
	3. Enter stalls or small houses (interact animation) for 2–3 minutes
	4. Leave town at the back exit near the river or walls after 20–30 minutes
* Actions / Animations :
	* Inspecting items at stalls
	* Talking to merchants
	* Sitting/standing idle near fountains
	* Reacting to nearby environmental mechanics (balloons, firepots, minor chaos)
* Group Interaction : Avoid clustering; they naturally form small pairs or triplets while walking
* Interaction With Player :
	* Player may overhear gossip or rumors
	* Players can interact for small trade or purchase of basic goods
	* If player causes chaos nearby, NPCs flee to nearest safe point

---

	Mechanics / Triggers : 
* Interaction Triggers :
	* Player trading near stalls triggers dynamic NPC movement (crowd shifts)
	* If player helps an NPC carry an item, chance to receive minor reward or "Merchant’s Gratitude Token"
* Environmental Triggers :
	* Heat shimmer at noon: NPC slows down and shields eyes
	* Fog/Mist: NPCs talk about weather and huddle near buildings
* Hidden Interactions :
	* If player positions near a specific terrace corner and makes a loud shout or ability: NPCs will scatter, potentially exposing a hidden mechanic (LCWM)
	* NPC noticing a rare “item drop” on the ground may call attention to player, unlocking small secret dialogue

---

	Quotes / Chatter : 
* Ambient Chatter :
	* “Did you hear about the last caravan from Stormreach Watch?”
	* “The baker’s fresh bread smells heavenly today.”
	* “Watch your step, the cobblestones are slippery.”
* Player Interaction Quotes :
	* “Care to lend a hand with these goods?”
	* “Ah, a traveler! You might find what you need at the east stalls.”
	* “Please, don’t block the aisle.”
* Hidden Interaction Quotes :
	* “Did that basket just move on its own?” (if LCWM triggered)
	* “Strange… I could’ve sworn I heard voices from nowhere.”

---

	Rewards / Loot (Optional) : 
* Minor EXP if interacting or assisting NPC
* Chance to receive "Minor Crafting Materials" (like herbs or small fish)
* "Merchant’s Gratitude Token" occasionally

---

	Notes / Misc : 
* Synchronized with other town NPC mechanics (guards, crowd, shimmer, fog)
* Can participate in emergent LCWM interactions if triggered by player actions
* Spawned as a time-based, wandering pack with randomized short routes for realism

