			Harlan Stitchfist - Tailor Trainer Profession



	Location : Sunveil Market Town
* Hut next to Flying Stall
* Often glances nervously at "General Merchant Lady" while working
* Limited roaming within hut and nearby terrace area
* Always near Flying Stall / Market hub

---

	Visual Description : "Harlan" is a young, rough-looking human in his early 30s. His hands are calloused and stained from constant tailoring work. His tunic and 
apron are patched and worn. His expression is often gruff or frustrated, especially when dealing with high-level players.
	When he notices General Merchant Lady nearby, he blushes,glances away, and mutters under his breath, showing a shy, nervous side contrasting his rough exterior.

---

	Personality : 
* Rude and impatient with high-level players
* Often takes longer than expected for crafting, sparking rumors of incompetence
* Secretly shy and nervous around the General Merchant Lady
* Reacts dynamically to player quest actions (flowers collected correctly or incorrectly)
* Holds grudges for poor quest results: crafting delays, specific dialogue

---

	Primary Function : 
* Tailor Profession Rank III : crafting armor, garments, and specialty outfits
* Vendor : sells cloth, tailoring tools, and pattern recipes
* Questing NPC : Chain quest involving flower bouquet for General Merchant Lady

---

		Mechanics / Interactions

	Crafting Delay / Penalty Mechanic :

* Normal crafting takes longer than average for Rank III (adjusted by +1-3 minutes randomly)
* If player provides bad flowers for the bouquet quest:
	* NPC remembers the player
	* Adds up to 5 minutes extra crafting time for any services
	* Dynamic quotes triggered when the player interacts

	Quest Mechanics – Flower Bouquet :
* Quest: Collect flowers for General Merchant Lady
* Flower types:
	1. "Wild Terrain Rose" (poisonous) — common, spawn anywhere, player often chooses these without knowledge
	2. "Terrain Rose" — rare, 3 spawns per location every 5–8 hours
* Optional: Player consults "Herbalist NPC"
	* Herbalist provides exact flower type and spawn timing
	* Herbalist may request a favour for information
* Outcomes:
	* Correct flowers → chain quest continues to collect materials / crafting
	* Incorrect flowers → poison problem, must return to Herbalist, NPC remembers and penalizes crafting

	Dynamic Memory / Behavior :
* Tracks players who failed the flower quest
* Modifies dialogue and crafting time for a period after failure
* Quotes reflect mild annoyance / grudge

