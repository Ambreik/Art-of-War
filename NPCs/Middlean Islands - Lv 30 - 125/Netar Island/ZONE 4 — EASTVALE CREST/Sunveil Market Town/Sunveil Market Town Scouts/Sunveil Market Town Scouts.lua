		Scout NPCs

	1. Kaelen Swiftfoot : 

* Appearance : Slim build, mid-20s human male. Wears soft green leather armor for blending with terrain. Short hooded cloak, lightweight boots for silent steps. Carries a shortbow and a small dagger at his belt. Has a quiver slung over shoulder. Dark brown hair tied back in a short ponytail.
* Role : Forward observer, specializes in tracking enemy movement and reporting back to town walls.

	2. Elira Windtracker : 

* Appearance : Athletic elf female, light leather armor with blue accents to match the morning sky. Long, flowing hair tucked under a leather circlet. Small pack for tools, binocular-like spyglass. Carries a recurved bow and throwing knives for stealth attacks. Agile, moves quickly through forests and ridges.
* Role : High vantage reconnaissance, monitors terrain for incoming threats and reports on creature movement near the town outskirts.

	3. Tovan Cliffstride : 

* Appearance : Stockier human male, wears reinforced leather armor with earthy tones. Short cloak that can double as a camo wrap. Carries a hand crossbow and a small shield for quick skirmishes. Balding with a graying beard, giving him a veteran look. Boots with extra grip for climbing rocky surfaces.
* Role : Ground-based scout, excels at following tracks, observing patterns of enemy patrols, and guiding incoming player reinforcements.

	Functions / Interactions for all Scouts :
* Move along designated scouting paths outside the town.
* Alert guards or archers when enemies are detected.
* Provide information for quests or emergent events (e.g., Cragwallower ambushes, Crusader waves).
* Can interact with players in escort or reconnaissance quests, sometimes giving buffs or advice.
* Limited combat: primarily hit-and-run attacks, retreats if overpowered.

	Behavior / AI Mechanics :
* Respond dynamically to environmental conditions (fog, wind, glare).
* Use stealth / concealment to avoid detection while scouting.
* Can relay information to other NPCs or trigger events if spotting enemy units.

