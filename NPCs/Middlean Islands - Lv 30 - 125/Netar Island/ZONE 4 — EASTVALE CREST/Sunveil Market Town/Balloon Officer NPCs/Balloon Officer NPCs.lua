			Balloon Officer NPCs

	NPCs Names : 

1. "Captain Theron Steelwing"
2. "Lieutenant Valric Hammerfall"
3. "Sergeant Isolde Ironspire"
4. "Officer Brannor Stonehold"
5. "Lieutenant Kaelen Stormshaft"

---

	Appearance (General / Individual Notes) : 

* General : Solid plate armor, polished steel with bronze accents; long spears with banner streamers; leather boots; helmet with visor; belts with pouches for small tools. All carry a small satchel for balloon tools and signaling flags.
* Captain Theron Steelwing : Tall, broad-shouldered, plume on helmet, stern look.
* Lieutenant Valric Hammerfall : Muscular, scar across left cheek, slightly hunched from heavy armor.
* Sergeant Isolde Ironspire : Female, agile stance, visor slightly lifted showing determined eyes.
* Officer Brannor Stonehold : Heavyset, thick beard, helmet with small horns.
* Lieutenant Kaelen Stormshaft : Lean, wiry, quick reflexes, carries a map rolled in a tube strapped to back.

---

	Primary Functions : 
* Balloon Operation : Can pilot / man defensive balloons; can launch small explosive or incendiary projectiles from balloon platforms during invasions.
* Defensive Combat : Patrol balloon platforms or walls; engage enemies with AoE spear attacks.
* Player Assistance : Can buff nearby players during invasions (minor attack or defense boost).
* Skillable : Players may train / duel with officers if high enough level and authorized by town events.

---

	Behaviors : 
* Patrols balloon platforms and terrace edges during invasions or training drills.
* Moves in synchronized group formations during combat.
* During idle times, checks balloon ropes, maintains gear, or interacts with NPC engineers.
* Can respond to alarm signals from bell towers or LCWM triggers.
* Prioritizes AoE attacks on clustered enemies while avoiding allies.

---

	Mechanics : 
* AoE Spear Attack : Hits enemies in 3–4 meter radius; applies knockback but does not harm friendly NPCs or players.
* Balloon Deployment : Officers may move to balloon platforms and perform hot air balloon ascend / deployment, allowing other NPCs or players to interact with balloons safely.
* Emergency Response : During invasions, officers automatically move to hotspots and may trigger small AoE buffs for nearby defenders (attack +5%, defense +5%).
* LCWM Synergy : Officers react to "Echo Mechanics", Firepots, and Wind Drafts. For example, if a player triggers a hidden firepot combo, officers may reposition to the affected area or fire counter-projectiles.

---

	Skills : 
* Spear Sweep (AoE) : Sweeps in front of officer, moderate damage, knockback.
* Spear Thrust : Single-target high damage, pierces armor.
* Battle Command : Buffs nearby allies for 10–15 seconds (+5% attack/defense).
* Ballista / Balloon Launch Prep : Interacts with balloon platform mechanics to enable special projectiles or deploy minor AoE hazards.
* Quick Reposition : Short burst movement, avoids obstacles, used in terraces or balloon platforms.

