			Sunveil Market Town Guards

	Names : 

* Thalen Ironshield
* Roderic Broadhelm
* Kaelin Stonewatch
* Darvos Redbanner
* Elric Frostguard
* Jorn Valewatch

	Appearance / Visuals :

* Uniform: Pale steel chestplate with bronze trim and Sunveil crest on the chest.
* Helm: Rounded steel helmet with a short visor; plume color matches town colors (orange and white).
* Cloak: Orange trimmed with white edges, fastened with a bronze clasp.
* Accessories: Leather belt with pouches for scrolls, small weapons, and keys; boots are reinforced leather; gauntlets polished steel.
* Weapons: Standard issue longsword and small round shield; some carry crossbows for ranged patrols.
* All guards have a similar posture, disciplined and alert, standing straight but capable of moving quickly.

	Functions / Interactions :

* Patrol town walls, gates, and streets in dynamic routes.
* Respond to player-triggered events (balloon use, artillery firing, alarms).
* Assist NPCs or escort important NPCs if attacked.
* Observe player suspicious behavior (loitering, pickpocket detection, blackmarket interactions).
* Can challenge or block access to restricted areas temporarily.
* Provide minor combat support during Crusader invasions or emergent events.

	Behaviors / AI Mechanics :

* Patrol routes dynamically change depending on alert state: passive → suspicious → alert.
* Alert levels influenced by: player noise, balloon/ballista mechanics, crowd chaos, environmental conditions (fog, shimmer).
* Can temporarily block roads or paths if town laws are broken.
* Cooperation between guards: adjacent guards respond to alarms from fellow guards within a radius.
* Guards automatically engage low-level threats, flee or call for reinforcements for stronger threats.

	Skills / Abilities (for mechanics and event integration) :

* "Shield Bash" : Stuns small enemies or weak monsters for 2–3 seconds.
* "Whistle Call" : Summons nearby guards to their location.
* "Defensive Stance" : Reduces damage taken while in defensive formation around NPCs or gates.
* "Alert Signal" : Boosts guard awareness in the area for 10–15 seconds.

