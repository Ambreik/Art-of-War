			4 PM Children Pack (Sunveil Market Town)

	General Info : 

* Name / Group Name : Evening Playmates
* Spawn Type : Time-Based / Location-Specific
* Spawn Time : 16:00 – 17:00
* Group Size : 2 children
* Zone / Location : Town Entrance & Central Courtyards
* Persistence : Spawned only during this time, despawn afterward

---

	Appearance : 
* Clothing: Simple, playful attire (short tunics, patched pants, light dresses)
* Accessories: Handcrafted leaf ball, small sticks, toy wooden figures
* Distinctive Features:
	* One child with curly brown hair, freckles, and a mischievous smile
	* Second child with straight black hair, slightly shy, follows the other
* Footwear: Worn leather sandals or barefoot

---

	Behavior / AI : 

* Movement Route :
	1. Spawn near town entrance
	2. Wander toward central square or fountain area
	3. Play near low walls, benches, and steps
	4. Occasionally chase ball around town square, interacting with each other
	5. Despawn after 60 minutes or fade near home residences

* Actions / Animations :
	* Rolling / kicking handmade leaf ball
	* Jumping on low walls or steps
	* Laughing and waving at players passing by
	* Sitting briefly on benches to rest

* Interaction With Players :
	* Players can kick/throw the leaf ball → triggers mini-game interaction
	* Players may fetch the ball if it goes out of reach → NPC thanks player, small EXP reward
	* NPCs react to player actions like jumping or running nearby

* Interaction With Environment :
	* Ball can knock over small crates or items → triggers chaos/crowd mechanics
	* If near fountains → may splash water → can interact with water/slippery mechanics
	* Avoid areas with large crowds or ongoing combat

