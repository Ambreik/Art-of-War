			Sunveil Market Town Archers

	Names :

* Lyrian Swiftbow
* Faelan Arrowwind
* Merric Shadowstring
* Tovan Skyshot
* Elsin Hawkeye
* Jorik Longaim

	Appearance / Visuals :

* Uniform: Light leather armor dyed in town colors (orange and white) for mobility.
* Helm: Leather cap with small visor, plume optional (orange).
* Cloak: Short, hooded, allows free movement of arms.
* Accessories: Quiver on the back with 20–30 arrows; utility belt with minor tools, daggers, or small potions.
* Weapons: Longbows with reinforced limbs; some carry short swords for close encounters.
* Boots: Soft leather, designed to be silent on stone or wood surfaces.
* All archers have a tall, lean posture, capable of rapid movement along tower ramps and parapets.


	Functions / Interactions :
* Stationed on walls, terraces, and watchtowers during day and alert events.
* Patrol upper walkways and parapets when town is under no threat.
* Engage enemies from range, including invading "Crusader monsters", "Cragwallowers", and other hostile creatures.
* Coordinate with guards on the ground; can provide fire support for defensive mechanics like ballistas and terraces.
* React dynamically to player and environmental mechanics (fog, wind draft, glare).

	Behaviors / AI Mechanics :
* Adjust shot trajectory based on wind draft and sun glare mechanics.
* Prioritize attacking highest threat enemies or those closest to civilians/important NPCs.
* Can switch to melee if an enemy reaches the parapet.
* Communicate with other archers to focus fire or call for support from nearby guards.
* Can be affected by fog / shimmer mechanics, temporarily reducing accuracy.

	Skills / Abilities :

* "Precision Shot" : High damage single-target arrow, ignores a portion of enemy armor.
* "Volley" : Fires multiple arrows in an area (~3–4 meters radius) for AoE damage.
* "Flaming Arrow" : Optional incendiary attack when triggered by environmental mechanics (firepot, hot air balloon combo).
* "Tactical Call" : Alerts ground units to enemy approach, boosting guard awareness nearby.

