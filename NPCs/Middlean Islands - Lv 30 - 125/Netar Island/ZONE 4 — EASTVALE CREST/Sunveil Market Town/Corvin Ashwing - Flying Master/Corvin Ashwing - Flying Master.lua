			Corvin Ashwing - Flying Master

	Location : Sunveil Market Town — Flying Stall Platform (Eastvale Crest, Zone 4)

---

		Visual Description : 

	Corvin Ashwing is a lean, sharp-featured human with wind-tanned skin and layered flight gear that has clearly seen many seasons in the skies above Netar Island.
His attire is built for resilience — reinforced leather with feather-like stitching — and always includes a pair of tinted flight goggles resting atop his brow, 
only rarely lowered. Around him, giant trained birds perch dutifully on roosts, their harnesses bearing Sunveil’s transit sigils.
	Corvin’s gaze is steady and focused, constantly flicking between sky and horizon, as if tracking the unseen movements of wind currents and the distant pull of 
trade routes.

---

	Personality & Demeanor : 

* Professional and unflappable — Corvin rarely wastes words or expresses emotion outside necessity.
* Punctual and disciplined — his sense of time is as exacting as the flight paths he oversees.
* Respectful of seasoned adventurers — though rarely impressed, he gives nods of approval to those who clearly know the skies well.
* Dislikes recklessness — he is quietly intolerant of those who treat flight as a shortcut without considering risk.

	Despite his composed exterior, Corvin is known to loosen slightly with familiar travelers, offering tips about seasonal winds or rare route opportunities.

---

		Primary Role

	Corvin Ashwing is the "Flying Master" for "Sunveil Market Town". In this capacity he:
* Coordinates all sanctioned aerial travel from this settlement.
* Oversees route selection and validation, ensuring all travelers meet conditions before flight.
* Handles mount assignment, limited to giant birds authorized for cross-island travel.
* Ensures players are informed of restrictions such as the island boundary enforcement and authorized flight paths.

	All long-distance aerial travel — including progression to Ravenwatch Encampment, Southfall Village, and Stormreach Watch — begins with Corvin’s oversight.

---

		Interaction Mechanics

	When interacting with Corvin, players have access to the following options:
* Request Aerial Transport
* View Available Destinations
* Inquire About Flight Restrictions
* Step Away / Decline

	Corvin’s dialogue will adjust based on:
* Time of day
* Smuggler influence conditions
* Global events (e.g., festivals, invasions)

	He reflects the mechanics defined externally in zone mechanics, such as:
* Flying Stall economic system, including currency options and pricing logic
* Route selection system
* Island boundary enforcement
	These systems are not owned by Corvin, but he interfaces with them narratively and functionally when selling services.

---

		Restrictions & Conditions

	Corvin refuses service when any of the following is true:
* Player is flagged as hostile or wanted
* Flying Stall services are temporarily disabled (e.g., during major alarms)
* Requested destination is physically or mechanically blocked
* Player is attempting to evade the island’s flight boundary rules

	He does not :
* Provide personally-owned mounts
* Offer personal flight training
* Allow route changes mid-flight

---

		World Integration

	Corvin’s service is deeply integrated with the world systems:
* Coordinates with Town Guardians and local authorities
* Flight schedules subtly shift based on:
	* Time of day and daily cyclic shifts
	* Regional threat levels
	* Active events (festivals, invasions)
    These shifts are reflected in flight availability and pricing rather than direct dialogue exposition.

* During night-time smuggler influence windows, Corvin may mention discounted rates or whispers about alternative gear/hiring conditions, without flagging any 
reputation loss.

---

	Dialogue Examples : 
* “Destination first. Once you’re airborne, there’s no turning back.”
* “These birds don’t hesitate. Neither should you.”
* “If you intend to jump mid-flight, make sure your affairs are in order.”
* “The sky remembers mistakes longer than the ground does.”
* (At night, if smuggler conditions apply) “Quiet winds tonight… some say it’s better to travel under cover of dusk.”

---

