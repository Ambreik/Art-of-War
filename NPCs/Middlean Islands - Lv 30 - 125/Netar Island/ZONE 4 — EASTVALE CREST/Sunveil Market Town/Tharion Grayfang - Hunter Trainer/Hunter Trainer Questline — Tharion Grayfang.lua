			Hunter Trainer Questline — Tharion Grayfang

	Quest Tree Overview : 

* NPC : Tharion Grayfang
* Location : Sunveil Market Town / Outskirts
* Quest Focus : Hunting local threats, protecting NPCs, collecting rare drops, assisting with monster control
* Profession Restriction : Only players with Hunter profession active (buffs and guidance apply)

---

	Quest 1: Patrolling the Shore

* Objective : Hunt 8 Sea Crokodils along the eastern shore of Eastvale Crest.
* Details :
	* Crokodils are aggressive near the shore, threatening fishermen.
	* Player is instructed to thin their numbers before they attack NPCs.
* Reward :
	* EXP: Moderate
	* Gold: Small
	* Item: “Hunter’s Tracking Feather” (+2% rare mob awareness for 30 min)
* Hidden Interaction : If the player hunts all Crokodils without getting hit, a small rare drop appears: “Crokodil Scale” for crafting.

---

	Quest 2: Cragwallower Interference

* Objective : Track and kill 12 Cragwallower Scouts (prefix: Scout) that are roaming near the outskirts.
* Details :
	* Tharion explains that these scouts harass merchants and occasionally attack town outskirts.
	* Optional: Player can observe their patrol patterns and trigger an emergent ambush if timed correctly.
* Reward :
	* EXP: Moderate
	* Gold: Moderate
	* Item: “Cragwallower Fang” (crafting or trade item)
* Hidden Interaction : If player lures Cragwallowers toward Tharion’s hunting traps, they can gain a bonus Hunter Buff (+5% attack speed) for the next 10 min.

---

	Quest 3: Crow Harassment

* Objective : Deal with the Crows at Sunwell Basin that disturb local fishermen.
* Details :
	* Player can track, scare off, or kill crows (small mobs).
	* Optional: Use environment creatively (e.g., nearby fire pits or scare devices) to disperse them without killing.
* Reward :
	* EXP: Low–Moderate
	* Gold: Small
	* Item: “Crow Feather” (crafting/trade)
* Hidden Interaction : Using environmental methods (non-lethal) awards a special title : “Protector of Sunveil Fishermen.”

---

	Quest 4: Merchant Escort

* Objective : Assist a caravan of NPC merchants ambushed by Cragwallowers while returning from Sunwell Basin to Sunveil Market Town.
* Details :
	* NPC merchants are carrying valuable trade goods.
	* Player must defeat the Cragwallower raiders (use Scouts, Berserks, or Elites for variation).
	* NPC merchants follow a patrol route, and timing is important; if merchants are delayed too long, quest fails.
* Reward :
	* EXP: High
	* Gold: Moderate
	* Item: “Merchant’s Gratitude Token” (can be turned into unique crafting material or quest chain)
* Hidden Interaction : If player kills all raiders before they reach merchants, extra reward: rare drop from Cragwallowers (+chance for special crafting item).

---

	Quest 5: Elite Cragwallower Hunt (Optional / End of Tree)

* Objective : Hunt 1 Cragwallower Berserk (suffix: Berserk) roaming near the forest outskirts.
* Details :
	* Berserk is elite and aggressive; recommended for group participation.
	* Requires strategy: Tharion can assist with tagging and highlighting the target.
	* Drops are high-value crafting items.
* Reward :
	* EXP: High
	* Gold: High
	* Item: “Berserk Fang” (crafting)
* Hidden Interaction : If the player uses Hunter buffs + Tharion’s aid, there’s a chance to obtain a rare cosmetic pet or mount item.

---

	Quest Progression / Tree Notes

* Linear with optional branching :
  1 → 2 → 3 → 4 → 5
* Emergent mechanics : Player actions can alter the flow of ambushes or spawn locations 
(e.g., Cragwallower Scouts may appear more frequently if previous quest is ignored).
* Integration with Tharion Mechanics : Player receives buffs, pet healing, and guidance throughout the tree.

