	Quotes / Dialogue Examples :

1. General Greeting :
	* “Stay sharp, friend. The forest doesn’t forgive carelessness.”
	* “A hunter’s eyes must always be open, even when the sun rises.”
	* “Your pet is loyal, but even the loyal can be caught unprepared.”

2. While Assisting Players :
	* “Keep your distance, aim steady — let the wolf do some work.”
	* “A clean shot is better than ten reckless arrows.”
	* “Focus on the target, not the chaos around you.”

3. Dawn Social / Town Interactions :
	* “I prefer the wild, but even hunters need rest.”
	* “Herbalist, old friend, how fares your little companion today?”
	* “The tavern smells different in the morning — less blood, more bread.”

4. Pet Interaction / Buff :
	* “Your wolf seems eager — channel that energy.”
	* “Keep your companion safe; it’s as much part of the hunt as you are.”
	* “Speed is key, but patience wins the prey.”

	Story / Lore :

* Grew up in forests near Sunveil, trained in tracking and survival from childhood.
* Formed a strong bond with his gray wolf companion, raised from a pup.
* Became Hunter Trainer to mentor aspiring hunters and protect the town’s surrounding wilds.
* Known to venture alone for dangerous hunts, returning with rare knowledge and resources for students.

