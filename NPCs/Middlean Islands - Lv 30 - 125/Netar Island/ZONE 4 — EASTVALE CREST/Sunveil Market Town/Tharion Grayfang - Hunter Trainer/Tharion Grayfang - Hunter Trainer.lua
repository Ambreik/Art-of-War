			Tharion Grayfang - Hunter Trainer

	Location :
* Primary : Roaming outskirts of Sunveil Market Town (often near forests and hunting grounds).
* Secondary : Visits the town at dawn, stops at Tavern or Herbalist NPC for social interactions.

	Appearance / Visuals :
* Tall and lean, muscular build.
* Wears rugged leather armor with hunter’s insignias.
* Carries longbow and quiver with assorted arrows.
* Always accompanied by a big gray wolf pet, loyal and aggressive when threats are near.

	Personality / Behavior :
* Strict, disciplined, and highly motivated.
* Respects skill and preparation; scolds careless players.
* Passionate about tracking elite monsters and teaching hunting strategies.
* Friendly but reserved with town NPCs; occasionally shares hunting stories.

	Functions / Interactions :

1. Training :
	* Hunter profession skills (tracking, ranged attacks).
	* Provides temporary attack speed buff to players who are Hunters when in his radius.
	* Can heal pets of players within his radius.

2. Combat Assistance :
	* Roaming NPC; can assist players with monsters in his radius.
	* Pet attacks nearby hostile creatures, especially elite or rare beasts.

3. Skilling / Profession :
	* Provides quests related to hunting rare or elite monsters.

