			Kiera Steelbane - Accessories Merchant NPC

	Appearance :
* Elegant young woman, mid-20s, long auburn hair tied in a braid.
* Wears a finely embroidered green and gold tunic, leather boots, and a belt with pouches holding rings, amulets, and gems.
* Her stall is neatly arranged with jewelry, rings, necklaces, trinkets, and magical charms.
* Always smiling, polite, and friendly to players, but occasionally rolls eyes at her brother’s scolding or Armor Merchant’s flirting.

---

	Primary Functions :
* Accessory Sales : Rings, amulets, necklaces, trinkets, charms, earrings. Some may provide minor buffs when equipped.
* Trading : Can trade accessories for rare components or special items, if players meet certain conditions.

---

	Behavior :
* Extremely friendly and polite to players, encourages them to try her wares.
* Occasionally flustered by Armor Merchant’s flirting, while secretly amused.
* Protective of her brother Roran, especially when he scolds or teases.
* Keeps her stall organized meticulously, often rearranging trinkets or checking gem quality.
* Moves around the stall and interacts with players dynamically, nodding or pointing to items.

---

	Mechanics :
* Sibling Interaction Mechanics :
	* Will react to Roran nearby (friendly glances, acknowledgment).
	* Reacts to Armor Merchant flirting with dialogue and gestures.
* Special Discounts / Hidden Mechanics :
	* Regular players, or those who have completed certain quests, may receive minor discounts.
	* Rare chance to unlock “Hidden Accessory Crafting” when interacting with both her and her brother.
* Skillable NPC Interaction : Players can gain temporary buff or knowledge in accessory enchantment.

---

	Skills :
* Charm of Vitality : Temporary buff to player health regen.
* Protective Aura : Minor resistance buff to allies near her stall for a few minutes.

---

		Quotes :

