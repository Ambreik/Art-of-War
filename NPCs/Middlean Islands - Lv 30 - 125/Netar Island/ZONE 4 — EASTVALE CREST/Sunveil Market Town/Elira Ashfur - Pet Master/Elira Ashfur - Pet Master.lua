			Pet Master - Elira Ashfur

**Relationship : Wife of the Mount Master
**Location : Sunveil Market Town – adjacent to the Mount Stall
**NPC Type : Profession & Companion Specialist
**Faction : Neutral / Town Civilian
**Skillable : Yes

---

	Appearance : 

* Graceful, calm presence
* Long hair, usually braided, decorated with feathers or small bones
* Wears:
	* Soft leathers
	* Herbal pouches
	* Pet charms
* Always surrounded by:
	* Small animals
	* Pets resting near her
* Animals feel safe around her instinctively

---

		Core Functions

	1. Pet Hosting & Care

* Temporarily hosts pets when players:
	* Log out
	* Enter restricted zones
* Improves:
	* Pet happiness
	* Loyalty
	* Recovery speed

	Cost :
* Uses the same "Sun-Dried Beast Fodder"
* Smaller pets cost less

---

	2. Pet Revival & Trauma Recovery

* Can:
	* Revive pets without penalties
	* Remove fear, panic, or obedience debuffs
* Time-based recovery (not instant for rare pets)

---

	3. Pet Bond Enhancement

* Temporary buffs:
	* Faster response
	* Reduced chance to flee
	* Improved combat coordination
* Stronger effect if player frequently uses this NPC

---

	4. Pet Training (Non-Combat Skills Only)

* Teaches pets:
	* Fetch
	* Guard
	* Follow patterns
	* Calm reaction in towns
* Does NOT teach combat abilities
	(keeps balance with Hunter / future Trainer NPCs)

---

	5. Smuggler Awareness (Hidden)

* She knows.
* Never says it directly.
* Smuggler pets behave calmer around her.
* Occasionally offers quiet discounts at night.

---

		Unique Hidden Mechanics

	Pet Recognition : 

* Pets remember her.
* Returning pets may:
	* Emote
	* Gain small loyalty boost
* Some pets refuse service elsewhere if bonded strongly here.

---

	Invasion Behavior : 

* During town invasions:
	* She releases hosted pets
	* Pets assist nearby players defensively
	* Some pets escort civilians

---

	Mount Master Synergy : 

* If both NPCs are active:
	* Mount + Pet services cost slightly less
	* Faster processing times
* If one is absent (event-related):
	* Services become slower

