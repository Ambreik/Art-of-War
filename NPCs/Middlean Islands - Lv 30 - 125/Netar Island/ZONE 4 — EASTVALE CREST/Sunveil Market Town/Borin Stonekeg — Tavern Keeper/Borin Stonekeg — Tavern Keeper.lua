			Borin Stonekeg — Tavern Keeper

*(Widely known simply as “Stonekeg” — even guards use the surname)*

---

	Appearance : A stocky dwarven male with a broad chest and thick arms, his beard braided with iron rings stained by ale foam and smoke. His face is ruddy, nose 
slightly crooked, and eyes sharp despite constant exposure to drink and noise.

	He wears:
* A heavy leather apron reinforced with metal studs
* Rolled-up sleeves revealing scarred forearms
* A belt full of tankards, corks, and small keys
* Always smells faintly of malt, smoke, and oak barrels

	Behind the counter, a massive hammer hangs — not decorative.

---

	Location : 
* Inside the main tavern of the town
* Always behind the bar during evenings and nights
* At dawn, he is seen cleaning, restocking, or arguing with suppliers

---

	Personality : 
* Gruff but fair
* Excellent memory for faces
* Zero tolerance for trouble
* Deep respect for regulars

	He does not flatter players — he evaluates them.

---

		Primary Functions : 

	Shop (Beverages & Food)

* Sells:
	* Beverages (alcoholic & non-alcoholic)
	* Simple meals
	* Rare tavern-only consumables (condition-based)

---

	Rest & Social Anchor

* Acts as a soft respawn / recovery hub
* Some effects:
	* Faster fatigue recovery
	* Minor morale buffs
	* NPC rumor propagation

---

		Visitor Tracking System (Hidden)

	Stonekeg tracks player attendance, not via UI.

		Mechanics:

* Each visit is logged per character
* Visits count only if:
	* Player stays ≥ 3 minutes
	* Player interacts (buy, talk, sit)

	Thresholds:

* 3 consecutive days → unlocks hidden beverage
* 7 visits total → new dialogue tone
* Repeated long absences → dialogue cools off

---

	Hidden Beverage: "Stonekeg’s Private Reserve"

	Unlock Condition : 
* Player visits the tavern 3 days in a row

	Properties : 
* Extremely strong
* One drink triggers "Tipsy State"

	Tipsy State : 
* Duration: 15 minutes
* Effects:
	* Slight camera sway
	* Altered footstep timing
	* Occasional delayed input feel
	* NPC dialogue feels “off” (subtle tone change)

> No direct combat advantage — purely experiential and social.

---

	Skillable NPC — Tavern Defense

* Stonekeg is fully skillable and actively defends the tavern.

	Trigger Conditions : 

* Player starts a fight
* Drunken NPC brawl escalates
* Hostile ability used indoors

	Behavior : 
* First warning (verbal)
* Second offense:
	* Immediate stun
	* Forced ejection from tavern
* Repeated offenses:
	* Temporary tavern ban
	* Guard notification

	Combat Style : 
* Short-range control
* Knockbacks
* Stuns
* Non-lethal damage

> Guards respect his authority — they intervene only if he fails.

---

		Additional Mechanics

	Drunk Memory Effect : 

If a player is "Tipsy":

* Stonekeg may:
	* Reveal rumors
	* Slip half-truths
	* Hint at hidden NPC behaviors

	Some information is:
* True
* Incomplete
* Intentionally misleading

---

	Tavern Reputation (Hidden)

	Stonekeg internally tracks:
* Fights caused
* Drinks bought for NPCs
* Quests started via tavern info

	Higher reputation:
* Faster service
* Occasional free drink
* Access to rare tavern events

---

	Bar Presence Mechanic

	If a player:
* Sits at the bar long enough
* Does not AFK
* Observes conversations

	They may overhear:
* Scout reports
* Merchant gossip
* Dungeon rumors

	This does not trigger quests directly — it unlocks options elsewhere.

---

	Tavern Lockdown

	During invasions or unrest:
* Tavern doors shut
* Windows barred
* Stonekeg becomes a defensive anchor

	Players inside:
* Receive minor protection buffs
* Can assist in defense

---

