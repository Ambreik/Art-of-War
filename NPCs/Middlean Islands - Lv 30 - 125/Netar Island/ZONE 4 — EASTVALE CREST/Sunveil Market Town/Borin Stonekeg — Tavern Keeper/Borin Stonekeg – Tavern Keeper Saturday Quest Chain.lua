			Borin Stonekeg – Tavern Keeper Saturday Quest Chain

	Prerequisite : Must be Saturday, only repeatable weekly.
	
	Reward Overview :
* Small amount of EXP
* Gold
* Tavern Hidden Beverages unlocks for a day (bonus effect)
* Minor reputation boost with town NPCs

---

	Quest 1 – The Missing Herbs

* Objective : Collect 5x "Frostmint Leaves"
* Source : Found in northern cliffs of "Sunwell Basin" near shallow streams
* Description :

> "Borin says: ‘I need these leaves to brew a special tonic for the winter… don’t crush them, careful now.’"

	Mechanics :
* Leaves have a low spawn rate (1–2 per patch)
* Can be gathered solo or in a small party

	Notes : First quest unlocks chain, introduces player to the gathering mechanic for tavern goods.

---

	Quest 2 – Sweet Sap for the Stock

* Objective : Collect 3x "Golden Maple Sap"
* Source : Tall trees around "Ridge Crest Terraces"
* Description :

> "Borin says: ‘The sap must be fresh. Only the tallest trees give the right sweetness. Mind the bees!’"

	Mechanics :
* Bees are environmental hazard (AoE attack if player gets too close)
* Sap can only be harvested with a special vial, sold at Borin’s shop

---

	Quest 3 – Frosty Ice Crystals

* Objective : Mine 5x "Ice Crystal Shards"
* Source : Found along glacial rock outcrops in "Vaelthorn Frontier"
* Description :

> "Borin says: ‘These shards keep the drinks cool, naturally. Don’t drop ‘em!’"

	Mechanics :

* Each shard requires mining skill check
* Random chance to encounter "Water Elementals" while harvesting (combat)

---

	Quest 4 – Exotic Fruit Delivery

* Objective : Gather 10x "Sunveil Berries"
*  Source : Shrubs near "Eastvale Crest Market outskirts"
* Description :

> "Borin says: ‘These berries add punch to my secret recipes… better not let the crows get them.’"

	Mechanics :
* Berries attract crows that may swoop on players
* If all 10 are collected without losing more than 2, bonus reputation awarded

---

	Quest 5 – Rare Honeycomb

* Objective : Retrieve 1x "Moonlit Honeycomb"
* Source : Special bee nests in "Stormreach Watch Hills" (harder-to-reach)
* Description :

> "Borin says: ‘Ah, the last ingredient for this week. Only the bees under the moon give this quality. Quick now!’"

	Mechanics :
* Nest guarded by bee swarm mini-event (AOE damage)
* Only available during evening/night time
* Completing this quest triggers hidden beverage unlock for players

---

	Chain Completion**

* Player returns all items to "Borin Stonekeg"
* Rewards:
	* 300–500 Gold
	* 1 Hidden Beverage unlocked (can consume immediately or sell)
	* 50–75 EXP
	* 1% Town REP

* Optional: Random Bonus :
	* Chance to receive a small recipe fragment used for crafting unique beverages

