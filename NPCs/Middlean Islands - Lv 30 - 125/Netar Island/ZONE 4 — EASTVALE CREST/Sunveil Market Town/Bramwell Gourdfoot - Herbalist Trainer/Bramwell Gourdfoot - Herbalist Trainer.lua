			Bramwell Gourdfoot - Herbalist Trainer

	Location : Sunveil Market Town

* Small, cluttered hut on Market Terrace
* Tables stacked with herbs, vials, dried roots, and herbalist tools
* Turtle pet roaming freely, occasionally knocking over ingredients or bottles

---

	Visual Description : "Bramwell Gourdfoot" is a rotund, elderly human with a gentle face framed by white whiskers and a bald crown. His robe is patched and 
stained with herbal residues. He moves very slowly, deliberately, as if each step must be carefully measured.
	A pet turtle wanders his hut, knocking over ingredients and creating minor chaos. He is usually seen tending his herbs, grinding roots, or stirring cauldrons 
with herbal concoctions.

---

	Personality : 
* Kind, patient, and gentle, especially toward players’ pets
* Loves interacting with animal companions
* Humorous about the mess his turtle makes
* Encourages careful crafting and experimentation with herbal ingredients

---

	Primary Function : 
* Crafting : Potions, soups, remedies, and other herbal consumables
* Training : Herbalism Profession Rank III
* Vendor : Sells herbalism tools, ingredients, and basic potions
* Pet Interaction Mechanic :
	* If a player has a summoned pet nearby, random 5% discount applies
	* Quote: “I like your relationship with the little one, take 5% discount”

---

	Mechanics / Interactions : 

* Pet Discount Mechanic : Triggered when a player’s pet is present
* Turtle Pet Chaos : Occasionally knocks over items, creating minor animations in hut
* Crafting / Training :
	* Recipes scale with player rank and trainer level
	* Teaches potions, soups, and specialty consumables

