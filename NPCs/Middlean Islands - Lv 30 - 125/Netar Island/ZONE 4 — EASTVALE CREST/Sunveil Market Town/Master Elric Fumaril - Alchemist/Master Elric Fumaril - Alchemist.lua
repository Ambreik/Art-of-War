			Master Elric Fumaril - Alchemist

	Location : Sunveil Market Town

* Small alchemy house in Market Terrace
* Interior: cluttered with tables, bottles, flasks, herbs, and distillation apparatus
* Exterior: small signage with bubbling cauldrons

---

	Visual Description : "Master Elric Fumaril" is an elderly human with a long, white mustache and deep-set eyes that glint behind round spectacles. He wears a red
magician-style hat, patched robes, and leather gloves stained with alchemical residues.
	When surrounded by more than three players, he occasionally crushes volatile potions, causing a spectacular explosion of smoke, sparks, and colorful dust, 
temporarily obscuring him and nearby tables.

	His workshop is aromatic with herbs, alchemical fumes, and faintly glowing vials.

---

	Personality : 
* Curious, slightly eccentric, but brilliant
* Passionate about experimentation
* Playful in demonstration, but serious about crafting and training
* Enjoys surprising players with harmless “explosions” during crowded moments

---

	Primary Function : 
* Disenchanting : Break down items to extract magical essence
* Enchantment : Infuse weapons, armor, or tools with magical effects
* Crafting : Create potions, elixirs, and alchemical consumables

---

	Mechanics / Interactions : 
* Can interact with players for crafting lessons or services
* Crowd interaction:
	* When >3 players are in proximity, explosion animation triggers
	* Effects: visual dust/smoke; no damage to players or items
* Training unlocks recipes and abilities based on profession rank

