			Tharok Ironshield - Town Guardian NPC

	Appearance :

* A tall, broad-shouldered man in his late 50s.
* Wears polished steel plate armor with the town crest engraved on the chest.
* Wields a large tower shield and longsword, both battle-worn but meticulously maintained.
* Short gray hair, thick beard, stern expression, and a few visible scars from past battles.
* Carries a utility belt with maps, signaling flares, and small potions for emergencies.

	Personality / Behavior :
* Highly disciplined, calm under pressure, and very protective of the town and its inhabitants.
* Observant and authoritative; players and NPCs follow his guidance during emergencies.
* Interacts warmly with trusted players and teaches respect through example.
* Acts as both healer and buffer during town invasions.

	Functions / Interactions :
1. Skillable : Players can train basic town defense strategies or leadership buffs here.
2. Map Assistance : Can point out the location of key NPCs in town (trainers, Flying Master, Smuggler, Tavern Keeper, etc.).
3. Invasion Support :
	* When Crusader or Cragwallower events occur, Tharok automatically casts AoE protective buffs on players nearby.
	* Can apply minor healing over time to players suffering DoTs (bleed, burn, poison).
4. Escort Assistance : Players on quests in the town or nearby outskirts can trigger Tharok to follow or assist temporarily.
5. Player Guidance : Can give strategic hints for mechanics like hot air balloon use, ballista fire, and terraces.

	Mechanics / Unique Features :
* Guardian’s Aura : Constant passive radius (~10 meters) giving minor defense and damage reduction to friendly players.
* Tactical Flare : Can place signaling flares during events to mark enemy positions or alert NPCs (triggers guard patrol AI).
* Shield Wall Formation : Temporarily boosts nearby NPCs’ defense during town defense events.
* Healing Intervention : Activates a “Guardian’s Intervention” skill during active raids, removing one negative status effect (burn, poison) from players within range.
* Interactive Map : Players may speak to him to reveal dynamic waypoints for quests or mechanics in the town, such as hot air balloons, ballistas, or underground paths.
* Dynamic Player Feedback : If a player acts suspiciously (loiters too long near NPCs or is flagged by Smugglers), he may approach and warn, preventing AFK abuse or suspicious behaviors.

		Quotes (by situation)  :

	Patrolling / Guidance :
* “Stay close and watch your flanks.”
