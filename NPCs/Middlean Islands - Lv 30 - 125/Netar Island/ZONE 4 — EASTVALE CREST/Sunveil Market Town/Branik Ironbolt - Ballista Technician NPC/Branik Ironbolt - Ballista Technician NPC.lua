			Branik Ironbolt - Ballista Technician NPC (Sunveil Market Town)

	General Info : 

* Name : Branik Ironbolt
* Role : Ballista Technician & Projectile Crafter
* Location : Workshop near upper terraces, close to ballista platforms
* Function : Skillable NPC, provides crafting of ballista projectiles and upgrades, daylies for materials collection
* REP : Completing tasks increases player reputation with Sunveil Market Town

---

	Appearance : 

* Clothing / Gear :
	* Leather apron with metal clamps and straps
	* Utility belt full of small tools (hammers, chisels, vials)
	* Goggles resting on forehead
	* Fingerless gloves and sturdy boots
* Hair / Facial Features :
	* Short, messy brown hair with streaks of soot
	* Small mustache
	* Soot-stained hands and arms
* Accessories :
	* Satchel with schematics
	* Small wooden board with measurements clipped to belt

---

	Behavior / AI : 
* Stays in workshop unless moving to inspect ballista platforms
* Interacts with nearby ballista for calibration checks
* Occasionally looks frustrated at players who interrupt while working
* Can give tutorial dialogues about ballista operation and projectile crafting

---

		Quest System : 

	Duration : 3 weeks of daylies (5 quests per week, player may miss days)
	Reward : REP with town and minor materials (or special item on rare chance)

		Daily Quest Pool Examples : 

1. Stone Collecting
	* Collect 20 Stone Fragments from cliffs or quarries nearby
	* Stones must be undamaged (bonus REP if pristine)
   
2. Plank Gathering
	* Collect 10 Oak Planks from trees in northern forest
	* Rare bonus: 1 extra plank from a “giant oak” spawn

3. Giant Leaf Collection
	* Collect 5 Giant Leaves from deep forest areas
	* Leaves used to wrap ballista projectiles
	* Stealth requirement: avoid herbivore encounters for extra REP

4. Tree Sap Harvest
	* Collect 3 vials of sap from tall trees
	* Can only harvest sap during morning or late afternoon

5. Ballista Ammunition Assembly
	* Combine previous materials into projectiles for ballista
	* Requires crafting skill check; success grants REP

6. Special Quest: Reinforced Projectiles (once per week)
	* Player gathers rare rocks (Lumen Quartz) + giant leaves + extra sap
	* Craft special reinforced projectiles → bonus REP + minor cosmetic reward

---

	Mechanics / Triggers : 

* Player Activity :
	* Material collection contributes to daylies, increases REP with town
* Emergent Mechanics :
	* While assembling projectiles, if player misplaces materials near terrace edge → may trigger ballista accident LCWM (minor AoE, harmless for demo)
* Hidden / Bonus :
	* Rare chance that correct sequence of materials produces enhanced ballista ammo, usable in actual town defense event

---

		Quotes / Chatter

	Ambient / Workshop :
