	General Look : 

* Constructed of weathered bronze and black iron with Sunveil Market Town crest emblazoned
* Shape is slightly curved like a scroll-case standing on two sturdy legs
* A glowing crystal lens sits atop the mailbox, pulsing faintly at intervals
* Multiple slots and compartments:
	* Letters slot (top)
	* Parcel drawer (bottom)
	* Special enchantment rune embedded on side
* A small bell and feather quill sits beside it — indicating readiness to deliver or retrieve mail

	Ambient Flavor : 

* Mail jangles when opened, as if full of whispers
* Crystal lens flickers brighter at dawn and dusk
* Near this mailbox, nearby NPCs occasionally say related lines like:
	* “Did you send your thanks yet?”
	* “I expect word by sunset.”
	* “Often the mailbox brings news… and secrets.”

---

		Core Functions (Player Utility)

	1. Mail Sending

	Players can send:
* Messages to other players
* Items (with restrictions)
* Notes to NPCs with flavor text

	UI Features : 
* Recipient selector (friends / within guild / mail code)
* Item attachment (cost in Silver + local item fee)
* Message window

	Costs : 
* Silver for basic mail
* Rare stamps can be crafted/earned to:
	* Remove delivery delay
	* Add decorative flair
	* Add enchantment effects

---

	2. Parcel Retrieval : 

	Players can receive:
* Stored items
* Gold
* Letters
* Craft materials

	Notifications appear when mail is available.

---

	3. Quest Mail : 

	Certain quests deliver:
* Story letters
* NPC instructions
* Serialized narrative handoffs via mail

---

	4. NPC Feedback Loop : 

	Some NPCs will:
* Respond to letters
* Initiate side quests
	  Example:
* Write to Mount Master about lost mount and receive a tracker
* Write to Herbalist for hints on rare herb timings
* Write to Tharion Grayfang about elite mob sightings

---

		Mechanical Rules

	Delivery Times : 

* Standard mail: 24 in-game hours
* Express stamps: 6–12 in-game hours
* Special event mail: instant (rare)

---

	Item Attachment Logic : 

	Allowed:
* Non-bound items
* Craft materials
* Quest items (if flagged)
	Not Allowed:
* Soulbound items
* High-tier boss drops (rare exceptions via special stamps)

---

		Unique Mechanics & Interactions

	Mailbox Echo & Secret Messages : 

The crystal atop occasionally captures “echoes” — fragments of messages from other planes, past events, or historical whispers.

	Triggered by :
* Dusk / Dawn
* Fog / Mist events
* Player in specific locations / LCWM anchors

	Effect :
It reveals a mysterious line of text in local chat:

> “The ridge remembers when wind spoke…”

	These echoes can:
* Hint at hidden quests
* Trigger LCWM activations
* Provide clues for secret interactions

	Echo messages only appear when:
* Player approaches mailbox at key times
* A specific mechanic is active
* On rare random intervals

---

		Special Runic Slot

	Players may insert a "Rune of Request" — a world item that:
* Gives access to limited NPC responses
* Rewards rare items
* Activates chain quests

	Example:
* Insert Rune of Echo
  → Mailbox “speaks”
  → Reveals hidden shop mechanic for a day
  → NPC merchants discount rare goods

---

		Hidden Interactions

	1. Graveyard Echo : 
* If mailbox is activated at night (without NPCs nearby), it may whisper cryptic lore.
* Activates an event chain if player deciphers message.

	2. Flame-Stamped Mail : 
* Drop letter with Flame Stamp near Ballista/Firepot
* Mailbox vibrates and grants rare fire-infused mail items

	3. Wind-Sealed Message : 
* During strong wind draft events, mailbox glows, allowing special mail that:
	* Reduces delivery time
	* Adds hint text for hidden quests


