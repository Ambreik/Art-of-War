			2. Flame-Stamped Mail Interaction

	(Risk / Elemental / Craft-linked interaction)

	Core Fantasy : Certain messages demand fire — not destruction, but transformation.

---

	Trigger Conditions : 

* Player carries:
	* Any sealed letter, parcel, or blank parchment
* Nearby heat source:
	* Firepot, forge, brazier, siege weapon, or invasion flame
* Mailbox must be interacted with within 10 seconds of touching fire

---

	Phase I – Heat Reaction : 

	When opening mailbox after heat exposure:
* UI flickers
* Mailbox emits low crackling sound
* New option appears: “Imprint with Flame”

---

	Phase II – Flame Choice : 

	Player selects intensity of flame:
1. *Smolder* – Safe
2. *Brand* – Risky
3. *Inferno Seal* – Dangerous

	Each level increases:
* Reward quality
* Chance of item loss or backlash

---

	Phase III – Outcome Roll : 

	Possible Results : 

* "Flame-Stamped Letter"
	* Faster delivery
	* Can trigger NPC urgency responses
* "Charred Contract"
	* Used for mercenary / bounty systems
* "Ember Parcel"
	* Item inside gains minor fire affinity
* "Ash Memory"
	* Consumable lore fragment
* "Burned Void"
	* Item destroyed (Inferno risk)

---

	Special Interaction : 

	If done during:
* Town invasion
* Siege preparation
* World fire event

→ Flame-Stamped mail can:

* Rally NPCs
* Trigger emergency quests
* Reduce enemy reinforcement timers

---

	Failure State : 

	Overusing Inferno Seal:
* Mailbox locks flame option for a full in-game day
* NPC guards comment on the “reckless courier”

