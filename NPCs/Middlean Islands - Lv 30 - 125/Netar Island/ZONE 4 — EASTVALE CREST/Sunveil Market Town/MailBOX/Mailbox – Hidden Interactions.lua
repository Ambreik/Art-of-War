			Mailbox – Hidden Interactions


			1. Graveyard Echo Interaction

	(Night / Silence / Memory-based interaction)

	Core Fantasy : The mailbox remembers messages meant for the dead — letters never delivered, last words unsent, and regrets bound in ink.

---

	Trigger Conditions : 

* Time: Night only (22:00 – 04:00)
* Weather: Fog, Mist, or Light Rain increases chance
* No hostile NPCs nearby
* Player stands idle near mailbox for 6–10 seconds
* No recent mailbox interaction (cooldown ~30 minutes)

---

	Phase I – The Echo : 

The crystal lens dims instead of glowing.
A faint text appears (not voiced, subtle UI text):

> *“Some letters were never meant to arrive.”*

	Mailbox UI subtly changes:
* Normal send/retrieve options are hidden
* New option appears: **“Listen”**

---

	Phase II – Echo Selection : 

When the player chooses "Listen", one of several "Echo Types" is randomly selected:

	Echo Types : 
1. Lost Soldier
2. Forgotten Merchant
3. Unanswered Love
4. Broken Oath
5. Nameless Grave

	Each echo displays a short, fragmented message:

> *“…I never returned… tell her the ring is buried…”*
> *“…the path behind the mill… don’t trust the bells…”*

---

	Phase III – Player Response : 

	The player must choose how to react:
* *Preserve the Letter* (store it)
* *Release the Words* (burn the echo)
* *Rewrite the Ending* (write a response)

	Each choice affects:
* Town reputation
* Future mailbox echoes
* Possible quest availability

---

	Outcomes : 

* Preserve : 
  → Unlocks a delayed side quest tied to a grave, ruin, or NPC descendant
* Release : 
  → Grants a temporary buff (*“Quiet Resolve”* – DoT resistance)
* Rewrite : 
  → Alters a minor NPC’s dialogue or behavior permanently

	Some echoes return later, referencing how the player handled them.

---

	Failure State : 

Leaving during the echo causes:

> *“The words scatter like ash.”*

Echo is lost permanently.

