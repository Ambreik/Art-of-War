			3. Wind-Sealed Message Interaction

	(Timing / Environment / Exploration-based interaction)

	Core Fantasy : The wind carries words farther than feet ever could.

---

	Trigger Conditions : 

* Strong wind weather OR coastal gust event
* Player stands facing wind direction
* Player holds a message or blank scroll
* Must interact exactly as wind sound peaks

---

	Phase I – Wind Alignment : 

	Mailbox crystal spins slightly.
	UI displays subtle indicator (no explicit prompt):

> “The wind presses against the seal…”

	New hidden interaction becomes available:
“Let the Wind Carry It”

---

	Phase II – Directional Choice : 

	Player does not choose a recipient.

	Instead, the player selects:
* "Release Upwind"
* "Release Crosswind"
* "Release Downwind"

	Each corresponds to:
* Mountains / inland
* Nearby islands / coast
* Sea / distant regions

---

	Phase III – Wind Fate : 

	Mail becomes unaddressed.

	Possible delayed outcomes (hours or days later):
* NPC scout arrives with reply
* Item returns altered
* Quest starts elsewhere on island
* Message never returns (lost forever)

---

	Unique Rewards : 

* "Wind-Torn Reply"
	* Contains coordinates or riddles
* "Salt-Stained Letter"
	* Leads to sea-based content
* "Feather-Marked Scroll"
	* Grants speed or travel buffs
* "Silent Return"
	* Blank parchment, but unlocks hidden map markers

---

	Advanced Interaction : 

	If combined with:
* Scout NPC reputation
* Town Guardian assistance
* Certain mount travel routes

→ Wind mail can pre-unlock zones or bypass quest prerequisites.

---

	Failure State : 

	Releasing wind mail during calm weather:
* Scroll simply drops
* Player receives warning message:

> “The wind refuses to listen.”

