			Core World-Memory System
			
---

		I. Mailbox Echo & Secret Messages

	*(World Memory / LCWM Signal System)*

---

		Core Concept

	The mailbox crystal is a passive world receiver.
	It does not generate messages — it captures echoes produced elsewhere in the world.

	Echoes are:
* Not player-generated
* Not repeatable on demand
* Influenced by time, weather, world state, and LCWM anchors

	Think of this as *the world whispering through infrastructure*.

---

		Echo Classification

	Each echo belongs to a type, which determines its function:

	1. Environmental Memory Echo

> “The ridge remembers when wind spoke…”

	Source : 
* World geography
* LCWM anchor activation
* Terrain-based events

	Function : 
* Hints at:
	* Hidden paths
	* Conditional spawns
	* Non-obvious traversal mechanics

---

	2. Historical Echo

> “Three banners fell before the gate was sealed.”

	Source : 
* Past invasions
* Failed world events
* Locked legacy content

	Function : 
* Unlocks:
	* Forgotten quest chains
	* Ruined NPC stories
	* Altered dungeon states

---

	3. Interplanar Echo

> “They answered before you asked.”

	Source : 
* Other planes
* Echo-heavy zones
* Rune-based interference

	Function : 
* Activates:
	* Special vendors
	* Unusual item drops
	* Temporary rule-breaking mechanics

---

		4. Player-Triggered Residual Echo

> “Someone waited here. Not you.”

	Source : 
* Another player’s completed secret action
* LCWM chain completion
* Rare cooperative echoes

	Function : 
* Encourages:
	* Player curiosity
	* Non-direct cooperation
	* Shared mystery without direct interaction

---

		Trigger Conditions

	An echo may surface only when multiple layers overlap:

	Temporal Layer : 
* Dawn / Dusk (strongest)
* Midnight (rare)
* Storm thresholds

	Environmental Layer : 
* Fog, Mist, Ashfall, Heavy Wind
* Seismic tremor aftermath
* World event residue

	Positional Layer
* Player near:
	* LCWM anchor
	* Watchtower
	* Old road
	* Forgotten ruin
* Player standing still for ≥ 5 seconds

	Mechanical Layer : 
* Recent Rune usage
* Failed secret interaction nearby
* NPC world-state change

---

		Echo Delivery Rules

* Echo appears only once per player
* Delivered via:
	* Local chat (no UI highlight)
	* Fades after 5–7 seconds
* Cannot be logged or replayed
* Cannot be screenshotted via in-game journal

> *Players who miss it — miss it.*

---

		Echo Consequences

	Echoes never give instructions.

	Instead, they:
* Change interaction prompts elsewhere
* Enable otherwise locked dialogue lines
* Alter object behavior subtly

	Example:

> “The ridge remembers when wind spoke…”

	Effects:
* Certain cliffs gain wind-assisted jump windows
* Scout NPC acknowledges the ridge
* Wind-Sealed Mail success chance increases

---

	Echo Saturation & Cooldown : 

* Each mailbox has:
	* Echo Charge Level
* Too many echoes:
	* Cause distortion
	* Lead to misleading fragments
* Natural cooldown:
	* 1–3 in-game days
* Rune interference can override cooldown (with risk)

---

		II. Special Runic Slot

	*(Mailbox Invocation & World Query System)*

---

		Core Concept

	The mailbox contains one dormant runic socket.

	This socket:
* Does not “cast” magic
* It requests attention from the world

	Runes inserted do not guarantee results — they invite responses.

---

	Slot Rules : 
* Only one rune active at a time
* Rune duration:
	* Fixed (hours)
	* Or conditional (until response occurs)
* Rune is consumed or altered
* Some runes can backfire

---

			Rune Categories

		1. Rune of Request (Base)

	Function : 
* Requests attention from NPC systems

	Effects : 
* Enables:
  * Rare NPC dialogue options
  * Emergency delivery quests
  * Hidden vendor inventories

	Risk : 
* None

---

		2. Rune of Echo

	Function : 
* Amplifies mailbox echo reception

	Effects : 
* Echo frequency increased
* Echo clarity improved
* Historical echoes unlocked

	Side Effects : 
* Conflicting echoes possible
* Misleading memory fragments

---

		3. Rune of Binding

	Function : 
* Anchors mailbox to a specific LCWM node

	Effects : 
* Mailbox becomes a relay
* Unlocks:
	* Zone-wide interaction changes
	* Cooperative echo events

	Risk : 
* World backlash event possible

---

		4. Rune of Interference (Forbidden)

	Function : 

* Forces a response

	Effects : 
* Triggers:
	* Immediate quest
	* Forced NPC appearance
	* Rare item offer

	Risk :  : 
* NPC hostility
* Guard suspicion
* Town instability meter increases

---

		Rune–NPC Interactions

	When a rune is active:

* NPCs may
	* Comment cryptically
	* Act out of routine
	* Appear where they shouldn’t

	Example:
Rune of Echo active

→ Merchant says:

> “Prices change when the past listens.”

→ Rare items appear for one day only.

---

		Chain Quest Activation

	Certain chain quests:

* Cannot be started from NPCs
* Only activate if:
	* Correct rune inserted
	* Correct echo heard
	* Correct time window met

	Mailbox becomes the quest gate, not the giver.

---

	Failure & Corruption States

	Misuse can cause:

	1. Echo Corruption : 
* Messages become nonsensical
* Wrong clues appear
* Requires world reset or time decay

	2. Rune Rejection : 
* Rune shatters
* Mailbox locks for 24 hours

	3. World Attention : 
* Scouts investigate
* Guardians intervene
* Invasion chance slightly increases

