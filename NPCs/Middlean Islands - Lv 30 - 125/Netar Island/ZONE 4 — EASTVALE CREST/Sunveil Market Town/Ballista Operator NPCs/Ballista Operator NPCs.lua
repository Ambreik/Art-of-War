			Ballista Operator NPCs

	NPC Names : 

1. "Master Gunner Thalen Ironmark"
2. "Chief Operator Kiera Stonebolt"
3. "Gunner Rolf Blackspire"
4. "Operator Selwyn Ashforge"
5. "Gunner Daria Windbore"

---

	Appearance (General / Individual Notes) : 
* General : Heavy leather aprons with reinforced metal shoulder pads; fingerless gloves; belts full of tools and ropes; leather boots; goggles strapped to forehead;
small satchel with calibration instruments and ammunition. All carry short hammers or wrenches for adjustments.
* Master Gunner Thalen Ironmark : Middle-aged, strong build, greying hair, tattoo of crossed ballista bolts on right forearm.
* Chief Operator Kiera Stonebolt : Female, lean, hair tied back, keen eyes scanning horizon, stained gloves.
* Gunner Rolf Blackspire : Tall, scarred face, slightly hunched, cautious movements around ballista.
* Operator Selwyn Ashforge : Young, energetic, apron covered in soot, always checking projectiles.
* Gunner Daria Windbore : Focused, calm, meticulous, glasses slipping down nose.

---

	Primary Functions : 
* Ballista Operation : Load, aim, and fire ballista projectiles during invasions or drills.
* Projectile Management : Maintain and craft ammunition (standard bolts, incendiary bolts, firepots).
* Skillable : Players may train or duel with operators if authorized by town events.
* Event Activation : Can react to LCWM mechanics like Firepot Combos, Water Channel Exploits, or Balloon Distraction Events.

---

	Behaviors : 
* Patrol around ballista platforms when idle, ensuring projectiles are loaded and bolts aligned.
* During invasions, prioritize firing at enemy clusters, especially Crusader Soldiers, Cragwallowers, or other hostile mobs.
* React to player experimentation: if a player triggers Firepot + Ballista combo or other LCWM mechanics, operator may assist or adjust firing angles.
* Occasionally communicate with Balloon Officers for coordinated defensive fire.

---

	Mechanics : 
* Ballista Fire :
	* Standard bolt: single target, high damage.
	* Firepot bolt: AoE explosion, ignites enemies, can interact with flooded channels or wind mechanics.
* Multi-Ballista Coordination : If multiple operators are on platforms, can chain coordinated firing for increased AoE effect.
* LCWM Interaction :
	* Respond to Echo Mechanics and Firepot Combos.
	* Adjust trajectory if water channels are active (slippery or steam effects).
* Maintenance Buff : If a player assists the operator in loading or repairing ballista, operator provides minor temporary accuracy buff (+5% for 10–15 seconds).

---

	Skills : 
* Bolt Shot : Standard projectile, single-target damage.
* Incendiary Bolt : AoE fire effect on impact, ignites enemies, small pushback.
* Firepot Launch : Special skill; interacts with player-triggered Firepot mechanics for LCWM synergy.
* Quick Reload : Reduces reload time for nearby operators (stackable if multiple operators nearby).
* Calibrate Aim : Temporary buff to accuracy for entire terrace (duration 15 seconds).

