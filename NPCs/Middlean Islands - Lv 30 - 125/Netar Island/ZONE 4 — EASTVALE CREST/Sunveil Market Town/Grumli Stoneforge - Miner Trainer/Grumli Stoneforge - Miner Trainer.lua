			Grumli Stoneforge - Miner Trainer

* Name : Grumli Stoneforge
* Race / Gender : Dwarf / Male
* Profession / Rank : Miner Trainer / Rank III
* Location : Northern sector of Sunveil Market Town — near a forge with furnace and ore storage.

---

	Appearance / Visuals :
* Stout, broad-shouldered dwarf, around 145 cm tall.
* Rugged face streaked with soot and dust.
* Wears a heavy leather apron, gloves, sturdy boots, and a miner’s helmet with a dim lamp.
* Often covered in mineral residue from daily ore processing.
* Carries a small pickaxe at his belt, used for demonstration and testing ores.

---

	Personality / Behavior :
* Serious, disciplined, and focused on precision in mining and smelting.
* Patient with apprentices and players who demonstrate effort and attention to detail.
* Respectful of dwarven tradition and the craft of metallurgy.
* Occasionally offers advice or anecdotes about mineral veins and ore quality.

---

	Functions / Interactions :
* Ore Refinement / Crafting : Converts raw minerals and ores brought by players into ingots, alloys, or other usable crafting materials.
* Profession Training : Provides Miner skill leveling and advancement for players.
