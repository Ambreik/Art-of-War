---

	Story / Lore :
* Born and raised in the "Sundrop Quarry" region, Grumli learned mining from his father and honed smelting techniques over decades.
* Journeyed to "Sunveil Market Town" to establish a dwarven standard of ore refinement, bringing centuries of expertise in metallurgy.
* Known for crafting flawless ingots and training apprentices to uphold the high standard of dwarven mining.
* Believes that every piece of refined metal is a testament to discipline, patience, and the miner’s art.

