			Grumli Stoneforge — Daily Quest Batch (Sunveil Market Town)

---

	Week 1 – Core Daily Quests

1. Quest : *Salted Water Delivery*
	* Objective : Bring 2 pots of “Salted Water” from the northern side of the island to Grumli.
	* Mechanics / Notes : Water must be transported carefully; certain terrain may slow player.
	* Difficulty : Easy
	* Reward : 50-75 Silver + 25 EXP

2. Quest : *Waste Rock Disposal*
	* Objective : Dispose of collected “Waste Rock” in 3 secret locations around the zone.
	* Mechanics : Locations are random; must avoid detection by NPCs or other players.
	* Difficulty : Medium (requires stealth)
	* Reward : 100 Silver + 50 EXP + 1 Miner Skill Point

3. Quest : *Tree Sap Collection*
	* Objective : Collect 5 Tree Saps from the tallest trees in the zone.
	* Mechanics : Players must climb/collect carefully; some trees may spawn minor hostile creatures.
	* Difficulty : Easy-Medium
	* Reward : 75 Silver + 30 EXP + 1 Craft Material (Tree Resin)

4. Quest : *Escort & Mining Operation*
	* Objective : Escort Grumli to a special mining site and help collect 1000 ores.
	* Mechanics : NPC has HP; wild animals attack; UI tracks ore collection; final escort back to town. Last stage has 50/50 chance for bonus reward.
	* Difficulty : Hard
	* Reward : 200 Silver + 150 EXP + 2 Craft Materials + Bonus Reward (if successful, rare ore or cosmetic)

5. Quest (Random Daily Pool) : *Transport to Southfall / Stormreach*
	* Objective : Deliver goods to Southfall Village or Stormreach Watch.
	* Mechanics : Timer 20–30 minutes; no flying allowed.
	* Difficulty : Medium-Hard
	* Reward : 150-250 Silver + 100 EXP + 1 Rare Craft Material

---

	Week 2 – Core Daily Quests

1. Quest : *Tomb of Great Crusaders Supply Run*

   * Objective : Deliver materials to dungeon entrance; flying allowed.
   * Mechanics : Timer shorter, reward smaller (gold + minor EXP).
   * Difficulty : Easy
   * Reward : 75-100 Silver + 25 EXP

2. Quest (Random) : *Mine Collection in Dungeon*

   * Objective : Collect specific ores/mines from dungeon (1 of 3 random types).
   * Mechanics : Player must gather a big amount before next day noon.
   * Difficulty : Hard
   * Reward : 250 Silver + 200 EXP + Rare Craft Material

3. Quest : *Pale Iron Fragment Collection*
	* Objective : Collect 200–800 Pale Iron Fragments (Rank I).
	* Mechanics : Spawned near stone outcrops; easy to find.
	* Difficulty : Easy
	* Reward : 50–100 Silver + 50 EXP

4. Quest : *Creststone Ore Extraction*
	* Objective : Collect 200–800 Creststone Ore (Rank II).
	* Mechanics : Found along ridges; requires careful traversal.
	* Difficulty : Medium
	* Reward : 75–125 Silver + 60 EXP

5. Quest (Random) : *Lumen Quartz Collection*
	* Objective : Collect 200–800 Lumen Quartz (Rank III).
	* Mechanics : Slight shimmer at sunrise/dusk; found in cliffs.
	* Difficulty : Medium
	* Reward : 100–150 Silver + 75 EXP + Craft Material

