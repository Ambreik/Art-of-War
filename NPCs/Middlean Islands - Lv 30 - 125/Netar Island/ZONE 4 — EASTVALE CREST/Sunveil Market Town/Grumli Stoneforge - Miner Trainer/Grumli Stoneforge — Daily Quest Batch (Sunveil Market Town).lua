	Week 4 – Core Daily Quests
1. Quest : *Tomb of Great Crusaders Supply Run* (Repeat)
2. Quest : *Mine Collection in Dungeon* (Random from 3 types)
3. Quest : *Dawnheart Geode Collection* *(Rare, Rank IV)*
	* Objective : Collect Dawnheart Geodes after small seismic tremor event.
	* Requirements : Player must be Rank IV Miner and level 80+.
	* Mechanics : Rare spawn; respawns only after next tremor.
	* Difficulty : Very Hard
	* Reward : 500–750 Silver + 500 EXP + Rare Craft Material + Bonus Cosmetic / Item
4. Quest : *Pale Iron / Creststone / Lumen Quartz Collection* (Repeat variant for level scaling)
5. Quest (Random) : *Special Mining / Escort Challenge* (combines escort + mining from earlier weeks)

---

	Mechanics Integrated :

* Randomized daily quests : some quests chosen from a pool.
* Escort mechanics : NPC has HP, can be attacked by wildlife.
* Timer mechanics : some quests must be completed in time (Southfall/Stormreach transport).
* Spawn mechanics : rare mines or resources appear at specific world conditions (seismic tremor).
* Player rank / level gating : rare quests only available to high-level, experienced miners.

