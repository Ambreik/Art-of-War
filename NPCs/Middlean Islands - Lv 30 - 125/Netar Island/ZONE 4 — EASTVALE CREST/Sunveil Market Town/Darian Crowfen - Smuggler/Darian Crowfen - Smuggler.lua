			Darian Crowfen - Smuggler

	Location : Sunveil Market Town — Wandering

* Roams market terraces, alleys, and streets
* Moves constantly to simulate surveillance of potential “targets”
* Can appear near Flying Master for discount interactions

---

	Visual Description : "Darian Crowfen" is a wiry human with quick, calculated movements. His dark cloak blends into alley shadows, and his hood often covers half
his face. Subtle leather pouches hang from his belt, some jingling faintly with hidden items. His eyes are sharp, always scanning, occasionally locking on players 
of interest. His hands are gloved, concealing tools of trade and small concealed items.

---

	Personality : 
* Clever, sly, and opportunistic
* Knows the town like the back of his hand
* Interacts only with players involved in the Smuggling System
* Observant — subtle cues in movement or glance can signal interactions
* Mischievous with town merchants, but respected in underground circles

---

	Primary Function : 
* Operates blackmarket interactions (restricted to Smuggling System participants)
* Offers Flying Stall discounts if:
	1. Player is part of Smuggling System
	2. Darian is near Flying Master

* Provides underground transport and item exchanges
* Can eventually offer underground quests (TO BE DONE LATER)

---

	Mechanics / Interactions : 
* Blackmarket : Only players carrying the “flavor” Smuggling System item can transact
* Flying Discount : Discount is dynamically applied only when near Flying Master
* Transport / Item Exchange : Limited, bound by inventory and Smuggling System permissions
* Roaming AI : Moves continuously in town, seeks out potential participants, reacts to player presence

