			Thalric Ironveil - Armor Merchant NPC

	Appearance :
* Middle-aged human male, slightly muscular build, wears a dark green tunic with leather apron, leather boots, and gloves.
* His shop is small, stacked with armor pieces: helmets, breastplates, greaves, shields.
* Wears a pendant that sparkles faintly in the sun (rumored to be for good luck in trades).
* Often adjusts his apron pockets full of armor samples.

---

	Primary Functions :
* Armor Sales : Sells light, medium, and heavy armors for players.

---

	Behavior :
* Always checks customers’ armors and comments on condition or style.
* Interacts with Weapon Merchant and Accessories Merchant: flirts with his friend’s sister (Accessories Merchant), tries to impress her with armor samples.
* Patrols shop aisles slowly, adjusts armor on stands, keeps watch over valuable pieces.
* Occasionally criticizes Weapon Merchant: “You call that a sword? My grandmother’s whetstone is sharper!”

---

	Mechanics :
* Reputation-Based Discounts : Frequent customers or players completing quests for the town get minor discounts on some armors.
* Armor Quality Checks : Players crafting armor can have it inspected; high-quality results may unlock hidden bonus effects 
(e.g., +5% resistance to fire or ice for 5 minutes).

---

		Quotes :

	Idle / Shop Interaction:
* “Ah, a fine piece of mail you have there. Could use a little polish.”
* “Step inside, step inside! Quality armors for quality adventurers!”
* “Try this plate—keeps out more than just arrows, if you know what I mean.”
* “My armors are old-fashioned? Perhaps, but they save lives.”
* “Oh, Daria, you should see this new shoulder guard I have in stock!”

	Player Interaction / Training:
* “Careful with that chainmail—it can tear if you’re not gentle.”
* “Let me show you a trick to reinforce the leather straps.”
* “Bring me materials, and I’ll teach you the secrets of plate crafting.”

	Event / Combat:
* “Make sure you’re protected out there!”
* “Never underestimate the value of a sturdy breastplate.”
* “That’s why I craft the best—so none of you get caught off guard!”

	Cross-NPC Interaction:
* “Don’t mind him, Roran, he can’t even polish a helmet without scratching it!”
* “Kiera, you must see this cuirass! Perfect for a lady of your taste!”

---

	Placement / Notes:
* Located in a small armor shop near the market hub in Sunveil Market Town.
* Interaction radius for training: ~5 meters.
* References Weapon Merchant and Accessories Merchant NPCs for social and dynamic dialogues.

