			Eremor the Stonebound

	Type : Lorekeeper / Secret Mechanic Guide NPC

	Location : Middlean Islands / Netar Island / Zone 4 Eastvale Crest
	Found at : Ruined Watchpost overlooking the lower terraces, along the old mountain trail between Luminous Ridge and Silentshade Hollow

	Visibility : 
* Semi-hidden
* Not marked on minimap
* Revealed through exploration or quest dialogue

	Appearance : 
* Eremor appears as an elderly man wrapped in layered stone-gray robes, frayed at the edges as if weathered by decades of wind and dust.
* His skin bears faint mineral-like veins across his arms and neck, giving him the unsettling look of slowly turning to stone.
* One of his eyes glows dim amber, while the other is clouded and lifeless.
* A collection of carved stone talismans and shattered relics hang from his belt, softly clinking when he moves.

* He leans heavily on a staff formed from fused rock and bone, its head shaped like a broken circular seal.

	Personality : 
* Quiet, restrained, and deeply cautious.
* Speaks slowly, as if choosing every word with care.
* Shows no fear of death, but great respect for ancient powers and forgotten rites.
* Dislikes recklessness and openly warns players against arrogance.

	Role : 	

	Guide NPC for:
* The Silentshade Hollow
* Zhar’tek the Verdant Exile encounter
* Ritual-based boss mechanics
* Statues and hidden risk-reward system

---

		Core Functions

	1. Encounter Hints Provider

	Eremor does NOT directly explain the entire fight, but gives layered clues:

	Dialogue Sample : 

> “The Hollow does not answer blades alone.
> It answers offerings, will, and the right to be seen by what sleeps beneath.”

> “The wall does not open for blood… only for memory bound in iron.”

> “Burn what marks you as chosen — or remain a ghost in its eyes.”

> “Three watchers stand in silence.
> Two must fall.
> One must endure, if you value your future more than your pride.”

---

	2. Explains Required Items

	Obsidian Seal of Binding (Tomb of Great Crusaders) : 

> “A broken empire forged keys not for doors… but for spirits.
> Without such a seal, the Hollow will never stir.”

	Parchment of Silent Claim : 

> “The fire accepts only those who declare themselves bound to the rite.
> Without that ash upon your soul, your strikes will mean nothing.”

---

	3. Statue Mechanic Hints

	Without directly naming colors:
> “Two watchers drink strength from the beast — silence them.”
> “The third holds the beast’s rage in chains.
> Break it… and you invite both glory and ruin.”

	This teaches:
* Destroy Red & Yellow
* Leave Orange intact unless seeking high-risk reward

---

	4. Quest Hook (Optional)

	Quest: "Whispers Beneath the Stone"
Given by Eremor after player discovers Silentshade Hollow entrance.

	Objectives:
* Obtain Obsidian Seal of Binding
* Obtain Parchment of Silent Claim
* Return for ritual knowledge

	Rewards:
* Permanent unlock of ritual interaction UI
* Small reputation with Stonebound Lorekeepers
* Access to ritual hint system

---

	Connection to Lore

	Eremor is implied to be:
* One of the last surviving stonebinders
* Possibly linked to the same ancient culture that created Zhar’tek
* A former ritualist who failed to stop the corruption

	Subtle hint:

> “I stood once where you will stand.
> The fire answered me… but I was not worthy of what came after.”

---

	Special Interaction

	If player has defeated Zhar’tek:

> “So… the Hollow breathes again.
> May your shadow never walk where his once did.”

