			Side Quest Line — *Whispers of Still Water*

	A loose, non-linear set of quests. They can be completed independently, but subtle dialogue changes acknowledge progress.

---

	1. “The Pond That Watches Back”

* Quest Type : Side / Environmental
* Given By : Oscar Fisher D.

	Objective(s) :
* Observe the pond during three different rare fish emergence cues
* Do not fish during observation
* Return to Oscar with findings

	Mechanics :
* Requires standing still near the pond
* Visual/audio cues must fully play
* Moving away resets progress

	Rewards :
* Discovery XP
* Access to Stillwater Glassbob in shop rotation

	Quest Dialogue :
* Opening :
	*“Most folk think fish come when you pull. They don’t. They come when they decide.”*
* Completion :
	*“Good. You learned the first rule — don’t rush what’s older than you.”*

	Additional Notes :
* Teaches patience and non-interaction mechanics

---

	2. “Scales Beneath the Surface”

* Quest Type : Side / Collection
* Given By : Oscar Fisher D.

	Objective(s) :
* Catch any two different common freshwater fish
* Release them back into the pond *(interaction prompt)*

	Mechanics :
* Released fish increase short-term pond stability
* Using aggressive lures during quest fails it

	Rewards :
* Pondbone Fragment x2
* Minor freshwater reputation flag *(hidden)*

	Quest Dialogue :
* Opening :
	*“Not everything you catch is meant to leave with you.”*
* Completion :
	*“Good hands. Careful hands.”*

---

	3. “When the Water Turns Hostile”

* Quest Type : Side / Risk Awareness
* Given By : Oscar Fisher D.

	Objective(s) :
* Trigger an Aggressive Aquatic Encounter *(via Provoking Tadpole Lure)*
* Survive the encounter

	Conditions :
* Must be alone
* Other players cannot intervene

	Rewards :
* Access to Deepcoil Sinkline
* Unique title *(optional later)*

	Quest Dialogue :
* Opening :
	*“You keep pokin’ the deep long enough… something pokes back.”*
* Completion :
	*“Still breathin’? Then you learned.”*

	Additional Notes :
* Oscar never explains how to trigger it
* Players must experiment

---

	4. “Currents Don’t Lie”

* Quest Type : Side / Knowledge
* Given By : Oscar Fisher D.

	Objective(s) :
* Use Windscale Spinner successfully
* Catch a Windscale Trout
* Return without selling or using the fish

	Mechanics :
* Requires correct reel timing
* Fog or wind increases success

	Rewards :
* Oscar’s Field Notes: Currents
* Unlocks future hidden interactions

	Quest Dialogue :
* Opening :
	*“Water moves honest. Folk don’t.”*
* Completion :
	*“You listened.”*

---

	5. “What Lurks If You Overfish” *(Hidden Quest)*

	Trigger :
* Overuse aggressive lures in a short window
* Pond reaches high aggression saturation

	Objective(s) :
* Escape Deepwater Clearing alive

	Rewards :
* None (intentionally)
* Permanent dialogue change with Oscar

	Dialogue Change :
* “Told you once. Water remembers.”

---

		Quest Design Philosophy (Important)

* No gold farming
* No repeatable abuse
* Rewards are access, knowledge, or permission
* Oscar never praises — only acknowledges

