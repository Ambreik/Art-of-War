## **Appearance**

Oscar is an elderly man with weathered skin, sun-faded hair, and a long beard braided with small river stones and carved wooden fish charms. His eyes are a cloudy sea-green, always half-lidded as if he is listening to distant waters.

He wears:

* a **patched leather fishing coat**
* a wide straw hat decorated with colorful feathers
* rolled-up trousers tied with rope
* waterproof boots coated in moss
* a belt with **artisan fishing tools**, tiny carved hooks, bone needles, and drying herbs
* a woven net-bag on his back filled with supplies

He always carries a **handmade wooden fishing spear**, though mostly as a walking stick. His clothes smell faintly of river mint and dried scales.

The entire appearance reflects rustic craftsmanship, age-earned wisdom, and deep connection to water and forest environments.

