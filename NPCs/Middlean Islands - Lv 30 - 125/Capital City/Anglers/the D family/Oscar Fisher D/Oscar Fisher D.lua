## **Behavior & Interaction**

Oscar behaves differently from common shop NPCs:

* Moves slowly, often observing water, listening to wind, or adjusting bait racks.
* Hums soft river chants when idle.
* Interacts politely but speaks in short, thoughtful sentences.
* Never leaves the Mossroot Basin clearing — his home is his world.
* If players observe him long enough, he performs micro-animations:

  * sorting fish bones
  * repairing nets
  * sharpening hooks
  * placing jars of brightly glowing bait on shelves
* When players approach, his eyes brighten slightly, acknowledging presence even before turning.

### Interaction Notes

* **Non-targetable** — cannot be attacked or used for combat interactions.
* **Skillable** — players with certain professions gain long-term benefits by interacting with him repeatedly.
* Provides **small hints about water flow, fish behavior, or hidden forest streams**, enriching world immersion.
* He reacts differently during day/night cycles:

  * **Day:** more talkative, shares fishing advice
  * **Night:** quieter, warns players about forest dangers

