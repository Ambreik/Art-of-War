## **Mechanics and Functions**

Oscar offers specialized services, distinct from any trainer or general shop:

### **• Unique Crafting Services**

Transforms raw fish or rare aquatic resources into specialized items:

* preserved fish bundles
* rare baits
* lure upgrades
* water-resistant containers
* scent-masking oils used in aquatic or stealth exploration

### **• Shop Inventory**

Sells:

* rare bait types
* strong artisan fishing lines
* hand-carved floats
* fish preservation kits
* decorative fish charms for player housing *(future system)*
* “river whisper bottles” containing faint water sounds used for roleplay or cosmetic effect

### **• Localized Buff Items**

Certain crafted foods or tools provide temporary bonuses to fishing, swimming, or stamina near water, tied exclusively to Oscar’s creations.

### **• Small Hidden Mechanics**

* Standing in Oscar’s clearing increases ambient water sound cues.
* Approaching with low fishing skill may trigger a comment on your “unbalanced stance.”
* High fishing skill characters may receive an exclusive passive hint or mini-item.

### **• Clan Lore Relevance**

As the elder of the D. Fisher family, he occasionally references:

* **Carp Barbeau D.**
* **Sushi Vischer D.**
  This builds clan identity and future NPC expansion options.

