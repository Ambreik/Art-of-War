## **Dialogue / Audio — Quotes**

Oscar speaks slowly, with a soft watery cadence.

### **Idle Quotes**

* “Mm… currents speak, if you listen long enough.”
* “Fog’s thick today. Fish’ll stay low.”
* “These old hands still remember every river on this island.”

### **When Player Approaches**

* “Ah… a traveler. The forest brought you here, did it not?”
* “Mind your steps. Moss is deep… deeper than it looks.”
* “Looking for treasures? The river always gives… if you know how to ask.”

### **When Opening the Shop**

* “My goods are simple… but honest.”
* “Everything here I made with the river’s patience.”
* “Take what you need. Leave the currents in peace.”

### **Crafting Dialogue**

* “Mm. This will hold. Strongest line I’ve made all week.”
* “Careful now… hooks bite deeper than teeth.”
* “A proper lure sings underwater. Hear it?”

### **Night Quotes**

* “Dark waters hide the forest’s oldest teeth. Best not stir them.”
* “Quiet now. Listen… something’s moving beneath the roots.”

### **Final Farewell**

* “Safe waters… wherever your path flows.”

