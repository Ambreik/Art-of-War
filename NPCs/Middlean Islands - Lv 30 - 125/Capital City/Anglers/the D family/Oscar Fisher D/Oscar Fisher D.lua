			Oscar Fisher D.

---

## **Overview**

Oscar Fisher D. is the eldest member of the D. Family — a scattered, semi-nomadic clan of fishermen known across the Middlean Islands Region.
He lives **deep within the Emeralddeep Wildwood** of Zone 3, hidden far off the main path inside the **Mossroot Basin**, settled near a calm water inlet with rich aquatic life. His isolated shop, **“Rainbow Trout Goldmine,”** has become a whispered destination for travelers brave enough to delve into the forest’s heart.

Oscar is not merely a fisherman — he is a **master craftsman of aquatic-based tools, artisan baits, water-preservation charms, and exotic fish preparations.** His knowledge of the island’s waterways is unmatched, and local legends claim he can tell the age of a fish simply by listening to the current.

Although not a trainer, Oscar plays a vital role for players who explore the island’s interior. His shop offers rare items that improve fishing efficiency or unlock deeper water mechanics, and his crafting services allow players to turn rare catches into more valuable forms.

He is one of the most peaceful NPCs on Netar Island — calm, slow-speaking, and difficult to notice unless you understand the flow of the forest.

