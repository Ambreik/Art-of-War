		Special Mechanics — Provoking Tadpole Lure

* Each use increases Aggression Saturation of the pond
* At threshold:
	* A Rank III Aquatic Beast (giant tadpole with fin-limbs) spawns
	* Attacks only the lure user
	* Cannot be interfered with by other players
* Aggression resets after:
	* Beast is defeated or
	* Player leaves Deepwater Clearing

