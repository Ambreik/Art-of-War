			"Alphy" the Watchman
			
		Description : 
	""Alphy" the Watchman" is a slender figure with an agile build, reflecting his role as a vigilant observer aboard the Elisabeth Mighty Ship. His skin has a sun-
kissed hue, a testament to countless hours spent under the open sky. Alphy’s face is framed by tousled black hair that falls just above his sharp, mischievous eyes,
which seem to sparkle with curiosity and mischief. A prominent scar runs along his left cheek, hinting at past escapades and close calls.

		Attire :
	"Alphy" dresses in a mix of practical attire and whimsical flair. He wears a fitted leather vest, adorned with pockets filled with various tools and gadgets 
essential for his duties. Beneath the vest, he sports a loose, billowy white shirt with rolled-up sleeves, allowing for ease of movement. His trousers are made of 
durable, dark fabric, tucked into knee-high, well-worn leather boots that enable swift movement across the ship's deck. A colorful bandana is tied around his neck, 
adding a splash of personality to his look. 
	On his belt, he carries an assortment of items, including a small grappling hook, a spyglass, and pouches filled with prank supplies and basic contraband. This 
eclectic collection reflects both his role as a lookout and his playful nature.

		Duties on the Ship :
	As the ship’s watchman, "Alphy" has a multifaceted role that blends observation with entertainment. He stands vigilantly at various points on the deck, always 
scanning the horizon for potential threats or exciting opportunities. His keen senses make him an excellent lookout, capable of spotting distant ships or changes in
weather well before others.
	Beyond his watchful eye, "Alphy" serves as a liaison among the different factions that frequent the ship. He interacts with players, offering lighthearted jokes
and playful tricks to keep spirits high, particularly among the Dendera players. With the Pindars, he engages in pranks, testing their reactions and fortitude.
	When it comes to PK players from Abalan, "Alphy"’s demeanor shifts to one of serious observation. He discreetly monitors their activities, searching for signs 
of trouble or potential smuggling operations. "Alphy" often gathers intel to report back to the ship’s authorities, ensuring the safety of all passengers.

	In addition to these duties, "Alphy" sometimes offers contraband goods or insider information during special events, such as the : 
*"Halloween Event - Terror of the Underworld Spirits," creating opportunities for cunning dealings that keep the ship’s dynamics lively.

