			Ruined Temple
			
	This dungeon is located on "Arliton" island, that is part of "Middlean Islands".
	
	Consist in 7 floors. Floors are regions of this dungeon. the only region that can be considered big is the "side floor" and "6th Floor".
	
		1st Floor
	A big chamber with monsters.
	
		2nd Floor 
	At this floor players have two choises : 
*to reach next floor : by doing the request of the floor.
*to reach the side floor by unbreaking the seal of the Idol and open the gate to the side floor.
		Side Floor 
	Players reach this area by simple unbreaking the seal of the Idol statue from the "2nd Floor". Once they reach here, are spawned in the middle of a big room 
that lead to other 8 rooms interconected by corridors. (see picture "Side Floor")
	In the middle of the central room, there is "Statue of Stirr" that is inactive until is clicked by the leader of the group.
	Once clicked, a short chat is showed to the whole group that is present here.
<Message> : "Welcome warriors, arriving here i can offer my services but only if you kill all those evil creatures that long time ago they put me down after an exhausting fight. Me and my comrades tried to keep them back in this annexe chambers preventing them to escape in the temple to create more chaos. Please warriors, avenge my men and I and i will be very grateful with you using my magic that is still with me after those centuries and keep me alive as a spirit."

...

<Message> : "Collect "Piece of Broken Steel" and "Piece of Clean Crystal" from the monsters and put them on my statue. Once a stack of 100x is collected, i can offer you 10x keys for each warrior that helped. Also keep in mind, that i can only keep you all in here for about 60 minutes, before i must rest."

...

<Message> : "Don't you worries, collecting "Esence of Accuracy" from the archers, you can increase the duration of this time, once you add one by one to my statue. But don't be too greedy, those essences have a low chance to drop, and one represent only 1 minute added to my time here. A maximum of 4 hours is my capacity to stay around as a spirit before i am send back to rest, probably this time forever. So good luck my warriors !!!"
	
		Mechanics : 
	Central room is full of Rank 3 monsters, melee who have the ability to ignore ACC of players. Also affect with "Bleed" and "Stun".
	Archers are located in all other rooms except central room. Can affect with "Stun" and "Slowdown". Also they ignore ACC of players. Spawn every 20 seconds.
	Tank creatures are located in every other rooms except central room. Slow movement speed and attack speed but can resist very well against any kind of damage.
They spawn every 5 seconds. Can drop 2x "Piece of Broken Steel" as they have heavy armors. Also theose tanks can use "AoE Skills".
	Spirit creatures are some shadows who spawn from place to place random in time and location and heal the tank monsters. Those spirits use "AoE Skills" with 
high damage output. Drop 2x "Piece of Clean Crystal". Also have chance once they are killed to transform into "Ancestral Spirit Leader" who cannot be defeated 
unless the mages doesnt use a fire damage while is stunned killing it from fire.

	Creatures here spawn every 30-60 seconds depending of the distance from the central room. The farther the distance is the longer is the cooldown, excepting the 
archers and tanks who have specific timer.
	Those creatures here are "Vampiric" type, undead monsters.
	Archers have high attack speed.
	
	Spirit of Stirr, create 10x random keys for every player who is online here, being active or not in battles. Those keys are stackable every time are produced, 
into the inventory. He is wondering around the corridors and rooms for the whole duration of the instance before he is warped back into the "Underworld", creating 
a shockwave that kill all the monsters here and make everyting to tranble. The image screen of players tramble and a loud shout from Stirr can be heard into the whole temple, where into the anexe here, the shout is very loud.
	Also a timer is placed under the minimap, helping the players to know how much minutes they have until the departure of Stirr.

