		Dungeon Location Within the Sewage District

	The entire Sewage District is built on two layers :

1. Surface Layer :

   * Twisting alleyways, broken infrastructure, old guard stations, ruined housing, broken sewage control towers.
   * Multiple access points to the underground: locked hatches, destroyed tunnels, cracked manholes.
   * Some encounters, NPCs, and scripted mini-events are located on the surface (like rogue cultists, scavenger NPCs, or even mini-ghost encounters).
   * This is the overworld / entrance space to the dungeon system.

2. Underground Layer (The Sewage Labyrinth) – THIS is the dungeon:

   * The actual instance or "raid" takes place deep beneath the surface district.
   * Accessed through five main entrance points:

     *  Docks (flooded canal gate)
     *  Outer Wall (collapsed cave entry guarded by ghosts)
     *  Residential Hatch (hidden in a destroyed home basement)
     *  Market Access Shaft (old grain silo repurposed for sewage flow)
     *  Maintenance Hatch (a hard-to-find secret backdoor hidden behind rubble)

	These entrances are connected to the same underlying instance, meaning when a player enters through any, they are phased into the same dungeon, starting from 
one of a few spawn rooms near the Overflow Channels.

---

	How This Works in Gameplay Terms

* The surface level is like a public overworld area with random mobs, side quests, and lore clues (and also for PvP, bounty hunting, etc.).
* The underground dungeon/raid is instanced (like in WoW) and is triggered when entering through any of the major entry points.
* The maze-like labyrinth that players navigate inside the dungeon is canonically beneath the entire Sewage District, making it feel like players are crawling 
through the guts of the city.

---

	Lore Justification : According to the lore you’ve established:

* The "Sewage District" was once a highly functional undercity, used for waste, emergency flood routing, dark rituals, and forgotten prison vaults.
* Over centuries, it collapsed inward, making the entire area a multilayered death trap, now infested by filth, decay, and cult activity.
* The lower level—where the dungeon takes place—was sealed off by the city long ago, with only a few risky access points remaining.
* Most inhabitants of the surface avoid this area. Only the desperate, brave, or insane venture into the deep sewer system.

