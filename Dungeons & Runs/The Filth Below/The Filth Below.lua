			The Filth Below

	Zone Type : 5-10 Player Dungeon / Sewage Raid Wing

> Accessed through any of the five entrances (Market, Residential, Dockside, Outer Wall, Abandoned Maintenance Hatch). Once a player enters, the instance is phased.
> Can be completed solo (scaling difficulty), but optimized for party play.

---

	General Design & Layout Structure (5 Segments – Progressive Wing System) :

	Each section is gated with environmental hazards, scripted triggers, or locked doors that require keys, puzzles, or boss defeats to progress.

1. The Overflow Channels – Trash mobs, corrupted sewer beasts, and ooze traps.
2. The Drowned Slums – First mini-boss zone, patrolled by flooded specters and undead.
3. The Pipe Spine – Maze of elevated pipe platforms with mechanical security.
4. The Forgotten Sanctum – Dark ritual zone for the cult and Wretched Scour.
5. The Filth Heart (Boss Chamber) – Final boss in the heart of the system, a toxic lake chamber where decay is alive.

---

		ENCOUNTER SCRIPTING & MECHANICS

---

	Trash Encounters :

* Goremuck Larvae (Pack mobs) : Splits into two smaller mobs when killed. Use AoE burst.
* Sludge Spitter (Ranged caster) : Launches aoe “Acid Splash” zones. Interrupt & reposition.
* Filthbound Rats (Swarm mechanics) : Appear in large waves when walking near certain pipes. Slow CC works best.
* Drained Watchmen (Spectral Undead) : Patrols can fear players toward traps.

---

	Mini-Boss 1: Vilegrin, the Pipe Gnawer

Location : The Overflow Channels
Role : Tunnel guardian mutated rat-thing with poison-enhanced attacks.

	Mechanics:

* Gnawing Burrow (Targeted Charge) : Burrows and appears behind a healer or ranged DPS, interrupting spellcasting.
* Toxic Spray (Frontal Cone) : Leaves stacking poison on tanks. Healer dispels needed.
* Call of the Nest : Summons rat swarms at 75% and 40% health thresholds. Players must kite and AoE.

---

	Mini-Boss 2 : Mire Sister Velune

Location : The Drowned Slums
Role : Undead cultist specter haunting a collapsed chamber.

	Mechanics:

* Ghostly Chains (Single-Target Root) : Breakable with 3 hits or purge. Used on random players.
* Flood Pulse (Raid-wide AoE) : Submerges room partially, forcing players to move to high ground.
* Waterlogged Souls (Summons) : Possesses nearby drowned spirits to explode on death.
* Enrage Phase (20%) : Becomes fully visible, doubling damage and blinking between players.

---

		Mid-Encounter Mechanic: The Pipe Spine Navigation Event

	Design : Parkour-style navigation through spinning pipe wheels, geysers, valve-controlled gates, and spike vents.

* Players must rotate valves to open paths while avoiding traps and pressure bursts.
* Falling drops the player back to the last checkpoint, summoning 3 Filthbound Rats.

---

	Boss 3: The Wretched Scour
Location : Forgotten Sanctum
Role : Plague Cult's creation — a mass of corrupted flesh, spirit, and machine.

	Mechanics:
*Phase 1 :
  * Corruption Spray : Targets 3 players with a spreading DoT. Must move apart.
  * Sewage Surge : Random pipes gush waves, knocking players back.
  * Taint Brand : Places sigils on the floor that erupt unless stepped on in order.
* Phase 2 (50%) :
  * Breaks into two fused entities :
    * Scour Flesh : A tank-buster brute with cleave.
    * Scour Mind : Caster that uses haunting visuals, fears, and confusion.
  * Must be killed within 10 seconds of each other, or one revives with 25% HP.

---

	Final Boss: Ssy’rrh the Filth Heart

Location : Core Chamber
Type : Ancient, amorphous sludge horror born of forbidden magic and rot.

* Phase 1:
* Toxic Vortex : Spins clockwise, leaving acid trail.
* Erode Essence : Leech DoT on healer. Must be cleansed.
* Miasma Bulge : Random blob explodes. Area denial.

* Phase 2 (60%):

* **Corrupt Heartbeats : Echoing sound pulses inflict damage and reduce movement.
* **Summon: Pustule Horrors : Adds that explode after 15 sec.
* **Environmental Mechanic : Players must redirect sewage flow by pulling 3 levers before being overwhelmed.

*Phase 3 (25% - Soft Enrage):
* Massive Form : Expands, covering 80% of the chamber.
* Spawns Ghosts of Old Keepers : Each must be banished with a separate player channeling a soul trap.
* Timer Enrage : Ssy’rrh will consume the entire area if not killed in 2 minutes.

---

	Navigation Tools & Player Interactions :

* Map Fragments : Players can loot map pieces from slain trash to reveal secret rooms or shortcuts.
* Environmental Triggers : Steam valves, grate levers, and pressure tiles activate bridges or disable traps.
* Faction Interaction : Rogue or Assassin classes can sneak into optional vaults without triggering certain mobs.
* Cursed Relics : Optional mini-objectives grant temporary buffs or unlock lore-driven dialogue from ghost NPCs.

