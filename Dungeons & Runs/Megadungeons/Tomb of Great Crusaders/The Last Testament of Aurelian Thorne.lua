# Lore Record

## The Last Testament of **Aurelian Thorne**, Constructor-Deserter of Dendera

*(Recovered from the walls of the Hidden Archive of Felgrace Shelf)*

---

**My name is Aurelian Thorne.**
I was once a constructor in the army of Dendera — not a soldier by oath, but a builder of their war, a mover of stone and siege and scaffold.

I am also a deserter.

Me and my entire crew betrayed the nation during the Second Great War. We fled the endless summons, the blood tithes, the burning banners. We ran not because we feared death… but because we no longer believed the war against the Old Gods was meant to be won.

We came to this island seeking freedom.

We found a paradise — or what we believed to be one.

---

### The Hole in the Mountain

We discovered a hole in the mountain.
Not made by hand.
Not made by beasts.
It breathed.

Strange noises rose from it — sounds not human, not animal, not wind.
So we sealed it quickly, poured stone and iron over the opening, and marked it with warning glyphs.

Then, foolishly… we began building our citadel above it.

We believed we were burying a threat.
In truth, we were building a tomb atop a wound.

---

### The First Taken

Two days into construction, screams came from the depths.

Four men went to investigate.
Only two returned.

Their skin was pale, altered.
Their breath rose in icy vapor, though the air was warm.
Their eyes… turned a hollow, frozen blue.

They collapsed at the gates without speaking.

Only me and one other soldier ran outside in terror.
The others mocked us, laughed, accused us of fear and fatigue.

One more man came back later — colder than the rest.
He whispered something…
Few heard it.
None repeated it.

Later I learned our leader forbade the words from spreading.

---

### The Sealing

A full group of soldiers and mages was sent below.
They returned hours later and sealed the inner chambers with concrete and arcane bindings.

For three days after, all was calm.

But I had already heard enough.

A captain named **Valerius** spoke in hushed tones near the fire.
He said the whisper was:

“They are coming.”
“There are spirits living beneath.”
“Save yourself.”
“She looked at me… and I became cold.”
“Our fire scared her… but her gaze consumed us.”

Valerius met often with a strange man — one who had been below, yet was not marked by frost.
That alone terrified me more than the dead-eyed ones.

---

### The Second Wave

Then came the earthquake.
We blamed dragons.
We blamed the sky turning red and gold.
But I knew better.

That night, many vanished.

Not killed.
Not found.
Taken.

Our leader pretended order remained.
But I saw fear in his eyes.

I planned to flee — but first I confronted Valerius.

He warned me only this:
Be cautious of where you go.
And of who sees you.

Especially the forest at night.

---

### The Sinkhole

Fifty-five were already gone.

I fled into the woods and found this place —
A natural sinkhole hidden by stone and vine.

Here, I carved what you now see.
I made this hidden archive.

Every word you read is cut by my hand, not inked — because paper lies, but stone remembers.

I brought supplies.
This became my refuge.

Or my tomb.

---

### The March of the Cold

I returned after two days.

I saw soldiers that looked like us.
Same armor.
Same weapons.

But they moved without breath.
Patrolling like hollow guardians.

They were guided by spirits.

I smeared myself with animal waste to hide my scent.
Moved through trees, shadow to shadow.

I made it back alive.

Barely.

---

### The End of the Crusaders

We were eighty-one.
Then fewer.
Then nearly none.

Valerius planned to flee north.

His companion grew strange — silent, distant, avoiding all speech.
I urged Valerius to leave with me.

He refused.

He said he must speak with the northern expedition first.

I never saw him again.

---

### The Last Night

Two weeks passed.

I witnessed Valerius fight our leader.

Then both were dragged eastward — toward the shore — then north.

Then the forest erupted.

An army emerged from the trees.
The noise drowned the world.
Even the animals vanished.

I ran.

I do not know how I reached this place.
It felt like moments — yet the sun had moved when I arrived.

I sealed myself inside.

---

### The Banshee

I believe now I have seen her.

The Banshee Queen.

She cannot corrupt the already dead.
Bodies I saw in the north remained…
But they were marked by ice afterward.

Her gaze steals warmth.
Her presence steals memory.

Tonight, I heard the noises again.

I carry a silver blade.

If you find my body here,
Know that I chose the earth over her chains.

May this place remain hidden.

May you learn what we could not.

— **Aurelian Thorne**,
Last Constructor of the Fallen Crusaders

