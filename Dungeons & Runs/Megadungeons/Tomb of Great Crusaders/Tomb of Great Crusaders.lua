	Tomb of Great Crusaders - Zones Layout

*one zone : is a long corridor with 3 statues on a side 3 on a side and at the end of the corridor is a shinning rock. the statues are of soldiers with spears that stay still. A player must go further and click on the rock. then the statues activate and start attacking the players. the location near the stone produce a wwave that damage the player/s which are there killing them instantly. If a tank is there, automatically remain only with 20%HP. If is a Shaman or Warlock and they have any kind of Magical Barrier, they will reman only with 1 HP. The stone will be damaged until remain with 1HP, all the statues must be killed in order to deal thelasthit to the stone. Constntly (every 30-60 seconds) the Stone pulsate a wave of magic damage in the corridor. Damage dealt to players, is according with the distance between player location and stone location with 95% damage near the stone and 5% at the fartest location in the corridor. Statues (need aa name) use the spears to push back the players towards the walls affecting with knockback and stun when hit the walls. statues have 1 hit attack, using the spear to hit from a side and 2 skills : hit the floor with the spear creating a wave that pushback the players around and another skill that move faster the spear from left to right side as aa fan shape that will knockback the players towards the walls.
The corridor design : made by blocks of stone, carved with some line design. Torches are every few metters on both sides of the corridors for lighting the corridor. on the floor, there are squares with a red dot in the middle, place where the player must stay when the statues use skills for displacing CC damage, where thr players are immune to those CCs. Torches can be estinguished if the statues hit them and the corridor can become dark if all the lights are shut, moment when the players must survive only with the light emited by the stone until all the statues are destroyed and the stone smashed.
Players that die here, they will spawn at the beginning of the corridor after 10seconds with 1HP and all buffs and Runes gone (so the cooldown of all the active skills and Runes on 0, meaning that they can use imediate).
No Regen for Statues or the Stone.
Mounts on this floor are forbidden.
