			Scroll of Thornbrand’s Testament

* Type : Lore Artifact / Dungeon Drop
* Rarity : Unique
* Bind : On Pickup
* Stackable : No
* Destroyable : No
* Tradable : No
* Sellable : No

---

	How to Obtain : Dropped by "Sir Valerius Thornbrand" if he survives the undead onslaught and performs his final self-sacrifice.
	Only one scroll can drop per dungeon instance.
	It is assigned randomly to a player in the group. end

---

	Item Description : 
* A brittle piece of parchment, darkened by age and stained with dried blood.
* The writing is uneven, trembling in places — yet resolute.
* A faint green shimmer still lingers along the edges, as if the ink itself remembers the one who wrote it.

---

	Item Effect : 

	When used/read:
* Permanently unlocks a Lore Entry related to:
	* The Undead Crusaders
	* Queen Redrose Banshee
	* The Tomb of the Great Crusaders
* Grants the player a passive dungeon awareness effect:
	* Slightly highlights statue healers in Queen Redrose’s throne room
	* Increases fire and magic damage visibility feedback against Queen Redrose
* Does not provide combat stats directly


---

		Scroll Content (In-Game Text) : 

> *To whoever still walks among the living,*
>
> Know this: we remember.
>
> We are not empty shells nor mindless shadows. Each of us carries what we were, bound now to what we have become. We are subjects — not servants — of the one who reigns beneath this tomb.
>
> "Queen Redrose, the Banshee", rules not with chains, but with sorrow sharpened into will. She knows you are here. She has always known.
>
> Fire wounds her where steel fails. Magic cuts through what faith cannot reach. Yet even so, she does not stand alone.
>
> Her healers hide in stone, posing as statues in her throne hall. While they stand, she will not fall. Break them first, or your blades will taste only despair.
>
> Do not lose hope, even as this place seeks to devour it.
>
> If you read this, then perhaps our suffering was not in vain.
>
> — *Sir Valerius Thornbrand, once Captain of the Crusade*

---

	Special World Interaction

	After reading the scroll:
* Players will notice:
	* Subtle glow on healer statues in Queen Redrose’s throne room
	* Fire and magic attacks produce clearer reaction effects on her
* NPCs and systems never acknowledge the scroll explicitly
	→ This preserves immersion and avoids meta-dialogue

