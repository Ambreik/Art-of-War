			Tomb of the Great Crusaders

		Queen Redrose Banshee – Throne Chamber

	Future Design Notes (Preserved for Mega-Dungeon Development)

---

	1. Room Architecture – “The Womb of Redrose”

	Shape & Scale

* Massive circular chamber
* Flat stone floor
* High rounded ceiling, dome-like
* Designed to allow:
	* Full raid-scale movement
	* Split positioning
	* Kiting
	* Environmental usage

	The chamber must feel:
* Ancient
* Organic
* More like a living organism than a fortress hall

---

	2. Ceiling Mechanic – Egg Clusters

	Visual Design

	The ceiling is covered with:
* Large, pulsating egg sacs, similar to spider eggs
* Some translucent
* Some darkened
* Slowly twitching

	They represent:

> The reproductive and summoning source of Queen Redrose’s minions.

	Gameplay Role (Future)

* Periodically, eggs rupture
* Spawn spider-like or spirit-spider hybrids
* These creatures:
	* Disrupt formations
	* Force repositioning
	* Prevent static tank-and-spank tactics

	Eventually:
* All eggs must be destroyed
* Or naturally exhausted
	Before Queen can be fully vulnerable

---

	3. Statue System – Dual Pillars of Power

	Around the sides of the chamber:

	Green Statues – *Healer Anchors*

	Purpose : Sustain Queen Redrose.
* When intact:
	* Provide continuous healing to Queen
* When destroyed:
	* Release the "Healer Spirits"

	Healer Spirits (Post-Destruction)

* Extremely weak alone
* But:
	* Buffed by Queen
	* Protected by remaining statues
	* Strengthened by proximity to other healers

	Intended Strategy :
* Drag healers away one by one
* Isolate them
* Kill them quickly
* Prevent mass healing loops

	This creates:

> Tactical pulling + team coordination gameplay.

---

	Blue Statues – Storm Conduits

	Purpose : Offensive pressure and zone control.

* Emit continuous or periodic lightning arcs
* These:
	* Spawn minions
	* Or empower minions
	* Or directly pressure players

	These ensure:
* Players cannot ignore the environment
* They must choose between:
	* Removing support
	* Removing offense
	* Or pushing Queen

---

	4. Floor Sigils – The Ritual Geometry

	The floor is engraved with ancient designs:

	Known Figures

* 🔺 Three Triangles
* ⚪ Two Circles
* ⬛ One Square (Center)

	Each represents an ancient ritual control point.
	These are not decorative — they are the key to defeating Redrose.

---

	The Circles – Sanctuary of Nullification

	Purpose : Provide temporary immunity zones.

	When activated:
* Players inside are immune or highly resistant to:
	* Queen’s AoE
	* Fear
	* Root
	* Banshee screams

	This gives:

> Tactical safe zones for recovery, casting, and survival.

---

	The Triangles – Purge Sites

	Purpose : Designed for dealing with healers and summoned threats.

	Possible functionality:
* Increased damage to spirits/healers inside
* Or suppression of healing effects
* Or vulnerability zones for support entities

	Encourages:

> Dragging healers and summons onto these areas for execution.

---

	The Square – Binding Seal (Final Phase)

	Purpose : The only place Queen Redrose can be truly slain.

	Key idea:
* If Queen is killed outside the square:
	* She reforms
	* Or phase resets
	* Or triggers catastrophic retaliation

	Therefore:

> She must be lured, forced, or tricked into standing on the central square for the final blow.

	And she knows this.

	She will:
* Avoid it
* Displace players
* Fear
* Root
* Summon obstacles

	This creates:

> A positional endgame, not just DPS.

---

	5. Environmental Degradation

	Throughout the fight:
* Pieces of the ceiling fall
* Stone debris litters the floor
* Edges of the room become dangerous:
	* Cramped
	* Uneven
	* Poor for escaping AoEs

	This enforces:
* Mid-room dominance
* Use of sigils
* Risk in corner camping

---

	6. Crowd Control Pressure

	Queen Redrose’s kit is designed to include:
* Fear
* Root
* Forced movement
* Taunt manipulation

	This makes:

	Buffs like “Oath of the Last Crusader” absolutely critical, not optional, for survival.

	Without resistance:
* Players are chain-controlled
* Broken formations
* High wipe probability

---

	7. Final Phase Design Intention

	Before Queen can die:

✔ No eggs on ceiling
✔ All healer spirits destroyed
✔ All statues neutralized
✔ No external support remains

	Only then:
* Queen becomes isolated
* Vulnerable
* And forced into the center

	At that point:

> The fight becomes a duel against despair itself, not mechanics.

