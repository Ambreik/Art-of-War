	Abson Catacomb
57 rooms total plus boss chamber.
55 rooms interconected by corridors, devided by difficulty where start with
• 10 easy rooms with corridors coloured in yellow and made by stone as a cave.
• 20 medium rooms with corridors coloured in orange and made by blocks, rooms wider and taller and closer to the last part is, can be found trees and vegetation in 
the rooms and some corridors.
• 25 hard rooms with corridors coloured in red and made by blocks, big rooms and tall, with trees in and a river that flow through last rooms, coming from boss 
chamber, creating a small swamp.This section contain additional 2 rooms with long corridors and are side rooms that end the path. Here specific semi bosses are found,
spawning random from 1 to 3 hours.One of this room is colored in blue and contain also a statue that lead to "Gorilla Realm" or also called "Gorilla City" or village.